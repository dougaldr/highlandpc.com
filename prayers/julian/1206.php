<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 6
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When the angel appeared to Mary and greeted her, she 
was puzzled and afraid. The angel told her that she had found grace with You, O 
Lord. You blessed Mary with Your grace, not that she deserved it. If she had, 
it would not be grace.</p>
<p>So, Lord, You give us stewardships and responsibilities and 
blessings by grace. We are all unworthy. Some of us You bless in greater ways 
than others.</p>
<p>Mary had the privilege of bearing and rearing Jesus. The 
angel said to her, &quot;He will be great and will be called the Son of the Most 
High. The Lord God will give him the throne of his father David, and he will 
reign over the house of Jacob forever; his kingdom will never end&quot; (<b>Luke 
1:32-33</b>).</p>
<p>Although only one woman could be privileged to bear the 
Messiah, all who believe are privileged to declare the Messiah. Help us 
remember that we too have a stewardship, a trust. O Lord, use me to tell others 
that Jesus is the Son of the Most High and that light and life come through 
faith in Him alone!</p>
<p>Lord, sometimes I confess that Your ways truly are above my 
ways. I sometimes have difficulty understanding what Your Word reveals; I dare 
not think about what Your Word has not revealed. I remember the wisdom of <b>
Deuteronomy 29:29</b>, &quot;The secret things belong to the L<span class="sm">ORD</span>
our God, but the things revealed belong to us and to our children forever.&quot; 
Help me to understand what You want me to understand; give me neither a lazy 
brain nor a mind that loves to speculate in unprofitable areas.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You. My mind is important; You 
value it greatly. Help me to be cautious about what I put into it.</p>
<p>Today I pray for the nation of Eritrea, bordered by 
Ethiopia and Sudan. Thank You for the 1.7% of the population who are born-again 
evangelical Christians. Lord, there is much work that needs to be done in this 
land. We pray that You would use the Jesus film, radio broadcasts and the 
small evangelical church to share salvation by grace through faith alone in 
Jesus Christ. Please restore freedom of religion so that evangelicals and 
other groups may have freedom to share their faith. Please work among the 
Muslim and Eastern Orthodox groups to recognize the importance of true freedom. 
May the Bible-oriented churches see a tenfold growth over the next decade and 
provide good leaders and leadership training, I pray.</p>
<p>Thank You for the ministry of Japan Christian Link whose &quot;goal 
is to help make a significant difference in the progress of the gospel among the 
Japanese people over the first half of this century. [They] seek to do this 
through educating, encouraging and equipping the Body of Christ.&quot; Lord, bless 
the efforts of this work. The Japanese people need You more than they realize.</p>
<p>Lord, I am not aware of how much I need you either. Help 
me to reflect on this today, I pray in Jesus' Name, Amen.</p>

