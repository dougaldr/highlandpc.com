<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 24
<br><span class=byln>by Ed Vasicek</p>

<p>Faithful Father,</p>
<p>You are so good to me. Your love extends downward toward 
all and in Your love, You warn of Your wrath. You are the God Who Warns. 
Before Cain killed Abel, You warned him to correct his attitude. Before the 
Flood, You warned that You would not always strive with man. Righteous Lot 
served as a warning to Sodom and Gomorrah. You sent Your prophets to warn 
Israel of the consequences of her apostasy and the judgment You would bring. 
Sometimes people heed Your warning, as in the repentance of the people in 
Nineveh who surprisingly repented at Jonah's preaching. But more often, it 
seems Your warnings are not heeded. A lonely eternity awaits those who have not 
heeded the warning of the Gospel. You warn us through our consciences, Your 
Word, other people and even the disasters of nature. Thank You that because 
of the work of Your Spirit, we were primed to heed Your warning.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________. Lord, I am thankful for many things three of which 
are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today. Keep me 
from grieving Your Spirit by hardening my heart or disregarding Your will or 
flirting with sin, I ask.</p>
<p>Lord, the nation of Sudan has been a mixed blessing. On 
the one hand, many have responded to the Gospel especially in the south. As a 
result, over 10% of the nation claim to be born-again evangelicals and nearly 
a fourth of the population professes some sort of Christianity. Yet the Muslims 
control the more populous north and they are radical Muslims. They have 
massacred tens of thousands in the South and have taken over 100,000 as 
slaves! O Lord, stop the bloodshed and slavery. Grant true freedom to this 
nation. Halt the spread of evil in Sudan and fortify and nurture the spread of 
the Gospel. Indeed, the behavior of the people in the north emphasizes how much 
they need the Gospel! Bless the relief and mission agencies (like Samaritan's 
Purse) that seek to help the many refugees. Work good out of this, O Lord.</p>
<p>Lord, I pray for the great ministry of Samaritan's Purse as 
it goes out &quot;to the 
aid of the world's poor, sick and suffering. [They] are an effective means of 
reaching hurting people in countries around the world with food, medicine and 
other assistance in the Name of Jesus Christ. This in turn, earns us a hearing 
for the Gospel, the Good News of eternal life through Jesus Christ. [Their] 
emergency relief programs provide desperately needed assistance to victims of 
natural disaster, war, disease and famine.... [Their] community development and 
vocational programs in 
impoverished villages and neighborhoods help people break the cycle of poverty 
and give them hope for a better tomorrow. [They]...impact the lives of vulnerable 
children through educational, feeding, clothing and shelter programs that let 
them know they are not forgotten.&quot; O God, provide for, protect, empower and 
grant harmony to this ministry, I pray.</p>
<p>Help me to live for You today, I ask in Jesus' Name, Amen.</p>

