<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 30
<br><span class=byln>by Ed Vasicek</p>

<p>My Father in Heaven,</p>
<p>You are the Living God. You are not some force, nor are You dead. You are the 
source of all life. All who live came about through You. Life comes from life, 
not non-life. And You, the Living One, created all mortal beings and all 
physical things. You are also the author of spiritual life. Your image in us 
means that we have a spiritual side to us. You created the angels and You 
created mankind. In Christ, You have redeemed us and by Your Spirit You have 
regenerated those of us who believe. You have given us the abundant life and 
have spiritually resurrected us. Our Lord Jesus is not only the way and the 
truth, but also the Life. Indeed, He is the resurrection and the life.</p>
<p>Lord, help me to seek the best in people and interpret things in a positive 
light unless I have reason to believe otherwise. Remind me of the proverb, &quot;He 
who seeks good finds goodwill, but evil comes to him who searches for it&quot; (<b>Proverbs 
11:27</b>). Remind me that You look at the heart, but I can only see the outward 
appearance. Help me not to jump to conclusions about the motives of others.</p>
<p>Help me to do my part by volunteering to serve or continuing to serve in a 
Bible-believing, Bible-teaching church. May I set my roots down deeply and 
attend faithfully every week if I am able to do so. Help me be a team player and 
to remember that Christianity is not just a private faith but requires me to 
participate in the body of Christ.</p>
<p>Lord, help me to control my temper. Anger makes fools of 
people and tears down. The damage caused by anger often cannot be reversed. 
Anger that is under control and directed toward problems, not people, is a 
source of energy, and in a sense, a gift from You. However, it is also a dangerous power. 
Channeled properly, it helps me address problems. So help me neither to stuff 
it nor explode, but to address that which makes me angry, when possible.</p>
<p>Today I pray for the Gujarat State of India, with its 50 
million people. Perhaps .3% of the population is made up of evangelical 
born-again believers. That’s tiny. Thank You that, despite the small 
percentage, the church is growing in some regions in this state. The pressure 
from the majority Hindu population (nearly 90%) is great. O Father, pour out 
Your Spirit in this region. May the percentage of believers grow tenfold over 
the next decade, I pray.</p>
<p>Lord, Turkey is the largest unreached nation in the world. 
Thank You for Turkish World Outreach as it &quot;goes to the Turkic world and groups 
associated within it such as the Kurds and Tajiks, seeks to establish 
reproducing, indigenous churches...[and]...partners with sister agencies where 
possible and useful.&quot; O Lord, &quot;Only about 6% of all foreign missionaries are 
working for the blessing of Muslims who constitute a third of all 
non-Christians.&quot; So Father, please bless and prosper this work; grant them much 
fruit, provision, safety, harmony and Your wisdom, I ask.</p>
<p>Now, Lord, help me to reach out in my world, I pray in the 
Savior’s Name, Amen.</p>

