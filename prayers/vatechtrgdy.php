<br>A Prayer in Light of the Virginia Tech Tragedy
<br><span class=byln>by Ed Vasicek</span></p>

<p>Our Father, </p>
<p>Our hearts were grieved and traumatized when we heard of the disastrous and 
senseless mass shooting at Virginia Tech. As bystanders, we marveled that anyone 
would kill so many innocent people for no objective reason -- other than the 
killer's impersonal, calculated, and misdirected rage. </p>
<p>Lord, although we value our ability to make choices, sometimes we regret that 
we not only have choices between good and better, but also between evil and 
horrendous evil.&nbsp;And it saddens us that sometimes we are the victims of choices 
others make. </p>
<p>O God, words cannot express the intensity of our desire that You somehow 
comfort the family and friends of those who have died. We ask You to help them 
through this immediate shock, and help them as they grieve and address their 
anger in the weeks, months, years, and decades ahead. It takes years to build 
lives, but only seconds to destroy them. You create good things, O Lord, and 
evil is the spoiling of the good. Although we grieve most painfully over the 
deceased, thousands of others have been victimized and traumatized. </p>
<p>We pray that You would help heal those who were wounded. Minster to the 
physical, psychological, and spiritual needs of all involved: the survivors, the 
grieving families, the traumatized law enforcement and hospital personnel, 
indeed, the entire campus. May each one turn to You to find the strength to go 
on, we pray, in Jesus' Name, Amen. </p>

