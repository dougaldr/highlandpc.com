<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 15
<br><span class=byln>by Ed Vasicek</p>

<p>Holy God,</p>
<p>You are the God of Redemption. Your Son, Our Lord Jesus 
Christ, was particularly referred to as Redeemer although the title also 
pertains to You as Father, Son, and Holy Spirit, for each divine Person has 
played a part in redemption. Thank You, Father, that You sent the Son. Thank 
You, Son, that You provided for the redemption of all. Thank You, Spirit, that 
You apply redemption on an individual basis through Your work of regeneration 
for all those who as a result believe. You have bought us from the slave 
market of sin, from the required justice of a holy God, from a destiny of 
condemnation to freedom, forgiveness, and eternal bliss.</p>
<p>Although I am now redeemed, I still do not always live like 
a redeemed person. Work in my heart to make me holy and pure in daily life.</p>
<p>Your Word tells me that if I 
confess my sins, You are faithful and just to forgive me of them, and You will 
cleanse me from all unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You 
now. I do not excuse them, but I freely admit them and ask for Your 
forgiveness.</p>
<p>I ask You to send angels to watch 
over me and my family throughout our lives.</p>
<p>Please address the following 
needs in my life or in the lives of those dear to me: __________.</p>
<p>Lord, help me to participate in 
some way in the life of my community. I am tempted to take the light and hide 
it under a bushel, or to let it shine only among others' lights, my brothers and 
sisters in Christ. Instead, help me to make the effort to be in the world, to 
penetrate society; yet help others see the difference because I am not of the 
world. I do not have to highlight the difference or emphasize it. It will 
surface.</p>
<p>Today I pray for the world. As 
the population is rushing toward the 7 billion mark, I praise You that 6.9% of 
the population claim to be born-again evangelical Christians. Thank You that 
the Gospel is especially bearing fruit in non-western countries, and that there 
are now more evangelicals elsewhere than in the west for the first time. Thank 
You that Bible-oriented churches are growing at a good pace; we pray that the 
pace would not only hold its own, but, Lord, that You would double the growth 
rate within the next decade. Lord, build Your Kingdom to Your glory!</p>
<p>Father, particularly bless World Team, “a mission organization striving to 
glorify God by working to establish 
reproducing churches focusing on the unreached peoples of the world.” Bless 
them as they seek to multiply churches around the world. Provide for the 
missionaries and their families, give them fruit for their labor, protect them, 
and may the Holy Spirit empower them and grace them with a cooperative and 
harmonious spirit.</p>
<p>Gracious Master, help me 
remember that I am serving You today, I pray, in Jesus’ Name, Amen.</p>

