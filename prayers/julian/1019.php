<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 19
<br><span class=byln>by Ed Vasicek</p>

<p>Lord Jesus,</p>
<p>You are my Ark of safety. At Calvary, You 
endured the deluge of God’s wrath, much like the Ark endured the waters gushing 
upward from the earth and the rains pouring down. Your baptism of wrath 
(<a href="javascript:scripture('../../scripture/luke1250.htm')" target=_self class=pop>Luke 12:50</a>) 
saves us who are in You and safely protected. As those outside the Ark 
perished physically, so those outside Christ will perish spiritually. As those 
inside the Ark were safe as soon as they entered, so are we saved as soon as we 
personally accept Jesus Christ as Savior.</p>
<p>Thank You, Lord Jesus, for enduring the outpouring of wrath 
so that we could be delivered. I pray that my loved ones and friends would 
enter the Ark of salvation as I have by turning from sin and believing in the 
Lord Jesus Christ.</p>
<p>I pray for the elderly, sickly and shut-in including: __________.</p>
<p>Help me to be an alert Christian, Lord. I do not want to 
keep my head in the sand or deny undesirable realities. I need to understand 
how I am led into sin, how the devil tempts me and I need to have answers for 
the questions people are asking. Help me read, study and tool up as one of 
Your workers.</p>
<p>Father, I intercede for believers who are struggling with 
substance abuse, addictions, or other bad or undesirable habits. Some practices 
are wrong, even in moderation. Others are wrong in excess. Others are not 
wrong in themselves, but are wrong because they are enslaving. Help us all to 
put off our bad ways and replace them with positively constructive ways. Rather 
than steal, let us labor to give to those in need. Rather than gossip, let us 
praise. Instead of being drunk with wine, help us to be filled with Your 
Spirit. Help us replace the undesirable with the good—or at least the 
neutral.</p>
<p>Lord, I pray for the Shanxi Province of China with its 32 
million people. Although this area has suffered much repression, thank You that 
perhaps 3% of the population are born-again Christians. Lord, even during the 
Boxer Rebellion (which centered in this area), many of Your children were 
martyred. Lord, we pray for freedom and the working of Your Spirit. We ask 
that many would be granted the repentance that leads to life and that the 
percentage of believers would double over the next decade.</p>
<p>Lord, thank You for the work of Rafiki Mission whose &quot;aim 
is to turn helpless children in Africa into godly contributors in their 
countries. Rafiki’s goal is to establish one Christian training village&quot; in ten 
African nations. &quot;A Rafiki Training Village consists of 16 homes for 10 orphans 
each, schools for these orphans, three-year vocational training day schools for 
60 vulnerable teenage girls and 60 boys, medical care for occupants and 
facilities for training select African church denominations to replicate.&quot; 
O Father, help this ministry to be successful and to make 
an impact for Christ’s Kingdom and for the betterment of these nations, I pray. 
Empower, provide and protect, I ask.</p>
<p>Help me honor You, my Lord, throughout my day, I pray in 
Jesus' Name, Amen.</p>


