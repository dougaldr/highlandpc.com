<p>The Christmas narrative really begins by setting the stage 
for Jesus' coming through explaining how his forerunner, John the Baptist, was 
miraculously conceived. Our narrative begins with Zechariah and Elizabeth, an 
older couple (probably mid to later 50s) who had never been blessed with 
children.</p>
<p>Zechariah was a priest, but not of the high priestly 
family. This meant he was one of many hundreds of priests who only occasionally 
served at the Temple. Today Jewish men with the last name of Cohen, Conn, Cohan 
(or similar names based on the Hebrew word for priests) are likewise priests, 
descendants of Aaron. These priests did not receive much compensation for their 
work but supported themselves through a trade or skill; they were what we might 
call &quot;lay priests.&quot;</p>
<p>Since there were so many male descendants of Aaron (and 
therefore priests), the priesthood was divided into divisions. Twice a year, a 
lay priest would take his turn to journey to Jerusalem and serve by offering sacrifices, 
etc., for one week. Anne Punton writes:</p>
<blockquote>The priests did far more than offer daily sacrifices, 
burn the incense, care for lamps and provide the weekly shewbread. In addition, 
there were special sacrifices ordained for Shabbat, new moons and other 
festivals and feasts. They also dealt with the public…. Practically every aspect 
of a religious person's daily life focused on the Temple. The priests received 
freewill gifts, tithes, offerings and firstfruits. They absolved vows, 
supervised rituals to do with the redemption of the firstborn, the conclusion of 
a nazirite vow or for a cleansed leper…&quot; (from <i>The World Jesus Knew</i>, p. 
171)</blockquote>
<p>The priests cast lots to determine the special task of 
offering incense. It was a once-in-a-lifetime opportunity; after a priest 
served, he was no longer involved in the lot. Zechariah's turn had come.</p>
<p>While the priest offered incense, the Jewish <i>ma'amad </i>
would gather outside the Temple and bow down before the Lord with outstretched 
hands offering silent prayer while the incense was offered. The entire Temple 
area was in a hush. These <i>ma'amad</i> were laymen who were likewise 
organized in divisions. Other devout Jews would gather in synagogues throughout 
the land during the time of the incense offering. <b>Revelation 8:1-5a</b> describes 
the exact same scene in the heavenly sanctuary:</p>
<blockquote>When he opened the seventh seal, there was silence in heaven for about half an 
hour. And I saw the seven angels who stand before God, and to them were given 
seven trumpets. Another angel, who had a golden censer, came and stood at the 
altar. He was given much incense to offer, with the prayers of all the saints, 
on the golden altar before the throne. The smoke of the incense, together with 
the prayers of the saints, went up before God from the angel's hand. Then the 
angel took the censer, filled it with fire from the altar, and hurled it on the 
earth....</blockquote>

