<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 2
<br><span class=byln>by Ed Vasicek</p>

<p>Holy Father,</p>
<p>You are a patient God. You are longsuffering and rarely 
in a hurry. Because of Your patience, You delay Your wrath. You are not willing 
for any to perish, but for all to come to repentance. Before You drove out the 
people of Canaan before the Israelites, You gave them decades—even centuries—to 
repent. Before You sent the children of Israel into captivity, You gave them 
centuries, about 490 years. Because they did not observe the sabbatical years 
every seventh year, You caught up by giving the land rest for 70 years, the 
Babylonian Captivity 
(<a href="javascript:scripture('../../scripture/2chr3621.htm')" target=_self class=pop>2 Chronicles 36:21</a>).
Without our repentance, Your wrath 
is merely delayed. But during the times of Your patience, You call us to turn 
from our sins and to turn to You.</p>
<p>I remember parents. Lord, parents are so important to 
those little ones entrusted to their care. May parents everywhere protect and 
train their children in humane and balanced ways. Parents are just as important 
to junior and senior high teens. Even though parents change the way they relate 
to older children, help them adapt but not pull back. Remind them that their 
kids still need them even though teens may not want to admit that. Help them to 
love their children, not to spoil them, and to encourage them toward positive 
directions. We pray that Christian parents would prove to be consistent role 
models for their young ones. Send angels to protect both parents and children, I 
pray.</p>
<p>Thank You Lord for Norway. Father, Norwegian Christians 
have been among the most missions-minded in the world and they still are! 
Thank You that over 9% of the population claim to be born-again evangelicals, 
while about 5% claim to be born-again but attending non-evangelical churches. I 
know that for Europe, this is exceptional. I thank You that a renewal seems to 
be taking place and I pray that it would result in great progress for Your 
Kingdom. May the percentage of born-again believers double over the next decade 
and help Norwegian believers to reach Muslim immigrants for Christ. Thank You 
for the remarkable fact that the State Church is very evangelical in belief. May 
Your Spirit move mightily in this land.</p>
<p>Bless the ministry of Gospel for Asia, O Lord, as they seek 
to “plant churches among the unreached.... Gospel 
for Asia trains and sends native missionaries because they have proven extremely 
effective. They are already familiar with the language and culture and they 
live at the level of the people they serve, thus removing many social 
barriers.” Lord, Asia is one of the most unreached sections of the world. 
Bless this ministry. Empower them by Your Spirit, provide for them, protect 
them and grant them harmony and efficiency, I pray.</p>
<p>Lord, I pray for those who work 
as roofers. Give them safety, Lord, and wisdom. Help them do their tasks well 
and may those who know You freely share their testimony with their coworkers. 
May they be used of You, O Lord.</p>
<p>Now help me to honor You today, 
I pray in Jesus’ Name, Amen.</p>

