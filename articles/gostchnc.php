<p id="pgt"><span class="pgh">Articles:
<br>Apologetics, Questions, Issues</span>
<br>A Ghost of A Chance
<br><span class=byln>by Ed Vasicek</span></p>

<p>When I was a young child, I remember learning my first 
riddle: &quot;What do ghosts eat for breakfast? Ghost Toasties and evaporated milk.&quot;</p>
<p>Then there's the one about the woman who told her friend, 
&quot;We had a s&#233;ance last night with a new spiritist.&quot;</p>
<p>&quot;Really?&quot; asked the friend. &quot;And how was she?&quot;</p>
<p>The first woman replied, &quot;Oh, medium.&quot;</p>
<p>Unfortunately, ghosts are no laughing matter. According to
<i>Pulpit Helps</i> magazine, &quot;A total of 2,012 people in the U.K. (Great Britain) 
were polled on their beliefs regarding the supernatural. More than two-thirds 
(68%) said they believe in the existence of ghosts and spirits while just over 
half (55%) said they believe in the existence of a God.... The survey found that 
12% believe they have actually seen a ghost&quot; (<i>Pulpit Helps</i>, January 2006, 
p. 27).</p>
<p>Although we addressed the UFO phenomenon a couple of years 
ago via our Sunday night cult series (and a video about UFOs from a Christian 
perspective is available in our church library), we never tackled ghosts&#8212;until 
today. </p>
<p>So how do we handle this issue? Are people who see ghosts 
crazy? If not, what are they really seeing? Do the spirits of your deceased 
relatives wander your hallways?</p>
<p>Although skeptics tend to write off anyone who sees a ghost 
or UFO as mentally unstable, this is frequently not the case. Because people 
with psychological disorders might drink coffee does not mean everyone who 
drinks coffee has a personality disorder. Likewise, because many mentally 
unstable people see UFOs or ghosts does not negate the fact that many sane 
people do so. Jimmy Carter, for example, saw a UFO, and I have a couple of very 
sane relatives who have seen ghosts. Sane people DO see these things. As a 
matter of fact, the odds are that a number of Body Builder readers have seen one 
of the above phenomena or know a person whom they respect who has.</p>
<p>So how does this mesh with the Christian faith? We believe 
that at death (We'll define death as the cessation of brain waves.), the soul 
of a person returns to our Maker. We believe that the saved are immediately 
conscious with the Lord 
(<a href="javascript:scripture('../scripture/2cor58.htm')" target=_self class=pop>2 Corinthians 5:8</a>) 
while the lost are conscious in the outer 
darkness of hell 
(<a href="javascript:scripture('../scripture/luke162223.htm')" target=_self class=pop>Luke 16:22-23</a>). 
We believe that &quot;man is destined to die 
once, and after that to face judgment&quot; (<b>Hebrews 9:27</b>).</p>
<p>If you have seen the UFO video or read books from a 
Christian viewpoint about this, such as John Ankerberg's, <i>The Facts on UFOs 
and Other Supernatural Phenomena</i> or <i>The Omega Conspiracy</i>, some UFOs 
can be accounted for by mistaking objects in the sky. However most are 
sightings of evil spirit beings who may pose as friendly and benevolent 
creatures. People who are &quot;abducted&quot; by &quot;aliens&quot; usually develop 
characteristics associated with the demonically oppressed. These &quot;aliens&quot; are 
not visitors from another planet but from another dimension.</p>
<p>But this particular article is focusing upon ghosts. 
Appearances of ghosts can have a number of explanations. Ghost visitations can 
be the result of mental disorder. The brain thinks it is seeing what is not 
there. The brain is highly complex, and we are still very ignorant of its 
workings. Many apparitions can be accounted for via this explanation.</p>
<p>Whether we are talking about ghosts or UFOs, they 
typically encourage those to whom they have appeared to forsake Christianity, 
accept New Age religion and embrace occult practice.&nbsp; Sometimes when people 
see ghosts, they consult mediums because they conclude a dead relative has a 
message to deliver. These demons imitate the voice and appearance of someone 
they have studied, but that is precisely what they are: imitators. People want 
to believe they are contacting a loved one; such a thought appeals to the 
emotions (For example, I would love to talk to my parents.) and lures 
unsuspecting folks into the occult.</p>
<p>Interestingly, most demonic problems such as ghosts, 
oppression, etc. are restricted to families where someone perhaps a parent or 
even grandparent has dabbled in the occult (interpreting dreams, astrology, 
reading tea leaves, palm reading, ouija boards, etc.). Christian authorities on 
demonism, e.g., Mark Bubeck, <i>The Adversary</i>, and Merrill Unger, <i>What 
Demons Can Do to Saints,</i> have documented that demons love to stay in 
families; some relate it to the curse of 
<a href="javascript:scripture('../scripture/ex205.htm')" target=_self class=pop>Exodus 20:5</a> (to the 4<sup>th</sup> and 
5<sup>th</sup> generation).</p>
<p>God's Word to believers is to lay off the occult. In <b>Deuteronomy 
18:9-12</b> we read,
<blockquote>When you enter the land the L<span class=sm>ORD</span> your God is giving you, do 
not learn to imitate the detestable ways of the nations there. Let no one be 
found among you who sacrifices his son or daughter in the fire, who practices 
divination or sorcery, interprets omens, engages in witchcraft, or casts spells, 
or who is a medium or spirits or who consults the dead. Anyone who does these 
things is detestable to the L<span class=sm>ORD</span>, and because of these detestable practices the 
L<span class=sm>ORD</span> your God will drive out those nations before you.</blockquote>
<p>In <b>Acts 19:19</b> converts to Christianity forsook their 
occult practices: &quot;A number who had practiced sorcery brought their scrolls 
together and burned them publicly. When they calculated the value of the 
scrolls, the total came to fifty thousand drachmas.&quot;</p>
<p>As a Christian, God calls us to follow Him completely. We 
cannot be involved in both the doctrines of demons and the truth of God. 
Remember there is power in the Name of the Lord Jesus Christ. There is also 
power in the prayers of believers. But if we dabble in the occult, we are 
asking for problems.</p>



