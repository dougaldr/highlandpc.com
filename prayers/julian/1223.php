<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 23
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>After the Magi had left Joseph, Mary and baby Jesus, 
You warned Joseph in a dream to take Jesus out of the country and to sojourn in 
Egypt for some time. You had provided them with plenty of wealth to make the 
trip and sustain themselves through the precious gifts of the Wise Men.</p>
<p>Just as Israel found refuge from the great famine in Egypt, 
so the Messiah found refuge there. Just as You called Israel out of Egypt under 
the leadership of Moses, so You called Jesus and family back to Israel from 
Egypt. </p>
<p>After they had left, Herod massacred the infant boys in 
that region. But Lord, no one can thwart Your ordained purposes. Yet it brings 
tears to us to think of the many who have or will die out of hatred for Jesus 
Christ. Many of Your children are suffering right now, O Lord. Please bring 
them relief and release!</p>
<p>Father, I move toward some personal needs. Lord, help me to 
handle anger as a gift from You. Like atomic power, anger can be used to help 
others and provide a needed energy for me to address problems. Or it can be used 
to destroy and contaminate. If I stuff it, I become depressed or eventually 
explode. Instead, help me use anger to energize me to tackle problems in a 
reasonable and effective way. Help me address offenses as they occur so that I 
do not accumulate &quot;straws,&quot; with one eventually breaking the back of the 
proverbial camel. Help me distinguish between sinful anger, legitimate anger and 
the type of anger known as righteous indignation.</p>
<p>Lord, sometimes the devil tempts me to misdirect anger. 
Help me to resist the fiery darts of the devil by taking up the shield of faith. 
Help me to go on the offense with the sword of the Spirit. May I stand firm, O 
Lord. Remind me that the Christian life is often a battleground, not always a 
playground.</p>
<p>Thank You Lord for Norway. Father, Norwegian Christians 
have been among the most missions-minded in the world and they still are! Thank 
You that over 9% of the population claim to be born-again evangelicals, while 
about 5% claim to be born-again but attending non-evangelical churches. I thank 
You that the renewal seems to be taking place. I pray that it would result in 
great progress for Your Kingdom. May the percentage of born-again believers 
double over the next decade and help Norwegian believers to reach Muslim 
immigrants for Christ. Thank You for the remarkable fact that the State Church 
is very evangelical in belief. May Your Spirit move mightily in this land.</p>
<p>Bless the ministry of Gospel for Asia, O Lord, as they seek 
to &quot;plant churches among the unreached....Gospel for Asia trains and sends 
native missionaries because they have proven extremely effective. They are 
already familiar with the language and culture and they live at the level of the 
people they serve, thus removing many social barriers.&quot; Lord, Asia is one of the 
most unreached sections of our world. Bless this ministry. Empower them by Your 
Spirit, provide for them, protect them and grant them harmony and efficiency, I 
pray.</p>
<p>Now use me as a native witness in my community, I ask in 
Jesus' Name, Amen.</p>

