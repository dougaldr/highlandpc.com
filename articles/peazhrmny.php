<br>Peace and Harmony
<br><span class=byln>by Ed Vasicek</span></p>

<div>

<p>Arguments, disagreements, conflicts...who needs them? Yet 
they are as much a part of life as the air we breathe. We cannot always be at 
peace with others. That's why Paul, in <b>Romans 12:18</b> only urges us to do our 
part: &quot;If possible, <i>so far as it depends on you</i>, be at peace with all 
men.&quot;</p>
<p>Peace is such a valuable entity that the Jewish people 
greeted (and still greet) one another with the phrase, &quot;Shalom Elechem&quot; (&quot;peace 
to you&quot;) to which the other responds, &quot;Elechem Shalom (&quot;to you peace&quot;).</p>
<p>Jesus did not find it possible to be at peace with all 
men. His ministry involved telling the truth about who He was--and exposing 
the false teachers of His day. He probably broke every rule in the book <i>How 
to Win Friends and Influence People</i> and ended up &quot;despised and rejected of 
men&quot; (<b>Isaiah 53:3</b>). Sometimes the truth is offensive, even if presented 
lovingly.</p>
<p>But most times, conflict is unnecessary. It usually occurs 
when one or more persons do not feel respected. If we go out of our way to 
communicate our respect for others, that often alleviates many tensions. But, 
as in the case with Jesus, sometimes truth gets in the way of peace. Sometimes 
misunderstanding clouds the issue. Sometimes a disagreement brings up baggage 
in a person's life that is not even connected to the issue at hand. Since there 
are many varieties of conflict in life, we cannot address them all in the same 
way. Let's look at a variety of ways to address conflict Biblically.</p>
<p><b>The first way is Overlooking.</b> God overlooks many of 
our sins, sins we commit while walking in the light 
(<a href="javascript:scripture('../scripture/1john17.htm')" target=_self class=pop>1 John 1:7</a>). 
Sometimes people insult us or hurt us unintentionally. We can easily irritate others 
simply by being ourselves. It is easier to overlook a wrong if we realize the 
wrong was not done maliciously.</p>
<p><b>First Peter 4:8</b> refers to overlooking these small offenses as 
&quot;covering&quot; sins: &quot;Above all, keep fervent in your love for one another, because 
love covers a multitude of sins.&quot; The Old Testament term for &quot;atonement&quot; 
literally meant a covering. Yom (day) Kippur (of atonement) is actually the day 
when sins were covered. The Hebrew name for a skullcap worn by Jewish men in a 
synagogue is &quot;kippa&quot; (covering). So Peter is telling us that our love for 
others can sometimes &quot;atone&quot; for their sins in our relationship to them.</p>
<p>The problem is that we cannot always overlook offenses. 
Indeed, sometimes this is a very individual thing: I can overlook offenses that 
might give you a grudge, and you can overlook offenses that might give me a 
grudge.</p>
<p><b>That leads us to a second way to address conflict 
biblically--Discussion.</b> Jesus spoke about this in 
<a href="javascript:scripture('../scripture/matt52324.htm')" target=_self class=pop>Matthew 5:23-24</a> 
where he suggested confronting a brother with whom you are offended. Jesus' words are 
probably a commentary on <b>Leviticus 19:16-18</b>, &quot;You shall not go about as a 
slanderer among your people, and you are not to act against the life of your 
neighbor; I am the L<span class=sm>ORD</span>. You shall not hate your fellow countryman in your 
heart; you may surely reprove your neighbor, but shall not incur sin because of 
him. You shall not take vengeance, nor bear any grudge against the sons of your 
people, but you shall love your neighbor as yourself; I am the L<span class=sm>ORD</span>.&quot;</p>
<p>When something is not minor enough to overlook, you need to 
kindly bring up the issue to the person who has offended you. If not, you will 
be tempted to slander that person (<b>16</b>), hate that person (<b>17</b>), get even (<b>18</b>) or 
bear a grudge (<b>18</b>). It takes a little courage to bring up an offense, but it is 
a way to &quot;love your neighbor as yourself.&quot; If you let the offense fester, you 
do not love that person; instead, you are constructing a wall to isolate 
yourself from that person.</p>
<p><b>If discussion fails to correct the problem, it is time 
to move to the third way to address conflict, Negotiation.</b> This takes great 
humility of mind, a real <b>Philippians 2:3-4</b> attitude: &quot;Do nothing from 
selfishness or empty conceit, but with humility of mind regard one another as 
more important than yourselves; do not merely look out for your own personal 
interests, but also for the interests of others.&quot;</p>
<p>In negotiation, you must be willing to compromise and meet 
another part way. You might consider suffering some loss just for the sake of 
peace and getting along. Nothing stands in the way of negotiation like pride; a 
humble person really attempts to put himself in the shoes of another.</p>
<p>In <b>Philippians 4:2</b>, Paul urges two women to negotiate their 
differences: &quot;I urge Euodia and I urge Syntyche to live in harmony in the 
Lord.&quot; If there is one thing I have learned in the ministry, it is how to 
negotiate! My first reaction is often one of pride, so I too have to force 
myself to humble myself before I can honestly compromise.</p>
<p>What do you do if you cannot overlook an offense, 
discussion gets you nowhere, and you have failed at negotiation? <b>The next 
resort is Mediation</b>, bringing in a solidly Christian and neutral third party 
to offer direction. </p>
<p>If Mediation fails, it is on to <b>the fifth means of 
addressing the conflict, Arbitration.</b> In Arbitration, the decision of a 
board or committee is binding. Both parties agree beforehand to submit to the 
ruling. Arbitration is a function of church leaders, as Paul implies in 
<b>1 Corinthians 6:1-4</b>:</p>
<blockquote>When one of you has a complaint against another, do you 
take your complaint to a court of sinners? Or do you take it to God's people? 
Don't you know that God's people will judge the world? And if you are going to 
judge the world, can't you settle small problems? Don't you know that we will 
judge angels? And if that is so, we can surely judge everyday matters. Why do 
you take everyday complaints to judges who are not respected by the church? 
(CEV)</blockquote>
<p><b>If an individual will not abide by the arbitrated 
decision, then it is on to Church Discipline</b> in which the Elders will 
determine what needs to be done, even to the extreme of excommunication 
(<a href="javascript:scripture('../scripture/matt1817.htm')" target=_self class=pop>Matthew 18:17</a>). 
But such a decision loses its potency if the members of a church do not 
back the decision of the Elders. Church solidarity is crucial in such 
situations.</p>
<p><b>Ephesians 4:3</b> tells us that we should be willing to work 
hard to be at peace: &quot;Make every effort to keep the unity of the Spirit through 
the bond of peace.&quot;</p>
<p>But because we are willing to work hard at being at peace 
does not mean it is always possible or reasonable. As Christians, we also need 
to remember what Peter said, &quot;We must obey God rather than men&quot; (<b>Acts 5:29</b>). 
Making a generous effort to be at peace is not the same as &quot;peace at all 
costs.&quot; We must remember, &quot;love rejoices with the truth but not with 
iniquity.&quot; This brings us back to where we began, with Jesus whose teachings 
were true but detestable to the religious and political establishment. Christ 
often broke the peace, but He did so only because it was necessary. Yet His 
general approach was, &quot;Blessed are the peacemakers&quot; (<b>Matthew 5:9</b>).</p>

