<p>Here are a few additional thoughts not in the paper. David Daube
in his work, <u>The New Testament and Rabbinic Judaism</u> (Hendrickson)
devotes a chapter entitled, &quot;Eye for Eye&quot; to discuss the Jewish
understanding of Talion (the law of retribution), the subject of the Jewish
understanding of the &quot;Eye for Eye&quot; command. To the
first century Jew, that expression was more or less synonymous with the idea of
financial compensation and litigation. This sets the entire tone for
<a href="javascript:scripture('../scripture/matt53842.htm')" target=_self class=pop>verses 38-42</a>&#151;litigation.&nbsp;
Turning the other cheek, etc., connects to litigation. The only issue that is not about litigation is that of walking the extra mile,
which is encapsulated within litigation issues because the principle of walking
the extra mile is also the attitude we should take in matters of litigation.</p>
<p>Here is my sermon outline proper:</p>
<p align="center"><b>Walking That Extra Mile
(</b><a href="javascript:scripture('../scripture/matt53848.htm')" target=_self class=pop>Matthew 5:38-48</a><b>)</p>
<p>Introduction</b></p>
<ol>
<li>If you have old computer disks from an Apple II computer, they will not
work on a Windows based unit without conversion.<br>
<li>Why? Two different operating systems.<br>
<li>Christ demands His followers operate under a different operating system
than society at large: a different way of thinking with different
values and a different lifestyle.<br>
<li>You must be willing to be different and sometimes radical in the eyes of
the world.</ol>
<p>MAIN THOUGHT: Being A Disciple of Christ means operating our
minds with a different program than most of society, motivated by our
relationship with Christ.</p>
<p>That program can be defined by three rules by which to play the
game of life.</p>
<p><b>I. Being Generous With Grace
(</b><a href="javascript:scripture('../scripture/matt53842.htm')" target=_self class=pop>38-42</a><b>)</b>
<br><a href="javascript:scripture('../scripture/matt538.htm')" target=_self class=pop>Vs. 38</a>
sets the tone: the matter at hand is litigation and compensation.</p>
<ol>
<li>Accepting Insults
(<a href="javascript:scripture('../scripture/matt539.htm')" target=_self class=pop>39</a>)
<p>The issue here is NOT self-defense, passivity, or non-confrontation. Christ encourages us to confront those who offend us.&nbsp;
The issue is taking
an INSULT.</p>
<p>In almost every culture, the ultimate insult is a slap in the face. At debate in the first century was that fifth point of compensation, namely, for
insult. We already noted that quotation from the Mishnah:</p>
<blockquote>
<p>If anyone wounds his fellow, he becomes liable to compensate the injured
party for five different aspects of the injury: damage, pain, healing, loss of
time from work, and insult....</p>
</blockquote>
<p>Jesus obviously disagreed with this trend (according to Daube, it was being
debated in the first century and then solidified into the Mishnah in the 2nd
century).</p>
<p>Jesus was saying &quot;take the insult and ask for more.&quot; This is in contrast to the latter Rabbinic ruling in the Talmud:</p>
<p>&quot;Does he give him a blow upon the cheek? Let him give two
hundred zuzees; if with the other hand, let him give four hundred.&quot;</p>
<p>(Bara Kama, cap. 8, hal. 6&#151;quoted by John Lightfoot)</p>
<p>In contrast to suing a brother for every possible infringement, Christ is
saying, &quot;give people space.&quot; This has a pragmatic effect,
reconciliation (see <b> Proverbs 15:1</b>, &quot;A soft answer turns away wrath.&quot;),
and it is also WALKING THE EXTRA MILE, giving people the benefit of the doubt.</p>
<p>Please note Christ is not talking about stabs to the chest, punches to the
nose, nor twenty slaps on the cheek. Please pay
attention to the MODERATION of this section. You turn the other
check for a second slap&#151;but it ENDS there.</p>
<p>You walk the extra mile, but not an unlimited amount of miles. Once you perceive the absolute moderation of what Jesus is talking about, you
realize that things like pacifism are not even being contemplated.</p>
<p>Jesus is saying, &quot;put up with insult and even a moderate amount of abuse
before you take someone to court. Let people have space to be human,
to err. Do not take the attitude of an opportunist, perched to
exploit every infraction.&quot;<br><br>
<li>Avoiding Litigation
<p>Again, the main theme of litigation comes into play. Here
an example is placed before us; once again, the example is strikingly MODERATE.&nbsp;
A person is wanting to sue another for a tunic. Perhaps there is some
disagreement or dispute over a matter, a damage, etc. Rather
than go to court, it is better to take loss and settle out of court. This is related to
<a href="javascript:scripture('../scripture/matt525.htm')" target=_self class=pop>5:25</a>,
so please compare it.</p>
<p>Rather than fight over ones claim to a tunic (as compensation), the Christian
should give it to him (and ones coat as well) if that would preclude litigation.</p>
<p>It is interesting to note that Christ did not use the example of a house or
fortune. Although clothing in the ancient world was more of a commodity
back then, the concession is still not THAT major. Better to
take a moderate loss and stay on good terms with others than prove your point or
win your case. If you won't schmooze, you lose.</p>
<p>Again, the principle is WALK THE EXTRA MILE, not letting people abuse and
exploit us.<br><br>
<li>Going Beyond Obligation
<p>I have dealt with the Roman Law and the ability of a Roman solider to
constrain any non-citizen to walk one Roman mile (1,000 paces), carrying his
supplies. Christ says go beyond&#151;walk two miles.</p>
<p>Again, the attitude is that of being gracious, going BEYOND what is REQUIRED.&nbsp;
Once again, note the moderation. The text does not say, &quot;Go as many
miles as he wants you to go.&quot; Christ is exhorting us to an
attitude of service, grace, letting people have space, and generosity. But
NOT to the extreme. There is still plenty of space for tough love,
not being exploited, knowing how to set boundaries.</p>
<p>If you think about it, Christ IS advocating setting boundaries. But He does not want us to be concerned about others going over the line as much
as he does those who CLEARLY go indisputably way over that line. We
give others the benefit of the doubt, but we have our limits.<br><br>
<li>Practicing Responsible Generosity
<p>I believe the part of the Law Christ was addressing was <b> Deuteronomy
15:9-11</b>:</p>
  <blockquote>Be careful not to harbor this wicked thought: &quot;The seventh year, the
year for canceling debts, is near,&quot; so that you do not show ill will toward
your needy brother and give him nothing. He may then appeal to the L<span class="footer">ORD</span> against
you, and you will be found guilty of sin. Give generously to him and do so
without a grudging heart; then because of this the L<span class="footer">ORD</span> your God will bless you
in all your work and in everything you put your hand to. There will always be
poor people in the land. Therefore I command you to be openhanded toward your
brothers and toward the poor and needy in your land.</blockquote>
<p>Christ was not talking about giving handouts to every stranger or pan-handler
around. He was saying that we do have a responsibility to loan money or
offer help to responsible people (destitute because of circumstances beyond
their control) we know whether we will be paid back or not.</p>
<p>Whether &quot;asking&quot; and &quot;borrowing&quot; are merely parallel
terms or not is a matter of debate. We should feel under no
obligation to give money to anyone who asks us for handouts. If you
thought otherwise, a five minute trip to Chicago would find you completely
cleaned out.</p>
<p>Remember, Christ spoke at great lengths about these matters, we have only
sermon highlights. Do not confuse truth with whole truth.</p>
<p>If I am right, that Christ was addressing <b> Deuteronomy 15</b>, then we can see what was
at issue: not the worthiness of the destitute person; it is assumed that
he is worthy; what is at stake is the uncertainty of being paid back. Folk wisdom is true:
do not loan out money you cannot do without. On the positive side, we cannot let responsible people we know go hungry.</ol>
<p><b>II. Being Stingy With Hatred
(</b><a href="javascript:scripture('../scripture/matt54348.htm')" target=_self class=pop>43-48</a><b>)</b></p>
<ol>
<li>Hatred and love can co-exist&#151;see
<a href="javascript:scripture('../scripture/ps55.htm')" target=_self class=pop>Psalm 5:5</a>&#151;a
time to love and a time to hate
<p>Based on
<a href="javascript:scripture('../scripture/ps55.htm')" target=_self class=pop>Psalm 5:5</a>
and other texts, God both hates (is angry at, has wrath
stored up toward) sinners and yet loves them
(<a href="javascript:scripture('../scripture/john316.htm')" target=_self class=pop>John 3:16</a>).<br>
<li>Loving an enemy means choosing to let our love OVERRIDE our feelings of
disgust.
<p>Not all forms of love are the same. The secret is NOT the Greek word,
but the context<br>
<li>This love is in the form of duty, not feeling<br>
<ol type=a>
<li>note
<a href="javascript:scripture('../scripture/matt545.htm')" target=_self class=pop>vs. 45</a><br>
<li><a href="javascript:scripture('../scripture/ex2345.htm')" target=_self class=pop>Exodus 23:4-5</a><br>
<li><a href="javascript:scripture('../scripture/rom121821.htm')" target=_self class=pop>Romans 12:18-21</a>
</ol>
<p>We do not love our enemy the same way we love God, our spouse, our children,
our brethren, or our neighbor.<br>
<li>Love includes greeting others
(<a href="javascript:scripture('../scripture/matt54647.htm')" target=_self class=pop>46-47</a>)<br>
<ol type=a>
<li>important way to show love; we must change habits<br>
<li>including new people (not just greeting our friends.)</ol>
<p>It is great to have a circle of friends; it is horrendous to never include
others or expand ones relationships; Greeting people is IMPORTANT
and crucial toward LOVING PEOPLE. Let me say it again: if you don't schmooze, you (and they) lose.</ol>
<p><b>III. Valuing Godliness Above Earthly Status
(</b><a href="javascript:scripture('../scripture/matt548.htm')" target=_self class=pop>48</a><b>)</b></p>
<ol>
<li>Even though none of us CAN be perfect, that should still be our aim.<br>
<li>Bow and Arrow&#151;can't always hit bull's eye, but can try<br>
<li>Our models, icons, and heroes influence us more than we think<br>
<li>Television sets the &quot;status&quot;&#151;causing great financial stresses.<br>
<li>Aiming to be like God, not some upper middle class singles on TV</ol>
<p>You can aim for designer clothes, a house in that exclusive neighborhood,
that brand of car which shouts, &quot;prestige&quot; or you can aim to be like
God. You cannot aim at both.</p>
<p><b>Conclusion</b></p>
<ol>
<li>You cannot follow Christ and use society's operating system.<br>
<li>You cannot be out to take advantage of or regulate others.<br>
<li>Christ calls you to be generous with grace, stingy with hatred, and to aim at
the right target: knowing God and being godly.</ol>

