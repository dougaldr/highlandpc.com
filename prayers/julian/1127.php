<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 27
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>I know that You, Almighty God, are the Fountain of All 
Wisdom. You are wise in every meaning of the Word. No one is more in touch with 
reality than You are. You completely understand cause and effect, You know the 
consequences of choices Your creatures make, You understand discretion, 
prudence, reason and are more rational than the sum total of all creation.</p>
<p>Lord, help me to draw from Your wisdom. Your Word tells me 
that You enjoy giving Your children wisdom; we need to ask for it, cultivate it 
and value it. So often the people who value wisdom are wise, while those who do 
not seek wisdom are typically foolish. So help me seek wisdom as You direct in 
Your Word, particularly with the book of Proverbs.</p>
<p>Lord, thank You that prayer changes me. Thank You that 
prayer often changes events. Although You will not grant our prayers when 
outside Your will, there are many things You will only give us IF we pray. So 
often we have not because we ask not. So today, I am asking!</p>
<p>Thank You, Lord, that I need others to be happy and 
fulfilled. Sometimes I feel lonely and alone; help me not to cower in a corner, 
but help me to jump into my church, neighborhood and community. You made me to 
be social, just as You are social. Help me nurture that social part of my being 
and not cower in fear.</p>
<p>Help me remember that our church is a body and that I have 
a part to play. Help me to be forbearing, to confront 
when necessary and not to be touchy. Help me maintain a realistic view about 
human nature even among the regenerate, so that I do not hold false expectations 
that will be frustrated.</p>
<p>Father, I remember persecuted believers everywhere. Many 
thousands have died for their faith over the last few decades and the war of 
abuse continues. Help believers in countries where persecution is part of 
government policy—or where the government encourages others to do the 
persecution and looks the other way—to be especially wise at averting it. Help 
those who cannot avoid persecution without denying Christ to be strong and firm. 
Empower them for the trials that await them. We pray that the persecutors would 
themselves be won to Christ and that persecuting nations would be won over to 
the idea of true religious freedom. We pray especially for Guatemala, the Muslim 
portion of the Philippines, Eritrea, Kurdistan and Ethiopia, nations 51-55 of 
the 86 nations noted for persecution. Lord, please intervene as Your children 
suffer and die for the Gospel. I ask You to stop the persecution.</p>
<p>Lord, I pray for The Fellowship of Faith for the Muslims, 
&quot;prayer, information and literature service for Christians who are concerned 
with sharing the Gospel with Muslims.&quot; They are an interdenominational, 
international service ministry. Although they do not send out missionaries, 
they help equip others to share their faith with Muslims. O God, bless this 
work, I pray.</p>
<p>Help me show love to those I come in contact with on a 
daily basis, I ask in Jesus’ Name, Amen.</p>

