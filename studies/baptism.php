a predicament where the very Savior of the world does not demonstrate proper 
evangelism for the church era He was about to found. That there is a difference 
between God's dealings with Israel and the Church seems clear enough to me, but 
Jesus DID say He was about to build His church and He did train His apostles to 
lead that church.</p>
<p>What did change after 
