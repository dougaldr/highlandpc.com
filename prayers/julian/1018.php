<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 18
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Thank You that You are a Mighty Rock to Whom I am anchored 
(<a href="javascript:scripture('../../scripture/heb619.htm')" target=_self class=pop>Hebrews 6:19</a>). 
By faith, I have a grip upon the Rock of Ages. I will 
not be swept away by the storms of life.</p>
<p>I also thank You, Lord, that I enter into Your holy of 
holies every time I address You in prayer. In Old Testament times, only the 
high priest could enter the Holy of Holies and that only once a year. On Yom 
Kippur, the Day of Atonement, he would sprinkle the blood of a goat on top of 
the Ark of the Covenant, the Mercy Seat. This would cover over the sins of the 
people for another year, rescuing them from Your wrath while on the earth. But, 
my God, Jesus Christ has become the one and final sacrifice to avert Your wrath 
in this way. He not only covers over my sins, He has removed them! That is why 
I can barge into Your heavenly Holy of Holies without so much as an 
appointment. I am forever cleansed by the blood of the Lamb.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________. Let me remember that You are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>Lord today, I especially remember those looking for a job. 
Open doors of opportunity and match the right person to the right job. 
Encourage those chronically unemployed, I ask.</p>
<p>Also, dear Father, help me to handle my finances well. 
Guide me when it comes to saving and giving also help me know when to say no. Help 
me control my money under Your guidance; please, do not let money control me.</p>
<p>Lord, today I remember the 46 countries that make up the 
Latin American and Caribbean region. First, I praise You that 10.6% of this 
population claims to be born-again evangelicals. I also thank You that the 
evangelical church is growing at a four percent rate! At that rate, the church 
will double every 18 years. But the challenges are many: the moral consequences 
of drug trafficking, the great poverty and nations postured for upheavals like 
Cuba or Haiti and nations trying to recover from traumatic wars or disasters 
like Nicaragua, Honduras, and Paraguay. In many instances, evangelical 
Christians are in positions of influence. They need Your wisdom, Lord, for the 
problems are complex. Please continue to grow Your church in South America 
and may the Gospel completely transform these societies, I ask.</p>
<p>Today I pray for the evangelical missionaries who are 
serving Christ around the world and hail from my county or district. O Lord, 
keep them true to You. Provide for them, protect them, empower them and use 
them to Your glory, I ask.</p>
<p>Now help me to live out what I believe today, I pray in 
Jesus’ Name, Amen.</p>

