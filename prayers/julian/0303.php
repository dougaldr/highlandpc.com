<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 3
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Lord,</p>
<p>You are the Owner of all. Not only do You own the cattle 
on a thousand hills, You own the entire universe with its vast and countless 
stars. You own all, for You created all. You are the Uncaused Source of all 
events. Nothing has its being apart from You. You own me by virtue of 
creation but also by virtue of redemption. You have purchased me with the 
blood of Jesus Christ. I have been bought with a price, therefore You expect me 
to glorify You in my body.</p>
<p>Thank You that my body is not evil. Although my whole 
being is affected by sin, my body is no more affected than my soul and mind, for 
Your Word tells me that the heart is deceitful and desperately wicked. But my 
body can be used for good or evil. Help me not accept the lie that the physical 
is evil and the spiritual is good. After the six days of creation, You saw all 
You had made, and it was all good, including the human body. But my sinful 
nature, the flesh, refers to that invisible operating system, that mentality 
leads both my body and my mind in directions contrary to You. And physical 
deprivation can do nothing to control that nature 
(<a href="javascript:scripture('../../scripture/col223.htm')" target=_self class=pop>Colossians 2:23</a>). 
No, it is by walking in the Spirit that I can put to death the deeds of the flesh 
(<a href="javascript:scripture('../../scripture/gal516.htm')" target=_self class=pop>Galatians 5:16</a>).</p>
<p>Lord God, I have many requests to present before You, a few 
of which are: __________.</p>
<p>I also need motivation, direction, perseverance, wisdom, 
and Your Spirit’s power in my life. Both the routines of life and the 
challenges of life have a way of wearing me down. I need to have the attitude 
that says, “Do not grow weary in doing well.” Lord, I confess my weariness, and 
look to You to reinvigorate me.</p>
<p>Today I pray for the many students 
in the world, especially those on college campuses in lands other than their 
homelands. In the world, there are over 50 million such students in 46,000 
universities and colleges. Few of these know Christ as Savior.</p>
<p>Bless and use the International 
Fellowship of Evangelical Students to impact these campuses. The Navigators has 
made an impact on these students, as have organizations like Campus Crusade for 
Christ and InterVarsity Christian Fellowship. Also bless the efforts of groups 
that attempt to place Christian faculty members in universities around the 
world. </p>
<p>Lord, we know that a sampling of the 
world comes to universities in the U.S. We pray that the evangelical community 
there would extend warm welcomes and befriend these individuals. We pray that 
many will come to know You, but even among those who do not, we pray that the 
claims of Christ would be grasped, and that they would return home tolerant and 
favorable toward Christianity. We pray that many such individual would later 
accept Christ.</p>
<p>Help 
me keep You in mind today, I pray, in Jesus’ Name, Amen.</p>

