<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Heart of the Gospel: 
<br>Propitiation and Romans 3:24-26 - Part II
<br><span class=byln>by Ed Vasicek</span></p>

<p>Many church members know that Christ died--that's history--but fewer understand that Christ died for 
<i>our</i> sins--that's theology. But what 
did the death of Christ (the atonement) accomplish? Probably more than we can 
imagine. One basic and significant consequence of Jesus' sacrificial death is 
the <i>propitiation</i> He made.</p>

<p>In part one of this series, we began dissecting what is 
arguably one of the Bible's most crucial passages. We defined three of the terms 
mentioned in this text: justification (the legal act in which God declares us 
&quot;not guilty&quot;), redemption (the payment of a price to obtain another's freedom 
or to restore that which was lost), and grace (God's generosity and favor 
apart from anything we do). This is made possible because of today's potent 
word, propitiation. But first, let's review the text of <b>Romans 3:24-26</b>:</p>

<blockquote>Justified by his grace as a gift, through the redemption 
that is in Christ Jesus, whom God put forward as a propitiation by his blood, to 
be received by faith. This was to show God's righteousness, because in his 
divine forbearance he had passed over former sins. It was to show his 
righteousness at the present time, so that he might be just and the justifier of 
the one who has faith in Jesus. (ESV)</blockquote>

<p>So what do we mean when we say that Christ's blood 
<i>propitiated</i> God's justice? Because it is unpopular to believe in an angry God, 
more and more theologians are distancing themselves from the obvious teaching of 
Scripture that Jesus died <i>to pay the penalty </i>for our sin. The idea that 
God is holy and requires a penalty be paid (to settle our sin debt) does not sit 
well with those who believe God's only significant attribute is love.</p>

<p>Before we look at the term &quot;propitiation&quot; (or &quot;sacrifice of 
atonement&quot; as per the NIV) we need to rehearse what Paul had just written to the 
Roman believers. Paul painted a picture of an angry God full of wrath. This 
wrath is revealed from God's abode, heaven 
(<a href="javascript:scripture('../scripture/rom118.htm')" target=_self class=pop>1:18</a>). 
God handed corrupt mankind 
over to horribly sinful lifestyles as an act of judgment 
(<a href="javascript:scripture('../scripture/rom124.htm')" target=_self class=pop>1:24</a>). 
No unsaved 
person will escape God's judgment 
(<a href="javascript:scripture('../scripture/rom33.htm')" target=_self class=pop>3:3</a>). 
Later in 
<a href="javascript:scripture('../scripture/rom59.htm')" target=_self class=pop>Romans 5:9</a>, 
Paul tells the believer that it is God's wrath from which he is delivered (saved). Although we are 
sinners (as bad as that is), our greater problem is that God is angry and raging 
at us. Though the Scriptures repeatedly assure us that God loves us, there is 
another sense in which He hates us: &quot;you hate all who do wrong&quot; (<b>Psalm 5:5b</b>). We 
are truly &quot;sinners in the hands of an angry God.&quot; Those who reject God's burning 
rage find it easy to redefine what propitiation means.</p>

<p>The idea of propitiation (Greek, <i>hilasterion)</i> is <i>
a satisfaction of God's justice and wrath through the payment of an appropriate 
penalty.</i> The death of Christ averts God's wrath toward the believer because 
the sacrifice of Christ (on our behalf) satisfies God's holy justice.</p>

<p>Christ took our sins upon Himself (<b>2 Corinthians 5:21</b>, 
<a href="javascript:scripture('../scripture/1cor1513.htm')" target=_self class=pop>1 Corinthians 15:1-3</a>, 
<b>1 Peter 2:24</b>). The <b>First Peter</b> imagery is especially picturesque: &quot;He 
himself bore our sins in his body on the tree.&quot; Our sins were &quot;in his body.&quot; <b>2 
Corinthians 5:21</b> suggests that a transfer occurred at some point: &quot;God made 
him who had no sin to be sin for us, so that in him we might become the 
righteousness of God.&quot;</p>

<p>Many commentators believe that this transfer of our sin 
upon Christ happened when the earth became dark during the crucifixion. Although 
this is possible (in my opinion, even likely), we really cannot produce a 
chapter and verse to advance this theory to fact. It really does not matter if 
we can pinpoint when it happened; what does matter is that it did happen, and 
that Jesus bore our sins while He died on the cross. He was suffering to be the 
ultimate propitiation to satisfy God's just wrath.</p>

<p>In the Torah, when the high priest would practice the <i>
Yom Kippur</i> (Day of Atonement) rituals, He would take two goats and lean his 
weight upon them. This signified both the creation of a substitute and the 
transfer of sin.<sup>1</sup> One goat would be slaughtered and its blood 
sprinkled on the Mercy Seat; the other would be released into the wilderness as 
the Scapegoat. </p>

<p>It is likely that this ritual foreshadowed the work of 
Christ. He is our substitute and our sins were transferred to Him. His death was 
represented by the first goat. His blood was offered to the Father as a 
settlement for the debt of sin. The second goat pictures the burial of Christ: 
our sins were not only paid for, but their memory dissipated and removed.<sup>2</sup> 
The hymnist captures the thought: &quot;Living He loved me, dying He saved me, buried 
He carried my sins far away.&quot;</p>

<p>But was the sacrifice of Christ--which propitiated God's 
wrath--directed toward the Father? The answer is an unequivocal yes for many 
reasons. The simplest reason is that sacrifice is the ultimate form of worship 
(<a href="javascript:scripture('../scripture/rom1212.htm')" target=_self class=pop>Romans 12:1-2</a>), 
and the Scriptures implore us to worship only God and God alone 
(<a href="javascript:scripture('../scripture/matt410.htm')" target=_self class=pop>Matthew 4:10</a>). 
For Christ to have offered His blood to anyone other than God the 
Father would have been blasphemous indeed.</p>

<p>But the Scriptures do not leave us to infer that Jesus 
offered His blood sacrifice to the Father. <b>Hebrews 9:11-12, 14</b> make the point 
obvious:</p>

<blockquote>When Christ came as high priest of the good things that 
are already here, he went through the greater and more perfect tabernacle that 
is not man-made, that is to say, not a part of this creation. He did not enter 
by means of the blood of goats and calves; but he entered the Most Holy Place 
once for all by his own blood, having obtained eternal redemption....How much 
more, then, will the blood of Christ, who through the eternal Spirit <u>offered 
himself unblemished to God</u>, cleanse our consciences from acts that 
lead to death, so that we may serve the living God!</blockquote>

<p>Like a high priest on <i>Yom Kippur</i> (the Day of 
Atonement), Jesus entered as our high priest. But, unlike the Aaronic priest who 
sprinkled the blood of a goat upon the mercy seat, Christ presented His own 
blood. He is both the priest and the sacrifice.</p>

<p>We still have some questions to answer. Does the wrath of 
God mean God is not in control of His emotions? How is the Christian concept of 
propitiation different from pagan concepts? Why wouldn't God forgive us out of 
grace without a price being paid?</p>
<p>How can we be sure that the death of Christ was actually and 
truly the penalty for our sins? Join us next time for part three!</p>

