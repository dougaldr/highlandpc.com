<p>In the magazine, “<i>Israel My Glory</i>,” (May/June 2001), 
Steve Herzig writes about <i>Aliyah</i>. This Hebrew word, <i>Aliyah</i>, 
means to “ascend or go up” and refers to the Hebrew ritual of walking up to the 
platform (<i>bema</i>) of the synagogue, blessing the Scripture scroll, and 
reading a portion from it.</p>
<p>In traditional Judaism, a layman has the responsibility of 
reading a portion of the <i>Torah</i> (Law) during at least four life-cycle 
events: when he makes his <i>bar mitzvah</i>, the Saturday before he marries, on 
the anniversary of the death of a relative, and when his baby is named.</p>
<p>Besides reading the <i>Torah</i> (Law), it is common for 
the synagogue to feature a second reading (or second set of readings) from the 
Prophets and Writings (the rest of the Old Testament); this portion of Scripture 
is called the <i>haftarah</i>.</p>
<p>Up to seven Scripture portions could be read in a service, 
and it was not unusual to honor a respected guest with the privilege of reading 
one of these portions. Jesus was honored in this way.</p>
<p>We know much about how modern synagogues are organized; 
nowadays Scripture portions are clearly delineated. Back then, it may have 
been possible that Christ was able to choose the place on the Isaiah scroll 
where He was to read. It is also possible this portion was assigned to Him.</p>
<p>What is fascinating about the portion He read is WHERE HE 
STOPPED. He did not complete <b>Isaiah 61:1-2</b> because these verses deal with both 
His First and Second Coming. He fulfilled the first portion during His First 
Coming; the latter portion will not be fulfilled until He comes again.</p>
<p>The Old Testament text reads:</p>
<p>“The Spirit of the 
Sovereign L<span class=sm>ORD</span> is on me, because the L<span class=sm>ORD</span> has anointed me to preach good news to 
the poor. He has sent me to bind up the brokenhearted, to proclaim freedom for 
the captives and release from darkness for the prisoners, to proclaim the year 
of the L<span class=sm>ORD</span>'s favor and the day of vengeance of our God, to comfort all who 
mourn...”</p>
<p>Note that Jesus stopped 
after the word “favor,” but before, “and the day of vengeance of our God.” He 
came the first time as the “Lamb of God” to offer Himself for our sins; He will 
come the second time as, “the Lion of the Tribe of Judah” to judge the world.</p>
