<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 16
<br><span class=byln>by Ed Vasicek</p>

<p>Holy One,</p>
<p>You are a virtuous God. When You communicated the Torah葉he Law葉o the 
children of Israel, You revealed Your virtue. Your Word and 
Your deeds reveal the high caliber of Your character. You are true, honest and 
moral in every way, truly a God of integrity. You set the standard. Even though 
we sinful people fall short of that standard, we respect those who aspire to be 
like You when it comes to virtue.</p>
<p>Thank You for the atonement of Jesus Christ. An atonement 
means a penalty has been paid for a sin 
(<a href="javascript:scripture('../../scripture/lev556.htm')" target=_self class=pop>Leviticus 5:5-6</a>), 
and this penalty is accepted on behalf of another 
(<a href="javascript:scripture('../../scripture/lev14.htm')" target=_self class=pop>Leviticus 1:4</a>). 
So in the New Testament we are told that &quot;Christ became sin for us&quot; 
(<b>2 Corinthians 5:21</b>) and that Jesus &quot;bore our sins in his 
body on the tree&quot; (<b>1 Peter 2:24</b>). Truly, &quot;Christ died for our sins&quot; 
(<b>1 Corinthians 15:3</b>). </p>
<p>Father, may others note me for my good character. Help me 
to remember that, &quot;A good name is more desirable than great riches; to be 
esteemed is better than silver or gold&quot; (<b>Proverbs 22:1</b>). Help me to believe that 
verse and help me to live it out.</p>
<p>God, help me to take time to rest, time for fun, to smell 
the roses. Remind me that You do not need me and that the world can get by a 
little while without me!</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I&nbsp;find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Today I pray for the nation of Greenland. The percentage 
of evangelicals is only 1.6% and there are only 4 evangelical pastors in this 
nation of 57,000 people. Thank you that the Jesus film has been translated into 
Greenlandic. O, Father, help the few believers there to reflect Your light and 
may the Bible-believing church triple over the next decade.</p>
<p>Lord, thank You for the Mission to Unreached Peoples as 
they &quot;focus on the unreached peoples of the world who have not heard the 
gospel of Jesus Christ specifically those who live in the 10/40 Window (a 
region of the world containing parts of Asia and Africa with the fewest 
believers).&quot; Thank You for their &quot;225 missionaries in 22 countries.&quot; Bless 
them as they evangelize, disciple and invest their lives in fulfilling the 
Great Commission. Prosper them by providing for their needs, protecting them 
and giving them fruit for their labor. Give the missionaries harmony among 
themselves and empower them by Your Holy Spirit.</p>
<p>I remember those who work in our factories. Lord, keep 
them safe. Help them to be honest, to do their best and help the rest of us to 
appreciate their labors. May those who know You live out the Christian life 
before their fellow workers; help them to freely share Christ with their 
associates.</p>
<p>Now, Lord, pave the way for me today, I pray in Jesus' 
Name, Amen.</p>

