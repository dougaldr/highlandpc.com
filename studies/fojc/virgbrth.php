<p>Back in the Garden of Eden, God promised that the seed of the <b><i><u>woman</u></i></b> 
would crush the serpent's head 
(<a href="javascript:scripture('../../scripture/gen315.htm')" target=_self class=pop>Genesis 3:15</a>). 
Throughout the Old Testament, 
posterity is kept track of <i>through the man</i> or <i>the couple</i>. But in 
<b>Genesis 3:16</b> the promise stands alone because it emphasizes that the Messiah 
would be the seed of the woman <b><u>ALONE</u></b>, at least apart from a man. 
This is the first reference to the virgin birth of the Messiah.</p>
<p>Ancient religions corrupted this theme. Since the events 
of Genesis were passed down generation to generation, it should come as no 
surprise that the accounts varied and were corrupted as societies left the 
belief of one God and corrupted their religions to embrace many gods and the 
worship of created beings (see <b>Romans 1</b>).</p>
<p>
John MacArthur summarizes these deteriorated versions of the virgin birth:</p>
<p>
&quot;.... the Romans believed that Zeus impregnated Semele without contact and that 
she conceived Dionysus, lord of the earth. The Babylonians believed that 
Tammuz...was conceived in the priestess Semiramis by a sunbeam. In an ancient 
Sumerian/Accadian story inscribed on a wall, Tukulti II (890-884 BC) told how 
the gods created him in the womb of his mother.... At the conception of Buddha, 
his mother supposedly saw a great white elephant enter her belly. Hinduism has 
claimed that the divine Vishnu, after reincarnation as a fish, tortoise, boar, 
and lion, descended into the womb of Devaki and was born as her son Krishna.… 
There is even a legend that Alexander the Great was virgin born by the power of 
Zeus...&quot; (MacArthur, John, <u>Matthew</u>, Vol. 1, p. 12).</p>
<p>Those opposed to the truth of Scripture reason thusly: 
&quot;Christianity borrowed the idea of a virgin birth from the pagan religions 
surrounding it.&quot; We interpret matters thusly: &quot;The fact that so many religions 
include a virgin birth indicates a common source centuries earlier, namely the 
promise in the Garden of Eden.&quot;</p>
<p>The 
prophecy of the virgin birth is found in <b>Isaiah 7:14</b>, &quot;Behold, a virgin shall 
conceive and bring forth a son…&quot; Numerous Old Testament prophecies 
deal with the Messiah so that the devout could identify Him when He came. 
Christ fulfilled those prophecies, including being born of a virgin. But 
besides pointing to the Messiah, the virgin birth has theological significance.</p>
<ol>
<li> 
Christ had to be fully human, connected to Adam but not contaminated with a sin 
nature. Jesus was not merely cloned from Mary's genes because He would have 
been a sinner as Mary was. Christ was born a sinless male, though genetically 
connected to the human race of Adam.<br>
<li> 
Christ had the legal right to rule through his legal father, Joseph, the son of 
David.&nbsp; Had the line of David been honored, Joseph would have been king in 
Israel.<br>
<li> 
In this way, Christ was genetically connected to David through Mary, had the 
right to rule because Joseph was his legal father, but still avoided the curse 
pronounced on Joseph's forefather, Coniah (Jehoiachin) in 
<a href="javascript:scripture('../../scripture/jer2230.htm')" target=_self class=pop>Jeremiah 22:30</a>.
</ol>
