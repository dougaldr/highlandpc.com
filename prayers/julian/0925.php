<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 25
<br><span class=byln>by Ed Vasicek</p>

<p>My Heavenly Father, </p>
<p>You are the God Who hears me when I cry out to You. I have 
had times in life when nothing could comfort or help me, when I could not sleep, 
when my heart was grieved. I remembered better times and Your faithfulness and 
it reminded me that You would help me through my current troubles because You 
are a faithful God. It might seem that Your love has vanished, but it is merely 
hidden for a time. You worked in wondrous ways in days gone by: delivering 
Israel from Egyptian bondage, resurrecting Your Son, Jesus Christ, from the 
grave.</p>
<p>In Your holy indignation, You flooded the earth and 
destroyed all those who breathed the air—except for those preserved on the 
ark. You destroyed Sodom and Gomorrah and brought down the walls of Jericho.</p>
<p>You have worked in my life, redeeming me from the slave 
market of sin, paying the price of redemption for my sin. You have directed 
Your wrath toward Your Son Who died in my place, so that I could have His 
righteousness in exchange for my sin. Lord, to even conceive of such a plan is 
something I cannot imagine.</p>
<p>And so, Lord, I find myself today dependent upon You. As 
the hymn writer put it, &quot;I need Thee every hour.&quot; And I freely confess that it 
is so. Thank You that You enjoy being needed. You do not view me as a pest, 
but You love me with a deep love.</p>
<p>Lord, I pray for Christians who genuinely know You but have 
strayed from Your will. You warn us to turn neither to the right nor the left of 
Your perfect will, but we all have experienced our chosen detours. Some slow us 
down a bit and we are quickly back on the straight and narrow. Others turn 
sharply and take us far away from where we need to be.</p>
<p>Today I pray for nations 26 to 30 from the list of the 86 
nations where persecutions of evangelical Christians is a problem. I pray for 
Burma, Libya, Bahrain, Brunei and Turkey. Lord, in some of these nations, it 
is illegal to share the Christian faith. Those who convert do so at the 
jeopardy of their life or wellbeing. In some lands, like Turkey, religious 
freedom is technically espoused, but not necessarily embraced by the 
population. O Father, stop the persecution. Give the believers wisdom to be 
able to evade it and help them to find strength in You when they cannot.</p>
<p>Lord, thank You for Iranian 
Christians International, as they reach out through &quot;friendship and literature 
evangelism, establishing and strengthening Persian language interdenominational 
Bible study groups, churches and organizations, Publishing <b>Mojdeh</b> 
(Good News)...providing counseling, communication, information and referral 
services and monitoring human rights of Christians in Muslim countries and 
providing advocacy on their behalf.&quot; Protect, prosper and empower this 
ministry, I ask. May they effectively train others to reach out to the Muslim 
community. Thank You for the tens of thousands of Iranians who have come to 
know You.</p>
<p>Protect my loved ones and me 
today, I pray, in Yeshua's Name, Amen.</p>

