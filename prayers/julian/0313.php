<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 13
<br><span class=byln>by Ed Vasicek</p>

<p>Precious Lord,</p>
<p>You are with me right now. You have promised never to 
leave me, never to forsake me. You promised Your disciples that You would be 
with us, &quot;always, even to the end of the age.&quot; Yet there is a sense in which 
You are present in heaven in a way in which You are not present on earth. 
Indeed, we look forward to going &quot;to be with the Lord.&quot; This refers not only to 
the splendors of heaven but to experiencing Your presence in a way we cannot 
experience it on earth.</p>
<p>When believers gather in the Name of Jesus, You are there 
in our midst in a way that You are not there when we are alone. And there is a 
sense in which You &quot;go with us.&quot; When we draw near to You, You draw near to us, 
thus affecting Your presence in our lives. When we quench the Holy Spirit, this 
somehow affects Your presence in some way. When we remember the broken body and 
the shed blood of our Savior, it seems as though we experience Your presence in yet 
another way. But whatever the complexities and variations, we thank You that 
Your Son is Emmanuel, God with us. And He is still with us.</p>
<p>Help me to resist temptation and the lures of Satan 
today. Although Satan is not present everywhere, he does cover a lot of ground 
via his multitude of demons. When I stand up against Satan, I am very unlikely 
standing up against Satan himself, but rather against one of his demons. Help 
me to resist the devil and he will flee.</p>
<p>I pray for parents of children who have gone astray. 
Whether in the realm of premarital or extramarital sex, drugs, alcohol, 
gambling, crime, violence, bitterness toward parents, abandoning the faith or 
in any other way, it hurts like nothing else can.</p>
<p>Yet children have wills of their own, and they make 
choices. Perhaps our own past decisions have contributed toward the problem, 
perhaps not. Why we behave as we do or make the choices we do is a complex 
issue, but all of us are capable of great sin, for our hearts are &quot;deceitful and 
desperately wicked&quot; (<b>Jeremiah 17:9</b>), and &quot;The hearts of men are full of evil, 
and there is madness in their hearts while they live&quot; (<b>Ecclesiastes 9:3b</b>). Yet we can 
have victory through our Lord Jesus Christ 
(<a href="javascript:scripture('../../scripture/rom814.htm')" target=_self class=pop>Romans 8:1-4</a>) 
if we present ourselves to Him as a living sacrifice and renew our minds constantly 
(<a href="javascript:scripture('../../scripture/rom1212.htm')" target=_self class=pop>Romans 12:1-2</a>).</p>
<p>I remember the nation of Cyprus in prayer. The division of 
the island into two or one nation has been contested, and there is great 
bitterness over this issue. Evangelical Christians have been the object of 
propaganda from both the dominant Greek Orthodox Church and obviously the Muslim 
population. Less than one half of one percent of the population claim to be 
born-again evangelical Christians. We pray that the people of Cyprus would 
solve their conflicts peacefully and in a way that maintains religious freedom. 
We pray that the number of evangelical Christians would multiply tenfold over 
the next decade.</p>
<p>We remember Answers in Genesis, as they use Scientific 
Creationism to reach the lost and help arm Christians against the onslaughts of 
godless evolutionary theory. We know they are fighting a difficult battle, and 
we pray You would provide for them, that they would be recognized as seekers of 
truth, not merely people with an agenda. Bless the new creation museum in 
suburban Cincinnati, and use it to reach and educate many.</p>
<p>Now, Lord, help me go through my day as working for You, I 
pray, in Jesus' Name, Amen.</p>

