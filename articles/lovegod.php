<p id="pgt"><span class="pgh">Articles:
<br>Spiritual Growth, Devotional</span>
<br>If We Love God, Will We Want To Know Him?
<br><span class=byln>by Ed Vasicek</span></p>

<p>I know a lot about my wife. Because I do, for Mother's Day, I'll
probably grill spare ribs smothered in Yoshida's Sweet 'N Sour Sauce for
her. I know the colors that look best on her&#151;bright ones. Sadly,
I am sometimes oblivious to new earrings and even new clothing that she wears.&nbsp;
After all, I am a man. But many times I do notice. I make a serious effort to
understand her.</p>

<p>I know details about her family, her past, her education,
her hobbies, talents and skills, and I find I am always learning more. No
one on earth knows more about Marylu than I. That's the way it should be.&nbsp;
Why have I learned so much about her? Because I love her! Because I
love her, I expend effort to learn about her. Sounds too obvious to state,
doesn't it?</p>

<p>So, if, when we love a person, we want to find out as much as we can about
that person, is it too much of a leap to suggest that it is no different with
our love relationship to God? Is it possible to love God and choose to
remain ignorant of Him? I do not think so. It is possible to know
volumes about God and not love Him, but I do not think it is possible for most
of us to love God and yet continue to know little about Him. Miniscule
knowledge betrays an insincere heart.</p>

<p>Some people are hindered by circumstances from getting to know about God:
mental limitations, immaturity, health, persecution, or lack of materials
(including Bibles) come to mind. But I have found it sadly true that many,
claiming to know Christ, are only willing to love God with their feelings, not
their minds. And such an incomplete love is not love at all.</p>

<p>If I knew nothing of Marylu's preferences after twenty years of marriage, if
I could not list her parents' names, her favorite foods, or her cherished
dreams, you might tell me that I really didn't love her. And you'd be
right. No matter what my claims about deep feelings, my lack of concern,
evidenced by not spending time with her or listening to her, would expose my
true attitude. It would become clear that mine was a love that was at best
defective, at worst, merely an illusion.</p>

<p>When supposed followers of Christ do not grasp the most elementary truths
about God, I am suspicious. How can you say you love God, and yet spend an
infinitesimal amount of time trying to understand His ways? God has His
plans, concerns, likes and dislikes. It takes a lifetime sometimes for the
genders to begin to understand one another, but God is so much more complex.&nbsp;
Yet we often put in such little effort to understand who He is and what He is
like. Show me a veteran believer who &quot;feels God's presence&quot; but knows
little about who God is (and is doing nothing to remedy his ignorance), and I'll
show you a personification of Christianity's present shallowness. Label me
judgmental, but I don't believe such a person loves God very deeply, no matter
what they claim. Show me a believer who understands something of the
nature of God and I'll show you a person who may or may not love God.</p>

<p>But at least there's a chance he does. Correct me if I'm wrong, but if
we love God, won't we make the effort to understand who it is we love? How
can it be otherwise? If you want to know about God, try meditating on His Word.&nbsp;
(See
<a href="javascript:scripture('../scripture/ps1.htm')" target=_self class=pop>Psalm 1</a>
and
<a href="javascript:scripture('../scripture/ps119a.htm')" target=_self class=pop>Psalm 119</a>.)&nbsp;
Or delve into a little theology&#151;pick
up a book by A.W. Tozer, for example. As you read the Bible, note things
about God's character and dealings. If you're not much of a reader, you
could listen to the Bible on tape and leave your radio set to WIWC (FM 91.7).&nbsp;
Determine to attend church faithfully. You experience God by living life
with Him, but you get to know about Him through studying His Word and becoming
interested in who He is.</p>

<p>If you want to know Him, you must first learn ABOUT Him. It's
impossible to love Him and yet not care about who He is. You wouldn't
accept such logic in human relationships. You would call it nonsense, and
you would be right. It doesn't make any more sense in our relationship to
Christ either. Yet, out of fear of being called judgmental, we patiently
swallow this nonsense. Jesus said, &quot;If you love Me, keep My
commandments.&quot; He didn't bother to say what was so obvious it didn't
need to be stated, &quot;If you love Me, know what My commandments are!&quot;</p>

