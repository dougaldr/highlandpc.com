<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>The Olivet Discourse
<br><span class=byln><b>Notes on Matthew chapters 24-25, Mark 13:1-37, Luke 21:5-36</b>
<br>by Ed Vasicek</span></p>

<p>Some 
people love to study end-time events and all the big words that accompany it, 
such as &quot;eschatology,&quot; which is &quot;the study of last things.&quot; Other Christians 
have sour attitudes. Perhaps the best approach is to study it because it is a 
significant part of God’s Word but not to get hung up there.</p>
<p>The 
lesson that Jesus gave while teaching from the Mount of Olives should be called 
&quot;The End-Times Lecture.&quot; Instead, it has come to be called by the flowery title 
of &quot;The Olivet Discourse.&quot; These portions of Scripture can be very confusing, 
even frustrating. Issues become clearer, however, when we realize (1) Christ 
was asked four questions, and (2) if we have an outline. The four questions 
Jesus answered were:</p>
<ol><li>When shall the Temple be destroyed? 
(<a href="javascript:scripture('../../scripture/matt2423.htm')" target=_self  class=pop>Matthew 24:2-3</a>)
<li>What will the sign be for its destruction? 
(<a href="javascript:scripture('../../scripture/luke217.htm')" target=_self  class=pop>Luke 21:7</a>)
<li>What shall be the sign for your coming? 
(<a href="javascript:scripture('../../scripture/matt243.htm')" target=_self  class=pop>Matthew 24:3</a>)
<li>What shall be the sign of the end of the age? 
(<a href="javascript:scripture('../../scripture/matt243.htm')" target=_self  class=pop>Matthew 24:3</a>)</ol>
<p>
<b>Luke</b> provides the answers to the first two questions 
concerning the Temple. The Romans would destroy the Temple in 70 A.D. 
<b>Matthew</b> provides the answers to the last two questions, which address end 
time events and the return of Christ. Here is the outline of <b>Matthew 24-25</b>:</p>
<ol type=I><li><a href="javascript:scripture('../../scripture/matt2448.htm')" target=_self  class=pop>24:4-8</a>
— The First half of the Tribulation period</p>
<li><a href="javascript:scripture('../../scripture/matt24925.htm')" target=_self  class=pop>24:9-25</a> 
— The Second half of the Tribulation period</p>
<li><a href="javascript:scripture('../../scripture/matt242630.htm')" target=_self  class=pop>24:26-30</a> 
— The Second Coming of Christ</p>
<li><a href="javascript:scripture('../../scripture/matt2431.htm')" target=_self  class=pop>24:31</a> 
— The Gathering of Israel</p>
<li><a href="javascript:scripture('../../scripture/matt243251.htm')" target=_self  class=pop>24:32-51</a> 
— A Preaching Break (parenthetical and out of sequence)</p>
<li><a href="javascript:scripture('../../scripture/matt25130.htm')" target=_self  class=pop>25:1-30</a> 
— Judgment of Israel</p>
<li><a href="javascript:scripture('../../scripture/matt253146.htm')" target=_self  class=pop>25:31-46</a> 
— Judgment of the Gentiles</ol>
<p>
Whether the parenthetical section 
(<a href="javascript:scripture('../../scripture/matt243251.htm')" target=_self  class=pop>Matthew 24:32-51</a>) 
deals with the Second Coming 
to Earth or the Rapture is hotly debated. I personally believe this refers to 
the rapture, which I understand to occur before the Tribulation, when everything 
is normal. Our church is tolerant of a variety of positions about this matter, 
and every position has its arguments. I agree with the suggestion offered by 
Robert Schoenle:</p>
<blockquote>The 
people on the day He is to be revealed to His Church will be eating and drinking 
and getting married, then divorcing and remarrying, just as the people of Noah’s 
and Lot’s day had been 
(<a href="javascript:scripture('../../scripture/luke1730.htm')" target=_self  class=pop>Luke 17:30</a>). 
They would also by buying and planting and 
building 
(<a href="javascript:scripture('../../scripture/luke172729.htm')" target=_self  class=pop>Luke 17:27-29</a>). 
In other words...life will be going on as usual.... We are 
now going to compare this description of the activities on earth at this coming 
of Christ with what the Book of Revelation tells us will have taken place on the 
earth by the time Christ returns physically.... Then we will ask if it is possible 
for anyone...to be unconcerned with other than the usual doings of life, as we 
have known them.... (<u>Warnings from Jesus</u>, p. 37).</blockquote>
<p>
Schoenle’s argument is formidable. Jesus seems to go out of His way, becoming 
almost tedious, to point out this return (the rapture?) occurs during a period 
of normalcy, while His return to reign occurs during a time of great upheaval.</p>

