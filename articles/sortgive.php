<br>Sorting Out Giving
<br><span class=byln>by Ed Vasicek</span></p>

<p>The truth is that giving feels good&#151;at least sometimes. If we are
convinced that our personal sacrifice is doing a significant good, we tend
to be more generous. But giving also betrays a spirit of self-interest: many
of us are anxious to give to something that we see, something that benefits
us directly. Fewer of us get excited about giving to foreign missions.
</p>

<p>
Although the things we can see, including local needs, are to be taken
seriously, we need to remind ourselves that we are building the invisible
Kingdom of God, and we must aspire to reach beyond ourselves. Many of us, I
think, do have this more mature view of serving the Lord. But we are left
with nagging questions. How much should we give? What counts as
&quot;giving?&quot;
</p>

<p>
Some teachers of the Word have easy answers. But the answers, friends, are
NOT easy. I have done little preaching on this subject for several reasons:
</p>

<ol>
  <li type=1>I think some Christians have brought disgrace to the Gospel by their
constant pleas for money. No group is more guilty than television
evangelists, promising everything under the sun if people will give
generously to their cause. It is sickening.
</li>
<br>
  <li type=1>I have seen good organizations manipulate people through guilt trips.&nbsp;
    I
have always resented the squeeze play, and I have tried to avoid anything
that smacks of it.
</li>
</ol>

<p>Let me be frank. I do not know that I understand the Bible's teaching about
giving. Oh, I know plenty of Bible teachers and even scholars who think it
is a piece of cake, but their interpretations do not satisfy me. Yet it is
my job to lead this congregation, to offer direction. So what I am doing
today is simply sharing some of my thoughts about giving.
</p>

<p align="center"><b>To Tithe or Not To Tithe</b>
</p>

<p>
There are a lot of arguments in favor of tithing (i.e., giving 10% off the
top). In the Middle East, in ancient times, it was the custom of the devout
to tithe, though not necessarily to the true God. Abraham seemed to know
this custom, for he naturally paid a tithe to Melchizedek in <b> Genesis 14:20</b>:
&quot;Then Abram gave him a tenth of everything.&quot; Jacob likewise made this
promise in <b> Genesis 28:20-22</b>. &quot;If God will be with me and will watch over me
on this journey I am taking and will give me food to eat and clothes to wear
so that I return safely to my father's house, then the L<span class="footer">ORD</span> will be my God
and this stone that I have set up as a pillar will be God's house, and of
all that you give me I will give you a tenth.&quot;
</p>

<p>
Under the Law of Moses, there were perhaps three tithes, the third being
paid every third year. However, some of this 23% included benevolence and
might be considered more of a &quot;tax&quot; than giving.
</p>

<p>
In the epistles to the churches, tithing is not mentioned, but rather
freewill giving. Paul writes in <b> 1 Corinthians 16:2</b>, &quot;On the first day of
every week, each one of you should set aside a sum of money in keeping with
his income, saving it up, so that when I come no collections will have to be
made.&quot;
</p>

<p>
Note that there is no mention of a tithe in this key text, but rather of
giving in proportion to one's income. Some would argue that this proportion
is determined by a tithe, which would have been understood by the
Corinthians. Since the only Scripture they had were the Old Testament books,
they may have presupposed the concept of tithing.
</p>

<p>
In Old Testament law, tithing was a principle to be obeyed, not a choice to
be made. Godly people were expected to obey the law out of an attitude of
gratitude and love, but obey they must. New Testament giving places more
emphasis on attitude.
</p>

<p>
<b>
2 Corinthians 9:6-8</b> reads, &quot;Remember this: Whoever sows sparingly will also
reap sparingly, and whoever sows generously will also reap generously. Each
man should give what he has decided in his heart to give, not reluctantly or
under compulsion, for God loves a cheerful giver. And God is able to make
all grace abound to you, so that in all things at all times, having all that
you need, you will abound in every good work.&quot;
</p>

<p>
Under New Testament principles, the amount one gives seems to be a personal
decision, in proportion to one's income. This understanding of giving is
called &quot;grace giving.&quot;
</p>

<p>
Abraham and Jacob paid tithes before the law of Moses. Believers are to give
in proportion to their income, and tithing would be a good way to make this
happen. Yet it also seems clear that each of us should determine what we
give with an open heart before God.
</p>

<p>
So do I believe in tithing? Yes and no. I do not believe God demands us to
tithe, but I think tithing is a good starting point. It is consistent with
the example of Old Testament believers, and it is a good way to give
proportionately. Many of us would give less if we were not in the routine of
tithing. Although our lives are not regulated by Jewish law, we are supposed
to find principles from the law for our own application.
</p>

<p>
So we can choose to tithe, freely, as we pour over matters before God. But
we are not CONSTRAINED to tithe. For some, tithing helps them discipline
themselves to give (and there is much to be said for this). For others,
tithing may be unrealistic. For still others, God may expect more than a
tithe.
</p>

<p>
Note this: there are frequent promises of blessing to those of us who give,
and we are encouraged to be liberal in giving. (There is a time for even we
conservative Christians to be liberal!) As Jesus himself said, &quot;It is more
blessed to give than receive.&quot; (<b>Acts 20:35</b>) In a way, our life is to be a
gift of thanksgiving to God, and our finances are part of that gift. Everything we have is God's, and we hold it in trust.
</p>

<p align="center"><b>Sidebar: Pesky Tithing Questions</b>
</p>

<p>
Q: Should 100% of my tithe go the church? If I give money to Moody Radio,
for example, should I deduct that from my tithe?
<br>
A: I don't know. But don't get stuck on the idea of a tithe.
</p>

<p>
Q: If I am in debt because of unforeseen circumstances, is it better to keep
the bill collectors away or give a tithe and be taken to court?
<br>
A: Pay your bills, but change your spending habits. Keep a good testimony of
financial integrity. Don't sabotage your spending so that you can always
claim this excuse. God commands you to be responsible. He may not call you
to tithe&#151;ever. Frankly, some people's means are so limited that they have
no business giving much at all; they must find other ways to give, perhaps
through additional time or talent. For others, God might direct more than a
tithe.
</p>

<p>
Q: I heard a evangelist promise that whatever money I gave would come back
to me, multiplied. What do you think of that?
<br>
A: Then why doesn't he give all his money away? Then he won't have to ask
you anymore!
</p>

<p>
Q: Why is giving called a &quot;sacrifice?&quot;
<br>
A: It should &quot;hurt&quot; to give. If you are not saying &quot;no&quot; to
something by
giving, you are not giving enough.
</p>


