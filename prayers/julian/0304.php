<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 4
<br><span class=byln>by Ed Vasicek</p>

<p>Loving Father,</p>
<p>You are a Glorious God. When Isaiah saw a vision of You, 
You filled the Temple with Your glory. Your glory, or, in Hebrew, Your Shekinah, 
filled the Tabernacle and the Temple. Your Shekinah led the children of Israel 
through the wilderness. Your Shekinah was in Jesus Christ. When the guards came 
to arrest Him in Gethsemane and He claimed the divine name, &quot;I Am,&quot; the guards 
fell back 
(<a href="javascript:scripture('../../scripture/john1848.htm')" target=_self class=pop>John 18:4-8</a>). 
As the songwriter put it, &quot;veiled in flesh the godhead 
see; hail the incarnate deity.&quot; Jesus Christ is the radiance of Your glory 
(<a href="javascript:scripture('../../scripture/heb13.htm')" target=_self class=pop>Hebrews 1:3</a>).</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control me 
today.</p>
<p>Abba, I thank You for fellowship, having something in 
common with You and with fellow believers. It is Jesus Christ we have in 
common. He gives me fellowship with You, and He is why my friends who are in 
the Kingdom of God and I see eye-to-eye on so many things and enjoy one 
another's company. The early church was characterized by teaching solid 
doctrine, eating together and celebrating the Lord's Supper, praying together, 
and enjoying one another's company as they shared life together 
(<a href="javascript:scripture('../../scripture/acts242.htm')" target=_self class=pop>Acts 2:42</a>).</p>
<p>So help me to develop my social skills. Help me learn the 
wisdom that comes from the book of <b>Proverbs</b>, and to become familiar with the 
protocols within my own culture so that I can &quot;become all things to all men.&quot;</p>
<p>Today I pray for the sparsely populated Cook Islands. 
Thank You that about 12% of the population claim to be born-again evangelical 
Christians. Unfortunately, some cults are also significant within the 
population. We pray that You would help the believers to be well-grounded in 
Scripture so that they might both defend the faith and win others to it. Help 
them glorify You by their lives and walks.</p>
<p>Today I pray for the great ministry of Source of Light. 
Their correspondence Bible studies are used by many mission agencies and pastors 
to help ground new believers and lead them to the path of discipleship. Their 
material is used literally around the world and available in a multitude of 
languages. Bless the missionary staff, provide for them financially, healthwise, spiritually, and help them to relate well relationally. May Your Spirit 
keep using this ministry and help it to multiply.</p>
<p>Today we remember all the sports coaches, particularly 
those who are employed as coaches or P.E. instructors. Help them to be good 
role models for team members, help them espouse fairness and honest play. May 
those who are Christians find tactful and appropriate ways to share their faith, 
and use them to win other coaches to Christ.</p>
<p>Now help me know You better today, I pray, in the name of 
the Savior, Amen.</p>

