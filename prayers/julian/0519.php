<br>May 19
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the Beginning and the End. You started the 
events of history; they came to a crescendo when our Lord Jesus died on the 
cross as a curse for us 
(<a href="javascript:scripture('../../scripture/gal313.htm')" target=_self class=pop>Galatians 3:13</a>) 
and then rose again, being boldly declared as the Son of God 
(<a href="javascript:scripture('../../scripture/rom14.htm')" target=_self class=pop>Romans 1:4</a>). 
But one day, the events of history 
will culminate in the return of Christ to reign and eventually a new heaven and 
a new earth will replace the current creation. And just as You were sovereign 
over eternity past, a time before time, we are now here to enjoy Your sovereign 
grace for an eternity future, a time after time. You are the first and the 
last, the beginning and the end, the alpha and omega.</p>
<p>Father, soon millions of young people will be graduating 
from high school or college, and they will scatter in many directions. Lord, 
from a human standpoint, our future is in their hands. May You reach down and 
draw these young adults to faith in You and commitment to serve You and others. 
Guide them as they choose careers, spouses and locations. We pray that this 
new young generation would surprise the world by turning to You and Your values 
and way of thinking. Help them to defy the agendas set by Hollywood and other 
anti-Christian value groups and instead to return to a Biblical faith and 
value system. Grant them great wisdom and insight, I pray.</p>
<p>I pray for my lost friends and family members including: __________. Father, 
please send Your Spirit to draw them to Yourself and grant&nbsp;them the 
repentance that leads to life. Lord, salvation is such a precious gift—I pray 
that they would receive it.</p>
<p>I pray for the nation of Laos. Thank You that a little 
more than one percent of the people claim to be born-again Christians.&nbsp; O Lord, 
double that number over the next two years, I pray. We pray that persecution 
might stop completely and that the anti-Christian government and private 
citizens would recognize these rights You give to all particularly the right of 
religious freedom. Despite the dangers and lack of trained leadership, thank 
You that the church is growing. May Your Spirit change hearts and draw even the 
government leaders to faith in Jesus, I pray. Help the Christians to be shrewd 
and able to avoid persecution. When they cannot, grant them the grace they 
need, I ask.</p>
<p>Lord, thank You for Biblical Literature Fellowship and 
their &quot;vision 
for spreading God's Word in the French language through the printed page.... BLF 
is unique in that it is possibly the only Christian 
publishing house with a vision for reaching Francophone Africa for Christ by 
means of literature to evangelize and disciple the Africans.&quot; Lord, use this 
branch of missions to under-gird other outreach ministries and use them to 
evangelize by simply getting Your printed Word out! Give the missionaries 
unity and harmony, empower them by Your Spirit, provide for them, their 
families and needed resources and protect them, I ask.</p>
<p>Lord, today is Yours. Remind me of that, I pray in name of 
Christ, Amen.</p>

