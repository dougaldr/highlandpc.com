<p>You need to understand that the early church was made up of 
many individual congregations with strong local leaders. As a result, there 
were a number of &quot;local canons.&quot; Some New Testament books were accepted by some 
leaders but not by others; we call these &quot;The Disputed Books.&quot; These include: 
Hebrews (because we are uncertain who the author is, though no one questioned it 
as have originated among the first generation of believers), James (because it 
apparently contradicted Paul), 2 Peter (some doubted whether Peter really wrote 
this and whether it was a first or second century work), 2 and 3 John (perhaps 
debating how useful they were), and Jude (because of its quotation from the Book 
of Enoch). Later, some doubted Revelation because of its unique content and 
style.</p>
<p>There also were a couple of books that some argued should 
be included in the New Testament: The Shepherd of Hermas and the Epistle of 
Clement. These were finally rejected, not because there was a question of 
authorship but because they were written after the apostles had died. The 
Muratorian fragment (ca. 170 A.D.) lists most of our books of the Bible, 
including Jude, Revelation and 1 and 2 John, rejects the Shepherd of Hermas, but 
is silent about Hebrews, James, and 2 Peter. Origen (225 A.D.) accepts Hebrews, 
rejects James, 1 and 2 John, 2 Peter, Jude, but accepts Revelation. Eusebius 
(324 A.D.) only accepts Hebrews (of the disputed books), even rejecting 
Revelation. Cyril (348) accepts all the disputed books that found their way 
into our New Testament except for Revelation. In Syria, the Apostolic Canons 
(380) accept all the New Testament books except for Revelation, but includes the 
Epistle of Clement.</p>
<p>The Gnostic Gospels, mentioned in <i>The Da Vinci Code</i>, 
were all written after the apostolic era by groups of religious cults with their 
special agendas that mixed eastern religion with smatterings of Christianity. 
The oldest of these is really not a gospel, but a collection of supposed sayings 
of Jesus, titled &quot;The Gospel of Thomas&quot; and is early second century. Most of 
the Gnostic Gospels are 3<sup>rd</sup> and 4<sup>th</sup> century fabrications.</p>
<p>It is interesting to note that during the Reformation, 
Luther and Calvin reexamined the issue. They both eventually concluded that 
the early church leaders had done well, although, for a while Luther rejected 
James. He finally accepted it, though he never liked it, and labeled it &quot;an 
epistle of straw.&quot;</p>
<p>So, in a nutshell, here is an honest assessment of the 
canon of the New Testament. These books were accepted as authoritative very 
early on, either immediately or by the mid-second century (as soon as they 
circulated): Matthew, Mark, Luke, John, Acts, Romans, 1 and 2 
Corinthians, Galatians, Ephesians, Philippians, Colossians, 1 and 2 
Thessalonians, 1 and 2 Timothy, Titus, Philemon, 1 Peter, 1 John, and 
Revelation.</p>
<p>So when people claim that the New Testament canon was not 
firmly established until the fourth century, there is some truth in that claim. 
But the heart of the New Testament – the books mentioned above, were no-brainers 
and were accepted. The heart of the canon was established almost immediately.</p>
<p>So do the rest of the books belong in the New Testament? I 
believe so. Hebrews and James, for example, clearly date from the apostolic era, 
and the authors were close associates of the apostles. The case for 2 Peter is 
not quite as clear, though obviously strong enough to satisfy evangelical (and 
other forms of Christianity) scholars worldwide. Readers can find information 
about why these books are authentic and belong in our Bibles by reading the 
introductory section of solid Bible commentaries by publishers like Moody, 
Zondervan, Baker, Eerdmans, IVP, etc. We will address manuscript differences 
and a few other odds and ends in our next installment.</p>  
