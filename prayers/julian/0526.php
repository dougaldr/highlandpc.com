<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 26
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>Your Word describes Jesus Christ as &quot;the Power of 
God&quot; 
(<a href="javascript:scripture('../../scripture/1cor124.htm')" target=_self class=pop>1 Corinthians 1:24</a>). 
Of course He is much more than that. He is God in 
the flesh, He is the Second Adam, He is the Savior드nd a host of other 
descriptions fit Him. But He is also the Power of God. Although He seemed to 
be weak in His death듇e was led like a lamb to the slaughter and suffered the 
pain, humiliation and indignity of death on a cross등hat appeared to be 
weakness was actually power. For by His submission He ransomed us, doomed 
Satan and provided the foundation for a New Heaven and a New Earth. It was soon 
apparent that His weakness was not weakness at all, as He arose powerfully from 
the grave!</p>
<p>Lord, when I am down and depressed, help me not to feel 
guilty about that. The Psalmist did not feel guilty when he cried, &quot;Out of the 
depths I cry to you, O L<span class=sm>ORD</span>&quot; (<b>Psalm 130:1</b>). 
Depression and feeling blue are 
part of life. Even Jesus is described as &quot;a man of sorrows&quot; 
(<a href="javascript:scripture('../../scripture/is533.htm')" target=_self class=pop>Isaiah 53:3</a>). 
There is &quot;a time to weep and a time to laugh&quot; (<b>Ecclesiastes 3:4</b>). But do not 
let me think that my depression will last forever, that I am a complete failure, 
or that I am defective. Lord, those are lies from the devil and I claim the 
protection of the blood of Christ. Help me put on the helmet of salvation so 
that my thoughts become guarded!</p>
<p>I pray for my lost friends and family members, including: __________. 
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>I pray for Christians who are straying from their first 
love or compromising their walk with You. Rekindle the faith they once 
professed and help them fervently love the Savior once again. Remove their 
boring view of serving You and help them see the adventure and fulfillment of 
walking daily in Your light. Stir up their hearts and help them make the 
routines of godliness a part of their lives. I especially remember: __________.</p>
<p>Lord, thank You that .4% of the population of Lithuania 
claim to be born-again evangelical Christians. Father, I would love to see that 
number multiply tenfold over the next decade. But the work is slow. Thank You 
for the freedom to share the gospel and thank You for the growth that has 
occurred, but I pray, Lord, that the pace might grow. Provide for trained 
leaders and more missionaries to work this field. May Your Spirit open hearts 
to a personal relationship with Jesus Christ.</p>
<p>Lord, bless Christians in Action, an 
&quot;international, interdenominational and transcultural evangelical missionary 
society with ministries in 21 countries. Its 300 ministry personnel with the 
goal of planting indigenous national churches carry out personal evangelism, 
discipleship and church planting. Other activities include short-term 
evangelistic teams and medical and dental clinics in needy areas.&quot; Fill them 
with Your Spirit, grant them harmony, protection, provision and much fruit for 
their labor.</p>
<p>Now help me keep You in mind today, I request in Jesus' Name, Amen.</p>

