<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 22
<br><span class=byln>by Ed Vasicek</p>

<p>Precious Lord,</p>
<p>You are a caring God. You are not some disinterested 
spectator, but You care for my needs and me. You share my sorrows and rejoice in 
my praise. When Mary and Martha were grieving over the death of their brother, 
Lazarus, something important happened: Jesus wept. Jesus was “a man of sorrows 
and acquainted with grief.” He felt their pain even though He was soon to 
resurrect Lazarus. He is a high priest in understanding the feelings of our 
infirmities and “was tempted in all points as we are yet was without sin.” 
Your love and care for us is constant.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You. Help me put 
<a href="javascript:scripture('../../scripture/phil48.htm')" target=_self class=pop>Philippians 4:8</a>
to work by letting my mind dwell on beautiful and praiseworthy things.</p>
<p>Lord, help me to get to know my neighbors where I live and 
at work or school. Teach me the importance of participating in my community as 
an avenue to benefit me and also as a way to show love to others.</p>
<p>Thank You for freeing me from the curse of the Law. You 
are the God who justifies the ungodly 
(<a href="javascript:scripture('../../scripture/rom445.htm')" target=_self class=pop>Romans 4:4-5</a>) 
not the law keeper. Indeed, none of us can keep the Law. The Law is good, but we are sold into 
bondage. Thank You that Jesus has kept the Law on our behalf and that since I 
am in Christ I have His righteousness. This righteousness comes to me by faith 
in Christ not my self-effort.</p>
<p>I remember my marriage (if I am married) and the marriages 
of my extended families, friends and church families. Lord, hold them 
together. Helped married couples to lean upon you and to work hard at 
addressing problems and experiencing harmony. I also pray for those who are 
divorced that You would help them to go forward with Your hand in theirs.</p>
<p>Today I pray for the Jewish people who are scattered 
throughout the world. Although many Jews are in Israel most are not. New York 
City, Miami and several other cities in the world including Central and South 
America have Jewish minorities. Lord, we know Paul had a special burden for 
his people, the Jews. The early church understood the priority of preaching to 
the Jew first and then the Greek. O Lord, You have preserved this people from 
repeated attempts at genocide. They have blessed our earth and are the people 
of our Lord Jesus Christ, the Apostles and the majority of the early church. 
In grace, Lord, reach down and bless the physical descendents of Abraham, Isaac 
and Jacob. May growing number comes to accept Yeshua (Jesus) as the Messiah 
(Christ).</p>
<p>Thank You for the work of Peter Deyneka Russian Ministries 
building “Christian partnerships that touch and change lives in communities 
across the former Soviet Union. With headquarters in Moscow, Russia and Wheaton, 
Illinois, Russian Ministries partners with thousands of agencies, churches and 
individuals to bring the love of Christ to hearts and homes across Russia, 
Ukraine and other places in the former Soviet Union.” Bless their staff, 
missionaries and nationals who work together for this cause. Keep them true to 
You, provide for their needs, empower them by Your Spirit and grant them 
harmony among one another.</p>
<p>Lord, I offer this day to You in Jesus’ Name, Amen.</p>

