<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 31
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>I know that You, Almighty God, are the Fountain of All 
Wisdom. You are wise in every meaning of the Word. No one is more in touch 
with reality than You are. You completely understand cause and effect, You know 
the consequences of choices Your creatures make, You understand discretion, 
prudence, reason, and are more rational than the sum total of all creation.</p>
<p>Lord, help me to draw from Your wisdom. Your Word tells me 
that You enjoy giving Your children wisdom; we need to ask for it, cultivate it, 
and value it. So often the people who value wisdom are wise, while those who do 
not seek wisdom are typically foolish.&nbsp; So help me seek wisdom as You direct in 
Your Word, particularly with the book of Proverbs.</p>
<p>Lord, thank You that prayer changes me. Thank You that 
prayer often changes events. Although You will not grant our prayers when 
outside Your will, there are many things You will only give us IF we pray. So 
often we have not because we ask not. So today, I am asking!</p>
<p>Today I ask for energy. Grant me energy to get through my 
day, to do a good job with my responsibilities, energy to enjoy life, to serve 
others, and to grow in You.</p>
<p>I pray for my lost friends and family members, including: __________.
Father, please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Today I pray for the small nation of Belize, bordering 
Mexico and Guatemala. The evangelical church has grown in numbers 
significantly, perhaps to fifteen percent. Yet many of the Christians are not 
maturing in the Lord while some of the people groups have hardly been reached. 
Help the Christians to support local evangelical churches, provide for fulltime 
workers, and reach out to some of the native American groups in the area. May 
the church in Belize double in size and quadruple in commitment.</p>
<p>Lord, today I pray for CAM missions as they seek to 
evangelize and disciple people throughout Central America. We thank You for 
their significant work throughout Central America. We pray that You would 
provide workers, strategy, funding, and harmony for this mission and its 
personnel. We pray that Your Spirit would draw growing numbers to Yourself in 
these areas, and that the level of spiritual maturity among these new believers 
would be outstanding.</p>
<p>We pray today for our surgeons. Although surgery is a word 
we would rather not hear, we pray that You would bless the efforts of these 
professionals as they seek to save lives.</p>
<p>Now, Lord, please 
bless my day with joy and peace, in Jesus' Name, Amen.</p>

