<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 23
<br><span class=byln>by Ed Vasicek</p>

<p>Lord Jesus,</p>
<p>You have the right to rule; all authority is Yours on 
heaven and earth. As God the Son, all things were created by You and through 
You. As the Son of Man, Adam's sinless seed, Your human nature identifies You 
with us. As the God-man, You died on the cross to pay for my sins so that I 
could be redeemed. I have been bought with a price and therefore You want me 
to glorify You. I owe You my allegiance both as my Creator and as my Redeemer.</p>
<p>Lord, give me an insatiable appetite for Your Word. Help me 
to eagerly study and read the Bible. Give me the enthusiasm of the Psalmist in 
<a href="javascript:scripture('../../scripture/ps1.htm')" target=_self class=pop>Psalm 1</a>, 
a desire to meditate on Your Word throughout the day. Help me to 
devour Your Words as exemplified by the Psalmist in the longest chapter of the Bible, 
<a href="javascript:scripture('../../scripture/ps119.htm')" target=_self class=pop>Psalm 119</a>. 
I know that the Bible is the most crucial source for coming 
to know You better and it is the measuring stick of all truth. It is my 
spiritual food. Help me not to suffer malnutrition by neglecting it.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>Lord, I remember my country's chief executive officer (President, Prime 
Minister, Secretary General, etc.) and our legislative and congressional 
branches. May they all favor religious freedom. May they make it easy for us 
to be at peace without compromising our convictions. Help them to govern, rule 
and judge rightly and fairly.</p>
<p>Help those who are seeing Christian counselors. Guide 
these counselors into Your will, save marriages and lives and help people 
adjust to the hardships of life. Lord, help counselors to fear You, thus truly 
offering their clients the counsel of the godly. Help counselors who do not 
know You to be constructive and supportive of their Christian clients.</p>
<p>Lord of the Harvest, thank You for the work in 
Guinea-Bissau. Thank You that religious freedom reigns and though the 
evangelical church is small, only 1.1% of the population, it has a great 
testimony and strong leaders. Use the Jesus film, Christian radio and the 
testimony of these Christians to multiply the church tenfold over the next 
decade. May Your Spirit hover over this land.</p>
<p>Great Shepherd, thank You so much for the strategic 
ministry of SAT-7; their vision is to help the church in the Middle East and 
North Africa grow in number and depth via this television ministry. Lord, this 
area of the world is so hard to reach for Jesus Christ; use this ministry to 
that end in a mighty way. May Your Spirit empower these efforts, protect those 
who serve, provide for them, guide the leadership and bless this ministry with 
harmony.</p>
<p>I pray for all those employed as secretaries. Help them to 
be efficient, honest and ethical. May those who know You live and actively 
share their faith as You open the doors.</p>
<p>Help me to be a peacemaker today, I pray in Jesus' Name, 
Amen.</p>

