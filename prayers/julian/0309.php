<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 9
<br><span class=byln>by Ed Vasicek</p>

<p>Precious Lord,</p>
<p>You are the Father of Eternity. There was a time when there was no time or 
dimension. But You were there. You created height and 
depth, two created entities that cannot separate us from Your love. You created 
time. All events have a cause, but You are not an event. All creation had a 
beginning, but You are not created and therefore not part of creation, though 
You interact with Your creation. You are the Eternal Father.</p>
<p>Lord, I am totally helpless to 
deliver myself from Your wrath. I owe You total obedience to begin with, so if 
I owe You my all and yet sin, how can I make up for sin? How could I give You 
more than the “all” I already owe You? My helplessness and spiritual bankruptcy 
means I am totally depraved, spiritually broke. That is why I needed a Savior 
to rescue me; I could not rescue myself.</p>
<p>Now that I have received, help me 
to learn to both give and receive. Help me give to others of my resources, 
including things like time and energy. When others give to me, help me to 
thankfully receive. Keep me from being one of those persons who wants to owe no 
one but wants others to owe me. Help me to see such egotism and such a 
controlling disposition for what it is.</p>
<p>Please address the following 
needs: __________.</p>
<p>Lord, help me get along with 
those I see most: family, coworkers, friends and neighbors. Help me make extra 
effort to resolve conflict and to initiate fellowship. Keep me from being too 
aggressive or too passive.</p>
<p>Father, thank You for Your work 
in Costa Rica. We are grateful for the 12.5% of the population who are involved 
in Evangelical congregations, and we pray that these churches might become 
solid, avoiding legalism, and maintaining moral and doctrinal integrity. We 
know that there are many born-again believers attending nonevangelical 
churches, and we pray that You would lead them to fellowship with believers of 
like faith instead of trying to change other churches toward their beliefs. We 
ask that the church would develop solid leadership, and that the number of true 
believers would double.</p>
<p>Today I remember The U.S. 
Center for World Mission (USCWM), “a place dedicated to making the glory of God 
known, and to bringing all the people groups on the earth to obedience and 
worship of Him (as revealed in the Lord Jesus Christ). The USCWM produces and 
publishes resources to motivate and equip Christ's body to join Him in His 
Biblical purpose to &quot;bless all the families of the earth&quot; (<b>Genesis 12:1-3</b>). The USCWM also engages in a variety of activities toward ensuring, as soon as 
possible, that each distinct people group is ‘reached’—that a viable movement is 
established to evangelize and disciple each people group.” Lord, bless and 
prosper this ministry and the role it plays in world missions.</p>
<p>Father, bless all those who are having their hours reduced, 
being downsized, or who have experienced financial reversals. Help them turn to 
You and bless them, I pray, in Jesus’ Name, Amen.</p>

