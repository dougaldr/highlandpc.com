<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Disasters, Calamities and Evil in the World: Is God Asleep 
at the Switch?
<br><span class=byln>by Ed Vasicek</span></p>

<p align="left">As a pastor, one of my most challenging responsibilities is 
to comfort the grieving. People can suffer in horrific ways. But one need not be 
a pastor to observe or experience these sad realities. Life has a way of 
educating us all.</p>
<p>The earthquake and tsunami that hit Japan killed thousands 
and left multitudes in utter despair. Many of us are praying for the survivors 
and contributing toward relief to this once great nation now in shambles.</p>
<p>This tragedy resurrects a question we have all heard time 
and time again, &quot;How can a good God allow such things?&quot; Locally, nearby 
tragedies involving our families or people we know bring direct grief into our 
lives. For example, some friends recently lost a nine-year-old child to an auto 
accident. A teen was diagnosed with cancer and died two months later; a recent 
newspaper headline documented that a child in our community died from abuse.</p>
<p>Some grief results from an instance of human choice. Others 
are considered Acts of God. But we return to that well-worn refrain: &quot;If God 
is good, if God is all-powerful, and God knows all things, why do seemingly 
random disasters take place?&quot; Do they take God by surprise, as some assert? Has 
God created the universe and then taken leave, as some of our Founding Fathers 
believed? Or does he not exist, as the modern atheistic movement avers? Or is he 
not good? Or do we humans not understand the definition of &quot;good&quot; as it applies 
to God?</p>
<p>Who among us has not agonized with these questions? 
Although some people bury their heads in the sand and quickly dismiss anything 
requiring intellectual exercise, the question catches up to even the most 
denying of people. Sadly, I have seen persons in their seventies or eighties 
wrestling with this question for the first time.</p>
<p>Ultimately, our choice is to go through calamities and 
trials with God or without Him, but go through them we must. Still, it helps to 
understand what we can of the whys and wherefores about God’s relationship to 
tragedies.&nbsp;</p>
<p><b>First, please note that God takes responsibility for 
tragedies</b>. Although some teachers of the Word disagree with my perspective, 
I believe this is cleared asserted in <b>Romans 1:18-20</b>, &quot;The wrath of God is being revealed from heaven 
against all the godlessness and wickedness of people, who suppress the truth by 
their wickedness.&quot;</p>
<p>This verse is lifted from Paul's argument that natural 
revelation is enough to convince men of their lost condition and the existence 
of a powerful God. I believe this verse is a <i>Midrash</i> based on passages 
like <b>Psalm 29</b>. In my view, God allows disasters and tragedies partly to make it 
clear that his wrath is still burning; God is angry. An honest person trying to 
learn about God from nature would rightly conclude that the Creator is filled 
with rage. Earthquakes, tsunamis, tornados, thunder storms, plagues, famines. All these natural disasters reveal God's wrath.</p>
<p>If we isolate one of God's attributes, like love, we can be 
puzzled why a loving God allows these things. But if we take some of his other 
attributes, like his holiness and justice that are connected to his wrath, we 
could equally be puzzled by this question, &quot;If God is holy and demands justice, why does 
He allow 
sinners to enjoy life at all?&quot;</p>
<p>From this perspective, what we call normal life, or the 
eras of life in which we are healthy, wellfed, and get along with our family 
members, is the result of God's grace. We are not entitled to this. We have no 
right to it. Thus <i>disasters and wrath are what should be</i>, but the 
pleasures and joys of life are gravy. They flow from God's grace and his lovingkindness.</p>
<p>In his Word, God takes the blame for both disaster and 
handicap. <b>Isaiah 45:7</b> reads, &quot;I form the light and create darkness, I bring 
prosperity and create <i>disaster</i>;&nbsp;I, the L<span class=sm>ORD</span>, do all these things….&quot;</p>
<p>In <b>Exodus 4:11</b> we read, &quot;The L<span class=sm>ORD</span> said to him, 'Who gave human beings 
their mouths? Who makes them deaf or mute? Who gives them sight or makes them 
blind? Is it not I, the L<span class=sm>ORD</span>?'&quot;</p>
<p>The reason we struggle with this issue is because of our 
sinful nature. Before our new birth, we are naturally hostile to the <i>true</i> 
God 
(<a href="javascript:scripture('../scripture/rom311.htm')" target=_self class=pop>Romans 3:11</a>,
<a href="javascript:scripture('../scripture/rom87.htm')" target=_self class=pop>Romans 8:7</a>,
<b>Colossians 1:21</b>).
<b>Colossians 1:21</b> makes the case, &quot;Once you were <i>alienated</i> from God and were <i>
enemies in your minds</i> because of your evil behavior.&quot; (Emphasis mine.)</p>
<p>The words that described us before we were born-again and 
the words that define most of mankind, whether part of Christendom or not are 
&quot;alienated&quot; and &quot;enemies.&quot; The natural man hates God in a passive-aggressive 
way. The religious, in particular, feign love for God, but when his distasteful 
attributes surface, they prefer to deny them. It takes regeneration, also known 
as the new birth or circumcision of the heart, to love the true God as he has 
revealed himself. He is not naturally appealing to the sons of Adam, which is 
why most people have replaced him with a modified version. The masses demand a 
kinder, nicer God. Such is not Yahweh our God. As C.S. Lewis' described his 
allegorized Christ, Aslan, the lion, he is kind but far from tame. Redefining 
him in an attempt to tame him is futile.</p>
<p>Have you ever noticed that typically the same people who 
rarely give thanks are often first in line to express their disgust at him when 
disaster strikes? Again, the passive-aggressive hatred springs up through the 
cracks in the façade. </p>
<p>Jesus predicted there would be many wars and earthquakes 
throughout the age and that they would worsen in the end times. Revelation 
talks about the horrible plagues that will make life on earth miserable during 
the Great Tribulation. Biblical Christianity has always been married to a God 
who brings disaster. As long as this marriage is presented in general 
terms, most people accept it, but when reality hits, it is tough to take.</p>
<p><b>Second, please note that disasters are among the 
consequences of the curse.</b></p>
<p><b>Romans 8:18-23</b> reads:</p>
<blockquote><p>I consider that our present sufferings are not 
worth comparing with the glory that will be revealed in us. For the creation 
waits in eager expectation for the children of God to be revealed. For the 
creation was subjected to frustration, not by its own choice, but by the will of 
the one who subjected it, in hope that the creation itself will be liberated 
from its bondage to decay and brought into the freedom and glory of the children 
of God.</p>
<p>We know that the whole creation has been groaning 
as in the pains of childbirth right up to the present time. Not only so, but we 
ourselves, who have the firstfruits of the Spirit, groan inwardly as we wait 
eagerly for our adoption to sonship, the redemption of our bodies.</p></blockquote>
<p>God created man knowing that man would fall into sin and 
that the physical universe would be cursed as a result. When a Christian is 
confronted with evil and disaster in the world, the word &quot;curse&quot; should jump 
into his mind.&nbsp; Many Christians somehow completely forget about the curse. 
Consciousness of the curse is important if we want to embrace a genuinely 
Christian worldview.</p>
<p>Understanding the curse is central to understanding life 
under the sun. As our bodies age or deteriorate, we need to remember that this 
is because of the curse, not just basking in the sun too long or eating too many 
preservatives. Disease, pain, injustice, and natural disasters—all these 
things testify that our planet is cursed by God.</p>
<p>As Phillip Yancey points out in <i>Disappointment With God</i>, 
we know God loves us not because of how our lives go well, but because of 
Calvary. <b>Romans 5:8</b> states it clearly: &quot;But God demonstrates his own love for us 
in this: While we were still sinners, Christ died for us.&quot; In the world we see 
God's wrath and God's curse as well as his common grace and goodness, but in the 
cross we see his amazing love and special grace.</p>
<p><b>Third, consider that we do not know what we do not know
</b>(<b>1 Corinthians 13:12</b>). This may sound like an evasion, but it is actually a 
humble approach; being cognizant of our ignorance is a virtue, not a vice. The 
apostle writes in <b>1 Corinthians 13:12</b>, &quot;For now we see only a reflection as in a mirror; 
then we shall see face to face. Now I know in part; then I shall know fully, 
even as I am fully known.&quot;</p>
<p>Ancient mirrors were quite different from their modern 
counterparts. Besides viewing 3D images in a 2D format, as we would expect in 
mirror, the distortion in these mirrors made the clear and sharp, cloudy and 
tenuous. This is how we now see eternity and the dealings of God. There is so 
much we do not know; so much of our understanding is partial.</p>
<p>Consider that God has all power. That does not necessarily 
mean he can do anything. He cannot contradict his nature: he cannot sin nor lie, 
for example. In addition, his complex plans may make it unwise to do what seems 
like an obvious no-brainer to us. Whatever God is accomplishing might require 
this cursed universe we inhabit to run its course with God altering that course 
only as suits his purpose. Ultimately, <b>Psalm 115:3</b> comes into play: &quot;Our God is 
in heaven; he does whatever pleases him.&quot;</p>
<p>When we cannot understand, we should not deny our feelings 
or live in a dream world. Take the example of Job, a man who could not 
understand God’s ways. He lost 10 children and all his wealth at once. Yet Job 
responded in <b>Job 1:20</b>, &quot;'Naked I came from my mother’s womb, and naked I will 
depart. The L<span class=sm>ORD</span> gave, and the L<span class=sm>ORD</span> has taken away; may the name of the L<span class=sm>ORD</span> be 
praised.'&quot; He faced the reality of loss, did not pretend to like it, but trusted 
God nevertheless.</p>
<p>David haggles out his grief in the <b>Psalms</b>, and one of the 
Psalms of the Sons of Korah,
<a href="javascript:scripture('../scripture/ps88.htm')" target=_self class=pop>Psalm 88</a>,
is called &quot;The Dark Night of the Soul.&quot; 
Notice its bleak conclusion, &quot;darkness is my closest friend.&quot; I hope neither you 
nor I have to go through such a time, but I cannot guarantee that we will not.</p>
<p>We know God loves us, not because of how our lives go, but 
because of Calvary. <b>Romans 5:8</b> puts it clearly, &quot;But God demonstrates his own 
love for us in this: While we were still sinners, Christ died for us.&quot;</p>
<p><b>Fourth, consider that God allows evil for a greater good.</b></p>
<p><b>Romans 8:28</b> communicates this thought, &quot;And we know that in all things God works for the 
good of those who love him, who have been called according to his purpose.&quot;</p>
<p>This promise only applies to those who know God, who are 
called according to his purpose, and yet we know, in another sense, God is good 
to all. <b>Psalm 145:9</b>, &quot;The L<span class=sm>ORD</span> is good to all; he has compassion on all he has 
made&quot;. For now, we will limit ourselves only to God’s plan for his elect.</p>
<p>We can take the fall of Adam and Eve as a case in point. 
God created them, knowing they would sin; this was all part of God's plan. We 
must remember that creation was planned around Jesus being the Lamb of God 
The wise in heart accept commands,
   but a chattering fool comes to ruin. thus the whole plan of salvation brings glory to God. Yet we 
need to face the obvious: people cannot be saved unless they are first lost. 
Thus if the Lamb is to be slain for sinners, we must have sinners.</p>
<p>In Christ, we believers have more than Adam and Eve ever 
had. Not only will we rule over the earth, we will judge angels; we need never 
fear losing our status through sin. Thus, by allowing evil, we experience a 
greater good, and God is glorified in a greater way which is perhaps the 
ultimate good.</p>
<p>We know God loves us, not because of how our lives go, but 
because of Calvary. <b>Romans 5:8</b> puts it clearly, &quot;But God demonstrates his own 
love for us in this: While we were still sinners, Christ died for us.&quot;</p>
<p><b>Fifth, consider that the best perspective is the eternal 
perspective.</b></p>
<p><b>2 Corinthians 4:17</b> states the case. &quot;For our light and momentary troubles are achieving 
for us an eternal glory that far outweighs them all.&quot;</p>
<p>For the believer, temporal life is presented as a struggle 
populated with troubles. In contrast, eternal life is one of glory beyond 
imagination. We should view tragedies as wake-up calls to the human race in 
general and the individual in particular. Tragedies remind us of our 
vulnerability and our need to be right with God, to look at the eternal 
perspective.</p>
<p>In <b>Luke 13:1-5</b>, Jesus suggests that God sends tragedies to 
wake us to shake us:</p>
<blockquote><p>Now there were some present at that time who told 
Jesus about the Galileans whose blood Pilate had mixed with their sacrifices. 
Jesus answered, &quot;Do you think that these Galileans were worse sinners than all 
the other Galileans because they suffered this way? I tell you, no! But unless 
you repent, you too will all perish. Or those eighteen who died when the tower 
in Siloam fell on them—do you think they were more guilty than all the others 
living in Jerusalem? I tell you, no! But unless you repent, you too will all 
perish.</p></blockquote>
<p>Rather than apologize for God or hide from view, Christians 
need to follow the Master's example and use tragedies as a brutal reminder for 
all that life is short, death is sure, and, as the jingle continues, sin is the 
curse, but Christ is the cure.</p>
<p>Tragedies hound us to prepare for eternity and remind us 
that our eternal destiny is more important than our earthly lives, a truth that 
eludes us like a loose helium balloon. We know it is so, but we struggle to hang 
onto this reality.</p>
<p>We know God loves us, not because of how our lives go, but 
because of Calvary. <b>Romans 5:8</b> puts it clearly, &quot;But God demonstrates his own 
love for us in this: While we were still sinners, Christ died for us.&quot;</p>
<p><i>Responding to Tragedy</i></p>
<p>Comprehending this framework does not mean we ignore this 
life; obviously we should try to avoid unnecessary suffering by being wise 
(<a href="javascript:scripture('../scripture/prov108.htm')" target=_self class=pop>Proverbs 10:8</a>).
We should be eager to comfort and help the suffering. When 
major tragedies occur, we should consider giving to a relief agency that will 
help in Jesus' Name.</p>
<p>We must recognize that we are or will be fellow sufferers, 
and that showing practical love to those is distress is a clear mandate from our 
L<span class=sm>ORD</span>. We cannot help but wonder when our turn will come, for such is life in a 
fallen world.</p>
<p><b>Conclusion</b></p>
<p>In Yancey's book, <i>Disappointment with God</i>, the 
author makes a point we would do well to contemplate. When we go through trials, 
we can either go through them with God or without God, but go through them, we 
will. Let us choose to take God’s hand—not bite it—during our worst moments.</p>
  
