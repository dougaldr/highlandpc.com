<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 12
<br><span class=byln>by Ed Vasicek</p>

<p>Precious Lord,</p>
<p>You are reliable. You are never fickle; You are never out 
of energy or ability and You are never oblivious or caught off-guard. You are 
true to Your Word and completely trustworthy. Men will fail and human beings 
can vacillate. People may have good intentions and resolve, but they may lack 
the resources or ability to fulfill their pledges. But You, O Lord, are 
reliable.</p>
<p>Father, I can never be reliable as You are. But help me to 
be faithful and trustworthy. May I hesitate to make promises, but when I do, 
help me to keep them. May I not be “yes” and “no,” or hot and cold. Instead, 
help me to decide using wisdom, to count the course, and then to refuse to turn 
my shoulder from the plow and look back.</p>
<p>Please meet the following needs, requests, and desires as 
You see fit: __________.</p>
<p>God, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I&nbsp;find my 
strength in His power not my own. May Your Spirit control me&nbsp;today.</p>
<p>Father, remind me that I am to be part of the Great 
Commission. I need to pray for missions and either individually or through my 
church I need to support world evangelism financially.</p>
<p>Today I pray for the nation of Germany. Thank You that 
Germany is reunited and that the Gospel may now be shared freely. Yet it 
saddens me that this land of Reformation has abandoned the Gospel. But Father, 
the evangelical church seems to be growing. Although only 2.9% of the 
population is evangelical—in the American sense of the word—hundreds of new 
churches have been planted in both West and East Germany. Lord, send Your 
Spirit to hover over this land and draw men, women, and young people to Jesus 
Christ in record numbers. May the percentage of believers double within the 
next decade.</p>
<p>Lord, thank You for the ministry of Link Care Center, a 
ministry that “provides numerous services to missionaries and their 
organizations. These services range from the Restoration/Personal Growth 
program, to training and consultation services, to crisis debriefing services.” 
Thank You that they also help pastors by offering “therapy and pastoral 
counseling for pastors and their families...[and] to receive pastors, and their 
families, into the full Restoration and Personal Growth Program. Link Care deals 
with all types of issues including individual, marital, children, family and 
church relations issues.” Guide those who minister through this work and may 
Your Spirit grant them direction beyond human wisdom. Use Link Care to 
maintain, restore, or redirect Your servants according to Your will.</p>
<p>I remember politicians in prayer. Although we often think 
of politics as an unpleasant realm, the truth is that some politicians are more 
in tune with You and Your will, O Lord, than others. Help those who will 
further Your purposes to advance and hold back those who will hinder Your 
purposes. May ethics, integrity and conviction become more common in the 
political world and may those who know You argue their points fairly and 
honestly. Help them to be more concerned about pleasing You than advancing 
their careers.</p>
<p>Now I commit my day to my Lord and Savior, Jesus Christ, in 
whose Name I pray. Amen.</p>

