  <p>Dear God,
  <p>Recovering from an affair. Easy words to say. Yet 
  you know my heart is breaking. Lord, I confess to you that what I did was 
  wrong. I sought love from another contrary to the teaching of your Word. 
  Although it felt right, it was an unholy act.</p>
  <p>It started out with friendship. I stopped feeling lonely. I never thought 
  it would become so deep, nor that I would become so emotionally dependent upon 
  someone other than my God and my spouse. But it happened.</p>
  <p>I want to put it behind me, but it is hard. I have given part of my soul to 
  another, and another has done the same to me. And now that I want to get right 
  with you, I have your blessing, but I also have an ache in my soul. I can’t 
  have my lover and my spouse, too. And I can’t have your blessing when I rebel 
  against one of the clearest and boldest teachings of your Word. So I choose 
  You instead. I choose to honor the marital vows I have compromised. I have 
  chosen to break things off, but part of me now feels broken.</p>
  <p>Lord, they say that time heals. They say I’ll get over it as others have. 
  Part of me believes that is true, but part of me is not convinced. Lord, help 
  me believe that You will honor me and help me to be whole again. I choose to 
  trust You, even in my pain.</p>
  <p>Others cannot understand right now. But You do. So I bring my grief and 
  loss to You time and time again, and I reaffirm my commitment to faithfulness. 
  Faithfulness to you, and faithfulness to my spouse. I cannot change the past, 
  but I choose to walk hand in hand with You in the future. </p>
  <p>Yes, I have brought this suffering upon myself and hurt another badly in 
  the process. Yes, my sin has caught up to me. But no, You will not abandon nor 
  forsake me, O God my Savior! You offer me a clean slate and a new beginning. 
  Help me to hold your hand when my eyes are blinded by tears. Lead me, slowly 
  but surely, into a better lifestyle, a deeper love for the one I married, and 
  a life richer than I have ever known. Help me to pursue You as I never have 
  before.</p>
  <p>In Jesus’ Name, Amen</p>

