<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 19
<br><span class=byln>by Ed Vasicek</p>

<p>My wonderful Lord,</p>
<p>The Magi brought the baby Jesus gifts of gold (symbolizing 
His Kingship), frankincense and myrrh. These gifts seemed to be prophetic of 
His Person and Ministry. The frankincense was a rare form of incense. This 
implies that they recognized Him as God in the flesh.</p>
<p>In the tabernacle and later, the temple, the priest would 
offer prayers in the holy place near the altar of incense. In <b>Revelation 5:8</b>, 
we see that in heaven, prayer is represented as incense: &quot;When He had taken the 
book, the four living creatures and the twenty-four elders fell down before the 
Lamb, each one holding a harp and golden bowls full of incense, which are the 
prayers of the saints.&quot;</p>
<p>Prayer is the ultimate act of worship, which is why we 
should pray to no one other than You, my Lord God. For only You are everywhere. 
Only You, O God can hear and answer every prayer. And so we perform our acts of 
worship for You and You alone, Father, Son and Holy Spirit!</p>
<p>I pray for my lost friends and family members including: __________. Father, 
please send Your Spirit to draw them to Yourself and grant them the repentance 
that leads to life.</p>
<p>I pray for the elderly, sickly and shut-in people I know or 
know of including: __________.</p>
<p>Father, help me to value my conscience. When it speaks to 
me, help me to be online. The new nature you've given me has intensified my 
conscience, making the Law of God written on my heart more pronounced. Help me 
to align my conscience with Your will. I do not want to become the weaker 
brother by cringing under the false guilt of an unhealthy conscience. I do not 
want to harden my heart and dull the whisper of conscience.</p>
<p>I remember the African nation of Namibia. Thank You that 
over 10% of the population claim to be born-again Christian evangelicals. Many 
people claim some form of Christianity, but theological liberalism has overtaken 
many churches. As a result, people are turning away from liberal &quot;Christianity&quot; 
while others are Christians in name only; the youth are turning to drugs and 
rejecting Christian values. Thank You that the evangelical Christianity is on 
the increase. Thank You, Lord, but the challenges are great. May Your Spirit 
work mightily in this nation, recall it from a &quot;post-Christian era&quot; and may the 
percentage of evangelicals double over the next decade, I ask.</p>
<p>Lord, bless Medical Ambassadors International as they share Christ’s Gospel 
and &quot;teach specific methods to prevent disease, improve agriculture and develop 
economic resources. Volunteer 'medical ambassadors,' selected by the community 
to receive this training, share what they are learning with others. In this way, 
a process of transformative change is initiated that is community-owned, 
sustainable and which enhances human dignity....Medical Ambassadors is a 
community of people now extending across more than 70 countries, and numbering 
in the tens of thousands.&quot; O Father, give this ministry harmony, success and the 
power of Your Spirit. May they see entire communities change both spiritually as 
they share Christ, socially and economically. Please keep Your hand on this 
ministry.</p>
<p>Now, Lord, I commit this day to You in the Messiah’s Name, Amen.</p>

