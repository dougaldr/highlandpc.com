<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 8
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>Thank You that God the Son is the 
Mighty God, the Father of Eternity, and the Prince of Peace. There is only One 
God, and there is no one like God. But, according to <b>Hebrews 1</b>, the Son is the 
exact representation of God and like God in every way. <b>Isaiah</b> tells us that the 
Messiah would be called, “The Mighty God.” Coupled with His incarnation, He is 
truly Emmanuel, God with us. The idea that there is one God, but that the 
Father, Son, and Holy Spirit are the One God and yet have distinct personas is 
difficult to understand, but it is the summary of what the Scriptures teach. 
Jesus Christ is the Son of Man, and therefore man. Jesus Christ is the Son of 
God, and therefore God. </p>
<p>If our Savior were merely a man, 
He could not atone for our sins, as <b>Psalm 49:7-8</b> says, “No man can redeem 
the life of another or give to God a ransom for him—the ransom for a life is 
costly, no payment is ever enough.” Because Christ was also God and His human 
and divine nature are inseparable, His shed blood was in some sense God’s blood 
(<a href="javascript:scripture('../../scripture/acts2028.htm')" target=_self class=pop>Acts 20:28</a>). 
Thank You for the redemption I have by believing in Jesus 
Christ as my once-and-for-all-time offering for sin.</p>
<p>Lord, I am thankful for many 
things, three of which are: __________.</p>
<p>Today I pray for the evangelical church worldwide, all 
congregations made up mostly of people who claim to have been born-again and 
have a personal relationship with Jesus Christ, who believe in the deity of 
Christ, His death on the cross for our sins, the literal resurrection, who stand 
for the Bible as the inspired and authoritative Word of God, and who believe in 
salvation by grace through faith alone, apart from works. Thank You that 
although Christianity has declined slightly as a percentage of the world’s 
population, evangelical Christianity has grown from 84.5 million (2.8%) in 1960 
to 420 million (6.9%) in 2000.</p>
<p>But, Lord, we know that growth does not necessarily mean 
truth. Indeed, sometimes those who follow the truth decline in number. So 
please, Lord, help evangelical Christians to crave Your Word, to value knowledge 
and doctrine, and to address the questions of our day with intelligence and 
conviction as has been our heritage.</p>
<p>Lord, I remember Trans World Radio (TWR), “the most far-reaching 
Christian radio network in the world. Programs in over 190 languages and 
dialects are aired from more than 2,700 broadcasting outlets around the globe, 
including 14 international transmitting sites, satellite, cable, Internet and 
local AM and FM stations. Every day, TWR’s broadcasts reach millions in over 160 
countries.” Thank You for the over 2,000 team members, and for the great fruit 
of this ministry. Provide for them, protect them, bless them by Your Spirit, and 
help the workers to experience harmony among all. May Your Spirit draw men, 
women, and young people to Christ by using this ministry.</p>
<p>Father, help me 
please You today, I pray, in Jesus’ Name, Amen</p>

