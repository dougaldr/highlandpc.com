<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 31
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When I wake up, You are with me. During the night, 
You did not sleep or even nap. You never have attention deficit even though 
Your attention is divided between every creature and every inch of the universe. 
But Your attention is infinite and when I divide every atom in all creation 
into your infinite attention span, I am left with the quotient “infinity.” How can 
this be? But it is so. When I pray, You hear my prayers with complete and 
intense attention while at the same time listening to the pleadings of all Your 
children. Lord, infinite knowledge and infinite attention—these concepts are 
beyond my understanding. I use the words, but I cannot grasp them!</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God.</i>).</p>
<p>I pray for my lost friends and family members including: __________. 
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. I give them to You and ask You to help me constantly do so.</p>
<p>Lord, help me to get to know my neighbors where I live and 
at work or school. Teach me the importance of participating in my community as 
an avenue to benefit myself and a way to show love to others.</p>
<p>Help me, Lord, to be a peacemaker and not a troublemaker. 
May I be a blessing to my church, my pastor and my friends. Keep me from 
instigating problems, from gossip, slander and disrespect for those in 
authority. When I disagree, help me to disagree agreeably.</p>
<p>Lord, the European nation of Switzerland, with its 7 
million people, is a wealthy nation. But it is spiritually poor. Only about 4% 
of the population is made up of born-again evangelicals despite its rich 
Reformation heritage. The churches have turned away from the Bible. Thank You, 
Lord, for some of the newer denominations that have planted solid churches 
there like the Evangelical Free Church. Bless their efforts with great fruit; 
work in the hearts of the up and coming generations, I ask.</p>
<p>Lord, thank You for the Swiss missions agency, 
Arbeitsgemeinschaft Evangelischer Missionen. They seek to unite Evangelical 
German-speaking Swiss ministries consisting &quot;of approximately 40 mission works 
and 6 theological schools&quot; which stand for the fundamentals of the evangelical 
faith. Lord, bless this work mightily. May Your Spirit again move in Europe 
and may the land of Switzerland become a nation filled with zealous born-again 
believers, I pray. Provide for, protect, empower and grant harmony and fruit 
to the members of the Arbeitsgemeinschaft Evangelischer Missionen.</p>
<p>Help me see Your Spirit produce His fruit of love in my 
life today, I ask in the Messiah’s Name, Amen.</p>


