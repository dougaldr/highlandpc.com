<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 21
<br><span class=byln>by Ed Vasicek</p>

<p>God in Heaven,</p>
<p>Thank You that though You are so much above us in every 
way we can still know You. True, we can never understand You completely. You 
are infinite and the total of the knowledge of Your creatures is not even a 
drop in an immense ocean compared to Your knowledge, Your depth and Your 
greatness. Because we cannot know You fully does not mean we cannot know You 
truly. To know You involves at least two things: to be in personal relationship 
with You and to have learned about some of Who You are. As our relationship 
develops and our knowledge grows, we come to know You better. Throughout 
eternity, our relationship will deepen and our knowledge broaden. But we will 
never reach a limit, never master You, and we will never be bored with You. I 
praise Your Name for that!</p>
<p>Lord, I remember my marriage (if I am married) and the 
marriages of my extended families, friends and church families. Lord, hold 
them together. Helped married couples to lean upon you and to work hard at 
addressing problems and experiencing harmony. I also pray for those who are 
divorced, that You would help them to go forward with Your hand in theirs.</p>
<p>Today I pray for missionaries that my church or I support 
including: __________.</p>
<p>Father, Your Word tells me that, &quot;Pride goes before 
destruction, a haughty spirit before a fall&quot; (<b>Proverbs 16:18</b>). Help me to run 
from pride and to pursue humility which is simply sanely seeing reality from 
Your perspective and looking at things in proper proportion.</p>
<p>I pray for the Asian nation of Nepal, the world's only 
Hindu Kingdom. Even today, persecution is still a problem in this nation though 
it has lessened since the 1990 democratic constitution was enacted. Thank You 
for the 1.6% of the population that claims to be born-again evangelical. Thank 
You for the new opportunities to train leaders and the twelve languages into 
which the Bible is now being translated. Yet there are 83 other languages in 
this nation where translation has not even begun. Bless the outreach of 
individual Christians, literature ministries, tape ministries, radio and the 
Jesus film. May the number of born-again evangelicals triple over the next five 
years, I ask.</p>
<p>Lord, bless the mission of Christian Outreach 
International, &quot;formed in 1984 by a group of believers concerned about the 
spiritual needs of the lost people of the world. The task is twofold—reaching 
the lost and the development and training of believers who are called to &quot;go  
into all the world and preach the Gospel.&quot; [Their] aim is to aid believers in 
being witnesses to the world by reaching people where they are on the streets, 
in sports arenas, at places of business and in their homes....&quot; Bless them, Lord, 
by empowering their efforts by Your Spirit. Provide for them, protect them, 
grant them fruit for their labor and help them to work harmoniously, I pray.</p>
<p>Help me to be at peace with You today, I request in Jesus' 
Name, Amen.</p>

