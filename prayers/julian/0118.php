<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 18
<br><span class=byln>by Ed Vasicek</span></p>

<p>Yahweh, the Great I AM,</p>
<p>I thank You that You are completely capable. Not only do 
You have all power, and not only do You have all knowledge, but You also have 
the ability to coordinate the power with the knowledge to get the job done, and 
to do it perfectly. We thank You that You have the skill and competence to be 
fully capable. Therefore, we know You are able to oversee our lives—miniscule as they seem—in comparison to Your grandeur.</p>
<p>Your Word tells us that there is one mediator between God 
and man, the Lord Jesus Christ. As man, He understands our lot and represents 
us because He is one of us. As God the Son, He is deity, one Person of the 
blessed Trinity. He therefore not only fully understands both perspectives, He 
has experienced humanity: temptation and the limitations of human life, yet 
without sin. Yet He has the infinite understanding and holy character that comes 
from being God the Son.</p>
<p>Lord, Help me develop holiness in my life. Help me to 
consider myself dead to sin and alive to God today.</p>
<p>Father, I have many needs. I need the sense of security 
that only You can give. The news headlines are often frightening and grievous; 
help me to remember that my citizenship is in heaven. Conquer my fears.</p>
<p>Please address the following needs: __________.</p>
<p>I remember the nation of Aruba in prayer. This Dutch 
speaking Latin-American island is noted for its beauty. We pray that this would 
become the case in the spiritual realm as well. We pray that the relatively 
small evangelical church there would double over the next decade, that rivalry 
among evangelical churches would lessen, and we thank You for the religious 
freedom in that nation.</p>
<p>Today we pray for a mission organization that focus upon 
the other side of the world, the African Inland Mission (AIM). Africa has its challenges: 
the spreading of Islam, AIDS, tribal hostility and polarization, a competitive 
spirit among evangelical churches, and poverty. We pray that You would provide 
solid indigenous leaders for the good churches there, that You would push back 
Islam, and that many Africans would become consistently surrendered to Your 
will. Protect, provide, prosper with health, and guide the missionaries who 
served there.</p>
<p>At home we pray for our State Senators. Help them to come 
to know You, to fear You, to govern in light of their relationship with You, and 
to seek the best interest of the people of our state.</p>
<p>Now, Lord, open 
doors for me today, I pray, in Jesus' Name, Amen.</p>

