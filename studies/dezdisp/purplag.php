<p><b>Conclusion</b></p>
<p>Cnidius, a skillful 
architect, building a watchtower for the King of Egypt, caused his own name to 
be engraved upon a stone in the wall in great letters, and afterwards covered 
it, with lime and mortar, and upon the outside of that wrote the name of the 
King of Egypt in golden letters. This was all done by Cnidius, pretending that 
it was for the honor and glory of the King of Egypt. But herein was his cunning: 
he very well knew that the dashing of the water would, in time consume the 
plastering, as it did, and then his name and memory should abide to after 
generations.</p>
<p>So men build monuments to 
themselves, but, in time, their memories fade away, and all that is left is 
God’s Name, for every good and perfect gift is from above.</p>
