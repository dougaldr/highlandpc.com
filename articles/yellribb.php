<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>A Yellow Ribbon or a Scarlet Chord or Both
<br><span class=byln>by Ed Vasicek</span></p>

<p>I remember a surprisingly popular song topping the charts in the early 1970's,
&quot;Tie A Yellow Ribbon Round the Old Oak Tree.&quot; It was
surprisingly popular because it did not sound like a typical 1970's song.&nbsp;
The theme of the song was simple: a man had been in jail for some time.&nbsp;
His time had been served, but would his girlfriend (or wife) still be interested
in him? If so, she was to &quot;tie a yellow ribbon round the old oak
tree&quot; to indicate a positive response. The song ends happily as he
spots a hundred yellow ribbons. She has emphatically demonstrated the
intensity of her love.</p>
<p>But this was not the first time tying a ribbon would signify a choice.&nbsp;
Back in early Old Testament times
(<a href="javascript:scripture('../scripture/josh21718.htm')" target=_self class=pop>Joshua 2:17-18</a>),
we read about a woman named
Rahab. Though notoriously sinful, it seems Rahab had turned from her
sinful ways and believed in the true God of Israel. She completely
cooperated with the Hebrew spies who were surveying Jericho, even though they
were planning conquest; her compliance would have been considered treasonous to
her own people. In return, the spies promised her (and her family) safety
during the attack IF she would hang a red chord from her house. She gladly
complied and was conscientiously delivered.</p>
<p>From that event, theologians have developed an expression to describe a theme
that runs throughout the Bible, &quot;the scarlet thread of redemption.&quot;&nbsp;
I think the expression communicates an important truth: the central theme
of the Word of God is redemption through the Lord Jesus Christ. You can
see hints, clues, and sometimes bold statements made about his blood sacrifice
(scarlet) throughout the ins and outs of the Bible (thread). We preachers
find it easy to incorporate the Gospel into many seemingly unrelated texts
because the scarlet thread is often easy to find.</p>
<p>We are living in a day in which there is growing debate about the PURPOSE of
the Bible. All conservatives agree that the Scripture reveals the will and
plan of God, but the agreement ends there. Seeker-sensitive pastors
sometimes maintain that the Bible is primarily an instruction manual about life,
God's &quot;how to&quot; book for mankind (though they do express the conviction
that the Scriptures do address redemption through Jesus Christ).&nbsp;
Doctrinally oriented ministers frequently advocate that the overwhelming purpose
of Scripture is to exalt the Lord Jesus Christ and reveal the wonders of the
salvation He has provided.</p>
<p>Of course the fallacy in both camps begins with an assumption that I would
challenge, namely, that there is ONE purpose for the Scripture. Take the
scarlet thread of redemption, for example. Beginning with Creation, the
Fall, the Flood, God's calling of Abraham, his work within the nation of Israel,
the Tribe of Judah, the House of David, the Prophets—then the Gospels, Acts,
the Epistles, and culminating in Revelation, it is quite clear that Jesus Christ
is all over the sacred book! He is the most important theme of Scripture,
in my observation. It seems obvious.</p>
<p>Yet who can argue with the practical intent and nature of a book like
Proverbs, or even James? There are portions of Scripture that are
&quot;how to&quot; sections, and we should not arrogantly think we are above
&quot;how to&quot; instruction. Although Jesus Christ IS the central theme
of Scripture, He is not the only theme.</p>
<p>The Scriptures were given to us (all Christians, not merely church leaders)
for many purposes. According to
<a href="javascript:scripture('../scripture/2tim31617.htm')" target=_self class=pop>2 Timothy 3:16-17</a>,
all Scripture is
inspired—not just the words Jesus spoke on earth. Studying,
internalizing, and applying Scripture helps bring believers to maturity and
trains them to do the kinds of works God expects. It corrects the
followers of Jesus Christ, both doctrinally and morally.</p>
<p>Studying Scripture with an open, teachable spirit is an act of worship
(submission) in itself (see
<a href="javascript:scripture('../scripture/ps119.htm')" target=_self class=pop>Psalm 119</a>). Sharing
the message of salvation,
as defined in Scripture, is God's means to bring individuals to a saving
knowledge of Jesus Christ. Studying the Word gives us wisdom and helps us
make wise and right decisions. Memorizing and using Scripture helps us
resist temptation. Implementing the Scripture helps further the Kingdom of
God.</p>
<p>In several of Jesus parables, the Word of God is represented as a seed.&nbsp;
A seed, if you think about it, contains genetic information that can produce a
beautiful or useful plant. It is bursting with potential energy, and,
given the right conditions, keeps both the animal kingdom and we human beings
alive and frisky.</p>
<p>The Word of God is that seed, but we must nurture it and let it take
root. It is not an end in itself, but is meant to produce a harvest of
fruit in our lives. If we neglect the seed, if we do not water it,
fertilize it, or weed around it, its fruit will be diminished.</p>
<p>The mammoth focus of Scripture is Jesus Christ. That's a no-brainer.&nbsp;
But there are other major focuses in the Word as well. Peter puts it in a
nutshell in <b> 2 Peter 1:3-4</b>.</p>
<blockquote>
  <p>&quot;His divine power has given us everything we need for life and
  godliness through our knowledge of him who called us by his own glory and
  goodness. Through these he has given us his very great and precious
  promises, so that through them you may participate in the divine nature and
  escape the corruption in the world caused by evil desires.&quot;</p>
</blockquote>


