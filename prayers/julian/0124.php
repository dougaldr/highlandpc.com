<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 24
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>Thank You that You are a God Who is near and not far away. We do not need to 
find You in some building or shrine, nor must we work ourselves into a frenzy to 
communicate with You. You are near to all who call upon You, to all Who call 
upon You in truth. It is amazing that You can relate to one person as they 
rejoice and praise You, You can be overjoyed when a sinner comes to repentance, 
and at the same time You can sympathize with another Who is grieving the loss of 
one close to that individual. You can give each one of us complete, focused, 
and undivided attention at the same time!</p>
<p>God, I thank You for the assurance of salvation You want me 
to have. Your Word tells me, “These things I have written to you who believe in 
the Name of the Son of God, in order that you might <b>know</b> that you have eternal 
life” (<b>1 John 5:13</b>). Thank You that I can be sure.</p>
<p>Lord, help me to direct my attention where it should be 
focused. I cannot do what You can; if I focus on one situation, I displace 
another. Help me set priorities that are Your priorities and to put my family 
and those nearest to me before everything but You.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>I remember the nation of Lithuania in my prayers. Though I 
know that the people of this land have suffered much under Communism, we know 
that only a small number have been born again by Your Spirit. We pray that the 
evangelical church would double there over the next five years, and that the 
Lithuanian people would come under the deep convicting and drawing ministry of 
Your Spirit. Help the Christians there to be a good testimony, and use them. 
Keep out cults and false religions, we pray.</p>
<p>We also remember the ministry of rescue missions throughout 
the world. Help them to truly rescue lives by the Gospel. Protect, provide, 
and, most of all, Spiritually fill the workers. May the number of genuine 
conversions outpace the number of “convenient” conversions. Encourage those who 
work or support such difficult ministries. Bless them as they offer food, 
clothing, and eternal hope to so many.</p>
<p>Help me today to honor You, I pray, in Jesus’ Name, Amen.</p>

