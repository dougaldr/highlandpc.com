<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 26
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are the Lord our sanctifier, the one Who sets us apart 
and makes us holy. At the point of conversion, when we were born-again and 
exercised faith in Jesus Christ, You gave us the position of holiness. You gave 
us the righteousness of Christ and set us apart for Yourself. Then You, O Holy 
Spirit, began working in me both to will and to do of your good pleasure. You 
help us to become more holy in practice. I praise You as the Lord our 
sanctifier.</p>
<p>Lord, I know Your children will appear before the judgment 
seat of Christ. I will be evaluated for rewards. The Judgment Seat of Christ 
is for believers only and the issue is degree of reward 
(<a href="javascript:scripture('../../scripture/1cor31415.htm')" target=_self class=pop>1 Corinthians 3:14-15</a>,
<a href="javascript:scripture('../../scripture/2cor510.htm')" target=_self class=pop>2 Corinthians 5:10</a>) 
not heaven or hell. This will consist of a reporting in 
and evaluation of my life as a Christian; I will be held accountable for the use 
of my life and gifts including my motives. Then my rewards will be measured 
out. In contrast, the Great White Throne Judgment is mainly for the lost and 
determines the degree of punishment for them 
(<a href="javascript:scripture('../../scripture/luke124748.htm')" target=_self class=pop>Luke 12:47-48</a>) 
and occurs after the Kingdom Age 
(<a href="javascript:scripture('../../scripture/rev201115.htm')" target=_self class=pop>Revelation 20:11ff</a>). </p>
<p>I pray for my lost friends and family members including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>I pray for Christians who are straying from their first 
love or compromising their walk with You. Rekindle the faith they once 
professed and help them fervently love the Savior once again. Remove their 
boring view of serving You and help them see the adventure and fulfillment of 
walking daily in Your light. Stir up their hearts and help them make the 
routines of godliness a part of their lives. I especially remember: __________.</p>
<p>Today I remember the central American nation of Honduras. 
Thank You for the great harvest of souls in this nation! Almost 18% of the 
population is born-again evangelical Christians. This is a poor nation with lots 
of street children and social problems. Help the church to make a difference in 
the nation and to draw even more people to You as a result. Provide 
opportunities for solid leadership training and empower Your children in that 
land.</p>
<p>I pray for the U.S. Center for World Mission, a ministry 
burdened for the “10,000 people groups [who] do not yet have a community of 
disciples among them.... The USCWM produces and publishes resources to motivate and 
equip Christ's body...by gathering, applying and publishing insights.... The U.S. 
Center for World Mission advances shared solutions to common hindrances in the 
mission effort.”</p>
<p>I intercede for urban planners. May they serve their 
communities well. May they be noted for their integrity, far-sightedness and 
realistic approaches. May those who know You live out their faith and share 
Christ and may many in this field come to know You.</p>
<p>Bless my attempts 
to be responsible today, I pray in the Messiah’s Name, Amen</p>

