<p>Jesus compared Himself to a vine and His followers to branches. Ron Moseley 
(<i>Yeshua, A Guide to the Real Jesus and the Original Church</i>) comments upon 
this figure:</p>
<blockquote>At 
the time of Christ, a golden grapevine was draped across the four columns at the 
entrance to the Temple. Josephus records that its beauty was such that it was 
known as a 'marvel of size and artistry to all who saw with what costliness of 
material it had been constructed.' The <i>Mishnah</i> [a collection of ancient 
Jewish rabbinic writings] says that people would sometimes make a freewill 
offering by purchasing a golden leaf, berry, or cluster which the priests would 
attach to the vine. Often those who gave generously to the Temple had their 
names inscribed on the golden leaves. This was a custom that all were familiar 
with in Jerusalem. When Jesus depicted Himself as the <i>True Vine</i>, He was 
	undoubtedly contrasting Himself with this artificial vine, suggesting that 
	if the disciples would offer themselves to Him to the degree that people 
	offered their substance to this golden symbol, the result would be abundant 
	spiritual fruit.</blockquote>
<p>The 
use of the term “fruit” is common in the New Testament. It denotes the produce 
of spiritual maturity: virtues, influences for the Kingdom, depth of character 
and spirit. The genuine Christian produces good fruit; the counterfeit 
Christian does not. Fruit does not make a plant good or evil, but it merely 
reveals the nature of the plant (or branch). Lack of fruit or poor fruit 
denotes insincerity and a hard heart. Note some of the uses of the word “fruit” 
in the New Testament.</p>
<p>
<b>Matthew 7:16</b> “By their fruit you will recognize them. Do people pick grapes 
from thorn bushes, or figs from thistles?” </p>
<p>
<b>Matthew 21:43</b> “Therefore I tell you that the kingdom of God will be taken away 
from you and given to a people who will produce its fruit.”</p>
<p><b>John 
15:8</b> “This is to my Father's glory, that you bear much fruit, showing 
yourselves to be my disciples.”</p>
<p>
<b>Romans 7:4</b> “So, my brothers, you also died to the law through the body of 
Christ, that you might belong to another, to him who was raised from the dead, 
in order that we might bear fruit to God.”</p>
<p>
<b>Galatians 5:22</b> “But the fruit of the Spirit is love, joy, peace, patience, 
kindness, goodness, faithfulness....”</p>
<p>As is 
plain to see, God’s goal for us is to BEAR FRUIT. Or, to put it another way, we 
exist to glorify God, and we glorify God by bearing fruit. Although “fruit” 
does include reaching others for Christ and expanding the Kingdom, fruit-bearing 
is not so much about achievement as it is developing spiritual depth. Fruit is 
what God and others can see in our lives, the result of the Spirit of God 
conforming us to the image of Christ.</p>

