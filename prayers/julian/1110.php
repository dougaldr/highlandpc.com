<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 10
<br><span class=byln>by Ed Vasicek</p>

<p>My gracious Master and My God,</p>
<p>The Savior is referred to in Your Word as &quot;the Lamb of God.&quot; Thank You, 
Jesus, that You laid down Your life for me as a sacrifice to the Father for my 
sins, as my sin offering. You did this freely of Your own will. You likewise 
took Your life up again through the resurrection.</p>
<p>Just as the angel of death &quot;passed over&quot; the homes of the Israelites and the 
believing Egyptians who applied the blood of the Passover lamb to their 
doorposts, so Your wrath passes over all those Who have accepted Jesus Christ 
and are washed in His blood by faith.</p>
<p>Lord, remind me to walk the extra mile in my dealings. Your teaching is a 
moderate one: You instruct me to make extra effort and be gracious, to walk the 
extra mile. But only an extra mile, not an extra twenty miles! So help me to 
extend a reasonable amount of grace to others, but at the same time help me to 
assert myself and not be taken advantage of after walking that extra mile.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 
John 1:9</b>). I accept responsibility for them and freely bring the sins Your 
Spirit brings to my mind now: __________.</p>
<p>Father, I pray for strained relationships. We read of the return of Elijah 
that &quot;He will turn the hearts of the fathers to their children and the hearts of 
the children to their fathers&quot; (<b>Malachi 4:6</b>). Lord, there are many 
strained relationships between parents and children or in-laws. There are many 
other strains between family members or friends all around my community. Father, 
bring about repentance and reconciliation in my family—if this be Your will—or 
prevent these heartaches if all seems to be well now.</p>
<p>Lord, today I pray for the Spanish-speaking world. Most of these people 
reside in Latin America or in the European nation of Spain. Spanish is the 
official language of 20 countries and there are more than 30 countries in which 
Spanish is widely spoken. It is the third most widely spoken language in the 
world with 250 million people claim Spanish as their mother tongue. Thank You 
for the Scriptures in this language and that good evangelical literature abounds 
in Spanish. Thank You that many Spanish-speaking people have come to know 
Christ, especially in Latin America. May these nations become pillars for future 
missionary effort.</p>
<p>I remember OMF in prayer. Founded by Hudson Taylor in the 19<sup>th</sup> 
century, OMF, &quot;is a global network of Christians proclaiming the glory of 
Jesus Christ among East Asia's peoples through fervent prayer, loving service 
and personal witness.&quot; Thank You for the over 1,000 members from 30 nations who 
serve with OMF. Lord, bless the missionaries with harmony, safety, provision 
and much fruit for their labors. Keep them close to You and empower their 
efforts by Your Spirit.</p>
<p>Lord, bless those who work as maintenance and repair workers. Grant them 
safety and may those who know You make the effort to share the Gospel with 
their coworkers, I ask.</p>
<p>Help me to experience Your peace today, I pray in the Name of the 
Cornerstone, rejected by men but precious in Your sight, Amen.</p>


