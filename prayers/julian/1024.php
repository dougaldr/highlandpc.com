<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 24
<br><span class=byln>by Ed Vasicek</p>

<p>My Lord,</p>
<p>You are the God Who speaks. According to Your Word, in the 
past You spoke to our forefathers through the prophets at many times and in 
various ways, but in these last days You have spoken to us by Your Son, whom You 
appointed heir of all things and through whom You made the universe 
(<a href="javascript:scripture('../../scripture/heb112.htm')" target=_self class=pop>Hebrews 1:1-2</a>, altered). 
Thank You, Father, for sending Jesus, the Son, to be my 
Savior. One day He will sit on David’s throne and rule not only over all 
Israel, but over all the earth. And He shall reign forever and ever over heaven 
and earth. He is truly the King of Kings and Lord of Lords.</p>
<p>Lord, I admit I am depraved. I do not use the term as the 
person on the street would, but as a theologian would. I am not as bad as I 
could be. By depraved, I mean I have nothing to offer You to take away my sin 
and make me right with You. Helpless as I was, You helped me by freely 
providing me with salvation and forgiveness through the death of my Lord Jesus 
Christ. Thank You, Lord!</p>
<p>Father, sometimes it is hard to get along with my brothers 
and sisters in Christ. Sometimes maturity or pride issues get in the way. 
Sometimes, it is precisely because we care about Your will that we disagree. 
Who argues a case for something he does not care about? I remember that even 
Paul and Barnabas had to separate because of a disagreement about allowing John 
Mark to accompany them on their next missionary trip. They ended splitting up 
into two separate teams. But Lord, You want Your children to have harmony and 
to be at peace with our brothers and sisters in Christ. Help me take seriously 
the verse that says, &quot;Make every effort to keep the unity of the Spirit through 
the bond of peace&quot; (<b>Ephesians 4:3</b>).</p>
<p>Lord, I have some requests to bring before Your throne. 
They are: __________.</p>
<p>Lord, I am thankful for plenty of things. You have been 
good. Two of my blessings include: __________.</p>
<p>Lord, may Your Spirit fill me. May I find my strength 
in His power, not my own. May Your Spirit control me&nbsp;this day.</p>
<p>O Lord, today I pray for the Uttar Pradesh and Uttaranchal 
States of India, with its massive population of over 167 million. O Lord, this 
is one of the darkest spots on the earth, the home of Hinduism, Buddhism and 
Jainism. The few &quot;Christians&quot; in the region generally were not evangelical. 
Within the past few years, however, the evangelical church has begun to grow, 
although well less than .1% of the population would profess an evangelical 
faith. O God, I plead with You to pour out Your Spirit in the region. Empower 
Your children with mighty power to share the Word and see great fruit. Turn 
this region around. May Your church multiply twenty-fivefold over the next decade, I ask!</p>
<p>Today I pray for all chaplains in my community who are true 
to Your Word, whether they minister in hospitals, jails, community or civic 
organizations. Empower them and use them to lead others to Yourself and to 
strengthen Your children.</p>
<p>Lord, help me to be on the lookout to encourage someone 
today, I pray in Jesus’ Name, Amen.</p>

