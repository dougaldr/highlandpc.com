<li><p><b>What Can We Learn from These Plagues?</b></p>
<ol type=A><li><p>The righteous
often experience judgment with the wicked in this
life.</p>
    <p>
        Distinction
between the
Hebrews and the Egyptians in the 4<sup>th</sup>, 5<sup>th</sup>, 7<sup>th</sup>,
9<sup>th</sup>, and 10<sup>th</sup> plagues 
(<a href="javascript:scripture('../../scripture/ex823etal.htm')" target=_self class=pop>v. 8:23; 9:4, 6, 26; 10:23; 11:7</a>).</p>
<li><p>We depend upon God for
our daily survival. 
    </p>
<ol type=1><li><p>Turning the Nile to blood is a reminder of how vulnerable 
we human beings are; we require clean water to drink or we die.  It doesn't 
matter if we build great monuments or are the most powerful nation on earth.  
We still must depend upon God for our most basic needs, like it or not, admit 
it or not.  Additionally, the righteous are challenged by those who can 
duplicate the plagues.  We must address many competing religions and 
philosophies, and it is not always easy.</p>
<li><p>The magicians are examples of the world, false religion, and the wisdom 
of man; and it is amazing what they can do.</p></ol>
<li><p>God does things His way,
not ours.</p>
<ol type=1><li><p>Didn&#8217;t act for 400 years.</p>
<li><p>Spread out the plagues for 8 months.</p>
<li><p>For some of them, His people had to suffer with the
Egyptians.</p>
<li><p>He orchestrated events so that He would get the glory.</p></ol>
<li><p>God can make us
miserable with
the very things we put above Him.</p>
<ol type=1><li><p>The Egyptians worshipped a frog god, so God made them 
	miserable with frogs.</p>
<li><p>The Egyptians worshipped the Nile, so God made the Nile
detestable.</p></ol>
<li><p>The Pharaoh is typical
of an obstinate sinner.</p>
<ol type=1><li><p>He is confronted
with God but has no interest.</p>
<li><p>When he gets
desperate, He acknowledges God but soon forgets Him.</p>
<li><p>He thinks even God
cannot control him.</p>
<blockquote><p><b>Romans 9:17-18</b> says, &quot;For the Scripture says to Pharaoh: 
	'I raised you up for this very purpose, that I might display my power in you 
	and that my name might be proclaimed in all the earth.' Therefore God has 
	mercy on whom he wants to have mercy, and he hardens whom he wants to 
	harden.&quot;</p></blockquote>
<p>Modern day example: Joseph Stalin, anti-Semitic, but allowed the formation of 
the nation of Israel when he could have vetoed it on the security council. 
Happened to be in a &quot;high&quot; mood and let it pass.</p></ol></ol></ol>
<p><span style="font-weight: bold;">Main Idea:</span> <u>False
religion is often an attempt to control the fears and uncertainties of
life.
But whether we approve of the way He runs the universe or not, only
Yahweh is
Sovereign.</u></p>
<p><b>Conclusion</b></p>
    <p>
Human nature has not changed; some of us reinvent God to
make a controllable deity; others of us are hardened against Him;
others are
deceived by this world's magicians and wise men.
        But when God is good and ready to enter the contest, He
always wins.</p>
