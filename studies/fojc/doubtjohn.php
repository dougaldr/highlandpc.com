<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>The Doubt of John the Baptist
<br><span class=byln>by Ed Vasicek</span></p>

<p>Jesus was tested in many ways. He was “despised and 
rejected of men.” Yet one heartbreak that we often do not consider is how Jesus 
must have felt when John sent a messenger to Him from prison asking, “Are you 
the one who was to come, or should we expect someone else?”</p>
<p>John and Jesus were related. How much they saw one another 
growing up is uncertain, but it is probable that they visited several times a 
year as pilgrims caravanned to Jerusalem, usually gathering by extended 
families. John and Jesus very likely had played together.</p>
<p>When God called John to his ministry of preaching a baptism 
of repentance, Jesus and John had probably not been in recent contact. The 
Father revealed to John that the Holy Spirit would alight on the Messiah in the 
form of a dove. It is interesting to note that John had no doubt that his 
relative, Jesus, was the Messiah. Unlike the people in Jesus’ town of Nazareth, 
familiarity did not breed contempt as far as John was concerned. John freely 
confessed that he was unworthy to even loose Jesus’ sandal.</p>
<p>Although Christ worked no miracle until He turned the water 
into wine at Capernaum, His character was such that John had no problem 
believing He was the Holy One of God. Since John had known Jesus His entire 
life, this validates other references that tell us Jesus was sinless.</p>
<p>But things were not going the way John thought they would. 
He was imprisoned (soon to be beheaded), and Jesus had not yet brought in the 
Kingdom (millennium). Was Jesus really the Messiah?</p>
<p>Jesus did not verbally affirm who He was to John. Instead, 
He offered the evidence of His well-publicized miracles. He was performing 
miracles that only the Messiah could perform. Then Jesus added, “Blessed is the 
man who does not fall away on account of me.”</p>
<p>It surely must have hurt the Savior that John doubted Him. 
Yet Jesus was patient with John because John’s perspective was bleak and 
depressing. Jesus knew that He would be rejected by His people, and that the 
Kingdom of God in its millennial form would not be established immediately. 
John did not. From his bleak prison cell, it seemed the Messiah was failing. 
Yet the real problem was the lack of receptivity of the majority of the people.</p>
<p>One day Jesus will return to establish His Kingdom on 
earth, a day John longed for but never saw. What a wonderful time that will be.</p>
  
