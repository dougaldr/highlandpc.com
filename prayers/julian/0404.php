<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 4
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>I am glad You are a sane God. You are completely 
reasonable and Your mind is never clouded or confused. According to 
<a href="javascript:scripture('../../scripture/ecc93.htm')" target=_self class=pop>Ecclesiastes 9:3</a>, 
there is madness—insanity—within the hearts of men. 
Therefore, to us You sometimes appear to be off. The truth is the other way 
around: You are perfectly sane; we are not. Your ways are higher than ours and 
You see the big picture. So in our confusion, we anchor ourselves to You, the 
completely sane God.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I&nbsp;find my strength in His power not my own. May Your Spirit control 
me&nbsp;today.</p>
<p>Lord, help my church be harmonious. &nbsp;May the leaders be 
firm when it comes to the clear teachings of Scripture and gracious about the 
not-so-clear matters. Keep antagonists, troublemakers and those who resent 
authority away from our congregation. Help my church to be effective in 
evangelism, discipleship, loving one another and world missions.</p>
<p>Help me, Lord, to exemplify the fruit of the Spirit in my 
life. Help me to be patient especially with those to whom I am closest. Help 
me not fall into the trap of respecting those I hardly know and treating my 
spouse, children, parents or good friends with disrespect.</p>
<p>Lord, I remember the nation of French Guiana which is 
still under French domain. This country just north of Brazil has very few who 
claim to be born-again evangelicals, only 3.3%. Eighty percent of the births are out of 
wedlock and there are many people groups without any permanent mission work. 
Having been a penal colony for over eighty-five years, this has been a tough area for the 
Gospel to penetrate. May Your Spirit move over this needy land and may the 
evangelical church there triple over the next decade. Train leaders and help 
the believers to break away from the defeating lifestyle of the area and truly 
espouse Christian values and convictions.</p>
<p>Dear Lord, thank You for the ministry of Far Eastern 
Broadcasting Company, whose mission is to, “bring the Good News of Jesus 
Christ to those who have little or no opportunity to hear about God's love for 
them. Every day [their] radio network broadcasts programming in 150 languages, 
to a potential listening audience of more than 2.5 billion people. FEBC's 
broadcasts are heard in many countries with limited access to Christian ministry 
or tremendous political and cultural opposition to the gospel.” Thank You, 
Father, that this ministry has reached so many considered unreachable. Provide 
for the needs of the ministry, help the workers to work in harmony and may Your 
Spirit empower the broadcasts. We pray that the number of people who come to 
Christ through this ministry would double every year over the next decade and 
that many of those who know Christ would grow in their faith through this 
ministry.</p>
<p>Now, Lord, use me today, I pray in Jesus’ Name, Amen.</p>

