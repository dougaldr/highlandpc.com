<p>Jesus was not the first 
to miraculously multiply loaves. The prophet Elisha worked a similar miracle. 
<b>2 Kings 4:42-44</b> reads,</p>
<blockquote>A man came from Baal Shalishah, bringing the man of God 
twenty loaves of barley bread baked from the first ripe grain, along with some 
heads of new grain. &quot;Give it to the people to eat,&quot; Elisha said. &quot;How can I 
set this before a hundred men?&quot; his servant asked. But Elisha answered, &quot;Give 
it to the people to eat. For this is what the L<span class=sm>ORD</span> says: 'They will eat and 
have some left over.'&quot; Then he set it before them, and they ate and had some 
left over, according to the word of the L<span class=sm>ORD</span>.</blockquote>
<p>In Elisha's case, 20 
loaves were multiplied to accommodate 100 men. Christ multiplied 5 loaves and 2 
little fish to feed 5,000 men, besides women and children! In both instances, 
the loaves were made from barley.</p>
<p>A loaf of bread should 
not be confused with that package of Wonder Bread in your pantry. A &quot;loaf of 
bread&quot; would have been one serving of what we call &quot;pita bread,&quot; (a round, flat 
bread) perhaps equivalent to at most two slices of our common white bread. If 
you have ever eaten a gyros sandwich, that is made with a &quot;loaf&quot; of pita.</p>
<p>The two fish the boy 
offered were probably small salted fish. Additionally, the barley pita bread 
&quot;loaves&quot; were designated as &quot;small.&quot; What the boy shared was a hardy lunch for 
one hungry boy.</p>
<p>Those who do not 
believe in the inerrancy of Scripture try to salvage a few of the Bible's 
miracles as historically-based events that were misunderstood. These 
theologically liberal people tell us that there was no miracle at all. They 
interpret the event as follows: people had brought plenty of food with them, but 
they were stingy and kept it to themselves. When they saw how generous this 
little boy was, they felt ashamed and contributed their food to the group. This 
is <u>absolute nonsense</u>! Like the disciples &quot;…they had not understood about 
the loaves; their hearts were hardened&quot; (<b>Mark 6:52</b>).</p>
<p>If you think about it, 
this liberal interpretation is based on anti-Semitism. Most cultures back then 
had a strong ethic of sharing. The Jews excelled in this area, perhaps more 
than any people of all antiquity, or even modern times, for that matter. 
Preachers who insinuate that these people were holding back food from their 
neighbors until shamed by a little boy understand <i>nothing</i> about Jewish 
ethics. This miracle was simply that: a miracle—a miraculous intervention, 
contrary to the laws of nature, by God Almighty.</p>

