<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 21
<br><span class=byln>by Ed Vasicek</p>

<p>Adonai,</p>
<p>You are the Righteous God. You also do that which is 
consistent with righteousness and justice. You are never corrupt. We can count 
on You to pass the right verdict. You may sometimes be gracious, but even Your 
grace is consistent with Your righteousness. Part of being righteous means You 
are honest.&nbsp; You Yourself set the standard of righteousness, for righteous is 
part of Who You are.</p>
<p>Lord, I am unrighteous in myself, but thanks be that 
because Jesus Christ is my Savior and my representative as the �Second Adam,� I 
am righteous in Him. You choose not to view me as I am, but You view me through 
the prism of the blood of Christ. Though my sin is as scarlet, in Christ You 
accept me as though I were white as snow.</p>
<p>Lord, help me to be a learner throughout my life. 
Obviously I need to learn more about You and Your Word, but there are many 
subjects I must learn about. If I am to appreciate the beautiful things of this 
life, I must learn of them. I must learn how to love my neighbor, impact my 
family and to enjoy the wholesome pleasures of this life to Your glory. I 
must learn how to serve, how to be a peacemaker and how to reflect the light of 
Christ into this dark world. I must learn and pray for Your kingdom work around 
this world.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. I claim my victory because I am identified with the Victor, the Risen 
Christ!</p>
<p>Today I pray for the media. Journalists, newscasters, 
editorial writers: they have so much influence on the way people think, what is 
included in the news and what is excluded, and the interpretational framework of 
reporting. We pray that many of Your children would find their way into 
influential positions in the media so that the Judeo-Christian viewpoint may be 
included and respected in the media.</p>
<p>Please address the following personal needs in my life or 
the lives of those close to me: __________.</p>
<p>Today I pray for the entire continent of Africa. Lord, 
about 14.8% of the population claim to be born-again evangelical Christians. 
Thank You for the many who have responded to Christ in this difficult part of 
the world.</p>
<p>We know, Lord, that Africa is a large continent and varies 
immensely by region. The north is mostly Muslim with a significant Arab 
population. Many portions of Africa have been colonies of other countries, 
mostly British and French. AIDS has devastated much of the population, as have 
famine and �ethnic cleansing.� The people are horrendously poor, governments 
unstable, war common and the church often without strong leadership and 
stability. Lord, we pray that the situation would improve dramatically over the 
next decade. May the church become strong, the believers solid and moral, the 
economies and governments better.</p>
<p>Today I pray for the Moody Bible Institute. I pray for the 
school that trains pastors, evangelists and missionaries including the 
majority of the world�s missionary pilots. I remember Moody Publishing and the Moody Broadcasting 
Network. Bless the president, Dr. Mark Jobe, and provide for this strategic 
ministry.</p>
<p>Now, Lord, help me serve You today, I pray, in Jesus� Name, 
Amen.</p>

