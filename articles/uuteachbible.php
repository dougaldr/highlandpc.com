<br>Using the Bible or Teaching the Bible?
<br><span class=byln>by Ed Vasicek</span></p>

<div>

<p>When leaders from a predominantly liberal denomination were 
criticized for their disbelief in Scripture, their defense was that they used 
more Scripture in their liturgy than any other denomination. We would counter 
that it is one thing to use Scripture, another to believe it, and yet another to 
teach it.</p>
<p>By definition, evangelicals are conservative Christians who 
believe that the Bible is the inerrant Word of God; it is the final authority 
for faith and behavior. Nonetheless, many modern evangelicals are shamelessly 
ignorant of Bible content.</p>
<p>Modern evangelical trends advocate using the Bible as part 
of worship but dismiss actually preaching/teaching Bible content in their 
services. Some Seeker-Sensitive churches go so far as to discourage their folks 
from bringing Bibles to church or church functions because they do not want to 
offend lost people. They may refer to a Bible verse here or there, but they do 
not really teach the Bible.</p>
<p>Should we (A) use the Bible, (B) teach the Bible, or (C) 
both teach and use the Bible in our services? My conviction is choice &quot;C.&quot;</p>
<p>Using the Bible can help us focus upon, and thus worship, 
God. Reading relevant Scriptures before or after singing songs adds meaning and 
authority to those lyrics. That God's Word is associated with worship is clear 
from <b>Psalm 138:2b</b>: &quot;for you have exalted above all things your name <i>and</i> 
your word.&quot; </p>
<p>But do the Scriptures themselves emphasize teaching the 
Word? Let's begin with the three epistles dedicated to church organization and 
practices: <b>1 Timothy</b>, <b>2 Timothy</b> and <b>Titus</b>. These letters to Timothy and Titus 
were actually written to advise church leaders (Timothy and Titus) who served in 
a capacity somewhat similar to the role of modern pastors.</p>
<p>By casually perusing these three epistles, we can note 
several themes surfacing: the importance of Bible teaching, the importance of 
sound doctrine and indoctrination, and the importance of personal integrity and 
leadership skills. We'll focus on only the first two.</p>
<p><b><i>Here are just a few excerpts from 1 Timothy that 
emphasize Bible teaching and indoctrination: </i></b></p>
<p>&quot;If anyone aspires to the office of overseer, he...must be...able to teach&quot; (<b>3:1-2</b>).</p>
<p>&quot;A good servant of Christ Jesus, being trained in <i>the 
words of the faith</i> and of <i>the good doctrine</i>&quot; (<b>4:6</b>).</p>
<p>&quot;Until I come, devote yourself to the <i>public reading of 
Scripture, to exhortation, to teaching</i>&quot; (<b>4:13</b>).</p>
<p>&quot;Let the elders who rule well be considered worthy of 
double honor, <i>especially those who labor in preaching and teaching&quot;</i> 
(<b>5:17</b>).</p>
<p>&quot;<i>Teach and urge these things</i>. If anyone teaches a 
different doctrine and does not agree with the <i>sound words</i> of our Lord 
Jesus Christ and <i>the teaching that accords with godliness</i>, he is puffed 
up with conceit and understands nothing&quot; (<b>6:2b-4a</b>).</p>
<p>&quot;O Timothy, <i>guard the deposit</i> entrusted to you&quot; 
(<b>6:20</b>).</p>
<p><b><i>Second Timothy emphasizes teaching the Bible during church 
gatherings even more so:</i></b></p>
<p>&quot;Follow <i>the pattern of the sound words</i> that you have 
heard from me, in the faith and love that are in Christ Jesus. By the Holy 
Spirit who dwells within us, guard the <i>good deposit</i> entrusted to you&quot; 
(<b>1:13-14</b>).</p>
<p>&quot;What you have heard from me in the presence of many 
witnesses <i>entrust</i> to faithful men who will be <i>able to teach</i> others 
also&quot; (<b>2:2</b>).</p>
<p>&quot;Do your best to present yourself to God as one approved, a 
worker who has no need to be ashamed, <i>rightly handling</i> the word of truth&quot; 
(<b>2:15</b>). (Note here that teaching is predicated upon careful and intelligent 
interpretation.)</p>
<p>&quot;But as for you, continue in what you have learned and have 
firmly believed, knowing from whom you learned it and how from childhood you 
have been acquainted with the sacred writings, which are able to make you wise 
for salvation through faith in Christ Jesus. <i>All Scripture</i> <i>is</i> 
breathed out by God and <i>profitable for teaching</i>, for reproof, for 
correction, and for training in righteousness, that the man of God<sup> </sup>
may be competent, equipped for every good work&quot; (<b>3:14-17</b>).</p>
<p>&quot;I charge you in the presence of God and of Christ Jesus, 
who is to judge the living and the dead, and by his appearing and his kingdom:
<i>preach the word</i>; <i>be ready</i> in season and out of season; reprove, 
rebuke, and exhort, with complete patience and <i>teaching&quot;</i> (<b>4:1-2</b>).</p>
<p><b><i>From Titus we read:</i></b></p>
<p>&quot;He [an elder] must hold firmly to the trustworthy message 
as it has been taught, so that he can encourage others by <i>sound doctrine</i> 
and refute those who oppose it&quot; (<b>1:9</b>).</p>
<p>&quot;But as for you, teach what accords with <i>sound<sup>
</sup>doctrine&quot; </i>&nbsp;(<b>2:1</b>).</p>
<p>&quot;That <i>the word of God</i> may not be reviled&quot; (<b>2:5b</b>).</p>
<p>&quot;<i>Declare these things</i>; exhort and rebuke with all 
authority. Let no one disregard you&quot; (<b>2:15</b>).</p>
<p>You do not have to be Albert Einstein to connect that 
church life is supposed to be saturated with Bible teaching and the declaration 
of sound doctrine. Although James warns about hearers of the Word who do not put 
it into practice 
(<a href="javascript:scripture('../scripture/james122.htm')" target=_self class=pop>James 1:22</a>), 
the way to solve that problem is not to become spiritually deaf! The proper 
resolution is to first hear the Word and <i>then</i> to follow what the Word 
says. Remaining ignorant of the Word is not the way to avoid hypocrisy!</p>
<p>At HPC, most of our sermons are expository in nature. 
According to Wikipedia, &quot;Expository preaching is preaching that concentrates on 
explaining the meaning of a particular text or passage of Scripture.&quot; Most 
expository preachers preach through the Bible in a book-by-book fashion. 
Expository preaching is truly &quot;preaching the Word.&quot; It is both fad-resistant and 
balanced.</p>
<p>Based on the above verses, pastors and teachers need to 
take the importance of careful interpretation and well-studied preaching or 
teaching seriously. When one preaches in an expository fashion, he shows that he 
takes his responsibility seriously. Like Paul, he can say, &quot;Therefore I testify 
to you this day that I am innocent of the blood of all of you, for I did not 
shrink from declaring to you <i>the whole counsel of God</i>&quot; (<b>Acts 20:26-27</b>, ESV).</p>
<p>Paul would have considered himself culpable if he had not 
taught the Ephesian believers the &quot;whole counsel of God.&quot; And no method of 
preaching tackles <i>everything</i> like expository preaching does. Although 
topical preaching (dealing with subjects mentioned in the Bible) has its place, 
only expository preaching addresses complete texts of Scripture. I know of only 
two or three other pastors in Howard County who typically preach expository 
sermons. Once we get outside our county, I know many (for example, our Camp 
Emmanuel pastors are all expository preachers). We have become an endangered 
species.</p>
<p>We live in a day when the postmodern philosophy of the 
world has crept into the church. The issue of worldly philosophies creeping into 
the church is nothing new; what is new is the nature of the philosophies 
themselves. Although the trends may convince us that expository preaching is 
unappealing (or that doctrine is a bad word), and although some view the idea of 
actually studying the Bible as passé, there are still millions of Christians who 
love and want the Word of God! Hopefully you are among them.</p>
<p>No matter where you may live or where your future may lead, 
I hope you never participate in a church that does not emphasize deep Bible 
teaching and sound doctrine. Never turn your back on the Word; take up the sword 
of the Spirit, which is the Word of God 
(<a href="javascript:scripture('../scripture/eph617.htm')" target=_self class=pop>Ephesians 6:17</a>). 
The Word of God is our 
only offensive weapon against the onslaughts of Satan; the rest of our armor is 
defensive. If Christians are to make a difference in the quality department, we 
must use our sword!</p>
