<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Prayer for the Survivors of Hurricane Katrina
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Father,</p>
<p>It is hard to believe that yet another great disaster is 
upon us. Some have labeled hurricane Katrina our &quot;Tsunami&quot; as we Americans once 
again are forced to remember that we are as vulnerable as the rest who share our 
world.</p>
<p>Perhaps hundreds perished as the winds ripped and the 
waters flooded cities and towns. Father, we know this world is cursed, and that 
natural disasters are part of that curse. Lord, we know You have a master plan 
that makes sense of these things, even though we confess we are clueless. Your 
ways are higher than our ways, and we know that, in the end, a greater good will 
come about, a good that could not be were it not for the tragic events of 
history. We trust You as the holy, good, and sovereign Lord—even though we do 
not understand many of Your ways. </p>
<p>And so, Lord, we take Your hand and ask You to help our 
people through this tragedy. Minister grace to the rescue workers; guide our 
leaders and the individuals who make up our relief agencies. Provide them with 
what they need to get the job done. We pray that You might use them to preserve 
many lives. </p>
<p>Comfort the grieving as they mourn their dead; help them 
during the trauma of what seems to be an unbelievable nightmare. Others have 
lost homes, businesses and irreplaceable possessions. In time, help them to 
rebuild their lives and limp along as they lean upon You.</p>
<p>And so, Lord, once again we cast ourselves upon You, 
recognizing that You created us for more than this life, for we pray in Jesus' 
Name, Amen.</p>


