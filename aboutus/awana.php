<p>Awana is an acrostic based on <b>2 Timothy 2:15</b>: &quot;Do your best to present
yourself to God as one approved, a workman who does not need to be ashamed and
who correctly handles the word of truth.&quot; This reveals the heart of
Awana:
Scripture memory.</p>
<p><b>Awana meets from 6:15 p.m. to 7:45 p.m. most Wednesdays during the school year. 
Call if weather is questionable. </b>Children age 3 by July 1 through grade 6 are 
placed in age-appropriate groups. 
<!--
(For youth grades 7-12, we offer a Wednesday night Bible study called SOL which
stands for Soldiers of Light*.) 
-->
They spend approximately half an hour in 
Council Time where they hear a Bible lesson, half an hour in Handbook Time 
where they earn points for reciting verses they have learned in their handbooks, 
and half an hour in Game Time where they play games on the Awana circle.</p>
<p>We often describe Awana as a Bible-oriented scouting program because the
program includes uniforms and achievement awards. As they pass the various
sections, club members receive achievement patches and plastic
&quot;jewels&quot; which are placed on their uniforms. Typically, the average Awana
child memorizes one verse a week, although highly-motivated children often
memorize more. Memory review is scheduled throughout the workbooks along with
other character-building tasks. In addition, the clubbers can earn ribbons,
plaques and trophies by completing one or more workbooks.</p>
<p>Internationally, Awana has nearly one million children who participate
weekly. Our club is well-staffed. 
Parents are welcome to observe and help with the club, if they wish to do so. 
Attendance at Highland Park Church is not required for involvement.

