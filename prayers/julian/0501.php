<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 1
<br><span class=byln>by Ed Vasicek</p>

<p>Great Father,</p>
<p>You are the God of the heavens. The first heaven is our 
atmosphere, the second outer space, but the third heaven 
(<a href="javascript:scripture('../../scripture/2cor122.htm')" target=_self class=pop>2 Corinthians 12:2</a>)—to 
which Paul was caught up—is where Your throne room is. As the heavens are 
higher than the earth, so great is Your compassion toward those who fear You. 
You are above all when it comes to quality, depth of character, power and 
privilege. You are the God of the heavens.</p>
<p>Thank You, my God, that You care about the widow and the 
orphan, the divorced, the abused, the impoverished, the persecuted, the lonely, 
the abandoned and the unhappily married. You feel compassion upon the sick and 
injured, the handicapped and the distressed. You empathize with the depressed, 
and You offer a special blessing to the poor in Spirit. Lord, I encounter some 
of these people throughout my weekly routine. Perhaps I am one of these 
people. Lord, may these and others experience Your love. Thank You that in 
Christ our present sufferings are not worthy to be compared to the glory that 
will be revealed to us.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________.</p>
<p>Let me remember that You are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>Lord, help me to be a good steward of the funds with which 
You provide me. <b>Proverbs 17:18</b> reads, &quot;Of what use is money in the hand of a 
fool, since he has no desire to get wisdom.&quot; O, Lord, may I be a wise person. 
Help me be wise in all areas, including money management.</p>
<p>Father, the nation of Iran is in my prayers today. 
Although we have heard a lot about this nation in the news, we pray for the 
people of Iran, that they might be saved. &nbsp;Lord, we cannot get reliable figures, 
but there are probably only a few thousand believers in Iran if that, less than 
one tenth of one percent. With Iran being over 99% Muslim and noted for 
intolerance, five million Iranians live outside the nation. Thank You for their 
seventy congregations. We pray, Lord, that freedom would reign in Iran, and that the 
Gospel would spread and multiply. The Magi came from Iran (Persia) to see the 
Christ child and we pray that the people of Iran would likewise come to Christ.</p>
<p>Father, thank You for the ministry of World Harvest Mission, &quot;a missions sending agency 
that...has over 130 missionaries in 13 
countries.&quot; Their vision is to &quot;see people and communities everywhere being 
made new by the gospel.&quot; They &quot;serve the church by sending people who are 
being changed by the gospel to begin new churches and strengthen existing 
churches.&quot; O Father, bless their efforts, grant them harmony, provide for them 
and empower their work by the Holy Spirit.</p>
<p>Now help me find my joy in You, I pray in Jesus' Name, 
Amen.</p>

