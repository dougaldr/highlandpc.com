<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 12
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Holy Spirit,</p>
<p>We praise You and appreciate Your work in our lives. 
Although You focus us upon the Lord Jesus Christ, we know that You are the 
active Agent behind so much of the Christian life. You convict us, regenerate 
us, baptize us into the Body of Christ, fill us, and empower us to fulfill the 
will of God in our lives. You create a hunger in us for God: Father, Son, and 
Holy Spirit. You give spiritual gifts to us as You will. You lead us into 
truth and whisper to us that we are the children of God. You are our down 
payment and Your seal provides us with Your security.</p>
<p>So, Holy Spirit, stir us up, motivate us, and continue to 
conform us to the character of Jesus Christ. &nbsp;Change us day by day, little by 
little.</p>
<p>Lord, help me to become more dependent upon You to help me 
do what I cannot do alone. Help me become a person of faith, a person who 
trusts You with the details of life. Help me view my life as an adventure with 
You, and to be willing to walk through those open doors. Do not allow me to 
cower in the corner, strapped by fear.</p>
<p>Father, minister to my body.&nbsp; Help me to be healthy, if 
this is Your will. Help me to discipline myself and to take care of Your 
temple, my body. Help me to become a person known for moderation and balance.</p>
<p>Please address the following needs: __________.</p>
<p>Today I remember the small Latin American nation of 
Anguilla. Although many of the people on this island know You, there is little 
concern for missions or cooperation among the Bible-oriented churches. We pray 
that you would help the believers to reach out both locally and, through 
missions, around the world. Help the Bible-believing congregations work 
together for the common good.</p>
<p>Today I remember the Christian Veterinary Mission as they 
help third world people learn skills that will provide more food and prosperity 
to these impoverished people. Help them as they offer this help with the 
Christian message included. May many come to know Christ, and may You be 
glorified through this labor of love.</p>
<p>Locally, we pray for those who organize and lead our street 
and highway departments. Help them to do a good job in keeping our roads safe 
and passable. Continue to use them to save lives.</p>
<p>Fill me with Your 
Spirit today that I might glorify You, I pray, in Jesus' Name, Amen.</p>

