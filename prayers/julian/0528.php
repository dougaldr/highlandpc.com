<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 18
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>Your Son is the Beloved 
(<a href="javascript:scripture('../../scripture/matt1218.htm')" target=_self class=pop>Matthew 12:18</a>) in whom You 
are well pleased. You are only well pleased with us when we are in Christ by 
faith because those who are in the flesh cannot please God 
(<a href="javascript:scripture('../../scripture/rom88.htm')" target=_self class=pop>Romans 8:8</a>). 
Father, You have delighted in Your Son from eternity past and You delighted in 
Him when He emptied Himself to become a man. You were please with how He lived 
and You were satisfied with His death as a complete sin offering. You, the 
Spirit, and He Himself all had part in His resurrection and He ascended up to 
heaven to sit at the place of honor, Your right hand.</p>
<p>My Master, I am tired of being in a hurry. I hurry to pray 
before a meal with my family because it is so hard to get them all at the table 
at once. I hurry to work in some prayer time. O Father, help me better budget 
my time, to learn to say no and to live at a more leisurely pace. Help me to 
be more concerned about pleasing You than making other people happy or avoiding 
their complaints. Help me live a balanced life based upon a reasoned mix of 
events and adapted to who I am, not who the workaholics, over-energized, or 
guilt-driven are. May godly reason and balance tame the wild monster of 
extremism and frenzy.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I 
find my strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Today I remember the Anhui Province of China with its 63 
million people. Thank You that anywhere from 3 to 12 million people in this region 
claim to be Christians, most of them evangelical. O Lord, persecution by the 
government sometimes gets intense, and we pray that 1) the persecution might 
stop, 2) that the persecutors would be won to Christ, 3) that You would give 
the Christians wisdom to evade persecution and 4) that You would strengthen 
them when they are persecuted. May the church continue to grow and grow in this 
province. May Your Spirit continue to hover over this nation!</p>
<p>Thank You for Helps International Ministries whose purpose 
is &quot;to strengthen and equip evangelical Christian ministries in proclaiming the 
Gospel of Jesus Christ, worldwide, by providing various 'helps' as found in 
<a href="javascript:scripture('../../scripture/1cor1228.htm')" target=_self class=pop>1 Corinthians 12:28</a>...a 
faith supported missionary sending agency and a service 
mission to world missions.... HIM provides services to assist the body of Christ 
in various areas of need such as: Bible teaching and leadership training in 
overseas seminaries, Bible colleges and pastors' conferences, church planting in 
areas of unreached people groups, Christian counseling, short-term mission trips 
and evangelistic events for teens and adults, short-term medical mission trips 
abroad. HIM assists with financial support of national church leadership, Bible 
college faculty and other national Christian workers and organizations as 
approved by the HIM&nbsp;Board of Directors.&quot; Bless this work, O Lord. Give them 
harmony, wisdom and the empowerment of Your Spirit.</p>
<p>Now empower me to serve You today, I ask in the Messiah's 
Name, Amen.</p>

