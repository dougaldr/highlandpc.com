<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Stuff You Don't Hear Just Anywhere
<br><span class=byln>by Ed Vasicek</span></p>

<div>
<p>One of the super fantastic blessings of our church is that 
Highland Park Church people crave the deep teaching of the Word of God.&nbsp;Because 
of that we are able to explore areas that few churches dare delve into.</p>
<p>This eagerness for the Word encourages your pastor to dig 
deeply into Scripture and into studies that add insight to our understanding of 
the Word, particularly studies about the Jewish background of the New 
Testament.&nbsp;We also notice Scriptures that others gloss over.</p>
<p>Let me give you a recent example.&nbsp;Take <b>John 11:49-53</b>:&nbsp;“Then one of them, named Caiaphas, who was high priest that year, spoke up, ‘You 
know nothing at all!&nbsp;You do not realize that it is better for you that one man 
die for the people than that the whole nation perish.’&nbsp;“He did not say this on 
his own, but as high priest that year he prophesied that Jesus would die for the 
Jewish nation, and not only for that nation but also for the scattered children 
of God, to bring them together and make them one.&nbsp;So from that day on they 
plotted to take his life.”</p>
<p>Have you ever wondered why we read of no reaction to 
Caiaphas’ prophecy?&nbsp;The answer is that such a statement would not have been 
considered unusual in first century Judaism. David Flusser (by no means an 
evangelical), in his book, <i>Jewish Sources in Early Christianity</i>, (p.59) 
writes:</p>
<blockquote>Since the age of the Hasmoneans, Jews 
had believed that the saints who died to sanctify the name of God atoned for the 
sins of Israel.&nbsp;The story of the mother and her seven sons in the <i>Second 
Book of Maccabees</i> acquires a greater significance in the <i>Fourth Book of 
Maccabees</i>, where their death is seen as an atoning sacrifice.&nbsp;In another 
Jewish source, <i>Midrash Sifre</i>, the idea is expressed that the killing of 
the Children of Israel by the Gentiles atones for the former’s sins.</p>
<p>It is reasonable to assume that during 
the Roman period this idea was applied not only to Jesus, but also to all those 
who were executed by the authorities.&nbsp;Even Jews who did not accept Christianity 
evidently believed that Jesus, like the other martyrs of the Roman authorities, 
had atoned for the sins of Israel.</blockquote>
<p>Isn’t that fascinating?&nbsp;Even before the New Testament was 
penned, the idea of the atonement was, in a sense, already part of the culture. 
That is fascinating in light of <b>Psalm 49:7-8</b>, which reads, “No man can redeem 
the life of another or give to God a ransom for him—the ransom for a life is 
costly, no payment is ever enough…”&nbsp;Jesus could actually make an atonement 
because He was both God and man, hence capable of paying an infinite price.&nbsp;Not 
only were the blood of bulls and goats incapable of removing sin, neither could 
the blood of good men. But the sinless God-man alone could pay the penalty. 
Only the Lamb is worthy to open the seals.&nbsp;You just don’t hear this stuff 
everywhere!</p>
<p>Another portion of fascinating Scripture is right before 
everyone’s eyes. <b>Acts 3:19-21</b> is intriguing:&nbsp; “Repent, then, and turn to God, 
so that your sins may be wiped out, that times of refreshing may come from the 
Lord, <i>and that he may send the Christ</i>, who has been appointed for 
you—even Jesus.&nbsp;He must remain in heaven until the time comes for God to 
restore everything, as he promised long ago through his holy prophets.”</p>
<p>If you carefully read this verse, you may conclude what 
seems obvious. As Peter is preaching some time after Pentecost, he basically 
tells the Jewish people that if they would repent, then the times of refreshing 
(the Kingdom Age or the Millennium) would come and Jesus would return!&nbsp;I’m not 
making this up—it’s right there in <b>Acts 3</b>. Really.</p>
<p>Peter writes in a similar way in <b>2 Peter 3:12</b>:&nbsp; “…as you 
look forward to the day of God and speed its coming.&nbsp;That day will bring about 
the destruction of the heavens by fire, and the elements will melt in the heat.”</p>
<p>Besides the fact that Peter is addressing events at the end 
of the Millennium, which are part of the broad category of the “day of God,” 
notice that he implies believers “speed its coming.” David Stern, in <i>The 
Jewish New Testament Commentary</i>, informs us about a Jewish tradition of&nbsp; <i>
tik-kun-ha’olam</i>, meaning “fixing up the world.” Stern writes:&nbsp;“According 
to Jewish tradition such activity hastens the coming of the Messiah…”</p>
<p>Peter probably refers to the idea that when the Jewish 
people turn to Christ, this will result in the return of Jesus to reign, and he 
is certainly in line with Old Testament teaching.&nbsp;We know that near the very 
end of the Tribulation period, the Jewish people will turn <i>en mass</i> to the Messiah 
(<a href="javascript:scripture('../scripture/zech12810.htm')" target=_self class=pop>Zechariah 12:8-10</a>), 
which then brings about or at least precedes the return of Christ to the earth.</p>
<p>Although people are held accountable to believe, and 
although our actions do affect history, the other part of the equation is that 
God is Sovereign; He has a plan and timetable that cannot be thwarted.&nbsp;But He 
can and does work within people to accomplish those purposes right on time 
(<a href="javascript:scripture('../scripture/gal445.htm')" target=_self class=pop>Galatians 4:4-5</a>). 
And you don’t hear this stuff everywhere either.</p>
<p>Yet another teaching rarely heard is that we are all still 
accountable to the Covenant of Noah.&nbsp;Note these passages from <b>Genesis 9</b>:</p>
<blockquote>Then God blessed Noah and his sons, saying to them, ‘Be 
fruitful and increase in number and fill the earth.... Everything that lives and 
moves will be food for you. Just as I gave you the green plants, I now give you 
everything.&nbsp;But you must not eat meat that has its lifeblood still in it. And 
for your lifeblood I will surely demand an accounting.&nbsp;I will demand an 
accounting from every animal. And from each man, too, I will demand an 
accounting for the life of his fellow man.&nbsp;Whoever sheds the blood of man, by 
man shall his blood be shed; for in the image of God has God made man.&nbsp;As for 
you, be fruitful and increase in number; multiply on the earth and increase upon 
it.’</p>
<p>Then God said to Noah and to his sons 
with him:&nbsp;‘I now establish my covenant with you and with your descendants after 
you.... This is the sign of the covenant I am making between me and you and every 
living creature with you, a covenant for all generations to come:&nbsp;I have set my 
rainbow in the clouds....'</blockquote>
<p>Note that this covenant was made for all generations, and 
that this is not part of the “Law,” which came under Moses centuries later. It 
is still in effect.</p>
<p>From this covenant, the Jews claimed that 7 of the 10 
commandments could be interpolated. The practice of equity (lying and 
coveting), not blaspheming the Name (not taking God’s name in vain), no idolatry 
(no images), no sexual immorality (do not commit adultery…also addresses 
homosexuality, incest, etc.), no murder, no robbery, and another regulation not 
part of the 10 but found throughout the Old Testament—no eating or drinking 
blood. The Jews also somehow included not eating a limb torn from a live 
animal.</p>
<p>Interestingly, the Jerusalem Council in <b>Acts 15</b> seemed to 
say that Gentile believers, while not under the Law, were to live in light of 
the Covenant of Noah.&nbsp;<b>Acts 15:19-20</b> summarizes some of the key ideas in the 
Jewish understanding of this covenant: “It is my judgment, therefore, that we 
should not make it difficult for the Gentiles who are turning to God.&nbsp;Instead 
we should write to them, telling them to abstain from food polluted by idols, 
from sexual immorality, from the meat of strangled animals and from blood.”</p>
<p>This was typical instruction given by Jewish Rabbis to 
God-fearing Gentiles who did not want to become full converts to Judaism.</p>
<p>This code answers the question: “What moral values should 
Christians expect all societies to uphold?” The answer is this: the values 
espoused in the Covenant of Noah.&nbsp;Again, you don’t hear that everywhere!</p>
<p>Thanks, that as a pastor, you allow me to preach and teach 
at a higher level than is common.&nbsp;Like the psalmist in <b>Psalm 138:2</b>, many of you 
believe that “you have exalted above all things Your Name and Your Word.”&nbsp;There 
are not many congregations like ours!</p>
