<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 10
<br><span class=byln>by Ed Vasicek</p>

<p>You Who Reign Forever and Ever,</p>
<p>I am overwhelmed by Your power. You humbled proud King Nebuchadnezzar. 
You struck him with insanity so that he thought he was an 
animal. He ate grass for a period of seven years until his humbled mind 
returned to him. You ordained the powers that exist, even though they have 
been marred by the fall. You even created Lucifer as a holy angel until he and his 
allied angels fell into sin and became demons. When some of these angels sinned 
before the Flood, you imprisoned them until the final judgment. You have the 
keys of death and hell. We humans cannot even make a single living cell; You 
made all creation with its marvelous complexity and unfathomable engineering.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God.</i>).</p>
<p>Lord, strife within families is awful. Please bring 
together members of our families. Soften hearts. We pray that those offended 
would be gracious, yet not naïve. We pray that the offenders would be 
repentant. We know some situations can never be repaired, but we pray that many 
relationships would be reconciled.</p>
<p>Lord, help me to be a good manager of my time. I do not 
want to live the hurried lifestyle, but I need to be efficient in some areas 
that I might have more relaxed times later. Enlighten me as to how I might do 
this.</p>
<p>Father, the African nation of Somalia is a mess. It is a 
lawless land with warlords fighting for territory. The economy is devastated 
and seemingly beyond repair. Because the nation is 99.95% Muslim, missionaries 
cannot work there. Thank You for the few hundred “underground” Christians; 
strengthen them and may they wisely share their faith. Work in the land as only 
You can, O Lord, for this is truly an impossible situation. Only You can do the 
impossible and I ask that You would!</p>
<p>Thank You, my God, for Blessings International, &quot;a 
non-profit organization whose mission is to alleviate suffering and provide 
medicines worldwide by facilitating relationships that promote health.&quot; Thank 
You that they have been providing medical supplies for 24 years, have been involved 
with disaster relief, AIDS clinics and church relationship development. They 
work in nations like Southern Sudan, Iraq, Afghanistan, Nicaragua, Sierra Leone, 
Liberia, North Korea and with Tsunami relief. O Father, bless them as they share 
Your love. May others see Christ in them and may many come to Christ through their 
example of compassion, I ask.</p>
<p>I pray for those who work as carpenters. Grant them 
safety, help them to do their work well and may those who know You boldly share 
their faith with their coworkers, I pray.</p>
<p>Now help me be a 
good testimony to You, I ask in Christ’s Name, Amen.</p>

