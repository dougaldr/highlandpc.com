<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Personal Devotions</span>
<br>Lord of All
<br><span class=byln>by Ed Vasicek</span></p>

<p>Lord of all, great Yahweh, Ruler of the universe, God and Father of our Lord
Jesus Christ, who, along with him, have sent us the refreshing person of the
Holy Spirit, I praise you and acknowledge you as Lord not only of all creation
but specifically as Lord of my life. Indeed, be Lord of my all: my heart, my
mind, my strength, my soul, my spirit, my earthly body, my ambitions, my
relationships, and my dreams.</p>
<p>Father, I must confess that though I ask you to be Lord of all and mean it
with my innermost being, there are parts of me that only yield for moments.&nbsp;
They
bounce back, and I allow them to reclaim sovereignty in their own domains.&nbsp;
Certainly I am one of my own worst enemies. So deliver me not only from the
schemes of the devil and the peer pressure of the world, but also from my own
self. Help me follow the path of your Spirit. Strengthen the new creation within
me. Help me consider myself dead to my old master of sin and alive to you.</p>
<p>Every day I am a day older. As I grow older, may I grow wiser. Help me
emulate the wise person of Proverbs, not the fool. May others know me as a
fountain of godly wisdom. Help me bind kindness and truth around my neck, that I
might win the favor of God and men.</p>
<p>As I fellowship with others in the community of faith, help me draw from
their strengths while rejecting the compromises of others. Keep me from becoming
cynical when others let me or you down. May I not be turned away from you out of
anger at the hypocrisy of others, and, please, Father, I beseech you, keep me
from becoming a stumbling block to others because of my hypocrisy. If others
stumble over me, may it be because I am one with the scandalous stumbling block
of Christ, the foundation stone. If I be rejected or spoken of in an evil
manner, may it be because of my identification and unswerving obedience to
Christ, and not because of sin in my life or lack in my character.</p>
<p>Sovereign Lord, open my eyes to see your will for me today. Arrange the
appointments of my life so that you are glorified. Help me join in with your
Kingdom work, whether it be sharing the gospel with a lost person, encouraging
one who is discouraged, using the creativity you have given me to your glory, or
shining as light in a dark world. Help me be patient, kind, and considerate of
those who cross my paths. Help me not be naive nor gullible but discerning and
willing to give of myself to others for your name's sake.</p>
<p>I give you this day as a sacrifice of thanksgiving for the salvation I
already possess in Christ Jesus our Lord. It is in that name above all names in
which I pray, Amen.</p>

