<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 3
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the Most High God. You are not merely above us nor do You share Your 
status. You are truly above all; none are higher than You. No one outranks You; 
no one matches Your rank. You alone, Father, Son and Holy Spirit, are the Most 
High God. Although the evil one is powerful, he is far from at parity with You. 
As the hymnist put it, &quot;one little word will fell him.&quot; There is no competition. 
The difference between a single cell creature and a mortal man is great, but the 
difference between You, O Lord, and any created being is greater! Indeed, how 
can anyone who is finite be compared with You Who are infinite!</p>
<p>Father, as a sinful human being, I sometimes need to apologize to others. 
Help me not excuse my wrongs, or predicate my apologies based upon how others 
wronged me. Help me to ask for forgiveness by taking full responsibility for my 
actions and isolating them from the behavior of others. Help me utter those 
words, &quot;I was wrong; please forgive me.&quot;</p>
<p>Lord, I know that I have not because I ask not. Please, Lord meet the 
following needs and requests according to Your perfect will: __________.</p>
<p>Lord, troubled marriages are all around me. Often I am not aware of that. A 
friend at work, a neighbor, a relative. A mom with young children feels trapped 
and the couple could use some time away, but no family is near 
or there are no trusted friends to watch the children; finances are tight and 
the tensions high. A man or woman was reared in a messed up family and the 
baggage he or she brings into the marriage is destroying it. Long work hours, 
addictions, anger, violence and immorality—people all around us are struggling. 
Help us to be supportive in every way and help the marriages around me to get 
stronger especially within my church family.</p>
<p>Today I pray for the Indian State of West Bengal with its 
82 million people. Perhaps only .2% of the population is made up of born-again 
evangelicals. Lord, the enemy is strong here, as evidenced by demonic 
activity. There are many barriers that keep the people from even considering 
the claims of Christ. O Lord, do a mighty work in this state for Your Name’s 
sake. Grant many the repentance that leads to life. O Lord, may the percentage 
of believers grow tenfold during the next decade, I ask.</p>
<p>&quot;ZOE International Ministries is a mission’s organization 
called to reach and influence the unreached people of the world by fulfilling 
the Great Commission to preach the Gospel of Jesus Christ to every person and 
make disciples of all the nations.&quot; Thank You for their work in Asia; bless, 
prosper, provide for and empower the workers who identify with this ministry, I 
pray. Grant them much fruit!</p>
<p>May the fruit of 
the Spirit be evident in my life today, I ask in Jesus’ Name, Amen.</p>

