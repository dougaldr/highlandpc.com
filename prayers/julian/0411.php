<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 11
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are unbounded. Although the 
habitations of mankind are within boundaries You have set, we can set none for 
You. As the Psalmist explains, if we go to heaven or the deepest parts of the 
sea, You are there before us and without us. You are a limitless God.</p>
<p>Thank You for sustaining the universe. Although You—God 
the Father, Son and Spirit—created the universe and all that is in it 
within six days, You hold everything together moment by moment. Every breath I 
have is dependent upon Your willingness to allow me to live. The same is true 
regarding salvation. Since I have accepted Jesus Christ as my personal Savior, 
I am kept by the power of God, Your power. Just as You hold the atoms and 
molecules together, You keep my soul.</p>
<p>I pray for my lost friends and family members including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>I pray for safety on the road, particularly for myself and 
my loved ones but for all in general. There are many hazards to be avoided. 
Help us to be alert and send angels to guard, protect and deliver us.</p>
<p>Help me not only to be alert on the road, but also alert to 
the devil’s schemes. Do not allow me to live in ignorance of these schemes, but 
help me to be alert to them and to recognize it when I myself have been lured 
into creating unnecessary conflict or embracing attitudes that are destructive 
and contrary to building up my brothers and sisters in Christ and pleasing You.</p>
<p>Today I pray for the Pacific region of the world. The 
successful work of missionaries a century ago has abolished cannibalism and over 
15% of the region’s people claim to be born-again evangelical Christians. Yet, as 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>
describes it, “The decline of Christianity is especially 
acute in Australia, New Zealand and New Caledonia with a rapid rise in 
secularism, family breakdown and indifference to spiritual things. The churches 
need new vision, flexibility in methods and understanding of cultural changes to 
communicate a relevant biblical message and become a more effective discipling 
community.” Our Father, please send Your Spirit to revive the work there! May 
people turn to You in great numbers and help those who know You to be strong.</p>
<p>Thank You for the work of International Teams. Thank You for their Refugee 
Ministries, as they help the 34 million refugees searching for a new home. Thank 
You for their evangelistic efforts via their Pioneer ministries. Bless the 
outreach of their ethnic, children’s, and economically disadvantaged 
ministries. Help them as they nurture meaningful youth ministries. May Your 
Spirit empower and guide both the leadership and team members; help them to work 
in harmony. Use the agency to make a difference for the Kingdom of God.</p>
<p>Now, Lord, help me be faithful to Your calling on my life today, I pray in 
the Name of the Messiah, Jesus, Amen.</p>

