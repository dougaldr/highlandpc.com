<p id="pgt"><span class="pgh">Articles:
<br>Ethics, Morals, Behavior</span>
<br>Different Kinds of Love
<br><span class=byln>by Ed Vasicek</span></p>

<p>Ancient New Testament Greek has about a 6,000-word vocabulary. Common
English has a vocabulary of over 75,000 words (not including technical or
rare terms). English has by far the broadest vocabulary of any language;
my three-volume dictionary lists over 450,000 words! The vocabulary of
modern English, for example, is about five times that of modern German.</p>

<p>
As a result of such a massive vocabulary, the English language can offer
nuances of meaning and word slants beyond the wildest dreams of any other
language, including New Testament Greek. This is especially true regarding
the common New Testament word for love, &quot;agape.&quot; Contrary to what you may
have heard, the word &quot;agape&quot; is not unique to the New Testament at
all. It
was used in common Greek language much as we would use our English
&quot;love.&quot; The other Greek word for love, &quot;phileo,&quot; is
truly a special word,
but &quot;phileo&quot; is used sparingly in the New Testament and its meaning,
&quot;brotherly love,&quot; is self-explanatory. &quot;Agape&quot; can
mean anything from
&quot;niceness&quot; (as in <a href="javascript:scripture('../scripture/1cor421.htm')" target=_self class=pop>1
Corinthians 4:21</a>)
 to the self-sacrificing love of God
(<a href="javascript:scripture('../scripture/john316.htm')" target=_self class=pop>John 3:16</a>)
 and the whole spectrum in between.</p>

<p>
In all languages, particularly those with a small vocabulary, words may
change meaning depending upon their context. I could say, &quot;The boy threw
the ball,&quot; or &quot;The princess went to the ball,&quot; or &quot;The kids
who went to
Camp Emmanuel last week had a ball.&quot; &nbsp;The context tells the reader which
definition of &quot;ball&quot; I mean. Though the word &quot;love&quot; is
not defined in
radically different ways, the context does color the meaning of the word.</p>

<p>
We do not love our enemy in the same way in which a man is to love his
wife. The distinction is not determined by the word &quot;love&quot; but
rather by
its implications in the context. With that in mind, let me survey some of
the key differences between several types of love in Scripture. I will use
our vast English vocabulary to demonstrate what I believe to be the
different natures of love.</p>

<ol>
  <li>The love of God for us is the love of redemption. (<a href="javascript:scripture('../scripture/john316.htm')" target=_self class=pop>John 3:16</a>;
    <b>
   Romans 5:8</b> &quot;But God demonstrates his own love for us in this: While we were still
sinners, Christ died for us.&quot;) God Himself becomes a man to die for
our
sins. We might call this the love that sacrifices oneself for another, the
love that gives of oneself, or SACRIFICIAL LOVE.<br><br>

<li>A man is to love his wife in a way that is also sacrificial, but there
is an additional element in the love between spouses. We might call this
love a BLENDING LOVE as two lovingly function as one. A man puts his wife
above himself
(<a href="javascript:scripture('../scripture/eph52528.htm')" target=_self class=pop>Ephesians 5:25-28</a>)
 while a woman tries to learn how to love her husband
(<a href="javascript:scripture('../scripture/titus24.htm')" target=_self class=pop>Titus 2:4</a>).<br><br>

<li>The parent-child love and affection is described for us in <b> 1
Thessalonians 2:7-8</b>. (&quot;But we were gentle among you, like a mother caring
for her little children. We loved you so much that we were delighted to
share with you not only the gospel of God but our lives as well, because
you had become so dear to us.&quot;) We can call this a BONDING LOVE. The
parent-child bond should change as the child matures; though it changes
form it can remain deep. At marriage a child is to leave the authority of
his or her parents and form a new family. Woe to the man or woman who puts
parents above spouse. Such misdirection is not the result of a healthy
bonding love which releases a child to adulthood.<br><br>

<li>Then there is the love between siblings. <b>Proverbs 17:17</b> is often
misinterpreted, in my view, to teach the concept of sibling rivalry. I
understand it to teach that brothers can be leaned upon during difficult
times. (&quot;A friend loves at all times, and a brother is born for
adversity.&quot;) We can call this a SUPPORTING LOVE or perhaps &quot;a love of
reliance.&quot;<br><br>

<li>The love of close friends is a love many people sadly never
experience. David and Jonathan were unique in this regard they
exemplified an intense form of the LOVE OF COMRADERY. (<b>1 Samuel 18:1</b> &quot;After
David had finished talking with Saul, Jonathon became one in spirit with
David, and he loved him as himself.&quot;) Soldiers sometimes experience this
love as they risk life and limb for a buddy. &quot;He's not heavy, he's my
brother.&quot;<br><br>

<li>Fellow Christians are to be known for their love one for another
(<a href="javascript:scripture('../scripture/1john210.htm')" target=_self class=pop>1 John 2:10</a>,
<a href="javascript:scripture('../scripture/1john21618.htm')" target=_self class=pop>16-18</a>).&nbsp;
  We are to do good to all, but fellow believers are a
priority. (<b>Galatians 6:10</b> &quot;Therefore, as we have opportunity, let us do
good to all people, especially to those who belong to the family of
believers.&quot;) This is FELLOWSHIP LOVE. We have the Lord in
common, and His
love binds us together. Incidentally, it is this type of love which is
addressed in the famous &quot;love&quot; chapter,
<a href="javascript:scripture('../scripture/1cor13.htm')" target=_self class=pop>1 Corinthians 13</a>.<br><br>

<li>The Bible commands us to love our neighbor, as illustrated in the
Parable of the Good Samaritan
(<a href="javascript:scripture('../scripture/luke103637.htm')" target=_self class=pop>Luke 10:36-37</a>).&nbsp;
  We could call this love a CONCERNED LOVE. Rather than pass by, we show a level of care.<br><br>

<li>Last in my list is the command to love our enemies
(<a href="javascript:scripture('../scripture/matt544.htm')" target=_self class=pop>Matthew 5:44</a>;
<a href="javascript:scripture('../scripture/matt79.htm')" target=_self class=pop>7:9</a>
 &quot;But I tell you: Love your enemies and pray for those who persecute
you.&quot;) This command tells us to obey the Golden Rule even for those
we do
not get along with, those who have wronged us. Love in this case is
certainly not a feeling, though in all of the above with the exception of
loving our neighbor, perhaps feelings are involved. In reality, loving our
enemy is a love in contradiction to our feelings. This love is the LOVE OF
DUTY.</li>
</ol>
<p>
It isn't always easy to love others, but God demands nothing less. And we
would be wise to make it easier for others to love us!</p>

