<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 20
<br><span class=byln>by Ed Vasicek</span></p>

<p>O Sovereign Lord,</p>
<p>You are the Sovereign, the Supreme Ruler, the One Who 
Determines, and the King of Kings. As King Nebuchadnezzar confessed, &quot;His 
dominion is an eternal dominion; his kingdom endures from generation to 
generation. All the peoples of the earth are regarded as nothing. He does as he 
pleases with the powers of heaven and the peoples of the earth. No one can hold 
back his hand or say to him: &quot;What have you done?&quot; (<b>Daniel 4:34-35</b>) You 
truly set up kings and take them down. You are in charge, and we are foolish to 
think otherwise.</p>
<p>Today I thank You for the burial of Christ, which is part 
of the Gospel: Christ died for our sins, was buried, and rose again on the third 
day. My sins were buried with Him and dissipated in the earth. Just as one 
goat was slaughtered during Yom Kippur and another released into the wilderness 
as a &quot;scapegoat,&quot; so in His burial Jesus carried my sins far away.</p>
<p>And so, Lord, I ask You to help me have a clear conscience, 
believing I am forgiven in Christ and clean before You. Help me keep my 
conscience clear, avoiding sin and confessing my sins to You when I have failed 
to avoid them.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Father, I need wisdom every day. Your Word encourages me 
to both develop wisdom and to pray for it. Lord, I know the start of wisdom is 
fearing You, and I also know that wisdom comes to those who crave, respect, and 
value it. Help me to place wisdom high on my priority list.</p>
<p>Today I remember the European nation of Austria. Even 
though we think of Europe as religiously free, Austria often restricts smaller 
denominations, many of which are Evangelical. We pray that this 1998 law would 
be seen for what it is, an enemy of freedom. It is sad to hear that less than 
half of one percent of this nation is evangelical Christians. There are more 
Jehovah Witnesses in Austria than there are born-again believers! Thank You that 
the small evangelical church in Austria is growing, but they are so minute in 
number. We pray that the church would grow ten fold over the next decade!</p>
<p>Today we pray for AmeriTribes Mission. Use them to reach 
Native Americans throughout the Western Hemisphere.&nbsp; Provide workers, funds, 
and, most of all, the working of Your Spirit to draw America's first inhabitants 
to Yourself—and for the native church to provide its own leaders and church 
planters.</p>
<p>Bless and guide our elected Prosecutors. We know their job 
is important: may they seek justice for righteousness sake, and may they do 
their work with integrity.</p>
<p>Motivate me today 
to keep serving You, I pray, in Jesus' Name, Amen.</p>

