<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 21
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God and King,</p>
<p>When Jesus was presented at the Temple when he was 40 days 
old, before the Wise Men had visited Him, Joseph and Mary offered the sacrifice 
of a dove, the prescribed sacrifice for one who was poor but not totally 
destitute. Although Joseph and Mary were not wealthy, they held the greatest 
treasure in the universe: the Lord Jesus Christ.</p>
<p>Simeon was an unusually godly man. You, O Lord, had 
revealed to him that he would not die before seeing the Messiah. When Joseph and 
Mary brought Jesus to the Temple complex, Your Spirit prompted Simeon to meet 
them there. Simeon took the baby Jesus in his arms and blessed Him and 
prophesied that He would be, &quot;a light for revelation to the Gentiles and for 
glory to [His] people Israel&quot; (<b>Luke 9:32</b>).</p>
<p>Indeed, Simeon was right. The Lord Jesus Christ has 
brought redemption to both believing Jews and believing Gentiles. Thank You, 
Lord, that I am among the multitude of those who believe! Thank You also, Lord, 
that the same Holy Spirit who guided Simeon both guides and dwells within me!</p>
<p>Lord, I remember my marriage (if I am married) and the 
marriages of my extended families, friends and church families. Lord, hold them 
together. Help married couples to lean upon you and to work hard at addressing 
problems and experiencing harmony. I also pray for those who are divorced that 
You would help them to go forward with Your hand in theirs.</p>
<p>Today I pray for missionaries that my church or I support 
including: __________.</p>
<p>Father, Your Word tells me that, &quot;Pride goes before 
destruction, a haughty spirit before a fall&quot; (<b>Proverbs 16:18</b>). Help me to 
run from pride and to pursue humility which is simply sanely seeing reality from 
Your perspective and looking at things in proper proportion.</p>
<p>I pray for the Asian nation of Nepal, the world's only 
Hindu Kingdom. Even today, persecution is still a problem in this nation though 
it has lessened since 1990. Thank You for the 1.6% of the population that claims 
to be born-again evangelical. Thank You for the new opportunities to train 
leaders. Bless the outreach of individual Christians, literature ministries, 
tape ministries, radio and the Jesus film. May the number of born-again 
evangelicals triple over the next five years, I ask.</p>
<p>Lord, bless the mission of Christian Outreach 
International, which is &quot;concerned about the spiritual needs of the lost people 
of the world. The task is twofold—reaching the lost and the development and 
training of believers who are called to 'go into all the world and preach the 
Gospel.' [Their] aim is to aid believers in being witnesses to the world by 
reaching people where they are on the streets, in sports arenas, at places of 
business and in their homes.&quot; Bless them, Lord, by empowering their efforts 
by Your Spirit. Provide for them, protect them, grant them fruit for their labor 
and help them to work harmoniously, I pray.</p>
<p>Help me to be at 
peace with You today, I request in Jesus' Name, Amen.</p>





