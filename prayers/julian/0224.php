<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 24
<br><span class=byln>by Ed Vasicek</p>

<p>King of Kings,</p>
<p>You are the Creator of all things. You were here before 
all things; indeed, You were here before time. You planned Your creation, and 
life came from You, the Fountain of all life. You are an orderly God, and so 
Your creation is orderly. You said, &quot;Let there be light,&quot; for You are light and 
You share Your light with us. Of God the Son we read that all things were 
created through Him. All events have a beginning, but You are not an event. 
You are the Beginner of all events. Your awesome designs leave us spellbound.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power not my own. May Your Spirit control me 
today.</p>
<p>Great Lord, I thank You for Your sacrificial love. Often I 
see injustice around me; sometimes I have felt like life has not always been 
even-handed with me. But I know You love me, not because everything goes well, 
but because of Calvary. According to Your Word, that is when You demonstrated 
Your love for me, when Christ died for me 
(<a href="javascript:scripture('../../scripture/rom58.htm')" target=_self class=pop>Romans 5:8</a>).</p>
<p>So, Lord, help me to trust You and Your goodness when it 
seems like You have forgotten me. Help me to be patient and remember that You 
are in no hurry. I am not wrong to want speedy answers for my prayers, but You 
are going to follow Your timetable not mine!</p>
<p>God, like every one of the billions of people who inhabit 
our world, I am vulnerable. So, Lord, please send angels to watch over me and 
protect me. Keep me from harm and evil of every kind.</p>
<p>Today I pray for the massive nation of China. Oh, Lord, 
praying for this country is overwhelming. Over one and a fourth <i>billion</i> people! 
Lord, I praise You that over 6% of the population are born-again. Thank You for 
the great growth in the Chinese Church, from 1.8 million in 1949 to 17 million 
(and with secret house churches perhaps 45 million). We pray that the church 
would keep growing and doubling every 5 years. Lord, provide more freedom for 
the Chinese people. Help the government to stop viewing the church as evil, and 
may freedom reign in this vast land. We pray, Lord, that China would become a 
spiritual lighthouse for the rest of the world. Help the Gospel to transform 
lives and society in phenomenal ways. Provide leadership training, heal this 
nation, and may Your Spirit do a work there greater than He has ever done 
anywhere!</p>
<p>Thank You, God, for PIONEERS, a 
ministry that mobilizes teams to
glorify God among
unreached peoples&#8232;by 
initiating 
church-planting movements&#8232;in 
partnership with
local churches. 
Thank You for the summer, short term, and long-term missionaries who organize 
outreach in nations like Chad, Iraq, and Sumatra. Help them get along, empower 
them by Your Spirit, and may they be used of You to give a real spiritual &quot;shot 
in the arm&quot; to the churches with whom they minister.</span></p>
<p></p>
<p>Bless our city, town, village and county council board 
members throughout this land. Give them servants' hearts, integrity, and help 
them to reasonably serve their communities. May many come to know You.</p>
<p>Help me to remember to pray throughout the day, I pray, in 
Jesus' Name, Amen.</p>

