<p>Since 
I do not have time to bring all these details to you during the sermon, here are 
some extra comments to help you understand this passage in a greater way. Let 
me present this info by asking and answering two questions.</p>
<ol><li><b>Why did Jesus say that 
if the Torah expert kept the two great commandments—loving God and his 
neighbor—he would inherit eternal life?</b> 
	<p>Here are two 
potential answers from two respected commentators.</p>
<p>
Reformed scholar William Hendrickson writes, &quot;…If any human being would actually 
fulfill this law of love to perfection, he would indeed obtain everlasting 
life. There was nothing wrong with this high requirement of the law: 'The Law 
is holy, and the commandment is holy and righteous and good' (<b>Romans 7:12</b>). 'The 
man who does these things will live by them' (<b>Galatians 3:12</b>). The trouble is not 
with the divine principle that perfect obedience results in everlasting life. 
What, then, is wrong? Paul answers in these words, 'We know that the law is 
spiritual; but I am carnal, sold under sin' (<b>Romans 7:14</b>). If only the law-expert 
will now admit this. If only he will cry out, 'God, be merciful to me a 
sinner....&quot;</p>
<p>
Dallas Seminary scholar J. Dwight Pentecost writes, &quot;...It must be realized that 
in Jewish thinking entrance into the messianic kingdom was equated with 
inheriting eternal life. The millennial kingdom was the stepping stone to the 
eternal kingdom.... The question of the law expert in 
<a href="javascript:scripture('../../scripture/luke1025.htm')" target=_self  class=pop>Luke 10:25</a>, 
then, must be 
understood to mean, 'How righteous must I be to enter the messianic kingdom that 
You are offering?'&quot;</p>
<p>
However we look at it, the matter is academic. Outside of Jesus, no one has 
perfectly—or even near perfectly—loved God and one's neighbor as they should. We 
are lost because we are sinners by nature, and our failure to live up to God's 
expectations merely reflects who we are, sinners in need of a Savior.</p>
<li><b>Are there Jewish 
aspects to this parable that are overlooked?</b> 
	<p>Glad you 
asked. And how there are. In the parable, it is very important to realize that 
the priest and the Levite were Sadducees by creed. That meant they believed 
only the Torah, the first five books of the Bible. The Samaritans (who were 
basically a mixed breed of disenfranchised Jews) likewise only recognized the 
Torah. The Law Expert was a Pharisee; he believed the entire Old Testament and 
recognized the traditions and rulings of the Rabbis. The Pharisees taught that 
a Jew was OBLIGATED to help a wounded or weak person, even if it meant 
ceremonial defilement. The Law expert would have anticipated a Pharisee coming 
by and helping the man. When Jesus gave a Samaritan the role of hero, it must 
have been an added shock to this expert in the Law.</p>
<p>Also 
worth noting is that the priest was coming DOWN the road. Since the road that 
connected Jerusalem to Jericho was steeply downhill from Jerusalem, the priest 
had already performed his duty and was returning. Getting defiled was not an 
issue here.</ol>

