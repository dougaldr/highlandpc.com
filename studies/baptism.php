<ol type=1><li><p class="bighpc"><a name="sctn1">Introduction, Approach, and Principles</a></p>
<p>As I address the issue 
of the relationship of baptism to salvation and the broader issue (that 
salvation is by God's sovereign grace), I want to be up front about where I am 
coming from in several areas. </p>
<p><b><a name="sctn11">My Attitude Toward Folks in Churches of 
Christ/Christian Churches</a></b></p>
<p>First of all, I think 
that <u>many</u> who belong to "Christian" Churches or the Church of Christ know 
and love the Lord. I think there are significant numbers of true believers 
within these ranks, even in the stricter Church of Christ, and I anticipate 
enjoying heaven with them. However, to be frank, I do think that a number of 
folks within those ranks have a false hope of salvation precisely because their 
mixed message encourages them to trust in good works and acts of obedience to 
make them right with God. Nonetheless, others are trusting in the merits of 
Jesus Christ and not merely acknowledging His work on Calvary, but depending 
upon it. This is sometimes <i>in spite</i> of what they are taught, not because 
of it. I do want to be clear on this as well: some Christian Church pastors 
agree with me in the realm of salvation by faith alone apart from baptism. They 
might have a conviction that weekly communion is God's will or that baptism 
should occur on the spot, but those are minor differences. So in the rest of 
this paper, when I refer to "Christian" churches, I am referring to those 
churches holding the view that baptism is necessary for salvation.</p>
<p>To be fair, we must 
admit that there are also very likely many lost people (who have a false 
confidence regarding their salvation) within the confines of conservative 
evangelicalism -- but often not because they are taught incorrectly. I believe 
tares are mixed with the wheat in even the best congregations. Those who 
embrace Jesus as Savior (the one who has provided salvation for us) are truly 
saved, as I see it; in my view, others (who merely acknowledge the atonement 
and give assent to the salvation Christ provided but focus upon their baptism, 
communion, and good works) are like the Jews of old in <b>Romans 10:2-3</b>: "For I 
can testify about them that they are zealous for God, but their zeal is not 
based on knowledge. Since <b><i>they did not know the righteousness that comes 
from God and <u>sought to establish their own</u></i></b>, <b><u>they did not 
submit to God's righteousness</u></b>."</p>
<p>This problem of making 
oneself out to be his own savior is common, unfortunately, even within 
evangelical churches preaching salvation by grace through faith. In such cases, 
preachers preach grace, but many folks still hear "works." But when additional 
conditions and works are added as grounds for salvation, the problem escalates. 
Obviously those who believe in the necessity of baptism for salvation would see 
things differently! So rather than try to conceal my opinion, I am expressing 
it upfront.</p>
<p>The branch of the 
Campbellite family called, "The Disciples of Christ", is not addressed in this 
discussion, since that denomination is predominantly liberal and does consider 
itself bound by and accountable to the Scriptures (understood in their intended 
sense); I view them as any other liberal mainline denomination.</p>
<p><b><a name="sctn12">My Theological Conclusions</a></b></p>
<p>I would also like to 
also be <b>upfront</b> regarding my theological perspective. Like many on the 
other side of this issue, I <b><u>seek</u></b> to be Biblical. I am not out to 
follow any man-made system. My conclusions about what the Bible teaches 
(regarding salvation) include unconditional election (you'll read more about 
this below) and perseverance of the true believer (eternal security, though I 
prefer the first term). This closely parallels 80% of the conclusions drawn by 
Calvinists (I recognize four out of five of the "Points of Calvinism," but I 
certainly am not into following Calvin; I just happen to think he and his 
followers have drawn proper conclusions from studying the Scriptures – well, at 
least 80% of the time). </p>
<p><b><a name="sctn13">Terms Defined</a></b></p>
<p>Because of my 
assumption that readers will be driven to find out what the Bible actually 
teaches, I will save space in presenting my Biblical arguments by defining 
terminology. That is the purpose for using terminology in the first place. 
Even the word "Bible" is an extra-scriptural term that saves us from saying a 
mouthful: "the 66 book division of Scripture, including the 39 books of the 
Hebrew Scripture and the 27 books of the Christian Scriptures." Instead, we 
just say, "Bible." </p>
<p>Those who have 
concluded that the doctrine of election is unscriptural and hold the conviction 
that believers can lose their salvation will be referred to in this paper as 
Arminian. That does not mean they follow all the teachings of Arminius, just 
that they happen to agree with Arminius on those points. I will employ the same 
logic for the opposite term Calvinistic. It becomes rude and arrogant in such a 
discussion to say, "I am not a ---, I just believe the Bible" when both sides 
lay claim to a true alignment with Scripture. Let us all assume in this 
discussion that we want the teaching of the Bible to be the final authority.</p>
<p>Another term I need to 
define refers to those who follow the doctrinal perspective common in the 
Church(es) of Christ and Christian Churches, namely that one is not saved until 
he repents, believes, AND is baptized (and sometimes other conditions are added 
as well). The term referring for this belief system incorporates the name of 
the founder of that movement, Campbellism. By using this term, I do not believe 
that folks in these churches follow the teachings of Campbell blindly as Mormons 
do Joseph Smith, for example. The term "Campbellism" or "Campbellite" are used 
to refer to those who agree that the Bible's teachings and what Campbell taught 
about the relationship of baptism to salvation are in agreement. </p>
<p><b><a name="sctn14">Assumption of Integrity</a></b></p>
<p>I hold a view of 
baptism similar to most Baptists (unlike Calvin, who practiced infant 
sprinkling). However, I hold these viewpoints not because I have an agenda to 
align myself with Calvinists or Baptists, but <u>because I have concluded this 
is what the Scriptures teach</u>. There are very few conclusions we might come 
to about the Bible's teaching that someone else has not already concluded (be 
they accurate or inaccurate).</p>
<p>I will also say upfront 
that there are verses, when, taken by themselves, do <i>seem </i>to teach that 
baptism is necessary for salvation. Those who hold the Campbellite view <i>are 
not stupid</i>, nor are they necessarily any more stubborn than conservative 
evangelicals (both groups excel in this area!). If we only had the verses that 
Church of Christ folks camp on (such as 
<a href="javascript:scripture('../scripture/acts238.htm')" target=_self class=pop>Acts 2:38</a>), 
I could conclude what they 
have concluded. Although other conclusions are possible even when those verses 
are isolated, the Campbellite conclusions certainly seem a viable option.
</p>
<p>My contention is that 
looking at the broader teaching of Scripture persuades us to select differing 
conclusions than do the Campbellites. But this is not an unusual situation: 
many verses in Scripture taken in isolation could be disastrous (plucking out an 
eye, the example of Jephthah sacrificing his daughter, Abraham marrying his half 
sister, etc.). Although I disagree with some of the Campbellite views, please 
understand this: I do not view them as I would Jehovah Witnesses, Mormons, or 
the cults. I do think they err significantly, and that some of their errors 
distort salvation by grace through faith; their beliefs also result in an 
exclusivist mentality that isolates them from the evangelical community; this 
limits the blessings (and challenges required to hone truth) that come from 
experiencing the broader Body of Christ. </p>
<p><b><a name="sctn15">An Assumption of Goodwill</a></b></p>
<p>So, for purposes of 
simplicity and clarity, <i><u>I will assume that readers of my viewpoint and 
readers who differ are both driven to be true to the teaching of God's Word</u></i><u>.</u> 
That is why, for example, I will take the pains to enumerate and provide vast 
amounts of (taken in-context) Scriptural quotations. <i>Readers are encouraged 
to examine the contexts themselves</i>; my personal attempts at sincerity are no 
guarantee that I am correct. I could be sincere, but still sincerely wrong. I 
recognize I am not the ultimate interpreter, and, frankly, some of my 
interpretations somewhere must be wrong (since no two Christians agree on every 
detail in every area). It is up to the reader to discern my batting average in 
the case before us.</p>
<p>So what I am presenting 
is my UNDERSTANDING of what the Scriptures teach. The Scriptures are 
infallible, but our interpretations are not. We can try to enter into the 
culture and mindset of the human authors and audiences, and we can try to 
interpret objectively and without preexisting agendas. But if we are serious 
about humility, we have to admit that none of us are the ultimate in 
objectivity; we can only, with God's help, try to keep ourselves in check.</p>
<p><b><a name="sctn16">Is the Belief That Baptism Is Not Necessary for 
Salvation Tied Directly to a Calvinistic Model?</a></b></p>
<p>Contrary to what some 
theologians from the Churches of Christ and Christian Churches have written, <b>
most evangelicals (by far) do not believe in the doctrine of unconditional 
election</b> (that God chooses people for salvation apart from anything they do) 
although more (but still a minority) do believe in eternal security (once saved, 
always saved<i>). I am among the minority of evangelicals who believe both of 
the above.</i> </p>
<p>Most evangelicals, then 
(like the Assemblies of God, most independent Charismatic churches, Wesleyans, 
Nazarenes, Free Methodist, those conservatives within mainline Protestantism, 
those evangelicals who have chosen to remain within Catholicism, and even 
significant minorities of Baptist and Brethren groups) reject eternal security, 
yet almost all of the above do NOT believe one must be baptized to be saved.</p>
<p>Most believe that baptism is not an option for the Christian, but 
that it is separate from (though associated with) salvation. For the sake of 
simplicity, I will leave out the various views held by evangelicals who believe 
in infant sprinkling (not to insult them, but merely to limit this work), as 
well as views held by liberal mainline Protestants (I don't mind insulting 
them!).</p>
<p>So the belief that one 
is saved upon belief, before being immersed, is the common belief of most 
evangelicals, those who believe in election and those who reject unconditional 
election. Nonetheless, if one believes in election, the issue itself loses 
force, as I will hope to convince you below.</p>
<p>In this paper, I will 
not argue much about mode or timing of baptism. I am assuming that readers 
recognize that I am addressing believer's baptism by immersion after a 
profession of faith in Jesus Christ. The baptismal candidate recognizes that 
Jesus is God in the flesh, Who bled and died for our sins and then arose a 
conqueror. I happen to agree with Campbellism on the idea that baptism is for 
believers only after a profession of faith. </p>
<p>If I fail to persuade 
you to my viewpoint, I at least hope to persuade you that those of us who 
believe in salvation by grace through faith (and baptism as a non-optional 
ritual following but separate from salvation) have some scripturally reasonable 
arguments, even if you reject our viewpoint. </p>
<p>I do not believe 
baptism is an optional matter, but is a mandatory command. As a matter of fact, 
I would never join a church that did not exclusively practice believer's baptism 
by immersion. But I do not believe baptism is crucial to salvation. (I believe 
that my born-again Presbyterian or Reformed friends are saved, even though they 
disagree with me about mode and manner of baptism). Baptism is a matter of 
obedience.</p>
<p><b><a name="sctn17">Interpretational Approach</a></b></p>
<p><b><a name="sctn18">The Two Goals of Interpretation</a></b></p>
<p>To my way of thinking, 
there are <b><u>two goals in interpretation</u></b> (as opposed to application), 
and they may be summarized in the form of two questions:</p>
<ol type=1><li><p><b><u>What was the 
original thought of the author?</u></b>
In some Scripture, God dictated His Word. In other portions, the Scripture is 
BOTH the Word of God and the Word of Man. Paul, for example, knew just what he 
meant when he wrote Romans. We should therefore try to enter into his thinking.</p>
<li><p><b><u>How would the 
original recipients (or audience) have understood what was written or said?</u></b>
Too many Christians ask, "What does this mean to me?" before they ask, "How 
would the Roman believers have understood this?" The Bible is the Word of God, 
a verbally inspired and inerrant book, but it is not a magical book. The words 
express thoughts, and we must get at those thoughts. Attention to grammar and 
to the cultural background of the original recipients provides meaningful clues 
to the meaning of a text.</p>
<p><u>I also embrace the 
conviction that Biblical Christianity was best understood by the early church, 
composed of Messianic Jews</u> (see
<a href="javascript:scripture('../scripture/acts2120.htm')" target=_self class=pop>Acts 21:20</a>).<b><i>
When the majority of the church became 
gentile </i></b>(the halfway point 
was reached in about 75A.D.), <b><i>the Jewish assumptions of the New Testament 
were not as well understood and soon lost.</i></b> This is why the church of 
the second and third centuries took such sharp turns away from Biblical teaching 
and objective interpretation. Beliefs deteriorated -- (as I see it) into infant 
sprinkling, one central leader, etc. -- almost immediately after the apostles 
were off the scene.</p></ol>
<p><b><a name="sctn19">Four Interpretational 
Judgment Calls That Come Into Play</a></b></p>
<ol type=1><li><p><b>Is a statement a partial truth or a whole, complete 
truth?</b></p>
<p>"All women are human, therefore all humans are women." 
Saying "all women are human" does not mean there are not other categories of 
human (in this instance, men). This is perhaps the most common interpretational 
(and logical) error made.</p>
<p>On the other hand, Jesus statement in 
<a href="javascript:scripture('../scripture/john146.htm')" target=_self class=pop>John 14:6</a> 
about being THE way, and no one coming to the Father but by Him is so worded that it 
is a complete truth. He cannot be "a" way because He is "the" way, and He makes 
clear that there are no exceptions.</p>
<li><p><b>Does a group of things connected together mean all 
elements of that grouping contribute equally (or at least partially) to the 
result stated?</b></p>
<p>Take Jonah 4:11, "But Nineveh has more than a hundred and 
twenty thousand people who cannot tell their right hand from their left, and 
many cattle as well. Should I not be concerned about that great city?&quot; Just because God mentioned cattle here does not mean that 
his compassion for the city was as greatly affected by the number of cattle as 
it does the number of men. As a matter of fact, we might presume that God would 
have still had compassion upon Nineveh if they had no cattle.</p>
<p>At the tomb of Jesus, Mark tells us that an angel said, "He 
is not here…" Matthew tells us there were two angels at the tomb. Both are 
true. In this instance, Matthew gives us the fuller truth, Mark simply wants to 
let us know who is doing the talking (an angel). He does not address the issue 
of how many angels were present. The second angel was present, but did not 
contribute to the conversation, so Mark was not inaccurate by leaving him out of 
his narrative; Matthews's inclusion does not mean that both angels spoke.</p>
<p>I would suggest that some of the small number of verses 
that seem to imply baptism is necessary for salvation should be included under 
this category.</p>
<li><p><b>Do two seeming realities that seem to preclude one 
another force a choice between one or the other, or can they both be true, even 
though it is beyond our ability to reconcile them with our level of information 
and understanding?</b></p>
<p>A bumble bee, according to the laws of physics, cannot 
fly. So does the bumble bee really not fly, or are the laws of physics wrong? 
Or do we accept them both, believing they are somehow reconcilable, even though 
we seem incapable of making that reconciliation?</p>
<p>God is one being, yet He is Three Persons. So is His 
oneness really incorrect, or are His three personalities really not distinct and 
separate? Or do we accept them both, believing they are somehow reconcilable, 
even though we seem incapable of making that reconciliation?</p>
<p>Jesus is fully human. Jesus is fully God. Do we say Jesus 
was just a phantom, or do we deny His deity? Or do we accept them both, 
believing they are somehow reconcilable, even though we seem incapable of making 
that reconciliation?</p>
<p>Much of the Bible was written by men, using their own 
vocabulary and styles (except for the parts dictated), yet it is the Word of 
God. So is the Bible the Word of God but not the word of man, or is it the word 
of man but not the Word of God? Or do we accept them both, believing they are 
somehow reconcilable, even though we seem incapable of making that 
reconciliation? The Bible offers many such paradoxes and antimonies.</p>
<p>Some can be easily reconciled. The Scriptures are clear 
that no man has seen God, yet we read of Moses, Isaiah, and Ezekiel seeing God 
(among others). The reconciliation in that case is simply that no one has seen 
God in His pure, unmasked form. They have, instead, seen visual representations 
of God.</p>
<p>One real issue between those who believe baptism (and any 
number of other potential requirements in addition to faith and repentance) is 
necessary for salvation and those who do not relates to this question: Is it a 
number one situation or a number two judgment call? <b><u>Do we collect all 
potential contributors to salvation and create a master list, or are the 
Scriptures so repetitively clear that we understand these other numerous items 
to be merely the evident fruits of regeneration?</u></b></p>
<li><p><b>Do I distinguish between what was commanded and what 
was done?</b></p>
<p><i>To my knowledge, there is no clear cut verse that says 
we are to follow all the practices of the early church</i>. This should not be 
automatically assumed. We are commanded to observe the traditions Paul taught 
(as revealed and commanded in the epistles).</p>
<p>For example, the early church generally conducted their 
services in Greek, both the men and women wore robes, folks greeted one another 
with a holy kiss, and the men and women sat in segregated sections of the home 
in which they gathered. They did not own church buildings, nor did their 
leaders attend Bible College or seminary. Are we obligated to follow their 
pattern?</p>
<p>We must therefore make careful distinctions between what 
the early church <i>did</i> and what we are <i>commanded to do</i>. Much of 
what they did, we are likewise commanded to do, but <i>not necessarily in the 
same way</i> (e.g., there is no command to my knowledge that says we must have a 
love feast meal before celebrating the Lord's Supper, but that does seem to be 
the example of the early church based on <b>1 Corinthians 11</b>; we are nowhere 
commanded to observe the Lord's Supper every week, though the early church 
seemed to do so on a sometimes daily basis, we are not commanded to have all 
things in common and sell all we have and give our means to the church leaders, 
etc). </p>
<p>These four judgment calls must be made intelligently, 
prayerfully, and <b>consistently</b>.</p></ol>
<li><p class="bighpc"><a name="sctn2">Necessary for What?</a></p>
<p>You can tell what one's 
priorities are by how one understands incomplete questions. Like ink blotches in 
a psychiatrist's office, these questions reveal what is on our minds.</p>
<p>Most often, when I hear 
someone ask, "Is baptism necessary?" I have learned to interpret it as, "Is 
baptism necessary to salvation?" The question is generally asked by folks who 
DO believe baptism is necessary to salvation, and it reveals that they are 
focused upon "making it to heaven."</p>
<p>On the other hand, 
those of us who believe that baptism is NOT necessary to salvation want to hear 
the question as, "Is baptism necessary to please God and grow in ones walk with 
Him?" The answer to that question is, "Of course it is.&quot;</p>
<p><b><a name="sctn21">Differing Concerns</a></b></p>
<p>The way the question is 
initially heard advertises the differing emphases between these two camps. One 
is mainly concerned about "making it to heaven," the other feels secure and 
(ideally) is concerned with pressing on toward spiritual maturity. Sadly, there 
are people in both camps who are not much concerned about anything doctrinal!</p>
<p>The believer who 
understands that salvation is an unearned gift, not a probationary loan, can 
more logically focus on serving his Lord and building up the kingdom; he might 
be concerned about losing his rewards (being saved "so as by fire"), but eternal 
life is not in the balance; the believer who considers himself just a step away 
from damnation and whose destiny hangs in the balance moment by moment is more 
obsessed with what he <i>doesn't</i> do. His concern is making the grade and 
not losing eternal life at the last moment.</p>
<p>The result is two very 
differing forms of Christianity. The issue is much broader than, "Is baptism 
necessary for salvation?" The matter addresses this question: Is salvation a 
joint effort between man and God or is it totally of God? (Theologically, this 
contrast is between monergism –one, namely God, doing all the work, and 
synergism – a cooperative joint effort; I am obviously a monergist). Is Jesus 
Christ the Savior, or are we partly our own saviors? Is He a half or even 90 
percent savior? Or is He 100 percent Savior—He completely does ALL (in the 
fullest sense of the word) the saving?</p>
<p><b><a name="sctn22">Two Conflicting Viewpoints: Faith Alone or Not?</a></b></p>
<p>Within Bible-believing 
Christianity, we find many differing opinions in a multitude of arenas. There 
is nothing I believe that someone else, also claiming to be a Christian, denies. 
And that is true for each of us. Although descriptions vary, most definitions of 
what it means to be an evangelical Christian include the belief in salvation by 
grace through faith apart from works.</p>
<p>Yet within this broader 
category, there are numerous views as to what a "work" is. Most of us would 
include in this category rituals (such as baptism, fasting, communion, church 
membership). Others argue, "Those are not works – they are 'means of grace.'"</p>
<p>Works, we are told, are 
things like helping the little old lady across the street (even if she doesn't 
want to go?) or visiting a shut-in. Yet the Bible makes no distinction between 
rituals and good works. Works are put in contrast consistently with being saved 
by grace through faith. Grace means God's undeserved, unmerited favor.</p>
<p>Very few (if any) of 
those I have met who believe one has to accept Christ (repentance and faith) AND 
be baptized in order to be saved stop there. They invariably include additional 
works as necessary for salvation (in contrast to merely evidencing salvation). 
So we are ultimately addressing a much broader issue: is salvation by God's 
undeserved favor – graciously given to him "who works not," or is it a 
multi-step (you're on probation) "trial size" salvation? Is it "shareware" 
that expires after the thirty day trial unless you pay for it, or is it truly 
and completely free?</p>
<p>This contrast between 
grace and works -- something we earn and something we receive freely -- is made 
clear for us in <b>Romans 4:4-5</b>. Paul elaborates upon this theme (talking about 
God graciously choosing some Jews for salvation) further in <b>Romans 11:5-6</b>, "So too, at the 
present time there is a remnant chosen by grace. <b><u>And if by grace, then it 
is no longer by works</u></b>; <i>if it were, grace would no longer be grace</i>.&quot;
</p>
<p>Logically, everything 
must be divided into one of these two categories. Since grace comes solely from 
God, <b><u>everything else</u></b> MUST be a work. There are no 
third choices allowed in Paul's contrast. As <b>Romans 4:4-5</b> puts it: "Now when a man works, his wages are not credited to him as 
a gift, but as an obligation. However, to the man who does not work but trusts 
God who justifies the wicked, his faith is credited as righteousness."</p>
<p>Note here that God 
justifies (credits as righteous) the "wicked." They are still "wicked" when 
they are justified. It doesn't say God justifies the "formerly wicked." If 
they cleaned up their own act, that would be a work, a wage – not an unmerited 
favor. </p>
<p><b>Romans 3:21-27</b> tell us 
that God provided a gracious payment for our sin through the death of Jesus 
Christ. This is how a God Who is holy, righteous, and just can freely forgive; 
through His Son, He Himself paid the penalty.</p>
<blockquote><sup>21</sup> But now righteousness <b><u>from God</u></b>, apart from 
law, has been made known, to which the Law and the Prophets testify. <sup>22</sup>
	<u><b>This 
righteousness from God comes through faith in Jesus Christ to all 
who believe</b></u>. There is no difference, <sup>23</sup> for all have sinned and fall short 
of the glory of God, <sup>24</sup> and are justified <b><u>freely by his grace</u></b> through 
the redemption that came by Christ Jesus. <sup>25</sup> God presented him as <b><u>a 
sacrifice of atonement</u></b>, through faith in his blood. He did this to 
demonstrate his <b> <u>justice</u></b>, because in his forbearance he had left the sins 
committed beforehand unpunished-- <sup>26</sup> he did it to demonstrate his justice at the 
present time, <b><u>so as to be just and the one who justifies those who have 
faith in Jesus. </u></b><sup>27</sup> <b><u><i>Where, then, is boasting? It is excluded.</i></u></b> On what 
principle? On that of observing the law? No, but on that of faith.</blockquote>
<p>Salvation by grace 
through faith involves nothing of which we could boast about. <i>If we can take 
even a slight credit for our salvation, we have left the realm of complete 
grace.</i> If we take credit for anything we do – even believing – we have a 
basis for boasting about making the right choice. I would like to add this: if 
we take credit for choosing to be baptized and thus saved, we have contributed 
something, albeit something relatively minor, to the salvation process. The 
righteous we receive comes from God <u>completely</u>, not from within 
ourselves. Although God does help us to become righteous in character, the 
righteousness being spoken of here is bestowed upon us from God Himself. In the 
words of the KJV, this is what we call "imputed" righteousness (see also 
<a href="javascript:scripture('../scripture/2cor521.htm')" target=_self class=pop>2 Corinthians 5:21</a>).</p>
<p>Coupled with the idea 
that baptism is necessary for salvation is a firm Arminianism which stresses 
human responsibility to the exclusion of a Sovereign and Immutable God who 
needs no one </span>(<b>Acts 17:25</b>, "And he is not served by human hands, as if he 
needed anything, because he himself gives all men life and breath and everything 
else..."). See <a href="#appendix2" class="reg">Appendices below</a> regarding God's immutability.</p>
<p>Although it may seem 
that the heart of the debate revolves around a few passages in Acts, it really 
centers upon the very nature of God Himself (and the nature of fallen man). 
God's attributes, especially His immutability and sovereignty, are perhaps the 
core issues. I shall address some of these issues later in this study.</p>
<p><b><a name="sctn23">A Deductive Check</a></b></p>
<p>In math class, I was 
taught to "check" a problem by reversing it. In other words, you check a 
subtraction problem by adding the difference, etc. The same is true in Biblical 
interpretation. If our viewpoint is correct, we should see it displayed or 
lived out in Scripture.</p>
<p>Please note this 
important logical inference: if one is teaching salvation as Paul taught it, the 
same question should arise, the one they asked Paul. It is: "Shall we sin that 
grace may abound?" (<b>Romans 6:1</b>). <b><u>If this question would not 
logically come to mind with your presentation of salvation, then you are not 
proclaiming the same message that Paul proclaimed.</u></b> <i>If we preach the message Paul 
preached, this question should arise.</i></p>
<p>An Arminian or works 
Gospel would not raise the question. The obvious answer to "Shall we sin that 
grace might abound" would be, "No, for we would lose our salvation." But that 
is not the answer Paul gives. An Arminian presentation would never provoke that 
question. But if someone taught that we were saved freely by grace through 
faith in Christ, and that we were foreordained to believe and therefore secure, 
the question of why we should avoid sin WOULD arise<i>. This is a very logical 
argument, and I urge my readers to ponder this one deeply</i>. Please think 
about this. This argument itself should cause an Arminian to reassess his 
views.</p>
<p><b><a name="sctn24">The Implications of That Conflict</a></b></p>
<p>The implications of 
this difference of opinion vary with the Campbellite, since there are variations 
from extreme "you have to be baptized to be saved/non-instrumental Campbellism" 
to an approximation of the evangelical view that one is saved by faith alone 
espoused by a minority of the Christian churches.</p>
<p>Some teach that even 
believing in Christ and being baptized upon profession by immersion is not 
enough; they require candidates to be baptized under their auspices. Others 
recognize any believer's immersion as adequate, while others are unsure whether 
baptism is or is not necessary for salvation. Some independent "Christian" 
ministers even agree with my position and identify with the evangelical 
mainstream. </p>
<p>Some would say that a 
person who has believed in Christ and been immersed upon his profession of faith 
is still lost if not indoctrinated with Campbellite viewpoints and is not 
genuinely born again. Others would say he is a brother in the Lord. Most would 
say that a Presbyterian or Reformed or Methodist believer (like Jonathan Edwards 
or Wesley) who has professed Christ is unsaved because he has never been 
baptized by immersion as a believer.</p>
<p>The same perspectives 
are reciprocated. Many evangelicals doubt the salvation of someone embracing 
Campbellite theology because he is not trusting Christ as the sole basis of his 
salvation, but shares that trust with baptism and/or other good works. They 
would apply the principle from <b>Galatians 5:4</b>, </span>"You who are trying to be 
justified by law have been alienated from Christ; you have fallen away from 
grace."</p>
<p>So people on both sides 
of the argument accuse their opponents of being unregenerate and preaching a 
false gospel, thus labeling their opponents as false teachers. Certainly both 
schools cannot be right; someone is (or both are) wrong somewhere. </p>
<li><p class="bighpc"><a name="sctn3">Viewed from the Viewpoint of a Sovereign God Who 
Provides Sovereign Grace: Prove Election and Baptism is Placed in Perspective</a></p>
<p>As I see it, a correct 
view of the Sovereignty of God and of unconditional election destroys many false 
plans of salvation and clarifies how gracious (completely and totally unmerited) 
grace really is. Of course, not everyone agrees that my understanding of 
Scripture is correct! I respond to such folks with tongue in cheek: "Nobdody's 
perfect." </p>
<p>I must admit that I am 
convinced that the Scriptures teach that God is Sovereign and that salvation is 
totally -- not just mostly -- of God.</p>
<p><b><a name="sctn31">Unconditional Election Guarantees That WHATEVER the 
Requirements Are for Salvation, They Will Be Fully Met for the Elect.</a></b></p>
<p>Like God zapping Saul 
on the Road to Damascus (how much of a choice did he have?), the turning of the 
Jews to the Messiah at the end of the Tribulation 
(<a href="javascript:scripture('../scripture/zech1212.htm')" target=_self class=pop>Zechariah 12:1-2</a>), 
or his saving and sealing the 144,000 Jews during the Tribulation, God is not at the 
mercy of human fickleness, but draws His elect to Himself irresistibly. At 
least, that's how I see it. He does the converting -- completely. He coaxes us 
along to faith. Paul, who said his conversion was to serve as an example 
(<b>1 Timothy 1:16</b>), did not have too much of a choice. "But for that very reason I was shown mercy so that in me, 
the worst of sinners, Christ Jesus might display his unlimited patience <u> <b>
as an example for those who would believe on him and receive eternal life</b></u>.&quot;</p>
<p>Although man exercises 
faith, God creates that faith and coaxes the elect to salvation. The "zap" may 
not be as impressive as the zap Paul received, but it is a zapping nonetheless. 
Enough chatter. Here are the verses!</p>
<p><b><a name="sctn32">A Crash Course in Sovereign Grace</a></b></p>
<p>The doctrine of 
election and the idea of God's sovereign grace are deep subjects. Many Bible 
scholars have written books about this meaty matter, and the theme itself raises 
many questions (most of them are quite easy to answer, though we do not always 
like the answers).</p>
<p>What I hope to prove is 
this: God has chosen individuals for salvation apart from anything good in 
them. All those whom God has chosen (elected) will be saved; those He has not 
chosen will be lost. Whatever is necessary for salvation (in my view, 
repentance and faith in Christ), the elect will attain – no if, ands, or buts.</p>
<p>Let's lay our 
foundation. If God did not elect certain individuals, no one would choose Him. 
<b>Romans 3:10-11</b> reads, "As it is written: 'There is no one righteous, not even 
one; there is no one who understands, no one who seeks God.'" </p>
<p><a name="romcor">In Romans</a>, Paul argued 
that when society had knowledge of God, they abandoned the knowledge of the true 
God and created their own gods. <b>Romans 3:11</b> does not say, "no one seeks after a 
god;" the issue is seeking the true God. The true God is not appealing to the 
natural man.</p>
<p><b>Romans 8:7</b> continues 
along this theme: "the sinful mind is hostile to God." Again, the God being 
spoken of here is not just any god, but the true God. The sinful mind is not 
necessarily hostile to a false god. The sinful mind is especially hostile 
toward a God Who sovereignly elects some to salvation while passing over others, 
as is implied below. People do not like this kind of God. </p>
<p>Since, "<b><u>the man without the Spirit does not accept the 
things that come from the Spirit of God</u></b>, for they are foolishness to 
him, and <b><u>he cannot understand them, because they are spiritually discerned</u></b>&quot; 
(<b>1 Corinthians 2:14</b>), it takes a miraculous 
work of the Spirit for a man to accept the things that come from the Spirit of 
God and to understand them. Through the Holy Spirit, God overcomes man's lack 
of desire for the true God, man's hostility toward the true God, and man's 
refusal to accept the things that come from the Spirit of God.</p>
<p><b><a name="eph2">Ephesians 2:1-10</a></b> 
presents the sequence of this work. I will indent the Scripture verses with my comments 
presented between the 
verses:</p>
<blockquote><p><sup>1</sup> As for you, you were <b>dead</b> in your 
transgressions and sins, </p>
<p><sup>2</sup> in which you used to live when you followed the ways 
of this world and of the ruler of the kingdom of the air, the spirit who is now 
at work in those who are disobedient. </p>
<p><sup>3</sup> All of us also lived among them at one time, 
gratifying the cravings of our sinful nature and following its desires and 
thoughts. Like the rest, <b>we were by nature</b> objects of wrath.</p></blockquote>
<p>Note that all of us were at 
one time, "objects of wrath." We were dead in our transgressions. Dead men 
don't believe. They just stay dead. So it takes a miracle – a spiritual 
resurrection we call the New Birth or regeneration to bring us to life so that 
we can believe.</p>
<blockquote><p><sup>4</sup> But because of his great love for us, <b>God</b>, who 
is rich in mercy, </p>
<p><sup>5</sup> <b>made us alive</b> with Christ even <b>when we were 
dead</b> in transgressions--it is by grace you have been saved.</p></blockquote>
<p>Note that God made us 
alive; it wasn't some joint effort we and God did, nor something other 
Christians did – God Himself brings us to life, even though we were dead. This 
is done not because we somehow deserve it (it is not a reward or earned), but by 
grace (favor) precisely because we deserve none of it.</p>
<blockquote><p><sup>6</sup> <b>And God raised us up</b> with Christ <b>and seated 
us with him</b> in the heavenly realms in Christ Jesus, </p>
<p><sup>7</sup> in order that in the coming ages he might show the 
incomparable riches of his grace, expressed in his kindness to us in Christ 
Jesus.</p></blockquote>
<p>Once we are brought to 
life, we have a position in heaven with Christ already, and we are enrolled in 
His eternal school, the school of learning about His incomparable grace forever. 
Although we remain on earth for a time, our legal position is in heaven. Also 
worth noting is this: if we have free will now (and I do not believe we ever 
have a truly free will, I prefer responsibility), then it cannot be that 
important because we will not have free will once we are in heaven (unless you 
believe we can lose our salvation after we are already in heaven). Since we are 
already legally and positionally in heaven, seated with Christ, an Arminian 
viewpoint could logically be pressed to logically conclude that we could lose 
our salvation a million years into eternity OR that our free will is not 
absolutely necessary for our soul's health and we will lose it in glory. </span>
</p>
<blockquote><p><sup>8</sup> For it is <b>by grace</b> <i>you have been</i> saved,
<b>through faith</b>--and <b>this</b> not from yourselves<b>, it is the gift of 
God</b>-- </p>
<p><sup>9</sup> <b>not by works</b>, so that <b>no one can boast</b>.</p></blockquote>
<p><u>The whole process of 
salvation</u>: God's grace that saves us 
and even the faith we <u>exercise is not from ourselves </u>(the term "this" or 
"that" is neuter in the Greek, referring neither to only grace nor faith, but 
the whole process of salvation, all of <b>Ephesians 2:1-10</b><u>)</u>. Even the faith 
with which we believe comes from God as a free gift. We are brought to life 
apart from anything we do. The evidence of this spiritual resurrection, this 
regeneration is that we exercise the faith God gives us and we become 
believers. We are brought to spiritual life first, and then we believe. 
Although this occurs in a split second, spiritual life comes first, and then our 
belief follows. Only a spiritually alive person, brought to life by the Spirit, 
can believe (see the verses mentioned above from <a href="#romcor" class="reg">Romans and Corinthians</a>).</p>
<p>It is also worth noting 
that the phrase, "you have been saved" is the perfect tense, meaning <u>the 
event has already occurred and carries with it a current result</u>, "you have 
been and now stand saved." Although some aspects of our salvation are yet 
future ("the redemption of our body" in <b>Romans 8</b>), believers are already saved 
and are positionally in heaven because they are in Christ and Christ is in 
heaven. Note <b>Colossians 3:3-4</b>, "For you died, and your life is <u>now hidden 
with Christ</u> in God. When Christ, who is your life, appears, then you also 
will appear with him in glory."</p>
<p>If I did one thing, no 
matter how minor, that distinguished me from lost people and resulted in my 
salvation, I could boast about that one thing. But I did nothing. God did it 
all. That's why it is completely grace. No saved person can boast about 
anything as it pertains to salvation. It is not that he shouldn't, but that he 
cannot for there is nothing to boast about.</p>
<blockquote><p><sup>10</sup> For we are God's workmanship, created in Christ Jesus 
to do good works, which God prepared in advance for us to do.</p></blockquote>
<p>God not only brings us to 
spiritual life so that we believe, He also brings us to spiritual life so that 
we fulfill the good works He has prepared for us to do. Faith and good works do 
not make a person elect, but rather they are the result of being elect, the 
fruit of genuine regeneration. They are the fruit by which Jesus said others 
would recognize His disciples. Only people who are saved can do good works <u>
that truly please God</u>, for <b>Romans 8:8</b> says, "Those controlled by the sinful 
nature cannot please God."</p>
<p>So in order to do good 
works in the fullest sense, one must already be regenerate.</p>
<p>Although this portion 
gives us a logical order of the process of conversion, let me go to some 
clear-cut Scriptures that, to my way of thinking, prove that God elects us.&nbsp;
</p>
<p>But please note this: 
the term election is not double talk, it means something. If it merely meant 
God knew what people were going to do, then the authors of Scripture did us a 
disservice. The Scriptures abound with statements about God knowing what would 
happen before it did happen. We should not try to cancel out the meaning of the 
word "elect;" the term is used for a reason, to add information, not to be 
neutralized by being thrown into some circular argument. </p>
<p>I agree that God knows 
who would believe apart from election. No one. <b>Romans 3:10-11</b> is clear. But 
God knows (especially loves) certain people in advance. He knows whom He is 
going to chose before they are born. Although this contradicts the American 
idea that "all men are created equal," (which we know is not true simply by 
observation), election is, I believe, what the word of God teaches. It is not 
so much a question of whether lost people can believe, but whether they would. 
Without election, no one would believe. Yet God does hold every person 
responsible. I admit that this creates a difficult question, but the question 
did not stop Paul. They raised it on him in <b>Romans 9</b> (a text we will see later).</p>
<p>But please note this: <u><b>If what we teach about salvation matches what 
Paul taught, we should logically provoke the same questions that Paul's readers 
asked.</b></u> <i>If our "plan 
of salvation" could not logically raise the questions that Paul addressed when 
he presented the Gospel, then there is something defective in our presentation.</i> 
Does that make sense to you? </p>
<p>But first, let us note 
the basis of God's electing of both individuals and His nation. We can begin 
with the reason for God choosing Israel from </span><b>Deuteronomy 7:7-8</b> 
"The L<span class="sm">ORD</span> did 
not set his affection on you and choose you because you were more numerous than 
other peoples, for you were the fewest of all peoples. But it was because the 
L<span class="sm">ORD</span> loved you and kept the oath he swore to your forefathers."</p>
<p>God's singling out 
Israel for His blessing had nothing to do with the people's might or 
disposition. God had a special love for them because He had a special love for 
Abraham, Isaac, and Jacob. In this sense, He "foreknew" or foreloved this 
nation. But His choice of that nation had nothing to do with a special openness 
to God (they were often called a stubborn and stiff necked people).</p>
<p>Now let's talk about a 
few of many New Testament passages; please look at them in their context (you 
might take a moment and read <b>John 6</b>, at least the latter half); I think you will 
agree that I have not torn them out of context:</p>
<blockquote><b>NIV John 6:37</b> All that the Father gives me will come to me, 
and whoever comes to me I will never drive away.</blockquote>
<p>Note the promise that "All" 
the Father gives Jesus WILL come to Him, and those Who come (because they are 
given as a gift by the Father) will never be driven away. Jesus will not reject 
the Father's gift. The only condition for coming to Jesus in this way is that 
one be a gift from the Father. Once the elect (another way of saying those who 
are the Father's gift) do come, they are never in danger of being rejected.</p>
<p>This passage clearly 
teaches that salvation is dependent upon the Father's choice of individuals who 
make up a gift to the Son. It also strongly argues for eternal security.</p>
<blockquote><b>NIV John 6:44</b> No one can come to me unless the Father who 
sent me draws him, and I will raise him up at the last day.</blockquote>
<p>Only the elect are the true 
believers; it is impossible for someone to truly come to Jesus if He is not 
drawn by the Father. But those who are drawn are raised up. Additionally, 
<a href="javascript:scripture('../scripture/john66168.htm')" target=_self class=pop>John 6:61-68</a>
contrasts the "disciples" who were non-elect from 
those who were elect. The non-elect found His teaching offensive; the elect 
were drawn by the Father. Even though the elect disciples failed Jesus (and 
Peter even denied Him), they demonstrated that those who truly (unlike Judas) 
come to Jesus will never be cast out.</p>
<blockquote><p><sup>38</sup> For I have come down from heaven not to do my will but 
to do the will of him who sent me. <sup>39</sup> And this is the will of him who sent me, that I shall 
lose none of all that he has given me, but raise them up at the last day. <sup>40</sup> For my Father's will is that everyone who looks to 
the Son and believes in him shall have eternal life, and I will raise him up at 
the last day.</p></blockquote>
<p>Note here again that the 
Father's will is that the Son loses none of all that the Father gave Him. Note 
also that all who look (in faith) to the Son and believe shall be resurrected to 
life.</p>
<blockquote><p><b><a name="john123940">John 12:39-40</a></b> For this reason 
	they could not believe, because, as Isaiah says elsewhere: &quot;He has blinded 
	their eyes and deadened their hearts, so they can neither see with their 
	eyes, nor understand with their hearts, nor turn--and I would heal them.&quot;</p></blockquote>
<p>Note here that God's 
Sovereign hand is so active that He even deadened the hearts of many of the 
Jewish leaders in Jesus' day. Ultimately, the bottom line, whether we like it 
or not, is found in <b>Psalm 115:3</b>. "Our God is in heaven; he does whatever pleases him."</p>
<p><b>We can fight 
Him and impose our views of justice upon Him, or we can recognize that: Isaiah 55:89</b>, &quot;For my thoughts are not your thoughts, 
neither are your ways my ways,&quot; declares the L<span class="sm">ORD</span>. As the heavens are higher 
than the earth, so are my ways higher than your ways and my thoughts than your 
thoughts."</p>
<p>Here is another clear verse 
on election:</p>
<blockquote><p><b>NIV Acts 13:48</b> When the Gentiles heard this, they were 
glad and honored the word of the Lord; and all who were appointed for eternal 
life believed.</p></blockquote>
<p>Note the cause and effect 
relationship here. Luke is very careful to say that the result of being 
appointed to eternal life was believing, not the other way around. This helps 
reinforce the sequence Paul sets forth for us in the 
<a href="#eph2" class="reg">Ephesians 2 reference above</a>.</p>
<p>Note in the text below that those who came to 
Christ were those, "who by grace had believed." This implies that we who 
believe did so because God's undeserved grace caused us to believe!</p>
<blockquote><p><sup>27</sup> When Apollos wanted to go 
to Achaia, the brothers encouraged him and wrote to the disciples there to 
welcome him. On arriving, he was a great help to those <b><u>who by grace had 
believed</u></b>.</p>
<p><b>NIV Romans 11:5-7 </b>So too, at the present time there is a remnant chosen by 
grace. And if by grace, then it is no longer by works; if it were, grace would 
no longer be grace. What then? What Israel <b><u>sought</u></b> so earnestly it 
did not obtain, <b><u>but the elect did</u></b>. The others were <b>hardened</b>...</p></blockquote>
<p>This passage distinguishes 
the believing remnant of Israel (Messianic Jews or "The Israel of God") from the 
non-believing Jews. </p>
<p>Note the believing Jews 
were chosen by God though His grace. Grace is the opposite of merit. When you 
accept the doctrine of election, you really learn how gracious Grace is. God 
literally does it all; the good that appears in our life flows from His grace. 
The Jews who believed did not deserve salvation anymore than those who did not. 
But they believed because God graciously softened their hearts and brought them 
to spiritual life.</p>
<p>Those who earnestly sought 
eternal life did not find it. But those chosen by God did. The others were 
hardened. Note that, in this text, God seems to be the hardener (note 
<a href="#john123940" class="reg">John 12:39-40 above</a>).</p>
<p>Now if what I am saying is 
similar to that which Paul taught, you should be ready to make an objection that 
the Romans made to Paul's teaching. You should ask, "How can God hold us 
responsible if He elects some and hardens others?" You should object, "That 
isn't fair." These are very close to the questions they asked Paul, which may 
mean I am saying what Paul said. <u>If, in your explanation of salvation, these 
questions would not arise, then you are not teaching what Paul taught</u>:</p>
<blockquote><p><b>NIV Romans 9:14-16</b> What then 
shall we say? Is God unjust? Not at all! For he says to Moses, 'I will have 
	mercy on whom I have mercy, and I will have compassion on whom I have 
	compassion.' It does not, therefore, depend on man's desire or effort, but 
	on God's mercy.</p>
<p><b>NIV Romans 9:19-24</b> One of you will say to me: &quot;<u>Then why does God still blame us</u>? For who resists 
his will?&quot; But who are you, O man, to talk back to God? &quot;Shall what is formed 
say to him who formed it, 'Why did you make me like this?'&quot; Does not the potter 
have the right to make out of the same lump of clay some pottery for noble 
purposes and some for common use? What if God, choosing to show his wrath and 
make his power known, bore with great patience the objects of his 
wrath--prepared for destruction? What if he did this to make the riches of his 
glory known to the objects of his mercy, whom he prepared in advance for 
glory--even us, <u>whom he also called</u>, not only from the Jews but also from 
the Gentiles?</p></blockquote>
<p>You know, I think we've got 
a match! Interestingly, Paul does not provide us with a satisfying answer; he 
merely reaffirms the reality of God's nature and ways. We do not have to like 
to the sovereignty of God to accept it.</p>
<p><b><a name="sctn33">Putting It All Together Sequentially</a></b></p>
<p>Here, then, seems to be the 
logical sequence of events:</p>
<ol type=1><li><p>The Persons of the 
Trinity planned to glorify one another before creation (or time, for that 
matter). The plan was that the Son would become the "the Lamb that was slain 
from the creation of the world&quot; (<b>Revelation 13:8</b>).</p>
<li><p> God planned to create 
the world and mankind, and chose his elect beforehand (God did not "wing it" as 
things went along).</p>
<li><p> God regenerates each of 
His elect children; at the point of regeneration, believers are already legally 
seated with Christ in heaven and have received the deposit of God's Spirit.</p>
<p>Afterward, these 
regenerate elect choose to repent and believe the Gospel (or believe the Gospel 
and repent – the order is debatable); those ordained to eternal life believe. 
Although this all may take place in a split second, there is a sequence of 
events here, causes and effects. Ideally, through proper teaching, the believer 
follows His Savior in believer's baptism by immersion. </p>
<li><p> God keeps these elect 
saved and works in them both to "will and do of His good pleasure." He will 
discipline His elect when they stray; He will even take His elect home early 
(like Ananias and Saphira) rather than allow them to lose their salvation. God 
know the wheat from the tares, the seed that fell on the soil He prepared as 
opposed to the other soils. </p></ol>
<p><b><a name="sctn34">Questions Raised By Election</a></b></p>
<p>As mentioned above, 
this doctrine raises many questions, most of which are fairly easy to answer. 
But the point here is that whatever questions are raised, this doctrine stands 
and all answers must be given in light of it. Although there are a number of 
additional verses we can use to bolster the argument for straightforward 
election, I think I have presented enough verses for the open-minded student of 
Scripture to consider.</p>
<p>So if salvation 
requires faith and repentance, then the elect will believe and have faith. If 
salvation requires faith, repentance, baptism, and skipping on one leg, the 
elect will do that.</p>
<p>This posture now gives 
us a less desperate, less panic-driven approach to investigate the Scriptures to 
see whether baptism is necessary for complete obedience or whether it is 
necessary for salvation, two very different though related questions.</p>
<p>Some super quick answers to a few of the questions that flow from the 
doctrine of election....</p>
<p>I am not going to 
attempt to back all these answers up with Scripture at this time, nor answer 
them more than briefly. My only purpose here is to show that there are answers, 
even though they may not all satisfy us. </p>
<p><b><a name="sctn35">Common Questions That Arise When One Embraces 
Unconditional Election</a></b></p>
<p><b>Q: Why witness?</b></p>
<p><b>A:</b> (1) For the same 
reason we do anything – to glorify God. We offer Him a sweet sacrifice when we 
witness, whether an aroma of life or death 
(<a href="javascript:scripture('../scripture/2cor21516.htm')" target=_self class=pop>2 Corinthians 2:15-16</a>); 
(2) to fulfill the Great Commission 
(<a href="javascript:scripture('../scripture/matt281920.htm')" target=_self class=pop>Matthew 28:19-20</a>); 
and (3) to be used of God and rewarded for the same (implied in 
<b>Philippians 4:1</b>). 
Indeed, many great missionaries (like William 
Carey, considered the first modern missionary) and evangelists and pastors (like 
Jonathan Edwards, Whitfield, and Spurgeon) believed in election (as did 
Augustine, Calvin, and Luther).</p>
<p><b>Q: Does man have a 
free will?</b></p>
<p><b>A:</b> The Bible 
nowhere explicitly says man has a free will. I do think the Bible teaches that 
men are responsible for their actions, choices, and attitudes. But we certainly 
do not have a free will in the physical realm (I cannot float across the room, 
no matter how often I decide to do so), so it should not surprise us that we 
have freedom <b>only within boundaries</b> in the spiritual realm. J.I. 
Packer's book, <u>Evangelism and the Sovereignty of God</u> grapples with this 
issue. I prefer to use the term responsibility. </p>
<p><b>Q: If election and 
eternal security are true doctrines, then why not sin all you want? What 
restraint is there?</b></p>
<p><b>A:</b> Our baptism 
indicates God's desire for our lives (see later under <b>Romans 6</b>), living the 
resurrection lifestyle. Although I prefer the term "Perseverance of the 
Believer" to "Eternal Security," the bottom line is the same: once GENUINELY 
saved, always saved. But, when viewed from a perseverance perspective, it 
presents a truer picture: a genuinely saved person will continue to believe and 
if he falls away from the Lord, he will return; the Lord would take him home to 
glory before He would allow the errant one to lose his salvation.</p>
<p>Because a man or woman 
is elect, he or she is given a new nature that <i>predisposes that person to 
seek and serve God</i>. Additionally, assurance of salvation is partly based 
upon lifestyle. Believers who are not living for the Lord <i>could and should</i> 
question the genuineness of their conversion (see 
<a href="javascript:scripture('../scripture/2cor135.htm')" target=_self class=pop>2 Corinthians 13:5</a>) and Peter 
admonishes the believer to, "be all the more eager to make your calling and 
election sure" (<b>2 Peter 1:10</b>). God is sure, but our lifestyle and pattern of 
growth assures us.</p>
<p>So part of being a 
genuine believer IS the motivation to walk with the Lord. On the fear side, we 
can fear the disciple of the Lord, which is no pleasantry, and we will appear 
before the judgment seat of Christ in which every work will be evaluated. This, 
however, will not determine heaven or hell (lost people do not appear at the 
judgment seat of Christ, but at the Great White Throne), but rewards and review 
are in mind. But the real motivation is a positive one: the implanted desire to 
glorify God as a result of the Law of God written on our hearts.</p>
<p>There are other 
motivations besides fear of not "making it to heaven." As a matter of fact, if 
salvation were properly taught and an individual truly believes, that fear 
should forever be gone, at least that's what I understand <b>Hebrews</b> implies:
<b>2:14-15</b> "Since the children have flesh and blood, he too 
shared in their humanity so that by his death he might destroy him who holds the 
power of death--that is, the devil-- and free those who all their lives were 
held in slavery by their fear of death."</p>
<p>Those believers who 
recognize God's Sovereign grace do not live life in the fear of death. The fear they have is 
displeasing their master, being saved, but so as "by fire&quot; 
(<a href="javascript:scripture('../scripture/1cor315.htm')" target=_self class=pop>1 Corinthians 3:15</a>), 
and the chastening hand of God. </p>
<p><b>Q: Isn't the quality 
of spiritual life better for Arminians?</b></p>
<p><b>A:</b> I do not think 
so, at least, not in my personal observation. I think there are lost people who 
think they are saved and hide behind eternal security just as there are Arminians 
who avoid serving the Lord because they want to wait to the end, or 
who are "in and out" of the kingdom. Just as children tend to turn out better 
when they know their parents accept them, despite their infractions, so, I 
believe, God's children work best from a perspective of acceptance. Fear of 
hell does restrain some sin, but, if one stops there, the deeper motivations of 
the child of God are never realized, and these other motivations (mentioned 
above) are stronger in the long run.</p>
<li><p class="bighpc"><a name="sctn4">Is Old Testament Salvation That Different from 
New?</a></p>
<p><b><a name="sctn41">Nicodemus, A Teacher of the Jews Who Should Have 
Known About the New Birth...</a></b></p>
<p><b>John 3:1-21</b> is a frequently quoted text by 
Bible-oriented Christians:</p>
<blockquote><p><sup>1</sup> Now there 
was a man of the Pharisees named Nicodemus, a member of the Jewish ruling 
council. <sup>2</sup> He came to Jesus at night and said, &quot;Rabbi, we know you are a teacher 
who has come from God. For no one could perform the miraculous signs you are 
doing if God were not with him.&quot; </p>
<p><sup>3</sup> In reply 
Jesus declared, &quot;I tell you the truth, no one can see the kingdom of God unless 
he is born again." </p>
<p><sup>4</sup> &quot;How can a 
man be born when he is old?&quot; Nicodemus asked. &quot;Surely he cannot enter a second 
time into his mother's womb to be born!&quot; </p>
<p><sup>5</sup> Jesus 
answered, &quot;I tell you the truth, no one can enter the kingdom of God unless he 
is born of water and the Spirit. <sup>6</sup> Flesh gives birth to flesh, but the Spirit 
gives birth to spirit. <sup>7</sup> You should not be surprised at my saying, 'You must be 
born again.' <sup>8</sup> The wind 
blows wherever it pleases. You hear its sound, but you cannot tell where it 
comes from or where it is going. So it is with everyone born of the Spirit.&quot;&nbsp;
	</p>
<p><sup>9</sup> &quot;How can this 
be?&quot; Nicodemus asked. </p>
<p><sup>10</sup> <u>&quot;You are Israel's teacher,&quot; said Jesus, 
&quot;and do you not understand these things?&quot; </u><sup>11</sup> I tell you 
the truth, we speak of what we know, and we testify to what we have seen, but 
still you people do not accept our testimony. <sup>12</sup> I have spoken to you of earthly 
things and you do not believe; how then will you believe if I speak of heavenly 
things? <sup>13</sup> No one has 
ever gone into heaven except the one who came from heaven--the Son of Man. <sup>
14</sup> Just 
as Moses lifted up the snake in the desert, so the Son of Man must be lifted up, 
<sup>15</sup> that everyone who believes in him may have eternal life. </p>
	<p><sup>16</sup> &quot;For God so loved the 
world that he gave his one and only Son, that whoever believes in him shall not 
perish but have eternal life. <sup>17</sup> For God did not send his Son into the world to 
condemn the world, but to save the world through him. <sup>18</sup> Whoever believes in him 
is not condemned, but whoever does not believe stands condemned already because 
he has not believed in the name of God's one and only Son. <sup>19</sup> This is the 
verdict: Light has come into the world, but men loved darkness instead of light 
because their deeds were evil. <sup>20</sup> Everyone who does evil hates the light, and will 
not come into the light for fear that his deeds will be exposed. <sup>21</sup> But whoever 
lives by the truth comes into the light, <u>
so that it may be seen plainly that 
what he has done has been done through God</u>.</p></blockquote>
<p>Nicodemus was not just 
a devout Jew; he was a Rabbi who was also part of the "ruling council" known as 
the Sanhedrin. <u><b>It is worth noting that Jesus expected Nicodemus to be 
familiar with His teaching that a man must be born of water and the spirit. 
This implies that <i>whatever Jesus taught was also seen in the Old Testament</i>.</b></u> 
Hence we know that water here does NOT refer to baptism, for believers were not 
baptized in Old Testament times.</p>
<p>The key to unlocking 
this mystery is the fact that both the Old Testament Hebrew word (ruach) and the 
equivalent New Testament word (pneuma), can mean "spirit" or "wind" or "breath" 
(you might recognize the term "pneumonia"). The context determines whether the 
term is translated "wind" or "spirit" or "breath." Jesus uses this play on 
words by saying that whom the Spirit regenerates is unpredictable because the 
"wind blows where it will." He adds, "so is everyone who is born of the wind" 
(or spirit).</p>
<p>Here are the passages I 
believe Jesus expected Nicodemus to recall, passages that deal with being born 
of water and/or wind:</p>
<p><b>Ezekiel 36:25-27</b> I will sprinkle clean water on you, and you will be 
clean; I will cleanse you from all your impurities and from all your idols. I 
will give you a new heart and put a new spirit in you; I will remove from you 
your heart of stone and give you a heart of flesh. And I will put my Spirit in 
you and move you to follow my decrees and be careful to keep my laws.</p>
<p><b>Ezekiel 37:9-10</b> Then 
he said to me, &quot;Prophesy to the breath; prophesy, son of man, and say to it, 
'This is what the Sovereign L<span class="sm">ORD</span> says: Come from the 
four winds, O breath, and breathe into these slain, that they may live.'&quot; So I 
prophesied as he commanded me, and breath entered them; they came to life and 
stood up on their feet--a vast army.</p>
<p>Also in <b>Ezekiel 18:31</b>, 
we read, "Rid yourselves of all the offenses you have committed, and get a new 
heart and a new spirit. Why will you die, O house of Israel?"</p>
<p>Of King Saul, we read 
in <b>1 Samuel 10:6</b>, "The Spirit of the L<span class="sm">ORD</span> will come upon you in power, and you 
will prophesy with them; and you will be changed into a different person…."
</p>
<p>Of course one point in 
Jesus' explanation is that it is out of the hands of man to be born again, but 
is as mysterious as the wind and rain. It takes a sovereign act of the 
sovereign God to work the miracle of regeneration. That's why John writes, in 
the prologue of his gospel, "Yet to all who received him, to those who believed 
in his name, he gave the right to become children of God-- children born not of 
natural descent, <b><i>nor of human decision</i></b> or a husband's will, but 
born of God" (<b>John 1:12-13</b>).</p>
<p>Now if Jesus taught 
that baptism WAS necessary to salvation, and this was before His resurrection, 
then the thief on the cross could not be said to have been saved because He 
lived before Christ arose. You can't have it both ways. </p>
<p><b><a name="sctn42">Paul's Arguments Become Meaningless If People Were 
Saved Differently in the Old Testament Than the New (Romans 3-4).</a></b></p>
<p>Under the Old Covenant, 
animal sacrifices could avert the wrath of God physically and outwardly so that 
the people of Israel could become "clean" to participate in Israel's religious 
life and festivals. The sacrifices could not cleanse the inward man, but simply 
covered (the idea of the Hebrew word kippur, atonement) over sin. <i>To put it 
in slang terms, it kept God from zapping them like He zapped Uzzah for touching 
the Ark. The modern Jewish skull cap (kippa) is 
based upon the same concept: the covering hides man's sinfulness from God. As 
<b>Hebrews 10:4</b> tells us, </i>"because it is impossible for the blood of bulls and goats 
to take away sins." </p>
<p>Impossible means just 
that. Although not all saved people understood as much as Job, the basis of 
their salvation was still the work of Christ. <b>Job 19:25-27</b> reads: &quot;I know that my Redeemer lives, and that in the end he will 
stand upon the earth. And after my skin has been destroyed, yet in my flesh I 
will see God; I myself will see him with my own eyes--I, and not another. How my 
heart yearns within me!&quot;</p>
<p>The sacrifice of 
Christ, on the other hand, not only covered sin, but it washed sin away. Jesus 
presented His sacrifice to the Father as an offering for all our sins for all 
times (see 
<a href="javascript:scripture('../scripture/heb912152428.htm')" target=_self class=pop>Hebrews 9:12-15, 24-28</a>).</p>
<p>Most Israelites were 
not concerned about being "clean" on a daily basis, since their eternal destiny 
did not hinge upon ceremonial cleanliness. Instead, they were concerned about 
ceremonial cleansing when it became necessary to participate in a community 
religious event. Their salvation hinged upon being rightly related to God 
through faith, as Paul expounded.</p>
<p>Salvation can be 
divided into two segments: <b>objective</b> and <b>subjective</b>. Objectively, 
salvation was provided when Christ bore our sins on the cross as the sacrificial 
Lamb of God. He offered Himself to the Father on our behalf (see 
<a href="javascript:scripture('../scripture/1cor315.htm')" target=_self class=pop>Romans 5:8-10</a>, 
<b>1 Corinthians 15-15</b>, and 
<a href="javascript:scripture('../scripture/1cor315.htm')" target=_self class=pop>Hebrews 9:11-15</a>).</p>
<p>On this matter, 
conservative Roman Catholics and conservative Protestants tend to agree. Luther 
and Calvin rightly understood that the Catholic Church was right about a number 
of things, including the nature of the atonement.</p>
<p>Where Luther and Calvin 
disagreed with Catholicism was in the realm of <i>Subjective</i> Salvation (the 
technical term would be Subjective Soteriology). Luther and Calvin 
understood Paul's arguments in books like Romans and Galatians, to the effect 
that salvation was by faith (<i>personal trust</i>) not the practice of religion 
(including rituals and good works). Catholicism said salvation came by grace, 
but grace was measured out by the church through an ordained priesthood in small 
allotments through the seven "sacraments" and other works of piety.</p>
<p>Paul's argument in 
<b>Romans 3</b> and <b>especially 4</b> and <b>Galatians</b> is that <b><u>God has ALWAYS saved 
people the same way, by faith</u></b> (in contrast to works or rituals). That 
the intellectual content of faith between Old Testament believers and New 
Covenant saints differs is obvious. But the subjective element, forgiveness and 
salvation by faith (which is often linked to and inclusive of repentance) has 
remained constant.</p>
<p>Note these early verses 
of <b>Romans 4</b>:</p>
<blockquote><p><sup>1</sup> What then shall we say that Abraham, our forefather, 
discovered in this matter? <sup>2</sup> If, in fact, Abraham was justified by works, he had 
something to boast about--but not before God. <sup>3</sup> What does the Scripture say? &quot;<b>Abraham believed God, 
and it was credited to him as righteousness</b>.&quot; </p>
<p> <sup>4</sup> <u> <b>Now when a man works, his wages are not credited 
to him as a gift, but as an obligation.</b></u> <sup>5</sup> <u> <b>However, to the man who does not work but trusts 
God who justifies the wicked, his faith is credited as righteousness.</b></u>
<sup>6</sup> David says the same thing when he speaks of the 
blessedness of the man to whom God credits righteousness apart from works: </p>
<p> <sup>7</sup> &quot;Blessed are they whose <i>transgressions are forgiven</i>, 
whose <i>sins are covered. </i></p>
<p> <sup>8</sup> Blessed is the man <i>whose sin the Lord will never 
count against him.&quot; </i></p>
<p> <sup>9</sup> Is this blessedness only for the circumcised, or also 
for the uncircumcised? We have been saying that Abraham's faith was credited to 
him as righteousness. <sup>10</sup> Under what circumstances was it credited? <b>Was it 
after he was circumcised, or before? It was not after, but before!</b> <sup>11</sup> And he received the sign of circumcision, a seal of 
the righteousness that he had by faith while he was still uncircumcised. So 
then, he is the father of all who believe but have not been circumcised, in 
order that righteousness might be credited to them. <sup>12</sup> And he is also the father of the circumcised who not 
only are circumcised but who also walk in the footsteps of the faith that our 
father Abraham had before he was circumcised.</p></blockquote>
<p>Note in this text that 
both David and Abraham were forgiven or declared righteous (justified) by their 
faith. Rather than God counting our sins against us, He counts us as righteous.</p>
<p>Where do rituals come 
into play? Again, Paul is clear: the ritual follows the justification. In the 
case of Abraham, he was justified first, while still uncircumcised. But because 
he was saved, he now wanted to follow the Lord and therefore obeyed the command 
to practice circumcision.</p>
<p>Abraham was justified 
before God when he exercised faith and justified in the eyes of others when he 
obeyed. I would suggest the same holds true with baptism. We are saved when we 
believe, but we are recognized by the church community and society at large as a 
follower of Christ when we submit to baptism.</p>
<p>Interestingly, in 
Muslim countries, converts to Christianity are not persecuted until they follow 
the Lord in baptism (any mode). In the early church and anywhere where 
Christianity carries a price tag, baptism communicates identification with the 
faith of Jesus Christ; the individual moves from a seeker to a finder to an 
outward profession of faith.</p>
<p>But back to our 
subject. Why does God ALWAYS save by grace through faith? The underlying issue 
is related to "why anything?" The big picture answer is "to glorify God" (see 
<a href="#appendix3" class="reg">Appendix 3</a>). Only salvation by Sovereign Grace through faith can bring maximum 
glory to God.</p>
<p>Sovereign Grace means 
God gets 100% of the credit. Faith is the ultimate way to glorify God, since it 
focuses upon the very nature and character of God. Faith affirms that God is 
faithful, dependable, gracious, good, and keeps His Word. Unbelief is the 
opposite of glorifying God, for it calls His very character in question.</p>
<p>Dr. Ron Moseley, in his 
book, <u>Yeshua, A Guide to the Real Jesus and the Original Church</u> 
(Messianic Jewish Publishers, Baltimore, 1996) comments on pages 70-71,</p>
<blockquote><p>The notion that the 
grace of God originated with Paul is not only wrong, but shows a grave 
negligence on the part of the Church concerning its roots in Jewish history and 
faith. Abundant records, written in Jewish literature before the time of 
Christ, speak of the grace of God as the sole means of salvation. The 
Thanksgiving Scroll (one of the Dead Sea Scrolls) is an excellent example. It 
declares, &quot;Only by your goodness is man righteous or justified and by the 
multitude of your mercy...and by your magnificence you have glorified him.&quot; How 
much more clearer could it be than when this scroll says, &quot;By Your grace You did 
save my soul, for from You is my step&quot;? The scroll also says, &quot;And all the sons 
	of Your truth You bring forgivingly before You to purify them from their 
	sins by the plenty of Your goodness and the multitude of Your mercies to 
	make them stand before you forever.&quot;</p></blockquote>
<li><p class="bighpc"><a name="sctn5">The Jewish Understanding of Baptism</a></p>
<p><b><a name="sctn51">The Common Rabbinic Understanding</a></b></p>
<p>In first century 
Judaism, gentiles who were wicked and unrepentant (the vast majority) were 
called goyim, gentiles. Those who wanted to be saved and believed in the true 
God were called "fearers" (the Hebrew term means foreigners living in the land); 
these repented of their sins and believed in the God of Israel as the only true 
God. They were considered forgiven and heaven-bound by the Rabbis, and were 
expected to keep the 7 Commandments implied within the Covenant of Noah 
(<a href="javascript:scripture('../scripture/gen9118.htm')" target=_self class=pop>Genesis 9:1-18</a>), 
including: 1) no idolatry, 2) no incest/adultery; 3) no murder; 4) 
no blasphemy/misusing God's name, 5) no theft; 6) practice justice toward 
others, and 7) no eating flesh with blood in it or cutting flesh from a living 
animal.</p>
<p>If they wanted to 
become fuller converts (but not full) to Judaism, they could become known as 
"God fearers." They would keep the 7 commands of Noah, practice the Sabbath, 
eat kosher, and participate in synagogue discipleship.</p>
<p>Lastly were the 
Proselytes (the "righteous foreigners") who did all the above plus observing 
circumcision and mikveh (baptism), practicing Temple sacrifices, tithing, etc...</p>
<p>Baptism was considered 
the initiation into the religious community, thus placing the converts under the 
Mosaic Law, the priesthood, and rabbis. They were not identified as part of the 
community of Israel until they were baptized; <i>they were considered saved 
earlier, as "Fearers," when they turned from their sins to the true God</i>, <b>
<u>NOT</u></b> when they were baptized. Baptism was the identifying ritual 
initiating them into the privileges and responsibilities of keeping the Law.</p>
<p>(For documentation, 
see, Fischer, John, (editor), <u>The Enduring Paradox: Exploratory Essays in 
Messianic Judaism</u>, Messianic Jewish Publishers, Baltimore, 2000, pp. 
174-178.)</p>
<p>Since Judaism was well 
known throughout the Roman world (and gentiles by the drove were being converted 
to Judaism in the first century), this understanding of baptism was common 
then. It was, I believe, carried over into the church's understanding.</p>
<p><b><a name="sctn52">John's Baptism</a></b></p>
<p>"Baptism" comes from the 
Greek word "baptizo," meaning "to immerse or dip." We understand the Scriptures 
to teach that believers in Christ are to be immersed in "the name of the Father, 
Son, and Holy Spirit." Although baptism may be the formalizing of conversion, 
we believe individuals are saved at the point of belief. </p>
<p>Here is some interesting 
material from Brad Young's book, <i>Jesus, the Jewish Theologian</i>, pp.15-16:</p>
<blockquote><p>In the Jewish teachings of 
the Mishnah, living water—defined as rivers or seas—was the highest grade of 
cleansing for ritual immersion (m. <i>Mikvaot</i> 1:6). The person was required 
to be immersed in living water which could be collected from rain or rivers. … 
It is … significant that the Dead Sea sect required religious purity before 
baptism. The individual seeking baptism was required to turn from wickedness 
before entering the waters of ritual immersion. Repentance preceded baptism in 
the Dead Sea community.</p>
<p>John the Baptist 
employed the same standard. The one who desired baptism was required first to 
turn from sin and then approach the waters with a pure heart. … Josephus 
explains that in John's teachings repentance purified the soul while baptism 
cleansed the body. Josephus remarks concerning John the Baptist:</p>
<blockquote>For Herod had 
put him (John the Baptist) to death, though he was a good man and 
had exhorted the Jews to lead righteous lives, to practice 
justice towards their fellows and piety towards God, and  so doing to join in 
baptism. In his view this was a necessary  preliminary&nbsp;if baptism was to 
be acceptable to God. They must not employ it to gain pardon for 
whatever sins they committed, but as a consecration of the body implying 
that the soul was already thoroughly cleansed by right behavior.</blockquote>
<p>John called on the people 
to practice piety and pursue a lifestyle of right conduct. The center of John 
the Baptist's activities was located close to the geographical area of Tel 
Qumran and near the caves where the Dead Sea Scrolls were discovered. Moreover, 
John's theological understanding of baptism seems to be similar to the approach 
taken to the ritual immersion practiced by the Dead Sea sect. Was John a member 
of the Dead Sea community?...No...He did not stipulate that they sell all 
their possessions and join his group of disciples who waited for the last days 
in the wilderness. Instead his prophetic message implored the people to live a 
holy life in the mainstream of society. <u>The religious mindset at Qumran could 
not tolerate John's approach</u>.</p></blockquote>
<p>Jesus' teachings often 
paralleled and agreed with those of Hillel. So in first century Judaism, a 
gentile would be considered saved apart from becoming a FULL Jew, but could be a 
God-fearing gentile (like Cornelius). When he wanted to become a full convert, 
the ritual necessary to welcome him into the Jewish community was baptism (by 
immersion). (See also <u>Yeshua: A Guide to the Real Jesus and the Original 
Church </u>by Ron, Dr. Moseley</a>, 
Messianic Jewish Publishers, 1998 </span></p>
<li><p class="bighpc"><a name="sctn6">Jesus Example of Pronouncing People Saved Apart 
