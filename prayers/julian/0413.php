<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 13
<br><span class=byln>by Ed Vasicek</p>

<p>My Father,</p>
<p>Thank You that You are an understanding God. You are 
understanding in that You are supportive and appreciate our situations. You are 
understanding in that You are perceptive. You are keenly aware of all things, 
events, results and feelings. You grasp every situation and You discern even 
the thoughts, motives and intentions of our hearts completely. Indeed, all 
things are laid bare before You. There is no reality You do not understand, no 
problem that perplexes You and no situation that confuses You. You are God, the 
One who completely understands all that was, is, or shall be.</p>
<p>Lord, help me to be an encourager. So many of my brothers 
and sisters in Christ need an uplifting word, a thank you, a “well done,” an “I 
appreciate you.” But do not limit my encouragement to my brothers and sisters 
in Christ. Help me never view the lost as not mattering, or only mattering as 
far as the Gospel goes. May I be an encouragement and source of blessing even 
to lost family and friends, whether they come to Christ or not. Help me never 
use people, or love people only because I have an agenda for them, as well 
intentioned as I might be. Help me to love them because they are people created 
in Your image, be they lost or saved.</p>
<p>Help me to resist temptation and the lures of the Satan 
today as I choose to draw near to God. Bind the evil one and control me by the 
Holy Spirit.</p>
<p>Lord, help parents everywhere. May Christian parents in 
particular invest a quantity of time with their children. Help parents to be 
kind but firm, to be able to say no when it is in the best interest of their 
children and to adjust to differences in children based upon personality, 
gender and stage of life. May parents exemplify a Christianity that works and 
may they lead their families in prayer on a daily or near daily basis.</p>
<p>Father, thank You for the Republic of Ghana. I praise You 
that almost 15% of the population claims to be born-again evangelicals and I 
thank You for the spiritual growth that has invigorated many congregations. Yet 
many professing Christians are not faithful church attenders and solid leaders 
are scarce. Multiply the number of trained leaders. May the evangelical church 
double over the next decade and may the Gospel better penetrate the Muslim 
north and the remote areas. May Your Spirit raise up Ghana as a lighthouse in 
the region.</p>
<p>Lord, bless Medical Ambassadors International as they share 
Christ’s Gospel and “teach specific methods to prevent disease, improve 
agriculture and develop economic resources. Volunteer ‘medical ambassadors’, 
selected by the community to receive this training, share what they are learning 
with others. In this way, a process of transformative change is initiated that 
is community-owned, sustainable and which enhances human dignity.... Medical 
Ambassadors is a community of people now extending across more than 70 
countries, and numbering in the tens of thousands.” O Father, give this 
ministry harmony, success and the power of Your Spirit. May they see entire 
communities change both spiritually as they share Christ, socially and 
economically. Please keep Your hand on this ministry.</p>
<p>Now, Lord, I commit this day to You in the Messiah’s Name, 
Amen.</p>

