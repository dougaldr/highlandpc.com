<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 7
<br><span class=byln>by Ed Vasicek</p>

<p>My Triune God,</p>
<p>Despite that fact that You are 
three Persons, You are One God. You are the one creator, and there is no other 
god besides You. There is simply none like You, and no one or nothing—nor the 
total of everyone and everything—can compare with You. You are in a class by 
Yourself. You alone are God; only You alone are to be worshipped. We pray to 
no others, we sings songs of praise to no others, we believe no others can hear 
the silent prayers of our heart.</p>
<p>You are supreme and above all 
supposed gods and deities. You are the one true God. O Lord, Adonai, Yahweh, I 
am so privileged to know the one true God. My prayer is that the mass of 
mankind may turn to You and know You personally as I do.</p>
<p>Lord, help congregations that 
are seeking pastoral staff. Father, finding the right man to lead a church 
family is a challenging and sometimes risky proposition. When they find the 
right leader, Lord, help them not to try to form him into a clone of a 
well-beloved successor. May the congregation allow him to be himself. Give Him 
your sense of direction and help him bring the congregation along rather than 
to try to force them to accept His leadership direction. Please help the entire 
transition process, I ask. And may church members grow attached to their new 
pastor realizing that they are not betraying their previous pastor by loving 
the new leader.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>[for the married]</p>
<blockquote>Lord, thank You for what marriage has done for our society 
and this world. Help my marriage to be a blessing. Guard it, put a hedge 
around it and help us to be determined to work out our differences with an 
unswerving commitment. Help us to respect one another, to talk to one another, 
to compromise with one another and to enjoy one another's company. Help us to 
work at our marriage, to nurture it, to go out together, to occasionally read a 
book to help us renew our marriage and to really understand one another. When 
I do not understand, help me to know this. Do not allow me to think that I 
understand my spouse when I do not. Never let an attitude of contempt or 
sarcasm master me.</blockquote>
<p>[for the single, divorced, separated, or widowed]</p>
<blockquote>Lord, I have to admit that sometimes life is lonely. 
Although friends and family enrich my life, sometimes I regret that I am alone. 
At other times, I appreciate it! Help me to appreciate the positives: time to 
focus upon You, freedom to come and go as I please and a less complicated 
life. Help me to participate in church and community. If I am left out, help 
me to avoid being sensitive about that. Remind me that this is usually not 
intentional. Many couples have no idea that I want to be included; their minds 
blind them to the reality that singles have social needs, too. Help me to 
clearly express that I want to be included—rather than dropping hints. Lead 
me to a few good Christian friends of my gender to pal around with. Keep me 
from taking the stance of a victim.</blockquote>
<p>Lord, thank You for the work of Mission Safety 
International as they help train and counsel missionary pilots to reduce the 
number of missions-related accidents: &quot;Mission Safety International (MSI) has 
worked tirelessly to meet the safety needs of mission aviation departments 
spread from one side of the globe to the other. While small and agile as an 
organization, MSI has developed connections with a host of experienced aviation 
and safety volunteers who can be called upon at a moment’s notice to meet 
specific needs just about anywhere in the world.&quot; O Father, bless and prosper 
this work, I pray.</p>
<p>Use me to impact lives, I pray in Yeshua's Name, Amen.</p>

