<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Prayer Written for One Whose Spouse Has Had an Affair When the Offender Seems Repentant
<br><span class=byln>by Ed Vasicek</span>
<br><span class=redbld>Note: This was written to folks going through such a time and is 
therefore written in first person; this prayer does not reflect a personal experience by the author.</span></p>

<p>Dear Father in heaven,</p>
<p>I still cannot believe it. I’ve heard of other people 
whose spouses were unfaithful, but I never thought it would strike home. I did 
not anticipate that my spouse would hurt me in such a painful way, a pain that 
penetrates into my inner being. I do not know if I am angry, devastated, guilty 
of failure, foolish or a victim. Perhaps the combination of feelings I am 
experiencing cannot be described by mere words.</p>
<p>Lord, You are going to need to help me to process the 
upheaval taking place in my heart. There is so much to sort out. Will I ever 
be able to forgive my spouse? If so, will I ever be able to trust my life’s 
partner again? How long will it take for me to feel whole and human again? 
What can I do with the brokenness that drains me or the anger that rages within 
me?&nbsp;</p>
<p>I want a guarantee that this will never happen again. I 
want things fixed. But an external solution cannot resolve the tsunami that’s 
swept over my soul. It can’t be fixed today and healed tomorrow. Like a 
festering deep wound, the dangers of infection and setbacks are all too real.</p>
<p>Although I might like to think otherwise, I am not a robot 
who works on logic alone. I have feelings and am fully human. And sometimes it 
is not so nice to be human. I can’t just “get over it,” though I would love to 
do so. Instead, I must struggle through this with you.</p>
<p>Not only do I have to struggle with issues of forgiveness, 
trust, and regaining my own sense of self respect, so does my straying spouse. 
I want our marriage to be healed, but yet I am afraid my anger and the distance 
I feel will drive my partner further away from me. But I must get over that 
fear, Lord. If we are to get better, we both must take responsibility for 
ourselves. Betrayal has consequences, not because I want them, but 
because I am human. It is going to take me time, lots of time. Help me and my 
spouse to accept that, but also to believe that You are a God of grace and 
healing.</p>
<p>Lord, countless thousands, even millions have felt what I 
feel. You have restored so many marriages. People do not usually advertise it, 
but I know the statistics. So, Lord, if I am not willing to forgive now, please 
make me willing to be made willing. I know trust builds over time, and you do 
not expect me to immediately trust the repentant, but you do expect me to begin 
the forgiveness process with at least expressing a willingness to forgive.</p>
<p>I pray, Lord, that, during this fragile time, you would 
help my spouse and I to rebuild together. Help us to develop a healthier and 
closer marriage than we had. Help us develop skills that will result in us 
experiencing a satisfying marriage in the future. Lead us to counselors and 
friends who will nurture us in the right direction. Lord, with Your help and 
with time, help us not only see the damage healed, but our marriage and level of 
closeness deepened.</p>
<p>I pray in the Name of our Savior, Jesus Christ, Amen.</p>

