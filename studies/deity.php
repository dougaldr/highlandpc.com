<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Why Did the Messiah Have To Be Deity?
<br><span class=byln>by Ed Vasicek</span></p>

<p align="center">
  <b>Have you ever asked why Jesus could not be just the &quot;perfect
  man?&quot; Why did He also have to be deity?</b></p>
<p align="left">
  I think there are several reasons why, and I am going to highlight three such
  reasons. But first, we need to lay some groundwork.<br>
  <br>
  As one reads the Psalms or the praise of the heavenly host in Revelation, or
  the first chapter of Ephesians, one might conclude that all creation exists to
  demonstrate Who God is and what He is like. His &quot;glory&quot; is what is
  impressive about Him,
  (<a href="javascript:scripture('../scripture/rev411.htm')" target=_self class=pop>Revelation
  4:11</a>,
  <a href="javascript:scripture('../scripture/eph16.htm')" target=_self class=pop>Ephesians
  1:6</a>,<a href="javascript:scripture('../scripture/eph112.htm')" target=_self class=pop>12</a>,<a href="javascript:scripture('../scripture/eph114.htm')" target=_self class=pop>14</a>;
  <a href="javascript:scripture('../scripture/eph31011.htm')" target=_self class=pop>Ephesians
  3:10-11</a>,
  <a href="javascript:scripture('../scripture/ps145150.htm')" target=_self class=pop>Psalm 145-150</a>).<br>
  <br>
  If we assume that Creation (and this whole thing we call &quot;life&quot;) is
  part of a plan to showcase God's greatness, then we have a foundation. Right
  now, I am not trying to prove this is the case: you need to study the many
  Scriptures involved and come to your own conclusion.<br>
  <br>
  The angels (fallen and holy), mankind, and all creation are to view and be
  impressed with God's character,
  (<a href="javascript:scripture('../scripture/eph31011.htm')" target=_self class=pop>Ephesians 3:10-11</a>).<br>
  <br>
  But if my assumption is correct, why did Christ have to be deity, not just
  humanity? And why was His deity so intertwined with his humanity that people
  could bow in worship before Jesus and the church could be said to have been
  purchased with God's blood
  (<a href="javascript:scripture('../scripture/acts2028.htm')" target=_self class=pop>Acts 20:28</a>)?</p>
<p align="center"><b>Consider the Temptation of Jesus in the Wilderness</b></p>
<p align="left">
  Although there is division among those who believe Jesus could have sinned and
  those who believe He could not have done so, please consider the following in
  light of our assumption.<br>
  <br>
  Created beings (Satan, man) could challenge God thusly: &quot;God, you are
  holy because you are not limited as a creature. If you were limited with
  creaturely limitations, you might be very different.&quot; Now, admittedly,
  this is speculation.<br>
  <br>
  But perhaps this is what happened during the temptation. Jesus, who is God
  with creaturely limitations, is solicited to sin by Satan. But God is holy,
  even when bound with the same boundaries we experience. That, then, becomes
  the point of the temptation (and all subsequent temptations) -cf.
  (<a href="javascript:scripture('../scripture/heb415.htm')" target=_self class=pop>Hebrews 4:15</a>).<br>
  <br>
  Satan tempted God in Job
  (<a href="javascript:scripture('../scripture/job11011.htm')" target=_self class=pop>Job 1:10-11</a>,
  <a href="javascript:scripture('../scripture/job245.htm')" target=_self class=pop>2:4-5</a>)
   as God, but in the wilderness,
  Satan tempted God as Man.<br>
  <br>
  So perhaps one reason the Messiah had to be God was to prove He is holy under
  any circumstances. God is holy because He is holy, not only by virtue of His
  omnipotent position.</p>
<p align="center"><b>Consider the Debt of Sin</b></p>
<p align="left">
  As a mere man (even though perfect), Christ could not have paid for the sins
  of mankind. If His death was not merely a demonstration of God's justice but a
  just punishment for the sins of the world, the death of Christ had to pay an
  infinite debt,
  (<a href="javascript:scripture('../scripture/rev525.htm')" target=_self class=pop>Revelation
  5:2-5</a>,
  <a href="javascript:scripture('../scripture/rev5910.htm')" target=_self class=pop>9, 10</a>,
  <a href="javascript:scripture('../scripture/rev512.htm')" target=_self class=pop>12</a>).<br>
  <br>
  Who could pay an infinite debt except for an infinite being
  (<a href="javascript:scripture('../scripture/ps4978.htm')" target=_self class=pop>Psalm 49:7-8</a>)?&nbsp;
  In my view, this is perhaps the main reason why Christ had to be God. So perhaps
  the Messiah had to be God to show how gracious God is, even when He himself
  had to foot the bill!</p>
<p align="center"><b>Consider Again the Death of Christ</b></p>
<p align="left">
  Way before Jesus was born, God's people knew that God loved them with an
  everlasting love
  (<a href="javascript:scripture('../scripture/jer313.htm')" target=_self class=pop>Jeremiah 31:3</a>).&nbsp;
  Old Testament imagery was that of a mother hen
  and her chicks or a newlywed married couple. Even a newborn baby and his/her
  nursing mother were used as imagery of God's love
  (<a href="javascript:scripture('../scripture/is4915.htm')" target=_self class=pop>Isaiah 49:15</a>).<br>
  <br>
  But the death of Christ did not just inform us of God's love, it demonstrated
  or proved it. By Christ dying for our sins, He showcased God's love in the
  most profound way! This could only be accomplished through God becoming man
  (<a href="javascript:scripture('../scripture/rom58.htm')" target=_self class=pop>Romans
  5:8</a>)!<br>
  <br>
  So God is glorified (what is impressive about Him is seen) before watching
  angels and believing (and one day even unbelieving) mankind. God becoming a
  man was not, I think, just a casual change of pace for God. It involved
  showcasing His glory before a watching universe. And we, in Christ are part of
  that showcase!</p>
  
