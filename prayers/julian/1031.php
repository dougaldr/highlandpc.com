<p>Thank You, Lord, that there is solidarity within Your body. Your church is 
made up of all genuinely born-again believers in Jesus Christ regardless of 
denomination. Although we acknowledge that You have Your children within many 
congregations, we particularly pray that You would lead them into congregations 
that sincerely teach the Word of God and seek to implement its directives. Help 
Your children know the difference between religion and the teachings of the Word 
of God.</p>
<p>Help me, Lord, to be a peacemaker and not a troublemaker. May I be a blessing 
to my church, my pastor and my friends. Keep me from instigating problems, from 
gossip, slander and disrespect for those in authority. When I disagree, help me 
to disagree agreeably. </p>
<p>Lord, today I especially remember those looking for a job. Open doors of 
opportunity and match the right person to the right job. Especially help those 
who work with integrity and have good character to find employment.</p>
<p>Also, dear Father, help me to handle my finances well. Guide me when it comes 
to saving, giving and help me know when to say no. Help me control my money 
under Your guidance; please do not let money control me.</p>
<p>Lord, today I pray for the Tianjin Municipality of China with its nearly 10 
million people. Because this area is near Beijing, the authorities are 
especially strict. Perhaps .4% of the population would be made up of born-again 
evangelicals. O Lord, please work within the governing authorities to ease up 
on the restrictions in this area. May the church multiply and triple over the 
next decade, I ask. Grant many the repentance that leads to life.</p>
<p>Father, please bless the efforts of VELA Ministries, &quot;a Latin American 
evangelistic ministry centered on leadership development and evangelism. VELA is 
blessed to be doing ministry throughout Latin America in partnership with 
organizations such as the Billy Graham Evangelistic Association and Global 
Action as well as with local churches all over the USA, Canada and Mexico.&quot; O 
Father, grant this ministry a mighty harvest for Your kingdom. Provide, 
protect, empower, grant harmony and prosper this work, I pray.</p>
<p>I pray for those who work as licensed practical and vocational nurses. May 
those who know You shine in their character and compassion. Help them to share 
the Gospel with those who are interested, including patients, peers and staff 
members.</p>
<p>Help me to live out my convictions today, I request in Yeshua뇹 Name, Amen.</p>


