<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 4
<br><span class=byln>by Ed Vasicek</p>

<p>My God,</p>
<p>You are the God of the long-term plan. Of course You exist 
outside the realm of time, so You see all points of time at once. Not that I can 
understand that! So You see the big picture, and You are nudging history—including 
the infinitesimal part of history involving me—in directions that 
seem imperceptible to us. King David collected materials to build the Temple, 
Solomon, his son, had it built. Four hundred years later, the Babylonian 
(general and later king) Nebuchadnezzar destroyed it. Then it was rebuilt by 
Ezra and destroyed centuries later by the Romans—but on the same day and month 
it had been destroyed the first time! In the End Times, it will be built again, 
only to be defiled the by the Antichrist. But Lord, there is method to the 
madness. What seems random is part of Your overall plan.</p>
<p>In my life, Lord, You bring me through refining fires and 
times of blessing. As Solomon wrote, &quot;There is a time for everything, and a 
season for every activity under heaven&quot; (<b>Ecclesiastes 3:1</b>). Your Holy Spirit is 
conforming me to the image of Christ. When I suffer, I share in the sufferings 
of Christ. When I am healthy, when my relationships are solid and when I have 
plenty, I can give You thanks or I can be so busy enjoying my blessings that I 
forget where they have come from. Then You shake me to wake me from my stupor. 
You remind me that I literally depend upon You for every breath and although I 
can have the assurance of salvation, there is little else in life that is sure 
and guaranteed.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Lord, help my church family be harmonious. May the leaders 
be firm when it comes to the clear teachings of Scripture and gracious about the 
not-so-clear matters. Keep antagonists, troublemakers and those who resent 
authority away from our congregation. Help my church to be effective in 
evangelism, discipleship, loving one another and supporting world missions.</p>
<p>Father, I praise You for what You have done in the Jiangsu 
Province of China with its 74 million people. Thank You that over 6% of the 
population claim to be born-again evangelicals. Lord, please keep the church 
growing and safe. Put a hedge around this area, Lord, and may the testimony of 
the believers be strong. May the percentage of believers double over the next 
decade, I ask.</p>
<p>Lord, bless the work of Emmanuel Ministries International, and their work in 
Aruba, Czech Republic, India, Kenya, Latvia (along with Estonia 
and Lithuania), Mexico, Romania, Russia and the USA. &quot;EMI works with, within and 
for the local church, as it is God's pillar of truth and chief instrument for 
His glory in this present age.... EMI invests primarily in emerging leaders wherever 
poverty, persecution or other circumstances make training and ministry 
difficult.&quot; Provide, prosper, protect, empower and grant harmony to this 
ministry, I pray.</p>
<p>I commit my day to You, in the Savior's Name, Amen.</p>

