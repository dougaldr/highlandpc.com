<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 8
<br><span class=byln>by Ed Vasicek</p>

<p>Holy Father,</p>
<p>You are immense. By that I mean that all heaven and earth 
cannot contain You. You are limitless and endless, yet You are not restricted by 
dimension since You created space: height and depth are part of the created order 
(<a href="javascript:scripture('../../scripture/rom839.htm')" target=_self class=pop>Romans 8:39</a>). 
When we build church buildings, they are not built to house 
or contain You; they are as a convenience and a facility for Your people. They are 
not shrines but ministry centers. As Solomon prayed, &quot;The heavens, even the 
highest heaven, cannot contain You. How much less this temple I have built.&quot; (<b>1 
Kings 8:27</b>).</p>
<p>Lord, the Old Testament Feast of Passover pictures a lamb 
whose blood is shed so that Your wrath would &quot;pass over&quot; when the angel saw the 
blood on the doorposts.&nbsp; So those of us in Christ are protected from Your wrath 
by the blood of Jesus Christ. Passover reminds us that Christ, the sacrificial 
Lamb of God, died for our sins. As Paul put it, &quot;Christ, our Passover lamb, has 
been sacrificed&quot; (<b>1 Corinthians 5:7</b>). Indeed, He has.</p>
<p>Lord, I thank You today for two of the many things with 
which You have blessed me: __________.</p>
<p>Lord, I empty myself before You and ask You, Holy Spirit, to fill me. Help me 
find my strength in Your power. Holy Spirit, I give You control of my life.</p>
<p>Lord Jesus, as head of the Church, you know our church is 
supposed to gather for Christians to build up one another (edification). 
Help me to acknowledge that I need other people for me to mature evenly, that I 
am imperfect and therefore should give others room for imperfection and that it 
is not just about me, but about You AND my attempts to encourage other 
Christians. Remind me that it is about us, not just me and my needs. Help me 
to serve others within my church. Remind me that it is more blessed to give 
than to receive and that this does not only apply to money.</p>
<p>Dear God, I remember the African nation of Gambia in 
prayer. Most of this nation is Muslim, but fortunately the Muslims in this 
nation are tolerant of religious freedom. Please keep freedom strong here. 
Lord, only .3%�three tenths of one percent�of the population claim to be 
born-again evangelical Christians. O God, help the few Christians in this land 
to stand strong and may they see the evangelical church double over the next 5 
years and may that doubling pattern continue. Lord, Your Spirit is capable of 
opening eyes and working within individuals to bring them to saving faith. We 
pray that He would do so in growing numbers.</p>
<p>Thank You, Lord, for Grace Ministries International and 
their ministries in Australia, Bolivia, Congo, Costa Rica, India, Puerto Rico, 
Tanzania and Zambia. Thank You for their literature ministries. Keep them true 
to You, help them experience harmony with one another and may Your Spirit 
empower their efforts at church planting, running Christian camps, evangelism, 
literature ministry and training.</p>
<p>Today I pray for photographers. May people in this 
vocation do their work honestly and with integrity. May those who know You 
share their faith and may many in this field come to know Jesus.</p>
<p>Open my eyes to see the doors you are opening for me today. 
I am Your servant. I pray in Jesus' Name, Amen.</p>

