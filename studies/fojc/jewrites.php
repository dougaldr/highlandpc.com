<p>In today's text, we are immediately swept into the world of 
Jewish (Old Testament) rituals. Jesus was a Jew, from the tribe of Judah and 
the family of David. He was an observant Jew, raised by a mother and step-dad 
who were also observant and devout Jews.</p>
<p>Based on the Old Testament Law, Jesus was circumcised on 
the eighth day, probably in the local synagogue in Bethlehem. Since most 
fathers are uncomfortable with this procedure, a <i>mobel</i> (trained 
specialist) usually performed the circumcision.</p>
<p>Joseph and Mary took the baby Jesus a few miles away to 
Jerusalem to pay the &quot;redemption money&quot; when Jesus was 30 or 31 days old, in 
accordance with the Law of 
<a href="javascript:scripture('../../scripture/ex13216.htm')" target=_self class=pop>Exodus 13:2-16</a>.
Rather than dedicate every firstborn 
son to Temple service, this money was paid to &quot;redeem him&quot; and the Levites were 
allowed to serve in place of all Israel's firstborn. This kept families 
together. (Note: in the case of Samuel, he was a firstborn son who actually 
served as a priest even though he was not a descendant of Aaron or Levi).</p>
<p>They may have remained in Jerusalem for another 10 days so 
they could offer the purification sacrifice mentioned in 
<a href="javascript:scripture('../../scripture/lev1218.htm')" target=_self class=pop>Leviticus 12:1-8</a>.
When a woman gave birth to a male child, she had to offer one of three sacrifices, 
depending upon her financial state, 40 days after birth, or on the 41<sup>st</sup> day.
Folks who were dirt poor could offer grain, those who had enough to get 
by on could offer two turtle doves, and those who could afford it offered a sheep 
(<a href="javascript:scripture('../../scripture/lev1268.htm')" target=_self class=pop>Leviticus 12:6-8</a>). 
The priest would then offer the sacrifice, and the woman 
would dip herself in water and be considered ritually &quot;clean.&quot;</p>
<p>Because Joseph and Mary (or <i>Yosef</i> and <i>Miryam</i>, 
as they would have actually been called) offered the doves,  it is obvious that this event occurred 
before the Magi's visit as they could have easily afforded a sheep after their visit. Sequentially, 
then, we have the birth of Jesus at Bethlehem, His circumcision, redemption, and Mary's 
cleansing. Then the Wise Men appear.</p>
<p>The next main ritual to take place would have been the <i>
Bar Mitzvah</i>, generally &quot;made&quot; at the age of 13. <i>Bar Mitzvah </i>literally 
means, &quot;Son of the Commandment,&quot; and marked the passage into manhood, 
characterized by a young man's responsibility to obey the Law of Moses. Paul 
may be alluding to this in <b>Romans 7:9</b>, &quot;And I was once alive apart from the Law; 
but when the commandment came, sin became alive and I died.&quot; From this ritual 
and the Romans passage, some have postulated an &quot;age of accountability&quot; before 
God.</p>
<p>When Jesus was 12, He questioned the scholars who 
patronized the Temple courts in preparation for his <i>Bar Mitzvah </i>at age 
13. But that's where our &quot;Jesus series&quot; began!</p>
