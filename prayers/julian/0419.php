<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 19
<br><span class=byln>by Ed Vasicek</p>

<p>Heavenly Father,</p>
<p>I worship You as Maker of Heaven and Earth. This planet is 
absolutely beautiful. If it were tilted at a different angle, it would be 
submerged with water as the polar ice caps would melt. The atmosphere is just 
right, the distance from the sun is perfect and the atmosphere filters out 
harmful rays while allowing beneficial ones to 
pass. The earth, which brings forth food for all living creatures, is a place 
that we, your children, are ideally suited to inhabit and subdue. When we 
consider the vast number of stars and galaxies, 
we are overwhelmed that You should take any interest in us. Yet You do more 
than take interest in us, You love us—to the point of Calvary.</p>
<p>Thank You that You have given us all sorts of food. You 
provide us with all things to enjoy. Father, thank You for Your provision of 
daily bread.</p>
<p>I pray for those in the world who do not have daily bread, 
who are struggling just to live. Father, provide prosperity for all nations and 
people. May jobs abound and may the people of the world no longer experience 
hunger or famine at least until the end times.</p>
<p>Lord, I am burdened for my lost friends and family members 
including: __________.
O, God, I would love to see them saved! Convict them by Your Spirit and draw 
them to Yourself. Work faith in their hearts that they might believe in the 
Lord Jesus Christ in a saving way.</p>
<p>Today I pray for the Pacific island territory of Guam. 
Although much of the economy of this nation is based around the U.S. military 
bases there, the people of Guam also including Johnston and Midway Islands 
have their own culture that is only now beginning to be penetrated with the 
Gospel. Thank You for the 12.6% including many Americans who claim to be 
born-again evangelicals. Help the church to better penetrate the indigenous and 
migrant peoples. Thank You for the missionaries working to that end. Prosper 
their labors, and may many on Guam—as well as Johnston and Midway Islands—come to 
know You.</p>
<p>Thank You, Lord, for the ministry of OMS, whose mission is 
“to establish and support mature Christ-centered churches among the nations. 
[Their] founders believed that <em>‘a nation can best be won by its own sons and 
daughters.’</em> Thus, [their] mission is to build strong, indigenous, 
self-governed churches and train nationals to pastor these churches.<b>” </b>OMS 
works “in 30 different mission fields and missionaries being sent from 6 
sending countries.” O, God, use this mission to reach multitudes, train 
leaders and establish churches. Empower them by Your Spirit, give the 
missionaries harmony with one another, protect them, provide for them and use 
them.</p>
<p>Today I remember our nation’s teachers. Help them to teach 
children how to think reasonably and help them teach content and to instruct 
them well so that our young people are prepared to compete well in this world. 
May many in this field come to know You and may those Who do share their faith 
by both their actions and their words.</p>
<p>Now help me to 
walk with You today, I pray in Jesus’ Name, Amen.</p>

