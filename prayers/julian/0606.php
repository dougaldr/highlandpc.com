<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 6
<br><span class=byln>by Ed Vasicek</p>

<p>Father in Heaven,</p>
<p>Your Son, our Lord Jesus the Messiah, was referred to as 
“The Chosen One” (<b>Luke 9:35</b>). This does not mean that there were other candidates to 
become Messiah; indeed, the humanity of our Savior is unique in that He was 
conceived of a virgin and although fully human and a descendant of Adam, He was 
not contaminated by Adam’s sin. Although He could be and was tempted, He did 
not have a predisposition toward sin that we have.</p>
<p>When He was tempted, He never surrendered to sin. The 
human nature of Jesus was perfect; You chose to unite this perfect humanity to 
God the Son, and Jesus became one Person with two natures: a human and a 
divine. He was and is “The Chosen One.”</p>
<p>Lord, sometimes I confess that Your ways truly are above my 
ways. I sometimes have difficulty understanding what Your Word reveals; I dare 
not think about what Your Word has not revealed. I remember the wisdom of 
<b>Deuteronomy 29:29</b>, “The secret things belong to the L<span class=sm>ORD</span> 
our God, but the things revealed belong to us and to our children forever.” 
Help me to understand what You want me to understand; give me neither a lazy 
brain nor a mind that loves to speculate in unprofitable areas.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You. My mind is important; 
You value it greatly. Help me to be cautious about what I put into it.</p>
<p>Lord, today I remember the Latin American island of 
Martinique. Thank You that 5.4% of the population claims to be born-again 
evangelicals and that the church is growing. Lord, triple the church over the 
next decade, I ask. Help the believers to become firm; many are from messed up 
homes and need the stability and reasonability that comes from a close walk with 
You. Raise up leaders and help the Christians who reside in France 
(Antilleans) to make a difference in that nation, too. May Your Spirit grant many 
the repentance that leads to life.</p>
<p>Father, thank You for the ministry of Rogma International. 
Their goals are: “To win lost souls to Christ and to encourage and strengthen 
the saints in every way possible. To provide missionaries with an effective 
means of enhancing and enlarging their ministries. To promote and disseminate 
sound and clear Bible teaching throughout the world by books, tracts, cassette 
recordings, radio and The ROGMA International Free Bible Correspondence courses. 
To translate our Correspondence Course into as many languages as possible so as 
to reach vast numbers of people with their native tongue.” Bless their outreach, 
educational and ministry efforts. Grant them harmony with one another. Empower 
them by Your Spirit. Protect them and provide for them, I pray.</p>
<p>I pray for those who work in the petroleum, mining and geological fields. 
Keep them safe, bless their efforts and may those who know 
You shine Your light and spread Your good news.</p>
<p>Bless me today, I ask in Jesus’ Name, Amen.</p>

