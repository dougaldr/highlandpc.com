<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Personal Devotions</span>
<br>Asking For God’s Healing Touch Upon Me
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great God,</p>
<p>I love You. My life is in Your hands. I know that, unless 
I happen to be alive when Jesus returns, I will eventually face death. I know 
that all creation groans, and that I am part of creation. Although I have been 
given the “firstfruits of the Spirit,” and although my soul is now redeemed, the 
redemption of my body is yet future, something I am waiting for 
(<a href="javascript:scripture('../scripture/rom823.htm')" target=_self class=pop>Romans 8:23</a>).</p>
<p>Like the Lord Jesus Christ, I say, “Not my will, but Yours 
be done.” I know what I want, but I also know Your plans are so involved and 
complicated and intricate that I cannot perceive how You work all things 
together for good, at least often I cannot. But You are good. You are loving. 
And You are all powerful. You allow evil and the effects of the curse to 
produce a greater good in the long run. Only You alone see the big picture.</p>
<p>But I also know that often I have not because I ask not. I 
also know that You have chosen to touch millions; please, Lord, include me in 
that number. I go on record as asking. I ask You to heal me from the disease or 
condition in which I find myself. Please touch me and help me have a complete 
recovery, or at least to improve.</p>
<p>Bless the medical experts who are trying to help me. Give 
them wisdom and direction. Prosper their treatments, help them pinpoint the 
best strategy, and may it be effective. Help me respect them as I remember that 
a doctor, Luke, was privileged to write the third Gospel. Help me to 
intelligently seek competent medical attention.</p>
<p>Lord, it is my will that I would be like Epaphroditus and recover 
(<a href="javascript:scripture('../scripture/phil22530.htm')" target=_self class=pop>Philippians 2:25-30</a>) 
and not like Trophimus whom Paul had to leave behind 
because he was sick 
(<a href="javascript:scripture('../scripture/2tim420.htm')" target=_self class=pop>2 Timothy 4:20</a>). 
I know Your power is made strong in my 
weakness, and that You ways are far above mine. I know there is a time to laugh 
and a time to cry, a time for everything under the sun.&nbsp; But Lord, in Your 
mercy and grace, touch me I pray. Make me better if that is Your will for me.</p>
<p>I remember that it is not about me, it is about glorifying 
You. So touch me, for Your name’s sake, that I may glorify You. But if You do 
not, give me the grace to glorify You as well.</p>
<p>I pray in the Name 
of Jesus and for His glory, Amen.</p>

