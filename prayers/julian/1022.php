<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 22
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>I thank You for the colors of the Tabernacle and what they 
tell us about our Savior, the Lord Jesus Christ. The blue tells us His origin 
is heaven, for no man has ascended into heaven except He Who descended, even the 
Son of Man. The Red tells us that He came to offer His blood as a sacrifice and 
atonement for our sin. The white speaks of purity, for our Savior was without 
sin, completely pure. The purple tells us of His royalty. Not only is He, in 
His human nature, a direct descendent of King David and thus the legal heir to 
Israel’s throne, He is also the King of Kings, God the Son through Whom all 
things were created.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You.</p>
<p>Lord today, I especially remember those looking for a job. 
Open doors of opportunity and match the right person to the right job. 
Especially help those who work with integrity and have good character to find 
employment.</p>
<p>Lord, help me to get to know my neighbors where I live and at work or school. 
Teach me the importance of participating in my community as an avenue to benefit 
me and also as a way to show love to others.</p>
<p>Lord, help me not to be a sensitive, touchy person. Neither allow me to have 
a hard heart. Instead help me to evaluate corrections and sincere criticism 
without punishing those who expose my weak points. Help me to be teachable not 
touchy; eager to grow not defensive.</p>
<p>Thank You for freeing me from the curse of the Law. You are the God who 
justifies the ungodly 
(<a href="javascript:scripture('../../scripture/rom445.htm')" target=_self class=pop>Romans 4:4-5</a>)
not the law keeper. Indeed, none of us can keep the Law. The Law is 
good, but we are sold into bondage. Thank You that Jesus has kept the Law on our 
behalf and that since I am in Christ I have His righteousness. This 
righteousness comes to me by faith in Christ not my self-effort.</p>
<p>Lord, the Sichuan Province of China has a population over 
87 million, but perhaps only 1% of that population could be described as 
born-again evangelicals. Although missionaries have labored in the region 
(before communism), fruit was sparse. Thank You, that, though the percentage is 
low, the 1% statistic represents a surge. Your Spirit is beginning to stir 
hearts in this area. O Lord, triple the percentage of believers over the next 
three years, I ask! Give wisdom to the believers so that they maximize their 
influence for the Kingdom of God, I pray.</p>
<p>Father, bless and use the Romanian Missionary Society as 
they seek to &quot;prepare Romanian Christians to become effective pastors, 
teachers, evangelists, missionaries and Christian leaders, to publish Christian 
literature and training materials in the Romanian language, to proclaim the 
Gospel through Christian radio, to propagate churches in Romania [and] to 
provide for orphans and abandoned children.&quot; Use, provide, protect, empower 
and grant harmony to this organization to Your glory, I ask.</p>
<p>Lord, help me to look out for others today, I ask in 
Jesus’ Name, Amen.</p>

