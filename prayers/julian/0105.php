<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 5
<br><span class=byln>by Ed Vasicek</span></p>

<p>Our Creator God,</p>
<p>I confess that I believe You are a just God. You always do 
what is righteous. You may shed more grace on some than you do others, but You 
never do less than what is right. You hate corruption and injustice, and in 
Your justice You will hold us all accountable for everything we have—or have 
not—done. Thank You, that, through the work of Jesus Christ, I need not 
experience the brunt of Your just sentence. Yeshua (Jesus) took that for me.</p>
<p>Lord, I pray that You would send angels to protect my 
family and me. Thank You for sending some of the heavenly host to guard my 
loved ones who know You—and me.</p>
<p>Father, help me develop personal trust in You, a trust that 
takes the edge off of worry, a trust that looks for You to be at work in the 
routine of life. A trust that sees Your hand throughout the day.</p>
<p>Lord, please keep me healthy. Protect me from serious 
illnesses, and, if being under the curse, I must suffer viruses and ailments, we 
pray that they would not interfere with what You consider important in my life.</p>
<p>Today I remember the nation of Albania. Having been 
delivered from Communism, many Muslim groups are actively working there. 
Although many people claim some form of Christianity, only a few thousand folks 
claim to be born again. May the evangelical churches there see exponential 
growth. Please maximize the testimony of every true believer there, and hinder 
the work of the Muslim teachers.</p>
<p>Today I remember the great youth outreach clubs operating 
in our nation and around the world. I think of AWANA, the Christian Service 
Brigade, Word of Life Clubs, and denominational clubs like the Royal Rangers and 
Calvinettes. Keep them true to Your Word and use them to reach and begin 
discipling boys and girls.</p>
<p>Today we remember our local police chief or sheriff. Bless 
him or her with protection, an ethical and responsible force, and grant him or 
her the knowledge of You.</p>
<p>Guide me in my decisions today, I pray, in Jesus' Name, 
Amen. </p>

