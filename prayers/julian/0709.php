<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 9
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the Matchless God. Although Satan thinks he 
is hot stuff and although many of we humans think the sun rises and sets just 
for us, the truth is that it does—but for You! At our best, combined with all 
the saints and angels, we are not even a drop in the infinite ocean of Who You 
are. There is no comparison at all. That is why, when we reflect upon the 
reality of Who You are—and who we are in comparison—we are humbled and You 
are exalted. You have no real rivals. You are without equal, incomparable, our 
matchless God!</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Lord, I pray that You would protect my loved ones and I 
throughout our lives. Sends guardian angels to watch over us, I pray. Keep us 
from harm and evil of all kinds: physical, spiritual, mental and relational. 
Help me, and those close to me, to love You with all my heart, mind, soul, 
spirit, will and strength. Let all we are be laid before Your feet as a living 
sacrifice. May my integrity and value system be such that others know that I do 
not associate money with happiness, but I take seriously the command to owe noone 
anything except to share Your love.</p>
<p>Father, help me view my daily life—and especially Sundays—as times of training 
for Your service. The training never ends, for I can 
always be closer to You. There is always <i>so much more</i> to learn, so much growth 
to experience. Please do not allow me to rest on my laurels. And help me take 
advantage of the opportunities around me.</p>
<p>Lord, I remember the South American nation of Paraguay in 
my prayer today. Thank You for the 4.8% who claim to be born-again evangelical 
Christians, but, Lord, most of them are from immigrant colonies. The mainstream 
culture of Paraguay is in dire need of solid evangelical churches. Please, 
Lord, send laborers into the fields. Also raise up institutions to train 
leaders and help the immigrant believers to have a burden to reach their 
neighbors, I ask.</p>
<p>Thank You, my God, for the 
creative outreach of Infinity Sports as they enlist and involve “Christian 
sportsmen and sportswomen in international sports ministry.” They focus upon 
the many unreached people found within the 10/40 window. This means their 
focus is upon &quot;North Africa, the 
Middle East, Central Asia, the Indian subcontinent, China and South East Asia.&quot; 
They seek to reach people in the &quot;Gateway Cities&quot; and within nations that 
restrict missionaries or even the freedom to share the Gospel.</p>
<p>Lord, bless this work and empower it by Your Spirit. Grant 
the workers harmony among themselves, protection, direction, provision and much 
fruit for their labor, I pray.</p>
<p>I remember those who work as automotive service technicians and mechanics. Grant 
them safety on the job, help them do their work well so 
that others are safe and may those who know You shine and share Christ.</p>
<p>Help me to shine for You today, I ask in Jesus’ Name, 
Amen.</p>

