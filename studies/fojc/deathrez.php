<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>What Did Christ Do Between Death and Resurrection?
<br><span class=byln>by Ed Vasicek</span>

<p>In today's sermon, we are looking at various texts from the epistles that some 
Bible students place as occurring between the death and resurrection of Jesus. 
I have to admit that today's message includes some speculation, so please feel 
free to disagree with me. I hope I don't lose you on this one!</p>
<p>The 
burial of Christ is connected to the ritual of Christian baptism (when we are 
&quot;buried with Him&quot; in the water—see 
<a href="javascript:scripture('../../scripture/rom634.htm')" target=_self  class=pop>Romans 6:3-4</a>). 
In my mind, it fulfills the type of the two scapegoats of Yom Kippur 
(<a href="javascript:scripture('../../scripture/lev16152022.htm')" target=_self  class=pop>Leviticus 16:15, 20-22</a>). 
The first goat 
was sacrificed as a sin offering. The second goat received a symbolic transfer 
of sin and was released in the wilderness. Together, they picture the death of 
Christ (sacrifice) and the burial of Christ (our sins are carried far away).</p>
<p>The 
<a href="javascript:scripture('../../scripture/heb92327.htm')" target=_self  class=pop>Hebrews 9:23-27</a> 
passage is a bit more confusing. <b>What</b> happened is clear: 
Christ offered His blood to the Father as a sacrifice for sin in heaven itself;
<b>when</b> it happened is not so clear.</p>
<p>But 
the strangest passage by far is that of <b>1 Peter 3:18-20</b>: 
<blockquote><b><i>For 
Christ died for sins once for all, the righteous for the unrighteous, to bring 
you to God. He was put to death in the body but made alive by the Spirit, 
through whom also he went and preached to the spirits in prison who disobeyed 
long ago when God waited patiently in the days of Noah while the ark was being 
built....</i></b></blockquote>
<p>The &quot;spirits in prison&quot; are apparently the &quot;sons of God&quot; or fallen angels of 
<b>Genesis 6:1-5</b>. 
These fallen angels were somehow able to manipulate human 
genes (perhaps possessing men?).
<blockquote><b><i>When men began to increase in number on the earth and daughters were born to 
them, the sons of God saw that the daughters of men were beautiful, and they 
married any of them they chose.... The Nephilim were on the earth in those 
days—and also afterward—when the sons of God went to the daughters of men and 
had children by them. They were the heroes of old, men of renown. The L<span class=sm>ORD</span> saw 
how great man's wickedness on the earth had become, and that every inclination 
of the thoughts of his heart was only evil all the time.</i></b></blockquote>
<p><b>Jude 1:6-7</b> interprets <b>Genesis 6</b> for us:
<blockquote><b><i>And the angels who did not keep their positions of authority but abandoned 
their own home—these he has kept in darkness, bound with everlasting chains for 
judgment on the great Day. In a similar way, Sodom and Gomorrah and the 
surrounding towns gave themselves up to sexual immorality and perversion. They 
serve as an example of those who suffer the punishment of eternal fire.
</i></b></blockquote>
<p>Another point of controversy is: &quot;Did Christ make this victory 
announcement to these imprisoned spirits between His death and resurrection or 
just after He was raised by the Spirit? The text is ambiguous, so we cannot be 
sure.</p>
<p>Also note that some hold that the configuration of Sheol (Hades) was such 
that before the Resurrection, it was divided into two compartments, torment for 
the lost and paradise for the saved, as seen in the Rich Man and Lazarus parable in 
<a href="javascript:scripture('../../scripture/luke161931.htm')" target=_self  class=pop>Luke 16:19-31</a>. 
When Christ arose, He emptied the paradise compartment, 
carrying them to heaven 
(<a href="javascript:scripture('../../scripture/eph4910.htm')" target=_self  class=pop>Ephesians 4:9-10</a>), 
leaving those in the torment compartment 
in place until judgment day, when Death and Hades are cast into the Lake of Fire 
(<a href="javascript:scripture('../../scripture/rev201415.htm')" target=_self  class=pop>Revelation 20:14-15</a>). 
Although this is possible, we want to avoid being dogmatic 
about such unclear passages. Instead, we need to develop our convictions based 
upon clear and repeated Scripture.</p>

