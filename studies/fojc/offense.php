<p>One of the best ways to 
determine whether you are interpreting Scripture correctly is to do what I call 
the “deductive check.” In mathematics, we can check a subtraction problem by 
adding the answer to the amount we subtracted. The result should match our 
beginning number. In the Bible, we can draw our conclusions and then see if 
Jesus (or other key characters) always practiced those principles as we 
understand them.</p>
<p>When you come to a verse like 
<b>Philippians 2:14</b>, “Do all things without complaining....” and you conclude that 
one should never complain, you can check that against the life of Christ. Did 
Jesus complain? Certainly. How about <b>Mark 9:19</b>, “O unbelieving generation...how 
long shall I put up with you?” Oops, there goes our conclusion. Better adjust 
it to “the Christian should not be characterized by complaining.”</p>
<p>Some people have adopted a “Mr. 
Rogers” view of Jesus. They distort the true Jesus and believe He never raised 
His voice, never confronted anyone, never got angry. But this is not the Jesus 
of the Bible. In our text today, Jesus goes on the offense. He presents a 
puzzling question to His adversaries as opposed to merely fielding their 
inquiries. The Pharisees have been putting Jesus down. Now in 
<a href="javascript:scripture('../../scripture/matt231336.htm')" target=_self  class=pop>Matthew 23:13-36</a>, 
He calls them all sorts of unpleasant names, like: hypocrites, sons of hell 
(what would you think of me if I referred to a false teacher by that name?), 
blind guides, blind fools (after Jesus warned us about calling someone a fool!), 
white-washed tombs, descendants of those who murdered the prophets, snakes, and 
brood of vipers. Whew—that’s name calling.</p>
<p>This is the real, sinless Jesus 
of the Bible. Yet His labeling and “judging” others would not meet modern 
popular “ethical” standards. There is something wrong with modern standards if 
Jesus does not live up to them!</p>
<p>We cannot speak with the 
authority in which Jesus spoke. God the Son is perfect, our hearts are 
corrupted by sin. But two things are clear: 1) it is not necessarily sinful to 
label, nor 2) is it wrong to expose and condemn lies and error. Indeed, a 
willingness to tell it like it is can be defined as part of Christ-likeness. 
Unlike Jesus, we must guard our inclination to turn loving the truth into mere 
sinful slander.</p>
<p>Since we are sinners, we must be 
careful that our attempts to discern truth from error do not cross the line into 
reckless arrogance, as Jude warns us, &quot;...these dreamers...reject authority and 
slander celestial beings...” (<b>Jude 1:8</b>). We had better be sure before we condemn. 
But sometimes to do less would be cowardly. Being Christ-like does mean having 
compassion, but does not mean being wimpy.</p>

