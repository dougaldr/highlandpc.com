<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 7
<br><span class=byln>by Ed Vasicek</p>

<p>Precious Lord,</p>
<p>You are Who You Are, the Great I Am, the One Who Has Always 
Existed, Yahweh, the King of Kings and Lord of Lords. You are the God Who keeps 
His covenant and honors His agreements. We cannot fathom Your majesty; as we 
examine the vastness of the universe, we get just a taste of Your power and 
grandeur. We are awestruck at Who You are and thus we should be. I, O Lord, 
marvel that You should even know who I am. But more than that, You know every 
detail of my life and You care about me more than I can imagine.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>[For the married]</p>
<blockquote>Lord, thank You for what marriage has done for our society 
and this world. Help my marriage to be a blessing. Guard it, put a hedge 
around it and help us to be determined to work out our differences with an 
unswerving commitment. Help us to respect one another, to talk to one another, 
to compromise with one another and to enjoy one another's company. Help us to 
work at our marriage, to nurture it, to go out together, to occasionally read a 
book to help us renew our marriage and to really understand one another. When 
I do not understand, help me to know this. Do not allow me to think that I 
understand my spouse when I do not. Never let an attitude of contempt or 
sarcasm master me.</blockquote>
<p>[for the single, divorced, separated, or 
widowed]</p>
<blockquote>Lord, I have to admit that sometimes life is lonely. 
Although friends and family enrich my life, sometimes I regret that I am alone. 
At other times, I appreciate it! Help me to appreciate the blessings of the 
single life: time to focus upon You, freedom to come and go as I please and a 
simplicity of life. Help me to participate in church and community. If I am 
left out, help me to become tough to that. Help me dive in and include myself. 
Help me remember that this is usually not intentional. Many couples have no 
idea I want to be included and their minds blind them to pick up on my desire to 
be included. Help me express to them in no uncertain terms that I want to be 
included. Keep me from taking the stance of a victim and help me to celebrate 
the single life.</blockquote>
<p>Today, Lord, I am praying for the African ministry of 
Mauritius. Thank You for the 7.9% of the people who claim to be born-again 
evangelical Christians. Yet this is a tough land to evangelize because of its 
religious diversity: Hindu, Islamic and other religions are competing with the 
Gospel. Empower the missionaries and Christians and may Your Spirit draw many 
to Christ. Please double the evangelical population within the next decade, I 
ask.</p>
<p>Thank You for the ministry of Titus International. &quot;The mission of Titus 
International is to train nationals to reach their world for Christ. This 
objective is being accomplished primarily by...missionaries (American and national) serving in the United States and on foreign 
fields.... Nationals work full time...in Moldova...in Romania, Ukraine, Czech 
Republic, Austria and Jewish evangelism.&quot; Lord, bless this ministry: protect 
them, provide for them, empower them by Your Spirit, and grant them harmony.</p>
<p>I pray for the those working in rail-track laying and maintenance-equipment operation. Keep them safe, help them do their 
job well to keep others safe and may those who know you display a good 
testimony of the grace of God.</p>
<p>Help me keep my eyes on You today, I pray in Jesus’ Name, Amen.</p>

