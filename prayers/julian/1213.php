<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 13
<br><span class=byln>by Ed Vasicek</p>

<p>Father in heaven,</p>
<p>As Christmas approaches, I recall the birth of the Savior 
and the events that surrounded it. The same night Jesus was born, some 
shepherds were out in the fields supervising their flock. Since Temple sheep 
were brought through Bethlehem year round as they headed toward Jerusalem, these 
sheep remind us that the Lamb of God would one day give His life as the ultimate 
and final sacrifice for sin.</p>
<p>While they were watching their sheep on what seemed to be 
an ordinary evening, suddenly an angel appeared to them, announcing he had good 
news of great joy: the Savior had been born in city of David, Bethlehem. They 
would find Him wrapped in swaddling cloths and lying in a manger. The angel was 
joined with a large number of heavenly beings who were glorifying God and then 
returned to heaven.</p>
<p>So Jesus Christ was welcomed into this world with no pomp 
from His fellow Israelites or the human race at large. But the heavenly hosts 
realized the import of the incarnation. So today the truly heavenly minded 
pursue the truth of Your Word. Help me to be among their number, I ask.</p>
<p>Lord, I pray for balance in my life. Your Word challenges 
me in so many areas, it seems like I am on a sailboat sometimes running from one 
end to the other to keep it from tilting into the water. If I grow in 
discipline, I find myself becoming less compassionate. If I focus on godly 
behavior, my zeal for You dissipates. If I focus on overcoming the world, I 
start viewing lost people as the enemy rather than victims of the enemy. I 
remember the example of Jesus who socialized with tax collectors and sinners, 
but I also remember &quot;bad company corrupts good morals&quot; (<b>1 Corinthians 15:33</b>).</p>
<p>Gracious Master, some of the people I have to deal with are 
unpleasant or unimaginative. Sometimes my own personality defects make me 
uninviting. So help me, Lord, to be patient with others but to humble myself and 
check my own arrogance or rudeness.</p>
<p>Help me to resist temptation and the lures of Satan 
today. Help me to remember that my faith is like an anchor to my soul. It 
reaches through the deep and troubled waters of life and makes a firm connection 
with the Rock of Ages, the Lord Jesus Christ.</p>
<p>Lord, bless the ministry of AIDS Orphans and Street Children &quot;established in 
1993 to help care for the nearly 38,000 children who are living in sub-Saharan 
Africa that have been left orphaned or who do not have a sustaining family 
because of the HIV/AIDS virus. AOSC runs an orphanage in Mozambique and operates 
AIDS Orphans Rescue Units in several sub-Saharan African nations. Food, 
clothing, shelter, discipline, Bible studies and practical training in such 
things as sewing, cooking, construction, gardening, farming and mechanics are 
provided. [Their] goal for these children is to grow up to be self-sustaining in 
their adult life.&quot; Lord, empower these workers, provide for them and may many of 
these children come to know Jesus and live responsible and productive lives, I 
ask.</p>
<p>Now help me to love You today, I ask in Messiahï¿½s Name, Amen.</p>

