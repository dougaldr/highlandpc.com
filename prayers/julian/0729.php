<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 29
<br><span class=byln>by Ed Vasicek</p>

<p>Omnipotent King,</p>
<p>Your Word tells me, &quot;Let not the wise man boast of his 
wisdom or the strong man boast of his strength or the rich man boast of his 
riches, but let him who boasts boast about this: that he understands and knows 
me, that I am the L<span class="sm">ORD</span>&quot; (<b>Jeremiah 9:23b-24a</b>). 
What glory, that I can both know 
and understand You. I cannot fully know or fully understand another human 
being and You are infinite. I confess I cannot know You fully or understand 
You completely. But, when I put my faith in Christ, I began to know You—not 
just to know about You. You enjoy walking with me daily and revealing Yourself 
to me. Your Word sometimes jumps out at me and like the Psalmist, You give me 
insights that my (lost) teachers do not have 
(<a href="javascript:scripture('../../scripture/ps11999.htm')" target=_self class=pop>Psalm 119:99</a>). 
You are the God Who wants to be known and understood.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. I claim Your protection and ask You to bind Satan and his kingdom from 
hindering me.</p>
<p>Father, I have some personal health concerns including: __________. 
I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>I am so thankful that this body of mine does not have to 
last forever. When Jesus returns, I will receive my glorified body!</p>
<p>Lord, help me to be a wise and reasonable person. When I 
have a disagreement, help me to see the other side and to believe that the 
other side has some reason to it. Help me to step out of my emotions and seek 
to fairly understand opposing viewpoints, I ask. May I work hard at being fair, 
even if it is to my disadvantage.</p>
<p>I remember the nation of Senegal in prayer today. 
Recently, Islamic fundamentalists have taken over the government. Over 92% of 
the population is Muslim and born-again evangelicals are a mere .1% of the 
population. Some missionary work has been fruitful, but as a whole, not very 
successful. There are many unreached people groups. Six New Testaments have 
been translated with 19 underway, but it is unclear if this work will be able to 
continue under an Islamic regime. Grant wisdom and safety to the Christians and 
missionaries there; may this nation turn around by the power of Your Spirit, I 
ask. May many from Senegal find the freedom that Christ brings.</p>
<p>Today I pray for The Mission Society. It &quot;has a two-fold 
vision: First on behalf of Christ, [they] go to the lost...sending out 
witnesses to engage in an evangelical, holistic ministry… [and they are] involved in 
evangelism and discipleship, church-planting, relief and development, vocational 
training, micro-economic development assistance, medical/humanitarian aid, 
theological education and ministries to youth and children.&quot; Bless their work 
with fruit; may many come to know and follow Christ. Grant them harmony, 
protection, provision, and the power of Your Spirit, I ask.</p>
<p>May Your grace be with those who work as structural metal 
fabricators and fitters. Grant them safety, help them to work heartily and 
honestly and may those who know You demonstrate their faith by their works.</p>
<p>Help my works to demonstrate my faith today, I pray in Yeshua's Name, Amen.</p>

