<p id="pgt"><span class="pgh">Articles:
<br>Ethics, Morals, Behavior</span>
<br>The Power of an Anti-Example
<br><span class=byln>by Ed Vasicek</span></p>

<p>I am persuaded that positive thinking, tempered by realistic thinking, is
crucial to a happy life. But if you find yourself like me, keeping a cheerful
spirit is an arduous task. Numerous studies certify that people with a positive
disposition do better across the board: they are healthier, enjoy better
relationships, and relish life in general. It is worth struggling to be
positive, even if our success is limited. Generally speaking, the more positive
we are, the better off we are.</p>
<p>I sometimes find it helpful to focus my mind upon a positive person and then
try to imitate his or her responses to certain situations. There are many
Scriptural admonitions to do just that.</p>
<p>Another trick that helps me think more positively sounds strange at first,
yet it sometimes works: I remember people with undesirable qualities and tell
myself, &quot;I don't want to be like them. I'd better improve my
attitude!&quot; When I feel unhappy, discontent, and whiny, I think of others
who habitually nurture unhappiness. When I feel lazy, I think of some of my
family members who lacked initiative and tell myself, &quot;I don't want to be
like them!&quot; When I sense arrogance taking over, I think of some proud
people I have experienced and remember how childish they appeared to me, or I
think about one of my own humbling episodes. When I feel covetous, I think of a
few folks who are into material possessions but display little depth of
relationship. Yes, it is possible that these anti-examples might, in some cases,
be in the right and perhaps my perspective is wrong. But I try to think of the
extreme examples in which matters seem clear. (None of you who are reading this
are those examples, by the way.) Just as virtues jump out in some instances, so
do vices. Is such a thought process a Christian way to think? Is there power in
an anti-example of Christian virtue? My answer to both of these questions is
&quot;yes.&quot;</p>
<p><b>Luke 17:32</b> is a verse kids in camp love to learn (along with
&quot;Jesus wept&quot;) because they get credit for memorizing a full verse, yet
this verse is short. The Luke passage simply reads, &quot;Remember Lot's
wife.&quot; Jesus uses Lot's wife as an anti-example to teach the lesson of <b>verse
33</b>, &quot;Whoever tries to keep his life will lose it, and whoever loses his
life will preserve it.&quot; The picture of Lot's wife, who was turned into a
pillar of salt (e.g., buried alive in salt), was worth a thousand words. She
tried to hang onto the wealth and lifestyle she had in Sodom, and because she
looked back and wouldn't let it go, died a horrible death. Lot, on the other
hand, released his old life and lived. When we are tempted to prioritize
material goods, wealth, and prestige over our relationship with God, we should
remember Lot's wife as an anti-example.</p>
<p>Scriptural anti-examples are not hard to come by. I can choose to remember
&quot;...Balaam's error...&quot;
(<a href="javascript:scripture('../scripture/jude11.htm')" target=_self class=pop>Jude 11</a>).&nbsp;
Balaam was a man who compromised the truth for material gain and prestige. When I sense myself
hardening my stubborn heart in the midst of a disagreement, I try to recall the
foolish decision of Rehoboam, son of Solomon, who refused to compromise with
others and saw his kingdom halved overnight.</p>
<p>Positively Paul writes, &quot;Join with others in following my example,
brothers, and take note of those who live according to the pattern we gave
you...&quot; (<b>Philippians 3:17</b>). Negatively, <b>Hebrews 4:11</b> warns us to be
aware, &quot;...so that no one would fall by following their example of
disobedience.&quot; We learn from both positive and negative examples.&nbsp;
Just as
we are to emulate the strengths of others, so we are to shun their weaknesses.</p>
<p>If we are to be completely honest, we must admit that even good people have
their times of failure. Peter denied Christ three times and later snubbed
gentile believers
(<a href="javascript:scripture('../scripture/gal21113.htm')" target=_self class=pop>Galatians 2</a>).&nbsp;
Paul had to confront him about this
latter behavior. Doubting Thomas is remembered for a single lapse, not for his
lifetime of faithful service and martyrdom. Noah's drunkenness, David's sin with
Bathsheba, Moses' striking the rock, Abraham's compromising behavior with
Hagar&#151;even good examples are not perfect ones. Of course, Christ is the only
exception to this rule.</p>
<p>So are we &quot;anti-examples&quot; for others? Do good Christian people look
at me and say, &quot;I don't want to be ________ like Pastor Ed?&quot; I am sure
it does happen, at times. If Noah, Abraham, Moses, David, John Mark, Peter,
Miriam, and Thomas had their anti-example moments, we can count on the reality
that we will, too. Our goal should be to be a positive example, an example of
what should be, not an anti-example.</p>
<p>Part of growth is &quot;monkey see, monkey do,&quot; but we have to
&quot;see&quot; the right monkeys! We grow by both imitating good examples and
by avoiding the behavior of anti-examples. Or, to put it in more pithy terms, we
can learn from the mistakes of others rather than making our own. Part of the
secret of being positive is to repel the negative.</p>

