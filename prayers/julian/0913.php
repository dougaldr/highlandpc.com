<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 13
<br><span class=byln>by Ed Vasicek</p>

<p>My Lord,</p>
<p>You are certainly just; You always do right. True, Your grace means that You 
bless me even though I am unworthy, but the wrath that was due me fell upon my 
Savior, Jesus Christ, Who was made a curse for me 
(<a href="javascript:scripture('../../scripture/gal313.htm')" target=_self class=pop>Galatians 3:13</a>).</p>
<p>Your justice does not mean that You treat everyone equally, but rather that 
no one gets less than he or she deserves. Although I must admit that Your ways 
differ from the common concept of justice, You are just in the truest and purest 
sense of the Word. Your character defines justice and Your Law reveals the 
wisdom of Your justice.</p>
<p>Lord, please send angels to protect my family and me. Thank You for making 
Your angels ministering spirits who serve the heirs of salvation.</p>
<p>Mighty King, help me to trust in You. May that trust cut down my propensity 
to worry. When I am afraid, help me to trust in You. Help me to look for and 
find Your hand in daily life, I ask.</p>
<p>Lord, please keep me healthy. Protect me from serious illnesses, I ask. I 
know You bring us through times of suffering to develop us and make us holier, 
but, Lord, I also know that we have not because we ask not. So, Lord, I am 
asking.</p>
<p>Father, help me confront my brothers and sisters in Christ when necessary. 
Although love covers a multitude of sins—those small infractions or things I 
can get over—sometimes an offense wedges itself within me. Then I need to 
follow the advice of 
<a href="javascript:scripture('../../scripture/matt181517.htm')" target=_self class=pop>Matthew 18</a> 
and go to the one who offended me. Talking 
behind his/her back will not satisfy You, nor will dropping hints. Help me 
speak the truth gently, but help me speak it indeed.</p>
<p>Lord, thank You for how You have worked in the Jilin Province of China. I 
praise You that perhaps 5% of the population is born-again evangelicals. O 
Lord, multiply the number of believers in this province of 27 million people. 
May government officials appreciate the contribution of Christians to their 
society rather than persecuting them, I ask. Help the Gospel to penetrate all 
regions within this province, I pray.</p>
<p>Lord, thank You for the ministry of The India Children's Choir as they 
travel the world to tell how the Gospel has transformed their community. 
The Hmar people of India were at one time headhunters, but missionaries arriving in 
India in 1910 found the people responsive to the Gospel. As the descendents of 
these people (The Children’s Choir) present this story via musicals, Your people 
are reminded of the impact of the Word of God. Working with &quot;Bibles for the 
World,&quot; the choir moves participants to give toward providing Bibles in foreign 
lands. O Lord, bless this ministry, I ask.</p>
<p>Lord, I pray for those who work in the realm of 
electricity: electrical and electronic engineering technicians and 
electricians. Although their job descriptions are very different, help those 
who know You in these fields to shine for Jesus Christ.</p>
<p>Now, Lord, help me to shine for Jesus in my world, I pray, Amen.</p>

