  <p align="center"><b>Introduction</b>
  </p>

  <p>
  The &quot;judgment seat of Christ&quot; is an often ignored or
    side-stepped doctrine. But the New Testament is filled with information on
    this subject. It is presented as very important, not trivial, as is the
    related subject of rewards.
  </p>

  <p>
  There are three common reasons that result in the avoidance of
    this subject.
  </p>

    <ol>
  <li>The first is confusion between the Great White Throne Judgment
    and the Judgment Seat (Bema) of Christ.<br>
    <br>
  The Great White Throne Judgment is mainly for the LOST and
    determines the degree of punishment for them
    (<a href="javascript:scripture('../scripture/luke124748.htm')" target=_self class=pop>Luke 12:47-48</a>)
    and occurs after the Millennial Kingdom
    (<a href="javascript:scripture('../scripture/rev201115.htm')" target=_self class=pop>Revelation 20:11ff</a>).<br>
    <br>The Judgment Seat of Christ is for Believers only, and the issue
    is degree of reward
    (<a href="javascript:scripture('../scripture/1cor31415.htm')" target=_self class=pop>I
    Corinthians 3:14-15</a>, <a href="javascript:scripture('../scripture/2cor510.htm')" target=_self class=pop>2
    Corinthians 5:10</a>),
    not heaven or hell. (We are not discussing here the judgment between the sheep and goats of <b>Matthew
    24-25</b>).<br><br>
    <li>The second reason is the subject is avoided is because it does
    not fit in a neat &quot;Scofield/Ryrie&quot; package. For some reason the
    top trend setters in our literal (dispensational) way of understanding the
    Bible have downplayed the judgment seat and transformed it into a mere
    awards ceremony, and peer pressure is hard to resist. When people we respect
    hold these views, we tend to assume our questions are not valid, and the
    obvious Scripture is maybe not so obvious.<br><br>
  <li>The third reason is that of reconciling what appears to be two
    conflicting views: When we are saved, our sins (past, present, and future)
    are forgiven. How, then, can we say God will bring every work into judgment,
    since many of our works either sinful or contaminated by sinful motives?&nbsp;
    My answer is this: everything we've done will be evaluated based on the act
    itself and its motive, not as &quot;sins,&quot; per se. The Judgment Seat of
    Christ is not a scolding for sin, but a review of EVERYTHING (no
    exceptions). It is a total disclosure so that the rationale for our rewards
    cannot be disputed. Indeed, Christians today no longer fear God partly
    because they have lost a sense of accountability.
</ol>
