<p>A man rushed into the doctor's office and pleaded with the 
nurse, &quot;Please, I must see the doctor right away. I'm shrinking an inch every 
five minutes.&quot; &quot;Have a seat over there,&quot; pointed the nurse coldly, &quot;and be a 
little patient!&quot;</p>
<p>Patience is not my strong suit. Although I do admit that I 
have grown in this area, I still find it challenging to do plumbing and maintain 
a good Christian testimony at the same time! I sometimes do that new dance 
called &quot;the impatience slide.&quot; </p>
<p>Even though I am ultimately responsible for my own 
behavior, a lot of factors influence my struggle. My dad was generous when it 
came to money, but impatient and unusually stingy with time and energy. As a 
result, I learned to associate impatience with masculinity; women are patient, I 
was taught (by example), men are not.</p>
<p>Stress doesn't help. How does the old song go&#8212;&quot;so 
little time, so much to do?&quot; I can get particularly unpleasant when people try 
to rush me, or when I perceive they are rushing me&#8212;even if they are not. I 
have learned to handle this by asking, &quot;Will you please stop rushing me?&quot; Others 
often reply, &quot;I am not rushing you!&quot; My own imagination sometimes works against 
me.</p>
<p>As I grow in the Lord and in years, I do find it easier 
(but not necessarily easy) to be patient. I have learned a number of tricks. For 
example, I try to allow plenty of time to avoid situations that force me to 
rush. Sometimes I remember to pray and ask God to help me get His perspective.
</p>
<p><b>The Importance of Patience</b></p>
<p>But why is patience important? <b>The first and main reason 
is clear: patience is a characteristic of God</b>, and we are to imitate God's 
character 
(<a href="javascript:scripture('../scripture/eph51.htm')" target=_self class=pop>Ephesians 5:1</a>). 
In <b>Jonah 4:2</b>, Jonah prayed, &quot;I knew that you are a 
gracious and compassionate God, <i>slow to anger</i> and abounding in love, a 
God who relents from sending calamity.&quot; The idea of being &quot;slow to anger&quot; is 
synonymous with patience, and patience is about controlling anger and delaying 
it just as God does.</p>
<p>Peter tells us: &quot;The Lord is not <i>slow</i> in keeping his 
promise, as some understand slowness. He is <i>patient</i> with you, not wanting 
anyone to perish, but everyone to come to repentance&quot; (<b>2 Peter 3:9</b>). God's 
patience toward us can be misinterpreted as &quot;slowness,&quot; when in reality it is an 
intentional delay of His wrath. We can conclude that <b>a second reason patience 
is important is because it allows for some problems to be resolved</b> (in this 
example, through repentance). Many problems, for example, are the result of 
miscommunication. It takes patience to detect and correct miscommunication.</p>
<p><b>A third reason patience is important is that it nurtures 
reasoned, rather than emotion-driven, behavior.</b></p>
<p>God is patient in His relationship to us because <b>
relationships thrive in an atmosphere of patience, a fourth reason.</b> 
Impatience intimidates and stifles; patience is a gracious attribute composing 
one of the sections of the &quot;fruit of the Spirit.&quot;</p>
<p>Impatient people are socially undesirable; in marriage, 
impatience can turn into contempt and sarcasm, potent poisons that have killed 
many marriages. The Bible's command to parents is clear: &quot;Fathers, do not 
exasperate your children.&quot; When we think of exasperating children, we usually 
mention scolding, constantly raising the bar, hiding criticisms in complements, 
etc., but impatience is also a formidable exasperator. Some children avoid their 
parents because mom or dad's impatience is intimidating and belittling.</p>
<p><b>What is Patience?</b></p>
<p>Let's talk about what patience is not: denying reality or 
passivity. As far as God's patience goes, &quot;Nowhere along the chain are the 
claims...of God's righteousness abandoned. Nowhere is patience reduced to a kind 
of apathetic tolerance; everywhere anger, wrath, and any final judgment are 
delayed for the sake of repentance&quot; (<u>International Standard Bible 
Encyclopedia</u> ,Vol. 3, p. 689).</p>
<p>On a human level, patience is not
apathy, disinterest, laxness, passivity, indifference, unconcern, being 
lukewarm, neutrality, isolation, detachment, or listlessness. </p>
<p>Although patience is 
not apathy, we must recognize that some people seem patient simply because they 
are unproductive! What some people consider &quot;patience&quot; might simply be a lack of 
initiative or purposelessness. Ones personal patience (or lack thereof) is seen 
in the pressure cooker, not the easy chair.</p>
<p>Two Greek words are used in the New Testament for patience. <b>The first 
is <i>makrothumia</i></b><i>.</i> This is the choice to refrain from avenging 
oneself, at least immediately; it can include either a delay or erasure of 
wrath. If a patient individual chooses to exercise his wrath or his rights, it 
is not during the heat of the moment. Controlled anger (or seeking justice) 
surfaces after rational thought and Biblical analysis, not in the heat of the 
moment. We might call this &quot;the patience motivated by love.&quot;</p>
<p>This &quot;delay&quot; aspect of patience runs against the grain of the Fight 
Instinct. If we experience the urge to fight (usually verbally) we wait; we 
gather our thoughts, get our composure, and perhaps seek counsel.</p>
<p><b>The second Greek 
word is <i>hupomone.</i></b> This involves the choice to bravely endure what cannot or should not be 
controlled. It involves the concept of resignation. This form of patience bucks 
the flight instinct. We face the situation and endure the consequences. Honoring 
commitments we are tempted to renege upon is one example of this kind of 
patience. This patience keeps us from seeking unethical shortcuts and improper 
compromises. We might call this &quot;patience motivated by hope&quot; (<b>1 Thessalonians 
1:3b</b>).</p>
<p>Put it together and what do you get? &quot;Patience is accepting what cannot be 
changed and responding to what can be changed by first delaying wrath and then 
developing a reasoned response.&quot;</p>
<p><b>What Can I Do About 
It?</b></p>
<p>Good question! <b>
First, I must recognize the factors that contribute toward impatience.</b> 
Heredity is one of them: some of us are born with a propensity to be impatient. 
The playing field is not level. Environment is another factor. If my parents 
were impatient, their example adds an obstacle to overcome. But my current 
environment is another factor. If I do not have margin in my life (time margin, 
financial margin, and energy margin), then I am asking for a stressed lifestyle. 
This stress increases the temptation toward impatience.</p>
<p>These factors do 
influence our struggles with impatience. But even if we have such propensities, 
we still need to <b>(secondly) remind ourselves that we are responsible for our 
own behavior. </b>We must take ownership of ourselves.</p>

<p>So here are a few tips:</p>
<ol type=><li><p><i>Orchestrate 
success through wise living</i>. Do not overstretch yourself. Note the 
situations that make you impatient and try to preclude them. Be a student of 
yourself.</p>

<li><p><i>&quot;Put on&quot; patience</i> (see 
<a href="javascript:scripture('../scripture/col312.htm')" target=_self class=pop>Colossians 3:12</a>); 
we can choose to be patient at the point of temptation, 
but it takes a conscious decision.</p>

<li><p><i>Grow in the Lord</i>. 
As we grow in the Lord, we begin to manifest the &quot;fruit of the Spirit&quot; which 
includes patience 
(<a href="javascript:scripture('../scripture/gal522.htm')" target=_self class=pop>Galatians 5:22</a>).</p>

<li><p><i>Cooperate with God</i>
who is trying to build patience within you through the trials and stresses of life 
(<a href="javascript:scripture('../scripture/rom535.htm')" target=_self class=pop>Romans 5:3-5</a>). 
If you resign yourself to learn from these trials, you will 
find that you grow in patience; if you fight tooth and nail, you make little 
progress.</p></ol>
<p>Patience&#8212;it's worth 
waiting for!</p>

