<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 19
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>Thank You for saving us by Your grace. Certainly if we 
were forgiven and saved by our works, none of us would be able to stand before 
You, for all our good deeds are like filthy rags before You 
(<a href="javascript:scripture('../../scripture/is646.htm')" target=_self class=pop>Isaiah 64:6</a>). 
David knew You, but he fell into awful sin. He coveted a woman and committed adultery 
with her. Then he had her husband murdered through his general; he left the 
dirty work to another. King Ahab, who did not know You, also coveted. He 
coveted Naboth’s vineyard, but he did not commit adultery. Yet, he too had a man 
murdered. His wife set things up for Naboth’s murder—this king also had 
another one do the dirty work. You worked repentance in David’s heart and David 
repented. His relationship was restored, while Ahab did not repent and died 
having never been forgiven, as far as we know. Lord, 
to know You, to have a relationship with You is everything that really matters 
in my life. To miss knowing You is the greatest tragedy of this life and the 
next.</p>
<p>I pray for my lost friends and family members including: __________. Father, 
please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>I pray for the elderly, sickly and shut-in including: __________.</p>
<p>Help me to be an alert Christian, Lord. I do not want to be 
numbered among those who value peace so much that I keep my head in the sand or 
deny undesirable realities. No, I need to understand how I am led into sin, how 
the devil tempts me and to have answers for the questions people are asking. I 
need to read, study and tool up as one of Your workers. I can neither rest on 
my laurels nor live in the past, for You have placed me in today's world. That 
means You want me to address today's challenges. Help me to be brave and to do 
so, I ask.</p>
<p>Lord, the Latin American island nation of St. Kitts-Nevis 
is a challenge. Drugs and all sorts of illegal activities are characteristic on 
St. Kitts, but the folks who reside on Nevis tend to be godlier. Thank You that 
almost 23% of the people claim to be born-again believers, but response to the 
Gospel among the unsaved seems particularly poor. May Your Spirit sweep over 
these islands and may the number of believers grow by 50% over the next decade 
and may believers be noted for their spiritual depth and consistent lifestyle, I 
ask.</p>
<p>Father, please bless the work of Christian World Outreach. 
&quot;The mission of Christian World Outreach is to participate with the body of 
Christ in responsible evangelism, to bring dignity, self worth and encouragement 
through leadership development and to meet the physical needs of people 
through humanitarian assistance.&quot; Bless their work in Burkina Faso, Haiti, 
Kenya, Zambia and Zimbabwe. Empower them, grant them harmony among themselves, 
provide for them, protect them and give them great fruit for their labors, I 
ask.</p>
<p>Now, Lord, help me to serve You faithfully today, in 
Yeshua’s (Jesus’) Name, Amen.</p>


