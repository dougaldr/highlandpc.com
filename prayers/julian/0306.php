<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 6
<br><span class=byln>by Ed Vasicek</p>

<p>Lord, You are wonderful!</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Please, Father, meet the following needs: __________.</p>
<p>God, help me to handle conflict well. I hate it when 
conflict comes my way, and sometimes it is so hard for me to be objective when 
my emotions become involved. Help me to look at the long-term consequences of 
my response, and help me value relationships above being proven right.</p>
<p>I pray for those who are unemployed or underemployed. 
Father, I pray that our economy would continue to grow in strength and that good 
jobs would abound. Comfort those who are out of work, keep them from quitting 
to pursue employment, and bless them by opening up doors of opportunity.</p>
<p>Gracious 
King, thank You for the reality of heaven. Eye has not seen, nor has ear heard, 
nor has entered into the heart of men the things that God has prepared for those 
who love Him. Heaven is just as real as earth, only more permanent, and better 
in every way. I look forward to seeing my Savior and those I know who are 
already enjoying its bliss. I look forward to meeting new friends and 
interviewing the great men and women of faith mentioned in the Bible. An 
eternal party awaits me.</p>
<p>Help me, 
Lord, to rest in You. May I experience Your peace in my daily life, even amid 
turbulent times! May I be known as a person of Shalom, peace. The Savior said, 
&quot;Blessed are the peacemakers,&quot; and I want to be one of them.</p>
<p>Today I pray 
for the South Pacific coral island nation of Niue with a population of fewer 
than 2,000. I ask that the 8.5% of those who profess to be born-again 
evangelical Christians would double over the next decade.</p>
<p>I remember 
South America Mission, and their vision: &quot;to see the Church of Jesus Christ 
established so that thousands of local, witnessing communities of believers are 
maturing in their own knowledge and faith, while working to ensure that everyone 
in South America has multiple opportunities to hear a clear, meaningful and 
persuasive presentation of the gospel from within their own ethnic or social 
group.&quot; Bless their evangelistic efforts, their discipleship training and 
leadership training. Raise up strong lay leaders with the believing community. 
Provide for the missionaries, empower them with Your Spirit, help them work in 
harmony, and may they see many both brought into the Kingdom of God and brought 
to spiritual maturity to Your glory.</p>
<p>Today I 
remember computer engineers, software designers, and those who work in computer 
development or repair. Bless them, and may those who know Christ boldly share 
the Gospel with their coworkers. </p>
<p>Help me 
follow Your lead today, I pray, in the Name of the Messiah, Amen.
</p>

