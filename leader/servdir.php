<p id="pgt"><span class="pgh">Church and Leadership:</span>
<br>Service Detail Director Job Description
<br><span class=byln>by Ed Vasicek</span></p>

<p>The Service Detail Director will attempt to serve as a 
“quality controller” and “double checker” to help support our volunteer staffs 
and coordinate the physical details of the Morning Service (10:30 a.m.).  He 
will serve as a resource to the sound technicians to assure that the microphones 
are set up and in their proper positions by 10:30. He will work with the 
singers, instrumentalists, drama teams, video technicians, light technicians 
and other service participants to organize practice times and assure that 
necessary props are in place or special effects are ready to go (much like a 
stage manager would).  He will work with the above volunteers to funnel 
equipment requests to the deacons (like the need for additional microphone 
stands, etc.), offer suggestions for consideration and address other details as 
he deems appropriate.</p>
<p>The Service Detail Director is not responsible to plan 
services, nor shall he interfere with the authority structures of the 
above-mentioned groups.  He is there to serve, coordinate, help, offer ideas 
and make himself available as a resource.  He shall be directly accountable to 
the board of elders.</p>
  
