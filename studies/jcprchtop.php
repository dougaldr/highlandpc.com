<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Did Jesus Preach Topically?
<br><span class=byln>by Ed Vasicek</span></p>

<div>
<p>I love to question assumptions. Assumptions blind us to 
the truth; they narrow our minds to endless possibilities. We sometimes call 
this narrowing of options, &quot;Paradigm Blindness.&quot; The problem of Paradigm 
Blindness is huge, and we all experience it. I certainly have not have risen 
above it!</p>
<p>One assumption (in the world of preaching philosophy) that 
has been accepted without challenge is this assertion: &quot;Jesus preached topical 
sermons.&quot; There are a number of other assumptions implied here: (1) Jesus’ 
style of teaching is what church leaders should emulate, (2) the Bible records 
ALL the words Christ spoke while on earth, and (3) Jesus’ ministry as Messiah 
was not unique, so we should imitate not only his character, but His lifestyle. 
I personally disagree with all the above (though I have felt like turning over 
tables in Christ-like fashion a few times!).</p>
<p>I would like to challenge the assumption that &quot;Jesus 
preached topical sermons&quot; apart from arguing whether the above assumptions are 
valid.</p>
<p>First, we must remember that whatever Jesus said WAS an 
exposition. Since He spoke the Word of God, listeners heard him 
section-by-section, verse-by-verse.</p>
<p>Secondly, in His teaching, Jesus was often expositing a 
text. The reason many of us struggle with the real meaning of much of the Sermon 
on the Mount (a sermon many believe lasted 2 hours or more but of which only 11 
minutes have been preserved) is because we fail to realize that it is an 
exposition!</p>
<p>Based upon the Jewish tradition of teaching and applying 
passages of Scripture (<i>midrash</i>), I believe a segment of the Sermon on the 
Mount is an exposition of <b>Leviticus 19:15-18</b>, 
<blockquote>
Do not pervert justice; do not 
show partiality to the poor or favoritism to the great, but judge your neighbor 
fairly. Do not go about spreading slander among your people. Do not do anything 
that endangers your neighbor's life. I am the L<span class="sm">ORD</span>. Do not hate your brother in 
your heart. Rebuke your neighbor frankly so you will not share in his guilt. Do 
not seek revenge or bear a grudge against one of your people, but love your 
neighbor as yourself. I am the L<span class="sm">ORD</span>.</blockquote>

<p>This forms the basis for Jesus’ teaching about judging 
consistently 
(<a href="javascript:scripture('../scripture/matt712.htm')" target=_self class=pop>Matthew 7:1-2</a>),
reconciling to an offended brother 
(<a href="javascript:scripture('../scripture/matt523.htm')" target=_self class=pop>Matthew 5:23</a>)
and anger toward ones brother 
(<a href="javascript:scripture('../scripture/matt522.htm')" target=_self class=pop>Matthew 5:22</a>).</p>
<p>Another passage that Christ was expositing seems to be <b>Deuteronomy 15:9-14</b>, <blockquote>Be careful not to harbor this wicked thought: 
	&quot;The seventh year, 
the year for canceling debts, is near,&quot; so that you do not show ill will toward 
your needy brother and give him nothing. He may then appeal to the L<span class="sm">ORD</span> against 
you, and you will be found guilty of sin. Give generously to him and do so 
without a grudging heart; then because of this the L<span class="sm">ORD</span> your God will bless you 
in all your work and in everything you put your hand to. There will always be 
poor people in the land. Therefore I command you to be openhanded toward your 
brothers and toward the poor and needy in your land.</p>
<p>If a fellow Hebrew, a man or a woman, sells himself to 
you and serves you six years, in the seventh year you must let him go free. And 
when you release him, do not send him away empty-handed. Supply him liberally 
from your flock, your threshing floor and your winepress. Give to him as the 
L<span class="sm">ORD</span> your God has blessed you.</blockquote>
<p>Jesus applied this text when teaching about giving ones 
tunic as well as ones cloak 
(<a href="javascript:scripture('../scripture/matt540.htm')" target=_self class=pop>Matthew 
5:40</a>) 
and being extravagantly generous (walking the extra mile, 
<a href="javascript:scripture('../scripture/matt541.htm')" target=_self class=pop>Matthew 
5:41</a>)
and giving to anyone who asks of us 
(<a href="javascript:scripture('../scripture/matt542.htm')" target=_self class=pop>Matthew 
5:42</a>).</p>
<p>The Sermon on the Mount is challenging, but it becomes more 
intelligible once we contemplate the Torah portions under discussion (yes, there 
is some guesswork involved!). It is precisely because Bible teachers do not 
recognize that Christ is engaging in expository preaching (<i>midrash</i>) that 
we have such difficulty harmonizing these passages with other portions.</p>
<p>The same is true with the Olivet Discourse of <b>Matthew 
24-25</b>, which relies heavily upon texts in <b>Daniel</b>, <b>Zechariah</b>, 
<b>Ezekiel</b> and <b>Isaiah</b>. Christ's lesson to Nicodemus about the new birth is more or less an 
exposition upon 
<a href="javascript:scripture('../scripture/ez362427.htm')" target=_self class=pop>Ezekiel 36:24-27</a>
and <a href="javascript:scripture('../scripture/ez37910.htm')" target=_self class=pop>37:9-10</a>.</p>
<p>The bottom line is that Christ did—at least sometimes—use exposition. So did other New Testament 
authors. The <b>Book of Hebrews</b> is an exposition of a section of <b>Numbers</b>. The difficult 
<b>Hebrews 6</b> passage
is clarified when we study it in light of <b>Numbers 14</b>.</p>
<p>Although I do not deny that topical and textual sermons 
have their place, exposition IS modeled in the New Testament. But there is an 
added benefit to exposition: the preacher himself is forced to grapple with 
difficult passages he might otherwise evade. Expositors are forced to dig into 
the Old Testament/Jewish roots of our faith to find answers. This sets the tone 
for a faith that celebrates thinking—and the pursuit of truth, particularly 
truth beneath the surface. Topical preachers can easily evade many of the 
difficult questions and toughest issues (creation, prophecy, deep theology, role 
of women, etc.). As a result, Expository Preaching results in a balance of 
coverage that is not determined by the preacher, but by the Sovereign Who is 
allowed to set the agenda.</p>
