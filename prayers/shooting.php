<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>Kokomo Tragic Shooting Prayer
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Lord,</p>
<p>Our community is generally a safe community. If we exercise 
reason, we can walk our streets safely and with confidence over 99.99% of the 
time. Crime is everywhere in our country and in the world; we cannot deny its 
threat. It always has been. At a time like this, Lord, the reality of rare but 
tragic events grip our hearts.</p>
<p>We ask Your grace, mercy, and healing for the victims of 
the horrible shootings that took place within an infamous 24-hour period. We 
pray that You would physically heal the survivors and help them to recover both 
physically and emotionally. Those who have lost loved ones have aches within 
their hearts that we cannot fill; only You can help them grope along in these 
dark times. Strengthen, comfort, enable, and help them by Your grace.</p>
<p>Lord, many people have been traumatized because they 
witnessed these shootings or the aftermath of them. Even librarians and children 
felt the fear and uncertainty of the time. We thank you that no children were 
harmed in these tragic shootings. Others were tightly closed into their homes 
with all doors bolted, some hearing the gunshots. Many of us heard siren after 
siren, making us worry about the wellbeing of our community.</p>
<p>Father, our police officers are human beings, and so are 
our city officials and security personnel. Because they fulfill a function does 
not make them mere functionaries; they were sickened, stretched, and stressed as 
they experienced challenges beyond the normal crimes they encounter. Being paid 
to do a job does not somehow make us something other than human.</p>
<p>Father, many people in Kokomo are now experiencing 
sleepless nights and symptoms of grief, loss, stress, fear, and emotional 
trauma. The foundations of our city have been shaken. We need Your help to once 
again gain our bearings and live with confidence.</p>
<p>Lord, we know that this criminal spree is not the 
initiation of a new era of violence. Violent crime has been declining for years, 
and we praise You for that. Still, if that occasional crime affects us or 
someone we love--or if much of it happens at once, as it did on that day--we are 
shaken and prone to panic and overreact. Help us to temper this tragic day by 
balancing it with the years that preceded it and those that will follow. Help us 
react in a balanced and sane way.</p>
<p>Our great God, we know that You are holy and that sins mars 
the human heart. Change us within so that sin does not become master over us. 
Help us not to fear, but to remember that You sit as Sovereign over the 
universe. And, for those who are grieving, thank You for the promise of eternal 
life. &nbsp;We pray in Jesus' Name, Amen.</p>


