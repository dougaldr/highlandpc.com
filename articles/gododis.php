<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The God of Disasters
<br><span class=byln>by Ed Vasicek</span></p>

<p>
The recent earthquake and resultant tsunamis that devastated Asian coastlines shocked the 
world: tens of thousands of people dead, lives ruined, and entire villages 
gone! When such a monumental disaster occurs, we are tempted to ask, &quot;Why, 
God?&quot; And if asked with a spirit of humility, that’s not wrong. The Psalmists 
often struggled with disasters happening even to those who walk closely with 
God. One example is <b>Psalm 44:17</b>, &quot;All this has happened to us, though we had 
not forgotten you or been false to your covenant.&quot; Some try to shield God from 
accusation by claiming He is limited by time and ignorant of future events. But 
the Almighty is not embarrassed to take responsibility for disaster. <b>Isaiah 45:7</b> 
reads, &quot;I form 
the light and create darkness, I bring prosperity and create disaster; I, the 
L<span class="footer">ORD</span>, do all these things.” In <b>Exodus 4:11</b> God even takes the blame for 
individual handicaps: &quot;The L<span class="footer">ORD</span> said to him, 'Who gave man his mouth? Who makes 
him deaf or mute? Who gives him sight or makes him blind? Is it not I, the 
L<span class="footer">ORD</span>?'&quot; In what sense is God responsible for these tragedies? Did He directly 
create these events, or did He know about them and allow them to occur? Were 
the men, women, teens, and children who died as a result of the tsunamis more 
wicked than those who were spared? Let’s contemplate this.</p>
<p>When God cursed the earth after the Fall of Man (<b>Genesis 
3</b>), part of that curse was the process of death, entropy, and disaster. Paul 
writes in <b>Romans 8:18-22</b>, &quot;I consider that our present sufferings are not worth 
comparing with the glory that will be revealed in us. The creation waits in 
eager expectation for the sons of God to be revealed. For the creation was 
subjected to frustration, not by its own choice, but by the will of the one who 
subjected it, in hope that the creation itself will be liberated from its 
bondage to decay and brought into the glorious freedom of the children of God. 
We know that the whole creation has been groaning as in the pains of childbirth 
right up to the present time.&quot; Paul paints the picture of a &quot;groaning universe&quot; 
characterized by suffering. This suffering is a result of the curse, but our 
consolation is that this curse is temporary: there will be no curse when the era 
of the New Heaven and the New Earth debuts 
(<a href="javascript:scripture('../scripture/rev223.htm')" target=_self class=pop>Revelation 22:3</a>).</p>
<p>
Until then, natural 
disasters remind us that our holy God is angry at the physical universe (and 
mankind in particular) because of sin. <b>Romans 1:18-20</b> reads, &quot;The wrath of God 
is being revealed from heaven against all the godlessness and wickedness of men 
who suppress the truth by their wickedness, since what may be known about God is 
plain to them, because God has made it plain to them. For since the creation of 
the world God's invisible qualities—his eternal power and divine nature—have 
been clearly seen, being understood from what has been made, so that men are 
without excuse.&quot;</p>
<p>
Although we technically 
refer to such natural disasters as &quot;Acts of God,&quot; we fail to realize that this 
is precisely what they are. Disasters intermittently reveal God’s wrath from 
heaven. Although the complexity of nature teaches us that God is the Master 
Engineer of all that is created, disasters remind us that something is wrong: 
God is angry. So when disaster attacks a certain group of people, does that 
mean God is angrier at that group than another? While this is sometimes the 
case (as, for example, with Sodom and Gomorrah in <b>Genesis 18-19</b>), natural 
disasters are not typically aimed at the worst offenders. They are, instead, 
reminders that this world is under the curse. And the curse is anything but 
fair and equitable.</p>
<p>
We have an audience 
with God, but we cannot control Him. He is faithful and He keeps His promises. 
Jesus promised, &quot;In this world, you will have trouble. But take heart! I have 
overcome the world&quot; (<b>John 16:33</b>). Although we prefer to cling to the latter 
part of this verse, we have to acknowledge, albeit grudgingly, that both 
portions are true. We do know that God knows all things (even what would have 
happened IF—see 
<a href="javascript:scripture('../scripture/1sam231013.htm')" target=_self class=pop>1 Samuel 23:10-13</a>), 
and that our omnipotent God can do as He 
pleases. If God merely allowed the earthquake to occur as a consequence of 
nature being cursed (my view) or if He proactively caused it, we must remember 
that He has a long-term goal: to glorify Himself by working all things together 
for good to them that love God (<b>Romans 8:28</b>). In ways beyond our understanding, 
God allows disaster and evil to work a greater good than would have occurred had 
not the evil been allowed.</p>
<p>
Trying to figure out 
HOW the particulars contribute toward His long-term plan is worse than futile. 
We do not have to like it. We do not have to defend God’s ways, which are 
&quot;higher than ours.&quot; But sometime, somewhere, we will have to come to terms with 
these spiritual facts of life.</p>


