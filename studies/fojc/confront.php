<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>Biblical Confrontation and Matthew 18:15-19
<br><span class=byln>by Ed Vasicek</span></p>

<p><b>Matthew 18</b> presents us with a pattern for confronting those who have fallen into 
blatant sin. Although this passage does not include all the Bible’s teaching 
about this subject, it is the most detailed. It addresses typical situations, 
and should not be viewed as applicable to all. Although not stated, both sense 
and numerous Scriptures make it clear that we are not necessarily to confront 
others about every sin, but rather the sins that “jump out” and are beyond the 
realm of mere human weakness. </p>
<p></p>
<p>The 
procedure for confronting others is simple and logical. First, we begin by 
bringing up the matter privately. If it is resolved at that level, the matter 
is generally closed (though not always). If the confronted party refuses to 
address the wrongdoing, then we are to bring others with us—usually one or two 
more. They serve a triple-purpose: </p>
<ol><li>discerning whether the accused party 
really has done wrong, 
<li>witnessing what is said, and 
<li>adding more “peer 
pressure” to repent.</ol>

If this matter does not bring repentance, the church 
(leadership) gets involved. If the offender will not straighten out (or make a 
reasonable effort), then that individual is excluded from church life and 
requested to attend church functions no more until he or she is willing to mend 
his/her ways. This is called “church discipline” or “excommunication.”</p>
<p></p>
<p>This 
procedure is used for gross sins, described for us by Paul: </p>
<blockquote><b>1 
Corinthians 5:9-11</b> “I have written you in my letter not to associate with 
sexually immoral people—not at all meaning the people of this world who are 
immoral, or the greedy and swindlers, or idolaters. In that case you would have 
to leave this world. But now I am writing you that you must not associate with 
anyone who calls himself a brother but is sexually immoral or greedy, an 
idolater or a slanderer, a drunkard or a swindler. With such a man do not even 
eat.”</blockquote>
<p>Although not stated here, there are other forms of church discipline that are 
less severe (or, in the case of leaders, more severe!). In some instances, a 
person might be asked to leave rather than be completely excommunicated, 
particularly if family is involved. In other instances, discipline might 
include surrendering a position or personal ministry, or even being restrained 
from participating in communion for a time. Some forms of discipline follow 
forgiveness in a strategy to reestablish the credibility of the offender.</p>
<p></p>
<p>In 
most churches, including ours, the first step (talking to the person privately) 
clears up the majority of problems. In those rare instances when we do have to 
practice discipline, it is best if all of us know and accept the rules 
beforehand. (It is too late to learn this when a crisis is in progress!) When 
we do have to go all the way and either ask someone to leave or completely 
excommunicate them, does the person usually repent? In my experience, no. But 
if we are to be disciples of Jesus Christ, we must obey Him—even when it gets 
messy or our emotions are high. We have no choice. We simply must believe that 
He knows best. Such times test whether we have convictions or just warm 
wigglies!</p>


