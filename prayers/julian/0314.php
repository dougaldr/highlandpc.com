<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 14
<br><span class=byln>by Ed Vasicek</p>

<p>Great King Above Creation,</p>
<p>Your Word describes You as a jealous God. When it comes to 
worship, You want a complete monopoly: we are to worship the Lord our God, and 
Him only should we serve. That is why You do not want us to make images and 
kneel before them 
(<a href="javascript:scripture('../../scripture/ex2045.htm')" target=_self class=pop>Exodus 20:4-5</a>), 
and You do not want us to perform acts of 
worship toward anyone other than You. Only You are omnipresent and omniscient 
to hear all our prayers, and only You are everywhere to hear our prayers and 
enjoy our hymns of praise. You do not want us to associate Your unique 
attributes with any but You. You alone are God.</p>
<p>Although Lucifer committed the first sin by starting a 
rebellion against You (<b>Isaiah 14</b>), thus becoming Satan, the Adversary, sin and 
death entered the human race and the physical universe through our forefather, 
Adam. By His disobedience, all creation—and generations yet to be born 
(including us) are corrupted by sin. But the Second Adam, the Lord Jesus 
Christ, lived a life of perfect obedience. His death on the cross satisfied 
Your wrath, and His Resurrection declared to all that He is the Son of God. 
Because we have believed in the Lord Jesus Christ, You have removed us from the 
family of the First Adam and adopted us into Your family, the family of the 
Second Adam. How we praise You for this!</p>
<p>Oh, God, I feel so inadequate to live the Christian life. 
My heart and mind are so often filled with sinful and selfish thoughts, I lack 
the faith to trust You with the details of my life at times, and I know, deep 
down, that I am not what You want me to be. It does me no good to punish myself 
or get down on myself; instead, I need to catch a fresh vision of You, to focus 
upon You and not my failings. Please help me to grow in this area of my life: __________.</p>
<p>Today I pray for the Czech Republic. Only a little more 
than 1% of the population claim to be born-again evangelical Christians. Help 
the work of CEF, The Bible Society, The Jesus Film, Christian literature 
distribution, and radio broadcasts true to Your Word. May Your Spirit hover 
over this land, and may the number of believers multiply ten-fold over the next 
twenty years. Raise up strong leaders in the church, and provide good training 
for them.</p>
<p>I remember World Reach, &quot;an interdenominational foreign 
mission organization serving fields in East Africa, Central and South America, 
and Europe. Its thrust is the establishment and nurturing of churches among unreached people while training national pastors and lay-leaders.&quot; Bless this 
ministry as they are actively trying to put to work the Great Commission. 
Provide for them, protect them, and may Your Spirit prosper their labor.</p>
<p>Help me to walk hand in hand with You today, I ask, in 
Christ's Name, Amen.</p>

