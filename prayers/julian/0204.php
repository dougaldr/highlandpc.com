<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 4
<br><span class=byln>by Ed Vasicek</span></p>

<p>My Father in heaven,</p>
<p>Lord, You are described in many ways, and one of them is as 
a &quot;consuming fire.&quot; As a fire is pure, so You are completely clean and holy. 
As a consuming fire cannot be quenched, so You have infinite energy and none can 
stand in Your way. As a fire separates the impurities from fine gold, so Your 
gaze sees through all and sorts through all. As a fire represents Your wrath 
and judgment, so it is right for all creation to fear You.</p>
<p>Father, I thank You that one day Jesus Christ shall return 
to the clouds, and we will meet Him in the air. The dead will be raised and 
those of Your children alive on earth will be changed into immortality as they 
ascend upward to meet You, all in the twinkling of an eye. We eagerly await 
this &quot;blessed hope.&quot;</p>
<p>So, Lord, help me to have endurance as I wait for Christ's 
return. Sometimes life becomes overwhelming and I become disappointed in 
circumstances, disappointed with people, and, frankly, disappointed with 
myself. Sometimes, I confess, I even feel disappointed with You. So help me to 
endure, to persevere in faithfully serving You, because I know my labor in You 
is not in vain.</p>
<p>Lord, I have many things for which I can thank You. A few 
of them are: __________.</p>
<p>Father, help me to be content with the things I have and my 
lot in life. At the same time, help me take initiative and deliver me from 
passivity. Help me not be like the servant who fearfully hid his talents. Help 
me to boldly invest myself in Your work. Keep me from comparing my skills and 
talents to others.</p>
<p>Today I pray for the nation of Bolivia. Thank You for the 
12% of the population who claim to be born-again, and we pray that that 
percentage will soon reach 30. We are grateful, Lord, for the growth You have 
given the Bolivian Church. But we know that the major income for many Bolivians 
is growing coca and cocaine. Christians are stressed: if they grow cocaine, 
they can support their families; if they do not, they live in poverty. We pray 
that You would make a way for the believers to provide well for their families 
while not violating their consciences at the same time.</p>
<p>Today I remember the ministry of CrossWorld, formerly known 
as UFM. As their 400 missionaries help reach and disciple thousands, we pray 
that You would keep them walking with You, true to Your Word, safe, and amply 
supplied. May Your Spirit bless their labors, and help them to relate well to 
one another in ways that please You.</p>
<p>We pray for our nation's emergency radio dispatchers and 
those who work the 911 emergency lines. Give them accuracy, clarity, and 
wisdom. We pray that many would come to know You.</p>
<p>Strengthen me for 
the challenges of this day, and, Lord, help me to apply my heart toward wisdom, 
I pray, in Jesus' Name, Amen.</p>

