<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 28
<br><span class=byln>by Ed Vasicek</p>

<p>All Powerful Creator,</p>
<p>You are a jealous God 
(<a href="javascript:scripture('../../scripture/ex205.htm')" target=_self class=pop>Exodus 20:5</a>). 
Although Your 
jealousy may not be exactly the same as human jealousy, it is close enough that 
You use the term &quot;jealous&quot; rather than create a new term. We see in the Old 
Testament how Your wrath is kindled when Your people worship someone else드nother 
god듪r something else드n image. Hosea and Jeremiah parallel such 
behavior to adultery. As a man or woman feels betrayed when a spouse has been 
unfaithful, so You are jealous when Your children are unfaithful to You.</p>
<p>Help us offer our acts of worship듫rayer, trust and 
worship듮o no one but You. Only You are present everywhere to hear our 
prayers and read our minds. Only You are to be praised and worshipped.</p>
<p>Father, keep me from being self-righteous. Nothing will 
eliminate the temptation to be self-righteous quite like getting a glimpse of 
Your holiness. When the godly prophet, Isaiah, saw a vision of you, he declared 
himself &quot;undone&quot; and a man of &quot;unclean lips&quot; 
(<a href="javascript:scripture('../../scripture/is65.htm')" target=_self class=pop>Isaiah 6:5</a>). 
Help me to cultivate 
humility, not by comparing myself to others but to You and Your perfect Law. 
For we sinners saved by grace, being humble is simply being realistic. Help me 
not to put myself down, but to gain Your realistic perspective. And help me 
remember that it is not about me but You.</p>
<p>Lord, help me to avoid every form of superstition. Being 
superstitious is a denial of Your Sovereign control and is in a sense a form 
of idolatry. Instead help me to trust You alone and help me remember that I 
am legally seated with Christ 
(<a href="javascript:scripture('../../scripture/eph26.htm')" target=_self class=pop>Ephesians 2:6</a>)
in heavenly places 
(<a href="javascript:scripture('../../scripture/eph12022.htm')" target=_self class=pop>Ephesians 1:20-22</a>)
far above demonic forces often associated with superstition.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I 
find my strength in His power not my own. May Your Spirit control me 
today.</p>
<p>Lord, the nation of Iceland is cold not only physically but 
spiritually. Only 3.3% of the population claim to be born-again believers, and 
most of the churches are liberal and have forsaken the truth of the Gospel. 
Mission efforts have made some difference, but the people are not very 
receptive. O Father, send Your Spirit to draw more Icelanders to Yourself. Use 
Christian radio and the testimonies of the few believers there. Do a mighty 
work to Your glory in this land.</p>
<p>Thank You, Lord, for the great ministry of WEC 
International &quot;an organization with 1,719 active workers from 49 nations...committed to unity in [their] fellowship as [they] work in multinational teams...to 
complete [their] God-given commission...passionate about reaching people and 
planting churches.&quot; Dear Lord, help them to receive Your full backing! Empower 
them by the Holy Spirit, grant them harmony and unity of purpose, protect them, 
provide for them and lead them in the way they should go!</p>
<p>Lord, I commit myself to Your care today, in Jesus' Name, 
Amen.</p>

