<p><b>Jesus was forced to carry His own cross.</b> Since 
crucifixion was a common form of capital punishment used by the Romans on 
non-citizens, it was all too familiar to first century Jews.</p>
<p>Philip Yancey, in his book,
<u>The Jesus I Never Knew</u> makes these comments:</p>
<blockquote>“It took 
time for the church to come to terms with the ignominy of the cross. Church 
fathers forbade its depiction in art until the reign of the Roman emperor 
Constantine, who had seen a vision of the cross and who also banned it as a 
method of execution. Thus, not until the fourth century did the cross become a 
symbol of faith. As C.S. Lewis points out, the crucifixion did not become 
common in art until all who had seen a real one died off...” (p. 203).</blockquote>
<p>As victims hung naked on 
these crosses and experienced a slow, agonizing death, those decent people who 
witnessed these horrors wanted nothing to do with even the thought of the 
cross. It was a concept too horrible to entertain. Yet it is through Jesus’ 
work on the cross that our sin debt was paid in full (the term, “It is finished” 
can also be translated, “It has been paid in full”).</p>
<p><b>Simon the Cyrene</b> was very likely a Jew who had made to the 
trip to Jerusalem to celebrate the Passover. There were many Jews living in 
Cyrene, a city of Libya. Since Jesus was too weak (after all the beatings and 
abuse He received) to carry His own cross, they forced the first man they saw to 
carry it (the Romans would never carry a cross), so Simon just happened to be 
there. Mark mentions that he was the father of Alexander and Rufus, two key 
believers mentioned in 
<a href="javascript:scripture('../../scripture/rom1613.htm')" target=_self  class=pop>Romans 16:13</a>.</p>
<p><b>Women were crying</b> and grieving along the street as they saw 
Jesus walking by. Some of these may have been disciples who had hailed Him on 
Palm Sunday. Jesus’ response is interesting: “...weep for yourselves....” He then 
went on to predict a horrible time for them and their children. He then says, 
“For if men do these things when the tree is green, what will happen when it is 
dry?” This statement alludes to an obscure text in 
<a href="javascript:scripture('../../scripture/ez2045217.htm')" target=_self  class=pop>Ezekiel 20:45-21:7</a>, 
according to David Bivin in his work, <u>Understanding the Difficult Words of 
Jesus: New Insights from a Hebrew Perspective</u>. In Ezekiel, the “green tree” 
refers to those who are righteous, the dry tree to the unrighteous. In 
Ezekiel’s illustration, the green trees catch fire because of the intensity of 
God’s wrath against the dry, godless trees. So Jesus is saying, “If they do 
this to me, the Righteous Messiah, the unrighteous will receive a tragedy as 
well.” He was referring to the destruction of Jerusalem that took place in 70 
A.D.</p>
<p><i>*Via Dolorosa is Spanish 
for the Way (path) of Suffering, the route Jesus took from Pilate’s seat to the 
Golgotha.</i></p>

