<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 21
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>You are so patient! You are a God of wrath, but You delay 
Your wrath because You are patient. You are not willing for any to perish and 
so You are long-suffering. Your patience has limits, but it is impressive. You 
have allowed this world to continue on in sin for thousands of years; one day, 
Your Son will return to set up His Kingdom and bring in the reign of 
righteousness. We are in a hurry for that time, but You have not been. Yet 
Your delay is not a sign that You are changing Your mind or will not do as You 
have promised. What seems to us a delay is merely Your patience and wisdom at 
work.</p>
<p>Father, help me to rejoice in You. Remind me that. &quot;All 
the days of the oppressed are wretched, but the cheerful heart has a continual 
feast&quot; (<b>Proverbs 15:15</b>). O Father, I release my feelings of oppression to You. 
Deliver me from Satan’s attempts to oppress me, from controlling and 
manipulative people and from perhaps a conscience that is too sensitive and too 
responsible. Instead, bless me with a cheerful heart that I might have a 
continual feast.</p>
<p>I remember my marriage (if I am married) and the marriages 
of my extended families, friends and church families. Lord, hold them 
together. Helped married couples to lean upon you and to work hard at 
addressing problems and experiencing harmony. I also pray for those who are 
divorced, that You would help them to go forward with Your hand in theirs.</p>
<p>Father, sometimes I struggle with doubt. This is not a bad 
thing, for there would be no room for faith if it were not for the option of 
doubt. Help me to find answers for my questions. If I cannot, help me be 
patient. Remind me that because I do not know an answer does not mean no answer 
exists. Help me not to use doubt as an excuse to sin or to rationalize values 
inconsistent with Your Word.</p>
<p>Lord, I pray for the Ningxia Hui Autonomous Region of China 
and its over 5 million people. More than a third of these people are Muslim 
and less than 2% are born-again evangelicals. I also pray for the Qinghai 
Province with its 5 million, with perhaps 2% of the population being born-again 
evangelicals. There are many labor camps in this area, camps where persecuted 
Christians are living in misery. Father, be a God of grace and strength for 
these Christians. Thank You for how the Gospel has spread in these areas; even 
though the percentage of believers seems low, these numbers represent a 
significant growth. O Lord, may the number of evangelicals double over the next 
7 years, I ask.</p>
<p>Thank You, Lord, for International Messengers, &quot;an 
evangelical interdenominational missions organization comprised of an 
international staff team...committed to making disciples of all nations through 
partnering with local churches to renew, train and mobilize believers for active 
involvement in reaching the world for Christ.&quot; Thank You for their work in 
Central and Eastern Europe; empower them, provide and protect for them, grant 
them harmony and unity. Use them, O Lord.</p>
<p>Help me to be zealous for You today, I ask in the Savior’s 
Name, Amen.</p>


