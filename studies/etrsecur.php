<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Eternal Security in Light of 1 and 2 John
<br><span class=byln>by Ed Vasicek</span></p>

<div>
<p>I number myself among those who believe that the Doctrine of Eternal Security is 
soundly based in Scripture, but I think the Bible approaches this doctrine from 
a different angle than is typically presented in many churches and schools. 
Although I do affirm that a genuinely regenerate individual cannot lose his 
salvation, the Biblical presentation takes this approach: the true believer will 
continue to believe. That’s how we find out who is and is not truly born again. 
Some theologians refer to this as “the perseverance of the saints [believers].”
<p>The 
first and second letters of John are no exception to this perspective. The 
approach is simple: if one makes a profession and continues on in the faith, 
that profession evidences itself as genuine. If one turns away from the faith, 
the problem is not that a genuine faith has been abandoned, but rather that a 
defective faith has been exposed.
<p>
John 
makes the point that a believer with genuine faith, one who truly belongs to 
God, will not abandon the Savior. Observe <b>1 John 2:19-20</b>: “They went out from 
us, but they were not really of us; for if they had been of us, they would have 
remained with us; but they went out, so that it would be shown that they all are 
not of us. But you have an anointing from the Holy One, and you all know.”
<p>
Note 
the part of the passage that reads, “if they had been of us, they would have 
remained with us…” It rings of a guarantee, does it not? Those who are of us 
will indeed remain among us. Those who abandon the faith were never really one 
of us. That is pretty potent.
<p>
But 
that is not the passage in question. In order to understand these passages,
we must understand the difference between sin (as mentioned in 1 John) and the sin
(or sinning “the” sin). I refer to this as “the sin” of 1 John (this is not original
with me).
<p>
John 
makes it clear that all believers continue to sin, and educates us as to God’s 
provision for cleansing. This cleansing is mentioned in regards to fellowship 
with God, not salvation.
<p>
<b>1 
John 1:8-10</b> reads: “If we say that we have no sin, we are deceiving ourselves 
and the truth is not in us. If we confess our sins, He is faithful and righteous 
to forgive us our sins and to cleanse us from all unrighteousness. If we say 
that we have not sinned, we make Him a liar and His word is not in us.”
<p>

Verse eight reminds us all of an unpleasant reality: we are all still sinners. 
The verb for “have” in the Greek language is in the present tense, thus implying 
duration (“if we say that we are not having sin”). So John’s discussion is not 
whether we have ever sinned, but rather whether we STILL are sinning. John says 
if we say that we still do not sometimes sin, we are deceiving ourselves.
<p>

Those verses address sin in general. But there is “the sin,” namely the sin of 
turning away from Christ, that is impossible for the genuine (and I want to 
emphasize the word “genuine”) to commit.
<p>
This 
is the sin John has in mind in <b>1 John 3:9-10</b>, “No one who is born of God 
practices sin, because His seed abides in him; and he cannot sin, because he is 
born of God. “By 
this the children of God and the children of the devil are obvious: anyone who 
does not practice righteousness is not of God, nor the one who does not love his 
brother.”
<p>
The 
NASB translation “practices sin” (and the NIV “continues to sin”) is an 
interpretive attempt to express the durative nature of the present tense. Yet 
the translators did not choose to do this with <b>1 John 1:8</b>; here they chose to do 
so. The text in <b>3:9</b> literally reads, “the one who is born of God is not 
sinning.” I understand this to mean, “the one born of God is not sinning the sin 
of apostasy.”
<p>
The 
idea here is not that the born again believer rarely sins, but that he is not 
among those sinning in the way John has in mind, the sin fully expressed in 
<b>2:19-20</b>, namely leaving the faithful behind and denying Christ (apostatizing). 
True believers do not and cannot sin “the sin.” Not only does the true believer 
continue in the faith, he also advocates the truth of the Gospel. In addition, 
he embraces the Gospel and it shows its impact by his lifestyle. He is drawn to 
fellow believers and experiences a camaraderie with them that produces love for 
them.
<p>

Those genuinely saved evidence the reality of their conversion by 1) continuing 
in the truth, 2) evidencing a new life in Christ and 3) caring about other 
believers in the congregation.
<p>
The 
fruit of conversion is not what produces conversion. Once justified by faith 
alone and cleansed by the blood of Christ, the seed has been planted and the 
tree nurtured. Good trees produce good fruit. The fruit does not determine one’s 
destiny; it only reveals it.
<p>
The 
fact that John is using a two-track definition of sin (sin in general and THE 
sin of deserting the faith) is further evidenced in <b>1 John 5:16-18</b>. There, “the 
sin” is labeled as a sin “leading to death.” This distinction between “sin” and 
“the sin” is not the fabrication of theologians; it is made by John himself. 
Here are the verses:
<blockquote>
“16 If 
anyone sees his brother commit a sin that does not lead to death, he should pray 
and God will give him life. I refer to those whose sin does not lead to death. 
There is a sin that leads to death. I am not saying that he should pray about 
that. 17 All wrongdoing is sin, and there is sin that does not lead to death.
<p>
“18 We 
know that anyone born of God does not continue to sin; the one who was born of 
God keeps him safe, and the evil one cannot harm him.”
</blockquote>

Professed believers in the New Testament were presumed to be brothers in the 
Lord. But, like Judas Iscariot, tares are planted among the wheat. Since the 
true Christian cannot sin (in a way that leads to death), such a sin (apostasy) 
indicates that one is not truly a child. There is no change of status here, only 
a revelation of status.
<p>
In 
the case of typical sins, God’s discipline (which may even result in premature 
death, as evidenced in 
<a href="javascript:scripture('../scripture/1cor1130.htm')" target=_self class=pop>1 Corinthians 11:30</a>)
 can be averted through prayer and 
repentance. In the case of the sin of apostasy, John tells us not to even bother 
praying. This is the sin of <b>Hebrews 6</b> for which there is no repentance. This is 
the sin not of physical death but spiritual hopelessness. This rejection of 
Christ after professing Christ evidences that one is not on the path to life, 
but to death. Paths have not changed, matters have only become transparent.
<p>
The 
true believer need not fear such a bleak destiny. John knew what many today 
deny: no one born of God can commit that sin. God keeps Him safe. <b>1 John 5:17</b> 
is obvious in meaning once we make a distinction between sin and “the sin.”

<p><b>Interpretation of the Verses in Question</b>
<blockquote>
<p align="left"><b>1 John 2:15-17</b> &quot;Do not love the world or anything in the world. If anyone loves the world, the love 
of the Father is not in him. For everything in the world&#151;the cravings of sinful 
man, the lust of his eyes and the boasting of what he has and does&#151;comes not 
from the Father but from the world. The world and its desires pass away, but the 
man who does the will of God lives forever.&quot;


<blockquote><b>Expanded, Interpreted, Amplified, and Paraphrased</b>

<p>Society and the philosophy of man-centered society should not be our first love. 
Since the will of God and popular or traditional trends may be at odds, your 
love for the Father must be greater than your desire to fit in or your lust for 
illegitimate pleasure and stimulation.
<p>
The 
things that make sin attractive—your own sinful nature, the beautiful things that 
are appealing to the eye, or the things that make you feel good and produce 
arrogance, these are not going to make you what God wants you to be.
<p>

Society and sinful temptations are temporary, but the one who genuinely knows 
Christ will evidence it by continuing on in the faith, staying true to God and 
ordering his life with Christ as Lord. This person will enjoy God’s presence 
forever.
</blockquote></blockquote>
<blockquote>
<p align="left"><b>1 John 3:7-10</b> &quot;Dear children, do not let anyone lead 
you astray. He who does what is right is 
righteous, just as he is righteous. He who does what is sinful is of the devil, 
because the devil has been sinning from the beginning. The reason the Son of God 
appeared was to destroy the devil's work. No one who is born of God will 
continue to sin, because God's seed remains in him; he cannot go on sinning, 
because he has been born of God. This is how we know who the children of God are 
and who the children of the devil are: Anyone who does not do what is right is 
not a child of God; nor is anyone who does not love his brother.&quot;

<blockquote><b>Expanded, Interpreted, Amplified, and Paraphrased</b>

<p>Brothers, do not be led astray. Anyone who does what is righteous by staying 
true to the faith, living a life that is consistent with God’s standards, and 
caring about fellow believers, this person has the righteousness that comes from 
faith in Jesus Christ.
<p>

Others who are not meeting these standards evidence that they belong to the 
enemy’s camp. Though they are in the church, they are planted among us by Satan 
himself. The devil has a track record of falling away from the truth of God 
since the beginning. After all, remember that one of the reasons Christ came was 
to destroy the devil’s work, so we should not sympathize with those who are 
trying to lead others into error regarding salvation or the nature of Christ. It 
would be inconsistent for us to help the devil’s work when we believe Christ 
came to destroy that work.

<p>
The 
genuine believer is born of God and is a new creation; it is impossible for the 
man or woman who is a child of God to desert the faith. His spiritual genes 
come from God, so his soul is safe from the type of sin that denies the God who 
gave him life.
<p>
This 
is how we can sort out the wheat from the tares, the true believer from those 
planted in our midst by Satan. If we hold fast to the faith and if we are drawn 
to fellow believers like siblings are drawn together, it is because we are of 
the same family, God’s family.
<p>

Those who are led away from the true teaching about Christ, after having once 
understood it and professing it, are evidencing their kinship to another family, 
the devil’s family.</blockquote></blockquote>
<blockquote>
<p align="left"><b>2 John 1:8-9</b> These verses cannot be fully understood unless 
approached in context. I will 
quote <b>2 John 1:8-11</b>: “Watch out that you do not lose what you have worked for, 
but that you may be rewarded fully. Anyone who runs ahead and does not continue 
in the teaching of Christ does not have God; whoever continues in the teaching 
has both the Father and the Son. If anyone comes to you and does not bring this 
teaching, do not take him into your house or welcome him. Anyone who welcomes 
him shares in his wicked work.”

<blockquote><b>Expanded, Interpreted, Amplified, and Paraphrased</b>

<p>Watch out and do 
not lose some or all of your rewards by aiding and abetting false teachers. 
Enjoying heaven is wonderful, but you want the joy that comes from having a full 
reward (crowns and treasures in heaven). You can lose those rewards if you are 
naïve and not alert to false teachers. So here is one basic criteria: those who 
continue in the teaching of Christ—not just those who made a start and then 
deserted or altered what we apostles taught—are true, but false teachers who 
deny the Son don’t have the Father. They may talk about God, but if they deny 
that the Son is God then they have no true relationship to the true God.</span></p>
<p>
If you receive, 
encourage, and help these false teachers to propagate their errors, then you are 
partly responsible for what they are doing. If you do so, you will lose your 
rewards in heaven and be one "saved so as by fire."
</blockquote></blockquote>
<b>Conclusion</b>
<p>
John boldly asserts that the true believer, the true seed, does persevere. The genuine 
believer has been born of God and he cannot sin that “sin unto death.” Rather 
than teaching us that believers can lose their salvation and should live life in 
fear of doing so, the Apostle offers us a template to interpret the reason for 
defections within the Kingdom. The tares have been sown among the wheat. Paul 
had felt this pain when two friends, Hymaneus and Philetus, defected,&nbsp; “….The 
Lord knows those who are his…” (<b>2 Timothy 2:19</b>) Paul concluded. If Paul was 
surprised to see this happens, we should not delude ourselves into thinking that 
Satan has run out of tares.
<p>
But the Lord does 
know them that are His. It’s not that they shift back and forth. Those who 
belong to God belong to God. Those who do belong not do not belong. Those of us 
who are wheat need to bless His Name all the more!

