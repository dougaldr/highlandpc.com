<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 15
<br><span class=byln>by Ed Vasicek</p>

<p>L<span class="sm">ORD</span>,</p>
<p>When Ezekiel saw a vision of You, this is how he 
described the vision: &quot;Above the expanse over their heads was what looked like a 
throne of sapphire and high above on the throne was a figure like that of a 
man. I saw that from what appeared to be his waist up he looked like glowing 
metal, as if full of fire, and that from there down he looked like fire; and 
brilliant light surrounded him. Like the appearance of a rainbow in the clouds 
on a rainy day, so was the radiance around him. This was the appearance of the 
likeness of the glory of the L<span class="sm">ORD</span>. When I saw it, I fell facedown, and I heard 
the voice of one speaking.&quot; (<b>Ezekiel 1:26-29</b>) Lord, when I get just a small 
glimpse of Who You are, it humbles me. Like Ezekiel, I prostrate myself before 
You.</p>
<p>My Father, pride is such an awful thing. That I constantly need to remind 
myself of Who You are. Familiarity breeds contempt, or so 
they say. But I do know that it is possible to get too familiar with You. But 
there is such a difference between becoming familiar and getting to know You 
better which breeds respect. For the more I learn about You, the more I 
marvel.</p>
<p>Lord, life can drain me of my energy. I thank You that 
through renewing my strength by spending time with You I not only regain a 
sense of peace 
(<a href="javascript:scripture('../../scripture/phil447.htm')" target=_self class=pop>Philippians 4:4-7</a>) 
but I also regain internal energy and motivation 
(<a href="javascript:scripture('../../scripture/is402931.htm')" target=_self class=pop>Isaiah 40: 29-31</a>).</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God.</i>).</p>
<p>Father, I pray for the Commonwealth of Puerto Rico. Thank 
You that 27.6% of the population claims to be born-again evangelicals Yet, Lord, 
the translation of Your Word into daily life has been a weak link in the chain. 
Alcoholism, drugs and crime rates are high. Lord, help the churches grow in 
discipling believers and help those who know Christ to make Him their priority.</p>
<p>Thank You Lord for MAP (Medical Assistance Programs) International, &quot;a Christian relief and development organization that addresses 
the total health needs of people living in the world's poorest communities. 
Working in partnership with Christian nationals, missionaries and other 
Christian health organizations, MAP provides community health training and 
essential medical supplies for hospitals, clinics, and emergency relief.... MAP 
offers short-term mission fellowships for medical students, enabling them to 
have first hand experience in a Christian mission hospital.&quot; Bless this 
ministry, O Lord. Provide for them, protect them, give them fruit for their 
labors, empower them by Your Spirit and grant them harmony amongst themselves, 
I ask.</p>
<p>Now help me to live out the Christian life at home, I ask 
in the Savior's Name, Amen.</p>

