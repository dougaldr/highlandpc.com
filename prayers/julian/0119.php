<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 19
<br><span class=byln>by Ed Vasicek</span></p>

<p>O God,</p>
<p>You have been so good to me. You are excellent in all Your 
ways, and Your gracious kindness overwhelms me. Because You are good, You work 
all together for good toward those Who love God. </p>
<p>Lord, thank You that You have created me with a 
conscience. That is part of what it means to be in Your image. Thank You what 
when I accepted Jesus, You intensified that conscience by giving me a new 
nature, a deeper etching of Your Law upon my heart of flesh. Help me to live my 
life according to that new nature—not according to my old, rationalizing one.</p>
<p>Lord, help me to set my mind on the good and lovely things 
of life. Prevent me from becoming cynical; instead help me be a positive 
testimony toward others as I highlight God's grace in my life.</p>
<p>Lord, help me to keep a positive and constructive attitude 
when I have to deal with people I do not enjoy. Part of me wants to take a dig, 
but I don't think this would please You. &nbsp;Although You do encourage me to assert 
myself and speak the truth, You want me to do so in love, not out of getting 
even. Help me to leave justice in these small matters to You.</p>
<p>&quot;Help me to resist temptation and the lures of the Satan 
today.&quot;</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, the nation of Australia is one that usually brings a 
smile to our faces as we envision kangaroos and crocodile hunters. But 
spiritually, only 10 percent of the population attends church in a given week. 
We thank You that there are some strong evangelical churches, even within the 
Anglican church. But we pray that You would stir up Your people to seek Your 
face, and that the Australian believers would become more committed than ever. 
We also pray that You would double their number within the next decade. Please 
revive the church in Australia.</p>
<p>Today we remember the American Missionary Fellowship, a 
ministry that seeks to reach people in the United States. In the past, they 
helped organize and plant Sunday Schools, and now they seek to reach the various 
cultures residing within the U.S. Give them fruit for their labor, keep them 
true to You, and provide for their needs.</p>
<p>We remember our local Red Cross chapters. Thank You for 
the help they offer, provide for them, and guide them.</p>
<p>Please make my day 
a day of service of You, I pray, in Jesus' Name, Amen.</p>

