<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Nation at War
<br><span class=byln>by Ed Vasicek</span></p>

<p>Our Father in Heaven,</p>
<p>We thank You that You know exactly what is going on here on earth. We
sinful human beings constantly find ourselves confused, distressed, and alarmed
because of our own behavior and that of others. What miseries result from
our sinful ways!</p>
<p>Lord, we find ourselves at war. Many of us believe that this war is
just; others own differing views. But whatever our viewpoint, Lord, we
pray that You would protect the innocent, bring justice to the guilty, and help
this war to be resolved quickly and with minimal loss of life. We ask that
this conflict would resolve many more problems than it creates.</p>
<p>We pray that You would guide our president and his staff. Oh, Father,
we cannot imagine the pressures President Bush faces. We thank You that he
is a man of faith, but we also know that he is but human. So please lift
the weight of his burdens, protect him, and fill him with Your wisdom and
Spirit. We ask that this conflict would draw us nearer to You, causing us
to examine the direction of our lives. Help us to realign ourselves with
Your Word and dedicate ourselves to follow You anew.</p>
<p>We pray for our service men and women, our citizens, and the common people of
Iraq. We ask you to send angels to protect them. We pray that Iraq
would become a free and prosperous nation where justice prevails.</p>
<p>We also pray that You would protect us from terrorist attacks. We know
that both the formal and informal alliances of evil can inflict untold harm to
our people, and so we petition You to shield our citizens and foil the plots of
evildoers.</p>
<p>Dear gracious Lord, shower wisdom upon all in authority, we pray. We
choose to trust that You shall work these things out in accord with Your perfect
and sovereign will. We claim Your provision of peace and protection.</p>
<p>We pray in Jesus' Name, Amen.

