<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 3
<br><span class=byln>by Ed Vasicek</p>

<p>God of all comfort,</p>
<p>Thank You for Your great compassion. You are a God who is 
sympathetic, empathetic and a God Who has pity upon us as a father does upon 
his child. Thank You that you understand misery and suffering and that You 
comfort us in our tragedies. Your Spirit alleviates the pain in our hearts 
when we seem to be at the end of ourselves. You suffer with us, you bear with 
us, and You care about our lives and feelings.</p>
<p>Father, it breaks my heart to think about people who attend 
evangelical churches faithfully and yet do not personally know Jesus Christ as 
Lord and Savior. A good church accurately teaches salvation by grace alone 
through faith alone. But preaching the Gospel accurately does not guarantee 
understanding. So many people think that heaven or hell are determined by our 
works, that we are our own saviors. They fail to grasp that they are helpless 
and hopeless, which is why they need a Savior, Jesus Christ. Open their eyes, 
Lord.</p>
<p>Lord, troubled marriages are all around me. Often I am not 
aware of that: a friend at work, a neighbor, a relative. A mom with young 
children feels trapped, and she could use some time away with her husband, 
but no one is available to watch the children; perhaps finances are tight 
and the tensions high. A man or woman was reared in a messed 
up family and the baggage they bring into their marriage is destroying it. Those 
are just a few. Help the marriages around me to get stronger especially 
within my church family.</p>
<p>Please meet the following needs, requests, and desires as 
You see fit: __________.</p>
<p>Today I remember the nation of France. It is distressing 
to know that less than one percent of the population claim to be born-again 
evangelicals. On the other hand, I praise You that the number of believers in 
France has grown almost five times since 1960. But much of this growth comes 
from the great work You have done among the gypsy community—the Roma people. 
We pray that Your Spirit would sweep over the French people, as it did during 
the Reformation when nearly half the population was evangelical. Father, we pray 
that the evangelical church would make a deep impact upon the native French 
population and that the evangelical church would double over the next five 
years. May a growing number of the Islamic population find Christ as well, and 
keep working with the gypsy peoples, I pray.</p>
<p>Lord, thank You for the ministry of Evangelistic Faith 
Missions. Thank You for their outreach into many nations, and I pray that Your 
Spirit would empower their Gospel presentations and discipleship efforts. Keep 
them true to You, bless them with harmony amongst themselves, give them wisdom, 
discretion and safety, and provide for all their needs.</p>
<p>Help me to be faithful to You today in everything, I ask 
in Jesus' Name, Amen.</p>

