<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 20
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Christmas is approaching. Soon we will celebrate the 
birth of our Savior and His first coming. Lord, the Magi’s gifts teach us much 
about that first coming.</p>
<p>Jesus is King, as represented by the gold. Jesus is God 
and is to be worshipped, as signified by the gift of frankincense. But He would 
also be a man of sorrows and acquainted with grief, as represented by the myrrh. 
Although myrrh was sometimes used as a perfume, it was also used as a drug for 
those in pain.</p>
<p>This tells us that Jesus was born to die that we might 
live. His painful death on the cross was accepted by the Father as a sweet 
sacrifice. Jesus came not only to set an example, to found the church and to 
teach, He came to give His life as a sacrifice. As He put it in <b>Mark 10:45</b>, 
&quot;For even the Son of Man did not come to be served, but to serve, and to give 
his life as a ransom for many.&quot; Praise Your Name!</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I find 
my strength in His power, not my own. May Your Spirit control me today.</p>
<p>First John says that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 John 
1:9</b>). I confess the sins I am aware of now and I ask Your Spirit to search 
my heart. (Pause to admit them to God.)</p>
<p>My Father, pride is such an awful thing. That I constantly need to remind 
myself of Who You are. Familiarity breeds contempt, or so they say. But I do 
know that it is possible to get too familiar with You. There is such a 
difference between becoming familiar and getting to know You better which breeds 
respect. For the more I learn about You, the more I marvel.</p>
<p>Today I pray for the massive nation of Russia. O Lord, how complex this land 
is. On the one hand, religious freedom has been greatly expanded since the fall 
of Communism; on the other, some of those gains are being lost. So many millions 
have died in concentration camps for their faith, O Lord. May those days never 
return! But now the government and particularly the Orthodox Church is promoting 
intolerance of other faiths. Less than 1% of the population claim to be 
evangelical and the challenges are many. Lord, may Your Spirit sweep across this 
land and draw a large number of people to saving faith, I ask. Give the church 
wisdom.</p>
<p>I pray for MECO, Middle East Christian Outreach. Bless them as they seek to 
nurture evangelism, discipleship, servanthood and prayer within evangelical 
Middle Eastern churches. Their burden is to reach the main &quot;21 cities with 
populations of over a million people, including some of the world's largest: 
Cairo (10m+), Tehran (8m+), Istanbul (8m+), Baghdad (nearly 5m), Alexandria 
(3m+) and Ankara (nearly 3m).&quot; They are also burdened to work among &quot;the 
refugees meeting physical needs...demonstrating God's love and grace.&quot; O Lord, 
bless this mission's work. Empower them with Your Spirit, grant them harmony 
among themselves, safety, wisdom, provision and great fruit for their labor.</p>
<p>Dear God, I commit my day to You, in the Savior's Name, Amen.</p>

