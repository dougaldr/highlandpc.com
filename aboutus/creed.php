<p id="pgt">A Firmly Evangelical Creed
<br><span class=byln>by Ed Vasicek</span></p>

<p>I believe that the 66 books of the Bible are the inspired Word of God,
without error as originally given. I believe the Bible is the only infallible
source of doctrine and practice.<br>
<br>
I believe in only one personal God, Yahweh, manifested in three Persons, Father,
Son, and Holy Spirit. The Father, Son, and Holy Spirit had no beginning, but
have existed from all eternity.<br>
<br>
I believe that human beings are in the image of God, but because of sin are
alienated from God and are by nature lost and in need of salvation.<br>
<br>
In His love, God the Father sent the Son in the person of Jesus Christ. He was
born of Mary while she was yet a virgin, and He lived a sinless life. He offered
Himself on the cross as a sacrifice for our sins, bearing our transgressions.&nbsp;
His death satisfied the holiness and justice of God.<br>
<br>
On the third day, He rose again, physically and literally. He ascended into
heaven, from whence He will one day return to set up His Kingdom on earth.<br>
<br>
I believe I have forgiveness of sin, eternal life, and new birth the moment I,
in repentance, trust Jesus as my personal Savior. It is not through my good
works, rituals, or church involvement that I attain eternal life. Jesus has
attained eternal life for me: all I have done is receive this gift.<br>
<br>
I believe in a literal, eternal heaven and hell.<br>
<br>
I believe that after I am saved, I am to live a life of obedience to the Lord,
thus evidencing the genuineness of my conversion. One step of obedience is water
baptism by immersion which is to follow my salvation. Baptism is not necessary
for salvation, but is necessary as one of many steps of obedience.<br>
<br>
I believe that every genuine believer becomes part of the Universal Church, and
that every believer possesses and has been baptized by the Holy Spirit, but I
believe it is my responsibility to allow the Holy Spirit to fill me on a
continual basis.<br>
<br>
I believe that it is God's will for believers to assemble regularly with other
believers and be held accountable to them. I believe the purpose of the church
is to glorify God, especially by making disciples at home and through Missions,
around the world.<br>
<br>
I believe the church gathers as an opportunity for believers to build up and
encourage other believers. It also gathers to promote spiritual maturity and to
demonstrate our appreciation to God in praise. I believe that God is more
interested in my heartfelt obedience than in religion, and that some truths are
absolute and non-negotiable. Therefore, I am trusting God to help keep me from
error of both belief and behavior.<br>
<br>
I believe that God wants to manifest the fruit of the Spirit in my life, and
that my disposition and behavior should draw people to Jesus, not drive them
away. Therefore, I realize I need to serve Jesus seven days a week, twenty-four
hours a day, resisting the snares of my own sinful nature, the snares of the
devil, and the temptations of the world.</p>

