<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Jesus and Women
<br><span class=byln>by Ed Vasicek</span></p>

<p><i>The DaVinci Code</i> has been, in some ways, a boon to 
pastors. As more Christians realize that their assumptions are open to 
challenge, they are interested in subjects that used to draw yawns. A few years 
ago, no one wanted to hear about the Gnostic Gospels and why we reject them. 
Most people could not have cared less how the Bible came to be, and even fewer 
seemed concerned about the real Jesus. (I am convinced that the real Jesus is 
the Jesus of the Bible, but this Jesus is not the passive, feminized version 
many Christians and churches present.)</p>
<p>If you participated in our &quot;Focus on Jesus&quot; series, we examined the entire 
life of Christ as recorded in all four Gospels. We started the series in early 
2001 and completed it during 2003. The handout material is still available on 
our <a href="../studies/index.php#fojc" target="offline" class="reg">website</a>. Fortunately, while some 
soldiers in the Lord's army were daydreaming, many of you took your training 
seriously!</p>
<p>One issue raised by <i>The 
DaVinci Code</i> is Jesus' relationship to women. In a previous article, I 
discussed why Jesus was neither married nor fathered children. He was truly a 
&quot;eunuch for the Kingdom of God,&quot; dedicated to become the sacrificial Lamb.</p>
<p>But what was Jesus attitude 
toward women? On the one hand, He established the church by training her basic 
leaders, the apostles. Only men were chosen as apostles, and the concept of 
male leadership in the church is consistent throughout Scripture: the Old 
Testament priests had to be male (Moses), the New Testament Apostles were male 
(Jesus), and Paul teaches that church leaders who teach doctrine/Bible <i>to men</i> 
must be male, as must elders 
(<a href="javascript:scripture('../scripture/1tim2915.htm')" target=_self class=pop>1 Timothy 2:9-15</a>, 
<a href="javascript:scripture('../scripture/1tim312.htm')" target=_self class=pop>3:1-2</a>). 
Yet, on the other hand, 
God used prophetesses as a channel of communication (e.g., 
Miriam, Deborah, in the Old Testament and the daughters of Agabus in the New), 
and both men and women were encouraged to prophesy in the early church 
(<a href="javascript:scripture('../scripture/1cor11311.htm')" target=_self class=pop>1 Corinthians 11:3-11</a>). 
The reasons for these restrictions on leadership have <i>
nothing</i> to do with competence or ability, nor are they justified on the 
basis of that day's culture. Instead, they are anchored to the order of 
creation and the events of the Fall, past events that do not change (as does 
culture). The leadership of men in the home or in the church rises or falls 
together, since they are mandated with the same justification. However, it 
takes quite a stretch to translate this concept into the political or work 
world.</p>
<p>Just because Jesus did not 
appoint women to positions of leadership does not mean that he did not hold 
women in high regard. As a mater of fact, the Jews had a long heritage of 
respecting womanhood. The Pharisees are often given a bum rap for thanking God 
daily that they were not born women. But what is typically left out of the 
discussion is WHY they were thankful for this. You see, Jewish women were not 
required to make the pilgrimage to Jerusalem three times a year for the three 
festival seasons. Since many women would be pregnant or caring for young 
children, this would have been a hardship. The Old Testament Law <i>allowed</i> 
women to make the trip (and many did, as did Mary when Jesus was a boy of 12), 
but did not require them to do so. The Pharisees were glad they were men so 
they could better observe the Law without exemption. It was not that they 
looked down upon women. Nor did Jesus.</p>
<p>We read about some important 
women in the Kingdom of God in <b>Luke 8:1-3</b>: &quot;After this, Jesus traveled about 
from one town and village to another, proclaiming the good news of the kingdom 
of God. The Twelve were with him, and also some women who had been cured 
of evil spirits and diseases: Mary (called Magdalene) from whom seven demons had 
come out; Joanna the wife of Cuza, the manager of Herod's household; Susanna; 
and many others. These women were helping to support them out of their own 
means.&quot;</p>
<p>Please note the variety and crucial ministry these women 
served. Some had been demonized, others were wealthy. They also provided the 
financial backing for Jesus' ministry. Ponder that. Most Christians are unaware 
of this almost invisible group of supporters.</p>
<p>When they traveled, the women would caravan together and 
make a separate camp. Some of the Apostles were married (like Peter), and so 
were some of these women but they traveled in distinct groups, as was the 
custom. When Jesus addressed the crowds, these women took in his teaching. 
They probably counseled women and children, baptized female converts, and, in a 
sense, were the forerunners of the office of deaconesses. </p>
<p>Although fictitious stories about Jesus being involved with 
or married to Mary Magdalene are popular right now, not only was Jesus never 
romantically involved with any woman, his closest friends were two sisters and a 
brother, Mary (not Magdalene), Martha, and Lazarus. We will call them Mary or 
Martha Bat Bethany (daughter of Bethany) or Lazarus Ben Bethany (son of Bethany) 
after their hometown. Mary Bat Bethany was the woman who sat at Jesus feet 
while her sister Martha fussed in the kitchen.</p>
<p>Mary Bat Bethany was the only disciple who really seemed to 
comprehend that Jesus was actually going to die. Although earlier an immoral 
woman (possibly Mary Magdalene) had expressed repentance by anointing Jesus' 
feet, Mary Bat Bethany prepared Jesus for burial by anointing his head with perfume 
(<a href="javascript:scripture('../scripture/matt26613.htm')" target=_self class=pop>Matthew 26:6-13</a>) 
the day before His crucifixion. She was the only one of 
the disciples—male or female—who really knew how to listen. She was the 
only believer who comprehended what was about to happen. Ponder that as well.</p>
<p>Women have always played a prominent role in the Kingdom of 
God. Their influence is every bit as great as the influence of men, although 
the <i>way</i> they influence might be more discreet. Sarah certainly 
influenced Abraham, Deborah served as a reluctant judge (<i>like a mother</i> to 
Israel; see 
<a href="javascript:scripture('../scripture/judg57.htm')" target=_self class=pop>Judges 5:7</a>) 
and military strategist, and it appears that Aquilla 
would not have been very aggressive without the initiative of his wife, 
Priscilla (who is usually mentioned first) in 
<a href="javascript:scripture('../scripture/acts181828.htm')" target=_self class=pop>Acts 18:18-28</a>.</p>
<p>Jesus' view toward women—and the view of the original 
church He established—is neither the traditional (put-down/inferiority) view 
we inherited from Medieval culture nor that of the Feminist movement. God 
created both man and woman in His image 
(<a href="javascript:scripture('../scripture/gen127.htm')" target=_self class=pop>Genesis 1:27</a>), 
and they are more alike than different. But the genders are different to a degree; like apples and 
oranges, it is moot to argue which is superior. In the Kingdom of God, men may 
seem to have more overt influence, but women have plenty of covert influence. 
And sometimes the covert is more influential than the overt. Together, through 
teamwork, brothers and sisters in Christ serve God as laborers in His field. 
Jesus respected women far more than many men do, but He was far removed from the 
feminist agenda or the push for a unisex society. Perhaps the best summary was 
written in <b>1 Corinthians 11:11</b>, &quot;In the Lord, however, woman is not independent 
of man, nor is man independent of woman. For as the woman came from man, so 
also man is born of woman....&quot;</p>


