<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>What About Mary? Part IV - Finale: Mary's Challenges
<br><span class=byln>by Ed Vasicek</span></p>

<p>Thus far in our series, we have discovered that Mary was a 
godly but very human woman. Although all sorts of legends evolved over the 
centuries, Mary was not all that different from other godly first-century women 
in Israel.</p>
<p>We believe that she was busy rearing her other children, 
Jesus' four brothers and sisters 
(<a href="javascript:scripture('../scripture/matt135355.htm')" target=_self class=pop>Matthew 13:51ff</a>) 
when Joseph died (Joseph is last mentioned when Jesus was 12). As Mary's 
firstborn son, Jesus very likely had to watch out for His mother.</p>
<p>When Jesus was somewhere near thirty (My guess is 33.), it 
was time for Him to begin His ministry as a sage (rabbi) and train disciples and 
apostles who would lay the foundation of the church. When Jesus had just a few 
disciples, He and His followers were invited to a wedding in Cana of Galilee.</p>
<p>Up until this point, Jesus had worked no public miracles. Most 
<a href="http://www.highlandpc.com/bb/" class="reg">Body Builder</a>
readers are familiar with the story in <b>John chapter 2</b>. During 
the wedding reception, the host ran out of wine to serve the people. Let's pick 
up the story with <b>verses 3-5</b> from the ESV, &quot;When the wine ran out, the mother of 
Jesus said to him, 'They have no wine.' And Jesus said to her, 'Woman, what does 
this have to do with me? My hour has not yet come.' His mother said to the 
servants, 'Do whatever he tells you.'&quot;</p>
<p>When Jesus addressed his mother as &quot;Woman,&quot; he was not 
being rude to her--not exactly. Such a term in that culture was not a slight 
as it would be in ours (&quot;Bring me some bread, woman!&quot;). But His statement, &quot;What 
does this have to do with me&quot; implies that Jesus did not appreciate Mary's 
interference.</p>
<p>Although Jesus may have had a gleam in His eye, because He 
did work the miracle, He was gently acclimating Mary to His new role as Rabbi 
and eventually as Savior. He no longer belonged to His mother or family but to 
the world. His domestic role as eldest son was at an end. Like all mothers, Mary 
had to learn to let go.</p>
<p>Although Mary knew that Jesus was destined to be the 
Messiah, she was probably bombarded with criticisms about Jesus from her other 
children. Jesus' four brothers rejected His claims as Messiah, as demonstrated by 
the harsh exchange of <b>John 7:3-7</b>: </p>
<blockquote>Jesus' brothers said to him, &quot;You ought to leave here and 
go to Judea, so that your disciples may see the miracles you do. No one who 
wants to become a public figure acts in secret. Since you are doing these 
things, show yourself to the world.&quot; <i>For even his own brothers did not 
believe in him. </i>Therefore Jesus told them, &quot;The right time for me has not 
yet come; for you any time is right. The world cannot hate you, but it hates me 
because I testify that what it does is evil.&quot;</blockquote>
<p>Jesus' brothers did not come to faith until after the 
Resurrection. Two of them went on to write books in our New Testament: James and 
Jude.</p>
<p>So we can only imagine what Mary went through at home. 
Perhaps, when she heard about how busy Jesus was and the uproar surrounding Him, 
she may have wondered whether He had gone too far. A text in <b>Mark 3</b> (<b>vs. 20-21</b>) 
seems to imply this:&nbsp; &quot;Then he went home, and the crowd gathered again, so that 
they could not even eat. And when his family heard it, they went out to seize 
him, <i>for they were saying, 'He is out of his mind</i>.'&quot;</p>
<p>But was it only Jesus' brothers who feared He was insane? A 
few verses later we read (<b>vs. 31-35</b>), &quot;And his mother and his brothers came, and 
standing outside they sent to him and called him. And a crowd was sitting around 
him, and they said to him, 'Your mother and your brothers are outside, seeking 
you.' And he answered them, 'Who are my mother and my brothers? And looking 
about at those who sat around him, he said, 'Here are my mother and my brothers! 
Whoever does the will of God, he is my brother and sister and mother.'&quot;</p>
<p>Jesus was hurt that His own family thought He was mad. Thus 
He explains that His spiritual family is more important than His genetic family. 
The fact that He includes the term &quot;mother&quot; in His statement might imply that 
Mary was in agreement with her other sons, that Jesus was not sane. Others 
believe she accompanied them but did not agree with their viewpoint.</p>
<p>Since John the Baptist had his doubts about Jesus 
(<a href="javascript:scripture('../scripture/luke719.htm')" target=_self class=pop>Luke 7:19</a>), 
should it surprise us that Mary, too, had her doubts? Whereas I do not 
think Mary ever doubted that her Son was the Messiah, it does seem that she had 
her doubts about HOW He was trying to establish His Kingdom!</p>
<p>Things got even stranger for Mary when her Son was taken by 
His powerful enemies at night and illegally sentenced to die. Could Jesus 
actually be crucified? Then she understood what Simeon's prophecy meant, &quot;…a 
sword will pierce your own soul too&quot; (<b>Luke 2:35b</b>).</p>
<p>As she watched the agony of her Son on the cross, we read: 
&quot;When Jesus saw his mother and the disciple whom he loved standing nearby 
[John], he said to his mother, 'Woman, behold, your son!' Then he said to the 
disciple, 'Behold, your mother!' And from that hour the disciple took her to his 
own home&quot; (<b>John 19:26-27</b>).</p>
<p>Like Mary, John had believed that Jesus was the true 
Messiah. Like Mary, John was present at the cross with His Savior. In time, when 
James came to faith in Jesus, Mary would have a loving family that would be 
united in faith. But, for that fateful period of time, John looked out for Mary 
in place of Jesus.</p>
<p>We know that Jesus made a special resurrection appearance 
to His brother, James 
(<a href="javascript:scripture('../scripture/1cor157.htm')" target=_self class=pop>1 Corinthians 15:7</a>), 
and we can but assume that Mary was among 
the crowd of 500 to whom He appeared 
(<a href="javascript:scripture('../scripture/1cor156.htm')" target=_self class=pop>1 Corinthians 15:6</a>).</p>
<p>Mary is mentioned the last time in 
<a href="javascript:scripture('../scripture/acts11214.htm')" target=_self class=pop>Acts 1:12-14</a>
as being part of the &quot;Upper Room&quot; crowd. She had found her place as a disciple of Jesus, 
under the authority of the Apostles. Despite her humble position, she knew that 
she had experienced the greatest blessing any human could experience, being the 
mother of the Messiah.</p>
<p>What did she do with the rest of her life? Probably served 
the church as best she could, and encouraged her naturally born son, James, to 
lead the church in Jerusalem (which he did). Her heart was no doubt filled with 
praise as she worshipped the Son she had reared, the One who was both Son of Man 
and Son of God.</p>
