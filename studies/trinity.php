<p><b>Conclusion:</b>
<p>There is such a wealth of information and many, many more
quotes available that I did not use here. I presented enough quotations to give
a glimpse into the doctrine of each of these men and to show a consistent line
from the apostles to the Council of Nicaea. These are not the heretics speaking,
but the church fathers that have never been disputed in the church for their
Trinitarian doctrine. They are the foundation of the church. They carried the
light directly from the source and passed it on. We see a consistent doctrine
passed down and in agreement as to what the church believed from the apostles
all the way through the Council of Nicaea, where the church officially wrote a
creed of beliefs. All of these bishops and theologians are unified in their
agreement. The early church believed that there was one God; the person of the
Father who was the one God; the person of the Son, Jesus Christ, who was the one
God; and the person of the Holy Spirit, who was the one God; and that these
three are one of essence or substance, and in unity yet eternally distinct.</p>
<p>We should take notice that there is no record of the church
fathers who believed in the Triune God as being counted as heretics or being
refuted by the church until after Constantine sided with Arius the heretic.&nbsp;
All
of the church’s documented position was always Trinitarian. The heretics were
always challenged for denying the Trinity and the deity of Christ. Historic
Christianity, which was passed down from the Apostles has always affirmed this
position. The heresies (those who denied the Trinity) were the challengers, as
we will see in the 3<sup>rd</sup> part of this study.</p>
