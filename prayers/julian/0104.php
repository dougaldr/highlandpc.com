<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 4
<br><span class=byln>by Ed Vasicek</span></p>

<p>Holy God, Your holiness forces me on my knees. I am 
awestruck. Like the angelic beings, I confess that You are holy, holy, holy—Father, Son, and Holy Spirit! You are separate from Your creation, You are 
completely pure and above me. You cannot stand sin, and Your holiness defines 
Who You are. You dwell in inapproachable light because You are pure.</p>
<p>Thank You, Lord, for the new nature, the new inclination 
You gave me when I received Your Son, our Savior, the Lord Jesus Christ, through 
personal faith in Him. Although my old sinful nature is far from dead, I am 
grateful that You have taken away my heart of stone and replaced it with a heart 
of flesh. I now relate to You on the basis of the New Covenant.</p>
<p>So, Lord, I pray that I would be fruitful in my service for 
You. Today I ask that You would open the door for me to witness to another 
about the truth of the Gospel. Open the door for me, Lord, and help me walk 
through it.</p>
<p>I pray for my daily and relational needs. Minister to me 
in Your grace and provide for me because You are my Good Shepherd.</p>
<p>Lord, I am thankful for many things, three of which are: __________.
</p>
<p>Today I pray for the massive nation of China. Lord, You 
are working mightily in the churches there, despite persecution. The number of 
believers is multiplying. I pray for continued numerical growth for the Kingdom 
there. I pray that You would provide the necessary materials for believers to 
grow, and that they would mature in the faith. I pray that, in Your grace, the 
persecutions would cease. Help the believers to be strong when they cannot 
evade persecution or discrimination.</p>
<p>Today I especially remember the ministry of TEAM. This 
missionary organization has hundreds of your servants scattered around the 
globe. Protect them, give them fruit for their labor and meet their needs, I 
pray.</p>
<p>I pray for our county clerk. Help he or she to do the job 
well and with fairness and efficiency. Help him or her to know You and fear 
You.</p>
<p>May Your blessing be upon me this day.</p>
<p>I ask in Jesus’ Name, Amen.</p>

