<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 14
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You take delight in Your people. This is so very 
amazing that You should be involved with us at all. How much more amazing is 
the fact that You are acquainted with all our ways. You are complete in 
Yourself and You know what is going on with everyone who makes up planet 
earth’s billions. Yet You are the God who enjoys people, a God Who knows how to 
celebrate and rejoice. Many of Your children fail to realize that You are a 
happy God. We know about Your wrath, but we often forget Your delight.</p>
<p>I pray for my pastor, staff and church board leaders, 
officers and teachers. If any do not know You, may they become born-again. 
Help them to be true to the Bible and to put Your Word over their own personal 
agendas. Help them to have genuine integrity, Biblical convictions, love, 
wisdom and courage. Help them put their walk with You first, their families 
second and their ministries third. Do not allow others to intimidate them into 
neglecting their families and remind them: &quot;the fear of man works a snare.&quot; 
Help them be most concerned about pleasing You, while also seeking peace and 
harmony within the congregation.</p>
<p>Father, I have some personal health concerns including: __________. 
I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>Lord, help me remember that “A righteous man is cautious in 
friendship” (<b>Proverbs 12:26</b>). Since my friends do influence me, help me to be 
cautious in friendship. My arrogance tells me that I am not like other people, 
but Lord, like it or not, I am. I am vulnerable.</p>
<p>Today I remember the European nation of Portugal. Thank 
You that 3.1% of the population claims to be born-again evangelicals. The church 
has been growing, for this I praise You, O Lord. May Your Spirit bring many 
more to repentance. May the percentage of believers double over the next 
decade, I ask. Many counties have no evangelical witness at all and many 
churches are also into prosperity teaching that diverts the true mission of the 
church. Work in these situations according to Your will, I ask.</p>
<p>Lord, bless the Macedonian Project. “The Mac Project is 
the short-term sending arm of the Jesus Film Project. Through short-term mission 
opportunities, [they] seek to give everyone everywhere a chance to see and hear 
Jesus in their own language, so that everyone knows someone who truly follows 
Jesus. [Their] focus is primarily the 10/40 Window.” O Lord, bless this 
ministry and the Jesus Film project. You have used this tool as a mighty 
outreach vehicle. So many thousands have come to know You through this. Bless 
this ministry more than ever; double the fruit this year, I ask. Protect, 
provide and empower by Your Spirit, I pray.</p>
<p>Lord, I remember those who work as iron and steel workers.
Keep them safe on the job, help them to work well and may those who know 
You share Christ with their fellow workers. Help them exemplify Christian 
character to Your glory as well.</p>
<p>Use me, Lord, I pray in Jesus’ Name, Amen.</p>

