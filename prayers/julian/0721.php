<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 21
<br><span class=byln>by Ed Vasicek</p>

<p>O God,</p>
<p>Thank You for being a God Who loves me. You love 
mankind; You care about Your vast creation. But You also care about and know 
<b>me</b>. Jesus died for me. You know the number of hairs on my head. You know 
every thought I think before I think it. Yes, Lord, You are Lord of all, but 
You are also my Lord. You are a personal God who knows each of us personally. 
You feel with this, think with us, You experience life with us. You are so much 
a part of everything I do that I sometimes forget You are here with me. Remind 
me, Lord.</p>
<p>Today I pray for missionaries that my church or I support 
including: __________.</p>
<p>I remember my marriage (if I am married) and the marriages 
of my extended families, friends and church families. Lord, hold them 
together. Help married couples to lean upon you and to work hard at 
addressing problems and experiencing harmony. I also pray for those who are 
divorced, that You would help them to go forward with Your hand in theirs.</p>
<p>I pray for those who struggle with loneliness. Although 
solitude can be good, loneliness causes emotional pain. You created us for 
Yourself and for others; there is something missing in our lives if we do not 
fellowship with You and there is something missing if we do not get close to 
others. We can also feel lonely if we are close to others but separated by 
schedule or distance. Sometimes it is Your will for us to be 
lonely. Our Lord Jesus often felt lonely, so help me not blame myself as though 
loneliness were a sin. On the other hand, help me to determine to connect to 
others and shun taking the role of the passive victim.</p>
<p>Lord, the nation of Rwanda has been a real heartbreak. 
Thank You that over a fifth of the population claim to be born-again 
evangelicals. But Lord, the horrible genocide in the 1990s has hurt the 
population horribly. Over a million died and some of the aggressors claimed to 
be Christians. Lord, such ethnic rivalry is a great tool of Satan and he used 
it skillfully in Rwanda. O Father, help the church and society to recover from 
this unimaginable trauma. It is still hard to believe this actually happened. 
Rwanda is also plagued with AIDS; over 11% of the population is affected. Lord, 
help the missionaries and church leaders as they try to pick up the pieces and 
rebuild this devastated land. Bring forth a pure church from the ashes of the 
past, I ask.</p>
<p>Lord, bless the work of North America Indian Ministries (NAIM), 
&quot;a non-denominational faith mission focusing on Native people groups [working] 
in relationship with North American Indians in evangelization, discipleship and 
church planting. This association brings believers together in obedience to 
God's Word for fellowship, worship, teaching, prayer, baptism and the Lord's 
supper and as witnesses in outreach.&quot; Lord, prosper this mission with fruit, 
provision and safety. May Your Spirit empower the missionaries and help the 
Native American church to become strong and zealous.</p>
<p>Help me to be zealous for You today, I ask in Jesus' Name, 
Amen.</p>


