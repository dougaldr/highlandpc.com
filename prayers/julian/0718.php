<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 18
<br><span class=byln>by Ed Vasicek</p>

<p>My Master and my God,</p>
<p>You are the God Who lifts us up out the slimy mud and 
mire. You love to reach down and set us upon You, the Rock of Ages. You take 
pleasure in exalting the humble and humbling the proud, for You want it known 
that You alone are God. We mortals forget that, You have to shake us to wake 
us. Those who have hardened their hearts against You have chosen eternal death 
in the lake of fire, for Your wrath, O God, is a consuming fire. Those who mock 
You think You need them, but they are hurting themselves. Although You rejoice 
and take pleasure when a person comes to repentance, You are not a weak God Who 
must depend upon mortal man.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________.</p>
<p>Let me remember that You are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>Lord, today I especially remember those looking for a job. 
Open doors of opportunity and match the right person to the right job. 
Especially help those who work with integrity and have good character to find 
employment.</p>
<p>Also, dear Father, help me to handle my finances well. 
Guide me when it comes to saving, giving and help me know when to say no. Help 
me control my money under Your guidance; please, do not let money control me.</p>
<p>Thank You, O Lord, for true churches where the doctrine of 
salvation by grace through faith alone is taught and where believers are led in 
Bible study and prayers directed to God alone and congregations where most 
people have been born-again into Your family. We pray that false churches would 
see the errors of their ways and correct them.</p>
<p>I remember the African nation of Reunion. Though this 
island in the Indian Ocean had few Christians before 1966, praise the Lord that 
5.2% of the population now claims to be born-again evangelicals. Yet the 
history of slavery in this region left a legacy of promiscuity, illegitimacy 
and poverty. Lord, help those who truly know You to escape this legacy by 
walking closely with You! Thank You for the work of Child Evangelism 
Fellowship and other youth outreaches in this nation. May the percentage of 
evangelicals double over the next decade and may the believers become more and 
more deeply committed, I pray.</p>
<p>I pray for the missionaries my home church supports, some 
of whom are: __________. Bless them, provide for their needs, protect them, empower 
them by Your Spirit and may they bear much fruit, I pray. Grant them harmony amongst one another 
and bless their families.</p>
<p>Now, Lord, help me to live for You today, in Jesus' Name, 
Amen.</p>

