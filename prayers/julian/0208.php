<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 8
<br><span class=byln>by Ed Vasicek</span></p>

<p>Precious Lord,</p>
<p>You are Yahweh, the great I AM. When Moses asked Your 
Name, You told Him, &quot;I AM.&quot; I Am that I Am is Who You are, the eternal God, 
Father, Son, and Holy Spirit. You had no beginning, and time is nothing to You 
because You transcend it. You are not, &quot;I Was,&quot; nor are You, &quot;I Will Be,&quot; but 
&quot;I AM.&quot;</p>
<p>As Jesus said, &quot;Before Abraham was, I AM&quot; (<b>John 8:58</b>), thus 
identifying Himself as Yahweh, the L<span class=sm>ORD</span>. Although You have many titles, the 
Name by which You are known is YAHWEH. We so reverence that Name that we 
translate it as L<span class=sm>ORD</span>, all in upper case letters.</p>
<p>Lord, You have been good to me. I am grateful for your 
blessings, a few of them include: __________.</p>
<p>I praise You and am overjoyed that You have adopted me into 
Your family. We all dread being alone, and had You not adopted me, my future 
would be a lonely eternity indeed. Thank You that now I will enjoy Your 
company, the company of other believers, and even myriads of angels. I am so 
glad to be in Your family.</p>
<p>Help me, Lord, to reflect the family to which I belong, the 
family of God. May others see Christ in me. Help me to be both bold and kind, 
realistic yet a person of faith.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control me 
today.</p>
<p>Dear God, help me to find meaning in the details of life. 
Sometimes I wonder what the purpose of it all is. Yet I remember that even the 
little things of life bring You glory. So help me not get discouraged or 
demoralized.</p>
<p>Today I pray for the large nation of Brazil. Lord, You 
have drawn many people to Yourself in that land, but many evangelical churches 
are shallow and legalistic. Pastors are often untrained, and there are a lot of 
ego issues that Satan uses to break up unity or justify sinful behavior. Help 
the believers in Brazil to espouse Christianity worth reproducing. Thank You 
for the increased training of leaders through Bible Institutes and seminaries.</p>
<p>Today I remember Global Outreach Mission and their 346 
missionaries serving in 39 countries around the world. Help them as they share 
the Good News of Jesus Christ and make disciples. Keep them true to You, help 
the missionaries to get along well with one another, provide for them, dear 
Lord, spiritually, financially, and with Your Spirit's power. Keep them safe, 
and may the individual families be strong.</p>
<p>Today I remember those who work in the food service 
industry. Help Christians who are employed in this area to be a good testimony 
and to demonstrate a servant's heart that glorifies God.</p>
<p>Help me to depend 
upon Your supply of grace this day, I pray, in Jesus' Name, Amen.</p>

