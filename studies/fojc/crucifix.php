<br>The Crucifixion of Jesus Christ
<br><span class=byln>by Ed Vasicek</span>

<p>It never ceases to amaze me 
that the major events of the New Testament were predicted or foreshadowed in the 
Old. For example, Isaiah 53, written about 600 BC (and included in the Jewish 
Bible) predicted the atoning death of the Messiah, His death between thieves, 
His burial in a rich man’s tomb, and His resurrection. This chapter not only 
predicts these events, but provides us with their theological interpretation, <nobr><b>Isaiah 53</b>:</nobr> 
<blockquote><b><sup>1</sup> “Who has believed our message and to whom has the arm of the 
L<span class=sm><b>ORD</b></span> been revealed? <sup>2</sup> He grew up before him like a tender shoot, and like a root 
out of dry ground. He had no beauty or majesty to attract us to him, nothing in 
his appearance that we should desire him.</b></p>
<p> <sup>3</sup> He was despised and 
rejected by men, a man of sorrows, and familiar with suffering. Like one from 
whom men hide their faces he was despised, and we esteemed him not. <sup>4</sup> Surely he 
took up our infirmities and carried our sorrows, yet we considered him stricken 
by God, smitten by him, and afflicted. <sup>5</sup> But he was pierced for our 
transgressions, he was crushed for our iniquities; the punishment that brought 
us peace was upon him, and by his wounds we are healed. </p>
<p> <b><sup>6</sup> We all, like sheep, 
have gone astray, each of us has turned to his own way; and the L<span class=sm><b>ORD</b></span> has laid on 
him the iniquity of us all. <sup>7</sup> He was oppressed and afflicted, yet he did not open 
his mouth; he was led like a lamb to the slaughter, and as a sheep before her 
shearers is silent, so he did not open his mouth. <sup>8</sup> By oppression and judgment he 
was taken away. And who can speak of his descendants? For he was cut off from 
the land of the living; for the transgression of my people he was stricken. </b>
</p>
<p> <sup>9</sup> He was assigned a grave 
with the wicked, and with the rich in his death, though he had done no violence, 
nor was any deceit in his mouth. <sup>10</sup> Yet it was the L<span class=sm>ORD</span>'s will to crush him and 
cause him to suffer, and though the L<span class=sm>ORD</span> makes his life a guilt offering, he 
will see his offspring and prolong his days, and the will of the L<span class=sm>ORD</span> will 
prosper in his hand. <sup>11</sup> After the suffering of his soul, he will see the light 
[of 
life] and be satisfied; by his knowledge my righteous servant will justify many, 
and he will bear their iniquities. <sup>12</sup> Therefore I will give him a portion among the 
great, and he will divide the spoils with the strong, because he poured out his 
life unto death, and was numbered with the transgressors. For he bore the sin of 
many, and made intercession for the transgressors. </blockquote>
<p>If <nobr><b>Isaiah 53</b></nobr> describes what 
Jesus accomplished on the cross, <nobr><b>Psalm 22</b></nobr> (written about 1000 BC) 
predicts some of the specific historical events involved: <b>Psalm 22:1-18</b>:</p>
<blockquote><b><sup>1</sup> My God, my God, 
why have you forsaken me? Why are you so far from saving me, so far from the 
words of my groaning? <sup>2</sup> O my God, I cry out by day, but you do not answer, by 
night, and am not silent. <sup>3</sup> Yet you are enthroned as the Holy One; you are the 
praise of Israel. <sup>4</sup> In you our fathers put their trust; they trusted and you 
delivered them. </b></p>
<p> <sup>5</sup> They cried to you and 
were saved; in you they trusted and were not disappointed. <sup>6</sup> But I am a worm and 
not a man, scorned by men and despised by the people. <sup>7</sup> All who see me mock me; 
they hurl insults, shaking their heads: <sup>8</sup> &quot;He trusts in the 
L<span class=sm>ORD</span>; let the L<span class=sm>ORD</span> 
rescue him. Let him deliver him, since he delights in him.&quot;</p>
<p> <b><sup>9</sup> Yet you brought me out 
of the womb; you made me trust in you even at my mother's breast. <sup>10</sup> From birth I 
was cast upon you; from my mother's womb you have been my God. <sup>11</sup> Do not be far 
from me, for trouble is near and there is no one to help. <sup>12</sup> Many bulls surround 
me; strong bulls of Bashan encircle me. <sup>13</sup> Roaring lions tearing their prey open 
their mouths wide against me. <sup>14</sup> I am poured out like water, and all my bones are 
out of joint. My heart has turned to wax; it has melted away within me.</b></p>
<p><b> <sup>15</sup> My strength is dried 
up like a potsherd, and my tongue sticks to the roof of my mouth; you lay me in 
the dust of death. <sup>16</sup> Dogs have surrounded me; a band of evil men has encircled 
me, they have pierced my hands and my feet. <sup>17</sup> I can count all my bones; people 
stare and gloat over me. <sup>18</sup> They divide my garments among them and cast lots for 
my clothing.</b></blockquote>

