<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 12
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are my light. You open the eyes of my heart to 
understand Who You are. If it were not for Your Spirit preparing my heart, I 
would not have really understood that You forgive me and offer me eternal life 
free of charge to me. I would not have understood that when Jesus said, &quot;It is 
finished,&quot; it was. The full price of my redemption had been paid. By receiving 
Christ as my Savior, I now have eternal life. It is not just something I will 
have; I already have it. I do not have to hope that my sins are forgiven; I 
know they are. I do not have to guess that I have eternal life, I know I do 
(<a href="javascript:scripture('../../scripture/1john51113.htm')" target=_self class=pop>1 John 5:11-13</a>).</p>
<p>Your light extends to my ability to perceive spiritual 
truths. The Word of God just jumps out at me. Although it is not always so, 
sometimes I get excited just reading my Bible as I discover new truth—new to 
me, that is. You guide me each day into Your way. Thank You that You are the 
God Who gives light. Sometimes You give light well in advance, sometimes You 
give light at the last moment. But You are my light and my salvation.</p>
<p>Lord, Your Word tells me to cast all my anxiety upon You, 
because You care for us 
(<a href="javascript:scripture('../../scripture/1pete57.htm')" target=_self class=pop>1 Peter 5:7</a>). 
I remember Jesus in Gethsemane and how 
stressed He was He sweat drops of blood. He felt the sting of the trauma He 
had to face and it wore on Him. But You strengthened Him, gave Him the courage 
to go on and in the end, worked things for a greater good.</p>
<p>Today I pray for the 10/40 window, that area which includes 
much of Asia and northern Africa. It includes the Arab world, the 
Indo-Iranian-Tibeto-Burman region, south and east Asia, the Sub-Sahara, We know 
that the Gospel has penetrated this area least of all. Persecution and lack of 
religious freedom have insulated and isolated these regions from the Gospel. 
Many forbid missionaries; some will not even allow the few existing Christians 
to meet. We pray for victories and for the Gospel to germinate and bear much 
fruit. Use radio, satellite television, The Jesus film and tactful witnessing 
by existing believers to multiply the church exponentially. Thank You that in 
recent years we have seen some great progress. Protect, prosper and provide 
for Your servants in this part of the world. Give them much fruit for 
their labors and bind the Evil One, I ask.</p>
<p>Father, thank You for New Hope International which is &quot;committed to 
equipping the church in Eastern Europe through partnerships 
that provide training, materials and encouragement as they win the next 
generation for Christ.... It partners 
with ministries such as Focus on the Family, Youthbuilders, BCM International, 
Injoy, as well as cutting-edge churches.&quot; O Father, help this ministry to make 
a big dent for the Kingdom of God as they nurture family life and seek to reach 
and disciple young people in Eastern Europe. Empower, provide, protect and 
grant unity to them, I ask.</p>
<p>Lord, help me to be a gracious Christian today, I pray in 
Jesus' Name, Amen.</p>

