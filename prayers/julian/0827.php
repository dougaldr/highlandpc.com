<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 27
<br><span class=byln>by Ed Vasicek</p>

<p>Great Master,</p>
<p>You are the Lord of all, but You are also the Lord of the 
individual. You have arranged a situation in which we can decide whether we 
want You to be the Lord of our lives or not. Although we recognize Your 
Sovereign workings behind the scenes, we do experience a choice on our part. 
You are not everyone's chosen Lord. But I have personally covenanted with You 
and I know You not only as the Lord of the Universe, but as MY Lord and Master. 
Help me to be less and less like the disciples when Jesus criticized them with, 
&quot;Why do you call me Lord, Lord, but not do the things that I say?&quot; (<b>Luke 6:46</b>). 
Help me, Lord, both to desire to obey You and to actually obey You.</p>
<p>Adonai, I pray for my lost friends and family members 
including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Help me to do my part by volunteering to serve or 
continuing to serve in a Bible-believing, Bible-teaching church. May I set my 
roots down deeply and to attend every week I am able to do so.</p>
<p>Lord, today I remember the nations in which believers are 
persecuted, facing death, dismemberment, torture, rape, sexual abuse, 
imprisonment, slavery, or confiscation of goods. These persecuting nations 
include: Comoro Islands, Cuba, Uzbekistan, North Sudan and Morocco. O Father, 
may these and the others among the 86 persecuting countries become enlightened 
in the realm of human dignity and God-given rights including the right we have 
to choose and practice our religious convictions. May the persecutors 
themselves be won to faith in Christ; may the governments mandate and enforce 
the recognition of true religious freedom, and help believers by granting them 
an extra portion of wisdom. Help them to evade persecution when possible and 
help them to find their strength in You when they cannot. </p>
<p>Lord, I pray for the Hunan province of China, the most 
difficult part of China to successfully evangelize. Your Spirit has been 
working mightily in other parts of China, but in Hunan, only about 2% of the 
population claim to be born-again evangelicals. The 67 million people identify 
with Chairman Mao who came from this region and they view the Gospel as 
foreign. Hearts are hard here and the Scriptures are sparse. O my Father, 
soften the hearts of the Hunan people. By Your Spirit, bring many to eternal 
life, I pray. May the number of believers double over the next five years, I 
pray.</p>
<p>Dear God, thank You for D&amp;D Missionary Homes: &quot;When a 
missionary family in transition seeks housing—either going to the field, or 
returning for a short stay, they face considerable expenditures that usually are 
not recovered, or cost-effective when considering the period of time 
involved—usually two to six months.&quot; Through D &amp; D &quot;an example of costs 
for a missionary couple...is about 30%-40% the cost of equivalent rentals for 
three months to one year.&quot; Father, bless this ministry as it serves Your 
servants, I ask.</p>
<p>Help me to bless Your servants and to be a blessing to lost 
people around me. In Jesus' Name I pray, Amen.</p>

