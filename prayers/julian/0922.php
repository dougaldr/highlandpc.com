<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 22
<br><span class=byln>by Ed Vasicek</p>

<p>Great Heavenly Father,</p>
<p>You are the God Who built up Jerusalem in Your time. You 
restored the city to her exiles under Ezra and You allowed it to be destroyed 
in judgment in 70 A.D. You have restored this city to the Jewish people and 
one day Your Son, our Savior, will reign from Jerusalem. Your calling and Your 
gifts are irrevocable.</p>
<p>You are the God Who heals the brokenhearted. Lord, so many people are 
brokenhearted right now as I pray. Bind up their wounds, O Lord. Comfort them 
as only You can. I know that I too, have and will need Your help. Lord, life 
is tough with You; the thought of going at it alone without You seems 
unbearable.</p>
<p>You have determined the number of stars in the universe 
and You call each of them by name. Billions upon billions of them flicker their 
distant light, but You never confuse them. I praise, You, O Lord.</p>
<p>My God, help me to be a good steward of the funds with 
which You provide me. <b>Proverbs 17:18</b> reads, &quot;Of what use is money in the hand 
of a fool, since he has no desire to get wisdom.&quot; O, Lord, may I be a wise 
person. Help me be wise in all areas including money management.</p>
<p>Almighty One, like every one of the billions of people who 
inhabit our world, I am vulnerable. I am a breath away from death and a step 
away from disaster. So, Lord, please send angels to watch over me and protect 
me. Keep me from harm and evil of every kind in the physical, relational, and 
spiritual realms, I ask.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. I give them to You and ask You to help me constantly do so.</p>
<p>Lord, help me to get to know my neighbors where I live and 
at work or school. Teach me the importance of participating in my community as 
an avenue to benefit myself and a way to show love to others.</p>
<p>Father, thank You for my senses. In Your grace and 
goodness, keep them all healthy and help me to appreciate them and use them in 
ways that please You. Help me to discover the joys of being a human being 
created in Your image, yet help me to enjoy life in ways consistent with Your 
revealed will.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You.</p>
<p>Lord, I pray for the Asian nation of Turkmenistan. O Lord, 
both Sunni Islam and Eastern Orthodoxy are tolerated in this nation, but 
evangelical Christianity is virtually unknown here. Zero percent of the 5 
million people who live in this land are evangelicals. But thank You that the 
number has grown over the past ten years from 1 or 2 to 600. Lord, I pray that 
this growth rate would continue over the next several decades! Please use the 
Jesus film, Christian radio, and the testimony of the few who know You, I ask.</p>
<p>Now use me today, I request in Jesus’ Name, Amen.</p>

