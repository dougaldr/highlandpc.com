<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 30
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty One,</p>
<p>You are Yahweh, the God Who heals. Although the health of 
my soul is first and foremost because my soul will live forever, You 
nonetheless also care about my body.</p>
<p>You designed the human blueprint, and You formed me within 
my mother's womb. Thank You for the times You do heal us. And thank You for the 
times that You choose not to do so; sometimes suffering is part of Your will for 
us to develop and nurture our faith and to teach us to depend upon Your grace. 
Yet so often You bless us with health and we thank You for that! We thank You 
for Your provision of doctors, medicines, preventive medicine and Your 
healing touch.</p>
<p>I know there is a special blessing that comes from praying 
with others. This might include family members who know You, friends from 
church, or fellow Christians at work, school, or in the neighborhood. We know 
You are specially present where two or three gather in the Name of Jesus 
(<a href="javascript:scripture('../../scripture/matt181920.htm')" target=_self class=pop>Matthew 18:19-20</a>).</p>
<p>Please meet the following needs, requests and desires as 
You see fit: __________.</p>
<p>Help me to do my part by volunteering to serve or 
continuing to serve in a Bible-believing, Bible-teaching church. May I set my 
roots down deeply and attend every week I am able to do so.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me of them, and You will cleanse me from 
all&nbsp;unrighteousness (1 John 1:9). So I bring my sins before You now. I&nbsp;do not 
excuse them, but I freely admit them and ask for Your
forgiveness.</p>
<p>I remember the nation of Ethiopia. Lord, this land has 
been through a lot: wars, famines and droughts. But thank You that You have 
worked mightily by Your Spirit, and almost 20% of the population are born-again 
evangelicals. Thank You that a large evangelical movement has penetrated the 
Orthodox Church there and many have been born-again.</p>
<p>Lord, Islam is also growing, gaining converts from others 
in the Orthodox Church and from the animistic religions. There seems to be a 
race going on and we pray that the evangelical church would win it! May Your 
Spirit continue to empower Your people there. Thank You for the growth in 
leadership training facilities and we pray that the leaders would be strong in 
all ways.</p>
<p>
Thank You for the ministry of Cook Communication Ministries &quot;committed to 
encouraging people everywhere to accept Jesus Christ as personal Savior. Cook 
supports, promotes and contributes to teaching and applying His two great 
commands—loving God and loving one another—by creating and disseminating 
Christian communications materials and services to people throughout the 
world.... Cook's&nbsp;international ministry focuses on nations and regions of the world 
where&nbsp;the Church is growing&nbsp;so rapidly that there is a desperate need for 
trained pastors and leaders as well as&nbsp;outreach materials in the heart language 
of the people.&quot; Provide for this ministry in every way, Lord, and may Your 
Spirit provide the workers with harmony and wisdom.</p>
<p>
Today we pray for those who work as loan officers or mortgage consultants. Help 
them to be honest, realistic and practical. We pray that many in this field 
would come to know Christ and that that those who do would share the Gospel as 
You open doors.</p>
<p>
Gracious Lord, help me to be a gracious person, I pray in Jesus' Name, Amen.</p>

