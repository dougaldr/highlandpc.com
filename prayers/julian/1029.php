<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 29
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are the God Who is near and not far away. You are close at hand, right 
next to me at all times. I do not have to work up a frenzy to enjoy Your 
presence because Your presence is a reality 24/7, although You can be present in 
special ways as, for example, when two or three gather in Your Name. Help me to 
be anxious for nothing because &quot;the Lord is near.&quot;</p>
<p>Help me, O Lord, to express myself to You completely. I do not like it when 
people complain to me time and time again; even my loved ones get tired of 
hearing me when I feel whiny and negative. And to deal with angry people, just 
the thought makes me shudder. But Lord, as I read the Psalms, I see David and 
others complaining, whining and bringing their disappointments and fears to You 
time and time again. Lord, we can't wear You out. You never say, &quot;enough, you've 
filled your quota.&quot; Lord, even if we are angry at You, You can take it. You will 
haggle things out with us and help us gain Your perspective. I have some dear 
ones on earth who help me, but none can do what You can. Thank You, O Lord, that 
You are such an accessible God through Jesus Christ.</p>
<p>O Father, help me to manage my time well. It seems I am so often in a rush. 
Modern life is busy for many reasons, but that is an extra reason why I need to 
carve out some time for You. Because life is so stressful, the pace is so 
quick and I must make decisions with speed, I need Your guidance all the more.</p>
<p>Father, I have some personal health concerns including: __________. I also 
pray for the physical, mental, or spiritual health of others including: __________.</p>
<p>Lord, the Wallis and Futuna Islands are a small Pacific island nation with a 
population of only fourteen thousand. Few of these have a personal relationship with 
Jesus Christ and the Bible has yet to be translated into their mother tongue. 
Only .5% of the population claim to be born-again evangelicals. O Lord, triple 
that number over the next decade. Grant many the repentance that leads to life, 
I ask.</p>
<p>Thank You, O Lord, for the ministry of Sonshine Ministries International as 
it &quot;works to change the future of those orphaned by AIDS. Tanzania, located in 
East Africa, is a country particularly hard hit by the AIDS pandemic. With a 
population of nearly 38 million people, Tanzania has a swelling population of 
over 1 million orphans; 22% of Tanzanian children under the age of 15 are AIDS 
orphans. This number is expected to increase to 1.5 million by 2010. This huge 
number of orphaned children has overwhelmed the extended and foster family 
systems.&quot; O Lord, bless and provide for this ministry of mercy. May these 
children come to know Christ and experience His love through this ministry, I 
ask.</p>
<p>Help me to be an instrument of grace to others, I ask in the Messiah’s Name, 
Amen.</p>

