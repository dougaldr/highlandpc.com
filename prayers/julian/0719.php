<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 19
<br><span class=byln>by Ed Vasicek</p>

<p>Merciful God,</p>
<p>Thank You that You work through Your providence behind the 
scenes. The Book of Esther never mentions Your Name, yet Your work is evident 
throughout it. When wicked Haman, the king's right-hand man, plotted to 
exterminate the Jewish people, You had arranged things—before he even schemed—to thwart his plan. Esther was in place as queen and Mordecai had previously 
saved the kings life—and they were both Jewish—something the king did not 
know. And that, Lord, is how You work. You work in advance to assure that Your 
goals are indeed attained.</p>
<p>Father, help me to remember the words of our Lord Jesus 
Christ, &quot;He who is not with me is against me&quot; (<b>Luke 11:23</b>). Lord, sometimes I 
pull back on my commitment to Christ. I jump in with both feet, but then I 
remove a foot and try to be my own master. But I can never be my own master: I 
either serve God or my sinful nature, for my new nature is out to serve God. 
Help me to consider myself dead to sin and my old nature and alive to You.</p>
<p>I pray for my lost friends and family members including: __________.
Father, please send Your Spirit to draw them to Yourself and grant&nbsp;them the 
repentance that leads to life.</p>
<p>I pray for the elderly, sickly, and shut-in, including: __________.</p>
<p>Help me to be an alert Christian, Lord. I do not want to 
be numbered among those who value peace so much that I keep my head in the sand 
or deny undesirable realities. No, I need to understand how I am led into sin, 
how the devil tempts me and to have answers for the questions people are 
asking. I need to read, study and tool up as one of Your workers. I can 
neither rest on my laurels nor live in the past, for You have placed me in 
today's world. That means You want me to address today's challenges. Help me 
to be brave and to do so, I ask.</p>
<p>Lord, I pray for the nation of Romania. Thank You that 
over 6% of the population is evangelical. Thank You for the growth in the 
church, but, Lord, that growth has slowed down. I pray that You would help the 
church to double over this next decade. I pray that the Orthodox Church would 
stop hindering the spread of Biblical evangelicalism and that many would come 
to know Christ in a personal and saving way. Keep western materialism from 
destroying the purity of the church, I ask.</p>
<p>Thank You, Lord, for New Life International, &quot;a Christian outreach organization dedicated to providing humanitarian and 
developmental activities by providing safe water to the world.... Through local, 
national and international evangelism, the water purifiers open doors and are a 
tool to present Jesus Christ, the 'Living Water.'&quot; Lord, as these workers bring 
water purifying systems to the world, may they also be effective in sharing 
Jesus Christ, the One Who can make rivers of living water flow from our 
innermost being.</p>
<p>Now, Lord, help me to share this Messiah with someone 
today, I ask in His Name, Amen.</p>

