which God did among you through him, as you yourselves know. <sup>23</sup> This man was handed over to you by God's set purpose 
and foreknowledge; and you, with the help of wicked men, put him to death by 
nailing him to the cross. <sup>24</sup> But God raised him from the dead, freeing him from 
the agony of death, because it was impossible for death to keep its hold on him.
	</p>
<p><sup>32</sup> God has raised this Jesus to life, and we are all 
witnesses of the fact. </p>
<p><sup>36</sup> &quot;Therefore let all Israel be assured of this: God has 
made this Jesus, whom you crucified, both Lord and Christ.&quot; </p>
<p><sup>37</sup> <b><u>When the people heard this, they were cut to 
the heart</u></b> and said to Peter and the other apostles, &quot;Brothers, what 
shall we do?&quot;</p></blockquote>
<p>What does God expect 
from them at this point, in light of their previous rejection of Jesus? 
Remember, the concern of the Great Commission is not primarily seeing people 
saved; it is producing maturing disciples (who observe all things). Note the 
Commission in <b>Matthew 28:19-20</b>: </span>"Therefore go and make disciples of all 
nations, baptizing them in the name of the Father and of the Son and of the Holy 
Spirit, and<b><u> teaching them to obey everything I have commanded you</u></b>. 
And surely I am with you always, to the very end of the age.&quot;
Salvation--which is by grace and apart from anything that can be 
"done&quot;--is merely phase one. God's will for us is never merely phase one! When 
we are disciples who have been taught and actually observe "all things," then we 
have arrived at maturity, which is a separate (but interrelated) issue from 
salvation.</p>
<p>The "forgiveness of 
sins," which we can roughly equate with salvation (they are not exactly one and 
the same, though closely related) is not the main issue here; forgiveness is, in 
this text, a consequence of knowing and following Christ and righting the wrong 
of rejecting Him by fully accepting Him.</p>
<p>Because they are told 
to do two things (repent and be baptized) does not necessarily mean that both of 
those things are <i>absolutely</i> necessary for the forgiveness of sins. That 
is not the issue under discussion. The issue is "what should we do?"</p>
<p>If the question were, 
"What are the minimal requirements for one to go to heaven at death?" that would 
be a different story. But that is not the nature of the question.</p>
<p>When the specific 
question related to salvation is asked in <b>Acts 16:30</b> (by the Philippian jailor), 
namely, "What must I do to be saved?" the answer is given specifically</span>, 
"Believe in the Lord Jesus 
Christ, and you shall be saved." We later 
learn that he and his family, were, in fact, baptized, because that is what God 
wanted them to do in addition to being saved (and it still is what God expects 
believers to do). But the answer to the more specific question, the minimal 
description of how one can be saved, is to "believe in the Lord Jesus Christ." 
Not "believe and be baptized."</p>
<p>Note that this text 
nowhere discusses what would happen to an individual who believed in Christ but 
was not baptized. It could also be argued that baptism, in this instance, was a 
test of the sincerity of repentance. Although the Bible does not directly 
answer the question, "Is baptism absolutely necessary to salvation?" it does 
frequently answer the question of how a person could be saved. If the 
overwhelmingly vast majority of texts clearly do not include baptism as a 
salvation requirement, then that question, to my way of thinking, is answered. 
We will enumerate some of those texts later in this document.</p>
<p>But first, we can find 
a relevant passage (for comparison purposes) in <b>Romans 10:9-10</b>:</p>
<blockquote><p><sup>9</sup> That if you confess with your mouth, &quot;Jesus is Lord,&quot; 
and believe in your heart that God raised him from the dead, you will be saved.
	<sup>10</sup> For it is with your heart that you believe and are 
justified, and it is with your mouth that you confess and are saved. <sup>11</sup> As the Scripture says, &quot;Anyone who trusts in him will 
never be put to shame.&quot;</p></blockquote>
<p>Note in this <b>Romans</b> 
passage there is no mention of baptism. Note in the <b>Acts 2:38</b> passage, there is 
not mention of "confessing with your mouth, 'Jesus is Lord.'" And, in the 
Romans passage, we could argue that a mute (someone who cannot speak) or 
paralyzed person could never be saved. Even though he could confess Christ 
through sign language or writing, the text does say, "with his mouth."</p>
<p>The Romans passage, 
like the Acts passage, does not deal with someone who believes but does not 
confess with his mouth. If you believed in your heart, you would be considered 
righteous, but if you did not confess with your mouth, we could argue, you would 
not be saved. So a fine situation this is: righteous before God but not saved? 
No, I don't think so. The issue of what happens if you believe in your heart 
without confessing with your mouth is not addressed here. But we are told that 
if we believe with our heart, the result is righteousness. This parallels in 
many ways our passage in <b>Acts 2</b>. </p>
<p>Also note that most of 
us would agree that someone who confessed with the mouth Jesus as Lord but did 
NOT believe with the heart is clearly lost. So most of us would agree that 
someone who is baptized apart from repentance and faith would be lost. This 
further highlights the concept found in both Old Testament and New, that it is 
the heart that matters, but that the heart shows itself outwardly. 
Incidentally, we'll save the thief on the cross for later.</p>
<p>So we might also 
conclude that it is possible to be a secret believer (for a time; I do not 
believe this can be permanent). Take Joseph of Arimathea in <b>John 19:38</b>; he was 
a secret "disciple," which means he was a follower of Christ. Yet he was NOT 
publicly declaring his faith. Yet the text presents him as a good man and a 
believer. <b>John 19:38</b> reads: "Later, Joseph of Arimathea asked Pilate for the body of 
Jesus. Now Joseph was a disciple of Jesus, but secretly because he feared the 
Jews. With Pilate's permission, he came and took the body away."</p>
<p>We see the same 
situation in <b>Acts 2</b>, the idea of repentance (turn from sin and turning toward 
Christ) and baptism (publicly confessing Christ). But what saves a person is 
being rightly related to Jesus Christ, of which both baptism (when understood as 
a clear command of Christ) and confession with the mouth are evidences. But it 
seems Joseph was saved, even though he did not confess with his mouth.</p>
<p><b>Now if one holds to 
an Arminian viewpoint and a Campbellite viewpoint at the same time, you can see 
that if one lost his salvation and therefore becomes unregenerate, he would 
logically have to be baptized again to restore his salvation.</b>
If you concluded otherwise, then either it is impossible to lose 
one's salvation or baptism is not absolutely essential to the new birth.</p>
<p><b><a name="sctn72">Question: Why Wasn't This Question in the Early 
Church Raised in Scripture: "Is Baptism Essential to Salvation? Can One Be 
Saved Without Being Baptized?"</a></b></p>
<p><b>Answer:</b> This question 
was probably not raised because the early church was concerned with making 
disciples, not merely seeing people saved from hell. Since baptism is a 
necessary part of discipleship, the question of when the threshold of salvation 
occurred was not a concern.</p>
<p>Another possible answer 
was that this matter was made clear by the Apostles and their teaching. For 
example, Paul seems to state in <b>Ephesians 2</b> that believers were born lost in sin, 
that God resurrected them spiritually (regeneration) as a sovereign act, and one 
result of this quickening (regeneration) was faith in Jesus Christ followed by a 
life of good works 
(<a href="javascript:scripture('../scripture/eph2810.htm')" target=_self class=pop>2:8-10</a>).</p>
<p>What has made the issue 
more complicated is the debate over mode and time of baptism. Since the 
apostles could authoritatively tell the early church, "baptism by immersion 
after faith," anybody that truly wanted to serve Jesus Christ would naturally 
follow Him in baptism. </p>
<p>When false modes 
(sprinkling or pouring) and erroneous timing (infant) for baptism came into 
play, it confused the New Testament pattern. So now we have people who view 
themselves as baptized because they were sprinkled as a baby. If these 
individuals come to trust in the work of Jesus Christ for their salvation but 
retain their erroneous teaching about baptism's mode and timing, are they truly 
saved? The early church never had to address this issue because of the presence 
and authority of the apostles at that time and the Jewish tradition of baptism 
that was well established.</p>
<p><b><a name="sctn73">Mark 16:9-20</a></b></p>
<p><b>Mark 16:9-20</b> are verses 
left out of the earliest manuscripts. Although it is possible that this text 
was in the originals and left out, I tend to "camp" on the shorter readings, 
recognizing at the same time that those questionable verse might, in fact, be 
the Word of God. I have included the NIV introductory note:</p>
<blockquote><p><sup>9</sup> [The most reliable early manuscripts and other ancient 
witnesses do not have <b>Mark 16:9-20</b>.] When Jesus rose early on the first day of 
the week, he appeared first to Mary Magdalene, out of whom he had driven seven 
demons. <sup>10</sup> She went and told those who had been with him and who 
were mourning and weeping. <sup>11</sup> When they heard that Jesus was alive and that she had 
seen him, they did not believe it. </p>
<p><sup>12</sup> Afterward Jesus appeared in a different form to two 
of them while they were walking in the country. <sup>13</sup> These returned and reported it to the rest; but they 
did not believe them either. </p>
<p><sup>14</sup> Later Jesus appeared to the Eleven as they were 
eating; he rebuked them for their lack of faith and their stubborn refusal to 
believe those who had seen him after he had risen. </p>
<p><sup>15</sup> He said to them, &quot;Go into all the world and preach 
the good news to all creation. <sup>16</sup> <b>Whoever believes and is baptized will be saved</b>,
<b><u>but whoever does not believe will be condemned</u></b>.<u><b> </b> </u><sup>17</sup> And these signs will accompany those who believe: In 
my name they will drive out demons; they will speak in new tongues; <sup>18</sup> they will pick up snakes with their hands; and when 
they drink deadly poison, it will not hurt them at all; they will place their 
hands on sick people, and they will get well.&quot; </p>
<p><sup>19</sup> After the Lord Jesus had spoken to them, he was taken 
up into heaven and he sat at the right hand of God. <sup>20</sup> Then the disciples went out and preached everywhere, 
and the Lord worked with them and confirmed his word by the signs that 
accompanied it.</p></blockquote>
<p>I will address this 
section because it might actually be legitimate Scripture. I appreciate the 
teaching of these verses because it makes my point exactly: the Bible labels a 
man lost NOT because he hasn't been baptized, but because he hasn't believed.&nbsp;
</p>
<p>The promise that "he 
who believes and is baptized will be saved" is just as firm as, "whoever does 
not believe will be condemned." If, as Campbellites assert, this verse teaches 
that only those who believe AND are baptized are saved, we now have to come up 
with some sort of limbo or purgatory for those who are "not condemned" because 
they are not unbelievers, but have not been baptized and are therefore not 
saved. So we have an in-between category sandwiched between those who are saved 
and those who are condemned, "Not condemned but not saved." Yet, if we use the 
same logic that the proponents of Campbellism use, this is indeed where we are 
left.</p>
<p>If people were labeled 
"saved" in Mark's Gospel without baptism, then, in a sense, the question is 
answered by precedent in the Gospel itself. Is there such the case? How about 
<b>Mark 2:5</b>, where Jesus said this to the man lowered from the roof: &quot;When Jesus saw their faith, he said to the paralytic, 
'Son, your sins are forgiven.'&quot; No mention 
of baptism here. So, based on previous precedent in the Gospel of Mark itself, 
the example was set that baptism and salvation are two separate but related 
issues, and that the forgiveness of sins is possible by faith apart from 
baptism.</p>
<p><b><a name="sctn74">1 Peter 3:20-21</a></b></p>
<p>Peter's verse is the 
key to unlock the entire issue. <b>1 Peter 3:20-21</b> is making a comparison to Noah 
and his family being saved in the ark through the flood of water in the context 
of an involved passage: "...who disobeyed long ago when God waited patiently in the 
days of Noah while the ark was being built. In it only a few people, eight in 
all, were saved through water, and this water symbolizes baptism that now saves 
you also--<b>not the removal of dirt from the body</b> <b><u>but the pledge of a 
good conscience toward God.</u></b> It saves you by the resurrection of Jesus 
Christ....&quot;</p>
<p>On the one hand, Peter 
clearly states that baptism saves a person, but then he backtracks and makes it 
clear that it is <b><u>not</u></b> the ritual of baptism that actually saves a 
person, but the <i>repentant attitude</i> of the person who submits himself to 
baptism.</p>
<p><b>Here we see evidence 
for the argument that the term "baptism" is often used for the whole process of 
conversion, with baptism being the "formalization of conversion"</b> as it was 
considered in first century Judaism.</p>
<p>JUST AS SACRIFICES IN 
THE OLD TESTAMENT COULD NOT CLEANSE THE SOUL BUT MADE ONE CLEAN FOR COMMUNITY 
LIFE (<b>Hebrews 10:4</b>, "because it is impossible for the blood of bulls and 
goats to take away sins") AND PARTICIPATION 
IN THE LIFE OF ISRAEL, SO BAPTISM CANNOT CLEANSE THE CONSCIENCE OR SOUL, BUT 
WAS USED BY THE CHURCH TO DETERMINE WHO WAS RECOGNIZED AS "CLEAN" AND OPEN FOR 
FULL PARTICIPATION WITHIN THE CHURCH COMMUNITY.</p>
<p>Like <b>Psalm 51</b>,
"The sacrifices of God are a broken spirit; a broken and contrite heart, O God, 
you will not despise...." so Peter tells us 
that what happens on the inside before baptism is the real issue, but baptism is 
crucial for the believer because it "saves us" on the outside (i.e., the church 
now recognizes the believer as one of their own and we are categorized among 
those who profess to follow Christ).</p>
<p><b><a name="sctn75">Acts 8:15-17</a></b></p>
<blockquote><sup>15</sup> When they arrived, they 
prayed for them that they might receive the Holy Spirit, <sup>16</sup> because the Holy Spirit had 
not yet come upon any of them; they had simply been baptized into the name of 
the Lord Jesus. <sup>17</sup> Then Peter and John placed 
their hands on them, and they received the Holy Spirit.</blockquote>
<p>
The Book of Acts offers very little 
consistency at all regarding the indwelling of the Holy Spirit. Sometimes the 
Holy Spirit came upon people when they believed, before they were baptized (see 
<b>Acts 10:44ff</b> below), other times when they were baptized, and other times when 
the apostles laid hands upon them. <i>This is one reason I think it is foolish 
to build a paradigm upon the book of Acts, unless the paradigm is that there is 
no consistent paradigm!</i></p>
<p>Since <b>Acts</b> is a book of transition, these 
passages can be explained in great detail, but I will not dive into that here. 
But note my logic:</p>
<ol type=1><li> Only saved believers possess and are baptized by the Spirit.</p>
<p><li> If there is one instance in the book of <b>Acts</b> where people receive the 
	Holy Spirit before they are baptized, then they were saved before being 
	baptized and the matter is settled.</p>
<p><li> There is such an instance,</p></ol>
<p><b><a name="sctn76">Acts 10:44-48</a></b></p>
<blockquote>While Peter was still speaking 
these words, the Holy Spirit came on all who heard the message. The circumcised 
believers who had come with Peter were astonished that the gift of the Holy 
Spirit had been poured out even on the Gentiles. For they heard them speaking in 
tongues and praising God. Then Peter said, &quot;Can anyone keep these people from 
being baptized with water? They have received the Holy Spirit just as we have.&quot; 
So he ordered that they be baptized in the name of Jesus 
Christ. Then they asked Peter to stay with them for a few days.</blockquote>
<p>The instance of the salvation of the thief 
on the cross apart from baptism coupled with the instance of <b>Acts 10</b> makes an 
awful good case for salvation apart from baptism, at least in my book.</p>
<p>As a matter of fact, the book of Acts 
minimizes water baptism and exalts Spirit baptism. <b>Acts 1:5</b> reads, "For John 
baptized with water, but in a few days you will be baptized with the Holy 
Spirit.&quot; <b>Acts 11:16</b> &quot;Then I remembered 
what the Lord had said: 'John baptized with water, but you will be baptized with 
the Holy Spirit.'&quot; </p>
<p>It is Spirit baptism that uniquely 
distinguishes the believer from others, not water baptism. Anyone can baptize 
with water, but only the Savior can baptize with the Holy Spirit.</p>
<p><b><a name="sctn77">Acts 22:13-16</a></b></p>
<p><b>NIV Acts 9:9-19</b></p>
<blockquote><p><sup>9</sup> For three days he [Saul] 
was blind, and did not eat or drink anything. </p>
<p><sup>10</sup> In Damascus there was a 
disciple named Ananias. The Lord called to him in a vision, &quot;Ananias!&quot; &quot;Yes, 
Lord,&quot; he answered. </p>
<p><sup>11</sup> The Lord told him, &quot;Go to 
the house of Judas on Straight Street and ask for a man from Tarsus named Saul, 
for he is praying. <sup>12</sup> In a vision he has seen a 
man named Ananias come and place his hands on him to restore his sight.&quot; </p>
<p><sup>13</sup> &quot;Lord,&quot; Ananias answered, 
&quot;I have heard many reports about this man and all the harm he has done to your 
saints in Jerusalem. <sup>14</sup> And he has come here with 
authority from the chief priests to arrest all who call on your name.&quot; </p>
<p><sup>15</sup> But the Lord said to 
Ananias, &quot;Go! This man is my chosen instrument to carry my name before the 
Gentiles and their kings and before the people of Israel. <sup>16</sup> I will show him how much 
he must suffer for my name.&quot; </p>
<p><sup>17</sup> Then Ananias went to the 
house and entered it. Placing his hands on Saul, he said, &quot;Brother Saul, the 
Lord--Jesus, who appeared to you on the road as you were coming here--has sent 
me so that you may see again and be filled with the Holy Spirit.&quot; <sup>18</sup> Immediately, something 
like scales fell from Saul's eyes, and he could see again. He got up and was 
baptized, <sup>19</sup> and after taking some 
food, he regained his strength. </p>
		<p>Saul spent several days with the disciples in 
Damascus.</p></blockquote>
<p>Now for a second account at a later time:</p>
<p><b>NIV Acts 22:13-16</b></p>
<blockquote><p><sup>13</sup> He stood beside me and 
said, 'Brother Saul, receive your sight!' And at that very moment I was able to 
see him. </p>
<p><sup>14</sup> &quot;Then he said: 'The God of 
our fathers has chosen you to know his will and to see the Righteous One and to 
hear words from his mouth. <sup>15</sup> You will be his witness to 
all men of what you have seen and heard. <sup>16</sup> And now what are you 
waiting for? Get up, be baptized and wash your sins away, calling on his name.'</p></blockquote>
</p>
<p>This passage seems to imply that Saul 
washed away his sins through baptism and is a formidable argument for the 
Campbellite view.</p>
<p>Note the following:</p>
<p>Before Saul receives his sight, Ananias 
refers to him as "Brother Saul," an implication that Saul is already saved. The 
earlier account reads: 
<blockquote><sup>17</sup> Then Ananias went to the house and entered it. 
Placing his hands on Saul, he said, &quot;Brother Saul, the Lord--Jesus, who appeared 
to you on the road as you were coming here--has sent me so that you may see 
again and be filled with the Holy Spirit.&quot; <sup>18</sup> Immediately, 
something like scales fell from Saul's eyes, and he could see again. He got up 
and was baptized.</p></blockquote>
<p>This raises the question of when the Holy 
Spirit came upon him. The most natural understanding would be that this 
occurred when Ananias placed his hands on Saul and the scales fell from his 
eyes, before he was baptized. Those two inferences (Brother Saul) and receiving 
the Spirit would lead us to believe Saul was already saved.</p>
<p>Yet the second passage seems to say that it 
was not until his baptism that Saul's sins were washed away</span>, "And now 
what are you waiting for? Get up, be baptized and wash your sins away, calling 
on his name.'"</p>
<p>One solution offered by we non-Campbellites 
is to associate the washing away of sin with the phrase, "calling on his name." 
The Common Greek of the first century was filled with participles (unlike 
English, participles in Greek include our English gerunds and sometimes even 
commands).</p>
<p>We could easily understand this as, "be 
baptized, "and "wash your sins away (by) calling on his name," two separate 
acts. This is a very legitimate possibility grammatically and has its merits.</p>
<p>I prefer to believe that Saul was already 
truly "Brother Saul" before his baptism and had received the Holy Spirit when 
Ananias laid his hands upon him. Again, I prefer to think of baptism as the 
outward formalization of his conversion, much like the Lord's Supper does not 
recreate Calvary but pictures it. Baptism washes away our sins in the same way 
that the bread is Christ's body and the cup His blood; it pictures what has 
already transpired. Paul's baptism was the confession of his faith and outwardly 
identified him with both the Lord Jesus Christ and the Church. He was taking a 
stand for Jesus. Paul was converted on the Road to Damascus, but his salvation 
was formalized (certified) through baptism.</p>
<p><b><a name="sctn78">Romans 6:3-4</a></b></p>
<blockquote>Or don't you know that all of us who were baptized into Christ Jesus 
	were baptized into his death? We were therefore buried with him through 
	baptism into death in order that, just as Christ was raised from the dead 
	through the glory of the Father, we too may live a new life.</blockquote>
<p>This passage is understood by some to refer 
to Spirit baptism. I do not think that is the most natural understanding, and I 
would add that many people miss the point Paul is making completely.</p>
<p>Because salvation does not depend upon our 
works, the question is logically raised, "Shall we sin that grace may abound?" 
If God is glorified by saving us graciously, then why not sin like crazy so God 
can forgive us more and He receive more glory? The question makes sense.</p>
<p>Paul's answer is not that we can lose our 
salvation (which is what an Arminian would answer), but that we were buried with 
Christ in baptism.</p>
<p>When we go into the water, we are buried 
with Him. When we come up, we are resurrected with Him. We are, therefore, 
committed to living a resurrection life.</p>
<p>Paul's point is this: we know it is not 
God's will for us to live in sin because God makes it clear through the ritual 
of baptism. What is missed by most is the idea that our baptism is a constant 
object lesson that our old life is to be buried and we are to live as a new 
creation.</p>
<p>That baptism symbolizes these things 
becomes quite clear because the whole passage is not to be taken literally. We 
are not LITERALLY buried with Christ, nor did we LITERALLY come back to life 
with Christ. We may be legally (in God's eyes) buried and resurrected with 
Christ, but literally it did not happen this way; we were not even born.</p>
<p>Still, it could be argued that this legal 
or positional death and resurrection with Christ, though non-literal, is 
affected by literal water baptism. <u><b>I would argue that baptism is for our 
benefit and for the church's, not for our relationship to God.</b></u> We are 
buried, visibly, through baptism. We are resurrected, visibly, through baptism. 
When we are tempted to sin that grace may abound, we look at our baptism. <i>It 
is a forgotten tool in our war against sin</i>. We saw earlier in 
<b>Ephesians 2</b> 
that God regenerates us before we believe and so that believe. The Spirit, like 
the wind, blows where He wills and causes the elect to be born again 
(<b>John 3</b>).
</p>
<p>Like Old Testament imagery and rituals, 
there is a distinction between substance and image. Human beings are image 
oriented, and we need something tangible to hang on to. The New Birth is 
invisible, so is the baptism by the Spirit; but water baptism is visible and can 
be etched on the mind.</p>
<li><p class="bighpc"><a name="sctn8">Can People Who Believe That &quot;One Has To Be 
Baptized in Order To Be Saved" Be Themselves Regenerate, Since They Are Trusting 
in a Work (Christ Plus Something Else)?</a></p>
<p>Is this like the 
Galatian Judaizers who had "fallen from grace" because they added circumcision 
to the Gospel?</p>
<p>This is a tough 
question indeed. At times like this, I defer to the doctrine of election. How 
clear of a Gospel is needed for a person to be saved? I think the elect can 
accept Christ and still be somewhat confused or, better yet, inconsistent. I 
have often wondered how saved people can remain within Catholicism, for example, 
with its bowing down to images and offering prayers (and other acts of worship) 
to Mary. These things are so overtly wrong in Scripture, yet I am convinced 
that there are many believers within those ranks as well.</p>
<p>I think there are 
people who somehow trust in Christ and not their works to make them right with 
God in both of these groupings, sometimes despite what they are taught. There 
is often a difference between what people agree to and what they really believe, 
deep down (what I call the funeral home test).</p>
<li><p class="bighpc"><a name="sctn9">A Correct Presentation of Salvation</a></p>
<p>I have demonstrated the 
wealth of verses declaring faith (belief) or faith and repentance as the grounds 
for salvation and contrasted them to the small number of verses used to support 
the idea of faith plus baptism as the grounds for salvation. </p>
<p>Is God intentionally 
misleading people with the very Gospel emphasizing personal salvation, John's 
Gospel? Yet that Gospel states time and time again that it is faith that saves 
us.</p>
<p>Here is a copy of my 
explanation as to salvation (right off our church's web page):</p>

<p id="pgt">How To Be Saved</p>

<p>To be saved means &quot;to be delivered or rescued.&quot; In this context, we
are talking about being delivered from sin and its penalty. Simply put, to be
saved means to be right with God, the consequence of which is eternal life in
heaven.</p>
<p>We are born separated from God and lost in our sins. Shortly before Jesus was
born, an angel appeared to Joseph and said, &quot;...you shall call his name
Jesus, for it is he who will save his people from their sins...&quot; (<b>Matthew
1:21</b>).</p>
<p>To be saved means to be transferred from God's disfavor to God's favor.&nbsp;
It
means becoming part of His Kingdom. It means a new, God-centered life.&nbsp;
It means
escaping the fear of hell and obtaining the assurance of heaven.</p>
<p align="center"><b>How Can We Be Saved?</b></p>
<p><b>In order to be saved, we must:</b></p>
<ol>
  <b><li>Want to be saved. This is called repentance. To repent means to change
one's mind. If you want to be delivered (saved) from a sinful, self-centered
life and you want God to work in your life, you are well on your way.</b>
<p>&quot;Repent therefore and return, that your sins may be wiped away...&quot;
(<b>Acts 3:19</b>)
