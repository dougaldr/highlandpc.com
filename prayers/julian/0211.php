<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 11
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great God,</p>
<p>Thank You, Father, that You are a Teacher. As Your Word 
states, You, &quot;instruct us in the way we should go.&quot; You counsel us, You lead 
us, and, by Your Spirit, You penetrate our hearts and minds. You are constantly 
developing us, teaching us, and leading us. You are the ultimate Teacher. It 
amazes me that You even want to tutor me. Help me to be a wiling and motivated 
student!</p>
<p>I pray for my lost friends and family members, including: __________. Father, 
please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Lord, thank You that you have given us all things richly to 
enjoy. Although the comforts of this world can distract us, there is no virtue 
in denying ourselves the wholesome pleasures of life. Treating ourselves 
harshly does nothing to make us more godly 
(<a href="javascript:scripture('../../scripture/col223.htm')" target=_self class=pop>Colossians 2:23</a>). 
Yet, Lord, You do want me to have self-control, a part of 
the fruit of Your Spirit. So help me to learn to be moderate in all things.</p>
<p>Almighty One, help me to feel well today. Check pain and 
illness, if this is Your will. Although sometimes You use bodily weaknesses to 
help us depend upon You, or You allow us to experience them as those who share 
in the consequences of the curse (for our bodies are not yet 
redeemed—<a href="javascript:scripture('../../scripture/rom82324.htm')" target=_self class=pop>Romans 8:23-24</a>),
yet it is also true that sometimes we have not because we ask not. So I am asking.</p>
<p>Today I pray for the nation of Bulgaria in Eastern 
Europe. Only about 2 percent of the population claims to be born-again, and 
the Orthodox Church has campaigned against evangelicals, accusing us of being 
cannibals and tied to the criminal element. Please stop these slanders and 
multiply the evangelical church tenfold over the next decade. Help the leaders 
to mature, and provide for them.</p>
<p>Today we pray for InterAct Ministries as they reach people 
around the top of the North Pacific Ocean including Alaska, parts of Canada and 
parts of Russia. Many of these are indigenous people who have not been 
evangelized. Bless their staff, missionaries, and ministries. Help them work 
together well, to be personally strong in You, and to be blessed by Your Spirit 
with much fruit for their labors.</p>
<p>Today I pray for 
those who work to make traffic control efficient and safe. </p>
<p>Help me bring the 
joy of Jesus to others today, I pray, in Jesus' Name, Amen.</p>

