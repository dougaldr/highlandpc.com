<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Why No Mention of Dan in Revelation 7?
<br><span class=byln>Revised February 2021</span>
<br><span class=byln>by Ed Vasicek</span></p>

<div>
<p>The list of the Twelve Tribes of Israel in <b>Revelation 7</b>
omits the tribe of Dan and the half tribe of Ephraim but includes the &quot;tribe of 
Joseph&quot; and the half tribe of Manasseh. What are the implications of this 
variation of the tribal listings?</p>

<p><b>Two Distinct People Groups in Revelation 7</b></p>
<p>I am convinced that tedious listings serve an obvious 
meaning: to clarify that a passage is to be taken literally (and thus within the 
category of genealogies and numberings found in the Torah and elsewhere). As 
another case in point, Ezekiel's Temple measurements are horrendously detailed; 
therefore, the most reasonable interpretation is that the Temple will actually 
be rebuilt during the Kingdom Age. Who else would want to tackle the meaning of 
the text if it were not literal? Who would have time or energy to decode it if 
it were allegorical? Tedious detail precludes the &quot;easy out&quot; of allegorical or 
spiritual interpretation.</p>

<p>The context also evidences the concept that the 144,000 
Israelites are to be taken in literal fashion. In our text, 
(<a href="javascript:scripture('../scripture/rev719.htm')" target=_self class=pop>7:1-9</a>)
we see TWO people groups: 144,000 tediously defined as Israelites and an uncountable 
multitude representing all of the redeemed (vs. 9); these groups are distinct, 
though the former are a subset of the latter massive group. So we have a modest 
number of Israelites who are sealed and a second, immense group of the redeemed. 
To make them the same is to make the logical error of the undistributed middle. 
(All women are people therefore all people are women).</p>

<p><b>Ephraim Included Under the Banner of Joseph</b></p>
<p>But why is &quot;Joseph&quot; added? In a sense, the tribe of Joseph 
was only a tribe in theory. As you might recall, Joseph's two sons were given 
separate tribal status (Genesis 48:1-7) because Joseph was specially honored 
with a double portion. Even though he was not born first (chronologically), he 
was, by Jacob's decree, given the rights of the firstborn socially (hence 
receiving the double portion given to the firstborn son). Jacob himself had been 
in a similar position: even though his twin Esau was born first, Jacob received 
the honors of the firstborn.</p>

<p>So we can assume that one of Joseph's sons, Ephraim, who is 
also Jacob's grandson, is included under the tribe of &quot;Joseph,&quot; while Joseph's 
other son, Manasseh, is listed separately as his own tribe. This idea is treated 
similarly in <b>Ezekiel 48</b>. Early in the chapter, we have the &quot;typical&quot; list of the 
12 Tribes (excluding Levi, whose land is not distributed in a block fashion, but 
including Dan and Ephraim). But in <b>Ezekiel 48:30-34</b>, where we have the four city 
exits, we see that Ephraim is omitted while Joseph is included:</p>

<p><i>These will be the exits of the city: Beginning on the 
north side, which is 4,500 cubits long, the gates of the city will be named 
after the tribes of Israel. The three gates on the north side will be the gate 
of Reuben, the gate of Judah and the gate of Levi.</i></p>

<p><i>On the east side, which is 4,500 cubits long, will be 
three gates: the gate of Joseph, the gate of Benjamin and the gate of Dan.</i></p>

<p><i>On the south side, which measures 4,500 cubits, will be 
three gates: the gate of Simeon, the gate of Issachar and the gate of Zebulun.</i></p>

<p><i>On the west side, which is 4,500 cubits long, will be 
three gates: the gate of Gad, the gate of Asher and the gate of Naphtali.</i></p>

<p>In this text, we can postulate that the Tribe of Ephraim, 
being the dominant tribe of the Northern Kingdom of Israel, is included under 
the banner of the &quot;Tribe of Joseph.&quot; But it is my opinion that the term &quot;Joseph&quot; 
is used to include MORE than merely Ephraim. I will elaborate on this further 
below.</p>

<p><b>Why was Dan omitted?</b></p>
<p><b>The Fluidity of the Tribes in the Old Testament</b></p>
<p>In the above passage (<b>Ezekiel 48</b>), we observe two distinct 
tribal listings in the same chapter. Do we see any other tribal variations in 
the Old Testament? Yes!</p>
<p>In <b>Deuteronomy 33</b>, the tribe of Simeon is left out of 
Moses' blessing. If we can discover why Simeon was left out there, then that 
could provide us with the clue to <b>Revelation 7</b>.</p>
<p>A reasonable answer for Simeon's omission is that the land 
of Simeon was an island surrounded by the territory of Judah. Note this point: 
few conservatives challenge that the tribes Moses mentions in <b>Deuteronomy 33</b> are 
literal (simply because one is left out). Between the passage mentioned above in
<b>Ezekiel</b> and the list of <b>Deuteronomy 33</b>, it is reasonable to conclude that the 
Jews viewed the 12 Tribes as a fluid expression of their national composition. 
Whereas the 12 sons of Jacob are absolute and unchanging in nature, the 12 
tribes have been fluid since the beginning (when Jacob, by blessing Joseph's two 
sons, created 13 tribes).</p>
<p>Perhaps I am cynical, but it seems to me that the reason 
some theologians point to the omission of Dan in <b>Revelation</b> is because they have 
an agenda to allegorize the tribes. Since there is no such agenda for 
<b>Deuteronomy 33</b>, some of the same scholars who defend the literal integrity of 
<b>Deuteronomy</b> advocate viewing the <b>Revelation 7</b> list as an allegorical listing.</p>

<p><b>More Clues</b></p>
<p>Also worth noting in our quest is the fact that the tribe 
of Benjamin is frequently included with Judah throughout the Old Testament. 
These clues advance our &quot;fluidity&quot; theory.</p>
<p><b>1 Kings 11:29-32</b> reads:</p>
<p><i>About that time Jeroboam was going out of Jerusalem, and 
Ahijah the prophet of Shiloh met him on the way, wearing a new cloak. The two of 
them were alone out in the country, and Ahijah took hold of the new cloak he was 
wearing and tore it into twelve pieces. Then he said to Jeroboam, &quot;Take ten 
pieces for yourself, for this is what the LORD, the God of Israel, says: See, I 
am going to tear the kingdom out of Solomon's hand and give you ten tribes. But 
for the sake of my servant David and the city of Jerusalem, which I have chosen 
out of all the tribes of Israel, he will have one tribe.</i></p>
<p>Note here that the ten strips plus the one strip of fabric 
equals only eleven strips. Yet we know that the &quot;one strip&quot; really represented 
two tribes, Judah and Benjamin.</p>
<p>Conventional interpretation suggests that Benjamin was so 
small at this time that it was not considered its own tribe. If so, we need to 
add Levi to get 12 tribes, and, of course, Levi's land allotments were scattered 
through all Israel. Although many faithful Jews later migrated to Judah from the 
Northern Kingdom, Levi, at least initially, probably kept its land scattered 
within both the Northern Kingdom (Israel) and the Southern Kingdom (Judah).</p>
<p>If Benjamin were included, we would have what we normally 
picture: 13 tribes (11 full tribes and two &quot;half&quot; tribes). If Benjamin indeed is 
not counted here as its own tribe, why is it unthinkable that Dan should suffer 
the same fate in <b>Revelation</b>?</p>
<p>In light of all this evidence, it seems logical to conclude 
the following: The Twelve Tribes of Israel were not as firmly set as many 
believe. The enumeration of the twelve tribes is a floating enumeration.&#8203;</p>

<p><b>New Testament Counterpart</b></p>
<p>We see something related in Paul's account of a 
post-resurrection appearance of Christ. In <b>1 Corinthians 15:5</b> we read that 
Jesus, &quot;appeared to the Twelve.&quot; The problem is that twelve apostles were not 
present. In His first appearance, He only appeared to ten. Even when Thomas was 
present a week later, He technically appeared to eleven apostles. (Judas 
Iscariot was dead). In that case, the term, &quot;The Twelve&quot; is not so much a 
numerical term as a figure for the apostolic group. The same seems to be true 
with the twelve tribes of Israel. In this case, we must ask, &quot;which twelve?</p>
<p>Incidentally, was there only to be twelve apostles? People 
debate whether Mathias was actually meant to be an apostle, or was that spot 
reserved for Paul?&nbsp; The idea of a baker’s dozen (13) seems to originate in 
Scripture!</p>

<p><b>Bad Vibrations</b></p>
<p>The Targum on Pseudo-Jonathan adds an interesting insight 
as to the views held by later Jews (perhaps in the eighth century) about the 
tribe of Dan. This Targum (a paraphrase, interpretation, and expansion of the 
Torah into Aramaic), in <b>Exodus 17:8</b>, reads:&#8203;</p>
<p><i>And Amalek came from the land of the south and leaped on 
that night a thousand and six hundred miles; and on account of the disagreement 
which had been between Esau and Jakob, he came and waged war with Israel in 
Rephidim, and took and killed (some of the) men of the house of Dan; for the 
cloud did not embrace them, because of the strange worship that was among them.</i></p>
<p>Contrasting this to the original text, <b>Exodus 17:8</b>, which 
simply reads: &quot;The Amalekites came and attacked the Israelites at Rephidim&quot; 
gives the reader an impression that the author of the Targum is venting by means 
of a diatribe against Dan!</p>
<p>For some reason unknown to me, the Jewish people had a 
negative attitude toward the tribe of Dan, at least at one point in time. I am 
not saying that this negative attitude was prevalent as far back as the Second 
Temple period, but it may have been. This may or may not have bearing on the 
<b>Revelation</b> passage.</p>

<p><b>The Best Solution: The &quot;Tribe of Joseph&quot; is a Regional, Not Primarily 
Genetic, Term!</b></p>
<p>I will eventually bring us back to this point, namely that 
&quot;Joseph&quot; is a regional term (and provides room for expansion), but I want to 
&quot;get us there&quot; first.</p>

<p><b>The Genetic Confusion Between Tribes</b></p>
<p>Although Ephraim and Manasseh were brothers and could 
therefore logically be called &quot;the tribe of Joseph,&quot; remember that there really 
was no such Old Testament tribe as &quot;Joseph,&quot; at least not in a practical sense. 
Instead, Joseph's line was represented by the phrase &quot;the half tribes of Ephraim 
and Manasseh.&quot;</p>
<p>We need to take into account that though many of the 
northern tribes were &quot;lost&quot; after the Assyrian Conquest (722 B.C.), the godly of 
the land had previously fled south to Judah 
(<a href="javascript:scripture('../scripture/2chr159.htm')" target=_self class=pop>2 Chronicles 15:9</a>,
<a href="javascript:scripture('../scripture/2chr349.htm')" target=_self class=pop>34:9</a>).
But that 
does not mean all tribal heritages were lost.</p>
<p>In New Testament times, for example, we are introduced to 
Anna, who was of the tribe of Asher 
(<a href="javascript:scripture('../scripture/luke23638.htm')" target=_self class=pop>Luke 2:36, 38</a>).
In addition to the tribes 
of Levi, Judah and Benjamin (of which Paul was a member), most or all of the 
other tribes were probably represented within the Jewish community, although 
probably in small numbers.</p>

<p><b>Possibility One: Dan and Ephraim Mixed Into the Tribe of Joseph</b></p>
<p>One solution seems possible: both Ephraim and Dan, and 
perhaps others, are included in the title, &quot;tribe of Joseph.&quot;</p>
<p>So here is one possible scenario: After the conquest of the 
Northern Kingdom of Israel in 722 B.C., the godly Israelites from the tribes of 
Ephraim basically absorbed the remnant from the tribe of Dan, and the people of 
Dan lost their separate identity perhaps because so few of them were faithful to 
the Lord and hence not mentioned in the migration of 
<a href="javascript:scripture('../scripture/2chr159.htm')" target=_self class=pop>2 Chronicles 15:9</a>. 
This combination of Dan, who descended from Rachael's handmaid, Bilhah, and Joseph 
who descended from Rachael, are so intertwined that they became known simply as 
the &quot;Tribe of Joseph&quot; because they really were such a mixture that perhaps their 
genealogies could no longer be sorted out, as could be done with the other 
tribes. Since Ephraim bordered the smaller region given to Dan, the groups 
absorbed each another. It could be that this conglomeration could have happened 
even before 722 B.C.; if so, the Ephraimites of 
<a href="javascript:scripture('../scripture/2chr159.htm')" target=_self class=pop>2 Chronicles 15:9</a>.
might be an Ephraim-Dan mix.</p>
<p>Since Ephraim and Dan had lost track of their genealogies 
by the first century A.D., they could not confidently call themselves 
Ephraimites or Danites, so they chose a term to explain this genetic 
uncertainty, the tribe of Joseph. This would also make it clear that their 
Ephraimitic line was the dominant strain and that they were all connected 
somewhere to Joseph but were not clearly legal heirs to the title &quot;tribe of 
Ephraim.&quot;</p>

<p><b>Possibility Two: Dan &quot;Reduced&quot;</b></p>
<p>Another possibility is that all those from the tribe of Dan 
are reduced in number in comparison to the other tribes. This might mean that 
there are still many descendants of Dan, but not enough to muster 12,000 virgin 
men (implied by <b>Revelation 14:4</b>) during the Tribulation period.&#8203;</p>
<p>If the invasion of <b>2 Chronicles 16:4</b> depleted Dan, then the 
invasion of the Assyrians (<b>2 Kings 15:29</b>) may have depleted them further to the 
point that they virtually disappeared. If so, the term &quot;Joseph&quot; may refer to 
miscellaneous, minority, or non-categorized Israelites.</p>

<p><b>Possibility Three: Dan's Descendants Are Too Mixed.</b></p>
<p>In <b>Zechariah 10:6</b>, we read: &quot;I will strengthen the house of 
Judah and save the house of Joseph. I will restore them because I have 
compassion on them. They will be as though I had not rejected them, for I am the 
LORD their God and I will answer them.&quot;</p>
<p>In these and other verses, the entire block of northern 
tribes is sometimes called &quot;Joseph.&quot; So perhaps the tribe of Joseph refers to 
descendants of Israel who are so mixed that they cannot be categorized into any 
other tribe, with the tribe of Dan being completely mixed and beyond sorting.</p>

<p><b>Possibility Four: The Samaritans and Gentile Converts are Included in the Tribe of Joseph.</b></p>
<p>Yet another intriguing possibility that provides an even 
simpler solution is this: the tribe of Joseph refers to the Samaritans! When the 
Assyrians conquered the Northern Kingdom, (aka, &quot;Samaria,&quot; &quot;Ephraim,&quot; or 
&quot;Joseph,&quot;) they left some of the peoples behind while deporting others and 
dispersing them throughout their kingdom. Perhaps the majority of those left 
behind were from Dan and Ephraim. The Assyrians then imported a variety of 
gentile peoples into the land, and these people intermarried, creating the 
Samaritan race, a mixed race the other Jews despised. These Samaritans could 
have been the troublemakers mentioned in the Book of <b>Nehemiah</b>.</p>
<p>Modern Jews (who have often done some intermarrying 
themselves) recognize the Samaritans as fully Jewish. They have never left the 
land and still sacrifice Passover lambs on Mt. Gerizim! The Samaritan Jews today 
are more observant than modern Orthodox Jews. So perhaps the Samaritans are 
considered the tribe of Joseph because the term Joseph is used for the Northern 
Kingdom.</p>
<p>While we are at it, we might even suggest that the term 
Joseph includes gentiles who have converted to Judaism. We know, for example, 
that when the Hebrews left Egypt, some gentile peoples (called a &quot;mixed 
multitude&quot; in <b>Exodus 12:38</b>) were amalgamated into the nation.</p>

<p><b>Possibility Five: The Tribe of Joseph is a Territorial Matter More than a Genetic One.</b></p>
<p>This could be the best single solution, although it might 
work in tandem with some others as well.&#8203;</p>
<p>To argue this case, we must ponder another obscure but 
relevant passage from Ezekiel 47:21-23.&nbsp; It reads:</p>
<p><i>You are to distribute this land among yourselves 
according to the tribes of Israel. You are to allot it as an inheritance for 
yourselves and for the aliens who have settled among you and who have children. 
You are to consider them as native-born Israelites; along with you they are to 
be allotted an inheritance among the tribes of Israel. In whatever tribe the 
alien settles, there you are to give him his inheritance,&quot; declares the 
Sovereign LORD.</i></p>
<p>This passage alone shows us a case of divine precedent: 
tribal identity is not merely genetic. Thus, the concept of &quot;The Tribe of 
Joseph&quot; leaves room for all kinds of converts to Judaism, mixed Jews, and 
Samaritans, especially if they live within the territory of Joseph.&nbsp; And perhaps 
the small population of the tribe of Dan relocated into the territory of Ephraim 
and were allocated land there. Thus, the &quot;tribe of Joseph&quot; would recognize both 
Ephraim's and Dan's contribution to the population, sort of like the temporary 
union of the Czechs and Slovaks to form the former Czechoslovakia.</p>
<p>This solution includes elements of the above solutions as 
well.</p>
<p>One related possibility hinges upon how we interpret the 
&quot;house of Joseph&quot; in the passage below.</p>
<p>Notice this obscure portion from Judges 1:34-35, &quot;The 
Amorites pressed the people of Dan back into the hill country, for they did not 
allow them to come down to the plain. The Amorites persisted in dwelling in 
Mount Heres, in Aijalon, and in Shaalbim, <b>but the hand of the house of Joseph 
rested heavily on them</b>, and they became subject to forced labor.&quot;</p>
<p>Some commentators take Joseph to refer to Ephraim, or 
Ephraim and Manasseh together.&nbsp; But might it be a regional term referring to 
Ephraim and Dan together, for example?&nbsp; It seems difficult to believe that 
Ephraim or Ephraim and Manasseh did all the work while Dan sat idle; they likely 
participated with them. Might this regional alliance, which includes Dan and 
perhaps Eprhaim, be what the tribe of Joseph means here? </p>
<p>This passage alone could explain why neither Dan nor 
Ephraim are mentioned in Revelation 7, but called &quot;Joseph&quot; as mentioned here. 
Ponder this one.</p>

<p><b>How Hard to Find 144,000 Jews?</b></p>
<p>Although there may not be enough virgin men to supply the 
12,000 required for the Tribe of Dan, the world is filled with people who have 
genetic connections to Israel. Perhaps most people of European, Middle-Eastern, 
or North African descent are genetically connected to Israel somewhere--very 
likely to the many children of Solomon who married royalty throughout the region 
(and then the grandchildren who likewise married royalty moving the genetic 
connection in all directions, including westward). Since the progeny of royal 
families tended to be large and healthy, many of us have a touch of royal blood 
somewhere, and thus probably a Jewish connection.</p>
<p>Note that God not only promises to make a great nation from 
Jacob in <b>Genesis 35:11</b>, but also suggests that Jacob will father God's special 
nation, Israel, but also that a &quot;community of nations&quot; will arise from Jacob.</p>
<p>The text reads, &quot;And God said to him, 'I am God Almighty ; 
be fruitful and increase in number. A nation and a community of nations will 
come from you, and kings will come from your body.'&quot;</p>
<p>This could refer to the each of the tribes as a &quot;nation,&quot; 
and thus Israel is a &quot;community of nations,&quot; or it could refer to the idea that 
Jacob's genes would be spread throughout the world, which has very likely 
happened (as mentioned above, many of Solomon's children would have married 
foreign royalty, thus moving Jacob's genes in all geographical directions; the 
rulers of the Northern Kingdom of Israel would also marry off their children 
(especially born to concubines and lesser wives) to foreign royalty, thus 
propagating the lines of the other tribes).</p>
<p>&quot;Even without a documented connection to a notable 
forebear, experts say the odds are virtually 100 percent that every person on 
Earth is descended from one royal personage or another.&quot; (Quoted from 
Genealogist: Almost Everyone on Earth Descended From Royalty, an Associated 
Press article dated July 5, 2006.)</p>
<p>Although I disagree with the various theories of &quot;British 
Israelism,&quot; the actual truth is that many people in the world have some genetic 
link to not only Abraham--but also to Jacob.</p>
<p>Although modern Jews determine whether one is born a Jew or 
not by whether one's mother was a Jewess, the ancient Biblical criteria seems 
oriented toward the heritage of the father. What criteria God will use to 
determine who belongs to which tribe and who is so far genetically removed as to 
be disqualified?&nbsp; Only He knows!</p>

<p><b>CONCLUSION</b></p>
<p>Throughout the Bible, the Twelve Tribes are listed in a 
fluid manner.&nbsp; And twelve does not always mean twelve.&nbsp; With two &quot;half tribes,&quot; 
the math works.&nbsp; But, when it comes to land divisions and other concerns, we see 
13 separate tribes and sometimes the incorporation of two tribes together as 
one.&nbsp; The best solution is to understand the Tribe of Joseph to include Ephraim 
and Dan at bare minimum, but also to perhaps include people groups who lived in 
that region.&nbsp; There are many possibilities as to why Dan and Ephraim are not 
listed in Revelation 7, but their absence is not more mysterious than tribal 
absences in several Old Testament listings.&nbsp; The ethnic nation of Israel is 
indicated by the twelve tribes, whatever their consistency or inconsistency as 
listed.</p>

