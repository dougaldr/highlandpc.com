<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 11
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>When my Savior was born, there was a decree sent 
forth from Caesar Augustus, requiring everyone to register in the city of their 
forefathers. Since Joseph and Mary, who was engaged to him, were both 
descendents of David, they went to Bethlehem to register, the town from which 
David hailed.</p>
<p>Lord, when Caesar issued that decree, he had no idea that 
he was fulfilling your design. So You move in the hearts of not only those who 
know You, but You also influence those who reject You to accomplish Your 
purposes. </p>
<p>For Your Word clearly stated that the Messiah had to be 
born in Bethlehem centuries before the birth of Jesus. In <b>Micah 5:2</b>, we read, 
&quot;But you, Bethlehem Ephrathah, though you are small among the clans of Judah, 
out of you will come for me one who will be ruler over Israel, whose origins are 
from of old, from ancient times.&quot;</p>
<p>So the Lord Jesus was prophesied to be born in Bethlehem 
and is said to be preexistent. Jesus, Immanuel, is &quot;God with us.&quot;</p>
<p>Father, I open myself to You. Help me be honest with You, 
others and myself. There are qualities about me that are not all positive. 
Sometimes I doubt, feel bitter, get depressed, arrogant, lazy, controlling 
perhaps even addicted to my own adrenaline. The room for improvement is a big 
room. Thank You, Lord, that You are not finished with me yet, but You are 
developing me. Thank You that imperfect and flawed people surround me. I am not 
alone. And thank You that, when I see Jesus, I will become perfect. I will be 
what I sense I should and could be.</p>
<p>Remind me that it is not good for me to focus on myself too 
much. I need moderation in all things even introspection. Also remind me that 
the best way to find my life is to lose it in pursuit of You and in service to 
others.</p>
<p>I pray for my lost friends and family members including: __________. Remind 
me that unbelievers are not the enemy; they are the victims of the enemy.</p>
<p>Father, please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Today I remember the central American nation of Honduras. Thank You for the 
great harvest of souls in this nation! Almost 18% of the population is 
born-again evangelical Christians. This is a poor nation with lots of street 
children and social problems. Help the church to make a difference in the nation 
and to draw even more people to You as a result. Provide opportunities for solid 
leadership training and empower Your children in that land.</p>
<p>I pray for the U.S. Center for World Mission, a ministry burdened for the 
&quot;10,000 people groups [who] do not yet have a community of disciples among 
them....The USCWM produces and publishes resources to motivate and equip 
Christ's body...by gathering, applying and publishing insights....The U.S. 
Center for World Mission advances shared solutions to common hindrances in the 
mission effort.&quot;</p>
<p>I intercede for urban planners. May they serve their communities well. May 
they be noted for their integrity, far-sightedness and realistic approaches. May 
those who know You live out their faith and share Christ and may many in this 
field come to know You.</p>
<p>Bless my attempts to be responsible today, I pray in the Messiah’s Name, Amen.</p>


