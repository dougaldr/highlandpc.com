<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 9
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great Father,</p>
<p>You are El Shaddai, God Almighty. You are not merely a 
strong God, You are infinitely strong. The billions of stars in billions of 
galaxies were merely spoken into being. Nothing is too difficult to You. You 
have packed more energy in a single atom than we expend in our entire lifetime. 
Unlike the false gods of this world, You can deliver on Your promises.</p>
<p>Since You are all powerful, I can bring my request before 
You and submit them to Your will, confident that my requests which are 
consistent with Your plans will be addressed. They include: __________.</p>
<p>Lord, I thank You for the angels You have created. They 
are ministering spirits, ministering to us, the heirs of salvation. Please send 
guardian angels to watch over my loved ones and me.</p>
<p>Dear God, help me to have a heart and mind that want to 
learn. Help me participate in church not just to get a spiritual lift, but also 
to serve and to learn and grow.</p>
<p>I am a needy person. I ask that You would give me peace of 
mind as I struggle through life. Help me offer my fears and worries to You and 
leave them before Your throne.</p>
<p>Today, Lord, I pray for the nation of the British Virgin 
Islands. Although over a fourth of the population claim to know you, and 86% 
profess some sort of Christianity, yet 75% of the births are out of wedlock. We 
pray that the profession of believers would better match their walk, and that 
you would help them to grow in spiritual maturity and stay pure in a tourist 
area famous for worldly pleasures.</p>
<p>Today I ask Your blessing upon Greater Europe Mission as 
they send out college students, short-term and long-term missionaries in an 
attempt to reclaim Europe for Christ. We pray that these individuals would have 
a positive influence upon the Kingdom of God, encouraging local Christians, 
witnessing and discipling others. May Your Spirit prepare and use Greater 
Europe Mission, and may You revive Your church in Europe, partly through their 
efforts.</p>
<p>Today I ask Your blessing upon those who assure the quality 
of our water or provide water service. We thank You for blessing us with this 
necessity. </p>
<p>Now, Mighty One, help me find my strength in You today, I 
pray, in Jesus' Name, Amen.</p>

