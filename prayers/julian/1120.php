<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 20
<br><span class=byln>by Ed Vasicek</p>

<p>Mighty Savior,</p>
<p>You are the head of the church, Your body. Church leaders answer to You. 
Although some people think that having a perfect leader would make for a perfect 
organization, such is not the case. You are the perfect leader, but Your 
children do not always follow Your directives. Although most of us want to 
follow You most of the time, Lord, we fail so often. You are the head in the 
sense that You are the leader of the organization. You are also the head when we 
view the church through the illustration of a body. Just as a human head 
containing the brain directs the actions of the Body, so You want to direct the 
actions of the church at large and each congregation in particular. Help us 
never get away from the concept of being held accountable to Your authority by 
being accountable to Your Word.</p>
<p>Lord, I pray for our government leaders: our President and his cabinet and 
staff, the Senate, Congress, Judicial System, our Governor, state legislators 
and state officials. I pray for county and municipal leaders and officials. 
Father, fill these offices with those who will accomplish Your purposes. Help us 
to be able to practice and share our faith freely and peacefully. </p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I find my 
strength in His power not my own. May Your Spirit control me today.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 
John 1:9</b>). I accept responsibility for them and freely bring the sins Your 
Spirit brings to my mind now: __________.</p>
<p>Lord, thank You for the Mission to Unreached Peoples as they &quot;focus on the 
unreached peoples of the world who have not heard the gospel of Jesus Christ, 
specifically those who live in the 10/40 Window (a region of the world 
containing parts of Asia and Africa with the fewest believers).&quot; Thank You for 
their &quot;225 missionaries in 22 countries.&quot; Bless them as they evangelize, 
disciple and invest their lives in fulfilling the Great Commission. Prosper them 
by providing for their needs, protecting them and giving them fruit for their 
labor. Give the missionaries harmony among themselves and empower them by Your 
Holy Spirit.</p>
<p>I remember those who work in our factories. Lord, keep them safe. Help them 
to be honest, to do their best and help the rest of us to appreciate their 
labors. May those who know You live out the Christian life before their fellow 
workers; help them to freely share Christ with their associates.</p>
<p>Father, I pray for those who work as medical assistants, medical records and 
health information technicians, medical and clinical laboratory technicians and 
medical secretaries. O Lord, help these workers to do a good job, to be free 
from significant mistakes and to minister mercy to others. May those who know 
You share their faith freely by word and deed, I ask.</p>
<p>Help me to let the light of Jesus Christ shine in my life 
today, O Lord. I pray these things in Jesusï¿½ Name, Amen.</p>

