<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Personal Devotions</span>
<br>In Disappointment
<br><span class=byln>by Ed Vasicek</span></p>

<p>Lord, this life has no shortage of letdowns. Each letdown brings back the
heartache of previous ones. It is an all-too-familiar feeling. But then again,
Lord, you know all about it. You know the sting of disappointment not only
because you are the sovereign God who knows all, but also through your Son who
took upon himself mortal flesh.</p>
<p>Our Savior understood what it was like to be despised and rejected of men, a
man of sorrows and acquainted with grief. He knew the feeling of abandonment:
those he had appointed as apostles fled at his arrest. He knew the bitter pain
of betrayal as he told Judas, &quot;What you do, do quickly.&quot; He came to
his own, but those who were his own did not receive him. Even his own family
rejected him and thought him insane
(<a href="javascript:scripture('../scripture/mark321.htm')" target=_self class=pop>Mark 3:21</a>).&nbsp;
He healed ten lepers, but only
one returned to give thanks.<br>
<br>
Abba, Father, disappointment is an integral part of life. We seem to have a
distant memory of Eden, when things were right and life was fulfilling. We have
eternity in our hearts, so we can imagine what heaven will not be: tears,
heartache, betrayal, letdown, conflict, misunderstanding, the frustration of
failure or being less than best. Agreements will not fall through up there, nor
will dreams be dashed. Others will no longer disappoint us, nor will we
disappoint others.</p>
<p>But, Sovereign Lord, Eden is past and heaven is future. We need your comfort
now. We need your shoulder to cry on, your ear to hear our cries, your
compassion to assure us of your love. You are the God who ministers to the
letdown, the broken-hearted, the torn-up inside. Our brokenness does not take
you by surprise. You have held the hand of millions who have gone through
similar or worse situations. It wasn't easy for them, and it is not easy for me.&nbsp;
Get me through the desert of disappointment. Send an oasis time, and then nudge
me forward until I reach the end of the desert and can once again drink at the
joyful spring. I pray in the matchless, compassionate name of Jesus, Amen.</p>

