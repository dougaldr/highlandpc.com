<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 12
<br><span class=byln>by Ed Vasicek</p>

<p>My Mighty God,</p>
<p>When God the Son became man and was about to be born, 
Joseph and Mary were on their way to the little town of Bethlehem. This area 
was flooded with visitors as all the descendents of David had to return there to 
register. When Joseph and Mary arrived, the Inn was filled. Although not an 
ideal environment, the best place they could find to deliver the baby was in a 
stable, probably a cave used for housing animals.</p>
<p>Lord, how this pictures the world’s attitude toward the 
Messiah. We are busy bustling about and we find little time or energy to pay 
attention to our great God and Savior, Jesus Christ. Even those of us who know 
Jesus have a hard time finding time to pray to Him, study His Word and be 
involved in His ministry by participating heavily in a local church. It is 
tough to carve out the time and energy. Yet know and serving Jesus Christ 
should be our top priority in life. Help it to be in my life!</p>
<p>Lord, I know temptation is part of the Christian life although You encourage 
us to pray that we be not lead into temptation. Instead we ask You to deliver us 
from the evil one. Father, between the devil’s legion and my personal sin 
nature, temptations can pull at me. Help me to remember that it is not a sin to 
be tempted, for even Jesus was tempted. But it is a sin to give into temptation. 
So help me to resist temptation and turn my attention to other matters. If I 
have started down the road of sin, remind me that the earlier I turn back the 
better. And if I have sinned, help me find the cleansing that comes from 
confessing my sin to You 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>).</p>
<p>Great One, help me to learn some simple lessons from the ant 
(<a href="javascript:scripture('../../scripture/prov66.htm')" target=_self class=pop>Proverbs 6:6</a>). 
The ant plans in advance and looks ahead. He does not wait for the 
winter, but stores up food in anticipation of it. The ant is industrious and 
works hard. Help me to develop those similar traits that I might be a truly wise 
person.</p>
<p>Lord, I have some specific requests to bring before You today. They are: __________.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find 
my strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>I pray today for the eastern European nation of Moldova. Like many of the 
nations formerly under Russian communism, Moldova is a poor country. Thank You 
that 3.3% of the population claims to be born-again Christians, but their work 
is often hindered by the dominant Orthodox church. Father, guide in the training 
of leaders, provide missionaries and help the churches to multiply. May the 
percentage of believers double over the next five years. May Your Spirit draw 
many to Christ.</p>
<p>I remember the missionaries my church supports today including: __________.</p>
<p>Guide and protect them. Provide for them, bless their families and give 
them great fruit for their labors.</p>
<p>So, Lord, I give this day to You. I do so in Jesus’ Name, Amen.</p>

