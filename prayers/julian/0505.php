<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 5
<br><span class=byln>by Ed Vasicek</p>

<p>O God,</p>
<p>You are the Ancient of Days. You, O Lord, not only go back 
to the beginning of all things, but You go back before all things. There was a 
time when there was no time, yet You were there. As we learned in geometry, we 
are like a ray; we have a starting point and go on eternally, but You are like a 
line, eternal in both directions. You are the Ancient of Days and this implies 
You have an eternal wisdom, too.</p>
<p>Yes, my past carries some negative memories, but it carries some pleasant ones 
as well.  I can be tempted to live in the past. My past carries some pleasant 
memories as well. Help me not to glamorize the past 
(<a href="javascript:scripture('../../scripture/ecc716.htm')" target=_self class=pop>Ecclesiastes 7:16</a>), 
but to remember that You are always 
active in my life. Although there are things about the past that I love to 
remember, help me to press forward to the goal You have set for me 
(<a href="javascript:scripture('../../scripture/phil313ii.htm')" target=_self class=pop>Philippians 3:13</a>). 
The best days are ahead 
(<a href="javascript:scripture('../../scripture/1cor29.htm')" target=_self class=pop>1 Corinthians 2:9</a>).</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I accept responsibility for my 
sins and freely 
bring the sins Your Spirit brings to my mind now: __________.</p>
<p>Help me, Lord, to be assertive—to be able to ask for what 
I want—but to avoid being demanding. Help me obey the Golden Rule when it 
comes to making requests, to do unto others as I would have them do unto me.</p>
<p>Lord, today I remember that nation that is especially near 
and dear to Your heart, the nation of Israel. Your Word reveals that Your 
determination to bless Israel remains firm 
(<a href="javascript:scripture('../../scripture/jer313637.htm')" target=_self class=pop>Jeremiah 31:36-37</a>, 
<a href="javascript:scripture('../../scripture/rom1129.htm')" target=_self class=pop>Romans 11:29</a>). 
Yet few of the children of Israel know Jesus, Yeshua, as Messiah. Thank You that 
the number of believing Jews (Messianic Jews) has increased from 200 to 7,000 
with Israel, yet only two-tenths of one percent of the population claim to be 
born-again evangelicals. I look forward to the time when the nation of Israel 
will turn to Christ in droves 
(<a href="javascript:scripture('../../scripture/zech1210.htm')" target=_self class=pop>Zechariah 12:10</a>), 
but I pray that many would come 
to know You before those times. I also pray for the peace of Jerusalem. </p>
<p>Father, praise Your Name for Wycliffe Bible Translators and 
the great work they do! Over &quot;5000 active members from 46 countries...help 
build the church around the world and give people access to the words of life.&quot; 
Wycliffe seeks to translate the Scriptures into the mother tongue of every 
language group: &quot;Already Wycliffe workers have helped to complete 611 
translations, making God's Word available to more than 76 million people.&quot; Over 
380 million people cannot read a Bible in their native language because it is 
yet to be translated into that language. Wycliffe's goal is to have a 
translation project started in each one of these languages by 2025. O Lord, 
help them reach this lofty goal to Your glory. Provide for them, grant them 
safety, harmony and empower them by Your Spirit. Raise up Christians to help 
complete the task.</p>
<p>Now use me today, I pray in the Messiah's Name, Amen.</p>

