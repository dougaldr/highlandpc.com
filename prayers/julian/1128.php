<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 28
<br><span class=byln>by Ed Vasicek</p>

<p>O Mighty King,</p>
<p>You are the God Who is trustworthy; therefore You want me 
to trust in You. You want me to exercise faith in You. You want me to submit 
to You even in the midst of my confusion, during dilemmas and when I am 
engulfed in despair. You are trustworthy. You love me even when circumstances 
cry out, seemingly denying Your love with their loud shrill cries. Your 
faithfulness reaches down out of heaven like a lifeline to a deep sea diver. 
You want me to surrender to You and You offer me strength and an inner joy that 
defies explanation or reason. You want me to delight myself in You.</p>
<p>Lord, sometimes I have to deal with people who are evil, 
wicked, unreasonable and fickle. Sometimes the evil advance ahead of me and 
leave me behind and I find myself vulnerable to their choices. O God, help me 
to remember that, in time, they will fade away. Remind me that &quot;wisdom is 
vindicated by her children.&quot; Help me to distinguish between loving people I 
dislike and loving my friends and family and then to show the proper kind of 
Christian love.</p>
<p>My Master, help me address the frequent sense I have of being in a hurry. O 
Father, help me control and organize my time; help me to learn to say &quot;no&quot; when 
appropriate. Help me to be more concerned about pleasing You than making other 
people happy or avoiding complaints. Help me live a balanced life based upon who 
I am, not who I should be or want to be. Lord, help me to leave room for the 
unknown in the realms of time, energy and money. Keep me from trying to 
squeeze every minute, every dime, every ounce of energy. Remind me that You do 
not need me. You delight in using me, but You do not need me.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I find my 
strength in His power, not my own. May Your Spirit control me today.</p>
<p>Lord, today I pray for the Zhejiang Province of China with its 45 million 
people. Thank You for the great work You have done here! Probably over 10% of 
the population is made up of born-again evangelicals. Thank You, Lord! Father, 
I pray that the government would encourage rather than persecute the church. O 
Lord, double the number of believers over the next decade and please do a work 
throughout China like what You have done in the Zhejiang Province. Help the 
church to mature, I ask.</p>
<p>Lord, thank You for the Church Mission Society. &quot;CMS is committed to 
responding to God's call to evangelistic mission, particularly in places where 
'the name of Jesus is rarely heard' and works in close fellowship with local 
Christians wherever possible.&quot; O Lord, provide, protect, empower and reap a 
generous harvest of souls as a result of the efforts of this mission, I pray. 
Grant them harmony and zeal.</p>
<p>Now, Lord, help me be zealous for You today, in Yeshua’s Name, Amen.</p>

