<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 26
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the Great Builder. Our Lord Jesus worked as 
a builder—a stonemason and wood worker—before He began His ministry. Then 
He began to build the Kingdom of God. So You, Father, Son and Holy Spirit, are 
still building Your church. Jesus Christ is the Cornerstone 
(<a href="javascript:scripture('../../scripture/1pete27.htm')" target=_self class=pop>1 Peter 2:7</a>), 
the Apostles and Prophets laid the rest of the foundation 
(<a href="javascript:scripture('../../scripture/eph220.htm')" target=_self class=pop>Ephesians 2:20</a>) 
and we believers are living stones built upon that foundation 
(<a href="javascript:scripture('../../scripture/1pete25')" target=_self class=pop>1 Peter 2:5</a>). 
You, Holy Spirit, are working in us to edify us, and you give us fellow 
Christians to help build us up while we use our gifts to help build them up. 
Thank You, Lord, for being a life builder. Help me to trust Your blueprints 
over my own or the blueprints of the world.</p>
<p>I pray for my lost friends and family members, including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>I pray for Christians who are straying from their first love or compromising 
their walk with You. Rekindle the faith they once professed and help them 
fervently love the Savior once again. Remove their boring view of serving You 
and help them see the adventure and fulfillment of walking daily in Your light. 
Stir up their hearts and help them make the routines of godliness a part of 
their lives. I especially remember: __________.</p>
<p>Lord, today I pray for the Kingdom of Saudi Arabia. This 
nation is very bad when it comes to religious freedom. Even preexisting 
Christians are not allowed to gather for a religious meeting. Saudis who come 
to faith in Jesus Christ are put to death, no exceptions. The few believers 
must work hard at keeping their faith secret or face execution. Thank You for 
Christian radio broadcasts and satellite television; use them to reach the 
Saudi people. O Lord, may the government and people of Saudi Arabia become 
enlightened and embrace true religious freedom. Help the few who know You to 
have special wisdom. Send angels to protect them and multiply their numbers. 
Perhaps .1% are evangelical believers; may 1% of the population come to know You 
over the next five years, I ask.</p>
<p>Father, thank You for Reign Ministries, &quot;an agency committed to Building 
and Advancing the Kingdom of God through youth and youth leaders.... [Their] 
focus is training and discipling over 5,000 student leaders in the next 10 years 
to have a radical impact on the Kingdom of God.... Many...have served in over 50 
countries in the last 10 years! [They] are placing staff on Pioneering teams to 
Japan, Burkina-Faso, Columbia and the UK.&quot; O Lord, provide, prosper and 
protect these missionaries. Grant them an abundance of fruit for their work, 
I ask.</p>
<p>Lord, I pray for those who work as motor vehicle electronic 
equipment installers and repairers. Help them to take pride in their work, to 
do it well and to find satisfaction in honest and sincere work. May those who 
know You radiate Your light to peers and clients.</p>
<p>Now, O God, help me to be a fruitful Christian; I surrender 
myself afresh to You, in Jesus' Name, Amen.</p>

