<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 18
<br><span class=byln>by Ed Vasicek</p>

<p>Gentle Shepherd,</p>
<p>You are a personal God. Lord, You are three persons: 
Father, Son, and Holy Spirit. Yet You are one God. One God with three 
Personalities, yet of the same essence. We say these words, but they are beyond 
our comprehension. But that should not surprise us: if we could understand You, 
our theology would be suspect. Certainly You must very different and very much 
above us! Yet, You are, in many ways, like us. Yet You possess characteristics 
often extended out to infinity. We have some power; You are infinitely 
powerful. We know some things; You know all things.</p>
<p>And, as a personal God, this means that You can relate to 
us. You have intellect, emotions, a will, and You are creative and spiritual.</p>
<p>I am grateful to be among the three categories of beings 
that has true personality: You (God), angels (both holy angels and fallen 
demons), and we human beings. Help me not to neglect my spiritual nature; that 
is such a big part of what it means to be personal.</p>
<p>Lord, help me 
to respect the institutions that you established. Help me do what I can to lend 
support to Biblical marriage, family, government, and ethics. I pray that you 
would strengthen marriages and families throughout our world, country, and 
area. Especially bless those with whom I have contact and influence.</p>
<p>Please 
address the following needs: __________.</p>
<p>Lord, today I 
pray for the Cayman Islands. We know that this is a wealthy area, with freedom 
of religion and many evangelical Christians. We pray that the churches would be 
strong, and that the believers there would not become entangled with prosperity, 
but that they would focus their hearts upon you. We are grateful that over a 
fourth of the residents are born-again; we pray that their number would grow to 
40 percent within the next decade.</p>
<p>Father, 
I pray for Tech Team, an important missionary agency that supports so many 
others by providing &quot;...technical expertise in engineering, construction, design, 
landscape design, graphic design, display and brochure design, computer 
networking, software development, furlough vehicle leasing and website design 
and development...&quot; Lord, this, then, frees up your other servants to spend more 
time harvesting the fields. Oh, Father, provide for this vital ministry in 
every way, and use them by Your Spirit.</p>
<p>Today we 
remember construction workers. Many of them know You, but most do not. So, 
Lord help the Christians to bear good testimony, and use them to draw in the 
net. May an evangelistic surge break out in the construction trades.</p>
<p>So, Lord, I 
commit my day to You. Use me, I pray, in Jesus' Name, Amen.</p>

