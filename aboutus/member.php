<p id="pgt">How To Become a Member of Highland Park Church
<br><span class=byln>by Ed Vasicek</span></p>

<p>Becoming a member of Highland Park Church is not complicated, but it is
significant. By becoming a member, you choose to identify our congregation as
<i>your</i> church; you place yourself under the care of our elders and pastor, and you
can vote on matters brought before the congregation. Membership makes the task
of the elders easier: it helps us identify those who intend to settle into the
church from those who are undecided or wish merely to attend.</p>
<p>To become a member, we expect that:</p>
<ol>
  <li>you are saved and have a simple testimony and understanding of
salvation and<br><br>
  <li>you have read the 
  <a href="const.php" class="reg">constitution</a> 
  and generally agree with it. (If you have serious trouble with a
particular article, please bring that up with an elder.)
</ol>
<p>Then you need to share your understanding of salvation with any elder. (The
pastor is also considered an elder. For a list of elders, see the bulletin board
in the foyer or page three of our phone directory.) If the elder is satisfied
with your testimony and if you don't have any major problems with our
constitution, he may then recommend you to our elders' board for membership.
It's that simple.</p>
<p>We do not require our members to meet certain quotas for contributions.
<p>If your name is on the membership roll of another church, please put
a note in the pastor's box. We can send them a letter informing them of the
change in membership, if you so desire. We do not transfer memberships because we want to be sure our
members profess to be saved by faith in Jesus Christ.</p>
<p>Let me encourage you to officially become a member if you feel God wants you
here at Highland Park Church.</p>
