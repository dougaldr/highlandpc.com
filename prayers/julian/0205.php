<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 5
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great God and Father,</p>
<p>I celebrate Jesus Christ as my Great High Priest. Because 
God the Son became a man, He can understand my human weaknesses and the plight 
of life in a fallen world. Because the Son of Man is also the Son of God, He is 
the perfect High Priest, completely experiencing both divinity and humanity. 
Therefore, Father, He presents my needs before Your throne. But unlike human 
high priests, He is neither sinful, mortal, nor limited. He not only offers 
sacrifices, He IS the sacrifice!</p>
<p>Thank You that the church is built upon the foundation of the Apostles and Prophets 
(<a href="javascript:scripture('../../scripture/eph220.htm')" target=_self class=pop>Ephesians 2:20</a>). 
A foundation is only laid once, and they 
did so. We now have that foundation in Your written Word, the Bible. Like the Berean believers, help us to use the Scriptures to prove whether a teaching is 
true or false.</p>
<p>Lord, as Your child, You call upon me to be light in this 
dark world. Actually, I am to reflect Your light as I am transformed, 
moment-by-moment. May I cultivate Your daily transforming grace.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all unrighteousness 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>). 
So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness. They 
include: __________.</p>
<p>Lord, help me to control my moods and attitudes by 
conviction and Your Spirit. Help me not to anchor myself in the vacillation of 
feelings, but in the bedrock of truth. Help me to do Your will, whether I feel 
like it or not.</p>
<p>Father, I pray for the tragic nation of Bosnia and 
Herzegovina. This land has been devastated by war and bitter hatred between 
people groups. The poverty is unimaginable. The spiritual darkness is even 
greater, with less than one in a thousand people claiming to be born-again. 
Thank You, Lord, the number of churches has grown since the war, however, and 
thank You that the small evangelical church has a good reputation for being a fellowship 
of racial reconciliation. Help the church to multiply one hundred fold over the 
next twenty years, I pray.</p>
<p>I ask You, Lord, to prosper the missionaries from the 
European Christian Mission. Help them as they partner with the evangelical 
churches of Europe to share Christ and disciple believers on the continent. 
Break the bonds of secular humanism and anti-supernaturalism; may the number of 
believers in Europe double within the next decade.</p>
<p>Today I pray for those who organize chambers of commerce 
and downtown organizations that help nurture prosperity and business within a 
community. Help them to uphold businesses that will add positive contributions 
to a community but to hinder businesses that would offend You.</p>
<p>Help me to walk 
with You this day, I pray, in Jesus' Name, Amen.</p>

