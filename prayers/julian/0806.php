<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 6
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are the God of Peace. Lord, this world is never at 
peace. War, crime, abuse—hateful acts…peace is not a word I use to describe 
our cosmos. Lord, You are aware of all that is happening. You grieve over the 
consequences of evil, but You are still confidently at peace. You care, but You 
are not shaken. You have compassion with those who suffer, but Your Sovereign 
plan remains on course. You are certain of the outcome and completely secure. 
Thank You for sharing Your peace with me. I often need to rely upon it. I am 
shaken, unsure, perplexed and sometimes shocked. But You are the rock that is 
higher than I, a place of refuge for me.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You.</p>
<p>Father, guide me and those I love. Help me to trust You 
with all my heart, to acknowledge You in all aspects of life and keep me from 
leaning upon my own understanding so that You may direct me 
(<a href="javascript:scripture('../../scripture/prov356.htm')" target=_self class=pop>Proverbs 3:5-6</a>). 
Grant my loved ones and me wisdom. Help us to live and make decisions in light 
of Your revealed will rather than our human emotions. Help us to distinguish 
between the spiritual and the emotional, not confusing the two.</p>
<p>Father, bless the work of Amor Ministries. “Amor Ministries is called by God 
to respond to the spiritual and physical poverty in the world. We respond 
through: partnership with the church, resources for the poor, and life-changing 
mission experiences.... Amor Ministries strives to create cross-cultural understanding 
through direct exposure to the Mexican culture while providing for the physical 
needs of the poor. This is accomplished through facilitating short-term 
house-building trips, maintaining a food bank and organizing donated goods.” 
Help these folks to share Christ’s Gospel as they share His love in practical 
ways. May Your Spirit draw many to Yourself through this work, including some 
future leaders for Your Kingdom.</p>
<p>Lord, I pray for the Kerala Province of India with its 33 
millions people. This region has the highest percentage of professing 
Christians, with over 19% professing some form of Christianity. Yet perhaps 
only 4% of this population would claim to be born-again evangelicals. Lord, 
many professing to be Christians in this region consider themselves born as 
Christians. Few witness to the lost around them. 
O Father, may more of those who claim the name of Christ truly come to know Him 
and ignite zeal within them to share their faith with the lost in their region. 
May Your Spirit work mightily here, I pray. Since this is a wealthy region, 
a work of Your Spirit here could have great impact throughout the nation. O Lord, 
may this become a reality.</p>
<p>Now help my testimony to have an impact on others and 
burden me for my lost friends, I pray in Jesus’ Name, Amen.</p>

