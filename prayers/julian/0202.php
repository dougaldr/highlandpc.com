<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 2
<br><span class=byln>by Ed Vasicek</span></p>

<p>El Shaddai,</p>
<p>You are pure. You are not corrupted in any way or by any 
means. You are not mostly pure but completely pure. There is no sin within 
You; You are completely light with not even a hint of darkness. Lord, You are 
very much unlike us, a people of unclean lips who dwell among a people of 
unclean lips. Even the good we do is often contaminated by sin or ulterior 
motives. But You are completely innocent of all wrongdoing.</p>
<p>Thank You for the stewardship You have given me. You gave 
Adam and Eve the responsibility to reign over the earth. Help me to be faithful 
to the degree of my influence to rule well. Help me realize that all that I 
have belongs to You; I may possesses it, but I do not own it. And one day, I 
will give an account for what I did with what You gave me.</p>
<p>Part of the stewardship You have given me is managing my 
funds. Help me to be wise in this area, to faithfully support Your church, and 
to manage my money in a way that bears testimony to the fact that this world is 
not my home. May my integrity and value system be such that others know that I 
do not associate money with happiness, and I take seriously the command to owe 
no one anything.</p>
<p>And so, Lord, I pray that You would help me to be properly 
responsible. Help me gladly accept the responsibilities of my station in life, 
but help me not feel false guilt for that which is not my responsibility.</p>
<p>I ask You, Lord, to help me get along with all of my family 
members. Sometimes this is not possible, but help me to walk the extra mile and 
to pursue peace--but not to the point of being taken advantage of beyond that 
extra mile.</p>
<p>Father, today I pray for the nation of Bermuda. The 
country is fairly wealthy, and many of them, nearly 20%, claim to know Jesus. 
Lord, help the Christians in Bermuda to be burdened for missions. Help the 
evangelical churches to work together for common causes that would benefit Your 
kingdom. Lord, infuse the church in Bermuda with Your Spirit's power and zeal.</p>
<p>Today we pray for Christar Mission in their attempts to 
reach the least-reached people throughout the world, including Asia and Africa. 
The challenge is great, the dangers many, and the resources never enough.&nbsp; We 
pray that the missionaries would work together harmoniously and efficiently, 
that You would open the doors and prepare the hearts, and may many, many come to 
Christ and mature through their efforts.</p>
<p>We pray for the CEOs of our towns, villages, and cities. 
Whether we call them mayors or presidents, please, may many come to know You, 
espouse Your values, and influence their communities in accord with Your will 
and the good of the region.</p>
<p>Shower Your grace 
on me today, I pray, in Jesus' Name, Amen.</p>

