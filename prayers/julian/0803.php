<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 3
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a social God, a God Who is socially gracious 
to mankind. You have given us the institution of marriage, of the home and of 
the government. Although evil is the ruining of the good and although this 
world has been ruined because of sin, there is still much good to be found in 
marriage, the family and governed society. Help me remember, that You, O God, 
formed these social foundations. They tell me much about You. Marriage 
involves being very close to another, yet only You, O Lord, know me fully. 
Rearing children can give one a sense of purpose and being needed, but 
ultimately, my greatest purpose is to know, serve and glorify You. And 
although You do not technically need me, You did create me for a purpose. And 
government shows that You are a god of order. You want my life to be ordered 
by Your priority system, not by my neighbors.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Lord, help my church be harmonious. Keep antagonists, troublemakers and those 
who resent authority away from our congregation. Help my church to be effective 
in evangelism, discipleship, loving one another and world missions.</p>
<p>I remember parents. Lord. Parents are so important to 
those little ones entrusted to their care. May parents everywhere protect and 
train their children in humane and balanced ways. Help them to love their 
children, not to spoil them and to encourage them toward positive directions. 
We pray that Christian parents would prove to be consistent role models for 
their young ones. Send angels to protect them, I pray.</p>
<p>I remember the nations where persecution of Christianity is 
great. The number 6 through 10 nations are: Iran, North Korea, Vietnam, the 
Maldives and Bhutan. O God, please stop the persecutions there! Help the 
believers to be able to avoid persecution, to be strong when they cannot and 
may their persecutors come to know You!</p>
<p>I pray for the Hainan Province of China, with its nearly 8 
million people. Thank You that about 4% of the population is made up of 
born-again evangelicals. Thank You that the church has been doubling every year—praise Your Name! But, Lord, the Li people are not responding nearly as 
well. May the thousand or so believers among the Li be used of You to 
initiate the doubling process among them, I ask. May Your Spirit hover over 
this area bringing His regenerating power to hundreds of thousands more this 
year!</p>
<p>Thank You for the ministry of 
YUGO Ministries, &quot;challenging North Americans with a vision for world missions through an 
inexpensive, hands-on missions experience in Mexico. [Their] focus is 
evangelism, training national believers and church planting. This is 
supplemented by programs providing shelter, food and education for the poor.&quot; 
Use this ministry to reach and disciple many. May You use this ministry to lead 
others into full-time mission work, I ask.</p>
<p>Help me to love 
You with all my being today, O Lord, I ask in Jesus' Name, Amen.</p>

