<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 21
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>In <b>Isaiah 40:11</b>, we read of You, &quot;He tends his flock 
like a shepherd: He gathers the lambs in his arms and carries them close to his 
heart; he gently leads those that have young.&quot; O God, You can be a 
gentle God, especially when it comes to Your sheep.</p>
<p>Lord, I know no one is born into Your fold. Indeed, we must 
be born-again to become part of Your special flock. Lord, You carry us close to 
Your heart. You love us and want us to feel the security of Your heartbeat. 
You gently lead us. O Father, help us be alert to Your gentle prodding. Just as 
a shepherd takes into account a sheep with young, so You are fully aware of the 
special circumstances in our lives. You understand my unique needs.</p>
<p>Thank You that You always stand ready to forgive. The only 
condition for the believer is confession, a repentant spirit. For those who do 
not know You, it is repentance toward You and faith in the Person and Work of 
our Lord Jesus Christ. When we trust Christ, all our sins are legally forgiven. 
When we sin as believers, sin causes relational problems between us, a tension 
in our relationship. Even though I am secure in You, sin creates an obstacle to 
our fellowship. When I confess my wrong, You immediately forgive me and cleanse 
me from all unrighteousness. </p>
<p>Help me to forgive as You forgive. You forgive me when I 
meet the condition of repentance; help me to forgive others when they repent as 
well. May I stand ready to forgive.</p>
<p>God, like every one of the billions of people who inhabit 
our world, I am vulnerable. So, Lord, please send angels to watch over me and 
protect me. Keep me from harm and evil of every kind.</p>
<p>Today I remember the nation of Indonesia. About 4% of the 
population claim to be born-again evangelicals, but many Christians are 
untrained and in need of discipleship. Lord, the forces of Islam are seeking to 
hinder the freedom Christians have to share their faith and live the Christian 
life. O, Father, we pray that a spirit of tolerance would grip the land. We 
pray that You would strengthen the prayer movement, use the Jesus film and help 
the church to be in line with the teachings of Your Word. May the number of 
believers double over the next decade and may You raise up strong leaders and 
strong training ministries.</p>
<p>Lord, thank You for the ministry of CAM: &quot;an 
evangelical non-profit, mission-sending agency currently serving in seven 
countries around the world.... CAM is devoted to proclaiming the gospel to 
Spanish-speaking people throughout the world and in partnering with the 
Spanish-speaking church to train and develop Latin leaders for service in global 
missions. CAM performs a wide variety of ministry efforts with a strong focus on 
evangelism, discipleship, church planting, leadership training and development, 
theological education and global missions mobilization.&quot; O Father, continue to 
bless this ministry. Provide, protect, empower and grant harmony to the 
workers, I pray.</p>
<p>Help me to be faithful to Your tasks for me today, I ask 
in Jesus' Name, Amen.</p>





