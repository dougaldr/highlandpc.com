<p id="pgt"><span class="pgh">Sunday Morning Pastoral Prayers:</span>
<br>Prayer #10
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>Thank You for Your work in our lives. You bless us, You 
guide us, You discipline us, and You bless us by meeting our basic needs.</p>
<p>Lord, sometimes we are tempted to trust in our own 
resources. We scheme and plan without consulting You. In other instances, we 
forget that it is &quot;not by might, nor by power, but by Your Spirit.&quot;</p>
<p>Despite our failures, You seek to prod us in the right 
direction. Sometimes You turn events or failures into great blessings for good, 
and sometimes what seems to be a blessing brings distress and unhappiness. You 
remind us how much we need You to interpret life and guide us through the 
confusing straits.</p>
<p>Father, we pray for our church family. Every one of us is 
struggling in some way. Some of us are struggling with patience. We find it too 
easy to fly off the handle or answer others harshly. Some of us are struggling 
with doubt. Others are struggling with depression. Finances, marriage, children, 
parents, friends, siblings, the job, the neighbors, or our walk with You can be 
stresses for us. We need one another. We need to encourage and be encouraged. We 
need to pray for others, and they need to pray for us. But more than these 
things, we need Your power, wisdom, and guidance for our lives. We ask You right 
now: please allow Your Spirit to fill us with His power, grant us wisdom for the 
challenges we face, and guide us in our decision-making. Whatever You want, 
Lord, that's what we want too!</p>
<p>Our Savior, Jesus Christ, demonstrated Your great love for 
us by freely laying down His life. &quot;While we were yet sinners, Christ died for 
us,&quot; Your Word tells us. You have certainly bought us with a steep price, and we 
want to faithfully serve You in response to Your love for us. We are Your 
representatives to this world, Your ambassadors. O Lord, help us to share the 
Good News of Jesus Christ with our friends, neighbors, and family members. We 
pray that soon we would experience the blessing of leading someone to faith in 
Jesus Christ our Lord.</p>
<p>And now, Lord, as we focus in upon Who You are, what You 
have done, and Your will and instruction for our lives, open our hearts. May the 
Word of God affect us in some small way so that, over time, our lives and minds 
would be conformed to Your image.</p>
<p>We pray in Jesus' Name, Amen.</p>

