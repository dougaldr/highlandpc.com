<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 9
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When Elizabeth gave birth to her son, family and friends 
wanted to know what his name was. She told them &quot;John&quot; and they would not 
believe her. It was not customary to use a first name that was not in the 
family's ancestry. So they asked the father, Zechariah, who had been smitten 
with dumbness because of his refusal to believe that he and his elderly wife 
would have a child. He wrote, &quot;His name is John.&quot; At this point, the dumbness 
left and he could now speak.</p>
<p>The people marveled at these things, as well they should 
have. But Lord, sometimes I find myself like those people, stuck in a rut and 
wrapped up in the traditions of men. Sometimes You call me to be different from 
the crowd, to be a non-conformist. Elizabeth prophesied of John, &quot;for you will 
go on before the Lord to prepare the way for him&quot; (<b>Luke 1:76</b>). Lord, thank You 
that You include all sorts of people with all kinds of roles when it comes to 
Your Kingdom. Help me to find my niche. </p>
<p>Lord, I pray that You would protect my loved ones and I 
throughout our lives. Send guardian angels to watch over us, I pray. Keep us 
from harm and evil of all kinds: physical, spiritual, mental and relational. 
Help me�and those close to me�to love You with all my heart, mind, soul, 
spirit, will and strength. Let all we are be laid before Your feet as a living 
sacrifice. May my integrity and value system be such that others know that I do 
not associate money with happiness, but I take seriously the command to owe noone anything except to share Your love.</p>
<p>Father, help me view my daily life�and especially 
Sundays�as times of training for Your service. The training never ends, for I 
can always be closer to You. There is always so much more to learn, so much 
growth to experience. Please do not allow me to rest on my laurels. And help me 
take advantage of the opportunities around me.</p>
<p>I remember my brothers and sisters in Christ who are being 
persecuted. Thousands have died for their faith over the last decade, while 
others have been tortured and discriminated against. I especially pray for the 
nations where persecution is worst: Egypt, Saudi Arabia, Peru, Cuba, Colombia, 
Uzbekistan, Turkmenistan, Burma, Indonesia, Sri Lanka, Pakistan, India, China, 
North Korea, Vietnam, Laos, Sudan, Nigeria and Eritrea. O Lord, may the 
persecution stop; give believers wisdom to evade persecution and when they 
cannot, help them to be strong. (For more information about the persecuted 
church, go to
<a href="http://www.persecution.com" target="offline" class="reg">www.persecution.com</a>.)</p>
<p>Thank You, my Father, for Biblical Ministries Worldwide, &quot;a 
mission agency whose passion is to help churches plant reproducing churches 
through evangelism, discipleship and leadership development.&quot; Thank You for the 
churches they are planting in North America, Latin America, Europe, Africa, 
Asia and the Pacific. We pray that You would grant them harmony among 
themselves, Your Spirit's power to make their ministry effective and pleasing to 
You, safety, wisdom, bountiful fruit for their labors and provision for their 
needs.</p>
<p>Now, Lord, provide for my needs today, I ask in Jesus' 
Name, Amen.</p> 

