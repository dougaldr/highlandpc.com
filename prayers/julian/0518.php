<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 18
<br><span class=byln>by Ed Vasicek</p>

<p>Lord Jesus,</p>
<p>Thank You that You are our Brother. When You, God the Son, 
took on flesh and became a man, You entered creation by becoming a creature and 
You entered humanity by becoming human. Yet You were sinless in Your humanity. 
You called us into Your new family and now we will enjoy eternity as Your companions 
(<a href="javascript:scripture('../../scripture/heb19.htm')" target=_self class=pop>Hebrews 1:9</a>) 
and brothers 
(<a href="javascript:scripture('../../scripture/rom829.htm')" target=_self class=pop>Romans 8:29</a>). 
Not only are You always above us, but You will also be with us 
(<a href="javascript:scripture('../../scripture/john143.htm')" target=_self class=pop>John 14:3</a>), 
enjoying our company as we enjoy yours. These truths are beyond my ability to imagine. Jesus Christ as my 
big brother—what a thought! Just as we respect the Father but also call Him 
&quot;Abba,&quot; so we respect the Son, but also call Him &quot;Brother.&quot;</p>
<p>My God, help me to fear You more than I fear people. Your 
Word says that, &quot;Fear of man will prove to be a snare, but whoever trusts in the 
L<span class=sm>ORD</span> is kept safe&quot; (<b>Proverbs 29:22</b>). Keep me from giving in to peer pressure or 
from compromising my faith because I want to please others. Help me to be 
considerate and kind to others, but help me to fear displeasing You more than 
displeasing them. </p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________.</p>
<p>Let me remember that You are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>Lord today, I especially remember those looking for a job. 
Open doors of opportunity and match the right person to the right job. 
Especially help those who work with integrity and have good character to find 
employment.</p>
<p>Father, be Lord of my finances. Help me regulate my money 
with Your wisdom.</p>
<p>Gracious One and Lord of the Great Commission, I bring the 
nation of Kyrgyzstan before Your throne. More than one half of one percent 
claim to be born-again evangelicals, a tiny representation of Your Kingdom. The 
nation is horribly impoverished and corrupt with all sorts of superstitious 
beliefs mixed with Islam. O, Father, raise up a people for Your Name in this 
land. Use them to lead the nation to honesty, prosperity, integrity and most 
importantly salvation. Do a work of Your Spirit here, we pray.</p>
<p>Lord, thank You for the ministry of Bibles for the World 
as they follow their &quot;dream to give a 
free copy of the New Testament to all the families of the world in response to 
the commission of the Lord, 'the Gospel must first be published among all the 
nations.'&quot; They have sent over 14 million New Testaments to individual homes in 
over 100 countries.&quot; Provide for their needs, give them unity and harmony and 
bless their work by empowering it with Your Spirit, I ask.</p>
<p>Help me to serve You today, I pray in the Messiah's Name, 
Amen.</p>

