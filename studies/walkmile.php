<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Walking That Extra Mile: A Slightly Different Twist on Matthew 5:38-48
<br><span class=byln>by Ed Vasicek</span></p>

<p>I can honestly say that I have been &quot;working&quot; on the above verses for
approximately 25 years in the back of my mind. Even while a student
at Moody Bible Institute, I kept my eyes open to comments or insights that could
help me understand these verses.</p>
<p>The problem with the above verses (if understood apart from the context I
will mention below) is simple: they fly in the face of BOTH Old Testament
teaching, the latter teachings of Christ, and the Epistles. I am not
going to elaborate about these differences here, but Lewis Sperry Chafer does a
thorough job of this in his book titled, <u>Grace, The Glorious Theme</u>.&nbsp;
Although I do not agree with Chafer's solution to the conflict between the
Sermon on the Mount and the latter teachings of Christ and the Apostles, he at
least acknowledges the reality of the conflict.</p>
<p>Of course I believe the conflict disappears when we do what good interpreters are SUPPOSED 
to do, namely answer these two questions: (1) How did the speaker/writer understand his own 
words? and (2) How would the
original audience have understood these words?&nbsp;</p>
<p>In light of this, I would like to go into the text with these pre-existing
convictions:</p>
<ol>
<li>What Christ taught did NOT contradict the Law nor the Epistles.<br>
<li>The Law is not some evil entity for childish disciples but is truly the
Word of God and, though incomplete, is as pure as the One Who gave it.<br>
<li>Christ did not come to destroy, put down, or devastate the Law, but to
fulfill it (properly interpret it).<br>
<li>The Law of Moses was not given to mankind, but to Israel; the Covenant of
Noah WAS given for all mankind.<br>
<li>When Christ quotes the OT passage upon which He is commenting, fine; but
when He doesn't, it might help to infer the passage and go from there. Since we
have perhaps 10% of all that Christ said in this sermon, and since the Sermon on
the Mount is a commentary and application of the Law, deducing the texts in
question can prove invaluable, though risky.</ol>
<p>I have pasted below a bulletin insert I wrote which offers additional
thoughts to contemplate:</p>
<p align="center"><b>Jewish Insights Upon the Sermon on the Mount</b></p>
<p>As I mentioned before, our version of the &quot;Sermon on the Mount&quot;
consists of sermon <i>highlights</i>; the actual sermon itself probably lasted
two hours. We have perhaps only a dozen &quot;highlight&quot; minutes from
which to reconstruct its meaning.</p>
<p>Since Christianity eventually abandoned its Jewish roots, and since gentile
believers eventually outnumbered (and later suppressed) Jewish believers, the
key to interpreting this sermon was lost. <u><b>It is only when we
remember that the speaker and audience were Jews and that Christ was addressing
the hot issues and controversies of the day (including applying the Old
Testament Law in light of current circumstances) that the sermon falls in line</b></u>
(not only with the Old Testament but the New Testament epistles as well).</p>
<p>For example, Israel was then under Roman occupation. A Roman solider
could legally constrain any non-Roman to carry his supplies for one mile. When Christ 
speaks of going two miles, He is talking about voluntarily going
beyond the requirement of the Roman law. Yet Christ does not say, &quot;as
many miles as someone wants.&quot; So he encourages us to offer generous
but reasonable boundaries.</p>
<p>It is also interesting to note the understanding of the Jews regarding the OT
command, &quot;an eye for an eye&quot; in the first century. David Stern
comments about how Jews in the first century probably understood this
command:</p>
<blockquote>
  <p>....eye for eye, etc. shows that God was not commanding revenge, but
  controlling and limiting it. Retribution and punishment must be
  commensurate with the crime; contrast Cain and Lamech's extraction of
  multiplied vengeance at
  <a href="javascript:scripture('../scripture/gen424.htm')" target=_self class=pop>Genesis 4:24</a>....</p>
</blockquote>
<p>Stern then quotes from an ancient Jewish source, the Mishna, to show that the
Jews did not understand &quot;an eye for an eye&quot; literally. Here is
the Mishnah quotation:</p>
<blockquote>
  <p>If anyone wounds his fellow, he becomes liable to compensate the injured
  party for five different aspects of the injury: damage, pain, healing, loss of
  time from work, and insult....</p>
</blockquote>
<p>Note that the Jews were required to compensate those they <u><b>insulted</b></u>.&nbsp;
In ancient Jewish culture (and other cultures, even today), a slap in the face
was considered the prime example of a great insult. (We talk about
&quot;receiving a slap in the face&quot; or &quot;kick in the pants.&quot;)&nbsp;
When Christ is talking about turning the other cheek, He is not addressing the
issue of self-defense in general, nor national policy, but He is addressing a
debate of the day, namely, did the &quot;eye for eye&quot; command apply to
being insulted? Christ made it very clear that individual believers who are
insulted for His Kingdom must bear it. Indeed, not availing ourselves of
all our rights may provide opportunities for others to see that we are not out
to exploit others, that Christians are more concerned with doing right before
God than we are with &quot;getting ahead.&quot; We are not out to get all
the &quot;gusto&quot; we can, but to glorify God.</p>
