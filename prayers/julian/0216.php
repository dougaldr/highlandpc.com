<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 16
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>Thank You that You are a God Who plans. Before the world 
was founded, You, Father, Son, and Holy Spirit, planned that God the Son would 
become a man in the person of Jesus Christ and offer Himself as the Lamb of 
God. He is the &quot;Lamb slain before the foundation of the world&quot; (<b>Revelation 13:8</b>).</p>
<p>When the fullness of time came, right on time, You sent 
Your Son, born of a woman, born under the Law (<b>Galatians 4:4</b>). At the right 
time, Jesus shall return to reign. You are a God who makes plans.</p>
<p>Lord, I am 
thankful for many things, three of which are: __________.</p>
<p>Lord, I empty 
myself before You so that Your Spirit may fill me. I find my strength in His 
power, not my own. May Your Spirit control me today.</p>
<p>Thank You 
that my Savior is interceding for me, Father, at Your right hand. He is my 
advocate, my defense attorney who defends me against Satan's accusations. He is 
my high priest and mediator, the One Who represents me to You. That is why I 
pray in Jesus' Name.</p>
<p>So. Lord, 
help me be a prayer warrior, someone who prays informally during my day and is 
always ready to offer up a prayer.</p>
<p>Lord, I need 
You to watch out for my physical and material needs as well. Help me manage my 
resources well, and open and close the right doors.</p>
<p>Today I pray 
for the African nation of Cameroon. Thank You for the recent growth of 
evangelical churches. Lord, You know how corrupt this nation is, and You are 
aware that many who profess to be Christians are not genuinely converted. Help 
Your true children to stand strong.&nbsp; Provide much-needed training for church 
leaders, and help the newer evangelical churches to set a standard that will 
challenge the older, compromised churches.</p>
<p>Thank You, 
Lord, for Latin America Mission (LAM). They serve in a variety of ways, from 
&quot;...friendship evangelism to theological education; medical relief to youth camps; 
church planting to children-at-risk work; office management to micro-enterprise 
development; personal counseling to emergency response.&quot; Bless their many 
missionaries, help them to get along well with one another, keep them walking 
with You, may their families be strong. and empower them with Your Spirit so 
that they may bear much fruit. Provide for all their needs.</p>
<p>I remember 
our nation's school bus drivers. Please give them safety and wisdom, and may 
many come to know You.</p>
<p>Now. Lord, be 
gracious to me today, I pray, in Jesus' Name, Amen.</p>

