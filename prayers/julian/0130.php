<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 30
<br><span class=byln>by Ed Vasicek</span></p>

<p>Oh Father God,</p>
<p>Thank You that Your love is everlasting. 
You do not grow weary or tired, and I will experience Your loving presence and 
blessing throughout eternity. Your love has no quotas. It does not need to be 
conserved or rationed. Even though you already shower me with Your love, heaven 
will be so much greater!</p>
<p>Lord, thank You that Your Spirit dwells within my heart, my 
inner being. Thank You that he has written Your Law upon my heart. Thank You 
also that He wants to produce His fruit in my life: help me cultivate His 
fruit-producing ministry by walking closely with You, confessing known sin, and 
trusting in Your strength.</p>
<p>Dear God, I pray that You would give me tact in relating to 
others today. Help me to speak tactfully, to write tactfully, and to conduct 
myself with consideration to others.</p>
<p>Help my church’s leaders to be true to the Word of God, to 
walk closely with You, and to help nurture a godly harmony and unity: not the 
unity of surrendering truth, but the unity that comes from a common faith and 
trust in the main truths of Your Word.</p>
<p>Father, I have many requests to make, a few of which are: __________.</p>
<p>Today I remember the persecuted church.&nbsp; I think of 
countries like Sudan, China, Saudi Arabia, North Korea, and Viet Nam, to name 
some of the worst. Please, Lord, change laws and attitudes so that this 
persecution would stop. Help the believers to evade persecution, when possible. 
Help them to stand firmly when they cannot evade it. I pray that their 
persecutors would be won to the faith, and that one day, Christians everywhere 
would be free to share their faith and live the Christian life without fear.</p>
<p>I remember the ministry of Voice of the Martyrs, and I pray 
that You would provide for them in every way. Give them wisdom, direction, 
finances, and harmony so that they would effectively serve the persecuted 
church.</p>
<p>May your blessing be upon all Emergency Medical Technicians as they save 
lives and provide emergency treatment. Help them get to the 
medical scenes safely and quickly, may they act wisely, and prosper their work, 
I pray.</p>
<p>As I go on my way, 
help me to walk with You and depend upon You today, I pray, in Jesus’ Name, 
Amen.</p>

