<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>The O&#039;Heart Factor
<br><span class=byln><b>Notes on John 11:1-57, 12:9-11</b>
<br>by Ed Vasicek</span></p>

<p>We are quickly approaching the final week of Christ&#039;s pre-resurrection ministry. 
The Gospels spend a lot of time on that final week, but the raising of Lazarus 
from the dead was the final motivation for the Jewish leaders to plot Jesus&#039; 
death. Because this miracle was so great, many came to believe in Jesus.</p>
<p></p>
<p>Since 
Israel was under Roman rule, and since many Jewish sects had rebelled against 
Rome, the nation of Israel suffered from these attempts at rebellion. The 
Romans crucified hundreds of Jews to intimidate them and prevent future 
uprisings. The non-believing Jewish leaders feared that Christ and His 
followers would start trouble like other individuals claiming to be the messiah 
at that time. They were out to keep the peace. Even though Christ’s message 
did not involve rebellion against Rome, the priests and Pharisees would not get 
the &quot;gain a following, start a rebellion&quot; paradigm out of their minds.</p>
<p></p>
<p>The 
nature of Christ’s disciples didn't help. One of Christ’s disciples, Matthew 
Levi, had been a tax collector, working for the Roman government. Simon the 
Zealot, on the opposite end of the spectrum, had belonged to a Jewish rebel 
group. This organization, the Zealots, attempted to terrorize the Romans and 
make their occupation of Israel as miserable and unproductive as possible. But 
both Levi and Simon were now different men.</p>
<p></p>
<p>The 
following verses reveal the concern of the Jewish leaders. It is interesting 
that an unbeliever, Caiaphas, utters a 
prophecy that he did not fully understand (but John expounds its true meaning). 
Can God speak through an unbeliever? Sure. But the deeper cause for the Jewish 
leaders rejecting Christ was not fear of the Romans, but hard hearts. Stresses 
do not make us as much as they reveal what we are made of. A good number of 
logistical and philosophical problems distill down to spiritual problems.</p>
<p></p>
<p><b>John 11:48-53</b> reads, &quot;'If we let him go on like this, everyone will 
believe in him, and then the Romans will come and take away both our place and 
our nation.' Then one of them, named Caiaphas, who was high priest that year, 
spoke up, 'You know nothing at all! You do not realize that it is better for 
you that one man die for the people than that the whole nation perish.' He did 
not say this on his own, but as high priest that year he prophesied that Jesus 
would die for the Jewish nation, and not only for that nation but also for the 
scattered children of God, to bring them together and make them one. So from 
that day on they plotted to take his life.&quot;</p>
<p></p>
<p>
Christians who are well taught and serious about following the Lord seem 
contradictory, even today. When we share Christ with others because we are 
concerned about their soul's well being, we are accused of trying to force 
religion upon them or of saying we are better than they. Maybe being 
misunderstood means we are on the right track.</p>


