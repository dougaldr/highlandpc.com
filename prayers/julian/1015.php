<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 15
<br><span class=byln>by Ed Vasicek</p>

<p>My Loving Creator and Redeemer,</p>
<p>You have so many qualities and none of them exist in isolation. You are a 
compassionate God, a gracious God and an angry God. You are also a logical God. 
No, You are not all reason; You have emotions and value relationships. But You 
are all these things and logical as well. Your thinking is sound and coherent.</p>
<p>That is why it is so important for me to cultivate my mind—to be like You. 
You want me to renew my mind, fill it with good things and to develop it. Lord, 
may Your logic motivate me to cultivate logical thinking. Since You define pure 
logic in the way You do things, I can be like the Psalmist is <b>Psalm 119:99</b>, 
&quot;I have more insight than all my teachers, for I meditate on your statutes.&quot;</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins and You will purify me from all unrighteousness (<b>1 
John 1:9</b>). I accept responsibility for them and freely bring the sins Your 
Spirit brings to my mind now: __________.</p>
<p>Please meet the following needs, requests and desires as You see fit: __________.</p>
<p>Lord, I pray for the peace of Jerusalem. I ask You to work out Your purpose 
for that part of the world. I also ask Your blessing upon Messianic Jews and the 
Messianic movement. Help me to remember that the priority of Your Word is “to 
the Jew first” (<b>Romans 1:16</b>). Help me to bold like Paul when it comes to sharing 
the Good News of Jesus Christ.</p>
<p>Lord, Your Word teaches this proverb: &quot;Plans fail for lack of counsel, but 
with many advisers they succeed&quot; (<b>Proverbs 15:22</b>). Although this is a 
principle and not a promise, it therefore holds true most of the time. So help 
me to get plenty of advice from godly people before making major decisions. Keep 
me from being impulsive and help me recognize how much I need other believers 
and how much they need me!</p>
<p>Father, today I pray for the Tripura State of India with its nearly 4 million 
people. Thank You that about 3% of the population is made up of born-again 
evangelicals. Lord, they have been persecuted by both Hindus and animists 
(spirit-worshippers). Lord, there is much work to do in this region. Please, 
Father, work by Your Spirit and grant many the repentance that leads to life. 
Help the percentage of believers to double within the next five years, I ask. 
Prevent persecution; work in hearts to create a positive attitude toward the 
Gospel.</p>
<p>Lord, thank You for Pacific Island Ministries, &quot;a discipling and training 
mission with accent on service with special interest in Papua New Guinea where 
it pioneers in remote regions to promote the knowledge of the Scriptures and 
obedience to its mandates. The mission aids rural and urban communities by sponsoring Christian 
elementary schools, Sunday schools, Bible schools, leadership training, 
community development, the installation of rain-catching water systems, health 
services and the supply of Christian literature.&quot; O Lord, provide, protect, 
empower and grant harmony to this ministry, I ask.</p>
<p>Help me to be a blessing to someone today, I pray in the Savior's Name, 
Amen.</p>

