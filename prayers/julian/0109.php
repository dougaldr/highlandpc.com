<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 9
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great God of Wonders,</p>
<p>I am impressed by all Your ways! When I think of things 
You have done, I am awestruck. When I contemplate Your nature, I am humbled at 
my inability to comprehend You. You are, and have always been One God. You 
are, and have always been, Three Persons. How this can be defies my ability to 
understand, for, Lord, I think in terms of comparison. I build upon what I 
already know. But there is no one or nothing that compares with You, O Triune 
God. So I embrace what I cannot fully understand. But, then again, if a finite 
creature could really understand the infinite, the infinite would no longer be 
infinite!</p>
<p>Lord, I thank You for the Church. I thank You for the 
Universal Church made up of all those who truly know You. I thank You for my 
local church, where I can use my talents and gifts in coordination with others 
so that we can do together what we cannot do alone. I thank You for the 
teaching of the Word, the accountability I have to the leadership, and the way I 
learn to work with and love others who sometimes differ from me. Thank You that 
church involvement stretches me and helps me to focus on giving and helping 
others, not just &quot;taking in.&quot;</p>
<p>Father, may Your joy be evident more and more in my life.&nbsp; 
Help me to appreciate salvation—especially my relationship to You—so 
that I am filled with joy.</p>
<p>Lord, some of my personal needs, issues, and problems make 
it difficult for me to rejoice, but I choose to do so anyway. I commit my 
requests to You, including: __________.</p>
<p>Today I remember the African nation of Angola. Although 
communism attempted to wipe out the church, the number of evangelical believers 
has blossomed. Although the persecution has taken its toll, we thank You that 
the church is strong and growing. We pray that the number of born-again 
believers would double within the next decade, and that this nation would arise 
as a beacon of the Gospel to the rest of the continent.</p>
<p>Today we remember the ministry of SIM, Sudan Interior 
Mission. Keep the missionaries true to You, protect them, provide for them and 
their families, and bless them with an abundance of fruit.&nbsp; Help the 
missionaries to get along with one another, and guide the leadership of the 
organization, showering the leaders with divine wisdom.</p>
<p>Locally, we remember our city/town/county legislators. 
Guide them toward making decisions that are in accord with Your truth and the 
best interests of their communities.</p>
<p>Help me enjoy Your 
peace today, I pray, in Jesus' Name, Amen.</p>

