<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 2
<br><span class=byln>by Ed Vasicek</span></p>

<p>Almighty God,</p>
<p>I praise You that You are the God of grace! You enjoy 
favoring me and showering me with undeserved blessings. Not only did You provide 
for my salvation, but You literally pour out Your grace upon me.</p>
<p>Lord Jesus, I anticipate the Day when You return to the 
clouds to call me to join You, and I look forward to the time when You will 
reign on this earth. You will judge Your enemies and squelch injustice.</p>
<p>In light of Your return and the vindication of Your people, 
help me to be patient when others around me reject You; they sometimes reject me 
because of You. I can be patient, knowing that all wrongs will be ultimately 
righted.</p>
<p>Father, I pray that You would meet my personal needs 
today. I ask You to send angels to protect both me and those I love from harm. 
Bind Satan and his forces today.</p>
<p>Lord, I pray for our town/city leader. May he or she do 
what is morally right and what is in the best interest of our people. May he or 
she take a positive attitude toward the church.</p>
<p>Today I pray for the nation of Italy. Many in that nation 
trust in salvation through the practice of religion, or do not believe You are a 
God of wrath and sense no fear of judgment. May You grant many in Italy the 
repentance that leads to life. Help the few evangelical Christians to shine 
brightly. Multiply their numbers.</p>
<p>I want to remember the Radio Bible Class and their 
publication, Our Daily Bread. Provide for all their needs, keep them true to 
Your Word, and use them to reach the lost and edify fellow believers.</p>
<p>Dear Holy Spirit, fill me today and bless the events You 
allow or determine for me today.</p>
<p>I ask this in the Name of the Messiah, Jesus, Amen.</p>

