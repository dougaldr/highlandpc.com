<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 1
<br><span class=byln>by Ed Vasicek</p>

<p>Great One,</p>
<p>You are truly incomprehensible. We can understand some 
things about You by Your creation. We can understand more about You from Your 
Word, and Your revelation of Yourself climaxes when You became man in the Person 
of Jesus Christ. But 
<a href="javascript:scripture('../../scripture/deut2929.htm')" target=_self class=pop>Deuteronomy 29:29</a>
reminds us that the secret things still belong 
to You. We can only glory in what You have revealed, and we must admit that we 
are awestruck. Our little minds can understand but a little about our majestic 
God, but what we do understand exceeds our ability to even imagine.</p>
<p>Lord, today is sometimes called, “April Fools Day.” 
Although decent fun might be a great stress reliever, the truth is that 
foolishness is no laughing matter. Your Word constantly emphasizes the importance 
of pursuing wisdom. Wisdom can prevent so many disasters while making life 
richer. Help me to love wisdom, to pursue wisdom and to spend time in the Book 
of Proverbs to gain further wisdom.</p>
<p>I remember the poor, sickly, downtrodden in my prayer 
today. Provide for them, encourage them, open doors of long-term opportunity for 
the poor, grant patience, strength and healing for the sick, and mend the 
broken hearts of the downtrodden. We pray that justice would reign throughout 
the earth and that societies would strike a balance between compassion and 
accountability.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life. Help me to trust You and to remember that 
trials are part of Your development plan for me. You never consulted me about 
this plan, but many of us would choose peace and safety over growth and the 
sometimes painful stretching of our faith. Please address issues that are 
troubling me including: __________.</p>
<p>Today I remember the Democratic Republic of Fiji in the 
South Pacific. Thank You that 17.5% of the population claim to be born-again 
evangelicals and thank You that the evangelical church is growing. 
Unfortunately the cults are growing too and many people are resistant to the 
Gospel because of the discrimination practiced by Christians while Fiji was 
under British rule. We pray that a new generation of Christians would exemplify 
love and consistency and be used by You to demonstrate that the Gospel really 
does transform lives. Bless the efforts to train leaders, to reach young people 
and to network evangelicals. Thank You for using the Jesus film, the Bible 
Society and other groups and alliances to reach the lost. May the percentage of 
evangelicals grow by 50% over the next decade and may they exemplify a 
Christianity worth reproducing.</p>
<p>Thank You for the ministry of Equip as they seek “to equip international 
Christian leaders to effectively serve the growing Body of Christ around the 
world...to provide strategic leadership training through conferences, resources, 
partnerships and technology.” We know that strong leadership is important to the 
local church, and we pray that the ministry of Equip would make a significant 
difference by training leaders to better fulfill the Great Commission.</p>
<p>Lord, bless those who work in the paralegal field. May they work honestly and 
effectively, and may those who know You live out and share their faith.</p>
<p>Give me Your direction today and send angels to protect my loved ones and 
myself, I pray in Jesus’ Name, Amen.</p>

