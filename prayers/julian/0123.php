<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 23
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>How awesome is Your creation. Like the Psalmist, when I 
consider the work of Your hands—the stars, the moon, the sun—it makes me 
wonder how You could even pay attention to me. You are the Creator of all, and 
Your handiwork evidences Your genius.</p>
<p>Thank You that You care about me—and even the tiny 
details of my life.</p>
<p>Father, thank You that You have called every believer a 
saint, a set apart one. Thank You that You have made all New Covenant believers 
priests. We can approach Your throne directly through our great High Priest, 
Jesus Christ. We can also help connect lost people to You by sharing the Gospel 
with them. </p>
<p>Father, help me learn to check in with You first before I 
make decisions, especially important ones. I have known Christians to move to 
another location, switch jobs, churches, and employment without waiting on You 
for direction and guidance. So, Lord, help me remember that if I am going to be 
in Your army, I need to check in with headquarters. Remind me that I am not my 
own.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>Meet my needs this day, father; help me to use my resources 
of time, energy, and money wisely and in a way that pleases You.</p>
<p>Today I remember that part of the United Kingdom called 
Northern Ireland. Thank You that the conflict there has subsided and that peace 
seems to be reigning. May it continue that way. May Christians shine their 
light, and may the evangelical church there double in size over the next decade.</p>
<p>We also pray today for the Messianic Jewish movement. We 
know, Lord, that some Messianic groups are more in tune with a proper 
understanding of Scripture than others, but many of these groups are solid. 
Help them in both reaching the Jewish community and in creating interest in the 
Jewish roots of our faith so that we might better understand Scripture.</p>
<p>Use me this day to 
serve You, I pray, in Jesus’ Name, Amen.</p>

