<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 16
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>How I praise You that You can be here with me; this is true 
no matter where I go. As the Psalmist put it, even if I go to the depths of the 
ocean, You are there. Indeed, You have long enjoyed the beauties of the ocean 
depths, beauties of which only a few men have sampled.</p>
<p>You are omnipresent, present everywhere. You are not 
merely ubiquitous—present in more than one place. No, You are literally 
present in every place at once. Yet, You are especially present when two or 
three believers gather in Your Name. You are present in a local way upon Your 
throne in heaven.</p>
<p>Lord, help me to fear You. Help me to remember that though 
You are my dearest friend—and knowing and glorifying You is the purpose of my 
life—I sometimes get too familiar with You. Though You are my friend, You are 
never just my “buddy.” Help me to strike the right balance between reverence 
and familiarity.</p>
<p>Lord, help my body to be healthy and strong. And help my 
loved ones to enjoy good health as well.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Today I pray for the country of Slovakia. Thank You that 
there are some evangelical believers in this land; help their small numbers to 
increase exponentially. Create a hunger in that land for a personal 
relationship with Jesus Christ, and may the believers there live out a 
Christianity worth reproducing.</p>
<p>Today I ask You to bless Spanish World Ministries. May 
their radio program, “El Camino De La Vida” continue to be used of you to reach 
many throughout Central and South America, as well as Spain and elsewhere. Help 
them to work well with the evangelical churches throughout these regions. 
Provide for them and protect them, we ask.</p>
<p>Today I remember our governor and his/her staff. May 
he/she execute his/her office with integrity, ethics, and do so in the best 
interest of the people of our state.</p>
<p>We ask Your joy to show itself in my life today, for I ask 
this in Jesus’ Name, Amen.</p>

