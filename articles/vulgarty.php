<p id="pgt"><span class="pgh">Articles:
<br>Ethics, Morals, Behavior</span>
<br>Vulgarity and the Christian
<br><span class=byln>by Ed Vasicek</span></p>

<p align="justify">The dictionary defines vulgarity as &quot;the state or
quality of being vulgar, crude, coarse, unrefined, etc.&quot; It can be
difficult to locate the line between vulgarity and merely telling it like it is.&nbsp;
The truth is that some of life is simply vulgar, no matter how you phrase it!</p>
<p align="justify">As the years go by, it is obvious that evangelical
Christianity has been loosening up from its overly inhibited heritage. The
question still remains, however: &quot;How far is too far?&quot; I
think we are on the edge, while others are over. As I have perused the
'Net looking for Christian drama, I have been disappointed at repeated incidents
of off-color humor included in some of them. I read it and I hear it.</p>
<p align="justify">Although the Bible sternly warns us about using God's name in
vain and the importance of respecting our Lord, is referring to God as &quot;the
man upstairs&quot; truly respectful? Certainly cursing God or saying
&quot;Jesus&quot; as a figure of speech when we are startled or angry is wrong.&nbsp;
But what about saying &quot;Honest to God&quot; when we are not really concerned
about God's will? Or what about &quot;Oh my God&quot; when we are not
thinking about Him? I tend to take a pretty conservative viewpoint&#8212;that
all these expressions should be avoided by serious followers of Jesus Christ.</p>
<p align="justify">But vulgarity and crude language are not so easy to address.&nbsp;
For one thing, it can be hard to determine exactly what is and is not vulgar.&nbsp;
Many boys (and, much to our chagrin, many of us men) are by nature gross.&nbsp;
We dads have the task of civilizing our sons without feminizing them.&nbsp;
There does seem to be a gross side to masculinity. Where do we draw the
line between gross conversation and vulgar conversation? Where do
Christians draw the line between what the boys can talk about with the boys,
what is appropriate for a mixed audience, and what is appropriate for no
audience?</p>
<p align="justify">The Scriptures do address some of these issues, but many of
our decisions must be based upon the culture in which we live. For
example, we see Biblical verbiage that I would never use in our culture.&nbsp;
Here are just a couple of cases in point (some from the KJV, because later
versions translate these expressions less literally for obvious reasons):</p>
<p align="justify"><b>NIV Galatians 5:11-12</b>, &quot;Brothers, if I am still
preaching circumcision, why am I still being persecuted? In that case the
offense of the cross has been abolished. As for those agitators, I wish they
would go the whole way and emasculate themselves.&quot;</p>
<p align="justify"><b>KJV Malachi 2:3</b>, &quot;Behold, I will corrupt your seed, and
spread dung upon your faces, even the dung of your solemn feasts; and one shall
take you away with it.&quot;</p>
<p align="justify"><b>KJV Philippians 3:8</b>, &quot;Yea doubtless, and I count all
things but loss for the excellency of the knowledge of Christ Jesus my Lord: for
whom I have suffered the loss of all things, and do count them but dung, that I
may win Christ.&quot;</p>
<p align="justify"><b>NIV Luke 23:29</b>, &quot;For the time will come when you will
say, `Blessed are the barren women, the wombs that never bore and the breasts
that never nursed!'&quot; (not vulgar but considered &quot;uncomfortably&quot;
worded)</p>
<p align="justify"><b>KJV 1 Samuel 25:22</b>, &quot;So and more also do God unto the
enemies of David, if I leave of all that pertain to him by the morning light any
that pisseth against the wall.&quot;</p>
<p align="justify">As you could imagine, these expressions would be thought of
as coarse or base in our culture. So we may conclude that some of our
definition about vulgarity is definitely cultural. But are we without
guidelines? I think not. A few passages offer us some good practical
direction. The first passage sets some boundaries:</p>
<p align="justify"><b>NIV Ephesians 5:3-4</b>, &quot;But among you there must not be
even a hint of sexual immorality, or of any kind of impurity, or of greed,
because these are improper for God's holy people. Nor should there be
obscenity, foolish talk or coarse joking, which are out of place, but rather
thanksgiving.&quot;</p>
<p align="justify">This passage is pretty clear that anything we call &quot;off
color&quot; (meaning sexual or filled with sexual innuendos) is taboo for the
believer. It is not that Christians need to be inhibited or uncomfortable
talking about sexual matters with their spouses, but, rather, they respect their
sexuality and human dignity. They also respect the privacy and sexuality
of others. Unfortunately, we live in a day of horribly untalented comedians.&nbsp;
Just as a dying regime changes its strategy when desperate, so comedians resort
to off-color humor to camouflage their shortage of truly funny material.</p>
<p align="justify">But what about other vulgar language that is nowhere
condemned in Scripture, but varies with culture? Let's bring up that
second Scripture:</p>
<p align="justify"><b>NIV 1 Peter 2:12</b>, &quot;Live such good lives among the pagans
that, though they accuse you of doing wrong, they may see your good deeds and
glorify God on the day he visits us.&quot;</p>
<p align="justify">Although Christians should not put up a phony front or
conceal their true selves (as, unfortunately, some of our well-meaning
evangelical brethren pressure us to do), we do have to be concerned&#8212;at least
somewhat&#8212;as to how we come across. As a result, we need to not only
avoid that which is absolutely wrong, but also that which is culturally
offensive. God had no trouble using toilet language, for example, in the
instances mentioned above. In our society, that would be considered
vulgar. Yet, among a group of boys, a moderate level of such gross
conversation might be permissible. But talking like a low-classed slob does
little to make the Gospel appealing, (<b>NIV Titus 2:10</b>, &quot;so that in every
way they will make the teaching about God our Savior attractive&quot;).</p>
<p align="justify">So it is tough, friends. Should Christians use such
expressions as &quot;hell&quot; or &quot;puke&quot; or a host of other
questionable terms? Some of that will be a matter of judgment (I do not
use &quot;hell&quot; because, to me, it mocks a reality that many deny, but
&quot;puke&quot; is fine by me&#8212;that is, the term). But a good rule of
thumb is this: use more respectful terms that communicate without offending.&nbsp;
Play it safe.





