<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 8
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>When the people of Israel had turned to Baal, 
You brought Your prophet Elijah to challenge the prophets of Baal and to force 
the people of Israel to a decision. When two altars were built, Baal’s would 
not ignite, but when it was Your turn, You sent fire from heaven. The people 
then rose up; for You, Yahweh, are the true God. Although my choice is 
different, it is still a choice: I have to choose between honoring You each day 
and honoring my sinful nature. During the day, I am often called to make that 
same choice again. O Lord, help me to choose You <i><b>consistently</b></i>. Help me 
remember that You are the God Who answered by fire.</p>
<p>Father, sometimes I think that my life is too complex. The 
technology around me, the red tape it takes just to live daily life, all the 
time I spend in maintaining my family, my surroundings, and myself overwhelm 
me. And the more I am absorbed in these things, important as they may be, the 
easier it is to forget You. Help me to meditate upon You and Your Word  
especially while doing the menial things of life.</p>
<p>Almighty One, I could thank You for many things, but two of 
them are: __________.</p>
<p>Lord, I want Your Spirit to fill me, so I choose to yield myself to You. May 
I&nbsp;find my strength in His power not my own. May Your Spirit empower me&nbsp;today.</p>
<p>King of kings, help me to become a more mature Christian. 
This does not happen in giant leaps, but by small and steady steps. Help me 
take those steps. I pray for some friends at church, that they might grow a 
little more every week: __________.</p>
<p>Father, I remember the nation of Mexico. Thank You that 
6.7% of the population claims to be born-again evangelical Christian, but 
somehow it seems that number should be greater. Lord, please double that 
percentage within the next decade. The Mexican media portrays evangelicals as 
anti-Mexican spies and claims they are destroying Mexican culture. Yet the 
evangelical church is growing at a good clip. Father, keep working in this 
land; I pray for spiritual breakthroughs especially in Mexico City. May the 
many minorities hear the Gospel and may Your Spirit work mightily.</p>
<p>My Creator, I praise You for Worldwide Tentmakers, Inc. 
whose purpose “is to assist local churches in the promotion, preparation and 
placement of self-supporting witnesses worldwide.&nbsp;A tentmaker is a dedicated 
Christian man or woman living and working under nonreligious auspices for the 
express purpose of using their secular position to witness worldwide for Jesus 
Christ.” Thank You for their work in China, Croatia, Poland and Scotland. Grant 
harmony to the missionaries, give them fruit for their labor, safety, provision 
and the power of Your Holy Spirit.</p>
<p>Lord, I pray for those who work as riggers. Give them safety on the job, help 
them do their work well and may many in this field come to know You and live 
for You. Help those who know Christ to set a good example and to share the 
claims of Christ with others, I pray in Jesus’ Name, Amen.</p>

