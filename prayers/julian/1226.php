<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 26
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>When You worked Your wonders in Egypt before 
Pharaoh and the Egyptians, You were not merely content to deliver Your people 
Israel from Egyptian bondage. You had a full plan: to prove that You were God, 
to judge the arrogance of the Pharaoh and the cruelty of the Egyptians, to judge 
the false gods of Egypt and demonstrate how impotent they were, to deliver the 
Hebrews and any others who wished to join them, and to provide them with wealth 
(<a href="javascript:scripture('../../scripture/ex667.htm')" target=_self class=pop>Exodus 6:6-7</a>, 
<a href="javascript:scripture('../../scripture/ex32122.htm')" target=_self class=pop>Exodus 3:21-22</a>).</p>
<p>Lord, though they had to wait 400 years, when deliverance 
came it came thoroughly and decisively. So we too await the return of our 
Savior, the Lord Jesus. We have just celebrated His first coming; we eagerly 
await His second. Eventually, He will establish His Kingdom. His will shall be 
done on earth as it is in heaven. When He returns, He will likewise do a 
thorough job as He brings in righteousness.</p>
<p>Lord, it is sometimes frustrating to know how to deal with 
people. Some people are sincere, open and forthcoming; others are deceptive, 
dishonest and manipulating; yet others are so tightlipped that I have no idea 
what is really going on. On the one hand, You want me to make an effort to 
encourage or help those who are on the level and open to truth; on the other 
hand, You warn me not to waste my time by casting my pearls before the swine. 
Please, Lord, give me discernment when it comes to determining where I should 
place my efforts.</p>
<p>I pray for my lost friends and family members, including: __________. Father, 
please send Your Spirit to draw them to Yourself and 
grant&nbsp;them the repentance that leads to life.</p>
<p>I pray for Christians who are straying from their first 
love or compromising their walk with You. Rekindle the faith they once professed 
and help them fervently love the Savior once again. Remove their boring view of 
serving You and help them see the adventure and fulfillment of walking daily in 
Your light. Stir up their hearts and help them make the routines of godliness a 
part of their lives. I especially remember: __________.</p>
<p>Abba, I pray for the Pacific island nation of New 
Caledonia. Thank You that 7.5% of the population claim to be born-again 
evangelical Christians. May that number double over the next decade, I ask. 
Bless the seven Bible translation efforts that are ongoing among these language 
groups and provide survey and translators for the other twenty-one, if that is 
Your will, O Lord. May the Christian churches that have strayed away from the 
faith of the Bible return to their roots.</p>
<p>I pray for the missionaries my home church supports. Bless, prosper, use and 
empower them, I pray. Help these families to function 
well as families. May Christ be honored at home as well as on &quot;the field,&quot; for 
our homes are our most important mission field.</p>
<p>Now help me trust in You today when I am prone to worry, I 
ask in Yeshua's Name, Amen.</p>

