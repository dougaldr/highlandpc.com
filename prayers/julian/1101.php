<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 1
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the God Who wants to be known and is 
knowable. Although we cannot know all about You등e will be learning about You 
for eternity without exhausting Who You are등e can know You as our friend. 
This is why Jeremiah tells us, &quot;Let not the wise man boast of his wisdom or the 
strong man boast of his strength or the rich man boast of his riches, but let 
him who boasts boast about this: that he understands and knows me&quot; (<b>Jeremiah 
9:23-24</b>).</p>
<p>O Lord, keep me from being like those who find their 
purpose in life apart from a relationship to You, &quot;My people have committed 
two sins: They have forsaken me, the spring of living water, and have dug their own 
cisterns, broken cisterns that cannot hold water&quot; (<b>Jeremiah 2:13</b>). O Lord, only 
You can satisfy. Whatever we attempt to put in Your place is like a broken 
cistern. Father, help me to believe this, for I must confess that sometimes my 
single-mindedness gives way to a divided heart. So, like David, I pray, &quot;give 
me an undivided heart, that I may fear your name&quot; (<b>Psalm 86:11</b>).</p>
<p>Father, today I pray for the world's intelligentsia 
class: our professors, top authorities, intellects, philosophers and scholars. 
I also pray for the entertainment industries that take the thoughts of the 
intelligentsia and spread them at a popular level.</p>
<p>O Lord, You call mostly common folks. Your Word states, 
&quot;Not many of you were wise by human standards; not many were influential; not 
many were of noble birth&quot; (<b>1 Corinthians 1:26</b>). Father, although Your Word says 
&quot;not many,&quot; this does not mean &quot;none.&quot; You have always called some from the 
intelligentsia to Yourself. O Lord, we pray that You might stretch this verse 
to full stretch. We pray also that many who already know You would take the 
helm as our society's great thinkers, as in the days of Jonathan Edwards. We 
also pray that You would get hold of the entertainment industry. Help them to 
disseminate a worldview reflecting the Judeo-Christian viewpoint, I ask.</p>
<p>Help Your children to restrict their participation in 
entertainment to that which is decent. Father, so many Christians feel 
constrained to see the movies or listen to the music that everyone else sees or 
listens to. Help true Christians to develop a selective mentality, I pray.</p>
<p>I pray for the nation of Denmark. Thank You for the 4.8% of 
the population that claim to be born-again evangelical Christians. Many of the 
people claim a formal form of Christianity, but they do not attend church and do 
not have a personal, living relationship with the Risen Christ. Please use the 
Jesus Film, the Danish Bible Society, and may Your Spirit also empower the new 
and promising Christian youth movement.</p>
<p>Lord, please bless Village Ministries International and 
their work in Africa, Asia, Central America, Eastern Europe and North America. 
Lord, as they seek to go into all the world, evangelize the lost, and teach them 
God's Word, I pray that You would prosper their work. Grant them much fruit, 
harmony, safety, provision and the power of Your Spirit, I ask. Help them as 
they, &quot;locate and fully equip nationals...for ministry so that they may 
evangelize and teach their fellow countrymen.&quot;</p>
<p>May I live in a 
way that pleases You today, I ask, in Messiah's Name, Amen.</p>

