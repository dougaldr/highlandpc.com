<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 13
<br><span class=byln>by Ed Vasicek</p>

<p>Thank You, Lord, that You are Yahweh–nissi : “the Lord my banner.” When the 
children of Israel fought their adversary, God wanted them to remember that He 
had fought for them and was on their side. He was their banner, their flag 
identifying where their allegiance was, whose soldiers they were 
(<a href="javascript:scripture('../../scripture/ex1715.htm')" target=_self class=pop>Exodus 17:15</a>). 
So we Your children, Lord, are among Your armed forces though we are 
not armed with weapons for physical warfare. Instead they are spiritual 
weapons, for we do not fight against flesh and blood.</p>
<p>Lord, I pray for balance in my life. Your Word challenges me in so many 
areas, it seems like I am on a sailboat sometimes running from one end to the 
other to keep it from tilting into the water. If I grow in discipline, I find 
myself becoming less compassionate. If I focus on godly behavior, my zeal for 
You dissipates. If I focus on overcoming the world, I start viewing lost people 
as the enemy rather than victims of the enemy. I remember the example of Jesus 
who socialized with tax collectors and sinners, but I also remember “bad company 
corrupts good morals” (<b>1 Corinthians 15:33</b>).</p>
<p>Gracious Master, some of the people I have to deal with are 
unpleasant or unimaginative. Sometimes my own personality defects make me 
uninviting! So help me, Lord, to be patient with others but to humble myself 
and check my own arrogance or rudeness.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. Help me to remember that my faith is like an anchor to my soul. It 
reaches through the deep and troubled waters of life and makes a firm 
connection with the Rock of Ages, the Lord Jesus Christ.</p>
<p>Lord, I remember the small European nation of Monaco. Even 
though it is tiny, many vacationers travel through this land. I pray that the 
meager statistic—namely that 1% of the population claims to be born-again and 
evangelical—would triple over the next decade. Also, bless the ministries 
that use Monaco as a production base to reach Muslim countries with the claims 
of Christ. Lord, multiply the “follow-up” ministries that help disciple new 
believers, I ask.</p>
<p>Lord, bless the ministry of AIDS Orphans and Street 
Children “established in 1993 to help care for the nearly 38,000 children who 
are living in sub-Saharan Africa that have been left orphaned (or who do not 
have a sustaining family) because of the HIV/AIDS virus. AOSC runs an orphanage 
in Mozambique and operates AIDS Orphans Rescue Units in several sub-Saharan 
African nations. Food, clothing, shelter, discipline, Bible studies and 
practical training in such things as sewing, cooking, construction, gardening, 
farming and mechanics are provided. [Their] goal for these children is to grow 
up to be self-sustaining in their adult life.” Lord, empower these workers, 
provide for them and may many of these children come to know Jesus and live 
responsible and productive lives, I ask.</p>
<p>Now help me to love You today, I ask in Messiah’s Name, 
Amen.</p>
