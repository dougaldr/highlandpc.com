<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 16
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a great God. You are grand, majestic, peerless, 
glorious, universally acclaimed and completely noble. You surpass our 
understanding and transcend our logic. Nonetheless, everything about You makes 
sense. Just as an infant cannot understand nuclear physics, so we cannot 
understand You. But because we cannot understand does not imply that You are not 
reasonable, consistent and logical—for You are. You are simply too awesome 
for us, too great for us to fathom.</p>
<p>Father, create a hunger in me—a hunger for Your Word. 
Sometimes my spiritual appetite is sated because I have filled it with junk 
food. At other times, my heart is focused elsewhere. But Your Word is such a 
priority that You put it on a par with Your Name, for the Psalmist wrote in 
<b>Psalm 138:2b</b>, &quot;for you have exalted above all things your name and your word.&quot; 
May I be like the wise man of 
<a href="javascript:scripture('../../scripture/ps1.htm')" target=_self class=pop>Psalm 1</a>
who delights in Your Word day and night.</p>
<p>God, sometimes I struggle with doubts. Doubts about You, 
Your love for me, Your Word, or my salvation. But my doubts can be good. They 
can drive me to find answers, to empathize with the doubts of others, to think 
more deeply and to remind myself that faith is choosing to believe despite my 
doubts. Like the man who asked Jesus to heal his son, I too say, &quot;I do believe; 
help me overcome my unbelief!&quot; (<b>Mark 9:24</b>). So help me not to deny my doubts 
but to address them.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I 
find my strength in His power not my own. May Your Spirit control me 
today.</p>
<p>Father, You have done a great work in South Korea. Nearly 
16% of the population is made up of born-again evangelical Christians. Ten of 
the eleven largest churches in the world are in Korea's capital, Seoul. Korea 
is second only to the U.S. in the number of missionaries it has sent out. We 
pray that the growth and success of the Korean church would continue forward 
and that more people would come to know Christ. Help the believers to hold high 
moral standards and speak out against corruption. And Father, I pray that Your 
Spirit would protect and multiply the Korean Church. May what happened in Korea 
occur in many other nations!</p>
<p>Today I remember AMG International, &quot;an evangelical mission and relief 
agency currently ministering in 55 countries of the world.&quot; Their mission is 
&quot;to give each person at least one opportunity to hear and respond to the 
gospel.&quot; Bless their efforts and empower them with Your Spirit as they &quot;endeavor 
to meet people at the point of their need 
whether it be hunger, sickness, or hopelessness and show them Jesus as the only 
answer to the problems of this life and the only hope for eternal life.&quot; Grant 
them harmony amongst themselves and provide for their needs bountifully, I ask.</p>
<p>Now help me be on the lookout for opportunities to minister Jesus Christ to 
others, I ask in Jesus' Name, Amen.</p>

