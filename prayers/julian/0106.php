<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 6
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great Heavenly Father,</p>
<p>Thank You for Your mercy! If Your grace is blessing me in 
ways I do not deserve, then Your mercy is not punishing me in ways I do 
deserve! Thank You for Your pity and Your personal care for me.</p>
<p>Lord, I thank You that though I am fallen, You created 
mankind in Your image. This does not mean You are a physical being, for Your 
Word says You are a Spirit. It does mean that I have a conscience, that I have 
a moral nature, that I have a spiritual side to me, that I am creative, can 
think, make decisions, and have feelings—just like You, only on a limited 
scale! Even though Your image in me has been messed up by the Fall, thank You 
that You are in the process of restoring me because of Jesus Christ.</p>
<p>I have to confess, Lord, that sin is still a strong part of 
who I am. I sin both consciously and often without even realizing it. Your 
Word tells me that if I confess my sins, You are faithful and just to forgive me 
of them, and You will cleanse me from all unrighteousness (<b>1 John 1:9</b>). So I 
bring my sins before You now. I do not excuse them, but I freely admit them and 
ask for Your forgiveness.</p>
<p>Please address the following needs: __________.</p>
<p>Lord, today I pray for the heavily Muslim nation of 
Algeria. Evangelical Christians make up less than one half of one per cent in 
this nation. Oh, God, create a hunger for You, the true God, in that land. We 
pray that the number of born-again believers would triple in this next year. 
Give tact, wisdom, and appropriate boldness to the believers. We pray that Your 
Spirit would convict and draw tens of thousands to yourself this next year.</p>
<p>Today I pray for HCJB, World Missionary Radio Fellowship. 
Use their broadcasts to reach and disciple many. Provide workers, safety, 
harmony, health, finances, and equipment with gracious extravagance. Minister 
to the families who make this ministry happen.</p>
<p>Locally, I ask you to bless our school system. Help the 
board, the administrators, and the teachers to know You and embrace a Christian 
worldview.</p>
<p>Encourage me 
today, I pray, in Jesus' Name, Amen.</p>

