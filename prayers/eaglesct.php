  <p>Our gracious God and Father,
  <p>We thank you for this special day in the life of Carl Christenson.
  Today we celebrate the accumulated efforts Carl has made over the years
  to advance in scouting.  We think of all the Merit Badges he earned along
  the way, the oaths he learned, the character he developed, and his service
  to our community and fellow scouts.  Little by little, month by month and
  year by year he was faithful,  and today we celebrate this marker of
  faithful and hard work.
  <p>So we ask You, our Lord, to bless Carl, his family who supported and 
  encouraged him; we ask your blessing to fall upon his fellow scouts whose
  fellowship he enjoyed along the pathway.  We thank You for the scout 
  leaders who gave so much time and energy to develop young men like Carl.
  Bless them with the certainty that their work has influenced the course of
  many lives. We all benefit because of their sacrifices and leadership.
  <p>And so our God, we pray that you would bless this celebration as we rejoice
  with Carl.
  <p>We pray in Jesus’ Name, Amen.</p>

