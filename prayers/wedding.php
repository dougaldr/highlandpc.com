<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Christian Wedding
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Father,</p>
<p>You are certainly a God of grace and compassion. When we 
see the beauty of love between a man and his bride or a woman and her husband, 
we recognize this is truly a gift from You. Human love is a reflection of your 
greater love, and the love that commits for a lifetime is even closer to Your 
faithful loving kindness.</p>
<p>As these two come now to be melded together into a new 
unity, we know, Lord, that they will not and should not completely discard their 
individual identities. They need Your grace to guide and enable them to fulfill 
the lofty vows of this ceremony, especially when they do not see things the same 
way. They need your Spirit to put their commitment to practical work in the 
daily grind of life after the honeymoon.</p>
<p>We pray that you would help this couple to be humble before 
You—and before one another. Help them to avoid the impossible trap of trying 
to jostle for influence. Help them to remember their own faults and their own 
sins with vivid clarity, but to be absent-minded and hazy when they try to 
recall the wrongs done by their partner. Dissipate pride, and remind them that 
it is vain to try to change one's spouse; one can only change one's self, and 
even that by Your grace and with great difficulty. May this couple be 
co-operative, meeting one another half-way, exemplifying the art of compromise.</p>
<p>We pray that You would grace their days with blessings, 
health, prosperity, and a sense of camaraderie. May an attitude of team-work 
replace any tendencies toward competition. Father, like the Lord Jesus Christ, 
help them to look out not only for their own interests, but also for the 
interests of others. Let this attitude begin at home and, though it may extend 
beyond the home, may it never leave the home. As they form a new family unit, 
may they guard their marriage and enjoy one another, but may they make friends 
and connect to others in their church and in their community. May they not hide 
their light under a bushel, but may they contribute toward influencing their 
world for the Kingdom of God. May others see their marriage and be drawn to the 
Sovereign of their marriage, the Lord Jesus Christ.</p>
<p>As stresses conspire to pull this couple apart, may they 
look to You to help draw them back together. May the storms of life bind them 
together in greater unity. May they celebrate the simple pleasures of life 
together and enjoy life in both a full way and a godly way. Bless them with a 
richness and quality of depth that testifies to Your grace.</p>
<p>We pray in Jesus' Name, Amen.</p>

