<p>In today&#039;s sermon, we see the account of a father who pours 
his heart out to Jesus because he craves to have his son healed of demonic 
possession. You cannot help but seen how the father extends himself with all he 
has on behalf of his son. As a rule, the Jewish people took the role of 
fatherhood as a sacred trust, a primary calling of life. As a result, when the 
Jews walked with God, they exemplified solid morality, strong family, and lived 
balanced, meaningful lives. Verses like the ones below presuppose a culture 
where dads take their roles to heart.</p>
<p>

<b>Hebrews 12:5-10</b> &quot;And you have forgotten that word of encouragement that 
addresses you as sons: 'My son, do not make light of the Lord's discipline, and 
do not lose heart when he rebukes you, because the Lord disciplines those he 
loves, and he punishes everyone he accepts as a son.' Endure hardship as 
discipline; God is treating you as sons. For what son is not disciplined by his 
father? If you are not disciplined (and everyone undergoes discipline), then you 
are illegitimate children and not true sons. Moreover, we have all had human 
fathers who disciplined us and we respected them for it. How much more should we 
submit to the Father of our spirits and live! Our fathers disciplined us for a 
little while as they thought best; but God disciplines us for our good, that we 
may share in his holiness.&quot; </p>
<p>

<b>Psalms 103:13</b> &quot;As a father has compassion on his children, so the L<span class=sm>ORD</span> has 
compassion on those who fear him.&quot;</p>
<p>Alfred Edersheim, a 
Messianic Jewish scholar who wrote over a century ago, penned these words:</p>
<blockquote>The 
general state of Jewish society shows us parents as fondly watching over their 
children, and children as requiting their care by bearing with the foibles, and 
even the trials, arising from the caprices of old age and infirmity. Such 
things as undutifulness, or want of loving consideration for parents, would have 
	wakened a thrill of horror in Jewish society. As for crimes against 
	parents…they seem happily to have been almost unknown....<p>The 
Rabbinical ordinances, however, also specified the obligation of parents, and 
limited their power. Thus a son was considered independent whenever he could 
gain his own living; and, although a daughter remained in the power of her 
father till marriage, she could not, after she was of age, be given away without 
her own express and free consent. A father might chastise his child, but only 
while young, and even then not to such an extent as to destroy self-respect. 
But to beat a grown-up son was forbidden on pain of excommunication… [Edersheim, 
Alfred, <u>Sketches of Jewish Social Life</u>, p.99]</blockquote>


