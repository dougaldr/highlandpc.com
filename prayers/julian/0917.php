<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 17
<br><span class=byln>by Ed Vasicek</p>

<p>My Father in heaven,</p>
<p>Lord, You are a &quot;consuming fire.&quot; If I put a knife in the fire, it is 
purified and clean; so You, represented by the fire, are pure. You are 
completely clean and holy. When Moses saw You represented by the Burning Bush, 
he marveled that the bush burned but was not consumed. So You, O God, are 
eternal and Your holiness cannot be quenched.  As a fire separates the 
impurities from fine gold, so Your gaze sees through all and removes the dross 
from the precious metal. The fire represents Your wrath and judgment, so we fear 
You. The fire represents Your guidance, as You led the children of Israel by the 
Shekinah of cloud during the day and a pillar of fire by night.  When Your 
Spirit was given on the Day of Pentecost, tongues of fire appeared over the 
heads of the disciples.  Fire represents the zeal we have for You and the 
cleansing work of Your Spirit.  Fire is a form of power and represents the 
power of Your Spirit.  You are a consuming fire, O Lord.</p>
<p>O Father, sometimes I need to have the fire rekindled in 
me.  I know I cannot live the Christian life with constant enthusiasm in high 
gear.  I know the difference between adrenaline and Your steady zeal.  The 
feelings I experienced as a new believer cannot be forever maintained.  But I 
can be aglow with love for You, Lord.  So please light the fire in me once 
again.</p>
<p>Thank You, Lord, for taking me from the Kingdom of Darkness 
into the Kingdom of Light.  You have made me a citizen of this new kingdom and 
enlisted me in Your army.  Then You dropped me back again behind enemy lines.  
As I live in the Kingdom of Darkness, help me remember my citizenship is not 
here; I am forever a foreigner in the Kingdom of Darkness.  I am an advance 
scout for the Kingdom of Light.  You, O Lord, are at the door waiting to 
return.  And so I live in the intersection between the Kingdom of Darkness, the 
Kingdom of the past—and the Kingdom of Light which will break forth perhaps very 
soon.  Help me maintain my identity and loyalty with that coming kingdom.  I 
pray, “Thy kingdom come, thy will be done on earth as it is in heaven.”</p>
<p>Thank You for The International Christians Centers for the 
Deaf as they minister in the U.S., Mexico, and Cuba.  Bless them as they, 
&quot;plant churches for the deaf: to provide pastoral care for the deaf; to involve 
the deaf in evangelism; to involve the deaf in church ministry; and as they 
establish schools: to bridge the communication gap to the deaf by teaching the 
students Sign Language in order to share the Good News of Jesus Christ, His 
salvation and abundant life which He offers to them; to give the deaf students 
an education, develop their God-given potential and a vocational skill.&quot; Lord, 
provide for, bless, empower and grant harmony to this ministry.</p>
<p>Now help me serve You today, in Jesus’ Name, Amen.</p>

