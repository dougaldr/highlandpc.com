<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>The Security of Money
<br><span class=byln><b>Notes for Matthew 6:25-34</b>
<br>by Ed Vasicek</span></p>

<p>When I preached my 
sermon last Sunday (February 17, 2002), I did not have time to get to my last point. I 
had proposed that Christ wants His followers to think differently from society 
at large when it comes to material things, money, and wealth. Although Jesus&#039; 
teaching method is “Rabbinic Oratory” with its exaggerations and use of the “hot 
and cold” method, we can summarize where we are in the materialism continuum by 
asking three questions.</p>
<p>We looked at the 
first, <b> <i>“Where Does Your Heart Camp?”</i></b> and the second, <i>“What Is Your 
Primary Source of Identity?”</i> (This section included 
<a href="javascript:scripture('../../scripture/matt61925.htm')" target=_self  class=pop>Matthew 6:19-25</a>.) 
Christ wants us to store up treasures in heaven and to find our identity in Him 
(and in the blessings of being a human in the image of God), not the sneakers we 
wear or the car we drive.</p>
<p>There is a real 
tyranny in having to succeed materially, having to be among the best. There is 
a real freedom when we are broken from the bondage of status and materialism. 
In some ways, we then can enjoy life—and even the things of this life—so much 
more when no longer enslaved to them. But now let me address the third 
question, <b><i>“How Do You Find Your Security?”</i></b></p>
<p><u><b>We worry because 
we feel insecure.</b></u></u></b> 
Although some forms of worry can be downright praiseworthy (e.g., 
<a href="javascript:scripture('../../scripture/1thes315.htm')" target=_self  class=pop>1 Thessalonians 3:1 with 5</a>),
other forms display a lack of faith (insecurity). <b><i>The 
temptation is to base our security on money rather than faith</i>.<i> Many 
people overspend or demand status items in a subconscious quest for security.
</i></b>There are many Proverbs that encourage us to plan ahead, to save for a 
rainy day, and even to save for our heirs, as 
<a href="javascript:scripture('../../scripture/prov1322.htm')" target=_self  class=pop>Proverbs 13:22</a>
recommends. I personally do not believe Christ is condemning a reasonable level of financial 
planning. I do think being obsessed in this area is another story. More 
isn’t necessarily better, nor is money to be our MAIN source of security. 
Money can only offer so much security. For example, someone could sue you and 
leave you broke!</p>
<p>The reason that 
the love of money is a “root of all kinds of evil” 
(<a href="javascript:scripture('../../scripture/1tim610.htm')" target=_self  class=pop>1 Timothy 6:10</a>)
is because money is connected to the lust for power (characteristic of the devil himself). 
If we have much money <b><i>but sense our vulnerability and therefore cultivate 
our relationship with God (and others)</i></b>, then money does not enslave us. 
If, on the other hand, we find our security primarily in the big bucks or status 
items, <b><i>we deceive ourselves thinking that we have escaped vulnerability 
and the rude awakening that we have not thusly escaped is rude indeed.</i></b><i>
</i>It is this &quot;drugging effect&quot; that inhibits many wealthy people from 
surrendering to the Lord. The key to it all is summarized in <b>Matthew 6:33</b>: &quot;But 
seek first his kingdom and his righteousness, and all these things will be given 
to you as well.&quot;</p>
<p><b>Ultimate security 
comes ONLY from a relationship to God.</b></p>
