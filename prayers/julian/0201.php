<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 1
<br><span class=byln>by Ed Vasicek</span></p>

<p>Creator of All,</p>
<p>I praise You that literally everything that I see has been 
made by You. The only thing not made by You is You, the uncreated One. All 
creation occurred by a conversation within the Persons of the Godhead. You 
said, &quot;Let us make man,&quot; or &quot;Let there be light.&quot; And by Your Word, You created 
all things out of nothing. You created space, dimension, time, matter, and 
spirit beings: angels. Then You created man, a being who is both physical and 
spiritual.</p>
<p>Although we humans cannot create something out of nothing, 
we do recombine what You have created. In that sense, we too are creative. 
When our creativity is wholesome, it showcases Your image in us. Help me, 
therefore, to welcome godly creativity and to nurture it in my life. Help me to 
think outside the box.</p>
<p>Thank You, Lord, that creation is heading somewhere. We 
wait for the return of Christ to rule during the Kingdom Age, and we anticipate 
the New Heavens and the New Earth.</p>
<p>I ask You to help me to think as a Christian should. Help 
me renew my mind. When I am anxious, help me present my burdens to You and 
trade them for Your peace. May I have Your calm when appropriate, and Your zeal 
and energy when appropriate.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control 
me today.</p>
<p>Lord, thank You for working in the African Republic of 
Benin. Even though only about 4% of the population claims to be born-again, the 
number of believers is rising sharply. Thank You for using the Jesus film and 
youth ministries to bring in the harvest. We pray that the church in Benin 
would become the strongest in Africa, and that the believers there would be bold 
and consistent. Lord, there are many completely unreached people groups in 
Benin; burden the Christians there to reach out to them.</p>
<p>Father, I remember Chosen People Ministries, as they seek 
to share Yeshua (Jesus) with the Jewish people. We pray that growing numbers 
would receive Him as Messiah. Bless their efforts, give them wisdom, and 
provide them with much fruit for their labors.</p>
<p>Today I remember our nation's teachers: public school 
teachers, private school teachers, home schooling parents, and tutors. It is 
more and more difficult to teach children in groups these days because of all 
the broken homes. May our teachers feel appreciated.</p>
<p>Direct my paths this day, I pray, in Jesus' Name, Amen.</p>

