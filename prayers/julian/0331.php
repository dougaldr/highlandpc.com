<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 31
<br><span class=byln>by Ed Vasicek</p>

<p>Lord of Hosts, </p>
<p>You truly are the Lord of hosts—the multitude of the 
heavenly army. This vast force of angels, cherubim and seraphim, is at Your 
command. We cannot imagine their number, their design, or their work. Your 
children in glory, although no longer fighting battles, join the heavenly host 
in praising and worshipping You.</p>
<p>Thank You, Lord, that there is solidarity within Your 
body. Your church is made up of all genuinely born-again believers in Jesus 
Christ regardless of denomination. Although we acknowledge that You have Your 
children within many congregations, we particularly pray that You would lead 
them into congregations that sincerely teach the Word of God and seek to 
implement its directives. Help Your children know the difference between 
religion and the teachings of the Word of God.</p>
<p>Help me, Lord, to be a peacemaker and not a troublemaker. 
May I be a blessing to my church, my pastor and my friends. Keep me from 
instigating problems, from gossip, slander and disrespect for those in 
authority. When I disagree, help me to disagree agreeably. </p>
<p>Lord, today I especially remember those looking for a job. 
Open doors of opportunity and match the right person to the right job. 
Especially help those who work with integrity and have good character to find 
employment.</p>
<p>Also, dear Father, help me to handle my finances well. 
Guide me when it comes to saving, giving and help me know when to say no. Help 
me control my money under Your guidance; please do not let money control me.</p>
<p>Lord, I pray for the Faeroe Islands between Iceland and 
Scotland. Thank You that 28 percent of the population claim to be born-again. 
Since they are a self-governing region of Denmark, use them to reach the Danes.</p>
<p>I thank You, Lord, for the fact that nearly 23% of the 
population of the Falkland Islands—small islands near the tip of Argentina—claim to be born-again evangelicals. We particularly pray for the British 
soldiers who are stationed there, that Missions to the Military would reach them 
and that many would return to the UK as born-again believers.</p>
<p>
Lord, thank You for the ministry of Development Associates International. They 
are, &quot;committed to develop the integrity and effectiveness of Christian leaders 
worldwide so that the church can fulfill its role in extending the Kingdom of 
God&quot; as they focus upon 4 functions: educating through interactive adult 
education courses, mentoring, consulting and connecting. Bless them as they 
minister throughout the world. Keep them true to You and Your Word and provide 
them with harmony and the power of Your Spirit.</p>
<p>
Lord, bless those who work in the music field both secular and Christian. We 
know that music is a gift from God, so help musicians realize this. Help them 
to play, write and promote music that looks at life from a Judeo-Christian 
viewpoint. May many musicians come to know You and may those who do liberally 
share the good news of Jesus Christ.</p>
<p>
Lord, renew my mind today, I pray in Jesus' Name, Amen.</p>

