<p align="center"><b>Part IV: Finale and Final Questions</b></p>
<p>Thus far in our series on forgiveness, I have attempted to answer these
questions. Here they are, in review, with references.</p>
<ol type=1><li> <b><u> Do I have to forgive someone who has not repented?</u></b> Ed's
answer: No
<p>&#8212;God is our model; He does not forgive us if we do not repent&#8212;<b>Luke
17:3</b>, <b>1 John 1:9</b>, <b>Acts 5:3-5</b>; complete forgiveness may, at
times, be impossible without repentance anyway...</p>
<li> <b><u> How can I cope when I have ill feelings toward another?</u></b> Ed's answer:&nbsp;
It is not wrong to have enemies (<b>Matthew 5:44</b>), and our love for enemies
is the love of duty (the Golden Rule); we should not take our revenge, but it is
right to want God to take revenge and to find comfort in the fact He will (<b>Romans
12:19-21</b>) and even souls in heaven want revenge (<b>Revelation 6:9-10</b>),
so the desire for justice is not sinful. Sometimes it just takes a
quantity of time to get over something, as in the example of Jacob and Esau (<b>Genesis
32:4-6, 33:1-5</b>); take your bitterness to God in prayer, as David did in the
Psalms. It's tough, and there are no easy answers. Don't lie to
yourself and say you've forgiven people when you haven't. Manipulating
truth by word games and insincerity simply buries the feelings that you must
work out. There is no short cut.
<li> <b><u> When I wrong someone, besides asking forgiveness of them and of God, what
else can I do?</u></b> If possible, make restitution (<b>Luke 19:8</b>, <b>Romans
14:9</b>).
<li> <b><u> Are wrongs done in ignorance different than wrongs done in knowledge?</u></b>&nbsp;
Yes. Jesus asked the Father to forgive His crucifiers for crucifying the
Son of God because &quot;they know not what they do.&quot; They were held
accountable for crucifying an innocent man (see <b>Acts 4</b>), but not for
crucifying the Son of God because they were ignorant of His true identity.&nbsp;
We, too, are automatically cleansed from our sins of ignorance if we are walking
with the Lord, but must confess the sins we are aware of (<b>1 John 1:7-9</b>).
<li> <b><u> What about credibility? Do I have to trust someone once they
apologize?</u></b> No. Credibility can only be restored by faithfulness over
time. Forgiveness gives that person a chance to begin re-establishing that
credibility (<b>Luke 3:9</b>, <b>Acts 15:36-41</b> with <b>2 Timothy 4:11</b>).
</ol>
