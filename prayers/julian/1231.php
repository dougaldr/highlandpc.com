<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 31
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>Where has the year gone? The older I get, the 
faster time seems to go by. I can now understand the idea that for You, a 
thousand years is like one day 
(<a href="javascript:scripture('../../scripture/ps904.htm')" target=_self class=pop>Psalm 90:4</a>). 
Lord, You have been good to me. You 
are a faithful God.</p>
<p>Father, although we designate another holiday for 
thanksgiving, as I look back over the past years, I must cry out, &quot;Thank You, 
Lord!&quot; Yet I am a realist: not everything has lined up with my preferences. I 
would be pretending if I claimed otherwise. In Your sovereign will, You only 
sometimes answered many prayers in the manner in which I asked them. In other 
instances, You chose not to answer my prayers. Instead You always granted the 
Holy Spirit's petitions on my behalf because He never fails to pray outside 
Your will, as I sometimes do 
(<a href="javascript:scripture('../../scripture/rom82627.htm')" target=_self class=pop>Romans 8:26-27</a>). 
But in both cases, I praise You because Your way is the best way.</p>
<p>Like the hymn writer, I agree that, &quot;through many dangers, 
toils and snares I have already come.&quot; Yet I look forward to where Your 
&quot;amazing grace&quot; will lead me in the next year—and the years to come.</p>
<p>Father, as I stand on this marker between years, I can 
hope that our Lord Jesus will return soon. Come quickly, Lord Jesus. I am 
waiting for You!</p>
<p>I pray that this year will be a pleasant and blessed year; 
I ask for health, provision and good relationships for me and those I love. I 
pray that, a year from now, I will have grown in the grace and knowledge of our 
Lord Jesus Christ 
(<a href="javascript:scripture('../../scripture/2pete318.htm')" target=_self class=pop>2 Peter 3:18</a>). 
May this be a year of great spiritual strides and new levels of service!</p>
<p>So today I dedicate my life anew to You. I ask that You 
help me learn the hard lessons from last year's &quot;school of hard knocks&quot; so that 
I will be a wiser, humbler and more godly Christian. Keep my vulnerability ever 
before me so I do not become reckless or arrogant.</p>
<p>I pray for my nation, that its people would seek You and 
true righteousness. I pray that You would restrain the ungodly and bring freedom 
to people everywhere. Please put a halt to all religious persecution, I beg. I 
ask that more and more people would come to know You in this world and that 
Your Gospel would spread more quickly and powerfully than ever before. I pray 
that Your children would mature in their walk with You at faster rates and with 
firmer progress than has ever been seen. I pray that zeal and a growing 
maturity would become the norm, not the exception. Begin in my life, O Lord.</p>
<p>Father, as I close the books on this year, I approach the 
New Year with the mentality Paul encouraged Your children to embrace: 
&quot;Therefore, my dear brothers, stand firm. Let nothing move you. Always give 
yourselves fully to the work of the Lord, because you know that your labor in 
the Lord is not in vain.&quot;</p>
<p>I thank You for the past year and I boldly walk into the 
New Year holding Your hand!</p>
<p>In the majestic Name of Jesus I pray, Amen.</p>


