<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 28
<br><span class=byln>by Ed Vasicek</p>

<p>High and Lifted Up One,</p>
<p>You are a God who wants us to be honest with You. Like 
David, pouring out his complaints before You, so You want me to be an authentic 
person. You are eager to hear about my fears, my frustrations, my dreams and my 
dashed hopes. Friends of family can help me, but they are limited and can only 
take so much. But You invite me to haggle things out with You because You are a 
God who enjoys living life with His children. Although this is so different 
from the God many follow, it is Who You, Yahweh, the God of the Bible are.</p>
<p>Lord, help us to remember that we are not our own. Before 
we make a major decision, help us to realize that we do not have that right: You 
do. We are Your servants and You want to guide and direct us. So help us 
pray, consider the spiritual impact of our choices and get the counsel of the 
godly. Remind us that we are not free agents. We belong to You. We should not 
move without first consulting You. We should not buy a house or change jobs 
without consulting You, O Lord. We should not bounce from church to church or 
spouse to spouse. We need to submit our wills to Yours, O Lord.</p>
<p>Father, I pray for Christian students at secular colleges. 
O Lord, help them to get involved in Christian fellowships like
<a href="http://www.campuscrusadeforchrist.com/" target="offline" class="reg">Campus Crusade for Christ</a>,
<a href="http://home.navigators.org/us/" target="offline" class="reg">The Navigators</a>, or
<a href="http://www.intervarsity.org/" target="offline" class="reg">InterVarsity Christian Fellowship</a>.
Help them not to be afraid of questions, but help them seek and find 
satisfying answers from Christian websites like
<a href="http://www.christiananswers.net/" target="offline" class="reg">www.christiananswers.net</a>. 
May they return home with a stronger faith and 
may they lead many of their fellow students to faith in Jesus Christ. Help them 
not to isolate themselves, I ask.</p>
<p>Lord, over 6 million people live in the Indian province of Himachal Pradesh, 
yet only .09% claim any form of Christianity. This province 
draws many Hindus as they make religious pilgrimages and participate in 
idolatry. Yet there is some light shining in this darkness:
"In 1991 all known Christians were 4,435 in number 
and only 90 churches were functioning. Between 1992 and 1998 over 1,200 were 
baptized and 150 churches were planted&quot; (source: 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>). 
O Lord, please double the number of believers every year for the next 5 years, I 
ask. May Your Spirit grant many the repentance that leads to life.</p>
<p>Father, please bless Sisters In Service, “a global outreach 
ministry that focuses on women. SIS informs, mobilizes and equips U.S. advocates 
to extend God's love to women and children through local partnerships in the 
least reached places of the world. [Their] goal is to multiply laborers who 
focus on the ‘least of these,’ on women in unreached lands. In these cultures 
women are the least valued, least fed, least educated and least reached over 
all. [They] open outreach doorways in Iraq, India, China, the Middle East and 
Southeast Asia.” O Lord, prosper and bless this work. Provide for them, grant 
them safety and harmony and empower them by Your Spirit, I ask.</p>
<p>Empower me to walk with You today, I request in the Savior’s 
Name, Amen.</p>

