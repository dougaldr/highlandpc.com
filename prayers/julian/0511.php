<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 11
<br><span class=byln>by Ed Vasicek</p>

<p>Lord of Mercies and God of Comfort,</p>
<p>You are the Light of the World. Although nature reflects 
many truths about You, such truths are muted and confused by the effects of 
the curse of the Fall into sin. Nature may expound Your glory, majesty, power 
and reveal Your wrath, but 
Your works are only echoes of Who You are. You are Light and in You is no 
darkness at all—not one bit. You dwell in inapproachable light, yet You 
enlighten those who come to know Christ and who seek Your face. Your light 
speaks of Your purity, Your spirituality and Your distinction as the uncreated 
and uncaused Creator Who is separate from His creation.</p>
<p>As my Mighty Fortress and Rock of Defense, You, O Lord, are 
out for my best interest. Sometimes I doubt this. I am more prone to believe 
this in theory than in practice. As I sit in my cushioned chair, it is easy to 
say that my trust is in You, but in the trenches of daily life, I am often 
fearful, worried and mistrusting. Help me, O God, to let my convictions affect 
the realities of my daily life. May I trust You not merely on paper or with lip 
service but in truth.</p>
<p>I pray for my lost friends and family members, including: __________. 
Father, please send Your Spirit to draw them to Yourself and grant&nbsp;them the 
repentance that leads to life.</p>
<p>Omnipotent One, I pray for the nation of Kenya. Thank You 
for the religious freedom that reigns there and for the great response to the 
Gospel: almost 36% of the population claim to be born-again evangelicals, yet 
only 12% of the population attends church regularly. The problem of &quot;nominalism&quot; 
is growing, so Lord, please revive Your children there! Alongside this poor 
church attendance is the AIDS epidemic which infects about 14% of the 
population leaving many orphans as a consequence. O Lord, if Your people would 
walk with You, how many miseries would be averted!</p>
<p><b>Lord, I pray for those who are suffering from AIDS and 
other like diseases.</b> Although many have received these sexually-transmitted 
diseases through sinful behavior not all have done so. Innocent children, 
faithful spouses, or those receiving blood transfusions, etc. have all been 
struck with these diseases. But even among those who have become infected 
through practices which are against Your revealed will, we know that all of us 
have sinned in varying ways and to varying degrees; <b>when we turn from our sin 
and believe in the Lord Jesus Christ all our sins are forgiven</b>. We pray, Lord, 
that all who suffer from such diseases will come to know Jesus. We pray that 
You would heal them, according to Your will. If You choose not to heal them, 
give them better health and help them use their remaining days to serve You. 
Bless and guide those who serve these folks; thank You for their ministries of 
mercy and love. <b>Help all Your children avoid immoral practices and help the 
rest of us to not look down upon those who have such a past.</b></p>
<p>Now, God use me to serve You this day, I ask in Jesus' 
Name, Amen.</p>

