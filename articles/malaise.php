<p>Spring is that season of discontent. Discontent is not always bad,
especially if we become discontent with ourselves. Most often, we project
our discontent with ourselves at others: &quot;I'd be happy if I had married the
right person. I'd be more spiritual if I was in the right program or
church. I'd be in clover if I made more money, lived in another town, or
owned a different house.&quot; Discontent is on the &quot;what's hot&quot;
list during the season when the flowers bud. And this discontent is
usually nothing short of sin. But when we project our discontent toward
ourselves instead of others, the result can be profitable. The Bible encourages
us to &quot;humble yourselves before the Lord, and he will lift you up&quot; (<b>James
4:10</b>). What does it mean to humble ourselves in this way? I think it
means to face the source of discontent: our sinful hearts. When we humble
ourselves, we are more conscious of our transgressions and failings. Rather than
comparing ourselves with others and gloomily resenting how much worse we have it
than they do or how disappointed we are with life, we can choose to contrast
ourselves with God. Like Isaiah the prophet we can say, &quot;Woe is me, for I
am undone!&quot; God's holiness overwhelms us.</p>

<p>This time of year is ideal for humbling ourselves. True, the spring season
stirs up those juices of discontent, but the holidays surrounding the Passion of
Christ meld nicely with these seasonal vacillations. As we celebrate Palm
Sunday, we think of Christ not only as the King, but also as the rejected King.&nbsp;
The &quot;Hosanna&quot; crowd was sizable, but only a tiny percentage of Israel,
the nation in which &quot;He came to His own, and those who were His own did not
receive Him&quot; (<b>John 1:11</b>). Even today, most people reject Jesus as Lord of
their lives. That's tragic.</p>
<p>As we celebrate Good Friday, we contemplate it as a bittersweet holiday.&nbsp;
The
bitter agony of Calvary, a Messiah bleeding for our wrongs, is an astringent
thought. What happened at Golgotha was tragic beyond description, yet the result
of the Atonement will be a matter of praise and joy for eternity! The Lamb of
God suffered and shed His blood, yet &quot;by His stripes we are healed&quot; (<b>Isaiah 53:4</b>). 
However we describe the events of the original Good Friday, one
word covers it all: SOLEMN.</p>
<p>As we celebrate Good Friday here at HPC and participate in the Lord's Supper,
consider making these observances part of a seasonal agenda of humbling yourself
before the Lord. Spend extra time in self-examination and confession of sin.&nbsp;
Face the ugliness of your transgressions and the stubbornness of your heart.&nbsp;
Don't lash yourself with the whip of false pride masqueraded as humility
(<a href="javascript:scripture('../scripture/col223.htm')" target=_self class=pop>Colossians 2:23</a>),
for nothing is more humbling than pure truth. Simply face reality.</p>
<p>The truth is that God is holy and we fall short&#151;way short&#151;of His mark. But
God is gracious and has offered us the provision of His Son's sacrifice. We are
grateful, painfully aware of our unworthiness, yet convinced we have been
accepted as perfect by God
(<a href="javascript:scripture('../scripture/rom51.htm')" target=_self class=pop>Romans 5:1</a>,
<a href="javascript:scripture('../scripture/eph16.htm')" target=_self class=pop>Ephesians 1:6</a>).&nbsp;
After we have faced the darkness of our sin and the price Jesus had to pay for those iniquities, we are
then ready to exuberantly celebrate the Resurrection of our Lord and Savior, the
King of Kings and Lord of Lords. Because He lives, we too shall live.&nbsp;
Forgiveness and eternal life are guarantees God will keep because it is the
LIVING Savior who promises life to His sheep.</p>
<p>So this Spring, instead of trying to fix your life or take a fresh stab at
Utopia, try to humble yourself before God. Compare yourself to the holiness of
the Almighty: you'll lose the contest, but gain spiritual depth.

