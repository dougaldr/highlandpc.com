<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>Safety from Terrorism and for the Defeat of Terrorism
<br><span class=byln>by Ed Vasicek</span></p>

<p>O Lord, we are arrogant by nature. We like to think that disasters happen to
other people, not us. Names printed in newspapers or enunciated over air waves
and cable wires are just names.</p>
<p>Yet, Lord, we know that those names represent faces, real people. People who
have families, people who have friends, people who are in Your image. People who
have a spiritual nature and a sense of right and wrong; people who love and
hate, who both sin and do good deeds. Human beings just like us.</p>
<p>Father, the threat of terrorism has awakened us to a reality that has always
been: our lives are always a moment from death. The same is true about those we
love. The evil of terrorism has also reminded us that there is an eternity, that
those without Christ need Him desperately if they are to be prepared for the
certainties of death and eternity. We believers are rudely reminded that we need
to share Christ with a lost and dying world.</p>
<p>Dear God, we have also come to see that we do not all worship the &quot;same
God.&quot; You are very different from the false god of the terrorists.&nbsp;
You tell
us, when possible, to live at peace with all men. They prefer to exterminate
those who disagree with their beliefs and values. We try to win others by love
and reason, they by force, coercion, and humiliation. Oh God, we thank you that
we have recognized not only that there is one God, but that You alone, Yahweh,
are that true God&#151;as revealed in Jesus Christ. How desperately the world needs
to find You. How awful the consequences to all mankind when others replace You
with a false god of their own making!</p>
<p>Lord, we in &quot;invulnerable&quot; America now recognize our vulnerability.&nbsp;
The days of ease and relative fearlessness have been replaced by days of
uncertainty and caution. We are at war and we do not want to be at war.&nbsp;
But we
were not consulted. So here we are, fighting hijackers, bombers, and germs.</p>
<p>Oh God, help us to respond justly to the attack of evil and malicious men.&nbsp;
Help us not only to protect our citizens, but also to defeat the enemy. Guide
and guard our President, his cabinet, and congressional leaders. Guide and
direct our military. Help our soldiers to be wise, brave, and bold. Protect
them. Protect the innocent among the nations in which wisdom demands we operate;
aid us in defeating the guilty. Please, Almighty Sovereign, defeat terrorism.&nbsp;
Use us as Your instruments to do so.&nbsp;</p>
<p>Here at home, protect us, O Lord. Guide our government, including our local
law enforcement agencies, the FBI, the CIA, and citizens everywhere. Protect us
from terrorist attacks of all kinds. Send the hosts of heaven to our aid.&nbsp;
Comfort those citizens who have already been victimized.</p>
<p>In all our fears and tribulations, help us to examine our own hearts and
repent of prioritizing other things above You. Help us seek You first and come
into a personal relationship with You through your Son, our Lord Jesus Christ.&nbsp;
He died for my sins, to pay our penalty that You could justly forgive us. He
rose again that third day, declaring Himself to be God in the flesh. Let Him
rule in our lives.</p>
<p>Help us renew our commitments to spend time in the Word, in prayer, and in
church. May Your Spirit search our hearts and show us what needs to be changed,
where we need to repent. Let the tragedies of terrorism remind us of the
wickedness of the human heart, and then help us to face our own wickedness.</p>
<p>Lord, we pray that good would come out of this evil. Our country will never
be the same. We pray that some of those changes would be for the good, that we
will lean upon You more, submit to Your will as revealed in Your Word, and
become people of depth who value you and others more than material things or
constant thrills. Make us deeper people, Father, as we adjust to the realities
of life and death.</p>
<p>We pray in Jesus' Name, Amen.</p>

