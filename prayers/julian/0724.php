<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 24
<br><span class=byln>by Ed Vasicek</p>

<p>My Awesome God,</p>
<p>You are the God Who loves to reveal Himself. The heavens 
declare Your glory. Creation shows Your masterful engineering skills. You have 
revealed Yourself to the nation of Israel and to all mankind through Your Word. 
And one day, when Jesus returns to earth to reign, You will reveal Yourself 
again. Thank You for those moments or eras of revelation. We are a people 
drawn to You and we want to know our God, even if that knowledge includes some 
fear and trembling! One day we will see You and then we will understand more 
fully, but for now, thank You that we can look in a dim first century mirror 
(<a href="javascript:scripture('../../scripture/1cor1312.htm')" target=_self class=pop>1 Corinthians 13:12</a>). 
When we see You, the image will become sharp and clear.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. May I&nbsp;find my strength in His power, not my own. May Your Spirit 
control me&nbsp;today.</p>
<p>Father, time is so intimidating to me. Your Word counsels 
me to number my days 
(<a href="javascript:scripture('../../scripture/ps9012.htm')" target=_self class=pop>Psalm 90:12</a>). 
The older I get, the faster times seems to 
move. At 5 years of age, one year is 20% of our lives. At 50 years, it is 2%. 
And so it seems to pass ten times more quickly. It will not seem long before I 
will be absent from the body and present with the Lord 
(<a href="javascript:scripture('../../scripture/2cor58.htm')" target=_self class=pop>2 Corinthians 5:8</a>). 
So Lord, help me live in light of eternity. Help me complete the task You have for 
me on earth. When I am in heaven, I will not be able to lead the lost to 
Christ, I will not be praying for others, I will not need to be salt or light or 
concentrate upon my responsibilities to my family, neighbor, church, or 
workplace. I can only do those things now. Help me to glorify You in how I do 
them!</p>
<p>Today I remember the small Goa Province of India. The area 
has some forms of Christianity, but there are very few evangelical believers in 
this region. Please reach some future leaders who speak the Konaki language 
and multiply the number of those who know You.</p>
<p>I also pray for the Gujarat Province of India with its 50 
million people. This is a &quot;focal point in India for the persecution of 
Christians&quot; according to 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>. 
Despite the Hindu-led persecution, the evangelical church is growing in some regions 
though less than .5% of the population claims any form of Christianity. So many people 
groups are unreached in this Province! Lord, may the number of evangelical Christians 
rise to 5% over the next decade. Do a mighty work of Your Spirit there. Reveal 
Yourself to these people in a powerful way, I ask.</p>
<p>Thank You, Lord, for Radstock Ministries. Bless them as 
they &quot;equip and mobilize the local church for missions...to see local 
churches in North America develop a radical commitment to missions and an active 
hands-on involvement... Radstock seeks to build a multinational network of churches through 
which missions opportunities are made available.&quot; Bless their efforts, I ask. 
May they see churches with little or no missions concern jump in with both feet, 
I pray.</p>
<p>Use me today, I ask in the Savior's Name, Amen.</p>

