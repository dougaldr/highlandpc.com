<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 14
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>In <b>1 Timothy 1:17</b> I read, “Now to the King eternal, 
immortal, invisible, the only God, be honor and glory for ever and ever. Amen.” 
You are the eternal King. Your reign is not for a term or a generation, but for 
eternity. You are invisible to us. You are Spirit, and for us to see You as 
You are would mean death. Although You are present everywhere and especially 
present within me, I cannot see You with my eyes or feel You with my sense of 
touch. I cannot hear or taste You. You are beyond my senses. And You are the 
only God. Although mankind has imagined thousands of gods, human imagination 
cannot change reality. You, O Lord, God of Israel and Father of our Lord Jesus 
Christ, are the only true God. And so I join the multitude of the heavenly host 
and ascribe to You honor and glory, and I will praise Your Name forever and 
ever.</p>
<p>I pray for my pastor, staff and church board leaders, 
officers and teachers. If any do not know You, may they become born-again. 
Help them to be true to the Bible and to put Your Word over their own personal 
agendas. Help them to have genuine integrity, Biblical convictions, love, 
wisdom and courage. Help them put their walk with You first, their families 
second and their ministries third. Do not allow others to intimidate them into 
neglecting their families and remind them &quot;the fear of man works a snare.&quot; 
Help them be most concerned about pleasing You, while also seeking peace and 
harmony within the congregation.</p>
<p>Father, I have some personal health concerns including: __________. I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>Today I ask You to watch me as I travel. This is a 
dangerous world, and I pray for safety. May my vehicle(s) perform well and 
watch over me, I pray.</p>
<p>Lord, thank You that prayer changes me. Thank You that prayer often changes 
events. Although You will not grant our prayers when outside Your will, there 
are many things You will only give us <u>if</u> we pray. So often we have not because 
we ask not. So today, I am asking!</p>
<p>Father, today I pray for the Latin American island nation of Trinidad and 
Tobago. Thank You, Lord, that over 15% of the population claim to be born-again 
evangelicals. Yet, Lord, we know that many families are dysfunctional and the 
churches are often without clear direction or missionary vision. I pray that the 
quantity of the church would continue to grow—to double over the next decade. 
But I pray with even more burden that the churches would have a vision to reach 
out to other people groups and that the believers would grow in depth, a depth 
that affects their family lives.</p>
<p>Lord, Thank You for the work of the India Gospel League. Praise Your Name 
that You have used them to organize the planting of 7,000 churches since 1992! 
O Lord, help them as they evangelize, baptize and disciple. Grant them great 
fruit for their labor, protection, provision and the power of Your Spirit!</p>
<p>Empower me to serve You today, in Jesus’ Name, Amen.</p>

