<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 8
<br><span class=byln>by Ed Vasicek</p>

<p>Merciful Father,</p>
<p>You have been so good to me. If evil is the spoiling of 
the good, then all the good that comes to me comes to me despite the spoiling 
nature of sin and the curse. Every good gift is from above, from You, O Lord. 
Although the term “good” does not describe all You are, You are always good. 
You always act justly or graciously. My life is not always good, nor do I 
always see justice around me. But, Lord, many things in the short term are not 
as they seem. Help me to remember the big picture and the final results of it.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Lord, I know the church gathers so that believers can build 
one another up in their faith, the result of this is spiritual maturity as seen 
in faith, hope and love. I know it is my job to encourage other believers to 
do good works and to walk with the Lord. Help me to view a church meeting as a 
place to minister and give help to others, not just a place for me to receive. 
Help me to become a more mature Christian. This does not happen in giant leaps 
but by small baby steps. Help me make those steps. I also pray for the 
following friends or relatives, that they do would grow a little more every 
week: __________.</p>
<p>Lord, I remember the Indonesian Island of Sumatra. “Sumatra 
is the largest unevangelized island on earth. Most of its peoples are staunchly 
Muslim. If it were a nation, only 9 other nations would have more unreached 
peoples. Sumatra is the home of 52 known unreached people groups consisting of 
25 million people. Of the 52, 48 have no indigenous churches and 34 of them have 
no known gospel workers” (from the 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a> 
website). Evangelism was 
allowed among the animist peoples and many of them have come to know Christ, 
thus bringing the percentage of people who profess some sort of Christianity to 
over 10%. Thank You, Lord, for the great response among the animist groups; 
now, O Lord, may Your Gospel penetrate the Muslim population.</p>
<p>Thank You for Aurora Mission which &quot;is an evangelical, 
conservative, nondenominational mission agency whose purpose is to communicate 
the Gospel with the goal of evangelizing, discipling, establishing churches and 
providing training for pastors and church leaders.&quot; O God, please bless this 
ministry to Italy, another of Europe’s &quot;post-Christian&quot; nations. May 
Your Spirit grant many persons the repentance that leads to life and start a 
massive movement toward You. Provide for the missionaries, empower them by Your 
Spirit, grant them harmony, wisdom, protection and much fruit for their labors. 
Soften hearts and regenerate multitudes, I ask.</p>
<p>Help me serve You today, in Jesus’ Name, Amen.</p>

