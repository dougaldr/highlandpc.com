<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 4
<br><span class=byln>by Ed Vasicek</p>

<p>My Omniscient King,</p>
<p>You know everything. The knowledge You have is not 
acquired, as is much of ours. Animals have instincts and have knowledge that is 
not acquired, as do we. Our body functions before we ever learned how it does. 
So we can understand the concept of innate knowledge. But in Your case, it 
is infinite.</p>
<p>You cannot add to Your knowledge base, nor do You learn by 
experience like we do. You have always known it all; Your understanding is 
infinite. You know what You have determined to happen, what You allow others to 
choose and even what could happen if other choices were made. You are involved 
in the day-to-day life of each one of us and You know when even a sparrow 
falls.</p>
<p>Lord, I am thankful for plenty of things. You have been 
good and two of them include: __________.</p>
<p>Lord, may Your Spirit may fill me. May I&nbsp;find my strength 
in His power, not my own. May Your Spirit control me&nbsp;this day.</p>
<p>Lord, help my church be harmonious. May the leaders be 
firm when it comes to the clear teachings of Scripture and gracious about the 
not-so-clear matters. Keep antagonists, troublemakers and those who resent 
authority away from our congregation. Help my church to be effective in 
evangelism, discipleship, loving one another and supporting world missions.</p>
<p>Lord, when Joshua was ready to enter the promised land, You 
encouraged Him: &quot;Be strong and courageous. Do not be terrified; do not be 
discouraged, for the L<span class="sm">ORD</span> your God will be with you wherever 
you go&quot; (<b>Joshua 
1:9</b>). Lord, my life may not be the equivalent of entering Canaan, but, Lord, I 
am often intimidated, afraid, or insecure. Help me to remember that You are the 
same God as Joshua's God. You do not want me to &quot;be terrified...discouraged.&quot; 
Help me take the principle from this verse and bravely go on.</p>
<p>Lord, today I pray for some of the nations where 
persecution is an issue. Father, believers have to fear death, torture, 
dismemberment, separation from their families in prisons or concentration
camps. Lord, it breaks my heart to even think of such things. Today I pray for 
nations numbered 36-40 on the persecution chart of the 86 nations where 
persecution is an awful reality. I pray for: Oman, Dagestan (part of Russia), 
Iraq, Mauritania and Kabardino-Balkaria (part of Russia). O Lord, may the 
persecution stop, may the Gospel produce an enormous amount of fruit and may 
those who do the persecuting be won to the faith. Help believers evade 
persecution when possible; when not, Lord, provide them with the grace that only 
You can provide.</p>
<p>Lord, thank You for Ministries in Action, &quot;an 
interdenominational...organization...committed to strengthening local Churches, 
particularly in the areas of fellowship, discipleship and evangelism, theological 
training, economic development and works of mercy. Further, as a result of 
building coalitions and establishing partnerships with a large network of 
churches, church denominations, government officials and other faith based 
organizations, MIA initiates relief efforts when natural disaster affects our 
ministry area.&quot; Bless, provide, prosper and empower this important ministry, I 
ask.</p>
<p>Use me today, I pray in the Messiah's Name, Amen.</p>

