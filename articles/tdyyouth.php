<br>Today's Youth: New and Improved
<br><span class=byln>by Ed Vasicek</span></p>

<p>&quot;Remember your Creator in the days of your youth, before the days of
trouble come.&quot; (<b>Ecclesiastes 12:1a</b>)</p>
<p>Are today's youth better in some ways than youth from years gone by? I
have been reading tidbits here and there to the effect that more high school and
college age people have been volunteering to serve their communities and
churches. Indeed, it does seem that something good is happening among our
young people.</p>
<p>But this &quot;good thing&quot; that is happening is not happening to all
young people-perhaps not to most. Broken families take their toll.&nbsp;
Situation ethics—that there are no absolute rights and wrongs—these trends
devastate so many of our young people. But what does seem to be happening
is this: today's youth are polarizing. The good ones are better than ever,
the bad ones, worse than ever.</p>
<p>Working with our youth this summer (and other young people in recent years)
has been an eye-opener. Young people in greater numbers are more prone to
volunteer, to serve, to get down to serious business about serving God.&nbsp;
The clearest evidence of this is seen in our local Child Evangelism Fellowship
Organization. Three or four years ago, CEF only had two Summer
Missionaries. The number of Summer Missionaries this year is 25.&nbsp;
Count 'em, 25. You heard me. This number includes one of our own,
Matt Cooney.</p>
<p>A good number of our own HPC young people have helped in constructing the
Kokomo Rescue Mission's &quot;Open Arms&quot; shelter. They are interested
in understanding the cults, apologetics, witnessing, and missions. They
are less selfish than my generation.</p>
<p>Today's young person enters a world more hostile toward the Gospel than the
world I entered. Every belief is tolerated except ours. It did not
used to be that way. Because evangelical churches have compromised on even
the absolutes, many of our young people are not being as well grounded as they
could be. But many are solid and rooted in the faith.</p>
<p>When I finally reached the age of 18 in 1974, today's nasty trends were well
underway: drugs, promiscuity, R-rated movies, and abandonment of the
Judeo-Christian value system. But it has gotten worse since then, much
worse. Yet my perception is that God is raising up an army of
soldiers—special elite forces—that will be bold and strong for the Kingdom of
God. Many of these remarkable Christian young people hail from unusually
strong homes where parents lived their faith and parented by conviction rather
than simply following the trends. Others have come to faith from all sorts
of backgrounds but have seriously sought to follow the Lord. Oh, sure,
they have their unique problems and weaknesses. But, all in all, they are
a new—and I think improved—generation.</p>
<p>Over the next few years, I think we will see the polarization continue.
Christian youth will grow in zeal and dedication, but evil will correspondingly
grow more intense. It will be the devil or the Lord with few in-between.&nbsp;
But if there is a glimmer of hope for the future of the evangelical church, it
is the generation stepping into the batter's box. We have to keep them in
our solid churches and send them up through the ranks. We simply must.

