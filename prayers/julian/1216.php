<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 16
<br><span class=byln>by Ed Vasicek</p>

<p>My Heavenly Father,</p>
<p>When Joseph and Mary took Jesus to the Temple, they 
presented an offering of turtle doves. This was an offering used for someone 
who was not completely impoverished, but was of humble means. So we know that 
the wise men came after this event, when Jesus was more than 40 days old. They 
had not yet presented their gifts of gold, frankincense and myrrh.</p>
<p>As they traveled toward Israel, they did not know exactly 
where Christ would be born. His star appeared in the east and they knew that 
the Messiah had to be born in Israel. But that was all. So they stopped in to 
visit with King Herod thinking that the &quot;King of Israel&quot; would know where the 
Messiah was.</p>
<p>But Herod did not know. As a matter of fact, he feared that 
someone would compete for the throne with him and his heirs. So he feigned a 
pleasant disposition and called for the Bible scholars. These men knew where 
Jesus would be born, for the Old Testament is clear in <b>Micah 5:2</b>, &quot;But you, 
Bethlehem Ephrathah, though you are small among the clans of Judah, out of you 
will come for me one who will be ruler over Israel, whose origins are from of 
old, from ancient times.&quot;</p>
<p>So Lord, today there are people who pretend to be believers 
or pleasantly disposed, but sometimes they are like Herod, putting on a front to 
serve their own purposes. Help me not to be such a person. Sometimes I confess 
that I have put on false fronts. I am a different person in different 
environments and it frustrates me that I am this way. May sincerity be my habit 
and integrity my mainstay.</p>
<p>And Lord, Jesus’ family was content with humble means. Help 
me to be content with my lot and to live within my means, not my credit limit! 
Lord, materialism and things can be like a drug that gives me a temporary high 
until my next fix. Deliver me from it, O Lord. Even this Christmas season, help 
me to enjoy the simplicity of the Nativity and the blessings of family and 
people. It is You and others that bring real joy to my life. Remind me that the 
best things in life may not be free but neither are they expensive.</p>
<p>I pray for the nation of Denmark. Thank You for the 4.8% of the population 
that claim to be born-again evangelical Christians. Many of the people claim a 
formal form of Christianity, but they do not attend church and do not have a 
personal, living relationship with the Risen Christ. Please use the Jesus Film, 
the Danish Bible Society, and may Your Spirit empower the new and promising 
Christian youth movement.</p>
<p>I pray today for doctors around the world. They have a tough job with a lot 
of stress and they must deal with many ethical issues. Help them to do what is 
right as You define right. May many come to know You and help the doctors who 
do know You to share their faith with both patients and peers.</p>
<p>Now lead me today, I pray in Christ's Name, Amen.</p>

