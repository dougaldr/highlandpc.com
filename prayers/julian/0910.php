<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 10
<br><span class=byln>by Ed Vasicek</p>

<p>Great King,</p>
<p>You are a God of joy. You offer joy to those Who know You and You fill them 
to overflowing. You are the source of joy, happiness and genuine contentment. 
Yet too often I look for contentment in other places. I settle for broken 
cisterns like the people about whom Jeremiah prophesied: &quot;My people have 
committed two sins: They have forsaken me, the spring of living water and have 
dug their own cisterns, broken cisterns that cannot hold water&quot; (<b>Jeremiah 
2:13</b>). Remind me that You are the source of meaning and a rich life. The 
invitation of <b>Isaiah 55:1</b> still stands, &quot;Come, all you who are thirsty, 
come to the waters; and you who have no money, come, buy and eat! Come, buy wine 
and milk without money and without cost.&quot;</p>
<p>Help me remember that our church is a body and that I have a part to play. 
Help me to be forbearing, to confront when necessary and 
not to be touchy. Help me maintain a realistic view about human nature so that I 
do not hold false expectations and become easily frustrated or disenchanted.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God</i>).</p>
<p>Lord, thank You that we live in such a great day of 
discovery in the spiritual realm. In recent times, they have found the real Mt. 
Sinai, the crossing of the Red Sea and now possibly even Noah’s Ark. Bless the 
work of Bob Cornuke and others like him. Help them to continue to make these big 
finds that we might better understand Your Word and actually see some of the 
things You have done. Although we know that reason alone will not bring someone 
into the Kingdom of God, we also know that You use a variety of ways to help 
people focus upon the Gospel. We pray that You might use these as well.</p>
<p>Thank You for the African nation of Togo with its 5 
million people, 9 percent of whom claim to be born-again and evangelical. While 
many more claim some form of Christianity, those versions have either gone 
liberal and deny the truth of Scripture or are spiritually dead. Thank You, O 
Lord, for raising up new works with new zeal and fruit to Your glory. Revive 
the church there, O Lord, and may the number of believers double in a decade, I 
ask.</p>
<p>Lord, thank You for Help for Christian Nationals which &quot;is an 
interdenominational, evangelical missionary society...dedicated to 
spreading the Gospel, primarily through service to Christian national workers 
with economic and educational assistance and equipping them to be more 
effective in reaching their own people for Jesus Christ. HCN ministers 
throughout the world including Guatemala, Paraguay, Peru, Nicaragua, India, the 
Philippines, Spain and Russia.&quot; Grant them fruit, provision, protection and 
the power of Your Spirit, I ask.</p>
<p>Protect my loved ones and I, in Jesus’ Name I ask, Amen.</p>

