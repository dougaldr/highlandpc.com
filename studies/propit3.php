&quot;Christ died for our sins&quot; have been understood in the first century? Why was it 
necessary for the Messiah to be &quot;God in the flesh&quot; to atone for our sins? And in 
what sense did the propitiation &quot;...show God's righteousness, because in his 
divine forbearance he had passed over former sins?&quot;</p>

