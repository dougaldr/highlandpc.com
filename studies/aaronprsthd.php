<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Did the Aaronic Priesthood Begin Before The Law?
<br>(Exodus 19:22, 18:18-20, 15:25)
<br><span class=byln>by Ed Vasicek</span></p>

<p>When commenting on God's restriction of even the priesthood 
from approaching Mt. Sinai in 
<a href="javascript:scripture('../scripture/ex1922.htm')" target=_self class=pop>Exodus 19:22</a>, 
Walter Kaiser (in The Expositor's 
Bible Commentary) writes: &quot;Certainly this was not the Aaronic priesthood that had not 
been established as yet. It must be a reference to the 'firstborn' of every 
family who were dedicated and consecrated to God 
(<a href="javascript:scripture('../scripture/ex132.htm')" target=_self class=pop>13:2</a>).&quot;</p>
<p>Although firstborn sons who were not descendents of Aaron 
could serve as priests even after the Law was given (as evidenced by the example 
of Samuel), I disagree with Kaiser's assessment.</p>
<p>When Jethro offers a sacrifice to the Lord to celebrate the 
deliverance of Israel and their arrival at Mt. Sinai, we find that Aaron was 
invited to the sacrifice (and mentioned by name) along with the elders of 
Israel. Coupling this with the instance in which Aaron fashioned the molten calf 
leads me to conclude that he had been considered a priestly leader for some 
time.</p>
<p>Notice the counsel Jethro gave Moses in <b>Exodus 18:18-20</b>:</p>
<blockquote>You and the people with you will certainly wear yourselves 
out, for the thing is too heavy for you. You are not able to do it alone. Now 
obey my voice; I will give you advice, and God be with you! You shall represent 
the people before God and bring their cases to God, and <i>you shall warn them 
about the statutes and the laws</i>, and make them know the way in which they 
	must walk and what they must do.</blockquote>
<p>One must ask, &quot;What laws and statutes?&quot; The Mosaic Law had 
not been given at this point. Jethro's advice is found in <b>Exodus 18</b>, but the 
first part of the Law is given in <b>Exodus 20</b>. Some have suggested that <b>Exodus 18</b> 
actually belongs later in the book of Exodus. But such relocation theories are 
usually an agenda-driven suggestion attempting to harmonize a supposed 
chronological discrepancy. So again I ask, &quot;What laws and statutes did Jethro 
have in mind?&quot;</p>
<p>The Noahic Covenant and the ancient Priesthood (of which 
Melchizedek was but one while Jethro was another) were around hundreds of years 
before Aaron was born. But there is no evidence that Aaron participated in this 
priesthood while a slave in Egypt.</p>
<p>The best answer is found in the Jersualem Targum. Its 
paraphrase and expansion of <b>Exodus 15:25</b> reads, &quot;And Mosheh prayed before the 
Lord, and the Word of the Lord showed him the tree of Ardiphne, and he cast it 
into the midst of the waters, and the waters were made sweet. <i>There did the 
Word of the Lord show unto him statutes and orders of judgment, </i>and there He 
tried him with trials in the tenth trial.&quot;</p>
<p>The ESV version of <b>Exodus 15:25</b> reads: &quot;And he cried to the 
L<span class=sm>ORD</span>, and the L<span class=sm>ORD</span> showed him a log, and he threw it into the water, and the 
water became sweet. There the L<span class=sm>ORD</span> made for them a statute and a rule, and there 
he tested them.&quot;</p>
<p>Although the Old Testament ambiguously states that God gave 
the Israelites &quot;a statute and a rule,&quot; the Targum demonstrates the Jewish 
understanding that this meant &quot;statutes and orders of judgment.&quot; The Targum of 
Jonathan enumerates a number of specific commands (though none applying to the 
priesthood).</p>
<p><b>Exodus 15:25</b> is an important yet relatively obscure 
passage. Just as Jesus breathed the Holy Spirit to His disciples as a preview of 
Pentecost (John 20:22-23), here the &quot;mini-law&quot; may have been given before the 
formal giving of the Law (which, incidentally, was probably given on the Day of 
Pentecost, based on traditional Jewish calculations).</p>
<p>In summary, my theory is this: some of the Law was given 
before Moses returned to Mt. Sinai. Some of these statutes could have included 
minimal priestly instruction, instruction which was repeated and expanded upon 
later in cohesive fashion (when the Law was formally and comprehensively 
decreed). The information that Aaron's line would become the priestly line may 
have been included in this prelude.</p>
<p>To put it in modern terms, God seems to have leaked out 
information about the Law before the news conference!</p>

