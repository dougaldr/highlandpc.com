<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>A Perspective on the Holy Spirit
<br><span class=byln>by Ed Vasicek</span></p>

<p>The Holy Spirit: who or what is it or he? Do we believe in the baptism
of the Spirit? Spiritual gifts? How can we sort out the many
contradictory teachings about the Spirit? Indeed, the teachings proclaimed
over television, radio, and from pulpits include an absolute mess of tangled
opinions, solid and dubious interpretation, with emotional experiences tossed in
for decor. What do I think about it?</p>
<p>Let's start with a few key verses from <b> John 16:13-14</b>: &quot;But when he, the
Spirit of truth, comes, he will guide you into all truth. He will not
speak on his own; he will speak only what he hears, and he will tell you what is
yet to come. He will bring glory to me by taking from what is mine and
making it known to you.&quot;</p>
<p>Note the following: The Holy Spirit is a &quot;he,&quot; not an
&quot;it.&quot; Like God the Father and God the Son, God the Holy Spirit
is a person. He feels (he can be grieved); he thinks (he knows and
searches); and he determines (he makes decisions in distributing gifts). He is every bit as much a person as the Son of God.</p>
<p>We believe all true believers have been baptized by (with/in) the Holy
Spirit. It is not possible to be saved and not have the Holy Spirit in us.&nbsp;
Note two key verses:</p>
<ul>
  <li><b>1 Corinthians 12:13</b> &quot;For we were all baptized by one Spirit into
one body&#151;whether Jews or Greeks, slave or free-and we were all given the one
Spirit to drink.&quot;<br>
  <li><b>Romans 8:9</b> &quot;You, however, are controlled not by the sinful nature
but by the Spirit, if the Spirit of God lives in you. And if anyone does
not have the Spirit of Christ, he does not belong to Christ.&quot;</ul>
<p>So if you belong to Christ, you have been baptized by and possess the Holy
Spirit. When people of charismatic persuasion ask you if you have been
&quot;baptized by the Holy Spirit,&quot; they usually mean, &quot;Have you
spoken in tongues?&quot; My answer is, &quot;I believe that all true
believers have been baptized by the Spirit, but I think that it is the fruit of
the Spirit, not speaking in tongues, that demonstrates His presence.&quot;&nbsp;
This usually results in the expression of disappointment, &quot;Oh.&quot;</p>
<p>Whereas all believers are baptized by the Spirit, we are only sometimes
&quot;filled with the Spirit.&quot; We are filled with the Spirit to the
degree that we allow the &quot;word of Christ to dwell richly in us.&quot;&nbsp;
Compare
<a href="javascript:scripture('../scripture/col31617.htm')" target=_self class=pop>Colossians 3:16-17</a> and
<a href="javascript:scripture('../scripture/eph51819.htm')" target=_self class=pop>Ephesians 5:18-19.</a>&nbsp;
The term &quot;filling&quot; is a figure of speech: The Holy Spirit is not a liquid, but
a person. To be &quot;filled&quot; with the Holy Spirit means to be fully
controlled by him.</p>
<p>A lot of times the confusion stems from trying to impose the occurrences
found in the Book of Acts on today's scene. The Book of Acts records what <b>
did</b> happen in the early church, not what must always <b> be</b> happening.&nbsp;
Much of
Acts is transitional: from Israel to the Jewish church, from the Jewish church
to a predominantly Gentile church. Do not put God in a box by saying he
must do today what he did then. He may do so. But then again, he may
not. He may even give spiritual gifts to today's church he never gave to
first century believers. He is sovereign. Personally, I do not think
most of what we see in the Charismatic movement (at least in the USA) is the
real thing. Am I saying God cannot give gifts of foreign languages? No,
I just don't think that <b> genuine</b> tongues are common today, and that most of what
are labeled &quot;tongues&quot; are merely psychologically induced utterances.&nbsp;
There is something miraculous about speaking a genuine language you have never
studied; there is nothing miraculous about speaking in ecstatic utterances (many
non-Christian religions practice the same). But it <b> feels</b> miraculous, and that's
its selling point.</p>
<p>If we are to keep on top of these and other issues, we need to go back to the
Word. But the Bible must be studied carefully with our assumptions set
before us. Whatever we believe about the Holy Spirit needs to be grounded
in the Word, objectively interpreted. If we have our agendas, we'll find
what we're looking for.</p>
<p>How important is it to understand the Holy Spirit? According to the
verses we began with, <b> John 16:13-14</b>, he ministers to us by focusing our
attention on Christ. So if we are Holy Spirit-oriented, we will be
Christ-oriented. At the same time, we need to understand who the Holy Spirit is,
to seek to constantly be filled with him, and to let him use us. So how
important is it to understand him? Very important!</p>
<p align="center"><b> Being Filled with the Holy Spirit</b></p>
<p>Francis Schaeffer once said that if the Holy Spirit were removed from the
Church, many of us would not know the difference. I do not know if
Schaeffer was right, but I am convinced that a lot of what is credited to the
Holy Spirit finds its source in the human spirit.</p>
<p>Nothing evidences the Holy Spirit's presence more than his <b>fruit</b>.&nbsp;
Note
<b>
Galatians 5:22-25</b>: &quot;But the fruit of the Spirit is love, joy, peace,
patience, kindness, goodness, faithfulness, gentleness and self-control. Against
such things there is no law. Those who belong to Christ Jesus have
crucified the sinful nature with its passions and desires. Since we live
by the Spirit, let us keep in step with the Spirit.&quot; When the Holy
Spirit is active, he lives up to his name&#151;he makes us more holy! Yet
there is a lot of confusion between adrenalin and the Holy Spirit's influence in
our day.</p>
<p>
If a church is characterized by folks who are walking by the Spirit, they will
show it in some very simple ways. They will stay married, get along well
with others, be approachable, dependable, consistent, kind and forbearing.&nbsp;
When people claim a variety of spiritual &quot;mountain top&quot; experiences,
but go from spouse to spouse, church to church, and are not respected by those
who know them, they may have had spiritual experiences, but not Holy spiritual
experiences! The human spirit, especially fueled by ego (even apart from
evil spirits), can produce many such &quot;experiences.&quot;</p>
<p>How can we get &quot;the real thing?&quot; Note the following
principle: Spiritual maturity comes from walking in the Spirit over time. We walk in the Spirit by being frequently filled with the Spirit.&nbsp;
To be
filled with the Spirit is put in contrast with becoming drunk with wine
(<a href="javascript:scripture('../scripture/eph518.htm')" target=_self class=pop>Ephesians 5:18</a>);
therefore, to be filled with the Spirit means to be influenced or
controlled by the Spirit. But how?</p>
<p>
First of all, we need to be saved. Although the Holy Spirit can even come
upon the lost if he so chooses, this is not his norm. Besides, why would
someone want to be filled with the Spirit if God is not number one in his or her
life anyway?</p>
<p>Second, you need to dedicate yourself to serve Christ
(<a href="javascript:scripture('../scripture/rom121.htm')" target=_self class=pop>Romans 12:1</a>).&nbsp;
You must want God to use you. You must be willing to be made willing.&nbsp;
If
you are not interested in yielding to God, the Spirit's filling is not for you.&nbsp;
You must be teachable, eager for God to work with you.</p>
<p>
Third, you must confess known sin
(<a href="javascript:scripture('../scripture/1john19.htm')" target=_self class=pop>I John 1:9</a>).&nbsp;
When we sin, we grieve the
Spirit and quench (stifle) Him. We can reopen our lives to him by
confessing our sins and being cleansed. This should become a habit.</p>
<p>
Fourth, and this is key, we must set our mind upon the things of the Spirit.&nbsp;
Note the parallel between the two following passages:&nbsp;</p>
<ul>
  <li><b>Ephesians 5:18-20</b>&nbsp;&quot;..be filled with the Spirit. Speak to
  one another with psalms, hymns and spiritual songs. Sing and make music
  in your heart to the Lord, always giving thanks to God the Father for
  everything, in the name of our Lord Jesus Christ.&quot;<br><br>
  <li><b>Colossians 3:16-17</b> &quot;Let the word of Christ dwell in you richly
as you teach and admonish one another with all wisdom, and as you sing psalms,
hymns and spiritual songs with gratitude in your hearts to God. And
whatever you do, whether in word or deed, do it all in the name of the Lord
Jesus, giving thanks to God the Father through him.&quot;</ul>
<p>Please note that the main difference between these verses are the terms
&quot;be filled with the Spirit&quot; in <b>Ephesians</b>, which is replaced by
&quot;Let the word of Christ dwell in you richly&quot; in <b>Colossians</b>. In
other words, in the second passage the cause, &quot;Let the word of Christ dwell
richly&quot; is put for the result, &quot;being filled with the Spirit.&quot;</p>
<p>The secret, friends, is in the <b> mind</b> and what the mind dwells upon.&nbsp;
Another verse,
<b> Romans 8:5</b>, makes this even more clear: &quot;Those who
live according to the sinful nature have their minds set on what that nature
desires; but those who live in accordance with the Spirit have their minds set
on what the Spirit desires.&quot;</p>
<p>What we set our mind upon determines what we are filled with, or, in the case
of the Holy Spirit, who we are filled with. Although the Holy Spirit does
work in mysterious ways, his filling is not that difficult to understand. To be filled, we must choose to deal with sin as it occurs and to spend time
reflecting on the things of the Spirit. We must want him to control us.&nbsp;
We must also pay the price by taking advantage of the means to his filling.&nbsp;
Reflection upon the Word of God coupled with a willing heart is probably the
single most important key to the Spirit's filling. Other important means
God uses include prayer, fellowship, praise, church participation, service, and
Body life in general.</p>
<p>If you're looking for feelings, you will rarely experience the Holy Spirit's
filling. If you want to know him, if you are hungering for the Word which
reveals him, and if you want him to control you, you have probably been filled
with the Spirit many times. To the degree we nurture ourselves with
teachable hearts in the things of God, to that degree we are filled.</p>
<p>You may have concluded this already, but &quot;being filled with the Holy
Spirit&quot; is one of many terms for the same concept. The battleground
is consistently the <b>mind</b>. <b>Romans 12:1-2</b> puts it this way: &quot;Therefore, I urge you, brothers, in view of God's mercy, to offer your
bodies as living sacrifices, holy and pleasing to God&#151;this is your spiritual&nbsp;
act of worship. Do not conform any longer to the pattern of this world,
but be transformed by the renewing of your <b>mind</b>. Then you will be able to
test and approve what God's will is&#151;his good, pleasing and perfect will.&quot;</p>
<p>Are you pursuing the things of the Spirit? Determine to regularly set
your mind on the things of the Spirit!
</p>

