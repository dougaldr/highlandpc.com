<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 22
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Father,</p>
<p>I thank You for the Holy Spirit. He is a Purifier. He 
makes me holier. He is the divine Revealer Who opened my eyes to the truth of 
the Gospel. He helped me to 
understand that Christ died for my sins and arose the Victor. He brought me 
from spiritual death to spiritual life through the new birth (regeneration) so 
that I would believe in the Lord Jesus Christ. He granted me the repentance 
that leads to life. He is the Unifier. He baptized me into the Body of Christ 
so that I might be one with those who have also been regenerated. He has endowed 
me with spiritual gifts and begun creating His fruit in my life. When I empty 
myself, He fills me. When I am grieving, He comforts me. When I grieve Him, He 
convicts me. When I cannot go on, He reminds me that it is not by might, nor by 
power, but by Him, the Spirit 
(<a href="javascript:scripture('../../scripture/zech46.htm')" target=_self class=pop>Zechariah 4:6</a>).</p>
<p>My God and King, help me with my thought life today. Help 
me think positive and pleasant thoughts today, thoughts that please You. I know 
that the battlefield is my mind. Help me to renew it faithfully by reading, 
studying and pondering Your Word. Remind me of the garbage-in-garbage-out 
principle. Help me to be cautious about what I watch and how I am entertained.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. I give them to You and ask You to help me constantly do so.</p>
<p>Lord, help me to get to know my neighbors where I live and 
at work or school. Teach me the importance of participating in my community as 
an avenue to benefit myself and a way to show love to others.</p>
<p>Father, if I am going to get attached to people, I have to 
risk rejection. If I am going to build significant relationships with friends, 
I have to learn how to handle conflict. May I neither run nor fight, but 
instead help me work at communicating, compromising and expressing myself 
tactfully. Help me to properly handle conflict in all my relationships 
including church life.</p>
<p>Today I pray for the Latin American Caribbean island nation 
of St. Vincent. Thank You that almost 25% of the population claim to be 
born-again evangelicals. At the same time, Lord, many of those who profess 
Christ do not live holy lives and many are ignorant of Bible doctrine and 
teaching. I pray that You would develop a core of strong leaders and that 
those who know You would exemplify a life that pleases You and bears testimony 
to Your regenerating and sanctifying grace.</p>
<p>Lord, bless the ministry of Church Ministries 
International, &quot;an evangelical, interdenominational, mission organization 
dedicated to helping urban churches in Latin America and Spain evangelize their 
city and nation.... Church Ministries International works with teams of missionaries 
and pastors of various evangelical churches...to carry on a concentrated program 
of evangelism, discipleship and church planting.&quot; Father, prosper, provide and 
protect this ministry and those associated with it. Grant them harmony and 
empower them by Your Spirit, I pray.</p>
<p>Help me to live in a way that attracts people to Christ, I 
pray, in Jesus’ Name, Amen.</p>

