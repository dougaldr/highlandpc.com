<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 19
<br><span class=byln>by Ed Vasicek</p>

<p>Great Chief Shepherd,</p>
<p>Thank You that You are a God who communicates! Not only is 
there conversation between the Father, Son, and Holy Spirit, but You also 
communicate to us, Your creation. You communicate to us through nature. The 
mighty storms tell us You are angry; the beautiful flowers tell us You are 
beautiful and loving; the weeds and thistles tell us something is wrong.</p>
<p>Then You communicate specially through Your Word, the 
Bible. The recorded words of Your messengers, the Prophets, God the Son in the 
Person of Jesus Christ, and the Apostles are more than words written by men: the 
Bible is truly the Word of God as well.</p>
<p>And then You communicate to us through Your Spirit.</p>
<p>Thank You that You have �breathed� out all Scripture 
(<a href="javascript:scripture('../../scripture/2tim31617.htm')" target=_self class=pop>2 Timothy 3:16-17</a>), 
and we must feast on Your Word if we ever hope to mature. 
Help us hunger and thirst for Your Word as did the Psalmist in 
<a href="javascript:scripture('../../scripture/ps119.htm')" target=_self class=pop>Psalm 119</a>.</p>
<p>Lord, help me resist the snares of the devil today. I 
claim my victory in Jesus Christ. I am seated with Him in the heavenly places, 
far above all principalities and powers.</p>
<p>Lord, help me get along well with those with whom I work 
or associate. At times I think it should be easy for reasonable people to get 
along. But then I remember that our hearts are deceitful, and that insanity is 
in our hearts. We can be reasonable, but we are not always so. Help me to be 
reasonable and to know how to handle it when others are not reasonable. Help me 
to be fairminded and not to think emotionally.</p>
<p>Today I pray for the nation of Sierra Leone. This is one 
of the most desperate nations on the earth with no strong government and chaos 
reigning. Between Islam and tribal religions, it has been difficult to nurture 
strong churches. The average resident has experienced violence of some sort: 
rape, torture and kidnappings. Lord, help the Christians to be strong. May 
Your Spirit sweep over the land and bring many to life, and, by your grace, to 
maturity. May the number of believers triple within the next decade.</p>
<p>Lord, I thank you for the Network of International 
Christian Schools. �NICS started in the Republic of Korea with the 
establishment of International Christian Schools. Presently, NICS operates 17 
schools in 13 countries. Plans are underway for expansion into other major 
cities around the world.� We pray that you would bless their plans as they seek 
to educate English-speaking students, often missionary children, with a 
thoroughly Christian education. Provide for this ministry in every way.</p>
<p>Today we ask you to protect and bless our firefighters. 
May many come to know You, and help the Christian firefighters to honor You with 
their love and example.</p>
<p>Now use me for Your glory today, I pray, in Jesus� Name, 
Amen.</p>

