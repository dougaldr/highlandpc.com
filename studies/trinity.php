<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Trinity: What the Early Church Believed
<br><span class=byln>by Eddie Snipes</span></p>


<p>In Part 1 we set the stage for the council. We looked at
the fact that these men were Christians who suffered for their faith. Just 14
years earlier, they came out of the last great Roman persecution. They resisted
against the attacks from outside the church at the peril of their own lives.&nbsp;
Now
a new threat was becoming strong inside the church. It is clear that the
participants of the Council of Nicaea believed in the Trinitarian view and voted
almost unanimously for the church to affirm this belief. The next logical
question to ask would be, is this what the first and second century church
believed?
 
</p>
<p>The first and second century believers are the closest to
the apostles. Some of the quotes we will look at are men who were taught
directly by the apostles. Some were even discipled one-on-one by the apostles.&nbsp;
Many of these men wrote epistles to churches to refute non-Trinitarian beliefs.&nbsp;
Some groups argue that the word Trinity is not in the scriptures and was not
used until after the Council of Nicaea which was in 325 AD. However, we will see
that this does not agree with the historical documents these men left behind.&nbsp;
Also, a concept does not have to be labeled by scripture to be taught in
scripture. This is an invalid argument because every religious group uses terms
to describe principles that they believe are taught in the scripture. Just
because we use a term to describe a principle does not obligate us to find that
term in scripture.
 
</p>
<p>It is also said that the Trinity comes from pagan origins
because several cults had similar beliefs. This claim does hold water either.&nbsp;
For one, Satan always counterfeits God. If a cult quotes scripture, does this
mean scripture is pagan? In Matthew 24 Jesus said,</p>
<blockquote>
<sup>23</sup>
&quot;Then if anyone says to you, 'Look, here is the Christ!' or 'There!' do not
believe it. <sup>24</sup> &quot;For false christs and false prophets will rise and 
show great signs and wonders to deceive, if possible, even the elect. 
<sup>25</sup> &quot;See, I have told you beforehand.
</blockquote>
 

<p>There are many people posing as Christ and many false
prophets and some using great signs. Does this mean we deny Christ and discredit
all the prophets of scripture simply because there are false prophets around?&nbsp;
No. Just because there are old pagan religions that used three as a sacred
number does not mean that Christianity is a pagan culture any more than I can
claim that all money is a counterfeit because some money has been counterfeited.
 
</p>
<p>What matters is, what do the scriptures teach, what did the
apostles believe and what did the apostles pass to their disciples? Let’s go
back to the first century and see what clues to this debate we can dig up.
 
</p>
<p><b>Barnabas</b>
<p>74 AD in The Letter of Barnabas, he says,</p>
<blockquote>
  <p class="MsoBodyTextIndent">“And further, my brethren, if the Lord [Jesus]
  endured to suffer for our soul, he being the Lord of all the world, to whom
  God said at the foundation of the world, 'Let us make man after our image, and
  after our likeness,' understand how it was that he endured to suffer at the
  hand of men” (Letter of Barnabas 5).
  
  </p>
</blockquote>
<p><b>The Didache</b>
<p>The Didache is a church manual written in Greek and is
dated around 60-80 AD. This manual quotes from Matthew, Luke, John, Acts,
Romans, Thessalonians, and 1 Peter. It quotes 22 times from the book of Matthew.
Concerning baptism, the Didache says,</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">As regards baptism, baptize in
this manner, having first given all the preceding instruction baptize in the
name of the father, and of the son and of the Holy Spirit and immerse 3 times in
running water.
 
</p>
<p><b>Ignatius</b>
<p>Ignatius died in 110 AD. He was a disciple of the Apostle
John and was the bishop of Antioch. He was martyred in Rome only 10 years after
John died. Ignatius is as close to the source as you can get. He had several
interesting quotes concerning the Trinity doctrine:</p>
<blockquote>
  <p class="MsoBodyTextIndent">&quot;Be deaf, therefore, when any would speak to
  you apart from Jesus Christ, who was descended from the family of David, born
  of Mary, who truly was born, truly took a body; for the Word became flesh and
  dwelt among us without sin&quot;</p>
</blockquote>
<p class="MsoBodyTextIndent" style="MARGIN-LEFT: 0in">Ignatius also wrote about
Jesus’ second coming:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“Look for him that is above the times, him who
  has not times, him who is invisible”.</p>
</blockquote>
<p class="MsoBodyTextIndent" style="MARGIN-LEFT: 0in">He believed that only God
is was timeless and in his letter to Polycarp, Ignatius stated, “Jesus is God,
God incarnate.”
 
</p>
<p>Ignatius also identified Jesus apart from the Father and
the Holy Spirit by saying,</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“<b>In Christ Jesus</b> our
Lord, by whom and with whom be glory and power <b>to the Father</b> <b>with the
Holy Spirit</b> for ever”.
 
</p>
<p class="MsoToc1">He also believed Jesus was fully God and fully man. He
clarified this by saying,</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“We have also as a Physician
the Lord our God Jesus the Christ the only-begotten Son and Word, before time
began, but who afterwards became also man, of Mary the virgin. For ‘the Word
was made flesh.' Being incorporeal, He was in the body; being impassible, He was
<b>in a passible body</b>; <b>being immortal</b>, He was in a mortal body; being
life, <b>He became subject to corruption</b>, that He might free our souls from
death and corruption, and heal them, and might restore them to health, when they
were diseased with ungodliness and wicked lusts.” ( The ante-nicene Fathers,
Grand Rapids: Eerdmans, Vol. 1, p. 52 .)
 
</p>
<p>He clarifies this further in one of his epistles to the
church in Ephesus:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“...God Himself appearing in the form of a man,
  for the renewal of eternal life.” ( Epistle of Ignatius to the Ephesians
  4:13)
  
  </p>
</blockquote>
<p>Also in this epistle, Ignatius again identifies Jesus as
God while identifying His personhood:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“For our God Jesus Christ, was, according to
  the appointment of God, conceived in the womb by Mary, of the seed of David,
  but by the Holy Ghost.” ( Epistle of Ignatius to the Ephesians 4:9)
  
  </p>
</blockquote>
<p>In his letter to the Trallians, Ignatius refers to Jesus as
God and being distinguished from the Father:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">( Epistle of Ignatius to the
Trallians 2:4)<br>
&quot;For even our God, Jesus Christ, now that He is in the Father&quot;.
 
</p>
<p>These writings are over 200 years before the Council of
Nicaea. He wrote during and very shortly after the life of the Apostle John who
was called the beloved of Jesus.
 
</p>
<p><b>Justin the Martyr</b>
<p>Around 130 AD, Justin explained Christian baptism:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“For, in the name of God, the Father and Lord
  of the universe, and of our Savior Jesus Christ, and of the Holy Spirit, they
  then receive the washing with water”
  
  </p>
</blockquote>
<p><b>Polycarp</b>
<p>Polycarp lived to be 90 years old. He was martyred in 160
AD. He was also a disciple of the Apostle John and the bishop of Smyrna.&nbsp;
His
doctrine about Jesus was also directly from the source. Polycarp also makes a
clear distinction between Jesus, the Father and the Holy Spirit:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“O Lord God almighty...I bless you and glorify
  you through the eternal and heavenly high priest Jesus Christ, your beloved
  Son, through whom be glory to you, with Him and the Holy Spirit, both now and
  forever”
  
  </p>
</blockquote>
<p>In Polycarp’s epistle to the church at Philippi, he
identifies the Father as God, Jesus as the eternal High Priest and God and also
distinguishes between the personhood of the Father and the Son:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“Now may the God and Father of our Lord Jesus
  Christ, and the eternal High Priest Himself, the God Jesus Christ, build you
  up in the faith...” ( The Epistle of Polycarp to the Church at Philippi, 12)&nbsp;
  
  </p>
</blockquote>
<p><b>Iranaeus</b>
<p>Iranaeus was influenced by Polycarp and later became the
bishop of Lyons. 145 years before the Council of Nicaea, Iranaeus confronted the
growing heresy in the church. In his work, ‘Iranaeus Against Heresy’, he
confronts the notion that Jesus was a man and not God in the flesh. He wrote:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“But he Jesus is himself in his own right,
  beyond all men who ever lived, God, Lord, and king eternal, and the incarnate
  word, proclaimed by all the prophets, the apostles …The Scriptures would not
  have borne witness to these things concerning Him, if, like everyone else, He
  were mere man.” (Against Heresies 3:19.1-2)
  
  </p>
</blockquote>
<p>Iranaeus then went on to explain that Jesus is the Word and
the Father created all things through Him while affirming the plurality of the
Godhead:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">&quot;For with Him were always
present the Word and Wisdom, the Son and the Spirit, by whom and in whom, freely
and spontaneously, He made all things, to whom also He speaks, saying, 'Let us
make man after our image and likeness'&quot;. ( Against Heresies, 4:10)
 
</p>
<p>Don’t miss the important focal point here. Iranaeus was
confronting the heresy that the Gnostics and other groups were trying to marry
to Christian doctrine. He was confronting a new doctrine the church stood
against, he was not introducing new doctrine.
 
</p>
<p>Iranaeus then went on to present a clear belief in the
Triune God:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">&quot;The Church, though
dispersed throughout the whole world, even to the ends of the earth, has
received from the apostles and their disciples this faith: ...<b>one God, the</b>
<b>Father Almighty</b>, Maker of heaven, and earth, and the sea, and all things
that are in them; and <b>in one Christ Jesus</b>, the Son of God, who became
incarnate for our salvation; and <b>in the Holy Spirit</b>, who proclaimed
through the prophets the dispensations of God, and the advents, and the birth
from a virgin, and the passion, and the resurrection from the dead, and the
ascension into heaven in the flesh of the beloved Christ Jesus, our Lord, and
His manifestation from heaven in the glory of the Father ‘to gather all things
in one,' and to raise up anew all flesh of the whole human race, in order that <b>to
Christ Jesus, our Lord, and God</b>, and Savior, and King, <b>according to the
will of the invisible Father</b>, ‘every knee should bow, of things in heaven,
and things in earth, and things under the earth, and that every tongue should
confess; to him, and that He should execute just judgment towards all...'&quot;&nbsp;
(Against Heresies X.l)
 
</p>
<p><b>Theophilus</b>
<p>Theophilus was the bishop of Antioch and is identified in
Luke 1:3 and Acts 1:1. He sent this in a letter to Autolycus in 160 AD (165
years before the Council of Nicaea):</p>
<blockquote>
  <p class="MsoBodyTextIndent">For the divine writing itself teaches us that
  Adam said that he had heard the voice but what else is this voice but the word
  of God, who is also his Son.
  
  </p>
</blockquote>
<p><b>Melito of Sardis</b>
<p>Melito of Sardis identified Jesus Christ as fully God and
fully man in his writings around 177 AD:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">Being God and likewise perfect
man, he gave positive indications of his two natures: of his deity, by the
miracles during the three years following after his baptism… he concealed the
signs of his deity, although he was the true God existing before the ages&quot;
(Anastasius of Sinai's, The Guide 13).
 
</p>
<p><b>Athenagoras</b>
<p>Athenagoras wrote in 160 AD an explanation as to what the
church believes:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“they hold the Father to be
God, and the Son God, and the Holy Spirit, and declare <b>their union</b> and <b>their
distinction</b> in order.” (A plea for the Christians.10.3)</p>
<p>Is the Trinity a new belief? Almost 200 years before the
Council of Nicaea, Athenagoras explained the church’s belief that the Father,
Son, and Holy Spirit were united and distinct.
 
</p>
<p><b>Clement Of Alexandria</b>
<p>In 190 AD Clement Of Alexandria makes a strong case for
Christ’s deity and the Trinity in several writings:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“I understand nothing else
than the <b>Holy Trinity</b> to be meant; for the <b>third is the Holy Spirit</b>,
and the <b>Son is the second</b>, by whom all things were made according to <b>the
will of the Father</b>.” (Stromata, Book V, ch. 14)</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“There was then, a Word
importing an unbeginning eternity; as also the <b>Word itself, that is, the Son
of God</b>, who being, <b>by equality of substance</b>, one with the <b>Father</b>,
is <b>eternal and uncreated</b>.”</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“When [John] says: 'What was
from the beginning [1 John 1:1],' he touches upon the generation without
beginning of the <b>Son, who is co-equal with the Father</b>. 'Was,' therefore,
is indicative of an eternity without a beginning, just as the Word Himself, that
is the Son, being one with the Father in regard to <b>equality of substance</b>,
is eternal and uncreated. That <b>the word always existed</b> is signified by
the saying: 'In the beginning was the Word' [John 1:1].” (fragment in Eusebius
History, Book 6 Ch 14; Jurgens, p. 188)</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“This Word, then, the Christ,
the cause of both our being at first (for He was in God) and of our well-being,
this very Word has now appeared as man, <b>He alone being both, both God and man</b>”
(Exhortation To The Heathen, ch 2)
 
</p>
<p><b>Tertullian</b>
<p>Tertullian was a respected apologist wrote much in defense
of Christianity. Around 200 AD he wrote:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“The origins of both his substances display him
  as man and as God: from the one, born, and from the other, not born” (The
  Flesh of Christ 5:6-7).</p>
</blockquote>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“God alone is without sin. The
only man without sin is Christ; for Christ is also God.” (The Soul 41.3)
 
</p>
<p>He was a defender of Christianity. He was addressing
attacks against the church, which questioned the deity of Christ. Critics accuse
the church of changing the message, but as we see from history, the church was
defending Christianity from those attempting to change it.
 
</p>
<p>Tertullian contended against Praxeas concerning the new
doctrine we now call Modalism. Praxeas took his beliefs from Sabellius.&nbsp;
This
view of Modalism teaches that there is one in the Godhead and He takes different
forms: the Father = the Son = the Holy Spirit. They are different modes of the
same God. Sabellius refined his doctrine around the beginning of the third
century. He explained his belief:</p>
<blockquote>
  <p class="MsoBodyTextIndent">&quot;the existence of a divine monad (which he
  named the huiopater) which by a process of expansion projected itself
  successively in revelation as the Father, Son, Holy Spirit. So each appeared
  in different periods of time, instead of existing simultaneously. The Father
  was the creator and law giver, the Son was the redeemer and the Holy Spirit
  was the giver of grace and the regenerator.&quot;</p>
</blockquote>
<p>Epiphanius, bishop of Salamis wrote in 375 in his volume
about heresies,</p>
<blockquote>
  <p class="MsoBodyTextIndent">“Their doctrine is, that Father, Son and Holy
  Spirit are one and the same being, in the sense that three names are attached
  to the one substance. A close analogy may be found in the body, soul and
  spirit of man.”</p>
</blockquote>
<p>This is similar to the ‘modes’ of God claimed by
Oneness Pentecostals today.
 
</p>
<p>This was never accepted by the church and was considered
heresy. Tertullian argued against this by stating the church’s historic belief
that,</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“We define that <b>there are
two, the Father and the Son</b>, and <b>three with the Holy Spirit</b>, and this
number is made by the pattern of salvation...[which] brings about unity in <b>trinity</b>,
<b>interrelating the three, the Father, the Son, and the Holy Spirit</b>. They
are of <b>one substance and power</b>, because <b>there is one God</b> from whom
these degrees, forms and kinds devolve in the name of Father, Son and Holy
Spirit.” (Against Praxeas p. 156-7).</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“The connection of the Father
in the Son the Son in the paraclete (Holy Spirit), <b>produces three coherent
persons</b>, who are yet <b>distinct one from another</b>. These three are <b>one
essence</b>, <b>not one person</b>, as it is said, ‘I and my Father are
one,’ in respect of <b>unity of substance, not singularity of number</b>.”…&nbsp;
“Yet we have never given vent to the phrases ‘two Gods’, or ‘two
Lords’: not that it is untrue the Father is God, the Son is God, the Spirit is
God, each is God.” ( Ante-Nicene fathers vol.3,p.621, Against Praxeas.)
 
</p>
<p>Tertullian ads to this definition by saying:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“We believe there is but one God, and no other
  besides the maker of the world, who produced the universe out of nothing, by
  his word sent forth first of all, that this word, called his Son, was seen in
  the name of God in various ways by the patriarchs, was always heard in the
  prophets, at last sent down, from the spirit and power of God the Father, into
  the virgin Mary, was made flesh in her womb, and born of her, lived as Jesus
  Christ...”</p>
</blockquote>
<p><b>Novatian</b>
<p>Novatian was Bishop of Rome. He was best known for his
opposition against allowing re-admittance of those who lapsed (or denied their
faith) during the time of persecution. He also defended the Trinitarian belief
in Christianity. In 235 AD (100 years before the Council of Nicaea) he wrote:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“The rule of truth demands
that, first of all, we believe in <b>God the Father</b> and Almighty Lord, that
is, the most perfect Maker of all things...' The same rule of truth teaches us
to believe, after the Father, also in the <b>Son of God</b>, CHRIST JESUS, our
Lord God, but the Son of God.... Moreover, the order of reason and the authority
of faith, in due consideration of the words and Scriptures of the Lord ',
admonishes us, after this, to believe also in the <b>Holy Ghost</b>, promised of
old to the Church, but granted in the appointed and fitting time.”</p>
<p>Novatian continues in the effort to uphold the belief of
the Triune God taught from the Apostles through the Council of Nicaea.</p>
<p><b>Hippolytus</b>
<p>Hippolytus was appointed Bishop in Rome by his followers
when he seceded from the church in protest of Zephyrinus being appointed as
Bishop. He opposed Zephyrinus because he taught modalist views. Hippolytus also
opposed Zephyrinus’ views on penance and absolution—the belief that the
earthly bishop could declare forgiveness of post-baptism sins. During
persecution, Hippolytus was sent to the mines with the new Bishop of Rome,
Pontain who did not hold to these views. Hippolytus reconciled himself with the
church and urged his followers to do so as well. Both bishops resigned to end
the division and to allow a new bishop to succeed them. Hippolytus and Pontain
were martyred together. Hippolytus wrote the following in the ‘Apostolic
Tradition’ around 200 AD:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“1. Do you believe in God, the Father Almighty?&nbsp;
  2. Do you believe in Christ Jesus, the Son of God, who was born by the Holy
  Spirit of the Virgin Mary, and was crucified under Pontius Pilate, and was
  dead and buried, and rose again the third day, alive from the dead, and
  ascended into heaven, and sat at the right hand of the Father, and will come
  to judge the living and the dead? 3. Do you believe in the Holy Spirit, in the
  holy church, and in the resurrection of the flesh?”</p>
</blockquote>
<p>Ten years earlier, Hippolytus wrote against the heresy of
Noetus. Noetus taught what is now known as Oneness doctrine. He believed that
the Father = the Son = the Holy Spirit. In response, Hippolytus wrote:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">John 1:1 In the beginning was
the Word, and the Word was with God, and the Word was God.</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“If then the word was with God
and was also God what follows? Would one say that he speaks of two God’s?&nbsp;
I
shall not speak of two Gods but of one; of two persons however and of a third
economy, the grace of the Holy Ghost. For the <b>Father is indeed one</b> but
there are <b>two persons</b> because there is <b>also the son</b>; and there <b>is
the third the Holy Spirit</b>. The Father decrees, the word executes and the son
is manifested, through whom the Father is believed on. The economy of the
harmony is led back to the one God, <b>for God is one</b>. It is the father who
commands and the son who obeys and the Holy Spirit who gives understanding; The
Father is above all the son is through all and the Holy Spirit who is in all.&nbsp;
And we <b>cannot think of one God</b>, but by believing in truth in <b>Father</b>
and <b>Son</b> and <b>Holy Spirit</b>”.</p>
<p>Hippolytus also wrote:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“God, subsisting alone, and
having nothing contemporaneous with Himself, determined to create the
world.... Beside Him there was nothing; but He, while existing alone, <b>yet
existed in plurality</b>.... And thus there appeared another beside Himself.&nbsp;
But
when I say another, <b>I do not mean that there are two Gods</b>.... Thus, then,
these too, though they wish it not, fall in with the truth, and admit that one
God made all things.... For Christ is the God above all..... He who is over all is
God; for thus He speaks boldly, 'All things are delivered unto me of my Father.'&nbsp;
He who is over all, God blessed, has been born; and having been made man, He is
(yet) God for ever.... And well has he named Christ the Almighty.” (Hippolytus
&quot; The Ante-Nicene Fathers, vol. 5, pp. 227, 153, 225)</p>
<p><b>Gregory the Wonder-worker</b>
<p>Gregory Thaumaturgus was called the miracle worker. He was
a theologian and oversaw the council that appointed Bishop Alexander who was
martyred during the last great persecution. Gregory wrote in 262 AD:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“But some treat the Holy
Trinity in an awful manner, when they confidently assert that there are not
three persons, and introduce (the idea of) a person devoid of subsistence.&nbsp;
Wherefore we <b>clear ourselves of Sabellius</b>, <b><i>who says that the Father
and the Son are the same [Person]</i></b> . . . We forswear this, because <b>we
believe that three persons</b>—namely, Father, Son, and Holy Spirit—are
declared to possess the one Godhead: for the one divinity showing itself forth
according to nature in the Trinity establishes the oneness of the nature” (A
Sectional Confession of Faith 8).</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“But if they say, <b>'How can
there be three Persons, and how but one Divinity?'</b> we shall make this reply:
That there are <b>indeed three persons</b>, inasmuch as there is one person of <b>God
the Father</b>, and one of the <b>Lord the Son</b>, and one of the <b>Holy
Spirit</b>; and yet that there is <b>but one divinity</b>, inasmuch as . . . <b>there
is one substance</b> in the Trinity” (A Sectional Confession of Faith, 14)</p>
<p>Notice that the pre-Nicaean church publicly condemned
Sabellius and declared him as not being a part of the church. He was cleared of
the church, yet some groups present Sabellius as a representative of the early
church. Sabellius was not considered a defender of the faith, but a promoter of
heresy. In Part 3 of this study, we will look at the heresies and how they
affect modern religious movements. Sabellius is quoted heavily in Oneness
theology, as are several other teachers who were publicly opposed by the early
church fathers.
 
</p>
<p><b>Methodius</b>
<p>Methodius worked together with Cyril to convert the Slavic
tribes. In his oration on the Psalms 5, Methodius taught:</p>
<blockquote>
  <p class="MsoBodyTextIndent">“For the kingdom of the Father, of the Son, and
  of the Holy Ghost, is one, even as their <b>substance is one</b> <b>and their
  dominion one</b>. Whence also, with one and the same adoration, we worship the
  <b>one Deity in three Persons</b>, subsisting without beginning, uncreated,
  without end, and to which there is no successor…. For nothing of the Trinity
  will suffer diminution, either in respect of eternity, or of communion, or of
  sovereignty”.</p>
</blockquote>
<p><b>Athanasius</b>
<p>Athanasius challenged Arius’ heresy on the non-deity of
Christ. Arius was the primary reason the council of Nicaea was called.&nbsp;
Arius
challenged the deity of Christ by claiming that Jesus did not pre-exist. Arius
claimed that Jesus was created for the purpose of being incarnated and was not
God. Athanasius challenged Arius and a bitter dispute arose when Arius began a
letter campaign to get leaders to side with him. He also created jingles and
taught his heresy through music. In defense of historic Christianity, Athanasius<span style="mso-spacerun: yes">
</span>wrote:</p>
<p class="MsoNormal" style="MARGIN-LEFT: 0.5in">“[The Trinity] is a Trinity
not merely in name or in a figurative manner of speaking; rather, it is a
Trinity in truth and in actual existence. Just as the Father is he that is, so
also his Word is one that is and is God over all. And neither is the Holy Spirit
