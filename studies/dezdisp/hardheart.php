<p>But God is not politically correct. Take the realm or religion.</p>
<p>The Egyptian people were very religious. In some senses, probably more 
religious than we are. But their religion was false religion, and their gods were 
false gods.</p>
<p>God is a jealous God, and it angers Him when people worship false gods. He is 
very narrow in this regard. You can be narrow when You are God. It angers Him 
when His true people are oppressed. And, in His own sweet time, He has or will 
address such issues. Enter the ten plagues of Egypt.</p>
