<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 5
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Before our Savior was born, You prepared the way for Him by 
sending John the Baptist. His parents conceived John in their old age. They 
had no children before him and had long given up hope that they would. But You 
had other plans. While John's dad, Zechariah, was minister in the Temple, You 
sent your angel to speak to him. He was scared to death when he saw the angel. 
The angel instructed him about what was going to happen, but Zechariah doubted 
that God could do what the angel said He would do 
(<a href="javascript:scripture('../../scripture/luke1120.htm')" target=_self class=pop>Luke 1:1-20</a>).</p>
<p>Lord, sometimes we simply do not know what Your will is. We would like to 
believe You will do this or that, but we do not know what You are planning. We 
recognize that faith is not conjuring up some sort of emotional energy and 
adrenaline. It is instead taking You at Your Word. Zechariah knew what You 
said You would do in that specific situation and he doubted You. So we too 
sometimes doubt what Your Word clearly reveals. So Lord, help us not to deny 
our doubts, but to trust You despite them.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 
John 1:9</b>). I accept responsibility for my sins and freely bring the ones 
Your Spirit brings to my mind now: __________.</p>
<p>I pray for parents of children who have gone astray. Whether in the realm of 
premarital, extramarital, or perverted sex; drugs, alcohol, gambling, crime, 
violence, bitterness toward parents, abandoning the faith or in any other way. 
Lord, this hurts like nothing else can. Comfort parents and restore these 
wayward sons and daughters.</p>
<p>Lord, I pray for harmony and reasonableness within family relationships. 
Sometimes it is difficult to get along with in-laws, parents, grown children, 
siblings, or other family members. Some people contend with irresponsible or 
meddling family members and tensions can run high. Help me to be reasonable, but 
firm when I need to be. Help me not to allow others to walk all over me, yet let 
me exercise a reasonable compassion. Keep me from enabling others to continue in 
their addictions and sins, yet keep me from assuming the police-like stance. </p>
<p>Lord, help me to develop humility. You command me to humble myself before 
You, but You also want me to be humble in my dealing with others. Help me not 
to look out for only my own interests, but the interest of others.</p>
<p>Today I pray for World Missionary Radio Fellowship, HCJB. Thank You for their 
many years of radio transmissions around the world. Thank You for their facility 
in Quito, Ecuador, and the multitudes of people who hear the Gospel and grow in 
the Lord through this strategic ministry. Provide for this ministry, grant them 
harmony and safety. May You empower all who serve by Your Spirit, I ask.</p>
<p>Help me to look for opportunities to share the Gospel today, I ask in Jesus' 
Name, Amen.</p>

