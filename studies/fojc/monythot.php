<p>
Since this text is part of the Sermon on the Mount, Christ continues in His same 
style of teaching, Rabbinic Oratory, which means teaching by exaggeration and 
the &quot;hot and cold&quot; technique of contrast. When it comes to litigation, Christ&#039;s 
main thought, in my view, is &quot;walking the extra mile.&quot; When it comes to alms, 
prayer, and fasting, the theme, as I see it, is &quot;not to be seen by men.&quot; In our 
text today about status through material goods, the theme that jumps out at me 
is &quot;For where your treasure is, there will your heart be also.&quot;</p>
<p>The 
Jews of Christ's day were well schooled in the Old Testament, and would have 
known verses like <b>Proverbs 13:22a</b>, &quot;A good man leaves an inheritance for his 
children's children...&quot; or &quot;The plans of the diligent lead to profit as surely 
as haste leads to poverty...&quot; (<b>Proverbs 21:5</b>). It must be understood that Christ's 
teachings do not trash these Proverbs, but rather are intended to correct an 
imbalance by focusing on the matter of priority. That priority is defined in 
<b>Matthew 6:33</b>, &quot;But seek first his kingdom and his righteousness, and all these 
things will be given to you as well.&quot; </p>
<p>
Gene Getz in his thorough work, <u>A Biblical Theology of Material Possessions</u> 
writes, &quot;'Spiritual realities' and 'material things' are not irreconcilable 
entities in our lives. They are a matter of our priorities....&quot; </p>
<p>The 
secular author and Harvard professor, Juliet Schor, has written a neat book 
titled <u>The Overspent American</u>. Though not an evangelical believer, she 
is quite observant of what is going on in our society (including the evangelical 
world). She writes:</p>
<blockquote> Throughout history, religious ideology has served as an additional 
	brake: nearly all the world's religions have stressed the sacredness of 
	simplicity and moderation. Coveting our neighbor's belongings was so 
	important a no-no that it qualified as one of the Ten Commandments.... But 
	by the early twentieth century, these restraints had been largely lifted. 
	The notion of sufficiency, which long had regulated consumption, was 
	discarded in the face of the promise of mass prosperity...frugality was 
	portrayed as the proclivity of a small, pinched personality. The result at 
	the century's end is that almost half the population of the world's richest 
	country say that they have just enough income to get by.</blockquote>
<p>
Regarding the &quot;good eye&quot; of verse 22, Dr. Ron Moseley (in <u>Yeshua, A Guide to 
the Real Jesus</u>) writes:</p>
<blockquote>The terms &quot;good eye&quot; 
and &quot;bad eye&quot;... were popular terms in ancient Judaism, but are often 
misunderstood by modern readers. In first-century Judaism the term &quot;good&quot; or 
&quot;single eye&quot;... meant that a person was generous. The &quot;bad&quot; or 
&quot;evil eye&quot; meant 
he was stingy. A rabbi would say, &quot;If a person gives a gift, let him give it 
with a good eye.&quot; During the first century, the rabbis in the school of Hillel 
taught that an individual who gave one-fortieth of his income has a good eye, 
but a person who gave only one-sixtieth of his income had a bad eye. Everyone, 
of course, was expected to pay tithes (one-tenth of their increase).</blockquote>
  
