<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 17
<br><span class=byln>by Ed Vasicek</p>

<p>Great Three in One,</p>
<p>You are beautiful, God. Your beauty is such that You are 
the ultimate in desirability. Like David, we want to gaze upon the beauty of 
the L<span class=sm>ORD</span> and meditate in His Temple. When we look at some 
of the physical beauty of creation, we 
are awestruck. But Your beauty is way beyond the physical, more than the sum 
total of all beauty in the universe. You are beautiful to an infinite degree.</p>
<p>Lord, help me to have a beautiful character and pleasant 
disposition. May my heart be pure and my motives blameless. Help the eternal 
me, the invisible part of me that will live forever with You—my very soul—to be 
a thing of beauty.</p>
<p>I thank You, God, for the convicting work of the Holy 
Spirit. He convicts people of sin, righteousness and judgment. Thank You that 
His convicting work was part of the process of creating faith within me.</p>
<p>Lord, I have some disappointments and burdens in life. A 
few of them hurt me and have left scars including: __________. I turn them over to 
You. Help me to find Your purpose and 
joy in life. There are many things I cannot change, but I can share all my 
thoughts with You. Thank You that You do not stifle me or pressure me to &quot;get 
over it.&quot; You are not in a hurry, and You can hear my recitations of woe over 
and over again. You are a God Who feels with me. But, Lord, You are also the 
God of victory. Help me get to a point where I am ready to move on.</p>
<p>Father, I pray for our president, governor, federal and 
local senators and representatives, and judges of all kinds. Help them to do 
what is right, to seek You, and to please You.</p>
<p>Today I remember the nation of Djibouti. It is a dry and 
impoverished land, and this is even truer in the spiritual realm where only 
one-tenth of one percent claim to be born-again evangelicals. Thank You that 
even though Muslims make up 91% of the population there is a relative level of 
religious freedom. Lord, please see to it that this continues. We pray that 
the Bible-oriented churches would grow fifty times over the next decade. Create 
a hunger in this land for You.</p>
<p>I thank You for the ministry of the Gideons as they 
distribute tens of millions of Bibles every year free-of-charge throughout the 
world. Thank You that they supply hotels, military personnel and students—among 
others—with the Word of God.</p>
<p>Bless those who work as electricians. Keep them safe, and 
help them do a good job so that we are safe from electrical hazards as well. 
May many in this field come to know You, and help those who do to share their 
faith.</p>
<p>I ask Your blessing and guidance today. I pray in Jesus' 
Name, Amen.</p>

