<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>A Hermeneutical Procedure To Best Interpret The Sermon on the Mount
<br><span class=byln>by Ed Vasicek</span></p>

<p>The average Christian finds it impossible to live both by 
The Sermon on the Mount and the other commands of Scripture.&nbsp; They often <i>seem</i> 
to contradict one another. </p>
<p>The problem is clearly not one of contradiction, but <i>
seeming</i> contradiction. Whereas all sorts of hermeneutical systems have 
tackled The Sermon on the Mount and essentially diluted the teachings of Christ in 
that sermon while fortifying other commands to try to find a happy medium, the 
averaging of extremes is at best unethical.</p>
<p>For example, if I take the command of Christ, &quot;Give to 
everyone who asks of you&quot; (<b>Luke 6:30</b>) and <b>2 Thessalonians 3:10</b>, &quot;if 
anyone is not willing to work, then he is not to eat, either&quot; we can 
see a clear contrast. Which do we do? Do we give to all (but not necessarily 
give them what they ask for), or do we refrain from giving food to fellow 
Christians who are lazy but give help to unsaved people who are lazy...on and on 
we go.</p>
<p>But the contradiction is cleared up if Christ's command to 
&quot;Give to everyone who asks of you&quot; is stated within its larger context because 
Jesus is applying a passage of the Torah.</p>
<p>And that is where the nature of The Sermon on the Mount 
needs to be understood. The Sermon on the Mount is the application of the Torah 
(Books of Moses, the Law) to the contemporary challenges of the Jews in the 
first century under Roman occupation. (See 
<a href="nterpsom.php" class="reg">my other article</a> on The Sermon on the 
Mount for further details.) The &quot;you have heard it said...but I say&quot; formula 
makes this clear.</p>
<p>The purpose of this paper is to enumerate the hermeneutical 
process for interpreting The Sermon on the Mount, as it would have been 
understood by the original audience, an audience well schooled in the Torah, and 
an audience who, unlike us, knew the Torah portions Christ was applying.</p>
<p><b>Categories of Teaching</b></p>
<p>Jesus teachings can be divided into three categories, and 
the interpreter must determine which of these best fits a particular verse under 
study:</p>
<ol type=1><li><p>Category one: Jesus takes a Torah principle and applies 
it to a controversial issue of that day 
(<a href="walkmile.php" class="reg">&quot;Walking That Extra Mile: A Slightly Different Twist on Matthew 5:38-48&quot;</a> and 
<a href="nterpsom.php" class="reg">&quot;Interpreting The Sermon on the Mount: Special Considerations&quot;</a> 
by yours truly for examples).</p>
<li><p>Category two: Jesus engages in the traditional Rabbinic 
practice of putting a &quot;fence&quot; around one of the 613 commands of the Torah. 
Rabbis offered many rulings to help keep people far away from a possible 
violation. Since Jesus is the Messiah, His fences now become new commands. (They 
are no longer fences.) Jesus' command not to lust after a woman is a fence built
around the command not to commit adultery; His command not to hate one's 
brother at heart is a fence built around the command to not commit murder.</p>
<li><p>Category three: Jesus adds new but related subject 
matter to the Torah.</p></ol>
<p>Most of Jesus teachings have been assumed to be in category 
3, but perhaps most of the Sermon on the Mount falls under categories 1 and 2. 
But even many of these are applications of Torah principles. For example, 
building one's house upon a rock is very much like heeding and obeying Torah.</p>
<p><b>The Process </b></p>
<ol type=1><li><p>Determine the category of the teaching.</p>
<li><p><b><u>Locate the Torah 
portion under discussion.</u></b> <i>This is often the neglected key to properly 
interpreting </i>The Sermon on the Mount. In fact, if one is not fluent in the 
Torah, he should be very careful about drawing conclusions from The Sermon on 
the Mount because he is groping in the dark. I am convinced much of The Sermon 
on the Mount is an application of 
<a href="javascript:scripture('../scripture/lev191118.htm')" target=_self class=pop>Leviticus 19:11-18</a>.</p>
<li><p>Determine the issue of the 
day being addressed. This takes research into Jewish roots literature, the 
Talmud, etc., although many issues are near the surface. For example, one 
obvious issue was, &quot;should we help carry items for Roman soldiers beyond the 
required mile?&quot; Christ answered with the concept of going the extra-mile, a 
concept He also applies toward insult (turning the other cheek--once).</p>
<li><p>Remember we have perhaps 
10% of the Sermon at most; be careful formulating dogma when we are also in 
great ignorance of what has NOT been recorded.</p>
<li><p>Ask the important question 
of all good Bible interpretation: How the original audience would have 
understood the teaching.</p>
<li><p>Ask why Matthew (or Luke) 
included such a portion in his Gospel and how his original readers would have 
understood it.</p>
<li><p>Remember, Christ spoke Hebrew or Aramaic; a portion of 
The Sermon on the Mount might become more clear if we discover the meaning of 
the original spoken (as opposed to written) word.</p>
<li><p>Remember, Christ spoke as a Jewish Rabbi to a Jewish 
crowd. Whatever insights you can glean from Messianic Judaism, the Talmud, 
or Hebraica can affect the meaning.</p></ol>

