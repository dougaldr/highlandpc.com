<p>
<b>Article VIII: Perseverance (Security)</b></p>
<p>
We believe that if a person is truly saved, he will, without exception, continue
to believe until the end. The fruit of genuine conversion is endurance.&nbsp;
Although some who appear to be truly saved may not be actually saved, the Lord
knows His own. A person who truly possesses salvation cannot lose that
salvation, but a truly saved person cannot permanently live in gross sin.<br>
(<a href="javascript:scripture('../scripture/1john219.htm')" target=_self class=pop>1 John 2:19</a>;
