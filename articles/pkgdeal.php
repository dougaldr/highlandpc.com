<p id="pgt"><span class="pgh">Articles:
<br>Ethics, Morals, Behavior</span>
<br>The Package Deal
<br><span class=byln>by Ed Vasicek</span></p>

<p>A retired man related the following story: &quot;We went out to 
breakfast. The waitress told us the 'seniors' special' was two eggs, bacon, 
hash browns and toast for $1.99. 'Sounds good,' my wife said. 'But I don't 
want the eggs.' 'Then I'll have to charge you two dollars and forty-nine cents 
because you're ordering a la carte,' the waitress warned her. 'You mean I'd 
have to pay for not taking the eggs?' my wife asked incredulously. 'Okay, I'll 
take the special,' she said. 'How do you want your eggs?' 'Raw and in the 
shell,' my wife replied. She took the two eggs home.&quot;</p>
<p>Many times God is like this waitress; He does not give us 
the option of choosing cafeteria style. His &quot;specials&quot; include package deals.</p>
<p><b>Take doctrinal matters, for example. </b>&nbsp;Most of us 
find great comfort by believing in the blessings of heaven. But, if we are 
honest, the idea of hell is upsetting to us. We are tempted to believe in 
heaven but reject hell. Fortunately, most of us realize the logical fallacy of 
trying to split up the package God has revealed!</p>
<p>Jesus taught about both heaven and hell. As a matter of 
fact, no Biblical personage spoke more about hell than Jesus. So if you believe 
in heaven because the Bible (or Jesus) said it was so, you must also accept 
belief in hell. Or you must reject both. God's restaurant is not a cafeteria.</p>
<p>The mystery of man&#39;s responsibility and God&#39;s sovereignty 
is another case in point. Many of us believe in the concept of God's Sovereign 
Grace&#8212;the idea that He freely chooses people for salvation (also known as the 
doctrine of election). Yet the Bible also teaches us that we are responsible 
for our choices. <b>John 6:37</b> is a verse that seems to contain both truths, &quot;All 
that the Father gives me will come to me, and whoever comes to me I will never 
drive away.&quot; The belief that God is sovereign but man is responsible is often 
labeled a &quot;paradox.&quot; A paradox is sometimes defined as two beliefs that seem to 
contradict one another, but actually do not. We are simply unable to harmonize 
them. For example, bumblebees do fly, but the laws of physics say this is 
impossible. It is a paradox.</p>
<p><b>Or what about the practical realm?</b> The relationship 
between faith and works is another case in point. Scripture is clear: salvation 
is a gift from God, freely given to those who trust Christ as Savior 
(<b>Ephesians 2:8-10</b>, 
<a href="javascript:scripture('../scripture/rom623.htm')" target=_self class=pop>Romans 6:23</a>). 
But the Ephesians passage does more than address the bare 
bones of salvation. Contemplate it with me: &quot;For it is by grace you have been 
saved, through faith&#8212;and this not from yourselves, it is the gift of God&#8212;not by 
works, so that no one can boast. For we are God's workmanship, created in 
Christ Jesus to do good works, which God prepared in advance for us to do.&quot;</p>
<p>Note the passage tells us that everything pertaining to 
salvation (even the faith with which we believe) is a gift given to us by God. 
We cannot brag about what we did to be saved because our only contribution to 
the whole process was the sin from which God saved us!</p>
<p>But just as God foreordained that we would believe, He also 
foreordained us to do good works 
(<a href="javascript:scripture('../scripture/eph210.htm')" target=_self class=pop>Ephesians 2:10</a>). 
What are the practical implications of this?</p>
<p>A person who claims to have come to know Christ&#8212;but who 
has no interest in doing good works&#8212;is not really saved. They go together. 
Works cannot save anyone, but someone who is saved will demonstrate that 
salvation by his or her works. This is James' point when he writes, &quot;&#8230;I will 
show you my faith by what I do&#8230;&quot; (<b>James 2:18</b>).</p>
<p>So works are like a thermometer, not a thermostat. A 
thermometer does not set the temperature of the room, it merely reveals it. The 
thermostat sets the temperature. Faith is the thermostat; works are the 
thermometer. If the thermometer shows it is 50 degrees, odds are that something 
is wrong with either the thermostat or the heating system.</p>
<p>Another tension we face is the responsibility to be in the 
world but not of the world. This is another one of those package deals we find 
difficult to handle. Christians have long gravitated toward either being part 
of the world or isolating themselves from it. And even if we agree that neither 
extreme is correct, we may struggle agreeing how this should look.</p>
<p>Some Christians trust authoritarian leaders to tell them 
how this looks. For example, churches associated with a rural, southern culture 
have their rules and regulations: women must wear skirts and no makeup, men must 
wear ties and coats to church, it is a sin to play cards, dance, or go to the 
movies. They are to stay so busy with church life that they have no time for 
clubs or a social life outside the church where they might rub shoulders with 
sinners. The bowling alley has a bar attached to it, so you don't want lost 
people to think you approve of drinking, so you stay away from the bowling 
alley, etc.</p>
<p>Interestingly, such an extreme position cannot be 
maintained without the King James version of the Bible. The translators of the 
KJV did a poor job translating <b>1 Thessalonians 5:22</b>. All other versions 
translate it something like, &quot;Avoid every kind of evil&quot; (NIV), &quot;abstain from 
every form of evil&quot; (NASB), or &quot;Abstain from every form of evil&quot; (New King 
James). But the original KJV reads, &quot;Abstain from all appearance of evil,&quot; 
nothing less than an inadequate translation.</p>
<p>So legalists have long enslaved others with the &quot;appearance 
of evil argument.&quot; So did the original legalists, the Pharisees, who accused 
Christ of engaging in evil because he socialized with evil people: &quot;The Son of 
Man came eating and drinking, and they say, 'Here is a glutton and a drunkard, a 
friend of tax collectors and sinners.' But wisdom is proved right by her 
actions&quot; (<b>Matthew 11:19</b>).</p>
<p>Although Jesus participated in His society, and was not 
into the &quot;appearance of evil&quot; thing, He was selective in what He did. So we too 
need to be selective. We must know our own selves, and we must realize that 
certain environments or activities make it easy for us to stumble. Arrogance 
about this sets us up for a fall 
(<a href="javascript:scripture('../scripture/1cor1012.htm')" target=_self class=pop>1 Corinthians 10:12</a>). 
Yet God wants us to participate 
in our community and engage our society. We cannot retreat into isolation. 
It's a package deal.</p>
<p>Achieving the Christian balance is a challenge; such is the 
result of God's package deals. Fortunately, God has not left us without a 
Word. Between studying the Word of God, the Spirit of God, and asking advice 
from the people of God, most of our choices become pretty clear.</p>

