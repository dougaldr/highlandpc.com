<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 7
<br><span class=byln>by Ed Vasicek</p>

<p>God of the earth and the sea, the heavens and all that is 
in them,</p>
<p>I praise Your Holy Name. I praise You for being everything 
You are. You are not simply holy, just, righteous, merciful, gracious, loving, 
all-knowing, all-seeing, all-powerful, all-present—You are all these things 
and all at once. You are not the sum total of all Your attributes, but these 
many attributes describe what You are like. Through them, we learn to 
understand a little about You. You are independent from Your creation, yet You 
are intimately involved with it. You do not need us, yet we can bring You joy 
or grief. You are too marvelous for words, too infinite for us to fully grasp, 
too perfect for us to imagine.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. I put on the full armor of God: the helmet of salvation to protect my 
mind, the breastplate of righteousness to guard my heart, the girdle of truth to 
protect my direction and footwear that helps me go to tell others about Jesus. 
To defend myself, I take up the shield of faith, the personal trust that is 
coupled to solid doctrine. And I go on the attack with the Word of God, my 
sword.</p>
<p>[for the married]</p>
<blockquote>Lord, thank You for what marriage has done for our society 
and this world. Help my marriage to be a blessing. Guard it, put a hedge 
around it and help us to be determined to work out our differences with an 
unswerving commitment. Help us to respect one another, to talk to one another, 
to compromise with one another and to enjoy one another's company. Help us to 
work at our marriage, to nurture it, to go out together, to occasionally read a 
book to help us renew our marriage and to really understand one another. When 
I do not understand, help me to know this. Do not allow me to think that I 
understand my spouse when I do not. Never let an attitude of contempt or 
sarcasm master me.</blockquote>
<p>[for the single, divorced, separated, or 
widowed]</p>
<blockquote>Lord, I have to admit that sometimes life is lonely. 
Although friends and family enrich my life, sometimes I regret that I am alone. 
At other times, I appreciate it! Help me to appreciate the positives: time to 
focus upon You, freedom to come and go as I please and a less complicated 
life. Help me to participate in church and community. If I am left out, help 
me to avoid being sensitive about that. Remind me that this is usually not 
intentional. Many couples have no idea that I want to be included; their minds 
blind them to the reality that singles have social needs too. Help me to 
clearly express that I want to be included—rather than dropping hints. Lead 
me to a few good Christian friends of my gender to pal around with. Keep me 
from taking the stance of a victim.</blockquote>
<p>Lord, I pray for the Madhya 
Pradesh and Chattisgarh states of India with their 62 million people. Well 
less than one per cent claim any form of Christianity and of that small 
percentage, most are not evangelical. Yet, Lord, reports indicate that churches 
are being planted and that You are beginning to work in greater ways. According to 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>,
"This state was one of the last to open up for 
missions with slow response to the gospel until recently. It is strongly Hindu 
with stern laws limiting conversions to Christianity.... Thousands of tribal 
peoples have been forced to renounce Christianity. The whole state is a pioneer 
mission field, but is becoming more responsive.&quot; O, Father, may many in this 
area come to know Christ. Grant millions here the repentance that leads to 
life, I ask.</p>
<p>I pray for my home church today and the missionaries it 
supports. Help my church to follow the teachings of Your Word and may its 
missionaries be true to the Gospel. Protect and provide for them, I pray.</p>
<p>Now help me remember that You have called me to serve You; 
help me live accordingly this day, I pray in the Savior’s Name, Amen.</p>

