<p>
“Baptism” comes from the 
Greek word “baptizo,” meaning “to immerse or dip.” We understand the Scriptures 
to teach that believers in Christ are to be immersed in “the name of the Father, 
Son, and Holy Spirit.” Although baptism may be the formalizing of conversion, 
we believe individuals are saved at the point of belief. </p>
<p>
Here is some interesting 
material from Brad Young’s book, <i>Jesus, the Jewish Theologian</i>, pp.15-16:</p>
<p>
“In the Jewish teachings of 
the Mishnah, living water—defined as rivers or seas—was the highest grade of 
cleansing for ritual immersion (m. <i>Mikvaot</i> 1:6). The person was required 
to be immersed in living water which could be collected from rain or rivers....  
It is...significant that the Dead Sea sect required religious purity before 
baptism. The individual seeking baptism was required to turn from wickedness 
before entering the waters of ritual immersion. Repentance preceded baptism in 
the Dead Sea community.</p>
<p>
John the Baptist 
employed the same standard. The one who desired baptism was required first to 
turn from sin and then approach the waters with a pure heart.... Josephus 
explains that in John’s teachings repentance purified the soul while baptism 
cleansed the body. Josephus remarks concerning John the Baptist:</p>
<blockquote>
 For Herod had 
put him (John the Baptist) to death, though he was
 a good man and 
had exhorted the Jews to lead righteous lives, to 
 practice 
justice towards their fellows and piety towards God, and so 
 doing to join 
in baptism. In his view this was a necessary preliminary
 if baptism was 
to be acceptable to God. They must not employ it to 
 gain pardon for 
whatever sins they committed, but as a consecration
 of the body 
implying that the soul was already thoroughly cleansed 
 by right 
behavior.</blockquote>
<p>
John called on the people 
to practice piety and pursue a lifestyle of right conduct. The center of John 
the Baptist’s activities was located close to the geographical area of Tel 
Qumran and near the caves where the Dead Sea Scrolls were discovered. Moreover, 
John’s theological understanding of baptism seems to be similar to the approach 
taken to the ritual immersion practiced by the Dead Sea sect. Was John a member 
of the Dead Sea community?... No.... He did not stipulate that they sell all 
their possessions and join his group of disciples who waited for the last days 
in the wilderness. Instead his prophetic message implored the people to live a 
holy life in the mainstream of society. <u>The religious mindset at Qumran 
could not tolerate John’s approach.”</u></p>
