<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 24
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty God,</p>
<p>You are the Lord God of Israel. You revealed Yourself to 
Abraham, Isaac and Jacob whose name was changed to Israel. You promised the 
Messiah would come from this line and more specifically the tribe of Judah and 
the family of David. You gave the Law through Moses to the children of Israel. 
You gave them the tabernacle and sacrifices and the temple and the prophets. 
And finally through Israel came the Messiah. He came to His own and those 
who were His own did not receive them. But to those who did, He gave them 
authority to become the children of God 
(<b>John 1:11-12</b>). And Your plans for Israel will yet be carried out 
(<a href="javascript:scripture('../../scripture/rom112529.htm')" target=_self class=pop>Romans 11:25ff</a>). 
In Christ, we are blessed 
along with believing Israel and have the same type of faith exemplified by 
Abraham. So You, the God of Israel, are the God of all those who have accepted 
Jesus (Yeshua) as Messiah.</p>
<p>I am a vulnerable person as are all we mortals. Every 
breath depends upon Your grace. Yet You are pleased when I depend upon You. You 
take pleasure in my prayers. So please, Lord God, meet the following needs, 
requests and desires as You see fit:</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I 
find my strength in His power not my own. May Your Spirit control me
today. I surrender myself afresh to Him. Instead of grieving and resisting 
Him, help me to please Him and submit to Him.</p>
<p>Father, help me to remember that there are always two sides 
to hear. I am reminded of <b>Proverbs 18:17</b>, “The first to present his case seems 
right, till another comes forward and questions him.” Let me remember this, even 
when it seems the party who has not been questioned could not possibly be 
innocent. Because I haven’t thought of an explanation does not mean there is 
none.</p>
<p>Father, the nation of Guyana in South America is truly 
unique. Many of the people who inhabit this country are from Asia or of African 
descent. As a result, many people are Hindu while a minority are Muslim; most 
profess some form of Christianity. About 11% of the people claim to be 
born-again evangelical Christians while many others claim to be born-again but 
not affiliated with evangelical churches. In many ways, You have greatly 
blessed missions outreach and the church is growing. Thanks for the religious 
freedom the citizens of this nation enjoy and bless the nation with peace, 
freedom and an ever-growing number of born-again evangelicals!</p>
<p>Thank You for the work of Seed International. Thank You 
for this Korean-based missionary organization that serves local 
churches, equips missionaries, evangelizes nations 
and disciples nationals. Help them remain true to Your calling, 
grant them harmony amongst themselves, empower them by Your Spirit and provide 
for their needs. May this ministry be fruitful and make a significant 
difference for the Kingdom of God.</p>
<p>I remember those who work as surveyors. May they do their 
jobs well and honestly. May those who know You live out their faith before 
others.</p>
<p>So, Lord, I go into my day as Your servant; help me to be a 
faithful servant today, I pray in Jesus’ Name, Amen.</p>

