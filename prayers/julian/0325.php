<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 25
<br><span class=byln>by Ed Vasicek</p>

<p>Lord, </p>
<p>You are the source of all life. Life does not come from 
non-life but only from life. Therefore all who live owe their existence, 
their possession of this mysterious property called &quot;life,&quot; to You. Thank You 
that Your Son came that we might have life, and have it more abundantly 
(<a href="javascript:scripture('../../scripture/john1010.htm')" target=_self class=pop>John 10:10</a>). 
Through Jesus Christ, we experience a deeper level of life as our 
spiritual nature is regenerated and we become new creatures in Christ. Although 
we were dead in our trespasses and sins, You have resurrected us to spiritual 
life.</p>
<p>Father, the longer it is that I have known You, the more 
difficult it is to remember what life was like before You made me one of Your 
own. At first the difference was so clear, but now I have become accustomed 
to this higher level of living.&nbsp; Lord, help me not forget the mire from which 
You have lifted me, the lost estate from which You delivered me!</p>
<p>Lord, You have not called all of us to be missionaries in 
the sense of crossing cultures to share Christ, but You have called all of us to 
be witnesses, to testify to the Gospel. Help us remember that all of us have 
this responsibility. Rather than responsibility, help us to view what it mostly 
is: a privilege. Although we can worship You now, we will worship You even more 
so in heaven, but we will not be able to reach lost people in heaven with the 
news of salvation. Help us to appreciate our current opportunity.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me of them, and You will cleanse me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I
do not excuse them, but I freely admit them and ask for Your&nbsp;forgiveness.</p>
<p>Lord, El Salvador is certainly a modern miracle. Thank You 
that the evangelical church has grown from 2.3% in 1960 to 22% in 2000. Despite 
war with all its consequences, Your Kingdom has surfaced in a strong way. As 
the land now experiences peace and freedom, we pray that the church would 
continue to grow in strength and we ask that You would help them minister to 
the many victims of war including the massive number of orphans. Provide 
strong leaders, and I pray that El Salvador would become a lighthouse to the 
rest of Latin America.</p>
<p>Oh, Father, I pray for “Campus Crusade for Christ International, a worldwide, 
interdenominational Christian ministry committed to showing people how they can 
know and experience God's plan for their lives.” Thank You for their presence 
on university campuses around the world. Thank You for their great outreach via 
The Jesus Movie, one of the greatest and most successful evangelistic methods of 
all time. Thank You for the hundreds and hundreds of thousands who have come to 
know Christ through this movie! Thank You for the 4 Spiritual Laws, their 
publishing and literature ministries and their defense of the faith. Keep them 
true to You and Your Word, help them work harmoniously with one another; provide 
for them and empower each staff member by Your Spirit.</p>
<p>Now help me serve You today, I ask, in Jesus’ Name, Amen.</p>

