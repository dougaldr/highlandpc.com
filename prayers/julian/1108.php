<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 8
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Heavenly Father,</p>
<p>I call You Father because You are my Heavenly Father, first by virtue of 
creation, second by virtue of redemption and third by virtue of Your Person. You 
are the Father of all who come to know You through Jesus Christ. Thank You that 
as a Father has pity on His children, so You have pity upon us. Thank You that, 
as a good father loves, trains and cares for the wellbeing of His children, You 
care for us. We glory in that we have been adopted into Your family by faith in 
Jesus Christ.</p>
<p>Lord, I thank You that You have called me to be among those titled “kingdom 
of priests.” I have direct access to heaven through Jesus Christ my Lord; I can 
intercede for those Who are lost or Who need special spiritual attention and I 
can offer the sacrifice of praise to You, namely, the fruit of my lips 
(<a href="javascript:scripture('../../scripture/heb1315.htm')" target=_self class=pop>Hebrews 13:15</a>). 
I am part of the royal priesthood; I am a child of the King.</p>
<p>So Lord, help me to pray without ceasing, praying off and on throughout the day. 
May I remember to send up casual and quick prayers and praise all day long. 
May I be ready to pray at the drop of a hat.</p>
<p>Father, help me to manage my finances in a way that pleases You. Prosper me 
to the degree You see fit. Do not allow me to become too wealthy if that means I 
will turn away from You; keep me from being too poor so that I am not tempted to 
be dishonest. Help me trust You during good times and bad and may I be a 
responsible and honest steward of what You have given me. Thank You for the 
trust You have given me.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and just to 
forgive me of them and You will cleanse me from all unrighteousness (<b>1 John 
1:9</b>). So I bring my sins before You now. I do not excuse them, but I freely 
admit them and ask for Your forgiveness.</p>
<p>Lord, what is left of Yugoslavia (Serbia and Montenegro) is a spiritual, 
political and economic mess. The horrible wars in the early 1990s have left 
scars and consequences that are difficult to address. Thank You for the 1.4% of 
the 10.5 million who know You. O Lord, may multitudes turn to You and find 
salvation in Christ, I pray. Use Christian literature, Christian radio and the 
few Christians who know You to multiply the church tenfold over the next ten 
years, I pray. Help this country recover and may they resolve their issues 
fairly and peacefully, I pray.</p>
<p>Today I ask You to bless Spanish World Ministries. May their radio program, 
El Camino De La Vida, continue to be used of you to reach many throughout 
Central and South America as well as Spain and elsewhere. Bless this mission 
as they help national workers to &quot;reach their own Jerusalem.&quot; Bless their 
director, Cornelius Rivera. Provide for them, protect them, grant them harmony, 
empower them and give them bountiful fruit for their labor, I pray.</p>
<p>Help me to show Christ’s love to someone this day, I request, in Yeshua’s 
Name, Amen.</p>

