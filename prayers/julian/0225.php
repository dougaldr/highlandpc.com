<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 25
<br><span class=byln>by Ed Vasicek</p>

<p>Divine Shepherd,</p>
<p>You are the Sustainer of all things. God the Son holds the 
entire universe together. We know that, if we condensed the earth to actual 
matter, removing all the forces between the parts of the atoms, it would be the 
size of a pea. And it could be that those smallest components are themselves 
mostly forces. It is You, Lord, Who sustain everything.</p>
<p>Father, thank You that one day my body will be 
resurrected. When my time on earth is through, my soul will enter Your presence 
in heaven. But when Jesus Christ returns to the clouds at the sound of the 
trumpet, my body will be resurrected as a new immortal one. As Job said, even 
after worms destroy my body, yet in my flesh shall I see God.</p>
<p>Of course if I am alive when Jesus returns, my body will be 
transformed as I ascend to meet Him.</p>
<p>So, Lord, help me to find encouragement in You day by day. 
When life is pleasant and positive, I do not think much about encouragement, 
other than encouraging others. But when times are bleak, disappointing, or just 
plain miserable, thank You that You are there for me. Help me to retreat from my 
bleak times and find You a Friend and Confidant.</p>
<p>Lord, please take care of my loved ones. Send angels to 
watch over them, Your Spirit to draw them to Yourself and to guide those who 
know You, and to direct them in the way they should go.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Today I pray for the nation of Egypt, a nation familiar to 
every student of Scripture. Lord, thank You for the nearly 3% of the population 
who know You. We know the challenges are great as Islam pressures the 
authorities to become less and less tolerant of Christians. We pray that 
persecution would stop, and that freedom would be guaranteed both on paper and 
in practice. Bless the Jesus film, the evangelical churches, and the 
evangelical movement within the Coptic Orthodox Church. We pray that the number 
of Christians would double within this decade, and that the people of Egypt 
would gain a new respect and tolerance for evangelical Christianity.&nbsp; We also 
pray that You would provide leaders for the church and training for lay leaders.</p>
<p>Today I pray for the Pandita 
Ramabai Mukti Mission, and evangelical outreach in India. Bless their orphanage, 
their housing, and their church as they reach out with the love of Christ and 
His Gospel. Provide for them and keep them true to You. May Your Spirit empower 
them so that they bear much fruit for You.</p>
<p>Today I pray for those who labor in our factories. Keep 
them safe and accident free, help the Christians who thusly labor to work as for 
You, and help them to reach many lost coworkers with the Gospel.</p>
<p>So, Lord, help me 
to walk with You today, I pray, in Jesus’ Name, Amen.</p>

