<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 17
<br><span class=byln>by Ed Vasicek</p>

<p>My Father,</p>
<p>You control what seems to be chance for us. Our uniqueness 
was determined when our parents' genes came together and were shuffled. Yet 
<b>Proverbs 16:33</b> says: &quot;The lot is cast into the lap, but its every decision is 
from the L<span class="sm">ORD</span>.&quot; According to 
<a href="javascript:scripture('../../scripture/ps13916.htm')" target=_self class=pop>Psalm 139:16</a>, 
even our development in the womb is 
under Your jurisdiction. In <b>Exodus 4:11</b>, we read, &quot;Who gave man his mouth? Who 
makes him deaf or mute? Who gives him sight or makes him blind? Is it not I, the 
L<span class="sm">ORD</span>?&quot; So, Father, if we like how You made us or dislike it, we have to 
believe You have a plan, a plan so complicated that sometimes we have no clue as 
to what it is You are trying to accomplish.</p>
<p>Lord, I have some disappointments and burdens in life. A 
few of them hurt me and have left scars including: __________. I turn them over 
to You. Help me to find Your purpose and joy in life. There 
are many things I cannot change, but I can share all my thoughts with You. You 
are a God Who feels with me. You help me to go on and grow and direct my 
attention elsewhere while, at the same time, not pressuring me to &quot;get over 
it.&quot;</p>
<p>Lord today, I especially remember those looking for a job. 
Open doors of opportunity and match the right person to the right job. 
Especially help those who work with integrity and have good character to find 
employment.</p>
<p>Also, dear Father, help me to handle my finances well. 
Guide me when it comes to saving, giving and help me know when to say no. Help 
me control my money under Your guidance; please, do not let money control me.</p>
<p>I pray for the Heilongjiang Province of China with its 38 
million people. Lord, many of the people in this region are Shaministic or 
Buddhist, but thank You that perhaps 4% of the population is made up of 
born-again evangelicals. Thank You that house churches are growing. Yet there 
are few trained Christian leaders in this region. In addition, the Mongolian 
and Altaic people in this area are less reached. Father, pour out Your Spirit 
on this region. May the number of believers double in the coming decade, I 
ask. Provide leadership opportunities to train solid pastors, I ask.</p>
<p>Father, please bless the ministry of Christian Associates 
International as they seek &quot;to reach the unchurched through the multiplication 
of high-impact leaders and high-impact churches.... [They] desire is to see God work 
powerfully in...the cities of Europe and the world.&quot; Grant them harmony, help 
them remain faithful to the truth of Your Word, protect and provide for them 
and grant them an abundance of fruit to Your glory, I ask.</p>
<p>I pray for those who work as auto service technicians/mechanics. 
Help those who know You to shine and share and may all be men and women of 
integrity, I ask.</p>
<p>Now, Lord, help me to a person of integrity today in 
Jesus' Name, Amen.</p>

