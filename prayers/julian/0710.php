<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 10
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Father,</p>
<p>When the children of Israel wandered in the wilderness, 
many of them were arrogant, demanding and had an attitude of entitlement. You 
let them get hungry before You provided them with manna. You let them get 
thirsty before You provided them with water from the Rock. So, Lord, You are 
our Divine Developer. You create a thirst in us which only You can satisfy. 
You are out for my best interest. You enjoy seeing Your children mature in their 
faith and You take us in paths we ourselves would not choose.</p>
<p>But too often, Lord, I fail You. First John says that if I 
confess my sins, You are faithful and&nbsp;just to forgive me those sins and You 
will purify me from all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am 
aware of now and I ask Your Spirit to search my heart (<i>Pause to admit them 
to God.</i>).</p>
<p>Lord, strife within families is awful. Please bring 
together members of my family (or of a family I know) who are at odds with 
other family members. We ask that the offenders would embrace repentance, 
apology, restitution and taking responsibility. Help them to be reasonable and 
willing to establish credibility over time if that is what is needed. And we 
pray that those offended would be gracious yet not naïve. We know some 
situations can never be fixed, but we pray that many relationships would be 
reconciled.</p>
<p>I especially remember those who are seriously ill and have 
younger children. Lord, we all expect to die—unless delivered by rapture. 
But, Lord, children need their parents so badly. Please preserve their lives 
and their health, I ask.</p>
<p>Today I remember the South American nation of Peru. I 
praise You that 8.7% of the population claims to be born-again evangelical. I 
thank You for the great growth rate. Father, this nation has experienced many 
internal wars. We pray that peaceful and just government would be established. 
Poverty, unreached people groups and native superstition are all challenges for 
the church. Thank You that translations of the New Testament are in progress 
for 17 people groups with 12 in planning. Lord, may the number of evangelical 
believers grow in Peru and may Christian values spread rapidly. Help the 
nation to become wealthier as these values are put into practice. May Your 
Spirit draw many to Yourself.</p>
<p>I pray for The International Fellowship of Evangelical 
Students, &quot;a global fellowship of indigenous national student movements formed 
in 1947 with the aim of starting and sustaining students witness in every 
university in the world. From an initial 10 movements, today the work spans more 
than 140 countries including IVCF in the United States alongside IVCF and GBUC 
in Canada.&quot; Lord, help Christians in these universities to stand strong and find 
answers to their questions. May they win many others to personal faith in 
Jesus Christ and see them discipled as Your Spirit empowers, provides and 
motivates Your children on campus.</p>
<p>I pray for those who work as home appliance repairers. May 
they do their work well. Help them to be fair, honest and wise in dealing with 
people. May those who know You share Christ freely with others especially with 
their peers.</p>
<p>Use me to Your glory today, I pray in Jesus' Name, Amen.</p>

