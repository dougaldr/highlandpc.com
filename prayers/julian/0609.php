<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 9
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a God Who needs nothing. You have no voids, 
deficiencies, or complexes that need addressing. You take pleasure in Your 
creation, but You do not need us. We need You, but You take pleasure in us and 
find joy in relating to us. As we grow in You, we learn to both rely upon You 
and we begin to take pleasure and to rejoice in You. But we will forever need 
You. So since You are not dependent upon us, You do whatever You please. And 
it is in Your good pleasure to call us Your friends, to adopt us into Your 
family and to shower and lavish us with Your grace. Although when it comes to 
money management, we view frugality as a virtue, You are anything but frugal 
with Your grace!</p>
<p>I pray for the elderly, sickly and shut-in including: __________.</p>
<p>Sometimes, Lord, I have to admit that I feel like I am in a 
rut. A happy routine can be wonderful, but a boring one is not so exciting. 
Remind me that it is in the daily grind of routine, duty and habit that gives 
me the best opportunity to prove myself faithful. And help me remember that as 
long as I live for You, there is always more to discover; there are always more 
adventures to be had, always new avenues to pursue. Help me stretch the muscles 
of faith and tackle something that will help Your kingdom. May Your joy in my 
life be real, not forced or contrived!</p>
<p>Lord, I pray that You would protect my loved ones and I 
throughout our lives. Send guardian angels to watch over us, I pray.</p>
<p>I remember my brothers and sisters in Christ who are being persecuted. 
Thousands have died for their faith over the last decade, while others have been 
tortured and discriminated against. I especially pray for the nations where 
persecution is worst: Egypt, Saudi Arabia, Peru, Cuba, Colombia, Uzbekistan, 
Turkmenistan, Burma, Indonesia, Sri Lanka, Pakistan, India, China, North Korea, 
Vietnam, Laos, Sudan, Nigeria and Eritrea. &nbsp;O Lord, may the persecution stop; 
give believers wisdom to evade persecution and when they cannot, help them to be 
strong. (For more information about the persecuted church, go to
<a href="http://www.persecution.com" target="offline" class="reg">www.persecution.com</a>.)</p>
<p>My Lord, I ask You to bless the ministry of REAP Mission 
Inc. in Japan. Thus far they have started 41 congregations, they conduct over 
30 evangelistic campaigns per year, distributed over 111 million pieces of 
literature in less than 50 years and sent out 301 Japanese believers to 24 
countries. O Lord, may the next 50 years—should You tarry—be five times 
greater. Bless this mission with the continued power of Your Spirit, provision, 
safety, great fruit and harmony among the members.</p>
<p>I pray for those who work as tire builders. Give them 
safety on the job and help those who know You to shine by their good works and 
disposition. Help them to share Jesus with their fellow workers, to bear 
witness casually and honestly.</p>
<p>Now send angels to watch over my loved ones and me today, I 
ask in Jesus' Name, Amen.</p>

