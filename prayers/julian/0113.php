<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 13
<br><span class=byln>by Ed Vasicek</span></p>

<p>O Lord our God,</p>
<p>I worship You as the unchanging one, the one who does not 
shift or vacillate, the One Who is never surprised, the One who is compared to a 
rock. You, O Lord, do not change. Everything around me changes. Everything 
around me had a beginning. But You are perfect, complete, and never needing to 
adjust. Therefore it makes sense that I should put my trust in You, build my 
life upon You, and love You with all my heart. I need never fear You to be 
fickle, or to alter Your promises after You have made them.</p>
<p>Father, I thank You for the ordinances that You ordained to 
help us appreciate the work of our Savior. I thank You for baptism which 
pictures on the outside what has happened on the inside the moment we believed: 
we died, were buried, and were resurrected with Christ. Thank You that we share 
these inward realities publicly by the outward ritual. Although baptism does not 
save me, it pictures the repentance that did.</p>
<p>I also thank You for the Lord's Supper. You structured 
this simplest of meals to remind us constantly that, though our salvation was 
free to us, it was costly to You. Our Lord Jesus had to suffer for us so that 
we could be forgiven. We look forward to His return and the feast that awaits 
us in glory.</p>
<p>Lord, these two ordinances remind me of the importance of 
examining my heart. Help me to care about my spiritual condition, to confess 
sin, and to seek Your face daily.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>Father, bless me with peace today. As our Savior greeted 
His disciples with &quot;Shalom,&quot; so I need Your Shalom, Your peace—to guard my 
heart and mind in Christ Jesus.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Today I pray for the Caribbean nation of Antigua and 
Barbuda. Although many claim to know Christ there, the moral situation is bad: 
drugs, prostitution, and kidnapping are common occurrences. We pray, Father, 
that You would revive the church and that these evils would be destroyed.</p>
<p>Lord, today we pray for Bible-believing evangelical 
denominations and fellowships. We also pray for the many good independent 
churches in our land. Bless them with fruit, keep them true to the doctrinal 
and ethical teachings of Your Word, and may there be a renewed interest in Bible 
study and content as opposed to a feel-good or consumer-driven Christianity.</p>
<p>Today we pray for the Federal Bureau of Investigation. May 
they continue to be a force for good, successfully attacking crime and evil. 
Help the agents to come to know You, and may You use them for good. Protect and 
direct them.</p>
<p>Now, Lord, bless 
this day as I give it to You. I pray in Jesus' Name, Amen.</p>

