<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 6
<br><span class=byln>by Ed Vasicek</span></p>

<p>Mighty God, Abba Father,</p>
<p>I thank You that Your Son took upon Himself the role of 
Savior. A savior is one who delivers or saves another from danger or disaster. 
The Lord Jesus Christ is our Savior from sin and from Your wrath. Our sin 
provokes You to anger and demands justice. Our Savior has delivered us from the 
demands of Your justice by satisfying the just penalty for our sins. He did 
this while on the cross, when He—the sinless one—became sin for us. Thank 
you that He bore our sin in His body on the tree.</p>
<p>Father, I thank You that You have grace for all. Your 
common grace causes it to rain for the just and the unjust. You feed the birds 
and sustain life, though You have no obligation to do so. Your special grace is 
showered upon those who know You.</p>
<p>Help me to respond to Your grace by trusting You, and 
thereby I affirm Your character and glorify You. As I experience Your saving 
grace and Your sustaining grace, help me, in turn, to be gracious to others, not 
exacting my due.</p>
<p>Lord, I have many needs to bring before Your throne. 
Provide me with people who will support me in life’s bad times and celebrate 
with me during life’s good times. I have many other needs, a few of which 
include: __________.</p>
<p>Today I pray for the nation of Iran. This country, 
formerly known as Persia, has a long history. Unfortunately, there are very few 
in this land who know Jesus Christ as personal Savior. Lord, change the 
government in this land and establish one that recognizes human rights, 
including the right to share and propagate religious beliefs that are not in the 
majority. Help the few Christians there to be wise, and open doors for the 
Gospel. Bless radio broadcasts and other avenues of communication for the 
Gospel. May the number of believers in Iran increase exponentially.</p>
<p>Lord, today I remember FIM, The Fellowship International 
Mission. Bless them as they minister to difficult areas, like Morocco and 
Niger. Missionaries serve on a number of continents, and are doing a vital 
work. Please, Lord, give them fruit for their labor, safety, provision, and 
help them to relate well and work with one another well. May Your Spirit use 
them greatly as they share the faith and disciple believers.</p>
<p>I also remember the counselors in our community, especially 
the Christian ones. Help them to direct their clients into Your truth, and may 
they impact many lives for the good.</p>
<p>Now bless me this 
day, I pray, in Jesus’ Name, Amen.</p>

