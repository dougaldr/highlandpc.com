<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>Peter’s Special Role
<br><span class=byln><b>Notes on Matthew 16:13-17:1-8, Mark 8:27-9:8, Luke 9:18-36</b>
<br>by Ed Vasicek</span></p>

<p>We see three 
seemingly unrelated events in the Gospel narrative (Peter’s confession, Christ’s 
rebuke of Peter, and the Transfiguration) which are, in fact, related. After 
Peter’s confession, Jesus begins discussing His Church, an entity that was yet 
future but still part of the “Kingdom of God.” This shift in direction was 
planned from all eternity 
(<a href="javascript:scripture('../../scripture/eph36.htm')" target=_self  class=pop>Ephesians 3:6</a>) 
but kept “hush” by God until Jesus uttered, 
“I will build my church.” Let me comment upon the first event that sparked the 
others – Peter’s confession of faith.</p>
<p></p>
<p>Let’s begin by noting that 
Jesus validated Peter’s confession, “You are the Christ.” Our Savior stated 
that the Father had revealed this truth to Peter. Some allege that Jesus never 
claimed to be the Messiah. The reality is that He repeatedly made such claims 
(see 
<a href="javascript:scripture('../../scripture/john42526.htm')" target=_self  class=pop>John 4:25-26</a>), 
although He was frequently tactful and evasive.</p>
<p></p>
<p>Roman Catholics believe 
that Peter was the first “pope” and use this passage to warrant their 
viewpoint. I suggest that they begin with their doctrine and try to force it 
upon the Scriptures. Approaching God’s Word with a preexisting agenda makes 
for strained interpretation at best.</p>
<p></p>
<p>Please note the following:
<ol><li>there is no mention or implication of succession of office here;
<li>Peter is nowhere called “pope,” nor is a specific church office 
mentioned. The privileges given to Peter were equally given to the other 
apostles (see 
<a href="javascript:scripture('../../scripture/matt1818.htm')" target=_self  class=pop>Matthew 18:18</a> 
where the privilege of “binding and loosing” were 
given to all church leaders, and 
<a href="javascript:scripture('../../scripture/eph220.htm')" target=_self  class=pop>Ephesians 2:20</a>, 
where all apostles and prophets were 
considered foundational); 
<li>in <b>Acts 15</b>, James seems to be the most 
influential, and as we progress within the book of Acts, Paul is the man to 
watch. Also note that Christ blessing Peter in this unique way is only found in 
Matthew. This does not negate the reality of the event, but it does demonstrate 
that whatever Jesus told Peter was not as important as are events included in 
three or all four gospels. Mark and Luke include Peter’s confession, but not 
the blessing Christ gave him. If the fate and direction of all Christendom 
relied on Peter, one would expect to find at least one other reference somewhere 
in the Bible to that effect!</ol>
<p><u><b>What IS significant</b></u>
about Peter’s confession is that it is the FIRST clear cut confession from among 
the Apostles (although many individuals had already made this confession, e.g., 
the Woman at the Well). Of the twelve, Peter is the one to go out on a limb (or 
walk on water) for Jesus. Peter is presented as a special initiator, and he did 
initiate the church’s ministry to the Jews, the Samaritans, and finally to 
gentiles. He was the church’s “jump-starter.”</p>
<p></p>
<p>The Jews called “Abraham” 
the “little rock.” Because he was the FIRST in the line of Jews, all Judaism 
was based on his example of faith. In the same way, Christ calls Peter a 
“little rock” (Greek), and all Christianity is based on his example of 
“confessing that Jesus is the Christ.” All believers are chips off of the 
ultimate Rock, the Lord Himself, or, as Peter explains in <b>1 Peter 2:4-5</b>,
“As you come to 
him, the living Stone—rejected by men but chosen by God and precious to him—you 
also, like living stones, are being built into a spiritual house to be a holy 
priesthood....”</p>

