<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 8
<br><span class=byln>by Ed Vasicek</span></p>

<p>Father of Eternity,</p>
<p>I praise You and worship You even though You do not need my 
praise and worship. You do not need anyone or anything. Although You interact 
intimately with Your creation, and although You keep track of the hairs on our 
heads, You are completely independent of Your creation. You need no 
infrastructure, no space, no time, and You have no vulnerabilities. How unlike 
me. I need oxygen for each breath, must depend upon my heart to pump, need food, 
water, and shelter. If I am careless, I can easily hurt myself. But You are 
independent and self-sustaining.</p>
<p>Father, because I am so vulnerable, I sometimes am strapped 
by fear. On the one hand, Your word tells me that a wise man is cautious, but 
when that caution turns to fear, I am held in bondage to it. Help me, 
therefore, to be cautious but not fearful; trusting in You, but not presuming.</p>
<p>Lord, please minister to my family and close friends. Help 
us to nurture and encourage one another; may our relationships be positive and 
constructive. May those who do not know You come to know You, and may those who 
do know You grow to an ever-deepening maturity.</p>
<p>Lord, I am thankful for many things, three of which are: __________. </p>
<p>Today I would like to pray for the nation of American 
Samoa. On the one hand, there are many believers in this Pacific Island 
nation. But cults, like Mormonism, are growing faster than Biblical 
Christianity. We pray that Your Spirit would hover over this land, and that 
many would come to know the One and True God.</p>
<p>Closer to home, we pray for the ministry of Focus on the 
Family. We thank You for their radio programs, books, publications, and 
influence for good, a good that helps literally millions of American families. 
Bless the leadership of this ministry, and help Dr. Dobson as he appears 
frequently as a guest throughout media news programs.</p>
<p>Today we pray for those in our area who work to get us 
clean and healthy water—or those agencies that test our water to make sure it is 
safe. Bless their work, and help us to appreciate their work.</p>
<p>Give me the energy I need to fulfill my responsibilities 
this day, I pray, in Jesus' Name, Amen.</p>

