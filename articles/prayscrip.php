<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Prayers—Descriptions and Prescriptions
<br><span class=byln>by Ed Vasicek</span></p>

<p>Those of us who teach the Word are sometimes guilty of 
oversimplifying complex subjects. This is especially true with prayer, one of 
the most complicated issues to understand. It is not difficult to understand 
HOW to pray, but it is difficult to understand God’s response to our prayers.</p>
<p>During my personal quiet times lately I have been reading 
through and thinking about the Book of Acts. When I found myself in chapter 12, 
I noted a prime example of this confusing issue.</p>
<p>We note in 
<a href="javascript:scripture('../../scripture/acts121.htm')" target=_self  class=pop>Acts 12:1</a>
that King Herod, son of Herod the 
Great, was arresting and persecuting Christians. In the process, he arrested 
the Apostle James, the brother of the Apostle John, (not James, Jesus’ brother, 
who wrote the book of James). Together, the Apostles James and John were known 
as the “Sons of Thunder.” Their mother tried to persuade Jesus to seat them at 
His left and right when Christ would establish His kingdom.</p>
<p>James was arrested. We can be sure that the church, 
including the other apostles, prayed and fasted for James and the other 
Christians who were imprisoned. But the text relays the bleak information that 
“He had James, the brother of John, put to death with the sword” (<b>Acts 12:2</b>).</p>
<p>But what is interesting is that a few verses down, we find 
that when Herod saw the execution pleased some of the Jewish rulers, he decided 
he would do the same with Peter. Peter was arrested and the plan was to execute 
him after the Passover. The church prayed for Peter, and this time God granted 
the request. The night before Peter was to be executed, God sent an angel to 
literally lead Peter by the hand out of prison 
(<a href="javascript:scripture('../../scripture/acts12310.htm')" target=_self  class=pop>Act 12:3-10</a>).</p>
<p>Note the similarities between these two situations. Both 
men were faithful apostles, hand chosen by Jesus Christ. Both men had great 
faith, and both were loved and prayed for by the church. But their outcomes 
were entirely different one from another.</p>
<p>Additionally, it is interesting that Peter had to suffer in 
prison for days before God delivered him. James also suffered in prison and God 
did not deliver him. In the case of Peter, we could postulate that God allowed 
him to suffer to develop his faith and toughen him up for his future ministry. 
But James was roughed up just before he went to heaven.</p>
<p>We have to face this reality: there is no consistent way 
God answers prayers. Or, better yet, God answers prayers in a way that is 
consistent with His character and sovereign will, but we cannot discern the 
pattern.</p>
<p>Up until chapter twelve, we might argue the following: For 
the time being, Peter had it easier. He did not suffer while James did. But, 
then again, James went to heaven and was rewarded for suffering as a martyr. 
And if we fast-forward in time, things take another twist.</p>
<p>The last chapter in John’s Gospel contains a prophecy. 
<b>John 21:18-19</b> reads, “Jesus 
said, ‘Feed my sheep. I tell you the truth, when you were younger you dressed 
yourself and went where you wanted; but when you are old you will stretch out 
your hands, and someone else will dress you and lead you where you do not want 
to go.’ Jesus said this to indicate the kind of death by which Peter would 
glorify God. Then he said to him, ‘Follow me!’”</p>
<p>
History tells us that Peter died in old age by crucifixion. Yet John's comment 
is revealing, “Jesus said this to indicate the kind of death by which Peter 
would GLORIFY GOD.” And therein lies one great insight: when it comes to 
prayer, sometimes God’s glory is prioritized over our comfort.</p>
<p>So 
when you contrast James and Peter, you see not only a difference when it comes 
to length of ministry, but also a difference in how they glorified God when they 
died. Peter was delivered from prison as a young man, but he was not delivered 
in old age.</p>
<p>We 
can add further to the unpredictability as to how God answers our prayers by 
looking at James' younger brother, John. Although John and Peter were briefly 
imprisoned and scourged (<b>Acts 4</b>), John lived the longest of any apostle. As a 
matter of fact, during Christ's earthly ministry, John was still a teenager! In 
old age, according to church tradition, he was boiled in oil (the Romans were 
cruel) but survived. He was then exiled on the isle of Patmos, from which he 
wrote the Book of Revelation in about 90 or 95 A.D., perhaps at about 80 or 85 
years of age. Although his brother, also an apostle, died in young adulthood, 
John lived to be an old man.</p>
<p>I believe in all the above cases, believers prayed 
aggressively for the deliverance of these individuals. But God’s answers 
varied. It might be no secret what God can do, but what He’s done for others, 
He will NOT NECESSARILY do for you. He WILL DO what glorifies Himself according 
to His plan for your life, fulfilling His overall purpose to work all things 
together for good to them that love God 
(<a href="javascript:scripture('../../scripture/rom828.htm')" target=_self  class=pop>Romans 8:28</a>).</p>
<p>Prayer is but one of many subjects Bible teachers mess up 
when they fail to distinguish between what the Scriptures <i>prescribe</i> and 
what they <i>describe</i>. Whenever we draw principles from particular 
instances or situations, we may wrongly conclude that what God did in that 
particular instance He will always do. Because He raised Lazarus is no 
guarantee that He will raise Uncle Thomas at the funeral home. But if Uncle 
Thomas is a believer, he is with the Lord—and his body will be raised at the 
rapture, because God has prescribed that hope for ALL believers. </p>
<p>Follow God’s prescriptions, learn from His descriptions, 
but don’t confuse the two!</p>

