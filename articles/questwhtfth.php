<br>Questioning What Faith?
<br><span class=byln>by Ed Vasicek</span></p>


<p>Satan often vaccinates people with a mild dose of 
Christianity to prevent them from getting the real disease.</p>

<p>As a pastor, I consider it both a privilege and a 
responsibility to serve sometimes as a confidant and counselor. I have insight 
into many lives and not just the lives of our church folks. People nab me to 
discuss problems or issues just because I am a pastor, and I am usually happy to 
oblige. Others avoid me because I am a minister and do not feel comfortable 
around me, which is why I usually do not mention that I am a pastor.</p>

<p>Sometimes people tell me about their spiritual background, 
how they began to question and eventually abandoned their faith. Yet, upon 
further discussion, I often ask myself, &quot;What faith did they have to abandon?&quot; 
Here are the thoughts I think:</p>

<p>&quot;You don't know your Bible, you don't understand even basic 
doctrine, you never asked the questions thinking Christians should ask, you took 
little or no advantage of learning and discipling opportunities, your church 
attendance was sporadic at best, and I have not known you to lead one person to 
Christ or even to witness. When you did attend church, you went to one that was 
worse than worthless and one that hardly believed anything. So what faith are 
you questioning? I don't see evidence of an existent faith for you to 
question!&quot;</p>

<p>Don't get me wrong&#8212;there are painful exceptions to this 
rule. I have known a number of individuals who did know their stuff and then 
later denied the Lord; we should expect to see some of this. Jesus taught about 
the tares hiding among the wheat, and about the seed that took root and seemed 
to be healthy until it was choked to death. John explained such painful 
defections to first century believers: &quot;They went out from us, but they did not 
really belong to us. For if they had belonged to us, they would have remained 
with us; but their going showed that none of them belonged to us&quot; (<b>1 John 2:19</b>).</p>

<p>Nonetheless, those with the &quot;vaccination&quot; dose of 
Christianity are most likely to drop by the wayside. Sometimes more is not 
better; sometimes a little is worse than none. When it comes to serving Jesus 
Christ, the latter seems more the case.</p>

<p>Don't get me wrong: a little--as a starting point--is 
great. But as an ending point, a little is worse than worthless. People who 
never knew nor served the Lord think they have done both; they believe they have 
tried Christianity and found it wanting. As a result, they have been vaccinated 
from getting the real disease!</p>

<p>Many Christians find the current rise of atheism in the 
public sector a great discouragement. In a sense, it is: it is easier to 
witness (and people generally view church more favorably) when the overwhelming 
majority at least acknowledge a Creator. But as far as &quot;who is saved and who is 
not,&quot; nothing has changed.</p>

<p>Indiana, like many Bible-belt states, has a lot of people 
who claim to know the Lord, but their faith has been unchallenged and is more of 
an &quot;inherited&quot; faith. If they go off to college or make friends with the 
trendier bunch, their professed Christianity is lightly cast by the wayside. 
The problem is that God has no grandchildren, only children: each individual 
must personally covenant with God by faith. And with the commitment to Christ 
comes the responsibility to grapple with the issues.</p>

<p>Many such professing Christians will fold once they hear 
seemingly convincing attacks from the other side; the atheist apologists are not 
stupid. Peer pressure nudged such &quot;believers&quot; into the evangelical camp, and a 
change in peer pressure will nudge them in a different direction.</p>

<p>But nothing has changed: their real status has simply 
surfaced. God's true children take root and grow in their walk with the Lord. 
They do not abandon their minds but use them to God's glory.</p>

<p>So here is my question: do you have a real faith to 
struggle with, or are you just not making waves? Do you yourself ask the big 
questions and seek answers from a believing perspective? Or will you let others 
ask the questions and listen to their answers from an unbelieving perspective? 
If your mind is not actively nurturing your faith, then your mind is only a step 
away from being redirected.</p>

<p>We have an entire section of our church library dedicated 
to &quot;apologetics&quot; (defending the faith); we have DVDs, tapes, and books. A 
simple read would be Paul Little's <i>Know What You Believe</i> and <i>Know Why 
You Believe</i>. By reading those two volumes (sometimes combined into one), 
you will at least get a start on understanding the &quot;what&quot; and &quot;why&quot; of Biblical 
faith. Authors like Lee Strobel, Norman Geisler, Ravi Zacharias, Josh McDowell, 
and Alister McGrath are among some of the best authors in this field (at a 
popular level).</p>

<p>The Internet has great resources, like 
<a href="http://www.johnankerberg.com/" target="offline" class="reg">Ankerberg Theological Research Institute</a>, 
<a href="http://christiananswers.net/" target="offline" class="reg">ChristianAnswers.Net</a>, or
<a href="http://www.probe.org/" target="offline" class="reg">Probe Ministries</a>, 
to name a few. If you are going to question your faith, you 
need a faith to question!</p>
