<p><b>Introduction</b></p>

<p>When it comes to matters of theology, most dictionaries do not recognize
  the vocabulary.</p>

<p>Take two words we will use today: aseity and immutability. Only my large
  3 volume dictionary has those words. You can check yours when you get home.</p>

<p>But why do we use such words? To impress people (who are we impressing)?&nbsp;
  To keep theology from average Christians? No.</p>

<p>We use these words to save breath, in the name of efficiency.</p>

<p>Aseity means that God is self-derived, self-sufficient, and independent
  of His creation. He depends upon us for nothing, and we cannot alter Who He
  is. This leads us into today&#39;s subject.</p>

<p>Today&#39;s topic will focus upon the immutability of God.</p>

<p>These matters are all being challenged today; we Christians need to
  decide whether we believe in the clearly infinite God of a Scripture or the
  toned-down, weak versions of God being proposed today on all fronts, within
  and without evangelicalism.&nbsp; You need to pay attention to what you are
  hearing in this series, because you need to know what and why you believe
  what you do; <b><u>it will be challenged, eventually even by people who will attend
this church.</u></b><p>
There are people out there today, claiming to be Christians and many
  claiming to believe the Bible, who would challenge every one of my main
  points today.</p>

<p>Immutability sounds bad. If someone called you immutable, you might
  punch them in the nose. The truth is that this mysterious word is true only
  of God.</p>

<p>To be immutable is to always be the same, unalterable. God is unchanging
  and unchangeable. <b>James 1:17</b> says, &#34;Every good and perfect gift is from
  above, coming down from the Father of the heavenly lights, who does not
  change like shifting shadows.&#34;</p>

<p>We can insert the word &quot;always&quot; to highlight this truth. God is <b>always</b>
  holy, <b>always</b> sovereign, <b>always</b> good, <b>always</b> wise, <b>always</b> just, and
<b>always</b>
  gracious. Whatever God is, he&nbsp;<b><u>always</u></b> is. His nature is an
<b>always</b> nature. He
  always is Who He is.</p>


