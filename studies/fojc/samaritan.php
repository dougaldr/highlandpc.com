<p>The “Samaritans” appear often in the Scripture: the Good 
Samaritan, the woman at the well, and the command to preach the Gospel in Samaria 
(<a href="javascript:scripture('../../scripture/acts18.htm')" target=_self  class=pop>Acts 1:8</a>).
Some Pharisees accused Christ of having a demon and being 
a Samaritan Himself. Who were these mysterious Samaritans?</p>
<p>The nation of Israel, even when united, was the union of 
the North (the majority of the tribes) and the South (Judah, the largest tribe). 
The union was always shaky. For years even King David ruled only over 
Judah—until the Northern Tribes elected to join in. When David’s grandson (Rehoboam) 
took the throne, the northern tribes broke rank. They appointed their own 
king and severed ties to the South. This Northern confederacy was sometimes 
called, “Samaria.”</p>
<p>When the Assyrians conquered this Northern Kingdom in 722 
BC, they imported gentiles into the land of Samaria and deported many 
Israelites. The people intermarried, forming the people group referred to as 
“Samaritans.” Eventually, the foreigners were converted to a Samaritan form 
of Judaism. The Samaritans turned history around, and claimed that they were 
the pure race, while the Judeans were the mixed race.</p>
<p>When the Judeans were carried off into Babylon (beginning 
in 606 BC). and then returned to Jerusalem seventy years later, they began to 
rebuild the Temple and City. The Samaritans opposed this; they had altered the 
Torah and rejected the rest of the Old Testament; they declared that Mount 
Gerizim was God’s chosen sight for the Temple, not Jerusalem. Nehemiah tells us 
they created all sorts of red tape to oppose the construction of Jerusalem’s 
walls; the animosity between the groups was strong. </p>
<p>In Rabbinic Judaism, the Samaritans were more despised than 
the gentiles. A Samaritan woman was always considered “unclean.” Both 
groups hated and went out of their way to irritate one another. For example, 
the Jews would signal the beginning of a holy day based on the sunset in 
Jerusalem. The Rabbi there would give the sign, and fires would be immediately 
lit from mountaintop to mountaintop throughout the entire region. The holy day 
had begun. The Samaritans, however, would sometimes light false fires just to 
confound the Jews. When Jesus spoke to the Woman at the Well, or told the 
Parable of the Good Samaritan, He was doing something very politically incorrect 
in His day. </p>
<p>Modern Judaism recognizes the full Jewishness of the 
Samaritan people, several thousand of whom still live in Israel (and have all 
along). Their Rabbis are considered fully accredited; they are stricter than 
even Orthodox Jews.</p>
