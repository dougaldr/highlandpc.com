<p>In this movie, the chickens want to learn how to fly so they escape the 
slaughter that is their destiny. But this raises a question: Why do chickens 
have wings?</p>
<p>The easy answer: Because God knew that we would love Buffalo wings. But 
there is a better answer.</p>
<p>&quot;There are at least two possibilities as to why flightless birds such as 
ostriches and emus have wings, either:</p>
<p>The wings are indeed &quot;useless&quot; and derived from birds that once could fly. 
This is possible in the creationist model. Loss of features is relatively easy 
by natural processes, whereas acquisition of new characters, requiring specific 
new DNA information, is impossible. Loss of wings most probably occurred in a 
beetle species that colonized a windy island. Again, this is a loss of genetic 
information, so it is not evidence for microbe-to-man evolution, which requires 
masses of new genetic information. </p>
<p>The wings have a function. Some possible functions, depending on the species 
of flightless bird, are: balance while running, cooling in hot weather, warmth 
in cold weather, protection of the ribcage during falls, mating rituals, scaring 
predators (emus will run at perceived enemies of their chicks, mouth open and 
wings flapping), sheltering of chicks, etc. If the wings are useless, why are 
the muscles functional, allowing these birds to move their wings?&quot; (source:
<a href="http://www.christiananswers.net/q-aig/aig-c035.html" target="offline" class="reg">
ChristianAnswers.net - Why Do Flightless Birds Have Wings?</a>)</p>
<p>Moses was a man who, in a sense, had wings but was flightless. He had 
tried to deliver the Hebrews in his own power and at his own time.</p>
<p>But now, God was going to recreate the pattern we saw when Moses left 
Egypt, but this time around, because it was God's time - and God had His 
blessing on Moses - he would succeed in bringing the Hebrews to freedom.</p>
