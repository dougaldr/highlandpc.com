<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Government, Right and Wrong, and the Justice of God
<br><span class=byln>by Ed Vasicek</span></p>

<p>I thought the cartoon interesting: a man was phoning his pastor during a
crisis. With a fraught look, the man asks the minister: &quot;My wife has left
me, my kids are on drugs, and my life is falling apart. Can you explain to me
the difference between amillennialism, postmillennialism, and premillennialism?&quot;
</p>

<p>
At that moment, why would someone worry about end-time events? (Okay, maybe I
could see asking about the rapture as a hope of escape.) That was the point of
the comic: some things that people heatedly debate mean little during a crisis.&nbsp;
But that does not mean we can just write off the importance of correct beliefs.&nbsp;
Proper theology is relevant to our personal lives and to our society.
</p>

<p>
For example, because Catholics, some of the Reformers, and the Puritans did not
agree with our perspective of end-time events, they assumed the church had the
right to oversee government. They believed that the church is now experiencing
the Millennium and that the church reigns in Christ's stead. Hence the intense
power of the Pope in the Middle Ages and the burning of witches in
Massachusetts. Thousands upon thousands have died because of this theological
belief.
</p>

<p>
Close to home, religious people debate over the death penalty, ordination of
homosexuals, pacifism, politics, abortion, genetic engineering, and a thousand
other issues that relate directly to our lives and society. Most of these issues
are debated because of theological differences.
</p>

<p>
Who cares what God is really like, as long as we believe there is one God?&nbsp;
Right? Wrong! What we believe about God is more than just a technical matter.&nbsp;
Logically, our entire world view, including our perspective about right and
wrong, firmness and grace, punishment or pardon, and even salvation flow from
what we believe God to be like. Such is especially true with God's qualities of
holiness, justice, and love.
</p>

<p>
Liberal theologians pave the way for social liberalism. In liberal theology, God
is PRIMARILY a God of love. Some even go so far as to say the God of the Old
Testament is a God of justice and holiness; they say Jesus came to change the
popular view of God (as though the Old Testament was misleading) to that of a
benevolent, tolerant deity. Those who emphasize God's love and either minimize
or deny his holiness and justice come out with a &quot;Christianity&quot; that
is very different from those of us who believe that holiness is his central
attribute.
</p>

<p>
If God's holiness is only a minor issue, then the God of love certainly would
not keep anyone out of heaven. If not benevolently allowing all into heaven, he
would perhaps deny only the particularly evil access to paradise. But then
again, &quot;How could a God of love send anyone to hell?&quot; If God is a God
of love ONLY, He would not send anyone to hell. If &quot;God doesn't send anyone
to hell, people send themselves&quot; is the answer, then you must insist that
God cannot undo what people have done. You now have a God who is a weakling.&nbsp;
Of
course, the reason God sends people to hell is because God is holy and just.
</p>

<p>
The case is strong that God's central attribute is his holiness. He is above
all, unique and separate from his creation. He is sinless, without flaw.&nbsp;
The
Bible says, &quot;God is light,&quot; and &quot;light&quot; is the chosen
imagery of holiness. The seraphim and cherubim cry out, &quot;Holy, Holy, Holy&quot;
(<a href="javascript:scripture('../scripture/is6.htm')" target=_self class=pop>Isaiah 6</a> and
<a href="javascript:scripture('../scripture/rev4.htm')" target=_self class=pop>Revelation 4</a>),
not &quot;Loving, Loving, Loving.&quot; The Third Person of the Trinity is the HOLY Spirit, not the Loving Spirit
(though He is that, too). A concordance study might convince you that God's
holiness is an attribute to be reckoned with.
</p>

<p>
This also explains why Jesus had to die, why the cup of God's wrath could not
pass from him. Because God is holy and just (justice is a key aspect of his
holiness), the death of Christ was necessary to satisfy God's righteous wrath
(<a href="javascript:scripture('../scripture/rom59.htm')" target=_self class=pop>Romans 5:9</a>).&nbsp;
Christ became a sin offering to the Father on our behalf
(<a href="javascript:scripture('../scripture/heb92428.htm')" target=_self class=pop>Hebrews 9:24ff</a>).
</p>

<p>
God's wrath and anger are provoked when his holiness is assaulted by our acts of
rebellion. God's wrath does not just &quot;go away.&quot; It can be redirected
or delayed, but it does express itself. When godly King Josiah first heard the
Law, he and the people repented. Because of this, God delayed his wrath upon
Judah. His wrath had been accumulating but was delayed, not eliminated.&nbsp;
Josiah
inquired of the prophetess Huldah, who<br>
stated:
</p>

<p>
&quot;This is what the L<span class="footer">ORD</span> says: 'I am going to bring disaster on this place
and its people&#151;all the curses written in the book...Because they have forsaken
me...my anger will be poured out on this place and will not be
quenched...Because your heart was responsive and you humbled yourself before
God...you will be buried in peace. Your eyes will not see all the disaster I am
going to bring on this place...'&quot; (<b>2 Chronicles 34:24-28</b>).
</p>

<p>
Note that God's wrath would be delayed, but not forgotten. At the cross, God's
delayed wrath toward those who lived in Old Testament times, and his anticipated
wrath toward those who lived after Calvary, was satisfied through the atoning
work of Jesus Christ. Like Josiah, the lost sinner needs to humble himself in
repentance and exercise saving faith. But in the case of salvation, God's wrath
toward us is not merely delayed, it has been exercised and turned to grace
through Calvary.
</p>

<p>
God is not a softie. He always stands ready to forgive the repentant, but he
does not forgive the non-repentant. As far as salvation goes, our sins have been
forgiven in a blanket fashion&#151;all of them&#151;the moment we turn from our sins and
turn to Christ. However, known sins break our fellowship with God. When we
consciously sin, our salvation is intact, but our relationship to God is
impaired. Confession&#151;admitting our guilt and seeking God's help to change&#151;is
required for us to be on cordial terms with God once again; we are cleansed.&nbsp;
A
holy God relates best to a cleansed believer.
</p>

<p>
But what about government, homosexuality, or enforcement of law? How is this
affected by our view of God?
</p>

<p>
God has ordained government to take revenge (exercise wrath) in his stead
(<a href="javascript:scripture('../scripture/rom134.htm')" target=_self class=pop>Romans 13:4</a>),
particularly for murder
(<a href="javascript:scripture('../scripture/gen96.htm')" target=_self class=pop>Genesis 9:6</a>,
hence the death penalty). God is concerned about justice, which is why most Christians take a non-pacifist
position if a war is deemed &quot;just.&quot; Although God loves the immoral, he
warns that adultery or homosexuality is descriptive of those who &quot;...will
not inherit the kingdom of God&quot; (<b>1 Corinthians 6:9-10</b>), and he tells them they can
indeed change
(<a href="javascript:scripture('../scripture/1cor611a.htm')" target=_self class=pop>1 Corinthians 6:11a</a>).&nbsp;
God is not merely the permissive God of
Love. God makes himself clear: &quot;...I will not acquit the guilty...&quot; (<b>Exodus
23:7</b>). God's acceptance is conditioned upon repentance. God's love
motivates him to offer escape from his wrath through repentance. This escape may
not always mean deliverance from the consequences of sin in this life but is
rather the promise of a &quot;not guilty&quot; verdict for eternity and unbroken
fellowship with God now.
</p>

<p>
Your theology affects more than which church you choose Sunday morning (and it
ought to at least do that!). If you take God's truth seriously, it affects your
entire perspective on life.
</p>

