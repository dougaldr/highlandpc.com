<p><b>Conclusion</b></p>
<p>What can we learn from these events?</p>
<p>The importance of timing and waiting on God.</p>
<p>Some Christians just leap into decisions like they were the captains of 
their own ship; they feel no obligation to consult God, make no attempt to 
counsel with the godly, feel free to do whatever they want to do.</p>
<p>Such Christians have not discovered the Lordship of Jesus Christ.</p>
<p>Jesus asked, &quot;Why do you call Me, 'Lord, Lord,' and do not do what I say?&quot; 
(<b>Luke 6:46</b>).</p>
<p>Let me lay some logic on you. God has revealed His will in His Word. How 
can we obey God, if we do not bother to check in with Him? And how can we check 
in with Him if we do not know His word? And how can we know His Word if we do 
not read and study it?</p>
<p>Moses tried things his way and failed. Moses was coerced to do things 
God's way and succeeded. Must we repeat this pattern? Do we have to learn the 
hard way, or can we, instead, get to know God in the first place?</p>
