<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 5
<br><span class=byln>by Ed Vasicek</p>

<p>O God,</p>
<p>You are the Ancient of Days. You, O Lord, not only go back to the beginning 
of all things, but You go back before all things. There was a time when there 
was no time, yet You were there. As we learned in geometry, we are like a ray; 
we have a starting point and go on eternally, but You are like a line, eternal 
in both directions. You are the Ancient of Days and this implies You have an 
eternal wisdom, too.</p>
<p>Yes, my past carries some negative memories, but it carries some pleasant ones as well.  
I can be tempted to live in the past. My past carries some pleasant memories as well. Help 
me not to glamorize the past 
(<a href="javascript:scripture('../../scripture/ecc716.htm')" target=_self class=pop>Ecclesiastes 7:16</a>), 
but to remember that You are always active in my life. Although there 
are things about the past that I love to remember, help me to press forward to 
the goal You have set for me 
(<a href="javascript:scripture('../../scripture/phil313ii.htm')" target=_self class=pop>Philippians 3:13</a>). 
The best days are ahead 
(<a href="javascript:scripture('../../scripture/1cor29.htm')" target=_self class=pop>1 Corinthians 2:9</a>).</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 
John 1:9</b>). I accept responsibility for my sins and freely bring the sins 
Your Spirit brings to my mind now: __________.</p>
<p>Help me, Lord, to be assertive—to be able to ask for what I want—but to avoid 
being demanding. Help me obey the Golden Rule when it comes to making requests, 
to do unto others as I would have them do unto me.</p>
<p>Lord, today I remember that nation that is especially near and dear to Your 
heart, the nation of Israel. Your Word reveals that Your determination to bless 
Israel remains firm 
(<a href="javascript:scripture('../../scripture/jer313637.htm')" target=_self class=pop>Jeremiah 31:36-37</a>,
<a href="javascript:scripture('../../scripture/rom1129.htm')" target=_self class=pop>Romans 11:29</a>). 
Yet few of the children of Israel know Jesus, Yeshua, as 
Messiah. Thank You that the number of believing Jews (Messianic Jews) has 
increased from 200 to 7,000 within Israel, yet only two-tenths of one percent of 
the population claim to be born-again evangelicals. I look forward to the time 
when the nation of Israel will turn to Christ in droves 
(<a href="javascript:scripture('../../scripture/zech1210.htm')" target=_self class=pop>Zechariah 12:10</a>), 
but I pray that many would come to know You before those times. I 
also pray for the peace of Jerusalem. </p>
<p>O God, thank You for the ministry of Wycliffe Bible 
Translators, the largest mission organization of the evangelical church. 
&quot;Wycliffe Bible Translators, USA is part of an international association of 
Wycliffe organizations dedicated to seeing God's Word become accessible to all 
people in the language that speaks to their heart. In its 70-year history, 
Wycliffe has been involved in more than 600 translations, representing greater 
than 77 million people. Today Wycliffe consists of more than 6,000 personnel 
working in partnership with expatriates and nationals worldwide.&quot; O Lord, 
please bless this crucial ministry. Help them to 
start projects to get God’s Word into the native language of everyone on earth 
by 2025, as they are so aiming. Provide for them, protect them, empower them, 
grant them harmony and use them mightily, I ask.</p>
<p>Use me to show the love to Christ to someone today, I pray 
in Messiah’s Name, Amen.</p>


