<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 24
<br><span class=byln>by Ed Vasicek</p>

<p>Holy Father,</p>
<p>You are a patient God. You are longsuffering and rarely in a hurry. Because 
of Your patience, You delay Your wrath. You are not willing for any to perish, 
but for all to come to repentance. Before You drove out the people of Canaan 
before the Israelites, You gave them decades, even centuries, to repent. Before 
You sent the children of Israel into captivity, You gave them centuries—about 
490 years. Because they did not observe the sabbatical years every seventh year, 
You caught up by giving the land rest for 70 years, the Babylonian Captivity 
(<a href="javascript:scripture('../../scripture/2chr3621.htm')" target=_self class=pop>2 Chronicles 36:21</a>). 
Without our repentance, Your wrath is merely delayed. But 
during the times of Your patience, You call us to turn from our sins and to turn 
to You. Your kindness leads us to repentance 
(<a href="javascript:scripture('../../scripture/rom24.htm')" target=_self class=pop>Romans 2:4</a>), 
and we should not confuse your patience with laxness 
(<a href="javascript:scripture('../../scripture/2pete39.htm')" target=_self class=pop>2 Peter 3:9</a>).</p>
<p>Lord, we thank You that You have called us to be a kingdom of priests. We 
have direct access to heaven through Jesus Christ our Lord; we can intercede for 
those Who are lost or Who need special spiritual attention, and we can offer You 
the sacrifice of praise, namely the fruit of our lips. We are a royal 
priesthood as children of the King.</p>
<p>So Lord, help me to pray without ceasing, praying off and on throughout the day. 
May I remember to send up casual and quick prayers and praise all day long.</p>
<p>Lord, help me to manage my finances in a way that pleases You. Prosper me to 
the degree You see fit. Help me trust You during good times and bad and may I 
be a good steward of what You have given me.</p>
<p>Abba, Father, I have many needs. Life has a way of throwing 
curveballs and I find it easy to strike out. Give me wisdom when I face the 
unexpected. Help me to delay my responses and conduct myself with wisdom. Keep 
me from making a fool of myself or blurting out responses I will later regret.</p>
<p>Lord, I pray for the North African nation of Libya. There 
are no opportunities in this land for open evangelism and only .3% of the 
population falls under the category of born-again evangelical. Secret police 
monitor to make sure evangelism does not occur and almost all of the Christians 
in the land are foreigners. O Lord, use radio, literature and Internet access to 
reach Libyans for Jesus Christ. May the number of Christians grow by twenty 
times over the next decade and may true religious freedom reign in this land. 
Hover over Libya, O Holy Spirit!</p>
<p>Lord, thank You for the Asia Evangelistic Fellowship, &quot;an 
indigenous and interdenominational faith mission dedicated to the task of 
evangelism and pioneer missions.... The AEF mobilizes and equips national 
evangelists and missionaries in Asian countries to reach out to their own people 
groups. Today AEF International...has more than 200 national missionaries and 
associates serving in nine fields...Singapore, Malaysia, Indonesia, Nepal, 
Myanmar, Philippines and India.&quot;</p>
<p>O Lord, help this ministry to be effective for You. Grant 
them harmony, provision, protection and the power of Your Spirit.</p>
<p>Please send angels to watch over me and my loved ones 
today, I ask in Christ's Name, Amen.</p>

