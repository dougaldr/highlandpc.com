<p>What is the background for Paul's statement in <b>2 
Thessalonians 2:3</b>, &quot;Let no one in any way deceive you, for it will not come 
unless the apostasy comes first, and the man of lawlessness is revealed, the son 
of destruction?&quot; What does the term, &quot;apostasy&quot; refer to?</p>
<p>Perhaps the understanding of the term lies within the 
Jewish roots of our faith.</p>
<p>In Edward M. Cook's translation of the <i>Psalms Targum</i>, 
(a Targum is a Jewish paraphrase – usually in Aramaic; it often adds additional 
content to the text, thus exposing the Jewish understanding of the text), note 
the Jewish paraphrase of <b>Psalm 2:1-2</b>, &quot;Why are the Gentiles disturbed, 
and the nations murmuring vanity? The kings of the earth arise and the rulers 
will join together to rebel in the Lord's presence, and to strive against his 
Anointed.&quot;</p>
<p>Could the interpretation of the <b>2 Thessalonians</b> portion 
(below) be based on previous, Old Testament revelation? If so, the passage 
becomes more understandable: </p>
<blockquote><p>Don't let anyone deceive you in any way, for (that day 
will not come) until the rebellion occurs and the man of lawlessness is 
revealed, the man doomed to destruction. He will oppose and will exalt himself 
over everything that is called God or is worshiped, so that he sets himself up 
in God's temple, proclaiming himself to be God.</p>
<p>Don't you remember that when I was with you I used to tell 
you these things? And now you know what is holding him back, so that he may be 
revealed at the proper time. For the secret power of lawlessness is already at 
work; but the one who now holds it back will continue to do so till he is taken 
out of the way. And then the lawless one will be revealed, whom the Lord Jesus 
will overthrow with the breath of his mouth and destroy by the splendor of his 
coming. (<b>2 Thessalonians 2:3-8</b>).</p></blockquote>
<p>Perhaps Paul's teaching is a <i>Midrash</i> (a teaching in 
the Jewish style) on <b>Psalm 2</b> with additional expansions based on further new 
revelation. Thus the entire age (as implied by 
<a href="javascript:scripture('../scripture/acts42528.htm')" target=_self class=pop>Acts 4:25-28</a>)
is an age when the 
kings of the earth rise up, but this &quot;rising up&quot; (apostasy) comes to a climax 
during the Tribulation when the kings unite under THE Antichrist (as opposed to 
the many antichrists that have littered history) and the Antichrist shows his 
true character by demanding worship (<b>Revelation 13</b>).</p>
<p>Instead of simply rebelling &quot;against the Lord,&quot; (according 
to the actual Biblical text of <b>Psalm 2</b>), the <i>Targum</i> interprets this as 
&quot;to rebel in the Lord's presence,&quot; thus alluding to the <i>Abomination of 
Desolation</i> (the act by which the Antichrist enters the Temple and demands to 
be worshipped as God) which takes place in the Temple! Throughout the Old 
Testament, the Presence of God (<i>Shekinah</i>) is said to dwell in the 
Tabernacle/Temple. So the Apostasy and the <i>Abomination of Desolation</i> are 
essentially the same thing. Thus Paul is stating that the Apostasy will take 
place, and then further defines as the <i>Abomination of Desolation</i>.</p>
<p>This same <i>Targum</i> continues (<b>Psalm 2:4-9</b>): 
<blockquote><p>The one 
who sits in heaven will laugh; the word of the Lord will mock at them. Then he 
will speak to them in his strength, and in his wrath he will frighten them.</p>
<p>I have anointed my king, and appointed him over my 
sanctuary. I will tell of the covenant of the Lord. He said: &quot;You are as dear 
to me as a son to a father (abba), pure as if this day I had created you.&quot; Ask 
me and I will give the riches of the Gentiles as your inheritance, the rulers of 
the ends of the earth as your holding. You will shatter them as with a rod of 
iron, like a potter's vessel you will break them.</p></blockquote>
<p>So what restrains the Antichrist (who is, in a sense, 
chosen from among the many antichrists dotting the world) from coming on the 
scenes? Although this is not as clear (and is a vulnerable theory), perhaps the 
&quot;restrainer&quot; (the one holding him back) is none other than the anointed king 
(the Messiah, Yeshua), but, more specifically, His request (&quot;Ask me and I will 
give the riches of the Gentiles&quot;). Until Jesus makes this request of the Father, 
the antichrist will be held at bay.&nbsp; </p>
<p>The idea that the apostasy is defined in <b>Psalm 2</b> seems like 
a noteworthy hypothesis whether the Psalm actually gives us a clue to the 
identity of the restrainer or not. My hope is that others (who have the 
expertise and resources) would examine <b>Psalm 2</b> in its Jewish context and see 
whether the Psalm is indeed key in Paul's understanding of the end times and the 
nature of our current era.</p>

