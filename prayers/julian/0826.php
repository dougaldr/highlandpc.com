<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 26
<br><span class=byln>by Ed Vasicek</p>

<p>O My Father,</p>
<p>Thank You for being there for me. You are truly my best 
friend. Although I revere You as the Creator, Redeemer and Holy Sovereign, 
You are the only one that really knows me through and through. You see my 
lifetime all at once. You know all my thoughts, fears, aspirations, joys and 
disappointments. You have comforted me, disciplined me, tried me and showered 
Your grace upon me. You know the subtle tactics my ego employs, the insincerity 
within me, the good intentions that never materialize and the times my heart is 
open before You, craving to do Your will. No one but You has shared or can 
share all of life with me. And so, My Lord, thank You for being my constant 
companion.</p>
<p>Father, today I pray for situations outside of my control. 
I can make certain choices about myself, but I cannot choose for others. And 
back in my own life, there are many choices I cannot make. I did not choose the 
family into which I was born, the genes that combined to make me, or 
traumatic events that I have experienced. Yet, Lord, despite my vulnerability, 
You want me to take responsibility for my choices and not pass the buck. Help me 
think and use wording that makes it clear that I am taking responsibility for 
all my choices, not just those which prove to be good or successful.</p>
<p>I pray for Christians who are straying from their first 
love or compromising their walk with You. Rekindle the faith they once 
professed and help them fervently love the Savior once again. Remove their 
boring view of serving You and help them see the adventure and fulfillment of 
walking daily in Your light. Stir up their hearts and help them make the 
routines of godliness a part of their lives. I especially remember: __________.</p>
<p>Today I remember evangelical missionaries everywhere who 
are trying to raise support. Lord, while many churches give anywhere from 10 to 
50% of their offerings toward foreign missions, the average church gives only 
2%. O Father, break Your people of becoming ethnocentric. Keep us from 
confusing our earthly citizenship with our heavenly one. Keep us from 
perfectionist thinking that anchors us to one mission until everything is as 
perfect as it can be while neglecting other important areas; instead, awaken us 
to the reality that our commission is to share Christ around the world NOW. And 
as these perspective missionaries are turned down time after time, we pray that 
You would encourage these candidates and provide for them financially and in the 
realm of wisdom.</p>
<p>Lord, the nation of Suriname in South America is different 
from the rest of the continent. It was a Dutch colony and they imported 
indentured servants from India and other Asian areas, as well as Afro-Caribbeans. Thank You that 
4.1% of the population claims to be born-again evangelicals and that the 
percentage is on the rise. Father, please develop warm and solid leaders for 
the churches, use the Jesus film and student outreaches and help the seven 
translation projects now in progress. Father, double the percentage of 
believers over the next 5 years, I ask.</p>
<p>Help me to exemplify Christian love today, I pray, in Jesus' Name, Amen</p>

