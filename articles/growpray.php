<br>Worshiping God and Growing Through Prayer
<br><span class=byln>by Ed Vasicek</span></p>

<p>
As some of you know, I was reared in a very formal, ritualistic church. One
characteristic of such churches is that prayers are read, usually without
meaning, and are rarely understood. I often wonder if these religious leaders
receive special training to learn how to remove the meaning from prayer and
Scripture reading!</p>
<p>
In the Sermon on the Mount, Jesus warned against praying the same prayers over
and over. Such meaningless repetition, Jesus tells us, does not please God.&nbsp;
A
quantity of words cannot compare to a prayer offered from the heart. Indeed, one
freedom we believers have is the right to enter God's presence crying out
&quot;Abba Father.&quot;</p>
<p>
One unfortunate misunderstanding of Jesus' teaching is the notion that praying
written prayers is somehow unspiritual or uppity. Repeating prayers over and
over, reading prayers without concentrating on their meaning, or praying
contrary to sound doctrine are certainly abusive practices and are displeasing
to God. But it is possible to pray a written prayer in a meaningful way.</p>
<p>
I often use written prayers at weddings, baby dedications, and sometimes at
funerals. I once attended a wedding where I heard the minister pray a
spontaneous prayer, something like, &quot;Lord, we just want to thank You for
salvation. We just...&quot; In my book, &quot;justs&quot; are on the
&quot;what's not hot list&quot; for wedding prayers.</p>
<p>
More recently, however, in an attempt to improve my prayer life, I began to
experience the blessing of written prayers. For years my prayers were almost
entirely acts of intercession, where I was asking God to heal someone, save
someone, or straighten out a particular mess. And that's great. But there are
other aspects of prayer, such as worship or humbling ourselves before God, that
are lost when we have an agenda of intercession. Indeed, it is wonderful, at
times, to pray without making any tangibly physical requests at all!</p>
<p>
The question arises, &quot;How do we engage our minds to both concentrate on our
prayers and develop those prayers (at the same time), particularly when we are
not interceding? Sometimes the Holy Spirit intercedes for us with groanings we
cannot utter, as
<a href="javascript:scripture('../scripture/rom82627.htm')" target=_self class=pop>Romans 8:26-27</a>
tells us. But, most often, prayer is not merely
spiritual, it is also mental. Written prayers&#151;perhaps even authored by
ourselves&#151;can sometimes help us pray with meaning while freeing us from the
pressure to create our prayers in the process.</p>
<p>
Sometimes it is also refreshing to &quot;pray a Scripture back,&quot; namely to
talk to God about a verse or group of verses. &quot;Lord, by faith I put on the
helmet of salvation. Father, protect my mind from thoughts that would displease
You. I take upon myself the breastplate of righteousness...&quot; etc.&nbsp;
<a href="javascript:scripture('../scripture/ps36510.htm')" target=_self class=pop>Psalm 36:5-10</a>
can be incorporated as part of a beautiful prayer of worship. You get the point.</p>
<p>
I have a software program called &quot;Sage&quot; that is a collection of
classic Christian resources. I have found it invigorating to pray some of the
prayers originally authored by John Wesley (making a few theological changes as
I pray). Indeed, it is freeing to escape the &quot;intercession only&quot; trap
and commune with God, to worship Him, to ask Him to develop my character and
walk, to humble myself before God, and to heartily concur with the author of the
prayer I might pray, adding my &quot;Amen.&quot;</p>
<p>
God does not call us to &quot;say prayers&quot; but to pray. If your prayer life
needs some doctoring, try using solidly evangelical prayers and pray them with
meaning. Maybe the ancients had a few good ideas. Don't get away from simple,
spontaneous praying, but try adding written prayers into your prayer mix.</p>
<p>
<b>Here is an example of a prayer I have written. </b>You might consider writing
some of your own to share and/or to file away for your own use.</p>
<blockquote>
  <p>My Gracious Father,</p>
  <p>Your Word tells me that You are the Father of lights. Your holiness and
  purity shine forth and are a contrast to the occasional flickers I possess
  through Your work in me. Yet I know your Spirit dwells within me and that you
  have set Your undying love upon me. Thank You for producing light within my
  life. May I more and more reflect Your holy light as the rule, not the
  exception.</p>
  <p>
  O Mighty God, thank you for redeeming my soul from destruction, my mind from
  futility, and my heart from emptiness. Without Your Son in my life, I could do
  nothing that could please You, nothing that would last. As I see Your patience
  with me and Your willingness to condescend to me, I am ashamed of my
  impatience with others. When I think of how Your Son humbled Himself for me, I
  feel the disgrace of my pride in judging others. Help me to emulate your
  patience and humility so that others may see I truly partake of the Divine
  Nature and that, as a result, You might be glorified in the process.</p>
  <p>
  Abba Father, Your Word is a light to my path and a lamp to my feet. Yet it is
  my nature to forget the light or to think I need no lamp. I stumble in the
  darkness because I do not hunger and thirst after You and Your Word. Create in
  me a hunger, O Father, for You and Your Holy Word. Convince me of the
  foolishness of trusting in what I have already learned as though it were
  adequate. Motivate me to renew my mind daily in Your Word. Beyond that, help
  me remember the precepts of Your Word throughout the day and night watches.</p>
  <p>
  Yahweh, the Great I Am, thank you for the freedom I have in Jesus. You do not
  call me to right all wrongs, nor do you call me alone to reach all the word
  with Your Gospel. But Lord God, you do have a perfect will for me today.&nbsp;
  Help
  me see the opportunities you provide for me to glorify Your Name. Let me
  anticipate the appointments You, in Your Sovereign Providence, have made on my
  behalf. Keep me ever on the look out. And let my only goal be that of
  faithfulness to You.</p>
  <p>
  Watch over my family and loved ones:___________, and encourage:_________.&nbsp;
  In
  your grace, I ask you to lay your hand of healing upon:__________. Father,
  thank You for using me. You do not need me, but I need You. Ever remind me of
  this simple truth.</p>
  <p>In the Name of Jesus Christ our Lord, Amen.</p>
</blockquote>
