<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 21
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty King,</p>
<p>It is amazing to think how You condescend to us. You 
existed before there was time or dimension. Your attributes are perfect and 
perfectly balance and harmonize with one another. Nowhere do we see this 
harmony more than in the cross. Although a holy God cannot let we who are 
tainted by sin into heaven, in Your love and mercy You want to forgive us. So 
You sent Jesus to bear our penalty, thus satisfying Your justice and holiness 
and allowing Your loving desire to save us come to pass. You have forgiven us 
without compromising Your character. How marvelous!</p>
<p>Lord, I thank You that I am free from guilt. Christ bore 
ALL my sins, not just some of them. You do not view me as I am, but as I am <i>
in Christ</i>. Instead of seeing me, You see Jesus. You accept me completely because 
You see Jesus. You have given me the righteousness of Christ! Although the Old 
Testament sacrifices had their place, they could not cleanse the conscience, but 
Jesus does: &quot;How much more, then, will the blood of Christ, who through the 
eternal Spirit offered himself unblemished to God, cleanse our consciences from 
acts that lead to death, so that we may serve the living God!&quot; 
(<b>Hebrews 9:14</b>). </p>
<p>I also pray that You would help me to love my family 
members as I should. Help me put You first, my family second and then the rest 
of life third. Help me balance the rest of life between work, church, work 
around the house, extended family, social activities and community involvement.</p>
<p>I thank You, Lord, that You have taken away my heart of 
stone and given me a heart of flesh, a new nature, the Law of God written upon 
my heart. I am so privileged to be under the New Covenant, to know You and be 
indwelt by Your Spirit.</p>
<p>God, help me to be a good listener. You gave me two ears 
and one mouth, help me to see the obvious implications of that. As James says, 
help me to be slow to speak but quick to listen.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. I give them to You and ask You to help me constantly do so.</p>
<p>Lord, today I remember the sparse North American French territory of St. 
Pierre and Miquelon. Only .4% of the population claim to be 
born-again evangelicals and the population itself is under 7,000. Father, 
though small in size, we pray that You would do a mighty work here. May half 
the population come to know You personally and then in turn, please use them 
to reach the French, I ask.</p>
<p>Lord, thank You for Christians in Action, &quot;an 
international, interdenominational and transcultural evangelical missionary 
society with ministries in 21 countries. Its 300 ministry personnel with the 
goal of planting indigenous national churches carry out personal evangelism, 
discipleship and church planting. Other activities include shortterm 
evangelistic teams and medical and dental clinics in needy areas.&quot; Father, 
grant the workers harmony, protection, provision and empower them by Your 
Spirit, I ask.</p>
<p>May I live like a peacemaker today, I ask, in the Name of 
He of Who died and rose for me, Amen.</p>


