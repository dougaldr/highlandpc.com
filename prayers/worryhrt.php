<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Personal Devotions</span>
<br>When Worry Grips my Heart
<br><span class=byln>by Ed Vasicek</span></p>

<p>O Sovereign God, I am a fearful man. Indeed, you, Lord Jesus, have promised
that in this world I will have tribulation. Yet you went on to say that I should
be of good cheer, for you have overcome the world. But, Lord, I admit to worry
about the in-between times. I trust my eternal destiny to the blood of the Lamb,
but my earthly pilgrimage, the things I feel I influence or should be able to
influence, that's another story. So, when I am afraid, I will trust in you.&nbsp;
Lord, I choose to trust in you now, against all my feelings. I stand against my
inner anxieties, for you have not given me a spirit of fear but of power, love,
and a sound mind. I take my place in the victory won by my Savior that first
Easter morning. I claim that resurrection power.</p>
<p>Father, my own fears remind me that I am not alone. I remember the many, many
Christians who are being persecuted even while I pray. The price tag of naming
the name of Christ is so high for many, yet costs me so little! Indeed, for
many, following you is an open invitation to persecution, a persecution both
intense and intimidating. In my weakness, I pray your strength for those who
need it most. Help your persecuted people to stand true. Help them be brave, and
in your grace, alleviate their persecution.</p>
<p>Lord, I also remember your chosen people, Israel. It is my prayer that many
Hebrews would come to know the Messiah, who is one of their own. May they look
up to him whom they have pierced, and mourn in repentance. May they come to know
Yeshua, the risen conqueror.</p>
<p>Almighty One, in your providence, guide our nation down the path that will
best accomplish your will. Direct those who vote and who know you to fulfill
their holy obligation in a responsible way. Make your leading abundantly clear
to all in authority who know you, and overrule those who are maliciously opposed
to you.</p>
<p>Lord, thank you for my weaknesses&#151;they remind me how much I need you.&nbsp;
I
certainly do not thank you for my sins, but I do praise you for the grace of
forgiven sin. Thank you that you work all things together for good to those who
love you, to those who are called according to your purpose. May the lost around
me recognize that I differ, in a good way. May they see character, conviction,
and humility in all I do. May they see Jesus at work in and through me.</p>
<p>Yahweh, work in my family. Help me lead my wife and children in the way of
godliness. May I exemplify what I claim to believe. May my sins, failings, and
weaknesses not repel my family from the faith. Indeed, may they be few and
minor. May my cheerfulness, love, and encouragement far outweigh them.</p>
<p>Help me to remember you throughout the day and at night, should I wake.&nbsp;
Though my mind must focus on my work, may the back of my mind never forget your
presence and your promises.</p>
<p>May the Holy Spirit, the Divine Comforter, teach me of you and keep me
tightly drawn to your heart. I pray in Jesus' name, Amen.</p>

