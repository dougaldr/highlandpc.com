<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 5
<br><span class=byln>by Ed Vasicek</p>

<p>Eternal Father,</p>
<p>Your faithfulness reaches to the heavens. You are a God 
Who stands by His Word, Who is dependable, Who is the same yesterday, today, and 
forever. Our world is always changing, and the pace of change often leaves us 
in shock. But You stay the same. Indeed, all eternal stability finds its 
source in You. We fade away like the grass, but Your Word endures forever.</p>
<p>Help me to be a man or woman or young person of integrity, 
honesty, and stability. Help me to resist making promises, but when I do, may I 
keep them unless I have promised in ignorance or my promise involves something 
sinful.</p>
<p>Today I remember the sick, the poor, and the oppressed in 
this world. They are everywhere, particularly in the poorer nations. Although 
we are the richest country in the world, we still have many in this category. 
Yet often our poor are wealthier than the average person in many nations.</p>
<p>I pray for the sick and afflicted whom I know, including: __________.</p>
<p>I pray for the poor and disadvantaged in the large 
metropolitan area nearest to where I am, the city of __________.</p>
<p>I also pray for the rural poor, the mentally handicapped 
and the physically disabled. Be a God of grace and provision for them, I pray. 
I also remember those who give care to the elderly and disabled.</p>
<p>I pray for the outreach in the urban centers of the world. 
Although many missions’ agencies and evangelistic strategies are in place to 
reach the lost in rural areas, we must adjust our strategies to reach people in 
the major non-Christian cities that are becoming more and more crowded as rural 
peoples move to these places for work and education. Help the missions 
organizations make the paradigm shift from the jungles to crowded and 
often unsanitary urban centers. Help the evangelical church to avail itself of 
modern technology, effective communication methods, and tapering their 
ministries to face the new challenges of urban outreach.
</p>
<p>Today I remember the many evangelical denominational missions agencies, 
particularly those associated with my denomination or fellowship (or I remember 
the missions outreach of a denomination I respect).</p>
<p>Help the missionaries to stay true 
to the Word of God, to get along well with one another, and to experience 
fruitful ministry. Bless them with health, safety, and provide for their needs 
and the needs of their families.</p>
<p>Bless all those who labor in nursing 
homes and other retirement facilities. Help them to be kind and loving toward 
those they serve, and to know how to be firm yet compassionate when necessary.</p>
<p>Now bless my attempts to be a good 
testimony to Your grace today, I pray, in Jesus’ Name, Amen.</p>

