<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 25
<br><span class=byln>by Ed Vasicek</p>

<p>God of Joy,</p>
<p>Thank You that Jesus Christ is my Horn Of Salvation 
(<a href="javascript:scripture('../../scripture/luke169.htm')" target=_self class=pop>Luke 1:69</a>). 
As an animal's horn represents strength, so You are strong. As the 
ram's horn, the shofar, was used to sound the battle cry, so You lead forth Your 
spiritual army of which I am a trooper. As the horn welcomed the feast of 
trumpets and the Jewish New Year, so You bring joy, celebration and happiness 
to those Who fellowship with You. You not only bring joy, but the joy of 
salvation, a joy for the long term—indeed, eternity! We look forward to 
entering the joy of our Master 
(<a href="javascript:scripture('../../scripture/matt2523.htm')" target=_self class=pop>Matthew 25:23</a>); 
the coming of Christ like a shofar blast announces the joy awaiting us!</p>
<p>Lord, help me to be a good steward of money and the 
material things of this life. Help me to support my local Bible-believing 
church by attending, serving and giving. Help me remember that, &quot;Everything 
comes from you, and we have given you only what comes from your hand&quot; (<b>1 
Chronicles 29:14b</b>). I offer You my time, my treasure and my talent. Help me 
to be an example to others of those who &quot;Let no debt remain outstanding, except 
the continuing debt to love one another.&quot; Help me not seek in material things 
what I can only find in You.</p>
<p>Your Word encourages me to confess my sins. Although my 
relationship with You is secure, my fellowship with You vacillates. My 
iniquities can distance me from You 
(<a href="javascript:scripture('../../scripture/is592.htm')" target=_self class=pop>Isaiah 59:2</a>). 
You faithfully forgive my 
sins when I do so and You cleanse me from all&nbsp;unrighteousness 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>). 
I confess them now to You: __________.</p>
<p>Help me read and understand my Bible, O Lord. Help me read 
portions that may not seem relevant right now believing that Your Word will 
somehow nurture my soul. Help me also realize that I need to prepare for 
challenges that may not be apparent right now. Help me to study the whole 
counsel of God. Help me renew my mind, not abandon it!</p>
<p>Lord, the small European nation of Liechtenstein has only 
33,000 people. Yet only .4% claim to be born-again evangelicals. There is 
only one evangelical congregation in the whole nation. Lord, work in this land 
and quadruple the number of believers there within the next decade, I ask.</p>
<p><span style="color:black">Father, bless Partners 
International &quot;</span><span style="color: black">a global ministry that works 
to create and grow communities of Christian witness in partnership with God’s 
people in the least Christian regions of the world.&quot; Thank You that they train 
nationals to reach their own country or nearby regions. 
&quot;By training church planters in subjects ranging from the Bible to 
income-generating skills, they are equipped to face the challenges of mission 
outreach among more than 390 people groups.&quot; Lord, bless the ministry of this 
mission; may the mission's leaders and the national workers experience harmony 
and unity of purpose, provide for them and empower them by Your Spirit. Help 
the national workers to be faithful stewards of the faith of the Bible. Protect 
them, motivate them and lead them, I pray.</span></p>
<p>Lord, help me to live in light of Your presence today, I pray in the Name of 
Yeshua.  Amen.</p>

