<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 17
<br><span class=byln>by Ed Vasicek</p>

<p>All-powerful One,</p>
<p>You are the One who orchestrates history. Like the 
conductor of a massive symphony, You direct the complexities of harmonies, 
melodies and rhythms. Although each instrument's contribution by itself may 
seem worthless, combined &quot;all things work together for good.&quot; As we hear our 
individual strings of life plucked, it doesn't make sense. But You harmonize it 
all in Your sovereign plan.</p>
<p>Thank You, Lord, that You have a plan. Your plan is to 
glorify Yourself through Your work of creation, provision and redemption, with 
redemption as central. You created the universe knowing full well that sin 
would enter and that God the Son would take the role of the sacrificial Lamb. 
Through redemption, You are causing all things to work together for good. One 
day Jesus Christ will return to reign and we look forward to that time.</p>
<p>Father, I remember those I know or know of who suffer 
chronic pain. In many instances, they have prayed for Your healing, but You have 
other plans. Like Paul the Apostle who struggled with an eye condition 
(<a href="javascript:scripture('../../scripture/gal41215.htm')" target=_self class=pop>Galatians 4:12-15</a>, 
<a href="javascript:scripture('../../scripture/gal611.htm')" target=_self class=pop>6:11</a>), 
many of Your children are suffering, as You know 
quite well. You feel compassion and empathy with them and, like them, You look 
forward to the time when their bodies will be redeemed 
(<a href="javascript:scripture('../../scripture/rom82325.htm')" target=_self class=pop>Romans 8:23-25</a>).
Thank You, that, in the meantime, Your Holy Spirit is praying on the behalf of all 
Your children. Please comfort these people and their loved ones, give them 
wisdom as to what they can do to feel better. If it be Your will, heal them. 
And if not, help them draw near to You as You often use suffering to this 
purpose. We admit that we do not always like the way You do things, but we 
trust You even when we do not understand.</p>
<p>Lord, I have some disappointments and burdens in life. A 
few of them&nbsp;hurt me and have left scars including: __________. I turn them over to You. </p>
<p>Help me to find Your purpose and joy in life. There 
are many things I cannot change, but I can share all my&nbsp;thoughts with You. 
Thank You that You do not stifle me or pressure me to &quot;get over it.&quot; You are a 
God Who feels with me.</p>
<p>Today I remember the nation of Grenada. Father, in some 
ways You seem to be building Your church there: over 18 percent of the 
population claim to be born-again evangelicals. Yet on the other hand, most 
babies are born out of wedlock and two parent families are rare. The effects of 
slavery many years ago seem present even today. Please, do a work of Your 
Spirit there and may the Christians choose to follow You on a daily basis. May 
their convictions carry into real life and daily decisions. Give them a burden 
to grow, evangelize and send out missionaries themselves.</p>
<p>Thank You for Mission: Moving Mountains. Their purpose is 
clear, &quot;Transforming Communities by Discipling for Development.&quot; Thank You for 
their work in Ghana, Senegal, Kenya, Sudan, Uganda and Nigeria. Protect the 
missionaries, provide for their needs, empower them by Your Spirit and grant 
them harmony among themselves. Use them to nurture many toward maturity and to 
develop solid leaders.</p>
<p>I commit myself to Your service today, in Jesus' Name, 
Amen.</p>

