<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 23
<br><span class=byln>by Ed Vasicek</p>

<p>Great Sovereign,</p>
<p>You are Three yet One, One yet Three. How can I fully 
grasp One God in Three Persons? If I really think I grasp it, I do not. 
Father, You are not the Son. Son, You are not the Spirit. Spirit, You are not 
the Father. Yet Father, You are God. Son, You are God. Spirit, You are God. 
But there is only One God. Yet, at the same time, You, God cannot be sectioned 
or divided. I accept these criteria because they are the boundaries Your Word 
sets. But then again, I am not surprised that You are much more complex than we 
simple products of Your creation! We think a single-celled creature is complex!</p>
<p>And, for our minds, it is! But You are higher than we, 
much higher.</p>
<p>Lord, help me to be an honest person. Help me not to lie 
to save my pride, not to say one thing while I am thinking another. Help my 
&quot;yes&quot; to be sincere, and help my &quot;no&quot; to be sincere. Help me be direct yet 
tactful; help me to avoid communication games.</p>
<p>Great God, help me find my greatest source of significance 
in You. I admit that I want people to like me and respect me. I want to 
achieve at least a few measurable goals in life. I want to make a difference. 
But help me find my greatest identity as a Christian—someone who knows God.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. Help me learn to avoid tempting situations, and to flee from temptation, 
when this is possible. I claim Jesus Christ, the victor, as the captain of my 
defense against the evil one.</p>
<p>I remember my family members who do not yet know Christ. I 
pray that I would have the assurance that they indeed know You. I pray for: __________.</p>
<p>Today I remember the nation of Egypt. I have read of 
ancient Egypt often in my Bible, but modern Egypt is very different. Almost all 
of the people are Muslims. Only 2.5% are born-again evangelical Christians. 
Persecution is a real problem. We pray, dear God, that You would help the 
believers to be able to evade persecution and that it would stop. Help the 
believers to be strong not in themselves but in You. We pray that You would 
use the Jesus film and media to get the Gospel out, and we pray that within ten 
years the number of evangelical Christians would triple.</p>
<p>I thank You for the unique ministry of Barnabas 
International, a ministry that seeks to encourage and refresh those serving as 
missionaries. As they provide an encouraging word, counseling, training and 
advice to those in the field, we pray that You would use them to keep 
evangelical missionaries spiritually, relationally, physically and emotionally 
healthy. Thank You for a specialized work that cares for the troops!</p>
<p>Now, Lord, help me remember You throughout my day, I pray 
in Jesus' Name, Amen.</p>

