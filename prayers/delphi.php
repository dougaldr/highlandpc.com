<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Prayer for the Well-being of Delphi Corporation
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Father,</p>
<p>We see the newspaper headlines; one of our largest area 
employers, Delphi, is considering bankruptcy. Lord, we fear because of this. 
We fear for our own well being, we fear for our families, we fear for our 
churches and the missionaries our churches support, and we fear for our 
community. We know that, economically, so much depends upon the health of this 
massive employer. If Delphi leaves, our city and counties will be devastated.</p>
<p>Lord, at a time like this, we realize that we need your 
hand of assurance. Our first choice is simple: we want the company to take an 
upward turn; we want to see a loss turned into gain, a loan turned into a 
savings cushion. We have no doubt that You can do this, and we pray that you 
would. This is not just about stockholders; it is very much about families.</p>
<p>But we are not Your fair-weather friends. Like the prophet 
Habakkuk, we affirm, &quot;Though the fig tree does not bud and there are no grapes 
on the vines, though the olive crop fails and the fields produce no food, though 
there are no sheep in the pen and no cattle in the stalls, yet I will rejoice 
in the L<span class="sm">ORD</span>, I will be joyful in God my Savior.</p>
<p>The Sovereign L<span class="sm">ORD</span> is my strength; he makes my feet like 
the feet of a deer, he enables me to go on the heights…&quot; (<b>Habakkuk 3:17-19a</b>).</p>
<p>Yet, Lord, we are asking You to guide the leaders of this 
corporation with a wisdom beyond themselves. We pray that Delphi would not only 
stay afloat, but prosper.</p>
<p>Lord, our individual futures, the future of our part of the 
state, indeed the future or our nation and this world are truly in Your hands. 
We can think of no better place for them to be. We know that whatever happens to 
us, Your children remain in Your care. And it is therefore with confidence that 
we offer this prayer to in You in the Name of our Savior, the Lord Jesus 
Christ. Amen.</p>

