<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 6
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When You gave the Law to Moses, the children of 
Israel were not even allowed near Mt. Sinai. You instructed Moses to set up 
boundary markers. Lightning and thunder accompanied this fearful event. 
According to Jewish tradition, You spoke the Law in the 70 languages of the 
nations.</p>
<p>Later, when Your Spirit answered Jesus prayer for oneness 
by baptizing all believers into the Body of Christ on the Day of Pentecost 
Your people praised You and gloried in Your grace. On Pentecost, 3,000 were 
saved; when the Law was given, 3,000 died 
(<a href="javascript:scripture('../../scripture/ex3228.htm')" target=_self class=pop>Exodus 32:28</a>). 
You are the God of 
both Law and Grace, Justice and Mercy, Holiness and Compassion.</p>
<p>Thank You for keeping us by Your power. When we stray from 
You, You discipline us and give us a spanking of sorts. Thank You that You are 
looking out for our best interests, working in ways that may seem mysterious or 
illogical to us. You are not an enemy to keep at bay but a true Friend, truer 
than any other.</p>
<p>So, Lord, help me to understand what I can of You and Your 
ways. Do not allow me to be ignorant of Your Word, but help me to truly read and 
study it. Help me to learn the Word even if parts of it may not seem relevant at 
the time. Help me to believe that all Scripture is both inspired and profitable.</p>
<p>Help me to guard my tongue, to choose my words carefully 
and to use my mouth to heal, not to hurt. Help me learn to listen, to remember 
that You created us with two ears but only one mouth.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You.</p>
<p>Lord, the Asian nation of Thailand with its 63 million 
people needs the Gospel. Even though missionaries have been working in 
Thailand for a long time, fruit has been scarce until now. Thank You that we 
are finally seeing some growth. O Lord, less than .7% of the population claims 
to be born-again evangelicals. Father, please multiply that percentage ten 
times over the next decade! May the Gospel penetrate hearts and spread 
throughout the society. Father, the sex industry with its abuse of children 
is well established in this land. O God, please let Your Word permeate this 
culture and help the citizens of Thailand protect their children and find more 
ethical means to generate income, I ask. Do a mighty work with Your Spirit 
there, I plead.</p>
<p>Father, thank You for the work of Global Advance as they 
seek to &quot;provide 
training and resources for one million pastors and church leaders in needy areas 
of the world. [They] believe empowering national church leaders is the most 
strategic, cost effective and culturally sensitive way to advance the Gospel.&quot; 
Lord, bless and guide their efforts, grant them harmony, the power of Your 
Spirit, protection and provision, I ask. Grant them much fruit for their 
labors.</p>
<p>Grant me fruit for 
my labor, I ask in the Messiah's Name, Amen.</p>

