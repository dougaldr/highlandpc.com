<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 4
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Your Word compares You to a Potter and us to the clay. 
Just as the potter has the “right to make out the same lump of clay some 
pottery for noble purposes and some for common use” (<b>Romans 9:21</b>), so we 
acknowledge that we have no grounds to talk back to you 
(<a href="javascript:scripture('../../scripture/rom920.htm')" target=_self class=pop>Romans 9:20</a>). We may 
not like where Your Sovereign hand has led us and our lot in life might be 
other than we would wish, but ultimately our main choice is whether to take Your 
hand or to kick against the thorns in protest. We choose to walk with You. We 
thank You that You understand exactly what You are doing, even though things are 
sometimes not as we think they should be. We confess that You are doing a much 
better job at running the universe than we could. So we submit ourselves to 
You, the potter. We remember that we are clay in Your hands.</p>
<p>Father, help me to keep my spiritual eyes open, and to join 
You where You are working. Perhaps a friend or associate is going through 
trials and You are using this to draw him or her to Yourself. Help me be 
alert and to minister accordingly.</p>
<p>Lord, thank You for blessing me so abundantly. Today I am 
thankful for: __________.</p>
<p>Father, I release the control of my life to You so that the 
Holy Spirit can control me. I remember that it is “not by might, nor by power, 
but by Your Spirit” that I can overcome (<b>Zechariah 4:6</b>).</p>
<p>Lord, help my church be harmonious. May the leaders be 
firm when it comes to the clear teachings of Scripture and gracious about the 
not-so-clear matters. Keep antagonists, troublemakers and those who resent 
authority away from our congregation. Help my church to be effective in 
evangelism, discipleship, loving one another and world missions.</p>
<p>Today I pray for the nation of Ireland. Lord, this land 
has a wonderful history. Yet there are so few born-again evangelical Christians 
there, only 3.3%. Thank You though that the evangelical church does seem to 
be growing, even as other forms of Christianity are rapidly declining. Thank 
You that there is an evangelical work in each county. O, Father, may more and 
more Irish people come to a personal, saving relationship with Jesus Christ. May 
Your Spirit draw many to Him.</p>
<p>Thank You for the great heritage and wonderful ministry of 
World Vision “a Christian relief and development organization dedicated to 
helping children and their communities worldwide reach their full potential by 
tackling the causes of poverty.” Thank You for their work in over 48 
countries. As they minister to the poor, we pray that many would find the 
Savior. Provide for their needs, bless them by empowering them with Your 
Spirit and help them to work in harmony.</p>
<p>Now, Lord, help me to live in harmony with Your will today, 
I request in the Name of the Savior, Amen.</p>

