<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 3
<br><span class=byln>by Ed Vasicek</p>

<p>Ever-present Lord,</p>
<p>You are the God Who delivered Daniel from the Lion&#039;s Den. 
You worked a miracle and closed the mouths of those hungry lions. Yet on the 
other hand, lions in the Roman Coliseum killed many faithful Christians. You are 
the God Who can deliver, but You do not always choose to do so 
(<a href="javascript:scripture('../../scripture/heb113238.htm')" target=_self class=pop>Hebrews 11:32-38</a>). 
And so, Lord, we trust You and Your power, but we also acknowledge 
that Your ways are higher than ours, for we cannot reduce You down to a 
formula. And we ask, not command; we request, not demand; we can plead our 
case, but we do not always understand Your long-term purpose.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Father, many couples struggle with their marriages. Lord, 
help struggling couples to endure, to address problems as they arise and to 
humble themselves before You. Help wives to accept the nature of men and men 
the nature of their wives. Help individuals to repent of the sins that create 
problems and help each one to face his or her own sin, reminding each one that 
they cannot change their spouses, only themselves. </p>
<p>Father, I intercede for believers who are struggling with 
substance abuse, addictions, or other bad or undesirable habits. Some practices 
are wrong, even in moderation. Others are wrong in excess. Others are wrong 
only because they become enslaving. Help us all to put off our bad ways and 
replace them with positively constructive ways. Rather than steal, let us labor 
to give to those in need. Rather than gossip, let us praise. Instead of being 
drunk with wine, help us to be filled with Your Spirit. Help us replace the 
undesirable with the good—or at least the neutral.</p>
<p>Great God, only .3% of the Asian nation of Oman claim to be 
born-again evangelicals and most of those are foreigners who reside in Oman. 
The Muslim majority (over 92%) is largely unreached. Thank You for Christian 
radio broadcasts and the Internet which at least make it possible for many of 
these people to be exposed to the Gospel. Lord, use these means and othersto 
reach the Arab peoples of Oman, I ask. May Jesus Christ be exalted as many come 
to know Him. My prayer is that within a decade 2% of the population would know 
Jesus in a saving way. Please display Your Spirit’s regenerating power to this 
nation.</p>
<p>Lord, I pray for Great Commission Ministries which &quot;plants and develops 
churches in the United States and overseas that both tell 
the story of Christ in the language of the emerging generation and foster 
Christian growth. [They] currently have a ministry presence on 44 U.S. campuses 
and in 23 countries. GCM mobilizes people in vocational ministry through [their] staffing 
program.... GCM sends missions to serve churches in Asia, Europe and Latin 
America and seeks to help believers become mission-minded, culturally aware 
Christians.&quot; Bless this work with fruit, keep them in tune with Your Word and 
Your Spirit and grant them harmony, I pray.</p>
<p>Help me keep my mind on You today, I ask in Messiah’s 
Name, Amen.</p>

