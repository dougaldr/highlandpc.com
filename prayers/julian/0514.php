<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 14
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Father in Heaven,</p>
<p>My Savior, the Lord Jesus, is the Christ, the Messiah. 
Whether I use the Greek term &quot;Christ&quot; or the Hebrew term &quot;Messiah,&quot; He is the 
&quot;Anointed One.&quot; His human nature is the &quot;firstborn of all creation,&quot; above all 
created natures. His divine nature as the Son of God is eternal, uncreated 
and has always been. Both natures are united in one Person, Jesus Christ. His 
task was pinpointed &quot;to save His people from their sins.&quot; As He said, He came 
&quot;to give His life a ransom for many.&quot; He is the holy and anointed one, our 
Messiah, Jesus Christ.</p>
<p>God, please send angels to watch over me and my loved 
ones. Keep us from harm and evil of every kind, night and day, over the course 
of our lives.</p>
<p>Help those within my physical and spiritual family to love 
one another. Sometimes this can be a tall order; not everyone is necessarily 
easy to love and we love different people in different ways according to who 
they are and how they act. So give me both love and wisdom, both kindness and 
truth. Help me to know when to be firm and when to be gracious. Make me neither 
a doormat nor hardhearted.</p>
<p>Lord, one principle from <b>Proverbs</b> is this: &quot;Plans fail for 
lack of counsel, but with many advisers, they succeed&quot; (<b>Proverbs 15:22</b>).&nbsp; This is a 
principle not a promise. So, Lord, help me not to rush into major decisions 
without getting godly counsel first. Help me realize my dependence upon both 
You and other people. Help me cultivate friendships with other believers so 
that I can call upon them for counsel and so that they can call upon me.</p>
<p>I pray for church board leaders, officers and teachers. 
If any do not know You, may they become born-again. Help them to be true to the 
Bible and to put Your Word over their own personal agendas. Help them to have 
genuine integrity, Biblical convictions, love, wisdom and courage. Help them be 
more concerned about pleasing You than advancing their agendas; may they seek 
harmony when reasonable within the congregation.</p>
<p>Father, I have some personal health concerns including: __________. 
I also pray for the physical, mental, or spiritual health of others 
including: __________.</p>
<p>I pray for the small Pacific Island nation of Kiribati. 
Thank You that the evangelical church is growing while liberal skeptical 
Protestantism is declining. Yet, Lord, some of the false religions are growing 
at an even faster pace. I praise You for the 7% of the population who claim to 
be born-again believers. May that number triple over the next decade. And, Lord, 
help those who know You to walk with You closely.</p>
<p>Thank You for Deaf World Ministries <b><i>&quot;</i></b>an 
independent...interdenominational ministry with an international perspective that 
provides opportunities for deaf, hard of hearing and hearing who have a call to 
reach out to the deaf and hard of hearing people group in the world who have 
little opportunity to know the Gospel.&quot; Thank You, Lord, for their attempts to 
reach the deaf with the Gospel. Grant them harmony, provision and the power of 
Your Spirit.</p>
<p>Now help me to put You first in the daily grind of my life, 
I request in Jesus' Name, Amen.</p>

