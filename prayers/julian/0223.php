<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 23
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Lord, </p>
<p>You are Yahweh, the Great I AM. You especially use Your 
Name when You are talking about keeping Your Covenants. Your Word is Your seal; 
what You promise, You deliver. Some of Your Covenants are conditional, others 
unconditional. Your unconditional covenants fascinate me: not matter what 
humans do, You will do what You have promised. Your determination to honor Your 
Word is one of many things that is so impressive about You!</p>
<p>Lord, one day You will judge those who know You for the 
purpose of reward. Some of Your children will be saved so as by fire. Others 
will have few rewards, while yet others receive many. Help me to store up for 
myself treasures in heaven. Help me remember that my labor for You is not in 
vain. May I make an abundant entrance, receiving Your, &quot;well done, good and 
faithful servant.&quot; Although I am saved by Your grace and not my merit, I will 
be rewarded in heaven according to my faithful service.</p>
<p>So, Lord, help me to be a person of endurance. Help me not 
to get weary in well-doing. Keep me from rusting out, keep me from burning out, 
instead, help me to faithfully hold out when it comes to serving You.</p>
<p>Lord, every day I am confronted with choices. Many choices 
do not even register as choices because habit or protocol makes the decision for 
me. Yet, Lord, I am called upon to decide things that affects others, myself, 
and the sometimes Your kingdom. May I be a person of wisdom, a person guided by 
Your Spirit.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>Today I pray for the South American nation of Chile. Thank 
You that almost 17% of the population claim to be born-again! But, Lord, the 
churches lack solid leaders, solid doctrine, and solid thinking. Help the 
Christians there to value and develop their minds for You, to love You, not just 
with their hearts but with their minds, strength, souls and hearts. Revive the 
lackluster condition of the church; help believers to be more faithful in church 
attendance and help them find and pursue good discipleship opportunities.</p>
<p>I remember Partners International, a ministry which, &quot;has 
actively sought opportunities to bring the hope of the gospel to the world's 
difficult places. We specialize in partnering with indigenous Christian 
ministries in the developing world. Partners International links prayer and 
financial support from Christians in the West with third-world agencies 
ministering to spiritually, emotionally and physically needy people.&quot; Bless 
their efforts, provide for the nationals, and may those who are supported be 
faithful to their calling.</p>
<p>Today I pray for the many good community organizations 
which help the needy or provide services that make a difference.</p>
<p>Bless my day, I 
pray, in Jesus' Name, Amen.</p>

