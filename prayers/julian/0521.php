<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 21
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>I speak to You person to Person. You care about me and the details of my 
life even though the purpose of my existence is to 
glorify You. You now indwell me. This is real. Help me to live in light of 
reality. There is no pretending here.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>I pray for Your children who are divorced that You would 
help them to go forward with Your hand in theirs. I pray for the children of 
divorced parents. Lord, the experts say that the scars remain for a lifetime 
and many children blame themselves for their parents' divorce. O God, You know 
the pain that children feel even if they do not express it. Please, Lord, help 
these children! And help adults whose parents were divorced. Help them 
persevere in their own marriage and not to panic at conflict.</p>
<p>Father, Satan roams about as a roaring lion. His demons 
network to accomplish maximum iniquity, to ruin whatever is good. As a believer 
in Jesus Christ, I take up the armor of God today. I put on the helmet of 
salvation. I am safe in the Savior's care having been redeemed by His blood. 
I put on the breastplate of righteousness, the perfect status You give me 
because You see me in Christ. I wrap truth around my waist. I love the truth 
and take no delight in lies, falsehoods, or exaggerations. I face the truth 
even if it is painful and humbling. My feet are ready to serve You in sharing 
Christ with others. I take up the shield of faith: I choose to trust You 
despite my doubts and despite my unanswered questions. And for my offensive 
weapon I wield the sword of the Spirit which is Your Word. I do not just own 
a Bible, I read it and meditate upon it. I replace lies and halftruths with 
the piercing truth of Your Word. So, Lord, bind the forces of evil in my life 
today.</p>
<p>Lord, the ancient nation of Lebanon needs a touch of Your 
Spirit. Only .6% of the population claim to be born-again believers and 
because of terrorism and hostage-taking many missionaries or foreign Christians 
left the country. Thank You that some advances have been made particularly in 
the realm of training church leaders. Father, multiply the number of believers 
five times over the next decade. In Your sovereignty, please create a safer 
environment and open hearts to the Gospel like never before. Remember that the 
temple was built from the cedars of Lebanon and shower this people with Your 
grace, I pray.</p>
<p>Thank You, Father, for Carver Foreign Missions and their ten missionaries 
who serve in Liberia. &quot;The mission operates the Carver Academy which is an 
elementary and junior high school with 350 students and the Monrovia Bible 
Institute.&quot; Most of the teachers at these schools are graduates from Carver 
Bible College. O Lord, bless both Carver Foreign Missions and Carver Bible 
College. Use them in mighty ways, empower the teachers, students and 
missionaries by Your Spirit, provide for them and grant them unity and harmony.</p>
<p>May I have a 
blessed day, I ask through our Lord Jesus Christ, Amen.</p>

