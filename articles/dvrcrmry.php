<p>The subjects of divorce and remarriage are complicated and have always been
matters of controversy among Bible-oriented Christians. When addressing this
matter, I recognize that many top-notch scholars agree AND disagree with my
understanding of Scripture. I am personally satisfied that the evidence in
Scripture is strong enough for me to come to certain conclusions, yet others
with differing views feel the same way.</p>
<p>Interestingly, the strictest Protestant groups in matters of divorce and
remarriage are many Baptist groups. Many Baptists believe it is never right to
divorce and remarry. The most liberal mainstream religious group is the Reform
Jews who view divorce as unfortunate but not really a moral issue. Surprisingly,
Baptists have the highest divorce rate while liberal Jews have the lowest.&nbsp;
The
lesson needs to be learned that a stricter position or harsher approach does not
necessarily produce better results.</p>
<p>Our goal in a Scriptural matter should be to find out the meaning of the text
as understood by the writer/speaker, not to use verses to further our own
personal agendas or viewpoints. The issue is not, &quot;What viewpoint controls
divorce the most?&quot; but rather, &quot;What do the Scriptures teach?&quot;&nbsp;
In
<b>Malachi 2:16</b>, God's general attitude toward divorce is clear, &quot;I
hate divorce.&quot; In
<a href="javascript:scripture('../scripture/matt53132.htm')" target=_self class=pop>Matthew 5</a> and
<a href="javascript:scripture('../scripture/matt1939.htm')" target=_self class=pop>19</a>,
Christ tells us that divorce and
remarriage are permissible on the grounds of immorality, a broad word that
includes the concepts of adultery, the practice of homosexuality, or other
perverted relationships. That does not mean one MUST divorce a spouse guilty of
such offenses, but rather that one MAY do so. I advise continuing the marriage
if the unfaithful spouse is truly repentant. If children are involved, it is
usually better for the kids to have two natural parents with a strained marriage
than one parent or step-parents (according to studies done by Dr. Wallerstein).</p>
<p>Here is a brief summary of my perspective on divorce and remarriage.</p>
<ol>
<li>Divorce and remarriage can be proper if there is a breach of faithfulness
through immoral practice, such as adultery. Even in these situations, divorce is
not always advisable (as per above), but often is appropriate
(<a href="javascript:scripture('../scripture/matt1939.htm')" target=_self class=pop>Matthew 19:3-9</a>).<br>
<li>Divorce and remarriage can also be proper if one is deserted. Paul tells
us in <b> 1 Corinthians 7</b> that Jesus did not address THESE issues, which is why Paul does
(<a href="javascript:scripture('../scripture/1cor712.htm')" target=_self class=pop>1
  Corinthians 7:12</a>).<br>
<li>If a spouse finds living with his/her spouse is intolerable and divorces
(due to things like abuse, illegal activity, etc.), then he or she must remain
single or be remarried to their former spouse, divorce with NO remarriage, NO
cohabitation (which is ALWAYS wrong), or NO dating
(<a href="javascript:scripture('../scripture/1cor71011.htm')" target=_self class=pop>1
  Corinthians 7:10-11</a>).&nbsp;
  Though &quot;unmarried&quot;&#151;hence divorced&#151;her spouse is still &quot;her
husband.&quot; This concession is obviously made for extreme situations, not
disappointment nor boredom.<br>
<li>Lack of happiness or incompatibility are no grounds for divorce. Marriage
is a commitment until death; we should not look back, second guess, or dream of
greener pastures.<br>
<li>When believers have fallen away from the Lord and divorce for improper
reasons and are now cohabiting, etc., it may be better for them to remarry in
certain instances. Since adultery has already been committed, sometimes the
question is, &quot;What is the lesser of evils?&quot; This is especially
complicated if the couple have had children together.
</ol>
<p>Remember this: whenever we leave God's ideal will and start looking for
&quot;least worst choices,&quot; we are the losers, even if things &quot;work
out.&quot; The marital vows are commitments, not options. Commitment, not the
chemical high of romance, is what holds most marriages together.</p>

