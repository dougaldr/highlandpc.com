<p><b>Why the Deviled Ham?</b></p>
<p>The 
event described below took place in the area around Decapolis. This area was 
directly Southeast of Galilee, but a gentile region embedded in the Greek 
culture. Unlike the Jews, these gentiles ate pork in great quantity (they were 
the precursors to we Hoosiers).</p>
<p>
When Jesus freed the demonized man who lived in the region of the Gerasenes, the 
demons asked not to be sent to the Abyss (the holding area for bound demons who 
are awaiting the Day of Judgment), but instead to remain in the region of the 
Decapolis (see below). These evil spirits asked permission to enter the nearby 
herd of swine. Christ granted them permission to do so, and the herd rushed off 
the edge of a cliff into the lake and was drowned.</p>
<p>
Some have asked the question, &quot;Was it morally right for Christ to allow the 
demons to do this, since the herd was the property of the swineherd?&quot; Not only 
were the pigs killed, but the owner lost a lot of money. Mark tells us that 
there were about 2,000 pigs involved. Although we call this incident &quot;deviled 
ham,&quot; you can be sure it wasn't too funny to those who earned their living 
raising pigs. This is a legitimate objection, for which we may offer these 
observations:</p>
<ol><li>Christ did not command the demons to enter the herd—he <b>allowed</b> them to 
do so.
<li>Christ did not direct the demons to drown the hogs; the demons made this 
choice. David Stern (<i>Jewish New Testament Commentary</i>) writes, &quot;Some have 
suggested the demons destroyed the pigs in order to prejudice the owners against 
Yeshua—which is what actually happened.&quot;</ol>
<p>Did 
Christ treat the owners with contempt because he was dealing with gentiles? 
Certainly not. The <i>Talmud</i> reveals an ethic held by the Jews: &quot;For a Jew 
to cheat a gentile is worse than cheating a Jew. In addition to breaking the 
moral law, it brings Jews into a position of contempt.&quot;</p>
<p><b><i>Taking things a little 
further, we can conclude the following:</i></b></p>
<ol><li>Human life is infinitely more important than animal life; the well-being of one 
man was worth the loss of 2,000 pigs.
<li>God likewise ALLOWS events in our lives that He does not necessarily command. 
God allows evil and loss to affect us all, whether by permitting Satan, the 
world, or our sin natures to run their course. This often does NOT seem fair. 
When we remember God owes us nothing, then we can try (although not always 
succeeding) to think this way: God prevents a whole lot of misery on our 
behalf; each prevention is an act of His grace. Sometimes God is more gracious 
to us than at other times.
<li>One's blessing can be another's curse; the demoniac's gain was the swineherd's 
loss. In a sense, the story of the exorcism of this man pictures our unvoiced 
objections as to how God does things and what He allows. It also demonstrates 
that He has a right to do as He chooses.</ol>
<p><b>The Decapolis</b></p>
<p>The 
Decapolis means the &quot;ten cities&quot; in Greek, and it was the Greeks who set up 
these Greek &quot;colonies&quot; during the time of Alexander the Great, but 
reestablished by the Romans under Pompey in 63 B.C. Since Jesus was previously 
employed as a builder (&quot;carpenter&quot; is not an accurate translation—Jesus 
probably worked mostly with stone and only somewhat with wood), and since this 
region was booming in the early first century, it is very possible that Jesus 
worked in some of the cities of the Decapolis.</p>

