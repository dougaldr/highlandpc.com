<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 23
<br><span class=byln>by Ed Vasicek</p>

<p>Yahweh, </p>
<p>I praise You with all my heart. I am not ashamed to do so 
and will praise You with Your people as well as in private. The Great things 
You do overwhelm me. I think about how You saved me from sin and condemnation, 
how You have given me Your Spirit and brought me into the blessings of the New 
Covenant—and how You are preparing a place for me in glory!</p>
<p>And Father, Your character lasts forever. I have outlived 
I do not know how many &quot;lifetime warranties.&quot; But You and Your qualities endure 
forever. Thank You for providing the world—and me—with food and 
sustenance. Thank You that You keep Your promises, and that in the future  
even more dramatically than You have done in the past You will show the 
nations Your power. Though You are powerful and though I hold You in awe, I do 
not fear You withdrawing Your promises or being unfaithful. You are completely 
trustworthy. There is none like You! So help me fear You, that I might learn 
wisdom, for You are the source of it.</p>
<p>O God, give me discernment when it comes to entertainment. 
On the one hand, I want to relate to my culture and participate in its good 
events. On the other hand, some movies, music, etc., harms my soul, my 
conscience and my mentality. I know You want me to guard my mind. So help me 
to strike the right balance. Keep my mind actively discerning and help me by 
Your Spirit, O Lord.</p>
<p>I pray for Christian students in secular colleges 
throughout our world. Lord, many professors have an anti-Christian agenda and 
they exploit their position of authority and make it into a bully pulpit, 
intimidating and deceiving others. O Lord, may professors begin to respect the 
rights of students and help students remember even when pressed that there 
is always another side.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>Lord, I remember my country's chief executive officer 
(President, Prime Minister, Secretary General, etc.) and our legislative and 
congressional branches. May all these leaders favor true and complete 
religious freedom. We want to be able to peacefully share our faith and live out 
our convictions; may they respect our rights to do so. Help them to govern with 
righteousness and equity.</p>
<p>Lord, I pray for the Latin American island nation of Turks 
&amp; Caicos Islands. Thank You that over a third of its 20 thousand inhabitants 
claim to be born-again evangelicals, but the temptations are many and 
licentiousness is common. O Father, help those who know You to walk with You in 
their daily behavior, I ask.</p>
<p>My King, thank You for the ministry of International Teams, 
as they minister to &quot;refugees searching for a new home, [in]...evangelism and 
church planting where there is no community of faith...affirming the dignity of 
the marginalized...serving the greatest in the Kingdom who often suffer the most 
[through] children's ministries...ministry to the economically 
disadvantaged...training up next generation leaders [through] youth ministries.&quot; 
Thank You for their work in Asia, Europe, Latin America, Africa, the Middle 
East, Australia and the Pacific. Grant them fruit for their labor, harmony 
amongst themselves, protection, provision and the power of Your Spirit, I ask.</p>
<p>Lord, help me to 
be a light in my world today, I pray in Jesus’ Name, Amen.</p>

