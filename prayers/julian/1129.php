<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 29
<br><span class=byln>by Ed Vasicek</p>

<p>My Wonderful God,</p>
<p>The Savior is sometimes referred to as The Bridegroom 
(<a href="javascript:scripture('../../scripture/john329.htm')" target=_self class=pop>John 3:29</a>). 
As a bridegroom anticipates being with his bride, so the Lord Jesus 
Christ is waiting to enjoy His bride—the completed church—forever. One day, we 
will all be with Him, celebrating the marriage supper of the Lamb and enjoying 
our destiny as Christians. Like the ancient bride in the Middle East, the 
bridegroom will come at an unexpected time to literally sweep the bride off her 
feet and carry her away to a banquet at her father's home, so we wait to hear, 
&quot;Here's the bridegroom! Come out to meet him!&quot; (<b>Matthew 25:6</b>). Just as a 
groom would build on a room to his father’s house for him and his bride, so our 
Lord Jesus is preparing a place for us in glory in His Father’s house 
(<a href="javascript:scripture('../../scripture/john1423.htm')" target=_self class=pop>John 14:2-3</a>).</p>
<p>But Lord, right now, I am not in Your house. I am an alien and a stranger, Your 
ambassador, bearing testimony to Your grace in a world that by and large 
rejects You. So help me to be in this world—to participate—but not to be &quot;of the 
world&quot;—to become corrupted by it.</p>
<p>Help me to resist temptation and the lures of the Satan today. I put on Your 
armor by faith, so that I may do battle for You as a faithful solider. I claim 
the victory because greater are You, Holy Spirit, Who is in me than he who is in 
the world—the devil (<b>1 John 4:4</b>).</p>
<p>Father, I have some personal health concerns including: __________. 
I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>Lord, today I pray for the Continent of Europe with its 725 
million people. With the collapse of communism and the predominance of secular 
humanism in western Europe, born-again evangelical Christians have become 
sparse, only about 2.4% of the population. But thank You, Lord, that the growth 
rate is upward, even if at just 1.8%. O Father, do a mighty work in Europe as 
You did twice before: when Christianity swept the Roman Empire and again 
(regionally) during the Reformation. May the former communist countries turn to 
You in epidemic proportions and may the Gospel reassert itself over the 
lifeless secular humanism which has swept much of the continent.</p>
<p>Thank You, Lord, for Chinese Overseas Christian Mission, 
&quot;an interdenominational mission with the objectives of bringing the gospel to 
the Chinese scattered over Europe through pioneering evangelism, church 
planting, training and literature work. The mission’s vision is 'where there are Chinese, may 
Christ be there also.' [They] also desire to ‘Reach the Chinese to reach 
Europe.'&quot; O Lord, grant them fruit for their labor. May they not only 
effectively reach the Chinese people scattered throughout Europe, but may they 
effectively reach other Europeans as well. Prosper, protect and empower them. 
Grant the workers harmony and wisdom, I pray.</p>
<p>I remember those who work as operating engineers and other 
construction equipment operators. Lord, give them safety on the job and may 
those who know You bear strong testimony to the salvation they have in Christ 
Jesus.</p>
<p>Help me to live wisely today, I pray in His Name, Amen.</p>

