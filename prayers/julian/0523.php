<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 23
<br><span class=byln>by Ed Vasicek</p>

<p>My Great Master in Heaven,</p>
<p>The Savior is the image of the invisible God 
(<a href="javascript:scripture('../../scripture/col115.htm')" target=_self class=pop>Colossians 1:15</a>). 
I do not mean that You, O God, have a physical body; but in Christ we 
see Your deity veiled by the humanity of Jesus. As the old Christmas carol puts 
it, “Veiled in flesh the godhead see; hail the incarnate deity.” Through Jesus 
Christ, we see God in a way that we humans can understand.</p>
<p>Lord, I call You “Master,” for indeed You are and I am 
Your servant. But sometimes, Lord, I live like I am the master and You are the 
Servant! Help me to remember that it is about You, not me. What an easy thing 
to say and what a hard thing to live. Like John the Baptist, help me realize 
that “He must increase and I must decrease” (<b>John 3:30</b>). Deliver me from a 
divided heart 
(<a href="javascript:scripture('../../scripture/ps8611.htm')" target=_self class=pop>Psalm 86:11</a>), 
a real condition with which I struggle and a battle 
I sometimes lose.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. He and his hoards are on the prowl, but help me not to be ignorant of his 
schemes. Help me overcome him by the blood of the Lamb and the word of my testimony 
(<a href="javascript:scripture('../../scripture/rev1211.htm')" target=_self class=pop>Revelation 12:11</a>).</p>
<p>Lord, help me to remember not to make decisions based upon 
my emotions. Help me not to confuse the emotional with the spiritual. Help me 
remember that, “He who trusts in his own heart is a fool” (<b>Proverbs 28:26</b>); 
instead, help me cling to Your Word and the direction given by wise fellow-believers. Help me be cautious 
and wise in making decisions and to weigh the 
long-term effects of my choices carefully and realistically.</p>
<p>Father, I remember the African nation of Liberia in 
prayer. Thank You that over 9% of the population claim to be born-again 
evangelicals while almost 5% of the population in other churches claim to be 
born-again. The evangelical church has its work cut out. With disease, language 
barriers and the effects of civil war—coupled with the tendency of people to 
blend Christianity with paganism—it makes for slow and tedious progress. O 
Lord, help the people of Liberia to respond to the Gospel in great numbers, 
doubling the number of believers over the next decade. But even more so, 
improve the quality of the believers. May Your Spirit hover over this land.</p>
<p>Thank You, Lord, for Christian Aid as they send “financial help 
to over 700 evangelistic ministries based in lands of great poverty or where 
evangelical Christians are a persecuted minority.” They “work among unreached 
peoples.... These missions...send out native missionaries—over 90,000 now on 
the field...plant new churches—over 52,000 each year...conduct evangelistic 
campaigns, broadcast on radio and TV, distribute literature” and are “training 
40,000 new workers in hundreds of Bible institutes.” They also provide 
“assistance to victims of persecution, diseases and disasters.”&nbsp; O Father, 
empower this work by Your Spirit, provide for their needs, grant them harmony 
and effectiveness in the tasks to which You have called them, I pray. Protect 
them and direct them, my Lord.</p>
<p>Use me today, I ask in Jesus’ Name, Amen.</p>

