<p id="pgt">The Constitution of Highland Park Church
<br><span class=byln>Adopted June 21, 1992. Revised April 30, 2017.</span></p>

<p class="sctn">SECTION I: GOVERNMENT<p>
<p align="left"><b>Article I: Operational Policy and Character of the Church</b></p>
<p>Our fellowship shall be based on the clear and obvious truth of Scripture.&nbsp;
The head of this church shall be Christ; the decision-makers shall be the
elders; and the elders shall be accountable to the membership.</p>
<p align="left"><b>Article II: Membership</b></p>
<ol>
  <li>Any person meeting the following requirements may be considered as a
    candidate for membership:</li>
   <ol type=a>
     <li>a profession of repentance toward God,</li>
     <li>a profession of dependence upon the work of Christ in His death, burial
      and resurrection as the only basis of forgiveness and
      salvation,</li>
     <li>general agreement with the doctrine and approach of Highland Park
      Church,</li>
     <li>a life not characterized by extreme hypocrisy (drug/alcohol abuse,
      immorality, spouse/child abuse, criminal activity, unethical business
      practices, etc.).</li>
    </ol>
  <li>A candidate for membership shall share his/her testimony with any current
    elder. The elder shall present a recommendation regarding the
    candidate to the board of elders, who may accept the candidate into
    membership.</li>
    </ol>
<p align="left">
    <b>Article III: Church Discipline and Accountability</b></p>
<p align="left">This section refers to members of Highland Park Church, not
unsaved people or non-members who attend (unless under discipline from another
evangelical church).</p>

<ol>
  <li>Church members shall be disciplined by the method and for the gross sin
    recorded in the following passages:
    <a href="javascript:scripture('../scripture/matt181517.htm')" target=_self class=pop>Matthew 18:15-17</a>;
    <a href="javascript:scripture('../scripture/1cor51167.htm')" target=_self class=pop>1
    Corinthians 5:11-6:7</a>;
    <a href="javascript:scripture('../scripture/gal19.htm')" target=_self class=pop>Galatians 1:9</a>;
    <a href="javascript:scripture('../scripture/2thes31015.htm')" target=_self class=pop>2
    Thessalonians 3:10-15</a>
    and
    <a href="javascript:scripture('../scripture/2john10.htm')" target=_self class=pop>2 John 10</a>.&nbsp;
    The elders shall be the final authority in all matters of discipline.<br>
  <li>Accountability principles require that members attend Highland Park Church
    on a fairly regular basis. Minimal regular attendance shall be defined
    as approximately twelve services per year. Members who neglect
    attending regularly shall be removed from the membership roll, unless
    exempted. Exemptions include shut-ins, elderly members, out-of-area
    members, members working Sundays, etc. Each situation shall be weighed
    by the elders.<br>
  <li>
    <p align="left">A member removed from the roll due to lack of attendance is
    welcome to fellowship with us and may reapply for membership when he/she is
    ready to become more involved in the church.</ol>
    <p align="left"><b>Article IV: Officers of the Church</b>
<ol>
    <li>A nominating committee consisting of eight to ten members shall meet
      annually, preferably in February, for the purpose of selecting candidates
      for office. This committee shall consist of two elders, two deacons,
      two deaconesses, and the remainder shall be non-office-holding members;
      all committee members shall be chosen by the elders. The committee
      shall be chaired by the senior pastor. The committee shall present
      its nominations to the elders. A screening form developed by the
      elders shall be distributed to the nominees. The completed screening
      form will assist the elders in evaluating the nominees (see
      <a href="#v5" target="rtop" class="reg">Article V, #5</a>). A list
      of the approved nominees shall be presented to the
      membership at the annual business meeting. Candidates must receive
      at least a two-thirds approval, written ballot, to confirm the position.<br>
    <li>Officers of the church include elder, deacon, deaconess, treasurer,
      assistant treasurer, financial secretary, church clerk. Other offices may 
      be created as necessary. All terms of office, except for staff pastors, 
      shall be for two years. Staff pastors are reaffirmed or removed every five 
      years.<br>
    <li>Officers shall be members of Highland Park Church. Teachers (for
      example, Sunday School and Children's Church), club directors, and club
      leaders are to be members as well. (Exceptions allowed if cleared by
      the elders.) Substitute teachers and helpers, though not required to
      be members, must abstain from sexual immorality, drunkenness and/or use of
      illegal drugs, dishonesty, etc. Failure to comply will result in
      dismissal.<br>
    <li>The board of elders shall consist of the senior pastor and three or more 
      men who are elected by the congregation and are not pastoral staff members. 
      Elders must be male, profess salvation, have been baptized by immersion, and 
      meet the criteria of
      <a href="javascript:scripture('../scripture/1tim317.htm')" target=_self class=pop>1 Timothy 3:1-7</a>
      and
      <a href="javascript:scripture('../scripture/titus169.htm')" target=_self class=pop>Titus 1:6-9</a>.&nbsp;
      They shall meet as necessary. The chairman of the board of elders
      shall be selected by the elders annually. The senior pastor may not serve
      as chairman.<br>
  <li>Elders shall have the following duties:
    <ol type=a>
      <li>making decisions regarding the ministry of the church,
      <li>stewardship of the church's funds,
      <li>exemplifying the Christian life for others to model,
      <li>shepherding the flock including praying, visiting, anointing (as per
      <a href="javascript:scripture('../scripture/james51316.htm')" target=_self class=pop>James 5:13-16</a>),
      <li>handling problems including church discipline,
      <li>taking charge of the Lord's Supper.
    </ol>
    &nbsp;
  <li>Staff pastors shall be responsible for:
    <ol type=a>
      <li>preparing and delivering edifying sermons from the Word of God,
      <li>visiting members who are hospitalized or shut-in,
      <li>counseling members as needed,
      <li>performing funerals for deceased members,
      <li>officiating wedding ceremonies according to each staff pastor's
        convictions,
      <li>overseeing ordinances,
      <li>initiating a vision for the future.
    </ol>
    &nbsp;
  <li>The board of deacons shall answer to the elders. Deacons shall serve
    as the church's trustees and shall be responsible for making decisions
    regarding buildings, grounds, matters of safety, and insurance. Deacons
    shall meet the qualifications of
    <a href="javascript:scripture('../scripture/1tim3813.htm')" target=_self class=pop>1
    Timothy 3:8-13</a>.&nbsp;
    The deacons shall elect a chairman annually.<br>
  <li>The board of deaconesses shall answer to the board of deacons and to the
    board of elders. Deaconesses shall assist those boards and pursue special
    concerns expressed by the women of the church, such as a safe and adequate
    nursery, women's banquets, baptism of women and other special concerns.&nbsp;
    Deaconesses shall meet the qualifications of
    <a href="javascript:scripture('../scripture/1tim311.htm')" target=_self class=pop>1
    Timothy 3:11</a>.&nbsp;
    They shall elect a chairwoman annually.<br>
  <li>The treasurer shall furnish a financial report for all regular elders'
    meetings. He/she shall also provide monthly reports for the
    membership. The treasurer shall receive all monies, pay all bills, and
    keep an accurate account of all transactions. He/she shall work in
    conjunction with the financial secretary and assistant treasurer.<br>
  <li>The church clerk shall maintain an accurate membership roll and take
    minutes at congregational business meetings.<br>
  <li>The financial secretary shall keep a record of incoming monies and verify
    the record with the treasurer as necessary.</ol>
<p align="left">
    <b>Article V: Limitations, Policies, and Accountability Checks</b>
<ol>
  <li>The church shall hold a regular business meeting at least once per
    quarter. The meeting date is to be determined by the elders and
    communicated to the congregation with at least one week advance notice.&nbsp;
    All church officers are expected to attend regular business meetings.<br>
  <li>Twelve members at a business meeting shall constitute a quorum. The
    elders, deacons, or twelve voting members may call a special business
    meeting. Two weeks notice shall be required for a special business
    meeting, except in an emergency.<br>
  <li>The church shall hold an annual meeting, preferably in April. Available
    church statistics shall be presented during this meeting. Additionally,
    candidates for office will be presented for confirmation.<br>
  <li>All officers seeking to resign shall give at least one month's notice to
    the elders unless resigning for moral, doctrinal, or crisis reasons.<br>
  <li><a name="v5"></a>Each candidate for office shall indicate his/her general
  agreement with
    the doctrine of our church.<br>
  <li>The deacons are the church's trustees but shall make no purchases without
    permission or delegated authority (and its accompanying limits) from the
    elders. The deacons shall not take out a loan or handle real estate
    without consent of the membership at a business meeting.<br>
  <li>The elders may not approve funds for any project exceeding one week's
    average offering (based on previous year) without permission from a
    two-thirds majority of the members present at a business meeting.<br>
  <li>All missionaries being considered by the church board for financial
    support must be approved by the elders and confirmed by a two-thirds
    majority at a business meeting.<br>
  <li>The board of elders or a two-thirds majority of the membership present at
    a business meeting may remove any officer from his/her office.<br>
  <li>If any office or position is vacated, the elders may appoint someone to
    serve until the next annual meeting.<br>
  <li>The elders&#39; board reserves the right to grant or refrain from granting 
    permission for any and all activities involving church properties. Highland 
    Park Church&#39;s property and facilities may be used only for purposes consistent 
    with the church&#39;s doctrinal beliefs as reflected in the Bible and spelled out 
    in this document, including its statements on morality, marriage, and human 
    sexuality. Property and facility use will not be permitted to persons or groups
    holding, advancing, or advocating beliefs or practices that conflict with 
    Highland Park Church&#39;s religious teachings as reflected in the Bible and this 
    document.<br>
  <li>Non-members of the church may be required to pay a fee for the use of
    church facilities.<br>
  <li>No drinking of alcoholic beverages shall be allowed on church properties.&nbsp;
    No smoking is allowed in the church building.&nbsp;<br>
  <li>All church organizations (for example, Sunday School, Awana,
    church-related home fellowship groups, Women's Missionary Society) shall
    answer to the elders and the membership.<br>
  <li>Guest speakers shall be in general agreement with the doctrinal statement
    of Highland Park Church.<br>
  <li>Men shall be taught doctrine or Bible content or be under the preaching of
    men only, whether in a service, Sunday School, or any official church
    function
    (<a href="javascript:scripture('../scripture/1tim21115.htm')" target=_self class=pop>1
    Timothy 2:11-15</a>).&nbsp;
    For practical purposes, a man shall be
    defined as a male eighteen years or older. Women may teach
    non-doctrinal materials (Biblical geography, languages, teaching techniques,
    etc.) to men, as well as sharing testimonies or personal applications of
    Scripture. Women may lead songs, be involved in drama, usher, minister
    in music, and anything else not specifically limited to men in our
    understanding of Scripture or the Constitution. Women may teach Bible or
    doctrine to women or children.<br>
  <li>Voting members must be at least sixteen years of age.<br>
  <li>The elders shall interpret and apply this constitution.
  <li>All new pastoral staff will be given a probationary status during the first 
year of employment. Even though pastoral staff is approved by the congregation 
for a five-year term, new pastors will be evaluated during the first year of 
employment. During the probationary period, the elders shall either 
    <ol type=a>
      <li>confirm employment, 
      <li>terminate employment, or 
      <li>extend the probationary period. 
    </ol>
This applies to new pastors only during their first term of service. Once 
employment is confirmed, they may only be removed as described in Article V, point 9.
</ol>
<p align="left">
    <b>Article VI: Amendments</b>
<p align="left">
    This constitution may be amended at any time by a two-thirds majority of the
    membership present at a business meeting. It is suggested that this
    constitution be evaluated every five years for possible adjustments.</p>
