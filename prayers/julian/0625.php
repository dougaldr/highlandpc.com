<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 25
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are not a God with needs or issues. You are a 
completely independent God. You do not need us; You are complete in Yourself. 
Yet you choose to befriend us, to love us, to feel with us and to experience 
emotions toward us. You allow Yourself to be grieved, so we are urged not to 
grieve the Holy Spirit. Your wrath burns against the non-repentant and You 
rejoice with us when we rejoice and You share our grief when we grieve. You 
sent Your Son to pay the price of our redemption not because You had to redeem 
us but because You chose to do so. Human hands do not serve you as though You 
needed anything nor do you dwell in temples we build 
(<a href="javascript:scripture('../../scripture/acts172425.htm')" target=_self class=pop>Acts 17:24-25</a>).</p>
<p>First John says that if I confess my sins, You are faithful and&nbsp;just to forgive 
me those sins, and You will purify me from all&nbsp;unrighteousness (<b>1 John 1:9</b>). I 
confess the sins I am aware of now and I ask Your Spirit to search my heart (<i>pause 
to admit them to God</i>).</p>
<p>Father, I pray for my health. My body is special, for You 
created not only man's spiritual nature but also His physical body. Since my 
body is a temple of the Holy Spirit 
(<a href="javascript:scripture('../../scripture/1cor619.htm')" target=_self class=pop>1 Corinthians 6:19</a>), 
I should care for it. 
Abusing my body by harsh treatment does nothing to make me more godly 
(<a href="javascript:scripture('../../scripture/col223.htm')" target=_self class=pop>Colossians 2:23</a>). 
Yet keep me from being obsessed with it; help me place the 
focus upon the spiritual 
(<a href="javascript:scripture('../../scripture/1tim48.htm')" target=_self class=pop>1 Timothy 4:8</a>).</p>
<p>Lord, today I remember the nations in which believers are 
persecuted, facing death, dismemberment, torture, rape, sexual abuse, 
imprisonment, slavery, or confiscation of goods. The ten worst nations for 
persecution include: Saudi Arabia, Afghanistan, Laos, China, Turkmenistan, Iran, 
North Korea, Vietnam and Maldives. O Father, may these and the others among 
the 86 persecuting countries become enlightened in the realm of human dignity 
and God-given rights, including the right we have to choose and practice our 
religious convictions. May the persecutors themselves be won to faith in 
Christ, may the governments mandate and enforce the recognition of true 
religious freedom and help believers by granting them an extra portion of 
wisdom. Help them to evade persecution when possible and help them to find 
their strength in You when they cannot. Bless those ministries (Open Doors and 
Voice of the Martyrs and others) that seek to minister to these victims. May 
believers everywhere remember the persecuted church in prayer.</p>
<p>Thank You, Lord, for the European Christian Mission 
International, &quot;an international and interdenominational organization...called by God and 
commissioned by local churches to serve in partnership in the 
evangelization of the peoples of Europe. [They] are currently working in 18 countries of 
Europe&nbsp;with approaching 200 workers and partners from 20 countries around the 
world serving with [them].... Ministries extend beyond these areas through radio, 
literature and technology initiatives.&quot; O Lord, bless this outreach and 
reignite what few embers remain in Europe. May Your Spirit move mightily and 
grant many the repentance that leads to life. Provide, protect, guide and 
grant harmony for all staff members, I ask.</p>
<p>Now use me today, in Jesus' Name, Amen.</p>

