<br>Of Course Christians Should Have Fun
<br><span class=byln>by Ed Vasicek</span></p>

<p>Having been raised in a non-Christian home is far from the ideal, but if such
a person eventually gets saved, it does have its advantages. Someone from
the outside can sometimes see things that insiders cannot see.</p>
<p>When I became a Christian at the age of 17, I was introduced to an excellent
church. Although not part of any &quot;discipleship program,&quot; they
nonetheless discipled me. But I always thought it odd that the Christians
I encountered there and elsewhere had to justify any fun event in the name of
&quot;edification.&quot; When I heard people stretch a verse from <b> 1
Timothy 4:8</b>, &quot;bodily exercise profits little but godliness is profitable for all
things&quot; to promote sports (&quot;it says bodily exercise profits a
little&quot;), all I could say was, &quot;Good grief.&quot; Why this guilt
about playing sports just because you want to? Why do you need some verse
or justification? It isn't condemned in Scripture, so why sweat it (no pun
intended)?</p>
<p>Here is one of many examples of Scriptures that reaffirm the obvious: &quot;Command those who are rich in this present world not to be arrogant nor to
put their hope in wealth, which is so uncertain, but to put their hope in God,
who richly provides us with everything for our enjoyment.&quot; (<b>Timothy 6:17</b>).&nbsp;
Does God provide us with everything to &quot;nurture our bodies that we might
receive strength to serve Him?&quot; Well, he does, but He also provides
all things FOR OUR ENJOYMENT. If you want explore this further, pull out a
concordance and look up the words &quot;enjoy&quot; and &quot;enjoyment.&quot;</p>
<p>Recent articles in <i>Moody Magazine</i> addressed this same issue and our
need to be comfortable with fun. Unfortunately, the articles were
themselves inhibited. The authors did point out that we shouldn't have to
justify everything we do, but they were still writing from that defensive
posture. So let me throw in my two cents.</p>
<p>Scriptures assume that we want to enjoy life. It is considered a
no-brainer, that is why we do not have a &quot;theology of fun&quot; in the
Bible. The Bible says little about enjoying life for the same reason it says
nothing about tying our shoes or combing our hair: God does not presuppose
us to be completely senseless. When we Christians try to engineer life as
though it were a highly-organized technical lab, we run into trouble. Or,
to put it in Vasicek language, everyone instinctively know they are supposed to
enjoy life. It is only when we &quot;become so smart that we become
stupid&quot; that we run into trouble.</p>
<p>But it is not just the Christian world that obsesses with justifying fun.&nbsp;
Many success-oriented or competitive parents try to push their little ones to be
ahead of the pack academically. Despite the fact that kids seem to catch
up in the third grade, they want to start their children reading early.&nbsp;
Fun activities are justified in the name of their educational value. Everything
is thought out and justified. Give me a break, will you? Better yet,
give them a break. Let the kids be kids. If you can't be a kid when
you are three or four, when can you?</p>
<p>Of course there is another type of enjoyment that is clearly wrong. We
read about Moses who, &quot;chose to be mistreated along with the people of God
rather than to enjoy the pleasures of sin for a short time...&quot; (<b>Hebrews
11:25</b>).</p>
<p>It is also easy to arrange having fun as a priority over God. Some
Christian men, for example, may choose golfing Sunday morning after Sunday
morning rather than keeping church a priority. But it's not wrong to play
golf on Saturday just for the fun of it (not to take care of the body God has
given you, not to refresh you for worship on Sunday—just for the fun of it!).</p>
<p>The problem is not so much that people elevate fun above God, but it is
rather that people depress God below fun. If a person takes <b> Matthew 6:33</b>
seriously (&quot;But seek first his kingdom and his righteousness, and all these
things will be given to you as well.&quot;), he will find plenty of time for God.&nbsp;
If a person will does not take <b> Matthew 6:33</b> seriously, he will place just about
anything before God. That's the real issue.</p>
<p>Another reason for this guilty obsession to justify fun can be traced to the
confusion between glorifying God and worshipping God. When people use
these two terms as synonyms, they create a mess.</p>
<p><b>1 Corinthians 10:31</b> reads, &quot;So whether you eat or drink or whatever you
do, do it all for the glory of God.&quot; In other words, I can glorify
God by enjoying my food. I can glorify God by simply appreciating His
blessings and being human. This is not the same as worship, which implies
a focus on God's uniqueness. If we all brought a sack lunch to church and
ate together, would that be considered a &quot;worship service?&quot; I
don't think so. But it could be a time in which we glorified God.</p>
<p>Simply enjoying life as a redeemed human who puts God first glorifies Him.&nbsp;
He
takes pleasure when we enjoy ourselves. Why complicate it?

