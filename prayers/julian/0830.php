<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 30
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Thank You for being a God Who looks out for the best 
interest of Your children. You lavish us with Your grace; You do not cut it 
tight. You work all things together for good though we are often perplexed 
when trying to understand how it is so. You care for our daily needs and You 
give us even more than we ask for. No, our lives are not beds of roses, for You 
bring trial and tribulations to us at times. But ultimately in the long run, 
we will appreciate the intricacies of your plans, plans to bless us and do us 
good. Your Son, our Lord Jesus Christ, is our Advocate against Satan’s 
accusations. Lord, You are the God Who justifies us, Who goes to bat for us, 
Who fights for us. Even when appearances seem otherwise, Your Providence is at 
work.</p>
<p>Father, I praise and worship You, for You are worthy of all 
praise. I bow my knee before You; I acknowledge my complete and utter 
dependence upon You. Every breath I take is a gift from You. You are an awesome 
God and I devote myself to You because I am awestruck with You. Like John the 
Baptist, may my motto be, “He must increase, but I must decrease.”</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Help me to do my part by volunteering to serve or 
continuing to serve in a Bible-believing, Bible-teaching church. May I set my 
roots down deeply and attend every week I am able to do so.</p>
<p>Today I pray for the Chinese province of Inner Mongolia 
with its 24 million people. The number of born-again evangelicals is perhaps 3%, 
mostly from the influx of Han Chinese people who now outnumber the Mongolians in 
the region. Lord, many of the people are steeped in Buddhism and demonic 
oppression is a common problem because of it. Thank You for the growth of house 
churches; Lord, within 5 years, help the number of born-again evangelicals grow 
to 7%. Do a mighty work here; penetrate the native Mongolian culture and the Evenki and Oroqen peoples, I ask.</p>
<p>Thank You, Lord, for East–West Ministries. &quot;God has called an unusual blend 
of academic and business people from varied professions and with varied life 
experience to come together and form East-West Ministries International.... What 
[they] share is a love for Jesus Christ and a passion to be agents of His grace 
in a world that desperately needs the salvation He alone can give.  East-West 
works hand in hand around the world with indigenous church leaders and other 
existing ministries to fulfill Christ's command to ‘make disciples of all 
nations...teaching them to observe all that I have commanded you.' (<b>Matthew 
28:19-20</b>).&quot; Thank You for their work in the nations of the former Soviet Union, 
other Eastern European and even Caribbean nations. Prosper their ministries, 
protect them, provide harmony for them, meet their needs and empower them by 
Your Spirit, I ask.</p>
<p>Bless my efforts to serve You here at home, I ask in Jesus’ Name, Amen.</p>

