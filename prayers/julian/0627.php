<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 27
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When Ezekiel saw a vision of You, he first saw wheels 
within wheels full of eyes and cherubim, each with four faces. He marveled at 
what He saw, but He then saw a vision of You. Although no man has seen God at 
any time as He is, You have appeared in a vision on several occasions. Ezekiel 
saw a throne of sapphire and a figure in the shape of a man, a vision of You. 
You appeared as glowing metal from the waist upward and downward You appeared as 
a fire with a rainbow-like brilliance surrounding You 
(<a href="javascript:scripture('../../scripture/ez12628a.htm')" target=_self class=pop>Ezekiel 1:26-28a</a>). At 
this vision, Ezekiel fell on His face before You. So when we get just a taste 
of Your majesty, we cannot help but be overwhelmed, for You are an overwhelming 
God!</p>
<p>O, Lord, please help the hurting, the emotionally 
wounded and the devastated. Strengthen those stung by disappointment, 
encourage the demoralized and bind up the emotionally wounded. Especially 
help: __________.</p>
<p>Father, I pray for those who go from marriage to marriage, 
job to job, church to church, credit card to credit card. Perhaps this includes 
me. Others are tossed when it comes to belief; again, perhaps I have this 
tendency. May I decide once and for all that my life is first and foremost 
about You, not me. Unite my divided heart, O Lord.</p>
<p>Lord, help me to be cautious myself, for I am prone to 
wander. I am not above falling away from You or being ensnared. Your Word 
warns, &quot;Let him who thinks He stands take heed lest he fall&quot; (<b>1 Corinthians 
10:12</b>). My own vulnerability and propensity to sin is easy to rationalize or 
deny. Please keep me from such foolishness. Help me make no provision for my 
sinful nature.</p>
<p>I pray for New Zealand and the 22% of the population who 
claim to be born-again evangelical Christians. Yet, Lord, the younger 
generations are not nearly as likely to be involved in church, and Christianity 
is often viewed as irrelevant in &quot;the real world.&quot; The Maori minority has 
hardly been reached. Thank You that some new churches are tapping into the 
younger generations and for the missionary zeal of the churches that are 
dynamic. O Lord, move by Your Spirit and draw sheer numbers of people to 
Christ. May the churches that are true to the Word grow at a good rate, 
doubling the number of believers over the next decade. Help them reach and 
disciple the youth, I ask.</p>
<p>Thank You for the tentmaking, ("tentmaking" means serving as a missionary while 
working at a secular job), ministry of Global Opportunities whose mission is 
to &quot;mobilize and equip missions-committed lay Christians to serve abroad as 
effective tentmakers, primarily to least-reached peoples. [They] provide 
prospective tentmakers with overseas job resources, job and missions' counsel, 
training and mentoring to increase effectiveness, connections with coworkers in 
country, and limited support on the field. Global Opportunities seeks to partner 
with individuals, churches and mission agencies to accomplish this task.&quot; </p>
<p>Now help me bear witness to Your grace today, I pray, in 
Jesus' Name, Amen.</p>

