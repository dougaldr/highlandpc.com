<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 25
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>I say Hallelujah! You sent Your Son to be my Savior 
over 2,000 years ago. As I reflect on this event, I marvel at Your love and 
care for me. Your word says, &quot;But when the time had fully come, God sent his 
Son, born of a woman, born under law, to redeem those under law, that we might 
receive the full rights of sons.&quot;</p>
<p>The time was right, the virgin Mary miraculously conceived 
and God the Son became a man! He was born &quot;under law.&quot; He was of the people of 
Israel, the tribe of Judah and the house of David. He came to buy us back from 
the curse of the Law because we are all lawbreakers and under that curse. And 
now, we are heirs together with Christ. We are Your privileged children!</p>
<p>Today we celebrate and well we should. For You, O Lord, 
have given us all things to enjoy. We join hands with others who know Christ 
and the real meaning of the holiday and we sing our songs of praise and joy.</p>
<p>Today I pray for those who are not as blessed as I am: the 
poor, the downcast, the depressed, the abused, the ignored, the broken-hearted, 
the infirm and the ill. O Father, please send some Christmas cheer their way 
today.</p>
<p>I pray for my loved ones, those who are nearest and dearest 
to me. May they all come to know You if they do not; if they do know You, may 
they stand firm in You and be serious about growing in the grace and knowledge 
of our Lord Jesus Christ.</p>
<p>I pray for the leaders of our world. O Lord, we know the 
promise of Christmas is &quot;peace on earth.&quot; Yet we know that this peace will only 
occur when the Prince of Peace, the Lord Jesus Christ, returns to reign. But in 
the meantime, O Lord, grant us a great measure of peace.</p>
<p>Father, help me to serve You faithfully. Holidays sometimes 
change my routine and it is easy to turn into a different person. Perhaps I am 
tempted to be impatient, or I let relatives upset me over trite issues or 
quirks. Help me exemplify godliness for the rest of this year and into the 
next.</p>
<p>Lord, today I pray for my church and evangelical churches 
everywhere. I pray for Bible-believing pastors, that they might walk closely 
with You and take their calling to teach Your Word with utmost seriousness. 
Provide for them, protect them, guide them and empower them by Your Spirit.</p>
<p>I pray for missionaries who are longing to be at home and 
with their extended families this Christmas. Lord, missionaries—and their 
families—make so many sacrifices. I especially pray for lost family members 
who cannot understand why their kin would move to a foreign land; may they come 
to know You and then embrace the Great Commission. </p>
<p>Now, Lord, help me to enjoy the wholesome pleasures of  
Christmas to Your glory I pray, in Jesus' Name, Amen.</p>

