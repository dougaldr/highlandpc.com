<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 31
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>How can I thank You for Your goodness to me? You are 
my Creator and You uphold the universe to keep it from dissipating. You are my 
Redeemer. I was lost in sin, alienated for You and under Your divine wrath. 
But You sent Your Son, Jesus Christ, to die and rise for me. Even though Christ 
did this for me 2,000 years ago, I still was born lost until Your Spirit brought 
me to life through regeneration. He coaxed me along. Although I could not 
understand the things of the Spirit, He opened my eyes. Even though my mind was 
hostile toward the true God, He made the true God appealing to me. Even though 
I was spiritually dead, He brought me to spiritual life. The result: my faith 
and trust are in Jesus Christ and I am safely held in Your hand. I know You as 
my friend and heaven is my eternal home. No wonder I find it easy to praise 
Your Name!</p>
<p>I pray for my lost friends and family members including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Lord, when it comes to relating to other people, help me 
not to be ignorant or inconsiderate. If I am a morning person, help me consider 
those who are not. As Your Word warns, &quot;If a man loudly blesses his neighbor 
early in the morning, it will be taken as a curse&quot; (<b>Proverbs 27:14</b>). Likewise, 
if I am a night person, help me respect the schedule of morning people. Keep me 
from trying to force everyone into my mold or putting them down for being 
different from me. Help me to keep my personal arrogance and agendas in check.</p>
<p>Today I pray for the small African nation of Sierra Leone. 
Lord, the devil has hold of this nation. Although 3.2% of the population 
claims to be evangelical Christian, missionaries have been in this land since 
the late 1700s and have made little progress. Occultism, violence—even the 
sick and senseless amputation of the limbs of children and helpless people as 
acts of terrorism are common, as is rape. The corruption and anarchy despite 
the nations wealth of natural resources has made it perhaps the poorest and 
most desperate country on the face of the earth. O God, please turn this nation 
around. Help the Christians to become strong, to triple their numbers over the 
next five years and to set a tone for restoration. Only Your Spirit can 
straighten out this unspeakable mess.</p>
<p>Lord, bless the efforts of Wiconi International whose 
mission is &quot;to see Native people find faith and life in Jesus Christ and 
fulfill their God-given place in the Body of Christ worldwide...and taking 
advantage of the international fascination with Native Americans for the purpose 
of communicating the Gospel.&quot; Lord, may this ministry be used of You to reach 
and disciple many. Empower them by Your Spirit, provide for them and grant 
them harmony, I ask.</p>
<p>Use me as a peacemaker today, I pray in the Messiah's 
Name, Amen.</p>


