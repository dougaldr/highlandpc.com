<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 4
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>You are a relational God and a God who loves families. 
You are the God of Abraham, Isaac and Jacob. You connected Yourself to men of 
faith, men who were sinners and flawed but men whom You declared righteous by 
faith. Because Jesus Christ would eventually die for their sins and because 
You, O Lord, are not bound by time, You could retain holiness yet freely endear 
Yourself to mortal men. You currently endear Yourself to those of us who know
 Christ Jesus our Lord. O Father, may Your faith also permeate my family for 
generations to come. Help me to enjoy my relationship with You so that I too, 
like Abraham, may be Your friend.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my 
strength in His power not my own. May Your Spirit control me&nbsp;today.</p>
<p>Father, because I am so vulnerable, I sometimes am strapped 
by fear. On the one hand, Your word tells me that a wise man is cautious, but 
when that caution turns to fear, I am held in bondage to it. Help me, therefore, 
to be cautious but not fearful; trusting in You but not presuming.</p>
<p>Lord, help my church be harmonious. May the leaders be firm when it comes to 
the clear teachings of Scripture and gracious about the not-so-clear matters. 
Keep antagonists, troublemakers and those who resent authority away from our 
congregation. Help my church to be effective in evangelism, discipleship, 
loving one another and world missions.</p>
<p>Speaking of missions, today I pray for the nation of 
Pakistan. Most of the people are Muslim while evangelical believers number 
less than .4%. Those who come to Christ from an Islamic background do so at the 
risk of their own lives. Pakistan is a land where all sorts of things take 
place: kidnapping, violent crime and strife between people groups. Lord, bless 
the missions which are true to Your Word, use correspondence, literature, the 
Jesus film, Christian radio and recordings and the discreet witnessing of 
believers to reach others. Keep the believer safe, give them wisdom and help 
them to be able to evade persecution, I pray. May the number of believers grow 
tenfold over the next five years.</p>
<p>Thank You for Heart To Heart International Ministries 
whose &quot;purpose is to share the gospel of Christ in Romania. [They] minister to 
children and youth, focusing on orphans, street children, homeless and poor. As 
[they] share the gospel of Christ, [they] meet the physical, emotional and 
spiritual needs. [They] minister by providing food and clothing; by educating 
and equipping with skills.&quot; Lord, bless this work; give them fruit for their 
labor. May they make a big difference for Christ; empower them by Your Spirit 
and provide for their needs, I ask.</p>
<p>I remember those who work as bus and truck mechanics and 
diesel engine specialists. Grant them safety on the job. Help them to do their 
work well and may those who know You freely share Christ and set a Christian 
example, I pray.</p>
<p>Help me to set such an example myself, I ask in Christ's 
Name, Amen.</p>

