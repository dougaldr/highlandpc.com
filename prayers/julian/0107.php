<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 7
<br><span class=byln>by Ed Vasicek</span></p>

<p>Yahweh, how I praise You that You are self-derived. You 
had no beginning and You will have no end. You are completely uncreated and 
though You take pleasure in Your creation, You are complete in Yourself. With 
the heavenly host, I praise He who was, and is, and is to come.</p>
<p>I thank You for raising me to life and coaxing me along to 
believe in the Lord Jesus Christ. Thank You that You have legally seated me in 
heavenly places along with Christ, far above all principalities and powers.</p>
<p>Father, since my citizenship is in heaven and I walk this 
earth as one of Your ambassadors, may my life show that my heavenly citizenship 
has my first allegiance. Although You expect me to fully participate in my 
civic and community duties, and although I confess I love my country, I put the 
cross before the flag.</p>
<p>Lord, I have a basket full of personal needs, including: __________. 
Meet these needs according to the riches I have in Christ 
Jesus.</p>
<p>Help me to resist temptation and the lures of Satan 
today.</p>
<p>I also pray for my lost friends and family members, including: __________. 
Father, please send Your Spirit to draw them to Yourself.</p>
<p>Today I pray for the Czech Republic. Although You have 
worked in great ways in this area in bygone centuries through the work of Jan 
(John) Hus and Jan (John) Komensky (Comenius), and although many people profess 
some form of Christian faith, others are atheistic, and those who do profess 
Christianity often do not have a personal, saving relationship with Jesus 
Christ. Oh, God, please bring great numbers of Czechs to saving faith in the 
living Christ.</p>
<p>Today we remember the Slavic Gospel Association. Bless 
this ministry as they minister throughout Eastern Europe.</p>
<p>Locally, Lord, we pray for our firefighters. Protect them, 
give them alertness, and may they serve our community well. We pray that You 
would use them to save lives and minimize damage.</p>
<p>Bless my day, Lord. Remind me of Your presence throughout 
it, I pray, in Christ’s Name, Amen.</p>

