<p><b>Part I</b></p>
<p>One of the most confusing questions to deal with in 
interpreting and applying Scripture is the believer&#039;s relationship to the Law. 
As is typical, we often find it attractive to reduce matters down to a nutshell 
or a pithy saying, providing simple answers to complex problems.</p>
<p>Even the question, &quot;What is the believer's relationship to 
the Law&quot; raises many other questions. Which believer—a believing Jew or a 
gentile? Before or after Pentecost? Which relationship to the Law (there are 
many)? Which Law? (Law, or Torah, means instruction; there are many set of 
instructions in the Bible.)</p>
<p>At the outset, we can begin with a few black and white 
conclusions that are more clear.</p>
<ol>
<li>It is, and always has been, impossible for sinners to 
be saved by law keeping.
<li>In every era, God demands His children to seek to obey 
His revealed will; the Bible is filled with instruction (Torah) along these 
lines.
<li>Obedience to God's instruction is a good measuring 
stick of our relationship to God.
<li>In both the Old Testament and New Testament, there are 
varying standards for believing gentiles (the Covenant of Noah) and believing 
Jews who wish to remain part of the covenant nation (the Law).</ol>
<p>Regarding the Torah given to Moses, Paul writes in <b>1 Timothy 1:8-11</b>:
<blockquote>
We know that the 
law is good if one uses it properly. We also know that law is made not for the 
righteous but for lawbreakers and rebels, the ungodly and sinful, the unholy and 
irreligious; for those who kill their fathers or mothers, for murderers, for 
adulterers and perverts, for slave traders and liars and perjurers—and for 
whatever else is contrary to the sound doctrine that conforms to the glorious 
gospel of the blessed God, which he entrusted to me.</blockquote>
<p>Note that there is a use for the Law (of Moses) even during 
the Christian era.</p>
<p>More next time.</p>
<p><b>Part II</b></p>
<p>Although Christians are not &quot;under the Law&quot; (that is, we do not find 
acceptance with God through striving to obey the Law), the term &quot;Law&quot; often 
refers to the commands of God. We cannot find forgiveness from sin by keeping 
the Law, but we can find God's direction through a careful study of His Word, 
and, in that sense, the Scriptures in general are Law.</p>
<p>Every follower of Jesus is expected to obey the Savior's commands (Torah, 
instruction), though some are admittedly difficult to understand. Incidentally, 
the commands of Jesus include the entire New Testament. The Apostles were 
special authoritative representatives of Jesus, wielding His complete 
authority.</p>
<p>Up until about 75 AD, the majority of believers were Messianic Jews. Did Jews 
who believed in Jesus as Lord and Messiah abandon their Jewish practices (as 
prescribed in the Old Testament)? The answer is clearly, &quot;No.&quot; 
(<a href="javascript:scripture('../../scripture/acts211726.htm')" target=_self  class=pop>Acts 21:17-26</a>)
Church history also verifies that for the first two centuries, Jewish 
believers in Jesus maintained their Jewish practice by attending synagogue, 
eating kosher, and celebrating the Feasts of Israel. Even James, the Lord's 
brother who wrote the Book of James, was put to death because he was trying to 
purify Temple worship by exposing the corruption of the priests. James was 
highly respected by both Messianic Jews and non-Messianic Jews.</p>
<p>Unlike some non-Messianic Jews, believing Jews recognized that they were 
saved by grace through faith in Christ, not law-keeping. They kept the Torah of 
Moses (though not necessarily the traditions) but accepted gentile believers, 
who did not abide by all the commandments given through Moses to Israel, as 
equals. These gentile believers sought to obey the New Testament Torah, part of 
which was based upon the Torah of Noah 
(<a href="javascript:scripture('../../scripture/acts151921.htm')" target=_self  class=pop>Acts 15:19-21</a>). 
Jewish believers were 
not absolutely required to remain within Judaism, but the peer pressure was to 
do so. They could choose to live the lifestyle of gentile believers and take 
the status of &quot;grafted in&quot; branches, if they wished.</p>
<p>It is crucial for all believers to study the Torah of Moses. True, we are 
not held accountable to obey the national, dietary, nor ritual commandments, but 
we can learn much about God, about His plan for the people of Israel, and about 
treating others fairly and justly. Additionally, many of the rituals and 
religious practices of the Law of Moses prefigure the Person and Work of 
Christ, like the Tabernacle and the Feasts.</p>
<p>The Law of Moses gives us wisdom, and its principles are often applicable 
to a host of contemporary situations. The Law of Moses is very often the basis 
of New Testament teaching. Additionally, we cannot deeply understand the New 
Testament without understanding the Old. The New Testament writers assume that 
their readers already had a background in the Hebrew Scriptures.</p>
