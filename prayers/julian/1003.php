<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 3
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the God of Power. When You consciously will 
something, You effectively accomplish it. We can lift many things, but we have 
to move our bodies, stoop down and exert energy to lift. You merely think the 
thought and it is so. In a way, we can understand Your power. We can imagine 
things and create things in our minds, but they do not materialize. You 
imagine and what You imagine comes into being. Thank You for imagination, for 
it helps us comprehend what little we can understand about You.</p>
<p>You are unlimited in Your power, but You often choose to 
use indirect means to create. For example, You created the earth and then You 
fashioned Adam from the dust and breathed into him the breath of life. You 
formed Eve from his rib and all of us are formed from Adam and Eve�s genes. It 
doesn�t have to be that way; You could create each one of us individually, if so 
wished. </p>
<p>And so, Lord, You normally work through the routine of 
life. But in some cases, You work a miracle or answer a prayer because You are 
not bound by Your creation as though it were a higher law above even You.</p>
<p>Lord, I have some requests to bring before Your throne. 
They include: __________.</p>
<p>Father, please fortify marriages everywhere. I pray for my 
family, my extended family, my church family, my friends and acquaintances. 
Help them to take marriage seriously, to work hard at maintaining and improving 
their marriages and not to allow them to suffer from neglect. Lord, help them 
work out differences and help couples to seek to please one another. Stifle 
the attitudes of demanding and distancing, I pray.</p>
<p>Lord, I pray for the Middle Eastern nation of the United 
Arab Emirates. Lord, this small nation of less than 3 million people has less 
than one percent (.8%) who claim to be born-again evangelicals and Christians 
are commonly arrested if they try to witness. Father, use television, radio, 
the Jesus Film, dreams and literature to reach many more for Christ. Please 
double the percentage of believers within the next five years and help the 
Christians to be strong and wise. Change the political climate so that 
believers are free to witness without persecution, I pray.</p>
<p>Lord, thank You for Mexican Medical Ministries, &quot;an 
interdenominational...organization...dedicated to providing low or no-cost health 
care to the people of Mexico in the Name of Jesus Christ. [They] believe part of 
the Great Commission is bringing healing and hope through medicine and 
the Good News of salvation through Jesus Christ.&quot; Lord, bless their efforts. 
May they indeed be messengers of mercy who also share the good news of Jesus 
Christ. Provide, bring harmony, protect and empower them by Your Spirit, I 
ask.</p>
<p>Lord, I pray for executive secretaries and administrative 
assistants including church secretaries. Help all in these fields to work well 
and get along with others. Father, may those who know You shine and be noted as 
excellent workers with servants hearts. Use them to win many to Christ, I pray.</p>
<p>Help me serve You 
this day, in the Savior�s Name I ask it, Amen.</p>

