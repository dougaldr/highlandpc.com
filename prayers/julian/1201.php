<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 1
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the Most High God. You do not live in houses 
made by human hands. Indeed, Heaven is Your throne. The earth is Your 
footstool. Although this is figurative language, it helps us imagine a little bit 
about how vast you are 
(<a href="javascript:scripture('../../scripture/acts74849.htm')" target=_self class=pop>Acts 7:48-49</a>). 
Lord, You are not served by human hands 
as though You needed anything 
(<a href="javascript:scripture('../../scripture/acts1725.htm')" target=_self class=pop>Acts 17:25</a>); 
all is from You, so how could we give You anything—it is all Yours to begin with?</p>
<p>Although You do not <i>need</i> us to serve You, thank You that 
You take pleasure in us as we serve You. As a Father does not need his five 
year old son to help paint a wall—but as he enjoys including him and cherishes 
his son's desire to be like his dad—so You, O Lord, cherish our efforts and 
take pleasure in us as we seek to be like You.</p>
<p>God, I pray for those of Your children who have social 
problems. Some suffer mental handicaps, others have social anxieties or severe 
introversion, while others have been traumatized or neglected, and now they have 
difficulty making friends or participating in society. Help those who know You 
to first find a social life within a solid church family; but may their social 
life not end there. Help them to make friends and participate in their 
community.</p>
<p>Father, thank You that our Savior understands sorrow, 
loneliness, rejection and even betrayal. He experienced them firsthand. He can 
therefore help us in our loneliness and rejection.</p>
<p>O God, money management can be a challenge, if not for me, 
than for others I know. O God, may Your children get a grip on their finances, 
I pray.</p>
<p>I pray for future generations, should Jesus tarry. I pray 
that today's church would plan for ministering in tomorrow's world. I pray that 
we would train a generation of leaders who can handle the challenges awaiting 
them in this complicated world of ours. I pray that You would help stabilize 
the moral fiber of modern society and that the masses would turn to You, O 
Lord. Help Your children to embrace the concept of absolute and objective 
truth.</p>
<p>I remember the nation of Japan in my prayers. O Father, 
this nation seems to be so resistant to the Gospel. Cults like Moonies and 
Mormons are growing faster than the evangelical church! Only four-tenths of 
one percent of the population claim to be born-again evangelicals, despite 
religious freedom. Only 10% of the population believes in a Personal God. The 
church has not really grown since 1960, but has barely held its own. O Father, 
break through with the power of Your Spirit, I pray!</p>
<p>Lord, bless the Institute for Bible Translation &quot;whose 
main aim is translation and publication of the Bible in the languages of 
non-Slavic peoples living in Russia and other countries of the CIS. Some of 
these peoples number many millions, other languages have only a few thousand 
speakers.&quot; O Father, provide, protect and use this ministry to spread Your 
Word, I pray.</p>
<p>Use me to spread Your Word in my world, I ask in Jesus' 
Name, Amen.</p>

