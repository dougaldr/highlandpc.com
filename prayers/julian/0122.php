<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 22
<br><span class=byln>by Ed Vasicek</span></p>

<p>Precious Lord,</p>
<p>I praise You that You are a kind God. Although it is true 
that You are not tame or always &quot;nice,&quot; You have a heart of compassion and You 
care about Your creation. Not a sparrow falls without Your knowledge, and You 
number the hairs on our heads. You comfort the downtrodden and broken hearted, 
and You have a special place in Your heart for children, the orphan, and the 
widow. You are generous, sending the rain upon both the righteous and the 
unrighteous. It is Your kindness that leads us to repentance.</p>
<p>Lord, I do not want to be paranoid, but there are three 
enemies constantly stalking me: the world, my sin nature, and the kingdom of 
darkness. The world—society—has its good and bad points. You want me to 
participate in my society, to be in the world. You want me to serve as light 
and salt, and You do not want me to retreat under a bushel. Yet, You want me to 
reject that way of thinking that refuses to put Your will and Your Word first. 
In the world, but not of it.</p>
<p>My sinful nature is strong, too strong. You want me to 
walk in the Spirit, and, with His power, to turn a deaf ear to the call of my 
fleshly desires. And, although Satan himself will probably never bother me, his 
lesser associates, demons, do. As they tempt me or put thoughts into my mind or 
create undesirable circumstances or happenings, help me to remember that even 
the demons are subject to You. Help me resist temptation in Jesus' Name, and 
help me not to give place to the devil. Thank You that I am secure in Your 
hand, and I claim the blood of Christ and word of my testimony. I choose to put 
on the armor of God.</p>
<p>Abba, Father, I have many needs. Life has a way of 
throwing curveballs, and I find it easy to strike out. Give me wisdom when I 
face the unexpected. Help me to delay my responses and conduct myself with 
wisdom. Keep me from making a fool of myself—or blurting out responses I will 
later regret.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control 
me today.</p>
<p>Lord, today I pray for the nation of Bangladesh. What a 
seemingly hopeless mess it is! The people live in utter poverty; the Islamists 
are pushing harder and harder to mandate Islamic law, and evangelical Christians 
make up less than one half of one percent of the population. The 230 million 
Bengali people represent one of the largest unreached groups in the world, and 
so Bangladesh and the Bengalis who live in India need to hear about Christ 
desperately. Bless the Jesus film and continue to reach people through this, we 
pray. May the number of born-again Christians grow exponentially in the next 
decade.</p>
<p>Yahweh, I ask for You to bless and prosper Arab World 
Ministries.&nbsp; We know that the Muslim people are among the most unreached in the 
world, and we know that Christians literally take their lives and wellbeing in 
their hands when sharing Christ or living the Christian life. Protect and 
direct the associates of this mission. Give them tact, shrewdness, and the 
wisdom and power of Your Spirit. Exceed the mission's goals, expectations, and 
even dreams.&nbsp; May Muslims flock to Jesus Christ in droves through their work.</p>
<p>Today we think of our community libraries and librarians. 
We know, Lord, that it is so important for Christians to think, and to think 
deeply. Thank You for community libraries that serve the entire population.</p>
<p>Lord, I surrender 
myself anew to You today, in Jesus' Name, Amen.</p>

