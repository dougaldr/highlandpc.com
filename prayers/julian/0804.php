<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 4
<br><span class=byln>by Ed Vasicek</p>

<p>My Master and My God,</p>
<p>You are King over all the earth 
(<a href="javascript:scripture('../../scripture/ps477.htm')" target=_self class=pop>Psalm 47:7</a>). 
Although Satan is the &quot;god of this world,&quot; the crucified and resurrected Christ has 
defeated him. Right now, You permit history to take its course, but all along 
You have been steering its direction toward its ultimate culmination when Jesus 
Christ returns to reign. As both God and Man, He will reign over all the earth 
and of His Kingdom there will be no end. On day we will look back and sing, 
&quot;The kingdom of the world has become the kingdom of our Lord and of his Christ 
and he will reign for ever and ever&quot; (<b>Revelation 11:15</b>). You, O Lord, are King 
of the Earth.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Lord, troubled marriages are all around me. Often I am not 
aware of that. A friend at work, a neighbor, a relative. A mom with young 
children feels trapped and could use some time away with her husband, 
but no family is near or there are no trusted friends to watch the children; 
finances are tight and the tension high. A man or woman was reared in a messed 
up family and the baggage they bring into their marriage is destroying it. 
Long work hours, addictions, anger, violence, immorality—people all around us 
are struggling. Help us to be supportive in every way and help the marriages 
around me to get stronger, especially within my church family.</p>
<p>Father, teach me not only to forgive the repentant, but 
also to be able to ask for forgiveness. When I sin against another, help me not 
to justify or rationalize it, but to isolate my wrong from whatever wrongs may 
have been done against me. Help me to say, &quot;What I did was wrong, please forgive 
me&quot; and nothing more. If others have wronged me, help me to address that at a 
separate time. May I be good at knowing how to ask for forgiveness. And as I 
grow, may I have less to repent from!</p>
<p>Today I pray for the nation of Slovakia. Lord, only 1.5% 
of the population claims to be born-again evangelicals. But I am grateful, Lord, 
that the evangelical church is growing even if at a slow 1.3% rate. Father, pour 
out Your Spirit on this land! Ignite the believers with holy zeal and great 
wisdom, use the Jesus movie, bless the youth outreach and help radio to 
effectively reach and help disciple many. Father, double the number of believers 
in Slovakia over the next decade; may You grant many the repentance that leads 
to life.</p>
<p>Lord, I remember Alongside Ministries. &quot;For over twenty 
years [they] have worked with churches and leaders who have sought [their] help 
in several countries in Europe and North America. These include Albania, 
Austria, the Czech Republic, England, Estonia, France, Germany, Scotland and 
Canada and the U.S. [They] develop ministries wherever [they] have the resources 
and competencies to serve responsibly, as God calls...and where the needs of 
Christian witness are not adequately served.&quot; Help them establish and nurture 
good, solid churches that glorify You, O Lord.</p>
<p>Now use me to honor You today, in Jesus' Name, Amen.</p>

