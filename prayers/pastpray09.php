<p id="pgt"><span class="pgh">Sunday Morning Pastoral Prayers:</span>
<br>Prayer #9
<br><span class=byln>by Ed Vasicek</span></p>

<p>Our Holy God,</p>
<p>You are the Sovereign, the Supreme Ruler, the One Who 
Determines and the King of Kings. As King Nebuchadnezzar confessed, &quot;His 
dominion is an eternal dominion; his kingdom endures from generation to 
generation. All the peoples of the earth are regarded as nothing. He does as he 
pleases with the powers of heaven and the peoples of the earth. No one can hold 
back his hand or say to him: ‘What have you done?’&quot; (<b>Daniel 4:34-35</b>) You 
truly set up kings and take them down. You are in charge and we are foolish to 
think otherwise.</p>
<p>But Lord, we are so limited: we are imperfect; You are 
perfect. We know a little; You know everything that was, is, shall be, or might 
be. We can be creative, but You created everything out of nothing by merely 
speaking the Word. We try to cover all the bases of life and we fall short. 
We rush from one place to another, hoping to be on time for our appointments. 
You are present everywhere at once. Yet, despite Your infinite superiority, 
Your love for us is strong. You proved it when Christ died for us. You proved 
it when Your Spirit brought us to new life in Christ.</p>
<p>Father, sometimes it is difficult for us to transfer our 
convictions into real life. We think our opinions matters as much as the 
decrees of Your Word. We are tempted to create a separation between spiritual 
truth and our daily lives and responsibilities. Biblical Christians are meant 
to live out their faith in the trenches and routines of life, to take the Word 
of God out of mothballs and put it to work. We are meant to hear the Word and 
then do the Word. And although doing the Word includes church life, it is just 
as relevant whether we are at work, school and especially home. Lord, unite our 
hearts. May each one here be one person, not two. The Kingdom of God is not 
about pretending.</p>
<p>Lord, as a church family, we have many needs. Some of us 
are hurting and need Your strength and aid. Others have lost family members or 
dear friends who are headed for a Christless eternity. Some of us are 
struggling with finances, health, shame, our marriages, our children, or are 
plagued by relational strife. Others are fighting enslaving addictions. Some of 
us might be depressed, worried and fearful. Perhaps some are so beaten down 
that they fear hoping for more joyful times lest they be disappointed. We have 
difficult decisions to make, sometimes choosing between lesser evils. We need 
Your wisdom, Your direction and Your Spirit’s power in our daily lives. Help 
us to experience that abundant life You promise in Your Word. We reaffirm Jesus 
Christ as Lord of our lives.</p>
<p>We remember the missionaries that our church supports, the 
ministries we offer and the Lord we serve. Guide our church leaders into Your 
perfect will and grant us harmony and unity of purpose as a church. Bless our 
president, legislators, judges and local officials. </p>
<p>And now, Lord, as we sing our songs of praise and 
contemplate Your Word, may Your Holy Spirit minister to each of us as we have 
need, for we pray in Jesus’ Name, Amen.</p>

