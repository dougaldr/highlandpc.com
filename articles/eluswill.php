<p id="pgt"><span class="pgh">Articles:
<br>Spiritual Growth, Devotional</span>
<br>Finding That Elusive Will of God
<br><span class=byln>by Ed Vasicek</span></p>

<p>Have you ever wished that God would call you on the phone 
and tell you exactly what to do during a difficult situation? I have. On other 
occasions, I wish that God would tell me the outcome of efforts in a certain 
area so I would know whether I was wasting my time or not.</p>
<p>Discerning the will of God is not always an easy matter. 
Part of our frustration is that we have short-term goals in mind. We want to 
know who to marry, what careers to pursue, what college to attend, and how we 
should direct our children. And it never ends.</p>
<p>Some of those decisions can be very difficult ones: do I 
give permission for the hospital to turn off the machine that is keeping my mom 
alive? Should I inform the boss that a fellow employee, who has several 
children and really needs the job, is goofing off, or do I keep silent? Do I 
let my son play with the neighbors so we stay on good terms with them, even if 
their children might be a bad influence upon him? It is not always easy to know 
exactly what to do.</p>
<p>When it comes to discerning God's will, we need to 
understand that His direction for our lives is often more fluid than we 
realize. Although God's character and His truth remain the same, He is taking 
us through life's journey. We are not merely the means to an end; God is 
concerned as much about the process as He is the end result. So the question is 
not, &quot;Have you found God's will for your life?&quot; but &quot;Are you regularly seeking 
to prove God's will for your life?&quot;</p>
<p>A few months back, I recall an appearance of Rick Warren on 
the O'Reilly Factor. O'Reilly was questioning Warren about his book, <i>The 
Purpose-Driven Life</i> (which is the all-time best seller with one exception, 
the Bible) and asked Warren something like, &quot;You don't believe God micromanages 
our lives, do you?&quot; Although Warren did not answer with a yes or no, he 
indicated that God did indeed care about many of the details of our lives, but 
that does not mean He cares about every choice we make.</p>
<p>Because God cares about the details of our lives does not 
mean we must or should consult God about every detail. God knows the number of 
hairs on our head, but that does not mean we should take inventory every night, 
although with my current progression toward baldness I am tempted to do so! I 
do not have to consult God about whether I should shower daily. Neither do I 
have to consult Him about what color socks to put on, nor whether I should go 
barefooted.</p>
<p>Finding God's will is a subcategory of &quot;Christian Decision 
Making.&quot; Although God sometimes reveals His will to us, we cannot escape having 
to make judgment calls. Sometimes we will make the wrong call. Let's take a 
look at two types of decision-making.</p>
<p><b>Typical decision-making</b> (type I) describes the vast 
majority of our choices. For the Christian, typical decisions are to be made 
with WISDOM. Most of the time the will of God and the principles of wisdom 
coincide. <b>Ephesians 5:15:17</b> reads: &quot;Be very careful, then, how you live—not as 
unwise but as wise, making the most of every opportunity, because the days are 
evil. Therefore do not be foolish, but understand what the Lord's will is.&quot;</p>
<p>Note here that the opposite of God's will is being 
&quot;foolish,&quot; but God's calling for us is to be wise. We build up a reservoir of 
wisdom through serious Bible study and meditation. (See 
<a href="javascript:scripture('../scripture/ps1.htm')" target=_self class=pop>Psalm 1</a>, 
<a href="javascript:scripture('../scripture/ps19.htm')" target=_self class=pop>19</a>, and 
<a href="javascript:scripture('../scripture/prov117.htm')" target=_self class=pop>Proverbs 1:1-7</a>, 
where the specific purpose of the book of <b>Proverbs</b> is to instill 
wisdom.) The big differences between wise and foolish decision-making are 
usually: (1) the value system involved, (2) the long-term consequences of the 
decision, (3) patience and (4) realism (versus idealism).</p>
<p>Although the Bible is not the only source of wisdom (we 
learn from experience, other people, literature, etc.), it is the only 
INFALLIBLE source of wisdom. Since the wisdom of society can sometimes run 
contrary to God's wisdom (see 
<a href="javascript:scripture('../scripture/1cor268.htm')" target=_self class=pop>1 Corinthians 2:6-8</a>), we must measure ideas that 
seem to be wise against the clear teaching of Scripture: &quot;But examine everything 
carefully; hold fast to that which is good....&quot; (<b>1 Thessalonians 5:21-22</b>).</p>
<p><a href="javascript:scripture('../scripture/james15.htm')" target=_self class=pop>James 1:5</a>
counsels us to pray to God for wisdom, and 
<a href="javascript:scripture('../scripture/ps1.htm')" target=_self class=pop>Psalm 1</a>
admonishes us to acquire advice from godly people. By cultivating wisdom, we 
can be more closely aligned with God's will for us. That's why <b>Romans 12:2</b> 
defines God's will not so much as something God reveals to us, but rather 
something we must prove. &quot;And do not<sup> </sup>be conformed to this world, but 
be transformed by the renewing of your mind, so that you may prove what the will 
of God is, that which is good and acceptable and perfect.&quot;</p>
<p>There is often no escape from the judgment call. But we 
need to embrace a fluid concept of God's will as His guiding us through helping 
us mature spiritually. This takes our cooperation, which is why a believer who 
does not make great effort to grow should not expect God to lead him or her 
anyway—see 
<a href="javascript:scripture('../scripture/john717.htm')" target=_self class=pop>John 7:17</a>. 
If you do not develop spiritual discernment, do not be 
surprised if you do not have discernment when you need it! Likewise, if you are 
not willing to do God's will, do not expect Him to guide you.</p>
<p>We can see that even Paul made most decisions on the basis 
of wisdom and judgment calls: &quot;Therefore, when we could endure it no longer, we 
thought it best to be left behind at Athens alone…&quot; (<b>1 Thessalonians 3:1</b>).</p>
<p>Can God have more than one option for you? Sometimes, 
yes! How much does your own personality come into play? A lot: God has made 
you who you are for a reason. Does God sometimes have a specific plan for one 
person in one area and a general plan for another? Yes!</p>
<p>Is there only one person God has prepared for you to 
marry? Maybe, maybe not. Is there only one career God has for you? Maybe, 
maybe not. Is there only one car He wants you to buy? Maybe, maybe not.</p>
<p>If you are walking with God, it is His job to reveal His 
will to you; if you are submitting your decisions to Him, and trying to act 
wisely, it is up to God to lead you otherwise if that is what He wants.</p>
<p><b>Special decision-making </b>(type II) usually begins 
with attempting to decide on the basis of godly wisdom, but involves God 
specially leading us, sometimes in contradiction to what seems wise, but never 
in contradiction to His Word. God may lead us to do something that appears to 
contradict wisdom, though He will never lead us to do something that will 
contradict His Word.</p>
<ol><li>God called Abraham to leave his town and move to a land 
He would show him.
<li>God called Moses, who was afraid to speak in public, to 
deliver His people.
<li>God called a corrupt tax collector, Matthew, to become 
an apostle.</li></ol>
<p>None of these calls make sense, but they did not contradict 
God's Word...just human logic.</p>
<p>Some Christians think every decision involves special 
decision-making. As a result even the mundane decisions of life take on a 
mystical aura, which makes people feel special. After all, the mystic reasons, 
if we view God as at our disposal to guide us through every detail, we can no 
longer be criticized for foolish decisions. If we err, God gets the blame.</p>
<p>Despite the abuses by arrogant people who think they are 
closer to God than the rest of us, the truth is that sometimes God can lead us 
in more mystical ways through His Spirit impressing our heart. The big 
challenge is to distinguish the psychological or emotional from the spiritual. 
And that is not easy. The fact that emotional and egotistical people are more 
prone to &quot;feel led&quot; than non-emotional or the humble makes the case that much of 
what is passed off as spiritual is, in fact, emotional.</p>
<p>The Holy Spirit may put an impression upon your heart. How 
can you tell the difference between that and a psychologically or 
emotionally-induced impression? Although no formula will guarantee perfect 
discernment, here are a few things I look at. If the Holy Spirit is truly 
leading us, His leading: (1) does not contradict the Word, (2) usually demands 
sacrifice on our part (this is a crucial guard against ego), (3) does not 
vacillate with our emotional cycles, and (4) is often consistent with the gifts 
and personality God has given you.</p>
<p>Since God has a master plan for His creation, sometimes we 
can be in the exact place that God wants us, yet feel like we have been locked 
out in some hallway. Being in God's will is where we need to be, but sometimes 
we have to recognize that God's ways are not our ways. He sees the big picture.</p>

