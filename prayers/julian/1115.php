<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 15
<br><span class=byln>by Ed Vasicek</p>

<p>Wonderful Savior,</p>
<p>You make me wonder indeed. You are my Savior meaning You are the one Who has 
saved me, who has delivered me from the consequence and power of my sinfulness. 
You have saved me from the divine wrath due me by absorbing it Yourself. You 
have delivered me from separation from God both for now and eternity. You 
bridged the gap between us by sacrificing Yourself. You saved me completely. </p>
<p>Father, I wish that all of life were clear and black and white! So many times 
I have to make choices between shades of gray and I find it difficult tell these 
shades apart. I need Your wisdom, for what sometimes seems to be the right 
choice works damage to my soul and what seems the wrong choice works out for 
good. Most of the time it is not that way, but sometimes it is. Only You know 
the beginning from the end, but You want me to think in terms of the long term. 
So help me look at long-term consequences, but also enlighten me with the wisdom 
from above.</p>
<p>Lord, I have some specific requests to bring before You today. They are: __________.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins, and You will purify me from all unrighteousness (<b>1 
John 1:9</b>). I accept responsibility for them and freely bring the sins Your 
Spirit brings to my mind now: __________.</p>
<p>God, like every one of the billions of people who inhabit our world, I am 
vulnerable. So, Lord, please send angels to watch over me and protect me. Keep 
me from harm and evil of every kind.</p>
<p>Lord, today I pray for the Xinjiang Uygur Autonomous region of China and its 
13 million people. Many of the people in this region are Muslims, but about 
1.6% or so are born-again evangelicals. But these believers are not from the 
indigenous people groups, so it is very difficult for them to reach others for 
Christ because of the cultural barriers. O Lord, use the Jesus film, 
literature and the testimony of believers to draw many to Yourself. I pray 
that the percentage of believers would double over this next decade.</p>
<p>O God, I pray for the ministry of The Navigators. "The 
Navigators is dedicated to helping people navigate spiritually, to know 
Christ and to make Him known as they look to Him and His Word to chart their 
lives. [Their] ultimate goal is to equip them to fulfill 
<a href="javascript:scripture('../../scripture/2tim22.htm')" target=_self class=pop>2 Timothy 2:2</a>--to 
teach what they have learned to others. Today, tens of thousands of people 
worldwide are coming to know and grow in Jesus Christ through the various 
ministries of The Navigators. Internationally, more than 4,000 Navigator staff 
of 64 nationalities serve in more than 100 countries.&quot; O King of King, bless 
this ministry! Provide, protect, empower, grant harmony and great fruit, I ask.</p>
<p>Father, help me to get along peacefully with others 
today, I pray in the Savior’s Name, Amen.</p>

