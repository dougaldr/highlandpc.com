<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 22
<br><span class=byln>by Ed Vasicek</p>

<p>Lord Jesus,</p>
<p>You are the &quot;heir of all things&quot; (<b>Hebrews 1:2</b>). One day, 
everything will be put under the feet of Jesus Christ 
(<a href="javascript:scripture('../../scripture/ps28.htm')" target=_self class=pop>Psalm 2:8</a>). 
As God the Son, You have a right to everything, for You are the Creator. But Your human 
nature will also receive the blessing of being over all things. You, God the 
Son, emptied Yourself and took on human nature, submitting Yourself to the 
Father. One day, Your human and divine nature forever together as one 
Person, Jesus Christ, will be exalted when You inherit all things. Every 
knee will bow and every tongue confess that You, my Jesus, are Lord.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You. You want me to renew my 
mind not empty it. You want me to study Your Word and think of ways to 
implement it. You want me to get to know You better by filling my mind with 
productive thoughts, to reflect Your glory by cherishing and developing my mind 
and to love You with all my mind. Lord, help me appreciate and respect the 
precious gift of my mind. Help me bring every thought into subjection to Christ.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. I give them to You and ask You to help me constantly and 
repeatedly give them to You. Thank You that I can give You the same worries 
over and over again and You never weary of them; You want to be my constant 
friend, my constant counselor and my constant source of strength. There are no 
quotas, no rationing, not protocol that limits what is reasonable with You. 
This is true with human beings; I can wear people out and make them dread my 
presence if I do not restrain my requests, but not so with You. With You, the 
more the merrier!</p>
<p>Lord, help me to get to know my neighbors where I live or 
where I work or the school I attend. Teach me the importance of participating 
in my community as an avenue to benefit myself and a way to show love to 
others. Help me to socialize.</p>
<p>I thank You that 8.2% of the African nation of Lesotho is 
evangelical Christian. Thank You also for the large born-again presence in 
non-evangelical churches, almost 13% of the population. But, Lord, AIDS is 
also a big issue affecting 24% of the adults. May the evangelical church 
double within the next decade and may the many nominal Christians get serious 
about serving Jesus Christ.</p>
<p>Lord, thank You for Chinese Christian Mission which “has 
developed an international network of missions in Taiwan, Hong Kong, Macao, 
Philippines, Singapore, Canada and Australia.” Bless them as they seek to reach 
the “1.3 billion Chinese in Mainland China and over 70 million Chinese 
overseas.” Provide the power of Your Spirit, the finances and harmony within 
the mission to accomplish this awesome task. Send angels to protect the 
missionaries and we pray that persecution with China would be a thing of the 
past. Do a mighty work, we pray, among the Chinese people—as You indeed have 
been doing. May the Chinese people become the world’s leading Christian people!</p>
<p>Bless my day, please, in Jesus’ Name, Amen.</p>

