<p id="pgt"><span class="pgh">Sunday Morning Pastoral Prayers:</span>
<br>Prayer #5
<br><span class=byln>by Ed Vasicek</span></p>

<p>Father, we praise You as You reign in majesty and glory. 
You dwell in inapproachable light, and if we were to see you as you are we could 
not live. Although Your holiness brings us to our knees, we can approach You 
through our great High Priest, the Lord Jesus Christ.</p>
<p>Our God, we are checking in with headquarters. We need Your 
direction for this coming week. Some of us have been faithful servants this 
past week, others not so faithful, and perhaps most of us somewhere in between. 
Instead of trying harder, help us to cultivate our relationship with You so that 
we will naturally review all significant decisions with You and seek to 
implement Your directions provided within Your Word.</p>
<p>Lord, we have lost friends and relatives. We pray that You 
would remove their hearts of stone and give them a new heart of flesh; open 
their eyes and grant them the repentance that leads to life. May they come to 
know our Savior, the Lord Jesus Christ.</p>
<p>We also remember your servants who serve You on the Mission 
Field. They have heard and responded to Your calling for their lives. Yet they 
are not up to the task, a task so broad that in 2,000 years we have yet to 
complete it, namely taking Your Word to all the world and making disciples of 
all nations. Bless and multiply their efforts. Open the ears of those who hear. 
May governments be friendly and accommodating, send angels to protect them, 
bless their families, and endow them with wisdom greater than Solomon’s.</p>
<p>Lord, we each have an inventory of requests and needs. Life 
has its struggles, and some of them are intense and relentless. Sometimes their 
sheer quantity overwhelms us. Remind us that You are the Sovereign Lord, and 
You only hold us accountable for that which is under our control. May we be 
faithful in trusting You through life’s challenges.</p>
<p>Help our families to be strong, and bind Satan as he tries 
to destroy our faith, our character, our fellowship, and our families. Help us 
to put on Your armor. And when we have to deal with the fallout of spiritual 
casualties, help us to make the best of each situation and to act wisely.</p>
<p>We ask You to comfort and strengthen our persecuted 
brothers and sisters throughout the world, guide our president and leaders, and 
help our nation, and, more importantly, our world, to be impacted by the Gospel 
as never before. Use our congregation to make a positive impact that is 
disproportionate to our size; may You maximize our efforts.</p>
<p>We pray in Jesus’ Name, Amen</p>

