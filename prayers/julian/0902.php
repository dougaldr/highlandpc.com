<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 2
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>I sometimes feel so unworthy of You. And, Lord, 
sometimes it gets worse: I feel unworthy in general. Life defeats me, it seems; 
I think I do not measure up. But Lord, though You are awesome, You are not 
intimidating to Your children. Your Son, our Savior, did not come to rub sin in, 
but to rub it out. You are a God Who corrects and disciplines, but You are not 
a God Who gives into lengthy scolding. And Lord, sometimes I feel inadequate 
not just because I am sinful, but also because I am limited by my own personality, 
gifts and outlook. Your calling for me is bigger than I am. The needs are greater 
than I am. Your plan for me differs from Your plan for others. Help me, Lord, to 
accept that. And thank You for not being the slavedriver that some imagine You 
to be. Thank You for working with me in gentle and patient ways. Thank You for 
Your mercies.</p>
<p>Father, protect my loved ones and I when we are on the 
road. Lord, traffic accidents, dangerous weather, even breakdowns can have 
disastrous consequences. Keep us safe as we travel, O Lord. Send angels to 
watch and protect us.</p>
<p>Lord, keep my heart ablaze for You. If it is not ablaze, 
please set it ablaze. You want me to be empowered by Your Spirit, not 
adrenaline. Help me not confuse the two. Lord, I do not want to become weary in 
well-doing because my labor for You is not in vain.</p>
<p>I remember parents. Lord, parents are so important to 
those little ones entrusted to their care. May parents everywhere protect and 
train their children in humane and balanced ways. Help them to love their 
children, not to spoil them and to encourage them toward positive directions. 
We pray that Christian parents would prove to be consistent role models for 
their young ones. Send angels to protect them, I pray.</p>
<p>Today I pray for the nation of Tajikistan, in Asia (a 
former republic of the USSR) with its over 6 million people. As a result of 
civil war, many foreigners—the main source for evangelism—left. Thank You 
though that their testimony of compassion lives on. Thank You that .1% of the 
population claim to be born-again believers; O Lord, may that number multiply by 
100 over the next 20 years! Thank You for the relative amount of religious 
freedom in this nation and I pray it would continue and expand. Use the Jesus 
film and Christian radio to reach many and help the few Christians there to 
multiply themselves time and time again, I ask.</p>
<p>Lord, &quot;the mission of the Great Commission Center 
International is to serve alongside evangelical churches and para-church groups 
for the realization of the Great Commission of our Lord Jesus Christ.&quot; Father, 
their focus is China where they &quot;support and participate in theological and 
mission training programs in China...publish training materials for Chinese 
intellectuals...publish and distribute training materials for churches in 
China...supply Christian books to major university libraries in China.&quot; 
O Lord, bless and prosper this ministry. Grant the workers 
harmony, safety, fruit and empower them by Your Spirit, I pray.</p>
<p>Lord, may my language today be gracious, seasoned with 
salt, I pray in Jesus’ Name, Amen.</p>

