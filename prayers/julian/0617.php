<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 17
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>We look forward to the day when, &quot;The pride of the 
man will be humbled and the loftiness of men will be abased,&quot; (<b>Isaiah 2:17</b>), for 
You &quot;alone will be exalted in that day.&quot; You are a god Who reveals Himself to 
the humble. Although You do reach and save many socially-respected people, You 
take great delight in revealing Yourself to common, ordinary people. In this 
way, You are the One Who receives the glory and thus it should be. For You are 
a God Who will not share Your glory with another.</p>
<p>As for me, Lord, my heart is deceitful and desperately wicked 
(<a href="javascript:scripture('../../scripture/jer179.htm')" target=_self class=pop>Jeremiah 17:9</a>).
 It is hard for me to be fair about matters involving 
myself and I tend to remember the past in ways that make me look better and 
others worse. Lord, help me be aware of this human tendency to lie to myself 
and to hold it in check.</p>
<p>I turn my hurts and dashed dreams over to You. Help me to find Your purpose and 
joy in life. There are many things I cannot change, but I can share all my 
thoughts with You. Thank You that You do not stifle me or pressure&nbsp;me to &quot;get 
over it.&quot; You are a God Who feels with me.</p>
<p>I gird parents in my prayer. Lord, parents are so 
important to those little ones entrusted to their care. May parents everywhere 
protect and train their children in humane and balanced ways. Help them to love 
their children, not to spoil them and to encourage them toward positive 
directions. We pray that Christian parents would prove to be consistent role 
models for their young ones. Send angels to protect them, I pray.</p>
<p>I bring the African nation of Mozambique before You in 
prayer, O Lord. Thank You that over 13% of the population claim to be born-again 
evangelicals, that religious freedom is now a reality and that disasters have 
softened hearts and people are spiritually hungry. Disease, violence and 
unusual hardship make it tough on the missionaries. But thank You for the 
harvest of believers and we pray that their numbers would double over the next 
decade. Guide missionaries and church leaders and use the Jesus film, radio 
and literature to reach people. There are between 10 and 15 language groups 
that do not have the Bible their own language; I pray that You would provide 
translators.</p>
<p>Lord, thank You for the ministry of Camp China which &quot;provides opportunities 
for Christian college students across North America to 
experience service overseas in East Asian countries during the summer months. 
Participants are placed on Asian campuses to teach conversational English to 
middle and high school students. The 'camp-like' nature of this program provides 
the foundation for life-changing relationships to be formed. Camp China believes 
that China and East Asia will play a very significant role in the future of the 
10/40 Window. Camp China desires to have a positive influence in this very 
strategic part of our world.&quot; Lord, bless this ministry and use it to reach 
lost people, develop Christians and provide strong relationships that will 
result in expanding Your Kingdom, I ask.</p>
<p>Now use me today, I ask in Christ's Name, Amen.</p>

