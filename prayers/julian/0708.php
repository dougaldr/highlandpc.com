<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 8
<br><span class=byln>by Ed Vasicek</p>

<p>Mighty God,</p>
<p>Thank You that Your ways are not like ours. We think 
bigger is better or more is better. We think comfort and prosperity are what 
makes life meaningful. But You teach us otherwise. When they built Solomon's 
Temple with all its splendor, the people were not as dedicated to You as those 
who built the simple Temple of Ezra. In Ezra's day, the people celebrated the 
Feast of Tabernacles with zeal and obedience unseen since the days of Joshua 
(<a href="javascript:scripture('../../scripture/neh817.htm')" target=_self class=pop>Nehemiah 8:17</a>). 
You taught us that &quot;success&quot; as defined by the world is worthless 
(<a href="javascript:scripture('../../scripture/luke925.htm')" target=_self class=pop>Luke 9:25</a>). 
Indeed, You are the God of the remnant. Help us to value 
what You value, for this is often counterintuitive for us. Thank You that Your 
thoughts are above ours, but thank You that You want us to learn from You and of 
You.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Lord, I know the church gathers so that believer can build one another up in 
their faith, the result of this is spiritual maturity as seen in faith, hope 
and love. Help me to become a more mature Christian. This does not happen in 
giant leaps but by small baby steps. Help me make those steps. I also pray 
for the following, that they do would grow a little more every week: __________.</p>
<p>Father, help me be an approachable person. When another 
comes to correct me, may I not react in anger or laugh it off; instead let me 
assess the criticism and determine how true it is, if it is. May I learn about 
myself and seek to change what needs to improve with Your help. Let me be the 
wise person who accepts and welcomes reproof.</p>
<p>Lord, I remember the Pacific island nation of Papua New 
Guinea. Thank You that over 21% of the population claim to be born-again 
evangelicals. Thank You for the freedom of religion in this land and the many 
good things You are doing. Yet, Lord, there is a lot of compromise with the 
previous pagan beliefs: witchcraft, evil spirits, etc. Additionally there are 
many unreached people groupsand at least 133 languages in this nation need a 
translation in their own language with perhaps several hundred more people 
groups needing their own language version. Lord, help the Gospel be brought 
into every area of life for each believer and may this nation become a strong 
bastion for the faith of the Bible.</p>
<p>Thank You for Hospitals of Hope. They operate a hospital 
in Bolivia seeking opportunities to share the Gospel of Jesus Christ and 
demonstrate His love. In addition to this work, they offer a &quot;hospital in a 
can,&quot; as they supply medical supplies for missionaries in several nations—18 
thus far. O Lord, please provide for this ministry, use them to reach out with 
the love of Christ, grant them harmony and great fruit for their labor in both 
the physical and spiritual realm, I ask.</p>
<p>Now use me today 
to bless others, I ask in Jesus’ Name, Amen.</p>

