<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>Peter's Restoration
<br><span class=byln><b>Notes on John 21:1-25</b>
<br>by Ed Vasicek</span> 

<p>When Jesus confronted Peter with the question, &quot;Do you love me?&quot; 
three times, the obvious implication is that Jesus is undoing Peter's triple 
denial of Jesus during the wee hours of Good Friday morning. The greatly 
respected Bible scholar, Leon Morris, writes:</p>
<blockquote>In a 
most instructive incident, Jesus talks with Peter by the lake, asking three 
times whether that disciple loves him.... Discussions often center on the fact that 
twice Jesus uses the verb agapao (&#945;&#947;&#945;&#960;&#945;&#959;) 
in his question, to be met each time by Peter's use of phileo (&#966;&#953;&#955;&#949;&#959;) 
in his answer. Then in the third question Jesus uses Peter's word and the 
apostle retains it in his reply. But the significant thing is surely not so 
much the variation in the verb, but the fact that Jesus is speaking about love 
at all. Peter's position in the apostolic band must have been at least dubious 
in light of his threefold denial of Jesus. This incident, in which Peter three 
times affirms his love for his Lord in the presence of the other apostles, must 
be seen as a reinstatement....&quot; (<u>Testaments of Love</u>, Eerdmans, 1981, 
	pp. 180-181)</blockquote>
<p>Jesus 
asks Peter two times, &quot;Do you 'agapao' me? and Peter answers, &quot;Yes, I 'phileo' 
you,&quot; until, on the third time, Jesus asks, &quot;Do you 'phileo' me?&quot; and Peter 
responds the same way as the other two times, &quot;Yes, I 'phileo' you.&quot;</p>
<p>I 
agree with Morris that the main concept is reinstatement, not the Greek words. 
But the fact that the words vary as they do is significant. Was Peter being 
stubborn? What was happening here?</p>
<p>
What no one seems to take into account is that Jesus and Peter never spoke in 
Greek. They 
never actually used the words &quot;phileo&quot; or &quot;agapao.&quot; We must remember that, 
though the authors of Scripture were inspired, they were translating the word of 
Jesus from Hebrew/Aramaic into Greek (unlike the epistles, Acts, and Revelation, 
which were in Greek to begin with). The detective work begins when we try to go 
from Greek back into Hebrew.</p>
<p>
Despite all the claims to the contrary, the Greek word &quot;agapao&quot; (or the noun 
form &quot;agape&quot;) was a commonly used word for love, especially the verb form. In 
the Greek translation of the Hebrew Old Testament book of Genesis, Jacob is said 
to &quot;agapao&quot; Esau's stew. Much like our English word for love, agape love can 
refer to anything on the spectrum between the idea of really liking someone or 
something all the way to sacrificial selfless love.</p>
<p>The 
Hebrew word Jesus could have used was &quot;ahebh.&quot; Some think that this is the word 
from which the Greek word &quot;agape&quot; originated. Generally speaking, this word can 
be used for all kinds of love.</p>
<p>
Although we cannot be sure, Peter may have answered with the Hebrew word &quot;Hesedh,&quot; 
which means a steadfast love, a deep, lasting affection that is based on 
relationship. This word is often used of God's faithful love for those under 
His covenant, but can also mean mercy or goodwill.</p>
<p>If my 
theory is correct, Jesus may have been asking, &quot;Peter, do you love me?&quot; and 
Peter responded, &quot;Yes, Lord. I love you steadfastly, based on our 
relationship.&quot; This would be especially meaningful since Peter had not been 
steadfast during his time of denial. When Jesus finally uses Peter's word, He 
was acknowledging that Peter was restored.</p>

