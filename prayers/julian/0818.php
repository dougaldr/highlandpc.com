<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 18
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>You are the God of the paradox. Many who shouted
&quot;Crucify Him! Crucify Him!&quot; later repented and 
believed, saved by the One Who was crucified for them! You reveal Yourself to 
those not considered wise in the eyes of society, but hide Yourself from those 
who are lauded by the masses. If we want to find a meaningful life, we must 
lose ourselves in You. When we are weak, then we are strong, for we trust all the 
more in You. You demand complete and total holiness if we are to be right 
with You, but You provide that holiness and perfection by putting us in Christ 
so that we are clothed in His righteousness. You are Lord of the paradox.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life, including: __________. Let me remember that You 
are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>I remember parents. Lord, parents are so important to 
those little ones entrusted to their care. May parents everywhere protect and 
train their children in humane and balanced ways. Help them to love their 
children, not to spoil them and to encourage them toward positive directions. 
We pray that Christian parents would prove to be consistent role models for 
their young ones. Send angels to protect them, I pray.</p>
<p>Lord, the Maharashtra State of India with its 95 million 
people needs the Gospel. Perhaps .2% of the population is evangelical 
Christians. Although their numbers are small, they are growing, for which I 
praise You. Hindus who have passed “anti-conversion laws” and used other 
means to persecute religions other than their own control the local government. 
Although many of the older churches are weak and not as effective as they should 
be, new churches have brought new life in the realm of outreach and zeal. Bless 
these new churches and revive the old ones, I ask. Multiply the number of 
believers in this State, I pray.</p>
<p>Father, prosper the ministry of Christian Life Missions, an 
“organization...whose sole purpose is to meet the 
challenges and existing needs of peoples throughout the world while sharing the 
gospel of Jesus Christ. Through the provision of goods, services and relief 
efforts to various nations, CLM has a vision to impact these areas which...are 
strategic in affecting world history towards the cause of Jesus Christ.”</p>
<p>Bless those, O Lord, who work in 
the fields of 
bookkeeping, accounting, and/or auditing. May 
they work conscientiously and help those who know Christ freely share their 
faith.</p>
<p>Help me to share 
the good news with someone soon, I ask. Open the doors and help embolden me to 
walk through them. I give my day to You, in Jesus’ Name, Amen.</p>

