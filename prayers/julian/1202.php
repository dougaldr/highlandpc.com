<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 2
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>Thank You that You are not slow. You are not slow 
in keeping Your promises 
(<a href="javascript:scripture('../../scripture/2pete39.htm')" target=_self class=pop>2 Peter 3:9</a>).
 When the children of Israel cried to 
You for deliverance, You delivered them right on time, just as You had promised 
(<a href="javascript:scripture('../../scripture/gen151314.htm')" target=_self class=pop>Genesis 15:13-14</a>). 
But to us, it seems like You are slow. To You, a thousand 
years is like a day. Time is irrelevant 
(<a href="javascript:scripture('../../scripture/ps904.htm')" target=_self class=pop>Psalm 90:4</a>). 
Whereas we need You to teach us to number our days, You need to do no such thing for Yourself. You exist 
outside of time's boundaries.</p>
<p>So Lord, help me to be patient. You seem slow because my 
reference point is that of a temporal mortal. To me, there is a vast difference 
between a day and a thousand years. Whereas I think in terms of minutes, hours, 
years and decades, You think in terms of millennia. How awesome You are. 
That's why I am glad to be connected to the Ancient of Days.</p>
<p>Lord, I remember what we call the Golden Rule, the words of 
our Savior in <b>Matthew 7:12</b>, &quot;Do to others as you would have them do to 
you.&quot; It is so much easier to recite than to do. Remind me to put myself in the 
shoes of others and to ask what is right. This is different from pleasing 
people, but it is considering what is in their best interest even if they do not 
like it.</p>
<p>Father, I remember those struggling substance abuse: 
alcohol, illegal drugs, legal drugs, or other abuses. O Father, help Your 
children to break free and bind Satan and his cohorts as they are often cheering 
on enslaving practices. Guide Your children to sources of help and give them 
renewed hope and direction, I pray.</p>
<p>I remember parents and their children. May parents 
everywhere protect and train their children in humane and balanced ways. Help 
them to love their children, not to spoil them and to encourage them toward 
positive directions. We pray that Christian parents would prove to be consistent 
role models for their young ones. Send angels to protect those children, I pray.</p>
<p>Lord, I remember the nation of Taiwan. Although it is 
independent from the People's Republic of China, the PRC does not view that 
independence as permanent. We know that this could be a real issue and we pray 
that You would prevent it from becoming just that. The 23 million people enjoy 
a great level of freedom, but this is also a corrupt nation. Only about 2.7% of 
the population claim to be born-again evangelical. Provide good pastors for the 
churches and multiply the number of believers there by ten over the next 
decade, I ask.</p>
<p>Thank You for the International Nepal Fellowship, &quot;a 
Christian mission called by God to serve Nepali people...involved in health and 
development work with Nepal’s government, non-government organizations and local 
communities. The work is an expression of Christian compassion and evidence of 
God’s grace...members are called by God to belong to, be committed to and serve 
Nepali churches.&quot; O Lord, use this work to glorify Yourself and to bring many 
to Jesus, I ask.</p>
<p>I commit my day to You, in the Savior's Name, Amen.</p>

