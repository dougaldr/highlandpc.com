<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 2
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord and Father of Mankind,</p>
<p>You are the One true God. The world claims many gods and many who do not know 
You claim that there is One God—but not necessarily You, the God of the Bible. 
We might agree that there is one God—but not necessarily the same God. Although 
we cannot understand all the mysteries of Your Tri-unity, we know that it is 
accurate to say that You are One; Yahweh our God is One Lord. And to the degree 
that we understand the teachings of Your Word, we believe in the true God. Help 
us to sharpen our understanding of Who You are so that Who we can say that we 
&quot;know Whom we have believed.&quot;</p>
<p>Lord, all around me are men, women and young people struggling with 
destructive substance abuses or other harmful addictions. Although it is great 
to be free from all addictions, some are definitely worse than others. And 
modern society with its dysfunctional families and high stress pressure makes a 
great hotbed to nurture these addictions. Help friends, family and possibly 
myself to learn to cope with life by walking more closely to You. Help them to 
believe that life can still be meaningful and livable, even without the 
addiction. Help us to know what to do to help our addicted friends and to 
recognize what we cannot do as well.</p>
<p>I pray for parents everywhere. Lord, parents are so important to those little 
ones entrusted to their care. May parents everywhere protect and train their 
children in humane and balanced ways. Help them to love their children, not to 
spoil them and to encourage them toward positive directions. We pray that 
Christian parents would prove to be consistent role models for their young ones. 
Help parents to give thought to childrearing in light of modern times; keep them 
from just repeating what previous generations did during simpler times. Please, 
Lord, help parents to get their heads out of the sand.</p>
<p>Lord, the nation of Iraq is a complicated one. Headlines every day 
demonstrate that this nation is having a challenge to rebuild itself. But thank 
You, Lord, for the flow of the Gospel into this land. Before the war, about 
one-tenth of one percent claimed to be born-again evangelicals. That seems to 
have grown since ministries have been allowed into the country. I pray that the 
Gospel would take a deep root there and I ask that You would help the believers 
to have unusual wisdom, boldness and be under the control of Your Spirit. O 
Lord, please do a mighty work in Iraq!</p>
<p>O Lord, thank You for the ministry of Worldlink International Ministries as 
they &quot;promote the Good News of Jesus Christ by expanding, 
equipping and encouraging the world's indigenous missionary workforce.&quot; O 
Father, provide for, protect, empower and grant harmony to this ministry. May 
many come to Christ and grow to maturity as a result of these efforts, I pray.</p>
<p>Now help me follow You today, I ask in Jesus’ Name, Amen.</p>

