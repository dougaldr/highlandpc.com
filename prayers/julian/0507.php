<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 7
<br><span class=byln>by Ed Vasicek</p>

<p>Lord Jesus,</p>
<p>You are the Root and Offspring of David. David was a type 
of You. He was a man after God's own heart and he ruled with integrity and 
honesty. But He was an imperfect type. David was a sinner who sometimes failed 
miserably. You never fail and You are the spotless Lamb of God, One Who was 
tempted in all points as we are, yet was without sin 
(<a href="javascript:scripture('../../scripture/heb415.htm')" target=_self class=pop>Hebrews 4:15</a>). 
David was of the tribe of Judah, of the nation of Israel. According to the flesh, Lord 
Jesus, You were genetically connected to David through Mary who descended from 
David's son, Nathan. Legally, You had the right to rule because Joseph was a 
descendent of David through Solomon. Even though this line was later cursed, 
You were actually connected physically to David through Mary, but only legally 
through the cursed line, thus avoiding the curse 
(<a href="javascript:scripture('../../scripture/jer2230.htm')" target=_self class=pop>Jeremiah 22:30</a>).</p>
<p>You did this, Lord, not so we could produce genealogical 
charts for our walls, but You determined these things so Your people could 
identify the Messiah: of the nation of Israel, of the tribe of Judah, the House 
of David but not the line of Coniah, born in Bethlehem 
(<a href="javascript:scripture('../../scripture/mic52.htm')" target=_self class=pop>Micah 5:2</a>) 
as the seed of the woman 
(<a href="javascript:scripture('../../scripture/gen315.htm')" target=_self class=pop>Genesis 3:15</a>) 
who was a virgin 
(<a href="javascript:scripture('../../scripture/is714.htm')" target=_self class=pop>Isaiah 7:14</a>). 
Yet many of Your own nation and family did not receive Him. But for those of us who have 
received Him, He has made us the children of God 
(<a href="javascript:scripture('../../scripture/john11112.htm')" target=_self class=pop>John 1:11-12</a>).</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>[for the married]</p>
<blockquote>Lord, thank You for what marriage has done for our society 
and this world. Help my marriage to be a blessing. Guard it, put a hedge 
around it, and help us to be determined to work out our differences with an 
unswerving commitment. Help us to respect one another, to talk to one another, 
to compromise with one another and to enjoy one another's company. Help us to 
work at our marriage, to nurture it, to go out together, to occasionally read a 
book to help us renew our marriage and to really understand one another. When 
I do not understand, help me to know this. Do not allow me to think that I 
understand my spouse when I do not. Never let an attitude of contempt or 
sarcasm master me.</blockquote>
<p>[for the single, divorced, separated, or 
widowed]</p>
<blockquote>Lord, I have to admit that sometimes life is lonely. 
Although friends and family enrich my life, sometimes I regret that I am alone. 
At other times, I appreciate it! Help me to appreciate the blessings of the 
single life: time to focus upon You, freedom to come and go as I please and a 
simplicity of life. Help me to participate in church and community. If I am 
left out, help me to become tough to that. Help me dive in and include myself. 
Help me remember that this is usually not intentional. Many couples have no 
idea I want to be included and their minds blind them to pick up on my desire to 
be included. Help me express to them in no uncertain terms that I want to be 
included. Keep me from taking the stance of a victim and help me to celebrate 
the single life.</blockquote>
<p>Lord, I pray for my own town, city, or county. Help those 
of us who know Christ realize that we are to be Your witnesses. Keep me from 
thinking that this is my pastor's job; his job is to train me to be a good 
witness; I am to do the work of sharing Christ. Help me believe and practice 
this reality.</p>
<p>Lord, I pray for the missionaries from my denomination (or 
an evangelical denomination I respect). Father, fill them with Your Holy Spirit 
constantly. Help them walk closely with You and to share the love of Christ 
with heartfelt compassion. Help them walk in harmony with their fellow 
missionaries. Provide for their families and meet their physical, social and 
spiritual needs. Keep them true to Your Word, encourage them and give them 
fruit for their labors and wisdom for their decisions. Protect and direct 
them, I pray. </p>
<p>I surrender myself afresh to You in the Messiah's Name, 
Amen.</p>

