<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 11
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>The Lord Jesus Christ is the Way, the Truth, and the Life. 
He Himself bridges the gap between sinful man and You, a holy God. No church or 
ritual is the way—He is. He is the Truth because He alone is the Messiah. And 
He is the Life; He came to give us the abundant life 
(<a href="javascript:scripture('../../scripture/john1010.htm')" target=_self class=pop>John 10:10</a>) 
and we can be born-again to new life through Him. As God the Son, He is the source of all 
life.</p>
<p>Father, I open myself to You. Help me be honest with You, 
others and myself. There are qualities about me that are not all positive. 
Sometimes I doubt, feel bitter, get depressed, arrogant, lazy, controlling 
perhaps even addicted to my own adrenaline. The room for improvement is a big 
room. Thank You, Lord, that You are not finished with me yet, but You are 
developing me. Thank You that imperfect and flawed people surround me. I am 
not alone. And thank You that, when I see Jesus, I will become perfect. I will 
be what I sense I should and could be.</p>
<p>Remind me that it is not good for me to focus on myself too 
much. I need moderation in all things even introspection. Also remind me that 
the best way to find my life is to lose it in pursuit of You and in service to 
others.</p>
<p>I pray for my lost friends and family members including: __________.
Remind me that unbelievers are not the enemy; they are the victims of the enemy.</p>
<p>Father, please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Today I pray for the Arunchal Pradesh province of India. 
Thank You the Christian churches have grown to 13% of the population and thank 
You that most of those churches are evangelical. Thank You that the persecution 
by the Hindus in the 1970s and 1980s has stopped and thank You that the 
believers there have a vision to reach other parts of India. May the work 
progress, O Lord.</p>
<p>I also pray for the Assam province. Things there have not 
been very positive; only 3% of the population claim to be Christian and that 
percentage has shrunk from 6.4% in 1960. The believers desperately need a work 
of Your Spirit, O Lord. Reverse this negative trend and may the percentage of 
believers double over the next five years. Raise up evangelists and leaders. 
Work in a mighty way, Lord of the Harvest, I ask.</p>
<p>Lord, thank You for Mission Without Borders. &quot;'Reaching 
people for Christ' has been and continues to be [their] heartbeat at Mission 
Without Borders,...an international organization dedicated to serving the 
physical, emotional and spiritual needs of those suffering the effects of 
poverty and/or persecution. Today, the countries of Albania, Bosnia, Bulgaria, 
Moldova, Romania, Ukraine and China are sites of [their] mainstream operation.&quot; 
Their programs include &quot;child rescue international, child care, family care and 
christian outreach.&quot; Father, provide, protect and enlighten the missionary 
workers. Grant them harmony with one another and empower them by Your Spirit.</p>
<p>Empower me to serve You today here at home, I ask in 
Christ's Name, Amen.</p>

