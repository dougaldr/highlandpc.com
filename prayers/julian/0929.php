<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 29
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the great Teacher. Teach me Your way. Give 
me understanding so that I might obey Your precepts. Empower me to do Your 
will, incline my heart to follow You. I cannot motivate myself, for my flesh is 
not compatible with Your holy nature. Your Spirit within me and the new nature 
You have given me—Lord, with this help—my heart can be Yours.</p>
<p>Father, help me to turn my eyes away from that which tempts 
me or distracts me from the priorities You have for my life. I want my longing 
for You and Your Word to grow stronger, not become weaker. I want to tell 
others about You, to have answers to those who ask me questions, to experience 
the freedom of a life aligned with Your Word and a conscience that is clear. I 
want to frequently consider my ways and make adjustments to conform to Your 
will.</p>
<p>Today I pray for criminals who are imprisoned. Lord, many 
people &quot;get religion&quot; while in jail, but soon abandon You once they are 
released. But sometimes, Lord, You turn someone around; they take the time to 
reflect upon the condition of their souls, they come to saving faith and 
continue to walk with You. Lord, work in hearts as You see fit.</p>
<p>I also pray for mixed-up friends and acquaintances. Some 
people seem to vacillate and I sometimes wonder if they are genuine; they seem 
so sincere and convincing at times. Help me to minister to such people, but 
not to entrust myself to them. Help me learn from Jesus’ example 
(<a href="javascript:scripture('../../scripture/john224.htm')" target=_self class=pop>John 2:24</a>).</p>
<p>Lord, help me to be alert to the devil’s schemes. He and 
his demons roam about bent on destruction and postured against Your kingdom. 
Bind Satan, put a hedge around my loved ones and me, and help me to take up Your 
armor today.</p>
<p>Father, I have some personal health concerns including: __________. I also 
pray for the physical, mental, or spiritual health 
of others including: __________. I ask for Your will to be done in each situation.</p>
<p>Thank You, Lord, for Your work in the African nation of 
Uganda. I praise You, Lord, that almost half the population claims to be 
born-again evangelicals and there are other born-again Christians in 
non-evangelical denominations as well. Thank You that the incidents of AIDS 
have been cut in half and that the church is strong. I pray for the yet unreached 
peoples and for the continued maturing of the church. Father, I pray 
that the evangelical movement would not only hold its own, but also grow even 
stronger. I pray that the believers would live out their faith and that You 
would bless with them with spiritual blessings and material blessings. Help the 
economy to grow and make Uganda a lighthouse to the continent, I pray.</p>
<p>Father, thank You for the ministry of LATCOM. &quot;LATCOM 
commits to working in cooperation with other evangelical organizations and 
churches by focusing on training, equipping and motivating their leaders for the 
task of reaching their countries with the gospel. As much as possible, LATCOM 
will seek to develop leaders and organizations that are self-supporting, 
self-governing and self-propagating.&quot; Bless their work throughout Latin 
America. Prosper them, protect them, guide them, grant them harmony and empower 
them by Your Spirit, I ask.</p>
<p>I commit my day to 
You, in Yeshua’s Name, Amen</p>

