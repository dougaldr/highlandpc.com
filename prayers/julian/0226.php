<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 26
<br><span class=byln>by Ed Vasicek</p>

<p>O Holy One,</p>
<p>You are perfect. You have no flaws, quirks, problems, or 
inadequacies. Your are complete and cannot be improved upon. Your attributes 
blend harmoniously. You make no mistakes; You do not need to learn by trial and 
error. No one can add knowledge, skill, or insight to Your perfection, for You 
have all of these to an infinite degree. You are morally perfect as well; You 
do not err in any way.</p>
<p>Lord, You made a covenant with Noah and his descendents 
after the flood. You promised that You would never destroy the entire world 
with a worldwide flood again. You also obligated we humans to practice justice 
in defending human dignity and to promote a solid family structure. Help me to 
remember Your faithfulness the next time I see a rainbow!</p>
<p>Lord, help me become a person who keeps his or her word. 
May my word be my bond, and help me even to honor my mistakes unless doing so 
would be sinful.</p>
<p>Gentle Shepherd, help me to develop good relational 
skills. Help me know how to make people feel relaxed and accepted in my 
presence. Keep me from policing others. </p>
<p>Today, Lord, I pray for the nation of Taiwan. Even though 
the people of Taiwan have more freedom and prosperity than those in Communist 
China, they have not responded to the Gospel as have those on the mainland. 
Only 2.7% of the Taiwanese claim to be born-again, and for decades the church 
has stagnated. Thank You that it is once again beginning to grow. Provide 
solid pastors and leaders for the church, and may Your Spirit bless the Jesus 
film and other outreaches. May His gentle wind blow over Taiwan.</p>
<p>I remember Red Sea Team International as it seeks, &quot;to 
bring the Good News of Jesus Christ to unreached people groups, primarily...within the 10/40 window...to share this Good News through both our words and our 
deeds...to lead lost [people]...into a living relationship with Christ. We seek to 
disciple and teach others...[and]...seek...church planting movements.&quot;&nbsp; Bless 
this important ministry. May Your Spirit empower and prepare the way for their 
efforts, and may You provide for them, protect them, and help them to work 
harmoniously.</p>
<p>I pray for Christian counselors throughout this world. 
Give them wisdom and insight beyond themselves as they help their counselees to 
overcome sin, nurture marriages, overcome the affects of trauma and abuse, or 
seek God's will for their situations.</p>
<p>I pray for my lost friends and family members, including: __________. Father, please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Bless me this day, 
I pray, in Jesus' Name, Amen.</p>

