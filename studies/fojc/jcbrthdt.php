<div>
<p>In 525 A.D., Dionysius, a Scythian monk, was asked to determine the year of Jesus’ 
birth. He did the best job he could. He determined that the Roman year 754 was 
the Christian year “1 A.D.” Although Dionysius’ effort is to be commended, it 
seems he was off a few years. But don’t go too hard on old Dion—even today we 
cannot be sure as to when Christ was born.</p>
<p>We do 
know Jesus was born during the reign of Augustus Caesar, who ruled from 44 B.C. 
to 14 A.D. We also know He was born shortly before the death of Herod the 
Great. We now know that Herod died in the spring of 4 B.C., something Dionysius 
did not know, so Jesus was probably born in early 4 B.C. or late 5 B.C. (Keep 
in mind that there is no year “zero” – after 1 B.C. comes 1 A.D.)</p>
<p>
Figuring out the day of Christ’s birth is even more impossible a task. Since 
Christians never celebrated Christmas until the fourth century, there are no 
early records as to the day of Christ's birth. The authors of the Gospels did 
not consider such a detail important. What mattered is that Jesus came for a 
purpose, to offer Himself as the Lamb of God for our sins and then to rise 
again. The holiday that mattered most—Easter Sunday—was well documented. So 
the earliest suggestion of a day for Christ's birth was made by a church leader, 
Hippolytus (b. 165, d. 235A.D.), who wrote that Jesus was born on December 25<sup>th</sup>. 
Since his comments were made 200 years after the birth of Christ—well, that's a 
big gap without substantiating documentation. The Eastern Church gravitated 
toward January 6<sup>th</sup> as the day of the year not only for Christ’s 
birth, but also the visit of the Magi (a year later), and the date for the 
baptism of Jesus (30 years later).</p>
<p>The 
more probable reason as to why western Christians settled on the December 25<sup>th</sup> 
date was a church scheme to discourage the supposedly “Christianized” Romans 
from celebrating the festival of the “unconquered sun,” a heathen feast 
celebrating the winter solstice, by replacing it with a Christian holiday. 
Another competing heathen festival, Saturnalia, began December 17<sup>th</sup> 
and lasted for days. That festival was marked by gift giving.</p>
<p>The 
truth is that we do not know even the time of year in which Christ was born. 
Since sheep for temple sacrifice were driven through Bethlehem year round, the 
Scriptures do not offer even a hint as to the time of year.</p>
<p>Of 
course what really matters is that God the Son became a man. His humanity was 
miraculously conceived of Mary. (She is rightly called “the mother of Jesus” 
but should not be called “the mother of God”—God has no mother, but has always 
been.) Although the early church rightly put the emphasis on Resurrection 
Sunday (Easter), celebrating our Savior’s birth seems appropriate indeed. It is 
a man-made holiday, not a God-ordained one. Yet I think it is a super idea, 
don't you? The angel host spoke of “great joy,” and the message of God stooping 
down to deliver us still brings joy to our hearts today.</p></div>
  
