<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 10
<br><span class=byln>by Ed Vasicek</p>

<p>My Wonderful Lord,</p>
<p>You are my best Friend, out for my best interest. You are 
Abba, my affectionate Father.</p>
<p>You are my Shepherd, my King, and my Tower of Refuge. But 
You are also my Judge. Thank You Father that as a believer in Jesus Christ, I 
will be judged through Your Son, the Lord Jesus Christ. I will appear before 
the Judgment Seat of Christ 
(<a href="javascript:scripture('../../scripture/2cor510.htm')" target=_self class=pop>2 Corinthians 5:10</a>), 
and all my works will be evaluated for the purpose of determining my rewards 
in Glory. Although I am saved by grace through faith apart from my works 
(<a href="javascript:scripture('../../scripture/eph289.htm')" target=_self class=pop>Ephesians 2:8-9</a>), 
I was saved to glorify God by—among other things—my works 
(<a href="javascript:scripture('../../scripture/eph210.htm')" target=_self class=pop>Ephesians 2:10</a>). 
Help me balance that I have been accepted in Christ with the truth that I am also 
accountable.</p>
<p>Father, I have learned that I cannot succeed in the 
Christian life by sheer will power, trying harder, or working up enthusiasm. It 
takes the daily routine of prayer and Bible study plus being engaged in active 
ministry of some kind. But even more so it takes Your Spirit's work within  
otherwise what I do is forced and contrived. Create in me a willing heart. 
Please continue to work in me. Help me be faithful in the routine of life; keep 
me from going the way of making &quot;decision after decision,&quot; as though there were 
a quick fix for all my areas of weakness. Instead, give me a hunger of You.</p>
<p>Lord, strife within families is awful. Please bring 
together members of my family or of a family I know who are at odds with 
other family members. We ask that the offenders would embrace repentance, 
apology, restitution and taking responsibility. Help them to be reasonable and 
willing to establish credibility over time, if that is what is needed. And we 
pray that those offended would be gracious, yet not naïve. We know some 
situations can never be fixed, but we pray that many relationships would be 
reconciled.</p>
<p>Today I remember the small countries included under the 
term, &quot;Micronesia&quot; including the Federated States of Micronesia, Marshall 
Islands, Northern Marianas and Palau. These small nations are affiliated with 
the U.S. in varying ways and there is a high percentage that professes to be 
born-again evangelical, averaging over 20% per country. Yet many professing 
Christians still practice native superstitions and these nations are weak when 
it comes to sexual morality. Lord, raise up strong leaders from within the church and help 
the Christians there to take deep root and forsake their pagan heritage.</p>
<p>Bless the ministry of Seamen's International Christian 
Association as they distribute literature in various American ports to sea 
merchants and their crews who hail from around the world. Seamen's uses 
literature in a variety of languages and serves as a way to &quot;seed the Gospel&quot; to 
remote areas or closed countries as individuals from around the world dock at 
American ports.</p>
<p>Now, Father, help me carry on responsibly with today's 
challenges, I pray, in Jesus' Name, Amen.</p>

