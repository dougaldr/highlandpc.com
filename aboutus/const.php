<p>
<b>Article IX: Spiritual Gifts</b></p>
<p>
We believe that every genuine believer has at least one spiritual gift and often
several. The Holy Spirit is sovereign and distributes gifts as He chooses.&nbsp;
He is under no obligation to give all types of gifts to every generation of
believers. We do NOT hold to the following:
</p>

  <ol>
    <li>It is always God's will for every believer to be physically healed in
      this life.<br>
    <li>Failure to be healed always indicates a lack of faith.<br>
    <li>Only those speaking in tongues have been baptized and/or filled with the
      Spirit.<br>
    <li>It is God's desire for all believers to speak in tongues.<br>
    <li>The gift of tongues is the most important spiritual gift.
  </ol>
<p>Although we do not endorse tongues as being a spiritual gift given today, we
will accept those who disagree into our membership as long as they do not hold
to the above beliefs and do not promote tongues.<br>
