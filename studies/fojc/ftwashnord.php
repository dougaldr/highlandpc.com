<p>The 
“Imperative Mood” is the grammatical description for the voice of command. The 
New Testament is filled with imperatives, including many uttered by Jesus. Many 
commands were given to individuals, such as, “Sell all you have and give it to 
the poor.” Other commands deal with principles in which a particular is used in 
place of a broad concept. For example, Jesus taught about not seeking honor 
from men (the concept). He commanded His disciples, “…But when you are invited, 
take the lowest place, so that when your host comes, he will say to you, 
‘Friend, move up to a better place’” (<b>Luke 14:10</b>). </p>
<p>We 
understand Jesus is teaching a principle, not a lesson about etiquette. We 
should not be out to exalt our egos. When Jesus said, “Do not store up for 
yourselves treasures on earth…” (<b>Matthew 6:19</b>), most of us do not refuse to open 
bank accounts in light of it. We look for the principle, which is clearly what 
Jesus has in mind (this “hot and cold” technique was a common rabbinic teaching 
method in the first century). Between reading on and noting Jesus’ other 
teachings, we understand He was teaching a matter of priorities, in this case 
culminating in <b>Matthew 6:33</b>, “But seek first the kingdom of God and His 
righteousness, and all these things shall be added to you.”</p>
<p>So 
when we come to the passage about foot-washing, when Jesus commands, “I have set 
you an example that you should do as I have done for you,” we need to ask 
exactly what Jesus intended. In the context, Jesus set the example of menial, 
practical service. In a sandaled culture where foot-washing was a constant need 
(and part of etiquette), it needs to be pointed out Jesus was talking about 
PRACTICAL service for others. Those who believe Jesus was initiating an 
ordinance fail to grasp that Jesus' example was just the opposite. Unlike the 
formalities of Passover (just at hand in this text), Jesus was serving others. 
“Foot-washing” is simply one of many examples of menial but useful service (see 
<a href="javascript:scripture('../../scripture/1tim510.htm')" target=_self  class=pop>1 Timothy 5:10</a>). 
Even the example of a willingness to serve is not without 
exception, but a general concept. If menial service displaces the ministry of 
the Word, as in the conflict between Mary and Martha, then menial service can be 
put off or delegated. See <b>Acts 6</b> where the Apostles designated others to serve 
on tables so they could be free for their ministry. All Christians should be 
willing to serve in menial ways: that is the lesson here.</p>
