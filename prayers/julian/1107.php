<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 7
<br><span class=byln>by Ed Vasicek</p>

<p>My L<span class="sm">ORD</span>,</p>
<p>Yahweh, how I praise You that You are 
self-derived! You had no beginning and You will have no end. You are completely 
uncreated and though You take pleasure in Your creation, You are complete in 
Yourself. With the heavenly host, I praise the One who was, and is, and is to 
come. From the rising of the sun to its setting, You are Lord. Before the sun 
was created, You are Lord. You are Yahweh, the eternal I AM.</p>
<p>I thank You for raising me to life and coaxing me along to 
believe in the Lord Jesus Christ. Thank You for seating me in heavenly places 
along with Christ, far above all principalities and powers. This is my legal 
position, even now. I look forward to the time when I am actually seated with 
You in glory!</p>
<p>Father, since my citizenship is in heaven and I walk this 
earth as one of Your ambassadors, may my life evidence that my heavenly 
citizenship indeed has my first allegiance. Although You expect me to fully 
participate in my civic and community duties, and although I confess I love my 
country, I put the cross before the flag. Help me not to confuse the two.</p>
<p>Lord, I have a bountiful list of personal needs, a few of 
which include: __________. Meet these needs according to the riches I have in 
Christ Jesus. Help me to pray without ceasing and not to become weary in 
well-doing. Remind me that my labor in You is not in vain 
(<a href="javascript:scripture('../../scripture/1cor1558.htm')" target=_self class=pop>1 Corinthians 15:58</a>).</p>
<p>Help me to resist temptation and the lures of Satan today.</p>
<p>I also pray for my lost friends and family members 
including: __________. Father, please grant them the repentance that leads to 
life. Draw them by Your Spirit and bring them into Your Kingdom.</p>
<p>Today I pray for the nation of Argentina and its 40 million 
people. Although this land has a track record for revolution, thank You that the 
nation is now more democratic. Thank You for the great growth of the 
Evangelical church in Argentina; about 11% claim to be born-again evangelicals. 
Thank You for the revival that has been breaking forth over the last decade. 
This has even spread through the prisons. Help the believers there to become 
more Scripturally anchored rather than depending upon their emotions. We pray 
that the Evangelical churches would double in number and size over the next 
decade.</p>
<p>Lord, bless the work of the International Bible 
Society. As they describe it, this agency &quot;translates and reaches out with
the Word of God so that people around the world can meet and 
become disciples of Jesus Christ. Our goal is to use the resources God provides 
to reach as many people as possible with accurate, readable, understandable 
translations of His life-transforming Word.... We currently pursue major outreach 
projects in 65 countries.&quot; Bless and prosper this ministry, I ask.</p>
<p>Bless those who work as machinists. Grant them safety, help 
them to do their work well and may those who know You testify by life and word 
of Your goodness.</p>
<p>Help me to serve You today, I request in Christ’s Name, 
Amen.</p>

