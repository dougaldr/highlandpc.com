<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 11
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Jesus,</p>
<p>Thank You for loving me. Thank You, God the Son, that You 
became man in the Person of Jesus Christ. Even though You, O Son, have always 
existed and have neither beginning nor end, yet, over 2,000 years ago, You took 
upon Yourself human flesh. You did this so You would be the Lamb of God Who 
takes away the sin of the world.</p>
<p>Lord, I thank You for the salvation You have literally 
given me as a free gift. I have done nothing to deserve it. This gift was 
freely bestowed upon me the moment I turned from my sins and believed in the 
Lord Jesus Christ. It is not by my works or efforts that I am forgiven, saved, 
and born-again, but by Your grace. You are the God who justifies the ungodly 
when they turn to You.</p>
<p>Lord, in like manner, when others sin against me and then 
repent, help me to forgive them. May I model my forgiveness after Yours. Let me 
always stand ready to repent and desiring all to come to repentance!</p>
<p>Lord, cleanse me from the sins of which I am conscious. I 
confess them to You now. Restore to me the joy of my salvation. Though my 
salvation cannot be lost, its joy grows or wanes depending upon my walk with 
You.</p>
<p>I pray for my lost friends and family members, including: __________. 
Father, please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Today I pray for the nation of Nicaragua. Thank You that 
this nation has been free from communist control for some time, but there are 
also political tensions and anti-government activities that trouble this land. 
We thank You for those who know Jesus Christ as Lord and Savior, and we pray 
that the evangelical church would multiply over the next decade.</p>
<p>We remember the International Bible Society. Thank You 
that they provide low-cost Bibles in many languages. Thank You for using them 
as part of evangelizing and discipling this world.</p>
<p>Locally, we pray for our city/county treasurer. Help he or 
she to be effective, honest, and a blessing to our government. If he or she 
does not know You, we pray that this would change!</p>
<p>Please go before me today. Open the right doors and close 
the wrong doors, I pray, in Jesus’ Name, Amen.</p>

