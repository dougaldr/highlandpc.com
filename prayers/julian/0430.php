<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 30
<br><span class=byln>by Ed Vasicek</p>

<p>My Father in Heaven,</p>
<p>You are the Living God. You are not some force, nor are 
You dead. You are the source of all life. All who live came about through 
You. Life comes from life, not non-life. And You, the Living One, created all 
mortal beings and all physical things. You are also the author of spiritual 
life. Your image in us means that we have a spiritual side to us. You created 
the angels and You created mankind.&nbsp; In Christ, You have redeemed us and by 
Your Spirit You have regenerated those of us who believe. You have given us the 
abundant life and have spiritually resurrected us. Our Lord Jesus is not only 
the way and the truth, but also the Life. Indeed, He is the resurrection and 
the life.</p>
<p>Lord, help me to seek the best in people and interpret 
things in a positive light unless I have reason to believe otherwise. Remind me 
of the proverb, &quot;He who seeks good finds goodwill, but evil comes to him who 
searches for it&quot; (<b>Proverbs 11:27</b>). Remind me that You look at the heart, but I can 
only see the outward appearance. Help me not to jump to conclusions about the 
motives of others.</p>
<p>Help me to do my part by volunteering to serve or 
continuing to serve in a Bible-believing, Bible-teaching church. May I set my 
roots down deeply and attend faithfully every week if I am able to do so. Help 
me be a team player and to remember that Christianity is not just a private 
faith but requires me to participate in the body of Christ.</p>
<p>Today I remember the nation of Indonesia. About 4% of the 
population claim to be born-again evangelicals, but many Christians are 
untrained and in need of discipleship. Lord, the forces of Islam are seeking to 
hinder the freedom Christians have to share their faith and live the Christian 
life. O, Father, we pray that a spirit of tolerance would grip the land. We 
pray that You would strengthen the prayer movement, use the Jesus film and 
help the church to be in line with the teachings of Your Word. May the number 
of believers double over the next decade and may You raise up strong leaders 
and strong training ministries.</p>
<p>Father, I thank You for World Gospel Mission and their 
&quot;nearly 350 missionaries and support staff serving on six continents and in 
more than 15 countries.&quot; Thank You that they are &quot;committed to making Christ 
known to all people.&quot; Their &quot;ministries include church planting, evangelism, 
discipleship, education, medical care, community health and development, 
agriculture, rescue missions, aviation, literature, etc.&quot;</p>
<p>WGM is also working to train national Christians to take 
the Good News to their own people, leading converts into self-propagating...congregations.&quot; Grant them harmony among one another, Your provision for their 
needs, safety and the power of Your Spirit.</p>
<p>I pray for all those who work in the appliance repair 
field. May they be competent, honest and may those who know You seek to share 
their faith with their clients and their peers. Give them patience or direction 
in dealing with rude clients.</p>
<p>Now I ask You to fill me with Your Spirit today, in Jesus' 
Name, Amen.</p>

