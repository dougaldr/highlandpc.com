<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 15
<br><span class=byln>by Ed Vasicek</p>

<p>Mighty God,</p>
<p>Thank You for Jesus Christ, the Jewish Messiah. He did not 
come to destroy the Law but to fulfill it. Thus Biblical Christianity is 
rooted in the Old Testament. We believe in One God Who created heaven and 
earth. We believe that when the first man fell into sin, all creation was 
cursed. We believe in the promised Messiah Who would be called The Mighty God 
(<a href="javascript:scripture('../../scripture/is96.htm')" target=_self class=pop>Isaiah 9:6</a>), 
Who existed from eternity past 
(<a href="javascript:scripture('../../scripture/mic52.htm')" target=_self class=pop>Micah 5:2</a>), 
Who would be rejected 
by His own people, die as a sacrifice for sin, would die among the wicked but be 
buried in a rich man’s tomb and after dying, see the light of life through His 
resurrection 
(<a href="javascript:scripture('../../scripture/is53.htm')" target=_self class=pop>Isaiah 53</a>). 
His death would justify us and satisfy the Father.</p>
<p>Even though there are false Messiahs being preached and 
false Gospels, the true Gospel has complete continuity with the Old Testament 
Hebrew Scriptures. What I believe can be connected backward to the very 
beginning of creation.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God.</i>).</p>
<p>Lord, help me to be a compassionate listener. Keep me from 
interrupting or offering solutions when another simply wants me to listen. I 
can offer my advice afterward or at a later time if it is wanted. Help me to 
listen to others as an act of Christian love.</p>
<p>Help me to know how to have good clean fun. Help me 
remember that You have given to me all things richly to enjoy, and that there is 
a time to laugh. Yet not all forms of fun are wholesome, so let me use 
discernment and balance.</p>
<p>Lord, today I pray for the Asian nation of Sri Lanka with 
its 19 million people. Thank You that 1.5% of the population claim to be 
born-again evangelicals. Although this is a relatively small percentage, it has 
grown from .36% since 1980, thus the percentage of believers has grown 500%. 
At the same time, liberal Protestantism has greatly declined. Buddhist 
majorities have sometimes persecuted Christians. Father, may such persecution 
stop. O Lord, keep the church growing at the same or higher rate, I pray. May 
those in the unreached people groups be reached, I ask. May the Gospel permeate 
this society and may You grant many the repentance that leads to eternal life.</p>
<p>I pray for Christ For India which “exists to fulfill 
Christ's Great Commission to India by training nationals as pastors and 
evangelists, establishing native churches, providing humanitarian aid, educating 
India's future leadership.” O Lord, provide, protect, empower and grant 
harmony to this ministry. May the workers bear much fruit for Your glory, O 
Jesus.</p>
<p>Bless those who work as architectural and civil drafters. 
May they do their work well, design projects with safety in mind and may those 
who know You testify of Your goodness.</p>
<p>Help me to bless Your Name today, I request in the Name of 
the Savior, Amen.</p>

