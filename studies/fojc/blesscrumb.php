<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>Gentiles Blessed With Crumbs
<br><span class=byln><b>Notes on Matthew 15:21-28, Mark 7:24-30</b>
<br>by Ed Vasicek</span></p>

<p>The Bible presents 
Israel as God&#039;s chosen nation, a nation He has sovereignly chosen to work 
through and a nation to which He will sovereignly keep His promises. In His 
grace, God has included gentiles in His church.</p>
<p>Here are some verses 
that demonstrate the transition in Jesus' ministry—and the ministry of the 
apostles—from focusing solely on the Jews (or gentile converts to Judaism) and 
looking toward ALL people. Today's sermon about the Syro-Phoenician woman is 
early in the process.</p>
<p>
<b>Matthew 10:5-6</b> &quot;These 
twelve Jesus sent out with the following instructions: 'Do not go among the 
Gentiles or enter any town of the Samaritans. Go rather to the lost sheep of 
Israel.'&quot;</p>
<p>
<b>Matthew 12:30-31</b> &quot;He who 
is not with me is against me, and he who does not gather with me scatters. And 
so I tell you, every sin and blasphemy will be forgiven men, but the blasphemy 
against the Spirit will not be forgiven.&quot;</p>
<p>
<b>Matthew 15:23-28</b> 
&quot;Jesus did not answer a word. So his disciples came to him and urged him, 'Send 
her away, for she keeps crying out after us.' He answered, 'I was sent only to 
the lost sheep of Israel.' The woman came and knelt before him. 'Lord, help me!' 
she said. He replied, 'It is not right to take the children's bread and toss it 
to their dogs.' 'Yes, Lord,' she said, 'but even the dogs eat the crumbs that 
fall from their masters' table.' Then Jesus answered, 'Woman, you have great 
faith! Your request is granted.' And her daughter was healed from that very 
hour.&quot;</p>
<p>
<b>John 1:11-12</b> 
&quot;He came to that which was his own, but his own did not receive him. Yet to all 
who received him, to those who believed in his name, he gave the right to become 
children of God.&quot;</p>
<p>
<b>John 10:16</b> &quot;I have 
other sheep that are not of this sheep pen. I must bring them also. They too 
will listen to my voice, and there shall be one flock and one shepherd.&quot; </span>
</p>
<p>
<b>Matthew 28:19-20</b>
&quot;Therefore go and make disciples of all nations, baptizing them in the name of 
the Father and of the Son and of the Holy Spirit, and teaching them to obey 
everything I have commanded you. And surely I am with you always, to the very 
end of the age.&quot;</p>
<p>
<b>Acts 1:8</b> &quot;But 
you will receive power when the Holy Spirit comes on you; and you will be my 
witnesses in Jerusalem, and in all Judea and Samaria, and to the ends of the 
earth.&quot;</p>
<p>
<b>Acts 13:46</b> &quot;Then 
Paul and Barnabas answered them boldly: 'We had to speak the word of God to you 
first. Since you reject it and do not consider yourselves worthy of eternal 
life, we now turn to the Gentiles.'&quot;</span></p>

<p>
<b>Romans 1:16</b> &quot;I am 
not ashamed of the gospel, because it is the power of God for the salvation of 
everyone who believes: first for the Jew, then for the Gentile.&quot;</p>
<p>
<b>Romans 11:17-18</b> 
&quot;If some of the branches have been broken off, and you, though a wild olive 
shoot, have been grafted in among the others and now share in the nourishing sap 
from the olive root, do not boast over those branches. If you do, consider this: 
You do not support the root, but the root supports you.&quot;</p>
