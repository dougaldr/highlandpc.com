	<p>Lord, most people on our planet think that their country is the 
	best or merits Your special attention. We Americans have been this way, 
	and, yet Lord, at times like this we realize how vulnerable we are. We have 
	no guarantee of our future earthly prosperity. We know, O Lord, that our 
	nation has turned away from You. Our sins are very many. When we think of 
	the sin of abortion and the rise of homosexuality, we remember what You did 
	to Sodom and Gomorra. We would not fault You if you annihilated our economy 
	or allowed our enemies to overrun us.</p>
	<p>But, O Lord, You were willing to spare Sodom if ten righteous men 
