<p><b>Part I</b></p>
<p>The four Gospels each 
contain bits of information about the events occurring after the resurrection of 
Jesus Christ. Putting the four accounts together provides us with a detailed 
harmonization. </p>
<ol><li><u><b>Christ rises from 
the dead very early Sunday morning.</b></u> 
A violent earthquake and an angel rolling away the stone and sitting upon it 
accompany this event. The guards are traumatized because they have seen this 
angel, and they freeze 
(<a href="javascript:scripture('../../scripture/matt2824.htm')" target=_self  class=pop>Matthew 28:2-4</a>).</p>
<li><u><b>Mary Magdalene and 
another Mary either walk to the tomb</b></u> 
together or they had planned to meet there. When she (or they) arrives, the 
stone has been rolled away. (The guards are gone by now.) She returns to find 
Peter and John and tells them that someone has moved Jesus’ body 
(<a href="javascript:scripture('../../scripture/john2012.htm')" target=_self  class=pop>John 20:1-2</a>; 
<a href="javascript:scripture('../../scripture/matt281.htm')" target=_self  class=pop>Matthew 28:1</a>).</p>
<li><u><b>Another group of 
women were scheduled to meet the two Marys at the tomb.</b></u> 
They have acquired spices to complete the burial process, which had been 
hurried. They are concerned about finding some men to help roll the stone away, 
since it was large and needed to be rolled against gravity. To their surprise, 
these women see two angels, only one of which speaks, telling them that Jesus 
has been raised. The women are scared to death and leave [seeing angels has 
this effect] 
(<a href="javascript:scripture('../../scripture/mark1628.htm')" target=_self  class=pop>Mark 16:2-8</a>; 
<a href="javascript:scripture('../../scripture/luke2418.htm')" target=_self  class=pop>Luke 24:1-8</a>, 
<a href="javascript:scripture('../../scripture/matt2858.htm')" target=_self  class=pop>Matthew 28:5-8</a>).</p>
<li><u><b>Peter and John 
arrive after being summoned by Mary Magdalene,</b></u> 
who apparently follows them there. John looks into the tomb, but Peter goes 
inside. All he finds are the burial clothes. They return, confused, but Mary 
Magdalene apparently stays at the tomb to grieve that someone has removed Jesus’ body 
(<a href="javascript:scripture('../../scripture/luke2412.htm')" target=_self  class=pop>Luke 24:12</a>, 
<a href="javascript:scripture('../../scripture/john20310.htm')" target=_self  class=pop>John 20:3-10</a>).</p>
<li><u><b>Jesus makes His 
first appearance to Mary Magdalene, after Peter and John have left.</b></u>
At first she supposes Him to be the 
gardener, but she is then overjoyed to realize that it is the Lord 
(<a href="javascript:scripture('../../scripture/john201117.htm')" target=_self  class=pop>John 20:11-17</a>, 
<a href="javascript:scripture('../../scripture/mark169.htm')" target=_self  class=pop>
Mark 16:9</a>).</p>
<li><u><b>Jesus then appears 
to these other women</b></u> who had left before Peter and John had arrived. 
These are the women who saw the angels. Jesus tells them to communicate that 
the disciples were to prepare to travel to Galilee 
(<a href="javascript:scripture('../../scripture/matt28910.htm')" target=_self  class=pop>Matthew 28:9-10</a>).</p>
<li><u><b>The women, joined 
by Mary Magdalene, report their meeting with Jesus to the disciples,</b></u> 
but they write it off as nonsense 
(<a href="javascript:scripture('../../scripture/mark161011.htm')" target=_self  class=pop>Mark 16:10-11</a>, 
<a href="javascript:scripture('../../scripture/luke24911.htm')" target=_self  class=pop>Luke 24:9-11</a>, 
<a href="javascript:scripture('../../scripture/john2018.htm')" target=_self  class=pop>John 20:18</a>).</p>
<li><u><b>The Roman guards 
report what they had witnessed to the chief priests.</b></u>
They were bribed to say that someone 
stole the body of Jesus while they were asleep. The priests promised the 
soldiers protection from military discipline through their clout 
(<a href="javascript:scripture('../../scripture/matt281115.htm')" target=_self  class=pop>Matthew 28:11-15</a>).</ol>
</span>
<p><b>Part II</b></p>
<p>Last week, we noted that the 
four Gospels each contain segments of information that, when put together, 
provide us with a detailed account of the events occurring after the 
resurrection of Jesus Christ. Combining the summary statements with the 
specific details provides us with a more detailed harmonization. We looked at 
the first eight events last week. Here are some more.</p>
<ol type=1 start=9><li><u><b>Jesus appears to 
Cleopas and his friend</b></u> 
(<a href="javascript:scripture('../../scripture/luke241322.htm')" target=_self  class=pop>Luke 24: 13-22</a>, 
<a href="javascript:scripture('../../scripture/mark161213.htm')" target=_self  class=pop>Mark 16:12-13</a>). 
This is on the afternoon of that first Easter Sunday as these two disciples were 
traveling toward Emmaus.</p>
<li><u><b>These two 
disciples make a report to the eleven apostles that evening</b></u> 
(<a href="javascript:scripture('../../scripture/mark1613.htm')" target=_self  class=pop>Mark 16:13</a>, 
<a href="javascript:scripture('../../scripture/luke243335.htm')" target=_self  class=pop>Luke 24:33-35</a>).</p>
<li><u><b>He appeared to the 
ten apostles,</b></u> 
(<a href="javascript:scripture('../../scripture/mark1614.htm')" target=_self  class=pop>Mark 16:14</a>,
<a href="javascript:scripture('../../scripture/luke243643.htm')" target=_self  class=pop>
Luke 24:36-43</a>, 
<a href="javascript:scripture('../../scripture/john201925.htm')" target=_self  class=pop>John 20:19-25</a>) either while Cleopas and his friend were still 
there or afterward. The eleven apostles are discussing this claim during the 
evening of that same Easter day. They had the doors locked, fearful that the 
Jews might plot against them and arrest them.</p>
<p>Jesus appears to the ten 
apostles (Thomas is absent), but they presume He is a ghost. He encourages them 
to look at His hands, feet, and side. He insists that they touch them. But 
this still does not convince the apostles that He is really risen. He eats with 
them, and the reality of the resurrection registers with them. They are 
overjoyed. Jesus breathes the Holy Spirit upon them.</p>
<li><u><b>A week later, on a 
Sunday, Jesus appears to the disciples with Thomas present. Doubting Thomas 
becomes convinced</b></u> 
(<a href="javascript:scripture('../../scripture/john202629.htm')" target=_self  class=pop>John 20:26-29</a>).</p>
<li><u><b>About two weeks 
later, Jesus appears to the disciples while fishing and eats with them. Jesus 
gives Peter a chance to “undo” his three denials by three affirmations of his 
love for Jesus</b></u> 
(<b>John 21</b>).</ol>
<p>There are several other 
appearances of Jesus mentioned in 
Scripture, including His appearance to a large crowd of over 500 
(<a href="javascript:scripture('../../scripture/1cor156.htm')" target=_self  class=pop>1 Corinthians 15:6</a>). 
He appeared individually to His brother James 
(<a href="javascript:scripture('../../scripture/1cor155.htm')" target=_self  class=pop>1 Corinthians 15:5</a>) 
and Peter. He appeared to Paul after the ascension 
(<a href="javascript:scripture('../../scripture/1cor158.htm')" target=_self  class=pop>1 Corinthians 15:8</a>). 
He also appeared to present the Great Commission 
(<a href="javascript:scripture('../../scripture/matt281920.htm')" target=_self  class=pop>Matthew 28:19-20</a>) 
and then during His ascension from the Mount of Olives 
(<a href="javascript:scripture('../../scripture/acts1611.htm')" target=_self  class=pop>Acts 1:6-11</a>). 
As a matter of fact, 
<b>Acts 1:3</b> summarizes as follows: <b>“After his suffering, he 
showed himself to these men and gave many convincing proofs that he was alive. 
He appeared to them over a period of forty days and spoke about the Kingdom of 
God.”</b></p>
<p><b>Why does Jesus eat with 
the disciples on several occasions? Is He hungry? No. The reason Christ eats 
with them is twofold: first, to convince them that He is physical. They are not 
seeing some ghost or apparition. Secondly, food is often associated with 
fellowship, like it or not. In almost all cultures, there is something special 
about eating a meal together.</span></b></p>

