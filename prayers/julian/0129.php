<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 29
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Yahweh,</p>
<p>You are the immortal God. You can never die, never be 
harmed, and never be vulnerable. Yet, at the same time, You know the sensation 
of death, being harmed, and being vulnerable. Because You, O God the Son, took 
on human flesh, and because Your deity was and is united to Your humanity, there 
is a sense in which we are saved by God's blood 
(<a href="javascript:scripture('../../scripture/acts2028.htm')" target=_self class=pop>Acts 20:28</a>). 
Nonetheless, as 
deity, You have never died, nor can You. All the great leaders of earth have 
been mortal; we know no one like You. But then again, we are not surprised.</p>
<p>Today I thank You that my standing before You is perfect, 
because You see me in Christ. When You look at me, You see not my own 
righteousness—which is like filthy rags; no, You see the righteousness of 
Christ which has legally been given me the moment I received Him. In daily life 
my state—what I am practically—is far from perfect, as anyone close to me 
can testify. Lord, help my state more closely align with my standing!</p>
<p>Today I ask You to watch me as I travel. This is a 
dangerous world, and I pray for safety. May my vehicle(s) perform well, and 
watch over me, I pray.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>I pray for the small European nation of Belgium with its 
varied languages and people groups. Sadly, secular humanism has swept over this 
land, as it has most of Europe.&nbsp; Help the small evangelical population of just 
better than 1 percent to be bold, wise, and shine their lights. May Your 
Spirit hover over this land, drawing many to Yourself. We pray that, beginning 
this year, the church would experience unprecedented growth.</p>
<p>Today I pray for Cadence International, a ministry seeking 
to reach and disciple our military. We know, Lord, that many strong Christians 
become that way during their young adult years. Help this ministry to make a 
profound impact upon many whom, in turn, impact many others.</p>
<p>Father, I remember home healthcare nurses today.&nbsp; They 
make it possible for elderly folks to avoid nursing home life. There is no 
place like home, and home becomes more precious when we sense we are near to 
losing our independence. Help these professionals, give them wisdom, and may 
they feel appreciated.</p>
<p>Shine Your light upon me today and help me reflect it to 
others, I pray, in Jesus' Name, Amen.</p>

