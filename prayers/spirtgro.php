<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Personal Devotions</span>
<br>A Prayer for Spiritual Growth
<br><span class=byln>by Ed Vasicek</span></p>

<p>Gracious and merciful God,</p>
<p>How I praise You for saving me and delivering me from the 
Kingdom of Darkness and translating me into the Kingdom of Light. But 
sometimes, Lord, I feel as though I were in some sort of twilight kingdom….not 
exactly in the darkness, but not exactly basking in the light of Your Son, 
either. I can see well enough to keep from bumping into things as though I had 
a few lumens of candlelight, but that’s about it.</p>
<p>Yet, Lord, You want me to experience Your bright and 
satisfying light. Like the psalmist of old, I pray, L<span class="sm">ORD</span>, 
for an undivided heart 
(<a href="javascript:scripture('../scripture/ps8611.htm')" target=_self class=pop>Psalm 86:11</a>). 
I want to serve You, but my desire is weak and fickle. At 
times, I want You to be among the gods of my life, not the only 
L<span class="sm">ORD</span> and God. At other times, I really do not care that 
much. Sometimes it hardly bothers me that You are not first in my life. I am 
distracted with pressing matters, and You can wait.</p>
<p>The truth is that You can wait and You do wait, but I lose 
so much when You are less than God in my life. Our relationship could be so 
much stronger, and how much more blessed I would be. How I could glorify You 
more than I do!</p>
<p>I want to grow out of this compromised state and experience 
the freedom of being intent on one purpose, glorifying and pleasing You. I want 
to seek first Your Kingdom and Your Righteousness and enjoy the blessings of 
life with You as Lord of my life in practice, not name only.</p>
<p>So, Lord, spur me on to growth. I don’t need to make more 
decisions. I can’t keep track of them all as it is. I want to enter into 
training, into a regiment, a holy routine that helps me to grow steadily. 
Slowly, perhaps, but steadily. Keep me from looking for quick fixes or novel 
experiences. Help me to know the difference between adrenaline in my 
bloodstream and the working of Your Spirit. Help me see the power of 
faithfulness and reject the temporary enthusiasm of more “decisions.”</p>
<p>May I cease looking for that “one missing part” or a 
“spiritual secret.”&nbsp; May I realize that there are many missing parts, but 
walking with You on a daily basis is where the growth comes from.</p>
<p>I must choose daily to take up my cross: I cannot set up a 
system that will force me to grow against my own daily will.</p>
<p>So, Father, help me connect to You in a better way. Help 
me spend time in Your Word. Help me connect to a good Sunday School class or 
Bible Study. May I attend a Bible-oriented church faithfully and enter the 
building with a heart that craves to learn. Help me to commit myself to get 
involved and serve in a ministry; I know a body of water that only receives but 
does not move water along becomes stagnant. </p>
<p>Help me develop a consistent, even if short, prayer life. 
Help me seek opportunities to share Christ with others. Help me read up on 
foreign missions and pray for those I know who have been thusly called. </p>
<p>Renew my enthusiasm for You as I seek not new experiences, 
but new routines that will nurture my faith and my walk with You. Help me 
realize in practice what I am legally before You: a true citizen of the Kingdom 
of Light, the Kingdom of Your dear Son.</p>
<p>For I pray in Jesus’ Name,<br>
Amen.</p>

