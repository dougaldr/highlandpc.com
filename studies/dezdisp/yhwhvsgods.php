<div>
<p>A pastor named Joe McKeever wrote:</p>
<blockquote><p>Recently, while the residents of Charlotte, N.C., were trying to
decide whether to name a new park after their most famous son, Billy
Graham, the city&#8217;s newspaper ran some of the mail they have received on
the
subject. Most writers felt this is a fitting tribute to the
world-famous
evangelist; some didn&#8217;t want to go that far; but the final letter
pulled me up
short.</p>
  <p>"Graham and his God don&#8217;t deserve honor," wrote Paul
Jamison. "He promotes a God who gets heinously violent (plagues,
floods, a
bloody sacrifice of his Son, threatens to destroy the earth and finally
a
torturous eternal punishment) for nonbelievers. So I say no to naming a
peaceful city park after a believer of (sic) this raging God."</p>
  <p>It probably does us church folk good to hear such from outsiders 
	occasionally. It’s like a glass of ice water in the face....</p>
  <p>I&#8217;m always fascinated when I hear people accusing
Bible-believers of concocting these stories. These are not
made-for-our-comfort
bedtime lullabies. They are in-your-face true accounts of the dealings
of
Almighty God with humankind. No one would voluntarily create such a
difficult
image of God. The gods earthlings manufacture tend to be the kind we
can manipulate
for our purposes. But this God will not be manipulated. One who knows
Him well
put it like this: "Our God is in the heavens; He does whatever He
pleases" (<b>Psalm 115:3</b>). (Pulpit Helps)</p>
</blockquote>
<p>It is true that false gods are generally gods people
believe they can manipulate.</p>
<p>But Yahweh, the L<span class=sm>ORD</span>, is the true God whether we find
Him repulsive or not. And no amount of anger or denial is going to
change
reality. The God of the Bible cannot be controlled by us.</p>
<p><b>Main Idea:</b> <u>False
religion is often an attempt to control the fears and uncertainties of
life. But whether we approve of the way He runs the universe or not, only
Yahweh is
Sovereign.</u></p>
<ol type=I><li><p><b>Introduction to the Plagues.</b></p>
<p><i>Where we have come from and
are going in this series so far.</i></p>
<ol type=A><li><p>Why these plagues were different.</p>
    <p>
John J. Davis, in his book Moses and the Gods of Egypt suggest 5
distinctives of these plagues.</p>
<ol type=1><li><p>Intensification: known plagues but worse
than ever.</p>
<li><p>Prediction: Moses
predicted the moment of arrival and departure.</p>
<li><p>Discrimination: distinction between the Hebrews
and the Egyptians in the 4<sup>th</sup>, 5<sup>th</sup>, 7<sup>th</sup>,
9<sup>th</sup>,
and 10<sup>th</sup> plagues (v. 
<a href="javascript:scripture('../../scripture/ex823etal.htm')" target=_self class=pop>8:23; 9:4, 6, 26; 10:23; 11:7</a>).
			<li><p>Orderliness (a progression of judgment in the plagues).</li>
			<li><p>Moral purpose (intended to teach a lesson).<p>Could have been
natural phenomenon specially controlled (like Jesus quelling the storm).</p></ol>
	<li>
	<p>The gods of Egypt.</p>
	<ol type=1>
		<li>
		<p>Well over 80 gods,
including the Pharaoh. </li>
		<li>
		<p><b>Exodus 12:12</b>, &quot;On that
same night I will pass through Egypt and strike down every
firstborn--both men and animals--and I will bring judgment on all the
gods of Egypt. I am the L<span class=sm>ORD</span>.&quot; </li>
		<li>
		<p>Why so many
gods? Superstitions? Life is scary, and we want to get some sense of control. 
<i>False religion is usually about controlling
God, or God management.</i>&nbsp;</li>
	</ol>
	<li>
	<p>The pattern of the 10 plagues.</p>
	<ol type=1>
		<li>
		<p>The first nine and
then the grand finale, the 10<sup>th.</sup></li>
		<li>
		<p>The first three: Aaron
lifts up Moses rod. </li>
		<li>
		<p>The middle three: God
begins plague without Aaron or Moses. </li>
		<li>
		<p>The last three: Moses
lifts up his hands. </li>
	</ol>
	<li>
	<p>Perhaps over a period of eight months (clues within the text).</p>
</ol>
<li><p><b>The First Series of Plagues.</span></b></b></p>
<ol type=A><li><p>Water turned to blood 
(<a href="javascript:scripture('../../scripture/ex71424.htm')" target=_self class=pop>7:14-24</a>).</p>
    <p>
