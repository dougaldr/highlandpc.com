<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 15
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Father,</p>
<p>Your Word refers to Your Son as the “Chief Shepherd” 
(<a href="javascript:scripture('../../scripture/1pete54.htm')" target=_self class=pop>1 Peter 5:4</a>). 
And that He is. Although we may have pastors (shepherds) who try to nurture our faith 
and train us, they are all accountable to the Chief Shepherd and—if true to 
the Word—are His servants. While it is very important for us to respect them, 
we do more than respect our Lord Jesus Christ: we worship Him as the incarnation 
of God Almighty! Thank You that He watches over our souls; keep us from 
resisting Him and fighting His leading. He speaks to us by His Spirit through 
His Word; help us pay attention and seek His direction. When He corrects us 
with His rod, it is not pleasant; help us remember that He is out for our good. 
He is not just our Shepherd, He is also our Chief.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now, and I 
ask Your Spirit to search my heart. (<i>Pause to admit them to God.</i>)</p>
<p>Father, when I disagree with someone, help me disagree 
agreeably. Let others see that I have found security in You by how I disagree. 
If my ego, my self-esteem, or confidence is not on the line, others will see 
it. If on the other hand I view a disagreement as a personal attack, others 
will see that I do not sense my security in You. So let me be mature, kind but 
firm, concerned about truth and respecting the rights of others. 
Let  me allow them to determine whether I am presenting truth that is convincing or not.</p>
<p>Today I pray for the tiny Latin American island nation of 
Montserrat. Thank You that more than 27% of the population claims to be 
born-again evangelicals, while other born-gain people attend non-evangelical 
churches in addition. Keep the church strong and help this tiny nation recover 
from devastation caused by severe volcanic eruptions.</p>
<p>Thank You, Father, for Athletes in Action, founded to 
“capitalize on society's great love of sports and utilize the platform given to 
athletes to reach the world for Jesus Christ. Included in the ministry efforts 
of AIA are ministry to college and professional athletes and coaches. 
International competing teams partner with in-country staff and other mission 
organizations to resource ministry around the world. AIA's media tools and 
materials expose millions of sports fans to the Gospel each year.” Father, 
grant harmony to the staff and volunteers, empower them by Your Spirit, provide 
for and protect them and may they see more fruit every year!</p>
<p>Lord, I pray for subway, streetcar, and other rail 
transportation workers. Give them safety, help them serve their passengers 
well and may many come to know You.</p>
<p>Now use me today to bear witness to Jesus Christ, I ask in 
His wonderful Name, Amen.</p>

