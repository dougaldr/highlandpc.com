<li><p><b>Lessons for Us</span>.</b></p>
<ol type=A><li><p>God's holiness should floor us.</p>
<p>When Peter comprehended the holiness of Jesus after a 
miraculous catch of fish, his response was, &quot;Get away from me, Lord, for I am a 
sinful man.&quot;</p>
<li><p>God invites us to join in what
He is doing.</p>
<p>The first time around, Moses tried to set the agenda and he invited God 
along; the second time around, God set the agenda and invited Moses along.</p>
<li><p>When God calls, He enables.</p>
<p>None of us feel “worthy” or 
“adequate”.</p>
<li><p>Excuses cannot displace truth.</p>
<p>Moses had retired; he was 80 years 
old, part of the Over the Hill gang, and content to be a shepherd; he had no 
heart for any more adventures.</p>
<li><p>God is a
bargainer who can be patient.</p>
<p>On the one hand, God can get by 
without us; on the other hand, he is intensely interested in developing every 
one of us. We can always say, &quot;God, I messed up. But what do You want me to do 
from this point on&quot; or &quot;God, I am fully surrendered to You; but use me as You 
can.&quot;</p></ol></ol>
<p><b>Conclusion</b></p>
<p>All of God's servants 
are human beings with quirks and hidden agendas. God often uses us not because 
of who we are, but in spite of who we are.</p>
<p>So if you are not the Christian you should be, join the 
club. What matters is that we are growing toward becoming the Christian we 
should be, and we are serving.</p>
