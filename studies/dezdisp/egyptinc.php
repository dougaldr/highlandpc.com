<ol type=A><li><p>God is patient, but His patience has limits.</p>
	<ol type=1><li><p><b>Genesis 15:13-16</b> reads:  &quot;Then the L<span class="sm">ORD</span> said to him, 
	'Know for certain that your descendants will be strangers in a country not 
	their own, and they will be enslaved and mistreated four hundred years. But 
	I will punish the nation they serve as slaves, and afterward they will come 
	out with great possessions. You, however, will go to your fathers in peace 
	and be buried at a good old age. In the fourth generation your descendants 
	will come back here<i>, for the sin of the Amorites has not yet reached its 
	full measure</i>.'&quot;</li>
	<li><p>In <b>1 Corinthians 10:22</b>, Paul asks, &quot;Are we trying to 
	arouse the Lord's jealousy? Are we stronger than he?&quot;  </li>
	<li><p>Christians can provoke God's wrath and discipline, 
	just as the Canaanites did.</li>
	<li><p>God can remove His blessing from us and give it to 
	others; we should not presume that God's hand of blessing will remain upon 
	us. Those whom He loves, He disciplines.</li>
	<li><p>I know many former pastors, former missionaries, former elders, former 
	Sunday School teachers who are on the shelf.</li>
</ol>
<li><p>God is working behind the scenes.</p>
	<ol type=1><li><p>God set Joseph up to deliver his nation from famine 
	and incubate them into a great nation of 2 million.</li>
	<li><p>God so worked in Joseph's life that even lost people 
	acknowledged God's presence in him.</li>
	<li><p>Do other people see God in us?</li>
</ol>
<li><p>How God works things out for good is very complex.</p>
	<ol type=1><li><p><b>Genesis 50:20</b>, &quot;<b><span style="font-size: 10.0pt; font-family: Verdana-Bold">
	</span></b>You intended to harm me, but God intended it for good to 
	accomplish what is now being done, the saving of many lives.&quot;</li>
	<li><p>If you could go back in time and try to change world 
	history, you would probably make things worse. Sometimes bad things, as bad 
	as they are, prevent worse things.</li>
	<li><p>Suppose you knew September 11<sup>th</sup> was going 
	to occur. If it was stopped, maybe a worse terrorist act would have 
	occurred.</li>
	<li><p>Your alarm fails to go off, you are late for work, but if you had gone 
	on time, you may have had a horrible accident.</li>
</ol>
<li><p>We can either join God in His plan or resist Him.</p>
