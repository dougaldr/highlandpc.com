<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 22
<br><span class=byln>by Ed Vasicek</p>

<p>Great One,</p>
<p>Your arm is mighty 
(<a href="javascript:scripture('../../scripture/ps7715.htm')" target=_self class=pop>Psalm 77:15</a>). 
Although, as God, You 
are a Spirit and You have no physical body per se (although God the Son is now 
joined to a body in the Person of Jesus Christ), Your &quot;arm&quot; refers to Your action 
and strength. The picture is that Your sleeve is rolled up and Your mighty 
muscles exposed. Although You have neither sleeves nor arms nor muscles, we 
can relate to this illustration. Thank You for condescending to what we find 
familiar so that we can better understand You.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. Sometimes they get me depressed or worried. They rob me of the 
joy You want me to experience. I give them to You and ask You to help me 
constantly do so. Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You.</p>
<p>Father, You are sovereign over world events. Lord, I ask 
that You would grant us a time of peace, safety and freedom, if this is Your 
will. I know that near the end things will get worse and worse and that 
there will be no ultimate peace until the Prince of Peace returns to reign. I 
also pray for the peace of Jerusalem 
(<a href="javascript:scripture('../../scripture/ps1226.htm')" target=_self class=pop>Psalm 122:6</a>).</p>
<p>Lord, help me to be friendly and sociable. May I make the 
effort to know my neighbors or folks I see everyday. Help me to be a team 
player within my church, neighborhood and community. If I am to love my 
neighbor and others, I need to get to know them! Help me overcome social 
anxieties and may others enjoy my company because Christ has brought me joy.</p>
<p>I pray for the European nation of the Netherlands. Thank 
You that 4.5% of the population claims to be born-again evangelical. In a way, 
this is sad because the Dutch nation was at one time one of the most 
evangelical nations of the world. Although most of the mainline churches have 
adopted liberalism and left the straightforward faith of the Bible, independent 
evangelical churches are growing. I praise You for that growth. Thank You that 
outreach ministries aimed at teens and young adults have been successful. Raise 
up a new and invigorated generation of believers. Use them, we pray, to make a 
big difference. May the number of evangelical Christians double over the next 
decade, I ask.</p>
<p>Lord, bless Engineering Ministries International and its 
mission &quot;to mobilize Christian design professionals to serve the poor in 
developing countries—[they] proclaim the Good News of Jesus Christ by 
empowering people to transform their world—through the design and development 
of hospitals, schools, orphanages, bridges, water supplies, wastewater 
facilities and more. [They] commit to provide excellent service to our clients, 
our volunteers, and to people in need.... EMI mobilizes both experienced design 
professionals and college students and recent graduates&quot; for short or medium 
term ministry. Use these engineers to lead people to Christ, testify to the 
grace and compassion of God and to show the love of Christ, I pray.</p>
<p>Use me to show compassion today, I ask in the Master's 
Name, Amen.</p>

