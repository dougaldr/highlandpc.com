<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 26
<br><span class=byln>by Ed Vasicek</span></p>

<p>My wonderful God,</p>
<p>I know that You are a God of joy. You bring joy to those 
Who know You, and You are the source of joy, happiness, and contentment. Your 
Word commands me to rejoice in You because You are a happy God. We will 
celebrate the wedding feast of the Lamb with You, and at Your right hand are 
pleasures forevermore. You are the only source of lasting joy. The world and 
the devil offer their temporary versions, but their counterfeit joy will one day 
turn into mourning. My joy will grow greater and last without expiration.</p>
<p>Father, thank You for adopting me—of all people—into 
Your family! You have given me Your name and all the rights that accompany this 
adoption. I will share eternity as a companion and brother of our Lord Jesus 
Christ. Although He is the unique Son of God, something I will never be, I will 
still be, nonetheless, family!</p>
<p>Lord, help me to be a social person today, to enjoy 
relating to others. You want me to love others, but I cannot love them if I do 
not participate socially with them in some way. Help me to converse in a way 
that pleases you. May I make others feel comfortable by my presence.</p>
<p>I have many personal needs, including the need for good 
health. Please bless me this way, I pray.</p>
<p>I pray for my lost friends and family members, including: __________.
Father, please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Today I pray for the nation of Bahrain, not far from Saudi 
Arabia. Although Bahrain has a mixture of religious groups, only a bit more 
than 3% are born again Christians. Particularly bless the Christian radio and 
literature outreaches. I pray that the number of believers would double within 
the next few years. Pour your Spirit out upon this nation with a population of 
about 1.3 million.</p>
<p>I remember the radio and literature ministry of Back to the 
Bible. Thank You, Lord, for this Scripturally-based teaching and outreach 
ministry. We know that these broadcasts have had an impact in helping to 
disciple many of your children in solid doctrine. Provide for and guide this 
ministry, I pray.</p>
<p>We remember nurses today in prayer. They have a stressful 
job, but we so appreciate them when we are recovering from surgeries or 
illnesses. Bless them, and may many come to know You.</p>
<p>Help me stand firm 
for You and Your will in my life today, I pray, in Jesus' Name, Amen.</p>

