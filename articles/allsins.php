<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Are All Sins Alike?
<br><span class=byln>by Ed Vasicek</span></p>

<p>Many leading Christians believe that all sins are alike and 
that it is wrong for us to make distinctions between them.&nbsp;The only problem 
with this belief is that it is totally and completely contrary to the teaching 
of both the Old and New Testaments.</p>
<p>What is true is that even the most minor sin makes us 
sinners, and because of this we cannot relate to God on the basis of the Law, 
which is a complete unit.&nbsp;This is the point of <b>James 2:10</b>, &quot;For whoever keeps 
the whole law and yet stumbles at just one point is guilty of breaking all of 
it.&quot;&nbsp;Like a pane of glass, one break and the whole pane is ruined.</p>
<p>I do not believe all sins are alike. Let me prove my point 
by presenting several convincing arguments that show that God does not count all 
sins as the same.</p>
<ol>
<li><b>Jesus taught about the lighter and weightier aspects 
of the Law.</b></p>
<p>In <b>Matthew 23:23</b>, Jesus argues that the Pharisees should 
have obeyed all the Law, but that some aspects of the Law bore more weight than 
others: &quot;Woe to you, teachers of the law and Pharisees, you hypocrites! You give 
a tenth of your spices—mint, dill and cummin. But you have neglected the more 
important matters of the law—justice, mercy and faithfulness. You should have 
practiced the latter, without neglecting the former.&quot;</p>
<p>Although sin is either failing to obey or disobeying, Jesus 
considers disobeying in matters of justice, mercy, and faithfulness more serious 
than disobeying in matters of tithing. This verse, in itself, proves my point.&nbsp;But I have much, much more to offer.</p>
<li><b>The Bible mentions sins that stand apart as more evil 
than others.&nbsp; </b></p>
<p>Moses spoke to Aaron after he made the golden calf in 
<b>Exodus 32:21</b>, &quot;What did these people do to you, that you led them into such 
great sin?&quot;&nbsp;Notice that the sin of idolatry was called &quot;such a GREAT sin.&quot;&nbsp;The 
term great implies that this sin was weightier than many other sins.</p>
<p>Note <b>Numbers 15:30-31</b>, &quot;But anyone who sins defiantly, 
whether native-born or alien, blasphemes the LORD, and that person must be cut 
off from his people.&nbsp;Because he has despised the LORD's word and broken his 
commands, that person must surely be cut off; his guilt remains on him.&quot;&nbsp; </p>
<p>Do you see it? God distinguish between sins of weakness and 
defiant sins! </p>
<p><b>1 John 5:16</b> says, &quot;If anyone sees his brother commit a sin 
that does not lead to death, he should pray and God will give him life. I refer 
to those whose sin does not lead to death. There is a sin that leads to death. I 
am not saying that he should pray about that.&nbsp;All wrongdoing is sin, and there 
is sin that does not lead to death.&quot;</p>
<p>John here makes a distinction between all wrongdoing, which 
is sin, and a sin so serious it leads to death (damnation); believers need not 
even pray for one committing such a sin.&nbsp;(This is THE sin of <b>First John</b>, 
denying the faith after embracing it, a sin no truly saved person can 
commit—<a href="javascript:scripture('../scripture/1john39.htm')" target=_self class=pop>3:9</a> 
and <a href="javascript:scripture('../scripture/1john21920.htm')" target=_self class=pop>2:19-20</a>.)</p>
<p>I believe this is the same as <b>Matthew 12:32</b>, &quot;Anyone who 
speaks a word against the Son of Man will be forgiven, but anyone who speaks 
against the Holy Spirit will not be forgiven, either in this age or in the age 
to come.&quot;</p>
<li><b>Some sins provoke God’s wrath more than others.</b></p>
<p>Why did God drive out the Canaanites?&nbsp;Because they were 
sinners? Well, we are all sinners.&nbsp;No, he drove out the Canaanites because 
they were sinning in ways that most offended and angered God. </p>
<p><b>Deuteronomy 9:4b-5a</b> reads, &quot;...No, it is on account of the 
wickedness of these nations that the LORD is going to drive them out before 
you. It is not because of your righteousness or your integrity that you are 
going in to take possession of their land; <b><u>but on account of the 
wickedness</u></b> of these nations…&quot;</p>
<li><b>Some sins demand church discipline; others do not.</b></p>
<p>Lesser sins are called &quot;stumblings.&quot;&nbsp;<b>James 3:2a</b>, “We all 
stumble in many ways.”</p>
<p>Other sins, especially sexual ones, though not as great as 
denying the faith, do require church discipline.&nbsp;<b>1 Corinthians 5:9-11</b> reads, &quot;I have 
written you in my letter not to associate with sexually immoral people—not at 
all meaning the people of this world who are immoral, or the greedy and 
swindlers, or idolaters. In that case you would have to leave this world. But 
now I am writing you that you must not associate with anyone who calls himself a 
brother but is sexually immoral or greedy, an idolater or a slanderer, a 
drunkard or a swindler. With such a man do not even eat.&quot;</p>
<p>In Old Testament times, the Ten Commandments (<b>Exodus 20</b>), 
when violated, resulted in differing penalties.&nbsp;A thief had to return the item 
with interest; an adulterer was to be stoned; there was no real penalty for 
coveting, but idolatry brought death AND a curse to the 4<sup>th</sup> and 5<sup>th</sup> 
generation.</p>
<p>Do you think God rained fire and brimstone on Sodom and 
Gomorrah because they were impatient (a sin) or they cheated on their taxes? </p>
<li><b>Some sins disqualify one from leadership in the 
church; others do not.</b></p>
<p>In 
<a href="javascript:scripture('../scripture/1tim3113.htm')" target=_self class=pop>1 Timothy 3</a>,
there are a number of qualifications for 
elders, deacons, and deaconesses.&nbsp;Since all elders, deacons, and deaconesses 
are sinners, if all sins are alike, it would not matter what sort of backgrounds 
they had.&nbsp;But the Scriptures specifically disqualify a man who practices 
dishonesty from being an elder, for example.&nbsp;Paul does NOT say, &quot;Well, we all 
still sin, so it doesn’t really matter how an elder sins.&nbsp;After all, all sins 
are alike.&quot;</p>
<li><b>Even Christians who believe all sins are alike do not 
live out this belief, because wisdom says all sins are not alike.</b></p>
<p>If you believe all sins were alike, would you free 
murderers to roam the streets or imprison those who drove 60 mph in a 55 zone?&nbsp;Both are lawbreakers.&nbsp;Are those sins really equal?</p>
</ol>
<p>In summary, it is not God’s will for us to sin in ANY way.&nbsp;We should not ignore lesser sins and pat ourselves on the back, content we are 
not committing greater ones.&nbsp;But that does not mean we should ignore the 
obvious: all sins are not alike.</p>


