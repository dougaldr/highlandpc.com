<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 28
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>You are good. You have redeemed me and I am proud 
to let others know that. You have satisfied my thirsty soul and You have given 
me of Your Spirit Who produces a never-ending fountain of living waters.</p>
<p>Lord, the frightening times of life remind us how puny and 
vulnerable we are. Near brushes in car accidents and a host of potential dangers 
sometimes alert us to our precarious position. At times like that, we remember 
how badly we need Your hand of safety to protect us. O Lord, please send angels 
to watch over us and keep us from all harm and evil. </p>
<p>So I praise You, O Lord, for You are my Constant Companion, 
the Pillar of my life. Make my heart steadfast and help me to praise You, O 
Lord, for You are worthy of all the praise I can give You. My praises will flow 
for eternity and even then they will not touch the tip of 
the iceberg!</p>
<p>Lord, I crave Your wisdom. Your Word declares, &quot;Wisdom 
strengthens a wise man more than ten rulers who are in a city&quot; (<b>Ecclesiastes 
7:19</b>). May I grow in wisdom from day to day until wisdom characterizes my 
life. Help me be impatient when I show lack of discretion, planning, or 
realism. May I not rest until I am characterized by wisdom.</p>
<p>Father, help me to remind myself that Jesus Christ is my 
Lord; I am not my own lord. Jesus said, &quot;A disciple is not above his teacher, 
nor a slave above his master.&quot; Jesus is both my teacher and my master. Help me 
consult with Him before I make any major life decision. Although You generally 
want me to follow the way of wisdom, sometimes You have plans that common wisdom 
cannot foresee.</p>
<p>Lord, I am thankful for plenty of things. You have been 
good. Two of the things for which I am thankful include: __________.</p>
<p>Lord, may Your Spirit may fill me. May I find my strength 
in His power, not my own. May Your Spirit control me this day.</p>
<p>Lord, today I remember the nations in which believers are 
persecuted, facing death, dismemberment, torture, rape, sexual abuse, 
imprisonment, slavery, or confiscation of goods. Today I pray for nations 31-35 
from the list of the top 86 nations where persecution is a problem. I pray for: 
United Arab Emirates, Djibouti, Chechnya, Kuwait and Tajikistan. O Father, may 
these countries become enlightened in the realm of human dignity and God-given 
rights including the right all have to choose and practice their religious 
convictions. May the persecutors themselves be won to faith in Christ, may the 
governments mandate and enforce the recognition of true religious freedom and 
help believers by granting them an extra portion of wisdom. Help them to evade 
persecution when possible and help them to find their strength in You when they 
cannot do so.</p>
<p>Thank You, Lord, for the ministry of Joy in the Harvest, &quot;a Christian 
mission dedicated to serving Christ in Africa...whose motto is 
'so 
that they may know Jesus.'&quot; Their stated purpose is &quot;to provide expertise and 
resources needed by the church in Africa for making Christian disciples.&quot; Bless 
them as they work in various parts of Africa in conjunction with national 
workers. Prosper their work, provide, protect, grant harmony and much fruit, I 
pray.</p>
<p>Protect me today, I ask in Jesus’ Name, Amen.</p>

