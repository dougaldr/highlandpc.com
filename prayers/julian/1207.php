<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 7
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>When the angel told Mary she would give birth to 
the Messiah, she was perplexed. How could she, a virgin, have a son? The angel 
explained that the Holy Spirit would come upon her, so that her son would 
actually be the Son of God in human flesh. The angel pointed out that her 
elderly relative, Elizabeth, was pregnant with John the Baptist.</p>
<p>Then the angel added, &quot;For nothing is impossible with God&quot; 
(<b>Luke 1:37</b>). Indeed, You are the God who can do the impossible, when it suits 
Your purpose. Because of Your foreknowledge and the way You have orchestrated 
the universe, we take history or the law of physics for granted. Science can 
explain the patterns You have set up, but they cannot explain the &quot;why&quot; of 
creation. O Lord, the universe runs like clockwork, even though we see evidence 
of the curse resulting from the Fall of man in the Garden. Yet You still 
sometimes intervene and make exceptions to those laws. Your greatest 
intervention was sending Your Son into the world as the sacrificial Lamb. 
Hallelujah!</p>
<p>Lord, I thank You for the Church. I thank You for the 
Universal Church made up of all those who truly know You. I thank You for my 
local church where I can use my talents and gifts in coordination with others so 
that we can do together what we cannot do alone. I thank You for the teaching of 
the Word, the accountability I have to the leadership and the way I learn to 
work with and love others who sometimes differ from me. Thank You that church 
involvement stretches me and helps me to focus on giving and helping others, not 
just &quot;taking in.&quot;</p>
<p>Father, may Your joy be evident more and more in my life. 
Help me to appreciate salvation—especially my relationship to You—so that I am 
filled with joy.</p>
<p>Help me be alert to Satan's schemes. Help me to take up the 
full armor of God and to draw near to You, O Lord. Help me to resist the devil 
and he will flee. Place a hedge around me so the evil one cannot hinder my 
service to You, Father.</p>
<p>Dear God, I remember the special administration area of 
Macau, now under Chinese control. Father, this area had at one time been 95% 
Roman Catholic and now it is only 5% Roman Catholic. Unfortunately, the 
conversion away from Catholicism has not been toward Evangelicalism, but toward 
non-Christian religions or secularism. The evangelical church is nearly 2% of 
the population. Though this percentage is small, the evangelical church is 
growing rapidly in this region of half of a million people. The church needs 
leaders and Your Spirit's working in this land! O God, may the percentage of 
believers double once again in this decade as it did in the last.</p>
<p>Lord, thank You for the ministry of Latin Link. They 
describe their ministry as &quot;an international community serving local churches 
throughout Latin America. Our passion is to live, teach and promote biblical 
Christianity, help meet identified needs and develop short and long term 
mission initiatives to, from and within the region.&quot; O Father, bless their 
work within Latin America.</p>
<p>Help me to be honest and hard working today, I pray in 
Jesus' Name, Amen.</p>

