<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 29
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are the God Who is near and not far away. You are 
close at hand, right next to me at all times. I do not have to work up a frenzy 
to enjoy Your presence because Your presence is a reality 24/7, although You can 
be present in special ways, as, for example, when two or three gather in Your 
Name. Help me to be anxious for nothing because &quot;the Lord is near.&quot;</p>
<p>Help me, O Lord, to express myself to You completely. I do 
not like it when people complain to me time and time again; even my loved ones 
get tired of hearing me when I feel whiny and negative. And to deal with angry 
people, just the thought makes me shudder. But Lord, as I read the Psalms, I 
see David and others complaining, whining and bringing their disappointments 
and fears to You time and time again. Lord, we can't wear You out. You never 
say, &quot;enough, you've filled your quota.&quot; Lord, even if we are angry at You, You 
can take it. You will haggle things out with us and help us gain Your 
perspective. I have some dear ones of earth who help me, but none can do what 
You can. Thank You, O Lord, that You are such an accessible God through Jesus 
Christ.</p>
<p>Father, I have some personal health concerns including: __________. I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>I remember the massive nation of India with its over one 
billion people and its many languages, religions and cultures.&nbsp; Lord, the 
needs and prayer requests for this nation are overwhelming. About 1.8% of the 
population claim to be born-again evangelical Christians. Other forms of 
Christianity are drying up, but thank You that Bible-believing churches are 
generally thriving and growing. As India becomes more prosperous and the 
economy improves, this nation is primed to become one of the world's leaders in 
the future. O, Father, we pray that Your Gospel might penetrate hearts and 
that the number of believers would multiply tenfold over the next decade. Help 
the leaders to receive good training and to lead the church in Your direction. 
We pray that Hindu extremism would die down and that religious freedom would 
reign. May Your Spirit sweep over this land.</p>
<p>O Lord, thank You for World Concern as they try to help and 
prevent the exploitation of women and children. They are a &quot;Christian 
humanitarian organization that provides emergency relief and community 
development in some of the most neglected areas of the world&quot; with &quot;a 
worldwide staff of 900 skilled and professional workers, 1300 volunteers, 
several hundred international partners and thousands of donors whose combined 
efforts help local communities help themselves.&quot; O, Father, help them as they 
help others and may many come to know Christ in the process. Empower them, 
grant them harmony among the workers and provide for their needs. May Your 
Spirit open doors and work in lives through them.</p>
<p>So, Lord, help me to remember Your presence with me today, 
I pray in Jesus' Name, Amen.</p>

