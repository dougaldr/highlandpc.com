<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 15
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great Father of Lights,</p>
<p>I praise You that You are a God Who makes decisions. You 
are not some force or some law of the universes. You are a personal God. You 
think, You feel, You create, and You decide. You have a perfect will. Since 
You decide, you also discern and are therefore the perfect Judge of the 
Universe. Your decisions are never wrong, never lacking knowledge, information, 
or reasonability. You are not a random God. Although Your ways are higher than 
ours, and although many of Your decisions do not make sense to us, we are the 
ones with the problem, not You.</p>
<p>Today I thank You for the atoning work of Jesus Christ. He 
earned my salvation by paying a penalty as a sin offering to You, Father. He 
took my sins upon Himself and gave me His righteousness in exchange. He became 
a curse for us 
(<a href="javascript:scripture('../../scripture/gal313.htm')" target=_self class=pop>Galatians 3:13</a>).</p>
<p>Father, in 
light of the work of Jesus for me, help me to remember that He has purchased me 
with His blood, and that I do not belong to myself, but to You. I am Yours. 
Help me view life accordingly.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>). 
So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Meet the personal needs of my life, Lord. My physical 
needs: health, strength, provision. Meet my spiritual needs: growth, nurture, 
fellowship. And meet my social needs: matters of family, friends, and 
community.</p>
<p>Today I am 
praying for the nation of Cambodia. Lord, the slaughters that took place there 
in past decades are horrible to even hear about. In the process of the ravage 
of internal warfare, most Christians were killed. Thank You for the 300 
evangelical congregations in the land. Even though evangelicals make up only 
about one half of one percent, the church is growing quickly. The challenges 
are many, with so many abandoned children who end up as prostitutes in addition 
to the emotional effects of past slaughters. May the church grow exponentially, 
raise up leaders, and help the government to take a positive stance toward 
religious freedom. May You turn Cambodia into a lighthouse for other nations. 
Bless the missionary agencies who work there with fruit and provision.</p>
<p>Lord, I thank 
You for the vital ministry of Kids Alive, as they rescue children from dangerous 
situations and provide them with housing and education around the world. Lord, 
we hate to hear how many children are suffering abandonment, abuse, and living 
on the streets. Help the missionaries and ministries of Kids Alive to make a 
big difference for the Kingdom of God. Provide for these ministries abundantly, 
help the workers to live in harmony, and rescue many children!</p>
<p>Bless all those 
who protect children locally too, I ask. </p>
<p>Now, Lord, help me keep my eyes on You 
today, I request, in Jesus' Name, Amen.</p>

