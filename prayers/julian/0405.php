<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 5
<br><span class=byln>by Ed Vasicek</p>

<p>God,</p>
<p>You are unlimited except by Your own nature. You have 
all power, and all sorts of things are possible with You, and You can do almost 
anything, yet You cannot contradict Who You are. Because of Who You are, You 
cannot sin or lie or do anything that would be 
contrary to Your nature. You could not create a rock that You could not lift 
because You cannot contradict Yourself. Moral absolutes come from You. 
Everything You do is right for two reasons: You do it (and thus define what is 
right) and You are in complete harmony with what is right (thus behaving in ways 
that are consistent with righteousness).</p>
<p>But, Lord, I am a sinful person. Your Word tells me that if 
I confess my sins, You are faithful and&nbsp;just to forgive me of them, and You will 
cleanse me from all&nbsp;unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You 
now.&nbsp;I&nbsp;do not excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Thank You that once I have 
accepted Christ as my Savior, the Holy Spirit has indwelt me. He is helping me 
to live the Christian life, empowering me and creating an appetite for God and 
the things of God within me. He causes me both to desire to do God�s will and 
then to do it. Others who are watching from the outside do not understand 
that You are working in me.</p>
<p>Lord, help me to not make promises I do not keep. Your Word 
tells me it is better not to make a vow than to make one and then not keep it. 
May others note me for my integrity, and may others see this as an evidence of 
Christ living in me.</p>
<p>Today I remember the continent of Asia in prayer. Lord, over 83% of the world's 
4.4 billion non-Christians live in Asia. Not only that, but over 87% of the 
world's unevangelized live in this most populated region of the world. Asia 
has 832 million Muslims, 805 million Hindus, and between 400 and 900 million 
Buddhists.<b> </b>Lord, the Asians are the least evangelized people on planet 
earth. Oh, Father, change that. Thank You for the spread of the Gospel in 
Korea and China among other places. May Your Spirit hover and breath upon this 
region and may the church multiply by 10 in Asia over the next decade. May the 
horrible persecutions stop and may the Asian church develop forms and 
ministries adapted to their situations.</p>
<p>Great God, thank You for Food for the Hungry as they seek 
to �walk with churches, leaders and families in overcoming all forms of human 
poverty by living in healthy relationship with God and His creation� [Food for 
the hungry] helps some of the world's most disadvantaged people in 47 countries 
through child development programs, agriculture and clean-water projects, health 
and nutrition programs, education, micro-enterprise loans and emergency 
relief.� Lord, bless their efforts; give them harmony, the wisdom and power of 
Your Spirit, and a liberal supply of provisions and funds.</p>
<p>I commit my day to 
You, in Jesus� Name, Amen.</p>

