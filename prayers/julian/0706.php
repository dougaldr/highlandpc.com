<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 6
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a God Who can be depended upon. Although I 
try to be a person of my Word, I cannot always do what I promise because I 
limited and unforeseen circumstances might rob me of the ability to follow 
through. I may be sincere, but I cannot always deliver. Then sometimes my 
motives may not be pure or my sincerity impeccable, for I have a heart that is 
deceitful 
(<a href="javascript:scripture('../../scripture/jer179.htm')" target=_self class=pop>Jeremiah 17:9</a>). 
But You are never self-deceived, insincere, taken by 
surprise, or unable to do what You promise. Your dependability is what keeps 
our universe together. It is responsible for every breath I take. Thank You 
that Your promise never to leave me is also dependable!</p>
<p>O God, sometimes life overwhelms me. The older I get, the 
faster time seems to move. Surprises await me around the corner and I find 
myself swamped with work, responsibilities, or commitments. But You have a 
perfect schedule for me; You do not hold me accountable for what is impossible. 
Instead You want me to prioritize life based upon Your will, not what is 
screaming loudest. </p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Help me with my thought life today. Help me think positive 
and pleasant thoughts today, thoughts that please You. Also help me to spend 
time in Your Word on a regular basis. </p>
<p>I pray for Christian (and other) marriages around me. 
Lord, it is in our nation’s best interest when marriages hold together—especially 
when there are children involved. If I am married, help me to 
continually nurture my marriage by intentionally dating my spouse, reading books 
that would help me and focusing on how I can be a better spouse, I ask.</p>
<p>I pray for the Chongqing Municipality province of China. 
This area is heavily polluted and over 31 million people populate this 
industrial area. Thank You that statistics show that over 2% of the population 
claim to be born-again evangelicals. Help the churches to grow significantly; 
may 4% of the population profess the above within the next decade, I ask. May 
Your Spirit draw many to Yourself there.</p>
<p>I thank Your also for the Fujian province where nearly 11% 
claim some sort of Christianity, most of them evangelical. May many more in 
this province of 34 million come to know Christ personally. Especially reach 
down and save many from the “She” population, few of who know You, O Lord.</p>
<p>Lord, thank You for the North American ministry of &quot;Here's 
Life Inner City,&quot; &quot;the urban ministry of Campus Crusade for Christ, a 
non-denominational, evangelical organization. [They] partner with holistic 
inner-city churches and ministries, seeking to transform neighborhoods through 
the redeeming power of Christ. [They] provide resources to these indigenous 
churches so that they can connect with the needy in their communities.&quot; Bless 
these ministries and we pray that their fruit would be so noticeable that even 
the lost will see the transforming power of Christ can do what no government 
program can.</p>
<p>Help Christ to work mightily through me in my little world, 
I pray in His Name, Amen.</p>

