<p>Lord, thank You for the wonderful ministry of the AWANA 
Youth Clubs. The acrostic, &quot;Approved Workmen Are Not Ashamed&quot; is based upon 
Your Word in <b>2 Timothy 2:15</b>, &quot;Do your best to present yourself to 
God as one approved, a workman who does not need to be ashamed and who correctly 
handles the word of truth.&quot; Thank You for the hundreds of verses that these 
young ones have memorized. We know this will provide a good foundation for a 
solid Christian life and we pray that our AWANA youth would choose to build 
upon that foundation.</p>
<p>Thank You, our God, for the many adults and teens who have 
volunteered to help lead our clubs. We ask You to bless our commander, our 
secretary, our directors, game director, store organizers, leaders, listeners, 
teachers and the many helpers who assist in so many areas, like music or crafts. 
And thank You, Father, for the parents, guardians and concerned adults who 
bring the children to the club. Thank You for the support and encouragement they 
have been to these youngsters.</p>
<p>And so, precious Lord, we ask You to bless our AWANA club 
and use it to Your glory. Grant us peace and harmony, enthusiasm and wisdom 
and build spiritual depth among the kids, help them make new friends, and grant 
our volunteers a sense of fulfillment in serving You. We pray that, like the 
Psalmist, our kids would be able to say, &quot;I have more insight than all my 
teachers, for I meditate on your statues&quot; (<b>Psalm 119:99</b>).</p>
<p>Bless AWANA headquarters, the individual AWANA 
missionaries and the over one million children around the world who participate 
in AWANA every week, I pray in Jesus' Name, Amen.</p>

