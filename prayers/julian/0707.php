<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 7
<br><span class=byln>by Ed Vasicek</p>

<p>Great God and King of Kings,</p>
<p>I worship You as the Infinite One. You, O Lord, have no boundaries. You fill 
the entire universe and Your attributes are extending outward toward infinity. 
Just as we never run out of numbers, so neither are Your attributes exhausted 
nor Your power limited. When we consider the billions of stars, some of which are 
actually galaxies whose light combines as though they were but single stars, we 
have to admit that we cannot comprehend the vastness of the universe. But You, 
Lord, are so much greater just as infinity is greater than any number.</p>
<p>Lord, I thank You for the Church. I thank You for the Universal Church made 
up of all those who truly know You. I thank You for my local church where I can 
use my talents and gifts in coordination with others so that we can do together 
what we cannot do alone. I thank You for the teaching of the Word, the 
accountability I have to the leadership and the way I learn to work with and 
love others who sometimes differ from me. Thank You that church involvement 
stretches me and helps me to focus on giving and helping others, not just 
&quot;taking in.&quot;</p>
<p>Father, may Your joy be evident more and more in my life. Help me to 
appreciate salvation—especially my relationship to You—so that I am filled with 
joy.</p>
<p>Help me to resist temptation and the lures of the Satan 
today.</p>
<p>Lord, help me to be a good steward of the funds with which 
You provide me. <b>Proverbs 17:18</b> reads, &quot;Of what use is money in the hand of a 
fool, since he has no desire to get wisdom.&quot; O, Lord, may I be a wise person. 
Help me be wise in all areas including money management.</p>
<p>Lord, You have worked mightily in the nation of Panama. In 
1970, 4.8% of the population was evangelical Christian. In 2000, the percentage 
rose to 18% for which I praise You. Lord, may many more come to know Christ and 
follow Him. Father, the illegitimate birthrate of 72% bears testimony to messed 
up family structures. Help the Christians to change that. Help believers to 
let their faith affect their daily lives and choices, I ask. Train strong 
leaders and work a great deepening of the church, I ask.</p>
<p>Thank You for the ministry of Horizons International, &quot;a 
nondenominational missions organization which exists to carry out the Great 
Commission.&quot; They are trying &quot;to reach out to the millions of internationals 
in the US.... Horizons [also has] ...international teams in Europe, Asia, Africa 
and the Middle East preaching the gospel.&quot; Bless this ministry; keep them true 
to Your Word, empower them by Your Spirit, provide for them, protect them and 
grant them harmony, I ask.</p>
<p>I remember those who work as heavy vehicle and mobile 
equipment service technicians. Give them safety on the job, help them to 
perform their tasks well and may those who know You freely share Christ with 
their fellow workers.</p>
<p>Help me remember that I am a pilgrim in this world, I pray 
in Jesus’ Name, Amen.</p>

