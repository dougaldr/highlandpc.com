<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 15
<br><span class=byln>by Ed Vasicek</span></p>

<p>God Almighty,</p>
<p>You are truly Almighty! You are omnipotent, meaning You 
have all power. Nothing is too difficult for You. Indeed, all creation came 
about by means of conversation between the Father, Son, and Holy Spirit. You 
said, &quot;Let there be&quot; and it came to pass. The only limitations You have are 
those limits that are demanded by Your nature. That is why though You easily 
created the universe in 6 days yet find it impossible to lie.</p>
<p>Lord, I thank You for the assurance of salvation I have by 
my faith in Jesus Christ and His presence in my life. Thank You that, according to 
<a href="javascript:scripture('../../scripture/1john513.htm')" target=_self class=pop>1 John 5:13</a>,
I know that I have eternal life. I do not need to merely hope 
or guess, I can know. Thank You that the blessed Holy Spirit bears witness to 
my spirit, that I am a child of God.</p>
<p>Lord, help me to be a gracious person. Help me not react 
to insults or slights or delays or mistakes with uncontrolled anger, a demanding 
attitude, or rudeness. Instead, help me to delay my anger and gain my composure 
and consider the effect of my responses on others.</p>
<p>Help me with my thought life today.&nbsp; Help me think 
positive and pleasant thoughts today, thoughts that please You.</p>
<p>Please address the following needs: __________.</p>
<p>This day I remember the nation of Armenia, a remarkable 
little nation and the first nation to ever declare itself a Christian nation in 
the early 4<sup>th</sup> century. We thank You for the growth of the evangelical 
church there. We are grateful that a new modern language translation of the 
Bible is now available there, and that You are blessing the showing of the Jesus 
movie. We pray that the evangelical church would grow at a rapid and solid 
pace. We also pray that the persecution encouraged by the Orthodox Church there 
in the past would remain buried, and that people would have complete religious 
freedom.</p>
<p>Today I remember Source of Light ministries with their 
wonderful correspondence courses. I thank You for the countless number of 
people who have been saved or who began being discipled through these studies. 
Provide for them in every way.</p>
<p>I also remember our local school superintendent. I ask You 
to bless him or her, and I ask that he or she would come to know You and would 
be open to the things of God.</p>
<p>I pray that You 
would encourage me today, for I ask it in Jesus' Name, Amen.</p>

