<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 12
<br><span class=byln>by Ed Vasicek</p>

<p>King of Kings and Lord of Lords,</p>
<p>You are the God the remnant. At one time, only Noah and 
His family were true to You. You delivered them in the Ark while the ancient 
world perished. Throughout the Torah, we find individuals who knew You in 
surprising places: Melchizedek, Jethro and even Balaam. Job lived about the 
time of Abraham. When You singled out Your special nation of Israel, many 
turned away from You, but You always had Your remnant who refused to bow the 
knee to Baal. When our Savior came to His own people, they rejected Him—mostly. 
But a remnant believed and together believing Jews and believing 
gentiles make up Your church. Even the word “church” means the “called out 
ones.” We are a God-fearing remnant from our society.</p>
<p>Lord, I have some requests to bring before Your throne. 
They are: __________.</p>
<p>Lord, I am thankful for plenty of things (You have been 
good) and two of them include: __________.</p>
<p>Lord, may Your Spirit fill me. May I find my strength in His power, not my 
own. May Your Spirit control me this day.</p>
<p>I ask that You would bless all of my relationships: the 
relationships within my family, among my friends, church members, fellow 
employees, neighborhood and community.</p>
<p>Help me to be a content person, not a constant complainer 
or negative. Help me encourage those who are close to me. Keep me away from 
put-down humor or rudeness. Help me to be the kindest toward those who are the 
closest to me.</p>
<p>Lord, help me to be a gracious person. Help me not react to 
insults or slights or delays or mistakes with uncontrolled anger, a demanding 
attitude, or rudeness. Instead, help me to delay my anger and gain my composure 
and consider the effect of my responses on others.</p>
<p>I remember those in our world who have lost loved ones to 
terrorists (or responses to terrorism), who have been injured, handicapped, or 
traumatized. O Father, please heal the broken hearts and help these precious 
survivors to find their strength in You.</p>
<p>Father, today I pray for the Pacific Island nation of Tonga 
with its 100,000 people. Thank You that almost 22% of the population identify 
themselves as born-again evangelicals and thank You that the King is a firmly 
committed Christian. Help the church to become healthier. Bring reconciliation 
to heal the many schisms and thwart the &quot;personality cult&quot; mentality that is 
all too common. May Jesus Christ be the true head of the church in Tonga, not 
just technically but practically!</p>
<p>Lord, thank You for the ministry of In-Touch Mission which &quot;has for the 
last 20 years ministered to the Persecuted Church in Eastern Europe, Africa and 
whenever a need is presented. Primarily, the ministry attempts to uphold the 
name of our Lord Jesus by ministering to those who have needs, both spiritual 
and material, in areas of persecution.&quot;</p>
<p>Lord, I pray for both dental assistants and dental 
hygienists. Father, they perform an important service for many of us. Help them 
to do their work well with integrity and may those who know You freely share 
the Gospel.</p>
<p>Lord, help me to be appreciative of my freedom to follow Christ, I pray in 
His Name, Amen.</p>

