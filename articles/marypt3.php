<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>What About Mary? Part III - Mothering the Messiah
<br><span class=byln>by Ed Vasicek</span></p>

<p>In my previous articles, we discussed that fact that Mary 
was a mere woman. Though she was certainly a godly person, she was still a 
sinner who needed the grace of God. She was chosen to bear the Messiah because 
she was a descendent of David--and betrothed to a man who was a <i>special</i> 
descendent of David.</p>
<p>Although the Davidic line had not been recognized for 
centuries, Joseph would have reigned as king if the line had been honored. The 
ruling line had been cursed about 600 years before Jesus was born! Jeremiah 
prophesied this message to Joseph's forefather, King Jehoiachin (Coniah): 
&quot;Record this man as if childless, a man who will not prosper in his lifetime, 
for <i>none of his offspring will prosper, none will sit on the throne of David 
or rule anymore in Judah</i>.&quot; </p>
<p>Mary, on the other hand, did not descend from Solomon's 
line (the ruling line), but descended from Solomon's brother, Nathan. The end 
result? Jesus is genetically connected to David through Mary; He has the legal 
right to rule through His stepfather, Joseph, but is not under the curse 
pronounced upon Joseph's forefather (since Jesus did not actually descend from 
Joseph's line). He was a legal descendent of Joseph, but not actually a genetic 
descendent of Joseph.</p>
<p>But little of this mattered to Mary, for David's line was 
like any other family in first century Israel. In fact, the reigning regent, 
the paranoid King Herod, was actually a descendent of Esau! This wicked tyrant 
heard about Jesus' birth from the Magi and determined to destroy Jesus. Before 
he could order his soldiers to kill all the boys in the vicinity of Bethlehem, 
Joseph had been warned in a dream to vacate.</p>
<p>Although Joseph and Mary were from the north of Israel (the 
city of Nazareth in the province of Galilee), they decided to rent a house in 
Bethlehem, perhaps avoiding the gossip that life in Galilee would bring. </p>
<p>God provided for them: they were now wealthy because of the 
generosity of the Wise Men. They could readily convert the gold, frankincense, 
and myrrh into cash. Perhaps they settled for a while in Alexandria, an 
Egyptian city with a large Jewish population.</p>
<p>We do not know how or when Mary received word of Herod's 
slaughter of the innocent children. No angel warned the other parents as he had 
Mary and Joseph. Whereas they appreciated God's grace to them--and the fact 
that they were singled out to rear the Savior of the world--the couple 
probably grieved to learn that friends and relatives had lost children to 
Herod's sword because of their special child! We can only imagine their 
struggles.</p>
<p>It wasn't long at all until they received a message from the angel that Herod had died 
(<a href="javascript:scripture('../scripture/matt21920.htm')" target=_self class=pop>Matthew 2:19-20</a>). 
Rather than return to a dubious 
situation in Bethlehem, Joseph and Mary decided to return to their hometown of 
Nazareth in the fertile province of Galilee. Their lives, at this point, were 
probably typical. Joseph and Mary brought four boys and a number of girls into 
the world, and Joseph worked as a contractor (although often translated, 
&quot;carpenter,&quot; the word for Joseph's vocation would be better translated &quot;stone 
mason,&quot; although he probably also worked with some wood).</p>
<p>We do know that the childhood of Jesus was uneventful; He 
was apprenticed in His stepfather's trade. When Jesus later presented Himself 
as the miracle-working Messiah, his friends and neighbors were completely 
perplexed. If He had a track record of working miracles or obviously stood out 
from His peers, we would never read what we do in <b>Matthew 13:52-58</b></p>
<blockquote>Coming to his hometown, he began teaching the people in 
their synagogue, and they were amazed. &quot;Where did this man get this wisdom and 
these miraculous powers?&quot; they asked. &quot;Isn't this the carpenter's son? Isn't 
his mother's name Mary, and aren't his brothers James, Joseph, Simon and Judas? 
Aren't all his sisters with us? Where then did this man get all these things?&quot; 
And they took offense at him. But Jesus said to them, &quot;Only in his hometown and 
in his own house is a prophet without honor.&quot; And he did not do many miracles 
there because of their lack of faith.</blockquote>
<p>There was one childhood event, however, that was unusual: 
His visit to the Temple at age 12. Like other Jewish boys, Jesus would make His 
Bar Mitzvah (becoming a &quot;son of the commandment&quot;) at age 13. It was typical for 
12-year-old boys to discuss Torah (the Law of Moses) with rabbis, sages, and 
teachers of the Law in preparation for one's Bar Mitzvah. Jesus had studied 
well, and the scholars were impressed with His knowledge and interpretative 
skills.</p>
<p>Joseph and Mary, however, were worried about Him. When the 
Jews traveled to Jerusalem to celebrate religious holidays (three times a year), 
they generally traveled in caravans with extended family and friends. Entire 
villages might make the pilgrimage together. Along the way up to Jerusalem, 
they would sing the &quot;Psalms of Ascent.&quot;</p>
<p>As they were hiking back to Nazareth, Joseph and Mary began 
looking for Jesus. Their search turned into panic as they realized He was 
nowhere to be found in the entire caravan. Finally, they turned back to 
Jerusalem to search for the lost boy. They searched for three days before they 
found him.</p>
<p>When they received word that He was engaging in Torah study 
at the Temple, they quickly made their way up toward Mt. Zion. Sure enough, 
Jesus was engaged in deep discussion with the Torah scholars.</p>
<p>Joseph and Mary did something they were not used to doing: 
they confronted Him about the unnecessary stress He had brought to them. Jesus 
defended Himself. In <b>Luke 2:49-51</b> Jesus speaks: &quot;'Why were you searching for 
me?' he asked. 'Didn't you know I had to be in my Father's house?' But they did 
not understand what he was saying to them. Then he went down to Nazareth with 
them and was obedient to them. But his mother treasured all these things in her 
heart.&quot;</p>
<p>Mary did not understand what was happening, but she quietly 
processed what Jesus had said. She was not merely the mother of a boy, but she 
and Joseph were special guardians of the Holy One. They should have expected 
the Messiah to be more zealous for the Torah than His peers. The Temple should 
have been the first place they looked!</p>
<p>Mary's quiet disposition and contemplative heart made her 
an ideal mother for God's Anointed One. She did not understand this unusual 
life of hers, but she knew God was in control. She probably believed that the 
pieces of the puzzle would eventually fit together.</p>
