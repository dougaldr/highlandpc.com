<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 9
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Lord,</p>
<p>Thank You that You have a will. You are not some sort of 
cosmic machine or impersonal force. You feel, You think and You have a will. 
You are a personal God and part of personality is the ability to decide—having a will.</p>
<p>Since You are independent of Your creation, creation exists 
as a choice of Your will. If You had all power and all wisdom but no will, You 
could do nothing with that power or wisdom. You could not have created, could 
not have redeemed, indeed, could not answer prayer.</p>
<p>Because Your will is perfect, we gladly submit all our 
requests to Your will. Our wills are imperfect. Even after being redeemed, we 
are tainted by sin as long as we have our old sinful natures. It is only when we 
see Jesus that we will lose our sinful bent. In the meantime, O Lord, help us 
submit our wills to Yours.</p>
<p>Thank You, Lord, that I too need others to be happy and fulfilled. Sometimes 
I feel lonely and alone; help me not to cower in a corner, but help me to jump 
into my church, neighborhood and community. You made me social, just as You are 
social. Help me nurture that part of my being and not stifle it in fear.</p>
<p>Help me remember that our church is a body and that I have a part to play. 
Help me to be forbearing, to confront when necessary and 
not to be touchy. Help me maintain a realistic view about human nature even 
among the regenerate so that I do not hold false expectations that will leave me 
frustrated.</p>
<p>Lord, I pray that You would protect my loved ones and I 
throughout our lives. Send guardian angels to watch over us, I pray. Keep us 
from harm and evil of all kinds.</p>
<p>Lord, I have some requests to bring before Your throne. 
They are: __________.</p>
<p>Lord, I pray for the Indian State of Tamil Nadu with its 63 million people. 
Thank You that over 5% of the population claims to be born-again evangelicals 
and that the church is growing rapidly. The growth, however, has not been even. 
Many groups have no Christian witness whatsoever, O Father, please keep 
working mightily in this region. Raise up a people for Your Name among all the 
areas in this State, I pray. May the number of believers triple over this next 
decade, I ask.</p>
<p>Father, thank You for Mission Training International, as 
they seek to train missionaries to adjust well and endure on the field. Thank 
You that more than 14,000 missionaries have been trained over the years. The 
focus upon character development and family issues &quot;focusing on the heart 
issues that make or break those in missions.&quot; O 
Lord, bless their efforts. The percentage of missionaries who quit their work is 
high; Father, use this ministry to help conserve Your servants so that they 
might fulfill the tasks to which You have called them.</p>
<p>Now, Lord, fulfill Your purposes for my life, I pray in the Messiah's Name, 
Amen.</p>

