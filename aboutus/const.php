 <a href="javascript:scripture('../scripture/rom1423.htm')" target=_self class=pop>Romans 14:23</a>)
Highland Park Church&#39;s ministers are never obligated to officiate any 
wedding, even for church members.</li></ol>
  <li>Pro-Family<br>
We embrace the conviction that children are owned by God and entrusted to 
parents, not the state. We repudiate child abuse (sexual, physical, or 
mental), spousal abuse, and irresponsibility. We acknowledge the Biblical 
teaching of male headship and its implications. We believe the rearing of 
children to be a worthwhile and dignified vocation. 
(<a href="javascript:scripture('../scripture/ps12734.htm')" target=_self class=pop>Psalm 127:3-4</a>;
 <a href="javascript:scripture('../scripture/eph52264.htm')" target=_self class=pop>Ephesians 5:22-6:4</a>;
