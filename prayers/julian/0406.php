<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 6
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Lord,</p>
<p>You are a relational God. The Persons of the Trinity—Father, Son, and Holy 
Spirit—have always had a perfect relationship. Your 
Nature is the basis of all good relationships. Because You are a relational 
God, You actually enjoy interacting with Your people. Having a relationship with 
You saves us. At the time of judgment, Jesus will dismiss the lost by saying, &quot;I 
never knew you&quot; (Matthew 7:23). But to us, He says, &quot;I know my sheep, and my 
sheep know me&quot; (John 10:14). Lord, when I repented of my sins and accepted the 
Lord Jesus Christ, I was adopted into Your family. Thank You for our 
relationship.</p>
<p>Savior, as Your disciple You want me to bear fruit. You 
want me to demonstrate the fruit of the Spirit in my lifestyle, the greatest of 
which is love. You want me to salt the earth and be a light. You want me to 
encourage my brothers and sisters in Christ, to use my spiritual gifts in 
conjunction with my church, and to share the good news of Jesus Christ with 
others. Help me to be a fruitful Christian. May I start at home with those 
closest to me.</p>
<p>Help me to organize my life and environment in a healthy, 
balanced and reasonable way. Keep me from obsessive behavior. May I neither 
model a control-freak lifestyle that is nothing more than a fearful approach to 
life nor that of disorganization because You are a God of order. Help me to 
seek a medium mentality: one that trusts You without obsession but 
disciplined enough so that I can find what I need and model responsibility.</p>
<p>Please meet the following needs, requests and desires as 
You see fit: __________.</p>
<p>Help me with my thought life today.&nbsp;Help me think positive 
and pleasant thoughts today, thoughts that please You.</p>
<p>I remember the South Pacific island nation of French 
Polynesia. This is a nation that needs Your help desperately. The people are 
promiscuous (Over 60% of babies born are illegitimate.) and substance abuse is 
commonplace. Only about 5.6% of the population consider themselves born-again 
evangelicals and many of the people have returned to the pagan occult practices 
of their forefathers. Father, help Your children to remain pure in such an 
environment with its many temptations. May the evangelical church double within 
the next decade. May Your Spirit draw multitudes to You.</p>
<p>Thank You, Lord, for Global Mapping, &quot;a Christian 
inter-denominational missionary research agency. [Their] highest priority is 
serving evangelical mission ministry leaders in the developing world.&quot; Bless 
them as they engage in &quot;research and development of strategic missions 
information...to enable Christian ministry leaders to acquire, manage, analyze, 
apply, communicate and share strategic information.&quot; We know, Lord, that 
foreign missions is a complicated challenge and that weeks spent in thinking, 
evaluating and developing a strategy save years of misdirected labor on the 
field. Bless them as they help other missionaries and missions organizations 
minister wisely. Give them harmony and the special wisdom of Your Spirit.</p>
<p>So, Lord, help me to honor You today, I pray in Jesus' 
Name, Amen.</p>

