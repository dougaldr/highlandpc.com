<p id="pgt"><span class="pgh">Church and Leadership:</span>
<br>What Does It Mean for a Man To Lead?
<br><span class=byln>by Ed Vasicek</span></p>

<p>I don't know about you, but I was not raised to be a 
leader.  My dad never sat me down and taught me about leadership. Indeed, many 
men find themselves weak in this department.</p>
<p>When you dedicate your life to Christ, all of a sudden you 
find out God wants you to lead your family.  Even if you do not have a family, 
you realize God wants you to be a disciple who, in one way or another, must 
practice some leadership.</p>
<p>Let me share a few basics about leadership:</p>
<ol class="deci">
	<li>Leadership boils down to influence.  It is not a matter 
of title or office, but influence.</li>
<li>Leadership is taking initiative.  It is having a plan.  
That plan is flexible, but a leader has some sort of plan ready.
<li>Leadership is making it happen.  Put it on the calendar, 
initiate the routine, make the decision.  Good ones plan ahead.</li>
<li>Leaders are out for the best interests of those they 
serve, not personal comfort.</li>
<li>Good leaders do not force or coerce.  They offer 
direction; others must choose whether to follow that direction.  Leaders make 
mistakes, but we cannot default in leading to avoid criticism or being told, &quot;I 
told you so.&quot;  We'll live.  And even good leadership does not guarantee good 
results.  Christ is the head of the church, but the church is far from perfect!</li></ol>
<p>Become a leader because God demands it!</p>
  
