<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 24
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>We welcome the memory of Your Son's first coming as 
we celebrate Christmas Eve. Like the great hymn, we sing, &quot;Hark! The herald 
angels sing: 'Glory to the newborn King.'&quot; 
<a href="javascript:scripture('../../scripture/phil259.htm')" target=_self class=pop>Philippians 2:5-9</a>
tells us that God the Son was fully God, with all the glory and privileges that only You have, O 
Lord. Yet He set aside the use of those privileges to become a man, humbling 
Himself to the point of death on a cross.</p>
<p>Indeed, Jesus was born to die that man might live. He 
emptied Himself for me. He became a baby and grew to be a man for me. He humbled 
Himself to save and rescue me. That's what Christmas is really about.</p>
<p>Lord, as carolers sing the verse, &quot;Born to raise the sons 
of earth, born to give them second births,&quot; may that expression, &quot;born to give 
them second birth&quot; lodge in their minds. Help those who sing these words to 
examine their own hearts to see if they have truly been born again.</p>
<p>But thank You that though the Lord Jesus came the first 
time to be the Lamb of God 
(<a href="javascript:scripture('../../scripture/john135b.htm')" target=_self class=pop>John 1:35b</a>), 
He will come a second time to reign and be exalted. Indeed, all creation 
will one day bow before Him and acknowledge that He is Lord 
(<a href="javascript:scripture('../../scripture/phil21011.htm')" target=_self class=pop>Philippians 2:10-11</a>).</p>
<p>Lord, for many folks, this is a lonely time of year. 
Father, I have to admit that I feel lonely at times too. I remember Christmases 
past and long to see loved ones who have departed from this life. While many of 
us are running around to party after party, some folks have few friends or 
family. O God, comfort the lonely. I especially remember the single, the widow, 
widower, the unhappily married, the divorcee, and the orphaned, abused or 
neglected. O God, reach out to these with Your love this Christmas.</p>
<p>This time of year also reminds me that the things of earth 
cannot satisfy. Though I can be excited for a short period over a fine gift, I 
remember that the greatest gift of all is the &quot;gift of God, which is eternal 
life through Jesus Christ our Lord&quot; (<b>Romans 6:23</b>).</p>
<p>Lord, I ask that Your Spirit would draw many to Jesus this 
holiday season. Open the eyes of the lost and help them realize that they need 
Jesus. Use church service sermons, religious programming, Your Word and the 
testimony of believers to draw them to Yourself, I pray. May we see a mighty 
harvest over the next few days, I pray.</p>
<p>My Father, I especially appreciate the ministries whose 
services are associated with Christmas. I thank You for Samaritan's Purse and 
the tens of thousands of Christmas boxes they deliver to the world's neediest 
places. O God, bless the children who receive these packages of love. Bless 
those who receive nothing! Bless Angel Tree as they seek to provide gifts to 
children whose parent is incarcerated. Bless the Salvation Army and local rescue 
missions throughout our world.</p>
<p>Now, Lord, I commit this day to You. May the Spirit of 
Christ within me help me to enjoy the true meaning of this holiday. And whether 
others understand or not, I can rejoice in God my Savior! Amen.</p>

