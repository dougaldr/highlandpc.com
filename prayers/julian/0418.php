<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 18
<br><span class=byln>by Ed Vasicek</p>

<p>Merciful God,</p>
<p>Lord, You are above all. This does not mean that You are 
physically elevated although that thought does communicate a bit of the 
concept. But You are above all in that no one outranks You, no one outpowers 
You, no one outperforms You in anything good and virtuous. All are accountable 
to You and You will bring every act into judgment. The buck truly stops with 
You. There is no court of appeals above You.</p>
<p>Thank You for the genealogy of our Lord Jesus Christ. In 
regards to His human nature, He descended from Israel, the tribe of Judah and 
the House of David. This was in accord with the prophecies You gave to the Old 
Testament prophets. His divine nature as the Son of the God has always been. 
The Son of God, like You Father, and the Holy Spirit have always been.</p>
<p>But Father, I now need to pray for myself. Lord, help me to 
bear up under the trials, disappointments, fears and tribulations of life 
including: __________.</p>
<p>Let me remember that You are developing my faith and that 
You often use difficult times to make me stronger in You. Our Lord Jesus Christ 
suffered and died for many reasons. Although we think of the transforming truth 
that He died in our stead to pay our penalty for sin, He also died to set an 
example for His followers. We too are sometimes called upon to suffer even 
though this is a difficult reality to embrace.</p>
<p>Lord, I pray for those seeking employment. Open doors of 
opportunity and match the right person to the right job. Especially help those 
who are willing to work with integrity and have good character.</p>
<p>Also, dear Father, help me to handle my finances well. 
Guide me when it comes to saving, giving and help me know when to say no. Help 
me control my money under Your guidance; please, do not let money control me. I 
remember that I cannot serve both God and money, yet if I am irresponsible with 
the use of my money, the resulting stress becomes my obsession. Help me head 
off this temptation.</p>
<p>I bring my prayer before You on behalf of the nation of 
Guadeloupe, an island nation in Central America. Thank You for the 5.4% who 
claim to be born-again Christians and thank You that the evangelical church is 
growing. The family unit has not yet recovered from the devastation of slavery 
in past centuries. Occultism is widespread. Please, Lord, help the believers 
to mature, the church to double over the next decade and provide for good 
leadership training. May Your Spirit do a mighty work in Guadeloupe.</p>
<p>Thank You for OC (One Challenge) International, &quot;an 
interdenominational faith mission. [Their] strategy is to target entire nations 
through the ministries of research, training, motivation and mobilization. With 
more than 380 missionaries, OC is involved in evangelism, discipleship, 
leadership training, church growth, missions and support services. &quot; O Father, 
grant them success as their mission is Your mission. Empower them by Your 
Spirit, grant them harmony among themselves, safety, wisdom, provision and may 
them make a deep impact for the Kingdom of God.</p>
<p>Now, Lord, help me remember You throughout the day, I pray 
in Jesus' Name, Amen.</p>


