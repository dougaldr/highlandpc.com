<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 22
<br><span class=byln>by Ed Vasicek</p>

<p>Compassionate God,</p>
<p>You are the Truth. As John tells us, You are light, and in 
You is not darkness at all. When God the Son became man, He declared Himself, 
&quot;...the way, the truth, and the life.&quot; Your Word says it is impossible for You to 
lie. Every Word of Yours is sure. Your integrity is unquestionable. You 
delight in us when we value truth, even if the truth sometimes seems unpleasant 
to us.</p>
<p>I pray for my lost friends and family members, including: __________. Father, 
please send Your Spirit to draw them to Yourself 
and grant them the repentance that leads to life.</p>
<p>Lord, I thank You that Jesus Christ was tempted in all 
points, as we are, yet was without sin. Jesus did not just live a good life, He 
lived a sinless life. Thus He could offer His life as a spotless Lamb without 
blemish. He lived the only sinless and perfect life. </p>
<p>Since You see me in Him, 
through Him I am credited with having met all the righteous requirements of the 
Law. Help me live out this righteousness in a practical way. 
Help me to become what You have declared I already am: righteous. Help my state 
(my actual behavior) match my standing (the legally perfect position I have 
because I am in Christ).</p>
<p>Lord, help me to consider others more important than 
myself. Help me to put myself in the shoes of others. Help me develop both 
sympathy and empathy.</p>
<p>Today I pray for the African nation of Chad. Thank You 
that the church is growing, and that about 14 percent of the population claims 
to be born-again. Yet more people claim to be Muslims, and the Muslims control 
much of this poor nation. We pray that the level of religious freedom people in 
Chad enjoy will remain, and that Your Spirit will continue to empower the many 
zealous believers there. Chad has more unreached people groups than any other 
nation in Africa. Bless the Jesus' Film and use it to reach many. May You pour 
Your Spirit out upon Chad.</p>
<p>I remember OMF in prayer. Founded by Hudson Taylor in the 
19<sup>th</sup> century, OMF, &quot;...is 
a global network of Christians proclaiming the glory of Jesus Christ among East Asia's 
peoples through fervent prayer, loving service and personal witness.&quot; 
Lord, bless the missionaries with harmony, safety, provision, and much fruit for 
their labors. Keep them close to You, and empower their efforts by Your Spirit.</p>
<p>I also remember those involved in the funeral business. 
Help them to be honest, kind, and compassionate as they serve people during 
times of grief. May many come to know you, and may those who do seek tactful 
opportunities to share Christ with their clients.</p>
<p>Now strengthen my faith today, I pray, in Jesus' Name, 
Amen. </p>

