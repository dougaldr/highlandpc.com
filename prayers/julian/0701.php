<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 1
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a no-nonsense God. You become angry with 
those Who give you mere lip service, as You explain, when &quot;people come near 
to me with their mouth and honor me with their lips, but their hearts are far 
from me&quot; (<b>Isaiah 29:13</b>). You are not a God Who wants image or impression; You 
want substance and heart-felt sincerity. You do not want our token efforts or 
half-hearted allegiance. You want us to love and serve You with all of our 
hearts. Your Son discouraged us from praying with useless repetition or a sheer 
volume of words 
(<a href="javascript:scripture('../../scripture/matt67.htm')" target=_self class=pop>Matthew 6:7</a>). 
You are only pleased with the simple and sincere. You are a no-nonsense God.</p>
<p>Lord, I remember those who have humble means. Others are 
fighting horrible illnesses, diseases, or constant pain. Others have gone 
through trials that defy description. O Father, shower Your grace upon such 
people. Reveal Yourself to them and provide for their needs. Bring people into 
their lives who can bless them, I ask.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________.</p>
<p>Lord, I remember that the trying of my faith works 
patience, and patience experience, and experience hope 
(<a href="javascript:scripture('../../scripture/rom534.htm')" target=_self class=pop>Romans 5:3-4</a>). 
You use trials in my life to develop me, to teach me, to try me, to discipline me and 
to make me strong, for when I am weak, then I am strong. Lord, sometimes this 
sounds more like theory than practice. I often do not appreciate this program 
You have to develop me. I wish You would consult me first for my opinion. But 
then I remember that You are God, the potter, and I am the clay. The only 
rights I have are the ones with which You endowed me. So, with Your help, I'll 
try to trust You, even when I cannot understand Your ways.</p>
<p>Lord, I pray for the Federal Republic of Nigeria. a rather 
large nation with over 100 million people. Thank You that 23.5% of those people 
claim to be born-again evangelicals. Thank You, Lord! On a sad note though, 
the northern part of the country is mostly Muslim and has a track record of 
persecuting Christians. But the Christians there are a praying people and You 
have been answering many prayers, but some of the 
northern states enforce Islamic law and still persecute believers. Thousands 
have died over the last 15 years because of persecution. We pray that the 
entire nation will experience religious freedom and we pray that many Muslims 
would come to know Christ. Lord, help the percentage of born-again evangelicals 
grow by 50% in the next decade, I ask.</p>
<p>Today I remember the missionaries my church supports. I 
pray that, if it is Your will, you would raise up individuals from our 
congregation to serve in full time ministry, be that missions, pastoral work, 
evangelism, or any of many ministry opportunities.</p>
<p>I pray for those who work as plasterers and stucco masons. 
Give them safety, help them do their jobs well and may those who know You share 
their faith freely by example and by word.</p>
<p>Now use me to serve You today, I pray in Jesus' Name, 
Amen.</p>

