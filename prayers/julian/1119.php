<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 19
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are the Beginning and the End. You started the events of history; they 
came to a crescendo when our Lord Jesus died on the cross as a curse for us 
(<a href="javascript:scripture('../../scripture/gal313.htm')" target=_self class=pop>Galatians 3:13</a>) 
and then rose again being boldly declared as the Son of God 
(<a href="javascript:scripture('../../scripture/rom14.htm')" target=_self class=pop>Romans 1:4</a>). 
But one day, the events of history will culminate in the return of 
Christ to reign and eventually a new heaven and a new earth will replace the 
current creation. And just as You were sovereign over eternity past, a time 
before time, we are now here to enjoy Your sovereign grace for an eternity 
future, a time after time. You are the first and the last, the beginning and the 
end, the alpha and omega.</p>
<p>Lord Jesus, You are the head of the Church, which is Your 
Body. Yet, Lord, church troublemakers are a constant blight to Your body. 
Unhealthy individuals who either want attention or who bare a grudge against 
authority or even a secret grudge against You create havoc in church after 
church. They rationalize their behavior and believe that anyone who opposes 
them is evil. They know how to manipulate people and often the silent majority 
refuses to stand up to them. Some go from church to church, while others are 
entrenched and make it uncomfortable for those who might begin gaining 
influence. It is sad, sick, unhealthy and devastating to pastors, their families 
and congregations.</p>
<p>Yet, Lord, this has always been a problem. I read in <b>3 John 
9-10</b> about Diotrephes: &quot;I wrote to the church, but Diotrephes, who loves to be 
first, will have nothing to do with us. So if I come, I will call attention to 
what he is doing, gossiping maliciously about us. Not satisfied with that, he 
refuses to welcome the brothers. He also stops those who want to do so and puts 
them out of the church.&quot; O Father, what constant plague troublemakers have been 
to Your church. Help me not to be one of them. And keep me from being part of 
the silent majority. Remind me that cowardice is a sin.</p>
<p>O God, encourage my heart. This world is constantly putting 
me down. Keep me from putting others down and help me stay positive, I request.</p>
<p>I pray for the nation of Laos. Thank You that a little more than one percent 
of the people claim to be born-again Christians. O Lord, double that number 
over the next two years, I pray. We pray that persecution might stop completely 
and that the anti-Christian government and private citizens would recognize 
these rights You give to all particularly the right of religious freedom. 
Despite the dangers and lack of trained leadership, thank You that the church is 
growing. May Your Spirit change hearts and draw even the government leaders to 
faith in Jesus, I pray. Help the Christians to be shrewd and able to avoid 
persecution. When they cannot, grant them the grace they need, I ask.</p>
<p>Lord, today is Yours. Remind me of that, I pray in name of Christ, Amen.</p>

