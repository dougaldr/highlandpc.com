<p>One of the most misunderstood portions of Scripture 
includes a few choice words Jesus spoke after His Ascension. We find them in 
<b>Revelation 3:15-16</b>: &quot;I know your deeds, that you are neither cold nor hot; I 
wish that you were cold or hot. So because you are lukewarm, and neither hot nor 
cold, I will spit you out of My mouth.&quot;</p>

<p>Without going into great detail, let me suggest that what 
Jesus was demanding did not have to do with extremes in dedication (although He 
does want us to be extremely zealous) as much as it addresses His demand that 
His followers refuse to conform to the ungodly values and trends that are 
peppered throughout society.</p>

<p>If you take a chilled Coke and plant it on the table for a 
few hours, it will approach room temperature. If you take a steaming cup of 
coffee and leave it out for a few hours, it conforms to room temperature. And 
that is the issue at hand: conforming to the world.</p>
<p>On one hand, the human tendency to follow the herd is a 
great asset. We are all better off because we live in a society within a 
developed culture, and much of this advancement can be traced to a cooperative 
spirit and teamwork. But, on the other hand, following the herd can sway us the 
wrong way if the herd is out of tune with God's desired will.</p>

<p>If we follow Christ, sometimes society will ostracize us, 
misrepresent us, and mock us. Jesus told His disciples in <b>John 15:19</b>, &quot;you do 
not belong to the world, but I have chosen you out of the world. That is why the 
world hates you.&quot; We see this demonstrated clearly today in Islamic, Hindu, and 
Communist societies.</p>

<p>Although American society is every bit &quot;the world&quot; as other 
societies, American values advocate tolerance and respect for religious views--to 
a degree. Although things are changing, American Christians still struggle 
more with conforming to the world than persecution.</p>

<p>In time, we can easily spoil our spiritual appetites and 
dismiss our primary identity as Christians. Perhaps a second job prevents us 
from attending church or reading the Word. Maybe we get wrapped up with hobbies 
or interests that absorb our Sundays. Or maybe we become movie addicts, watching 
questionable films and renting DVD after DVD. Yet we are not as dumb as we 
pretend to be: we know that the law of displacement will go into effect. If I am 
doing something, that means I am not doing something else. I would suggest that 
nine times out of ten, becoming lukewarm is a choice. It is an attitude/priority 
issue.</p>

<p>The story below describes the process. Soren Kierkegaard, 
the Danish theologian and philosopher, told this &quot;Parable of The Wild Duck of 
Denmark:&quot; </p>

<blockquote>
<p>A wild duck was flying northward with his mates across 
Europe during the springtime. En route, he happened to land in a barnyard in 
Denmark, where he quickly made friends with the tame ducks that lived there. The 
wild duck enjoyed the corn and fresh water. He decided to stay for an hour, then 
for a day, then for a week, and finally, for a month. At the end of that time, 
he contemplated flying to join his friends in the vast Northland, but he had 
begun to enjoy the safety of the barnyard, and the tame ducks had made him feel 
so welcome. So he stayed for the summer. </p>

<p>One autumn day, when his wild mates were flying south, he 
heard their quacking. It stirred him with delight, and he enthusiastically 
flapped his wings and rose into the air to join them. Much to his dismay, he 
found that he could rise no higher than the eaves of the barn. As he waddled 
back to the safety of the barnyard, he muttered to himself, “I’m satisfied here, 
I have plenty of food, and the area is good. Why should I leave?” So, he spent 
the winter on the farm. In the spring, when the wild ducks flew overhead again, 
he felt a strange stirring within his breast, but he did not even try to fly up 
to meet them. When they returned in the fall, they again invited him to rejoin 
them, but this time, the duck did not even notice them. There was no stirring 
within his breast. He simply kept on eating corn which made him fat.&quot; (Steve Malone, 
<a href="http://www.sermoncentral.com/" target="offline" class="reg">SermonCentral</a>)</p>
</blockquote>

<p>John warns us in <b>1 John 2:15</b>, &quot;Do not love the world or 
anything in the world. If anyone loves the world, the love of the Father is not 
in him. For everything in the world--the cravings of sinful man, the lust of his 
eyes and the boasting of what he has and does--comes not from the Father but from 
the world. The world and its desires pass away, but the man who does the will of 
God lives forever.&quot;</p>

<p>Whatever John means by the term &quot;world&quot; is not good. We 
generally understand the Greek word &quot;Kosmos&quot; to refer to the ordered world 
system. Perhaps the best definition would be &quot;society in its choice to ignore or 
violate God's Word.&quot;</p>

<p>In the past, evangelicalism produced its own &quot;competing 
society&quot; and defined &quot;worldliness&quot; for its constituents through a long list of 
rules. Yet--at least in the past--such definitions would lead us to accuse 
Jesus of being worldly. Just as Jesus did not meet the standards of the 
Pharisees, so He often would not meet the standards of modern legalists.</p>

<p>There is a tension between Jesus' desire for us to be in 
the thick of things (in the world), protected by God's power. &quot;My prayer is not 
that you take them out of the world but that you protect them from the evil 
one&quot; <b>John 17:15</b>.</p>

<p>Jesus &quot;came eating and drinking, and they say, 'Behold, a 
gluttonous man and a drunkard, a friend of tax collectors and sinners!'&quot; Jesus 
bucked the man-made rules of the Pharisees because God does not want us to 
isolate ourselves from lost society.</p>

<p>Paul wrote: &quot;I have written you in my letter not to 
associate with sexually immoral people--not at all meaning the people of this 
world who are immoral, or the greedy and swindlers, or idolaters. In that case 
you would have to leave this world. But now I am writing you that you must not 
associate with <i>anyone who calls himself a brother</i> but is sexually immoral 
or greedy, an idolater or a slanderer, a drunkard or a swindler. With such a man 
do not even eat&quot; (<b>1 Corinthians 5:9ff</b>).</p>

<p>On the other hand, when lost people have a bad effect on 
us, we need to part company. Paul also wrote in <b>1 Corinthians 15:33</b>, &quot;Do not be 
misled: 'Bad company corrupts good character.'&quot;</p>

<p>In the past, evangelical Christians isolated themselves 
from society. Now the pendulum has swung the other way as we notice Christians 
who feel free to do anything (rampant materialism, immodest dress, living 
together without being married, using God's Name in vain, getting drunk, smoking 
marijuana, etc.).</p>

<p>The Biblical balance requires us to monitor ourselves and 
compare our behavior and attitudes to God's Word; we can follow the trends of 
society that do not run contrary to Scripture, but we need to remember the 
&quot;Parable of the Wild Duck of Denmark.&quot; We need to monitor our hearts. After all, 
worldliness is an attitude. Jesus wants me to be hot or cold--different where 
it counts. He wants me to be engaged in society--not isolated. But He wants me 
to do so without compromising the truths of His Word. If we put our light under 
a bushel, how will others see it? If our flame is extinguished, how will we 
illumine others?</p>

