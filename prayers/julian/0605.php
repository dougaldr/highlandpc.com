<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 5
<br><span class=byln>by Ed Vasicek</p>

<p>My Father in Heaven,</p>
<p>Lord, You are the God Who is praised by Your creation. The 
sun, moon and stars exalt You. So do the waters, the deepsea creatures and 
all heaven 
(<a href="javascript:scripture('../../scripture/ps14837.htm')" target=_self class=pop>Psalm 148:3-7</a>). 
Although we cannot visit the depths of the sea or 
rocket to the highest heaven, these things exist to glorify You, for You alone 
see them all. And You will expound Your wonder to Your children, who will learn 
of You and the wonders of Your grace throughout eternity 
(<a href="javascript:scripture('../../scripture/eph27.htm')" target=_self class=pop>Ephesians 2:7</a>). 
So we say not only that everything that has breath should praise You, but also we know 
that even Your inanimate creation brings You glory.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I accept responsibility for my 
sins and freely bring the ones Your Spirit brings to my mind now: __________.</p>
<p>I pray for parents of children who have gone astray. 
Whether in the realm of premarital, extramarital, or perverted sex; drugs, 
alcohol, gambling, crime, violence, bitterness toward parents, abandoning the 
faith or in any other way. Lord, this hurts like nothing else can. Comfort 
parents and restore these wayward sons and daughters.</p>
<p>Lord, I pray for harmony and reasonableness within family 
relationships. Sometimes it is difficult to get along with in-laws, parents, 
grown children, siblings, or other family members. Some people contend with 
irresponsible or meddling family members and tensions can run high. Help me to 
be reasonable, but firm when I need to be. Help me not to allow others to walk 
all over me, yet let me exercise a reasonable compassion. Keep me from enabling 
others to continue in their addictions and sins, yet keep me from assuming the 
police-like stance. </p>
<p>Lord, help me to develop humility. You command me to humble 
myself before You, but You also want me to be humble in my dealing with others. 
Help me to not look out for only my own interests, but the interest of others.</p>
<p>Today I pray for the small European island-nation of Malta 
located in the Mediterranean. Thank You for the 1% of the population claiming 
to be born-again evangelicals. May that number double over the next decade. 
Thank You for the Maltese Bible translation. May people in this land hunger for 
You. Grant them the repentance that leads to salvation. Use the Christian radio 
station to bring in a harvest for Your kingdom, I ask.</p>
<p>Lord, thank You for Remnant Ministries whose mission is 
&quot;to evangelize Jews in this present age, to reach the 'remnant according to the 
election of grace' 
(<a href="javascript:scripture('../../scripture/rom115.htm')" target=_self class=pop>Romans 11:5</a>).
Our method is quality witnessing through personal 
contacts developing mutual trust and expressing authentic Christlike love. A 
Jewish believer is then discipled and directed to a local New Testament church.&quot; 
Bless their work in Cleveland&#8232;(main office and mission home), Cincinnati, Charleston, Huntington, WV, Sebring, FL, Phoenix, Brazil and 
Israel. Empower them by Your Spirit, give them harmony, provision and 
protection, I ask.</p>
<p>Help me to look for opportunities to share the Gospel 
today, I ask in Jesus' Name, Amen.</p>

