<p><b>Article X: Sanctification</b></p>
<p>
We believe that every true believer is perfect in God's eyes, even though he/she
continues to struggle with sin. With the power of the Holy Spirit, the
believer will grow in his/her walk as a new creature in Christ.<br>
(<a href="javascript:scripture('../scripture/titus11.htm')" target=_self class=pop>Titus 1:1</a>;
<a href="javascript:scripture('../scripture/eph2810.htm')" target=_self class=pop>Ephesians 2:8-10</a>;
