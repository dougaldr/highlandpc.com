<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Phenomenon of Double Fulfillment
<br><span class=byln>by Ed Vasicek</span></p>

<div>
<p>Many prophecies have two fulfillments, a near (in time) 
less literal fulfillment and a distant (in time) more literal fulfillment. This 
is not true of all prophecies, and often even the more literal fulfillment is 
not completely literal because most Old Testament prophecies (in the Psalms or 
the prophets) are in the poetic genre (i.e., Hebrew poetry is based upon meter, 
parallel thoughts, and frequently figurative language).</p>
<p><u>This can be exemplified by Pentecost.</u> <b>Acts 2:14ff</b> 
reads:</p>
<blockquote><sup>14</sup> But Peter stood up with the Eleven, raised his voice, and 
proclaimed to them: &quot;Jewish men and all you residents of Jerusalem, let this be 
known to you and pay attention to my words. <sup>15</sup> For these people are not drunk, as 
you suppose, since it's only nine in the morning. <sup>16</sup> On the contrary, this is 
what was spoken through the prophet Joel: <sup>17</sup> And it will be in the last days, 
says God, that I will pour out My Spirit on all humanity; then your sons and 
your daughters will prophesy, your young men will see visions, and your old men 
will dream dreams. <sup>18</sup> I will even pour out My Spirit on My male and female slaves 
in those days, and they will prophesy. <sup>19</sup> I will display wonders in the heaven 
above and signs on the earth below: blood and fire and a cloud of smoke. <sup>20</sup> The 
sun will be turned to darkness, and the moon to blood, before the great and 
remarkable day of the Lord comes; <sup>21</sup> then whoever calls on the name of the Lord 
will be saved.</blockquote>
<p>Note particularly <b>verse 19</b> regarding &quot;displays and wonders 
in the heavens.&quot; This did NOT occur at Pentecost. But the Spirit was poured 
out on Pentecost, yet not upon ALL humanity. So this prophecy was fulfilled <b>
in a limited way</b> on Pentecost. But it will be fulfilled <b>more literally</b> 
during the end times: the Tribulation and Millennium. During the Tribulation 
the sun WILL appear to be dark and the moon will appear to turn to blood 
(<a href="javascript:scripture('../scripture/rev612.htm')" target=_self class=pop>Revelation 6: 12</a>).
During the Kingdom Age (the Millennium) ALL will know the Lord and ALL 
will prophesy. </p>
<p><u>This phenomenon is true even regarding New Testament 
prophecy.</u></p>
<p><b>Matthew 16:27-28</b>&nbsp;<br>
<sup>27</sup> For the Son of Man will come in 
the glory of His Father with His angels, and then He will reward each according 
to his works. <sup>28</sup>&nbsp;<b>Assuredly, I say to you, there are some 
standing here who shall not taste death till they see the Son of Man coming in 
His kingdom.</b></p>
<p>The Gospels sequentially record the Mount of 
Transfiguration event<i>, </i> 
<b>a brief visit into the Millennial Kingdom Age</b>. 
Peter interprets this event as the “coming” of Christ, a near, less literal 
fulfillment:</p>
<p><b>2 Peter 1:16-18<br>
</b><sup>16</sup> For we did not follow 
cleverly devised tales when we made known to you the power and coming of our 
Lord Jesus Christ, but we were eyewitnesses of His majesty. <sup>17</sup> For 
when He received honor and glory from God the Father, such an utterance as this 
was made to Him by the Majestic Glory, &quot;This is My beloved Son with whom I am 
well-pleased&quot;— <sup>18</sup> and we ourselves heard this utterance made from heaven when we 
were with Him on the holy mountain.</p>
<p>Yet the New Testament leaves us awaiting the literal 
Kingdom Age, the permanent coming of Christ, and a distant, more literal reign 
of Christ.</p>
<p><u>The New Covenant is another example. In <b>Jeremiah 
31:31-34</b>, we read:</u></p>
<p><sup>31</sup> &quot;Behold, days are coming,&quot; declares the L<span class=sm>ORD</span>, &quot;when I will 
make a new covenant with the house of Israel and with the house of Judah, <sup>32</sup> not 
like the covenant which I made with their fathers in the day I took them by the 
hand to bring them out of the land of Egypt, My covenant which they broke, 
although I was a husband to them,&quot; declares the L<span class=sm>ORD</span>. <sup>33</sup> &quot;But this is the covenant 
which I will make with the house of Israel after those days,&quot; declares the L<span class=sm>ORD</span>, 
&quot; I will put My law within them and on their heart I will write it; and I will 
be their God, and they shall be My people. <sup>34</sup> They will not teach again, each man 
his neighbor and each man his brother, saying, 'Know the L<span class=sm>ORD</span>,' for they will 
all know Me, from the least of them to the greatest of them,&quot; declares the L<span class=sm>ORD</span>, 
&quot;for I will forgive their iniquity, and their sin I will remember no more.&quot;</p>
<p>Christ instituted the New Covenant at the Last Supper, and 
the ministry of the Gospel is the ministry of the New Covenant 
(<a href="javascript:scripture('../scripture/2cor36.htm')" target=_self class=pop>2 Corinthians 3:6</a>). 
Yet the Jeremiah passage tells us that all Israel will know the Lord. This has 
never yet been the case. </p>
<p>So the near fulfillment applies only to a remnant of Israel 
(and gentiles believers who are blessed because they are connected to Israel), 
but the future fulfillment will be the more literal one when all Israel is 
converted at the end of the Tribulation (<b>Zechariah, chapters 12-14</b>).</p>
<p><u>The same is true with the Virgin Birth of Isaiah 7:</u></p>
<p><sup>10</sup>&nbsp;Moreover the Lord spoke again to Ahaz, 
saying, &quot;Ask a sign for yourself from the Lord your God; ask it either in the 
depth or in the height above.&quot; <sup>12</sup>&nbsp;But Ahaz said, &quot;I will not ask, 
nor will I test the Lord!&quot; <sup>13</sup>&nbsp;Then he said, &quot;Hear now, O house of 
David! Is it a small thing for you to weary men, but will you weary my God 
also? <sup>14</sup>&nbsp;Therefore the Lord Himself will give you a sign: Behold, 
the virgin shall conceive and bear a Son, and shall call His name Immanuel. <sup>15</sup>&nbsp;Curds and honey He shall eat, that He may know to refuse the evil 
and choose the good. <sup>16</sup>&nbsp;<b>For before the Child shall know to 
refuse the evil and choose the good, the land that you dread will be forsaken by 
both her kings.</b></p>
<p>The idea was that King Ahaz need not fear the kings of 
Damascus and Samaria because before a virgin could get married, conceive, and 
rear a son to age two, those kings would no longer be an issue. Within less 
than three years of this prophecy, the Assyrians conquered Damascus and Samaria, 
removing the dual threats to Judah. So the sign was a sign <b>to King Ahaz at 
that time</b>, a NEAR, less literal fulfillment.</p>
<p>The distant, <b>more literal fulfillment relates to Jesus 
Christ</b> who was literally conceived while Mary was YET a virgin, and who was 
not just called “Immanuel” but literally was “God with us.”</p>
<p>If you agree with this principle, this will lead you into 
an interpretative paradigm known as &quot;Progressive Dispensationalism.&quot;</p>
<p>Progressive Dispensationalism is a literal way to approach 
the Scriptures that recognizes a future Tribulation and literal Millennium in 
which Christ rules from Jerusalem and the nation of Israel is exalted (like 
traditional Dispensationalism) but also recognizes that God's present dealings 
with the church and Israel are the less literal fulfillment of those 
prophecies. For example, the conversion of the gentiles is often predicted in 
the Old Testament and will find its fuller, more literal fulfillment during the 
end times, but finds its less literal, near fulfillment right now as gentiles 
come to faith within the church.</p>
