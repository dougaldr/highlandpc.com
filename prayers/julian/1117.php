<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 17
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>Thank You that Your Son, our Savior, the Lord Jesus Christ, is the way, the 
truth and the life. He is the way to You, the way to forgiveness, the way to 
eternal life. He is not just <u>a</u> way, but <u>the</u> way. As He said, &quot;no 
one comes to the Father but by me&quot; (<b>John 14:6</b>). We do not walk down a 
path (a way) of rituals and achievements—no, Jesus Himself is the way. He is the 
truth; His Gospel is the only true Gospel. And He is the life; all other &quot;ways&quot; 
lead eventually to death. But His way leads to life, an eternal life that begins 
now and extends forever and ever.</p>
<p>Lord, help me to rest in You and in Your truth. I do not want to be tossed to 
and fro by every wind of doctrine. I need to carefully develop convictions from 
Your Word and stick to them. I do not want to be among those Jude calls, &quot;clouds 
without rain, blown along by the wind; autumn trees without fruit and 
uprooted...wandering stars&quot; (<b>Jude 12,13</b>). Help me be settled in You.</p>
<p>You are the God Who sees. When Hagar was mistreated by 
Sarah and then cast out with her son, Ishmael, into the desert, You saw Hagar’s 
plight and helped her and her son. It was tough for her to be a single mom, but 
You were the God Who was there to help her. </p>
<p>O Father, help single moms everywhere to turn to You and 
surrender their lives to You. Provide them with the means, strength, health, 
provision and wisdom to rear their child or children alone, I pray. Whether 
these moms have always been single, are divorced, separated, widowed, or 
temporarily single by virtual of circumstance (husband working in another 
country, military duty, etc.), watch out for them, O Lord.</p>
<p>Lord, I pray for those in pain. Lord, life is rough enough when we are 
feeling healthy, but pain drains us of our energies and affects our disposition. 
Sometimes pain is a long-term taskmaster that will not go away. Sometimes pain 
is emotional, the devastation of our spirits and the breaking of our hearts. O 
God, help me in my pain and help those I know who are suffering, 
I pray.</p>
<p>Today I pray for one of the nations of the former Soviet Union, Azerbaijan, 
just north of Iran. Lord, most of this nation is Islamic with less than 
one-tenth of one percent Evangelical Christian. Although there is some measure 
of religious freedom, we know the nation allows no foreign missionaries. This is 
a poor land and people need Jesus. We pray that You would multiply the efforts 
of the few Christians who live there. We pray that the church would grow 
exponentially and that Your Spirit would draw great numbers to Jesus Christ.</p>
<p>Today I remember AMF, the American Messianic Fellowship. Bless their efforts 
in sharing the Gospel &quot;to the Jew first&quot; as they build bridges of friendship. 
Thank You that this ministry is conducted ethically and honestly. Bless them as 
they reach out and train others to reach out to Your chosen people.</p>
<p>Father, help me to love people of all races and backgrounds, I ask in the 
Name of the Messiah, Amen.</p>

