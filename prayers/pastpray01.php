<p id="pgt"><span class="pgh">Sunday Morning Pastoral Prayers:</span>
<br>Prayer #1
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Gracious God,</p>
<p>We are awestruck by You. All created things had a beginning, but You are uncreated; there has 
never been a time before You were. You created time and are 
not restricted by it. You are self-sufficient. You do not 
need us, but You enjoy us and love us. But we need You, for 
apart from a relationship to You, existence has no enduring meaning.</p>
<p>Father, we thank You for Your 
Word which lights our path. Although we can find pieces of 
truth here and there, only Your Word is breathed by You. And 
so our Bibles are the only infallible source of truth. Help 
us, O Lord, to cherish and study it so that we might know You better and serve 
You in both knowledge and zeal.</p>
<p>Lord, we value the many people 
who enrich our lives. We especially remember those who serve 
our government at a state level. We pray for our governor, 
our state senators and representatives, and our state judicial system. We pray for the leaders and workers who serve within our state’s 
agencies. Help them to pass laws that meet with your 
approval, and help them wisely and properly administer those laws. We pray that we might be at peace with them, and that you would guide and 
bless their work and their families.</p>
<p>Our church family includes 
many individual families. And All-Knowing One, we know that 
You know how stressful some of our family situations are. We 
pray that You would shower grace, blessing, guidance, and wisdom to our 
families. We ask that You would help them stay in tact; help the relationships 
within them to be nurturing, positive, and godly.</p>
<p>Our God, we are mindful of our 
brothers and sisters in Christ who are suffering persecution. Many are suffering because of oppressive Islamic governments; others are 
victims of left-wing societies. We pray that the persecution 
might stop, that the persecutors would be won to faith in Jesus, and that the 
believers would be given supernatural wisdom so as to circumvent persecution 
when possible. Help them to be strong in You.</p>
<p>Closer to home, Lord, we know 
people who need Christ. Use us to reach out to the lost, and 
may our church be used of you to bring in some of the harvest, for the fields 
are white.</p>
<p>Minister, we pray, to our 
personal needs, including spiritual needs, health needs, relational needs, 
financial needs, or a host of other needs. We thank You that 
care about the details of our lives, and we thank You that we can trust You in 
times both good and bad.</p>
<p>And so Lord, it is with 
confidence that You have heard us which makes us want to pray in Jesus’ Name, 
Amen.</p>
