<p>Jesus 
told two parables that have related lessons: 1) The Friend Who Drops in At 
Midnight 
(<a href="javascript:scripture('../../scripture/luke1158.htm')" target=_self  class=pop>Luke 11:5-8</a>), 
and 2) The Unjust Judge 
(<a href="javascript:scripture('../../scripture/luke1818.htm')" target=_self  class=pop>Luke 18:1-8</a>). 
In both of these parables, we are told to imitate the persistence of the characters 
involved. Like the man in the first parable who hounded his neighbor until he 
got out of bed and provided him with bread, so we are to persist in prayer. 
Like the widow in the second parable who hounded the judge until he gave into 
her demands, so we are to persistently petition God in prayer.</p>
<p></p>
<p>
Although the New Testament was written in Greek, Jesus spoke Hebrew (or the 
related Aramaic, or, most likely, a combination of the two). The Greek work 
translated as &quot;persistence&quot; is more than likely a translation of the Hebrew 
word, <i>chutzpah</i>.</p>
<p></p>
<p>Brad 
Young, in his work, <u>Jesus, the Jewish Theologian</u>, comments on the meaning 
of <i>chutzpah</i>:</p>
<p></p>
<p>&quot;The 
word <i>chutzpah </i>is difficult to define in a single word. It means 
headstrong persistence, brazen impudence, unyielding tenacity, bold 
determination, or what in current English terms might be referred to as raw 
nerve. Can faith be described by the Hebrew word <i>chutzpah</i>?&quot; Young's 
answer is &quot;yes.&quot;</p>
<p></p>
<p>When 
we think of the nature of God, we are reminded of two realities: 
<ol><li>We can 
grasp some of what God is like because He is somewhat like us; we are created in 
His image, and, like Him, we have intellect, emotion and will. We are capable 
of a low-level of creativity (we recombine what He has created), we value 
relationships, and we have a spiritual element that is a key component of our 
identity. 
<li>We are unlike Him. He is completely pure, separate from His 
creation, and possesses all positive human attributes to an infinite degree (He 
is all knowing, all loving, all-powerful, etc.), obviously without any taint of 
sin.</ol>
<p></p>
<p>But 
when it comes to the matter of <i>chutzpah</i>, we differ. We dislike it when 
someone pesters us, is persistent, and doesn't let things go. It drives us 
crazy and often we avoid such people. But God enjoys it when His children 
persist. When a believer (in fellowship with God) engages in prayer, &quot;Zoom&quot; 
the doors to God's throne room are cast open instantly. No appointment is 
necessary. If we know Jesus, we have an immediate audience. And God invites us 
to reach out and express audacious <i>chutzpah</i>. Some believers reject this 
entire concept because, while they recognize where God and we are similar, they 
fail to recognize how we differ.</p>
<p></p>
<p>
Abraham is the ultimate example when he bargained with God 
(<a href="javascript:scripture('../../scripture/gen182332.htm')" target=_self  class=pop>Genesis 18:23-32</a>). 
It took nerve, <i>chutzpah</i>, to do that. Jacob wrestles with God in 
<a href="javascript:scripture('../../scripture/gen322232.htm')" target=_self  class=pop>Genesis 32:22-32</a>. 
And David hounds God with his complaints throughout the 
Psalms. When directed toward God, <i>chutzpah</i> is a Christian virtue! In 
all cases, this bold determination was never disrespectful, just persistent. 
So the writer to the Hebrews tells us to approach God's throne &quot;with confidence&quot; 
(<a href="javascript:scripture('../../scripture/heb416.htm')" target=_self  class=pop>Hebrews 4:16</a>); 
some versions say, &quot;boldly.&quot; Maybe if he had written in Hebrew, 
the Spirit may have directed him to choose the word, <i><u>chutzpah</u></i>. We 
simply can't pester God too much.</p>

