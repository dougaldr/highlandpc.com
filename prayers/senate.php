<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>Opening the Indiana State Senate on Feburary 12, 2004
<br><span class=byln>Written and prayed by Pastor Ed Vasicek</span></p>

<p>Our Father in Heaven,<br>
&nbsp;<br>
We come to you in humility, Lord, because we remember that we are but men and
women. Our knowledge is tiny in comparison to your infinite knowledge. Our
power is trivial compared to your complete and total power. Our level of
compassion, wisdom, and projection of the future is flawed; yours is without
bounds.<br>
&nbsp;<br>
So we ask You, our God, to guide the men and women of the Senate. Though our
citizens have selected them to serve, You are the one Who ultimately and
sovereignly controls the course of human history. Like our forefathers before
us, we must trust in Your Divine Providence.<br>
&nbsp;<br>
So we ask that you give each senator and each staff member clarity of mind,
wisdom, and the ability to put your will and the best interests of the people of
Indiana above all else.&nbsp;&nbsp;We pray that an attitude of goodwill would embrace this
chamber. Help the Senate to be a collective whole, at least in attitude and
motive. Where there is disagreement as to what is in the best interest of the
people of Indiana, help there to be an appreciation for sincerity of motive. We
pray that the ultimate decisions made in this chamber would be those decisions
meeting your approval.<br>
&nbsp;<br>
Where solutions seem impossible, make a way. When our legislators walk along
new paths or seek to meet truly unique challenges, light the way. When obvious
solutions are not really solutions, expose their inadequacy. When compromise
and concession is the right road, may all experience a sense of satisfaction.<br>
&nbsp;<br>
We pray that You would not only guide in the legislative decisions of each
Senator, but that You would be the help and pillar of each man and woman in
daily life. Bless each with good health, strong families, deep relationships,
balanced lifestyles, and integrity of character. Help them cast the weight of
their responsibilities upon You, the Great Burden Bearer, and may You play a key
role in each life.<br>
&nbsp;<br>
We pray in Jesus’ Name, Amen.</p>

