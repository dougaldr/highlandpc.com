<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 15
<br><span class=byln>by Ed Vasicek</p>

<p>Loving Creator,</p>
<p>You have so many qualities and none of them exist in 
isolation. You are a compassionate God, a gracious God and an angry God. But 
one quality we often overlook is that You are a logical God. True, You are not 
all reason; You are also emotional and relational. But You are nonetheless 
logical. Your thinking is sound and coherent. You are lucid and reasoned. </p>
<p>That is why it is so important for me to cultivate my 
mind. You want me to renew my mind, fill it with good things and to develop 
it, not check it out at the door. Lord, may Your logic motivate me to cultivate 
logical thinking. Since You define pure logic in the way You do things, I can 
be like the Psalmist is <b>Psalm 119:99</b>, &quot;I have more insight than all my teachers, 
for I meditate on your statutes.&quot;</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I accept responsibility for them and freely 
bring the sins Your Spirit brings to my mind now: __________.</p>
<p>Please meet the following needs, requests and desires as 
You see fit: __________.</p>
<p>Lord, Your Word teaches this proverb: &quot;Plans fail for lack 
of counsel, but with many advisers they succeed&quot; (<b>Proverbs 15:22</b>). Although this is 
a principle and not a promise, it therefore holds true most of the time. So 
help me to get plenty of advice from godly people before making major 
decisions. Keep me from being impulsive and help me recognize how much I need 
other believers and how much they need me!</p>
<p>Today I pray for the ancient nation of Greece, the Hellenic 
Republic. Father, it is so sad that only .4 of one percent of the population 
is made of born-again evangelical Christians. Because of its long history of 
domination by the Turks, the Greek culture is famous for being xenophobic. 
(Since most Greeks are 
firmly connected to the Greek Orthodox church, they are noted for intolerance of 
other forms of Christianity.) As a result, evangelicals suffer discrimination 
and informal persecution. Oh, God, may Your Spirit humble the pride of this 
people and may many come to a personal relationship with Jesus Christ and know 
the blessings of salvation by grace through faith alone and the assurance of 
salvation that accompanies it.</p>
<p>Lord, Mission Aviation Fellowship is a crucial ministry 
that provides vital support to missionaries around the world. For &quot;six full 
decades...vast numbers of people in remote areas of the world have gained access 
to essential spiritual and physical assistance. Mission Aviation Fellowship is 
privileged to provide aviation, information technology and learning technology 
services to nearly 600 Christian and humanitarian organizations worldwide.&quot; 
Lord, provide protection, safety, support and blessing to this ministry. Thank 
You for the great work they do and prosper this ministry, I pray, to Your 
glory.</p>
<p>Now, Father, help me glorify You by my attitudes today, I 
pray in Jesus' Name, Amen.</p>

