<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 17
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Almighty One,</p>
<p>Father, You gather your sheep like a shepherd. We are Your 
flock. The Lord Jesus declared Himself to be the Good Shepherd, the One Who 
laid down His life, only to take it up again. Unlike a hired hand who flees 
when the wolf approaches, the Good Shepherd sacrifices Himself to preserve the 
sheep. As an Omnipotent Shepherd, You hold us in Your hand, and no one is 
powerful enough to remove us. You watch over us, You guide us with Your rod and 
staff. You lead us in the way we should go.</p>
<p>Thank You, 
Holy Spirit, that You have baptized me into the Body of Christ. I am united 
with my fellow believers around the globe, those who genuinely know Christ.</p>
<p>Help me, 
therefore, Holy Spirit, to allow You to fill me. I give You control of my 
life. I understand that there is only one baptism of the Spirit, but many 
fillings. Indeed, You command me to &quot;be being filled with the Holy Spirit.&quot;</p>
<p>Lord, we 
humans beings are complicated, and our lives are sometimes tangled webs of 
responsibilities and duties. We wear many hats. Help me, Father, to take life 
in stride. Help me to cast my cares on You, for You care for me.</p>
<p>Lord, I have 
some disappointments and burdens in life. A few of them hurt me and have left 
scars, including: __________. I turn them 
over to You. Help me to find Your purpose and joy in life. There are many 
things I cannot change, but I can share all my thoughts with You. Thank You 
that You do not stifle me or pressure me to &quot;get over it.&quot; You are a God Who 
feels with me.</p>
<p>Today, Lord, 
I remember the nation of Cape Verde Islands, a small island nation off the coast 
of West Africa. Although most of the inhabitants claim some form of 
Christianity, only between 5 and 10 percent claim to be born-again. We thank 
You that the evangelical church is growing, and we pray that the number of 
believers there would double over the next five years. Help the believers to 
give up native superstition, and to develop strong leadership within the 
church. Many Caboverdians are migrants in other nations, including the U.S. We pray that these communities would become strong evangelical bastions for You.</p>
<p>Father, I 
pray for Liebenzell Mission as they plant churches, evangelize, and offer 
theological training for church leaders throughout Africa, Asia, Europe, and, to 
a lesser extent, South America. Help the many missionaries to get along well 
with one another, to agree upon strategies, and to be empowered by Your Spirit 
for their vital work. Help their families to be strong, healthy, and provide 
what they need, we pray. May their ministry be fruitful.</p>
<p>Today we pray 
for our nation's journalists. May an epidemic of fairness and understanding 
sweep our media, and may many come to know You.</p>
<p>Now bless my 
day, I pray, in Jesus' Name, Amen.</p>

