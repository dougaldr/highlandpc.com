<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 4
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Father,</p>
<p>Thank you that God the Son, Who later became Jesus the 
Messiah, has always been. He, along with the Holy Spirit, was with You from 
eternity past. He had no beginning. All creation came about through Him—without 
exception. He is the Light of the World. When He came down to earth 
that first Christmas, He came for a purpose: to save His people from their sins, 
as the angel told Joseph. But though He came to earth to visit the human race, 
most of His own people, the people of Israel, failed to recognize Him as the 
promised Messiah.</p>
<p>But many did and along with others from a variety of 
people groups, those Who received Him were given the status of &quot;children of 
God.&quot; These true believers did not receive acceptance from God by their own 
efforts or decisions—no, Lord, You worked faith in their hearts; You gave them 
new birth.</p>
<p>And so as we progress into the Christmas season, help me 
reflect upon the wonder of the incarnation: that God became man! Lord, I thank 
You that I am among those who believe in the Lord Jesus Christ as Your Word 
reveals Him!</p>
<p>Lord, I am thankful for many things. Two things that come to mind are: __________.</p>
<p>Lord, I pour myself out before You so that You may fill me with Your Holy 
Spirit. I find my hope in His power and dare not trust my own. May Your Spirit 
completely influence me today, I ask. Help me realize that nothing on earth can 
meet the hunger and longings of my heart; it is only in heaven that I will be 
completely satisfied. I agree—as far as ultimate longing goes—with the psalmist 
in <b>Psalm 73:25</b>, &quot;Whom have I in heaven but you? And earth has nothing I 
desire besides you.&quot;</p>
<p>Lord, help my church be harmonious. May the leaders be firm when it comes to 
the clear teachings of Scripture and gracious about the not-so-clear matters. 
Keep antagonists, trouble-makers and those who resent authority away from our 
congregation. Help my church to be effective in evangelism, discipleship, loving 
one another and world missions.</p>
<p>Dear God, remind me that my obedience does not bring acceptance with You; You 
accept me in Christ. My obedience brings You pleasure and glorifies Your Name. 
My disobedience incites Your anger and brings You displeasure. Remind me that I 
exist to glorify You!</p>
<p>Father, I pray for the African nation of Mali. Most of the people there are 
Muslim. Thank You for the growth we saw in the 1980s, but things screeched to a 
halt in the 1990s. O Lord, we pray that future growth would surpass what 
happened in the 1980s. </p>
<p>Lord, bless the work of Voice of the Martyrs as they seek to: empower 
persecuted Christians, give relief, evangelize the persecutors, do projects of 
encouragement and inform the world about the problem of persecution. O God, 
protect, provide, empower and grant wisdom and success to this ministry, I beg, 
O Lord.</p>
<p>Empower me, protect me and provide for me this day, in Jesus’ Name, Amen.</p>

