<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 4
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You have made for “yourself a glorious name” (<b>Isaiah 
63:14</b>). When You divided the waters of the Red Sea, when You delivered 
Israel, indeed when You resurrected Jesus Christ from the dead, You made Your 
might and power known. Your Name is awesome and to be revered. You, O Yahweh, 
do great works for Your Name’s sake. Your fame will one day extend from shore 
to shore and all creation will acknowledge Who You are and the awesomeness of 
Your Name.</p>
<p>Lord, I am thankful for many things. Two things that come 
to mind are: __________.</p>
<p>Lord, I pour myself out before You so that You may fill me 
with Your Holy Spirit. I find my hope in His power and dare not trust my own. 
May Your Spirit completely influence me today, I ask. Help me realize that 
nothing on earth can meet the hunger and longings of my heart; it is only in 
heaven that I will be completely satisfied. I agree—as far as ultimate longing 
goes—with the psalmist in <b>Psalm 73:25</b>, “Whom have I in heaven but you? And 
earth has nothing I desire besides you.”</p>
<p>Lord, help my church be harmonious. May the leaders be 
firm when it comes to the clear teachings of Scripture and gracious about the 
not-so-clear matters. Keep antagonists, trouble-makers and those who resent 
authority away from our congregation. Help my church to be effective in 
evangelism, discipleship, loving one another and world missions.</p>
<p>Dear God, remind me that my obedience does not bring 
acceptance with You; You accept me in Christ. My obedience brings You pleasure 
and glorifies Your Name. My disobedience incites Your anger and brings You 
displeasure. Remind me that I exist to glorify You!</p>
<p>Father, I pray for the African nation of Mali. Most of the 
people there are Muslim and the nation has its share of problems and a 
number of people groups without the Scriptures in their language. Thank You for 
the growth we saw in the 1980s, but things hit a brick wall in the 1990s. O 
Lord, we pray that future growth would make the 1980s pale in comparison. Only 
.8 % of the population claim to be born-again evangelicals. Lord, multiply that 
number fivefold over the next decade. Guide the Bible translators and draw many, 
many people to Yourself, I pray. Use literature, cassette tapes and the Jesus 
film to reach hundreds of thousands, I ask. Please maintain the freedom of 
religion now present in this land.</p>
<p>Lord, bless the Gospel Mission of South America. “Founded 
in 1923 by William M. Strong, Sr. as a means of evangelizing the military in 
Chile, Gospel Mission of South America continues to enlist dedicated men and 
women who will go, pray and give to enlarge the work. Supplication, service and 
sacrifice are needed so that the message of redemption might be presented 
through the power of the Holy Spirit.” Bless their work in Argentina, Chile 
and Uruguay. May they successfully plant churches and make many disciples of 
Jesus Christ. Grant them harmony among themselves, the power of Your Spirit 
and protect them and provide for them, I request.</p>
<p>So I likewise ask You to empower me, protect me and 
provide for me this day, in Jesus’ Name, Amen.</p>

