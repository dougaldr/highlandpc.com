<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 12
<br><span class=byln>by Ed Vasicek</p>

<p>Father in Heaven,</p>
<p>Thank You for hearing Your children on earth! I look 
forward to joining my spiritual brothers and sisters in heaven along with 
mighty angelic armies as I enjoy Your presence, fellowship and glory forever.</p>
<p>God the Son, Lord Jesus, You are my one and only Mediator. 
Your Word says that there is only one mediator, and that is You 
(<a href="javascript:scripture('../../scripture/1tim25.htm')" target=_self class=pop>1 Timothy 2:5</a>). 
I need no other. Through You I approach the throne; I have direct 
access.</p>
<p>Lord, I know temptation is part of the Christian life 
although You encourage us to pray that we be not lead into temptation. Instead 
we ask You to deliver us from the evil one. Father, between the devil’s legion 
and my personal sin nature, temptations can pull at me. Help me to remember 
that it is not a sin to be tempted, for even Jesus was tempted. But it is a sin 
to give into temptation. So help me to resist temptation and turn my attention 
to other matters. If I have started down the road of sin, remind me that the 
earlier I turn back the better. And if I have sinned, help me find the 
cleansing that comes from confessing my sin to You 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>).</p>
<p>Great One, help me to learn some simple lessons from the ant 
(<a href="javascript:scripture('../../scripture/prov66.htm')" target=_self class=pop>Proverbs 6:6</a>). 
The ant plans in advance and looks ahead. He does not wait 
for the winter, but stores up food in anticipation of it. The ant is 
industrious and works hard. Help me to develop those similar traits that I 
might be a truly wise person.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>I pray today for the Eastern European nation of Moldova. 
Like many of the nations formerly under Russian communism, Moldova is a poor 
country. Thank You that 3.3% of the population claims to be born-again 
Christians, but their work is often hindered by the dominant Orthodox church. 
Father, guide in the training of leaders, provide missionaries and help the 
churches to multiply. May the percentage of believers double over the next five 
years. May Your Spirit draw many to Christ.</p>
<p>Lord, thank You for the work of World Team Mission. They are focused upon 
the “10/40 Window,” that part of the world where the Gospel has barely 
penetrated. “Two billion people in our world are considered 'unreached'. They 
are isolated from the Gospel because of language, culture, geography, social, or 
political circumstances. They are waiting to hear the Gospel communicated in a 
culturally appropriate manner. [Their] desire is to make His name known among 
those who have never heard.” Give them harmony, provision, safety, wisdom, 
personnel and the power of Your Spirit, I ask.</p>
<p>So, Lord, I give this day to You. I do so in Jesus’ Name, 
Amen.</p>

