<p>Lord,</p>
<p>You move in the hearts of people to accomplish your sovereign will. 
During an election, every candidate feels she or he is the best choice, or they 
would not be running for office. Sometimes You do not always want the best 
person to win, because your purposes and plans are very complex. We would like 
to believe that most of the time, however, You do want the more ethical and 
wiser individual to serve. You want your children to vote wisely, with 
conviction. We pray, Lord, that if I am the best candidate, and if it is Your 
sovereign will, that you would allow me to serve my constituents by being 
elected. But what is most important is Your will, not mine. And that is what I 
ask for. Help me to do my part with enthusiasm, and then to rest in Your hands, 
I pray, in Jesus' Name, Amen.</p>


