		<p>Begins with curiosity (1-3)</p>
<p><img border="0" src="../../images/dezdisp/horeb.jpg" width="310" height="194"></p>
<p>Horeb is also called Mt. Sinai.</p>
<ol type=1><li><p>&quot;Says <sup>&nbsp;</sup>R. Eliezer*, from the day 
the heavens and the earth were created, the name of this mountain was called 
Horeb; but after the holy blessed God appeared to Moses out of the midst of the 
bush, from the name of the bush '(Seneh)', Horeb was called Sinai.'' [*Pirke 
Eliezer, c. 41. Aben Ezra in Exod. iii. 2. quoted by John Gill]</p>
<li><p>One ancient Rabbi sees the Bush's burning as both 
symbolic of God's holiness and God's awareness of the Hebrew' plight: Aben 
Ezra has this note on 
(<a href="javascript:scripture('../../scripture/ex32.htm')" target=_self class=pop>Exodus 3:2</a>):</p>
<p>&quot;the enemy is compared to fire, and Israel to the bush, wherefore it was not 
burnt.&quot;</p>
<li><p>Walter Kaiser writes:</p>
<p>“What took place was a 'strange sight' (v. 3) to Moses. 
Therefore, to explain what happened here as a temporary mirage of reflected 
sunlight on some red leaves or a campfire of some Bedouin...is to substitute our 
experience for Moses' forty years in that area and his estimate that it was 
indeed unusual. The burning bush was not consumed; that was the miracle. Notice 
how miracle is used here, as it so typically is in Scripture, to accredit God's 
message (or messenger). Miracles are not circus side shows intended to 
entertain; rather they accredit the Word of God given to his special 
messengers.&quot; (from <i>Expositor’s Bible Commentary</i>)</p></ol>
<li><p>Moses hears the bush's call. (4)</p>
<p>Angel of the Lord is the Lord, as most of the Rabbis understood.</p>
<p>Years later, just before his death, Moses blesses each of the twelve tribes of 
Israel. Of Joseph, he said, &quot;About Joseph he said: 'May the L<span class=sm>ORD</span> bless his 
land...with the best gifts of the earth and its fullness and the favor of him 
who dwelt in the burning bush.&quot; <b>Deuteronomy 33:13-16</b> <i>passim</i>)</p>
