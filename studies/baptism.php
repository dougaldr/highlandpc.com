<li><p class="bighpc"><a name="appendix4">Appendix 4: The Integrity of a Written Doctrinal 
Position</a></p>
<p>When so many people and 
groups claim to believe the Bible, their claim tells us little. For example, 
Jehovah Witnesses, Mormons, and Seventh Day Adventists all claim to believe and 
align themselves with the teaching of Scripture.</p>
<p>If we are to truly 
discuss and even challenge one another's convictions, it is important to be 
upfront about them. This is only one reason why a written doctrinal statement 
makes sense. It is not enough to say, "I believe the Bible." That communicates 
very little. When I hear something like that, I think, "These folks are so 
embarrassed about what they believe or uncommitted to a set of beliefs, or they 
have such little unity that they want to conceal their beliefs."</p>
<p>Although this may not 
always be true, it often is the case. Written doctrinal statements are helpful 
communication tools, and they proclaim, "When we say we believe the Bible, we 
have concluded that the Bible teaches…."</p>
<p>We all have to draw the 
line as to where heresy begins. Better to do so upfront with a written 
statement than through a silent code. It is a matter of integrity.</p>
<li><p class="bighpc"><a name="appendix5">Appendix 5: The Nature of Sin: Sins of Commission, 
Sins of Omission, and the Deeply Rooted Problem--Deceitful and Desperately 
Wicked Hearts</a></p>
<p><i><u>Why Arminianism 
must take a shallow view of sin.</u></i></p>
<p>The viewpoint that we 
have a completely free will and that we can attain sinlessness in this life 
often (not always) flows from an Arminian viewpoint. So is the viewpoint that a 
regenerate person can lose his salvation through sin or a series of sins. I 
have many objections to the Arminian perspective, one of which is the 
downplaying the nature of sin.</p>
<p>The idea that we can 
lose our salvation encourages a fake spirituality, legalism, and shallow 
doctrinal understanding because followers of this way of thinking are often 
obsessed with hanging onto their salvation.</p>
<p>Sins are therefore 
often called, "mistakes." Sin is also defined in a shallow fashion. For 
example, I can do a good deed for a wrong reason (to manipulate someone, for my 
ego, for my image or pride), thus polluting a good act with a sinful motive.</p>
<p>I do agree with some 
Arminians who recognize that all sins are NOT alike. Under the Law, for 
example, the penalty for some sins was death, for others a financial penalty, 
while other sins required only repentance and sacrifice. Although any sin would 
break the Law, which is an entire cohesive unit (see <b>James 2:10</b>,
"For whoever keeps the whole law and yet stumbles at just one point is 
guilty of breaking all of it") not all sins 
bear the same weight in Scripture (<b>John 19:11</b> reads, </span>"Jesus answered, 
'You would have no power over me if it were not given to you from above. 
Therefore the one who handed me over to you is guilty of a <b><u>greater</u></b> 
sin'").</p>
<p>In <b>1 Corinthians 5 and 
6</b>, Paul lists the types of sins requiring church discipline, and in <b>chapter 6</b> he 
lists the sins that typify the unregenerate, telling us that those professing 
believers who practice such things will not inherit the Kingdom of God. Those 
of us who believe in salvation by grace and adhere to security argue that those 
whose lives are characterized by the sins of <b>1 Corinthians 6</b> are very likely not 
truly saved (not that a genuine believer cannot fall into serious sin for a 
time, but, like a boomerang, he will come back to repentance much like Peter 
after the Resurrection).</p>
<p>Nonetheless, the 
Arminian position looks at sins as wrong acts or (better) wrong attitudes and 
thoughts. The problem, however, lies much deeper.</p>
<p><b>Jeremiah 17:9</b> says,
</span>"The heart is deceitful above all things and beyond cure. Who can 
understand it?"</p>
<p>Jesus said, </span>"For 
from within, out of men's hearts, come evil thoughts, sexual immorality, theft, 
murder, adultery, greed, malice, deceit, lewdness, envy, slander, arrogance and 
folly. All these evils come from inside and make a man 'unclean'&quot; 
in <b>Mark 7:21-23</b>.</p>
<p>We often are unaware of 
the many ways in which we sin. <b>John 1:7</b> offers some interesting implications: 
"But if we walk in the light, as he is in the light, we have fellowship with 
one another, and the blood of Jesus, his Son, purifies us from all sin."</p>
<p>If we are "walking in 
the light," meaning living in fellowship with God, then the blood of Jesus 
"purifies us from all sin." But if we are walking in the light, what sins are 
there that need purification? The best answer is that we sin in ways we do not 
recognize, even when our walk with God is at full force!</p>
<p>As our attitudes are 
contaminated by ego and the desire for control; our heats deceive us and we lie 
to our own selves. We say we are not angry, just hurt when we are both. 
Although we receive a new nature when we receive Christ, we still battle our old 
nature, and it can be insidious and cause us to sabotage ourselves. Larry Crabb 
rightly points out that the biggest problem in marriage (and in any close 
relationship, for that matter) is "justified self-centeredness" (see <u>Men and 
Women, Enjoying the Difference</u>). Psychologists tell us we forgot almost all 
the wrong things we do, but we remember well the way others have wronged us. 
This is very much part of "deceitful heart syndrome." This is why Solomon tells 
us, "He who trusts in his own heart is a fool" (<b>Proverbs 28:26</b>).</p>
<p>We must recognize that 
there are sins of omission (things I fail to do that God wants me to do) and 
sins of commission (lines I cross against God's will). Those who believe in 
salvation by sin management usually downplay sins of omission. It is okay if I 
don't spend time with the kids or do not take time to treat my wife with 
gentleness or offer her my time, but if I would swear I would lose my 
salvation. When God wills for us to do something and we don't do it, that is 
every bit as much a sin as if He forbids us to do something but we choose to do 
it anyway. Failing to show love, for example, is a sin.</p>
<p>Another problem I have 
with the Arminian viewpoint is their selective use of Scripture. For example, 
if you could lose your salvation based on <b>Hebrews 6</b>, you could never be saved 
again. One thing is clear: whoever is being talked about here (a believer who 
loses his salvation or a fake believer) cannot be saved: <b>Hebrews 6:4-6</b> reads, "It is impossible for those who have 
once been enlightened, who have tasted the heavenly gift, who have shared in the 
Holy Spirit, who have tasted the goodness of the word of God and the powers of 
the coming age, if they fall away, to be brought back to repentance, because to 
their loss they are crucifying the Son of God all over again and subjecting him 
to public disgrace."</p>
<p>At the funeral home, 
every Arminian has somehow held onto his salvation, no matter what his 
lifestyle. The practical outworking of Arminianism is, "people I am not close to 
can lose their salvation, but no one near and dear to me will." I have no 
problem with understanding <b>Hebrews 6</b>: such people are with God's people, but not 
part of the elect. If they were, they 
could not have deserted the faith and wandered into apostasy: "They went out from us, but they did not really belong to 
us. For if they had belonged to us, they would have remained with us; but their 
going showed that none of them belonged to us" (<b>1 John 2:19</b>).</p>
<p><b><u>If you had to be 
born again and again and again you should logically be baptized again and again 
and again</u></b>. Some Arminians do 
practice this; most do not.</p>
<p>Arminians do not excel 
in godliness over others, as measurable by divorce rate, personal debt, church 
splits, and other quantifiable statistics. Fear IS one major motivator in life, 
but it can also inhibit a believer from growing. From a practical viewpoint, 
the threat of losing one's salvation, statistically, does not reduce divorce, 
poor money management, or poor child rearing.</p>
<li><p class="bighpc"><a name="appendix6">Appendix 6: Collecting Criteria and Complicating 
Salvation: Where Do We Draw the Line?</a></p>
<p>Why do we draw the line 
at repentance and faith?</p>
<p>All of the following 
additional requirements could conceivably be added as requirements for salvation 
if the Bible is interpreted at a superficial level (rather than seeking to 
understand how the original audience would have understood these things):</p>
<ol class=1><li><p> Baptism (dealt with 
above)</p>
<li><p> Selling all one has 
and giving it all to the poor</p>
<p>NIV <b>Matthew 19:21</b> Jesus 
answered, &quot;If you want to be perfect, go, sell your possessions and give to the 
poor, and you will have treasure in heaven. Then come, follow me.&quot; </p>
<p>NIV <b>Mark 10:21</b> Jesus looked 
at him and loved him. &quot;One thing you lack,&quot; he said. &quot;Go, sell everything you 
have and give to the poor, and you will have treasure in heaven. Then come, 
follow me.&quot; </p>
<p>NIV <b>Luke 12:33-34</b></p>
<p><sup>33</sup> Sell your possessions and 
give to the poor. Provide purses for yourselves that will not wear out, a 
treasure in heaven that will not be exhausted, where no thief comes near and no 
moth destroys. </p>
<p><sup>34</sup> For where your treasure 
is, there your heart will be also. </p>
<p>Like the baptism passage, the question, 
"What happens to someone who doesn't sell all his possessions but follows 
Christ?" In the first two quotations, Jesus was exposing the covetousness of 
the rich young ruler; the latter is often considered an exaggeration to get His 
followers to look at life through non-materialistic eyes, not as a literal 
command.</p>
<p>Those who consider we who do not believe 
baptism is necessary for salvation (but recognize that baptism is necessary for 
obedience) are often accused of ignoring the plain meaning of the text. Okay, 
let's see those who so accuse explain the texts above and explain why they do 
not make it a requirement for salvation. When they do so, they will sound a lot 
like the rest of us!</p>
<li><p> Exceeding the righteousness of the Pharisees</p>
<p><b>Matthew 5:20</b> could be 
easily construed to be an additional requirement of salvation: "For I tell you that unless your righteousness surpasses 
that of the Pharisees and the teachers of the law, you will certainly not enter 
the kingdom of heaven."</p>
<p>Although some Pharisees 
were hypocrites, most were very devout and sincere, like Paul the Apostle, as he 
himself describes in <b>Philippians 3:5-6</b></span>, "circumcised on the eighth day, 
of the people of Israel, of the tribe of Benjamin, a Hebrew of Hebrews; in 
regard to the law, a Pharisee; as for zeal, persecuting the church; as for 
legalistic righteousness, faultless.&quot;</p>
<p>I think it would be 
appropriate to say that most Pharisees knew the Scriptures and had committed 
more verses to memory than almost all Christians in any denomination or belief 
system; they lived clean lives, were noted for family faithfulness and acts of 
charity and kindness. Like Paul, many of them passed the "legalistic 
righteousness" test; when they were measured against the law, they were not 
"highhanded" sinners, and they followed the Law's demands as far as atoning for 
their other (besides highhanded) sins and weaknesses. </p>
<p>Although we tend to 
think of the Pharisees who hated Christ, we need to remember that they were the 
powerful and often corrupt version. Far more Pharisees were like Nicodemus and 
Joseph of Arimathea than of the evil variety. Most followed the godly example 
of Hillel, one of Judaism's great Rabbis who lived shortly before the time of 
Christ. Jesus teachings often parallel his. For example, Hillel said, "If you 
don't want other people to do something to you, don't do it to them" (the Golden 
Rule stated in a different fashion). For documentation, see the chapters about 
the Pharisees in Dr. Ron Moseley's book, <u>Yeshua, A Guide to the Real Jesus 
and the Early Church.</u> </p>
<p>But is this what Christ 
is talking about? I think He is very clearly trying to wear people down to see 
how utterly lost and helpless they are. He actually intensifies the Law, 
showing us that we cannot find salvation through our works, no matter how hard 
we try. It is hopeless and vain, as Paul explains: <b>Romans 7:14</span></b>, 
"We know that the law is spiritual; but I am unspiritual, sold as a slave to 
sin." That is where Jesus wanted to get 
folks, I believe.</p>
<li><p> Submitting to the 
Lordship of Christ/taking up the cross daily</p>
<p>Like many of the things 
mentioned before or after this point, it IS God's will for us to take up the 
cross daily (whatever that means) and submit to the Lordship (control and will) 
of Jesus Christ. But the question is, "Is my salvation dependent upon doing 
these things, or do these things flow as I grow after my salvation?"</p>
<p>If we must be willing 
to do anything God calls us to do in order to be saved (which is how living 
under the lordship of Christ is generally understood), then assurance of 
salvation is impossible. Yet, according to <b>1 John 5:13</b>, we can KNOW that we 
have eternal life: "I write these things to you who believe in the name of the 
Son of God so that you may <b><u>know</u></b> that you have eternal life."</p>
<p>Please understand this 
point: I cannot possibly <b>know</b> that I would do any thing God led me to do
<i>until </i>He actually asks me (and then I am tested). Peter, for example, 
thought he would never desert Christ. Note his words,
"Peter replied, 'Even if all fall away on account of you, I never will.'" 
(<b>Matthew 26:33</b>). Yet that same night 
he denied Christ three times before the rooster crowed. </p>
<p>All we can know is that 
we <i>want</i> to be willing to do God's will, but we cannot ever know <i>if</i> 
we would do ANYTHING God told us to do unless He tested us in <i>every</i> area.</p>
<li><p> If we state that 
obeying every one of the Lord's commands is necessary for salvation, then it 
would be easy to conclude that traveling as a representative of Jesus Christ to 
foreign lands was necessary for salvation, for Jesus said,
</span>"All authority in heaven and on earth has been given to me. Therefore <i>
go</i> and make disciples <i>of all nations</i>, baptizing them in the name of 
the Father and of the Son and of the Holy Spirit, and teaching them to obey 
everything I have commanded you. And surely I am with you always, to the very 
end of the age" (<b>Matthew 28:19-20</b>).</p>
<p>Most of us understand 
this as a task given to the church at large, not each individual Christian. 
Still, it could be argued that there are no limitations here. If we are looking 
for a collection of specific criteria about how to be saved apart from the clear 
and oft repeated "believe," then this idea is certainly a candidate.</p>
<li><p> Speaking in 
Tongues. Although I am skeptical about <b>Mark 16:9ff</b>, one could conclude that 
unless one spoke in tongues, he is not saved: </span>"And these signs will 
accompany those who believe: In my name they will drive out demons; they will 
speak in new tongues."</p>
<li><p> Picking up snakes. 
While we're collecting criteria, <b>Mark 16</b> also says that "these signs 
will accompany those who believe....They will pick up snakes with their hands.&quot;</p>
<li><p> Drinking poison (no, 
not your wife's coffee!), While we're collecting criteria, <b>Mark 16</b> also says 
that "these signs will accompany those who believe...when they drink 
deadly poison, it will not hurt them at all; they will place their hands on sick 
people, and they will get well.&quot;</p>
<li><p> Ability to heal 
others. While we're collecting criteria, <b>Mark 16</b> also says that "these 
signs will accompany those who believe...they will place their hands on sick 
people, and they will get well.&quot;</p>
<li><p> Working greater 
miracles than Jesus worked.
<b>John 14:12</b> reads, "I tell you 
the truth, anyone who has faith in me will do what I have been doing. He will do 
even greater things than these, because I am going to the Father.&quot;</p>
<p>I am not sure I 
completely understand this verse's true meaning, but if I thought my salvation 
(which comes by faith) depended upon me working greater miracles than Jesus 
worked, I'd be in trouble. Yet because I believe in the oft repeated teaching 
that I am saved by faith in Christ, I know my salvation is secure whether I meet 
this condition or not.</p>
<p>I could go on and on, 
but here are just SOME other criteria that could be added: being perfect as the 
Father in heaven is perfect, holiness (perfection) without which no one will see 
God, undergoing persecution, pressing onward to attain the resurrection from the 
dead, being a peacemaker, cultivating meekness, receiving persecution, not 
eating meat sacrificed to idols, never eating food prepared with blood, no 
divorce or remarriage under any circumstances, receiving communion, must not be 
cowardly, receiving the anointing with oil and prayer from the elders, not 
committing the unpardonable sin, not calling a brother a "fool," and on and on 
it goes.</p>
<p>It seems to me a lot 
wiser approach to cling to the oft-repeated message of salvation by "repentance 
toward God and faith in our Lord Jesus Christ." Once we are saved, "God works 
in us both to will and to do of His good pleasure." Faith is taking God at His 
word that, "He who believes in the Son has everlasting life." When God 
GENUINELY saves a person, that person evidences fruit, by which they are known.</p>
<p>We must embrace 
principles of interpretation that seek the plain meaning of a text but avoids 
being infantile by jumping to conclusions.</p></ol>
<li><p class="bighpc"><a name="conclusion">Conclusion</a></p>
<p>Thank you for 
journeying with me through this theological trail. As I stated in the 
beginning, if I have not persuaded you that my viewpoint is the Biblical one, 
hopefully you at least realize that there are other perspectives which do 
attempt to uncover the original meaning of Scripture. </p>
<p>For others, I hope this 
paper and the Scriptures I have highlighted have stretched you. Understanding 
God and His ways is no small feat. The best of us can understand so little 
about God. We can only understand what He has revealed 
(<a href="javascript:scripture('../scripture/deut2929.htm')" target=_self class=pop>Deuteronomy 29:29</a>). 
Most of the time, His revelation is straightforward; in other instances, it gets 
tough.</p>
<p>Even if you embrace my 
views, there are many questions I left unanswered. Sometimes I have no answer. 
In many instances, I do have complete or partial answers, but, if I were to 
address all common questions, I would need to write a multi-volume set. 
Fortunately, there are books out there on some of these topics, and I would be 
happy to recommend them. For the most obvious set of questions (those involving 
man's limited free will/responsibility and its harmony with the doctrine of 
election), I suggest J.I. Packer's classic book, <u>Evangelism and the 
Sovereignty of God</u>.</p>
<p>Once again, thank you 
for joining me on this theological journey and giving my thoughts a fair shake.</p></ul>
  
