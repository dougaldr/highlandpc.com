<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 1
<br><span class=byln>by Ed Vasicek</p>

<p>Yahweh,</p>
<p>You are the great I AM. You are 
always what You are. Your intellect, emotions and will are not passing or 
transient, nor do they follow one another sequentially or develop. Your 
qualities have always been and always will be infinitely mature. You have no 
room for improvement or growth because You are infinite and absolute in all 
Your ways. You are not a God Who is a composite of Your attributes, yet Your 
attributes are not mere illusions but reveal different aspects of Who You are. 
Additionally anywhere You are present, You are present entirely. These things 
are hard to grasp. But we can understand it simply just by realizing that You 
are one unity. There are no dark and light sides to You but only light. There 
is no good and evil, only good.</p>
<p>Father, I remember the poor, sickly and downtrodden of 
this world. In some nations, the average person subsists on one percent of what 
the average American lives on and in many, many nations people live on between 
3 and 7 percent of what Americans do. In many lands, the government does not 
recognize the rights with which You have endowed us. So, Lord, help these 
nations to become prosperity producing rather than maintaining the old status 
quo.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________.</p>
<p>Thank You that the church is built upon the foundation of 
the Apostles and Prophets 
(<a href="javascript:scripture('../../scripture/eph220.htm')" target=_self class=pop>Ephesians 2:20</a>). 
A foundation is only laid once and they did so. We now have that 
foundation in Your written Word, the Bible. Like the Berean believers, help us 
to use the Scriptures to prove whether a teaching is true or false.</p>
<p>Lord, I pray for the Indian 
provinces of Jammu and Kashmir. Only about .16% professes any sort of 
Christianity in this province of 9 million people. Thirty thousand people have 
been killed with 800,000 refugees as a result of the war with Pakistan. 
According to 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>, 
&quot;In 1990 there were but 12,000 Christians 
in 45 churches, many of which were nominal and either low-caste or immigrant in 
origin. Evangelical believers were a few thousand, but in the ‘90s their numbers 
doubled and churches increased to 167 in 1998.&quot; Lord, I pray that Your Spirit 
would work and the evangelical church would grow tenfold over the next five 
years. Grant safety, provision and the power of Your Spirit to Your children 
there, I ask.</p>
<p>Father, bless the ministry of 
World Impact, Inc., &quot;a Christian missions organization dedicated to 
ministering God's love in the inner cities of America. Its purpose is to honor 
and glorify God and delight in Him in the inner cities by knowing God and making 
Him known. World Impact ministers crossculturally to people unreached by the 
gospel of Jesus Christ through evangelism, follow-up, discipleship and 
indigenous church planting. World Impact empowers urban disciples, training 
leadership for the advancement of the Kingdom of God.&quot; Prosper, protect and 
provide for this ministry. Grant its leaders harmony and empower the workers 
with Your Spirit resulting to fruit that glorifies You, O Lord.</p>
<p>Help me glorify You today, I ask in the name of my Savior, 
Amen.</p>

