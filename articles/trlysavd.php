<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Can a TRULY Saved Person Become Unsaved?
<br><span class=byln>by Ed Vasicek</span></p>

<p>At the height of her fame as the other woman in the Ivana 
and Donald Trump breakup, Marla Maples spoke of her religious roots. She told 
interviewers she believed in the Bible, then added the disclaimer, &quot;But you 
can't always take [it] literally and be happy.&quot;&nbsp;So relates Chuck Colson in his 
book, <i>The Body</i>.</p>
<p>As many of you know, I am into Bible interpretation (big 
time).&nbsp;But interpreters fall into two categories: those who seek to interpret 
the passage objectively with respect for the original meaning of the authors, 
and those who have an agenda.&nbsp; </p>
<p>Some passages are really “bears” to interpret.&nbsp;In a number 
of cases, we can never be exactly sure what a verse means.&nbsp;Some of the passages 
in <b>Ezekiel</b> or <b>Revelation</b>, for example, give us clues as to their meaning, but we 
are on thin ice if we declare their meaning with certainty.</p>
<p>Another challenge of interpreting Scripture is harmonizing 
seemingly conflicting passages.&nbsp;Such is the case when it comes to the issue of 
what some call “eternal security.”&nbsp;I prefer the term “perseverance of the 
believer.”</p>
<p>The issue as to whether a genuine believer can lose his or 
her salvation is usually wrapped around two questions: (1) Do I have to be 
afraid that I will lose my salvation? and (2) What about this person I know who 
claimed to be a Christian and participated actively in church and now has washed 
out?</p>
<p>Both sides of the issue have an arsenal of Scripture 
verses. If you took either set in isolation, they would win you over to one 
viewpoint or the other.&nbsp;But putting them together demonstrates that both camps 
are attempting to line up with Scripture—at least some Scriptures.&nbsp;The real 
question is this:&nbsp;do you interpret the verses that seem to imply that you can 
lose your salvation in light of the verses that teach otherwise, or vice-versa?</p>
<p>Take a verse cluster that implies believers are safe and 
secure in Christ.&nbsp;These verses tell us that we are held safely in God’s hand, 
and the only way to get out of that hand is to be greater than God.&nbsp;<b>John 
10:27-29</b> reads: “My sheep listen to my voice; I know them, and they follow me. I give them 
eternal life, and they shall never perish; no one can snatch them out of my 
hand. My Father, who has given them to me, is greater than all; no one can 
snatch them out of my Father's hand.” </p>
<p>Someone who believes we can lose our salvation will say, 
“Yes, but you can take yourself out of God’s hand.”&nbsp;Yet they miss the point: 
the reason we are safe in God’s hand is because no one is greater than He.</p>
<p>Here’s a verse that seems to teach we can lose our 
salvation, the famous <b>Hebrews 6:4-6</b> passage.&nbsp;“It is impossible for those who 
have once been enlightened, who have tasted the heavenly gift, who have shared 
in the Holy Spirit, who have tasted the goodness of the word of God and the 
powers of the coming age, if they fall away, to be brought back to repentance, 
because to their loss they are crucifying the Son of God all over again and 
subjecting him to public disgrace.”</p>
<p>Someone who believes in security, as I do, would interpret 
this verse as referring to those who professed salvation but then later denied 
Christ.&nbsp;I would argue that such persons were never genuinely saved.&nbsp;I would 
also argue that whoever these people are in Hebrews 6, they could not be saved 
after they have reached the point the author mentions.</p>
<p>Most of us do agree that there are people who are not saved 
who, at one time, gave every indication of being true believers.&nbsp;The only 
difference is this: Were they at one time saved and then lost their salvation, 
or were they never originally saved (my view).</p>
<p>Judas Iscariot is a good case example.&nbsp;Was Judas saved and 
then lost it, or was he never really saved?&nbsp;He worked miracles, and was never 
held in suspicion by the other apostles. As a matter of fact, he was probably 
their most trusted companion, for he was the treasurer!&nbsp;According to <b>John 
16:4-6</b>, Judas was never a sincere follower of Jesus.&nbsp;“But one of his disciples, 
Judas Iscariot, who was later to betray him, objected, 'Why wasn't this perfume 
sold and the money given to the poor? It was worth a year's wages.' He did not 
say this because he cared about the poor but because he was a thief; as keeper 
of the money bag, he used to help himself to what was put into it.”</p>
<p>When it comes to determining the GENUINELY saved from those 
who merely look saved, we should contemplate the words of Paul.&nbsp;“Nevertheless, 
God's solid foundation stands firm, sealed with this inscription: ‘The Lord 
knows those who are his.…’” (<b>2 Tim. 2:19</b>) But, in contrast, we do not.&nbsp; We 
presume those who profess salvation and who bear fruit, as Judas did, are saved; 
we do not know with absolute certainty.</p>
<p>Jesus taught a parable of wheat and tares (a weed that 
resembles wheat). <b>Matthew 13:37-40</b> explains it. “And He said, ‘The one who 
sows the good seed is the Son of Man,
and the field is the world; and as for the good seed, these are the sons of the 
kingdom; and the tares are the sons of the evil one; and the enemy who sowed 
them is the devil, and the harvest is the end of the age; and the reapers are 
angels. So just as the tares are gathered up and burned with fire, so shall it 
be at the end of the age.’”</p>
<p>There are many people who look like genuine believers, but 
then desert the faith.&nbsp;The devil is actively intermixing them among genuine 
believers.&nbsp;Ponder this. So how do we account for defections?&nbsp;John brings the 
best harmonization I have seen in <b>1 John 2:19</b>: “They went out from us, but they 
were not really of us; <b><u>for if they had been of us, they would have 
remained with us</u></b>; but they went out, so that it would be shown that they 
all are not of us.”</p>
<p>The way we are to distinguish true believers (wheat) from 
tares is perseverance in the faith. If you look at John’s wording, the 
implication seems to be that true believers cannot desert the faith.&nbsp;If the 
apostles could not separate the wheat from the tares, it would be a little 
presumptuous to think that we could!&nbsp; </p>
<p>By why is it so important to understand that we are secure 
in Christ? If we were not, then our salvation would be at least be partially 
dependent upon us and God would not get ALL (and I mean all) the glory.&nbsp;But God 
gets all the glory.&nbsp;Salvation is totally and completely of God.&nbsp;Even our 
decision to believe is a result of His planning and working (see 
<a href="javascript:scripture('../scripture/acts1348.htm')" target=_self class=pop>Acts 13:48</a>).
If you understand this, you can really understand grace.</p>
<p>But note this:&nbsp;the grace that saves us is also the grace 
that transforms us. That is why true believers are in the process of being 
conformed to the image of Christ.&nbsp;That is why sin bothers the genuine 
believer.&nbsp;And that is why the genuine believer, like a boomerang, always comes 
back.&nbsp;The genuinely regenerate person does not need the threat of losing 
salvation as a motivator to walk the straight and narrow.&nbsp;Instead, he has the 
Law of God written on his heart (a new nature), and, although he fails time and 
time again, he knows that it is “God who works in you to will and to act 
according to his good purpose” (<b>Philippians 2:13</b>).&nbsp;He also knows he has a 
loving but firm heavenly Father who will discipline him or even take him home 
early rather than allow him to lose his salvation.</p>
<p>From a practical viewpoint, do those who believe they can 
lose their salvation live in a godlier fashion than those who believe 
otherwise?&nbsp;In my observation, just the opposite is true, or, at bare minimum, 
it makes no difference.&nbsp;The secure believer is free to focus upon maturing, the 
insecure believer may merely focus upon “hanging on.”</p>
<p>What do our families teach us?&nbsp;That children who are on 
the verge of being disowned because of every infraction come out better than 
those who are unconditionally accepted?&nbsp;I don’t think so. The same is true, I 
am convinced, in the spiritual realm.&nbsp;We operate best from a position of 
security.</p>
<p>Now there are all sorts of positions in between the two.&nbsp;Some people believe every time you sin you have to be saved again, others say 
you have to be saved again when you sin a big sin.&nbsp;Still others, who are more 
moderate, say that you lose your salvation only if you desert the faith or deny 
the Lord.&nbsp;But the issue still comes down to this: is salvation totally of God 
or mostly of God?</p>
<p>So Joe Schmo used to be a deacon and witness, but now Joe 
Schmo lives at the bar and is an atheist.&nbsp;Is Joe saved?&nbsp;Most of us would say 
no. The only difference is this:&nbsp;those who reject security say he was saved 
and lost his salvation, those of us who believe otherwise say he did not have 
the genuine disease.&nbsp;Unfortunately, there are some in the eternally secure camp 
who would maintain that Joe was saved and still is. But, in an attempt to 
disassociate myself from that abuse, I prefer to label my conviction, “The 
Perseverance of the Believer.”&nbsp; </p>
<p>As we get into <b>Romans 8-11</b> during upcoming Sunday morning 
services, I’ll kick things up a notch as we discuss election and ponder, “How 
free are our wills?”&nbsp;But we’ll save that for later.</p>

