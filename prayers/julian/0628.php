<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 28
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Master and Father,</p>
<p>Your Son, Our Savior, the Lord Jesus Christ, is the King of 
Kings. Of course as God the Son, He is equal to You, even though He made 
Himself subservient to You when He was here on earth as a man 
(<a href="javascript:scripture('../../scripture/phil267.htm')" target=_self class=pop>Philippians 2:6-7</a>). 
In His human nature, He was King, for He was heir to the throne of 
David. He was genetically connected to David through Mary and He received the 
right to rule through His stepfather, Joseph, a man who would have been King had 
the line of David been honored. So both from human and divine perspectives, God 
the Son is the King of Kings and Lord of Lords! And to think that He has 
identified Himself as our brother! 
(<a href="javascript:scripture('../../scripture/rom829.htm')" target=_self class=pop>Romans 8:29</a>).</p>
<p>Father, I have so many things I think I should do: chores 
around the house, errands to run, things to clean or organize, people I really 
should invite over, assignments I should be working on, matters of exercise or 
diet.... O Lord, sometimes I am overwhelmed and do not know where to begin. I 
always feel behind, under the pile and crave a simpler life. Yet, on the other 
hand, I need purpose and dislike being bored. Help me to accept that it is a 
no-win situation and that most of us struggle with these same things. So give 
me peace and guidance. Help me to prioritize things based upon Your will and 
let me take time to smell the roses.</p>
<p>Lord, I am thankful for many things, two of which are: __________.</p>
<p>Lord, I present myself before You; may Your Spirit fill me. I trust in His 
power, not my own. I surrender control of my life to Your Spirit today.</p>
<p>O God, the Beijing Municipality is the capital province of 
the People’s Republic of China with nearly 14 million people in this city area. 
Maybe 4% of the people there are evangelical Christians. Since this is the seat 
of Chinese government, the persecution in this province is especially severe. 
Lord, help the house churches and percentage of believers to double over the 
next 5 years despite the government’s effort. Lord, turn the hearts of 
government leaders to You and toward true religious freedom, I ask. Grant the 
believers special wisdom as they seek to live out the Christian life in such a 
dangerous area. May Your Spirit work mightily in this region!</p>
<p>Adonai, thank You for Global Partners, USA which aims to 
&quot;glorify God by evangelizing the globe by developing local leadership in 
cooperation with creative mission strategies.&quot; They seek candidates for short- 
and medium-term mission trips in CIS (former Soviet), Chile, China, Indonesia, 
Japan, Myanmar, Paraguay, Philippines, Thailand, Turkey, Uzbekistan. O Lord, 
use this mission to make an impact on the world for Jesus Christ, I pray. 
Empower them with Your Spirit, protect them, provide for them and give them 
great fruit for their labors.</p>
<p>Lord, bless those who work in the fields of installation 
and repair. May many in these fields come to know Jesus. Help them do their 
work well.</p>
<p>Now, Lord, help me to work or fulfill my responsibilities 
in a way that reflects positively upon You, I ask in the Messiah’s Name, Amen.</p>

