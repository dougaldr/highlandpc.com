<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 14
<br><span class=byln>by Ed Vasicek</span></p>

<p>Almighty King,</p>
<p>I praise You because You are omniscient—You know 
everything and anything. There was never a time when You did not know 
everything and anything. You did not need to learn by experience, and You 
certainly have never made a mistake. You may condescend to us and experience 
life as though You've never had a run through, but we know You take that &quot;second 
track&quot; as part of relating to us, we who are bound by time and space. But You 
have the sum total of human knowledge, plus an infinite knowledge beyond that. 
You are our limitlessly intelligent God.</p>
<p>Lord, when You created the first man, You gave him the 
responsibility to care for this world. He was to use its produce, but He was 
accountable for being a good steward. Although life has become more complex, 
You expect us to be good stewards of everything You have given us: our bodies, 
our wealth, our time, our energy, our influence for You, and the condition of 
our souls. Help us to realize that You own all. As Christians, You own us in a 
double way: You are the creator, and You have bought us back from the slave 
market of sin by the blood of Jesus Christ. We are redeemed. Help me, 
therefore, to be a good manager of all the resources I have, because I do not 
really have them...they are Yours. I just manage them.</p>
<p>I ask that You would bless all of my relationships: the 
relationships within my family, among my friends, church members, fellow 
employees, neighborhood and community.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control 
me today.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Today I pray for the nation of Argentina. Although this 
land has a track record for revolution, thank You that the nation is now more 
democratic.&nbsp; Thank You for the great growth of the Evangelical church in 
Argentina. Thank You for the revival that has been breaking forth over the last 
decade. This has even spread through the prisons. Help the believers there to 
become more Scripturally anchored rather than depending upon their emotions. We 
pray that the Evangelical churches would double in number and size over the next 
decade.</p>
<p>Today we remember the ministry of WEGO. Bless them as they 
share the love of Christ in South and Central America and elsewhere. Bless 
those who participate in the short-term missions trips and those career 
missionaries who set down their roots to do a lasting work. Give them safety, 
fruit, and good relationships amongst themselves.</p>
<p>We pray for all those in our community who are involved in 
some way with homeland security. Help them keep abreast of the latest and best 
techniques to help defend our communities.</p>
<p>Now, Father, I ask 
that Your grace would work mightily in me today, for I pray in Jesus' Name, 
Amen.</p>

