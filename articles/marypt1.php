<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>What About Mary? Part One - What We Don't Believe
<br><span class=byln>by Ed Vasicek</span></p>

<p>One of the great differences between Biblical evangelical 
(fundamental) Christianity and Roman Catholicism is our varying views about 
Mary. They are not just a little bit different, but extremely different. 
Although our views differ, we must remember to respect the right others have to 
believe as they choose, even if we disagree with those views. We are not out to 
bash but to contrast. It is important to understand these distinctions because 
a blossoming, but compromised, evangelical movement <i>within</i> Catholicism is 
backwashing Roman Catholic teaching into Protestant evangelical churches. So 
let's examine our differences. </p>
<p>On the one hand, Biblical evangelicals believe Mary was a 
good woman but chosen primarily because she was a descendent of David. God was 
gracious to her by allowing her to experience the dream of every Jewish young 
woman: mothering the Messiah. It was God's grace, and not Mary's merit, that 
gave her this blessing. She was not necessarily the godliest woman at the time, 
though she was a godly woman of faith. In our viewpoint, women as godly as Mary 
could be with us today. But we are quick to admit that no woman will ever 
fulfill the special role Mary had.</p>
<p>We believe Mary was a sinner who needed a redeemer like 
everyone else. She miraculously conceived Christ by the Holy Spirit and 
delivered Him while she was yet a virgin. This belief is called &quot;the Virgin 
Birth&quot; and should not be confused with the additional Roman Catholic belief of 
&quot;the Immaculate Conception&quot; explained below. Both Catholics and evangelicals 
believe in the Virgin Birth, but Bible-oriented evangelicals believe that only 
the God-man, Jesus Christ, was born without a sin nature. Catholics tend to 
believe that Mary was also sinless.</p>
<p>Catholicism exalts Mary to having been miraculously 
conceived herself (thus the term &quot;Immaculate Conception&quot;), and they believe Mary's body was 
resurrected and assumed into heaven. Catholics teach that Mary serves as a 
mediator between Christians and Jesus. They also believe it is appropriate to 
sing praises to her and to pray to her--acts of worship that would be 
considered sacrilegious to us (we would argue such acts are only appropriate 
when directed toward God). Catholics refer to Mary as the &quot;Mother of God;&quot; we 
refer to Mary as the &quot;Mother of Jesus,&quot; the term used in Scripture. Although 
both groups would acknowledge that she was the mother of only Jesus' human 
nature, calling her &quot;the mother of God&quot; is often misunderstood at the popular 
level. The great divide over Mary is massive.</p>
<p>How can two groups, both claiming to follow Christ, hold 
such divergent views? The answer boils down to authority--how does Christ 
lead? Catholicism recognizes three sources of <i>infallible</i> doctrine: (1) 
the church's decrees and accumulated traditions, (2) the Pope, and (3) 
Scripture, respectively. Most Catholics sense no burden to defend these beliefs 
Scripturally; they trust their church. They acknowledge that many of their 
beliefs have evolved over time, and see nothing wrong with that.</p>
<p>Evangelicals/fundamentalists believe in <i>Sola Scriptura</i>, 
that the Bible is the only <i>infallible</i> source of doctrine. Every doctrine 
is to be challenged and refined by accurate Bible study; even the church is 
accountable to the Scripture and not to be trusted implicitly. The result is 
that the two belief systems sometimes converge and at other times diverge. </p>
<p>To make matters more confusing, the Eastern Orthodox and 
the Episcopal/Anglican churches often have a view similar to Roman Catholicism 
when it comes to the authority of church tradition, though neither group 
recognize papal infallibility. These churches also perform acts of worship 
directed toward Mary, but they do not believe in the <i>Immaculate Conception</i> 
or the doctrine of the <i>Assumption of Mary</i>. These are relatively recent 
Roman Catholic beliefs.</p>
<p>Catholics believe Mary remained a virgin for her entire 
life; our understanding of Scripture is that Joseph &quot;kept her a virgin <i>until</i> 
she gave birth to a son&quot; (<b>Matthew 1:25</b>). We also maintain that Jesus was her 
&quot;firstborn son&quot; (<b>Luke 2:7</b>), not her only-born son. We lean toward the view that 
she and Joseph had several children after Jesus was born, four sons and a number 
of unnamed daughters, as per <b>Matthew 13:53-56</b>:</p>
<p class="MsoNormal" style="margin-top:0in;margin-right:27.0pt;margin-bottom:
0in;margin-left:27.0pt;margin-bottom:.0001pt">And when Jesus had finished these 
parables, he went away from there, and coming to his hometown he taught them in 
their synagogue, so that they were astonished, and said, &quot;Where did this man get 
this wisdom and these mighty works? Is not this the carpenter's son? Is not his 
mother called Mary? <b><i>And are not his brothers James and Joseph and Simon 
and Judas? And are not all his sisters with us?</i> </b>Where then did this man 
get all these things?&quot;</p>
<p>Roman Catholics accommodate this passage by postulating 
that Joseph was an older man and that these children were born from a previous 
marriage, which is why he is portrayed as much older than Mary in Catholic 
artwork and some Christmas cards. But, in light of the fact that Joseph only 
kept her a virgin <i>until </i>Jesus was born, and that Jesus was called Mary's
<i>firstborn</i>, such a conclusion seems strained to most evangelical 
interpreters. We would argue that such a belief evolved over the centuries but 
was foreign to both the New Testament and the first-century church.</p>
<p>We also believe that, &quot;For there is one God and ONE 
mediator between God and men, the man Christ Jesus&quot; (<b>1 Timothy 2:5</b>).</p>
<p>Now that we have contrasted our beliefs with those of Roman 
Catholicism, we will begin laying a Biblical foundation: what we DO believe 
about Mary. Mary's name was not actually Mary, but Miriam, a common Hebrew 
name. Miriam was probably named after Moses' sister, and there is at least one 
point of convergence. Miriam was a prophetess who sang a prophetic song (poem) 
to the children of Israel, tambourine in hand: &quot;Sing to the L<span class=sm>ORD</span>, for he is 
highly exalted. The horse and its rider he has hurled into the sea&quot; (<b>Exodus 
15:21</b>). When Mary was expecting Jesus and visited Elizabeth, she prophesied a 
poem as well 
(<a href="javascript:scripture('../scripture/luke14655.htm')" target=_self class=pop>Luke 1:46-55</a>),
 sometimes called &quot;The Magnificat.&quot; There are 13 
quotations in the poem from the Old Testament.</p>
<p>Mary's name is also related to the Hebrew word, &quot;Marah,&quot; 
meaning bitter. Some have understood her name to be prophetic of the bitter 
suffering she would experience, seeing her Son crucified. The elderly Simeon 
prophesied to her in <b>Luke 2:35</b>, &quot;and a sword will pierce even your own soul.&quot;</p>
<p>Besides her name, we need to remember that Mary, Joseph, 
Jesus, and all the Apostles were Jewish. Christianity's first description was 
&quot;a sect of the Nazarenes&quot; (<b>Acts 24:5</b>), and that it was. The early Christians 
were so devoted to Judaism that the big controversy in the early church was 
whether a gentile needed to become a Jew FIRST (beginning with circumcision then 
keeping all the laws of Moses) before becoming a Christian 
(<a href="javascript:scripture('../scripture/acts15.htm')" target=_self class=pop>Acts 15</a>). 
The idea that Jesus came to start a new religion, completely separate from Judaism, is 
incorrect. Christianity is best understood as a form of Judaism (Messianic 
Judaism), with most of us relating to God as God-fearing gentiles who are 
grafted into Israel 
(<a href="javascript:scripture('../scripture/rom11132.htm')" target=_self class=pop>Romans 11:1-32</a>).</p>
<p>Joseph and Mary were devout Jews, and they reared Jesus as 
an observant Jew. Note <b>Luke 2:21-24</b> to see an example of their obedience: </p>
<blockquote>On the eighth day, when it was time to circumcise him, he was named Jesus, the name 
the angel had given him before he had been conceived. When the time of their 
purification according to the Law of Moses had been completed, Joseph and Mary 
took him to Jerusalem to present him to the Lord (as it is written in the Law of 
the Lord, &quot;Every firstborn male is to be consecrated to the Lord&quot;), and to offer 
a sacrifice in keeping with what is said in the Law of the Lord: &quot;a pair of 
doves or two young pigeons.&quot;</blockquote>
