appearance&#8230;may have
been real blood, probably not.</p>
    <p>
        Dr. Thomas Constable's
Notes on
Exodus 2005 Edition, </p>
	<blockquote>Each year, toward the end of June, when the
waters
of the Nile begin to rise, they are colored a&nbsp;
dark red by the silt carried down from the headwaters. This
continues for three months, until the waters begin to abate, but the water,
meanwhile, is wholesome and
drinkable. The miracle of
<a href="javascript:scripture('../../scripture/ex71721.htm')" target=_self class=pop>7:17-21</a> 
involved three elements by which it differed from the accustomed phenomenon: the water was changed
by the smiting of Moses' rod; the water became undrinkable; and the condition lasted just seven days 
(v. 25).</blockquote>
    <p>
        <i>Targum of Jonathan, the waters of the Hebrews not being
affected with this plague</i>&nbsp;</p>
