<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 11
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a God Who keeps His promises. Although many 
Christians take Your promises out of context or misunderstand them, the promises 
You have made to us will be kept as they were originally understood. You will 
keep Your promises in Your time to those with whom You made them. Thank You that 
Your calling and gifts are irrevocable 
(<a href="javascript:scripture('../../scripture/rom1129.htm')" target=_self class=pop>Romans 11:29</a>).</p>
<p>Although You made many promises to individuals or nations 
that are descriptive (only for that particular situation), I especially 
appreciate the promises that are prescriptive (made to all who meet a certain 
condition). Your promise of eternal life is made to all who repent of their 
sins and accept the Lord Jesus Christ as personal Savior. You promise to hear 
our prayers and meet the needs we bring to You in prayer according to Your 
will. When our fellowship is broken because of sin, You promise to restore our 
fellowship when we confess our sin to You. And you have promised that You will 
be with me in a special way that You are not with unbelievers even until the 
end of the age 
(<a href="javascript:scripture('../../scripture/matt2820.htm')" target=_self class=pop>Matthew 28:20</a>).</p>
<p>I pray for my lost friends and family members including: __________. 
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Father, I intercede for believers who are struggling with 
substance abuse, addictions, or other bad or undesirable habits. Some practices 
are wrong even in moderation. Others are wrong in excess. Others are not 
wrong in themselves, but are wrong because they are enslaving. Help us all to 
put off our bad ways and replace them with positively constructive ways. Rather 
than steal, let us labor to give to those in need. Rather than gossip, let us 
praise. Instead of being drunk with wine, help us to be filled with Your 
Spirit. Help us replace the undesirable with the good—or at least the 
neutral.</p>
<p>Lord, today I pray for five more nations where persecution 
is an issue, nations 41-45 from the list of the 86 nations listed in the 
persecution index 
(<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>, 
2001, p. 222). I pray for Tartarstan, 
Nigeria (north), Ingushetia (part of Russia), Sri Lanka and Malaysia. Thank 
You that persecution is lessening in Nigeria, but I am distressed to hear that 
it is worsening in Malaysia. O Lord, may persecution in these and other lands 
cease. Help local and national governments to respect true and complete 
religious freedom. May the persecutors themselves be won to the faith. Give 
the believers wisdom to evade persecution and when they cannot, help them, 
Lord, with Your grace, I ask.</p>
<p>Lord, thank You for the work of Missionary Ventures 
International. &quot;Missionary Ventures International sponsors several ministries 
to supplement its purpose to encourage and support indigenous missions through 
personal involvement. The ministries rely on Spirit led leaders to recruit 
capable volunteers and marshal resources to fulfill their unique tasks to 'reach 
the un-reached' through practical applications of skills and training often used 
in secular endeavors.&quot; Bless this work as they come alongside national workers 
with volunteers and resources, I pray. Empower, guide, grant harmony and 
protect this ministry, I request.</p>
<p>Empower me today by Your Spirit to represent You well to 
others. I pray in the Savior's Name, Amen.</p>

