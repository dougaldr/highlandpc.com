<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 22
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>While Joseph and Mary were dedicating Jesus to You 
at the Temple, an elderly widow named Anna who was also a prophetess  
beheld the baby Jesus. She was so excited because she knew that Jesus was the 
promised Messiah. She told everyone she knew that the Messiah had been born.</p>
<p>Decades later, a Samaritan woman ran into Jesus at a well. 
&quot;The woman said, 'I know that Messiah…is coming. When he comes, he will explain 
everything to us.'&quot;</p>
<p><b>&quot;</b>Then Jesus declared, 'I who speak to you am he.'&quot;</p>
<p>This woman was so excited that she left her water vessels 
behind, returned to her hometown and began telling everyone that she had found 
the Messiah.</p>
<p>Lord, we have not met Jesus physically nor beheld His 
outward appearance. But those of us who know Christ have certainly met Him. 
And we too rejoice that we have experienced the Messiah's saving grace in our 
lives!</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. Sometimes they get me depressed or worried. They rob me of the 
joy You want me to experience. I give them to You and ask You to help me 
constantly do so. Help me with my thought life today. Help me think positive and 
pleasant thoughts today, thoughts that please You.</p>
<p>Father, You are sovereign over world events. Lord, I ask 
that You would grant us a time of peace, safety and freedom, if this is Your 
will. I know that near the end, things will get worse and worse; there will be no 
ultimate peace until the Prince of Peace returns to reign. I also pray for the 
peace of Jerusalem 
(<a href="javascript:scripture('../../scripture/ps1226.htm')" target=_self class=pop>Psalm 122:6</a>).</p>
<p>Lord, help me to be friendly and sociable. May I make the 
effort to know my neighbors or folks I see regularly. Help me to be a team 
player within my church, neighborhood and community. If I am to love my neighbor 
and others, I need to get to know them! Help me overcome social anxieties and 
may others enjoy my company because Christ has brought me joy.</p>
<p>I pray for the European nation of the Netherlands. Thank 
You that 4.5% of the population claims to be born-again evangelical. In a way, 
this is sad because the Dutch nation was at one time one of the most evangelical 
nations of the world. Raise up a new and invigorated generation of believers. 
Use them, we pray, to make a big difference. May the number of evangelical 
Christians double over the next decade, I ask.</p>
<p>Lord, bless Engineering Ministries International and its 
mission &quot;to mobilize Christian design professionals to serve the poor in 
developing countries—[they] proclaim the Good News of Jesus Christ by empowering 
people to transform their world—through the design and development of hospitals, 
schools, orphanages, bridges, water supplies, wastewater facilities and more.&quot; 
Use these engineers to lead people to Christ, testify to the grace and 
compassion of God and to show the love of Christ, I pray.</p>
<p>Use me to show compassion today, I ask in the Master's 
Name, Amen.</p>

