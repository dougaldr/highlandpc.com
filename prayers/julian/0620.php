<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 20
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Abba Father,</p>
<p>You are the Father of Mercies 
(<a href="javascript:scripture('../../scripture/2cor13.htm')" target=_self class=pop>2 Corinthians 1:3</a>). 
You delight in showing mercy over and over again to Your creation and to human 
beings in particular. You are so merciful that we presume upon Your mercy and 
we are surprised on those occasions when You pull back Your mercy. We thank You 
not only for Your mercies which keep us alive and sustain us, the rain for our 
crops or the health we have—but we especially appreciate the spiritual blessings 
You offer us. Through Jesus Christ, we escape what we deserve (Your mercy) and 
You grant us a favored status (Your grace). Thank You for being the Father of 
mercies!</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. May I&nbsp;find my strength in His power, not my own. May Your Spirit 
control me today.</p>
<p>First John says that if I confess my sins, You are faithful and&nbsp;just to forgive 
me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 John 1:9</b>). I 
confess the sins I am aware of now and I ask Your Spirit to search my heart (Pause 
to admit them to God.).</p>
<p>Today I pray for the small Pacific Island of Nauru. Thank 
You that 9% of the population claim to be born-again evangelicals. Continue to 
use the Jesus film to reach people. I pray that the number of believers there 
would triple over the next decade and use this island as a spiritual lighthouse 
in the region.</p>
<p>Thank You for the ministry of Children to Love 
International. &quot;As a result of [their] efforts, orphaned children are becoming 
fully devoted followers of Christ through the ministry of Christian churches in 
their country.... Every effort is extended to meet the goal of having children 
discipled by the local Christian churches in Romania. This is the primary way 
they...grow in their spiritual life.&quot; We know that Romania has tens of 
thousands of orphaned and abandoned children. Help these missionaries as they 
love them in Jesus' Name.</p>
<p>I pray for college and university students everywhere. 
Lord, college professors—especially in the social sciences—are noted to be 
negative, cynical and out to destroy the faith of Christians or others who 
stand in the way of their agendas. Thank You that not all are this way. Help 
all professors to learn to respect their students and their views, even if they 
disagree with them.&nbsp; Help them hold back from intimidation and persuasion by 
mockery. Keep them from exploiting a captive audience. Instead, may they 
respectfully educate their students as to the nature of various viewpoints and 
to encourage them to think reasonably. O Father, stop the virus of cynicism and 
intimidation. May Your children not be moved by professors with egos out to 
replicate themselves.</p>
<p>Bless those who work as earth drillers. Whether it is 
drilling for water, oil, natural gas, or a number of projects, give them safety 
and help them to do their jobs well. May those who know You shine and help 
them to share Jesus with their comrades.</p>
<p>Now open my eyes to Your will for me today, I pray in 
Christ's Name, Amen.</p>

