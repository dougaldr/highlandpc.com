<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 27
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Lord,</p>
<p>I praise You that You are a God of order, not confusion. 
We see this when we look at a single cell, a molecule, or an atom. The Laws of 
Science are laws because they were ordained in an orderly way by an orderly 
Creator. Even though this universe is cursed and the rate of decay intensified, 
even what seems to be chaotic is part of an orderly plan.</p>
<p>Lord, when life gets chaotic for me, help me to trust You 
as the one Who has a plan. Help me be a person of faith, one who trusts even 
when I cannot understand.</p>
<p>Lord, I celebrate my gender. Your Word tells me that You 
created mankind as male and female, and that both genders are in Your image. 
Both genders, therefore, reflect much of Who You are and what You are like. But 
both genders have been cursed by sin and its consequences, and this is likewise 
equally prevalent in both genders, even though it may surface in gender-related 
ways. Thank You for making me who I am. </p>
<p>Father, I have a number of personal needs, including the need for good 
relationships. Help me to get along with others, yet keep me from being held 
hostage to fear. If I want to be close to others, I need Your 
help to confront others when necessary; otherwise I will create the distance 
caused by a grudge. Other needs today include: __________.</p>
<p>Today I pray for the Afro-Caribbean nation of Barbados. 
Although this country recognizes complete religious freedom, and although many 
claim to be born-again, the quality of evangelical Christianity
there is often weak. Most babies are born out 
of wedlock, and false religions and cults are gaining ground because the Gospel 
does not seem to be working there. But we know that the Gospel does work, for 
true believers who submit to Christ as Lord. We pray that the Christian 
testimony there would be better. We pray that the evangelical church (i.e., the 
individual believers who make up the church) would espouse a Christianity worth 
reproducing.</p>
<p>Today we remember BCM, originally begun as the Bible Club 
Movement. We thank You for all BCM does: church planting, evangelism, 
discipleship, and teaching—working in 50 countries with over 700 career 
missionaries. Please, Lord, protect, provide, and give much fruit to each 
ministry attempt. Help the missionaries to get along with one another 
harmoniously, lead by Your Spirit, and bless all their efforts to Your glory.</p>
<p>Today we remember those linemen who repair our 
electrical lines after storms and disasters. We know their job is often 
thankless. We pray that You would keep them safe and bless their efforts as 
they bless us.</p>
<p>Now, Lord, help me 
follow Jesus today, for I pray in His Name, Amen.</p>

