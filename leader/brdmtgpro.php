<p id="pgt"><span class="pgh">Church and Leadership:</span>
<br>Board Meeting Protocol
<br><span class=byln>by Ed Vasicek
<br>Original September 1998&nbsp;&nbsp;&nbsp;&nbsp;  Revised July 2006</span></p>

<p>At Highland Park Church, we have three boards: an elders', 
deacons', and deaconesses' board. In an attempt to answer long-standing 
requests for guidance, I am offering this account of how we elders organize and 
move along our board meetings. Since this is an account of how we do it <i>and 
not a policy paper</i>, please take it for what it is: suggestions that have 
worked well for us.</p>
<p>Our approach might be called a &quot;Relaxed Parliamentary 
Approach.&quot; We sort of follow a few of <i>Robert's Rules of Order</i> in an 
indirect way.</p>
<p><b><u>Nominating Board Officers</u></b></p>
<p>Each board needs a chairperson and a secretary (to record 
minutes). It is helpful and almost necessary for the secretary to be 
computer-literate.</p>
<p>This can be a sensitive task, since we do not want to 
offend folks or make them feel rejected, yet we want to allow for change or 
variation <b>if</b> board members feel it is in the best interest of the board. 
So how do we select our Chairperson and Secretary? Sometimes several good 
candidates might be willing and able to serve.</p>
<p>Here's the best way to handle it. The current secretary 
takes nominations for chairperson. If the former secretary is no longer on the 
board, then a seasoned board member can take charge of nominations with consent 
from the rest of the board. Board members submit as many nominations (you may 
not nominate yourself) as they think appropriate. Sometimes there may be only 
one. The nominees should be seconded. A person may <i>nominate</i> more than 
one individual and may <i>second</i> more than one individual, but may only <i>
vote</i> for one nominee. The nominees can reject the nomination, but the 
burden is on them to speak up if they desire to reject the opportunity.</p>
<p>The board members choose the officer from among the 
nominees either by raising a hand (while those being voted upon close their 
eyes) or by paper ballot. If there are more than two candidates and none 
receives a majority, you need to conduct a run-off election between the top two.</p>
<p>The new chairperson then leads the process of nomination 
for the secretary, following the above procedure. At all meetings the 
chairperson only votes on motions to break a tie.</p>
<p>The chairperson is responsible to keep the meeting 
organized and on track. He or she can prepare a tentative agenda if desired.</p>
<p><b><u>Keeping Discussion on Target</u></b></p>
<p>One challenge we all face on our various boards is the 
enjoyment of conversation not related to matters of our particular board. We 
elders, for example, sometimes enjoy talking over deacons' matters. Why? 
Because we enjoy seeing things fixed up, purchased, etc., and so we are tempted 
to spend time in deacons' territory. We now actively work at NOT doing this. 
But how do you keep a meeting on track? Here are some tips we collected from 
the school of hard knocks.</p>
<p>First, if conversation runs amuck, the chairperson may say, 
&quot;I will entertain a motion on the subject.&quot; If someone makes a motion and it is 
seconded, focus of discussion can now center on the motion.</p>
<p>Second, if no motion is forthcoming or if the matter is not 
board domain, the chairperson may say, &quot;If there are no objections, let's move 
on to the next item.&quot; It is his/her job to nudge the group back into true board 
matters. I have been nudged this way many times myself, and I appreciate it 
when things are kept on target, even if I have been the culprit leading us to a 
tangent. Other board members can offer a suggestion to the chair that the board 
move on; but it is the chairperson who must make the decision one way or the 
other.</p>
<p>Third, if a board member senses that the discussion is 
getting off the subject at hand, he/she might make a motion. It is then the 
chairperson's job to ask for a second and then discuss the motion. This method 
of moving things along is open to all members except the chairperson, who cannot 
make a motion and can only vote in the event of a tie. However, he or she may 
say, &quot;I will entertain a motion on the subject.&quot;</p>
<p>Fourth, a board member might ask the chairperson for a 
ruling. Is the matter under discussion is really a matter for that particular 
board?</p>
<p>Fifth, if an issue is truly appropriate for the board upon 
which you serve, but discussion is complicated and tentative, suggest to the 
chairperson that a designated board member or volunteer study out the issues and 
bring a report to the next meeting.</p>
<p>Sixth, do not be afraid to table (temporarily set aside) 
matters. Believe it or not, some things clear up with time. One of our elders, 
Dale Daniels would often quote what I now call Dale's axiom: &quot;If it's a good 
idea this month, it will be a good idea next month.&quot;</p>
<p><b><u>Consent/Voting</u></b></p>
<p>There are two ways to pass motions: by vote or consent.</p>
<p>For many decisions, particularly important or potentially 
divisive ones, it is best to vote. Procedure may sound like this:</p>
<p><b>Voting</b></p>
<p>Chairperson: I'll entertain a motion on this matter. 
(Remember, the chair cannot make motions.)</p>
<p>Member 1: I move we buy a new changing table for the 
nursery, not to exceed $75.</p>
<p>Member 2: I’ll second the motion.</p>
<p>Chair: Is there any discussion?</p>
<p>Discussion (if any)</p>
<p>Chair: If there is no other discussion, all if favor of 
the motion say &quot;Aye.&quot; (After a moment), “All opposed, same sign. Motion carries 
(or fails). If a vote is expected to be close or the number of &quot;ayes&quot; is 
unclear, each member of the board should be polled (asked for his vote 
individually).</p>
<p><b>Consent</b></p>
<p>Consent is used for non-controversial matters, and we 
elders use this form of approval most often. For example, meeting minutes 
(reports) are typically accepted by consent or &quot;thumbs up.&quot; When the group is 
in obvious agreement with a matter, consent is used. A good rule is &quot;when in 
doubt, bring it to a vote.&quot;</p>
<p>Consent sounds like this:</p>
<p>Chair: <b>If there are no objections</b>, we'll accept the 
minutes (or motion) by consent.</p>
<p>If any board member objects, the matter must be brought to 
a vote.</p>
<p>You want to make it easy for folks to express themselves. 
We don't want board members saying one thing at a meeting (or being silent) and 
then expressing their disagreement in the parking lot after the meeting! We 
call this the &quot;parking lot syndrome.&quot; This is a mortal sin as far as church 
boards go! That's why anything controversial should be discussed and voted 
upon.</p>
<p><b><u>The Danger of Disharmony via Voting</u></b></p>
<p>It is often wiser to meet a dissenter part way, if 
practical. Voting can create winners and losers, and it is important that every 
board member feels respected and heard by all the other members. Often a new 
motion—with a compromise or qualification added—makes for board harmony. 
Harmony is a valuable asset and worth working for.</p>
<p>Motions can be withdrawn before voting if the one who made 
the motion and the one who seconded the motion agree to withdraw it.</p>
<p>Demanding motions be passed unanimously is a bad move; it 
creates an environment where people do not share their true opinions, but vote 
for motions out of peer pressure. This will create the &quot;parking lot&quot; syndrome&quot; 
mentioned above.</p>
<p><b><u>Other concerns</u></b></p>
<p>We open and close meetings with prayer. The elders’ board 
also spends time praying for those within the Flock who are experiencing 
struggles. It is always appropriate, on any board, to take time to pray for the 
needs of board members or pressing issues.</p>
<p>Thank you for serving our Savior and this local family of 
believers. God bless you!</p>
  
