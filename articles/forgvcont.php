<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Forgiveness Controversy
<br><span class=byln>by Ed Vasicek</span></p>

<p>Back in 1999, I preached a series on the subject of 
forgiveness. Many folks in our congregation had never heard the approach I 
took. A number commented that they heard Christian leaders on the radio or in 
magazines take the opposite position during the weekdays between my sermons.</p>
<p>The world and much of evangelicalism believe that we should 
forgive unconditionally. Secular psychologists and popular preachers have 
formed an alliance that intimidates many of us from even considering the 
alternatives. Yet many Bible teachers believe that forgiveness is conditioned 
upon repentance. I am in the latter group.</p>
<p>Bible-believing Christians agree that we are to forgive 
others as God forgives us. If you believe God forgives unconditionally, this 
would logically lead you to Universalism, the belief that everyone is saved; no 
one is under the wrath of God, because God’s wrath is not directed toward those 
who are forgiven. If God forgives unconditionally, then none are unforgiven. 
Most evangelicals recognize that multitudes are lost, yet they say that God 
forgives unconditionally. Do you see the contradiction here?</p>
<p>That the Lord does not forgive unconditionally is clear in 
Scripture: &quot;Surely at the command of the L<span class=sm>ORD</span> it came upon Judah, to remove 
them from His sight because of the sins of Manasseh… <b>and the LORD would not 
forgive</b>…&quot; (<b>2 Kings 24:3-4</b>).</p>
<p>So set aside your preconceptions and consider what the 
Bible actually teaches about forgiveness. See if my points are validated by 
God’s Word.</p>
<p>First, we are to forgive others in the same manner God 
forgives us. <b>Ephesians 4:32</b> reads, &quot;Be kind and compassionate to one another, 
forgiving each other, just as in Christ God forgave you.&quot;</p>
<p>Second, God’s manner of forgiveness requires our 
repentance. <b>Acts 3:19</b> gives us a taste of Apostolic preaching: &quot;Therefore 
repent and return, so that your sins may be wiped away, in order that times of 
refreshing may come from the presence of the Lord....&quot; Note that repentance (a 
change of mind, including regret for sin) is the required condition for God’s 
forgiveness. God does not hold us to a higher standard than He holds Himself. 
His grace extends only to those who repent; He does not expect us to be more 
gracious than He is.</p>
<p>In <b>Acts 8:22</b>, after Simon had sinned by seeking to buy the 
Holy Spirit, Peter rebuked him with these words: &quot;Therefore repent of this 
wickedness of yours, and pray the Lord that, if possible, the intention of your 
heart may be forgiven you.&quot; Note that forgiveness is neither presumed nor 
assumed. It is conditioned upon repentance.</p>
<p>So if we forgive as God forgives, and God forgives in 
response to repentance, logically we are to forgive others when they repent.</p>
<p>Third, we must ask the question, &quot;Forgiveness at what 
level?&quot; When we turn from our sins (repent) and turn to Christ, ALL our sins 
are forgiven: past, present, and future—at least, as far as salvation goes. 
Nothing can take us out of God’s hand. We are given the righteousness of 
Christ, the seal of the Holy Spirit, and already legally seated in heaven (<b>Ephesians 
1-2</b>).</p>
<p>Even though we cannot lose our salvation, we can lose our 
fellowship with God. When the believer lives in rebellion against God, sin 
blocks his ability to communicate with God 
(<a href="javascript:scripture('../scripture/ps6618.htm')" target=_self class=pop>Psalm 66:18</a>) 
and places him in a position to be disciplined by the Lord 
(<a href="javascript:scripture('../scripture/1cor1132.htm')" target=_self class=pop>1 Corinthians 11:32</a>). 
If we grieve the Holy Spirit 
(<a href="javascript:scripture('../scripture/eph430.htm')" target=_self class=pop>Ephesians 4:30</a>)—the 
One who assures us of our salvation 
(<a href="javascript:scripture('../scripture/rom816.htm')" target=_self class=pop>Romans 8:16</a>)—we 
could logically lose our sense of assurance as we quiet His voice.</p>
<p>How do we remedy this situation? <b>1 John 1:9</b> makes it 
clear: &quot;If we confess our sins, he is faithful and just and will forgive us our 
sins and purify us from all unrighteousness.&quot; To my way of thinking, this 
implies that if we do NOT confess our sins, he will NOT forgive us our sins. If 
God forgives us unconditionally, the verse becomes meaningless.</p>
<p>Sometimes forgiveness involves God not zapping people 
physically here on earth. I think that when Jesus asked the Father to forgive 
those who crucified Him, He meant, &quot;Don’t zap them.&quot; His example clearly makes 
a distinction between sinning in ignorance (&quot;for they know not what they do&quot;) 
and sinning with full knowledge. </p>
<p>Fourth, we must boldly apply these concepts to our 
relationships. If others confess how they have sinned against us, we are 
obligated to initiate the forgiveness process. That does not mean we 
immediately trust them, for credibility can only be restored through faithful 
behavior over time. Sometimes credibility can never be fully restored. In such 
a case, forgiveness (a restoration of relationship as if the offense had never 
occurred) can never be complete.</p>
<p>Jesus put it this way: &quot;So watch yourselves. If your 
brother sins, rebuke him, and if he repents, forgive him. If he sins against 
you seven times in a day, and seven times comes back to you and says, 'I 
repent,' forgive him&quot; (<b>Luke 17:3</b>). Although shorter accounts of Jesus’ teaching 
could be wrongly interpreted to suggest unconditional forgiveness, the longer 
passage in Luke presents a more detailed truth and qualifies our obligation to 
forgive.</p>
<p>We cannot always simply choose to forgive; forgiveness can 
be a struggle that may take a long time; it sometimes involves a process of 
sorting through emotions and moving gradually toward forgiveness (complete 
reconciliation). Sometimes we might need to say, &quot;With God’s help, I will try 
to forgive you, but I am not there yet.&quot; Honesty is crucial at this point. 
Christians who believe they must immediately forgive an offense often become 
good liars—to themselves first, and then to others.</p>
<p>Even when someone does not repent, it is often in our best 
interest if we can let a hurt go. We should do so because we know that 
bitterness drains us and keeps us from enjoying the life God has given us. We 
should not do so out of false guilt, but for our own benefit. Do not confuse 
this with genuine forgiveness, which brings complete relational restoration.</p>
<p>I have seen people horribly victimized and then plagued by 
guilt because they cannot seem to forgive the one who abused them. This guilt 
adds to their heavy burden. Often such guilt is unwarranted because the 
offender has not even requested forgiveness or evidenced genuine repentance. 
Sometimes people simply do not want to forgive because they are grasping for 
control—withholders of grace. Forgiveness evens the score, and some of us 
want the advantage and excuse of being a wronged person.</p>
<p>God recognizes that some situations are so evil that it is 
natural for us to want to get even. God Himself is a God of justice, so since 
we are in His image, we desire justice. Paul wrote: &quot;Do not repay anyone evil 
for evil.... Do not take revenge, my friends, but leave room for God's wrath, for 
it is written: 'It is mine to avenge; I will repay,' says the Lord. 
On the contrary: 'If your enemy is hungry, feed him; if he is thirsty, give him 
something to drink. In doing this, you will heap burning coals on his head.' 
Do not be overcome by evil, but overcome evil with good.&quot; Find consolation in 
knowing that God will hold everyone accountable; none can evade Him.</p>
<p>I have presented what I believe to be the correct teaching 
of Scripture about forgiveness. My challenge to you is to study the Scriptures 
and test the above, especially since my understanding is the minority 
viewpoint. If you disagree with me, I’ll forgive you!</p>

