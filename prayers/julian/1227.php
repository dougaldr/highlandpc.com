<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 27
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When Solomon dedicated The Temple, he prayed, &quot;But 
will God really dwell on earth with men? The heavens, even the highest heavens, 
cannot contain you. How much less this temple I have built! Yet give attention 
to your servant's prayer and his plea for mercy, O L<span class="sm">ORD</span> 
my God. Hear the cry and the prayer that your servant is praying in your 
presence&quot; (<b>2 Chronicles 6:18-19</b>).</p>
<p>Father, we dare not limit You to a building, an image, or a 
symbol. In our quest to control You, we try to reduce You down to manageable 
size. We want to believe that we can get You to do what we want You to do. 
Instead, O Lord, You remind us that You are God, not us. You are the Master, 
not us. Not only can our puny buildings not contain You, the entire universe 
cannot do so.</p>
<p>You meet our needs out of Your grace, not our controlling 
power. Even the faith we have is a gift from You. So instead of seeking to 
control You, O Lord, help me to trust in You.</p>
<p>O, Lord, please bring help to the hurting, the emotionally 
wounded and the devastated. Strengthen those stung by disappointment, encourage 
the demoralized and bind up the emotionally wounded. Especially help: __________.</p>
<p>Father, I pray for those who go from marriage to marriage, 
job to job, church to church, credit card to credit card. Perhaps this includes 
me. Others are tossed when it comes to belief; again, perhaps I have this 
tendency. May I decide once and for all that my life is first and foremost about 
You, not me. Unite my divided heart, O Lord.</p>
<p>Lord, help me to be cautious myself, for I am prone to 
wander. I am not above falling away from You or being ensnared. Your Word warns, 
&quot;Let him who thinks He stands take heed lest he fall&quot; (<b>1 Corinthians 10:12</b>). 
My own vulnerability and propensity to sin is easy to rationalize or deny. 
Please keep me from such foolishness. Help me make no provision for my sinful 
nature.</p>
<p>My King, today I pray for the Indonesian island of Sumatra 
with its 40 million people. The overwhelming majority of the people claim to 
follow Islam, but thank You that perhaps 10% of the people are born-again 
evangelicals. O Lord, help these ones to find Your wisdom and direction. May 
they grow strong in You and share the Word effectively. I ask that the 
percentage of believers would double over this next decade.</p>
<p>Thank You for the tentmaking (serving as a missionary while 
working at a secular job) ministry of Global Opportunities whose mission is to 
&quot;mobilize and equip missions-committed lay Christians to serve abroad as 
effective tentmakers, primarily to least-reached peoples. [They] provide 
prospective tentmakers with overseas job resources, job and missions' counsel, 
training and mentoring to increase effectiveness, connections with coworkers in 
country and limited support on the field. Global Opportunities seeks to partner 
with individuals, churches and mission agencies to accomplish this task.&quot; Bless 
this work and make it effective for You, I pray.</p>
<p>I commit my friends, family and life to You today, in 
Jesus' Name, Amen.</p>

