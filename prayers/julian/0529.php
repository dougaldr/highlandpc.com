<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 29
<br><span class=byln>by Ed Vasicek</p>

<p>My Wonderful God,</p>
<p>The Savior is sometimes referred to as “The Bridegroom” 
(<a href="javascript:scripture('../../scripture/john329.htm')" target=_self class=pop>John 3:29</a>). 
As a bridegroom anticipates being with his bride, so the Lord 
Jesus Christ is waiting to enjoy His bride—the completed church—forever. 
One day, we will all be with Him, celebrating the marriage supper of the Lamb 
and enjoying our destiny as Christians. Like the ancient bride in the Middle 
East, the bridegroom will come at an unexpected time to literally sweep the 
bride off her feet and carry her away to a banquet at her father’s home, so we 
wait to hear, “Here's the bridegroom! Come out to meet him!” (<b>Matthew 25:6</b>). 
Just as a groom would build on a room to his father’s house for him and his 
bride, so our Lord Jesus is preparing a place for us in glory in His Father’s house 
(<a href="javascript:scripture('../../scripture/john1423.htm')" target=_self class=pop>John 14:2-3</a>).</p>
<p>But Lord, right now, I am not in Your house. I am an alien 
and stranger, Your ambassador, bearing testimony to Your grace in a world that, 
by and large, rejects You. So help me to be in this world—to participate—but 
not to be “of the world”—to become corrupted by it.</p>
<p>Help me to resist temptation and the lures of the Satan today. I put on Your 
armor by faith, so that I may do battle for You as a faithful soldier. I claim 
the victory because greater are You, Holy Spirit, Who is in me than he who is in 
the world—the devil (<b>1 John 4:4</b>).</p>
<p>Father, I have some personal health concerns including: __________.</p>
 I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>God, I pray for the European nation of Macedonia once part 
of the former Yugoslavia. This has been a pretty bleak area for the Gospel to 
penetrate; only .2% of the population professes to be born-again evangelicals. 
Thank You, that though small, the evangelical church is growing. O Lord, whet 
the appetite of the people for You; help them taste and see that the Lord is 
good. Thank You for the new Bible translations and it is my prayer that people 
would eagerly procure and read them. May Your Spirit grant the repentance that 
leads to life for many here; may the church double over the next five years, I 
ask.</p>
<p>Lord, thank You for the bold ministry of Exodus 
International “a nonprofit, interdenominational Christian organization 
promoting the message of &quot;freedom from homosexuality through the power of Jesus 
Christ.&quot; Thank You for the over “120 local ministries in the USA and 
Canada...over 150 ministries in 17 countries.” Thank You for this ministry which guides Bible-believing 
churches to, on the one hand, extend the Gospel to all and, on the other 
hand, maintain that homosexual practice is wrong. We pray that many would find 
freedom from the power of homosexuality through this and affiliated 
organizations. Empower the workers by Your Spirit, empower their clients as 
well and provide for, protect and grant great success to this ministry.</p>
<p>Now, Father, help me to check in with You throughout my 
day, I pray in Jesus’ Name, Amen.</p>

