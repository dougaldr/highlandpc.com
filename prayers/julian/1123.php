<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 23
<br><span class=byln>by Ed Vasicek</p>

<p>My Great Master in Heaven,</p>
<p>You become angry with those Who give you mere lip service, as You explain, 
when &quot;people come near to me with their mouth and honor me with their lips, but 
their hearts are far from me&quot; (<b>Isaiah 29:13</b>). You are not a God Who wants 
image or impression; You want substance and heart-felt sincerity. You do not 
want our token efforts or half-hearted allegiance. You want us to love and serve 
You with all of our hearts. Your Son discouraged us from praying with useless 
repetition or a sheer volume of words 
(<a href="javascript:scripture('../../scripture/matt67.htm')" target=_self class=pop>Matthew 6:7</a>). 
You are only pleased with the simple and sincere. You are a 
no-nonsense God. May I never forget this truth, O Lord.</p>
<p>Lord, help me to bear up under the trials, disappointments, fears and 
tribulations of life including: __________.</p>
<p>Lord, I remember that the trying of my faith works patience and patience 
experience and experience hope 
(<a href="javascript:scripture('../../scripture/rom534.htm')" target=_self class=pop>Romans 5:3-4</a>). 
You use trials in my life to develop me, to teach me, to try me, to 
discipline me and to make me strong, for when I am weak, then I am strong. Lord, 
sometimes this sounds more like theory than practice. I often do not appreciate 
this. But then I remember that You are the Potter and I am the clay. I'll try to 
trust You even when I cannot understand Your ways.</p>
<p>Help me to resist temptation and the lures of the Satan today. He and his 
many demons are on the prowl, but help me not to be ignorant of his schemes. 
Help me overcome him by the blood of the Lamb and the word of my testimony 
(<a href="javascript:scripture('../../scripture/rev1211.htm')" target=_self class=pop>Revelation 12:11</a>).</p>
<p>Lord, help me to remember not to make decisions based upon my emotions. Help 
me not to confuse the emotional with the spiritual. Help me remember that, “He 
who trusts in his own heart is a fool” (<b>Proverbs 28:26</b>); instead help me 
cling to Your Word and the direction given by wise fellow-believers. Help me be 
cautious and wise in making decisions and to weigh the long-term effects of 
my choices carefully and realistically.</p>
<p>Lord, You have done a great work in the African nation of 
Zimbabwe. Thank You that over 25% of Zimbabwe’s 12 million people claim to be 
born-again evangelical Christians. I praise You that You doubled the number of 
believers during the 1990s. It is my prayer that the church would not only 
continue to grow in numbers but in depth. Father, AIDS and land issues have 
plagued this nation. Turn things around, O Father, and may You get the glory!</p>
<p>Lord, bless the Associations of Baptists for World 
Evangelism, a ministry that serves churches of many Baptist and non-Baptist 
groups. As they explain it, &quot;the Association of Baptists for World Evangelism 
is an independent mission agency that exists to serve local churches in the task 
of sending missionaries around the world. ABWE, with ministries in over 70 
countries, services over 5,000 churches and 1,250 missionaries.&quot; O Father, 
grant them harmony, safety, provision and the power of Your Spirit as they 
serve You and share Christ with others. Help those they reach to become mature 
in You. Bless them, I pray.</p>
<p>Now, Lord, help keep my heart true to You today, I ask in 
Messiah’s Name, Amen.</p>

