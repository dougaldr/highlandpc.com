<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 20
<br><span class=byln>by Ed Vasicek</p>

<p>Maker of Heaven and Earth,</p>
<p>Thank You that You are the God Who equips Your children 
&quot;with everything good for doing his will&quot; (<b>Hebrews 13:21</b>). Lord, many people 
find it is easy to tell us what to do.  But they often do not show us how to do it But You 
have set up Biblical churches to train us 
(<a href="javascript:scripture('../../scripture/eph41113.htm')" target=_self class=pop>Ephesians 4:11-13</a>) 
and one purpose of Your Word is to train us further 
(<a href="javascript:scripture('../../scripture/2tim31617.htm')" target=_self class=pop>2 Timothy 3:16-17</a>). 
Lord, help me think of You as an instructor, a coach, a trainer; help me take 
advantage of the training You offer me and remind me that I am Your apprentice-in-training.</p>
<p>Lord, help me to be a fair and just person. As a sinful 
human being, I have a strong bias toward my loved ones and myself. I admit that 
my heart—not just everyone else's but mine as well—is deceitful and 
desperately wicked 
(<a href="javascript:scripture('../../scripture/jer179.htm')" target=_self class=pop>Jeremiah 17:9</a>). 
When decisions involve me or those close 
to me, my emotions kick in and can easily distort my perspective. 
Additionally, I find it easy to be unsympathetic toward strangers or those who 
are not close to me. So help me put myself in the shoes of others and seek the 
counsel of the godly. </p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God.</i>).</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Today I pray for the massive nation of Russia. O Lord, how 
complex this land is. On the one hand, religious freedom has been greatly 
expanded since the fall of Communism; on the other, some of those gains are 
being lost. So many millions have died in concentration camps for their faith, 
O Lord. May those days never return! But now the government and particularly 
the Orthodox Church is promoting intolerance of other faiths. Less than 1% of 
the population claim to be evangelical and the challenges are many. Lord, may 
Your Spirit sweep across this land and draw a large number of people to saving 
faith, I ask. Give the church wisdom.</p>
<p>I remember New Mission Systems 
International whose &quot;vision is to support and place people in fields of 
service to minister God's love so that people of all nations may be saved, 
individuals may be discipled, churches may be planted and matured and light may 
be brought into those places which have little hope of encountering God.&quot; Bless 
them as they send young adults on summer or short-term missions (up to two 
years) to a variety of nations. Give them fruit for their labor, harmony, the 
power of Your Spirit, provision and protection, I request.</p>
<p>Help me to reflect the love of 
Christ today, I pray in His Name, Amen.</p>

