<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 21
<br><span class=byln>by Ed Vasicek</span></p>

<p>Precious Lord,</p>
<p>I know that You have many aspects to Your being, 
characteristics and attributes that Your Word reveals. The Bible is clear that 
You are an angry God. Although it is not popular for us to consider ourselves, 
&quot;Sinners in the Hands of An Angry God,&quot; that is, in fact, our situation. Your 
wrath is revealed from heaven. Earthquakes, hurricanes, tornados, lightning 
strikes, tsunamis and hailstorms all remind us that something is wrong with the 
universe, that it is indeed cursed. And, as nature leads us to believe that You 
are the Great Creator, so disasters remind us that You are angry.</p>
<p>Thank You, Lord, that Your anger and Your justice are not 
Your only attributes. You are a God of mercy, love, and grace as well. So You 
sent Jesus to bear the wrath and anger that was due us. As Your Word says, He 
&quot;became a curse for us.&quot; Because of Jesus, You—an angry and holy God—are 
now free to shed Your grace and mercy upon us without compromising Who You are.</p>
<p>Father, Son, and Holy Spirit, Your character is perfectly 
balanced. Help me to live a balanced life, to make balanced choices, and to 
seek to grow in all areas. Help me succeed spiritually, relationally, 
intellectually, physically, socially, and to live in a reasonable manner 
financially, if that is Your will. And if not, help me to accept what I cannot 
change and what You do not want to change. Help me not to be driven by a guilt 
complex (feeling more responsible than I should) nor by peer pressure, but by 
Biblical conviction and reason. Help me recognize nonsense and unhealthy 
attitudes for what they are.</p>
<p>Gracious God, help me to recover from past events that have 
harmed me in one of the above ways. Although I want to learn from the past, and 
although I recognize I can better minister and sympathize with others because of 
my past, these advantages are joined to disadvantages. Help me to escape from 
the prison of my past and to boldly agonize forward toward the calling of Jesus 
Christ for my life.</p>
<p>Please address the following needs: __________.</p>
<p>Today I pray for one of the nations of the former Soviet 
Union, Azerbaijan, just north of Iran. Lord, most of this nation is Islamic 
with less than one-tenth of one percent Evangelical Christian. Although there 
is some measure of religious freedom, we know the nation allows no foreign 
missionaries. This is a poor land, and people need Jesus. We pray that You 
would multiply the efforts of the few Christians who live there. We pray that 
the church would grow exponentially, and that Your Spirit would draw great 
numbers to Jesus Christ.</p>
<p>Today I remember AMF, the American Messianic Fellowship. 
Bless their efforts in sharing the Gospel &quot;to the Jew first&quot; as they build 
bridges of friendship. Thank You that this ministry is conducted ethically and 
honestly. Bless them as they reach out and train others to reach out to Your 
chosen people.</p>
<p>Today we pray that You would guide and assist all the 
emergency radio dispatchers in our area. We know that they form an important link 
between the need for emergency help and getting that help on the scene. Keep 
them alert, and may they excel in this important service.</p>
<p>Help me to think 
of You often throughout my day, I pray, in Jesus' Name, Amen.</p>

