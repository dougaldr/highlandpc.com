<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 28
<br><span class=byln>by Ed Vasicek</p>

<p>Shepherd of All Who Know You,</p>
<p>Thank You for being the God Who is a shepherd—a God Who 
leads His children. Although we must admit that Your leading is not always 
clear, it is clear when You choose it to be clear. You’ve given us Your Word 
which provides Your leading in most situations. You have also given us Your 
Spirit Who is the down payment for the complete redemption we will one day 
experience. You have given us the Body of Christ and those who are gifted with 
wisdom and knowledge. And You have given us prayer, by which we can ask You for 
wisdom. O God, You have taken such great pains to lead us! Help us listen to 
Your voice! Help us spend time in Your Word, with Your people and in prayer. 
As our Shepherd, You do not want us to go astray or wander into the paths of 
deception. Thank You, Lord, for being my Shepherd, for leading me in paths of 
righteousness for Your Name’s sake.</p>
<p>Today I pray for safety. I ask You to keep me safe. I ask 
You to keep my loved ones safe. Keep us from harm and evil of every kind: 
physical, spiritual, moral and relational. I ask You to put a hedge around my 
loved ones and me and send guardian angels to watch over and protect us, I 
ask. I pray for Your protection night and day, 24 hours a day, seven days a 
week.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I find my 
strength in His power not my own. May Your Spirit control me&nbsp;today.</p>
<p>Father, today I pray for the African nation of Swaziland. I thank You for the 
mighty spiritual harvest in that land over the years and that nearly 30% of the 
population claim to be born-again evangelicals. Praise Your Name. Yet, Lord, 
the church is not as strong as she should be. About 70% of the church members 
are women, so there is a gender lopsidedness that makes it seem as though the 
Gospel were not really masculine. In addition, many of the believers still 
struggle with witchcraft. Lord, the church needs a new generation of strong 
leaders; train them and raise them up, I ask. May the Christian men take deep 
root and take leadership in the churches and help the church to know how to 
respond to the major AIDS epidemic there. Help the economy to improve so that 
the youth can find jobs instead of turning to delinquency, I ask. May the 
quantity of believers grow somewhat, but may the quality of believers grow 
tremendously over the next five years, I ask.</p>
<p>Lord, thank You for DELTA Ministries International as they equip the 
local church for short-term missions with &quot;field opportunities, travel, 
logistical support, recruitment, cultural preparation, prayer and training that 
is both biblical and practical.... On-field: field communication management, 
financial accounting, logistical support and problem solving.&quot; Bless this 
ministry and prosper it, I ask.</p>
<p>Now help me to serve You faithfully today, I pray in the 
Messiah’s Name, Amen.</p>

