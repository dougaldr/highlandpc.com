<li><p><b>Moses Tries To
Resist the Draft.</b></p>
<ol type=A><li><p>He just did not want 
to do it.</p>
<ol type=1><li><p>Moses does not represent the shy person who has rarely 
	tried to do anything because he is afraid of failure or rejection; Moses 
	represents the bold person who tried to accomplish the impossible and 
	failed. He is the broken person who has given up his dreams. He is the 
	disheartened, broken-spirited person who would rather live an uneventful 
	life than risk failure again.</li>
<li><p>In my opinion, Moses felt like he had trusted God, and 
	God dropped the ball. The idea that trying to do God’s work involves doing 
God’s work God’s way. (The end does not justify the means; the process is as 
important as the result.)</li>
<li><p>Moses wants to stay in the desert. I know many 
	Christians who want to do the same. They are afraid to do anything 
	different, afraid to jump in with both feet, afraid to really get serious 
	about God. They want to stay in that desert.</li>
</ol>
<li><p>Some people ask questions but do not want answers.</p>
<ol type=1><li><p>Many times questions are 
	objections, not inquiries.</li>
<li><p>Many times questions are 
	statements (cowardly assertions – I hate that approach.).</li>
<li><p>Example: Don’t you think God 
	made the universe well enough so that miracles are not necessary?&nbsp; 
	(Translation: God has never worked miracles, if He exists.)</li>
<li><p>Example: Don’t you think God 
	wants us to use sense to determine what is right and wrong? Don’t you think 
	the Bible only addressed issues of that era? (Translation: The Bible is 
	irrelevant to life today.)</li>
</ol>
<li><p>If one is unwilling, logic cannot persuade him.</p>
<p>Some Christians are under the delusion that if they answer 
a lost person’s questions and take away his or her excuses, that person will 
have no choice but to be saved...nonsense!</p>
<p>Remember Vasicek’s law: Because people <i>can</i> be 
logical does not mean they respond to logic - or <i>typically</i> operate on a 
logical basis; most people do what they want to do and believe what they want to 
believe.</p>
<li><p>Look at Moses and learn about yourself.</p>
<ol type=1><li><p>Who am I? 
(unworthy)</p>
<li><p>I don't even know 
Your Name. (Yahweh)</p>
<ol type=a><li><p>God tells Moses his name. (v. 14a)</p>
<li><p>God explains its meaning, (v. 14b) and </p>
<li><p>God shows how His Name relates to past, present, and future. (v. 
15)</p>
<p><img border="0" src="../../images/dezdisp/yahweh.jpg" width="163" height="79"></p>
<li><p>Ancient LXX translation: And God spoke to Moses, saying, I am THE 
BEING; and he said, Thus shall ye say to the children of Israel, THE BEING has 
sent me to you.</p>
<li><p>One of the Targum (Aramaic paraphrase) puts it this way: And the 
Lord said unto Mosheh, He who spake, and the world was; who spake, and all 
things were. And He said, This thou shalt say to the sons of Israel, I AM HE WHO 
IS, AND WHO WILL BE, hath sent me unto you.</p>
<li><p><b>Revelation 1:8</b>, &quot;I am the Alpha and the Omega,&quot; says the Lord God, 
&quot;<b>who</b> <b>is</b>, and <b>who</b> was, and <b>who is to come</b>, 
the Almighty.&quot;</p>
<li><p>By definition, God is the being Who has always been, uncreated... 
(Who created God? A logically ridiculous question: Who created the uncreated 
One?)</p>
<li><p>Notes at the bottom of Your handout: the word translated L<span class=sm>ORD</span> is Yahweh.</p>
<li><p>Every believer should know God’s Name and understand the 
difference between a name and a title.</p></ol>
<li><p>I don't have a plan. 
(Assemble the elders.) (3:16-22)</p>
<ol type=a><li><p>Start off with a reasonable request. (Let people take a 3-day 
journey.)</p>
<li><p>Pharaoh’s stubbornness will give you a justification to <i>up the 
ante</i>.... You reneged on your promise, so this time we are asking for more.</p>
<li><p>In the end, the Hebrews will be free and rich. (People will give 
them whatever they ask for.)</p></ol>
<li><p>What if elders do 
not believe me? (Staff, leprous hand, water to blood) (4:1-9)</p>
<li><p>I can't speak. (I'll 
give you words.) (4:10-12)</p>
<ol type=a><li><p>Could be a shy speaker on account of his broken condition.</p>
<li><p>More likely: he had forgotten much of the Egyptian and Hebrew 
Languages; he had spoken the Midian dialect for 40 years. (You lose it if you 
don’t use it.)</p></ol>
<li><p><u><b><i>The truth 
comes out in verse 13</i></b></u>.... The questions were a facade.</p></ol>
<li><p>God accomodates Moses. (4:14-16)</p>
<ol type=1><li><p>God is angry with 
Moses.</p>
<p>God did not say it, but I imagine Him complaining, &quot;Am I going to have 
trouble with you, too?&quot;</p>
<li><p>But God negotiates 
a compromise.</p>
<ol type=a><li><p>vs. 27 is fascinating: God called for Aaron before He talked to 
Moses.</p>
<li><p>God appears to experience time with us, but He is beyond time.</p>
<li><p>God’s anger at Moses was real, but well before this discussion, 
before He got angry and announced He would accommodate Moses, God had summoned 
Aaron.</p>
<li><p>We call God experiencing time and events with us as though He were 
a mere man, &quot;anthropapetheia&quot; or condescension.</p>
<li><p>Where did Aaron come from? Had he been that way before? My theory is that 
God gave him a revelation and helped him escape.</p>
<li><p>He did not necessarily come up to Mt. Horeb, but could have met Moses 
along the way.</p></ol>
<li><p>Although God 
accommodated Moses, He would not let Moses beg out of serving; the believer's 
life is to be a life of <b>service</b>. For some Christians, service is a dirty 
word.</p>
<p>Some of you need 
a “push” when it comes to serving the Lord. Every one of you in good health 
should have some sort of job or special participation in the church. Nursery, 
meals, teaching (Sunday School, Children’s Church, Kid’s Klub), music, work 
days, youth chaperone, flock group participator, AWANA listener, offering 
counter, usher, visiting shut-ins, women’s Bible study, setting up tables, or 
being a good will ambassador during greeting times, etc.</p></ol></ol>
