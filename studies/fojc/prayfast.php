<p>
Since modern Judaism no longer has the Temple (in the Biblical sense) many 
Rabbis teach that charity, prayer, and fasting atone for sin in the absence of 
animal sacrifice. At the time of Christ, matters were understood differently: 
sacrifices were being offered and Jews firmly embraced the concept that &quot;without 
the shedding of blood there is no remission of sin.&quot; Still, these three 
practices were held in high esteem and dubbed, in Hebrew, &quot;righteousness.&quot;</p>
<p>Dr. 
Ron Moseley writes, &quot;Ancient Jews believed that the three virtues of prayer, 
charity and repentance were the evidences of a heart which had truly turned 
from sin.&quot; Regarding charity or &quot;sounding the trumpets,&quot; Moseley comments:</p>
<blockquote>In the women's 
court of the Temple, during the first century, there were thirteen collection 
boxes for alms. They were wide at the bottom and narrow at the top and 
resembled trumpets. These boxes made a very recognizable sound as the coins 
were dropped into them. Often those Pharisees who wished to boast would drop a 
large number of coins in at once. This was called &quot;sounding the trumpet.&quot; It 
was this practice of letting everyone know how much they were giving that Jesus 
opposed.</blockquote>
<p>
Regarding the Lord's Prayer, Jesus' model was not very different from other 
Jewish prayers. It was concise, began with reverence for God, and was balanced 
between worship, expression of submission to God, requests about advancing His 
Kingdom, requests for personal needs, and relational concerns. First century 
Jews often prayed memorized prayers in addition to spontaneous ones. For 
example, in 
<a href="javascript:scripture('../../scripture/acts114.htm')" target=_self  class=pop>Acts 1:14</a>
while the disciples were waiting for Pentecost, it 
literally says in the Greek that they were giving themselves to THE prayers (the 
article is used), which probably refers to a combination of recited Jewish 
prayers, Psalms, and spontaneous petitions. In the modern evangelical world, it 
seems prayer has degenerated into mere lists of personal requests.</p>
<p>Ann 
Punton writes, &quot;Of the numerous blessings known today, at least a hundred were 
used in Jesus day.... When you see a rainbow, you praise God for the sign of 
His covenant. If you experience a natural phenomenon or freak of nature, you 
proclaim God's wonders. After a safe journey or deliverance from danger, you 
thank God for His mercies. For every occasion there is an appropriate 
response. Was it with this background in mind, though not necessarily in this 
manner, that Paul ordered Christians to 'Pray without ceasing!'?&quot;</p>
<p>Fasting means to abstain from (usually) food for a 
certain period as a way to intensify prayer or, more often, to express 
brokenness and repentance. Jews were required to fast annually on the Day of 
Atonement. We also read of fasting during times of disaster and misery. 
Fasting is also associated with humbling ourselves before God. Although the New 
Testament only mentions fasting in a few places, Scripture does endorse fasting 
as a Christian discipline as long as it does not degrade into the sorts of abuse 
mentioned in 
<a href="javascript:scripture('../../scripture/col22023.htm')" target=_self  class=pop>Colossians 2:20-23</a>. 
Like prayer and giving, fasting needs to be 
embraced with the heart, not mere ritual, if it is to be pleasing to God. 
Fasting should be a quiet and personal matter.</p>
