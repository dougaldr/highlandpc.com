<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 28
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty Father,</p>
<p>Not only are You our Rock, You are also our Fortress. You 
are One to Whom we can flee in our desperation. You protect us, You refresh 
us, You train us and send us forth with Your orders in Your time. We are 
vulnerable, and the more we are conscious of our vulnerability, the more we 
learn to experience You as our Fortress. We sing, &quot;A Mighty Fortress is Our 
God&quot; because it is so.</p>
<p>Although You are in all and present everywhere, You are 
distinct from Your creation. All creation depends upon You, but You are 
uncreated and responsible for all that has come to be.</p>
<p>Help me, 
Lord, to be a man or woman of the Book. Like the Psalmist, help me to 
contemplate it day and night, to make it part of my daily mainstay. Create in 
me, O Lord, a hunger for Your Word.</p>
<p>I ask You to 
help me relate well to other generations, to those both older and younger than 
me. People are people, but the times in which we were raised and our 
environments certainly leave their stamps upon us. Help me to be patient but 
assertive when appropriate. Help me seek to understand others, not simply 
reject those who are different from me.</p>
<p>Help me to 
resist temptation and the lures of the Satan today. I choose to take up Your 
armor, and I claim my victory as I am legally seated with Christ in heavenly 
places.</p>
<p>Lord, I am 
thankful for many things, three of which are: __________.</p>
<p>Lord, I empty 
myself before You so that Your Spirit may fill me. I find my strength in His 
power, not my own. May Your Spirit control me today.</p>
<p>Today I pray for Mayotte, the part of the Comoro Islands 
that have remained under the wings of the French. Lord, only one-tenth of one 
percent of these people claim to be born-again Christians, while nearly 97 
percent 
are Muslims. Thank You for the two churches that do exist, and we pray that You 
would multiply the number of believers and help new works to prosper. Send Your 
Spirit to draw people to Yourself and to open up their eyes to the claims of 
Christ. We pray that the number of believers would double every year for years 
to come.</p>
<p>I offer my prayer on behalf of <b>SEND</b> International, &quot;an 
interdenominational faith mission agency, with 500 missionaries in more than 20 
countries of Asia, Europe and North America.&quot; Help them as they are in the 
process of &quot;<b>S</b>tarting churches where there are none by <b>E</b>vangelizing the unreached,<b> 
N</b>urturing disciples, and<b> D</b>eveloping leaders from among 
the national people.&quot; Provide for the workers in every way, protect them from 
all harms and evils, and may Your Spirit bless their efforts and draw people to 
Yourself. May harmony reign among the missionaries.</p>
<p>I pray for those in the entertainment business. I pray 
that Christians may influence this industry more and more, and that decent 
quality entertainment would become more commonplace. May entertainers be 
burdened to nurture and preserve the traditional family. Regenerate many in 
this field, and help these born-again entertainers to mature in You and to carry 
their convictions into their work world.</p>
<p>Now strengthen me 
to serve You today, I pray, in Jesus' Name, Amen.</p>

