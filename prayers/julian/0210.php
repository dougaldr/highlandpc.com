<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 10
<br><span class=byln>by Ed Vasicek</span></p>

<p>Good Lord,</p>
<p>Thank You for being so longsuffering. Although You become 
angry and You express Your wrath, revealing it from heaven, You delay Your 
anger. You are patient, not willing for any to perish, but for all to come to 
repentance. You have put up with me as well, tolerating my stubborn heart and 
nurturing me toward growth. Thank You that You are not quick-tempered, nor do 
You ever act unreasonably.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>). 
So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Father, thank You that the Lord Jesus, after dying once for 
all mankind and at one point in time, ascended back into heaven, never to die 
again. As the risen victor, He sits at Your right hand and prays for us and 
defends us against the devil's accusations.</p>
<p>Help me, Lord, to likewise be on the lookout for ways I can 
help my brothers and sisters in Christ. My life is so busy, and sometimes I 
feel pulled in too many directions. But help me to care about others.</p>
<p>That brings me to setting priorities. Help me to seek Your 
kingdom and righteousness first, and then I will be able to view the rest of 
life from the right perspective.</p>
<p>Lord, I remember the small nation of Brunei, near 
Indonesia. This is an oil-wealthy country ruled by a sultan who might be the 
richest man in the world. The Christians in this land are finding it more and 
more difficult to live their faith. Islam is the state religion—and Muslims 
are pushing to make Brunei 100% Muslim. What freedoms are left from the past are 
being eliminated. We thank You that about 5% of the people in Brunei claim to 
be born-again, and we pray that their numbers might double. We also pray that 
the government would recognize the rights of its people to freely choose and 
share religious beliefs.</p>
<p>Today I pray for World Missionary Radio Fellowship, HCJB. 
Thank You for their many years of radio transmissions around the world. Thank 
You for their facility in Quito, Ecuador, and the multitudes of people who hear 
the Gospel and grow in the Lord through this strategic ministry. Provide 
harmony to all who make up the organizations, finances, and, most importantly, 
the backing of Your Holy Spirit.</p>
<p>Also bless the mail carriers and postal workers who provide 
such an important service to the rest of us. May many come to know You, and may 
those who do freely share their faith.</p>
<p>Lord, guide me in 
all my choices today, I pray, in Jesus' Name, Amen.</p>

