<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 12
<br><span class=byln>by Ed Vasicek</span></p>

<p>Gracious Father,</p>
<p>Thank You that You always stand ready to forgive. The only 
condition for the believer is confession, a repentant spirit. For those who do 
not know You, it is repentance toward You and faith in the Person and Work of 
our Lord Jesus Christ. When we trust Christ, all our sins are legally 
forgiven. When we sin as believers, sin causes relational problems with You, a 
tension in our relationship. Even though we are secure in You, sin creates an 
obstacle to our fellowship. When we confess our wrong, You immediately forgive 
us and cleanse us from all unrighteousness. We forgive others, with Your help, 
in the same way.</p>
<p>So, Father, teach me not only to forgive the repentant, but 
also to be able to ask for forgiveness. When I sin against another, help me not 
to justify or rationalize it, but to isolate my wrong from whatever wrongs may 
have been done against me. Help me to say, &quot;What I did was wrong, please 
forgive me&quot; and nothing more. If others have wronged me, help me to address 
that at a separate time. May I be good at knowing how to ask for forgiveness. 
And, as I grow, may I have less to repent!</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control me 
today. Help me not to grieve Him, but rather to give Him complete control of 
who I am and what I do.</p>
<p>Today I pray for the African nation of Burkina Faso. Thank 
You that about 8 percent of the population claim to be born-again, and thank 
You for the growth of the Gospel in some areas. At the same time, Lord, Islam 
is gaining converts in the north of the country. This nation is impoverished, 
prone to drought and famine. Unemployment and illiteracy are high. The 
missionaries have a rough task cut out for them. Make them effective, provide 
health, finances, and harmony for them; may Your Spirit fill them and empower 
them; help them walk closely with You and not get discouraged with the 
monumental challenges before them. We pray that You would continue to use the 
Jesus Film, that You would raise up strong and solid leaders and training 
facilities, and that the church would multiply in both numbers and depth.</p>
<p>Today we remember Interserve, a network of 
Christians from all over the world who, using their professional skills, work 
together to serve people of Asia and the Arab world—getting alongside them in 
tackling material, physical, mental and spiritual needs. Bless them as they 
reach out with the love of God in sometimes difficult situations. Give them 
tact, wisdom, safety, and fruit for their labors.</p>
<p>Today we remember all those who manage our personnel 
departments. We know it takes compassion and professionalism to do this job 
well, and we pray that you would bless these people-helpers with discernment and 
wisdom. May many come to know You.</p>
<p>Again, Lord, I ask 
You to prosper my ways today, for I pray in Jesus' Name, Amen.</p>

