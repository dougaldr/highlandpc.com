<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 14
<br><span class=byln>by Ed Vasicek</p>

<p>Great God and Savior,</p>
<p>Your Word refers to our Lord Jesus Christ as the Chief 
Cornerstone 
(<a href="javascript:scripture('../../scripture/mark1210.htm')" target=_self class=pop>Mark 12:10</a>). 
He is the foundation upon which Your Kingdom rests. 
Everything that is good and true is anchored to Him. By walking with Him, we 
are not moved or led astray. We can lean upon Him, rest upon Him and cast all 
our cares upon Him, for He is strong enough to bear their weight. He is without 
fault or flaw, but a dependable foundation upon which to build our lives. As 
the hymnist wrote, “all other ground is sinking sand.”</p>
<p>Father, in a day when many challenge the idea of absolute 
truth, I remember my Rock, my Cornerstone. When doubts arise, when I am in a 
panic, I remember Jesus Christ. When false teachers show up at my door, when I 
read of those who claim to know You fall away or deny the truth of Your Word, I 
remember that I am built upon the Cornerstone. I cannot determine for others to 
be so built, but I choose to build my life on You.</p>
<p>I pray for my pastor, staff, church board leaders, 
officers and teachers. If any do not know You, may they become born-again. 
Help them to be true to the Bible and to put Your Word over their own personal 
agendas. Help them to have genuine integrity, Biblical convictions, love, 
wisdom and courage. Help them put their walk with You first, their families 
second and their ministries third. Do not allow others to intimidate them into 
neglecting their families and remind them: &quot;the fear of man works a snare.&quot; 
Help them be most concerned about pleasing You while also seeking peace and 
harmony within the congregation.</p>
<p>Father, I have some personal health concerns including: __________. I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>Today I remember the nation of Mongolia. Though the 
country is massive in size, its population is under 3 million. Yet only ½ 
of one percent claim to be born-again evangelicals. Yet in 1989, there were 
only 4 Christians in the land, while the number now might be approaching 10,000, 
and I praise You for that great growth! Unfortunately, cults are also growing. 
The nation is exceedingly poor, with only a $390 annual income per person. Bless 
the various outreach attempts: literature, broadcasting, the Jesus film, etc. 
May Your Spirit hover over this land and may the church double over the next 
five years, I ask.</p>
<p>Lord, bless Ambassadors Fellowship, “a growing agency with 
short-term and long-term ministry opportunities in Latin America, Europe, East 
and West Africa, West and South Asia. Ambassadors Fellowship is involved in 
church planting, church assistance, disciple reproduction, leadership 
development, youth ministry...AIDS prevention and Micro Business projects.” 
Bless the 14 missionaries on the field and may they and the home staff work 
together in harmony and agreement. Empower them by Your Spirit, provide for 
them, protect them and may they glorify You, O Lord, and make Your Name famous.</p>
<p>Now, my God, help me to reflect Your grace today, I ask in Jesus’ Name, Amen.</p>

