<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 13
<br><span class=byln>by Ed Vasicek</p>

<p>My Father,</p>
<p>Thank You that You are an understanding God. You understand in that You are 
supportive and appreciate our situations. You understand in that You are 
perceptive. You are keenly aware of all things, events, results and feelings. 
You grasp every situation and You discern even the thoughts, motives and 
intentions of our hearts completely. Indeed, all things are laid bare before 
You. There is no reality You do not understand, no problem that perplexes You 
and no situation that confuses You. You are God, the One who completely 
understands all that was, is, or shall be.</p>
<p>Lord, help me to be an encourager. So many of my brothers and sisters in 
Christ need an uplifting word, a thank you, a &quot;well done,&quot; an &quot;I appreciate 
you.&quot; But do not limit my encouragement to my brothers and sisters in Christ. 
Help me never view the lost as unimportant or only mattering as far as the 
Gospel goes. May I be an encouragement and source of blessing even to lost 
family and friends whether they come to Christ or not. Help me never use 
people, or love people only because I have an agenda for them, as well 
intentioned as I might be. Help me to love them because they are people created 
in Your image, be they lost or saved.</p>
<p>Help me to resist temptation and the lures of the Satan today as I choose to 
draw near to God. Bind the evil one and control me by the Holy Spirit. Help me 
to be alert and of sound mind so that I might detect his schemes. Help me remember 
that if I resist him, he will flee from me. I take up Your armor, O Lord, 
including the shield of faith and the sword of the Spirit, which is the Word of 
God.</p>
<p>Lord, help parents everywhere. May Christian parents in particular invest a 
quantity of time with their children. Help parents to be kind but firm, to be 
able to say no when it is in the best interest of their children and to adjust 
to differences in children based upon personality, gender and stage of life. May 
parents exemplify a Christianity that works and may they lead their families in 
prayer on a daily or near daily basis.</p>
<p>Lord, I pray for the important Shanghai Municipality of China with its 15 
million people. Thank You that perhaps 7% of the population is made up of 
born-again evangelicals and thank You for the positive impact of the churches 
there despite sometimes intense persecution. O Lord, continue to use the 
Christians in this region. In Your good grace, double the percentage of people 
who know Jesus Christ as personal Savior.</p>
<p>Lord, thank You for the ministry of Open Doors with Brother Andrew as they 
engage in: Bible delivery in closed nations, helping persecuted believers, 
publicizing the plight of those imprisoned for their faith and training 
believers in Africa, Asia, Eastern Europe and Latin America. O Father, please 
bless this important work. Empower, protect, provide and grant harmony to 
those who minister, I ask.</p>
<p>Send angels to 
protect my loved ones and me, I pray in Yeshua's Name, Amen</p>

