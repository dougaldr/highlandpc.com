<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Prayer for the Families of Victims of the Connecticut Shootings
<br><span class=byln>by Ed Vasicek</span></p>

<p>Our Father, </p>
<p>Our hearts were traumatized and shocked when we heard of 
the disastrous and senseless mass shooting in Connecticut. As bystanders, we 
marveled that anyone would kill so many innocent people, many of them children. 
This killing was for no objective reason other than an unbelievably cold 
heart, a heart allowing wickedness and evil to go unchecked.</p>
<p>Lord, sometimes we are ashamed that we belong to the human 
race. Why would anyone rob a parent of their child or punish the innocent as 
though they were guilty?</p>
<p>Lord, our shock is miniscule compared to the plight of 
family and friends who are grieving this senseless killing; killing for the sake 
of killing. We know, Lord, that so many will now experience a lifetime of 
repeated pain as they remember their tragic loss. Oh God, only you can help in 
such a situation.</p>
<p>Surviving children have been traumatized. Clergy, 
counselors, friends, and school officials are overwhelmed as they seek to help 
the grieving. Help those who are trying to listen to and pray with heartbroken 
parents, relatives, and friends. Help those injured to recover speedily.</p>
<p>Your Word tells us that all are sinners, but your image in 
us—marred as it is by sin—holds most of us back from such cruelty. We know, 
Lord, were it not for your grace, such behavior would be commonplace. </p>
<p>We look forward to the time when your kingdom comes to this earth, when the lion and 
lamb will lie down together. We also look forward to the time when your children 
will enjoy the joy of heaven forever. Help us, and especially those grieving, to borrow strength for 
today because we rest upon your promises for tomorrow. We pray in Jesus’ name, Amen.</p>

