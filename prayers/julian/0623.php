<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 23
<br><span class=byln>by Ed Vasicek</p>

<p>My Father,</p>
<p>You are a generous God. According to <b>Matthew 5:45</b>, Your 
generosity extends to both those who love You and those who do not: &quot;He causes 
his sun to rise on the evil and the good, and sends rain on the righteous and 
the unrighteous.&quot; Your generosity is connected to Your grace, for grace means 
that You do favors for us we do not earn or deserve—nor can we repay. You 
often do what You do with extravagance; You are the God who lavishes us with 
grace.</p>
<p>Lord, help me to handle anger as a gift from You. Like 
atomic power, anger can be used to help others and provide a needed energy for 
me to address problems. Or it can be used to destroy and contaminate. If I 
stuff it, I become depressed or eventually explode. Instead, help me use anger 
to energize me to tackle problems in a reasonable and effective way. Help me 
address offenses as they occur so that I do not accumulate &quot;straws,&quot; with one 
eventually breaking the back of the proverbial camel. Help me distinguish 
between sinful anger, legitimate anger and the type of anger known as 
righteous indignation.</p>
<p>Lord, sometimes the devil tempts me to misdirect anger. 
Help me to resist the fiery darts of the devil by taking up the shield of 
faith. Help me to go on the offense with the sword of the Spirit. May I stand 
firm, O Lord. Remind me that the Christian life is often a battleground, not 
always a playground.</p>
<p>Today I remember the Province of India known as Bihar and 
Jharkhand. Over 75 million people reside in this province and less than 2% 
of them profess any sort of Christianity. Many Christians are lackluster about 
their faith and the cultural and social obstacles are many. Of the 200 people 
groups in the region, 20 are blessed by a significant outreach, 100 groups have 
new Christian outreaches while 80 have no work at all. Thank You for the 500 
new churches in the region; O God, bless them, prosper them and may Your Spirit 
draw many, many to Yourself! Guide the missionaries and strategists and help 
the believers to mature and be zealous for You. May the percentage of born-again evangelicals double over 
the next two years!</p>
<p>I also pray for the Province of Delhi, with its 11 million 
residents. Less than 1% of the population claim any form of Christianity. 
Millions dwell in slums. O Lord, work in this poor region; may the number of 
believers triple over the next five years, I ask.</p>
<p>Today I pray for my church. Help us to reach out to our 
community, to disciple those who come to faith, and to stay true to You in every 
way. Bring folks into our church who would benefit from the ministry here and 
who would have a good effect on the body; lead those who would not benefit here 
to congregations that will develop them. Keep troublemakers and wolves in sheep 
clothing far from us, I ask.</p>
<p>Lord, I ask You to send angels to watch over my loved ones 
and I, in Jesus' Name, Amen.</p>

