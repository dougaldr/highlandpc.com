<p>Nicodemus was not just a devout Jew, he was a Rabbi who was 
also part of the “ruling council” known as the Sanhedrin. It is worth noting 
that Jesus expected Nicodemus to be familiar with His teaching that a man must 
be born of water and the spirit. This implies that <i>whatever Jesus taught was 
also seen in the Old Testament</i>. Hence we know that water here does NOT 
refer to baptism, for believers were not baptized in Old Testament times.</p>
<p>The key to unlocking this mystery is the fact that both the 
Old Testament Hebrew word (“ruach”) and the equivalent New Testament word (pneuma, 
or “&#960;&#957;&#949;&#952;&#956;&#945;”) can mean “spirit” or “wind” or “breath” 
(you might recognize the term 
“pneumonia”). The context determines whether the term is translated “wind” or 
“spirit” or “breath.” Jesus uses this play on words by saying that whom the 
Spirit regenerates is unpredictable because the “wind blows where it will.” He 
adds, “so is everyone who is born of the wind” (or spirit).</p>
<p>Here are the passages I believe Jesus expected Nicodemus to 
recall, passages that deal with being born of water and/or wind:</p>
<p><b>Ezekiel 36:25-27</b> “I 
will sprinkle clean water on you, and you will be clean; I will cleanse you from 
all your impurities and from all your idols. I will give you a new heart and 
put a new spirit in you; I will remove from you your heart of stone and give you 
a heart of flesh. And I will put my Spirit in you and move you to follow my 
decrees and be careful to keep my laws.”</p>
<p><b>Ezekiel 37:9-10</b> “Then 
he said to me, ‘Prophesy to the breath; prophesy, son of man, and say to it, 
`This is what the Sovereign L<span class=sm>ORD</span> says: Come from the four winds, O breath, and 
breathe into these slain, that they may live.'&quot; So I prophesied as he commanded 
me, and breath entered them; they came to life and stood up on their feet—a 
vast army.”</p>
<p>Also in <b>Ezekiel 18:31</b>, 
we read, “Rid yourselves of all the offenses you have committed, and get a new 
heart and a new spirit. Why will you die, O house of Israel?”</p>
<p>Of King Saul, we read 
in <b>1 Samuel 10:6</b>, “The Spirit of the L<span class=sm>ORD</span> will come upon you in power, and you 
will prophesy with them; and you will be changed into a different person....”
</p>
