<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Origin of the Bible, Part IV: The New Testament Canon
<br><span class=byln>by Ed Vasicek</span></p>

<p>In previous articles, I wrote that the Bible is an 
interlocking book. If you believe in the New Testament, you logically must 
accept the Old Testament because Jesus and the Apostles did so. On the other 
hand, if you embrace the Old Testament, the OT prophecies lead you to Jesus 
Christ. According to 
<a href="javascript:scripture('../scripture/dan926.htm')" target=_self class=pop>Daniel 9:26</a>, 
for example, the Messiah would be &quot;cut off,&quot; 
and then later Jerusalem would be destroyed. The destruction of Jerusalem took 
place in 70 A.D., so the Messiah had to be cut off before then. The Old 
Testament describes where the Messiah would be born, His genealogy, deity, 
atoning death on the cross, burial, and resurrection. If you believe in Jesus, 
you must therefore trust the writings of those He personally trained and 
authorized to initiate the church.</p>
<p>It is generally accepted that the four Gospels, Paul's 
epistles, Hebrews, 1 Peter, 1 John, and Revelation were completed by 95 A.D. 
They were written in the common (<i>koine</i>) Greek, the common language of the 
Roman Empire at that time. (Remember, the Romans had conquered the Greek 
Empire, and Greek remained the trade language for at least two centuries before 
Latin prevailed). We have all sorts of evidence, including &quot;hard copy&quot; 
manuscripts, although no originals have survived. We have fragments from a copy 
of John's Gospel (the John Rylands papyrus) that dates to somewhere between 100 
and 125 A.D., just a few years after the original was written (about 90 A.D.). 
The Apostle John was only a teen when Jesus appointed him as one of the twelve 
Apostles, and he wrote his Gospel, epistles, and Revelation as an elderly man, 
so they represent the final books of the New Testament. We may have papyrus 
fragments of Matthew from perhaps 70 A.D. (the Magdalen Papyrus), although most 
scholars date this later. We have complete copies of the entire New Testament 
from the fourth century.</p>
<p>Despite the attempts by the Roman government to burn copies 
of New Testament Scriptures, and despite persecution, the New Testament was 
copied and translated so aggressively that we have literally thousands of 
ancient manuscripts from which to evaluate the original text of the New 
Testament. There is no book in ancient times more documented than the New 
Testament, nor are there any ancient books with surviving copies so near to the 
time of writing.</p>
<p>We have so many quotations from the New Testament in the 
writings of the &quot;church fathers&quot; that we could reconstruct the entire New 
Testament (except for 11 verses) from their writings alone! In the latter 
second century, Christians began translating the New Testament into the Latin 
(&quot;old Italian&quot;) and Syrian languages. We have ancient copies in those languages 
as well. All in all, we have 10,000 ancient copies of parts or all of the New 
Testament. The New Testament is the best-documented book of antiquity.</p>
<p>The preservation of the New Testament books is one thing, 
but we need to ask several questions: 1) How did church leaders recognize which 
books were inspired Scripture? 2) Were there any books that were questioned or 
rejected in this process? and 3) How serious are the differences between the 
ancient manuscripts?</p>
<p>How did church leaders recognize which books were inspired 
Scripture? For most of the New Testament, this was a no-brainer. Unlike most 
ancient literature, which first circulated as oral tradition (sometimes for 
centuries) and then was finally written down, the New Testament was written by 
believers who were contemporaries with Jesus Christ. So by the middle of the 
second century, most of the New Testament Scriptures were firmly established 
because Christians were convinced that those who founded the church authored 
them in the first century. This was the key criterion: were the books authored 
while at least some apostles were still alive, and were the authors themselves 
either apostles or close associates of the apostles? If so, then their 
teachings and writings were authoritative. And that &quot;authoritative&quot; issue is 
the heart of canonicity, the measuring stick of truth.</p>
<p>Matthew, Mark, Luke, John, Acts, and all of Paul's epistles 
were immediately received. Their authorship, integrity, and &quot;apostolic era&quot; 
antiquity were well established. So were 1 Peter, 1 John, and Revelation. 
Later on some church leaders doubted Revelation because it is so unusual, but 
the fact it was received early implies no original question of its authorship.</p>

