<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 25
<br><span class=byln>by Ed Vasicek</span></p>

<p>Great, Almighty Father,</p>
<p>I praise You that You are a god who feels sorrow. Your 
Spirit can be grieved. He actually becomes sad when we resist Him and stifle 
His work in our lives. We know that You take no pleasure in the death of the 
wicked. In addition, Your Son, when He was on earth, was prophesied to be, &quot;a 
man of sorrows and acquainted with grief,&quot; according to <b>Isaiah 53</b>. You 
understand my heartbreaks, and You feel my sorrows. When I multiply my sorrows 
by the number of humans on earth at any time, I marvel that You feel each sorrow.</p>
<p>Today I celebrate the fact that I have been baptized by Your Spirit into the 
body of Christ, the blessing of all believers 
(<a href="javascript:scripture('../../scripture/1cor1213.htm')" target=_self class=pop>1 Corinthians 12:13</a>). 
I thank You that Your Spirit indwells me—and every genuine believer 
(<a href="javascript:scripture('../../scripture/rom89.htm')" target=_self class=pop>Romans 8:9</a>), 
and I pray that He would fill me completely. I would love to see 
His fruit in my life, day by day.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). I do not excuse my transgression and omissions, 
but I freely admit them and ask for Your forgiveness. So I bring my sins 
before You now: __________.</p>
<p>Lord, help me to be a good manager of my time. I do not 
want to live the hurried lifestyle, but I need to be efficient in some areas 
that I might have more relaxed times later. Enlighten me as to how I might do 
this.</p>
<p>Today I remember the Bahamas. Although there are many 
professed evangelical Christians there, the quality is weak. Drugs, 
materialism, and low commitment really make the church ineffective and often 
without strong leadership. We pray that Your Spirit would stir up the believers 
there, that they might truly be in the world but not of the world.</p>
<p>Today we remember the outreach of Avant Ministries. We 
thank you for the many countries in which they minister, and we pray that you 
would bless them with much fruit for their labor as men, women, and children 
accept Christ and grow in Him. Help the missionaries to work well together, and 
reduce conflicts and other relational problems. Provide for them financially, 
and bind Satan as he seeks to undermine their work. Keep them true to You.</p>
<p>Today we pray that you would help those who work in the tow 
truck business. We know they are often unappreciated—except when we need 
them. We also sympathize with their sometimes gruesome work. We pray that we 
would remember to seek to witness to them when we need them.</p>
<p>Lord, please help 
me honor You, I pray, in Jesus' Name, Amen.</p>

