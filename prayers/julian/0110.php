<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 10
<br><span class=byln>by Ed Vasicek</span></p>

<p>Heavenly Father,</p>
<p>We call You Father because You are our Father, first by 
virtue of creation, second by virtue of regeneration, and third by virtue of 
Your Person. You are the Father in heaven. Thank You that as a Father has pity 
on His children, so You have pity upon us. Thank You that, as a good father 
loves, trains, and cares for the wellbeing of His children, You care for us. 
We glory in that we have been adopted into Your family by faith in Jesus Christ.</p>
<p>Lord, we thank You that You have called us to be a kingdom 
of priests. We have direct access to heaven through Jesus Christ our Lord, we 
can intercede for those Who are lost or Who need special spiritual attention, 
and we can offer You the sacrifice of praise, namely, the fruit of our lips. We 
are a royal priesthood as children of the King.</p>
<p>So Lord, help me to pray without ceasing, praying off and on throughout the day.
May I remember to send up casual and quick prayers and praise all day long.</p>
<p>Lord, help me to manage my finances in a way that pleases 
You. Prosper me to the degree You see fit. Help me trust You during good times 
and bad, and may I be a good steward of what You have given me.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Today I remember the tiny European nation of Andorra. 
Since most of its citizens work in the hospitality business, they come in 
contact with many others. Father, we pray that Your Spirit might reach these 
Andorrans, and that these folks would then share the Gospel with Europeans and 
others who frequent that place.</p>
<p>I want to remember the Bible-based Rescue Missions 
throughout our lands. So many people go through their doors, eat their meals, 
and are clothed with clothing they provide. Many come to know Christ, and some 
testify of lives that have been totally transformed. May we see more genuinely 
transformed lives. We pray that the work of these missions would be appreciated 
not only by believers, but we pray that decent lost people would also value 
their contribution and consider the claims of Christ because of this labor of 
love.</p>
<p>Today we remember our city/county building inspectors. 
Help them to be honest, fair, and out for the best interests and safety of their 
communities.</p>
<p>Minister grace to 
me throughout this day, I pray, in Jesus’ Name, Amen.</p>

