<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 11
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are a God Who does not want to be forgotten. Sometimes we fail to realize 
how much You enjoy our thanksgiving. Your Word warns us, &quot;But remember the Lord 
your God, for it is he who gives you the ability to produce wealth&quot; (<b>Deuteronomy 
8:18a</b>). So, Lord, remind me that though You 
do not need my thanksgiving, You delight in it. And You take this very 
seriously. It is more than a manner of courtesy: You want us to remember that 
we are completely dependent upon You. This truth is very humbling, but we need 
to be humbled, for our hearts are prone to arrogance.</p>
<p>I pray for my lost friends and family members, including: __________. 
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Father, I thank You that one day Jesus Christ shall return to the clouds, and we 
will meet Him in the air. The dead will be raised and those of Your children 
alive on earth will be changed into immortality as they ascend upward to meet 
You, all in the twinkling of an eye. We eagerly await this &quot;blessed hope.&quot;</p>
<p>So, Lord, help me to have endurance as I wait for Christ's return. Sometimes 
life becomes overwhelming and I become disappointed in circumstances, 
disappointed with people and, frankly disappointed with myself. 
Sometimes, I confess, I even feel disappointed with You. So help me to endure, 
to persevere in faithfully serving You because I know my labor in You is not in 
vain.</p>
<p>Lord, help me to develop humility. You command me to humble 
myself before You, but You also want me to be humble in my dealing with others. 
Help me not to look out for only my own interests but the interest of others.</p>
<p>Father in heaven, thank You for Your work within the Asian 
Republic of the Philippines. Father, a whopping 16.7% of the population claims 
to be born-again evangelicals. The church has grown rapidly. Yet there are 
many unreached people including Muslims, the ethnic Chinese and a number of 
island people groups. O Father, continue to work mightily by Your Spirit in 
this nation. May the evangelical church double over the next decade and help 
the Gospel to penetrate all people groups and areas, I request.</p>
<p>Lord, I pray for International Messengers, &quot;an 
interdenominational missions sending agency committed to evangelism and 
discipleship in all nations through partnering with local churches. [They are] 
involved in church renewal, Bible teaching, training and sending. [They] seek to 
bring about renewal in the local churches of eastern and central Europe by drawing 
their adult membership into ministry alongside our staff and short-term teams.... 
[They] seek after national pastors with a heart for evangelism and 
discipleship to come alongside them for involvement in the above-mentioned 
process.&quot;</p>
<p>I pray for those who work in automotive and vehicle 
body repairs. Help them do their work with skill, integrity, fairness and 
interest. May those who know You shine before their fellow workers and their 
clients, I ask.</p>
<p>Now help me to 
reflect the light of Christ today, I ask in His Name, Amen.</p>

