<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 27
<br><span class=byln>by Ed Vasicek</p>

<p>Father in Heaven,</p>
<p>You are the Primary Cause of all things. You set time into 
motion. You created dimension. You wound the clock we call the physical 
universe. You created the laws of physics and the rules of life. The more we 
discover, the more we see how incredibly complex life is and how simple the 
laws of physics are. They all point to a beginning and to You as the One Who 
started it all. We know that the evidence of Your handiwork is clear for all to 
see—to those who are open to You.</p>
<p>Thank You for keeping us by Your power. When we stray from 
You, You discipline us and give us a spanking of sorts. Thank You that You are 
looking out for our best interests, working in ways that may seem mysterious or 
illogical to us. You are not an enemy to keep at bay but a true Friend, truer 
than any other.</p>
<p>So, Lord, help me to understand what I can of You and Your 
ways. Do not allow me to be ignorant of Your Word, but help me to truly read 
and study it. Help me to learn the Word even if parts of it 
may not seem relevant at the time. Help me to believe that all Scripture is 
both inspired and profitable.</p>
<p>Help me to guard my tongue, to choose my words carefully 
and to use my mouth to heal not to hurt. Help me learn to listen, to remember 
that You created us with two ears but only one mouth.</p>
<p>Please address these following personal needs for others or 
myself: __________.</p>
<p>O, Lord, please be a help to the hurting, a balm in Gilead 
for the emotionally wounded and devastated. Strengthen the disappointed; 
encourage the demoralized, bind up the wounded, particularly: __________.</p>
<p>Today I remember the millions of ethnic Chinese scattered 
throughout the continents. Help them break completely free from the tyranny of 
communism and totalitarianism. May the Gospel penetrate these communities and 
help those who know You to eagerly share the good news of Jesus Christ with 
their friends and families.</p>
<p>Thank You for the ministry of Christ for the City 
International. Bless their 104 missionaries and staff as they work in Canada, 
Colombia, Costa Rica, Haiti, Mali, Mexico, Nicaragua, Panama, Peru,&nbsp;Spain, El 
Salvador, Bolivia and the USA, mostly with Spanish-speaking individuals. Use 
them to plant churches, make disciples and further Your kingdom. Empower them 
by Your Spirit, provide for and protect them, and help them to work in harmony 
and peace with one another.</p>
<p>Lord, especially protect and bless our firefighters. Give 
them safety and a good <i>espirit de corps</i>. May those who know Christ freely 
share Jesus with others. Help the rest of us to encourage them and help us let 
them know that we view them as heroes. Especially help widows and children of 
firefighters who have given their lives to protect others.</p>
<p>I pray Your blessing upon me, in Jesus’ Name, Amen.</p>

