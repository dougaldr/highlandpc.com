<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 16
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>You are a jealous God. As Your Word says in <b>Exodus 
34:14</b>, &quot;the L<span class="sm">ORD</span>, whose name is Jealous, is a jealous God.&quot; When Joshua urged 
the children of Israel to count the cost and determine if they were going to 
serve the L<span class="sm">ORD</span> or not, Joshua did not hold anything back. He told them that 
You, the Lord, are &quot;a holy God...a jealous God.&quot;</p>
<p>Like a husband is jealous for His wife, so You are jealous 
for Your people Israel 
(<a href="javascript:scripture('../../scripture/zech11416.htm')" target=_self class=pop>Zechariah 1:14-16</a>). 
You are also eager for Your bride, 
the church, to be presented to You without spot or blemish. You resent it when 
Your children share what is rightfully Your worship with other gods. You remind 
us that even covetousness or greed removes You from first priority in our lives 
and is, in some sense, like idolatry 
(<a href="javascript:scripture('../../scripture/col35.htm')" target=_self class=pop>Colossians 3:5</a>).</p>
<p>Help me not to provoke You to jealousy. May I give You the 
first place in my heart and may there be no competition for first place. You, 
my Creator and Redeemer, are certainly in a league by Yourself; help me remember 
that You are Lord above all.</p>
<p>Lord, help me to value my family relationships above 
material goods. Remind me that, &quot;Better a little with the fear of the Lord than 
great wealth with turmoil.&quot; Instead of focusing upon succeeding in business or 
areas of my interest, help me be most concerned with succeeding in life as You 
define success. Help me categorize the things of the Spirit and the needs of my 
family and others as top priorities. Do not let me get caught up in the prestige 
or achievement rat race.</p>
<p>O Father, help me to crave Your Word. Let me fall in love 
with it. Your Word, the Bible, is Your communication to me. If I love You, I 
will want to know what You have to say. </p>
<p>Lord, today I pray for the continent of Africa. Over 800 
million people live there and 14.8% of the population claim to be born-again 
evangelicals. Praise Your Name. But, Lord, the challenges are many: war between 
tribes, the oppressiveness of Islam in the north, AIDS and governmental tyranny 
or instability. This continent is so poor and sometimes the believers are not 
maturing as they should. The evangelical churches are often very competitive 
and sometimes the pastors, being educated, can form personality cults. O Lord, 
help the church in Africa to grow both in number and depth. May Biblical 
Christianity so permeate the continent that honest and effective governments 
will become the norm. Bring harmony to Africa's various people and a spirit of 
religious freedom. Guide western missionaries as to how they can best develop 
indigenous churches.</p>
<p>Lord, thank You for Palm Missionary Ministries which 
&quot;exists primarily to enable and facilitate the development of national 
missionaries and workers to go into all nations, make disciples and establish 
them either into existing local evangelical congregations or newly organized 
churches.&quot; Thank You for their work (10 missionary families in Ecuador, 1 in 
Colombia and 1 in Spain) in helping nurture doctrinally solid indigenous 
churches. Prosper, protect, provide, empower and grant harmony to them, I ask.</p>
<p>Provide for my 
needs today, I request in Jesus' Name, Amen</p>

