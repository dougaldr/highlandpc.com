<p>Wonderful Lord,</p>

<p>Your Holy Spirit is the Great Purifier. He purifies us as 
a fire disinfects and cleanses 
(<a href="javascript:scripture('../../scripture/matt311.htm')" target="_self" class="pop">Matthew 3:11</a>). 
To be baptized by the Spirit is 
to be purified by divine fire. This happens when I accept Christ as my Savior 
(<a href="javascript:scripture('../../scripture/1cor1231.htm')" target="_self" class="pop">1 Corinthians 12:31</a>, 
<a href="javascript:scripture('../../scripture/rom89.htm')" target="_self" class="pop">Romans 8:9</a>). 
Your Spirit searches my heart and prays for me 
(<a href="javascript:scripture('../../scripture/rom826.htm')" target="_self" class="pop">Romans 8:26</a>); 
He convicts the world of sin, righteousness and judgment. O 
Father, unleash His power in me! Although I cannot get more of Him because He 
is a Person, He can get more of me!</p>

<p>Lord, we all have burdens to bear. Some of us have a small 
number of them and the burdens are not that bad. Others have many heavy 
burdens constantly and relentlessly. Most of us are somewhere in between. O 
God, Your Word tells us to cast all our cares upon You, for You care for us. I 
give You my burdens, my heartaches, my fears. Your Word also encourages us to 
bear one another&rsquo;s burdens. This means I have to be on the lookout to care. It 
is rather counterintuitive, but when I help bear another&rsquo;s burdens, mine are 
lessened. And when others help me, theirs are lessened. </p>

<p>Help me to resist temptation and the lures of Satan today.</p>

<p>Father, I have some personal health concerns including: __________. I also pray 
for the physical, mental, or spiritual health of others including: __________.</p>

<p>I pray for the nation of Nicaragua. Thank You for what 
You are doing there: over 16% of the population claim to be born-again 
evangelicals&mdash;praise Your Name. Many others claim to be born-again but attend 
non-evangelical churches. Thank You that almost the entire nation has seen the 
Jesus film. Lord, this is a poor country that had been ravaged by the horrors 
of a long war. Help the nation to rebuild and may the Christians mature, 
unite and affect their country for the good of all. Help the percentage of 
evangelical Christians grow by 50% over the next decade, I ask.</p>

<p>Lord, praise Your Name for Globalserve Alliance, &ldquo;a 
virtual airline serving the Christian charitable markets. The underlying 
motivation of the organization is to move Missionaries around the world in as 
efficient and cost effective way as possible. Not a Travel agency, but a virtual 
airline sold worldwide through various mission&rsquo;s travel offices, the Internet 
and associates. Backing up the virtual airline are travel professionals placed 
worldwide monitoring and following through all activities. All involved are 
committed Christians with a heart for missions. Globalserve and its associates 
are currently directly involved in and facilitate workers with: &middot; an orphanage 
in Indonesia &middot; mercy ministries in China &middot; tentmaking missionary employment 
opportunities in creative access nations like Romania, Dubai, India and China. &middot; 
Mountain Biking! An exciting Short Term Mission Adventure reaching rural Asia 
with the Gospel.&rdquo; Lord, use this ministry to help transport missionaries in a 
cost-effective way, give them safety, wisdom, provision and guidance, I ask.</p>

<p>I pray for those working as mining machine operators. Keep 
them safe, help them work efficiently, help them get along with one another and 
management and may those who know You shine!</p>

<p>Help me shine today, I ask in Yeshua&rsquo;s Name, Amen.</p>


