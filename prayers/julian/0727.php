<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 27
<br><span class=byln>by Ed Vasicek</p>

<p>Great One seated on the Throne above,</p>
<p>You reign in regal majesty. You never become weary or 
tired. You have no creaturely limitations, for You are the uncreated Creator. 
Yet, You enjoy giving strength to the weary and sustaining those who trust in 
You. You have taught us that when we are weak, then we are strong. For when we 
are weak, we depend upon Your strength.</p>
<p>Father, thank You that the Lord Jesus after dying once for all mankind and 
at one point in time ascended back into heaven never to die again. As the 
risen victor, He sits at Your right hand and prays for us and defends us against 
the devil's accusations.</p>
<p>Father, help my health to fare well. Health is such a 
precious gift, a gift we do not always appreciate unless it is taken away. So 
I thank You for the level of health that I now have and look forward to the 
time when my body is redeemed 
(<a href="javascript:scripture('../../scripture/rom82324.htm')" target=_self class=pop>Romans 8:23-24</a>). 
Then, there will be no more 
sickness, no more glasses or dental work or vaccinations or ingrown toenails or 
colds or flus. No more arthritis, cancer, heart problems, diabetes, muscular 
diseases, dementia, or pneumonia or other human ailments. Thank You for the 
times You have healed in answer to prayer and thank You for teaching me, using 
me and leading me when You have chosen not to heal 
(<a href="javascript:scripture('../../scripture/gal413.htm')" target=_self class=pop>Galatians 4:13</a>, 
<a href="javascript:scripture('../../scripture/2cor1210.htm')" target=_self class=pop>2 Corinthians 12:10</a>).</p>
<p>O, Lord, please help to the hurting, the emotionally 
wounded and the devastated. Strengthen those stung by disappointment, 
encourage the demoralized and bind up the emotionally wounded. Especially help: __________. 
</p>
<p>Father, I pray for the 20 million people in the Indian 
province of Haryana, one of India’s least-evangelized states. According to 
<a href="http://www.gmi.org/ow/" target="offline" class="reg">Operation World</a>, 
&quot;only 15 of the 92 people groups are known to have any 
congregations of believers.... There were a total of 265 Christian workers in Haryana 
in 1998. There are now 15 Christian training institutions in the 
state.&quot; Only .06% are Protestant, mostly evangelical. O God, the Gospel has 
barely touched this province. May Your Spirit raise up men, women and young 
people in increasing numbers. Lord, I pray that 1% of the population might come 
to know You within the next decade.</p>
<p>Lord, thank You for Servant Group International (SGI), “a 
Christian organization serving among Middle Eastern communities. SGI works to 
strengthen the development of all aspects of traditional Middle Eastern society, 
particularly the institutions of education and family. SGI works closely with 
local churches in the Middle East. Work has been difficult and dangerous at 
times, yet...SGI has been privileged to assist with the growth of fellowships in 
several cities and with the associated programs of those churches including 
radio stations, printing presses and bookstores. SGI field staff provide 
critical support, with activities ranging from discipleship training for church 
leaders to oversight of Christian Classical Schools enrolling hundreds of local 
students.” Father, use this ministry. Provide them with safety, provision, the 
power of Your Spirit and great fruit for their labor, I pray.</p>
<p>Help me to walk closely with You today, I ask in Jesus’ 
Name, Amen.</p>

