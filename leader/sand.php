	<p><b>Ring 
Ceremony</b></p>
	<p>The wedding ring 
is an outward and visible sign of an inward and invisible spiritual bond that 
unites two loyal hearts in endless love. As the ring is circular in shape and 
has no beginning or ending, so it is a symbol of love that know no bounds.
</p>
	<p>John, 
do you have a ring to give as a token of your love for Jane?</p>
	<p>(groom 
responds) I do.</p>
	<p>Repeat 
after me. &quot;With this ring, 
I thee wed and pledge my love to you alone.&quot;</p>
	<p>Jane, 
do you have a ring to give as a token of your love for John?</p>
	<p>(bride responds) I do.</p>
	<p>Then repeat 
after me. &quot;With this ring, 
I thee wed and pledge my love to you alone.&quot;</p>
	<p><b>Sand 
Ceremony</b></p>
	<p>Sand is a common 
substance on planet earth as bountiful as the stars in the universe. God 
promised Abraham that He would make his descendants as numerous as the stars of 
heaven and the sands of the sea.</p>
	<p>A container of 
sand holds many grains. So a human being is a multifaceted and complex 
individual with many talents, abilities, strengths, and weaknesses. When we 
marry, we join all that we are to another. We combine our lives into one greater 
vessel. </p>
	<p>But God is 
Sovereign and the foundation for a Christian marriage. The white sand in the 
larger flask represents the presence and blessing of God, our sure foundation.</p>
	<p>As the sand in 
these two containers is poured into the third container, the original, 
individual samples of sand no longer exist but are joined together as one. Just 
as these grains of sand can never be separated and poured again into the 
individual containers, so will your lives together as a married couple be 
intertwined as you become one flesh with God as the basis of the relationship.</p>
