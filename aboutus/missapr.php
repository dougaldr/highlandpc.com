<p id="pgt">Missions Approach</p>

  <p>Jesus told us to share the Gospel throughout the world
  (<a href="javascript:scripture('../scripture/matt281920.htm')" target=_self class=pop>Matthew 28:19-20</a>).
  Just as Jerusalem wasn't fully evangelized before the early believers spread abroad
  (<a href="javascript:scripture('../scripture/acts18.htm')" target=_self class=pop>Acts 1:8</a>),
   neither should we expect America to be completely
  evangelized before we send missionaries to other countries. As Americans, we
  are tempted to be ethnocentric and reason with the natural, rather than the
  spiritual, mind. The natural mind says, &quot;Why worry about others out
  there?&quot; The spiritual mind adjusts to Scripture and says, &quot;The field
  is the world. We cannot separate 'out there' from 'over here.'&quot;
  While at Antioch, Paul and Barnabas were called to evangelize in distant
  lands, even though Antioch had plenty of spiritual needs. We must lose the
  &quot;God is a white American&quot; mentality and renew our minds in
  Scripture, believing what we would sometimes rather not believe. Human logic
  must be exchanged for the teaching of the inspired Word.<br>
  <br>
  In North America, 27.9% of the population claim to be evangelical believers.
  In Asia, with over 10 times the population, the percentage of evangelicals is 3.1%.
  In the Middle East, it is less than one-half of a percent. People in other lands need the Lord.
  We need to remember this, yet continue to minister locally as well.<br>
  <br>
  In 2006, twenty-one percent of our church's tithes and offerings went toward 
  missions and evangelism. The chart below shows that we contribute toward 
  the support of the following missionaries or organizations on a monthly basis. 
  Many churches and organizations contribute to make up their total support.</p>
  <p><b>Ed's definition:</b>  A missionary crosses culters to share Christ or to
  translate the Word. An evangelist shares Christ within his own culture.
  Every Christian is called to be a witness and to share Christ, but the calling
  of missionary or evangelist is to be respected as a special calling.</p>
