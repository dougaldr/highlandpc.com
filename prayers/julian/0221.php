<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 21
<br><span class=byln>by Ed Vasicek</p>

<p>Heavenly King,</p>
<p>Thank You that You are a sensible God. You think 
reasonably. Your thinking is infallible and perfect. Although You are 
emotional, Your actions always reflect Your intellect, emotion and will. You 
are never carried away to irrationality, despite the intensity of Your feelings.</p>
<p>Dear Lord, please address the following needs in my life: __________.</p>
<p>I pray for friends and family, including: __________.</p>
<p>I also pray that You would help me to love my family 
members as I should. Help me put You first, my family second and then the rest 
of life third. Help me balance the rest of life between work, church, work 
around the house, extended family, social activities and community involvement.</p>
<p>I thank You, Lord, that You have taken away my heart of 
stone and given me a heart of flesh, a new nature, the Law of God written upon 
my heart. I am so privileged to be under the New Covenant, to know You and be 
indwelt by Your Spirit.</p>
<p>Help my heart to stay sensitive to Your leadings and will 
for my life. Help me to check in with headquarters often, not to make the big 
decisions on my own.</p>
<p>Today I pray for the Central African Republic. Thank You 
for the great growth of the evangelical church, nearly 35% profess to be 
born-again. Yet the level of depth, doctrinal purity, vision, and tribal 
divisiveness has kept the church from maturing as it should. We pray for the 
training organizations, and especially for discipleship ministries focusing upon 
children and young people. We pray that the evangelical church would work 
together, would grow in depth as well as breadth, and that Your Spirit would move 
mightily in this society.</p>
<p>I also remember the fine ministry of NTM, New Tribes 
Mission, as they, &quot;...are reaching people who've never had a single opportunity to 
hear the Good News. Mission trips carry Christ's Gospel to parts of the world 
where living and working conditions are often difficult.&quot; Thank You for the 
many missionaries who serve around the world and are involved in evangelism, 
discipleship, and church planting. Protect them, help them to have harmony with 
one another, and provide all they need and beyond. May Your Spirit prosper and 
bless their work.</p>
<p>Today I pray for computer technicians and others members of 
the communication/information profession. They are crucial to modern life, even 
to modern ministry. May many in these fields come to know You, and help the 
Christians to be good testimonies.</p>
<p>Now help me glorify You today, I pray, in Jesus’ Name, 
Amen.</p>

