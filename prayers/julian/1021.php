<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 21
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Achan sinned against You and the children of Israel 
by hiding some of the spoil for himself with his family's support. Father, 
through Achan, You warn us about the dangers of materialism and covetousness. 
Even though materialism or covetousness may seem harmless, it affects me and 
those close to me. Because of Achan’s sin, they were all put to death in the 
sad valley of Achor.</p>
<p>Sometimes, Lord, in my life, You take things away from me 
or bring me through rough times to discipline or develop me. I can feel pretty 
low. You want to shake me to wake me and it doesn’t always feel good. But 
Lord, thank You that You are out for my good. Thank You that You are the God of 
Ultimate Good 
(<a href="javascript:scripture('../../scripture/rom828.htm')" target=_self class=pop>Romans 8:28</a>).</p>
<p>Today I pray for missionaries that my church or I support 
including: __________.</p>
<p>Lord, help me to be a good steward of the funds with which 
You provide me. <b>Proverbs 17:18</b> reads, &quot;Of what use is money in the hand of a 
fool, since he has no desire to get wisdom.&quot; O, Lord, may I be a wise person. 
Help me be wise in all areas including money management.</p>
<p>I remember my marriage (if I am married) and the marriages 
of my extended families, friends and church families. Lord, hold them 
together. Help married couples to lean upon You and to work hard at 
addressing problems; help them enjoy harmony. I also pray for those who are 
divorced, that You would help them to go forward with Your hand in theirs. 
Guide them, provide for them and help them to build a strong life serving You.</p>
<p>Mighty One, Your Word says, &quot;A prudent man sees danger and 
takes refuge, but the simple keep going and suffer for it&quot; (<b>Proverbs 22:3</b>). 
Lord, life has many potential dangers ahead. Keep me alert and when danger seems 
likely keep me from living in denial like the simple. Help me prevent more 
problems by thinking ahead rather than wandering around in a daze or with my 
head in the sand.</p>
<p>Today I pray for five more nations in which Christians 
suffer persecution, numbers 46 to 50 on the persecution index. They are: 
Algeria, Syria, India, Indonesia and Peru. O Father, I pray that You would end 
both legal and informal persecution in these lands. Protect Your children; turn 
the hearts of the hostile toward tolerance. Give wisdom to the believers so 
that they might avert persecution and help them to be strong when they cannot, 
I ask.</p>
<p>Lord, thank You for World-wide Missions and their mission: &quot;To touch the 
suffering people of our world with the love of Jesus Christ, changing the lives 
of men, women and children forever.&quot; They accomplish this by supporting 
missionaries, national workers, churches, schools, orphanages and clinics 
through its affiliated organizations. Thank You that World-Wide Missions...ministers 
to needy people in 32 countries, including the USA and Canada.&quot; O 
Lord, provide, protect, grant harmony, safety and the power of Your Spirit to 
those involved with this ministry, I ask.</p>
<p>Help me live for You today, I ask in Jesus’ Name, Amen.</p>

