<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Prayer for Survivors of Hurricane Sandy
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>We cannot believe the reports we hear from our eastern 
states. Hurricane Sandy has struck, and scores of people have died. Entire 
communities are destroyed, people are stranded. So many homes lost, tunnels 
flooded, a congested New York City without power.</p>
<p>Father, we know this world is cursed, and that curse 
includes natural disasters. You sit as Sovereign Lord over the universe. Though 
these tragedies perplex us, and though we know Your wrath in general is revealed 
through natural disasters, we also know that the victims of this disaster were 
no worse than the rest of us. Things seem to happen so randomly from our 
perspective.</p>
<p>Lord, You have a master plan that makes sense of these 
things. We admit to being at a loss. Your ways are higher than our ways, and in 
the end, a greater good will come about. </p>
<p>Though perplexed and puzzled, we still trust You as the 
holy, good, and sovereign Lord that You are. We can choose to go through this 
with You or without You; those are our choices. We choose to experience these 
events with You at our side. We choose to take Your hand.</p>
<p>We pray for the victims of this natural catastrophe. We 
pray that You would keep them healthy and safe. We pray that You would help them 
cope with the horrendous losses, the trauma, the pain, the misery of it all. May 
many come to find You through this tragedy. </p>
<p>Please help the relief teams including the national guard. 
Give them health, wisdom, emotional and physical energy. May their efforts be 
multiplied and their efficiency increased. Guide the leaders and officials to do 
what is in the best interest of their people. Prevent looting, and may 
neighborly concern prevail among the masses. Provide the necessary provisions 
and the means to get the supplies to match the needs. Help restore this part of 
our country to a normal state.</p>
<p>We pray in Jesus’ Name, Amen.</p>

