<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 30
<br><span class=byln>by Ed Vasicek</p>

<p>Father of Lights,</p>
<p>I live in the shelter of Your wings. It’s not that you 
have literal wings, for You are Spirit 
(<a href="javascript:scripture('../../scripture/john424.htm')" target=_self class=pop>John 4:24</a>). 
But, nonetheless, You are 
the God Who shelters His children as a hen does her young. As Boaz told Ruth, 
&quot;May you be richly rewarded by the L<span class="sm">ORD</span>, the God of Israel, under whose wings 
you have come to take refuge&quot; (<b>Ruth 2:12</b>).</p>
<p>You are my shield, my home away from home, my constant 
protector and refuge. Life has its twists and turns; it meanders here and 
curves there and I never know what surprises await me. But You are there for 
me, You are ahead of the curve, You know the beginning from the end. You have 
always known everything that would ever happen.</p>
<p>I know there is a special blessing that comes from praying with others. This 
might include family members who know You, friends from church, or fellow 
Christians at work, school, or in the neighborhood. We know You are uniquely 
present where two or three gather in the Name of Jesus 
(<a href="javascript:scripture('../../scripture/matt181920.htm')" target=_self class=pop>Matthew 18:19-20</a>).</p>
<p>Father, help parents throughout our world. Although I pray especially for the 
parents who know You, I pray for all parents. Motivate them to do what is in the 
best long-term interest of their child or children. Keep them from abusing their 
children and from spoiling them. May parents take seriously the responsibility 
to be a good example, to keep their marriages together when possible and to 
nurture their child’s/children’s moral education. Give parents great wisdom to 
face the challenges of the modern world. We pray that more and more parents 
would surrender their lives to Jesus Christ.</p>
<p>Help me to do my part by volunteering to serve or continuing to serve in a 
Bible-believing, Bible-teaching church. May I set my roots down deeply and 
attend every week if I am able to do so.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me of them, and You will cleanse me from all&nbsp;unrighteousness (<b>1 John 
1:9</b>). So I bring my sins before You now. I do not excuse them, but I freely 
admit them and ask for Your forgiveness.</p>
<p>Today I pray for the Sikkim State of India with its 500,000 people. Thank 
You that 5% of the population claim to be evangelical Christians and thank You 
that the church is growing mightily. I also praise You that the Christians are 
freer from the fear of persecution than they were a few years ago. May the 
percentage of evangelical Christians double over this decade, I pray.</p>
<p>Thank You, my God, for the ministry of Love A Child, a ministry that 
operates in Haiti and is &quot;a Christian humanitarian organization sharing the 
message of God's love with children in extreme poverty.&quot; Bless those who 
sponsor a child and we pray that the love of Christ would transform lives! 
Bless, provide for and empower this ministry, I ask.</p>
<p>Lord, bless those who work as emergency medical technicians 
and paramedics. Help them to do their work well and give them skill, wisdom and 
strength beyond their own abilities. Help those who know Christ to share their 
faith with fellow workers.</p>
<p>Use me today, I 
pray in Christ’s Name, Amen.</p>

