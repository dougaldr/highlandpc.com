<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 18
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>You are a spiritual being. You have no body, except the 
resurrection body united to God the Son in the person of Jesus Christ. But in 
Your essence, You are a spiritual being. You do not depend upon the laws of the 
physical universe. You designed them. Thank You for creating us as both 
physical and spiritual beings. We are physical, so we can enjoy life on earth; 
we are spiritual, so we can enjoy fellowship with You. And though You are 
spiritual, You are also Personal. You are not some force.</p>
<p>I remember those struggling with addictions: alcohol, 
drugs, food, sex, materialism, clothes, work...and those addicted to their own 
adrenaline who cannot relax without getting depressed. Lord, so many of Your 
children struggle thusly. Help them to first of all believe that life can still 
be worth living, even without getting their &quot;fix.&quot; Help them to discover an 
entirely new world of adventure and meaning in You.</p>
<p>Please address the following needs in my life or in the 
lives of loved ones and friends: __________.</p>
<p>Today I remember the persecuted church. On the one hand, I 
am thankful for progress in the realm of freedom. Thank You that Qatar has 
allowed the first Christian church to be built in that country in 1400 years. 
But I pray for nations like Sudan: where Christians have been sold as slaves; 
China: where the government constantly persecutes the church; Saudi Arabia: 
where even small groups of believers are not allowed to meet; North Korea and 
Viet Nam: with their oppressive regimes. Lord, I have been told that more 
Christians died for their faith in the 20<sup>th</sup> century than in the first 
19 centuries of the church combined. Please, Lord, may the persecution stop, 
the Gospel spread, and may the persecutors themselves become believers in the 
Lord Jesus Christ.</p>
<p>I also remember the United States, where many previous 
religious freedoms have been challenged and sometimes lost. Whether it is the 
&quot;one nation under God&quot; clause in the Pledge of Allegiance or the displaying of 
Christmas nativities, forces are at work to obliterate the evidence that America 
was once a nation that favored Christianity and that such a favoring was not 
considered inconsistent with the constitution. Yet we know America's greater 
problem lies in the quality of believer we are producing.&nbsp; So often our 
negative statistics match those of people who know not Christ. Please help Your 
children to live in ways that please You.</p>
<p>I remember the International Bible Society and their 
ministry of making the Scriptures available at low cost prices. Thank You also 
for Faith Comes By Hearing as they make the recorded reading of the Bible 
available throughout the world as well. Provide for and use these important 
ministries. Thank You that Your Word will not return void. It is powerful. 
May Your Word spread throughout the world. On the other side, please hinder and 
hold back false religions from spreading.</p>
<p>Lord, help me serve You, I pray, in Jesus' Name, Amen.</p>

