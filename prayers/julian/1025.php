<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 25
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord Jesus,</p>
<p>I praise You for Your majesty. You are the majestic King 
of the Universe. Your power is without bounds, for You have all power and 
authority. You are God incarnate. You therefore love the truth and hate error.</p>
<p>And yet Lord, when You came the first time, You came with 
weakness as the Lamb of God to take away the sin of the world. When You come 
the second time, when You return to reign, You will come as the Lion of the 
Tribe of Judah. You will judge the peoples with equity, but rule with an iron 
fist, as 
<a href="javascript:scripture('../../scripture/ps2.htm')" target=_self class=pop>Psalm 2</a> 
describes. Lord, thank You that I have chosen to flee from 
Your wrath and to find Your mercy and grace. My heart aches for those who do 
not make such a choice.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart. (<i>Pause to admit them to God.</i>)</p>
<p>Lord, I thank You for the New Covenant. The symbol of the 
covenant You made with all mankind, the Covenant of Noah, is the rainbow. The 
symbol of the New Covenant is the bread and the cup that picture the broken body 
of Jesus Christ and His blood, shed for the remission of my sins. Every time I 
look back to Calvary by remembering the price of my redemption, help me to 
dedicate myself afresh to serve You, I pray. Keep Communion from becoming a 
meaningless ritual, I ask.</p>
<p>Lord, I pray for the people who attend the church I attend 
who have not yet accepted Jesus Christ as their personal Savior. Some people 
think they are Christians because they attend church; others believe they are 
Christians because they were raised in a Christian family, others, because they 
were baptized in some manner. Help such people to understand that salvation is 
a free gift 
(<a href="javascript:scripture('../../scripture/rom623.htm')" target=_self class=pop>Romans 6:23</a>), 
not earned or deserved but received by faith 
(<a href="javascript:scripture('../../scripture/eph289.htm')" target=_self class=pop>Ephesians 2:8-9</a>) 
because Christ died for us 
(<a href="javascript:scripture('../../scripture/rom58.htm')" target=_self class=pop>Romans 5:8</a>) 
and rose again 
(<a href="javascript:scripture('../../scripture/rom10913.htm')" target=_self class=pop>Romans 10:9-13</a>).</p>
<p>Father, when I am behind the steering wheel, help me to 
drive as a Christian should: with reasonable caution, alertness and obeying the 
laws. Protect my loved ones and me, I pray.</p>
<p>Today I pray for my city, town, county, or region—the 
most local level of government in my area. O Father, may many in my area come 
to know Christ, to follow Him and to walk closely with Him! May my local 
government officials be free from corruption and not hostile toward the Gospel. 
Protect the local law enforcement agencies and also protect the local 
firefighters. Thank You for the important services they offer.</p>
<p>Lord, thank You for Servant Partners which &quot;works with 
agencies all over the world to develop and encourage holistic church plants 
among the urban poor. This endeavor involves a wide array of strategies 
including micro-enterprise loans, leadership development and women's ministries 
to name a few.&quot; O God, provide, protect, prosper, empower, grant harmony to 
and use this ministry to Your glory, I ask.</p>
<p>Help me to demonstrate the joy of serving Jesus today, I ask in His Name, Amen</p>

