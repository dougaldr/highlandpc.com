<p>Understanding our Bible sometimes requires delving into 
three languages: our language (English), the language of the Old Testament and 
the Jews (Hebrew or the related Aramaic), and the language of the New Testament 
(Greek).</p>
<p>When studying the life of Christ, it is important to 
remember that Christ spoke Hebrew (or Aramaic—the matter is debated). The New 
Testament authors translated His statements into Greek. Bible scholars and 
linguists, in turn, translate the Greek into English. The epistles were 
originally written in Greek, but the sayings of the Gospels were spoken in 
Hebrew/Aramaic and then translated into Greek. With that intro, we can now 
“dive into” the name of our Savior, Jesus Christ.</p>
<p>God has many titles in Scripture but one name, “Yahweh.” 
In our versions it is translated as: “L<span class="sm">ORD</span>” or “G<span class="sm">OD</span>” and is 
distinct from “Lord” or “God.” “Yahweh” has been anglicized into “Jehovah,” but 
the name “Yahweh” is the actual name by which He identifies Himself. Yahweh 
means “I AM WHO I AM,” the self-existent One.</p>
<p>Jesus is the anglicized name for the Greek translation, “&#921;&#951;&#963;&#965;s”
pronounced (yae-soos); but the Greek is a translation from the Hebrew, “Yeshua,” 
which is what you would have actually heard Jesus called when He walked this 
earth.</p>
<p>Yeshua is a variation on the name Yehoshua (Joshua); both 
names mean “Yahweh is salvation” or “Yahweh is the Savior.” “Yeshua” (or Jesus) 
was a fairly common name among first century Jews.</p>
<p>God’s name is “Yahweh.” When God became a man in the 
person of Jesus Christ, His chosen name was “Yeshua.” Jesus is Yahweh in the 
flesh. He possesses two natures: He is both Son of God AND Son of Man. His 
humanity had a beginning in Mary’s womb; His deity (God-ness) has always been. 
The human nature of Jesus had a beginning, but the Son of God (His divine 
nature) has always existed.</p>
<p>We refer to Jesus as the “Christ.” We can say, “Jesus the 
Christ,” or “Jesus Christ” or “Christ Jesus.” Christ is not a name but a 
title. It is the Greek translation (Christos or “&#935;&#961;&#953;&#963;&#964;&#959;s”) 
of the Hebrew “Messiah,” meaning “The Anointed One.” Kings were typically 
anointed with oil as part of their initiation ceremony; this anointing also 
symbolized the Holy Spirit. The “Christ,” therefore, is God’s anointed King to 
whom He gives the Holy Spirit without limit. So there we have it, <b><u>
“Messiah Yeshua,” the anointed King, empowered by the Spirit who brings Yahweh’s 
salvation to mankind. He is God. He is Man. He is Yahweh Himself.</u></b></p>

