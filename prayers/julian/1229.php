<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 29
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are great and awesome! You keep Your promises, for 
Your gifts and calling are irrevocable 
(<a href="javascript:scripture('../../scripture/rom1129.htm')" target=_self class=pop>Romans 11:29</a>). 
You especially bless Your children who walk closely with You, who have 
learned to trust and obey.</p>
<p>Lord, I am a sinful person. I participate in a church made 
up of sinners saved by grace. I live in a sinful nation. You are right to 
bring wrath and judgment upon our nation, O Lord. And were it not that Jesus 
took my sins upon Himself, it would be right for You to take wrath upon me and 
my church as well. But even though we have fled to Your mercy to escape Your 
wrath, we know Your hand of discipline can be heavy upon us.</p>
<p>And so, by Your grace and with Your help, I repent of the 
sin in my life and ask You to renew my walk with You. I want to be cleansed. I 
want to be close to You. I want to walk with You that I might be pure in all my 
ways. Help me to close out this old year, to settle old accounts with You, O 
Lord, that I might prepare to enter the New Year with renewed vigor and zeal.</p>
<p>Help my nation likewise turn to You. May many come to 
saving faith and I ask that You would guide our leaders and officials. Create 
a climate of repentance and work by Your Spirit who both regenerates the saved 
and restrains the lost.</p>
<p>O Lord, make me a person who easily forgives those who ask 
for forgiveness. Remind me that asking for forgiveness is a bridge that I too 
have to cross. Remind me how the sting of guilt feels and how the remorse of 
brokenness works in one's soul. Help me to be good at both forgiving and asking 
for forgiveness.</p>
<p>Lord, we all have burdens to bear. Your Word tells us to cast all our cares 
upon You, for You care for us 
(<a href="javascript:scripture('../../scripture/1pete57.htm')" target=_self class=pop>1 Peter 5:7</a>). 
I give You my burdens, my heartaches, my fears. </p>
<p>Your Word also encourages us to bear one another’s burdens 
(<a href="javascript:scripture('../../scripture/gal62.htm')" target=_self class=pop>Galatians 6:2</a>). 
This means I have to be on the lookout to care for others. It 
is rather counterintuitive, but when I help bear another's burdens, mine are 
lessened. And when others help me, theirs are lightened.</p>
<p>Lord, thank You for You have done on the island of 
Kalimantan, part of Indonesia. Praise Your Name that perhaps 9% of the 
population would claim to be born-again evangelicals. Although literacy, 
poverty and travel difficulties have held back the church, please develop 
strong leaders, O Lord. Help the believers and missionaries to reach the many 
unreached peoples there, O God. May the percentage of evangelical believers 
double over the next decade, I ask.</p>
<p>Father, thank You for the UK based Leprosy Mission, the aim 
of which is &quot;to minister in the name of Jesus Christ to the physical, mental, 
social and spiritual needs of individuals and communities disadvantaged by 
leprosy; working with them to uphold human dignity and eradicate leprosy.&quot;</p>
<p>Now, Lord, help me to finish out this year in a way that 
pleases You, I ask in the Name of the Christ, Amen.</p>

