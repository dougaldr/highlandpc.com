<p id="pgt">How To Be Saved</p>

<p>To be saved means to be delivered or rescued. In this context, we
are talking about being delivered from sin and its penalty. Simply put, to be
saved means to be right with God, the consequence of which is eternal life in
heaven.</p>
<p>We are born separated from God and lost in our sins. Shortly before Jesus was
born, an angel appeared to Joseph and said, &quot;You shall call his name
Jesus, for it is he who will save his people from their sins.&quot; (<b>Matthew
1:21b</b>).</p>
<p>To be saved means to be transferred from God's disfavor to God's favor.
It
means becoming part of His Kingdom. It means a new, God-centered life.
It means
escaping the fear of hell and obtaining the assurance of heaven.</p>
<p><b>How can we be saved?</b></p>
<p><b>In order to be saved, we must:</b></p>
<ol>
  <b><li>Want to be saved. This is called repentance. To repent means to change
one's mind. If you want to be delivered (saved) from a sinful, self-centered
life and you want God to work in your life, you are well on your way.</b>
<p>&quot;Repent therefore and return, that your sins may be wiped away.&quot;
(<b>Acts 3:19</b>)
<p>This does not mean you must reform your life to be saved. It does mean you
want God to reform your life. You are open to His working. God is no longer an
enemy to keep at bay. Instead, He becomes the center of your life.</p>
<p>Repentance, as a change of mind, can occur in a split second, even though God
often prepares a person over time.</p>
  <b><li>Understand the basis of salvation: the work of Jesus Christ. Jesus is both
God and man. The Bible says, &quot;Unto us a Son is given...and his name shall be
called, Wonderful, Counselor, the Mighty God, the Prince of Peace.&quot;
(Isaiah 9:6b).</b>
<p>Jesus came to die on the cross as a sacrifice for our sins. He died to pay
our penalty so that God could forgive us.</p>
<p>&quot;Christ died for our sins, according to the Scriptures.&quot; (<b>I
Corinthians 15:3b</b>) &quot;Christ died for us. How much more then, having now been
justified by His blood, we shall be saved from the wrath of God through
Him.&quot; (<b>Romans 5:8b-9</b>)</p>
<p>After Christ died, He rose again, evidencing that He is the Son of God and
that His payment for sin was effective.</p>
  <b><li>We must depend upon what Christ did for us to make us right with God.
    A check is only <i>potential</i> until it is cashed. The work of Christ is
only <i>potential</i> until we believe. He died for everyone, but only
those who exercise faith experience the benefits of Christ's sacrifice.
    </b>
<p>We cannot obtain God's forgiveness by living a good life or by doing our
best. No one does his or her best all the time anyhow. Besides, we already owe
God everything. How can we give Him more than our all?</p>
<p>Good works or rituals cannot save us.</p>
<p>&quot;He saved us, not on the basis of deeds which we have done in
righteousness, but according to his mercy.&quot; (<b>Titus 3:5a</b>)</p>
<p>To be saved, we must stop trying to earn salvation. Jesus Christ is the
Savior. He saves. He does not help us to save ourselves. He does the saving!</p>
<p>&quot;If you confess with your mouth Jesus as Lord, and believe in your
heart that God raised Him from the dead, you shall be saved...for whoever will
call upon the name of the Lord will be saved.&quot; (<b>Romans 10:9b,11b</b>)</p></ol>
<p align="center"><b>Would you like to call upon the Lord for salvation?</b><br>
You might want to pray this model prayer:</p>
  <b>
<blockquote>
<p>Dear God, I know I am a sinner. I cannot make up for my sins. I believe Jesus
died as the sacrifice for my sin. He paid my penalty through His death on the
cross. I believe He rose again. I want to be saved from my sins.
I now claim the
promise of forgiveness and eternal life through my Savior. By faith I claim the
salvation you offer. Thank you for saving me. In Jesus' Name, Amen.</p>
</blockquote>
  </b>
