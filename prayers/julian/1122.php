<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 22
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>Your Word talks about the time when You, The Ancient of 
Days, will take Your seat to rule over all. But first You will judge all. In 
Daniel's visions 
(<a href="javascript:scripture('../../scripture/dan7910.htm')" target=_self class=pop>Daniel 7:9-10</a>), 
You are described as having hair like pure 
wool. Your throne seems to be on fire with a river of fire proceeding from Your 
throne. Millions worshipped and served You as they brought the books to open 
them.</p>
<p>Lord, I know Daniel saw a vision that represents You, not 
You as You really are, for &quot;no man has seen God at any time&quot; (<b>John 1:18</b>). 
Whether we are talking about the vision Moses had of You, the vision the elders 
of Israel had of You, the vision Isaiah, Ezekiel, or John the Apostle had—these are all 
representations of Who You are. You are Spirit, though You have 
appeared to men in a number of physical forms. In Jesus Christ God the Son 
became incarnate. But even then, Your majesty was veiled by human flesh.</p>
<p>O God, help me to be awestruck by Who You are. Help me to 
convert that sense of being overwhelmed into faithfulness. I remember the 
parable of the talents in which the master replied to his faithful servants, &quot;Well 
done, good and faithful servant! You have been faithful with a few things; I 
will put you in charge of many things&quot; (<b>Matthews 25:21a</b>). So Lord, worship 
experiences that do not affect me throughout the week and translate into service 
for You is mere emotion. You want me to worship in Spirit and in Truth. You 
are not interested in mere emotion, but repeatedly ask, &quot;Why do you call Me, 
'Lord, Lord,' and do not do what I say?&quot; (<b>Luke 6:46</b>). </p>
<p>I remember my enemies and those I do not really care for. I 
ask You to bless them and if appropriate to grant them repentance. Help me to 
respond in a proper way to them. Help me to understand the difference between 
loving my enemy, my neighbor, my brother or sister in Christ, my spouse, 
children and family. Help me distinguish between the love of duty and a love 
that embraces emotionally. May I not make more stringent demands upon myself 
than You make upon me.</p>
<p>I pray for the nation of Denmark. Thank You for the 4.8% of 
the population that claim to be born-again evangelical Christians. Many of the 
people claim a formal form of Christianity, but they do not attend church and do 
not have a personal, living relationship with the Risen Christ. Please use the 
Jesus Film, the Danish Bible Society and may Your Spirit also empower the new 
and promising Christian youth movement.</p>
<p>On another continent, thank You for Zion Evangelical 
Ministries of Africa which &quot;has formed a missionary partnership with TEAM and 
SIM to increase the work of the ministry&quot; in South Africa. Bless this work, keep 
it true to You and provide resources, protection and the blessing of Your 
Spirit.</p>
<p>Father, I pray for those who work as network systems and data 
communications analysts. May many in these fields come to know Jesus and help 
those who know You to spread the good news, I ask.</p>
<p>Lord, encourage me and help me encourage someone today. I 
commit this day to You in Jesus' Name, Amen.</p>

