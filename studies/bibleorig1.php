<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Origin of the Bible, Part I: Introduction to the Canon
<br><span class=byln>by Ed Vasicek</span></p>

<p>Almost all of our important beliefs as evangelical 
Christians can be found or at least implied in the Old Testament. And the 
nature of Scripture is such that if you believe in any part of the Bible, it 
pretty much leads you to the rest. This is called the &quot;colophon principle.&quot;</p>
<p>Put simply, if you believe in Jesus, you have to accept the 
Old Testament because Jesus did. He referred to the creation of Adam and Eve, 
the Flood, the Law, the Prophets, and the Writings (the Jewish term for the Old 
Testament) as the very Word of God.</p>
<p>Jesus taught that He would establish His church, and He 
trained the Apostles to be His authoritative representatives. So the teachings 
of the Apostles were the foundational teachings of the church—and 
authoritative. Hence the main criteria for accepting a book into the New 
Testament included: 1) Does it go back to the first century? 2) Is it 
genuine? and 3) Did an Apostle or a close associate of the Apostles write it? 
So if you believe the teachings of Jesus, by extension you must logically 
believe in the rest of the New Testament and the Old.</p>
<p>On the other hand, if you believe in the Old Testament, the 
prophecies of the Messiah lead you to Jesus Christ. According to 
<a href="javascript:scripture('../scripture/dan926.htm')" target=_self class=pop>Daniel 9:26</a>, 
for example, the Messiah would be &quot;cut off,&quot; and then later Jerusalem would be 
destroyed. The destruction of Jerusalem took place in 70 A.D., so the Messiah 
had to be cut off before then. The Old Testament describes where the Messiah 
would be born, His genealogy, deity, atoning death on the cross, burial, and 
resurrection.</p>
<p>The interlocking nature of Scripture addresses many 
issues. For example, Roman Catholics include some books in their Old Testament 
that are not included in ours. Protestants and Jews recognize the same Old 
Testament Scriptures. We even translate from the same manuscripts. Roman 
Catholics recognize some, but not all, of the Jewish writings known as &quot;the 
Apocrypha.&quot;</p>
<p>Although these Apocryphal books tell about some events that 
occurred between the Testaments, like the institution of Hanukah, most Jews 
considered them worthwhile reading but not Scripture. Whereas the Old Testament 
prophets seemed to register their prophecies 
(<a href="javascript:scripture('../scripture/ez139.htm')" target=_self class=pop>Ezekiel 13:9</a>), 
the Jews viewed God as no longer providing prophetic revelation after the 
time of Malachi. We refer to this period as &quot;the 400 silent years.&quot;</p>
<p>In 90 A.D., after the city of Jerusalem and the Temple 
within it had been destroyed and the Jews scattered, the leading rabbis got 
together for a major conference in the city of Jamnia (Yabneh). Their goal was 
to set a proper direction for Judaism since Temple worship and sacrifice were no 
longer possible. The Old Testament canon had pretty much been set from before 
the time of Christ, but at Jamnia the rabbis debated as to whether Ecclesiastes 
and Song of Solomon should continue to be included within the canon. They 
determined that the books truly were canonical. So Jamnia represented not so 
much the fixing of the Old Testament canon, but rather the end of debate.</p>
<p>Christians have been divided over the Apocryphal books for 
some time. In the fourth century, when Jerome translated the Greek New 
Testament and the Hebrew Old Testament into Latin, he did not want to translate 
these Apocryphal books. Augustine exerted great pressure on Jerome and he 
finally consented. But he rushed through the translation, with less concern for 
accuracy, to make his position known. A man convinced against his will is 
unconvinced still!</p>
<p>So how do we know that these books do not belong in our 
Bible? Are we relying on a Jewish council, or the views of the Reformers? No, 
the matter is simple to resolve. </p>
<p>Although we have the same Old Testament as the Jews, we 
arrange our books differently. For example, our twelve minor prophets are 
included as one book called &quot;The Twelve&quot; in the Jewish Bible. Lamentations is 
included as part of Jeremiah, and some of the historical books are lumped 
together (Ruth with Judges, for example).</p>
<p>&quot;The way modern Bibles are organized, there are 39 books in 
the Old Testament. Of these 39 books, only nine are not quoted in the New 
Testament. However, since the Hebrew Bible has long organized these books 
differently than they appear in modern Bibles, and some of these nine were 
originally combined with and part of other books, in reality only five of the 
Old Testament books are <i>not</i> quoted in the New Testament&quot; 
(<a href="http://www.gnmagazine.org/issues/gn15/otnt.htm" target="offline" class="reg">www.gnmagazine.org/issues/gn15/otnt.htm</a>).</p>
<p>NONE of the Apocryphal books found in the Roman Catholic Bible are quoted 
in the New Testament. True, much of the history mentioned in the Apocrypha 
might be factual, but so are history books of modern Israel. The odds that 80% 
of the recognized Old Testament books would be quoted as authoritative while 
0% of the Catholic Apocrypha books are quoted makes the matter clear.</p>
<p>Interestingly, in the original King James Version edition 
of 1611, the Apocryphal books were included between the Old and New Testament 
with an explanatory page. The translators stated that these books were not 
Scripture but that they were useful and helpful to the Christian. This tidbit 
is useful ammunition to irritate your &quot;King James only&quot; friends!</p>
<p>When it comes to the New Testament, Catholics and 
Protestants have the exact same canon, although Catholics used to go through 
three languages (English from the Latin which was translated by Jerome from the 
original Greek). Modern Catholic translations now have returned to the original 
Greek.</p>
<p>With that introduction, we can now begin to address the 
books of the Bible that go back the furthest in time, the Books of Moses. 
Although Moses takes us back to the beginning of creation, he wrote his books 
about 1450 B.C. Some have argued that Moses could not have written the Torah 
(the five books of the Law), since writing had not yet been invented in 1450 
B.C. This is absolute nonsense:</p>
<blockquote>&quot;In Mesopotamia the excavations of American scholars at 
Nippur in 1888-1900 brought to light thousands of clay tablets, including many 
bearing literary texts (among them the Sumerian narrative of the Flood) which 
can be dated to about 2100 BC or earlier.... The evidence from the other 
side of Palestine is equally impressive. 
From Egypt we have actual manuscripts, written on papyrus, datable to about 
2200-2000 BC, and containing texts which claim to have been written at a much 
earlier period. Probably the earliest of these are two ethical treatises, the 
Teaching of Kagemna and the Teaching of Ptah-Hetep...attributed to about 3100 BC 
and 2880 BC respectively&quot; (source:
<a href="http://www.katapi.org.uk/BibleMSS/I.htm" target="offline" class="reg">www.katapi.org.uk/BibleMSS/I.htm</a>).</blockquote>
<p>Henry Morris, in his commentary on Genesis, postulates that 
man has <i>always</i> been able to write. Just as Adam and Eve were created 
with the ability to speak, they may have been created with the ability to 
write. If so, the skill of writing would have been lost as people were 
scattered (by force at Babel) into remote regions after the Flood. As wild 
animals multiplied, survival became the main issue. A good example of the loss 
of civilization is the Aborigines of Australia who, at one time, wore clothing 
and were more civilized.</p>
