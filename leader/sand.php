	<p>Our Great God,</p>
	<p>We thank You for 
Your grace. It is Your grace that saves us. It is Your grace that provides 
strength for daily living, and it is Your grace that blesses with great joys. We 
rejoice in Your ordination of marriage. Thank You for leading John and Jane 
together by Your grace.</p>
	<p>Lord, we are all 
sinners, and when two become one, their unity will always be imperfect because 
we are imperfect. Yet, in Christ, we are a new creation, and Your Spirit is 
transforming us into the character of Christ. </p>
	<p>Help this couple 
to be considerate of one another, to be forgiving, to practice patience day 
after day and year after year, and to confront one another when necessary. Keep 
them far away from sarcasm and contempt. May they express themselves honestly 
and openly. Help them to work as a team, each looking out for the good of the 
other.</p>
	<p>God, unless You 
build the house, they labor in vain who build it. We pray, our gracious Father, 
that You would build this house. </p>
	<p>Accomplish Your 
will in this marriage and glorify Your Name. Help Jane and John to love one 
another as friends, as brother and sister in Christ, and as man and wife. May 
they not forget they are friends. Nurture their friendship, we ask. May they not 
forget they are followers of Jesus Christ. Help them to remember the call to 
build up one another spiritually as brothers and sisters in Christ are meant to 
do. Marriage does not replace friendship nor our place in the Body of Christ.</p>
	<p>As man and wife, 
help this bride and groom to relate to each other as Christ, the groom, does to 
His bride, the Church. The Savior leads but does not coerce. The church follows 
the direction of her Savior. </p>
	<p>Jesus gave His 
life for the church offering Himself as a sacrifice for our sins to the Father. 
In Gethsemane, Jesus did not feel like dying for the church, but He did what was 
in the church's best interest even when that best interest ran contrary to His 
feelings. Help this bride and groom to remember that love means sacrifice.
</p>
	<p>Just as Christ 
rose to newness of life on that first Easter Sunday, so You, O Father, bless and 
exalt those who serve You and follow Your plan.</p>
	<p>And so, Lord, 
John and Jane enter the marriage covenant with faith in You. We pray that You 
would bless and guide them throughout their lives, grant them peace and harmony, 
and we pray that You would help them observe the vows they make today. We ask 
all these things in Jesus' Name, Amen.</p>
