<p>
Our Savior was a Man of prayer. <b>Hebrews 5:7</b> reads, &quot;During the days of 
Jesus&#039; life on earth, he offered up prayers and petitions with loud cries and 
tears to the one who could save him from death, and he was heard because of his 
reverent submission.&quot;</p>
<p>
Although it is hard to categorize Jesus' prayer life, we can conclude that it 
consisted (more than likely) of adapted Jewish prayers and private petitions. In
<b>Luke 22:32</b>, Jesus says to Peter, &quot;But I have prayed for you, Simon, that 
your faith may not fail. And when you have turned back, strengthen your 
brothers.&quot; According to 
<a href="javascript:scripture('../../scripture/rom834.htm')" target=_self  class=pop>Romans 8:34</a>,
Jesus is STILL praying for us, His disciples.</p>
<p>
But what was Christ's earthly prayer life really like? Did He hold to any 
pattern? The answer to this second question is &quot;yes and no.&quot; Evidence suggests 
He prayed traditional Jewish prayers before meals and probably at morning, 
night, and during Sabbaths and feast days. Here are some traditional prayers 
Jesus may have recited (with alterations) as part of His prayer time.</p>
<p>
Before falling asleep: &quot;O Lord, grant that this night we may sleep in peace. And 
that in the morning our awakening may also be in peace. May our daytime be 
cloaked in your peace. Protect us and inspire us to think and act only out of 
love. Keep far from us all evil; may our paths be free from all obstacles from 
when we go out until the time we return home. Amen.&quot;</p>
<p>
Before a meal, Jesus likely would have prayed: &quot;Blessed are You, Lord our God, 
King of the Universe, Who brings forth bread from the earth.&quot;</p>
<p>
But Scriptures mention some times of INTENSE prayer. He probably prayed intently 
as He wandered in the wilderness. 
<a href="javascript:scripture('../../scripture/mark135.htm')" target=_self  class=pop>Mark 1:35</a> 
mentions one instance when He arose before sunrise to spend time in prayer. 
This was not His normal custom. In another instance, He seemed to begin a time 
of intense prayer in the latter afternoon 
(<a href="javascript:scripture('../../scripture/mark64647.htm')" target=_self  class=pop>Mark 6:46-47</a>). 
In 
<a href="javascript:scripture('../../scripture/luke612.htm')" target=_self  class=pop>Luke 6:12</a>,
He spent the whole night in prayer. Of course His experience in the Garden of 
Gethsemane was probably His most intense prayer time 
(<a href="javascript:scripture('../../scripture/matt263645.htm')" target=_self  class=pop>Matthew 26:36-45</a>).</p>
<p>
Since many of the personal Jewish prayers involved confession of sin, Jesus 
probably altered the words because of His sinlessness, although He may have 
prayed the Day of Atonement prayers unaltered since they involve a group 
confession. My suspicion is that much of His time in prayer was not merely 
asking God to help His disciples grow (though it is obvious that part of His 
time included this), but He probably majored on&nbsp; worshipping God, talking 
things over with God, and reviewing life with God the Father. He knew how to 
commune with His heavenly Father.</p>
