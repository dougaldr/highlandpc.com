<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 17
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>Thank You that Your Son, our Savior, the Lord Jesus Christ, 
is the way, the truth and the life. He is the way to You, the way to 
forgiveness, the way to eternal life. He is not just <u>a</u> way, but <u>the</u> way. 
As He said, &quot;no one comes to the Father but by me&quot; (<b>John 14:6</b>). We do not walk 
down a path (a way) of rituals and achievements—no, Jesus Himself is the way. 
He is the truth; His Gospel is the only true Gospel. And He is the life; all 
other &quot;ways&quot; lead eventually to death. But His way leads to life, an eternal 
life that begins now and extends forever and ever.</p>
<p>Lord, help me to rest in You and in Your truth. I do not 
want to be tossed to and fro by every wind of doctrine. I need to carefully 
develop convictions from Your Word and stick to them. I do not want to be among 
those Jude calls, &quot;clouds without rain, blown along by the wind; autumn trees 
without fruit and uprooted...wandering stars...&quot; (<b>Jude 12,13</b>). Help me be settled in 
You.</p>
<p>But, Lord, even though You are the life, sometimes life in 
the here and now is not so easy. Lord, I have some disappointments and burdens 
in life. A few of them&nbsp;have
hurt me and have left scars, including: __________.</p>
<p>I turn them over to You. Help me to find Your purpose and joy in&nbsp;my life. There 
are many things I cannot change, but I can share all my&nbsp;thoughts with You. 
Thank You that You do not stifle me or pressure&nbsp;me to &quot;get over it.&quot; You do not 
penalize me for honestly expressing my feelings as some people do. You are a 
God Who feels with me.</p>
<p>I remember parents. Lord, parents are so important to 
those little ones entrusted to their care. May parents everywhere protect and 
train their children in humane and balanced ways. Help them to love their 
children, not to spoil them and to encourage them toward positive directions. 
We pray that Christian parents would prove to be consistent role models for 
their young ones. Send angels to protect them, I pray.</p>
<p>Lord, I pray for the nation of Kuwait. Thank You that 
about .6% of the population are born-again evangelicals. Yet, Lord, it is 
against the law to proselytize Muslims, so any work has to be done quietly. O, 
Father, we pray that true religious freedom would reign in this land and that 
many would turn to You. Bless the Jesus Film, radio and satellite broadcasts, 
and the testimony of those who know You. May the church grow five-fold over the 
next decade! May Your Spirit irresistibly draw people to Jesus in this land.</p>
<p>Thank You for InterAct Ministries which &quot;exists to glorify 
God by fulfilling the Great Commission among unreached people groups.... [They] 
want to see a culturally relevant church in every community of the North Pacific 
Crescent.&quot; Thank You for their work in Canada, Alaska and Russia especially 
Siberia. We pray, O Lord, that You would provide harmony and empower the 
missionaries, provide for their needs and protect them. May they reap a 
harvest for Your Kingdom.</p>
<p>I ask You to help me be faithful to You today, in Jesus' 
Name, Amen.</p>

