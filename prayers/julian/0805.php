<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 5
<br><span class=byln>by Ed Vasicek</p>

<p>Precious Lord,</p>
<p>You are the God Who sanctifies suffering. We do not like 
to think of You that way, but Your Son, our Lord Jesus Christ, suffered both to 
atone for our sins and to set an example for us 
(<a href="javascript:scripture('../../scripture/1pete221.htm')" target=_self class=pop>1 Peter 2:21</a>). 
Lord, I do not 
want to suffer. I do not want my loved ones to suffer. Yet, Lord, You created 
mankind knowing that our first parents would sin. You allowed sin to produce a 
greater good in the long run. Those of us who are redeemed in Christ will have 
it much better than Adam and Eve ever did. So also You bring sorrow, trials, or 
suffering into our lives to form us and make us godlier. I confess that I do 
not like this plan, but I acknowledge that all things work for the good to those 
who love You and are called by You.</p>
<p>Today I pray for parents of children who have gone astray, 
one painful example of suffering. Whether in the realm of premarital or 
extramarital sex, drugs, alcohol, gambling, crime, violence, bitterness toward 
parents, abandoning the faith or in any other way, it hurts like nothing else 
can. O Father, may these children come back to You (if they truly know You) or 
come to You (if they do not). Lord, may the happy ending of The Prodigal Son be 
their experience as well, I plead.</p>
<p>I too need to address sin in my life. First John says 
that if I confess my sins, You are faithful and&nbsp;just to forgive me those sins, 
and You will purify me from all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the 
sins I am aware of now and I ask Your Spirit to search my heart (<i>Pause to 
admit them to God.</i>).</p>
<p>Today I pray for the nation of Slovenia, a nation of the 
former Yugoslavia. There is little interest here, but literature, radio, the 
Jesus film and a new Bible translation are steady attempts to reach the people 
in this European nation of two million. Only .2% of the population claim to be 
born-again evangelicals—that’s one in five hundred! My God, please send Your 
Spirit to draw the masses to Christ. May the number of born-again believers 
increase tenfold to 2% within a decade, I ask.</p>
<p>I 
remember the American Leprosy Mission in prayer. Their mission is &quot;to serve as 
a channel of Christ's love to persons affected by 
leprosy and related conditions, helping them to be healed in body and spirit and 
restored to lives of dignity and hope.... The ministry supports scores of projects in 
approximately 15 countries. Donor gifts provide training and transportation for 
leprosy workers, case-finding and diagnosis, physical and vocational 
rehabilitation, Christian encouragement, community development....&quot; 
My Father, prosper this ministry of mercy. May many see the love of Christ and 
turn to Him, I pray.</p>
<p>I remember those who work as structural metal 
fabricators and fitters. Grant them safety; may they work honestly and help 
these workers take pride in their work. May those who know You shine.</p>
<p>Help me to reflect Your character in my life today, I 
request in Jesus' Name, Amen.</p>

