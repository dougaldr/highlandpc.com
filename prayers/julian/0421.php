<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 21
<br><span class=byln>by Ed Vasicek</p>

<p>My dear God,</p>
<p>How good You are to me. How I appreciate knowing You. I 
very much appreciate that You are a social God. Not only do the Father, Son 
and Holy Spirit fellowship one with another, but You, O Lord, fellowship with 
Your creation. Your angelic messengers appear before Your throne and those of 
us redeemed from Adam's race will socialize with You forever. Right now You 
want to be included in my life not only as my Lord and Master but also as a 
friend, confidant and guide.</p>
<p>Lord, I thank You for creating human beings as male and 
female both in Your image 
(<a href="javascript:scripture('../../scripture/gen127.htm')" target=_self class=pop>Genesis 1:27</a>). 
The differences between male and 
female are more than physical and we should appreciate those differences and 
respect both our gender and the opposite gender. Even though men and women have 
differing roles in some areas, they are of equal personhood and value. So help 
us to appreciate and respect both men and women. Keep us from making derogatory 
statements or put-down jokes; although they are passed off as harmless humor, 
they really do nurture disrespect.</p>
<p>Mighty One, Your Word says, &quot;A prudent man sees danger and 
takes refuge, but the simple keep going and suffer for it&quot; (<b>Proverbs 22:3</b>). 
Lord, life has many potential dangers ahead. Keep me alert and when danger 
seems likely keep me from living in denial like the simple. Help me prevent 
more problems by thinking ahead rather than wandering around in a daze or with 
my head in the sand.</p>
<p>Please meet the following needs, requests and desires as 
You see fit: __________.</p>
<p>Lord, about the nation of Guinea in French-speaking west Africa, the evangelical church is small there only about 1 percent of the 
population. Most of the people are Muslim, but thank You that at least for 
now there is a good measure of religious freedom. Lord, please maintain that 
freedom and thank You that the work is indeed growing. I pray that You would 
protect the Christians and the missionaries who are working there and that the 
evangelical church would produce good leaders, be pure and multiply five-fold 
over the next decade.</p>
<p>Gracious One, thank You for the ministry of Paraclete, &quot;a 
group of experienced harvest workers who seek to expand the Kingdom of God 
through assisting mission agencies and churches in their mission to reach the 
least-reached peoples of the world.&quot; They offer resources to help missionaries 
with: team building, counseling, cross cultural training, conflict resolution, 
technology and administrative/organizational counsel. Use them to help 
missionaries get on the field, stay on the field, resolve problems and help 
educate them in ways that will significantly affect their ministries and the 
Kingdom of God.</p>
<p>Help me to serve 
You faithfully today, I ask in the Savior's Name, Amen.</p>

