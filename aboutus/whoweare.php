<p id="pgt">At A Glance: Who We Are</p>

<p>We are a Bible-believing evangelical church with an average 
attendance of about 100 people with a balanced mix of ages, noted 
for our balance and emphasis upon Bible content. Our 10:30 a.m. service style is 
blended and moderately contemporary.</p>
<p>Our pastor, Ed Vasicek, is a graduate of Moody Bible 
Institute and holds views typically associated with Moody. We are 
nondenominational, and we like to think of ourselves as a church where people 
can be themselves. We are not high pressured or into pretending.</p>
<p>Our mission is straightforward: To <b>reach</b> people with the Gospel of 
Jesus Christ, to <b>connect</b> believers to one another and to God, and to 
<b>deepen</b> them in their Christian walk.</p>
<p>Most of our folks dress informally. We are not a wild type of church, and we 
do not embarrass our guests!</p>

