<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>What About Mary? Part II - Bearing the Messiah
<br><span class=byln>by Ed Vasicek</span></p>

<p>Last month, we looked at the huge differences between the 
Roman Catholic perspective on Mary and the Bible-oriented evangelical 
perspective. In what we understand to be the Biblical perspective, Mary was a 
sinner who needed a Savior 
(<a href="javascript:scripture('../scripture/luke147.htm')" target=_self class=pop>Luke 1:47</a>), 
she remained a virgin only until Jesus was born 
(<a href="javascript:scripture('../scripture/matt125.htm')" target=_self class=pop>Matthew 1:25</a>--please 
note the term 'until.'), and she had other, naturally conceived children with 
Joseph as their biological father 
(<a href="javascript:scripture('../scripture/luke27.htm')" target=_self class=pop>Luke 2:7</a>--please 
note the term &quot;firstborn,&quot;
<a href="javascript:scripture('../scripture/matt135556.htm')" target=_self class=pop>Matthew 13:55-56</a>).</p>
<p>Before we look at Mary's eagerness to be the mother of the 
Messiah, we need to understand the Old Testament prophecies about the Messiah 
and how they relate to Mary's role.</p>
<p>After Adam and Eve fell into sin, God pronounced a curse 
upon all involved in the transgression and creation in general. We will pick up 
midstream with <b>Genesis 3:14-15</b>: &quot;Then the 
L<span class=sm>ORD</span> God said to the serpent: Because you have done this, you are cursed 
more than any livestock and more than any wild animal. You will move on your 
belly and eat dust all the days of your life. I will put hostility between you 
and the woman, and <i>between your seed and her seed</i>. He will strike your 
head, and you will strike his heel.&quot;</p>
<p>Note that the serpent (Satan--see 
<a href="javascript:scripture('../scripture/rev129.htm')" target=_self class=pop>Revelation 12:9</a> to 
document this) would harm the &quot;seed of the woman&quot; (the Messiah), but this &quot;seed 
of the woman&quot; would completely crush the serpent's head. The &quot;seed&quot; is the 
Messiah, Jesus Christ--and the woman is Mary.</p>
<p>In ancient times, a person's lineage was traced through the
<i>father</i>. But in the Garden of Eden, God predicts that it would not be the 
seed of the man, nor of the man and woman together--but the seed of the woman 
that would crush the serpent's head. This is the first implication of the 
Virgin Birth.</p>
<p>The second implication of the Virgin Birth is <b>Isaiah 7:14</b>, 
&quot;Therefore the Lord himself will give you a sign: The virgin will be with child 
and will give birth to a son, and will call him Immanuel.&quot; Isaiah probably saw 
a less-literal fulfillment in his day. A young woman, who was a virgin at the 
time of the prophecy, married and ceased being a virgin. She conceived a boy 
who was named Immanuel. But the more literal fulfillment occurred centuries 
later, when a woman (Mary/Miriam) conceived while still a virgin (this is a real 
sign) and gave birth to a Son who actually WAS Immanuel (&quot;God with us&quot;).</p>
<p>When Matthew quoted <b>Isaiah 7:14</b> in 
<a href="javascript:scripture('../scripture/matt121.htm')" target=_self class=pop>Matthew 1:21</a> 
and applied this passage to Jesus, he added the translation of the name <i>Immanuel</i> 
(&quot;God with us&quot;) to make this very point.</p>
<p>The Virgin Birth was certainly a miraculous sign in its own 
right, but it also fulfilled an important theological function. In order for 
the Messiah to redeem the human race, He had to be our &quot;kinsman,&quot; yet sinless. 
Christ was conceived miraculously so that He did not inherit a sin nature (the 
predisposition to sin that you and I have--nor the guilt of original sin), yet 
He was truly a descendent of Adam.</p>
<p>When the angel Gabriel appeared to Mary in <b>Luke 1:31</b>, he 
made this announcement: &quot;You will be with child and give birth to a son, and you 
are to give him the name Jesus. He will be great and will be called the Son of 
the Most High.&quot;</p>
<p>Mary could not understand this, so she inquired: &quot;How will 
this be...since I am a virgin?&quot;</p>
<p>Note the angel's explanation in <b>Luke 1:35</b>, &quot;The Holy Spirit 
will come upon you, and the power of the Most High will overshadow you. So <b>
the holy one</b> to be born will be called the Son of God.&quot; So Jesus would be 
born a holy infant, not tainted by original sin.</p>
<p>Mary's response of faith is meaningful indeed. She was 
fully submissive to the will of God: &quot;I am the Lord's servant.... May it be to me 
as you have said&quot; (<b>Luke 1:38</b>). What an awesome attitude she displayed!</p>
<p>Mary knew that few people would believe such an account. 
Compared to other people groups, the first century Jews were not a particularly 
superstitious people and would be skeptical about a virgin conception, despite 
the Old Testament prophecies. Whether Mary even tried to relay the angel's 
message to others beside Elizabeth is hard to tell.</p>
<p>Joseph had certainly concluded the worst. He decided a 
quiet divorce was the best route: &quot;Because Joseph, her husband, was a righteous 
man and did not want to expose her to public disgrace, he had in mind to divorce 
her quietly&quot; (<b>Matthew 1:19</b>).</p>
<p>Joseph and Mary were in a marital state unlike anything we 
have in modern western culture. In first century Israel, it was typical for a 
couple to be &quot;betrothed&quot; or &quot;engaged&quot; for about a year before they lived 
together as man and wife, living separately with their parents. When betrothed, 
they were considered legally married and, if they broke up, they would need to 
acquire a divorce. The average man would take that year to construct an 
addition to his father's house. Then, when the building was completed, the 
wedding feast was stealthily planned; he would then whisk his bride away in 
surprise, bring her to the wedding feasts, and then consummate the marriage. 
Incidentally, this is the imagery of 
<a href="javascript:scripture('../scripture/john1416.htm')" target=_self class=pop>John 14:1-6</a> 
and the Parable of the 10 Virgins 
(<a href="javascript:scripture('../scripture/matt25113.htm')" target=_self class=pop>Matthew 25:1-13</a>) 
who had to be ready on a moment's notice to accompany the bride to the surprise feast.</p>
<p>Joseph and Mary had been betrothed, and this made them 
legally married, but they had not yet consummated the marriage. It was during 
this &quot;in-between&quot; time that Mary became pregnant. Joseph knew that he and Mary 
had not come together, so his assumption naturally was that she had been 
unfaithful. Joseph experienced a change of heart when an angel appeared to him 
in a dream and verified that Mary was indeed supernaturally pregnant 
(<a href="javascript:scripture('../scripture/matt12125.htm')" target=_self class=pop>Matthew 1:21-25</a>).</p>
<p>Throughout Mary's life, she would carry the stigma of 
having been promiscuous, and Jesus would be accused of being illegitimate. 
Jesus' arguments with some unbelieving Jewish leaders carries a harsher picture 
if we postulate that they were attacking His dubious parentage. We join one 
such argument midstream (<b>John 8:18</b>) with Jesus speaking: &quot;'I am one who 
testifies for myself; my other witness is the Father, who sent me.' Then they 
asked him, 'Where is your father?'&quot;</p>
<p>Later, in the same chapter (<b>John 8:41</b>), Jesus is again 
arguing His case: &quot;'You are doing the things your own father does.' 'We are not 
illegitimate children,' they protested. 'The only Father we have is God 
himself.'&quot; </p>
<p>But the greatest insult came in <b>John 8:48</b>, &quot;The Jews 
answered him, 'Aren't we right in saying that you are a Samaritan and 
demon-possessed?'&quot;</p>
<p>The nature of the accusation that Jesus was 
&quot;demon-possessed&quot; is beyond the scope of this article, but the accusation that 
Jesus was called &quot;a Samaritan&quot; is not. The Samaritans were half Jewish and half 
gentile. Jesus' enemies were implying that Jesus' father was a gentile (a 
non-Jew). Decades later, rumors circulated that Jesus' real father was a Roman 
soldier named Panthera. About 200 A.D., the church father Origen wrote against 
this rumor: </p>
<blockquote>But let us now return to where the Jew is introduced [Celsus], 
speaking of the mother of Jesus, and saying that 'when she was pregnant she was 
turned out of doors by the carpenter to whom she had been betrothed, as having 
been guilty of adultery, and that she bore a child to a certain soldier named 
Panthera;' and let us see whether those who have blindly concocted these fables 
about the adultery of the virgin with Panthera, and her rejection by the 
carpenter, did not invent these stories to overturn His miraculous conception by 
the Holy Ghost: for they could have falsified the history in a different manner, 
on account of its extremely miraculous character, and not have admitted, as it 
were against their will, that Jesus was born of no ordinary human marriage. 
(Origen Against Celsus 1: Chapter XXXII)</blockquote>
<p>Origen argues if Joseph had been Jesus' true father, there 
would have been no need to concoct this story about Panthera.</p>
<p>Did Mary realize the stigma she would face when she told 
the angel, &quot;May it be to me as you have said?&quot; Probably so. Although every 
Jewish woman dreamt of mothering the Messiah, they probably did not understand 
that the Messiah would be conceived <i>this </i>way. The scandal of Christ's 
birth was but a foreshadowing of the scandal of the cross. While
<a name="OLE_LINK1">the unbelieving community looks at her as just another woman 
who went astray</a>, we are among those who recognize that she was truly blessed 
by God 
(<a href="javascript:scripture('../scripture/luke145.htm')" target=_self class=pop>Luke 1:45</a>). </p>
