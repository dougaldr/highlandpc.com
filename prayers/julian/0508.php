<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 8
<br><span class=byln>by Ed Vasicek</p>

<p>Savior,</p>
<p>You are the Last Adam. The first Adam made a sinful 
choice and he plunged the universe into a state of decay and death. He was our 
representative, our proxy. Since that time, all humans—except You, the 
virgin-born Jesus—have been born with a sin nature. You were born with a 
human nature for sure, but one that was not predisposed to sin, like Adam's 
before the Fall. Perhaps this was why it was necessary for You to be born of a 
virgin, so You did not inherit the sin nature.</p>
<p>Like the first Adam, You are the representative for others, 
the believer's proxy. In Your case, Lord, You chose to obey—even though You 
were repeatedly tempted. <b>Romans 5:17</b> tells me, &quot;For if, by the trespass of the 
one man, death reigned through that one man, how much more will those who 
receive God's abundant provision of grace and of the gift of righteousness reign 
in life through the one man, Jesus Christ.&quot; Thank You, Lord Jesus.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>My God, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control 
me&nbsp;today. </p>
<p>Lord, I know the church gathers so that believer can build 
one another up in their faith, the result of this is spiritual maturity as seen 
in faith, hope and love. I need to be part of the Body to mature properly. I 
need to socialize to be mentally healthy and I need Christian fellowship to be 
spiritually healthy. Although worship is part of church life, it's not just 
about worshipping You. Help me to become a more mature Christian. This does not 
happen in giant leaps, but by small baby steps. Help me make those steps. I 
also pray for the following, that they would grow a little more every week: __________.</p>
<p>Father, I pray for the Kingdom of Jordan. Thank You for 
the religious freedom there and I pray that it would continue. Although only 
two-tenths of one percent of the population claim to be born-again 
evangelicals, the evangelical church is growing. Although many Christians end 
up immigrating for other lands, help those who are committed to remaining 
multiply themselves by sharing Christ. May Your Spirit prepare more and more 
hearts for the good news of Jesus Christ. Bless the Jesus film and other 
outreach attempts. Help the church to train and retain good leaders, I pray.</p>
<p>Thank You for Missions to Military, a &quot;Christian 
ministry that&nbsp;works through&nbsp;local churches and with Bible-believing Chaplains to 
help evangelize and to disciple military men and women for Jesus Christ.&quot; Thank 
You for the six Military Christian Centers on the east coast and the two 
centers located overseas. Use these centers and the staff to win men and women 
to Christ and disciple them. Fill them with Your Spirit, provide for them and 
grant them harmony.</p>
<p>I pray for city and town employees who help maintain, 
protect and plan for the needs of their citizens. Bless many of them with 
salvation and a sense of satisfaction from their work.</p>
<p>Now use me as Your servant today, I ask in Jesus' Name, 
Amen.</p>

