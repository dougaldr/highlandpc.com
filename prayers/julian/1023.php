<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 23
<br><span class=byln>by Ed Vasicek</p>

<p>Lord Jesus,</p>
<p>You have the right to rule; all authority is Yours on heaven and earth. As 
God the Son, all things were created by You and through You. As the Son of Man, 
Adam's sinless seed, Your human nature identifies You with us. As the God-man, 
You died on the cross to pay for my sins so that I could be redeemed. I have 
been bought with a price and therefore You want me to glorify You. I owe You my 
allegiance both as my Creator and as my Redeemer.</p>
<p>Lord, give me an insatiable appetite for Your Word. Help me to eagerly study 
and read the Bible. Give me the enthusiasm of the Psalmist in
<a href="javascript:scripture('../../scripture/ps1.htm')" target=_self class=pop>Psalm 1</a>, 
a desire to meditate on Your Word throughout the day. Help me to 
devour Your Words as exemplified by the Psalmist in the longest chapter of the 
Bible,

<a href="javascript:scripture('../../scripture/ps119.htm')" target=_self class=pop>Psalm 119</a>. 
I know that the Bible is the most crucial source for coming to 
know You better and it is the measuring stick of all truth. It is my spiritual 
food. Help me not to suffer malnutrition by neglecting it.</p>
<p>Help me to resist temptation and the lures of the Satan today.</p>
<p>Lord, I remember my country's chief executive officer (President, Prime 
Minister, Secretary General, etc.) and our legislative and congressional 
branches. May they all favor religious freedom. May they make it easy for us to 
be at peace without compromising our convictions. Help them to govern, rule and 
judge rightly and fairly.</p>
<p>Help those who are seeing Christian counselors. Guide these counselors into 
Your will, save marriages and lives and help people adjust to the hardships of 
life. Lord, help counselors to fear You, thus truly offering their clients the 
counsel of the godly. Help counselors who do not know You to be constructive and 
supportive of their Christian clients.</p>
<p>Lord, today I pray for the great continent of Asia with its nearly 4 billion 
people! Almost two-thirds of the world’s population lives in this continent, yet 
only 3.6% of the population professes to be born-again and evangelical. Thank 
You, that despite the many, many hardships—persecution, prohibition to share 
Christ, refusing to allow missionaries to enter some countries and great 
poverty—the church is growing at a good rate.&nbsp; Lord, I pray the persecution 
would stop, that prohibitions against conversion would cease and that the Gospel 
would go forth with great power and effectiveness. I pray that the percentage of 
born-again evangelicals would double within the next seven years and double 
again over the next seven years, I ask. Give wisdom to the believers. Provide 
for them, prosper them and help them walk closely with You.</p>
<p>Father, thank You for the ministry of SEND International, &quot;an 
interdenominational faith mission agency, with over 600 missionaries in more 
than 20 countries of Asia, Europe and North America.&quot; For more than 50 years 
they have been &quot;starting churches where there are none by evangelizing the 
unreached, nurturing disciples, and developing leaders from among the national 
people.&quot; O God, prosper this mission. Provide for them, protect them, empower 
them by Your Spirit, grant them harmony and much fruit for Your Kingdom, I ask.</p>
<p>I submit today’s plans to You, O Lord, in Jesus’ Name, Amen.</p>

