<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 14
<br><span class=byln>by Ed Vasicek</p>

<p>O God,</p>
<p>You are MY God. Yes, You are the God of all Who 
call upon You in truth. Yes, You are the God of the Universe, the Creator of 
all things. But You are also MY God, concerned with my life, my problems and my 
little world.</p>
<p>As for me, I can barely keep track of family and friends. 
And my ability to remember details—or to even have the time to inquire—is so 
limited! But not so with You, my God. You take pleasure in me, my life and 
even the mundane and trivial aspects of it. Your mind is so immeasurable that 
You are fully aware of all the details of everything in creation and You know 
not only every tiny detail of all that is happening right now, but You 
simultaneously know all that has or will happen. I am impressed beyond measure.</p>
<p>I pray for my pastor, staff, and church board leaders, 
officers, and teachers. If any do not know You, may they become born-again. 
Help them to be true to the Bible and to put Your Word over their own personal 
agendas. Help them to have genuine integrity, Biblical convictions, love, 
wisdom and courage. Help them put their walk with You first, their families 
second and their ministries third. Do not allow others to intimidate them into 
neglecting their families and remind them that &quot;the fear of man works a 
snare.&quot; Help them be most concerned about pleasing You while also seeking 
peace and harmony within the congregation.</p>
<p>Father, I have some personal health concerns including: __________. I also 
pray for the physical, mental, or spiritual health of others including: __________.</p>
<p>Lord, I pray for the nation of Uzbekistan formerly part of 
the Soviet Union. Lord, this nation is far from free and only .3% of the 
population claims to be born-again evangelical. Over 85% of the population is 
Muslim and most believers have immigrated into this land. The government is 
willing to allow some freedom for registered churches, but it makes it almost 
impossible for a church to register. Bless Bible distribution, help the 
government change its mind to allow Christians to produce literature and bless 
the two hours a week of Uzbek broadcasts by Christian shortwave radio stations. 
Though the Jesus film has been banned, it is being used to share the Gospel; bless 
this attempt and protect the believers. Grant freedom to this land and may 
Your church multiply tenfold over the next decade, I ask.</p>
<p>Lord, thank You for ITS, the Institute of Theological 
Studies, a &quot;ministry that produces advanced independent study theology 
courses and participates in international Christian leadership development.... ITS 
was founded to address the need for reliable, flexible and accessible distance 
theological education resources for pastoral and ministry training.&quot; Thank You, 
O God, that this ministry has been used by You to train leaders throughout the 
world. Bless this work, provide, prosper, empower and grant harmony, I ask.</p>
<p>Lord, I pray for those who work as general and operations 
managers. May those who know You shine and may they lead others to Jesus.</p>
<p>Help me to be patient today, I request in Jesus’ Name, 
Amen.</p>

