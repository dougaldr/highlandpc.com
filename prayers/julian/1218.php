<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 18
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>When the wise men bowed before the infant Savior, they 
brought Him a gift of gold. Gold was the most valuable substance in that day  
and still is near the top two thousand years later. Gold is a gift fit for a 
King. Biblical Kings amassed great supplies of gold: Pharaoh, Solomon, 
Nebuchadnezzar. They had golden cups and golden bowls.</p>
<p>By traveling a great distance and referring to Jesus as 
&quot;King of the Jews,&quot; there is little doubt that the Magi recognized that Jesus is 
a King. He is not only &quot;King of the Jews,&quot; but also the &quot;King of Kings and Lord 
of Lords.&quot;</p>
<p>Speaking of God, <b>1 Timothy 6:15</b> reads, &quot;God, the blessed and only 
Ruler, the King of kings and Lord of lords, who alone is immortal and who lives 
in unapproachable light, whom no one has seen or can see. To him be honor and 
might forever. Amen.&quot;</p>
<p>Speaking of Jesus in <b>Revelation 19:15-17</b>, we read, &quot;Out of 
his mouth comes a sharp sword with which to strike down the nations. 'He will 
rule them with an iron scepter.'<sup> </sup>He treads the winepress of the fury 
of the wrath of God Almighty. On his robe and on his thigh he has this name 
written: KING OF KINGS AND LORD OF LORDS.&quot;</p>
<p>Jesus is King of Kings because He is God Almighty! Praise 
His Name!</p>
<p>Thank You, Lord, that as a follower of Jesus Christ, I enjoy fellowshipping 
with other believers and working together as a family. Help me to love my 
brothers and sisters in Christ in practical ways 
(<a href="javascript:scripture('../../scripture/1john41112.htm')" target=_self class=pop>1 John 4:11-12</a>).</p>
<p>Lord, help those looking for work. Guide Your children into fields and places 
of employment based upon Your purposes. Especially prosper those who work 
honorably </p>
<p>Today I remember the Asian nation of Myanmar, just north of Thailand. 
Although freedom of religion exists on paper in this nation, the military regime 
has a track record of forcing others to convert to Buddhism. Thank You that the 
evangelical church has grown to 5.2%, despite this pressure and despite the 
expulsion of missionaries in 1966. Thank You that missionaries have been allowed 
in. We pray that You would bless the translation work as 14 translations are 
started—but another 14 need to be started as well. O Lord, may the number of 
believers double within the next decade. Use Christian radio, the Jesus film and 
the testimony of believers. Provide solid education for church leaders and may 
the government become more tolerant.</p>
<p>Thank You for the ministry of Caring for China whose vision &quot;is to share the 
love of Christ with the people of China by sending Christians to work alongside 
them. [They] do this by placing teachers of English in positions at universities 
and secondary schools in China where there is a great need for teachers and by 
providing suitably qualified, motivated and experienced personnel to care for 
orphaned, handicapped, abandoned and sick children in cooperation with 
appropriate local authorities. In addition to providing practical help and care, 
the outworking of [their] vision involves training local personnel to assist 
[them] in this work of compassion.&quot; O Lord, bless their efforts, give them unity 
of Spirit, provide for and protect them and may they reach many.</p>
<p>Now help me honor You throughout my day, I pray in Jesus' Name, Amen.</p>

