<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 20
<br><span class=byln>by Ed Vasicek</p>

<p>Yahweh, the God Who Keeps His Covenant,</p>
<p>Thank You for Your faithfulness. You are utterly 
trustworthy and dependable, and You personify integrity. When You make an 
unconditional promise, You keep it unconditionally. When You make a conditional 
promise, You keep it as You stated. You do not change Your contracts after the 
fact, but Your calling and purposes are irrevocable. We humans renege, we 
rationalize and we fudge. But not You. You are faithful to an infinite 
degree. Your faithfulness reaches toward the heavens. Your yes means yes and 
Your no means no. You do not vacillate and have no hint of fickleness.</p>
<p>Oh, Lord, help me to be more like You. Help me to remember 
that it is better not to make a vow than to make a vow and not keep it. Make my 
commitments few, but help me to keep the few I make.</p>
<p>Father, I have some personal health concerns including: __________.</p>
<p>I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power not my own. May Your Spirit control me 
today.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (1 John 1:9). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>I remember the hungry, the destitute, the abandoned, the 
sick and the elderly. Oh, Lord, be a God of compassion and provide for them. 
Change world conditions so that fewer people are hungry, and bring medical help 
to the world.</p>
<p>Today I pray for the Dominican Republic, a nation that 
shares the island of Hispaniola with Haiti. Lord, the people are poor, the 
government corrupt, and the challenges many. Thank You for the 70 missionaries 
who serve there, and that 7.6% of the population claim to be born-again 
Christians. Thank You for the growth we have seen in recent decades. We pray 
that those who make professions of faith would become integrated into 
Bible-oriented churches and take root, developing spiritual maturity. We pray 
that You would raise up strong leaders and provide for them. Bless the solid 
theological schools on the island, and may the church in the Dominican Republic 
double over the next decade but quadruple in quality!</p>
<p>Lord, thank You for the ministry of African Enterprise, &quot;a 
Christian interdenominational, multicultural ministry of evangelism; 
reconciliation; leadership development training; relief and community 
development.... [Its] scope is all of Africa. AE leaders are African nationals that 
work with local church and parachurch leaders to mobilize congregations in 
outreach to African cities.&quot; Provide for and bless this ministry; keep them 
true to You, accountable and effective. May Your Spirit empower those 
ministering and help converts to get connected to Biblically solid 
congregations.</p>
<p>Today I pray for engineers, the men and women who design 
the products we use or that benefit us in some way. Bless them, and may many 
engineers come to know Jesus Christ. Help those who do to share their faith and 
to serve faithfully in local churches.</p>
<p>Now guide me today, I pray, in Jesus' Name, Amen.</p>

