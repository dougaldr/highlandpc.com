<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Heart of the Gospel: 
<br>Propitiation and Romans 3:24-26 - Part III
<br><span class=byln>by Ed Vasicek</span></p>

<p>Some pastors have neglected teaching theology because they 
felt the fundamentals of the faith were safely assumed within evangelical 
circles. This is no longer the case; every belief--no matter how clear the 
Scriptures are--is being &quot;revisited.&quot; This &quot;mix and match&quot; mentality means we 
can no longer assume people believe a series of doctrines that used to accompany 
one another. For example, &quot;young earth creationists&quot; might reject propitiation 
(as typically defined); firm Trinitarians might reject the sinlessness of Jesus 
Christ. They use our words, but redefine them.</p>

<p>Some &quot;evangelical&quot; scholars claim that traditional 
Reformation understanding of the atonement and justification by faith were 
reactionary (incorrect) interpretations espoused by the Reformers to distance 
themselves from Rome. In these articles, we are examining <b>Romans 3:24-26</b>, a 
central text in the debate. </p>

<p>We have looked at the nature of justification, redemption, 
and grace. Today we will dig more deeply into the concept of propitiation. First 
let's review our key passage, <b>Romans 3:24-26</b>:</p>

<blockquote>Justified by his grace as a gift, through the redemption 
that is in Christ Jesus, whom God put forward as a propitiation by his blood, to 
be received by faith. This was to show God's righteousness, because in his 
divine forbearance he had passed over former sins. It was to show his 
righteousness at the present time, so that he might be just and the justifier of 
the one who has faith in Jesus. (ESV)</blockquote>

<p><u>Today's article will answer these questions:</u> Doesn't the 
&quot;Penal Theory of the Atonement&quot; create an unflattering picture of God? Why 
doesn't God simply forgive us without demanding a payment for sin? How is the 
Christian concept of propitiation different from pagan concepts? Do the 
Scriptures really teach that the death of Christ was actually a payment made for 
our sins, or is such a view forced into the text? </p>

<p>Let's answer our first question: <u>Doesn't the &quot;Penal Theory 
of the Atonement&quot; create an unflattering picture of God?</u> No, God does not need 
an anger-management class. God's wrath is provoked logically because His justice 
(holiness) has been violated; His anger (which involves emotion) is a 
consequence of mankind's rejection of Him. His anger is truly a &quot;righteous 
indignation,&quot; exemplified by Jesus when He turned over the tables in the Temple 
courts.</p>


<p>Secondly, <u>why doesn't God simply forgive us without 
demanding a payment for sin?</u> <b>Romans 3:26</b> explains that God is,&quot; just and the 
justifier of the one who has faith in Jesus.&quot; God justifies and saves the sinner 
in a way that is &quot;just.&quot; God does not randomly justify us simply because He 
loves us, for then His justice would not be satisfied. No, He justifies us on 
the basis of Christ's work on the cross. He justifies us in a just way.</p>

<p>Although God is omnipotent (has all power), this is not 
identical with saying &quot;God can do anything.&quot; In <b>Genesis 18:25</b> we hear the 
rhetorical question, &quot;Shall not the Judge of all the earth deal justly?&quot; God 
either will not or cannot act unjustly. According to <b>Titus 1:2</b>, &quot;God...cannot 
lie.&quot;</p>

<p>If we agree that God cannot lie, act unjustly, or sin, this 
leads us to conclude that God cannot violate His own nature. Since God is holy 
and just by nature, these attributes limit what He can (or will) do; it seems 
likely that God can only forgive sinners in a way that is consistent with Who He 
is.</p>

<p>The Father did not answer the Son's Gethsemane prayer when 
He prayed, &quot;Father, if it is possible, let this cup pass from me&quot; (<b>Matthew 26:39</b>). 
We could only be saved through propitiation via the cross.</p>

<p>Thirdly, <u>how is the Christian concept of propitiation 
different from pagan concepts?</u> The Christian view of propitiation bears only 
superficial resemblance to pagan concepts. John Stott (in his <b>Romans</b> commentary) 
makes the contrast:</p>

<blockquote>In the pagan perspective, human beings try to placate their bad-tempered 
deities with their own paltry offerings. According to Christian revelation, 
God’s own great love propitiated his own holy wrath through the gift of his own 
dear Son, who took our place, bore our sin and died our death. Thus God himself 
gave himself to save us from himself.</blockquote>

<p>Fourthly, <u>do the Scriptures really teach that the death of Christ was 
actually a payment made for our sins, or is such a view forced into the text?</u></p>

<p>Although Anselm elaborated the Penal Theory in the eleventh 
century, its basis goes back to the Torah. <b>Leviticus 5:6</b> is but one example: 
&quot;when he realizes his guilt...and confesses the sin...he 
shall bring to the L<span class=sm>ORD</span> as his <b>compensation</b> for the sin that he has 
committed, a female from the flock...the priest shall make <b>atonement</b> for 
him for his sin.&quot; ESV</p>

<p>The death of Christ is clearly a &quot;compensation for...sin.&quot;</p>

<p><b>Isaiah 53</b> is the supreme chapter when it comes to 
explaining the nature of Jesus' death. </p>
<p>Note the phrases I have highlighted with bold font.</p>

<blockquote>Surely he took up our infirmities and carried our sorrows, 
yet we considered him <b>stricken by God</b>, smitten <b>by him</b>, and 
afflicted. But he was pierced for our transgressions, he was crushed for our 
iniquities; <b>the punishment that brought us peace was upon him</b>, and by his 
wounds we are healed. We all, like sheep, have gone astray, each of us has turned 
to his own way; <b>and the L<span class=sm>ORD</span> 
	has laid on him the iniquity of us all </b>(<b>4-6</b>).</blockquote>

<p>Also note some important phrases from <b>verses 8 and 10</b>:</p>

<blockquote>For he was cut off from the land of the living; <b>for the 
transgression of my people he was stricken...</b>Yet <b>it was the L<span class=sm>ORD</span>'s will to 
crush him</b> and cause him to suffer, and though <b>the L<span class=sm>ORD</span> makes his life a 
guilt offering, </b>he will see his offspring and prolong his days, and the will 
of the L<span class=sm>ORD</span> will prosper in his hand.</blockquote>

<p>It was the Father Who transformed Jesus’ death into a sin 
offering; the L<span class=sm>ORD</span> (Yahweh) laid our sins upon Christ. The death He suffered was 
a <b>punishment that brought us peace</b>. This is the heart of what 
propitiation means: Christ was punished by the Father to satisfy the justice of 
the Father on our behalf. The Scriptures are clear.</p>

