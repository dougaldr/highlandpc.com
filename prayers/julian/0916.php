<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 16
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>As soon as we are born, the timer of our lifespan begins. 
For some, it is long, for others short; for many others, somewhere in between. 
And Lord, we have all experienced the pain of losing someone close to us, 
someone whose absence grieves us. How our hearts can ache at times, how we would 
love to visit with those who are no longer consciously here on earth. At the 
same time, we find strength and courage in You; but we admit that those who have 
gone ahead of us, particularly those who knew You and served You well, also 
inspire us.</p>
<p>The same is true in the Kingdom of God. Your Kingdom is an everlasting 
Kingdom. For the believer, there is a better life to come. But in Christ, I have 
already &quot;come to Mount Zion, to the heavenly Jerusalem, the 
city of the living God...to thousands upon thousands of angels in joyful assembly, 
to the church of the firstborn, whose names are written in heaven...to God, the 
judge of all men, to the spirits of righteous men made perfect, to Jesus the 
mediator of a new covenant and to the sprinkled blood that speaks a better word 
than the blood of Abel&quot; (<b>Hebrews 12:22-24</b>). Lord, I am part of a big plan; help 
me to be faithful to You during my pilgrimage on earth.</p>
<p>Lord, I am thankful for plenty of things. You have been 
good. Two of the things for which I am thankful include: __________.</p>
<p>Lord, may Your Spirit fill me. May I find my strength 
in His power, not my own power. May Your Spirit control me this day.</p>
<p>Also, Father, create in me a hunger and thirst for Your 
Word. Nothing can substitute for time in Your Word: no music, no drama, not 
even prayer. Just as I must eat to be healthy, so I must feast on Your Word if 
I am to be spiritually healthy.</p>
<p>Lord, I pray for the Asian (Eurasian) nation of Turkey 
with its 70 million people. Although Turkey technically recognizes religious 
freedom, there is still some persecution and discrimination against Christians 
in this land where 99.64% of the population are Muslims. Evangelical Christians 
are so few that the percentage of them is 0%. Turkey is one of the least reached 
nations in the world. O Father, begin to do a work here like You are beginning 
to do in other Muslim lands. Send dreams to the people, use Christian radio 
broadcasts and draw many to Yourself, I pray. May the number of believers grow 
a thousandfold over the next few years, I ask.</p>
<p>Lord, I pray for India National Inland Mission, Inc., &quot;headquartered in New 
Delhi, India, with outreach concentrated in the largely unevangelized northern states 
of India. INIM includes Grace Bible College for 
the training of native evangelists who go out into North India and establish 
churches. The organization also maintains a children’s home for orphans.&quot; Lord, 
use and bless this ministry, I ask. Prosper them, provide for them, protect the 
workers, grant them harmony and empower them by Your Spirit to Your glory!</p>
<p>Dear God, I commit 
my day to You, in Jesus’ Name, Amen.</p>

