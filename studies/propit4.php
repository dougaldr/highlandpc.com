<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Heart of the Gospel: 
<br>Propitiation and Romans 3:24-26 - Part IV
<br><span class=byln>by Ed Vasicek</span></p>

<p>We sing, &quot;Jesus paid it all, all to Him I owe,&quot; or &quot;You 
came from heaven to earth to show the way, from the earth to the cross, my debt 
to pay.&quot; Yet millions simply do not get it: Jesus died to offer Himself as a 
penalty payment for our sins to the Father. Propitiation is the heart of the 
Gospel.</p>

<p>In previous articles, we saw that God's wrath is turned 
away from us because of the death of Jesus Christ; His death satisfied God's 
righteous justice and wrath. He became a curse for us 
(<a href="javascript:scripture('../scripture/gal313.htm')" target=_self class=pop>Galatians 3:13</a>). 
Just as God's wrath was averted toward Israel when the priest sprinkled the blood on the 
Mercy Seat (on <i>Yom Kippur</i>), so God freely expresses His mercy to us once 
the blood of the Lamb was let as a sin payment. The text we have been dissecting 
is <b>Romans 3:24-26</b>:</p>

<blockquote>Justified by his grace as a gift, through the redemption 
that is in Christ Jesus, whom God put forward as a propitiation by his blood, to 
be received by faith. This was to show God's righteousness, because in his 
divine forbearance he had passed over former sins. It was to show his 
righteousness at the present time, so that he might be just and the justifier of 
the one who has faith in Jesus. (ESV)</blockquote>

<p><u>In this final segment, we will answer:</u> How would the statement, &quot;Christ died 
for our sins&quot; have been understood in the first century? Why was it necessary 
for the Messiah to be &quot;God in the flesh&quot; to atone for our sins? And in what 
sense did the propitiation &quot;show God's righteousness, because in his divine 
forbearance he had passed over former sins?&quot;</p>

<p>First, <u>how would the statement, &quot;Christ died for our sins&quot; 
have been understood in the first century?</u> The Jewish people would have 
understood the sufferings of Christ--along with the sufferings of devout Jews--to 
have been a penalty to atone for sin. According to David Flusser in his book,
<u>Jewish Sources in Early Christianity</u>:</p>

<blockquote><p>Since the age of the Hasmoneans, Jews had believed that 
the saints who died to sanctify the name of God atoned for the sins of Israel. 
The story of the mother and her seven sons in the Second Book of Maccabees 
acquires a greater significance in the Fourth Book of Maccabees, where their 
death is seen as an atoning sacrifice. In another Jewish source, <i>Midrash 
Sifre</i>, the idea is expressed that the killing of the Children of Israel by 
the Gentiles atones for the former's sins.</p>

<p>It is reasonable to assume that during the Roman period 
this idea was applied not only to Jesus, but also to all those who were executed 
by the authorities. Even Jews who did not accept Christianity evidently believed 
that Jesus, like the other martyrs of the Roman authorities, had atoned for the 
sins of Israel (p. 59).</p></blockquote>

<p>Although the Jewish people would have understood the death 
of Christ atoned for sin, they failed to factor in the concept revealed in <b>Psalm 
49:7-8</b>: &quot;No man can redeem the life of another or give to God a ransom for 
him--the ransom for a life is costly, no payment is ever enough.&quot;</p>

<p><u>Jesus Christ could actually atone for the sins of mankind 
and make a payment that is &quot;enough&quot;</u> because (1) He was in a representative 
position as the Second Adam; (2) He was sinless; and, most significantly, (3) He 
was the God-man. Since the lost are cast into the Lake of Fire for eternity, the 
penalty Christ paid was likely an infinite one. If so, this penalty would be the 
same had He died for one person or all persons, for any number multiplied by 
infinity is infinity. </p>

<p>The human nature of Christ is forever joined to His divine 
nature (two natures united in one Person). Hence we read in <b>Acts 20:28</b>, 
&quot;shepherd the church of God which He purchased with His own blood.&quot; Since God 
is the nearest antecedent to &quot;His,&quot; it seems as though Paul is talking about 
&quot;God's blood.&quot; Although the human blood of Jesus was shed on the cross, His 
atoning work is not that of a mere sinless man; instead, it is amplified to 
infinity because of Jesus' union to His divine nature.</p>

<p><u>In what sense did the propitiation &quot;show God's 
righteousness, because in his divine forbearance he had passed over former 
sins?&quot;</u> The answer is fascinating: for centuries, Satan and his myriads could 
accuse God of being unjust for forgiving the sins of those who repented and 
turned to Him in faith. God was seemingly forgiving people without regard to 
righteousness or justice. But God &quot;put forward [Jesus] as a propitiation by his 
blood.... This was <i>to show God's righteousness</i>.&quot; At Calvary, God not only 
provided for the redemption of future generations, but Calvary also vindicated 
God's character as a just and righteous God before a watching universe.</p>

<p>Along with Paul, we can celebrate: &quot;Who will bring any 
charge against those whom God has chosen? It is God who justifies. Who is he 
that condemns? Christ Jesus, who died--more than that, who was raised to life--is 
at the right hand of God and is also interceding for us&quot; <b>Romans 8:33-34</b>.</p>

<p>Through the agency of faith, we appropriate the benefits of 
Christ's gracious propitiation. But why did God make faith the key? Because 
faith is our personal vindication of God's character. Faith is all about God's 
character. To doubt God is to insult His veracity, His trustworthiness, and His 
competence. To trust Him is to embrace His character as trustworthy and 
dependable. When we believe, we acknowledge that He is just, holy, loving, good, 
and Sovereign.</p>

<p>We find ourselves asking that great question: What is the 
purpose of God's creation? What are we about? If you answered, &quot;we exist to 
glorify Him,&quot; you are on target. The centerpiece of the glory of God is &quot;the 
Lamb of God who was slain from the foundation of the world&quot; (<b>Revelation 13:8</b>). 
All history was structured around Jesus' propitiation on Mount Calvary--and His 
glorious resurrection--nearly 2,000 years ago. Let us fully and confidently 
embrace the fact that Christ died to pay the penalty for our sins.</p>

