<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 18
<br><span class=byln>by Ed Vasicek</p>

<p>Creator of All,</p>
<p>I praise You that literally everything that I see has been made by You. The 
only thing not made by You is You, the uncreated One. All creation occurred by a 
conversation within the Persons of the Godhead. You said, &quot;Let us make man,&quot; or 
&quot;Let there be light.&quot; And by Your Word, You created all things out of nothing. 
You created space, dimension, time, matter and spirit beings: angels. Then You 
created man, a being who is both physical and spiritual.</p>
<p>Although we humans cannot create something out of nothing, we do recombine 
what You have created. In that sense, we too are creative. When our creativity 
is wholesome, it showcases Your image in us. Help me, therefore, to welcome 
godly creativity and to nurture it in my life. Help me to think outside the box.</p>
<p>Thank You, Lord, that creation is heading somewhere. We wait for the return 
of Christ to rule during the Kingdom Age and we anticipate the New Heavens and 
the New Earth.</p>
<p>I ask You to help me to think as a Christian should. Keep me from developing 
a lazy brain. May I stretch my mind for You. Help me renew my mind by 
meditating upon Your Word. When I am anxious, help me present my burdens to You 
and trade them for Your peace. May I have Your calm when appropriate and Your 
zeal and energy at other times.</p>
<p>Let me remember that You are developing my faith and that You often use 
difficult times to make me stronger in You. Though I admit that I usually hate 
such trials, I do appreciate what You have done in my life through them. My 
choice is to go through them with You or without You. I will go through them 
either way. I choose to go through them hand in hand with You.</p>
<p>Lord today, I especially remember those looking for a job. Open doors of 
opportunity and match the right person to the right job. Especially help those 
who work with integrity and have good character to find employment.</p>
<p>Father, be Lord of my finances. Help me regulate my money with Your wisdom.</p>
<p>Gracious One and Lord of the Great Commission, I bring the nation of 
Kyrgyzstan before Your throne. More than one half of one percent claim to be 
born-again evangelicals, a tiny representation of Your Kingdom. The nation is 
horribly impoverished and corrupt with all sorts of superstitious beliefs mixed 
with Islam. O, Father, raise up a people for Your Name in this land. Use them to 
lead the nation to honesty, prosperity, integrity and most importantly 
salvation. Do a work of Your Spirit here, we pray.</p>
<p>Lord, thank You for the ministry of Bibles for the World as they follow their 
&quot;dream to give a free copy of the New Testament to all the families of the world 
in response to the commission of the Lord, 'the Gospel must first be published 
among all the nations.'&quot; They have sent over 14 million New Testaments to 
individual homes in over 100 countries. Provide for their needs, give them 
unity and harmony and bless their work by empowering it with Your Spirit, I ask.</p>
<p>Help me remember relevant Scriptures throughout the day, I ask in Messiah’s 
Name, Amen.</p>

