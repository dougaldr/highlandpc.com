<p>At 
the time of Jesus, many Jewish rabbis (not all) and many people (not all) 
thought that sickness, particularly being born with a handicap, was an act of 
punishment from God for a specific sin. This was an old belief (see 
<a href="javascript:scripture('../../scripture/job47.htm')" target=_self  class=pop>Job 4:7</a>, 
<a href="javascript:scripture('../../scripture/job84.htm')" target=_self  class=pop>8:4</a>,
and the question of the disciples in 
<a href="javascript:scripture('../../scripture/john912.htm')" target=_self  class=pop>John 9:1-2</a>)
and is held by many today, particularly those characterized by &quot;folk religion.&quot; 
Whereas it is true that God 
is sovereign and He does not &quot;pass the buck&quot; of responsibility for disabilities, 
disability or illness is very often NOT a result of a specific sin, but is 
rather a result of the destructive power of sin that was unleashed when our 
first parents chose to rebel against God (see 
<a href="javascript:scripture('../../scripture/rom81828.htm')" target=_self  class=pop>Romans 8:18-28</a>).
Sickness드nd even death듞an sometimes be a form of discipline 
(<a href="javascript:scripture('../../scripture/1cor1130.htm')" target=_self  class=pop>1 Corinthians 11:30</a>). 
Our bodies are not yet redeemed (see 
<a href="javascript:scripture('../../scripture/rom823.htm')" target=_self  class=pop>Romans 8:23</a>, 
a key verse), so we believers also groan with the rest of creation.</p>
<p>God does take the responsibility for allowing individuals to be handicapped, as seen in 
<b>Exodus 4:11</b>, &quot;Who gave man his mouth? Who makes him deaf or mute? Who gives 
him sight or makes him blind? Is it not I, the Lord?&quot;</p>
<p>At 
the same time, God frequently mentions that he will not specifically punish the 
sons for the sins of the fathers (<b>Ezekiel 18</b>). Parents may cause a handicap 
through their own sin or neglect (e.g., a mother who takes drugs gives birth to 
a child who is brain damaged), but parents who are not neglectful may also give 
birth to the handicapped.</p>
<p>The 
paralytic in today's sermon 
(<a href="javascript:scripture('../../scripture/mark2112.htm')" target=_self  class=pop>Mark 2:1-12</a>, 
<a href="javascript:scripture('../../scripture/matt928.htm')" target=_self  class=pop>Matthew 9:2-8</a>, 
<a href="javascript:scripture('../../scripture/luke51726.htm')" target=_self  class=pop>Luke 5:17-26</a>) may have 
become paralyzed through an accident. The point is that, in the eyes of many 
people, his paralysis was a judgment for sin. If God forgave the sin, the man 
would be healed.</p>
<p>In 
today's texts, Jesus forgives the sin first, but the man is NOT healed. The 
Pharisees who had traveled from the South (Judea) into Galilee to check Jesus 
out were upset that Christ claimed to forgive this man's sins. Jesus did not 
offer to pray to God for this man's forgiveness, but pronounced forgiveness as a 
fact under His own authority. The Pharisees correctly believed that only God 
could forgive sin, and they were right. By claiming to have the authority to 
forgive sins, Jesus was claiming to be God.</p>
<p>But 
there is a problem. Forgiveness of sins is not something one sees. So, to 
prove His authority to forgive sin, Jesus tells the man to take up his stretcher 
and walk드nd the man is healed! I am of the persuasion Jesus would have healed 
this man even if he were not trying to provide the Pharisees with evidence that 
He was the Messiah. But Jesus performed these two miracles in order of 
importance: forgiveness lasts for eternity and means everlasting life; healing 
lasts for only a time and, at death, is not relevant.</p>
<p>
Today God cares about all our needs, but He emphasizes the spiritual over the 
physical: &quot;For what is seen is temporary, but what is unseen is eternal.&quot; Have 
you come to Jesus for forgiveness? He died to provide it for you, and He lives 
to secure this forgiveness for you들f you will only trust Him as your Lord and 
Savior!</p>
