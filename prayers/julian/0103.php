<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 3
<br><span class=byln>by Ed Vasicek</span></p>

<p>All Knowing One,</p>
<p>I am so glad that You are a God Who is known for His love. 
You have proven Your love by sending Jesus Christ into the world. I know You 
love me, not because life is always rosy—for it is sometimes anything but 
rosy—but because of Calvary.</p>
<p>I thank You that the shed blood of Jesus Christ, His 
sacrificial death, was an atonement, paying the penalty for all my sins: past, 
present, and future. Thank You that You, as a God of justice and love, are now 
free to forgive me without compromising Your justice because of that atonement.</p>
<p>Lord, as Christ suffered for me, I need to be ready to 
suffer as a Christian. Although I do not ask for suffering, I do ask that when 
it comes my way, I might know the special fellowship that comes by walking with 
Christ through difficult times.</p>
<p>Father, I always have many personal needs or desires. I 
place them before Your throne. Please address the following needs: __________.</p> 
<p>As the Lord of all, I pray that You would bless our 
nation. Help our state judges to rule wisely and fairly. Especially help those 
who must make decisions involving custody, child support, or other 
family-impacting rulings.</p>
<p>Today I pray for the country of Canada. Lord, secular 
humanism has conquered that land. Revive the evangelical church and multiply 
her numbers. May Canadians be drawn by Your Spirit to see that life without You 
at the center is woefully incomplete. Send a holy angst to those people, and may 
they flock to You in great numbers.</p>
<p>I pray for Wycliffe Bible Translators, as they seek to make 
Your Word available to every kindred, tongue, and nation. May they meet their 
2025 goal of having translations started in every language group needing one. 
Guide their leaders to that end, and help them think creatively and wisely.</p>
<p>Holy Spirit, give me a heart that hungers after You this 
day.</p>
<p>I pray in the name of Christ, Amen.</p>

