<br>Things We Can Only Do Now
<br><span class=byln>by Ed Vasicek</span></p>

<p>About the time this edition of the Body Builder hits the press, some of you
procrastinators will be busy finishing up a few summer projects. Perhaps you put
off painting till the weather cooled, or you are finally patching that broken
sidewalk. You are doing these tasks now because they may be difficult or
impossible to do during the winter.
</p>

<p>
You may have never thought about this, but contemplate this with me. There are
some tasks God has for us that we cannot do in heaven; we must do them now, on
earth, or they remain undone. The time is now. When winter comes, it will be too
late.
</p>

<p>
What heaven will be like is, to a large extent, a mystery. We know that we will
be able to worship God more purely and sincerely than we ever could on earth. It
seems we will help rule over the universe. It also seems that we will be
learning about the nature of God's grace throughout eternity
(<a href="javascript:scripture('../scripture/eph27.htm')" target=_self class=pop>Ephesians 2:7</a>).&nbsp;
On one hand, we can (to some degree) worship God while on earth, grow in Kingdom
leadership skill, and learn about the grace of God. In eternity, we will excel
in these areas. On the other hand, we can do some things on earth, in the here
and now, that we will not be able to do in heaven. Consider a few of these with
me.
</p>

<p>
On earth, we can evangelize. Whether people reject or accept Christ, God is
glorified
(<a href="javascript:scripture('../scripture/2cor215.htm')" target=_self class=pop>2 Corinthians 2:15</a>)
through the process. We can be a bulwark for
missions, helping fulfill the Great Commission. We will have no opportunity to
reach people for Christ in heaven. The Great Commission will be history.&nbsp;
None of
our peers will be called to the mission field from the city of golden streets.&nbsp;
We will have no lost neighbors with whom we can share the Good News.
</p>

<p>
On earth we can edify others, building them up in their faith. We can stimulate
others to good works, use our abilities and gifts to strengthen the Body of
Christ, and experience spiritual growth. We can progress toward holiness and
spiritual maturity. In heaven, we will have no sin to overcome, nor will we be
able to help others win their battles. Spiritual gifts will be unnecessary, and
edification will be a thing of the past.
</p>

<p>
On earth, we can intercede for others in prayer. We can do battle with Satan,
putting on the armor of God. We can be strong soldiers of the Lord Jesus Christ.&nbsp;
In heaven, we will but remember the victories and lay our laurels at his feet.&nbsp;
But we will be unable to add to our list of victories.
</p>

<p>
No one will need mercy in heaven. There will be no sick to visit, no moms with
new babies for whom we can bring meals, no need to baby-sit a sick neighbor's
child. But now, on earth, we hardly know where to begin. We can glorify God by
being careful to do good works.
</p>

<p>
In heaven, there will be no children to train (though many children will inhabit
heaven, probably appearing as adults). There will be no need to help strengthen
marriages and no counseling ministries. We will be like angels, neither marrying
nor giving in marriage. In contrast, the possibilities to serve the Lord now by
ministering to children and youth, by mentoring marriages, or by counseling
adults seems infinite.
</p>

<p>
In heaven, there will be no riches to earn, no sacrifices to make. They will not
pass the offering plate in glory. Financial secretaries and church
treasurers will be distant memories. But on earth, we can store up treasures in
heaven. Jesus could not have been more blunt in telling us so
<a href="javascript:scripture('../scripture/matt620.htm')" target=_self class=pop>(Matthew 6:20</a>).
</p>

<p>In heaven, we will not need to worry about doctrine since we'll finally all
agree up there. There will be no error to fight, no heresies to guard against,
no need for shepherds to guard the sheep. On earth we have so much of which to
keep abreast. Theological dangers can be found a little to the left or right of
the straight and narrow, and many sheep are so gullible.
</p>

<p>
In heaven, there will be no example to set, and no light will be needed to shine
on a hill. This little light of ours will pale in contrast to the light of God's
presence. There will be no family to rear, no marriages to show the world the
relationship of Christ and the Church, no need to work at loving our neighbor.&nbsp;
On earth these responsibilities are weighty ones indeed.
</p>

<p>
You see, there are many tasks God has for us during our stay on earth. We can do
things for the Lord now that we will not be able to do in eternity. We are able
to glorify God in ways now that will not be relevant in heaven. So appreciate
your life on earth. It can be an investment in eternity. Do the things on
earth now that you won't be able to do in heaven. Winter is coming, so get your
painting done!
</p>

