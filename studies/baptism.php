silence, but we have to wonder why the apostles were not re-baptized as were 
some of those in Acts who only knew John's baptism. We have no record of Peter, 
for example, being baptized AFTER his confession that Jesus was "the Christ, the 
Son of the Living God."</p>
<li><p class="bighpc"><a name="sctn7">Passages Pro and Con: In the Hermeneutical 
Trenches</a></p>
<p>In this section, I will 
attempt to address a number of verses that are used to promote the idea that 
baptism is absolutely necessary for salvation. Again, I will remind readers 
that I do agree that it is God's will for every believer to follow the Lord in 
believers' baptism by immersion after a confession of faith. That is not the 
issue here. I do not view baptism as optional, but rather as distinct from 
regeneration and salvation. It is, in my opinion, the formalization of 
conversion.</p>
<p>When you have the firm 
foundation of a Sovereign God who chooses His elect (apart from any foreseen 
response in them) and guarantees their conversion and final destiny by His side, 
you are free to examine verses from a different perspective. If you conclude 
that the real issue is glorifying God, and that God is not desperate nor 
helpless and that neither the flow of human history nor the population of heaven 
are dependent upon man's fickleness, it affects your entire outlook.</p>
<p>Rather than being 
obsessed with "holding out" and "making it to heaven" (a phrase, incidentally, 
never found in Scripture), you can press on because you know God and are 
securely in relationship to him. The key term moves from "salvation" to 
"maturity in Christ." Heaven, the indwelling Holy Spirit, and our adoption into 
God's family are seen as several among the many consequences of being rightly 
related to God.</p>
<p><b><a name="sctn71">Acts 2:38</a></b></p>
<p><b>Acts 2:38</b> is perhaps the strongest text used to promote the 
idea of baptism as necessary for salvation, so I will begin there.</p>
<blockquote><p><sup>36</sup> &quot;Therefore let all Israel be assured of this: God has 
made this Jesus, whom you crucified, both Lord and Christ.&quot; </p>
<p><sup>37</sup> When the people heard this, they were cut to the 
heart and said to Peter and the other apostles, &quot;Brothers, what shall we do?&quot;
</p>
<p><sup>38</sup> Peter replied, &quot;Repent and be baptized, every one of 
you, in the name of Jesus Christ for the forgiveness of your sins. And you will 
receive the gift of the Holy Spirit. <sup>39</sup> The promise is for you and your children and for all 
who are far off--for all whom the Lord our God will call.&quot; </p>
<p><sup>40</sup> With many other words he warned them; and he pleaded 
with them, &quot;Save yourselves from this corrupt generation.&quot; <sup>41</sup> Those who accepted his message were baptized, and 
about three thousand were added to their number that day. </p>
<p><sup>42</sup> They devoted themselves to the apostles' teaching and 
to the fellowship, to the breaking of bread and to prayer.</p></blockquote>
<p>Some interpretational 
debates center on the word, "for," which represents a translation of the Greek 
preposition "eis." Rather than haggle over how "eis" should be rendered, I 
would prefer to leave the Greek alone and simply follow the common translation, 
"for." It is not that the argument for translating "eis" as "because of" has no 
merit; I simply do not view it as the crux of the argument.</p>
<p>The Great Commission is 
often considered the "marching orders for the church.&quot; <u><b>The concern of the 
Great Commission</b></u> 
(<a href="javascript:scripture('../scripture/matt281920.htm')" target=_self class=pop>Matthew 28:19-20</a>) <u><b>is not seeing people saved; 
it is, rather, making mature disciples, which begins with salvation</b></u>. This was the 
emphasis of the early church; getting saved was a key step in the discipleship 
process, not the final product.</p>
<p>And that was the focus 
of <b>Acts 2:38</b>. Those who focus more on getting to heaven rather than becoming 
mature Christians may experience a paradigm blindness that reads material into a 
text. This is true with all theological positions, including my own, and we 
should be alert to this tendency in our attempts at interpretation. </p>
<p>If we understand 
Peter's main concern on Pentecost was making disciples <b><u>who would go on to 
maturity</u></b>, not merely seeing people saved, it changes our perspective in 
contemplating his preaching.</p>
<p>The question in this 
text is NOT specifically about salvation, but rather what God expects these 
repentant Jews to do. They have realized that they have crucified and rejected 
the Messiah in <b>Acts 2</b>, </p>
<blockquote><p><sup>22</sup> </span>&quot;Men of Israel, listen to this: Jesus of 
