<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 26
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>I sing for joy because You are my strength! When my 
energy is depleted and my batteries need charging, You enable me to keep going. 
You give me oil in my lamp that I might keep burning. Father, You do not want 
me to seek out other gods, but to worship and serve You alone. Many are tempted 
to literally seek out other gods, as literal idolatry is rampant in our world. 
Others of us are tempted to put things above You sometimes even good things. 
But, Lord, You demand to be Number One, for You are a jealous God. When I open 
my life to You, O Lord, You fill it and satisfy me as a mother bird does her 
fledglings. Help me to spend time with You, O Lord, and find strength in You.</p>
<p>I pray for my lost friends and family members including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>I pray for Christians who are straying from their first 
love or compromising their walk with You. Rekindle the faith they once 
professed and help them fervently love the Savior once again. Remove their 
boring view of serving You and help them see the adventure and fulfillment of 
walking daily in Your light. Stir up their hearts and help them make the 
routines of godliness a part of their lives. I especially remember: __________.</p>
<p>Lord, Your Word says, &quot;A simple man believes anything, but 
a prudent man gives thought to his steps&quot; (<b>Proverbs 14:15</b>). Lord, I do not want 
to be simple or foolish; I want to be wise and prudent. Help me, Lord, to look 
before I leap, to think things through first. Help me discipline myself to take 
those few moments to do so and remind me that an ounce of prevention is worth a 
pound of cure. Help me to develop a reasonable amount of caution.</p>
<p>Lord, I pray for the small Pacific island nation of Tuvalu 
with its 11,000 people. Thank You that over 5% are born-again evangelicals. 
Lord, may this number triple over the next five years. </p>
<p>Thank You, Lord, for the ministry of JAARS. &quot;Through 
partnerships worldwide, JAARS provides quality technical support services and 
resources to speed Bible translation for all people.... Its purpose is to provide 
high quality technical support services to Wycliffe Bible Translators (WBT) and 
to SIL International (SIL). Areas of support include: technology, logistics,...media, 
and transportation.&quot; Bless and provide for this ministry, I ask; it 
serves a crucial role in helping Bible translation actually get done and is 
therefore near and dear to Your heart, O Lord. Protect, prosper and grant 
harmony to these missionaries, I pray.</p>
<p>Lord, today I remember some of society's most influential 
people, elementary school teachers, and special education teachers. O Lord, help them 
to invest their energies wisely in the lives of the young people they touch. 
May many in these fields come to know You and may the youngsters see Christ in 
their lives, I pray.</p>
<p>May others see Christ in me today, I ask in Jesus' Name, 
Amen.</p>

