<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Water Baptism and You: The Basics
<br><span class=byln>by Ed Vasicek</span></p>

<p>A good place to begin discussing water baptism is to talk about what it is
NOT. It is not magic. It does not save you. It does not atone for original sin
or any other sin.</p>
<p>The thief on the cross joined Jesus in paradise without being baptized
(<a href="javascript:scripture('../scripture/luke2343.htm')" target=_self class=pop>Luke 23:43</a>).&nbsp;
Paul makes it clear in Romans that New Testament believers are saved in
the same way as Old Testament believers&#151;by faith. (For example, Abraham was
never baptized.) Peter says it is the inward baptizing&#151;the inward cleansing
from sin that saves one, not the outward
(<a href="javascript:scripture('../scripture/1pete321.htm')" target=_self class=pop>1 Peter 3:21</a>).&nbsp;
Since in New Testament
times, baptism usually took place right after faith and repentance, this ritual
was often used to refer to what occurred beforehand.</p>
<p>Although there is not magic in baptism, it symbolizes several things. By
obeying the Lord in this matter, we are built up in our faith. This is true
because God meets us when we obey Him. It also creates a memory that the Holy
Spirit can use to help us in our battle against sin
(<a href="javascript:scripture('../scripture/rom635.htm')" target=_self class=pop>Romans 6:3-5</a>).</p>
<p align="center"><b>What Baptism Means and Symbolizes</b></p>
<ol>
  <li>Baptism pictures repentance and conversion, so much so that the terms are
used interchangeably. When John the Baptist ministered, this primarily Jewish
ritual would not be performed unless the candidate for baptism evidenced a
repentant heart
(<a href="javascript:scripture('../scripture/luke339.htm')" target=_self class=pop>Luke 3:3-9</a>).<br><br>
  <li>Baptism pictures our death, burial, and resurrection with Christ. As we
are lowered into the water, our death and burial with Christ are symbolized.&nbsp;
    As
we arise out of the water, our new Resurrection life is pictured. We are now to
live as though we are dead to sin but alive to God
(<a href="javascript:scripture('../scripture/rom6111.htm')" target=_self class=pop>Romans 6:1-11</a>).&nbsp;
    Although this spiritual transformation took place instantly when we believed
(<a href="javascript:scripture('../scripture/eph2110.htm')" target=_self class=pop>Ephesians 2:1-10</a>), it
is pictured when we are baptized.<br><br>
  <li>Baptism is a way to publicly identify with Jesus Christ
  (<a href="javascript:scripture('../scripture/matt1032.htm')" target=_self class=pop>Matthew 10:32</a>) and
is the God-ordained way to profess Him. Identification is the heart of baptism:
Jesus was baptized to identify with us (He had no sin to repent of) and we are
to be baptized to identify with Him.<br><br>
  <li>Baptism can be a test of the sincerity of our repentance. If we want to
live for Christ, we need to obey Him. He commands us to be baptized.&nbsp;
    Therefore,
if we want to live for Christ, we will be baptized (among many other things).&nbsp;
    The logic breaks down if we do not have a clear understanding of baptism.</ol>
<p><b>Jesus commands us to baptize those who respond to the Gospel
(<a href="javascript:scripture('../scripture/matt281920.htm')" target=_self class=pop>Matthew 28:19-20</a>).&nbsp;
As a matter of fact, baptism is one third of the Great Commission!</b>
<p align="center"><b>How Should We Be Baptized?</b></p>
<p>Although many fine Christians disagree with our views, we take the simple
approach to water baptism. We believe baptism is reserved for believers and is
to be by immersion in the name of the Father, Son, and Holy Spirit
(<a href="javascript:scripture('../scripture/matt281920.htm')" target=_self class=pop>Matthew 28:19-20</a>).</p>
<p>Why immersion? Because that is the simple and basic meaning of the word
&quot;baptidzo&quot; in the Greek New Testament (the original language). The New
Testament speaks of converts going into the water and coming up out of the water
(<a href="javascript:scripture('../scripture/acts838.htm')" target=_self class=pop>Acts 8:38</a>,
<a href="javascript:scripture('../scripture/mark110.htm')" target=_self class=pop>Mark 1:10</a>).&nbsp;
Jewish baptism, which John the Baptist and the early
church adapted, was always by immersion. Immersion is the only method that fits
the picture of
<a href="javascript:scripture('../scripture/rom634.htm')" target=_self class=pop>Romans 6</a>.&nbsp;
The Greek words for &quot;sprinkle&quot; or
&quot;pour&quot; are never used of baptism in the New Testament.</p>
<p>The New Testament pattern was that of baptizing people after they had
believed
(<a href="javascript:scripture('../scripture/acts241.htm')" target=_self class=pop>Acts 2:41</a>).&nbsp;
We hold that such a simple approach is the best approach.</p>
<p>It is interesting that most church leaders who believe in sprinkling or
pouring will recognize immersion as legitimate baptism. But many of us who hold
to immersion do not recognize the other modes. So if you are immersed, very few
will challenge the legitimacy of your water baptism.</p>
<p align="center"><b>The Mechanics</b></p>
<p>At Highland Park Church, we have a baptistry (a small pool of water) in our
basement stage area. We heat the water before the service.&nbsp;</p>
<p>Candidates for baptism should:</p>
<ol>
  <li>Come prepared to share his or her testimony. Anything from &quot;I
accepted Christ as my Savior about 3 months ago&quot; to an extended testimony
is welcome.<br><br>
  <li>Wear old clothes: our robes show through when wet.<br><br>
  <li>Bring a plastic bag for wet clothes, a dry change of clothes, a towel,
and other grooming items (comb, hair dryer, if you wish, etc.).<br><br>
  <li>Arrive ½ hour early for final directions and changing time.</ol>
<p>Before you are baptized, you need to be sure you are saved. If you don't know
the Lord, baptism is just getting wet.</p>
<p>Believing parents also have the option of baptizing their own family members.&nbsp;
See Ed for details.</p>
<p>Water baptism by immersion&#151;God's revealed will for every believer!</p>
