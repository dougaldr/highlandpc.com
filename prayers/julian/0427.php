<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 27
<br><span class=byln>by Ed Vasicek</p>

<p>God of Wonders,</p>
<p>You are the Lord Who Provides. When Abraham demonstrated 
that he was willing to offer his son, Isaac, to You, You sent an angel to stop 
him from doing so. In place of Isaac, You called his attention to a ram which was 
trapped in the thicket. You provided a sacrifice in place of Isaac. You have 
done the same for us: You went through with the sacrifice of Your Son to provide 
salvation for us! Just as the ram was a substitute for Isaac, so Jesus Christ 
is our substitute. We glorify You and praise Your Name, O Lord, for you have 
provided us with salvation. You also provide for our daily needs. As Paul put 
it, &quot;He who did not spare his own Son, but gave him up for us all—how will he 
not also, along with him, graciously give us all things?&quot; (<b>Romans 8:32</b>).</p>
<p>Thank You, O Lord, for true churches where the doctrine of 
salvation by grace through faith alone is taught, and where believers are led in 
Bible study and prayers directed to God alone, and congregations where most 
people have been born-again into Your family. We pray that false churches would 
see the errors of their ways and correct them.</p>
<p>Also let me remember that bad company corrupts good 
morals. Yet You want me to be in the world and not of it. Help me strike the 
right balance: to be a positive influence without being corrupted myself. Help 
me to act wisely and cautiously. Help me never confuse faith with presumption.</p>
<p>Please meet the following needs, requests and desires as 
You see fit: __________.</p>
<p>O, Lord, please be a help to the hurting, a balm in Gilead 
for the emotionally wounded and devastated. Strengthen the disappointed, 
encourage the demoralized, bind up the wounded particularly: __________.</p>
<p>I remember the nation of Hungary, now enjoying religious 
freedom. Thank You for the 2.7% of the nation who claim to be born-again 
evangelical believers. But, Lord, good leaders in the church are hard to come 
by and many Christians are hesitant to share their faith. On the positive 
side, thank You that the Jesus Film is a big seller in Hungary. We pray that 
the church would multiply fivefold over the next decade. May Your Spirit do a 
mighty work in this land. Make Hungary a pleasant surprise to the evangelical 
world, I ask. </p>
<p>Thank You, my Father, for Walk Thru The Bible, a 
ministry which they believe has “been called by Jesus Christ to partner with 
the local church to be a worldwide catalyst for discipleship...by transforming how 
people think about God, how pastors think about preaching, how churches think 
about their communities and how every day believers live out their faith at 
home, school and work.” They attempt to do so through “the creation and 
distribution of reproducible, cutting-edge and user-friendly biblical teaching, 
training and tools for lasting life change.” Guide them by Your Spirit, 
empower them and help them to make more and more progress in achieving these 
noble goals.</p>
<p>Now I commit myself to You today, in Jesus' Name, Amen.</p>

