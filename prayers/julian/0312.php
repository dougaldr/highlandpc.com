<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 12
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Master,</p>
<p>You are without spatial dimension, for You created space: 
height and depth are among those that are &quot;anything...in all creation&quot; 
(<a href="javascript:scripture('../../scripture/rom839.htm')" target=_self class=pop>Romans 8:39</a>). 
You fill all things, but You are not those things. You are separate 
from and distinct from Your creation. Your Son upholds all things by His 
power. We cannot conceive of a Personal God Who exists apart from space and 
creation. But then again, if we could fully conceive of You, You would be a 
small god. But You are a big God, beyond our full grasp. Yet because You have 
revealed Yourself in nature and more specifically in Your Word, there are many 
things about You that You have revealed 
(<a href="javascript:scripture('../../scripture/deut2929.htm')" target=_self class=pop>Deuteronomy 29:29</a>).</p>
<p>Lord, when Solomon built a Temple for Your Name 
(<a href="javascript:scripture('../../scripture/1kings55.htm')" target=_self class=pop>1 Kings 5:5</a>), 
He knew that You would not be confined to it but that You do not dwell in temples built by hands 
(<a href="javascript:scripture('../../scripture/acts1724.htm')" target=_self class=pop>Acts 17:24</a>). 
Indeed those of us who know You are the temples of the Holy Spirit 
(<a href="javascript:scripture('../../scripture/1cor619.htm')" target=_self class=pop>1 Corinthians 6:19</a>). 
We are not our own property!</p>
<p>As Your temple, help me to take reasonably good care of my 
body. On the one hand, I am not afraid to die, but on the other, I am in no 
hurry. On one hand, I do not want to give the impression that my focus is this 
life, but on the other, I know You expect me to be a good steward of that which 
You have entrusted me, including my body which is Yours and not my own. Help 
me strike the balance You have for me.</p>
<p>Please address the following needs: __________.</p>
<p>Lord, I am thankful for many things, three of which are: __________.
</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control me 
today.</p>
<p>Today I pray for the nation of Cuba, a land that has 
suffered long under the oppression of communism and dictatorships. We pray that 
the persecution and oppression would end. We thank You for the 4.6% who claim 
to be born-again evangelical Christians, and we pray that their numbers will 
triple over the next decade. Bless the Jesus movie as a mighty vehicle to 
harvest souls for the Kingdom of God.</p>
<p>I especially remember children today. So many live in 
poverty or have suffered physical or sexual abuse. Others are neglected or have 
been traumatized. Some are reared with little positive adult attention. Lord, 
You love the children. You said it would be better for a man to put a millstone 
around his neck and cast himself into the sea rather than causing a little one 
to stumble. Please protect the children, help them come to know You, and help 
those who have been abused or traumatized to recover and to not repeat the sins 
of the fathers. Lead them to good pastors and solid counselors who will help 
them deal with the effects of such abuse, that they might face it, grieve over 
it and move on, with Your help and compassion.</p>
<p>Lord, help me to get along with my neighbors. Help me to 
get to know many of them, but help me to be wise and discreet and not naively 
trust anyone who happens to live nearby.</p>
<p>Help me to truly be Your servant today, I pray, in Jesus' 
Name, Amen.</p>

