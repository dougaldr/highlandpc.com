<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 9
<br><span class=byln>by Ed Vasicek</p>

<p>God the Son,</p>
<p>In Scripture, we read that You are the Word and the Word became flesh. You, 
God the Son, are the Living Word. When You return to conquer and reign, You will 
also be known by that title 
(<a href="javascript:scripture('../../scripture/rev1913.htm')" target=_self class=pop>Revelation 19:13</a>). 
You communicate the character of God to us and when incarnated You 
helped us understand Who God is by Who You are. We marvel at the relationship 
between Father, Son and Holy Spirit.</p>
<p>Lord, thank You that You have an agenda for me to become holier. You are in 
the process of sanctifying me, helping me to grow in spiritual maturity. This 
requires my cooperation; we are working on this together, though we do not work 
in the same ways or equally. The point is that You want me to nurture my walk by 
being involved with things like spending time in Your Word, prayer, building 
friendships with other believers, learning from others in the Body and serving 
You. Your Spirit will then take these things and use them as part of the process 
of growing in You. Help me adopt Your agenda and surrender my own.</p>
<p>Lord, I have some specific requests to bring before You today. They are: __________.</p>
<p>Father, I have some personal health concerns including: __________. I also 
pray for the physical, mental, or spiritual health of others including: __________.</p>
<p>I pray for expectant mothers everywhere especially those I know. Lord, 
grant them safe deliveries and healthy babies. Help them to be loving mothers 
who put the best interests of their child or children first.</p>
<p>Father, I would like Your peace which passes all understanding to guard my 
heart and mind in Christ Jesus. I give You my stress, my cares, my worries, my 
heartaches and my fears. Help me realign my perspective and to remember that all 
I have to do is to do Your will for me. I do not have to please everybody, I do 
not have to carry the world's worries on my shoulders and I can do nothing to 
change the past. But I can go forward with You today.</p>
<p>Today I pray for the nation of Egypt, a nation familiar to every student of 
Scripture. Lord, thank You for the nearly 3% of the population who know You. We 
know the challenges are great as Islam pressures the authorities to become less 
and less tolerant of Christians. We pray that persecution would stop and that 
freedom would be guaranteed both on paper and in practice. Bless the Jesus film, 
the evangelical churches and the evangelical movement within the Coptic 
Orthodox Church. We pray that the number of Christians would double within this 
decade and that the people of Egypt would gain a new respect and tolerance for 
evangelical Christianity. We also pray that You would provide leaders for the 
church and training for lay leaders.</p>
<p>Lord, I pray for AIM. &quot;Africa Inland Mission (AIM) is a nondenominational 
Christian missionary sending agency serving primarily in Africa and the islands 
of the Indian Ocean. AIM was founded in 1895 and currently has more than 850 
missionaries.&quot; O God, keep these missionaries true to You. Protect them, 
provide for them, empower them, grant them harmony with one another and bless 
them with much fruit, I ask.</p>
<p>May I experience Your joy today, I ask in Jesus’ Name, Amen.</p>

