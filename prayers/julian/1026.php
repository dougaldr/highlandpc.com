<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 26
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are the Lord our sanctifier, the one Who sets us apart 
and makes us holy. At the point of conversion, when we were born-again and 
exercised faith in Jesus Christ, You gave us the position of holiness. You gave 
us the righteousness of Christ and set us apart for Yourself. Then You, O Holy 
Spirit, began working in me both to will and to do of your good pleasure. You 
help us to become more holy in practice. I praise You as the Lord our 
sanctifier.</p>
<p>I pray for my lost friends and family members including: __________.
 Father, please send Your Spirit to draw them to Yourself and grant 
them the repentance that leads to life.</p>
<p>I pray for Christians who are straying from their first 
love or compromising their walk with You. Rekindle the faith they once professed 
and help them fervently love the Savior once again. </p>
<p>Lord, when I read Your Word, it encourages me to, &quot;be 
steadfast, immovable, always abounding in the work of the Lord, knowing that 
[my] toil is not in vain in the Lord&quot; (<b>1 Corinthians 15:58</b>, altered). Lord, 
sometimes I am tempted to be anything but steadfast. I am tempted to vacillate, 
shift and renege upon my commitments to You. O Lord, deliver me from 
fickleness until I am completely faithful.</p>
<p>Lord, sometimes I am not immovable. I know that 
stubbornness involves the sin of choosing to be foolish and You do not want me 
to be stubborn. But conviction and the ability to stick to Your will—even when 
difficult—is not as easily done. O Lord, help me not compromise Your truth, 
yet to be reasonable in evaluating what that truth is.</p>
<p>And help me to abound in Your work. Help me jump in with 
both feet. Lord, it is easy to choose to dabble rather than to abound in Your 
work. If I abound, at times I will get tired, stressed and hurried. Once in a 
while, I may even ask myself, &quot;why did I get myself into this?&quot; But You want me 
to abound. Help me take this seriously. Yet, keep me from the lie that You 
desperately need me. You do not need me, but You do glory in using me.</p>
<p>Lord, I pray for the nation of Vietnam, with its over 80 
million people. O Lord, only 1.4% of the population claims to be born-again 
evangelical. The government severely persecutes all religious groups, 
especially Christians. As communism has fallen in much of the world, the 
communist government fears that if the people embraced religion their control 
over the nation would be weakened. Despite the heavy persecution, the church is 
growing and bearing testimony to the grace of God. O Lord, change the hearts of 
the nation's leaders. Continue to use Christian radio and the testimony of Your 
children to impact the nation for Christ. O Lord, may the percentage of 
believers double over the next five years and then double again and again. May 
Your Spirit hover over this land and grant many the repentance that leads to 
life, I ask.</p>
<p>Lord, help me to be proud of my Savior, Jesus Christ. In 
His Name I pray, Amen.</p>

