<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 13
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Abba, </p>
<p>Thank You that You fill all in all. Although You are 
separate from Your creation, You are present everywhere and although space is 
simply a created entity of Your pleasure, the entire universe cannot contain 
You, O Lord.</p>
<p>Thank You, Lord, that You created man in Your image. We 
humans are creative, personal, and spiritual. We have both a real physical 
component and a non-material component. You want us to love you with both 
components, with all we are: soul, spirit, mind, strength, and will.</p>
<p>Help me to resist the temptations and snares of the devil 
today. I claim the blood of Jesus Christ and the word of my testimony. I take 
up Your armor, O God.</p>
<p>Lord, help me to be a Christian who is known for being 
loving. May I look out for the interests of others, be considerate, concerned, 
and willing to lend encouragement and help. Help me not to simply think how 
people can benefit me, but also how I can benefit them.</p>
<p>I have a variety of personal needs in my life. I ask You to 
take away any guilt I feel, real guilt or false guilt. Help me believe that my 
repentance results in Your cleansing. You bury my sins in the depths of the 
sea, separating my iniquities from me as far as east is from the west.</p>
<p>Today I pray for the nation still known as Yugoslavia 
(Serbia and Montenegro). There are only a few thousand evangelical believers in 
this impoverished Eastern European nation of 10 million people. Father, please 
multiply the number of evangelical Christians in this land tenfold within the 
next decade. Help the church develop strong leaders and a good testimony.</p>
<p>Father, today I pray for Janz Team Ministries, an independent faith mission that has just over one 
hundred workers overseas and in North America. Thank You for their work in mass 
evangelism and other forms of evangelism, including “Teaching English as a 
Foreign Language.” Give them unity, safety, provision, and the empowerment of 
Your Spirit.</p>
<p>Today we pray for those who inspect meat, agriculture, and 
other areas related to food safety and production. Help them to do a good job, 
and may the Christians in this field boldly share Christ with their 
coworkers.&nbsp; May many respond by turning to Christ.</p>
<p>Now, Lord, may I 
live in a way pleasing to You today, I pray, in Jesus’ Name, Amen.</p>

