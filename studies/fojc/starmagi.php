<p>The 
subject of the Magi and the Star of Bethlehem are fascinating indeed. 
Unfortunately, traditions have accumulated and been written into songs that we 
assume to be true, but the Scriptures do not actually state what these songs 
claim.</p>
<p>Take 
the hymn, “We Three Kings of Orient.” Although I love this carol, we do not 
know how many wise men came to see Jesus. The earliest traditions claim twelve 
Magi made the trip. They may have had a large entourage with them. And were 
the Magi really kings? We can’t say, but it is probable that they were 
religious leaders and perhaps ambassadors as well. The idea that they were 
kings comes from the prophecy of 
<a href="javascript:scripture('../../scripture/is606.htm')" target=_self class=pop>Isaiah 60:6</a>,
but this prophecy actually refers to the Millennial reign of Christ (though it could be a 
double-fulfillment prophecy with a near non-literal fulfillment and a far, 
literal one). How did the tradition evolve that there were three kings? They 
presented three gifts: gold, frankincense, and myrrh.</p>
<p>
Another tradition that has been often accepted as Gospel truth is that the star 
moved all the way from Persia (or wherever) and guided the wisemen to where 
Jesus was. I can hear Mario Lanza singing that verse from “The First Noel,” 
which says, “…and there it (the star) did both stop and stay, right over the 
place where Jesus lay…” I love the carol and admit Mario does it justice, but 
the text actually indicates that the Magi only admitted to seeing the star 
twice: once in the east, and once in Bethlehem. &nbsp;They apparently lost sight of 
it until they came to Bethlehem, and when they saw the star it was in its zenith 
position right over Bethlehem. So Mario was half right, but the star did not 
guide them all the way from the east or they would not have needed to inquire of 
Herod.</p>
<p>But 
how did the Magi know it was His (the Messiah’s) star? Henry Morris has written 
the best booklet on this subject, entitled “When They Saw the Star.” Morris 
connects the Magi to their founder, the compromising prophet Balaam. He is the 
one who prophesied, "I see him, but not now; I behold him, but not near. A star 
will come out of Jacob; a scepter will arise out of Israel...." (<b>Numbers 24:17</b>).</p>
<p>
Morris goes on to postulate, as have others, that the original signs of the 
Zodiac painted a picture of the Gospel. The virgin (Virgo) would give birth to 
the Messiah (pictured by Leo the Lion) who would crush the serpent’s head (the 
promise of 
<a href="javascript:scripture('../../scripture/gen31516.htm')" target=_self class=pop>Genesis 3:15-16</a>).
Since the Magi may have been influenced by the 
teachings of Daniel, they probably knew the rough era in which to expect the 
Messiah, based upon <b>Daniel chapter 9</b>. So it may have been a bright star 
(probably a super nova) that flared up in the constellation of Leo that made the 
connection. They knew the Messiah would be King of the Jews, but they did not 
know exactly where in Israel He would be born, which is why they inquired of 
Herod.</p>
<p>
Having said this, I also need to state that this thinking does not endorse the 
modern practice of astrology, which is basically personal fortune telling, an 
evil corruption of the original meaning of the constellations. The Bible 
forbids all forms for fortune telling 
(<a href="javascript:scripture('../../scripture/deut2929.htm')" target=_self class=pop>Deuteronomy 29:29</a>, 
<a href="javascript:scripture('../../scripture/deut1811.htm')" target=_self class=pop>Deuteronomy 18:11</a>).</p>
