<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 11
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious Lord,</p>
<p>Your Word declares in no uncertain terms that Your love is everlasting. Your 
love never waivers or vacillates and You Yourself do not grow weary or tired. 
You will shower me with Your love forever and ever and even now You do so. Your 
love is without limit; it reaches to the heavens. You do not need to release 
Your love in small portions to conserve it; it is infinite. Though I have begun 
to experience the wonders of that love, I will only see it in full force when I 
am with You in glory. But when I doubt Your love, all I have to do is look at 
the cross. You have proven Your love for me.</p>
<p>In Your love, Father, You have sent Your Spirit to live within me. He has 
written Your Law upon my heart in that He has given me a new nature that desires 
You. He has intensified my conscience. O God, may I become holier as I allow 
Your Spirit within me to control me and empower me.</p>
<p>Lord, today’s date brings back memories of the awful 
terrorist bombings which occurred in 2001. Father, I pray that You would wipe 
out terrorism from this planet. May those who have grievances go about trying 
to promote desired changes through legal and ethical means. Lord, I pray that 
terrorism would suffer an awful defeat.</p>
<p>I pray for my lost friends and family members including: __________. Father,
please send Your Spirit to draw them to Yourself and grant&nbsp;them the 
repentance that leads to life.</p>
<p>Dear God, I pray that You would help me to discipline 
myself to be tactful in relating to others today. Help me to speak kindly, to 
write thoughtfully and to conduct myself with consideration to others. May my 
love for You and my love for others—rather than peer pressure—motivate me to 
be considerate.</p>
<p>Father, today I remember the Orissa State of India with its 
38 million people. O Father, perhaps 1% of the population in this region claims 
to be born-again evangelicals. And Lord, persecution from those espousing 
Hinduism has been serious here. Anti-conversion laws make it illegal to convert 
from one religion to another. Yet, Lord, despite all this, the church has 
grown! Over half of the people cannot read, but thank You that Christian radio 
is reaching many. O Lord, protect the believers, empower them and multiply 
Your church in the Orissa State. May the number of believers double every five 
years for two decades, I ask. Do a mighty work here!</p>
<p>Lord, thank You for the ministry of Hope Builders 
International whose &quot;purpose is to help build up the body of Christ through 
local discipleship, national partnerships and international support of 
indigenous mission activity. [Their] intention overseas is to introduce Western 
believers to those ministries that are actively furthering the Kingdom of God 
around the world in lands of poverty and persecution and to facilitate a working 
relationship between the two...in Asia, the Middle East and the Turkish speaking 
world.&quot;</p>
<p>Father, use this ministry. Give them vast amounts of 
wisdom, provide for the work, protect those who do the work, grant them harmony 
among themselves and empower them by Your Spirit, I ask.</p>
<p>Help me walk the walk today, in Jesus’ Name, Amen.</p>

