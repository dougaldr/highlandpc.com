<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 5
<br><span class=byln>by Ed Vasicek</p>

<p>L<span class="sm">ORD</span>,</p>
<p>Your Shekinah, Your glory, was represented as a pillar 
of cloud by day and a pillar of fire by night. This glory lead the children of 
Israel throughout 40 years of wandering in the wilderness. Your glory can be 
with us even in bleak and monotonous times, even during times of gloom we 
ourselves created, as did the children of Israel.</p>
<p>Your Shekinah dwelt above the Ark of the Covenant, for a 
time. When Jesus Christ came, He was full of Your glory. When the soldiers came 
to arrest Jesus in the Garden of Gethsemane, they asked for Jesus. When He 
literally said, &quot;I am,&quot; (which is what Your Name, Yahweh, means), 
perhaps Your Shekinah was revealed for a second because, &quot;When Jesus said, 'I am he,' they 
drew back and fell to the ground&quot; (<b>John 18:6</b>).</p>
<p>Father, You spoke several times out of a cloud of glory, 
saying, &quot;This is my Son, whom I love. Listen to him!&quot; (<b>Mark 9:7</b>).</p>
<p>And now, Lord, we reflect that glory to others. As we 
experience Your glory in our lives through fellowship with You, Your glory 
transforms us, &quot;And we, who with unveiled faces all reflect the Lord's glory, 
are being transformed into his likeness with ever-increasing glory, which comes 
from the Lord, who is the Spirit.&quot;</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God</i>).</p>
<p>I pray for parents of children who have gone astray. 
Whether in the realm of premarital or extramarital sex, drugs, alcohol, 
gambling, crime, violence, bitterness toward parents, abandoning the faith or in 
any other way, it hurts like nothing else can.</p>
<p>Today I intercede for the African nation of Tanzania. 
Lord, it is great to see what You have done through Your people here. Over 17% 
of the population claim to be born-again evangelicals, praise Your Name. Yet, 
Lord, the believers are not spread evenly throughout the nation. The coastal 
and southern regions are largely unreached. Thank You for the 10 translation 
projects underway, but there are about 45 people groups without a Bible in their 
native tongue. Please bless and prosper the work there. May Tanzania become a 
lighthouse for the region, I ask.</p>
<p>Father, bless the Fellowship of Independent Missions 
(FIM). Thank You that this mission agency ministers on every continent and is 
noted for its high standards. Lord, empower the missionaries, protect and 
provide for them and grant them harmony among themselves. Lord, may You use 
them to reap a mighty harvest, I ask.</p>
<p>Today I pray for those who work as customer service 
representatives. Help them to work honestly, kindly and efficiently. May 
those who know Christ use the open doors You provide to share Christ with 
others, I ask.</p>
<p>Help me to remember that whatever I do today, I can do it 
to Your glory. I pray these things in Jesus' Name, Amen.</p>

