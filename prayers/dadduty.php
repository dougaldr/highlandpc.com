<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>When Dad Has Been Called Up from the Reserves to Active Duty in the Military
<br><span class=byln>by Ed Vasicek</span></p>

<p>Lord, we thank You that You are Sovereign. Sometimes it is easy to
believe that You were Sovereign of the past, but difficult to believe that You
are in control of the present or future. Yet we know that You have not
lost Your grip on the direction of history, nor have you lost Your grip on
us. Thank You that nothing can take us out of your mighty hand! Help
us to believe that and depend upon Your faithfulness.</p>
<p>Father, we respect those who rise to the defense of our nation. We are
proud of our family leader, and his willingness to serve his country. But
it broke his heart, and it breaks ours, to be separated like this. At
times, it doesn't seem real. These things happened in years gone by, but
how can they happen today?</p>
<p>You, O Lord, are Lord of time. Nothing takes You by surprise. We
have fears and heartaches, worries and stresses, emptiness and impatience, and
yet You love to hear about them. You gladly receive our burdens and infuse
us with Your great peace. O God, we need your peace. We need to
sense Your arms around us and Your mighty outstretched hand of protection.&nbsp;
Protect us, and protect our dad and husband. Send angels to watch him and
us. Guide the political and military leaders. Father, work in this
for good.</p>
<p>Since we do call You Father, may you show Yourself as Abba Father to us.&nbsp;
Fill the void in our family and in our lives. Use your body, the Church,
to encourage us. And please, Father in heaven, help our loved one to walk
with You, to be kept safe, and to return to us in Your time.</p>
<p>We know you love us, Lord, not because life is easy, for it is not. We
know You love us because You sent Your son to die for us. Your Word says,
&quot;But God demonstrated His love for us, in that, while we were yet sinners,
Christ died for us&quot; (<b>Romans 5:8</b>). So we know You love us.&nbsp;
Yet we fear. We fret. We worry. We feel the ache of missing
one so close to us and dear to our hearts. But God, You have helped our
fathers, and we trust that You will help us as well. Your Word is sure,
and we cling to You.</p>
<p>Thank you, Lord, for being there in our time of need.</p>
<p>We pray in Jesus' Name, Amen.

