<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 10
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are an active God. You are not passive. You are 
energetic, lively, vigorous and dynamic. You do not sit idly by wondering what 
will happen, nor are You some impersonal force that does neither process nor 
direct what happens. You participate with Your creation and You providentially 
steer the course of history. You are also active in my life working with me 
individually. You are active in the lives of all who have a saving relationship 
with Jesus Christ.</p>
<p>Lord, even though You are the sovereign God, You hold us 
accountable and responsible for our behavior. You made us moral beings. Even 
though we are predisposed to sin and be selfish, we still choose to be this way 
in every instance. Indeed, You are the objective standard by which all right and 
wrong is measured and that standard is revealed in Your Word. Help us not to 
blame others for the wrongs we do, but to take responsibility for our own 
wrongs.</p>
<p>O God, I pray for children who are Christians but who have 
a parent or parents who are not. O Lord, please be the advocate for such 
youngsters. Give them unusual wisdom and protection, I pray. Bring people into 
their lives that can nurture them in their Christian walk, I ask.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me of them and You will cleanse me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now: __________.</p>
<p>Lord, reconciliation within families is a painful issue. 
Please bring together members of my family—or of a family I know—who are at odds 
with other family members. We ask that the offenders would embrace repentance, 
apology, restitution, the embracing of responsibility and reasonability, or the 
reestablishment of credibility, whatever is needed to mend the fences. And we 
pray that those offended would be gracious yet not naïve. We know some 
situations can never be fixed, but we pray that repairable relationships would 
experience reconciliation.</p>
<p>Lord, I pray for the South American nation of Uruguay. I 
thank You for the 4.5% of the population who claim to be born-again 
evangelicals. I praise You for each one. But, Lord, the work has not gone 
particularly well in this nation. As Uruguay has become more secular, many have 
lost interest in spiritual things or turned to pagan superstitions. Thank You, 
Lord, that some evangelical church groups are seeing rising growth rates. O 
Lord, excel this, I plead. May the percentage of believers double over this 
next decade. Do a might work in Uruguay!</p>
<p>Lord, thank You for Mission: East Asian National Support, 
an American based &quot;Christian organization dedicated to sharing the Gospel and 
helping the needy by partnering with churches, mission groups, missionaries in 
East Asia and East Asians worldwide.... [Their] vision is to nurture and support at 
least one Filipino missionary in every country in the world by partnering with 
them in their work to spread the Gospel of the Kingdom of God.&quot; O God, provide, 
protect and empower this ministry. Grant harmony to the leaders and Your 
direction, I pray.</p>
<p>Now help me follow Your direction for my life today, I 
request in Jesus’ Name, Amen.</p>

