<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>Homeschooling Graduation Ceremony
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God and Father,</p>
<p>We honor You today as we honor these graduates Who fear Your Name and Who
love you dearly. We thank You that Your Son was called Rabbi,
teacher. He taught us to make disciples by teaching the nations.</p>
<p>Your word also instructs parents to teach their children. Thank you,
Lord, that we live in a land where we have the freedom to implement this command
in the fullest and most extensive sense. Thank you, Lord, for the parents
who have given of themselves to instruct their children in both matters of
heaven and matters of earth.</p>
<p>The young people graduating today have worked hard. The task has not
always been easy, but, by your grace, they have studied and read and written and
learned. We believe that they have not only learned facts, but we also
believe that they have learned how to think. Not only have they learned
how to think, but we trust they have learned how to think using your value
system and the priorities you announce in Your Word. We believe they are
ready to move forward into the next phase of your plan for their lives.</p>
<p>At this point, Lord, there is a fork in the road. Some will head toward
college, others toward a military career, others toward a trade or seek
employment; others are yet undecided. This fork has many prongs, just as
You, Sovereign Lord, have unique plans for each life. Thank You that You
are Lord of the Universe AND Lord of the Individual.</p>
<p>So we release each young man, we release each young woman—to Your
direction. For some, your will may appear hidden, just as it was for
Abraham when you simply told him to leave his country for one God would
eventually show him. For others, your will has been pinpointed, just as
the Savior told His disciples that He came to give His life a ransom for
many. Whether Your will is clear or not, we pray that each graduate's
priority would be to seek Your direction and follow it wholeheartedly.</p>
<p>We ask for Your blessing upon each graduate in attendance this afternoon,
because Your Word tells us that &quot;unless the L<font size="1">ORD</font>
builds the house, they labor in vain who build it.&quot; Send angels to
protect them, Your Spirit to direct them, and illuminate Your Word to guide
them. We pray in the Name of Jesus Christ our Lord, Amen.

