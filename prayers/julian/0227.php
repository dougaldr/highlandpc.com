<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 27
<br><span class=byln>by Ed Vasicek</p>

<p>Lord God of Heaven and Earth,</p>
<p>You are my Rock. You do not change, You are a sure 
foundation, and we are wise to build our lives upon You. We know what happens 
to those who build on the sand when a storm comes along. And life holds many 
storms. Just as the rock provided water for the children of Israel as they 
journeyed in the wilderness, so You too not only provide us with a sure 
foundation but You also refresh our very souls.</p>
<p>Please address the following needs: __________.</p>
<p>Guide our president, senators, representatives, judges, and 
state and local elected or appointed leaders. Help them know You, follow You, 
and do Your will. May Your children—those born of your Spirit—be able to live 
peacefully in our land without being pressured to compromise Your demands.</p>
<p>Lord, thank You that You have delivered Your children from 
the wrath that will be experienced by those who do not know Christ. We need not 
fear the fires of hell or eternity apart from You. Instead, we will be feasting 
with You, enjoying Your riches, and fellowship with our Savior and the 
multitudes that have trusted Him.</p>
<p>Lord, I want to be a considerate person. Help me to 
frequently put myself in the shoes of other people, and to do to them as I would 
have them do to me.</p>
<p>Help me with my friendships, Lord. It takes time and 
effort to maintain and nurture friendships, and sometimes I am hesitant to take 
initiative. I want to be invited, but not to invite. Help me not to take a 
passive role, Lord.</p>
<p>Today I pray for the nation of Comoro Islands. These 
islands off the coast of Africa are heavily Muslim; less than one-tenth of one 
percent claim to be born-again Christians. It is illegal to distribute 
Christian literature, and Christians are ostracized from society. Thank You for 
the Christian medical and veterinary workers who bear testimony to the love of 
Christ. We pray that the number of believers would increase tenfold over the 
next 5 years. May Your Spirit work mightily to overcome the darkness of this 
land. Help the Christians to stand strongly, firmly and wisely.</p>
<p>I remember the Rio Grande Bible Institute as they train 
workers for ministry. Help them particularly impact the Spanish-speaking world 
by training leaders, as well as through their radio and literature ministries. 
May Your Spirit guide the leadership and student body.</p>
<p>I pray for those who work in accounting and bookkeeping. I 
know these are important fields. Help them to do their work as for You, and may 
those who know Christ share their faith and show their faith by faithful, 
dependable work.</p>
<p>Work in me to will 
and to do of Your good pleasure, I pray, in Jesus' Name, Amen.</p>

