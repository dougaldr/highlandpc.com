<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 15
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>Thank You that You always stand ready to forgive. You are serious about 
forgiving. The only condition for me as a believer is confession 
(admission and repentance) for my sin. For those who do not know You, it is 
repentance toward You and faith in the Person and Work of our Lord Jesus Christ. 
When I trusted Christ, all my sins were forgiven. When I sin as a believer, sin 
creates relational problems with You, a tension in our relationship. Even though 
I am secure in You, sin blocks our fellowship. When I confess my wrong, You 
immediately forgive me and cleanse me from all unrighteousness 
(<a href="javascript:scripture('../../scripture/1john19.htm')" target=_self class=pop>1 John 1:9</a>). 
We forgive others following Your pattern.</p>
<p>It is Your nature, O Lord, to be a forgiving and gracious God. And even 
though Your forgiveness is certainly conditioned upon our repentance, You never 
forgive grudgingly.</p>
<p>Lord, I have some requests to bring before Your throne. 
They are: __________.</p>
<p>First John says that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins, and You will purify me from all&nbsp;unrighteousness (<b>1 John 
1:9</b>). I confess the sins I am aware of now, and I ask Your Spirit to search my 
heart (<i>Pause to admit them to God</i>).</p>
<p>Lord, I thank You for the assurance of salvation I have by my faith in Jesus 
Christ and His presence in my life. Thank You that, according to
<a href="javascript:scripture('../../scripture/1john513.htm')" target=_self class=pop>1 John 5:13</a>, 
I know that I have eternal life. I do not need to merely hope 
or guess, I can know. Thank You that the blessed Holy Spirit bears witness to my 
spirit, that I am a child of God.</p>
<p>Lord, help me to be a gracious person. Help me not react to insults or 
slights or delays or mistakes with uncontrolled anger, a demanding attitude, or 
rudeness. Instead, help me to delay my anger and gain my composure and consider 
the effect of my responses on others.</p>
<p>Today I pray for the African nation of Tunisia with its 10 million people. 
Lord, the percentage of evangelical believers in this nation is zero! There are 
about 200 people who know You, and that’s about it. Missionary efforts have 
produced little fruit, but, Lord, many of these were won to Christ in recent 
years. Thank You that this nation is not as hostile or intolerant as are many 
Islamic countries; Lord, break through by Your Spirit. May the number of 
believers double every year for the next decade, I ask. Give people dreams and 
prime them to come to know You; grant many the repentance that leads to life!</p>
<p>O Lord, thank You for the efforts of India Gospel Outreach 
as it, &quot;trains and sends Indian evangelists and church planters to all peoples 
of India...starts new training centers throughout India to help local Christians 
reach their own people with the gospel...aids poor people of India with relief, 
education and development in the name of Jesus...[and] enlists intercessors to 
pray for revival and spiritual awakening in India.&quot; Empower, provide, protect 
and bless this ministry, I ask. Grant the organization harmony amongst its 
staff, I pray.</p>
<p>Help me to get along well with others today, I pray in 
Jesus’ Name, Amen.</p>

