<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 7
<br><span class=byln>by Ed Vasicek</p>

<p>Lord God,</p>
<p>You are my Counselor. Of God the Son, it is said that His 
Name would be called, &quot;Counselor, the Mighty God…&quot; You are my guide, my 
confidant, the source of wisdom and direction. But Lord, sometimes I forget to 
consult with You. Sometimes, when I ought to be checking in with You for 
direction, I run ahead as though I were on my own. Help me to remember that You 
indeed are my Counselor.</p>
<p>Help me to resist temptation and the lures of the Satan 
today. In a sense, Satan and his kingdom are anti-counselors who would lead me 
to disgrace Your Name. Help me not to be ignorant of his schemes. I choose 
today to reaffirm my allegiance to Jesus Christ, and I put on His armor.</p>
<p>[For the married]</p>
<blockquote>Lord, marriage is Your institution. I cannot fathom how 
much marriage has improved life on planet Earth. Lord, guard my marriage, and 
help me to guard it. Keep us faithful to one another, and help me not to 
neglect my spouse, but rather to make pleasing You and my spouse life's top two 
priorities 
(<a href="javascript:scripture('../../scripture/1cor73334.htm')" target=_self class=pop>1 Corinthians 7:33-34</a>). 
Bind Satan, and help us both to walk in the 
Spirit that we might put to death the deeds of the flesh. Help us to respect 
one another, to talk to one another, to compromise with one another, and to like 
one another. Help us understand and accept one another. Help us renew our 
commitment to love one another on a daily basis. Help me address conflict, not 
avoid it; help me to argue fairly. Never let an attitude of contempt or sarcasm 
master me.</blockquote>
<p>[For the single, divorced, separated, or widowed]</p>
<blockquote>Lord, life can be lonely. I thank You for caring friends 
and family, but Lord, sometimes it is tough to stand alone. Help me to trust 
Your Sovereign hand, and lead me in the way I should go. Help me to participate 
in church and community. If others seem to leave me out because I am single, 
help me to become tough to that. Help me dive in and include myself. Help me 
remember that being left out is often not intentional. Many couples have no 
idea I want to be included. They think I would be uncomfortable with them. 
Help me express to them that I want to be included. Keep me from taking the 
stance of a victim. Help me to find good friends to share activities with, and 
help me remember that You are especially concerned about those who are alone.</blockquote>
<p>I thank You that all Scripture is inspired and profitable. 
Thank You that the Old Testament is just as inspired as the New. Thank You that 
the words of Paul are just as inspired as the words of Jesus. Thank You that I 
can depend upon all Your Word, for it is breathed of You in a plenary  
way.</p>
<p>In like 
manner, Lord, I pray that I would give You even control of my life. I tend to 
surrender bits and pieces of my life to You, but You want all of me. And even 
when I do surrender certain areas of my life, I often take those areas back. 
Rescue me, I pray, from a divided heart.</p>
<p>Today I pray 
for both Pitcairn Island and Tokelau Islands of the South Pacific. On Tokelau, 
less than 4% of the population (which is only 1,500 people) claims to be born 
again. Work in the lives of the believers on both islands, and may the number 
of born-again believers double, I pray.</p>
<p>I remember 
TEAM (The Evangelical Alliance Mission), one of the most respected, oldest, and 
largest evangelical missionary organizations. Thank You for their focus:
<blockquote>
&quot;Our work is 
focused on the church from beginning to end. We engage existing churches in 
cooperative efforts to establish new churches worldwide and then encourage those 
churches to establish even more new churches. We see the day when the people of 
God from every tribe, tongue, people, and nation will be gathered around the 
throne of heaven, worshipping Him.&quot; We say, &quot;Yes Lord.&quot; Thank You for the 
hundreds of missionaries who serve literally around the world in full time 
vocational ministry. Help them to work as a team, in harmony, and to be led by 
Your Spirit. Provide for them and their families, use them, and may they be 
effective in building the Kingdom of God.</p>
</blockquote>
<p>Today I pray for cost estimators. Help them to be 
accurate, honest, and fair. We pray that many in these fields would come to 
know Jesus, and that those who know Him would share the Gospel with others.</p>
<p>Now fill me with Your Spirit that I might glorify You 
today, I pray, in Jesus' Name, Amen.</p>

