<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 4
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Your Word compares You to a Potter and us to the clay. Just as the potter has 
the &quot;right to make out the same lump of clay some pottery for noble purposes and 
some for common use&quot; (<b>Romans 9:21</b>), so we acknowledge that we have no 
grounds to talk back to you 
(<a href="javascript:scripture('../../scripture/rom920.htm')" target=_self class=pop>Romans 9:20</a>). 
We may not like where Your Sovereign hand has led us and our lot in 
life might be other than we would wish, but ultimately our main choice is 
whether to take Your hand or to kick against the thorns in protest. We choose to 
walk with You. We thank You that You understand exactly what You are doing even 
though things are sometimes not as we think they should be. We confess that You 
are doing a much better job at running the universe than we could. So we submit 
ourselves to You, the potter. We remember that we are clay in Your hands.</p>
<p>Father, help me to keep my spiritual eyes open and to join You where You are 
working. Perhaps a friend or associate is going through trials and You are using 
this to draw him or her to Yourself. Help me be alert and to minister 
accordingly.</p>
<p>Lord, thank You for blessing me so abundantly. Today I am thankful for: __________.</p>
<p>Father, I release the control of my life to You so that the Holy Spirit can 
control me. I remember that it is &quot;not by might, nor by power, but by Your 
Spirit&quot; that I can overcome (<b>Zechariah 4:6</b>).</p>
<p>Lord, help my church be harmonious. May the leaders be firm when it comes to 
the clear teachings of Scripture and gracious about the not-so-clear matters. 
Keep antagonists, troublemakers and those who resent authority away from our 
congregation. Help my church to be effective in evangelism, discipleship, loving 
one another and world missions.</p>
<p>O Lord, I pray for the Asian country of Yemen, a nation on 
the list of the world’s least evangelized countries. Followers of Islam make up 
99.94 percent of the population. There are less than a thousand evangelical believers in 
this nation, a land where it is illegal for any non-Muslim to witness and 
illegal for Muslims to become Christians. Yet, Lord, You are beginning to work 
through radio, the Jesus film and tactful sharing. O Father, draw many to 
Yourself in the days ahead. May 5% of the population come to know Jesus over 
the next decade, I ask.</p>
<p>My Lord, I praise You for the work of Child Evangelism 
Fellowship: &quot;For almost 70 years God has been using Child Evangelism Fellowship 
to bring the love of Jesus Christ to children around the world...with workers in 
more than 150 countries around the world CEF has personal, individual ministry 
with more than 6 million children each year.&quot; O Lord, bless this vital ministry, 
an outreach that occurs even in my home town.</p>
<p>Help me to display the love for righteousness the Savior 
exemplified, I ask in His Name, Amen.</p>

