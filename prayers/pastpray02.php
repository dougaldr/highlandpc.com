<p id="pgt"><span class="pgh">Sunday Morning Pastoral Prayers:</span>
<br>Prayer #2
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Heavenly Father,</p>
<p>We appreciate Your love and watch care over Your creation. 
We know that all things were created by You, and we know that You hold all 
things together. We thank You that You share Your common grace with all. You 
even bless those who completely reject You by providing them with air, food, 
water, and a measure of health. We thank You for Your Providential care for all 
mankind, and we remember that You steer the course of history.</p>
<p>We appreciate You, our God, as Father, Son, and Holy 
Spirit. We are grateful that the Holy Spirit has regenerated us; He caused us 
to be born again and brought us from spiritual death to spiritual life. We are 
grateful that He indwells us, has baptized us into the Body of Christ, endowed 
us with spiritual gifts, and, as we permit Him, He fills us to overflowing.</p>
<p>We pray, O Lord, for our Supreme Court Justices. We know 
these individuals wield tremendous amounts of power. Help them to interpret our 
laws wisely and honestly. Help them put aside their own prejudices and seek the 
original intent of our constitution and its amendments. Give them clarity of 
mind and a heart that senses its accountability to a higher law, Your law.</p>
<p>Almighty God, You know our church family has many needs. 
Some are suffering from physical, psychological, or spiritual downturns. Others 
have relational or financial difficulties. Some are disappointed, others are 
grieving, while still others are searching for that deeper life. Father, You 
know each need and burden, and we commit them to Your will and Your care.</p>
<p>Abba, Father, we remember our missionaries, those who know 
You and have been called by You to carry the Gospel to regions beyond. This 
morning we remember those who minister is Asia, an area dominated by 
non-Christian religions. We pray that governments like China, North Korea, and 
Viet Nam would recognize the religious rights of their citizens. We pray that 
multitudes in nations like those we just mentioned, as well as nations like 
India, Bangladesh, and Japan would turn to the Lord Jesus in droves. Help those 
who minister in these highly populated but under-evangelized parts of the 
world. Protect them, direct them, enlighten them, and give them fruit for their 
labors.</p>
<p>At home, Lord, we have much work to do. Today we remember 
our Sunday School ministry. We pray that our Sunday School ministry would 
blossom and make a big difference in deepening and grounding believers. Bless 
our teachers who give of their time and energy. May Your Spirit prosper their 
instruction. May they seek Your guidance as they prepare. Convince Your 
children of the importance of this ministry.</p>
<p>As individuals, each one of us need to grow in so many 
ways. This week, we pray that you would help us to love others better. Help 
this growth in love to begin at home, spread to work and school and the 
neighborhood. May others see Christ in us because they see the love we have.</p>
<p>And so Lord we commit these requests to You. </p>
<p>Thank You Lord for hearing and changing things because we 
pray in Jesus’ Name, Amen.</p>

