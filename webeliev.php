<p id="pgt">We Believe</p>

<p>Highland Park Church is a non-denominational, evangelical church. We try to 
be true to the Scriptures (Bible-believing) while at the same time allowing 
freedom in extra-Scriptural matters, steering away from legalism. We are
not high pressured. We do not press for contributions, and we are not particular about 
styles of dress.</p>
<p><b>We believe in the fundamentals of the faith:</b></p>

<ul>
  <li>the inerrancy/inspiration of the Bible in the original manuscripts
  <li>the birth of Christ while Mary was yet a virgin
  <li>the dual nature of Christ: He is both God and man
  <li>the death of Jesus on the cross to pay the penalty for our sins so God 
  could forgive us
  <li>His literal, physical resurrection from the dead
  <li>His Second coming
  <li>the Trinity: one God in three Persons: Father, Son and Holy Spirit
  <li>salvation by grace through faith and repentance, not by good works or 
  sacraments
  <li>a literal heaven and hell.</li>
</ul>
<p>See also our <a href="/aboutus/const.php#sctnii" class="reg"><b>detailed</b> beliefs</a>
(Constitution, Section II).</p>

