<p id="pgt"><span class="pgh">Connect:</span>
<br>The Camp Emmanuel Association, Inc.</p>

<p>Camp Emmanuel was founded in 1946 under the leadership of Vern Stuber and the
Emmanuel Fellowship Church, and up until 1999, owned its own campgrounds near
Bunker Hill, Indiana. Camp Emmanuel is currently being held at Yellow Creek
Lake Ministries Campground in Claypool, Indiana.</p>

<p>The primary purpose of Camp Emmanuel is &quot;that the campers will be
strengthened in their faith in the Lord Jesus Christ and their knowledge of His
Word, the Bible, and will be encouraged by the leaders, counselors, and other
campers to live faithfully for Jesus Christ.&quot;</p>
<p>Of course, we frequently share the Gospel so that any campers who do not yet
know the Lord are encouraged to establish a relationship with Him. Kids at Camp
Emmanuel receive much Bible instruction, and we certainly want the campers to
have a good time as well.</p>

<p>Camp Emmanuel is a conservative, Bible-oriented camp that holds to the
fundamentals of the evangelical faith. Instructors teach positions similar to
those of the Moody Bible Institute or Grace Seminary, such as eternal security
and salvation by grace.</p>

<p>Camp Emmanuel is financed through the gifts of individuals and churches. The
official churches who provide representatives to the camp council are all located 
in Indiana. They include the following:</p>
   <ul>
     <li>Bourbon Bible Church (Bourbon),
     <li>First Community Church (Matthews),
     <li>Highland Park Church (Kokomo),
     <li>Lockport Church (Burnettsville),
     <li>Riverview Community Church (Tippecanoe),
     <li>Tippecanoe Community Church (Tippecanoe),
     <li>Whippoorwill Community Church (Rochester).
     </ul>
<p>The camp churches take turns providing cooks and food for the campers as well
as camp supplies. In addition, they contribute toward camp costs, making Camp
Emmanuel free to the campers.</p>

<p>Participating in Camp Emmanuel is a privilege that must be earned. In order
to qualify to attend Camp Emmanuel the camper must:&nbsp;</p>
   <ul>
     <li>have attended church 6 of the 8 Sundays previous to camp;
     <li>have memorized the Roman's Road Bible verses
     (<a href="javascript:scripture('../scripture/rom310.htm')" target=_self class=pop>Romans 3:10</a>,
     <a href="javascript:scripture('../scripture/rom323.htm')" target=_self class=pop>3:23</a>,
     <a href="javascript:scripture('../scripture/rom623.htm')" target=_self class=pop>6:23</a>,
     <a href="javascript:scripture('../scripture/rom58.htm')" target=_self class=pop>5:8</a>,
     <a href="javascript:scripture('../scripture/rom109.htm')" target=_self class=pop>10:9</a>, and
     <a href="javascript:scripture('../scripture/rom1013.htm')" target=_self class=pop>10:13</a>);
     <li>have completed the camp registration application, with a recommendation
signed by one of the pastors of the supporting churches. Included in the
registration is an agreement to follow camp regulations. The application MUST be
mailed in before the deadline.</ul>
<p>Campers who are involved in another church besides one of the official camp
churches may make arrangements by contacting a pastor of one of the participating churches.</p>
