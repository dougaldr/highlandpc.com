<p>
<b>Sermonette</b></p>
<p>
Paul wrote to the Ephesians in Ephesians 5:25, 
&quot;Husbands, love your wives just as Christ loved the church and gave himself up 
for her.&quot; Although men are to take the lead, both husband and wife are called 
upon to exercise strong love within a marriage and a family. From this simple 
text, we can make a few deductions.</p>
<ol type=I><li><b>
		Christ's Love for Us Is the Prototype for Love in Marriage.</b></p>
<ol type=A><li>The Son of God became a man, lived a sinless 
life, and freely chose to offer His life as a payment for our sins so a Holy God 
could forgive us.</p>
<li>Why? Because of love. Love is sacrificial by 
nature.</p>
<li>
Marital love is not always 
sacrificial: We give and receive; we surrender some of our rights 
and spouse surrenders some of his or her rights.</p>
<li>But the kind of love God demonstrated is on 
standby. Sometimes we give without receiving in return. Sometimes we receive 
without giving in return.</p></ol>
<p><li><b>Love Is a Choice to Take the Initiative.</b>
<ol type=A><li>Jesus chose to give Himself as an atoning 
sacrifice. The only thing we could contribute toward our salvation is the sin 
from which God saves us.
<li>Love is not passive but involves sacrifice 
and risk. Jesus did not feel like dying on the cross. The night before He died 
in Gethsemane, He struggled in prayer. &quot;Let this cup….&quot;</p>
	</p>
	<li>In marriage, that might mean being the first 
to apologize or the only one to apologize. When a conflict makes you and 
your spouse distant, it means being the first to swallow our pride and get over 
who is right and who is wrong.</ol></ol>
	<p>So, John and Jane, when you are confused as to 
what love is, remember the pricetag God paid because of his great love for you, 
and find strength there to love one another through thick and thin, through 
times both good and bad. </p>
