<p id="pgt"><span class="pgh">Sunday Morning Pastoral Prayers:</span>
<br>Prayer #4
<br><span class=byln>by Ed Vasicek</span></p>

<p>Holy One of Israel,</p>
<p>How we appreciate You as the Ancient of Days. You are 
self-derived, having neither beginning nor end. Time itself is Your creation. 
You have always been Father, Son, and Holy Spirit. It amazes us to think that 
our universe was created by a mere conversation between the Persons of the 
Trinity! We also thank You that You planned to send Jesus even before the world 
was founded. He is truly the “Lamb of God slain before the foundation of the 
world.”</p>
<p>Yet, Lord, we recognize that we are not worthy to receive 
Your grace. Our hearts beat about 130,000 beats per day, and yet everyone of 
those is by Your grace and with Your permission. Indeed, God the Son holds the 
entire universe together. We are at Your mercy: how grateful we are that You are 
gracious and faithful!</p>
<p>Father, we know that You desire Your Name to be glorified 
among all the peoples of the earth. We especially pray for the Church in Asia. 
Many of our brothers and sisters in that region are such a small minority, 
sometimes a persecuted minority. Yet You are working mightily in that region. 
We pray, Lord, for a great harvest in Asia. Give believers favor with the 
people and the governments, drive out Satan and may the truth of the Gospel 
become firmly established. Bless the missionaries and leaders who are true to 
You and Your Word.</p>
<p>Here at home, Father, we pray that You would help our 
suffering families. We especially remember single parents and their children. 
Help fill the voids they feel, provide for them, and be a Companion and Aid to 
them. May they learn to know You, live for You, and lean upon You.</p>
<p>Lord, each of us have personal needs. Some of us need a 
breath of fresh air, a spiritual lift to help us make it through another week. 
We pray that the pure breeze of Your Spirit would minister to each of us today.</p>
<p>We ask this in Jesus’ Name, Amen.</p>

