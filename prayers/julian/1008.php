<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 8
<br><span class=byln>by Ed Vasicek</p>

<p>My King,</p>
<p>You are Spirit. You are the One God, and You have a spiritual nature. You have 
created all spirits. When Jesus walked the earth, He stated: 
&quot;God is a spirit, and they that worship him must worship in spirit and truth” 
(<b>John 4:24</b>). Hebrews tells us, &quot;Furthermore, we have had the fathers of our 
flesh to chasten us and we gave them reverence: shall we not much rather be in 
subjection unto the Father of spirits and live?&quot; (<b>Hebrews 12:9</b>). This is why 
You do not dwell in Temples, nor do human hands serve you as though You needed 
something 
(<a href="javascript:scripture('../../scripture/acts172425.htm')" target=_self class=pop>Acts 17:24-25</a>).</p>
<p>Since Your nature is spiritual, You do not have a body or 
members of a body. You do not have any material structure. When we humans were 
created in Your nature, it does not mean that we look like You; it means, 
rather, that we are like You in a limited sense. We are spiritual and moral 
beings in addition to being physical beings.</p>
<p>Lord, I am thankful for plenty of things. You have been 
good and two of them include: __________.</p>
<p>Lord, may Your Spirit fill me. May I find my strength 
in His power, not my own. May Your Spirit control me this day.</p>
<p>Savior, as Your disciple You want me to bear fruit. You want me to 
demonstrate the fruit of the Spirit in my lifestyle, the greatest of which is 
love. You want me to salt the earth and be a light. You want me to encourage my 
brothers and sisters in Christ, to use my spiritual gifts in conjunction with my 
church and to share the good news of Jesus Christ with 
others. Help me to be a fruitful Christian. May I start at home with those 
closest to me.</p>
<p>Help me to organize my life and environment in a healthy, 
balanced and reasonable way. Keep me from obsessive behavior. May I neither 
model a control-freak lifestyle that is nothing more than a fearful approach to 
life nor that of disorganization because You are a God of order. Help me to seek 
a medium mentality: one that trusts You without obsession but disciplined enough 
so that I can find what I need and model responsibility.</p>
<p>Today, Lord, I pray for the United States of America. 
Father, I praise You that over 32% of its 285 million people claim to be 
born-again evangelicals. Thank You for raising up missionaries, ministries and 
great training centers in the U.S. Certainly, Lord, You have been good to the 
USA. But, Lord, the moral climate is eroding. The U.S. owns a high divorce 
rate and many Americans within the church are embracing situational ethics. 
Though many African-Americans profess the evangelical faith, the high 
illegitimacy rate and high crime rate raise suspicions about the power of the 
Gospel. The American church is noted for its materialism and many have 
embraced a prosperity Gospel. O Lord, help the American Church to grow in depth 
as well as breadth. May Americans model a Christianity worth reproducing, I 
pray. Continue to bless and use America and bring her back to Your side.</p>
<p>I ask that You would use me to influence my community, 
school, or workplace, in Jesus’ Name, Amen.</p>

