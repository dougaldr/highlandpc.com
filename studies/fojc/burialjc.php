<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:
<br>Focus on Jesus Series</span>
<br>The Burial of Jesus Christ
<br><span class=byln><b>Notes on John 19:31-42, Mark 15:42-47, Luke 23:50-56, Matthew 27:57-61</b>
<br>by Ed Vasicek</span>

<p>The burial of Jesus Christ 
involves many details, a few of which are mentioned below. The day of Jesus&#039; 
death was Friday, Passover Day, which was also the “Day of Preparation” for the 
High Sabbath, Saturday during the Feast of Unleavened Bread. (For information 
about reconciling the statement that Christ rose ON the third day and the phrase 
“after three days and three nights,” see the article <a href="on3rdday.php">&quot;After Three Days or ON the 
Third Day?&quot;</a>) </p>
<p>To hasten death on a cross, 
the Romans would break the legs of crucified victims so they could not raise 
themselves to breathe. They would soon suffocate. But Jesus was already dead, 
and to make sure, a guard pierced his side. The separation of “blood and water” 
meant that His heart had literally burst, his blood clotted and separated from 
the serum.</p>
<p>The secret disciple, Joseph 
of Arimathea, asked Pilate for the body of Jesus. We read in 
<a href="javascript:scripture('../../scripture/john1939.htm')" target=_self  class=pop>John 19:39</a> 
that Nicodemus helped him with the burial procedure. Jesus would have been buried in 
a common mass grave reserved for criminals or the poor, but that was not God’s 
plan.</p>
<p>The Romans would often let 
family members bury their loved ones, if requested. But Joseph was not a family 
member. Why did Pilate grant his request? Joseph was a member of the 
Sanhedrin, the Jewish ruling council, and he tried to use his influence to 
thwart Jesus’ crucifixion. Pilate would have probably recognized Joseph as a 
member of that Jewish ruling body, and thus may have assumed that the same 
people who pushed for the crucifixion now wanted Jesus’ body, perhaps to gloat 
or put it on display after the Sabbath. Since the Romans had to somewhat 
accommodate the Jewish Law’s demands that bodies be removed by nightfall 
(<b>Deuteronomy 21:22-23</b>, <i>“If a man guilty of a capital offense is put to death 
and his body is hung on a tree, you must not leave his body on the tree 
overnight. Be sure to bury him that same day, because anyone who is hung on a 
tree is under God's curse. You must not desecrate the land the L<span class=sm><i>ORD</i></span> your God is 
giving you as an inheritance</i>”), burying Jesus was one less problem for the 
Romans to deal with.</p>
<p>These events were prophesied 
in that wonderful chapter, <b>Isaiah 53</b>. A verse that seemed obscure is easier to 
understand in light of New Testament fulfillment. As you might recall, <b>Isaiah 
53</b> predicts the atoning death, burial and resurrection of the Messiah. <b>Verse 9</b> 
is the one I have in mind: “He was assigned a grave with the wicked, and with 
the rich in his death, though he had done no violence, nor was any deceit in his 
mouth.”</p>
<p>Crucifixion victims were 
“assigned” to be buried with the wicked, but instead Jesus was buried in the 
tomb of a wealthy man. Joseph unknowingly fulfilled that prediction. All the 
prophecies of <b>Isaiah 53</b> were fulfilled, yet those who brought them to 
fulfillment did so blindly. We should not be surprised if the same thing occurs 
regarding end-time prophecies.</p>

