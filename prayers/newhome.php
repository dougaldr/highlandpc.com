<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>Dedication for a New Home
<br><span class=byln>by Ed Vasicek</span></p>

<p>Lord,</p>
<p>Thank You for the blessings of food, clothing and 
shelter. Thank You that our food is abundant, our clothing comfortable and our 
shelter well beyond necessity. You do not merely provide for our needs, but you 
give us &quot;all things richly to enjoy&quot; 
(<a href="javascript:scripture('../scripture/1tim617.htm')" target=_self class=pop>1 Timothy 6:17</a>).</p>
<p>And so, Lord, we ask You to bless this dwelling. May You 
be the Lord of the Manor, the One everyone within follows and respects. May our 
home be a lighthouse for the Gospel of Jesus Christ. May our Christianity be 
the version that works in real life, for the way we live within these rooms is 
one of the great tests of our faith; may Your Holy Spirit be at home here and in 
our own hearts as well. May all Who abide here come to know You and serve You.</p>
<p>May these walls represent a hedge You place around those 
within to keep out the Evil One. Send angels to protect us here. Let us use 
this residence as an outpost for the Gospel and a place for Christian 
fellowship.</p>
<p>Help us to work out the practical aspects of our faith 
within these confines. Help us to address conflict in truthful and humble ways 
rather than denying our differences. Help us to show love and concern to one 
another so that this dwelling becomes a City of Refuge.</p>
<p>So we ask that harmony, faith, love, hope, wisdom and 
sincerity would be the rule of this small domain. We know these walls will 
witness both ecstatic happiness and deep grief and we know that You, O Lord, 
will rejoice with us when we rejoice and You will be there to comfort us when 
we are grieved. </p>
<p>Give us wisdom and moderation in taking care of this 
building. Remind us that all we have comes from You and is Yours: we are merely 
stewards, managing Your property. And as excited as we are about decorating 
and settling in, help us to prioritize relationships above environment.</p>
<p>May this home be a happy and safe one. Bless this house 
and all within, we ask, in Jesus' Name, Amen.</p>

