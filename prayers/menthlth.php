<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Personal Devotions</span>
<br>A Christian Undergoing Treatment in a Mental Health Services Facility
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>O Lord, it is no fun to not be myself. I feel like I am in 
such a dark valley; I am scared of so many things. I do not want to be here, 
but I realize I need to be. Help the workers and specialists here to do what is 
best for me, because I am at the end of myself.</p>
<p>Father, it sometimes feels like You have forgotten me, 
deserted me, and left me to fend for myself. I know that is not true, for Your 
Word says You will never leave me nor forsake me, and You make no exceptions. 
But it doesn't always <i>feel </i>that way.</p>
<p>I do not understand, and I keep asking, &quot;Why?&quot; Sometimes I 
wonder if I will ever get better. O Lord, please give me a generous dose of 
hope. I need to trust Your grace that better times are ahead for me. They may 
not appear immediately, but eventually it will get better.</p>
<p>I do not like this different life and this different way of 
thinking. I want to get back to my old self—no, maybe a better self. But 
this is so much easier said than done. I know I am a complicated being, and 
there are things going on within my inner being that I do not always 
understand. But I understand enough to know I don't like it.</p>
<p>So, Lord, deliver me from these unhealthy thoughts, and 
help me to gain a surer footing in life. Please, dear God, help me to recover. 
Help others to help me. And help me be patient, I pray, in Jesus' Name, Amen.</p>

