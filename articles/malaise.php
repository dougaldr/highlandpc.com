<p>Spring is a time of change. The weather changes and vacillates:
sometimes March comes in like a lion and out like a lamb, other times it begins
as a lamb and exits as a lion. Then there is the lion, lamb, lion, lamb,
and finally lion pattern. You just do not know.</p>
<p>Spring is also a time of uncertainty, bold moves, and sometimes change for
the sake of change. April is the top month for suicides. People
argue, switch jobs, churches, and even marital partners in the spring.&nbsp;
People make vows to make changes they cannot make immediately. Some
authorities blame spring fever on barometric and/or other atmospheric
changes. Do you ever get that sense of &quot;spring malaise&quot; when you
are driving down a country road and just want to keep going? Spring fever
is a reality.</p>
