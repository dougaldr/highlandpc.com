<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>New Year's or New Year's Eve
<br><span class=byln>by Ed Vasicek</span></p>

<p>Our gracious Sovereign Lord,</p>
<p>How we praise You, Lord Yahweh, for your unfailing and unchanging love!&nbsp;
As we march forward in this sinful world, it seems little stays the same.&nbsp;
Our mortal bodies live in a kingdom of flux and relativity, but our inner man is
anchored to the Rock. How we praise You for the stability of the Gospel
and the absolute truth revealed in Your holy Word.</p>
<p>In some ways, Lord, we are glad to say goodbye to the past year with its
disappointments, heartaches, and aggravations. On the other hand, Lord, we
praise and thank you for its blessings and really do not want to let go of it.&nbsp;
Such is the mixed nature of life in a sinful world.</p>
<p>Yet, through both the good and bad times, You are building your church, and
the gates of hell shall not prevail against it. And we are part of Your
Body, the Church. How we praise You for including us in Your Kingdom!&nbsp;
As we begin a New Year, burden us for our friends, family, and fellow human
beings who have never been born-again by faith in Your Son. We would love
to see them enter into that straight gate by trusting in Jesus the Christ.</p>
<p>Lord, we pray for the advancing of Your agenda in this coming year. We
pray that our personal agendas would meld with Your Kingdom plan, that we might
be included in the work of Your Spirit. So guide us into your perfect will
this coming year. Open the right doors, close the wrong ones. Create
in us a clean heart, and give us a hunger for You and Your Word. Then help
us taste and see that You, O Lord, are good. Break up the hard soil of our
hearts. Help us to grow in the grace and knowledge of our Lord Jesus
Christ.</p>
<p>We pray that you would meet our physical needs in the coming year. We
are always so vulnerable in matters of health, safety, financial provision, and
sound thinking. Our relationships are never far away from potential
tensions, and our sin nature, the devil, and godless society are constantly
pressing upon us. Help us to address all of life—the good times and the
bad times—by focusing on our Lord and Savior Jesus Christ. Help us to
meet with him frequently in our quiet times, to find group support and nurture
in our church, and to be alert to opportunities you send us to share the Good
News.</p>
<p>We pray for our nation, our president and leaders, and for the peace of
Jerusalem. Lord of the Past, you are also Lord of the Present and Lord of
the Future. You know all things before they come to pass. You are
not limited by this sequence of events we call time.</p>
<p>Since You are all powerful, You can do. Since You are omniscient, You
do know. Since You love us, You do care. And since You are gracious,
You are sharing Yourself with us. We can and do trust You with the coming
year.</p>
<p>We pray in Jesus' Name, Amen.

