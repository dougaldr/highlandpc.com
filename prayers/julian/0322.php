<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 22
<br><span class=byln>by Ed Vasicek</p>

<p>Loving Father,</p>
<p>Thank You that You are the Revealer of Truth. Sometimes 
You keep this truth from the wise of this world, but reveal Yourself to babes, 
to those considered foolish by society's standards. We know that You must 
reveal to us our lost condition, our helpless plight, and the sacrificial Lamb, 
Jesus Christ, as our sin-bearer and sacrifice. You help us recognize that He 
was declared the Son of God by His resurrection. You nurture faith within us 
and coax us along until we are saved, cleansed, forgiven and reconciled with 
You. Thank You for revealing Jesus Christ to us.</p>
<p>Lord, help me to seek to be used of You in the revelation 
process. Use me to explain the Gospel to someone soon, and by Your Spirit 
take those truths and open them up to this person that he or she may come to 
know Christ, my Lord.</p>
<p>Father, thank You for my senses. In Your grace and 
goodness, keep them all healthy, and help me to appreciate them and use them in 
ways that please You. Help me to discover the joys of being a human being 
created in Your image, yet help me to enjoy life in ways consistent with Your 
revealed will.</p>
<p>Help me to do my part by volunteering to serve or 
continuing to serve in a Bible-believing, Bible-teaching church. May I set my 
roots down deeply and attend every week I am able to do so.</p>
<p>Lord, I pray for our president and our military troops. 
Guide them, protect them and help them to love You.</p>
<p>I also pray for those who are seeking to help children in 
good and practical ways. Bless the children's hospitals in the U.S. and 
throughout the world. Help those ministries and agencies that minister help to 
street children, abused children and homeless children. I especially remember 
the orphaned and impoverished children and children who have suffered abuse. 
It sometimes breaks my heart to hear the stories that I hear, but, Lord, I know 
it breaks Your heart even more.&nbsp; I look forward to the return of Christ at 
which time true and complete justice will be established.</p>
<p>I remember the nation of Ecuador. Thank You that over 6% 
of the population claim to be born-again evangelicals, and thank You for the 
mighty work You have done among the Quichua people! Use the Jesus film to reach 
the urban areas, and may You move as mightily among them as You have the 
Quichuas! Help the government to stabilize, and may the evangelical church in 
Ecuador produce strong leaders. May their numbers double over the next decade. 
May the Gospel transcend class, ethnic groupings and denominational labels.</p>
<p>I pray for the ministry of Asian Access, &quot;an 
interdenominational evangelical organization that works throughout Asia to 
identify, develop and release leaders who serve as pastors of growing and 
reproducing churches.&quot; Thank You for their successful training cycles in 
nations like Mongolia, and their new operations in other Asian countries. 
Provide for them, guide them, protect them, bless them and empower them by 
Your Spirit, I ask.</p>
<p>I pray for the world's farmers. Thank You for the abundant 
food they produce, and I pray that their farms would become more efficient and 
successful. May farmers worldwide turn to You in great numbers and help those 
who know You to boldly share the news of the Jesus Christ, in Whose Name I pray, 
Amen.</p>

