<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 17
<br><span class=byln>by Ed Vasicek</p>

<p>Heavenly King,</p>
<p>When Jesus was worshipped by the Magi, they bowed down 
before Him. They did not merely show respect to Him, but the text says they 
worshipped Him 
(<a href="javascript:scripture('../../scripture/matt211.htm')" target=_self class=pop>Matthew 2:11</a>). 
They recognized Him as God in the flesh, for the 
Scripture says, &quot;You shall worship the Lord Your God and serve Him only&quot; 
(<b>Matthew 4:10</b>).</p>
<p>Their gifts of gold, frankincense and myrrh would provide 
for the family when they had to flee to Egypt because of Herod's persecution. 
Although these gifts were rich in symbolical value, they were also practical and 
needed.</p>
<p>Thank You, Lord, that You are the God of Reality. 
Sometimes others look at us as though we were escaping reality by believing in 
You. But, Lord, You are the God of real life. You paint a realistic picture of 
mankind: marred by sin but also capable of good (because of Your image in them, 
tainted as it is). This world is under a curse and so it is. Even our ability 
to reason is affected by our sinful predisposition, so that all sorts of wrongs 
are rationalized and justified.</p>
<p>And, Lord, You know we need our daily bread. We are 
vulnerable human beings. We are vulnerable physically, relationally, 
emotionally and spiritually. Thank You that as we hold Your hand, You help us 
go on with confidence. I ask You to meet my needs, O Lord.</p>
<p>Lord, I have some disappointments and burdens in life. A few of them hurt me 
and have left scars including: __________. I turn them over to You. Help me to 
find Your purpose and joy in life. There are many things I cannot change, but I 
can share all my thoughts with You. Thank You that You do not stifle me or 
pressure me to &quot;get over it.&quot; You are not in a hurry and You can hear my 
recitations of woe over and over again. You are a God Who feels with me. But, 
Lord, You are also the God of victory. Help me get to a point where I am ready 
to move on.</p>
<p>Father, I pray for our president, governor, federal and local senators and 
representatives, and judges of all kinds. Help them to do what is right, to seek 
You and to please You.</p>
<p>Today I remember the nation of Djibouti. It is a dry and impoverished land 
and this is even truer in the spiritual realm where only one-tenth of one 
percent claims to be born-again evangelicals. Thank You that even though Muslims 
make up 91% of the population there is a relative level of religious freedom. 
Lord, please see to it that this continues. We pray that the Bible-oriented 
churches would grow fifty times over the next decade. Create a hunger in this 
land for You.</p>
<p>I thank You for the ministry of the Gideons as they distribute tens of 
millions of Bibles every year free-of-charge throughout the world. Thank You 
that they supply hotels, military personnel and students among others with the 
Word of God.</p>
<p>I ask Your blessing and guidance today. Like the psalmist, may I look to You 
as my Light and Salvation 
(<a href="javascript:scripture('../../scripture/ps271.htm')" target=_self class=pop>Psalm 27:1</a>). 
I pray in Jesus' Name, Amen.</p>

