<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 11
<br><span class=byln>by Ed Vasicek</p>

<p>God of Israel,</p>
<p>You are a passionate God. In a day when people claim to be 
passionate about chocolate or music, it is refreshing to know that You have 
strong and deep feelings about what really matters. You are alive in every 
sense of the word. As a matter of fact, Your feelings are more intense than ours 
ever could be. Yet Your feelings are always in harmony with Your reason and 
Your justice. Although sometimes our feelings are our worst enemies and they 
tempt us to react in sinful ways, Your feelings never fail to lead You to 
righteousness.</p>
<p>Because You are passionate—a God with deep feelings—we 
know that being created in Your image means having deep emotions and feelings. 
Unfortunately, just as our bodies, intellects and wills have been affected by 
sin so have our feelings. So help me to check my feelings with the absolute 
truth revealed in Your Word. Help me to be led by Your Spirit Who never leads 
contrary to Your Word and not my feelings. Help me to discern the difference 
between the emotional and the spiritual.</p>
<p>I thank You that, though my works might reveal my destiny, 
they do not create it. I thank You that I am saved by Your grace, Your freely 
given gift, and not by my behavior, achievements or religious practice. I am 
saved in such a way that you get literally all the glory and all the credit. 
The only thing I have contributed toward my salvation is the sin from which I 
have been saved! Thank You that Jesus Christ is my Savior. He is not a 90% 
Savior with me being a 10% Savior. He saves me 100%, completely!</p>
<p>I pray for my lost friends and family members, including: __________. 
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Today I remember the nation of Croatia. Lord, this country 
has been involved in the nasty Balkan Wars of the 1990s with Serbia and 
Bosnia. Only ½ of 1 percent of the people claim to be born-again evangelical 
Christians. But thank You that this reflects growth, and thank You that the 
Christians have fellowship among the different ethnic lines, testifying to the 
unity Christ can bring. As the Jesus Film begins to be used to reach people, we 
pray that Your Spirit would hover over Serbia and the whole area of the former 
Yugoslavia. We pray that the evangelical church would multiply tenfold over the 
next decade.</p>
<p>We pray for White Fields as they seek to reach 
people with the Gospel and plant churches in several nations in South America, 
Africa and Asia. We pray that You would bless their missionaries with health, 
godliness, great family life, safety, provision and much fruit for their 
labors. We pray for harmony and effectiveness in Kingdom work. May Your Spirit 
empower and guide them.</p>
<p>I remember those who work as court reporters. Help them to 
do their work well, and may those who know Christ take opportunities to share 
the Good News with coworkers.</p>
<p>I especially remember the poor, the underemployed and the 
unemployed. Help those looking to find work.</p>
<p>Now, Lord, protect me today. Send guardian angels, I pray, 
in Jesus' Name, Amen.</p>

