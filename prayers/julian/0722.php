<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 22
<br><span class=byln>by Ed Vasicek</p>

<p>Yahweh,</p>
<p>You are a calling God. We are born with an 
emptiness within us that can only be satisfied with You. You call us by our 
consciences to settle our differences with You, the Lawmaker. You call us by 
natural revelation, as we see Your power in the storm and Your design in all of 
creation. You call whenever the Gospel is preached. And when Your Spirit 
convicts, draws and regenerates those who become Your children, that, too, is a 
very special call. As Peter puts it, You are He, &quot;who called [us] out of 
darkness into His wonderful light&quot; (<b>1 Peter 2:9</b>). You are the One Who calls.</p>
<p>Lord, please help me with my thought life today. Help me 
think positive and pleasant thoughts today, thoughts that please You. So much 
of the battle takes place in the arena of the mind. Lord, control and protect 
my mind, I ask.</p>
<p>Today I praise You that the Holy Spirit has sealed me for 
the day of redemption. I bear the royal seal of the King of Kings and none dare 
open the seal but He. I am Yours, Lord, bought with the blood of Jesus Christ. I 
am not my own.</p>
<p>Dear Lord, help me to be a discerning person. There are 
many false teachings out there, many deceivers have entered the world. Others 
name the name of Christ but are not sincere. Help me not to be gullible. Help me 
to be discerning about spiritual things, relationships and life.</p>
<p>Father, help me to get to know my neighbors where I live and at work or school. Teach me the importance of participating in my community 
as an avenue to benefit myself—and a way to show love to others.</p>
<p>Lord, the Pacific Island of Samoa needs a work of Your 
Spirit. Although 4% claim to be born-again evangelicals, cults and inconsistent 
behavior among Christians present a serious challenge to the believers there. 
Lord, may the number of evangelicals double over the next decade and may the 
church there become strong and stand up to its many challenges.</p>
<p>Lord, thank You for the ministry of OSM/ITIPS. &quot;Overseas 
Summer Missions (OSM) works with churches and provides affordable summer mission 
trips for college students and young adults to serve our Lord overseas. 
International Teacher Placement Service (ITPS) is a sister organization to 
provide opportunities for mission minded Christians to teach English overseas 
with paid positions for one year or longer, in the same time, to help national 
churches with their evangelical efforts.&quot; O God, empower those who serve in 
these ministries by Your Spirit. Grant them wisdom, boldness, tact and 
safety. Give them much fruit for their labor, I pray.</p>
<p>I pray for those who work as iron and steelworkers. Lord, 
please give them safety on the job, help them to do their work well and may 
those who know You be bold to share the Gospel of Jesus Christ with their 
coworkers.</p>
<p>Help me to be faithful to You and mindful of You today, I 
pray in Jesus' Name, Amen.</p>

