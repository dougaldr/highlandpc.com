<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 19
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are the God of peace. When I present my cares to You, 
You provide me with Your peace. Before I knew You when I was at enmity with 
You, Your Son became a man, paid my penalty for sin so that I could be at peace 
with You. You are the God of Shalom, peace. Although Your feelings are many 
and varied, You are always at peace: always confident, always sure, always 
having the satisfaction of doing right and being right. One day when Jesus 
returns, we will finally have Your peace on the earth, a peace prophesied by the 
Christmas angel. Thank You that Your children already experience this peace in 
their hearts.</p>
<p>This world is a world of turmoil, confusion and sudden 
change—particularly from the human perspective. Sometimes I do not have 
peace: I have worry, fear, frustration and uncertainty. Yet I can come to You 
for an oasis of peace, a break from the chaos, a time to readjust my 
perspective. Thanks for Your peace. Help me to avail myself of it more 
frequently.</p>
<p>Help me to resist temptation and 
the lures of Satan today. Help me to be strong in the Lord, and in the power 
of His might, to put on the armor of God and to claim the blood of Jesus Christ 
as the source of my status before You.</p>
<p>Lord, help me manage my resources: 
time, energy, money and the people in my life. Give me balance, wisdom and 
skill in all these areas. Help none of them to replace You as the priority in 
my life.</p>
<p>I pray for the island nation of 
Dominica. Thank You that nearly 14% of the population claim to be followers of 
You as born-again evangelicals. Help them, Lord, to live in light of that 
profession. We know that the slaves who once inhabited that island were not 
allowed to marry, and now that they are free, most births are still out of 
wedlock even in the believing community. Help the Christians there to learn a 
new appreciation for marriage and the importance of staying pure until 
marriage. We pray that young people who know Christ would particularly develop 
this conviction, and that You would use them to lead this nation out of poverty 
and dysfunction. Provide for the pastors, as many of them hold other 
jobs and therefore burn out of ministry.</p>
<p>I remember Christian radio 
stations that are true to the Bible throughout the nations. Help them stay 
true, provide equipment and good personnel, and use them to reach millions with 
the Gospel of Jesus Christ, as well as aiding believers to grow deeper.</p>
<p>I also remember the effective 
evangelistic ministries of the Billy Graham Evangelistic Association and the 
Louis Palau Evangelistic Association. Provide new leaders and evangelists for 
the future, and may the effectiveness of these ministries grow in the years 
ahead.</p>
<p>Help me now to serve You, I pray, 
in Jesus’ Name, Amen.</p>

