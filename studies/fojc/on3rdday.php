<br>After Three Days or ON the Third Day?
<br><span class=byln>by Ed Vasicek</span></p>

<p>There 
seem to be two sets of explanations as to when Christ died and rose again. The 
casual reader will find a contradiction between these two explanations, but the 
problems disappear when we take into account the audiences addressed.</p>
<p>
Christ repeatedly said things like, &quot;For as 
Jonah was three days and three nights in the belly of a huge fish, so the Son of 
Man will be three days and three nights in the heart of the earth…&quot; <b>Matthew 
12:40</b>. Yet Paul the Apostle seems to say something very different. He writes, 
&quot;…that he was buried, that he was raised <b><u>on</u></b> the third day 
according to the Scriptures…&quot; <b>1 Corinthians 15:4</b>.</p>
<p>So which is it? Was He raised <u>AFTER</u> three days, or <u>ON</u> the 
third day? The answer depends upon the audience: if you understand things as a 
first century Jew, then the answer is AFTER three days. If you understand 
things as a first century gentile or a modern American, then <b>the answer is ON 
the third day.</b></p>
<p>Every language and culture has its figures of speech. Some people claim 
to &quot;carry a fever.&quot; Do they carry it in a bag, box, or purse? Why should we 
&quot;fix a meal?&quot; Is the meal broken or something? You get the point. The Hebrews 
had their own way to express duration. <u>To the Jew, &quot;after three days&quot; meant 
&quot;ON the third day&quot;</u> (as we would understand the term &quot;on&quot;). But don't take 
my word for it. Note these Old Testament examples which prove the point that 
the Hebrew idiom &quot;after&quot; or &quot;for&quot; means our &quot;on.&quot; Note that I have had to 
sometimes use the ASV (NASV or KJV would also do) because the NIV sometimes 
paraphrases these Hebrew idioms.</p>
<p><b>ASV Genesis 42:17-18</b> &quot;And he put them all together into ward <b>three 
days</b>. And Joseph said unto them <b>the third day</b>, 'This do, and live: 
for I fear God.'&quot;</p>
<p><b>NIV 1 Kings 20:29</b> &quot;<b>For seven days</b> they camped opposite each other, 
and <b>on the seventh day </b>the battle was joined. The Israelites inflicted a 
hundred thousand casualties on the Aramean foot soldiers in one day.&quot;</p>
<p><b>ASV 2 Chronicles 10:5, 12</b> &quot;And he said unto them, 'Come again unto me <b>
after three days</b>.' And the people departed. ...So Jeroboam and all the 
people <b>came to Rehoboam the third day</b>, as the king bade, saying, 'Come to 
me again the third day.'&quot;</p>
<p><b>NIV Esther 4:16, 5:1</b> &quot;'Go, gather together all the Jews who are in Susa, 
and fast for me. Do not eat or drink <b>for three days</b>, night or day. I 
and my maids will fast as you do. When this is done, I will go to the king, 
even though it is against the law. And if I perish, I perish….' <b>On the 
third day</b> Esther put on her royal robes and stood in the inner court of the 
palace, in front of the king's hall. The king was sitting on his royal throne 
in the hall, facing the entrance.&quot;</p>
<p>So the best chronology is the traditional one: Christ instituted the 
Lord's Supper Thursday night, was crucified on Friday, and arose ON the third 
day, Sunday.</p>
