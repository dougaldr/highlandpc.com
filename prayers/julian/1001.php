<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 1
<br><span class=byln>by Ed Vasicek</p>

<p>Dear Lord,</p>
<p>You are the Lawgiver. Our consciences testify that there is 
indeed such a thing as right and wrong. This reminds us that there is a standard 
and that standard exists because You have written Your law on our hearts. You 
have the right to announce what is right and wrong, for You are perfect in all 
Your ways. Your justice and holiness are reflected in Your Law. Our opinions are 
no competition for Your Word. Your Word reflects Your will and is based upon 
Your Law. You do not merely share opinions.</p>
<p>Lord, thank You that You planned for Jesus Christ to die 
for our sins before the world was founded. Thank You that He took all my sins 
upon the cross as a sin offering. Thank You that He presented His blood to You 
in the heavenly sanctuary and that His offering was for all people for all 
time. Thank You that our Savior arose from the dead.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________. Let me remember that You 
are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>I remember the poor, sickly, downtrodden. Provide for them, 
encourage them, open doors of long-term opportunity for the poor, grant 
patience, strength and healing for the sick and mend broken hearts. </p>
<p class="prayer">Today I pray for the evangelical church worldwide, all 
congregations made up mostly of people who claim to have been born again and 
have a personal relationship with Jesus Christ, who believe in the deity of 
Christ, His death on the cross for our sins, the literal resurrection, who stand 
for the Bible as the inspired and authoritative Word of God and who believe in 
salvation by grace through faith alone, apart from works. Thank You that 
although Christianity has declined slightly as a percentage of the world’s 
population, evangelical Christianity has grown from 84.5 million (2.8%) in 1960 
to 420 million (6.9%) in 2000.</p>
<p class="prayer">But, Lord, we know that growth does not necessarily mean 
truth. Indeed, sometimes those who follow the truth decline in number. So, 
please, Lord, help evangelical Christians to crave Your Word, to value knowledge 
and doctrine and to address the questions of our day with intelligence and 
conviction, as has been our heritage.</p>
<p>Lord, thank You for the work of Media Associates 
International. &quot;MAI equips and nurtures talented men and women with a passion 
for producing Christian literature for their own people. Since its founding in 
1985, MAI has equipped local Christians in 57 countries on 5 continents [seeing] 
budding writers developed, publishing houses grown, periodicals begun and books 
and magazines produced that speak to the hearts of readers in their own 
languages.... [They] advance the Gospel by training Christians to produce literature 
that nurtures the church and attracts readers to Christ.&quot;</p>
<p>Lord, help me use whatever talents You have given me to 
reach and minister to others, I pray in the Messiah's Name, Amen.</p>

