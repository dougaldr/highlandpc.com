<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>Making Sense of Philippians 3:1-3
<br><span class=byln>by Ed Vasicek</span></p>

<div>
<p>For years, this text often 
came across to me as a disconnected hodgepodge. Recently the text seemed to fall 
into place. I thought I would therefore share this casual 
exegesis with others who may likewise be perplexed. Particularly at issue for me 
was "How does rejoicing in the Lord relate to false teaching?” 
Let’s dig in.</p>
<p>Paul begins 
<a href="javascript:scripture('../scripture/phil313.htm')" target=_self class=pop>Philippians 3:1-3</a> 
with the term, “finally” meaning, literally, &quot;the remaining&quot; (matters). 
The remaining matters at hand included the problems caused by false teachers, 
particularly those sometimes referred to as Judaizers. They probably had not 
gained much of a stronghold yet in the Philippian congregation, but Paul decided 
to make a preemptive strike.</p>
<p>These Judaizers are sometimes 
thought to be gentile Christians who embraced Judaism as part of their Christian 
profession, but the better guess is that they were mostly Jews who considered 
themselves Messianic Jews (Christians) or gentiles who had converted to Judaism 
beforehand, then later acknowledged Jesus as Messiah.</p>
<p>The early church was made up 
mostly of Messianic Jews (believers in Jesus who were Jewish, like the apostles) 
well past the first half of the first century. Somewhere between 65 and 75 
A.D., gentile Christians became the majority. The Jewish presence was mostly a 
blessing to the church; the Jews were well grounded in the Hebrew Scriptures and 
had a tradition of morality, monotheism, and solid theology (especially when 
compared to Christians who were converted from polytheistic idolatry). If 
immorality and idolatry were struggles common to gentile converts, the tendency 
toward salvation by faith in the Messiah PLUS Torah keeping was a problematic 
issue among some Jewish converts.</p>
<p>According to <b>Acts 15</b>, one of 
the big controversies in the early church was whether gentile believers needed 
to convert to Judaism before (or after) they became Christians. As David Stern 
points out (see <i>Restoring the Jewishness of the Gospel</i>), the issue as to 
whether Jews had to leave Judaism to become Christians was never addressed; it 
was <i>assumed</i> that Jewish believers would continue within Judaism. The 
controversy was whether gentiles could be saved without first becoming Jews. If 
Jewish believers had been instructed to forsake Judaism, there would have been 
no such controversy!</p>
<p>Circumcision, one of the 
rituals associated with Judaism and observing the Torah (the Law of God given to 
Moses), was used as the first installment in assuming the yoke of the Law—the 
obligation to obey the Law in its entirety. Circumcision was not merely a 
cultural boundary marker of culturally separatist Jews (as Sanders and the 
Covenantal Nomists maintain), but rather signified the requirement to keep the 
entire law. Paul describes these Judaizers as those who “want to be under law” 
(<a href="javascript:scripture('../scripture/gal421.htm')" target=_self class=pop>Galatians 4:21</a>), 
not just Jewish separatists.</p>
<p>So with this false teaching in 
the textual limelight (as is clear from <b>verse 2</b>, “Beware of the dogs…beware of 
the false circumcision”), why does Paul urge believers to rejoice in the (realm 
of the) Lord? The answer is quite simple, 
but we often are blinded to it because of the “worship movement.” When modern 
ears hear the words, “rejoice in the Lord,” they are converted ala paradigm 
blindness to refer to either the idea of singing, worship, or becoming 
enthusiastic (perhaps pressing an invisible button that causes the adrenal 
glands to shoot a bit of energy into our bloodstreams). But I do not believe 
that was Paul’s intent.</p>
<p>I think Paul here is talking 
about <i>the joy that comes from salvation</i> and <i>the knowledge that we are 
fully accepted by God because we are in Christ</i> by faith alone! Note the 
following Old Testament precedents that associate joy with salvation:</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Psalm 51:12</b>, &quot;Restore to me the <b>joy of your salvation</b> 
and grant me a willing spirit, to sustain me.&quot;</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Isaiah 12:3</b>, &quot;With <b>joy</b> you will draw water from the 
wells of <b>salvation</b>.&quot;</p>
<p>This joy, then, is in itself a refutation of the false 
doctrine of salvation by grace AND law keeping. It is the joy of one who 
believes God “justifies the ungodly,” 
(<a href="javascript:scripture('../scripture/rom445.htm')" target=_self class=pop>Romans 4:4-5</a>), 
not the law keeper.</p>
<p>False teachers would consider this sort of joy presumption; 
thus their works system is incompatible with the New Testament plan of 
salvation, <i>a salvation that brings the joy of assurance of salvation.</i> 
This is why Paul calls this rejoicing a “safeguard” and has no embarrassment 
about repeating his admonition (<b>3:1b</b> and <b>4:4</b>, where we can rejoice for the 
reason found in <b>4:3</b>—namely, our names are written in the Book of Life, and 
<b>4:4</b>, we can rejoice because the Lord is near and we look forward to His return; 
on the other hand, the law keeper cannot be sure he has met the standard, so the 
nearness of the Lord’s coming is bittersweet at best).</p>
<p>Paul then urges the believers to beware of (scavenging) 
dogs, redefining the negative label Jews often used when describing ungodly 
gentiles. In this case, the erroneous Jewish teachers are the “dogs.” It is worth noting that they are called dogs
not because they were Jewish, nor because they 
preferred to live according to the dictates of the Law. Their error was twofold: 
(1) advocating that law-keeping was necessary for salvation (and thus 
perhaps owning the mentality described in <b>Luke 18:9</b>, &quot;To some who were confident 
of their own righteousness and looked down on everybody else, Jesus told this 
parable&quot;) and (2) demanding that gentile converts also become Jewish converts at 
the risk of their own souls.</p>
<p>First-century Judaism had many diverse beliefs within its 
confines. Some Jewish rabbis believed that gentiles could be saved apart from 
being full converts to Judaism. These gentiles needed to repent of their sins, 
recognize the God of Israel, and were expected to relate to God under the 
Covenant of Noah, a covenant made with all mankind and still in effect as long as rainbows are observable. From 
<a href="javascript:scripture('../scripture/gen9117.htm')" target=_self class=pop>Genesis 9:1-17</a>, 
Jewish rabbis extrapolated seven of the ten Commandments. These “God-fearing gentiles” were not 
expected to eat kosher, observe the Sabbath, or be circumcised. Cornelius was a 
man in this category, and thus his house was considered ceremonially unclean to 
observant Jews, even though he had a good relationship to the synagogue leaders. 
If God-fearing gentiles wanted to become full converts to Judaism, they would 
submit to ritual immersion and circumcision. Some Jewish Rabbis considered 
baptism even more important than circumcision, but since Christians had already 
been baptized, the Judaizers did not raise that particular issue.</p>
<p><a href="javascript:scripture('../scripture/acts152229.htm')" target=_self class=pop>Acts 15:22-29</a> 
seems to verify that believing gentiles do 
indeed live under the stipulations of the Noahide Covenant, and thus are 
forbidden to eat blood, for example. Although it is difficult to understand the 
relationship of believing Jews to the Law, it seems as though Jewish believer 
could continue to observe the Law and maintain their Jewish identity, as long as 
they understood the Law could not save. This seems to be the most consistent 
viewpoint in light of 
<a href="javascript:scripture('../scripture/acts212026.htm')" target=_self class=pop>Acts 21:20-26</a>. 
It also meshes well with the institution 
of the Law and temple worship during the Millennium. The same reason Messianic 
Jews were comfortable participating in temple worship in <b>Acts 21</b> is why they 
will be comfortable in doing so when the Lord returns. Although there is no 
longer a sacrifice for sin, and sacrifices, we could argue, are not necessary; 
still sacrifices can never be wrong, since God ordained them. In one sense, 
sacrifices were <i>never</i> theologically necessary.</p>
<p>Applying things to our day, Jewish believers can continue 
identifying themselves as Jews who believe in Messiah and maintain Jewish 
customs while trusting in the work of Christ alone or choose to simply identify 
themselves as believers in Christ who do not maintain the Jewish customs. 
Jewish customs or law observance are wrong ONLY IF those practicing them 
believe they contribute toward salvation or if they set themselves up over 
gentile believers and refuse to fellowship with them.</p>
<p>The Judaizers, who demanded circumcision as a pre-requisite 
for salvation, are contrasted in <b>verse three</b> with true Christians who are 
circumcised in heart. Although outward circumcision is not a wrong thing, it 
can become a wrong thing if one trusts in that ritual or the whole of law 
keeping to make one right with God. But true believers are circumcised of 
heart, something untrue of these false teachers. This idea is not unique to the New Testament. God says in 
<b>Deuteronomy 10:16</b>, &quot;Circumcise your hearts, therefore, and do not 
be stiff-necked any longer.&quot; </p>
<p>He speaks prophetically in <b>Deuteronomy 30:6</b>, &quot;The L<span class=sm>ORD</span> your God 
will circumcise your hearts and the hearts of your descendants, so that you may 
love him with all your heart and with all your soul, and live.&quot; </p>
<p>Paul writes in <b>Romans 2:28-29</b>, &quot;A man is not a Jew if he is 
only one outwardly, nor is circumcision merely outward and physical. No, a man 
is a Jew if he is one inwardly; and circumcision is circumcision of the heart, 
by the Spirit, not by the written code. Such a man's praise is not from men, but 
from God.&quot; Since <i>Paul is addressing the Jews</i>, what he is saying, in my 
view, is that one is not a true Jew if he is ONLY circumcised outwardly, but 
must also have an inward circumcision. This teaching is not unique to Paul; 
even non-messianic Jews realized that outward circumcision was not enough. <i>A 
gentile who is circumcised of heart is regenerated, as is a Jewish believer; but 
the gentile is nowhere clearly called “a Jew.”</i> Paul here is arguing his 
case before the Jews (<b>Romans 2:17</b>, “But if you bear the name ‘Jew’…). <i>It is 
inappropriate to apply the Romans text to a non-Jew when it is Jews who are 
being addressed.</i> However, the Philippians text is written to all believers.</p>
<p>So the believer who is justified by faith apart from the 
Law boasts not only of an inward working, but he can do three additional things 
that the Judaizing “Christian” could not:</p>
<ol><li>The believer can worship the SPIRIT.</p>
<p>I agree with C.H. Lenski, one of the great vintage Bible 
scholars, who translates the verse, &quot;For we on our part are the Circumcision, 
those worshipping God's Spirit and boasting in Christ Jesus and not resting 
confidence in flesh.&quot; The term &quot;pneumati theou&quot;
is very likely in the 
dative case (as opposed to the locative or instrumental) and is WITHOUT a 
preposition and should be taken in its purest sense as a dative, “who worship TO 
the Spirit.” This would therefore approximate the accusative.</p>
<p>The most basic use of the dative case &quot;indicates the one 
for whom or in whose interest an act is performed.&quot; (Dana and Mantey, <u>A 
Manual Grammar of the Greek New Testament</u>, p. 84).</p>
<p>It is likely that the ancient Covenantal Nomists believed 
that the Holy Spirit was a force (much like modern Non-messianic Judaism), not a 
Person of the Godhead. So by rejoicing in the Lord (because our salvation is 
secured for us) and by worshipping to the Spirit, our actions refute the false 
teachings of the Judaizers!</li>
<li>Then we glory in CHRIST.
<p>Here, the term “in Christ” contains the preposition,
en, thus it is not 
quite grammatically parallel to “worshiping to the Spirit.” You cannot really 
appreciate Christ if you think you have to earn or contribute toward your own 
salvation. Although Christ was a theologian, an ethicist, a rabbi, a prophet, 
and king, He is first of all a Savior, someone who does the saving…ALL of it; we 
are therefore completely PROUD of our Savior and gladly wear His Name.</p>
<p>As Christians, because we glory in Christ ALONE for our 
salvation, that means we refuse to glory in what the false teachers glory in: 
one’s keeping of the Law (i.e., the works that we can do in our flesh).</li>
<li>We have no CONFIDENCE in the flesh, in our ability to 
merit God's favor. Since we recognize that we can do nothing to remedy our lost 
condition (Total Depravity), we look to another to do the saving…the Lord Jesus 
Christ.</li></ol>
<b>Conclusion</b>
<p><b>Philippians 3:1-3</b> sets up a defense against the false Judaizing teachers. The kind of salvation Paul taught results in the joy that 
comes from freedom from condemnation, not the fear of a temporary probation, 
always one step away from perdition. It is based on the work of God within the 
heart; a circumcision performed by God himself, not by our efforts.</p>
<p>Since we have this freedom and this new life, we worship 
the Father, Son, AND Spirit. We glory in Christ, the one who completely saved 
us, and not in our own discipline, accomplishments, or ability to keep the Law 
of God.</p>
