<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Wedding Involving a Blended Family
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>We thank You for the gift of marriage. Marriage is not 
merely a social contract; it is so much more: the melding of two into a 
permanent unity of one. It is, after all, Your idea, not an innovation by 
mankind. It is the beginning of a new family and a new life together.</p>
<p>Father, we know that a walk with You is the foundation for 
a strong family. You are the source of long-term joy, and walking closely with 
You day-by-day produces a life rich with meaning and purpose. When we, as 
family members, ALL choose to walk with You, how special our lives become. Next 
comes the priority of close unity and camaraderie between husband and wife. 
This, then, provides the basis for relating in a healthy and balanced way to the 
children.</p>
<p>Today we especially celebrate the joining together of man 
and wife. What a special time this is indeed! But we also celebrate the 
blending of a new family unit. Our prayer is for the entire family, that you 
would grant a spirit of peace and togetherness and cooperation and love. We 
pray for your special grace for this newly-blended family. We pray that 
each family member would seek your direction and desire to do what is in the 
best interest of the family at large.</p>
<p>And so, Lord, we celebrate a day of new beginnings. We are 
so grateful that You are with us for the journey, and we pray that it would be a 
blessed journey. Please shower Your grace upon this marriage and all within 
this new family, we pray, in Jesus' Name, Amen.</p>

