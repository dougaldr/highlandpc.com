<p><b>Family Connections</b></p>
<p>Many people are surprised at the family relations in the 
early church. The Apostles James and John were brothers, as were Andrew and 
Simon Peter.</p>
<p>Philip and Nathaniel (Bartholomew) were close friends. 
Friendship and relationship still provides for the most fertile ground for 
evangelism.</p>
<p>Eleven of the twelve Apostles were from Jesus’ region of Galilee 
(Northern Israel); only one was from Judea (southern Israel where Jerusalem 
was). He was Judas Iscariot, the treasurer of the group and the betrayer of 
Jesus.</p>
<p>Jesus was miraculously conceived of Mary. His Father was 
truly God the Father. After Jesus was born (he is called Mary’s firstborn son), 
Mary and Joseph had four sons and several daughters 
(<a href="javascript:scripture('../../scripture/matt135556.htm')" target=_self  class=pop>Matthew 13:55-56</a>).
The Bible tells us that his brothers did not believe in Him till after the Resurrection 
(<a href="javascript:scripture('../../scripture/john75.htm')" target=_self  class=pop>John 7:5</a>, 
<a href="javascript:scripture('../../scripture/1cor157.htm')" target=_self  class=pop>I Corinthians 15:7</a>).
Two of those brothers, James and Jude, wrote Bible books bearing their names.</p>
<p>The Bible implies that Jesus’ brother James led the 
Jerusalem church until his death. Historical sources tell us that the next 
leader was Simeon, Jesus’ cousin. Thirteen other pastors led this church until 
the Bar Kochba revolt of 135 A.D. Then the relatives of Christ were persecuted 
because they were part of the Davidic line.</p>
