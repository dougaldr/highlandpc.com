<p>When Christ died, three of 
the Gospels mention that the curtain (veil) of the Temple was torn in two, from 
top to bottom. This curtain separated the two inside compartments of the 
Temple, the Holy Place from the Holy of Holies, or “Most Holy Place.” The Ark 
of the Covenant used to rest in the Most Holy Place, but it had disappeared 
centuries before. (The Ark never rested in the temple built by Ezra and being 
remodeled during Christ&#039;s day.) On Yom Kippur, the Day of Atonement, the high 
priest would enter into the Most Holy Place and sprinkle blood on the floor 
where the Ark would have been. Practically speaking, the Most Holy Place 
represented the Throne Room and special presence of God.</p>
<p>The curtain that separated 
these two compartments was a whopping four inches thick! It was sixty feet long 
and thirty feet wide. Although not stated in Scripture, the implication is that 
it was torn by the hand of God, signifying that through the death of Christ we 
may now directly and boldly approach God.</p>
<p>Also worth noting are some 
Jewish records of events that coincide with the ministry and/or death of Jesus. 
Messianic Jewish authority Rachmiel Frydland discusses how the Talmud, a 
collection of ancient Jewish writings, mentions some mysterious events. Keep in 
mind that the Temple was destroyed in 70 A.D. and that Christ died somewhere 
between 30 and 33 AD (I prefer the 33 AD date) and that His earthly ministry 
began in 30 AD (or 27).</p>
<blockquote>Forty 
years before the Temple was destroyed the following things happened: The lot 
for the Yom Kippur goat ceased to be supernatural; the red cord of wool that 
used to change to white (as a symbol of God’s forgiveness) now remained red and 
did not change...the western candle in the candlestick in the sanctuary refused to 
burn continually while the doors of the Holy Temple would open of themselves... 
(Tractate Yoma 39:b).</blockquote>
<p>Frydland continues:</p>
<blockquote>There 
is another saying of the Rabbis in the same Tractate of the Talmud: Why was the 
first Holy Temple destroyed? Because of three things: idol worship, adultery, 
and murder. But in the second Temple in which time the Jewish people were 
occupied studying the Torah and doing good deeds and acts of charity, why was it 
then destroyed? The answer is: It was because of hatred without a cause to 
teach you, that hate without a cause is equal to these sins and that it is as 
serious a crime as the three great transgressions of idol worship, adultery, and 
murder (Yoma 9).
<p>The Talmud does not answer the question, &quot;Whom did we hate without a cause?&quot; 
If we hated the Romans, surely there was cause for it, as they were pagans bent 
on destroying us physically, spiritually, and morally….. Could the quotation in 
the Talmud be a veiled allusion to the One who...gave His life for their 
	salvation....</blockquote>
	
