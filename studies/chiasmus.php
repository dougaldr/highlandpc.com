<p>The four prophetic poems in 
<b>Luke</b> are not merely random accounts of history (though they are historical), but 
take us into a direction. In the Magnificat 
(<a href="javascript:scripture('../scripture/luke14655.htm')" target=_self class=pop>1:46-55</a>), 
the theme is that God loves to exalt the humble (like Mary) and humble the exalted; thus, He will 
provide salvation for his humbled, subjugated nation, Israel. In Zechariah's 
prophetic poem, the Messiah era is here, and John the Baptist will prepare the 
way of the Messiah who will deliver Israel, and, it is implied, others who dwell 
in darkness. The angelic song 
(<a href="javascript:scripture('../scripture/luke214.htm')" target=_self class=pop>2:14</a>) 
implies that Jesus is the focus of God's glory, and that God has a special gift of 
&quot;peace&quot; for those He favors (possibly 
a reference to the elect). The last poem, Simeon's blessing 
(<a href="javascript:scripture('../scripture/luke22932.htm')" target=_self class=pop>Luke 2:29-32</a>) 
extends the work of the Messiah as a &quot;light to the gentiles,&quot; (quoting 
<a href="javascript:scripture('../scripture/is426.htm')" target=_self class=pop>Isaiah 42:6</a>).</p>
<p>The theme: <b><u>Jesus 
fulfills the promises made to Abraham, and those promises extend not only to 
Israel, but also to all those &quot;living in darkness.&quot;</u></b> </p>
