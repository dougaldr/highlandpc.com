<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Origin of the Bible, Part III: The Prophets and Writings
<br><span class=byln>by Ed Vasicek</span></p>

<p>In previous installments, we noted how that the Scriptures 
form a unity. Believing in one portion of the Scriptures leads you to the 
rest. We also demonstrated that writing existed well before the time of Moses 
and that events recorded in the Torah (first five books of the Bible) have 
significant archaeological backing, including the recent discoveries of Mt. 
Sinai, the shelf in the Red Sea where the Israelites crossed, and now quite 
possibly Noah's Ark. (See note below.)</p>
<p>But what about the historical books and the prophets? 
What's the lowdown? We have to do a bit of wading through some scholastic muck 
and mire and concentrate to answer this question. We recognize the historical 
and prophetic books as Scripture based upon the facts that 1) they were 
accepted by the Jewish people centuries before the time of Christ, 2) Christ 
Himself unashamedly accepted them (<b>Luke 24:44</b>: &quot;This is what I told you while I 
was still with you: Everything must be fulfilled that is written about me in the 
Law of Moses, the Prophets and the Psalms&quot;), and 3) the apostles accepted these 
books and used them as Scripture. For some of us, that settles it. But some of 
us want to go further. What external evidence can we offer?</p>
<p>Mention of a number of the prophets and their times are 
recorded within the historical books (<b>Kings</b>, <b>Chronicles</b>, <b>Ezra</b>, etc.). The 
prophecies themselves seemed to have been registered shortly after they were given, based 
on a statement found in <b>Ezekiel 13:9</b>, &quot;My hand will be against the prophets who 
see false visions and utter lying divinations. They will <b>not belong to the 
council of my people or be listed in the records of the house of Israel</b>, nor 
will they enter the land of Israel. Then you will know that I am the Sovereign 
L<span class="sm">ORD</span>.&quot; The evidence that the prophets registered their writings and that copies 
were sent to the reigning kings and officials is both 1) implied by the Bible 
and 2) part of the Middle Eastern culture of the time.</p>
<p>K.A. Kitchen, Professor Emeritus at the University of 
Liverpool is considered perhaps the world's leading authority on Egyptology, 
Archaeology, and Oriental Studies. In comparing Old Testament prophets with the 
religions and &quot;prophets&quot; of neighboring countries, Kitchen concludes: &quot;Thus 
throughout the centuries, across the biblical [Middle Eastern] world, the 
firsthand external evidence shows clearly and conclusively that the record of 
prophecies among contemporaries [of the Biblical prophets] and their 
transmission down through time was not left to the memories of bystanders or to 
the memory-conditioned oral transmission…&quot; (p. 392). Kitchen explains, 
&quot;…officials invariably relayed them [the prophetic pronouncements] promptly back 
to the royal palace—not orally, <b>but in writing</b> and sent on with the 
least possible delay&quot; (p. 390<i>, On The Reliability of the Old Testament,</i> 
2003, Eerdmans). If you are really interested in this subject, this is the book 
to get.</p>
<p>The prophets themselves recognized the writing of their 
fellow prophets to be Scripture: &quot;.... in the first year of his reign, I, 
Daniel, <b>understood from the Scriptures, according to the word of the L<span class="sm">ORD</span> 
given to Jeremiah the prophet</b>, that the desolation of Jerusalem would last 
seventy years&quot; (<b>Daniel 9:2</b>). Note that Daniel had a written copy of Jeremiah's 
prophecy (and may have even met him) and that Daniel recognized Jeremiah's 
prophecy as Scripture!</p>
<p>We know that the threefold division of &quot;the Law, the 
Prophets, and the Writings&quot; predated the time of Christ as a description of the 
Old Testament canon. Modern Jewish Bibles still use this tri-fold divisional 
scheme. Additionally, these books were translated into Greek in 250 B.C. This 
translation, done by Jewish rabbis, was completed by 70 scholars, and thusly 
named the &quot;Septuagint,&quot; abbreviated by the Roman numeral for 70, &quot;LXX&quot;. The 
majority of Jews who had become Greek speakers rather than Hebrew speakers used 
the Septuagint. The early church used the LXX until Jerome translated the Bible 
into Latin in the 4<sup>th</sup> century.</p>
<p>Although we have already addressed the controversy over the 
Apocrypha, we need to understand that the canon was settled before the time of 
Christ. It is true that in 90 A.D. in the town of Jamnia/Yabneh, the Rabbis 
gathered to debate whether Ecclesiastes and Song of Solomon should remain in the 
canon. They agreed that these books should be included, but note this:</p>
<p class="MsoNormal" style="margin-top:0in;margin-right:27.0pt;margin-bottom:
0in;margin-left:.25in;margin-bottom:.0001pt">&quot;The witnesses to the OT canon are 
constant throughout the 1<sup>st</sup> century and early Christian history. The 
understanding of the 39 books of the OT as canonical is seen in Josephus, Philo, 
Ben Sira, Qumran (150 BC), Melito (170 AD), Baba Batra (before AD 200), Origen 
(before AD 231)&quot; (source: <i>The Old Testament in Early Christianity</i> by E. 
Earle Ellis. Grand Rapids: Eerdmans, 1991, pp. 6-33). </p>
<p>Shortly after the time of Christ (about 100A.D), the Jewish 
historian Josephus wrote: &quot;From Artexerxes to our own time the complete history 
has been written but has not been deemed worthy of equal credit with the earlier 
records because of the failure of the exact succession of the prophets.&quot; ... &quot;We 
have not an innumerable multitude of books among us, disagreeing from and 
contradicting one another, but only twenty-two books, which contain the records 
of all the past times; which are justly believed to be divine...&quot; (Flavius 
Josephus, Against Apion 1:8). Please note that the Jewish Bible is the same as 
our Old Testament, but they arrange the books differently (the 12 minor prophets 
as included as one book &quot;The Twelve,&quot; <b>Ezra</b> and <b>Nehemiah</b> are in one book, and 
<b>Lamentation</b>s is included as part of <b>Jeremiah</b>, <b>Ruth</b> is part of 
<b>Judges</b>, etc.).</p>
<p>According to <i>Easton's Bible Dictionary</i>, speaking of 
<b>Ezra</b>, we read: &quot;… there is no reason to reject the constant tradition of the 
Jews which connects his name with the collecting and editing of the Old 
Testament canon. The final completion of the canon may have been, and probably 
was, the work of a later generation; but Ezra seems to have put it much into the 
shape in which it is still found in the Hebrew Bible.&quot;</p>
<p>In the Babylonian Talmud, Baba Batra indicates that the 
leaders of the Great Synagogue (in about 400 B.C.) were responsible to collect 
and copy the Scriptures in the tradition of Ezra. It is very possible that Ezra 
had the job of settling any controversies as to which books were to be included 
in what is now our Old Testament.</p>
<p>Many Jews believed that the era of the prophets ended 
during the time of Ezra with Zechariah and Habakkuk. The rabbis said that God 
no longer spoke audibly through the prophets, but only through dove-like cooing 
heard by men of God.</p>
<p>The historical books of the Old Testament refer to 
historical events that have been verified by the archeologist's spade. I will 
mention some of these in my <b>Exodus/Numbers</b> series coming up, but we have 
independent witness of the Egyptian King, Shishak, invading Israel 900 B.C., 
King Omri, King Ahab, and King Jehu are mentioned in Assyrian texts, as are many 
later kings and historical events within Israel.</p>
<p>The historical books have proven themselves accurate time 
after time. True, there are a few problems here and there, and reconciling 
differences often leads back to my oft-repeated axiom, &quot;truth is not whole 
truth.&quot; But they are remarkable books indeed.</p>
<p>The prophets are remarkable not only for their historical 
accuracy, but also for the tremendous nature of the prophecies themselves! A 
study in 
<a href="javascript:scripture('../scripture/is53.htm')" target=_self class=pop>Isaiah 53</a> or 
<a href="javascript:scripture('../scripture/dan92526.htm')" target=_self class=pop>Daniel 9</a> 
can blow your mind. Although many religious 
writings have prophecy, there is nothing comparable to the detailed prophecies 
of the Old Testament.</p>
<p>In summary, the Jewish prophets—like the &quot;prophets&quot; in 
Assyria or Egypt, did not merely prophesy and depend upon oral tradition to 
preserve their words. They were considered too important; they were written 
down and a copy sent to the palace. The likelihood is strong that Ezra, the 
author of a Biblical book, settled any major controversies as to what did or did 
not belong in the first testament. The writings, including the historical 
books, bear the marks of authenticity; they record incidents, names of cities, 
and histories of kings, information that had been lost for centuries but have 
been, are, or will be discovered by the archeologist's spade. Jesus and the 
apostles appeal to the prophets as God's spokesman, and refer to their writings 
as Scripture and the Word of God.</p>
<p>It is reasonable to believe that the Old Testament writings 
and prophets can be traced back to ancient times in Israel, and it is reasonable 
to believe that their writings are accurate. Additionally, the nature of 
prophecy is such that it is reasonable to believe that the Old Testament is 
supernatural.</p>
