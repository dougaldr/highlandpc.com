<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 1
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are our strength. We freely admit that we are not 
adequate even for the limited tasks You have for us. We are incapable of living 
the Christian life in our own power, incapable of producing the kind of works 
that please You, indeed, we are helpless even as far as our own salvation 
goes. But when we were helpless, at the right time, Christ died for us. By 
Your Spirit, we can live the Christian life and have victory. We dare not fight 
our spiritual battles in our own name, but rather in the Name of Jesus Christ 
our Lord.</p>
<p>Lord, I have some disappointments and burdens in life. A 
few of them hurt me and have left scars including: __________.</p>
<p>I turn them over to You. Help me to find Your purpose and 
joy in life. There are many things I cannot change, but I can share all my 
thoughts with You. Thank You that You do not stifle me or pressure me to &quot;get 
over it.&quot; You are a God Who feels with me. </p>
<p>Lord, Your Word says it is appointed for me to die once, 
and after that, the judgment 
(<a href="javascript:scripture('../../scripture/heb927.htm')" target=_self class=pop>Hebrews 9:27</a>). 
Help me to realize that I can only 
earn rewards for glory now. When I am in heaven, I will enjoy You, worship You, 
and fellowship with You and with all who know You. But my work for You can only 
be done in this lifetime. I will not be reincarnated. Help my friends and 
family to understand that they need to accept Christ now because after death it 
is too late.</p>
<p>And so, Lord, I ask You to give me a burden and heart for 
the lost. Help me to share my faith, to support missions, and to care about 
those who do not know my Savior.</p>
<p>Sometimes, Lord, I feel unworthy sharing my faith with 
others because there are still frontiers in my life where Your Word has not 
penetrated. Stubborn sins and unexamined portions of my heart remind me that it 
is by Your grace, not my works, that I am accepted by You. As I share my faith, 
help me make it clear that I am but a sinner saved by grace.</p>
<p>Today I remember the nation of Congo in prayer. Thank You 
that nearly 14% of the people there claim to be born-again. They have survived 
over 20 years of communism and war since the demise of Marxism. Help them 
rebuild the congregations and catch up with ministry toward the young. Help 
those who have compromised their faith to dedicate themselves anew to serving 
You. Help the church to train leaders, and we pray that the church in Congo 
would grow in numbers, but even more so in depth.</p>
<p>Lord, I pray for SIM (formerly Sudan Interior Mission). 
Thank You for their many missionaries and their outreach particularly in Africa, 
but also in South America and Asia. Bless them as they, &quot;are 
committed to evangelism and starting churches. Where a church exists...to 
strengthen the church through teaching the Bible, developing leaders, and 
equipping the church to fulfill Christ's Commission.&quot; Help the missionaries to 
get along with one another in harmony, may Your Spirit provide much fruit and 
use these missionaries, and may the churches become truly indigenous and 
self-supporting. Provide for their needs, safety and harvest.</p>
<p>Bless the chemists in our society. May they regulate their 
work with Biblical ethics, and may many come to know you. Help the Christian 
chemists to use their position to reach others in this field.</p>
<p>Now help me serve You today, I pray, in Jesus' Name, Amen.</p>

