<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 12
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty God,</p>
<p>My Lord Jesus Christ is my great High Priest. He does not qualify to be a 
priest after the order founded by Aaron. No, His priesthood is after the order 
of Melchizedek, an order that preceded Aaron's. As a matter of fact, Aaron's 
forefather, Abraham, paid tithes to Melchizedek! As a sinless high priest, Jesus 
did not have to offer a sacrifice for his own sins. No, He had and has direct 
access to You. And He did not offer sacrifices of bulls and goats to make us 
merely ritually clean so that we could participate in the religious life of 
Israel. No, His sacrifice cleansed our consciences and our souls. He offered 
Himself! He is both the priest and the sacrifice. And His sacrifice only needed 
to be offered once for all time since the infinite Son of God is joined to the 
human nature of Jesus so that an infinite price has been paid. The sacrifice of 
Jesus is more than adequate to save all.</p>
<p>Father, help me remember not to take my own revenge, but to leave space for 
You to do so. Although I can confront or claim the protection offered me by law 
or policies, many things fall in-between those cracks. I am tempted to get even. 
But, Lord, by not getting even, I allow You to deal with matters thoroughly. </p>
<p>Lord, I have some specific requests to bring before You today. They are: __________.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I find my 
strength in His power not my own. May Your Spirit control me today.</p>
<p>Father, help me to be a person who thinks ahead. Help me learn the principle 
of counting the cost, redeeming the time, wisely making decisions and keeping 
the commitments I make. Keep me from procrastinating and help me to pace myself.</p>
<p>O Father, today I pray for the six union territories of 
India with its 2 million people scattered over scores of islands. Father, these 
people are virtually unreached. Just a tiny fraction of one percent claims any 
kind of Christianity at all. O Father, may You grant the repentance that leads 
to life for many of these people. Send forth laborers, use the Jesus film, 
literature and Christian radio. O God, do a work among these small island 
peoples, I pray.</p>
<p>I remember South America Mission and their vision &quot;to see 
the Church of Jesus Christ established so that thousands of local, witnessing 
communities of believers are maturing in their own knowledge and faith, while 
working to ensure that everyone in South America has multiple opportunities to 
hear a clear, meaningful and persuasive presentation of the gospel from within 
their own ethnic or social group.&quot; Bless their evangelistic efforts, their 
discipleship training and leadership training. Raise up strong lay leaders with 
the believing community. Provide for the missionaries, empower them with Your 
Spirit, help them work in harmony and may they see many brought into the 
Kingdom of God and brought to spiritual maturity to Your glory.</p>
<p>My King, help me a person of integrity. Keep me honest, I 
ask in the Messiah's Name, Amen.</p>

