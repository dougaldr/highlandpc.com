<p><b>What Does a Missionary Do?</b></p>
<p>by Bruce Moore</p>
<p>Well, I got to thinking recently about some of the things we did when we 
started working with the Colorado Indians in Ecuador back in the mid-1950s. We 
walked for hours along the jungle trails to get to our thatch-roofed home. The 
Indians didn't live in villages, so we walked a lot more all over the widely 
scattered community to make friends, learn the language, and get to know the 
culture. I'd never lived on a farm, but I had to learn how to get hold of a 
horse in the pasture and put a pack-saddle on it, and then walk the long trail 
into town, buy a week's supply of groceries, and get back home. Water? Down the 
hill to the river with a couple of buckets!</p>
<p>But how about real missionary work? To make more contact with the Indians 
(and also get more practice in the language) we opened a tiny general store on 
our front porch so that we could talk with our neighbors when they came to buy 
things. We were even able to give some very basic medical help. After we'd 
learned enough of the language (no classes, textbook or dictionary) we'd sit 
down with an Indian, explain what a Bible passage meant, talk about it, and 
write that meaning down in their language.</p>
<p>Spiritual progress? When we knew enough of the language, we began inviting 
our neighbors to informal home meetings where I used flannelgraph to present the 
Gospel to them. Joyce had excellent opportunities to teach the ladies and the 
children, as well as<br>
homeschooling our own kids. And as time went by some of the Colorados accepted 
Christ as their Savior.</p>
<p>I'll never forget the day when I had the opportunity to talk about the Lord 
to the witchdoctor in the area. When I invited him to accept Christ, his answer 
was that he would never do that and neither would any of his people! Some of you 
there in Kokomo may remember him, Samuel; when he did finally make his decision 
to accept Christ, it was there in Highland Park Church.</p>
<p>As the years went by, more and more Colorados came to the Lord, and by now 
there is a well-established church with faithful and talented Indian leaders! We 
got a portable keyboard that Joyce played and then taught one of the Colorado 
ladies to play. And now, of course, they have the translated New Testament in 
their language, along with portions of the Old Testament.</p>
<p>As the years went by, more and more of our time was spent as translation 
consultants, helping other Wycliffe people with their translations. So now, what 
might I be doing on a typical morning? Here in my office at home, across the 
road from the Wycliffe<br>
headquarters in Dallas, I keep right on working as a translation consultant. The 
Colorado Indian believers are doing very well, and Joyce and I pray for them 
every day, but it's been a year and a half since I've seen one of them.</p>
<p>So, are Joyce and I still doing &quot;missionary work&quot;? True, I expect to be 
making another consultation trip to Ecuador early next year, and I'm looking 
forward to seeing my Colorado brothers and sisters then; but in the meantime 
we're here at home, interacting with translators mainly by email. Is that what 
missionaries do? Well, those translators out there in the different countries 
wouldn't be able to move ahead with their Bible translations if we weren't doing 
that. They depend on us, just like they depend on missionary pilots, survey 
workers, directors, teachers in schools for missionary kids, doctors and nurses, 
secretaries in the mission offices in foreign countries--all sorts of &quot;backup&quot; 
members of the &quot;missionary team.&quot;</p>
<p>We all are so very glad to be able to serve; and we appreciate very much your 
prayers and your financial partnership!</p>
