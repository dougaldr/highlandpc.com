<p id="pgt"><span class="pgh">Prayers for Evangelical Believers:
<br>Specific Events</span>
<br>A Prayer Written for Delphi Employees Who Have Lost Their Jobs
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear God,</p>
<p>The prophet Jeremiah prayed to you and said, &quot;I know, O 
L<span class=sm>ORD</span>, that a man's life is not his own; it is not for man 
to direct his steps.&quot; Sometimes, Father, we forget that. We forget that You 
have a plan for our lives--and the lives of our families and friends.</p>
<p>Lord, You created us with the need to be responsible, to 
work, to provide for ourselves and others. You have closed the door where I now 
work, and I am unhappy about that. I felt secure and was pleased with my job. 
But it is gone. And I feel left in the lurch.</p>
<p>But You, O God, know my thoughts. You know the anxious 
thoughts of my family. And I know that I am not alone. Children, grandchildren, 
husbands, wives--so many are affected by these circumstances which are out of 
my control.</p>
<p>So I trust You, Lord, even though I do not understand. I 
rest in Your will, knowing that You have my best interest at heart. Help me to 
draw near to You at this time--to know You better than I ever have.</p>
<p>Dear God, please give me wisdom as to the next step. What 
should I do? Where should I apply? What parameters should I set? Lord, please 
open up a new door of opportunity for me soon. And I pray the same for my 
colleagues.</p>
<p>Thank You for helping me. In Jesus' Name I pray, Amen.</p>

