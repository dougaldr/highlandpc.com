<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 20
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are not a foreman or a slavedriver trying to get as 
much work out of us as You can. You do not need us, but You take pleasure in 
using us. You love it when we are freely motivated to serve You because we love 
You. Indeed, you have ordained that Your children should be characterized by 
good works 
(<a href="javascript:scripture('../../scripture/eph210.htm')" target=_self class=pop>Ephesians 2:10</a>). 
So You have work for us to do in the realm of 
ministry and service. You want us to love You, but You also want us to love 
others. You want us to use the spiritual gifts You have given us to constantly 
“prove” what Your will is 
(<a href="javascript:scripture('../../scripture/rom1212.htm')" target=_self class=pop>Romans 12:1-2</a>) 
and to view life as a series of 
opportunities to serve You in a variety of ways. Slavedriver, no. Assigner of 
work, yes. Help us to gladly report for duty realizing You are the one Who 
creates the desire in us to serve You. Some of us can remember a time before we 
knew You, when all we thought about was ourselves. We marvel that You have so 
changed us that it is now not just about us but about You and others too!</p>
<p>Lord, help me to carve out some time to contemplate Your 
Word and Your will for my life. Help me to examine my heart, to check in with 
You and see how good I am doing. Help me to take the time to realign the 
priorities of my life to accomplish Your will for me, not necessarily my own. 
When I find that I am really not willing to make changes that I know You want me 
to make, Lord, I am willing to be made willing.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>First John says that if I confess my sins, You are faithful and&nbsp;just to forgive 
me those sins and You will purify me from all&nbsp;unrighteousness (<b>1 John 1:9</b>). I 
confess the sins I am aware of now and I ask Your Spirit to search my heart (<i>Pause 
to admit them to God.</i>).</p>
<p>Today I pray for the Latin American island nation of St. 
Lucia. Thank You that 12.3% of the population claims to be born again and 
evangelical. At the same time, many Christians are nominal in their faith with 
80% of all children born out of wedlock. Pastors are now receiving better 
training and some churches are experiencing revival. Thank You, Lord. May the 
Christians in St. Lucia get “the real disease” and get serious about serving 
Jesus Christ and may the number of evangelicals double over this decade, I ask.</p>
<p>I pray for the missionaries my church supports including: __________. Keep 
them true to the Word, protect them, provide for them 
and empower them by Your Holy Spirit, I ask.</p>
<p>Keep me true to Your Word today I also ask, in Christ’s 
Name, Amen.</p>

