<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 3
<br><span class=byln>by Ed Vasicek</span></p>

<p>God my Father,</p>
<p>You are light, and in You is no darkness at all! You are 
pure, You are overpowering, You are distinct from Your creation. You entertain 
no dark ways. We have a dark side to us, hearts that are deceitful and 
desperately wicked. But You have no dark side, not hidden evils, not corrupt 
ways. You are light, and You shed light upon those who humble themselves before 
You. You expose us—and all creation—for what it is. Because You are light, 
we cannot hide from You.</p>
<p>I thank You, Lord, that Your Spirit is convicting the world 
of sin, righteousness, and judgment. Thank You that Your Spirit also restrains 
evil. We appreciate that, though men and women without Christ are lost, they 
are still in Your image, damaged as that image is because of the Fall. 
Therefore, things are not nearly as bad as they could be.</p>
<p>Lord, help me be a person of discernment. There are many 
false teachings out there, many schemes to trick or confuse me. Help me not be 
gullible but discerning, discreet, and perceptive. Many I know the truth so 
well that I easily recognize error.</p>
<p>Father, I pray for the health needs of friends and family 
members, including: __________. I also pray for a few other needs: __________.</p>
<p>Today I pray for the Asian nation of Bhutan. Less than one 
half of one percent of the people are born-again. Such a small number! We know 
that it is against the law to try to convert a Buddhist or Hindu in the Buddhist 
monarchy. Christians are barely tolerated, demonism abounds, and this nation is 
one of the least evangelized in the world. We know the country is closed to 
missionaries. Help the Christians to share Christ, and help the church there to 
grow exponentially. Bless the evangelists from India who reach out near the 
border areas.</p>
<p>I pray for Crossover Communications as they train and 
multiply church planters in Russia, Georgia, Turkey, Bulgaria, Romania, Moldova, 
and the Ukraine. May their work be effective, their cooperation exemplary, 
their fruit monumental. May Your Spirit pave the way as they seek to establish 
local churches in this region. May the churches flourish, and, dear Lord, 
please raise up individuals for this task.</p>
<p>Today we pray for school principals everywhere. May many 
come to know and follow You, and help them to influence others for Christ. May 
they promote values that honor You and bless others.</p>
<p>And now, Lord, 
remind me of Your presence throughout my day, I pray, in Jesus' Name, Amen.</p>

