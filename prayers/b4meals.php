<p><b><a name="p1"></a>#1&nbsp;
<br>Father in heaven, thank You for providing food for we on earth. We thank You
that You do not give us food merely to keep us alive, nor merely to keep us
healthy, but also to enjoy. You are good to us.</b></p>
<p><b>Thank you, in the Name of the Savior, Amen.</b></p>
<p><a name="p2"></a>#2&nbsp;
<br>Yahweh, Lord of all, we bless Your Name and praise You, that You, the great
God of the universe, care for our family's needs. As we enjoy this meal, help us
to realize that it is you who has given us the privilege of living in this land
of plenty. In Jesus' Name, Amen.</p>
<p><a name="p3"></a><b>#3&nbsp;
<br>Lord of all the earth, we ask You to send laborers into the field around the
world to share the Gospel with those who have never heard. Thank You that we
have heard and believed. We now understand that all of life's blessings come
from You, and so we thank You, in Jesus' Name, for the blessing of food before
us. Amen.</b></p>
<p><a name="p4"></a>#4&nbsp;
<br>Almighty One, You owe us nothing. Yet You bless us so bountifully.&nbsp;
How
grateful we are that our table is bountiful and our stomachs will soon be full.&nbsp;
We thank you that Jesus has filled our souls just as this food will satisfy our
appetite. Thank You, Abba Father. We pray through Christ, Amen.</p>
<p><a name="p5"></a><b>#5&nbsp;
<br>Lord, everyday Your blessings are new. As we sit down to enjoy another meal
together, we express our appreciation for the way You take care of us, day after
day. Thank You for not getting tired of us and always caring. We offer this
prayer in the Name of the Prince of Peace, Amen.</b></p>
<p><a name="p6"></a>#6&nbsp;
<br>You Who Created all, thank You for creating mankind. Thank You for our
existence and for our family. Thank You for providing us with more than we need.&nbsp;
Thank You for the pleasure of eating. Thank You for those who worked to prepare
this meal. We bring our prayer to You in Jesus' Name, Amen.</p>
<p><a name="p7"></a><b>#7&nbsp;
<br>God Most High, we know that You know our hearts. We also know that You know
what we need even before we ask. But thank You that You do not stop with our
needs, but You often graciously bless us with an abundance. Thank You for Your
provision of this food for our family. In the Name of Messiah Jesus, Amen.</b></p>
<p><a name="p8"></a>#8&nbsp;
<br>Father of lights, You who command the heavenly host, thank You for sending
Jesus to die on the cross for our sins. Thank You for raising Him up from the
dead so we could be forgiven. But thank You for also caring for our bodies.&nbsp;
We
appreciate Your supply of this food before us.</p>
<p>We praise You in Christ's Name, Amen.</p>
<p><a name="p9"></a><b>#9&nbsp;
<br>Lord of time, as days come and days go, we thank You that You stay the same.&nbsp;
You are the foundation of our lives and of our family. Your faithfulness is
eternal. So thank You for providing us with food for another day. We remember
those who are going hungry today, and we ask you to bless them with provision as
well. In the Savior's Name, Amen.</b></p>
<p><a name="p10"></a>#10&nbsp;
<br>Father, thank You for giving us a Christian family. We pray that any family
members who do not know you might come to do so. Just as You provide food for
our table, so You provide food for the souls who hunger for You. We are
grateful, in the Messiah's Name, Amen.</p>
<p><a name="p11"></a><b>#11&nbsp;
<br>O Holy God, as we smell the food before us, we are grateful that we do not
need to go hungry. Though sometimes, we must confess, we complain or do not
appreciate our blessings, right now we chose to thank You. Thank You, dear
Father. In the Messiah's Name, Amen.</b></p>
<p><a name="p12"></a>#12&nbsp;
<br>Father, we praise You, the One who sets up kings and topples empires. We
believe You are Sovereign, and in control of all that happens. Thank You for
permitting us to live in a land of abundance and plenty. We praise You, in
Christ's Name, for the food before us. Amen.</p>


