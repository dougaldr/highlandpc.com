<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 15
<br><span class=byln>by Ed Vasicek</p>

<p>Wonderful Savior,</p>
<p>You make me wonder indeed. You are my Savior meaning You 
are the one Who has saved me, who has delivered me from the consequence and 
power of my sinfulness. You have saved me from the divine wrath due me by 
absorbing it Yourself. You have delivered me from separation from God both 
for now and eternity. You bridged the gap between us by sacrificing Yourself. 
You save me completely. You do not help me to be my own Savior, nor do we have 
a &quot;shared plan&quot; whereby you do 80% of the saving and I do 20. No, it is not 
even 99 to 1. It is 100 to 0. The only thing I contribute to my salvation is 
the sin from which I am saved! You are my Savior. All I have to do is to be 
willing for You to save me and take You at Your Word. Lord Jesus, You paid the 
debt in full. I believe it and that settles it.</p>
<p>Father, I wish that all of life were so clear and so black 
and white! Lord, so many times I have to make choices between shades of gray 
and my mental and spiritual eyes often cannot tell them apart. I need Your 
wisdom, for what sometimes seems to be the right choice works damage to my soul 
and what seems the wrong choice works out for good. Most of the time it is not 
that way, but sometimes it is. Only You know the beginning from the end, but 
You want me to think in terms of the long term. So help me look at longterm 
consequences, but also enlighten me with the wisdom from above.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I accept responsibility for them and freely 
bring the sins Your Spirit brings to my mind now: __________.</p>
<p>God, like every one of the billions of people who inhabit 
our world, I am vulnerable. So, Lord, please send angels to watch over me and 
protect me. Keep me from harm and evil of every kind.</p>
<p>Lord, the nation of North Korea is a painful place for Your 
children to live. About 100,000 Christians are imprisoned in labor camps and 
believers suffer intense persecution. Three million Koreans have starved to 
death in the latter 1990s and cannibalism is not unknown because of it. The 
estimated 1.5% of the population who claim to be born-again Christians face 
threats of death and torture. In many ways, conditions in North Korea are 
exactly the opposite of conditions in South Korea which is free, prosperous 
and filled with evangelical believers. Lord, break the bonds of communism and 
totalitarianism. Help believers to be able to evade persecution and when they 
cannot give them strength. Deliver Your people, for their blood cries out 
against their persecutors.</p>
<p>Help those who broadcast the pure Gospel throughout the 
world. Provide for them, and empower their teaching and preaching by Your 
Spirit.</p>
<p>I pray in Jesus' Name, Amen.</p>

