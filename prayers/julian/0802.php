<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 2
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>You are spirit; You do not have a physical form. 
You are everywhere and cannot be restricted to a body. Even though You are 
spirit, You created both the physical and the spiritual and You called it all 
good. When sin infected the cosmos, it affected both the physical and spiritual 
realms. Through His death, Jesus provided a spiritual redemption (realized when 
we believe) and a physical redemption (realized when Christ returns).</p>
<p>Because You are spirit and not limited to body, any form of 
idolatry is offensive to You. You will not be misrepresented as though You 
could be localized or controlled. Even though You are spirit, You describe Your 
feelings, plans, or power in human terms, using human language so we can relate 
to You. You condescend to us. You even use examples of the animal kingdom to 
represent your perspective. For example, Your Word mentions finding shelter 
under Your wings 
(<a href="javascript:scripture('../../scripture/ruth212.htm')" target=_self class=pop>Ruth 2:12</a>).</p>
<p>My heart is heavy for those struggling with substance 
abuse. Not only do such persons find themselves enslaved, but also their 
behavior affects other people. Lord, this epidemic of substance abuse is 
defiling so many minds of men, women and young people created in Your image. 
The mind is such a beautiful thing but it is also central to our being. 
People mess up their minds, mess up their lives and mess up the lives of those 
close to them. Father, I don't have an easy answer, but I pray You would work 
among those I know who have abuse problems. I may be aware of some situations 
while others might be hidden from me. And, please Lord, I know we are all 
vulnerable. Keep me from being enslaved, I ask.</p>
<p>Today I pray for the Asian nation of Singapore, a very 
wealthy nation with a high standard of living. I praise Your Name that 7.8% of 
the population claim to be born-again evangelicals. Although church growth has 
slowed somewhat, the church is still growing and becoming more missions-minded. O 
Lord, please continue the wonderful work that You have started. May Singapore 
become a lighthouse for that part of the world. May the percentage of believers 
double over the next decade and may young people turn to Christ in droves, I 
ask.</p>
<p>Thank You for the great impact of Youth With A Mission (YWAM), 
&quot;an international 
movement of Christians from many denominations dedicated to presenting Jesus 
Christ, to mobilizing as many as possible to help in this task and to training 
and equipping believers for their part in fulfilling the Great Commission. As 
citizens of God's Kingdom [believers] are called to love, worship and obey our 
Lord, to love and serve His Body, the Church and to present the whole Gospel 
for the whole man throughout the whole world. YWAM is broad-structured and 
decentralized with operating locations linked together by relationship, shared 
values, accountability to international leadership and a commitment to world 
evangelization.&quot; O Lord, continue to bless this vital ministry! Grant them 
workers harmony, the power of Your Spirit, provision, protection, great wisdom 
and fruit for their labor, I pray. </p>
<p>Guide me with Your wisdom today, I request in the 
Messiah's Name. Amen.</p>

