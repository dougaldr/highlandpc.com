<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 9
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are my strength, O Lord. I am not adequate for the high calling and great 
trust You have given me. I cannot live the Christian life in my own power; I am 
incapable of producing the kind of works that please You. Were it not for Your 
grace, I could not even approach Your throne. Yet, O Lord, You have saved me by 
Your grace and given me the status of perfection. At the right time, Christ 
died for me. By Your Spirit’s indwelling, baptism and filling, I can live the 
Christian life and experience victory over the world, the flesh and the devil. 
I dare not fight these spiritual battles in our own name, but rather in the Name 
of my Savior, Jesus Christ.</p>
<p>Today I praise You that the Holy Spirit has sealed me for the day of 
redemption. I bear the royal seal of the King of Kings and none dare open the 
seal but He. I am Yours, Lord, bought with the blood of Jesus Christ. I am not 
my own.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Lord, I pray that You would protect my loved ones and I 
throughout our lives. Send guardian angels to watch over us, I pray. Keep us 
from harm and evil of all kinds.</p>
<p>Father, help me to work for Your Kingdom. Lord, I am 
tempted to do what I have to do to fulfill my daily responsibilities and then I 
am tempted to think that the rest of my life is mine to do as I wish. In a 
sense that is true. You want me to relax, to enjoy myself in wholesome ways 
and to develop social and personal interests. But Lord, You also want me to 
expend effort for Your Kingdom. Help me obligate myself to serve.</p>
<p>Lord, today I pray for Jiangxi Province of China with its 
43 million people. Perhaps 4% of the population is made up of born-again 
evangelicals. I praise You for the areas where the church has multiplied, but 
other areas are very needy. I also remember another province with about the same 
population, the Liaoning Province, with its 43 million. The percentage of 
evangelical Christians is about the same here, too. O Lord, help the percentage 
of evangelical Christians in these provinces double over the next decade, I 
ask. May the Gospel permeate these societies. Give wisdom to the believers and 
empower them by Your Spirit.</p>
<p>I praise You for the ministry of Gospel for Asia as they 
are "planting churches in unreached areas. In each aspect of Gospel for Asia's ministry, the 
primary aim is to plant churches among the unreached—those who have never 
heard the Gospel&quot; and as they are "sending 
native missionaries. Gospel for Asia 
trains and sends native missionaries because they have proven extremely 
effective. They are already familiar with the language and culture and they live 
at the level of the people they serve, thus removing many social barriers.&quot;</span></p>
<p>Father, please use and bless this outreach to win many to Christ. Help them establish
solid churches; empower them by Your Spirit, provide, protect, and grant harmony 
amongst fellow workers, I ask.</p>
<p>Lord, I give this day to You, 
in Jesus’ Name, Amen.</p>

