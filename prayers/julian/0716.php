<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 16
<br><span class=byln>by Ed Vasicek</p>

<p>Yahweh,</p>
<p>When You appear to Isaac, we read, &quot;The L<span class="sm">ORD</span> 
appeared to Isaac and said, 'Do not go down to Egypt; live in the land where I 
tell you to live. Stay in this land for a while, and I will be with you and will 
bless you. For to you and your descendants I will give all these lands and will 
confirm the oath I swore to your father Abraham.'&quot; We see here that You care 
about where we live, when we travel, and the part our current actions play upon 
future generations. We see that life is not just about us, but about Your glory 
and the long-term purposes You have. We also note that You are a God Who makes 
promises and keeps them. So, Father, keep us from looking at only our own 
lifetime, but help us to serve You with future generations in mind and for Your 
glory.</p>
<p>Remind me that, &quot;one generation will commend your works to 
another&quot; (<b>Psalm 145:4</b>). Help me therefore to associate with 
believers who are older than I and to invest my time in the younger generations. Help me 
disciple or influence those who will one day assume the reigns.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Lord, give me a hunger for Your Word. Your Word is sweet 
like honey, nourishing to my soul. There is no famine of the Word of God, but 
if I do not feast on it, there may as well be! Only You, Lord, can give me the 
appetite I need to devour the Scriptures, to digest them, to make them part of 
me.</p>
<p>Father, I pray for the small evangelical church in Qatar. 
Most of the 2.5% of the population who claim to be born-again evangelicals are 
foreigners who moved to Qatar for work, while most of the indigenous population 
is Muslim. Thank You for the few who have made commitments to Christ. Father, 
may many more come to know Jesus. May Your Spirit move mightily over this land 
and we pray that freedom of religion would be a reality in this land.</p>
<p>O Lord, how I praise You for the wonderful ministry of 
Mercy Ships. &quot;Mercy Ships...has operated hospital ships...[and] brings hope and 
healing to the forgotten poor, mobilizing people and resources worldwide and 
serving all people.... Mercy Ships has impacted over 5.5 million people; delivered 
more than $21 million of medical...completed close to 350 construction and 
agriculture projects including schools, clinics, orphanages and water wells; and 
demonstrated the love of God to people in 95 ports in 53 developing nations.&quot; 
Thank You for the &quot;career and short-term volunteers [who] raise their own 
support and pay crew fees to serve. 850 career staff and over 1600 short-term 
volunteers from over 40 nations join together to serve the poor and needy.&quot;</p>
<p>O Father, may those who serve experience harmony among 
themselves and may they not only heal bodies, but also use them to reach people 
for Jesus Christ by Your Spirit, I pray.</p>
<p>Help me to reach out for Christ too, I ask in His Name, 
Amen.</p>

