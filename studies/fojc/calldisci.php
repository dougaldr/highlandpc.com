<div>
<p>There are several different &quot;callings&quot; of Jesus&#039; disciples, and it can be 
difficult to sort them out. Let&#180;s take a look at at least three of them.</p>
<ol type=1><li>Some of the Disciples were called for the first time in <b>John chapter 1</b>. None 
of these men were familiar with Jesus at this time (probably). They were 
disciples of John and responded to Jesus as John pointed Him out as the &quot;Lamb of 
God&quot; and &quot;The one of whom I am not worthy to untie His sandal.&quot; These 
disciples spent a period of time with Jesus and then went back to their secular 
jobs.
<li>A second temporary calling can be found in <b>Mark 1</b> and also in <b>Matthew 4</b>. 
Jesus was calling His disciples to spend a period of time with Him once again.</p>
<li>The third calling took place about TWO YEARS after the initial calling of <b>John 
1</b>. At this point, the disciples were called to leave their work and families 
for a period of about a year and a half and to learn from Christ and work with 
Him on a fulltime basis. This is the calling referred to in <b>Luke 5</b>. For 
simplicity's sake, Luke only mentions this call to full time service and skips 
the initial calls.</p>
<li>The 12 Disciples were later titled, &quot;Apostles.&quot; What is the difference? 
Jesus had MANY disciples. As a matter of fact, most of us reading this page 
are Disciples (followers and learners) of Jesus Christ. Apostles were 
&quot;authoritative representatives&quot; of Christ who helped to found the Church 
(<a href="javascript:scripture('../../scripture/eph220.htm')" target=_self  class=pop>Ephesians 2:20</a>).
They were all Jews 
(<a href="javascript:scripture('../../scripture/matt1928.htm')" target=_self  class=pop>Matthew 19:28</a>),
all witnessed the resurrected Christ 
(<a href="javascript:scripture('../../scripture/1cor91.htm')" target=_self  class=pop>1 Corinthians 9:1</a>, 
<a href="javascript:scripture('../../scripture/acts12122.htm')" target=_self  class=pop>Acts 1:21-22</a>),
and could all work the miracles of an Apostle 
(<a href="javascript:scripture('../../scripture/2cor1212.htm')" target=_self  class=pop>2 Corinthians 12:12</a>).</ol>
<p>
Putting this all together, the Disciples/Apostles got to know Christ for two 
years through temporary meetings with Christ before they committed themselves to 
full time ministry. How much contact they had with their families during 
this time is not clear, but their families were left in good hands. This was 
a truly unique situation and is not intended to be any sort of pattern for 
Church Age discipleship.</p>
<p>
Also note that after the Resurrection, at least for a while, the disciples went 
back to their trades (see <b>John 21</b>). Following Christ does not mean we must 
forsake our careers or families, but it does mean we must put Jesus Christ first 
and be WILLING to follow His lead.</p>
