<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 14
<br><span class=byln>by Ed Vasicek</p>

<p>Father,</p>
<p>After the shepherds heard the good news of the 
birth of the Savior, they hurried to Bethlehem and found the stable where the 
baby Jesus lay. They knew that He was important, that salvation was important 
and that God was to be feared. O Lord, help me and those I love come to those 
same realizations. Help us not put off getting right with You by turning from 
our sins and believing in the Lord Jesus Christ!</p>
<p>Indeed, common shepherds had much more sense than many of 
the highly esteemed and respected men of the day who rejected Jesus. Thank You 
that many highly intellectual people have received Jesus as Savior, but thank 
You that a prepared heart is more important than prestige in the eyes of the 
world. The humble shepherds believed while many of the religious leaders 
doubted.</p>
<p>But Mary pondered these things in her heart. She had 
already been through so much and she probably had a lot of unanswered questions 
and confusion in her mind. But she filed away those spiritual lessons and would 
take time later to contemplate them and catch up to what You were doing. So, 
Lord, I need to learn Your Word and doctrine even if I cannot see its immediate 
relevance to my life. Help me file Your Word away in my heart so that when I 
need it, it will be there.</p>
<p>I pray for my pastor, staff, church board leaders, officers 
and teachers. Help them to be true to the Bible and to put Your Word over their 
own personal agendas. Help them put their walk with You first, their families 
second and their ministries third. Do not allow others to intimidate them into 
neglecting their families and remind them: &quot;the fear of man works a snare.&quot; Help 
them be most concerned about pleasing You while also seeking peace and harmony 
within the congregation. Put a hedge of protection around them, I ask.</p>
<p>Father, I have some personal health concerns including: __________. I also pray 
for the physical, mental, or spiritual health of others 
including: __________.</p>
<p>Today I remember the nation of Mongolia. Though the country 
is massive in size, its population is under 3 million. Yet only one half of one percent 
claim to be born-again evangelicals. Yet in 1989, there were only 4 Christians 
in the land, while the number now might be approaching 10,000 and I praise You 
for that great growth! Unfortunately, cults are also growing. The nation is 
exceedingly poor with only a $390 annual income per person. Bless the various 
outreach attempts: literature, broadcasting, the Jesus film, etc. May Your 
Spirit hover over this land and may the church double over the next five years, 
I ask.</p>
<p>I remember Compassion International in my prayers. 
&quot;Compassion International exists as an advocate for children releasing them 
from spiritual, economic, social&nbsp;and physical poverty&nbsp;and enabling them to 
become responsible, fulfilled Christian adults....Today, Compassion helps&nbsp;more 
than&nbsp;728,800 children in more than 20 countries.&quot; So, Father, provide support 
and safety for the workers, sponsors for children and the blessings of Your 
Spirit. Help the workers to experience harmony with one another and may they 
have Your wisdom as they engage in working for Your Kingdom.</p>
<p>Help me remember Jesus Christ throughout my day, I ask in 
His Name, Amen.</p>

