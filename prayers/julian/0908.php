<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 8
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are the God Who encourages. You give us courage. When 
Joshua was about to lead the people of Israel into the promised Land, You told 
Him, &quot;Be strong and courageous. Do not be terrified; do not be discouraged, for 
the L<span class="sm">ORD</span> your God will be with you wherever you go&quot;
(<b>Joshua 1:9b</b>). When Jeremiah was called to be Your prophet, he was a young 
man and very much afraid. You told Him, &quot;Before I formed you in the womb I 
knew you, before you were born I set you apart; I appointed you as a prophet to 
the nations.&quot;</p>
<p>But Jeremiah was still intimidated. He replied, &quot;'Ah, 
Sovereign L<span class="sm">ORD</span>,' I said, 'I do not know how to speak; I am only a child.'&quot;</p>
<p>But Lord, You replied, &quot;'Do not say, 'I am only a child.' 
You must go to everyone I send you to and say whatever I command you. Do not be 
afraid of them, for I am with you and will rescue you', declares the 
L<span class="sm">ORD</span>&quot; (<b>Jeremiah 1:5-8</b>, <i>passim</i>).</p>
<p>Lord, sometimes life seems too much for me; I feel 
inadequate and frightened. I am intimidated. Help me to remember that You, O 
Lord, are the God Who encourages Your children to do right and to serve You.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. May I&nbsp;find my 
strength in His power, not my own. May Your Spirit control me&nbsp;today.</p>
<p>Today I pray for the evangelical church worldwide, all 
congregations made up mostly of people who claim to have been born-again and 
have a personal relationship with Jesus Christ, who believe in the deity of 
Christ, His death on the cross for our sins, the literal resurrection, who stand 
for the Bible as the inspired and authoritative Word of God and who believe in 
salvation by grace through faith alone, apart from works. Thank You that 
although Christianity has declined slightly as a percentage of the world’s 
population, evangelical Christianity has grown from 84.5 million (2.8%) in 1960 
to 420 million (6.9%) in 2000.</p>
<p>But, Lord, we know that growth does not necessarily mean 
truth. Indeed, sometimes those who follow the truth decline in number. So 
please, Lord, help evangelical Christians to crave Your Word, to value knowledge 
and doctrine and to address the questions of our day with intelligence and 
Biblical convictions.</p>
<p>Lord, the nation of Timor Lorosae, an Asian island nation, 
gained its independence from Indonesia in 1999 and the Indonesian military went 
on a vindictive spree of destruction because of this leaving the new nation in 
a state of upheaval. Lord, thank You for the 2.5% who identify themselves as 
born-again evangelicals; help them to work with the traumatized children and 
young people who are trying to recover from loss of their parents and 
possessions. The people live in deep poverty. Help the Gospel to take root, 
Lord. May the number of believers grow tenfold over the next decade, I pray. 
Provide translators for the language groups that do not have the Bible in their 
own language, I ask.</p>
<p>Lord, bless the missionaries my home church supports, 
including: __________. Keep them true to You and Your Word; grant them safety, 
provision, harmony, fruit and the power of Your Spirit, I ask.</p>
<p>Empower me to serve You today, I pray in Jesus' Name, 
Amen.</p>

