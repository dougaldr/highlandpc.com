<p>In today&#039;s sermon (taken from all 4 Gospels because all 
include and exclude different elements), we will begin contemplating the 
teachings of Christ He shared while in the “Upper Room,” celebrating the 
Passover. This time of teaching would continue as Christ walked with the 
apostles to the Garden of Gethsemane, His final Pre-Resurrection time of 
instruction. John&#039;s Gospel includes much more information about this time than 
do the Synoptic Gospels (Matthew, Mark and Luke).</p>
<p><b><u>Notes of interest</u></b></p>
<p>When Jesus told Simon Peter that Satan asked to “sift him 
like wheat,” Ann Punton informs us about this idiom:</p>
<blockquote>“The grain is brought in a very dirty state, full of 
pebbles, straw, and dirt. Here starts the housewife’s work. Seated on the 
ground, she puts some grain into a large sieve and shakes it sharply to bring 
the pieces of rubbish to the surface. These she removes.... Chaff, straw and dust 
are blown away.... Finally, she sorts through the grain by hand to ensure there is 
no darnel [tares] amongst it.... To sieve or sift a person implies a screening and 
proving process by way of hardship and temptation...” (<u>The World Jesus Knew</u>, 
page 109). </blockquote>
<p>Regarding the “crowing of the rooster,” Matthew, Luke and 
John quote Jesus as saying, “before the rooster crows, you will disown me three 
times.” Mark’s account has Jesus saying, “Before the rooster crows twice, you 
will disown me three times.” Here is another case of truth versus whole 
truth. Mark quotes Christ completely, whereas the other Gospel writers edit 
Jesus’ words to capture only the essential concept.</p>
<p>The respected Bible scholar of another era, R.C.H. Lenski, 
writes, “Two crowings were distinguished as marks of time, one near midnight, 
the other just before dawn. They helped to divide the night into the midnight 
or silent period...and the period after the dawn...” (<u>The Interpretation of St. 
Mark’s Gospel</u>, p. 632).</p>
<p>Another important comment refers to 
<a href="javascript:scripture('../../scripture/luke2235.htm')" target=_self  class=pop>Luke 22:35</a> 
and following. Notice how the purposes of God can affect the nature of our 
perspective on the world. When Christ was offering the Kingdom to Israel, Jesus 
had one standard for His disciples. Now that the church age has been 
interjected into the equation, the rules have changed. Rather than seeing the 
Millennium introduced, Christians are spread through the world and must deal 
with tough situations, including the need for self-defense and financial 
planning.</p>

