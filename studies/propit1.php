<p id="pgt"><span class="pgh">Biblical/Doctrinal Studies:</span>
<br>The Heart of the Gospel: 
<br>Propitiation and Romans 3:24-26 - Part I
<br><span class=byln>by Ed Vasicek</span></p>

<p>Many people find Christianity repulsive because of its 
major tenet: God the Father sent His Son to be punished for our sins. </p>

<p>Jonathan Edwards preached the concept of an angry God 
(burning with rage and on the verge of releasing His wrath) without shame or 
reservation. His fiery sermon, &quot;Sinners in the Hands of an Angry God&quot; is often 
used as an example of what some claim to be a distorted view of God.</p>

<p>But, when measured by the Scriptures, Edwards was right. 
Although most modern theologians prefer to emphasize God's love while denying 
(or deemphasizing) His rage at the sinner, the Bible demands we embrace both 
perspectives: The Holy God Who is raging with anger is also the loving and 
compassionate God Who loves sinners.</p>

<p>This meeting of God's mercy and justice are mentioned 
repeatedly in the Scriptures 
(<a href="javascript:scripture('../scripture/ps85911.htm')" target=_self class=pop>Psalm 85:9-11</a>), 
but these concepts meld together 
most clearly in <b>Romans 3:24-26</b>: &quot;Justified by his grace as a gift, through the redemption 
that is in Christ Jesus, whom God put forward as a propitiation by his blood, to 
be received by faith. This was to show God's righteousness, because in his 
divine forbearance he had passed over former sins. It was to show his 
righteousness at the present time, so that he might be just and the justifier of 
the one who has faith in Jesus.&quot; (ESV)</p>
<p>Bursting with meaning, these three little verses explain 
the heart of the Gospel like no other. The key focus is upon the concept of 
propitiation, which we will define in the second article.</p>
<p>Today we will limit ourselves to <b>verse 24</b> and examine two 
benefits made possible because of Jesus' propitiation: <u>Justification</u> and 
<u>Redemption</u>. We will then note that they come to us on the basis of <u>grace</u>. Let's 
look at these three terms.</p>

<p><i><u>Justification</u> (in Paul's usage) is the legal act in 
which God declares us &quot;not guilty&quot; and &quot;holy.&quot;</i> This is not based on our 
behavior; God justifies us when we are ungodly. A chapter away, in <b>Romans 4:4-5</b>, 
Paul discusses Abraham's justification. We read, &quot;And to the one who does not 
work but trusts him who justifies the ungodly, his faith is counted as 
righteousness.&quot;</p>

<p>Because God justifies the ungodly, this justification 
occurs when we are converted and born-again. The term &quot;ungodly&quot; is never used of 
a true New Testament believer. Although we still sin (and some of our behavior 
may be ungodly), the term refers to our unregenerate condition. We are ungodly 
and a moment later--when we exercise saving faith--we find ourselves 
justified!</p>

<p>The idea that justification occurs at death when we have 
lived a godly life is contrary to Paul's usage here. No, <i>we are justified at 
the point of conversion</i>. God does not justify the godly (the converted), but 
the ungodly. Once justified, the ungodly <i>become</i> the godly and evidence 
that justification by our works. </p>

<p>Every major New Testament teaching has its basis in the 
Hebrew Scriptures. Therefore The New Testament can only be fully comprehended against 
the backdrop of the Old.</p>

<p>Whereas Abraham is an Old Testament example of 
justification by faith, <b>Zechariah 3</b> paints the picture of what justification 
looks like. Justification is receiving a clean status before God with, in this 
case, the pre-incarnate Christ acting as our defense attorney to maintain that 
status!</p>

<blockquote><p>Then he showed me Joshua the high priest standing before 
the angel of the L<span class=sm>ORD</span>, and Satan standing at his right hand to accuse him. And 
the L<span class=sm>ORD</span> said to Satan, &quot;The L<span class=sm>ORD</span> rebuke you, O Satan! The L<span class=sm>ORD</span> 
who has chosen Jerusalem rebuke you! Is not this a brand plucked from the fire?&quot; </p>

<p>Now Joshua was standing before the angel, clothed with 
filthy garments. And the angel said to those who were standing before him, &quot;Remove the filthy garments from him.&quot; And to him he said, 
&quot;Behold, I have taken 
your iniquity away from you, and I will clothe you with pure vestments.&quot; And I 
said, &quot;Let them put a clean turban on his head.&quot; So they put a clean turban on 
his head and clothed him with garments. And the angel of the L<span class=sm>ORD</span> was standing 
by. (<b>Zechariah 3:1-7</b>, ESV).</p></blockquote>

<p>In the New Testament, we discover that this clean garment 
is the righteousness of Christ 
(<a href="javascript:scripture('../scripture/rom322.htm')" target=_self class=pop>Romans 3:22</a>, 
<a href="javascript:scripture('../scripture/1cor130.htm')" target=_self class=pop>1 Corinthians 1:30</a>, 
<a href="javascript:scripture('../scripture/2cor521.htm')" target=_self class=pop>2 Corinthians 5:21</a>). </p>

<p>On to our second term, <u>redemption</u>. The Old Testament 
is filled with redemption imagery. The ethic of redeeming family land is a key 
element in the <b>Book of Ruth</b>. Even Ruth is redeemed from widowhood and 
childlessness. A number of Torah ordinances involve redemption of property and 
people. Although the Levites replaced all Israel's firstborn sons when it came 
to Tabernacle service, those firstborn sons were &quot;redeemed&quot; when the redemption 
money (ransom) was paid. 
(<a href="javascript:scripture('../scripture/num348.htm')" target=_self class=pop>Numbers 3:48</a>). 
<i>Redemption is the payment of a price 
to obtain another's freedom or to restore what was lost. </i>Jesus' death 
&quot;bought us back'&quot; from our lost condition and its consequences.</p>

<p>When Jesus died on the cross, the Greek New Testament says 
He shouted out, &quot;Tetelesthai,&quot; which is usually translated, &quot;It is finished,&quot; 
The Greek Perfect Tense indicates something has happened in the past and carries 
a current result. </p>

<p>Kenneth Wuest translates this phrase as, &quot;It has been 
finished and stands complete.&quot; The word &quot;Tetelesthai&quot; commonly meant &quot;paid in 
full.&quot; The redemption Christ provided (through propitiation) benefits us with a 
favorable status before God. We can say, &quot;If God is for us, who can be against 
us?&quot; (<b>Romans 8:31</b>). </p>

<p>Justification and redemption (provided by propitiation) 
come to us freely by God's <u>grace</u>, our third term. The best way to 
understand what Paul means by &quot;grace&quot; is to look at how Paul uses the word. In 
<a href="javascript:scripture('../scripture/rom116a.htm')" target=_self class=pop>Romans 11:6</a>, 
he contrasts grace to something we do; we need perform no act to 
justify or redeem ourselves. Grace and works are opposites.</p>

<p>In 
<a href="javascript:scripture('../scripture/rom623.htm')" target=_self class=pop>Romans 6:23</a>, 
God freely offers us eternal life as a gift 
(<i>charisma</i>) by His grace (<i>charis</i>) not as a wage. We cannot earn, 
deserve, or achieve it. We receive God's grace when we believe 
(<a href="javascript:scripture('../scripture/rom51.htm')" target=_self class=pop>Romans 5:1</a>, 
<a href="javascript:scripture('../scripture/rom10910.htm')" target=_self class=pop>10:9-10</a>). 
Since faith is an attitude and not a work, and since God even gives us 
the faith to believe 
(<a href="javascript:scripture('../scripture/eph289.htm')" target=_self class=pop>Ephesians 2:8-9</a>), 
salvation is truly &quot;by grace.&quot; <i>Grace 
is God's generosity and favor, and exists apart from anything we do.</i></p>

<p>In our immediate context (<b>Romans 3:27</b>), Paul asks, &quot;Where 
is boasting? It is excluded.&quot;</p>
<p>If we understand grace, Paul's conclusion is obvious! The 
only thing we contribute toward our salvation is the sin from which God saves 
us.</p>

<p>Although salvation is free to us, it is not cheap. Another 
paid our sin bill. Next month, we will see that this redemption was made legally 
possible because Jesus' death <i>propitiated</i> God's justice.</p>

