<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 20
<br><span class=byln>by Ed Vasicek</p>

<p>Mighty Savior,</p>
<p>You are the head of the church, Your body. Church leaders 
answer to You. Although some people think that having a perfect leader would 
make for a perfect organization, such is not the case. You are the perfect 
leader, but Your children do not always follow Your directives. Although most of 
us want to follow You most of the time, Lord, we fail so often. You are the head 
in the sense that You are the leader of the organization. You are also the head 
when we view the church through the illustration of a body. Just as a human 
head, containing the brain, directs the actions of the Body, so You want to 
direct the actions of the church at large and each congregation in particular. 
Help us never get away from the concept of being held accountable to Your 
authority by being accountable to Your Word.</p>
<p>Lord, I pray for our government leaders: our President and 
his cabinet and staff, the Senate, Congress, Judicial System, our Governor, 
state legislators and state officials. I pray for county and municipal leaders 
and officials. Father, fill these offices with those who will accomplish Your 
purposes. Help us to be able to practice and share our faith freely and 
peacefully. </p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may fill me. I 
find my strength in His power not my own. May Your Spirit control me 
today.</p>
<p>Your Word tells me that if I confess my sins, You are faithful and&nbsp;just to 
forgive me those sins, and You will purify me from all&nbsp;unrighteousness (<b>1 John 
1:9</b>). I accept responsibility for them and freely bring the sins Your Spirit 
brings to my mind now: __________.</p>
<p>Thank You for the European nation of Latvia. I am so 
grateful that Latvia is now free. As they recover from the harsh years under 
communism, may many turn to You. Thank You that 7.6% of the population claim 
to be born-again evangelicals. May that percentage double over the next 
decade. Provide training for Christian leaders and bless the broadcasts on 
radio and TV by Bible-believing Christians. Thank You for the blossoming youth 
ministries. May Latvia become a key base for evangelical Christianity in the 
years ahead!</p>
<p>I bless Your Name for the ministry of the Brazil Gospel 
Fellowship Mission. I am &quot;grateful that there are over 40 established national 
churches with Brazilian pastors. Five Brazilian families are serving as home 
missionaries and one young lady is serving on the Amazon, all supported by 
Brazilian churches.&quot; May &quot;the seminary, Bible institute, Priscilla 
Training School for Women and Theological Education by Extension...continue in 
the important task of training believers for service.&quot; Thank You for the &quot;23 
families and three single ladies serving as missionaries...in Northeast 
Brazil.&quot; Continue to expand and provide for this ministry, empower them by Your 
Spirit, grant them harmony and fruit.</p>
<p>Now help me to display the fruit of the Spirit today, I 
request in Messiah's Name, Amen.</p>

