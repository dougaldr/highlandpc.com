<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>July 5
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Even before I was saved, it was obvious that You 
exist. My innate instincts told me so; the fact that I have a conscience means 
that I recognize the concept of law and if there is a law, there must be a 
lawgiver. The complexities of nature are beyond mere chance. And I 
instinctively know that things like love, compassion, or the ability to dream 
dreams implies a reality beyond the mere physical. But Lord, apart from these 
evidences, it took the Gospel message to bring me into a true relationship with 
You. Christ died for my sins, was buried and rose again! When I turned from my 
sin and turned to Christ in faith, I began a new life.</p>
<p>Lord, it burdens me that some of my loved ones and friends 
do not know You. They have not been regenerated; they know nothing of a 
personal relationship with You. To them, You are either a God to be kept at 
bay, or they only want You when they need You. Please, Lord, grant them the repentance that leads to life!</p>
<p>Father, when I came to know Christ, I was blessed by You to 
be led to a Bible-believing Bible-teaching, and mind-building church. I did 
not have to check my mind at the door, but neither did they compromise Your 
Word. I found satisfying answers to my questions and I eventually developed 
some good Christian friendships that nurtured my walk with You. Lord, many of 
Your children are not part of a spiritual family. They are missing so much. 
Lord, every church on earth is imperfect, but some at least try to take Your 
Word seriously. Lead them to such a church, I pray.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart (<i>Pause to admit them to God.</i>).</p>
<p>I pray for parents of children who have gone astray. Lord, 
bring them to Yourself if they do not know You; if they do, may they return as 
did the prodigal son, I pray.</p>
<p>Lord, the Palestinian Authority is a mess of a region. 
Father, may peace prevail. Thank You for the .1% who claims to be born-again 
evangelicals while over 86% of the population is Muslim. The instability makes 
it difficult for Christians to witness—even difficult to be safe. Lord, may 
many of these people come to know Jesus. Work out Your will in this area, for 
matters are complicated.</p>
<p>Lord, thank You for Hellenic Ministries. They desire &quot;to 
see Greece evangelized and the Greek Church mobilized for cross-cultural 
missions service. [Their] motto is 'Christ for Greece and the Nations'. (HM also 
seeks to see Greece as a stepping-stone for ministry beyond the Mediterranean.) 
HM's ministry distinctives are Evangelism, Mercy Ministries, Discipleship and 
support of the local indigenous church.&quot; Lord, bless this work, empower the 
missionaries, grant them harmony, safety, provision and much fruit to Your 
glory!</p>
<p>Bless me today, I ask, in Jesus’ Name, Amen.</p>

