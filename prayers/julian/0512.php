<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 12
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty God,</p>
<p>My Lord Jesus Christ is my great 
High Priest. He does not qualify to be a priest after the order founded by 
Aaron. No, His priesthood is after the order of Melchizedek, an order that 
preceded Aaron's. As a matter of fact, Aaron's forefather, Abraham, paid tithes 
to Melchizedek! As a sinless high priest, He did not have to offer a sacrifice 
for his own sins. No, He had and has direct access to You. And He did not 
offer sacrifices of bulls and goats to make us merely ritually clean so that we 
could participate in the religious life of Israel. No, His sacrifice cleansed 
our consciences and our souls. He offered Himself! He is both the priest and 
the sacrifice. And His sacrifice only needed to be offered once for all time 
since the infinite Son of God is joined to the human nature of Jesus so that an 
infinite price has been paid. The sacrifice of Jesus is more than adequate to 
save all.</p>
<p>Father, help me remember not to 
take my own revenge, but to leave space for You to do so. Although I can 
confront or claim the protection offered me by law or policies, many things fall 
inbetween those cracks. I am tempted to get even. But, Lord, by not getting 
even, I allow You to deal with matters thoroughly. </p>
<p>Lord, I have some specific 
requests to bring before You today. They are: __________.</p>
<p>Lord, I am thankful for many 
things, three of which are: __________.</p>
<p>Lord, I empty myself before You 
so that Your Spirit may fill me. I find my strength in His power not my own. 
May Your Spirit control me today.</p>
<p>Father, help me to be a person 
who thinks ahead. Help me learn the principle of counting the cost, redeeming 
the time, wisely making decisions and keeping the commitments I make. Keep me 
from procrastinating and help me to pace myself.</p>
<p>Today I pray for the Andhra 
Pradesh state of India with its almost 80 million people. Although this area 
has seen growth over the decades, it has been in spiritual decline. Many 
Christians have a nominal faith and some have been converted back to Hinduism. 
This is the only state in India in which the Christian population has declined. 
Although 1.9% of the population claim some sort of Christianity, at one time it 
was up to 4.2% in 1971. Lord, reverse the negative trends with strong positive 
ones. Many areas have no Gospel outreach. Please, Lord, do a great work here.</p>
<p>Thank You, my God, for 
Ambassadors for Christ, whose mission is to “evangelize and disciple&nbsp;Chinese 
students and professionals in the United States and other parts of the world, to 
motivate and equip them to impact the culture for the Lord and to mobilize and 
channel them into the service of Christ as a vital force for God's Kingdom.” 
Bless their work, grant them harmony and the power of Your Spirit. &nbsp;Provide for 
the workers, and help them to develop many disciples for Jesus Christ.</p>
<p>Now, Lord, please send angels to guard me today, I ask in the Name of the Savior, Amen.</p>

