<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 6
<br><span class=byln>by Ed Vasicek</p>

<p>My Great King, Helper of the helpless,</p>
<p>Thank You, Jesus Christ, that You are God. You are the Son, the Word and the 
exact representation of the Father. You are also a sinless man. You are the 
God-man, the Savior of the World. Your divinity had no beginning but is 
eternal. As God the Son, You have always existed. Your humanity had a beginning 
within the virgin's womb. But even Your humanity is special, for Your human 
nature is the firstborn of all creation—it has the preeminence. And from the 
time Your human nature was conceived, Your two natures are united in one Person. 
We cannot understand the mystery of it all; we can merely define the boundaries 
Your Word gives us!</p>
<p>But Lord Jesus, You experienced all this for a reason, to be our Redeemer and 
Savior—as part of a plan to glorify the Father, Son and Spirit. You were the 
Lamb slain before the foundation of the world. All was created around Your 
sacrifice and resurrection to Your glory.</p>
<p>Lord, use my mouth to bless others. Help me remember that, &quot;Pleasant words 
are a honeycomb, sweet to the soul and healing to the bones&quot; (<b>Proverbs 16:24</b>). 
Help me remember that it is easier to damage others with my words than it is to 
build them up!</p>
<p>Lord, sometimes I have not because I ask not, so I am asking. Please meet the 
following needs, requests and desires as You see fit: __________.</p>
<p>Help me with my thought life today. Help me think positive and pleasant 
thoughts today, thoughts that please You.</p>
<p>Lord, I pray that You would strengthen the marriages in my family, among my 
friends and within my church family. Put a hedge of protection around them and 
help these folks to be determined to make their marriage work. Send help to them 
and may they avail themselves of books, seminars and counseling. Help them build 
their marriages upon You.</p>
<p>Today I pray for the Tibet-Xizang Autonomous Region of 
China with its 2.5 million people. Although perhaps only .1% of the population 
is made up of evangelical believers, this is a great improvement over the past. 
Although Tibetan Buddhism runs strong in this region, thank You, Lord, for the 
hearts You have opened. Bless the Jesus film, Christian radio, and the 
testimony of the few who already know You. Grant many the repentance that leads 
to life, I ask. Multiply the number of believers tenfold over the next decade, 
I pray.</p>
<p>Lord, thank You for the ministry of SIM. &quot;SIM: Serving In Mission has about 
1,600 active missionaries, from 37 countries serving in over 40 countries in 
South America, Africa and Asia.... Our distinctive is not our methodology but our 
church-focused vision. We are 
seriously involved in Bible translation, hospitals, radio, development and 
aviation, but all these are a means to plant, strengthen and partner with 
churches. Our focus is the church, not a strategy.&quot;</p>
<p>Father, strengthen and use this mission agency. Grant the 
workers safety, provision, harmony, the power of Your Spirit and much fruit, I 
pray.</p>
<p>Lord, You have not called all Your children to be 
missionaries in the sense of crossing cultures, but You have called us all to be 
witnesses. Help me to be a faithful witness of Your grace, I pray in Jesus’ 
Name, Amen.</p>

