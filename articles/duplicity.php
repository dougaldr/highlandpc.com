<br>Duplicity and Integrity in the Body
<br><span class=byln>by Ed Vasicek</span></p>

<p align="center"><b>The integrity of the upright guides them, but the unfaithful are
destroyed by their duplicity.</b>
<br><b>Proverbs 11:3</b></p>
<p>Webster defines duplicity as &quot;deception by pretending to feel and act
one way while acting another.&quot; Duplicity is related
to insincerity. Insincerity is a major difficulty in the
Christian world, and often occurs when believers sense
peer pressure to act and think in a godly way. Such peer
pressure can be valuable, but it can also drive our sinful ways and thoughts
underground. Social pressure cannot transform our hearts; all it can
do is modify our outward behavior (at least when we think we are likely to be
caught).</p>
<p>In the Christian world, insincerity works this way: We know we
ought to be a certain way. We are not that way.&nbsp;
We lie to
ourselves and tell ourselves we are the way we ought to
be. Then we tell others the lie we told ourselves,
thinking we are speaking honestly. Since we have expressed what
we think, we do not consider ourselves liars. But liars we are&#151;whether
we lie to ourselves first or not.</p>
<p>I believe it is worse to lie to yourself than it is to fib to
others. When you lie to others, you are conscious of it.&nbsp;
When you are dishonest to yourself, it can become such an
ingrained habit that you lose consciousness of it. That is
lying in its most sinister, devious form. Such deceit often
disguises itself as conviction, the &quot;leading of the Spirit,&quot; or even
Christian love.</p>
<p>Those who lie to themselves often makes statements such as
these: &quot;I'm not angry, just hurt.&quot; &quot;No,
everything's okay. I'm not upset.&quot; &quot;No, it doesn't
matter to me.&quot; &quot;I'm easy to please.&quot; &quot;I can accept things
either way.&quot; &quot;I'm a good loser.&quot;&nbsp;
&quot;I'll
support the decision either way.&quot; &quot;What matters
is the good of the organization.&quot;</p>
<p>We know we should be good losers. We believe we should be
mature enough to back our leaders even when we disagree
with them. We think we should not allow little things to
bother us, but sometimes they do bother us. Sometimes we
are not good losers, but we are too proud to admit it. So, with
deceitful words, we hide our true thoughts and feelings.</p>
<p>I would much rather deal with outright lies than with
duplicity. You can confront a liar and the books are open.&nbsp;
Like a greased pig, an insincere person can escape and
justify just about anything. It is like the difference
between a standing army and an army of espionage agents. With the
standing army, at least you can see what you're fighting.</p>
<p>It seems to me that while lost people lie directly more
frequently, Christians have more of a problem with
insincerity. If invited to church, a lost person may
respond, &quot;I have to work this weekend,&quot; when he actually doesn't.&nbsp;
On the other hand, a Christian might say, &quot;I didn't feel led by the
Spirit to go to church.&quot; If the Christian is not being truthful, he is
not only deceitful, he is also taking the Lord's name in vain.</p>
<p>The explanation as to why we discover ourselves to be
insincere; the reason we sabotage the things that would
help us live for the Lord and orchestrate life so that we
do not grow spiritually; the reason we do the very things we
condemn is because we have a thriving sinful nature.</p>
<p>Paul writes in <b> Romans 7:15-19</b>, &quot;I do not understand what
I do. For what I want to do I do not do, but what I hate I do....As it is, it is no longer I myself who do it, but it
is sin living in me. I know that nothing good lives in me,
that is, in my sinful nature. For I have the desire to do what is
good, but I cannot carry it out. For what I do is not the good I want to
do; no, the evil I do not want to do&#151;this I keep on doing.&quot;</p>
<p>Maybe we need to take Paul's perspective a little more
seriously. In the book, <u> False Assumptions</u>, the authors
(Drs. Henry Cloud and John Townsend) write: &quot;We may know what is right and
what our values are, but in our hearts are deep loves and affections for things
and people that are contrary to our values. For this
reason, the Bible always calls for change from the inside out, and not just
making right choices...&quot; (p. 166). Speaking of an
individual case, the authors continue, &quot;He had never faced how
dark his heart really was. He admitted that a part of him really did not
want to serve God at all....&quot; (p. 166).</p>
<p>Here is Vasicek's theory on spiritual growth: &quot;If you
will not be honest with yourself about what you really think, how you really
feel, and what you want to do, you cannot go anywhere spiritually.&quot; We are
all made of the same stuff, and some of that stuff is not too good. You can face
it or deny it. If you face it, you can become more victorious in your Christian
life. If you plaster over it, it will simply infest the walls of your heart.</p>
<p>If you will face the evils of your heart, you can fight them.&nbsp;
Resisting temptation is a form of suffering for the Kingdom of God. (<b>Hebrews
2:18</b>, &quot;Because he himself suffered when he was tempted....&quot;) Better to
face sin as a standing army than as an army of terrorists and saboteurs. Be
honest about what you think and how you feel. It will both humble you and help
you, with God's help, to prevail. You can then bring yourself to God, warts and
all. He already knows all about you anyway. Maybe it's time you started to
acknowledge your dark side.
</p>

