<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 25
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are majestic. You are splendid, superb, royal and 
kingly. You are King of Kings and Lord of Lords. Were I to see You in Your 
glory, I would die from being overwhelmed. There are many things in life that 
are beautiful and a number of overwhelming experiences or views. If I were to 
peer at a glorious sunset above majestic mountains, I might be impressed. But 
You are majestic to an infinite degree. You are royal and kingly in such 
massive ways that I cannot even begin to comprehend it. But one day, I shall 
see You and I shall enjoy Your majesty forever and ever.</p>
<p>Abba, Father, help me to resist the devil today. Lord, I 
ask You to bind Satan and his forces; I claim the blood of the Lamb and the word 
of my testimony. Thank You for my advocate, my defense attorney—our Lord 
Jesus Christ. He stands before Your throne and thwarts Satan's attempts to 
accuse me 
(<a href="javascript:scripture('../../scripture/1john212a.htm')" target=_self class=pop>1 John 2:1-2</a>, 
<a href="javascript:scripture('../../scripture/zech315.htm')" target=_self class=pop>Zechariah 3:1-5</a>).</p>
<p>Lord, Your Word tells me that, &quot;Pride goes before 
destruction, a haughty spirit before a fall&quot; (<b>Proverbs 16:18</b>). Help me to run 
from pride and to pursue humility which is simply sanely seeing reality from 
Your perspective and looking at things in proper proportion.</p>
<p>Lord, Hosea prophesied that, &quot;My people are 
destroyed from a lack of knowledge&quot; (<b>Hosea 4:6</b>). Father, remind me that 
ignorance is destructive. Especially help me cultivate knowledge of Your Word 
and Bible doctrine. Convince me that the most important thing I can do to grow, 
once I am saved, is to spend much time in Your Word. Help me never waver from 
that simple conviction. Fellowship, worship and body life—these are all 
important. But spending time in the Word is number one. Help me seek 
content—the meat of Your Word—over feelings, excitement, or motivation.</p>
<p>Father, I pray for the Manipur State of India, with its 2 
million people. Thank You that over a third professes some sort of 
Christianity with probably over a fourth of the population professing to be 
born-again evangelicals. Thank You, Lord. But there are many unreached people 
and drug addiction and AIDS are significant problems in this region as well. 
Help the Christians to point to better alternatives and may the church here 
keep maturing and keep reaching out. Bless their efforts, O Lord.</p>
<p>Lord, I pray for CRISTA Ministries, &quot;a gathering of 
Christians from all walks of life, demonstrating the love of Jesus Christ by 
working together for positive change in a fast-paced world.&quot; CRISTA is ten 
distinct ministries: 
1) CRISTA Senior Ministries, 
2) World Concern, 
3) King's Schools, 
4) CRISTA Broadcasting,
5) Island Lake and Miracle Ranch Camps, 
6) New Hope Child and Family Agency, 
7) Intercristo, 
8) Seattle Urban Academy,
9) CRISTA Women's Ministries and
10) Christian Veterinary Mission.
O Lord, bless this multitasked ministry as it fills 
important voids in the Body of Christ. Help the leaders to manage this work 
well; make it effective, efficient and one that brings glory to You. Grant 
those who labor in this ministry like-mindedness, integrity and wisdom, I pray.</p>
<p>Grant me wisdom today, I request in the Name of the 
Messiah, Amen.</p>

