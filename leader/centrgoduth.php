<p id="pgt"><span class="pgh">Church and Leadership:</span>
<br>The Centrality of God in Youth Discipleship
<br><span class=byln>by Matthew Hoskinson</span></p>

<p>The recent New York Times article, &quot;Evangelicals Fear the 
Loss of Their Teenagers,&quot; has stirred up a great deal of conversation among 
fundamentalists and evangelicals alike. While the article focused on broader 
Evangelicalism, many fundamentalists are wrestling with the same phenomenon. 
Upon graduation from high school, far too many teenagers follow the call of the 
wild, drift away from the church, and (in some cases) repudiate their faith in 
Christ.</p>
<p>There is a sense in which this development should not be 
surprising at all. Since World War II, youth ministry in the United States has 
taken on a life of its own. Parachurch organizations were founded to focus on 
evangelizing and serving teenagers. Pastoral positions were created for the sole 
purpose of meeting the needs of high school students. And the Christian school 
movement has deluded some parents into thinking that their children’s academic 
environment will inevitably produce a disciple of Christ.</p>
<p>Such things as these are not inherently evil. But their 
proliferation over the last half-century has unwittingly taught our children a 
destructive notion: the church exists for them. For the first 18 years of their 
lives, we tailor an unending succession of programs and events to cater to them. 
We entice them to come to church activities by telling them what they will get 
out of it. We create competitions based on spiritual things--Bible memory, sword 
drills, even personal devotional time--hoping that somehow God’s Word might lodge 
in their hearts. None of these things is inherently sinful, but taken together 
they give young people the impression that the church revolves around them. Even 
the phrase youth ministry implicitly teaches them to view themselves 
fundamentally as the objects of service.</p>
<p>Once teenagers graduate from high school, however, they are 
suddenly confronted with a church that no longer revolves around them. We 
explain to these young adults that God expects them to serve others and not 
themselves. But for years, our example has taught them that the church exists 
for them. So when the church stops meeting their perceived needs--when the church 
stops existing for them--they have no reason to stick around.</p>
<p>Our discussion must transcend simply answering the 
question, &quot;How do we get them to stay?&quot; If that is all we do, then we may think 
that the solution is either to remake the rest of the church so that it would 
continue to exist for them (a strategy that surely is being tried in various 
quarters) or simply to create more service opportunities at increasingly younger 
ages. But neither of these strategies addresses the heart of the matter, for 
both are horizontal answers to an essentially vertical question. The problem is 
not primarily the loss of recent high school graduates. The drop-off between 
high school and young adulthood is symptomatic of a much bigger issue: a failure 
to teach the centrality of God in all things and to demonstrate the centrality 
of God in our youth discipleship. Nothing exists for them--or for us, for that 
matter. All of life is all about God.</p>
<p>For believers the thought of the centrality of God in 
everything almost goes without saying. The Scriptures teem with passages that 
speak of God as the source and the end of all things. &quot;There is one God, the 
Father,&quot; Paul writes, &quot;from whom are all things and for whom we exist, and one 
Lord, Jesus Christ, through whom are all things and through whom we exist&quot; 
(<b>1 Corinthians 8:6</b>, ESV). Christ is the one 
&quot;for whom and by whom all things exist&quot; (<b>Hebrews 2:10</b>; <b>cf. Colossians 1:16</b>). 
Since all things are &quot;from him and through him and to 
him,&quot; God is the only one worthy of eternal glory (<b>Romans 11:36</b>). Addressing the 
centrality of God in any matter seems virtually unnecessary since this truth is 
self-evident.</p>
<p>But far too often believers fail to live with God’s 
centrality in mind. Though God’s glory in all things is quite obvious, our 
finite and fallen minds often seek to shut God out of our thinking so that we 
can go about our lives in our own way, for our own purposes. Why else would the 
apostle need to exhort us, &quot;Whether you eat or drink, or whatever you do, do all 
to the glory of God&quot; (<b>1 Corinthians 10:31</b>)?</p>
<p>Therefore, the greatest need our teenagers have is not a 
new program, a new enticement, or a new competition. Neither they nor we need a 
better activity than the last one or another list of dos and don’ts to govern 
our lives. Their greatest need--and ours--is to see God in all His glory. If our 
children and teenagers would see, know, and love this great God, everything else 
would fall into place.</p>
<p>Isaiah exemplifies this dynamic (<b>Isaiah 6</b>). What brought 
about his willingness to be God’s ambassador? It was his vision of the greatness of God 
(<a href="javascript:scripture('../scripture/is614.htm')" target=_self class=pop>vv. 1-4</a>), 
a vision that brought about a genuine brokenness and humility over his own sin 
(<a href="javascript:scripture('../scripture/is65.htm')" target=_self class=pop>v. 5</a>) 
and the tender word of forgiveness from a God who is as good as He is great 
(<a href="javascript:scripture('../scripture/is667.htm')" target=_self class=pop>vv. 6-7</a>). 
We cannot make our teenagers willing to serve 
others. We cannot atone for their sins. We cannot force them to contrite 
admission of their own sin. But we can expose them to the glory of God. And we 
must.</p>
<p>Exposing children and teenagers to the glory of God sounds 
good and right, but it requires much more than merely implementing a new 
program. The glory of God is not something we can simply add to our task lists; 
it is a wholly different paradigm through which we view everything. And if we 
ourselves are not seeing His glory in all things, can we reasonably expect the 
coming generations to do anything but follow our lead?</p>
<p>So where do we begin? We begin by following the example of 
Moses, on our knees begging God, &quot;Show me your glory!&quot; (<b>Exodus 33:18</b>). We begin by 
searching the written Word so we might see the incarnate Word, in whose face we 
see &quot;the light of the knowledge of the glory of God&quot; (<b>2 Corinthians 4:6</b>). 
We begin by exegeting life--every circumstance, every conversation, every relationship--to 
examine what this great Sovereign is seeking to demonstrate about Himself 
(<a href="javascript:scripture('../scripture/ps1153.htm')" target=_self class=pop>Psalm 115:3</a>). 
In short, we must become people who are consumed, not with ministry and 
events, but with God and His glory. Whatever else we do in youth discipleship--or 
anywhere else in the church--must flow from that.</p>
<p>Renewing our commitment to seeing and speaking of the glory 
of God in all of life may not alter the direction of teenagers who insist on 
going their own way after high school. Discipleship is not a mathematical 
formula that requires only the right input in order to churn out a lifelong 
follower of Jesus. And no church will have a perfect track record of success. So 
recovering the centrality of God in our lives and in our churches is not a new 
strategy that we should try for a little while to see if it works. The glory of 
God is not a means to the end of discipleship success. The glory of God is not a 
means to any end; it is the end. All of life is out of Him, all of life now 
exists through Him, and all of life will return to Him as the object of eternal 
glory. And every teenager who sits in our churches will glorify God 
forever--either in their salvation or in their condemnation. Therefore, we must 
confront our young people with the reality, centrality, and glory of God early 
and often so that they might find Him to be the sole object of their hearts’ 
affections. May God be pleased to use us in His gracious work of saving young 
people for His eternal glory.</p>

