<p>From Wikipedia: &quot;Imhotep...is the first architect and 
physician known by name in written history. As one of the officials of the 
Pharaoh Djosèr, he designed the Pyramid of Djzosèr (Step Pyramid) at Saqqara in 
Egypt around 2630-2611 B.C., during the 3rd Dynasty. He may also have been 
responsible for the first known use of columns in architecture. His name means 
'the one who comes in peace.'"
<p>Imhotep also served as chancellor to the pharaoh and high 
priest of the sun god Ra at Heliopolis. He was said to be a son of Ptah... He was 
revered as a genius and showered with titles. The full list of titles is: 
Chancellor of the King of Lower Egypt, First after the King of Upper Egypt, 
Administrator of the Great Palace, Hereditary nobleman, High Priest of Heliopolis, 
Builder, Sculptor and Maker of Vases in Chief. Imhotep 
is credited as the founder of Egyptian medicine, and as author of the Edwin 
Smith papyrus, detailing cures, ailments and anatomical observations...</p>
<p>Two thousand years after his death, his status was raised 
to that of a god. The location for Imhotep's tomb is still unknown. One fringe 
theory is that Imhotep has strong similarities to the biblical Joseph. Some have 
suggested the biblical Joseph is a composite created by the authors of the Torah 
from a Hebrew individual and Imhotep, the authors confusing Imhotep for Joseph.</p>
<p>National Geographic, January 1995, &quot;The annual Nile 
flood, which inundated surrounding fields every autumn before farmers sowed 
their seed, failed seven years in a row. Djoser asked Imhotep where the source 
of the river lay. The pharaoh intended to travel there to interrogate the river 
gods and beg them to show mercy on his people.</p>
<p>&quot;But Imhotep replied that sacred [God] books had given him 
the answer.... The floods returned, and the famine was over....&quot;</p>
<p>Anyone who studies Egyptology on even a superficial 
level knows of Imhotep. He had a mind as great or greater than Da Vinci and 
Einstein. Perhaps no one in all time advanced architecture as much as he: he 
invented the column, he invented the use of quarried stone for building, and he 
invented the pyramid.
<p>The Edwin Smith papyrus is the world's earliest known 
medical document, written around the 17th century B.C.E., but thought to be based 
on material from as early as 3000 B.C.E. It is an ancient textbook on trauma 
surgery, and describes anatomical observations and the examination, diagnosis, 
treatment, and prognosis of numerous injuries in exquisite detail. The papyrus 
contains the first descriptions of the cranial sutures, the meninges, the external 
surface of the brain, the cerebrospinal fluid, and the 
intracranial pulsations. The surgical procedures in the Egyptian Edwin Smith 
papyrus were quite rational given the time period. As well as having magical 
incantations against pestilence, it also contains a prescription for a wrinkle 
remover using urea, which is still used in face creams today.
<p>The Edwin Smith papyrus shows that the heart, vessels, 
liver, spleen, kidneys, ureters and bladder were recognized, and that the 
blood-vessels were known to come from the heart. Other vessels are described, 
some carrying air, some mucus, while two to the right ear are said to carry the 
breath of life, and two to the left ear the breath of death.
<p>Imhotep is credited as the founder of Egyptian medicine and 
the original author of the papyrus...
<p>(source: Wikipedia)</p>
