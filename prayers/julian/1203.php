<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 3
<br><span class=byln>by Ed Vasicek</p>

<p>My God and King,</p>
<p>You are so perplexing to us. We can tell a little about 
You from creation, but were it not for the revelation of Your Word, we would be 
in the dark. Even with Your Word, we must admit that You are wrapped in 
mystery. We remember that &quot;the secret things belong to the Lord&quot; but &quot;the 
things revealed belong to us and our descendents&quot; (<b>Deuteronomy 29:29</b>). Lord, 
help us to study Your Word so that we can learn more about You and Your ways. 
Help us to dig deeply and to grow in our knowledge little by little as we 
persevere in study.</p>
<p>O God, I know You want me to love You with my heart and I 
know that love is more than mere feeling, but a devoted and enduring love. 
Sometimes I find it easy to love You, particularly when I can control the 
environment. And that is not all bad. Sometimes the best way to avoid sin and 
to grow in You is to control our surroundings. But sometimes, Lord, I find 
myself in environments where it is easy to fudge or fear or vacillate. O God, I 
know character is important, but I know that sometimes I can stand firmly for 
You in one situation while not even thinking of You in another. With one group 
of people, I can be Christ-like and fair, with another group I can be arrogant 
and unfair. So, Father, help me to be a Christian in all environments.</p>
<p>Father, empower me by Your Spirit. I know Your Spirit 
lives in me if I know Christ 
(<a href="javascript:scripture('../../scripture/rom89.htm')" target=_self class=pop>Romans 8:9</a>). 
But I know Your Spirit wants to do 
more than dwell in me, He wants to control me. But I do not always want to give 
Him control. I surrender myself afresh to Him right now.</p>
<p>Lord, for many of us, even early December gets our thoughts 
turning toward Christmas. Office parties, family gatherings, church musicals, 
shopping and decorating absorb our time. It is a good time, Lord. Help me to 
remember that the season is about Jesus. Even though others may leave Christ 
out of Christmas, I choose not to. I choose to enjoy both the fun secular 
events and the spiritual ones. O Lord, keep Jesus on my mind.</p>
<p>I pray for my nation, my state or province and my township 
or village. O God, use my testimony to impact my neighborhood or community for 
Christ. Help the local church in which I participate to make an impact. Keep 
it on track, or if it is off-track, help get it back online.</p>
<p>Today I pray for pastors and ministers of the Gospel 
everywhere. I pray that those who truly know You would be strong for You and 
guided by Your Word. I pray that those who do not know You would come to know 
You in a saving way. Remove the bad pastors, O Lord, and support the good ones, 
I pray. Provide, protect and guide those who are true to You, my Lord.</p>
<p>Lord, help me to love You in my behavior today, I ask in 
Jesus’ Name, Amen.</p>

