<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 28
<br><span class=byln>by Ed Vasicek</p>

<p>Gentle Shepherd,</p>
<p>You are the God Who works miracles. When Jesus walked the 
earth, He healed the sick, gave sight to the blind and hearing to the deaf. 
You are a God Who answers prayer, even if not always in the way we would like 
You to answer. We thank You that although we cannot contain, quantify, or 
control You, we can always submit our requests to Your good grace and Your 
perfect will. Although we do not get everything for which we ask because some 
of our requests are outside Your plan, there are many things You will not do 
for us <i>unless</i> we ask. While it is true all requests must pass the filter of 
Your will, it is also true that we have not because we ask not.</p>
<p>So help me persevere in prayer. Let me not become weary in 
well doing. May serving You, Lord, never get old. I don’t want to be a 
quitter. I have set my shoulder to the plow. Help me, Lord, not to look back.</p>
<p>Lord, strengthen my family. Keep the families in my church 
strong and together. Help married couples to work out their differences, stand 
by their commitments and work as a team. Help parents not bury their heads in 
the sand, but keep them aware of the needs and challenges their children face.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control 
me&nbsp;today.</p>
<p>Lord, I know the church gathers so that believer can build 
one another up in their faith, the result of this is spiritual maturity as seen 
in faith, hope and love. Help me to become a more mature Christian. This does 
not happen in giant leaps, but by small baby steps. Help me make those steps. 
I also pray for the following, that they do would grow a little more every week: __________.</p>
<p>Today I pray for the nation of Eritrea bordered by 
Ethiopia and Sudan. Thank You for the 1.7% of the population who are born-again 
evangelical Christians. Lord, there is much work that needs to be done in this 
land. We pray that You would use the Jesus film, radio broadcasts and the 
small evangelical church to share salvation by grace through faith alone in 
Jesus Christ. Please restore freedom of religion so that evangelicals and 
other groups may have freedom to share their faith. Please work among the 
Muslim and Eastern Orthodox groups to recognize the importance of true freedom. 
May the Bible-oriented churches see a tenfold growth over the next decade and 
provide good leaders and leadership training, I pray.</p>
<p>Father, I pray for CLC (Christian Literature Crusade). 
“CLC serves in 55 countries where 700 men and women of diverse nationalities are 
joining hands to feed a world hungry for print.” Thank You for their 
development, production and distribution of Christian literature used around 
the world to reach the lost and to train believers. Provide for them, protect 
them, grant them harmony with one another and empower their efforts and 
literature by Your Spirit, I ask.</p>
<p>Now, Lord, use me today. I pray, in Jesus’ Name, Amen.</p>

