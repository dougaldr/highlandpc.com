<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 19
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>Though You have special plans for specific people, You are 
a God who still disciplines His own. When Moses, who had been faithful to You 
for years, struck the rock in anger when You had commanded him to merely speak 
to the rock 
(<a href="javascript:scripture('../../scripture/num20813.htm')" target=_self class=pop>Numbers 20:8-13</a>), 
you disciplined him. You told him that he would 
not have the opportunity to enter the Promised Land. You showed Yourself to be 
holy. I am reminded that a humble man like Moses could take you for granted. 
How much more do I struggle with getting &quot;too familiar&quot; with You? Help me, 
Lord, to always reverence Your Name. Do not allow familiarity to breed 
contempt.</p>
<p>I pray for my lost friends and family members, including: __________.
Father, please send Your Spirit to draw them to Yourself and grant&nbsp;them the 
repentance that leads to life.</p>
<p>I pray for the elderly, sickly and shut-in people I know or know of including: __________.</p>
<p>Father, help me to value my conscience. When it speaks to 
me, help me to be online. The new nature you've given me has intensified my 
conscience, making the Law of God written on my heart more pronounced. Help me 
to align my conscience with Your will. I do not want to become the weaker 
brother by cringing under the false guilt of an unhealthy conscience. I do not 
want to harden my heart and dull the whisper of conscience.</p>
<p>I choose to rejoice in You today. I reject all the 
thoughts of Satan's kingdom and instead I claim my victory through the Lord 
Jesus Christ and declare my allegiance to His Kingdom.</p>
<p>I remember the African nation of Namibia. Thank You that 
over 10% of the population claim to be born-again Christian evangelicals. Many 
people claim some form of Christianity, but theological liberalism has overtaken 
many churches. As a result, people are turning away from liberal 
'&quot;Christianity&quot; while others are Christians in name only; the youth are turning 
to drugs and rejecting Christian values. Thank You that the evangelical 
Christianity is on the increase. Thank You, Lord, but the challenges are 
great. May Your Spirit work mightily in this nation, recall it from a 
&quot;post-Christian era&quot; and may the percentage of evangelicals double over the 
next decade, I ask.</p>
<p>&quot;Casas por Cristo is a non-denominational missions agency 
dedicated to encouraging Christians to become involved in service projects that 
will enable the church in North America to grow. We offer service opportunities 
to people of all ages through short-term projects in Mexico. Typically our 
projects involve building a home for a poor family in Juarez, Mexico that is 
sponsored by a local church.... Participants are involved in cross-cultural 
outreach.&quot; Lord, use this ministry to glorify Yourself and to influence many 
for the Kingdom of God. Grant the workers safety, the power of Your Spirit and 
prosper their attempts to share the love of Jesus.</p>
<p>My God, bless those who work in small engine mechanics. 
Give them safety, help them to work with enthusiasm and may those who know 
Christ freely share their faith.</p>
<p>Work to make me more like You today, I ask in Jesus' Name, 
Amen.</p>

