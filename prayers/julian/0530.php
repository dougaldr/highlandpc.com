<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 30
<br><span class=byln>by Ed Vasicek</p>

<p>All Powerful God,</p>
<p>You are the God of the individual. You care not only about 
the needs of mankind, but You also care about my needs. You sent Your Son to 
die for the sins of the world, but He also died for my sins. You know the 
number of hairs on my head. Your Spirit has sovereignly given me spiritual 
gifts based upon what You want me to do. That’s why it is so foolish for me to 
compare myself to others. You want to be my Comforter, my Companion, my Friend 
and my Savior. And I worship You as my God. You are truly the God of the 
individual.</p>
<p>Lord, help me to be a kind, compassionate person. May I 
not sense the need to be in the limelight because I have meaning in You. Help me 
to check arrogance and get a true estimate of myself, Your estimate. Help me 
then to keep my mind off of myself, the mark of a truly humble person.</p>
<p>Also, Lord, make me a person of hospitality. Help me to 
reach out to newcomers at church, to open my home, to learn to relax and be 
at ease with others and to make them feel comfortable with me.</p>
<p>Lord, I have many needs and wishes for myself and for 
others. A few requests include: __________.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and&nbsp;just to forgive me those sins, and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I accept responsibility for my 
sins and freely 
confess them as Your Spirit brings them to my mind now.</p>
<p>Help me to do my part by volunteering to serve or 
continuing to serve in a Bible-believing, Bible-teaching church. May I set my 
roots down deeply and attend every week when I am able to do so.</p>
<p>Father, I remember the African nation of Madagascar today. 
Thank You the almost 9% of the population claim to be born-again evangelicals. 
But, Lord, the World Council of Churches is pressuring the government not to 
allow evangelical missionaries entrance into the country. Lord, help them to 
get in. Provide training for church leaders and thank You for the revivals 
within the mainline churches. I pray that the number of born-again evangelicals 
would double over the next decade and that consistently Bible-believing 
denominations and fellowships would prosper.</p>
<p>Thank You for Japan Evangelical Missionary Association which 
“provides fellowship, assistance, teaching, opportunities for exchange of ideas 
and mission strategies, a relationship with the Japan Evangelical Association, 
sponsorship of seminars and conferences and practical helps.” Bless this 
strategic organization, grant them the wisdom of Your Holy Spirit and use them 
to make the ministry more effective. </p>
<p>Today I pray for waitresses, waiters and other food 
servers. May those who know You exemplify Your character as they serve and 
help them to share Christ especially with fellow employees.</p>
<p>Now use me today, I ask in Messiah’s Name, Amen.</p>

