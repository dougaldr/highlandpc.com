<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 24
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>Lord, You are the God of Israel. When You led Your people 
out of Egypt, You did so by Your mighty arm: the plagues, the Passover, the 
crossing of the Red Sea—all so amazing. And then You rose up to fight for 
Israel, closing the waters of the Red Sea and drowning Pharaoh&#039;s army. You are 
a longsuffering God: the Israelites were slaves in Egypt for centuries before 
You destroyed Egypt&#039;s army. Entire generations were born and died without 
seeing Your deliverance. Though You act slowly by human standards, we know that 
You are in no hurry. Help us to believe that Your plans will all come to pass 
in Your time. Help us to be patient; we are such slaves to time.</p>
<p>Father, thank You that I have received Jesus Christ as my 
personal Savior, He became a curse for us so that we could be delivered from 
the curse You put upon us 
(<a href="javascript:scripture('../../scripture/gal313.htm')" target=_self class=pop>Galatians 3:13</a>). 
Thank You that I have escaped the 
curse of <b>1 Corinthians 16:22</b>, &quot;If anyone does not love the Lord—a curse be on 
him.&quot; I pray that Your Spirit would bring to spiritual life my loved ones and 
friends. It grieves me to know that they are under Your curse.</p>
<p>Lord, I am thankful for many things three of which are: __________.</p>
<p>Lord, I remember my country's chief executive officer 
(President, Prime Minister, Secretary General, etc.) and our legislative and 
congressional branches. May they all these leaders favor true and complete 
religious freedom. We want to be able to peacefully share our faith and live out 
our convictions; may they respect our rights to do so. Help them to govern with 
righteousness and equity.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Thank You, Lord, for the believers in Netherlands 
Antilles. Almost 8% of the population of these Latin American islands claims to 
be born-again believers. Although many people claim some form of Christianity, 
the society is immoral, superstitious and often carried away toward strange 
sects. Help the true believers remain firm; may the genuinely regenerate, pursue 
You and gain spiritual and moral stability, I pray. Double the number of solid 
believers within the next five years, I ask. May those who truly know you cease 
dividing themselves between the world and You.</p>
<p>Lord, thank You for the ministry of ESI (Educational 
Services International) as they give &quot;North American Christians an opportunity 
to build significant friendships in classrooms around the world through teaching 
conversational english, business...and other subjects.&quot; They build 
relationships &quot;with students in Muslim, Communist and former Communist 
nations&quot; like &quot;China, Czech Republic, Hungary, Kazakhstan, Kyrgyzstan, 
Morocco, Russia, Slovakia, Ukraine, Uzbekistan and Vietnam.&quot; Lord, use this 
ministry to sow seeds and reach the lost through friendship evangelism. May 
Your Spirit empower their efforts, grant them harmony and provisions, I ask.</p>
<p>Lord, bless those who work as heating, air conditioning, 
and refrigeration mechanics and installers. Give them safety, help them to do 
their work well and help those who know You to share their faith with fellow 
workers and when appropriate with clients.</p>
<p>May Your Spirit, O Lord, fill me and control me throughout this day, I ask, in Yeshua's (Jesus') Name, Amen.</p>

