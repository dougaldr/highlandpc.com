<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>April 9
<br><span class=byln>by Ed Vasicek</p>

<p>Great God,</p>
<p>You are exalted above Your creation. You are not part of 
creation even though You are present everywhere. The universe had a beginning: 
You have always been. The universe is impersonal, You are Personal. You are 
Father, Son and Holy Spirit. Created things are impersonal except for the 
personal created beings: angels (both holy and fallen) and human beings. You 
are spiritual; much of the universe is physical. The laws of physics have no 
bearing upon You, but the universe would destruct were it not for those laws You 
established.</p>
<p>Lord, I know that the natural man�the person without 
Christ who has never been born again�that person cannot understand the things 
of the Spirit because they are foolishness to him or her. Although such persons 
may be religious, their eyes are not open until You, O Lord, open them. I 
recognize that I cannot reason someone into the Kingdom of God, nor can I 
persuade him or her against his or her will. But, Lord, You can use me in the 
process if Your Spirit is opening their eyes and helping them to accept 
spiritual truth. So I want to be used of You, but help me to realize that 
ultimately without You, I can do nothing.</p>
<p>Lord, I pray for friends, family or even myself when it 
comes to addictions. Some struggle with clearly sinful addictions: illegal 
drugs, drunkenness, pornography, workaholism. Others with matters that are more 
health related: cigarettes, overeating, unhealthy diet, etc. Still others 
struggle with compulsions and enslaving behaviors (anorexia, 
obsessive-compulsive behavior, etc.). O Lord, we need Your help to overcome 
these things. Minister to those who are close to me and struggle with these 
things.</p>
<p>Please meet the following needs, requests, and desires as 
You see fit: __________.</p>
<p>Lord, I pray that You would protect my loved ones and I 
throughout our lives. Send guardian angels to watch over us, I pray.</p>
<p>Lord, I pray for the Asian nation of Georgia, a nation of 
the former USSR. Lord, only about 1.4% of the population claims to be 
born-again evangelicals. Additionally the majority Orthodox Church tries to 
suppress the evangelistic efforts of the evangelical church. Bless the attempts 
to distribute Christian literature, Christian radio broadcasts and the use of 
the Jesus Film. May the evangelical church double every two years, I pray.</p>
<p>Lord, thank You for the creative ministry of The 
International Institute for Christian Studies. They seek &quot;to bring glory to 
God and impact the world by developing godly leaders for every sector of society...as 
[they] provide key universities and academic institutions with...Christian 
faculty who teach and live in such a way as to draw others to faith and 
transformation in Christ.... ICS places faculty...in teaching positions at secular 
universities outside North America.&quot; Thank You for the many students who have 
come to follow Christ as a result of this ministry and may each of them bear 
fruit. Empower this ministry by Your Spirit and provide wisdom for all 
connected to it.</p>
<p>Now, Father, I 
rededicate myself to You today, I pray in Jesus' Name, Amen.</p>

