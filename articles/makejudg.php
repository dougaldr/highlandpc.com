<p id="pgt"><span class="pgh">Articles:
<br>Ethics, Morals, Behavior</span>
<br>Should Christians Make Judgments?
<br><span class=byln>by Ed Vasicek</span></p>

<p>There is a bad word in the contemporary Christian vocabulary, and it begins
with &quot;D.&quot; Can you guess it? The word is &quot;discernment.&quot;&nbsp;
&quot;Wait a second,&quot; you say. Discernment is not a bad word! All right,
discernment may not be a bad word, but its synonym is. Discernment is another
word for judgment, and making judgments is considered &quot;wrong&quot; in pop
evangelical culture.&nbsp;</p>
<p>It is comical to think that many Christians claim to believe in absolute
truth. At the same time, they also believe that applying absolute truth in
concrete situations is making a &quot;judgment,&quot; which is considered wrong.&nbsp;
So then, what good is absolute truth if you can't do anything with it?&nbsp;</p>
<p>That leads us to another bad word&#151;this one is an &quot;L&quot; word. No,
in this case the &quot;L&quot; word is not &quot;liberal,&quot; though I must
admit I don't care for that &quot;L&quot; word in the realm of theology. The
word I have in mind is &quot;label.&quot; It is politically incorrect to label
anyone these days.&nbsp;</p>
<p>Is this the approach we find in Scripture? Did Jesus, or Paul, for example,
refuse to label others? Not at all. In <b> Matthew 23:13</b> Jesus said, &quot;Woe to
you, teachers of the law and Pharisees, you hypocrites!&quot; In <b> Luke 13:32</b> he
replied, &quot;Go tell that fox...&quot; (speaking of Herod). Paul did the same
sort of thing. In <b> Philippians 3:2</b>, he wrote, &quot;Watch out for those
dogs...&quot; (false teachers). Does the Bible exhort us to sometimes label?&nbsp;
Certainly. Words like &quot;Jew,&quot; &quot;Gentile,&quot; &quot;saint,&quot;
&quot;fool,&quot; and &quot;hypocrite&quot; are all terms used frequently in the
Bible.</p>
<p>I think it is right to use discernment, that the Bible encourages us to use
absolute truth concretely, and that it is <b>sometimes</b> right to use labels.&nbsp;
That is not to say that there are no dangers involved in labeling. Often, once
we label others, we cannot see what they are really like. Once we have
stigmatized them with a negative title, we often cannot see their positive
qualities, and we may not be open to the possibility that we may have erred or
over-reacted in our labeling. As someone who has frequently been the object of
labeling, I know! Interestingly, the folks who spoke most about &quot;not
labeling others&quot; were the ones who classified my wife, Marylu, and me the
most! It has been a source of amusement to see the surprised look on faces when
we contradict some label we've been given!&nbsp;</p>
<p>But, to return to the &quot;D&quot; word&#151;many believers simply do not
understand the subject of discernment. They think it is wrong to make judgments,
even though we all do so many times a day, (what supermarket to shop at, who to
vote for, or who to trust as a babysitter, for example). The reason for this
discernment-resistant mentality is a misunderstanding of the words of Christ in <b>
Matthew 7:1-2</b>, &quot;Do not judge, or you too will be judged. For in the same
way you judge others, you will be judged, and with the measure you use, it will
be measured to you.&quot;</p>
<p>The real intent of these verses is to teach that we should not use a higher
standard to judge others than we use to judge ourselves. Christ is not saying
that we should never make judgment calls, but rather that we will be judged by
how we judge others.</p>
<p>Let me show that discernment is a good thing and that using truth to make
judgment calls is a sign of spiritual maturity. The Greek words &quot;krino&quot;
and &quot;anakrino&quot; can be translated as &quot;judging,&quot; &quot;making
judgments,&quot; or &quot;discerning,&quot; among other terms. So
discernment is making judgments. The following text, I think, proves the rest of
this point. <b>1 Corinthians 2:15</b> says, &quot;The spiritual man makes judgments
about all things...&quot; Paul goes on to scold the Corinthians for refusing to
make necessary judgments in <b> 1 Corinthians 6:22</b>, &quot;Do you not know that the
saints will judge the world? And if you are to judge the world, are you not
competent to judge trivial cases?&quot;</p>
<p>Furthermore, Christians are encouraged to judge on the basis of objective
truth and frequent behavior patterns, not impressions. Hear the words of Jesus
in <b> John 7:24</b>: &quot;Stop judging by mere appearances, and make a right
judgment.&quot; Christ here encourages us to judge, and to do so rightly.</p>
<p>To choose leaders, the early church had to make judgments. In <b> Acts 6:3-5</b> the
twelve apostles said, &quot;'Brothers, choose seven men from among you who are
known to be full of the Spirit and wisdom. We will turn this responsibility over
to them and will give our attention to prayer and the ministry of the word.'&nbsp;
This proposal pleased the whole group. They chose...&quot;&nbsp;</p>
<p>Later, when it came time to choose elders, a criteria was used that required
judgment and application of terms. <a href="javascript:scripture('../scripture/1tim327.htm')" target=_self class=pop>1 Timothy 3:2-7</a>
outlines that criteria. In
these verses alone, a good dozen judgment calls must be made.&nbsp;</p>
<p>My conclusion is that Christians should become spiritually mature people who
make judgments by applying scriptural principles, not by superficial impressions
nor worldly criteria. We should never judge others by a higher standard than we
judge ourselves; we must be our own worst critics. Also, we must be very careful
about labeling others, but we must be willing to do so when things are obvious,
blatant, and clear. It is the nature of love to give folks the benefit of the doubt
(<a href="javascript:scripture('../scripture/1cor137.htm')" target=_self class=pop>1 Corinthians 13:7</a>),
but there comes a point when doubt is gone. Such
labeling should not be based upon emotional vibrations, but upon behavior,
objectivity, and scriptural truth.</p>
<p>Sometimes the &quot;L&quot; word is a good word. The &quot;D&quot; word is
not only a good word, but is even a measurement of spiritual maturity. Truth is
meant to be put to work, and you can't use it if you are afraid to get specific
with it! Become a person of discernment! Gullibility is for squares.
</p>

