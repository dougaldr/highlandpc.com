<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>May 9
<br><span class=byln>by Ed Vasicek</p>

<p>God the Son,</p>
<p>In Scripture, we read that You are the Word, and the Word 
became flesh. You, God the Son, are the Living Word. When You return to 
conquer and reign, You will also be known by that title 
(<a href="javascript:scripture('../../scripture/rev1913.htm')" target=_self class=pop>Revelation 19:13</a>). 
You communicate the character of God to us and when incarnated You helped us 
understand Who God is by Who You are. We marvel at the relationship between 
Father, Son and Holy Spirit.</p>
<p>Lord, thank You that You have an agenda for me to become 
holier. You are in the process of sanctifying me, helping me to grow in 
spiritual maturity. This requires my cooperation; we are working on this 
together, though we do not work in the same ways or equally. The point is that 
You want me to nurture my walk by being involved with things like spending time 
in Your Word, prayer, building friendships with other believers, learning from 
others in the Body and serving You. You Spirit will then take these things and 
use them as part of the process of growing in You. Help me adopt Your agenda 
and surrender my own.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Father, I have some personal health concerns including: 
__________. I also pray for the physical, mental, or spiritual health of others 
including: __________.</p>
<p>Father, I would like Your peace which passes all 
understanding to guard my heart and mind in Christ Jesus. I give You my 
stress, my cares, my worries, my heartaches and my fears. Help me realign my 
perspective and to remember that all I have to do is to do Your will for me. I 
do not have to please everybody, I do not have to carry the world's worries on 
my shoulders and I can do nothing to change the past. But I can go forward 
with You today.</p>
<p>I ask You to work within the nation of Kazakhstan, one of 
the former Soviet Republics. Thank You for the .6% of the population who claim 
to be born-again believers. I pray that this number will grow to 1% within the 
next 5 years and then double every five years. Lord, this nation is made up of 
over 65 people groups and I pray that every one of them would have a thriving 
church community within the next decade. Use the Jesus Film, literature and 
the testimony of believers to draw people to Jesus. Help the church produce 
strong leadership.</p>
<p>I pray for the ministry of UIM, formerly United Indian
Missions. &quot;The 
purpose of UIM shall be to establish indigenous churches among Native Indian, 
Mexican and Hispanic peoples of North America built on the Scriptures and 
functioning within their cultural orientation.&quot; Thank You for their work in 
Canada, the United States and New Mexico as they work to reach, nurture and 
disciple Native Americans. Give them the leading and power of Your Spirit, I 
pray. Grant them harmony and unity of spirit and provide for their needs. 
Bless them with an abundance of fruit!</span></p>
<p>Help me love You with all I am, I 
pray in Jesus' Name, Amen.</p>

