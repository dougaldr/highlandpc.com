<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>June 18
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You are El Gadol waw Nora, &quot;the great and terrible 
God&quot; (<b>Deuteronomy 7:21</b>). The fear we feel for You as Your people makes us 
revere and respect You, and one day Your enemies will be in fear and dread of 
Your wrath and rightly so. You show Your might in the way You save Your people, 
but those who are far from You will experience the terror of Your divine wrath.
</p>
<p>Lord, help me face the trials, disappointments, stresses 
and sorrows of life including: __________.</p>
<p>Remind me that You are growing my faith and that You 
sometimes use trials to make me stronger in the long run. I will trust You, 
Lord, even though I do not always understand. Thank You, Lord, that You are 
pruning me 
(<a href="javascript:scripture('../../scripture/john152.htm')" target=_self class=pop>John 15:2</a>)
as a vinedresser does a vine. You do this so that I may 
bear more fruit.</p>
<p>Thank You, Lord, that as a follower of Jesus Christ, I 
enjoy fellowshipping with other believers and working together as a family. 
Help me to love my brothers and sisters in Christ in practical ways 
(<a href="javascript:scripture('../../scripture/1john41112.htm')" target=_self class=pop>1 John 4:11-12</a>).</p>
<p>Lord, help those looking for work. Guide Your children into 
fields and places of employment based upon Your purposes. Especially prosper 
those who work honorably and honestly.</p>
<p>As for me, dear God, help me to be a good steward of the 
money I must handle. Guide me when it comes to saving and giving, and help me know 
when to say no. Help me control my money. Do not let money control me.</p>
<p>Today I remember the Asian nation of Myanmar, just north of 
Thailand. Although freedom of religion exists on paper in this nation, the 
military regime has a track record of forcing others to convert to Buddhism. 
Thank You that the evangelical church has grown to 5.2%, despite this pressure 
and despite the expulsion of missionaries in 1966. Thank You that missionaries 
have been allowed in. We pray that You would bless the translation work as 14 
translations are started—but another 14 need to be started as well. O Lord, 
may the number of believers double within the next decade. Use Christian 
radio, the Jesus film and the testimony of believers. Provide solid education 
for church leaders and may the government become more tolerant.</p>
<p>Thank You for the ministry of Caring for China, whose 
vision &quot;is to share the love of Christ with the people of China by sending 
Christians to work alongside them. [They] do this by placing teachers of English 
in positions at universities and secondary schools in China where there is a 
great need for teachers and by providing suitably qualified, motivated and 
experienced personnel to care for orphaned, handicapped, abandoned and sick 
children in cooperation with appropriate local authorities. In addition to 
providing practical help and care, the outworking of [their] vision involves 
training local personnel to assist [them] in this work of compassion.&quot; O Lord, 
bless their efforts, give them unity of Spirit, provide for and protect them 
and may they reach many.</p>
<p>Lord, bless those who work as millwrights. Help them work 
honestly and may those who know You be bold to share Christ with their peers.</p>
<p>Now help me honor You throughout my day, I pray in Jesus' 
Name, Amen.</p>

