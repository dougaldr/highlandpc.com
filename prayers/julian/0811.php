<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>August 11
<br><span class=byln>by Ed Vasicek</p>

<p>My Heavenly Father,</p>
<p>You are a God Who has purpose. You have direction, a goal 
and a destination to which You direct history. Although life may sometimes seem 
aimless or random, You have a goal: to bring glory to Yourself. You, 
Father, seek to glorify the Son and the Spirit, Who in turn seek to glorify 
You and One Another. And we are wrapped up within this triangle of love. As this 
linear experience of history reaches its climax, You will be glorified in unique 
ways드 glorification unique to Your plan. As a result, we best serve You드nd 
ourselves등hen we live our lives for and to Your glory!</p>
<p>Lord, my Savior died on the cross for many reasons, but 
Paul tells us, first and foremost, for my sins 
(<a href="javascript:scripture('../../scripture/1cor153.htm')" target=_self class=pop>1 Corinthians 15:3</a>). 
Although this is of first importance, Christ died for many reasons: to provide for a new 
heaven and earth without the curse, to defeat Satan, as an additional 
condemnation for those who do not believe the truth, to demonstrate Your love for me 
(<a href="javascript:scripture('../../scripture/rom58.htm')" target=_self class=pop>Romans 5:8</a>) 
and also as a pattern for me to follow 
(<a href="javascript:scripture('../../scripture/1pete221.htm')" target=_self class=pop>1 Peter 2:21</a>). 
Because I follow Christ, my sufferings in life provide me with a unique 
fellowship with my Savior, Jesus Christ 
(<a href="javascript:scripture('../../scripture/phil310.htm')" target=_self class=pop>Philippians 3:10</a>). 
Even Christ did not seek suffering, as He prayed in Gethsemane for the cup of
Your wrath to pass from Him if possible. But since it was Your will for Him to 
suffer, He endured the cross. So I also do not want to suffer at all. But when 
I do등hether emotionally or physically듣elp me remember that I am in special 
fellowship with Christ, as painful as it may be.</p>
<p>I pray for my lost friends and family members, including: __________.
Father, please send Your Spirit to draw them to Yourself and grant them the 
repentance that leads to life.</p>
<p>Today I pray for the Hebei Province of China with its 67 
million people. Lord, the Chinese government has been exceptionally harsh and 
aggressive about persecuting Christians in this area. The suffering has been 
awful. Perhaps 2% of the population is evangelical Christians. O Lord, may the 
Chinese government stop persecuting Your children. Rise up, O Lord, and put a 
new spirit in the government leaders. Help the number of born-again 
evangelicals to double over the next five years, I ask. Give the believers 
wisdom as to how to avoid persecution and yet share their faith. Protect them, 
I ask, and empower them with Your Spirit.</p>
<p>I remember the Caleb Project before You, O Lord. &quot;For more 
than 25 years [they] have been a resource for the global church, providing media 
tools and training experiences to equip the body of Christ for strategic 
ministry to unreached peoples (with focus on the 10/40 Window and tribal, Hindu, 
unreligious, Buddhist and Muslim religions).... Caleb 
Project is a resource for the global Church providing media tools 
and training experiences to equip the body of Christ for strategic ministry to 
unreached peoplesvideos, books, prayer guides, short-term trips, the 
Perspectives courses, speakers, seminars and newsletters....&quot; Lord, bless 
this resource to the Body, grant them harmony and effectiveness to Your glory, I 
ask.</p>
<p>Now help me to be a Christian who makes a difference, I 
ask in Jesus' Name, Amen.</p>

