<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>March 10
<br><span class=byln>by Ed Vasicek</p>

<p>Almighty One,</p>
<p>Thank You that God the Son incarnate is the Prince of 
Peace. He came the first time as the Lamb of God to take away the sin of the 
world so that we could be at peace with You. He will come the second time as 
the Lion of the Tribe of Judah to reign. As the Prince of Peace, He brings me 
His peace during the turbulent storms of life. And we know that this world will 
never be at peace until the Prince of Peace reigns.</p>
<p>Dear Lord, help me to be a discerning person. There are 
many false teachings out there, many deceivers have entered the world. Others 
name the name of Christ but are not sincere. Help me not to be gullible. Help 
me to be discerning about spiritual things, relationships, and life.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (1 John 1:9). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control me 
today.</p>
<p>Father, I pray for my relationships.</p>
<p>[For the married]</p>
<blockquote>I pray that you would fortify my marriage. Help me to be 
committed to my spouse, to be faithful, considerate, patient and a good 
listener. Let me accept the qualities that accompany my spouse's gender and 
personality type. Help me be more concerned about our relationship than about 
power struggles of jostling for position as the &quot;wiser one.&quot; Help me respect 
the person to whom I am married. Help me speak respectfully, neither barking 
orders nor using sarcasm. Help me realize that my heart is deceitful and 
desperately wicked, and that I selectively remember and selectively forget so 
that it always seem like I am the one in the right. Not perfect, but in the 
right. Help me accept my own inability to be objective.</blockquote>
<p>[For the single]</p>
<blockquote>Lord, sometimes it is a lonely world. Help me to lean upon 
You as my companion, to make good friends, and to spend my life serving and 
helping others. Help me not to huddle in my home and create my own little world 
with me as its population. Help me embrace life and society.</p>
<p>Keep me pure and away from the temptations of improper 
relationships. Help me to either accept my singleness or to pursue a 
relationship in a way that honors Christ. Help me to find a support group of 
other singles, and help me realize that I am a whole person because I am in 
Christ.</blockquote>
<p>Today I pray for the nation of Cote d'Ivoire (Ivory 
Coast). Although this nation was considered safe and stable, recent civil wars 
and unrest have hindered the spread of the Gospel. Thank you for the 9.2 percent who claim to be born-again evangelical Christians. We know that Bible 
knowledge is low among the evangelicals, that Islam is spreading rapidly, and 
that the unrest in the land is serious. We ask that the number of evangelical 
Christians would double over the next five years, that Islam would decline, that 
the country would stabilize and that believers would make a complete break from 
the superstition of Spiritism (the worship of spirits).</p>
<p>I remember United World Mission in prayer, &quot;a fellowship 
of missionaries and missional churches from North America working in partnership 
with churches around the world to see nations saturated with new churches.&quot; 
Help them as they establish local Bible-believing, Bible-teaching churches 
throughout the world. May the missionaries work in concert and harmony with one 
another, and may Your Spirit empower and bless their work. Provide for them in 
every way, open the right doors and close the wrong doors, we pray.</p>
<p>Now use me to Your glory today, I request, in Jesus' Name, 
Amen.</p>

