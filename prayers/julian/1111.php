<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>November 11
<br><span class=byln>by Ed Vasicek</p>

<p>Gracious and Good God,</p>
<p>Thank You for the many blessings of life. Thank You for 
providing me with the depth that comes from being reconciled to You. I am such a 
deeper person because I know Jesus. I sometimes wonder where I would be without 
You, Lord. How different my life would be.</p>
<p>So Lord, I praise You as the only One Who can truly fill 
that God-shaped vacuum within me. Even though I have this blessing now, I know 
the fit will be perfect in heaven. I will have no unfulfilled longings there. I 
will enjoy You in Your fullness.</p>
<p>But right now, Lord, I am on earth, a sinful mortal who has 
been redeemed. I am Your representative to a lost and dying world. Father, 
help me to share the good news of Jesus Christ. Do not let me get detoured with 
trying to police lost people. You did not call me to get lost people to act 
like Christians; You want me to reach them so that they become Christians 
first. O Father, the message from many pulpits is that we are to use our 
influence to get lost people to embrace our values. Such a mentality does not 
work. Instead, we need to expect those who know Christ to abide by Your will.</p>
<p>I pray for my lost friends and family members including: __________. Father, 
please send Your Spirit to draw them to Yourself and grant them the repentance 
that leads to life.</p>
<p>Omnipotent One, I pray for the nation of Kenya. Thank You for the religious 
freedom that reigns there and for the great response to the Gospel. Almost 36% 
of the population claim to be born-again evangelicals, yet only 12% of the 
population attend church regularly. The problem of nominalism is growing, so 
Lord, please revive Your children there! Alongside this poor church attendance 
is the AIDS epidemic which infects about 14% of the population leaving many 
orphans as a consequence. O Lord, if Your people would walk with You, how many 
miseries would be averted!</p>
<p>Lord, I pray for those who are suffering from AIDS and other like diseases. 
Although many have received these sexually-transmitted diseases through sinful 
behavior not all have done so. Innocent children, faithful spouses, or those 
receiving blood transfusions, etc. have all been struck with these diseases. But 
even among those who have become infected through practices which are against 
Your revealed will, we know that all of us have sinned in varying ways and to 
varying degrees; when we turn from our sin and believe in the Lord Jesus Christ 
all our sins are forgiven. We pray, Lord, that all who suffer from such diseases 
will come to know Jesus. We pray that You would heal them according to Your 
will. If You choose not to heal them, give them better health and help them use 
their remaining days to serve You. Bless and guide those who serve these folks; 
thank You for their ministries of mercy and love. Help all Your children avoid 
immoral practices and help the rest of us not to look down upon those who have 
such a past.</p>
<p>Now, God use me to serve You this day, I ask in Jesus' Name, Amen.</p>


