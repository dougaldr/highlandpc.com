<p id="pgt">The Purpose and Principles of Highland Park Church</p>

<p align="right"><b>May 16, 1996</b></p>
<p align="left">As an independent church, Highland Park Church attracts members
from a variety of backgrounds and denominations. This sometimes causes
difficulties as we attempt to mold our varieties of perspective into a unified
body. A lack of denominational framework gives us freedom, which brings with it
an added responsibility for the leaders of the church to clearly define our
mission and the principles through which we seek to accomplish this mission.&nbsp;
That is the main thrust of this document. It is sometimes called &quot;getting
everyone on the same page.&quot; It is our hope that as you consider these
statements your spirit will agree with ours, and we can, together, find
ourselves on the &quot;same page.&quot;<br>
<br>
As a result of much prayer, study, and discussion, the elders have agreed to
this series of statements or affirmations.</p>
  <p><b>Basis for Decisions Where the Word of God Is Silent</b></p>
  <p>
  Decisions should be made first and foremost by the Word of God. However, in
  those cases where the Bible is silent on an issue, we must take great care to
  discern the right approach. For instance, the Word of God does not tell us
  what color to paint the auditorium. In making that sort of decision, the
  following are good questions to ask:</p>
<ul>
  <li>Will this contribute toward edifying people?</li>
  <li>Will it create obstacles to openness and warmth?
  <li>What is in the best interest of the church?
  <li>What is in the best interest of those we are trying to reach? The wrong
    question is &quot;What do I like?&quot;</ul>
<p>
    <b>Substance Over Image</b>
<p align="left">
    We believe that truth and sincerity are more valuable than slick delivery
    and professionalism. At the same time, we value individuals who do their
    best and plan ahead. We are interested in ministry more than production,
    integrity more than appeal, maturity more than emotional high.<br>
    <br>
    Our top concern is the inner man. We want to rightly discern the difference
    between innovations that will encourage spiritual growth and shallow fads.&nbsp;
    We want people to be attracted to HPC primarily because they see God at work
    in the lives of our people.<br>
    <br>
    We choose substance over image. We do not want to look like a group of
    loving believers as much as we want to be loving believers. We do not want
    marriages that look Christian as much as we want marriages that are
    Christian. We do not want to hold unproductive evangelistic meetings so that
    we have an image of concern for the lost; we want to actually care and to
    effectively reach them. We do not want to give an image of caring for youth,
    we want to minister to their needs. We do not want to call Jesus
    &quot;Lord&quot; and yet not do the things that He says.</p>
<p>
    <b>View of Facilities</b></p>
<p align="left">
    We view our facilities as existing to serve our ministry. The buildings are
    not a shrine or museum to be maintained but exist solely to facilitate
    ministry. The facilities are God's property, held in trust by the church.&nbsp;
    We
    have, therefore, the responsibility to care for them well. Yet, it is what
    we do as a church, not where we meet, that is sacred. Our facilities and our
    approach to ministry should enhance interaction and warmth among believers
    here.</p>
<p>
    <b>Purposes of the Local Church</b></p>
<p align="left">
    Our church exists for edification and evangelism. It is our view that our
    church gathers primarily for edification, both in group meetings and through
    personal ministries. We are also commissioned to bring lost people to a
    saving relationship with Jesus Christ through personal and corporate witness
    and through organized outreach.</p>
<p>
    <b>Edification</b></p>
<p align="left">
    One of the two great purposes of the local church is the edification (or
    encouraging growth toward maturity) of believers. It has many facets and
    incorporates much of what we do in the church.</p>
<p>
    <b>Foundation and Examples of Edification</b></p>
<p align="left">
    Teaching is a cornerstone of edification. A full view of edification,
    however, encompasses anything that encourages, trains, or motivates us
    toward love, good works, and a deepening walk with the Lord. We can do many
    things that edify, such as teaching, prayer, praising God in word and song
    (music), acts of mercy, all sorts of encouragement (notes, phone calls),
    connecting all people in the body (building family life), small group
    meetings, and service group meetings (Awana staff gatherings, boards, etc.).</p>
<p>
    <b>Multifaceted Approach to Edification</b></p>
<p align="left">
    Individual elements of edification should not be substituted for the whole.&nbsp;
    Being built up is a many-faceted process. Although a particular element of
    edification meets a felt need, this does not negate the needs we may not
    feel. Just like we need to eat a balanced diet, we need a sampling of the
    whole process of edification. It may be our nature to want all dessert and
    no vegetables, but we need the veggies! We might prefer teaching, but we
    also need prayer. The ministry of music might touch us, but we also need to
    be challenged.<br>
    <br>
    This also means that we reject the notion that our church gathers mainly for
    worship or that it gathers mainly for instruction. A particular service may
    highlight these elements, but the sum total of our church's activities must
    be much broader. Personalities and human nature draw us away from balance,
    but we must constantly seek to better obey the whole counsel of God.</p>
<p>
    <b>All Believers Involved</b></p>
<p align="left">
    We believe that it is God's will for every believer to be involved in the
    local church in some way, and we are convinced that every believer has
    something to offer. The Bible compares the church to a body, each member
    needing to function. The church needs to be structured to let that happen.</p>
<p>
    <b>Purpose of Meetings</b></p>
<p align="left">
    Most of our church meetings and events are designed to contribute to
    building up believers in some way and should encourage believers toward
    spiritual maturity. This maturity is especially evidenced by the qualities
    of faith, hope, and love.</p>
<p>
    <b>Spiritual Maturity</b></p>
<p align="left">
    Spiritual maturity is not an end&#151;it is a direction. We become more
    spiritually mature by &quot;walking in the Spirit&quot; over a period of
    time. Spiritual maturity is seen in both the absence of the works of the
    flesh and the presence of the fruit of the Spirit. Although the list in
    <a href="javascript:scripture('../scripture/gal5a.htm')" target=_self class=pop>Galatians 5</a>
    details what spiritual maturity looks like, the New Testament
    often summarizes maturity in three virtues: faith (which produces works),
    hope (which produces a constant, enduring walk), and love (which produces a
    heart-felt labor).<br>
    <br>
    <a href="javascript:scripture('../scripture/1cor13.htm')" target=_self class=pop>1 Corinthians 13</a> tells us that of faith, hope, and love, love is the
    greatest. Love is others-centered, which is not to be confused with
    people-pleasing. It involves compassion and forbearance but not
    permissiveness. Love can be firm for the sake of others but is not selfish.&nbsp;
    Hope refers to borrowing strength and endurance for today in view of our
    eternal inheritance. Hope centers around an eagerness for the second coming
    of Christ. Faith refers to our personal trust in the Lord Jesus Christ.&nbsp;
    It
    is closely related to hope but deals more with God's will and goes beyond
    our own ability. It manifests itself in good deeds, as well as in prayer,
    evangelism, and a hunger for growth.</p>
<p>
    <b>Christian Education</b></p>
<p align="left">
    We believe that Christian education is one of the keys to edification of
    believers. Our philosophy of education is based on the following principles:<br>
    <br>
    Our education classes should offer a balanced approach so that over time
    participants get instruction emphasizing many aspects, such as the
    following:</p>
    <ul>
  <li>the revealed truths of the Bible
  (<a href="javascript:scripture('../scripture/2tim316.htm')" target=_self class=pop>2
    Timothy 3:16</a>)
  <li>the historic truths of the Bible
  (<a href="javascript:scripture('../scripture/1cor1011.htm')" target=_self class=pop>1
    Corinthians 10:11</a>)
  <li>sound doctrine and theology
  (<a href="javascript:scripture('../scripture/titus21.htm')" target=_self class=pop>Titus 2:1</a>)
  <li>spiritual depth
  (<a href="javascript:scripture('../scripture/heb61.htm')" target=_self class=pop>Hebrews 6:1</a>)
  <li>relevancy of scripture to daily life
  (<a href="javascript:scripture('../scripture/prov623.htm')" target=_self class=pop>Proverbs 6:23</a>)
    </ul>
<p>
    Teaching the Bible, especially to children, is essential to passing on the
    faith to the next generation. Indeed, this was commanded to Israel
    concerning the ongoing teaching of God's law.
    (<a href="javascript:scripture('../scripture/2tim22.htm')" target=_self class=pop>2
    Timothy 2:2</a>,
    <a href="javascript:scripture('../scripture/2tim315.htm')" target=_self class=pop>3:15</a>;
    <a href="javascript:scripture('../scripture/ps7857.htm')" target=_self class=pop>Psalm 78:5-7</a>)<br>
    <br>
    We believe that parents bear the primary responsibility for instructing
    their children. However, we also recognize that God equips the body of
    believers with people especially gifted to teach. We, therefore, use these
    talents in the church to assist our parents' efforts.
    (<a href="javascript:scripture('../scripture/prov18.htm')" target=_self class=pop>Proverbs 1:8</a>;
    <a href="javascript:scripture('../scripture/rom127.htm')" target=_self class=pop>Romans 12:7</a>)<br>
    <br>
    The emphasis of training should vary depending on the age and spiritual
    maturity of the group being addressed. For instance, children need material
    at appropriate reading levels. Also, newer converts need a different
    emphasis than mature believers.
    (<a href="javascript:scripture('../scripture/1pete22.htm')" target=_self class=pop>1
    Peter 2:2</a>)<br>
    <br>
    We believe that many approaches to teaching are valid. Therefore, rather
    than leadership selecting all methods and materials, we normally prefer that
    teachers select those techniques and materials that fit their particular
    gifts and agree with our doctrinal positions.
    (<a href="javascript:scripture('../scripture/deut1119.htm')" target=_self class=pop>Deuteronomy 11:19</a>)<br>
    <br>
    All teachers within the church are merely servants of Jesus Christ. We
    recognize that the real teacher is God, working through the Scriptures, the
    Holy Spirit, and our relationship with Jesus.
    (<a href="javascript:scripture('../scripture/matt1352.htm')" target=_self class=pop>Matthew 13:52</a>;
    <a href="javascript:scripture('../scripture/rom154.htm')" target=_self class=pop>Romans 15:4</a>;
    <a href="javascript:scripture('../scripture/luke1212.htm')" target=_self class=pop>Luke 12:12</a>;
    <a href="javascript:scripture('../scripture/luke111.htm')" target=_self class=pop>Luke 11:1</a>)</p>
<p>
    <b>Respect for Parents' Authority</b></p>
<p align="left">
    We acknowledge and work with the sovereignty of the family. We recognize
    that parents, not the church, are responsible to rear their children in the
    nurture and admonition of the Lord. We try, therefore, to work with families
    and respect parental domain. We reject the idea that an institution
    automatically has a better idea of what is in a child's best interest than
    the parents. Therefore,</p>
    <ul>
  <li>We think parents should determine if their child is ready to participate
    in the Lord's Supper.
  <li>We believe that both fathers and mothers are to be the main source of
    Christian teaching, with Sunday School, Praise Time Kids, Awana, and
    Soldiers of Christ adding information and support. We recognize this is not
    always possible, especially in instances when the father isn't saved.
  <li>When approved by the elders, we allow believing parents to baptize their
    own children upon the child's profession of faith.
  <li>We allow parents to bring their child into an adult Sunday School class as
    long as the child does not disturb the class.
    </ul>
<p>
    <b>Building Relationships</b>
<p align="left">
    Every believer should seek opportunities to encourage other believers.&nbsp;
    This
    involves a willingness to meet, visit with, and get to know individuals
    beyond one's current circle of friends. As a church, we can provide
    opportunities for body life and relationships. Casual conversations after
    Sunday morning and evening services can develop into friendships. Attending
    Flock Groups or inviting people to dinner are other ways to build
    relationships. We encourage each person to take advantage of these and other
    opportunities.<br>
    <br>
    Unfortunately, it is human nature to want people to be available when we
    need them, yet to feel that we don't have the time when others need us.&nbsp;
    Many
    people want close friends, but they are not willing to move beyond intention
    when it comes to sacrificing time or eliminating other activities to have
    them. We hold the individual responsible to build friendships and the
    leadership responsible to organize ways to make this easier.<br>
    <br>
    Some individuals need help in getting to know others. The leadership and the
    body of believers need to help connect these people to appropriate
    ministries, to opportunities, and to other people. Offering to drive to a
    social gathering or flock group, sitting with new or unconnected folks
    during a service or at a church meal, and inviting them for coffee are some
    ways to accomplish this.</p>
<p>
    <b>Provision For the Needy</b></p>
<p align="left">
    Provision for the needy in the body is a basic tenet that dates to the first
    church in Jerusalem.
    (<a href="javascript:scripture('../scripture/acts245.htm')" target=_self class=pop>Acts 2:45</a>,
    <a href="javascript:scripture('../scripture/acts43435.htm')" target=_self class=pop>4:34-35</a>)&nbsp;
    Jesus instructs us that this is a
    test of our obedience and devotion to Him in the parable of the sheep and
    the goats in
    <a href="javascript:scripture('../scripture/matt253146.htm')" target=_self class=pop>Matthew 25:31-46</a>.&nbsp;
    There are many instructions in the New
    Testament pertaining to the care of those brothers and sisters who are in
    need. From these references, we draw the following principles:</p>
<ul>
  <li>We place a high emphasis on personal responsibility of believers. We are
    each to strive to be financially independent of the charity of others.&nbsp;
    Anyone who can, should work to provide for their own needs.
    (<a href="javascript:scripture('../scripture/1thes41112.htm')" target=_self class=pop>1
    Thessalonians 4:11-12</a>; <a href="javascript:scripture('../scripture/2thes3712.htm')" target=_self class=pop>2
    Thessalonians 3:7-12</a>)
  <li>We are to take care of our own relatives rather than rely on the church
    for charity.
    (<a href="javascript:scripture('../scripture/1tim54.htm')" target=_self class=pop>1
    Timothy 5:4</a>,
    <a href="javascript:scripture('../scripture/1tim58.htm')" target=_self class=pop>8</a>,
    <a href="javascript:scripture('../scripture/1tim516.htm')" target=_self class=pop>16</a>)
  <li>As a church, we will follow the scriptural guidance of helping those who
    are truly needy and devoted to the Lord within our church family. &quot;If
    anyone has material possessions and sees his brother in need but has no pity
    on him, how can the love of God be in him?&quot;
    (<a href="javascript:scripture('../scripture/1tim55.htm')" target=_self class=pop>1 Timothy 5:5</a>,
    <a href="javascript:scripture('../scripture/1tim5910.htm')" target=_self class=pop>9-10</a>;
    <a href="javascript:scripture('../scripture/1john317.htm')" target=_self class=pop>1 John 3:17</a>)
  <li>Whenever we help brothers or sisters in need, we do it quietly, seeking
    only the heavenly reward of the Father, not the praise of men.
    (<a href="javascript:scripture('../scripture/matt614.htm')" target=_self class=pop>Matthew 6:1-4</a>)</li>
</ul>
<p id="sctn">VIEWS ON MINISTRIES</p>
  <p><b>Flexibility Toward Ministries</b>
  </p>
  <p align="left">We believe ministry forms and patterns are not Biblical absolutes.&nbsp;
  We
  recognize the need for patterns of ministry and for variety in ministry.&nbsp;
  We
  must constantly evaluate whether an existing ministry adds to the edification
  of our body or whether it is merely a shrine to the past.<br>
  <br>
  We must also be cautious about establishing new, on-going ministries or
  practices; many events may be worthwhile on a one-time or occasional basis or
  for a limited time. We must flex based upon fruit, workers, and current needs.&nbsp;
  Therefore, we feel free to eliminate practices no longer productive, no matter
  how long-standing. We recognize that edification is a varied process which is
  not necessarily limited to existing methodology.
  </p>
  <p>
  <b>Ministry Style</b>
  </p>
  <p align="left">The style of our ministries is open to change. We freely draw on various
  styles based on who the Lord has placed in the local body and what we sense is
  most edifying. Although we value both our heritage and creative newer
  approaches, we consider them our servants, not our masters. Our concern is
  that we are headed toward involving our people in edification and ministry,
  not matching anyone's model.
  </p>
  <p>
  <b>View Regarding Varied Ministries</b>
  </p>
  <p align="left">We want to discourage &quot;tunnel vision&quot; when it comes to ministry.&nbsp;
  We
  value every ministry, while recognizing some as more crucial than others.
  </p>
  <p>
  <b>Evangelism</b>
  </p>
  <p align="left">
  The other of the two great purposes of the local church is evangelism, the
  proclamation of the Gospel in our world.<br>
  <br>
  We believe that the command given to the eleven disciples in the Great
  Commission to &quot;go and make disciples of all nations&quot; necessarily
  means both evangelism and edification.
  (<a href="javascript:scripture('../scripture/matt2819.htm')" target=_self class=pop>Matthew 28:19</a>)&nbsp;
  Just as we cannot
  successfully evangelize the world without a mature, built-up body, neither can
  we become a mature body without sharing the gospel. In the area of corporate
  evangelism&#151;which simply means those times when we work together as a church
  to reach the lost&#151;we hold the following principles:
  </p>
  <ul>
  <li>Every body of believers must be responsible to its own community first.&nbsp;
    To
    the church in Jerusalem, this meant witnessing to Jerusalem before reaching
    out to all Judea, Samaria, and to the ends of the earth.
    (<a href="javascript:scripture('../scripture/acts18.htm')" target=_self class=pop>Acts 1:8</a>)&nbsp;
    For our
    church, this means that we will not substitute world missions for local
    outreach. We will do both.
  <li>Corporate evangelism is basic to personal evangelism. This is seen in two
    ways. First, our effectiveness in individual witness is related directly to
    the strength of our body life. Jesus said, &quot;Love one another that all
    men will know that you are my disciples.&quot; Second, the witness of two or
    more establishes truth more effectively than the witness of one. Sometimes
    men will only be convinced if they see several people witnessing together
    for Jesus (e.g., Paul and Barnabas).
    (<a href="javascript:scripture('../scripture/john1335.htm')" target=_self class=pop>John 13:35</a>;
    <a href="javascript:scripture('../scripture/deut1915.htm')" target=_self class=pop>Deuteronomy 19:15</a>)
  <li>Presenting the gospel to the unsaved is to take place primarily in the
    world not in the church. While we certainly do not exclude non-believers
    from church gatherings, most of our contacts with them are outside the
    church. Rather than preaching salvation messages to the saved, we prefer to
    send the saved out to seek the lost.
  <li>The primary target for evangelism should be adults and consequently whole
    households. The biblical models we see for evangelism usually involve the
    salvation of adults who then reach their spouses and children.
    (<a href="javascript:scripture('../scripture/acts1615.htm')" target=_self class=pop>Acts 16:15</a>,
    <a href="javascript:scripture('../scripture/acts163134.htm')" target=_self class=pop>31-34</a>)&nbsp;
    This does not mean we should not try to reach children, but it does
    mean that our efforts are more likely multiplied when we focus on adults
    rather than children.
  <li>The church is responsible to identify those who are especially endowed by
    God as people who can carry the good news into the community and
    beyond&#151;&quot;even to the remotest part of the earth.&quot; While not
    implying that other members of the body are released from the responsibility
    to witness, we do recognize that God has gifted and chosen some as
    evangelists and missionaries.
    (<a href="javascript:scripture('../scripture/eph411.htm')" target=_self class=pop>Ephesians 4:11</a>)&nbsp;
    We bear the responsibility as a
    church to identify, train, encourage, and in some cases, support the work of
    evangelists and missionaries from among our people.
  <li>New believers should be integrated into the life of the church as soon as
    possible. This is clear given our emphasis on edification.
  <li>Today's church must develop its own contemporary structures and approaches
    to evangelism utilizing the principles and purposes just stated as biblical
    guidelines. We believe that some evangelism techniques which may have been
    effective at one time are not as effective today. This means that we seek
    new ways to transmit the gospel more effectively. &quot;Sermons from
    Science&quot; is an example of an approach that combines the gospel with a
    format that can reach people in our community today.
    </ul>
<p>
    <b>Interaction With Other Churches and Ministries</b></p>
<p align="left">
    We do believe that the broader evangelical church has much to offer us.&nbsp;
    We,
    therefore, interact with solid churches, quality magazines, and solid
    inter-church movements, campaigns, and outreaches. In seeking to &quot;salt
    the earth,&quot; we can join with people of various faiths to promote common
    cause items so long as we do not convey the image of endorsing the beliefs
    of those involved or that our differences are unimportant. In such cases,
    what unites us is a common cause not a common faith.<br>
    <br>
    Our level of cooperation with other churches shall be dependent upon the
    nature of the project or ministry and the beliefs of other churches
    involved. In matters that address detailed doctrinal issues (such as Bible
    conferences, prophecy conferences, etc.), we expect a higher level of
    agreement than in matters addressing evangelism. In matters of moral issues,
    the level of agreement is based on the issue itself.<br>
    <br>
    In both doctrinal and evangelistic cooperative efforts, we would like to see
    leaders and counselors who hold to the basic fundamentals of the faith, such
    as:</p>
    <ul>
  <li>Inerrancy, inspiration, and sole, absolute authority of the 66-book Bible;
  <li>Trinity, deity of Christ, personality of the Holy Spirit;
  <li>The virgin birth;
  <li>The sacrificial, atoning work of Christ on the cross and His physical
    resurrection;
  <li>The return of Christ (second coming);
  <li>Salvation by grace through faith apart from rituals or good works;
  <li>A literal heaven and hell.</ul>
<p align="left">
    In matters involving more doctrinal instruction, we would be concerned with
    many issues, such as the nature of the church, principles of interpretation,
    the security of the believer, the work of the Spirit, or the literal
    interpretation of Genesis. Again, the determining factor will be the issues
    discussed.<br>
    <br>
    We go on record as opposing the World Council of Churches because it is
    composed predominantly of non-Bible-believing churches. We can work with
    conservative churches in denominations affiliated with the WCC if we are
    satisfied with the individual church's footing.
<p>
    <b>Political Activity</b></p>
<p align="left">
    We recognize our calling as salt and light to the earth.
    (<a href="javascript:scripture('../scripture/matt513.htm')" target=_self class=pop>Matthew 5:13</a>)&nbsp;
    This means that we seek to persuade men by telling the world the truth about
    God's hatred of sin, His inevitable judgment, and the good news that Jesus
    died for the salvation of those who believe.
    (<a href="javascript:scripture('../scripture/2cor511.htm')" target=_self class=pop>2
    Corinthians 5:11</a>)&nbsp;
    At times, this
    means that we speak out concerning the sinfulness in our society, such as
    when we publicly condemn abortion.<br>
    <br>
    We also recognize our residence in a free and democratic land. The issues
    debated in the public square today cry out for moral direction, which can
    only be found in the unchanging truth of God's Word and the love of Jesus
    Christ. Therefore, it is right and proper that our people, as dictated by
    their conscience, should participate fully in the democratic process of our
    governance. If they speak out, let them speak with grace and truth. If they
    seek office, let them serve selflessly and righteously, while always
    remembering that a believer's citizenship is in heaven.
    (<a href="javascript:scripture('../scripture/phil320.htm')" target=_self class=pop>Philippians 3:20</a>)
    <br>
    <br>
    We, as a body, choose to refrain from endorsements or denouncements of
    particular candidates or political parties. We do so because we do not wish
    to be unequally yoked to ideologies and movements, which while appealing for
    evangelical support, are not truly Bible-based or accountable to the Holy
    Spirit.
    (<a href="javascript:scripture('../scripture/2cor614.htm')" target=_self class=pop>2
    Corinthians 6:14</a>)
</p>
