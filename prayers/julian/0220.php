<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>February 20
<br><span class=byln>by Ed Vasicek</p>

<p>Dear God,</p>
<p>Thank You that You are a moral God. Because of that, some 
things are always right and some things are always wrong. While those who 
reject You try to decide what is right and wrong on the basis of consensus, 
those who follow You base their views on Your character and Your revelation. 
Our consciences are at peace when they are tuned to Your ways.</p>
<p>Today I thank You that Your Son was born of a virgin. 
Since He was conceived supernaturally, He could be fully human yet 
uncontaminated by the sin of Adam. He was not born with a predisposition to 
sin, as we are, yet He could be tempted.&nbsp; And, although Mary was a godly woman, 
she was chosen because she was a descendent of David. She did not earn the 
right to bear the Messiah, but, You, O Lord, were gracious to her. Therefore we 
do not worship her or glory in her. Instead we worship and glory in the Lord 
Jesus Christ.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not 
excuse them, but I freely admit them and ask for Your forgiveness.</p>
<p>God, help me to be a good listener. You gave me two ears 
and one mouth, help me to see the obvious implications of that. As James says, 
help me to be slow to speak but quick to listen.</p>
<p>Lord, like everyone else, I have fears and insecurities 
that haunt me. I give them to You, and ask You to help me constantly do so.</p>
<p>Today I pray for the 10/40 window, that area which includes 
much of Asia and northern Africa. We know that the Gospel has penetrated that 
area least of all.&nbsp; We pray for victories and for the Gospel to germinate and 
bear much fruit. Protect, prosper, and provide for Your servants in this part 
of the world. Give them very much fruit for their labors, and bind the Evil 
One, I ask.</p>
<p>Today I pray for the North Canada Evangelical Mission, “...an 
interdenominational faith organization seeking to bring the Gospel of Jesus 
Christ to the First Nations of Canada.” I pray for its 50 points of ministry 
and its 270 members. Help them as they evangelize, disciple, and plant 
churches. Bless them with harmony, provision, and the power of Your Spirit, I 
pray.</p>
<p>I pray for artists in all fields. We know that artists 
have more influence on a society than most people realize. We pray that an 
epidemic of regeneration would break out among the art community, and that the 
number of Christians in this field would multiply tenfold.</p>
<p>Now, Lord, help me 
think with a renewed mind today, I pray, in Jesus’ Name, Amen.</p>

