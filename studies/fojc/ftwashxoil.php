<br>Foot-washing, Kisses and Oil
<br><span class=byln>by Ed Vasicek</span> 

<p>You do not have to be a Bible scholar to recognize that 
the culture of first-century Israel is very different from that of modern 
America. While it is true that people are people—no matter what the era or 
culture—it is also true that what makes an individual comfortable in one culture 
may make her very uncomfortable in another. Let's look at three cultural 
differences centering around today's text, which reads:</p>
<p></p>
<blockquote>Then he turned toward the woman and said to Simon, &quot;Do you see this woman? I 
came into your house. You did not give me any water for my feet, but she wet my 
feet with her tears and wiped them with her hair. You did not give me a kiss, 
but this woman, from the time I entered, has not stopped kissing my feet. You 
did not put oil on my head, but she has poured perfume on my feet.&quot; (<b>Luke 7:44-46</b>)</blockquote>
<p></p>
<p>The first cultural matter is foot washing. In the dry 
and dusty world of first-century Israel, individuals traveled everywhere by 
foot. Since the common footwear consisted of sandals bound around the ankle, 
dirty feet presented a constant problem. In order to relax (at home or while 
visiting at a friend's residence), one would wash one's feet.</p>
<p></p>
<p>Being a good host usually meant providing a pitcher of 
water and wash basin for guests. Sometimes a servant would do the washing. In 
today's text, Simon the Pharisee had not bothered even to present Christ with 
such water. This neglect was unusual in a culture that emphasized hospitality. 
When Christ admonished believers to &quot;wash one another's feet,&quot; it was within 
this context of hospitality, performing humble service for the sake of others.</p>
<p></p>
<p>Another common courtesy was offering a kiss, which is 
very much akin to our custom of shaking hands. Even in the modern Middle East, 
it is common for men to kiss other men who visit them. (Aren't you glad we do 
not live in the Middle East?) Simon had neglected to perform this courtesy.</p>
<p></p>
<p>
Israel is often dusty and dry. This affects not only the feet, but also the 
hair. Since the climate and sun dries the scalp and hair, individuals 
frequently put oil on their heads to moisturize them. Although a host would not 
typically offer his guest oil, he might occasionally do so if he wanted to &quot;roll 
out the red carpet.&quot; Since Simon had not presented the first two common 
courtesies, it is no surprise he did not offer the latter.</p>
<p></p>
<p>In contrast to Simon, this sinful woman who had 
experienced God's wonderful forgiveness was filled with love and devotion for 
Jesus. She had been forgiven much, so she also loved much. So we modern-day 
believers need to reflect upon how very much God has forgiven us—how precious 
and extravagant His grace has been—if our love for the Lord is to burn within.</p>

