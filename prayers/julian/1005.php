<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 5
<br><span class=byln>by Ed Vasicek</p>

<p>My Constant God,</p>
<p>You are unchangeable. You endure forever and You have been 
the Living God from eternity past and will be into eternity future. You are and 
will be always the same. Your nature does not vary because it is perfect and 
infinite. Your character is always the same; You are always just, always holy, 
always good.</p>
<p>Even though You do not change, You do experience changes of 
emotion.</p>
<p>Lord, even Your decision to create the world was an eternal 
decision. It was never not in Your mind to do so. After You created the 
universe, You rested from all Your activity. But it was not that You were 
tired; You rested only in the sense that You stopped creating. You are never 
drained, Your capacity is never even 1 percent full. For just as half of 
infinity is also infinity, so none of Your power can ever be depleted or even 
lessened.</p>
<p>How different I am from You! When I get proud, arrogant 
and puffed up, help me to stop comparing myself to others and compare myself to 
You, the only one and perfect standard. Nothing will bring me back down to 
reality as a glimpse of Who You are.</p>
<p>First John says that if I confess my sins, You are faithful 
and&nbsp;just to forgive me those sins and You will purify me from 
all&nbsp;unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I 
ask Your Spirit to search my heart. (<i>Pause to admit them to God.</i>)</p>
<p>I pray for parents of children who have gone astray. 
Whether in the realm of premarital or extramarital sex, drugs, alcohol, 
gambling, crime, violence, bitterness toward parents, abandoning the faith or in 
any other way, it hurts like nothing else can.</p>
<p>Lord, I pray for young adults who do no have direction in 
life. I pray that many would come to know You and seek Your will for their 
lives. Guide them in their church involvement, educational choices, career 
fields and relationships. Help them to abstain from sexual intimacy until 
marriage. Teach them the principle of delayed gratification, the hallmark of 
maturity. Help them to value marriage and guide them to spouses that are right 
for them. Help them be responsible, to think long term and to value people 
above things. Give them convictions like a belief in the truth of Your Word, 
the concept of absolute truth and Biblical convictions about life, marriage, 
sexuality, family and fiscal responsibility.</p>
<p>Today I pray for the Shandong Province of China with its 
90 million. Thank You that nearly 3% of the population claims to be born-again 
Christians. O Lord, may the Gospel permeate this entire area! Help the 
believers to be able to avoid persecution and may the percentage of Christians 
double over this next seven years, I ask. </p>
<p>I remember the missionaries my church supports including: __________. 
Bless them, provide for them, protect them, keep them from 
sin and put a hedge around them to protect them from the Evil One. Empower 
them and grant them harmony with their fellow workers. May they be fruitful 
for the Kingdom of God.</p>
<p>Help me to stand firmly for the Gospel today, I pray in 
Jesus' Name, Amen.</p>

