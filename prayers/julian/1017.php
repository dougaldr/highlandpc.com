<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 17
<br><span class=byln>by Ed Vasicek</p>

<p>&quot;O L<span class="sm">ORD</span> Almighty,</p>
<p>You who examine the righteous and probe 
the heart and mind&quot; (<b>Jeremiah 20:12</b>); thank You that You are constantly sweeping 
my heart and mind. Sometimes what You see there is impure and selfish; but You 
are the God Who sanctifies, Who sets me apart and is making me holier. You 
are the God Who &quot;searches our hearts and knows the mind of the Spirit.... The Spirit 
himself intercedes for us with groans that words cannot utter&quot; (<b>Romans 8:27, 
26</b>). Thank You that through Your Spirit's work in my life, I am &quot;being 
transformed into his likeness with ever-increasing glory, which comes from the 
Lord, who is the Spirit&quot; (<b>2 Corinthians 3:18</b>).</p>
<p>Father, when a sinner repents, heaven rejoices 
(<a href="javascript:scripture('../../scripture/luke1510.htm')" target=_self class=pop>Luke 15:10</a>). 
Lord, You want me to share Christ with unbelievers and to urge those who 
know You but have compromised their walk with You to surrender themselves afresh 
to You. You love it when sinners repent and You stand ready to forgive as 
pictured by the father in the parable of the Prodigal Son. You run out to meet 
the repentant. Your acceptance is conditional but simple—the condition being 
that we repent and believe in the Lord Jesus. O God, may all those close to me 
make such a decision. And use my church family (including me) to reach out to 
others in our community and through missions, around the world.</p>
<p>Lord, I have some disappointments and burdens in life. A 
few of them hurt me and have left scars including: __________.</p>
<p>
Today, Father, I pray for people trapped in religious cults. Some of these 
cults claim to be Christian; others make no such claim. They have an authority 
other than the Bible and they do not attempt to interpret the Bible 
objectively. They are agenda driven and accuse Christians of things that are 
simply not true. They are manipulative and know how to push all the right 
buttons. O God, help those who have been taken captive to see. Open their 
eyes, help them not to ignore the questions that plague them nor the 
inconsistencies that they observe. Open their minds by Your Spirit and lead 
them to Jesus Christ Who is the way, the truth and the life.</p>
<p>Lord, I praise You for the Pacific island nation of Vanuatu 
(formerly New Hebrides) with a population of 200 thousand. Thank You that the 
evangelical church is strong here: 32% of the population claim to be born-again 
evangelicals. O Lord, help the church here continue to grow both numerically 
and in spiritual depth. We pray that You would help the economy to grow and use 
this small nation as a lighthouse to the region.</p>
<p>Lord, thank You for Partners with Haiti. &quot;The 
purpose of Partners With Haiti is to exalt Jesus Christ by assisting the 
National Evangelical Church in Haiti...in preaching and teaching the whole 
counsel of God.... Included in this ministry are programs that provide education, 
housing, medicine, feeding programs and jobs.&quot; O Father, bless, prosper, 
provide and protect this ministry. Grant the workers harmony with one another 
and may they bear much fruit to Your glory.</p>
<p>Help me to remember that I am a partner with You for world 
evangelism, I ask in Jesus' Name, Amen.</p>

