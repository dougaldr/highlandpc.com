<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>October 28
<br><span class=byln>by Ed Vasicek</p>

<p>All Powerful Creator,</p>
<p>You are a jealous God 
(<a href="javascript:scripture('../../scripture/ex205.htm')" target=_self class=pop>Exodus 20:5</a>). 
Although Your jealousy may not be exactly the same as human jealousy, 
it is close enough that You use the term &quot;jealous&quot; rather than create a new 
term. We see in the Old Testament how Your wrath is kindled when Your people 
worship someone else (another god) or something else (an image). Hosea and Jeremiah 
parallel such behavior to adultery. As a man or woman feels betrayed when a 
spouse has been unfaithful, so You are jealous when Your children are unfaithful 
to You.</p>
<p>Help us offer our acts of worship—prayer, trust and 
worship—to no one but You. Only You are present everywhere to hear our prayers 
and read our minds. Only You are to be praised and worshipped.</p>
<p>Father, keep me from being self-righteous. Nothing will 
eliminate the temptation to be self-righteous quite like getting a glimpse of 
Your holiness. When the godly prophet, Isaiah, saw a vision of you, he declared 
himself &quot;undone&quot; and a man of &quot;unclean lips&quot; 
(<a href="javascript:scripture('../../scripture/is65.htm')" target=_self class=pop>Isaiah 6:5</a>). 
Help me to cultivate humility not be comparing myself to others but to 
You and Your perfect Law. For we who are sinners saved by grace, being humble is 
simply being realistic. Help me not to put myself down, but to gain Your 
realistic perspective. And help me remember that it is not about me, but You.</p>
<p>Lord, help me to avoid every form of superstition. Being 
superstitious is a denial of Your Sovereign control and is in a sense a form of 
idolatry. Instead help me to trust You alone and help me remember that I am 
legally seated with Christ 
(<a href="javascript:scripture('../../scripture/eph26.htm')" target=_self class=pop>Ephesians 2:6</a>) 
in heavenly places 
(<a href="javascript:scripture('../../scripture/eph12022.htm')" target=_self class=pop>Ephesians 1:20-22</a>) 
far above demonic forces often associated with superstition.</p>
<p>Lord, I am thankful for many things, three of which are: __________.</p>
<p>I remember my enemies and those I do not really care for. I 
ask You to bless them and if appropriate to grant them repentance. Help me to 
respond in a proper way to them. Help me to understand the difference between 
loving my enemy, my neighbor, my brother or sister in Christ, my spouse, 
children and family. Help me distinguish between the love of duty and a love 
that embraces emotionally. May I not make more stringent demands upon myself 
than You make upon me.</p>
<p>Lord, the nation of Iceland is cold not only physically but 
spiritually. Only 3.3% of the population claim to be born-again believers and 
most of the churches are liberal and have forsaken the truth of the Gospel. 
Mission efforts have made some difference, but the people are not very 
receptive. O Father, send Your Spirit to draw more Icelanders to Yourself. Use 
Christian radio and the testimonies of the few believers there. Do a mighty work 
to Your glory in this land.</p>
<p>Lord, thank You for Smyrna Ministries International as the 
focus upon persecuted Christians in the Islamic world—and reaching Muslims 
for Christ by working through national evangelical ministries, seeking to 
relieve the suffering of Christians and developing leaders. Lord, protect, 
provide for, empower and use this ministry mightily, I ask. Help the workers to 
experience harmony and may many under the influence of this ministry establish 
a personal relationship with Jesus Christ.</p>
<p>I give my day to You, in Jesus’ Name, Amen.</p>


