<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>January 1
<br><span class=byln>by Ed Vasicek</span></p>

<p>Dear Father,</p>
<p>At the beginning of this New Year, I recognize that You are 
the God of time. You do not age; You know the end from the beginning. Our Lord 
Jesus Christ described Himself as the Alpha and the Omega, the beginning and the 
end. Therefore I recognize that my times are in Your hands.</p>
<p>Lord, I love Your church. She is imperfect, and I must 
admit I contribute toward her flaws. But she is also strong, for the gates of 
hell will not prevail against her. I also contribute toward her strength, but I 
do so by Your grace. Lord, help me contribute more toward the church's strength 
and less toward her flaws.</p>
<p>This being the New Year, many people make resolutions. 
Instead, I ask that I might see more of the fruit of the Spirit, particularly 
self-control, in my life. I do not want to make more promises that I fail to 
keep; instead, I want to stay the course in walking with You.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power, not my own. May Your Spirit control 
me today.</p>
<p>Lord, I have many personal needs. My relationships, my 
health, the decisions I need to make—O, Lord, grant me wisdom and blessing.</p>
<p>Today I pray for the nation of Afghanistan. There are such 
few believers in that land; a few years ago, only 3,000 people claimed any form 
of Christianity. With the country's newfound freedom, I pray that Your Spirit 
would move and bring many to You. Help the few believers there are to have 
wisdom as they try to live within the confines of a deeply Muslim society.</p>
<p>Closer to home, I remember the ministry of Child Evangelism 
Fellowship. Bless them and give them fruit at all levels. </p>
<p>Please give our president wisdom, and help the president to 
put Your will and the needs of our people above political concerns.</p>
<p>I pray that this would be a positive day and that I would 
welcome the New Year with Your joy.</p>
<p>For I pray in Jesus' Name, Amen.</p>

