<p class="sctn">SECTION II: DOCTRINE (OUR BELIEFS)</p>
<p align="left"><b>Article I: The Holy Scriptures</b></p>

<p>We believe that the thirty-nine books of the Old Testament and the
twenty-seven books of the New Testament are verbally inspired by God and
inerrant in the original manuscripts. Scripture is the only infallible
source in determining matters of faith and practice.<br>
