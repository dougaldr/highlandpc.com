<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 20
<br><span class=byln>by Ed Vasicek</p>

<p>Yahweh,</p>
<p>You are the God Who Keeps His Covenant.</p>
<p>Thank You for being faithful, trustworthy and dependable. 
You personify integrity. When You make an unconditional promise, You keep it 
unconditionally. When You make a conditional promise, You keep it as You stated. 
You do not change Your contracts after the fact, but Your calling and purposes 
are irrevocable. We humans renege, we rationalize and we fudge. But not You. O 
Lord. Your faithfulness reaches toward the heavens. Your &quot;yes&quot; means &quot;yes&quot; and Your 
&quot;no&quot; means &quot;no.&quot; You do not vacillate and have no hint of fickleness.</p>
<p>Oh, Lord, help me to be more like You. Help me to remember 
that it is better not to make a vow than to make a vow and not keep it. Make my 
commitments few, but help me to keep the few I make. Let my &quot;yes&quot; mean &quot;yes&quot; and 
my &quot;no&quot; mean &quot;no.&quot; Keep working in me, O Lord, to conform me to the image of 
Christ.</p>
<p>Father, I have some personal health concerns including: __________. I also pray for the physical, mental, or spiritual health 
of others including: __________.</p>
<p>Lord, I empty myself before You so that Your Spirit may 
fill me. I find my strength in His power not my own. May Your Spirit control me 
today. May He control me at home, work and play. May the fruit of the Spirit 
characterize my life, O Lord. Search me, O Lord, and try my heart. Show me what 
I need to see about myself.</p>
<p>Your Word tells me that if I confess my sins, You are 
faithful and just to forgive me of them, and You will cleanse me from all 
unrighteousness (<b>1 John 1:9</b>). So I bring my sins before You now. I do not excuse 
them, but I freely admit them and ask for Your forgiveness.</p>
<p>I remember the hungry, the destitute, the abandoned, the 
sick and the elderly. Oh, Lord, be a God of compassion and provide for them. 
Change world conditions so that fewer people are hungry and bring medical help 
to the world.</p>
<p>Lord, I am humbled to realize that over 43 million people 
have died for their Christian faith since the time of Christ. Over 150,000 die 
every year. O Father, I beg of You to see to it that this persecution stops! I 
pray for nations #21 to 25 from the persecution index of 86 countries where 
persecution is a problem. Today I pray for Egypt, Qatar, Tunisia, Azerbaijan 
and Mexico (the southern portion of this country).</p>
<p>Lord, thank You for International Health Services, a branch 
of the Salvation Army: &quot;it has operated hospitals and clinics in some of the 
most needy areas of the world...currently has 183 health programs running in 39 
countries, focusing on healthcare and the prevention of disease. Almost all of 
these include response to HIV/AIDS.&quot; Thank You, Lord, for these ministries. 
Bless them, prosper them and may they share the love of Christ to those whom 
they serve.</p>
<p>Lord, help me to be filled with joy rather than negativism 
so that others might see Christ in me, I ask in Jesus' Name, Amen.</p>

