<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 24
<br><span class=byln>by Ed Vasicek</p>

<p>O Father,</p>
<p>Many imaginary beings are worshiped as God or gods. 
But You are the only God and the only TRUE God. You are the God of the Bible, 
the God and Father of our Lord Jesus Christ.</p>
<p>You are the God Who is loving and faithful. You exalt Your 
Name, O Yahweh. You exalt Your Name and Your Word above all things 
(<a href="javascript:scripture('../../scripture/ps1382.htm')" target=_self class=pop>Psalm 138:2</a>). 
I revere You by studying and meditating upon Your Word. Help me not 
neglect this most important form of worship! </p>
<p>Lord, I have often called upon You and You have given me 
the courage I need. I pray that one day the leaders of the world will praise 
You and take heed to Your Word. I look forward to the time when they will sing 
Your praises. For, Lord, You are especially gracious to the humble, but more 
distant from the proud. Therefore, I humble myself before You.</p>
<p>L<span class="sm">ORD</span>, You have a plan for my life. Some of it is out of my 
hands; You have determined certain things will happen or place me between 
impassible boundaries. Other things depend upon my cooperation with You. Help 
me not to fight You, but to submit to Your will, O Lord.</p>
<p>Lord, I bring my requests before You. I enter Your throne 
room boldly through the blood of Jesus Christ. My requests include:</p>
<p>Lord, I have much for which to give thanks. A few things I 
am thankful for include: __________.</p>
<p>Lord, may Your Spirit may fill me. May I find my strength 
in His power, not my own. May Your Spirit control me&nbsp;this day.</p>
<p>Lord, help me to be a kind person. I did not ask you to 
help me to be nice for niceness implies being a people-pleaser. Instead You want 
me to be kind. In being both kind and true, I will indeed win the favor of many 
people but not all. But more importantly I will please You 
(<a href="javascript:scripture('../../scripture/prov334.htm')" target=_self class=pop>Proverbs 3:3-4</a>).</p>
<p>Thank You that my soul will live forever. When my body 
dies, to be absent from the body is to be present with the Lord. One day when He 
returns to the clouds, Jesus will resurrect my body into an incorruptible one 
like His.</p>
<p>Lord, may Your Spirit hover over the Rajasthan State of 
India with its 53 million people. O Lord, perhaps .05% of the population claims 
to be born again and evangelical. Thank You that the church is growing at a big 
clip and it is my prayer that within the next ten years more than 1% of the 
population might come to know Christ. Empower the newly planted churches. 
Grant growing numbers the repentance that leads to life, O Lord.</p>
<p>Lord, thank You for &quot;Interserve...a network of Christians 
from all over the world who, using their professional skills, work together to 
serve people of Asia and the Arab world, getting alongside them in tackling 
material, physical, mental and spiritual needs.&quot; Father, bless this ministry; 
empower them, protect them, provide for them and grant them harmony.</p>
<p>Help me to remember Your presence throughout my day, I ask 
in Jesus’ Name, Amen.</p>

