<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 1
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>You never tempt us to sin, but You do test us so that 
we may prove our faith. It is not that You do not know the quality of our 
faith, but it is, rather, that we need to see it and tests help our faith 
grow. You tested Abraham’s faith by demanding that He sacrifice His only Son, 
Isaac. But You sent Your angel to stop Abraham before he could put his son to 
death. Then the Angel of the Lord said, &quot;Do not lay a hand on the boy.... Now I 
know that you fear God, because you have not withheld from me your son, your 
only son&quot; (<b>Genesis 22:12</b>). Although You knew the outcome before You gave the 
test, so You condescend to us and relate to us as though You existed in the 
realm of time. Lord, it reminds me of a tall giant of a man stooping down so he 
can talk to a youngster eye to eye. You are the God Who condescends.</p>
<p>Lord, I remember those who are poor, sickly and 
disadvantaged in some way. Father, life is rough. Sometimes we create our own 
miseries and then we develop a victim mentality or we inherit a victim 
mentality. But sometimes—all too often—we can be poor, sickly, or 
disadvantaged through no fault of our own. And the easy solutions others offer 
us simply do not work in our situation. Thank You, Lord, that You see through 
our excuses and rationalizations, but You also see through the advice of the 
simplistic. You know &quot;what is what&quot; and, like Job's &quot;comforters,&quot; You will 
demand that the know-it-alls give you an account.</p>
<p>Lord, help me to bear up under the trials, disappointments, 
fears and tribulations of life including: __________. Let me remember that You are developing my faith and that 
You often use difficult times to make me stronger in You. Though I admit that I 
usually hate such trials, I do appreciate what You have done in my life through 
them. My choice is to go through them with You or without You. I will go 
through them either way. I choose to go through them hand in hand with You.</p>
<p>Today I pray for the Asian (Middle Eastern) nation of 
Syria, with its 16 million people. Lord, less than .1% of the population claim 
to be born-again evangelicals. Thank You that Christians are fairly well 
tolerated, something unusual in a Muslim country. Although missionaries are not 
allowed into Syria, please continue to bless outreach through literature, the 
Jesus film, radio and television. May the number of believers grow tenfold 
over the next five years and may this pattern continue for four decades, I ask.</p>
<p>Lord, thank You for Grace Ministries International. &quot;The mission of Grace 
Ministries International is to send missionaries into various countries of the 
world with the purpose of: Preaching the gospel of God's grace.... Establishing 
indigenous churches where believers grow in the grace and knowledge of our Lord 
Jesus Christ...training and equipping men and women to do the work of the ministry 
in the churches.&quot; Thank You, Lord, for their work in Asia, Australia, Africa 
and South America. Grant them harmony, provision, protection and empower them 
by Your Spirit. May many come to know Christ and mature in Him as solid 
churches are established.</p>
<p>Help me to mature in You, I ask in Jesus’ Name, Amen.</p>

