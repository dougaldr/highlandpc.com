<p>Jesus 
&quot;cleansed&quot; the Temple on two occasions. The first occasion was early in His 
ministry (<b>John 2</b>), and the second during His final pre-resurrection week. 
Today's sermon will discuss why Jesus had a right to do what He did, a right we 
do not have.</p>
<p>John 
MacArthur discusses the type of business Jesus interrupted. Speaking of the 
&quot;Court of the Gentiles&quot; in the Temple complex where this occurred, he writes:</p>
<blockquote>
...the area had come to be 
used as a religious marketplace, operated under the auspices of the high priest, 
Annas. He was a corrupt and vile man, who saw the Temple and his exalted 
position only as a means to personal power and wealth. The business enterprises 
in the Court of the Gentiles came to be known as the &quot;Bazaar of Annas,&quot; whose 
chief priests and other associates oversaw the Temple franchises. Merchants 
would buy rights to a concession for selling sacrificial animals, wine, oil, or 
salt, or for exchanging money into the proper currency and denominations used in 
Temple offerings. In addition to the franchise fees the operators would often 
be required to pay a certain percentage of their profits to Annas.</p>
<p>
According to the levitical 
law, any animal approved by the priests could be offered in the Temple. But 
chief priests made certain that animals not bought in one of the franchises 
would be judged unacceptable, giving their concessionaires the de facto right to 
provide all the animals...a person would often have to pay as much as TEN TIMES 
what an animal normally cost. As if that extortion were not enough, those who 
needed to have foreign currency exchanged or who had to have their money 
converted into the exact amount for an offering were charged a twenty-five 
percent fee. Jesus was therefore speaking quite literally when He called the 
Temple marketplace &quot;a robbers' den.&quot; (John MacArthur, <i>The MacArthur New 
Testament Commentary, Matthew, Volume 3</i>, p. 268.)
</blockquote>
<p>
I personally do not think 
legitimate enterprise, for the convenience of worshippers, would have been 
wrong. It wasn't enterprise Jesus condemned, but corrupt, exploitable 
enterprise. Sacrifices were sacred, and to take advantage of people who were 
attempting to obey the Law was no small sin.
</p>
<p>
Also worth noting is that, 
unlike the priesthood, most Jews were sincere in their religious practice. The 
common Jews were often very devout, as were some of the leading Jews, like 
Nicodemus and Joseph of Arimathea.</p>
