<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>September 3
<br><span class=byln>by Ed Vasicek</p>

<p>O Lord,</p>
<p>You are the only One I can hang onto. More 
accurately, You are the only One Who can hang on to me. My loved ones are mortal 
and can be taken from me or turn away from me. Distance may separate us. But 
You, O Lord, are eternal and a Rock I can depend on. As years go by, there are 
fewer people with whom we can remember the past, and we can only shares chunks 
of the past together. But You, O Lord, were there for it all, the good and the 
bad, the happy and the sad. You understand who I am and how I came to be who I 
am. You orchestrated events and planned me for unique purposes so that I alone 
can fill the niche You have carved out for me. What a God You are: the God Who 
shares the entirety of my life with me! Even before I knew You, You knew me.</p>
<p>Father, today I pray for those burdened with substance 
abuse and addictions. Although the process often begins with sin, the addiction 
is more than simple sin—it is also bondage. Lord, You understand all that is 
involved when it comes to addiction; I know little. But Lord, I do not need to 
know it all to ask You to help those I love (and perhaps myself) with these 
forms of bondage.</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>Lord, I pray for troubled marriages and those not so 
troubled including my own (if I am married). Lord, help Christians put in 
effort to maintain and invigorate their marriages. Help those who know You not 
to take their spouses for granted but to nurture their relationship. Bind 
Satan and help each spouse to focus on his or her own shortcomings and avoid 
making demands upon their spouses. May Christians address all areas of marriage 
seeking Your will in everything, I ask.</p>
<p>Father, I pray for the Meghalaya State of India with its 2 
million people. Lord, most people in this region claim some sort of 
Christianity, but the younger generations are falling away. O Lord, shepherd 
these younger people back into the fold and strengthen the church there, I 
pray. I also pray for the Mizoram State. O Lord, what a work You have done 
here. Most of the 860,000 people know You! More than 2,000 people from this 
region have gone off to be missionaries themselves—would that it were this way 
all over India! They are highly educated and greatly blessed by You. Please keep 
them strong and keep using them, I pray.</p>
<p>Lord, thank You for Harvest Evangelism and their successful 
ministry in Argentina. &quot;In over twenty-five years of ministry, [You, Lord, have] 
allowed Harvest Evangelism to gestate a prototype for reaching entire cities and 
nations for Christ based on the biblical principles of marketplace 
transformation, prayer evangelism and intercession.&quot; Thank You for the 
phenomenal church growth and evangelism produced by this approach! Grant wisdom, 
harmony, protection, provision and the empowering work of Your Spirit to this 
ministry, O Lord. May many come to You and mature into discipleship, I ask.</p>
<p>Help me think of You throughout my day, I pray in Jesus’ 
Name, Amen.</p>

