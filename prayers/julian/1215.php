<p id="pgt"><span class="pgh">Daily Prayers for Evangelical Believers</span>
<br>December 15
<br><span class=byln>by Ed Vasicek</p>

<p>Lord,</p>
<p>Near the time Jesus was born, Magi from the East  
probably Persia (Iran) came to worship Him. They saw His star, as predicted by 
the purported founder of the Magi, Balaam, &quot;I see him, but not now; I behold 
him, but not near. A star will come out of Jacob; a scepter will rise out of 
Israel. He will crush the foreheads of Moab, the skulls of all the sons of 
Sheth&quot; (<b>Numbers 24:17</b>).</p>
<p>The forefathers of these Magi were probably taught by 
Daniel, who prophesied, &quot;Know and understand this: 
From the issuing of the decree to restore and rebuild Jerusalem until the 
Anointed One, the ruler, comes, there will be seven 'sevens,' and sixty-two 
'sevens'&quot; (<b>Daniel 9:25</b>). This gave them the general time of the Messiah.</p>
<p>Perhaps the constellation of the Messiah mentioned in 
<a href="javascript:scripture('../../scripture/rev1216.htm')" target=_self class=pop>Revelation 12:1-6</a> 
is where the star appeared; perhaps it was elsewhere. But 
wherever it was, You prepared Your own welcoming committee for the Lord Jesus.</p>
<p>So it is that wise men still worship Him. Lord, these Magi 
traveled a great distance to find Christ. But for all who will believe, He is 
near at hand. Thank You that &quot;whoever will call upon the Name of the Lord will 
be saved&quot; (<b>Romans 10:13</b>).</p>
<p>Lord, I have some specific requests to bring before You 
today. They are: __________.</p>
<p>First John says that if I confess my sins, You are faithful 
and just to forgive me those sins and You will purify me from all 
unrighteousness (<b>1 John 1:9</b>). I confess the sins I am aware of now and I ask 
Your Spirit to search my heart. (Pause to admit them to God.)</p>
<p>Father, when I disagree with someone, help me disagree 
agreeably. Let others see that I have found security in You by how I disagree. 
If my ego, my self-esteem, or confidence is not on the line, others will see it. 
If on the other hand I view a disagreement as a personal attack, others will see 
that I do not sense my security in You. So let me be mature, kind but firm, more 
concerned about truth and respecting the rights of others to determine whether I 
am presenting truth that is convincing or not than I am getting my way.</p>
<p>Father, I pray for the Commonwealth of Puerto Rico. Thank You that 27.6% of 
the population claims to be born-again evangelicals Yet, Lord, the translation 
of Your Word into daily life has been a weak link in the chain. Alcoholism, 
drugs and crime rates are high. Lord, help the churches grow in discipling 
believers and help those who know Christ to make Him their priority.</p>
<p>Thank You, Lord, for the Arabic Bible Outreach Ministry as they seek &quot;to 
glorify God by proclaiming His Word through the Internet to the unreached 
peoples of the Arab and Muslim world...reaching the unreached in the Arab and 
Muslim world. We commit to work diligently using all the tools provided on the 
Internet to win lost souls and establish believers in the precious faith.&quot;</p>
<p>Lord, lay Your hand of blessing upon me and upon my loved ones today, I pray 
in the Name of Jesus, Amen.</p>

